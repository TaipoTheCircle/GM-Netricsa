concommand.Add("netricsa_debug_anims", function(ply, cmd, args)
    local npcClass = args[1] or "npc_vj_ssc_grunt"
    
    for _, ent in ipairs(ents.FindByClass(npcClass)) do
        if IsValid(ent) then
            print("=== Анимации для " .. npcClass .. " ===")
            local sequenceCount = ent:GetSequenceCount() or 0
            for i = 0, sequenceCount - 1 do
                local seqName = ent:GetSequenceName(i)
                print(i .. ": " .. seqName)
            end
            break
        end
    end
end)