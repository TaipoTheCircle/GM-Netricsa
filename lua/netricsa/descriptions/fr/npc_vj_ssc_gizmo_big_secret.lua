MARSH-HOPPER DE RIGIL KENTAURUS, ÉNORME


CLASS: Marsh-Hopper de Rigil Kentaurus, énorme
METABOLISM: amphibien terrestre
PERCEPTION: animal sauvage
SIZE: 26 pieds (debout)
ENDURANCE: aucun
HOSTILITY: extrême
WEAPONS: éclaboussure de boue toxique
REWARD: 5000FC
THREAT: très faible

DESCRIPTION: 

Un étrange organisme originaire des friches marécageuses du système de Rigil Kentaurus. Ils vivent en grands groupes et ont un instinct suicidaire pour protéger leur territoire. Dans un chaos dévastateur, tous les individus tentent de bondir sur l'intrus et d'exploser en se gonflant excessivement. Lors de l'explosion, la bave toxique contenue dans leurs intestins éclabousse la cible.

Le favori de Mental et le summum de ses expériences de taille est le Léviathan Rouge, deux fois la taille de son prédécesseur déjà gigantesque. Son corps cramoisi ondule avec des milliers de litres de liquide toxique. Mais le principal secret et la surprise pour quiconque décide de l’attaquer réside à l’intérieur. Mental a placé une soixantaine de Leapers microscopiques dans des bulles d'incubateur spéciales à l'intérieur de son corps. Tuer le Léviathan n'est que le début d'un véritable cauchemar. À sa mort, le corps explose, libérant non seulement une mer de poison, mais également un essaim de minuscules sauteurs, presque invisibles, incroyablement rapides et mortels, qui attaquent instantanément tous les êtres vivants dans le rayon de l'explosion.

TIPS: 

- Ne le laisse pas s'approcher. Attaque à portée maximale.
- Détruisez le corps. Préparez-vous à battre en retraite immédiatement ou à utiliser une arme à dégâts complets (lance-flammes, lance-grenades) contre l'essaim de micro-sauteurs qui s'est échappé. - Il est préférable de déclencher son explosion à distance, en utilisant des explosifs ou des leurres à distance pour détruire à la fois le corps principal et l'essaim caché, sans trop s'approcher.
