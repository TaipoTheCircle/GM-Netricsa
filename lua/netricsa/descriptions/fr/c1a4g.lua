MESA NOIR, NOYAU DE CHAMBRE TENTACLE


Sam, la séquence de purge a été un succès. L'incendie du réacteur a fait son travail, et l'énorme tentacule qui régnait sur ce silo n'est plus rien de plus que des calamars extraterrestres carbonisés. Son corps s’effondra dans les profondeurs, ne laissant derrière lui que son noyau grotesque, encore tremblant d’énergie résiduelle.

À partir de là, l’environnement change. Le bassin extérieur entourant le silo est partiellement inondé et les systèmes de confinement sont défaillants. Des barils de déchets radioactifs – jaune vif, rougeoyants – dérivent lentement dans un courant circulaire autour de la chambre. Il s’agit moins d’un système d’élimination des déchets que d’un carrousel mortel.

Hazards: 

Recommendation: continuez à avancer le long des rebords étroits. Ne nagez pas, ne touchez pas aux barils et ne pensez surtout pas à la pêche. Avec le tentacule mort, votre voie est ouverte, mais Black Mesa ne sera pas à court d'horreurs de si tôt.
