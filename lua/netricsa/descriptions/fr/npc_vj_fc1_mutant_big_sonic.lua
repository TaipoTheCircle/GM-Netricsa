SUPER SANYA DE L'ARCHIPEL KRIEGER


CLASS: Super Sanya
METABOLISM: Mutant Trigen hyper-accéléré
PERCEPTION: Cogneur mutant supersonique
SIZE: 7,2 pieds
ENDURANCE: Très élevé, extrêmement faible
HOSTILITY: Hyper-agressif
WEAPONS: Bottes supersoniques, éperonnage rapide, booms soniques, griffes
REWARD: 2500FC
THREAT: Critique

DESCRIPTION: 

A unique, flawed byproduct of Dr. Egger's experiments to overcome hydrophobia in Trigens. Instead of being cured, the mutant acquired pathological super-speed. His mutated legs have transformed into biomechanical "supersonic boots" that generate a force field, allowing him to run across water and vertical surfaces, leaving behind an explosive sonic wave. However, his psyche hasn't survived the changes: il souffre d'une hydrophobie paradoxale et d'une folie des grandeurs, se croyant un "gigachad sous stéroïdes". Au combat, il se déplace si vite qu'il ne laisse derrière lui qu'un flou bleu et un bang sonique. Il est aussi direct que tous les Fat Boys, mais sa franchise se mesure désormais en kilomètres par seconde.

TIPS: 

- Il mène une guerre personnelle avec le Dr Egger, le croyant responsable de son état.
- Comme un Gros normal, il a un implant lumineux sur sa poitrine, mais il ne peut être touché que lorsqu'il arrête de bouger.
- Utilisez un fusil de chasse, des mitrailleuses lourdes ou des armes à effet de zone (comme un lance-flammes) pour couvrir la zone où il pourrait se trouver.
