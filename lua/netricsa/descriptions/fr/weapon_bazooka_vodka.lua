VODKA-BAZOOKA


WEAPON: Vodka-Bazooka
AMMO: Bouteilles de vodka pleine grandeur
ROF: 1 tour par minute

DESCRIPTION: 

La Vodka-Bazooka est une bouteille de vodka géante pouvant contenir d'autres bouteilles de vodka. Cela fonctionne sur le principe d’une bouteille dans une bouteille, d’une bouteille dans l’ennemi.

When fired, the inner bottle ejects with devastating force, shattering on the target and creating a zone of total vodka destruction. At close range, the effect is so deadly that it's not recommended to fire at all, as everyone will die: ennemis, alliés et valeurs morales.

L'arme a été développée par un ingénieur ivre inconnu au XIXe siècle, surnommé "Jack le Mocassin", qui voulait résoudre tous les conflits une fois pour toutes.

NOTES: 

- Mortel efficace à courte portée. À tel point que la portée rapprochée pourrait ne jamais exister. - Lorsqu'on la frappe, la bouteille explose dans un nuage de verre et d'alcool.
- Le rechargement s'effectue en insérant une nouvelle bouteille dans la plus grande.
- Risque élevé d'autodestruction.
- Les armes sont interdites dans toutes les dimensions civilisées, y compris celles où la civilisation a cessé d'exister.
