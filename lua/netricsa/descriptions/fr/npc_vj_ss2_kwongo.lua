KWONGO, LE GORILLE GÉANT DE M'DIGBO


CLASS: Kwongo, le gorille géant
METABOLISM: Primate géant
PERCEPTION: Divinité de la tribu Simba
SIZE: 427 pieds
ENDURANCE: Extrême
HOSTILITY: Absolu
WEAPONS: Poings, jetant des bâtiments
REWARD: 50000FC
THREAT: Absolu

DESCRIPTION: 

Un gorille géant, plus grand que les gratte-ciel des villes, avec une fourrure grise et des yeux rouges.

C'était autrefois une divinité locale vénérée par la tribu Simba sur la planète M'Digbo. Mais à un moment donné, Kwongo a acquis un morceau du médaillon, un artefact nécessaire pour combattre Mental.

Au combat, le Kwongo est pratiquement invulnérable aux armes conventionnelles. Les fusils, les fusils de chasse et même les lance-roquettes ne laissent que des égratignures sur sa peau. La seule façon d'infliger de sérieux dégâts est d'utiliser des catapultes spéciales, que les Simba chargent de boules de lave. Pendant que Kwongo s'approche lentement de vous, il jette périodiquement des maisons et les écrase avec ses poings.

Après la mort, le Kwongo sera mangé – toute la tribu rôtira ses restes sur un immense feu de joie.

TIPS: 

- Gérer les tourelles. C'est le seul moyen de protéger les Simbas et d'abattre les maisons volantes.
- Si les catapultes ne suffisent pas, achevez-les avec l'arme la plus puissante qu'il vous reste.
