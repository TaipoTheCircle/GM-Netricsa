KAMIKAZE À BOMBE


CLASS: Kamikaze à tête bombée
METABOLISM: Cyber-Zombie
PERCEPTION: Insensible, contrôlé par LCU
SIZE: 8 pieds
ENDURANCE: Très faible
HOSTILITY: Extrême
WEAPONS: Bombe géante
REWARD: 50 FC
THREAT: Haut

DESCRIPTION: 

Contrairement aux Kamikazes typiques, le Bombheaded Kamikaze est généralement utilisé pour des événements « caritatifs » tels que des marathons afin de collecter des fonds pour les chiots Fido malades.

Cependant, la vérité est que tout l’argent généré par ces événements va directement dans la machine de guerre de Mental.

Lorsqu'ils n'assistent pas à ces événements, les Kamikazes se lancent dans la bataille et explosent sur quiconque s'oppose à leur suzerain.

TIPS: 

- Explose lorsqu'il est tué.
- Ils sont dangereux lorsqu'ils sont mélangés à d'autres ennemis.
- Restez à l'écart ; ils n'ont pas d'armes à distance.
- S'il y en a beaucoup, le SBC Cannon et le Minigun vous aideront.
- S'ils sont groupés avec d'autres ennemis, l'explosion peut les tuer.
- Contre une foule de kamikazes, un fusil à pompe, un Uzi ou toute autre arme est efficace, mais seulement si le groupe est constitué uniquement de kamikazes, car faire exploser l'un fera exploser les autres.
