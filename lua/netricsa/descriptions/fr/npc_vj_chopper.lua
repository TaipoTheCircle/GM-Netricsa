HACHOIR DU MOBIUS


CLASS: Hachoir
METABOLISM: Batterie Hydro-Animale
PERCEPTION: Garde de périmètre d'embuscade
SIZE: 4 pieds
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Coque, mâchoires
REWARD: 300FC
THREAT: Faible

DESCRIPTION: 

When Dr. Eggman faced the problem of guarding bridges and waterways, he turned his attention to piranhas. Their lightning-fast attacks from the water became the basis for the Chopper project. The first models were assembled from literally whatever was floating around: la coque d'un poisson perroquet rouge, les nageoires d'un vieux ventilateur, les dents d'un râteau de jardin et le moteur d'un moulin à café Mriya.

Chopper est le seul Badnik qui ne poursuit pas sa cible, mais attend patiemment sous l'eau. Il reste suspendu dans l'eau, montrant les dents et surveillant la surface. Dès qu'une silhouette apparaît au-dessus du pont, Chopper bondit droit vers le ventre du malheureux voyageur. Si l'attaque échoue, il éclabousse à nouveau et réessaye après un certain temps. Dans des modifications ultérieures, il a appris à nager horizontalement et même à tendre des embuscades de groupe.

An oily liquid sloshes inside its hull, replacing its brain. The only command stored in its memory is: "S'il fait noir dessus, mords".

TIPS: 

- Attaquez lorsqu'il saute, alors qu'il est suspendu dans les airs.
- Ne restez pas immobile lorsque vous traversez le pont. - Chopper vise l'endroit où vous vous trouviez il y a une seconde.
- Certains modèles sont capables de poursuivre des cibles sous l'eau.
