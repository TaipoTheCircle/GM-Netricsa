CRABE ZOMBIE COMMUN, H.E.C.U.


CLASS: Headcrab Zombie Common, H.E.C.U.
METABOLISM: Hôte humain + crabe parasite commun
PERCEPTION: Entité zombifiée
SIZE: ~6 pieds
ENDURANCE: Moyen
HOSTILITY: Élevé (lent mais implacable)
WEAPONS: Attaques de mêlée (griffes, balançoires), grappling
REWARD: 250 FC
THREAT: Modéré individuellement, élevé en groupe

DESCRIPTION: 

Zombie H.E.C.U. sont d'anciens soldats de l'unité de combat pour environnements dangereux infectés par Xen Headcrabs. Leur formation militaire a disparu, ne laissant que le parasite extraterrestre aux commandes. Ils conservent la masse imposante du H.E.C.U. armure, ce qui les rend plus résistants que les zombies crabes ordinaires.

Ces zombies sont lents mais implacables, utilisant la force brute pour agripper et mutiler toute personne à proximité. Les rencontres sont particulièrement terrifiantes dans les couloirs ou les espaces confinés, comme le H.E.C.U. l'armure offre une protection supplémentaire.

TIPS: 

- Gardez vos distances : leurs frappes au corps à corps sont puissantes et peuvent épingler les cibles.
- Les armes à feu sont efficaces grâce à leur mouvement lent.
- Méfiez-vous des groupes : leur masse et leur armure combinées peuvent facilement coincer leurs proies.
