EARLY FIREFLY FROM THE KRIEGER ARCHIPELAGO


CLASS: Luciole précoce
METABOLISM: Insecte
PERCEPTION: Insecte bioluminescent
SIZE: 0,06 pied
ENDURANCE: Très faible
HOSTILITY: Aucun
WEAPONS: Aucun
