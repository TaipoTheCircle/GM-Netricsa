BLACK MESA, COMPACTEUR DE DÉCHETS


Sam, contre toute attente, tu es sorti du compacteur. Apparemment, les déchets montent vraiment au sommet. Votre issue de secours improvisée faite de caisses et de conduits de ventilation vous a amené dehors. Pour la deuxième fois depuis des heures... de l'air frais.

Mais ne faites pas encore la fête. Ce n'est pas la liberté. Vous avez fait surface dans une zone industrielle restreinte sur le terrain de Black Mesa. La disposition suggère une usine de transformation – des tuyaux massifs, des réservoirs de stockage et des machines lourdes dominent l’horizon.

Du bon côté ? Au moins, la vue est meilleure que d'être écrasé en pâte.
