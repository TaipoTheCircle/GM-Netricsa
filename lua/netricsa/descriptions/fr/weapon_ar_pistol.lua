PISTOLET À CHARGEMENT AUTOMATIQUE AR-15


WEAPON: AR-15
AMMUNITION: Cartouches 5,56x45 MM
ROF: ~20 tours/min

DESCRIPTION: 

Le pistolet AR-15 est une arme à canon court assemblée à l'aide du récepteur d'un fusil AR-15, mais avec des pièces pour créer un pistolet qui peut être tenu et tiré d'une seule main.

NOTES: 

- De par son calibre, ce pistolet possède une bonne pénétration.
- A ne pas confondre avec le fusil AR-15.






