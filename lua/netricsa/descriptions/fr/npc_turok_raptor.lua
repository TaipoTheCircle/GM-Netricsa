CLASS: Utahraptor
METABOLISM: Prédateur, théropode
PERCEPTION: Chasseur de meute
SIZE: ~8,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrêmement élevé
WEAPONS: Griffes, dents, vitesse, tactiques de meute
REWARD: 400FC
THREAT: Haut

DESCRIPTION: 

Il s'agit de la principale unité de combat de la population de dinosaures de la planète et du principal casse-tête pour quiconque s'aventure dans la jungle sans armes lourdes. Si vous imaginez les rapaces comme de petits lézards agiles jusqu’aux genoux, oubliez ça. Ces créatures sont aussi grandes qu'un homme, pèsent près d'une demi-tonne et ont des griffes qui peuvent déchirer des gilets pare-balles comme du carton.

Les utahraptors ressemblent à un hybride d’oiseau et de lézard, avec un penchant tueur distinct. Leurs longues pattes postérieures leur permettent d’atteindre une vitesse respectable et d’effectuer des sauts latéraux brusques.

La principale caractéristique des utahraptors est qu'ils chassent en meute. Un seul rapace est dangereux, mais le véritable enfer commence quand il y en a trois, cinq, voire une douzaine. Ils encerclent, flanquent, distraient et frappent par derrière. Si vous les laissez vous entourer, vous ne pouvez même pas essayer de vous échapper.

TIPS: 

- Ne reste pas immobile. Les rapaces sont plus rapides que vous, mais leur trajectoire d'attaque est prévisible.
