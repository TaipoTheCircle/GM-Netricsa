COMBINER LES GRENADES


WEAPON: Combiner des grenades
AMMO: Charge explosive
ROF: Lancer manuel, ~10 lancers/min

DESCRIPTION: 

Engin explosif standard utilisé par les soldats du Combine. Conçu pour la suppression tactique, il explose à l'impact ou après une courte mèche, produisant des dégâts importants dans un petit rayon. Idéal pour nettoyer des salles ou perturber les formations ennemies.

NOTES: 

- Efficace pour le déni de zone et l'utilisation antipersonnel.
- Un fusible court nécessite un timing minutieux.
- Peut être dangereux s'il est lancé à bout portant.
