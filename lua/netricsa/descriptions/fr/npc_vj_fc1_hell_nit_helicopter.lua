DESCRIPTION: 

Ce n’est l’arme d’aucune entreprise. C'est l'archipel lui-même qui vient chercher ce qui lui est dû. Et ça ne s'arrêtera pas tant qu'il n'aura pas tout pris. C'est la manifestation ultime de la colère des Moai non payés. Un esprit de rage immortel, matérialisé dans un hélicoptère UH-60 Blackhawk déformé, brillant d'un plasma volcanique cramoisi. La coque entière est repeinte d'un pourpre infernal, des flammes inextinguibles jaillissent des fissures, et le bourdonnement standard des moteurs est remplacé par le rugissement d'une haine ancienne et inextinguible et d'une musique génocidaire. Son arme principale est un minigun hyperchargé, dont les balles n'infligent pas de dégâts mais la notion absolue de mort. Son objectif n'est pas de patrouiller, mais de nettoyer complètement l'archipel de toute vie, y compris les mercenaires de Krieger, les Trigens et, bien sûr, Jack Carver. Il s'en prend à trois jours de salaire impayé, élevés à l'absolu.

TIPS: 

- RESTEZ HORS DU FEU. Une rafale, un obus, un coup - mort garantie. Une couverture, autre que des rochers massifs et indestructibles ou des bunkers souterrains, est inutile : ses tirs brûlent le béton et l'acier.
- Il est impossible de le détruire.
- Attirez-le vers des bases de mercenaires ou des clusters Trigen. Sa rage est aveugle et il attaquera tout, le distrayant de vous.
