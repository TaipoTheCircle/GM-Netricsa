KOLYVAN, UN MARCHAND-INTROMDEUR DE L'ANCIENNE RUS KIEVAN


CLASS: Kolyvan, un marchand intrigant de l'ancienne Russie kiévienne
METABOLISM: Humain, mâle
PERCEPTION: Marchand, aventurier, agent de Mental
SIZE: 10 pieds
ENDURANCE: Faible
HOSTILITY: Moyenne
WEAPONS: Ruse, invoque des taureaux-garous, des lance-roquettes
REWARD: 2200 FC
THREAT: Moyenne

DESCRIPTION: 

Kolyvan est un marchand noble et riche de l'époque de la Russie kiévienne, un homme qui a réussi à tout perdre, y compris lui-même. Dans un autre pari, il a mis sa propre liberté en jeu et, bien sûr, il a perdu. Son adversaire était Mental. Ainsi, Kolyvan est devenu membre de Mental's Horde, le premier marchand de l'histoire à vendre directement son âme.

While retaining his outward appearance-his height, plumpness, long-sleeved green robes, red boots, and cap-Kolyvan's inner being changed little. Greed, cunning, and cowardice remained, but now reinforced by an alien power. As a reward for his service, Mental endowed him with rocket launchers and the ability to summon two Sirian werebulls. This ability is activated strictly by a voice command, pronounced with particular pathos: 

"Et maintenant, le Bossu !"

Mental promised Kolyvan a generous reward: pour son aide dans l'élimination de Sam Stone, Mental aiderait au massacre des autres héros. Cette perspective réchauffe le cœur marchand de Kolyvan plus que l'or, car il accorde presque autant d'importance aux scores personnels qu'au profit.

ADVICE: 

- Vous avez entendu la phrase sur le Bossu ? Préparez-vous pour les taureaux.
- Se venger de tous ceux qui lui doivent 40 000.
