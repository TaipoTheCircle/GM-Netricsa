ASSASSIN MÂLE DES OPS NOIRES


CLASS: Opérateur masculin des Black Ops
METABOLISM: Humain, mâle
PERCEPTION: Membre des Black Ops
SIZE: ~6 pieds de haut
ENDURANCE: moyen-élevé
HOSTILITY: extrême
WEAPONS: MP5 supprimé, fusil de sniper (variante), grenades
REWARD: 1500FC
THREAT: très élevé

DESCRIPTION: 

Contrairement aux assassins féminins agiles et acrobatiques, les agents masculins des Black Ops sont plus lourds, plus blindés et se spécialisent dans les engagements à distance. Ils opèrent en escouades organisées, assurant souvent une surveillance des tireurs d'élite ou des embuscades rapprochées avec des SMG silencieux.

Ils font partie de l'équipe Black Ops envoyée pour effacer complètement les preuves de l'incident de Black Mesa, notamment en anéantissant H.E.C.U. survivants. Leurs uniformes noirs et leurs masques leur donnent un air menaçant et sans visage : des soldats du secret, ne répondant qu'aux ordres les plus élevés du gouvernement américain.

D'une précision mortelle et implacables, ils représentent la prochaine étape de l'escalade une fois que le standard H.E.C.U. échoue. Leur faire face signifie que vous êtes officiellement devenu un « perdant ».

TIPS: 

- Continuez à bouger et utilisez un abri ; rester immobile est un suicide.
