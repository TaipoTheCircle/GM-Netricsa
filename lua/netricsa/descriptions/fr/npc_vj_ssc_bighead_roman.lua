DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais au niveau de difficulté « Je suis Ugh-Zan », ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Roman Ribaric isn't just the co-founder and longtime CEO of Croteam. He's the prime mover, the visionary, and the main instigator of all the madness you see onscreen. His oversized cranium is the source of the initial impulse: c'est lui qui a imaginé le nom « Serious Sam », qui a remplacé le titre provisoire « In the Flesh ». Il a non seulement développé des stratégies commerciales, mais a également insufflé personnellement la vie à l'ennemi le plus célèbre de la série, en enregistrant le cri légendaire et déchirant du Kamikaze sans tête.

Sa philosophie de gestion – une indépendance fanatique et la création de jeux auxquels vous avez envie de jouer – a permis à Croteam de survivre et de prospérer. Son génie caché en tant que concepteur s'est manifesté dans l'idée du niveau secret "Sacred Yards" dans The First Encounter, qui peut être complété sans tuer un seul ennemi - un niveau secret découvert par les joueurs seulement 14 ans plus tard. Roman est le cerveau qui a d’abord généré l’idée, puis a créé les conditions pour que cette idée évolue vers le chaos, la créativité et des jeux emblématiques. Sans son sommeil et ses cris, rien n'existerait.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", détruisez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
