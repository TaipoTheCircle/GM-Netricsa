COOKIE CREEPER, MINECRAFT OVERMONDE CREEPER


CLASS: Creeper de biscuits
METABOLISM: Pâtisserie
PERCEPTION: Élément d'artefact unique
SIZE: 5,5 pieds
ENDURANCE: Faible
HOSTILITY: Passif
WEAPONS: Explosion
REWARD: 50 FC
THREAT: Moyen

DESCRIPTION: 

C'est le résultat d'une étrange expérience génétique, entre magie, pâtisserie et peut-être la mauvaise blague d'un moddeur fou.

Contrairement à ses frères verts alimentés à la poudre à canon, celui-ci est totalement inoffensif en termes de dégâts. Cela ressemble à un biscuit sablé.

La principale et unique caractéristique de cette foule est son explosion. Lorsqu'il touche un ennemi, il ne détruit pas les structures et n'inflige pas de dégâts. Au lieu de cela, il se transforme silencieusement en une multitude d'éclats en forme de biscuit qui volent dans toutes les directions. Ces cookies peuvent être récupérés et chacun restaure instantanément une petite quantité de faim et de santé.

Contrairement à la nourriture ordinaire, les cookies de la foule n'ont aucun effet négatif, autre qu'un léger sentiment de culpabilité d'avoir simplement « tué » un bonbon vivant.

TIPS: 

- Ne t'inquiète pas. Il ne fait pas exploser les blocs et n'inflige pas de dégâts. Mais si vous détestez les sucreries, vous pouvez les ignorer.
- Un meilleur ami le ventre vide. Si vous êtes à court de nourriture et loin de chez vous, c'est un véritable cadeau du ciel.
- Remember: ces cookies ont une date d'expiration d'environ 3 mois, après quoi ils deviennent périmés et se transforment en un zombie ordinaire. Je plaisante.
