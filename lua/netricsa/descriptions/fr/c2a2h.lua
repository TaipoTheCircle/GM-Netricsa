Sam, you've reached the rocket complex. This is what the scientist was talking about: le site de lancement où un satellite doit être mis en orbite. Apparemment, cet appareil aidera le secteur Lambda à stabiliser la faille dimensionnelle. Qu’il s’agisse de science, de magie ou simplement d’espoir désespéré, une chose est claire : ce lancement est essentiel.

Malheureusement, vous n'êtes pas le seul à le savoir. Le H.E.C.U. les forces se sont retranchées dans les silos, déterminées à empêcher le lancement.

Recommendation: Sam, tu n'as pas besoin de comprendre la science - assure-toi juste que la fusée monte. Si l'équipe Lambda a raison, ce satellite pourrait nous donner l'avantage de mettre fin à ce cauchemar. Et sinon... au moins le feu d'artifice sera spectaculaire.
