BLACK MESA, LA CAPSULE XEN WARRIOR


Ouf... sortir de cette usine de broyage, c'était comme sortir d'un hachoir à viande. Et juste au moment où je pensais que la journée ne pouvait plus nous surprendre, devinez quoi ? C’est effectivement le cas.

En analysant le secteur dans lequel nous venons d'entrer, j'ai repéré quelque chose d'inhabituel, quelque chose de bien plus gros que des crabes ou même des vortigaunts. Il s'avère que nous venons de tomber sur la première observation confirmée d'un Xen Warrior. Ce n’est pas un grognement extraterrestre moyen, non monsieur. Ce type est la vraie affaire, un commandant de première ligne décoré du côté Xen (et un déserteur). Il est enfermé dans une cellule de confinement, gardé, étudié... mais bien vivant.

Historical data cross-check: Les Alien Grunts sont considérés comme l’épine dorsale des troupes de choc interdimensionnelles. Plus fort, plus intelligent et doté de suffisamment de puissance de feu pour affronter des escouades entières. Ils n’étaient jamais censés apparaître aussi tôt dans le conflit. Ce qui rend sa présence ici... eh bien, disons simplement "préoccupante".

Now here's the interesting part: la télémétrie suggère qu'il est juste là. Regarder. C'est presque comme s'il attendait quelque chose – ou quelqu'un. Et entre nous, je n'aime pas ce look.

So, to recap: 

Nous avons échappé à la mort par convoyeur.

Nous sommes accueillis par des chiens.

Et oh oui, nous venons de rencontrer le premier Alien Grunt de cette guerre.

Félicitations! Vous êtes officiellement passé de la lutte antiparasitaire à la politique interdimensionnelle. Essayez de ne pas trop briser la glace, car si ce type s'en sort, le mot « amusement » prendra un tout nouveau sens.
