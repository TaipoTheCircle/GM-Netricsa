MONOLOPHOSAURUS DE LA PLANÈTE TERRAFORMÉE


CLASS: Monolophosaure
METABOLISM: Carnivore, Théropode
PERCEPTION: Prédateur d'embuscade
SIZE: ~8,2 pieds
ENDURANCE: Haut
HOSTILITY: Extrêmement élevé
WEAPONS: Mâchoires, griffes, crête unique sur la tête
REWARD: 1000FC
THREAT: Haut

DESCRIPTION: 

The Monolophosaurus resembles a smaller version of the Dilophosaurus, but with one key difference: au lieu de deux crêtes sur la tête, il n'en a qu'une seule.

D'où le nom de « lézard à crête unique ». Autrement, c’est un prédateur typique de taille moyenne, mince, rapide et, à en juger par ses relations familiales, plus avancé que le Dilophosaure primitif.

TIPS: 

- Il n'y a pas grand chose à recommander.
- Ne les laissez pas s'approcher, frappez-les avec des explosifs, ne les laissez pas vous tuer.
