WRAITH DU MONDE MINECRAFT


CLASS: Spectre
METABOLISM: Mort-vivant
PERCEPTION: Foule hostile, volant Wraith
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Touche
REWARD: 200FC
THREAT: Moyen

DESCRIPTION: 

Cette silhouette translucide et pâle avec un « mantra » flottant erre dans l'Overworld la nuit, émettant d'étranges gémissements.

Unlike many other mobs, the Wraith has no physical body. It flies and doesn't collide with other creatures, making it a difficult target. However, it is quite fast and aggressive: il peut repérer les ennemis à une distance de 12 à 16 mètres et les attaquer en les touchant.

Les Wraiths réguliers apparaissent dans l'Overworld à des niveaux de lumière de 7 ou moins, comme des zombies ou des squelettes. Un Wraith ordinaire inflige des dégâts simplement en le touchant.

However, they also have vulnerabilities. Like all undead, Wraiths fear the sun: à la lumière du jour, ils commencent à subir des dégâts et meurent, même s'ils ne prennent pas feu. Leur translucidité est une arme à double tranchant. D’une part, cela leur permet de se fondre dans les parois de pierre des grottes, sans être détectés. De l'autre, cela révèle leur nature Wraithly, permettant à leurs adversaires de repérer une menace à temps.

TIPS: 

- Les spectres sont difficiles à repérer dans les grottes en raison de leur transparence, alors soyez vigilant et écoutez leurs étranges gémissements.
Ils peuvent également émerger de porcs ordinaires s'ils sont frappés par la foudre. Ou directement depuis les portails du Nether dans l'Overworld : plus le portail est grand, plus souvent.
- Puisqu'ils volent et n'entrent pas en collision avec des blocs, se cacher dans les coins étroits est inutile. Mieux vaut garder vos distances et utiliser un arc.
- Les spectres émettent un gémissement aigu lorsqu'ils sont frappés et un cri étrange et surnaturel lorsqu'ils meurent.
