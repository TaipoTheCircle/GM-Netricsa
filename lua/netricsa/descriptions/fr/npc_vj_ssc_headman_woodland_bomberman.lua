BOMBARDIER DES BOIS DÉCAPÉ


CLASS: Bomberman des bois décapité
METABOLISM: Cyber-Zombie
PERCEPTION: Insensible, contrôlé par LCU
SIZE: 6 pieds
ENDURANCE: Très faible
HOSTILITY: Très faible
WEAPONS: Grenades à main
REWARD: 250 FC
THREAT: Faible

DESCRIPTION: 

Un soldat sirien exécuté, ressuscité des morts par Mental et télécommandé par Life Control Unit (LCU).

Transporte une grande quantité de grenades dans ses poches.

Le spécimen des bois tient constamment une deuxième grenade dans sa main gauche, lui permettant d'exploser à sa mort.

Il porte un camouflage vert tacheté, adapté aux environnements boisés. À en juger par l'état de l'uniforme et les marques sur le pantalon, le bas du corps a été soumis à un traitement chirurgical grossier lors de la résurrection - les organes reproducteurs sont absents, ce qui indique une perte complète de la fonction biologique et une transformation en une ressource de combat consommable.

Ils sont activement utilisés par les forces de Mental dans les forêts, les fourrés et les zones marécageuses, où l'apparition soudaine et la nature explosive de leurs attaques sont particulièrement efficaces.

TIPS: 

- Be careful: un Woodland Bomberman décapité tué explose.
- Détruisez-les à distance, de préférence avec un tir de précision.
- Soyez particulièrement vigilant en forêt : ils se camouflent souvent et sortent brusquement de leur abri.
- Une concentration de plusieurs de ces soldats transforme la zone en un champ de mines mortel.
