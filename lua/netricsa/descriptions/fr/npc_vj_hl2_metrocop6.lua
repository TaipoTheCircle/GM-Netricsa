COMBINER LA PROTECTION CIVILE METROCOP BOUCLIER GARDE


CLASS: Combinez la protection civile Metrocop Shield Guard
METABOLISM: Humain, mâle
PERCEPTION: Garde D'Assaut De La Protection Civile
SIZE: 9 pieds
ENDURANCE: Haut
HOSTILITY: Haut
WEAPONS: Bouclier tactique, paralysant Stun-Stick
REWARD: 350FC
THREAT: Moyen

DESCRIPTION: 

Le Shield MetroCop est un agent spécialisé de la protection civile, conçu pour réprimer les émeutes et contrôler les foules denses de citoyens. Contrairement aux patrouilleurs réguliers, il opère sur la ligne de front, absorbant les coups, les tirs et les tentatives désespérées de résistance.

Son uniforme tactique noir est orné de panneaux jaunes sur les épaules et les avant-bras, servant de marqueurs visuels de son rôle et de son unité. Un masque blanc dissimule complètement son visage, empêchant les ennemis de lire ses émotions ou ses intentions.

The primary piece of equipment is a gray tactical shield with a yellow Combine symbol. The shield is used not only for defense but also as a means of pressure: il peut être utilisé pour repousser, épingler les adversaires contre les murs et briser les formations de foule. Derrière le bouclier, le Gardien avance lentement et sans relâche, repoussant les adversaires dans des zones prédéterminées.

L’apparition de Gardiens dotés de boucliers marque presque toujours le début de la phase de suppression active.

TIPS: 

- Attack from the flanks or from behind - a frontal assault is ineffective.
- Le bouclier réduit les dégâts mais limite la ligne de vue du porteur.
