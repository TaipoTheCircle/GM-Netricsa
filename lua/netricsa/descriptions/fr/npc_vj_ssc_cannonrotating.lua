Canon rotatif CANON ROTATIF


CLASS: Canon rotatif
METABOLISM: machine
PERCEPTION: système de défense automatique
SIZE: 12 pieds
VIEW ANGLE: 360 degrés. (balayage)
HOSTILITY: haut
WEAPONS: boulets de canon
REWARD: 1000FC
THREAT: haut

DESCRIPTION: 

Mental a été émerveillé par le concept de son arme la plus puissante et a donc essayé de créer quelque chose de similaire. Mental a amélioré un canon médiéval ordinaire avec un système de chargement automatique, un radar et un réapprovisionnement en munitions, ce qui en fait une unité de défense autonome assez efficace et dangereuse.  La version améliorée de cet appareil est placée sur une base rotative qui lui permet une attaque complète à 360 degrés.

TIPS: 

- Évitez les boulets de canon par un mouvement constant.
