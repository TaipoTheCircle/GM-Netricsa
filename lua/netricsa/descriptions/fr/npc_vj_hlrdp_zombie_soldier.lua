Y.S.M. CRABE ZOMBIE, COMMUN


CLASS: Y.S.M. Crabe zombie, commun
METABOLISM: Hôte humain + crabe parasite de Xen
PERCEPTION: Entité zombifiée, ancien entrepreneur
SIZE: 6,2 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Griffes, Morsure
REWARD: 500FC
THREAT: Très élevé

DESCRIPTION: 

C’est ce que pensent les mercenaires de la société militaire privée Y.S.M. se transformer après une rencontre malheureuse avec un crabe. Contrairement aux scientifiques en blouse grise, aux techniciens en combinaison ou même aux gardes de la base Delta, ces types étaient à l'origine des soldats professionnels. Ils portaient des gilets pare-balles, des casques tactiques et savaient manier les armes. Cela ne les a pas sauvés de l'infection, mais cela a fait d'eux des adversaires beaucoup plus dangereux sous leur forme zombifiée.

Leur armure protège partiellement leur corps des tirs, les rendant plus résistants que les zombies ordinaires. Le crabe perché sur leur tête ressemble à celui des autres variantes, mais son hôte est un corps entraîné et musclé qui ne se désagrège pas sous quelques coups.

They are larger than the Delta Base guards, their armor is thicker, and their blows are heavier. Even after the headcrab takes control, the former mercenary retains muscle memory: ils se déplacent plus vite que les zombies ordinaires. Leurs griffes transpercent l'armure et leurs morsures arrachent des morceaux de chair.

Un tir dans la tête détache la tête.

TIPS: 

- Y.S.M. les zombies sont plus résistants que tous les autres zombies standards. Ne lésinez pas sur les munitions.
- Son armure protège sa poitrine et ses épaules. Visez la tête, elle est vulnérable, comme celle de tout le monde.
- Gardez vos distances, il est un peu plus rapide qu'un zombie normal.
- Ne pensez pas que les restes de son équipement tactique soient des trophées. Il n’en a plus besoin, et vous n’en avez certainement pas besoin.
