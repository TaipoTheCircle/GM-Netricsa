ARBALISTE PILLAGEUR DU MONDE MINECRAFT


CLASS: Arbaliste pilleur
METABOLISM: Humain
PERCEPTION: Pilleur avec une arbalète, infanterie légère Illager
SIZE: 6,2 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Arbalète
REWARD: 100 FC
THREAT: Haut

DESCRIPTION: 

L'une des variétés de villageois hostiles les plus courantes et reconnaissables, armé d'une arbalète et impitoyable. Alors qu'un villageois normal commerce avec vous et construit des fermes, un arbaliste pilleur veut simplement vous transformer en coussin de flèches.

Contrairement à leurs cousins ​​Vindicator, qui préfèrent le combat rapproché, les Pillager Arbalist sont des tireurs d'élite nés. Leur arme principale et unique est une arbalète, qu'ils tirent toutes les 3 secondes sur une cible dans un rayon de 16 mètres. Lorsqu'il est complètement immergé, l'arbaliste Pillager passe au combat rapproché, car l'arbalète est inefficace sous l'eau.

Les Pillager Arbalist habitent trois endroits, chacun promettant des ennuis. Premièrement, il y a les patrouilles Pillager Arbalist, des groupes de 2 à 5 maraudeurs qui parcourent le monde à la recherche de villages. Deuxièmement, il y a les avant-postes-tours de guet des Pillager Arbalists avec des cages pour les golems de fer, autour desquelles ces archers résident constamment. Et troisièmement, il y a les raids, où les Pillager Arbalist forment l'épine dorsale des premières vagues d'envahisseurs.

La principale caractéristique des Pillager Arbalist est leur tactique lâche mais efficace. Ils ne s'engagent jamais dans des combats rapprochés, préférant garder leurs distances et tirer méthodiquement sur leur cible. Si un joueur s'approche, l'arbaliste pilleur recule, essayant de maintenir sa distance. Dans les couloirs étroits, cela peut être fatal, car il est difficile d'esquiver une flèche.

Parmi les Arbalistes pilleurs, il y a des capitaines maraudeurs avec un drapeau inquiétant sur la tête. Ils sont facilement reconnaissables grâce à la bannière noire et blanche flottant au-dessus de leur capuche. Tuer un tel capitaine inflige l'effet de statut "Mauvais présage". Le niveau de l'effet dépend du nombre de capitaines tués. Avec cet effet de statut, un raid commence dès l'entrée dans un village - une série de vagues au cours desquelles Pillager Arbalist et d'autres Ilagers tentent d'éliminer tous les villageois.

Lors des raids, les Pillager Arbalist sont particulièrement agressifs, attaquant spécifiquement les villageois. S'ils parviennent à tuer un villageois ou à détruire une porte, ils poussent un rire triomphal et sautent sur place.

L'arbaliste pilleur peut apparaître monté sur des bêtes énormes qui détruisent tout sur son passage. Un cavalier saccageur est un adversaire particulièrement dangereux, car il doit esquiver à la fois les flèches et les béliers.

Lors des raids, l'arbaliste pilleur laisse parfois tomber des émeraudes. Les capitaines laissent toujours derrière eux un drapeau de mauvais augure, un trophée précieux et une source de problèmes.

Si l'arbalète d'un pilleur arbaliste se brise (les arbalètes ont une durabilité, comme n'importe quel outil), ils ne peuvent pas tirer et deviennent impuissants, bien qu'ils restent techniquement hostiles.

TIPS: 

- Un bouclier bloque complètement les flèches et vous permet de vous rapprocher sans faire de victimes.
- Ne courez pas directement sur un arbaliste pilleur. Tirez avec un arc ou une arbalète : vos flèches infligent les mêmes dégâts que les leurs.
- Ne vous démarquez pas à l'air libre dans les couloirs étroits - la flèche volera directement dedans.
- Si vous voyez un capitaine avec un drapeau, réfléchissez-y à deux fois avant de le tuer. Le buff "Ill Omen" lancera un raid dès que vous entrerez dans le village.
- Milk supprime le buff "Ill Omen" si vous n'êtes pas encore prêt pour un raid.
- Dans l'eau, l'Arbaliste Pillager est impuissant et passe au combat au corps à corps, qui inflige moins de dégâts.
- Lors des raids, essayez d'abord de tuer l'arbaliste pilleur, ils vous empêchent de vous concentrer sur des ennemis plus forts.
- Ne les laissez pas vous entourer : les flèches des deux côtés sont garanties de toucher.
- Si l'arbaliste pilleur chevauche un ravageur, tuez d'abord l'archer : la bête est moins dangereuse sans cavalier.
