GARDE SANGLANTE DU SPECTACLE DEMI-QUAKE


CLASS: Frémir
METABOLISM: Inconnu
PERCEPTION: Vagabond curieux
SIZE: 5 pieds
ENDURANCE: Faible
HOSTILITY: Aucun
WEAPONS: Aucun
REWARD: 150 FC
THREAT: Aucun

DESCRIPTION: 

Ces petits carquois ont été envoyés par Xen pour... juste se détendre, vraiment. Ils errent en faisant des bruits extraterrestres étranges, ouvrent les portes comme si les lieux leur appartenaient et n'avaient absolument aucun intérêt à attaquer quoi que ce soit. La plupart d’entre eux sont experts dans l’art d’éviter les conflits et d’observer poliment les humains.

TIPS: 

They're the friendliest creatures you'll meet: pas de combat, juste de la curiosité.
Écoutez leurs bruits extraterrestres - c'est leur façon de dire "bonjour".
Ils peuvent ouvrir des portes, alors ne soyez pas surpris si l'on vous aide.
