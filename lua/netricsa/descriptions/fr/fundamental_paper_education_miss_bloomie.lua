Mlle BLOOMIE DE L'ÉDUCATION FONDAMENTALE DU PAPIER


CLASS: Mlle Bloomie
METABOLISM: Organisme de papier (enseignant mutant)
PERCEPTION: Professeur de sciences
SIZE: ~8,2 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Lame de cutter (prothèse du bras gauche), griffes et dents acérées, langue de serpent fourchue
REWARD: 1000FC
THREAT: Haut

DESCRIPTION: 

L'une des trois enseignantes mortelles de Paper School, elle enseigne les sciences (biologie, chimie, physique). Reconnaissable à ses cheveux courts, ses cornes noires de différentes longueurs et son nœud blanc. Sa main gauche est remplacée par une lame de cutter géante et tranchante, et sa main droite a des doigts pointus. Elle possède une langue de serpent fourchue, faisant allusion à une nature prédatrice. Elle est plus réservée et sévère que Miss Circle, affichant moins d'enjouement. Ses attaques sont précises.

TIPS: 

- Sa lame prothétique est une arme pour des attaques rapides et tranchantes. Il est plus long qu’il n’y paraît et peut être utilisé aussi bien pour des attaques de mêlée que de fente.
- Elle travaille habituellement en trio avec Miss Circle et Miss Tavel. Elle est la plus petite des trois, mais non moins dangereuse.
