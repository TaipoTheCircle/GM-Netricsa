BLACK MESA, GÉNÉRATEUR DE CONGÉLATEUR



Sam, vous êtes tombé sur un autre secteur du complexe générateur – cette fois, des rangées de chambres de congélation industrielles, leurs portes fermées par le givre, l'air si froid qu'il vous brûle les poumons. La condensation adhère à toutes les surfaces.

Thermal scans confirm two hostile forces locked in combat: 

Faune Xen - vortigaunts et crabes.

Horde de Mental - Zorgs et décapités, se frayant un chemin à travers le couloir opposé.

Le résultat ? Un champ de bataille gelé où vous êtes pris entre deux feux. Des douilles de munitions glissent sur la glace, des arcs de foudre extraterrestres se dessinent entre les poutres en acier.

Recommendation: laissez-les réduire leurs effectifs, puis frappez lorsque les deux camps saignent. Le froid ralentit le temps de réaction – utilisez-le à votre avantage. Et Sam ? Essayez de ne pas vous brûler au congélateur.
