ZORG POURRI DE BEELMEZ IV


CLASSE : Zorg pourri
MÉTABOLISME : Mammifère
PERCEPTION : Vétéran de la race des guerriers
TAILLE : 6,5 pieds
ENDURANCE : Faible
HOSTILITÉ : Extrême
ARMES : Canon SBC
RÉCOMPENSE : 700 FC
MENACE : Élevée

DESCRIPTIF :

Mental a transporté ces soldats sur Terre depuis une partie inconnue de l'univers.  Ils étaient autrefois une race de guerriers redoutables, mais comme ils ont conquis plus de planètes qu'ils ne savaient quoi faire, ils ont décidé de ralentir pendant un moment.  La plupart de la population a pris des vacances attendues depuis longtemps sur l'une des plus belles planètes tropicales de leur système ; mais comme ils ne prenaient le soleil qu'à partir de 10h du matin. À 16 heures, après quelques années, la plupart d'entre eux n'avaient plus de peau sur le corps.

Au bout de quelques années, les effets sont devenus irréversibles. La peau pourrissait littéralement, les tissus commençaient à se décomposer et les guerriers autrefois puissants étaient réduits à des carcasses ambulantes à moitié pourries.

Tout commandant sensé aurait écarté de tels combattants. Mais Mental n’a jamais été connu pour son bon sens.

Ayant découvert toute une armée de vétérans à moitié décomposés, il décida de les remettre en service et les arma d'un lourd canon SBC. Cette arme puissante tire un énorme boulet de canon capable de détruire pratiquement n'importe quelle cible d'un seul coup. Le seul problème est que les Rotten Zorgs eux-mêmes sont loin d’être parfaits dans le maniement de cette arme. Il n’est pas rare qu’un tir mal placé entraîne la mort du tireur.

Malgré leur état horrible, ces créatures ont conservé l’expérience de combat de dizaines de campagnes militaires. Ils sont lents, facilement blessés mortellement et nettement moins résistants que les mercenaires ordinaires de Mental, mais un seul coup bien ciblé de leur canon peut instantanément changer l'issue d'une bataille.

Ironiquement, ces vétérans déchus sont parmi les soldats les plus âgés de l'armée de Mental. Ils ont participé aux premières conquêtes de la Horde bien avant l'invasion de la Terre et ont été transférés ici depuis un secteur lointain et inconnu de la galaxie.

