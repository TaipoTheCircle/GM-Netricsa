ICHTHYOSAURE DE XEN


CLASS: Ichthyosaure
METABOLISM: prédateur aquatique
PERCEPTION: Prédateur extraterrestre aquatique
SIZE: 15-20 pieds
ENDURANCE: haut
HOSTILITY: extrême
WEAPONS: mâchoires, vitesse, embuscade par le bas
REWARD: 600 FC
THREAT: très élevé (dans l'eau)

DESCRIPTION: 

L'Ichthyosaure est un terrifiant prédateur aquatique ramené de Xen lors de la cascade de résonance. Bien que ressemblant à un requin ou à un reptile muté, il s’agit d’une espèce extraterrestre adaptée au combat sous-marin. Avec ses énormes mâchoires et sa queue puissante, il se déplace à une vitesse effrayante dans son élément, attaquant souvent par le bas avant que la victime ne se rende compte de sa présence. Sur terre, il est impuissant, mais dans l’eau, il est rapide, implacable et presque impossible à échapper. Les survivants de Black Mesa s'en souviennent comme de l'une des créatures les plus redoutées libérées lors de l'incident.

TIPS: 

Évitez les eaux profondes autant que possible.

- Utiliser des explosifs ou un tir rapide au corps à corps est un suicide.
- Rester près de la surface ; la visibilité vous donne une chance de réagir.
