CHAT DU MONDE MINECRAFT


CLASS: Chat
METABOLISM: Mammifère à sang chaud
PERCEPTION: Animal domestique/neutre
SIZE: 2,6 pieds
ENDURANCE: Faible
HOSTILITY: Faible
WEAPONALS: Aucun
REWARD: 10 FC
THREAT: Faible

DESCRIPTION: 

Les chats sont des créatures neutres apprivoisées dans Minecraft Overworld, que l'on trouve le plus souvent dans les jungles, les villages et les huttes de sorcières. Une fois apprivoisés, ils deviennent de fidèles compagnons et vous suivent partout, périodiquement assis, allongés ou se pelotonnant à proximité.

Cats have an innate repellent instinct: Les plantes grimpantes et les fantômes évitent les chats, ce qui les rend non seulement décoratifs mais aussi stratégiquement utiles. Les chats sont disponibles dans une variété de couleurs, du noir et orange au tacheté et siamois.

Un chat apprivoisé peut apporter des cadeaux le matin (des plumes, de la viande pourrie, du fil et d'autres petits objets) et les laisser près de son propriétaire endormi.

TIPS: 

- Utilisez de la morue ou du saumon cru pour l'apprivoiser.
- Gardez les chats près de votre base pour vous protéger des fantômes et des plantes grimpantes.
- Différentes couleurs de chat peuvent être collectées et élevées.
