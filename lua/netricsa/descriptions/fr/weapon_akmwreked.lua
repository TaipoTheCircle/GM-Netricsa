FUSIL D'ASSAUT AK MAISON


WEAPON: AK assemblé à la main
AMMUNITION: 7,62x39mm
ROF: ~600 tours/min

DESCRIPTION: 

Assemblée quelque part dans les sous-sols des séparatistes en Tchétchénie ou quelque part aux États-Unis par un sociopathe local, une Kalachnikov reste une Kalachnikov. Il tire des cartouches de calibre 7,62 mm. Il est efficace à moyenne portée et capable d'effectuer des tirs soutenus pour la suppression ou l'engagement direct.

NOTES: 

- Haute durabilité et facilité d'utilisation.
- Recul modéré ; contrôlable en courtes rafales.
- Efficace contre les cibles non blindées et légèrement blindées.
- La capacité du chargeur nécessite un rechargement périodique lors de combats prolongés.
- Clearly unreliable design.
