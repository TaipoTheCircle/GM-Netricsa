PISTOLET TRANQUILISANT


WEAPON: Beretta M9
MUNITIONS Fléchettes tranquillisantes
ROF: ~10 tours/min

DESCRIPTION: 

Un Beretta M9 modifié pour tirer des fléchettes contenant un agent tranquillisant. Cet article est utilisé par les services spéciaux de divers pays.

NOTES: 

- Si vous souhaitez maîtriser quelqu'un sans le tuer, cette arme est idéale.
- Il est préférable de viser la tête, car lorsque la fléchette touche le cerveau, la substance à l'intérieur agit plus rapidement.


