FUSIL D'ASSAUT AK-74


WEAPON: AK-74
AMMUNITION: Fusil 5,45x39, lance-grenades sous canon de 40 mm.
ROF: 700 tours/min

DESCRIPTION: 

Le fusil d'assaut AK-74 est l'arme principale de l'armée soviétique/russe. Il a été développé par Mikhaïl Timofeevich Kalachnikov en 1970 et adopté par l'armée soviétique en 1974, d'où son nom. Ce fusil d'assaut est réputé pour sa fiabilité et son efficacité ; il se bloque rarement et peut tirer même après avoir été complètement immergé dans la boue. Il a participé à la guerre d'Afghanistan et à la guerre de Tchétchénie et est actuellement utilisé dans le cadre de l'opération militaire spéciale. Il existe une version modifiée avec la lettre M dans sa désignation ; cette version a des caractéristiques différentes et est de couleur noire. Ce fusil d’assaut est également célèbre pour être utilisé un peu partout dans le monde, principalement par des organisations terroristes, mais aussi par les armées de divers pays. Il dispose également d'un lance-grenades sous le canon GP-25 qui tire des grenades de 40 mm.

NOTES: 

- Utilisez-le pour prendre d'assaut des bâtiments ou des tranchées.
- Utilisez le lance-grenades sous le canon pour fumer l'ennemi hors de la couverture.
- Vous pouvez tirer à courte, moyenne et longue portée.
