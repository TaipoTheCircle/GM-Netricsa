BÉBÉ ARAIGNÉE DU MONDE MINECRAFT


CLASS: Bébé araignée
METABOLISM: Arthropode, Arachnide
PERCEPTION: Foule neutre
SIZE: 1,5 pied
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Mordant, Vitesse, Coordination avec les frères et sœurs
REWARD: 50 FC
THREAT: Moyen

DESCRIPTION: 

Cette petite créature extrêmement ennuyeuse suit sa mère partout où elle va. Avec la mère araignée et d'autres frères et sœurs, ils forment une armée d'araignées mobile, prête à détruire quiconque s'approche de leur matriarche.

Tant que leur mère est en vie, les bébés se comportent comme des soldats disciplinés. Ils la suivent sans poser de questions et attaquent n'importe quelle cible de son choix. Ses petits attaquent en groupe et encerclent leurs proies, utilisant leur vitesse et leur petite taille pour les confondre.

Mais le vrai plaisir commence lorsque la mère araignée meurt. Les jeunes, privés de leur chef, sombrent dans le désespoir. Ils se mettent à « pleurer », abandonnent instantanément leur attaque en cours et en perdent tout intérêt. De tueurs impitoyables, ils se transforment en orphelins effrayés. Dans cet état, ils ne sont agressifs qu’en réponse à une agression : s’ils sont frappés, ils se défendront mais n’attaqueront pas.

Les petits n'errent pas seuls, mais n'apparaissent qu'accompagnés de leur mère. De plus, un petit est toujours assis sur le dos de la mère, ce qui la rend encore plus dangereuse et protège les petits des premiers coups.

TIPS: 

- Si tu ne veux pas combattre une bande de petites créatures, essaie de ne pas attaquer les nouveau-nés jusqu'à ce que tu tues la mère. Ils pourraient crier et appeler à l’aide les autres.
- Une fois que vous avez tué la mère, vous n'avez pas besoin d'achever les nouveau-nés : ils cesseront d'être hostiles jusqu'à ce que vous les touchiez. C’est un excellent moyen de conserver les ressources.
- Cependant, n'oubliez pas que si vous décidez de les achever, ils se défendront farouchement.
- En raison de leur petite taille et de leur vitesse élevée, ils sont difficiles à toucher à courte portée. Utilisez des armes avec une large zone d’effet ou attaquez par le haut.
- Lorsque vous combattez une famille d'araignées, essayez de détruire en premier les nouveau-nés (surtout celui sur le dos) pour priver la mère de son soutien.
