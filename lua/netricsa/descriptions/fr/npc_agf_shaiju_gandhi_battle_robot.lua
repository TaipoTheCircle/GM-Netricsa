ROBOT DE COMBAT MARK 1 DE SHAIJU GANDHI


CLASS: Robot de combat MARK 1
METABOLISM: Artificiel (cœur du réacteur)
PERCEPTION: Machine de guerre lourde
SIZE: 10 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Deux miniguns, mode berserk
REWARD: 4560 FC
THREAT: Grave

DESCRIPTION: 

Développé par les ingénieurs de Lord Anton, le robot de combat MARK 1 a été conçu comme gardien de première ligne des bases d'invasion. Son châssis imposant est armé de deux miniguns rotatifs, montés de chaque côté de son châssis blindé. Ces armes libèrent des torrents de balles dévastateurs, déchiquetant l'infanterie et les véhicules.

However, this overwhelming firepower comes at a price: les barils jumeaux sont sujets à la surchauffe après un tir soutenu. Lorsqu'il se produit, la machine entre dans un mode fou furieux - ses systèmes s'enflamment avec des flammes d'énergie, la poussant dans un déchaînement où elle ignore la logique tactique et charge les ennemis de front.

Le MARK 1 est redouté non seulement pour son armement, mais aussi pour sa durabilité et son agressivité implacable. Partout où ces gardiens sont déployés, la survie devient une épreuve du feu et de l’acier.

Il convient également de noter que cette machine est capable d'attraper des adversaires et de les renvoyer à des distances décentes. Il y a un point faible au dos.

TIPS: 

- Use the overheating weakness: forcez-le à tirer continuellement jusqu'à ce que les systèmes échouent.
- Évitez les combats rapprochés lorsqu'il passe en mode berserk.
- Des explosifs lourds ou des armes perforantes sont indispensables pour l'abattre.
