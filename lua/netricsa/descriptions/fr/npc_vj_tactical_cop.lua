COMBINER LA PROTECTION CIVILE TACTIQUE METROCOP


CLASS: Combiner MetroCop tactique de protection civile
METABOLISM: Humain, mâle
PERCEPTION: Combiner l'unité d'assaut de la police
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Pistolet, SMG
REWARD: 450 FC
THREAT: Moyen

DESCRIPTION: 

Le Tactique MetroCop est une unité de transition entre la Protection Civile régulière et les forces paramilitaires du Combine. Il est moins formellement équipé, mais sensiblement plus agressif que les patrouilleurs standards. Un pantalon de camouflage militaire vert l'identifie comme une unité d'assaut de rue, et l'absence de manches souligne l'accent mis sur la mobilité et le combat rapproché plutôt que sur une apparence formelle.

Le gilet pare-balles noir couvre les organes vitaux et contient des amortisseurs intégrés, empêchant une balle dans la tête de provoquer une mort immédiate. Cela fait du Tactical Metrocop un adversaire étonnamment résistant pour ceux qui sont habitués à tout régler d’un seul coup précis.

Il porte l'armement standard d'un pistolet et d'un SMG, mais est plus agressif, réduisant activement la distance et essayant d'être plus nombreux que ses cibles. Il est souvent utilisé pour nettoyer des bâtiments, lutter contre les émeutes et mener des opérations dans des zones qui nécessitent une approche dure, mais toujours « policière ».

TIPS: 

- Ne comptez pas sur les tirs à la tête.
- Dangereux à moyenne et courte portée, surtout en intérieur.
- Mieux vaut l'éliminer à distance ou le neutraliser avec des explosifs.
