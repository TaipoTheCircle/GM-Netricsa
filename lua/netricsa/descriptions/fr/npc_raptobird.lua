RAPTOR-BIRD (RATH-BIRD, LEZARD-BIRD) DE ZENOZOIK


CLASS: Prédateur Zénozoïque
METABOLISM: Reptilien, à sang froid
PERCEPTION: Vision améliorée, réactivité au mouvement
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONALS: Griffes, bec, attaque par saut
REWARD: 400FC
THREAT: Haut

DESCRIPTION: 

Les rapaces font partie des prédateurs les plus dangereux de la forêt de Zenozoik, une région dense et regorgeante. Cette forêt est connue pour ses arbres et plantes exotiques, qui remplissent littéralement tout l'espace, créant des conditions idéales pour les chasseurs furtifs et rapides.

La créature ressemble à un croisement entre un caméléon, un oiseau et un rongeur. Sa tête allongée est recouverte d'une peau épaisse et écailleuse, de texture similaire à celle d'un poisson. De grands yeux aux pupilles rondes lui permettent de détecter instantanément les mouvements parmi le feuillage et les ombres. Des saillies en forme de corne, courbées en demi-cercles, s'étendent au-dessus de sa tête.

Le corps de l'oiseau rapace est couvert d'écailles sombres, se fondant par endroits dans une peau brune. Sa coloration va du brun et du rouge aux taches blanches sales, ce qui la rend presque invisible parmi les racines, la mousse et les arbres de la forêt. Ses membres sont minces, avec de longs doigts et des griffes acérées, idéaux pour grimper, faire des sauts brusques et retenir ses proies.

TIPS: 

- Les fourrés forestiers sont son habitat naturel, pas le vôtre.
- Les coups de pied et les coups forts sont les meilleurs contre lui.
