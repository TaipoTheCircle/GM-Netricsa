PNJ JOUEUR, LE JOUEUR IMITATEUR


CLASS: PNJ Joueur, Le Joueur Mimique
METABOLISM: Les humains
PERCEPTION: PNJ humain / Mimique
SIZE: ~5,9 pieds
ENDURANCE: Moyen (identique au personnage de joueur standard)
HOSTILITY: Variable (copie le comportement du joueur ; peut attaquer si le joueur attaque)
WEAPONS: Reflète l'inventaire du joueur (armes à feu, grenades, mêlée, etc.)
REWARD: 100 FC
THREAT: Élevé (si hostile)

DESCRIPTION: 

Le PNJ Joueur, également connu sous le nom de Mimic Player, est un PNJ très inhabituel qui reproduit toutes les actions du joueur. Qu'il saute, tire ou utilise des objets, ce PNJ reflète les mouvements et les interactions du joueur en temps réel.

Il est conçu comme une expérience sandbox ou un défi de gameplay, créant des scénarios imprévisibles et souvent chaotiques. Au combat, le joueur Mimic peut devenir une menace mortelle s'il reproduit les actions offensives du joueur, retournant ainsi vos propres compétences contre vous.

TIPS: 

- Observez son comportement pour anticiper son prochain mouvement : il reflète toutes vos entrées.
- Peut être utilisé pour résoudre des énigmes ou des scénarios de jeu expérimentaux.
- S'il est armé, il utilisera ses armes aussi efficacement que le joueur.
- Évitez de la provoquer inutilement : l’agression est copiée et dégénère rapidement.
