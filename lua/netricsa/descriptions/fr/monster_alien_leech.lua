SANGSE DE XEN


CLASS: Sangsue
METABOLISM: xénofaune parasitaire
PERCEPTION: Prédateur sous-marin du Xen
SIZE: 1 à 2 pieds de long
ENDURANCE: faible individuellement, moyenne en essaims
HOSTILITY: élevé lorsqu'il est provoqué ou en groupe
WEAPONS: morsure, succion, tactiques d'essaim
REWARD: 150 FC
THREAT: faible individuellement, modéré en groupe

DESCRIPTION: 

Les sangsues sont de petits prédateurs Xen ressemblant à des vers, essentiellement des ventouses vivantes avec des dents. Ils se cachent dans des flaques de liquide Xen, attendant que des proies sans méfiance s'approchent. Une fois attachés, ils s’accrochent avec une force horrible et commencent à drainer de l’énergie – une véritable sangsue vivante.

Malgré leur taille, les essaims de sangsues peuvent immobiliser des créatures plus grandes grâce à leur grand nombre, ce qui les rend étonnamment dangereuses. Leurs corps gélatineux et glissants et leurs mouvements frétillants les rendent difficiles à toucher, et leurs attaques soudaines sont un cauchemar pour les explorateurs qui s'approchent trop près des environnements aquatiques de Xen.

TIPS: 

- Gardez vos distances ; la mêlée est risquée.
- Évitez de rester dans l'eau Xen ou dans les surfaces visqueuses où se cachent les sangsues.
- Les explosifs ou les tirs nourris peuvent disperser rapidement les essaims.
