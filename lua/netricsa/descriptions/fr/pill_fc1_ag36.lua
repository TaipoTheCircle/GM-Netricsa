FUSIL D'ASSAUT G36 WTCH AG36 LANCE-GRENADE SOUS BARIL


WEAPON: G36 avec module AG36
AMMO: Grenades à basse vitesse 5,56 x 45 mm OTAN (primaire) / 40 x 46 mm (sous le canon)
ROF: ~750 tours/min

DESCRIPTION: 

Le package G36 + AG36 est une plate-forme de fusil d'assaut moderne équipée d'un lance-grenades sous le canon de 40 mm. Le G36 offre un tir automatique et semi-automatique fiable avec une bonne contrôlabilité et précision à portée intermédiaire. L'AG36 ajoute un effet de zone explosive pour dégager une couverture, refuser de l'espace ou engager des véhicules légers, le tout sans changer d'arme principale.

NOTES: 

- Versatile loadout: tirs de fusil de précision/suppression (G36) combinés à une capacité explosive de zone (AG36).
- AG36 tire un projectile en arc - tenez compte des chutes à moyenne/longue portée.
- Méfiez-vous des auto-mutilations dans des espaces confinés lors du tir de grenades ; utiliser à partir du couvercle.
- Le montage modulaire permet un retrait rapide de l'AG36 pour revenir au fonctionnement autonome du fusil.
- L'optique inclut souvent des marquages ​​ou des solutions de visée pour le lance-grenades afin de faciliter les engagements de 50 à 400 m.
