PISTOLET FOUDRE


WEAPON: Pistolet éclair
AMMO: Charges énergétiques
ROF: ~550 tours/min


DESCRIPTION: 

Le Lightning Gun tire des arcs de foudre continus qui transpercent plusieurs cibles en ligne droite, infligeant des dégâts soutenus au fil du temps. Excellent pour engager des ennemis dans de longs couloirs ou des formations regroupées. Le faisceau apparaît comme une décharge électrique reliant le tireur et la cible.

NOTES: 

- Capable de percer plusieurs cibles le long d'une ligne de vue.
- Efficacité diminuée par les obstacles et les blocages de la ligne de vue.
- La consommation d'énergie est élevée - des tirs inutiles dans un espace vide drainent les réserves.
- Mieux utilisé à partir de positions préparées avec un minimum de mouvements latéraux des ennemis.
