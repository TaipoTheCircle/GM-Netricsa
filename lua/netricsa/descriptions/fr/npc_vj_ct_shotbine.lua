DESCRIPTION: 

Ces soldats étaient autrefois de véritables Navy SEALs des États-Unis, maîtres des raids surprises et des opérations de sauvetage dans les zones les plus dangereuses du monde.

Ils opéraient au sein d'une équipe capable de contourner toute sécurité et d'éliminer une cible derrière les lignes ennemies, risquant parfois leur vie pour quelques minutes d'avantage stratégique.

Ce soldat était à l'origine un expert en assaut pour la SEAL Team 6, chargé de nettoyer les bâtiments, d'attaquer les installations fortifiées et de travailler dans des couloirs étroits.

Il aimait les solutions bruyantes : un fusil de chasse était son meilleur ami.

Après avoir été transféré au Combine, il a reçu une combinaison de combat rapproché marron et un casque orange pour le distinguer visuellement des soldats standard d'Overwatch.

