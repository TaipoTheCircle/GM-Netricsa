ROBOT SIRIEN, PATROUILLE DU SIRIUS


CLASS: Robot Sirien, Patrouille
METABOLISM: Mécanique, Energie
PERCEPTION: Unité de garde, patrouille
SIZE: ~6,56 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Canon laser et caméra
REWARD: 1500FC
THREAT: Haut

DESCRIPTION: 

Il s'agit d'un garde automatisé que Mental a chargé de patrouiller dans les niveaux inférieurs de Siriuspolis pour s'assurer que personne n'erre là où il ne devrait pas.

Il a un corps gris avec des accents rouge vif pour que l'ennemi puisse le repérer de loin et avoir le temps de se chier. Les bandes de roulement grises sur lesquelles il roule émettent un cliquetis métallique distinctif qui peut faire claquer des dents d'un combattant non préparé.

La tête du robot est ovale, du même gris que son corps principal, mais avec des yeux de capteur rouges qui scrutent l'espace à la recherche de créatures vivantes. Au sommet de sa tête se trouvent une antenne et une crête de pointes acérées, uniquement à des fins d'intimidation, car le robot ne peut percer personne avec sa tête. Son cou est segmenté, presque comme un ressort, lui permettant de tourner la tête n'importe où.

La caméra bleue sur le côté de sa tête n’est pas seulement une caméra. C'est un canon. Il transmet les images directement au centre de commandement de l'armée de Mental. Il tire également des rayons laser. Le robot vise d'abord sa cible avec un petit pointeur laser, puis tire avec son canon principal.

TIPS: 

- Extrêmement dangereux.
- Ne vous tenez pas dans sa ligne de mire lorsqu'il tire.
- Contrairement à de nombreux alliés de Mental, celui-ci n'est pas un tas de briques.
