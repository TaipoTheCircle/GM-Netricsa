TORSE DE ZOMBIE DE CRABE RAPIDE


CLASS: Zombie Headcrab, Torse Rapide
METABOLISM: Hôte parasite
PERCEPTION: Entité zombifiée
SIZE: 2,5 pieds
ENDURANCE: Très rapide
HOSTILITY: Extrême
WEAPONS: Coups de griffes, sauts frénétiques, morsures
REWARD: 120 FC
THREAT: faible-moyen

DESCRIPTION: 

Lorsqu'un Fast Zombie est tué au combat, le cauchemar ne s'arrête pas. Le crabe refuse de céder le contrôle, forçant le torse mutilé de son hôte à ramper avec une agilité effroyable. Ces demi-formes conservent la vitesse et la frénésie de leurs congénères corsés, se lançant en avant avec des cris aigus et effrayants. Bien que fragiles et faciles à éliminer, leurs attaques erratiques dans des espaces confinés peuvent surprendre même les combattants chevronnés.

TIPS: 

- Restez mobile : ils peuvent toujours tuer malgré l'absence de membres.
- Ne gaspillez pas de lourdes munitions ; un ou deux coups suffisent.
- Les cris annoncent leur présence-utilisez ça pour vous préparer.
