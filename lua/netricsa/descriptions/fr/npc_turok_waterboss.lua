BÊTE D'EAU DE LA PLANÈTE TERRAFORMÉE


CLASS: Bête d'eau
METABOLISM: Prédateur des grands fonds
PERCEPTION: Ancien monstre souterrain
SIZE: 60 pieds (partie proéminente)
ENDURANCE: Extrême
HOSTILITY: Absolu
WEAPONS: Tentacules, jets de pierres, stalactites
REWARD: 5000FC
THREAT: Absolu

DESCRIPTION: 

MARIO ROMAIN "PABLO"

En apparence, la bête est un hybride d'anguille, de poulpe et de dunkleosteus.

Au combat, la bête est immobile : elle est liée à son antre sous-marin et ne peut pas quitter l'eau. Mais cela ne le rend pas moins dangereux. Son attaque principale est une fente vers l'avant, tentant de dévorer sa cible. Entre les attaques, la bête se déchaîne avec ses tentacules, arrache les rochers du sol et les jette, et arrache les stalactites du plafond. Cette dernière est la plus meurtrière.

A chaque explosion réussie, la bête saignera de plus en plus jusqu'à périr complètement.

TIPS: 

- Ne reste pas immobile.
- Il y a une pause entre les attaques : utilisez-la pour recharger et reprendre votre souffle.
- Utilisez des explosifs.
