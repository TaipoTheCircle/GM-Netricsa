MOUTON DU MONDE MINECRAFT AETHER


CLASSE : Mouton
MÉTABOLISME : biologique, flottant
PERCEPTION : Animal flottant paisible
TAILLE : 4 pieds
ENDURANCE : Faible
HOSTILITÉ : Aucune
ARMES : Aucune
RÉCOMPENSE : 50 FC
MENACE : Zéro

DESCRIPTIF :

Ces créatures à fourrure habitent des îles flottantes dans le ciel et comptent parmi les créatures les plus paisibles de ce monde étrange.

Ils ressemblent à des moutons ordinaires, mais leur laine semble plus moelleuse et plus légère, comme si elle était tissée à partir de nuages. Leur couleur peut varier : blanc, bleu, rose, citron vert et cyan, les moutons colorés étant beaucoup plus rares que les moutons blancs.

C'est la principale différence avec les moutons ordinaires. Ils peuvent « gonfler » et se lancer haut dans les airs (comme une fusée), puis glisser lentement vers le bas. Cela les fait parfois tomber de l'île et s'écraser.

Ils broutent l'herbe des nuages, la mangent et la transforment en terre.

Ils peuvent être cultivés avec du blé, des oranges ou des myrtilles.

Lorsqu'ils sont tondus avec des cisailles, ils laissent tomber Cloud Wool, un matériau spécial de cette dimension. Une fois tondu, un mouton perd sa capacité à voler jusqu'à ce qu'il fasse repousser sa laine en mangeant de l'herbe.

CONSEILS :

- Pour obtenir beaucoup de Cloud Wool, rassemblez les moutons dans un enclos avec de l'herbe et tondez-les toutes les quelques minutes.
- Une fois qu'un mouton est rasé, il ne peut plus voler : c'est le moment idéal pour le mettre en sécurité.
- Contrairement aux moutons ordinaires, cette espèce peut être une source précieuse de ressources pour fabriquer des objets de la dimension éthérée.
- N'essayez pas de les utiliser pour le transport. Ils sont incontrôlables.