L'EMPIRE STROG


Les Stroggs sont une race cybernétique impitoyable. Leur culture est la guerre ; ils ne naissent pas comme des êtres normaux.

La stroggification est un processus violent et impitoyable qui transforme les humains captifs et d'autres espèces en un mélange chimérique de viande, d'implants et d'agression.

Leur monde natal, Stroggos, est ravagé par des usines et des usines industrielles, avec une atmosphère extrêmement polluée, où chaque être – soldat, médecin ou artisan – participe à une vaste machine de guerre qui produit des copies de lui-même.

Leur objectif est de récolter des composants organiques – tissus, organes, ADN – pour servir de « matière première » dans leurs transformations cybernétiques et la réalisation de leur vision de la forme ultime de vie biomécanique. Leur technologie est impitoyable, chirurgicale et inébranlable ; ils détruiront n’importe quelle planète s’ils trouvent ce dont ils ont besoin.
