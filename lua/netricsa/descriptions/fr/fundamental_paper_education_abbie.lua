ABBIE DE L'ÉDUCATION FONDAMENTALE DU PAPIER


CLASS: Abbie
METABOLISM: Organisme anthropomorphe en papier
PERCEPTION: Étudiant en école de papier
SIZE: ~5 pieds
ENDURANCE: ~3,2 pieds
HOSTILITY: Aucun
WEAPONS: Aucun
REWARD: Je VOUS retrouverai personnellement si vous posez le doigt sur HIM FC
THREAT: Aucun

DESCRIPTION: 

A 15-year-old Paper School student, but also timid, anxious, and not the brightest. His distinguishing feature is an apple stalk with a leaf on top. He wears a strict school uniform: une chemise blanche, un gilet de smoking noir avec une cravate et un short blanc. Ses yeux ne sont que de petites fentes noires. Canoniquement, il a été brutalement assassiné par son professeur, Miss Circling, pour avoir échoué à un examen, mais dans un scénario alternatif, il peut être considéré comme vivant. Il a un taux de survie extrêmement faible en raison de sa nature papier ; il se déchire et se brise facilement. Il éprouve des sentiments tendres pour sa camarade de classe, Lana. Il ne représente aucune menace, étant victime des circonstances et de l'ambiance de l'école.

TIPS: 

- Vulnérable à tout impact physique, explosion et incendie.
- En cas de danger, il tremblera, se retirera et tentera de s'échapper plutôt que de résister.
- Très sensible aux critiques et à la pression.
