DESCRIPTION: 

Generic Actors are mysterious xenobiological entities that hide beneath familiar faces. No one knows where they come from - some speculate they originated in Xen, others say they're a byproduct of the resonance cascade - but one thing is certain: ils peuvent parfaitement imiter n’importe quel être.

Ils apparaissent comme des scientifiques, des gardes de sécurité ou même des monstres, se fondant parfaitement dans les populations de Black Mesa ou de Xen. Bien que leur apparence extérieure puisse sembler inoffensive, ils sont imprévisibles et dangereux, utilisant souvent les capacités que leur apparence actuelle leur confère.

Leur véritable forme est inconnue ; Certains chercheurs suggèrent qu'il pourrait s'agir d'une masse informe de tissu psionique vivant, capable de scanner et de reproduire des modèles biologiques et comportementaux. En rencontrer un, c’est comme serrer la main d’un étranger qui pourrait mordre.

TIPS: 

- Ne vous fiez pas aux apparences : n'importe quel visage familier pourrait être un acteur générique.
- Observer attentivement les comportements ; des incohérences peuvent révéler le déguisement.
- Soyez prêt à faire face à des attaques soudaines provenant de sources inattendues.
