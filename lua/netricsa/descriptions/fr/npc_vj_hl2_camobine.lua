COMBINER LE SOLDAT OVERWATCH Ghillie


CLASS: Combinez le flic Blacklight de la protection civile
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combinez une unité de reconnaissance à longue portée et de tireur d'élite
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrême
WEAPONS: Fusil de précision Overwatch MK2, fusil à impulsion AR2
REWARD: 1600 FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine Overwatch Ghillie Soldier est une ombre que vous ne remarquez que lorsque vous faites une erreur. Le Combine utilise ces soldats pour traquer les commandants de la Résistance, surveiller les itinéraires clés et supprimer discrètement les activités dans les zones où la présence manifeste d’Overwatch n’est pas souhaitable.

Contrairement à la plupart des soldats du Combine, il lui manque une visière lumineuse. Au lieu de cela, il a des lentilles optiques qui obscurcissent sa ligne de vue et l'empêchent d'être démasqué. Son armure est entièrement recouverte de buissons artificiels, d'herbe et de fibres d'une combinaison ghillie adaptée à l'environnement. De loin, il ressemble à un morceau de paysage qui a soudainement décidé de tirer.

Son arme principale est le fusil de sniper Overwatch MK2, capable d'éliminer une cible avant même qu'elle ne se rende compte qu'elle est en danger. L'AR2 est utilisé comme arme secondaire en cas de détection ou de combat rapproché, même si à ce moment-là, il préfère se repositionner plutôt que de tenter de jouer le héros.

TIPS: 

- Ne vous attardez jamais à l'air libre, c'est ce qu'il attend.
- Changez de position après le premier tir.
- Les explosifs et le feu sont efficaces contre le camouflage, même si vous n'êtes pas sûr de sa présence.
