GRAND PATRICK STAR DU BAS DE BIKINI


CLASS: Grand Patrick Étoile
METABOLISM: Organisme d'étoile de mer
PERCEPTION: Une étoile de mer morte et vivante, dangereuse pour les autres
SIZE: 8,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Très élevé
WEAPONS: Tête en forme de club
REWARD: 5000FC
THREAT: Haut

DESCRIPTION: 

Big Patrick Star est le fidèle ami et collègue de Bob l'éponge, connu pour son incroyable stupidité, ce qui le rend mortel. Il se nourrit de Krabby Patties, à base de poisson, et attaque avec son arme principale : sa tête, qu'il utilise pour écraser tout ce qu'il voit.

Il n'a pas d'yeux, mais il peut se "téléporter" sur de courtes distances lorsque sa victime se détourne, à l'instar de SCP-173. Sa bouche est énorme et ovale, remplie de dents, et du sang violet jaillit à chaque coup. Il n'a pas de pantalon. Malgré son apparente immobilité, sa téléportation le rend extrêmement imprévisible et mortel.

TIPS: 

- Maintenez un contact visuel : dès que vous vous détournez, Patrick se rapproche instantanément.
- Le combat à distance est votre meilleure option ; éviter les contacts directs.
- Planifiez vos manœuvres et surveillez-le constamment pour éviter de vous retrouver dans sa zone de destruction.
- La bêtise de Patrick est mortelle - ne le sous-estimez pas.
