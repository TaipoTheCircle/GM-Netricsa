ANTI-DOULEUR


ITEM: Analgésiques (comprimés consommables)
USE TIME: bénéfice intégral délivré

DESCRIPTION: 

When taken, painkillers provide rapid health recovery: une partie de la santé est restaurée instantanément. Destiné à un traitement d'urgence sur le terrain lorsqu'une trousse médicale n'est pas disponible. Ils ne réparent pas les membres perdus ou les traumatismes internes graves ; ils fonctionnent comme un soutien médical temporaire.

NOTES: 

- Guérison instantanée.
- Ne guérissez pas les intoxications / infections / radiations - médicaments spécialisés requis.
