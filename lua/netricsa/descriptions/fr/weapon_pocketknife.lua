COUTEAU DE POCHE


WEAPON: Couteau de poche

DESCRIPTION: 

Un couteau de poche standard. Conçu pour être utilisé dans diverses situations où un couteau est nécessaire, y compris lors de combats.

NOTES: 

- Appuyer sur LMB + R permet de changer le type d'attaque.
- En mode coupe, il est recommandé de viser les membres pour provoquer des saignements artériels.
- Un appui RMB sur un article sécurisé avec du scotch permet de commencer à le dénouer.
