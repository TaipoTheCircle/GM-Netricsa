GARDE-FUSIL STROGG DE STROGGOS


CLASS: Variante de la Garde Strogg
METABOLISM: Lanceur de missiles (à gauche "manipulateur"), Energy Wave Scream, Close Combat "Claw"
PERCEPTION: Ciblage visuel + suivi de mouvement
SIZE: ~6 pieds
ENDURANCE: Moyen-Faible
HOSTILITY: Haut
WEAPONS: Mitrailleuse intégrée (bras droit), tir de balle rapide
REWARD: 500FC
THREAT: Moyen

DESCRIPTION: 

Le Shotgun Guard est un soldat Strogg légèrement blindé équipé d'un fusil de chasse greffé directement sur son bras droit. Conçues pour le combat rapproché, ces unités comptent parmi les troupes de première ligne les plus agressives rencontrées.

Ils se précipitent sur leurs ennemis avec une détermination imprudente, tirant des rafales de chevrotine à bout portant. Malgré leurs tactiques simples, un groupe de Shotgun Guards peut submerger des cibles non préparées grâce à leur proximité et à leur cadence de tir.

TIPS: 

- Gardez vos distances - leur fusil de chasse est mortel de près.
- Utilisez des explosifs ou des armes automatiques pour les abattre avant qu'ils ne se rapprochent.
