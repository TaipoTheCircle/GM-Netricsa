COMBINEZ LE SHOTGUNNER DE LA PRISON D'OVERWATCH


CLASS: Combinez le tireur de prison Overwatch
METABOLISM: Humain augmenté, mâle
PERCEPTION: Infanterie transhumaine (application des peines - unité de fusil de chasse)
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Fusil de chasse SPAS-12
REWARD: 400FC
THREAT: Haut

DESCRIPTION: 

Le soldat de la prison Combine Overwatch Shotgun sert de puissant exécuteur de Nova Prospekt. Contrairement aux soldats de prison standards, ces unités sont équipées de fusils de chasse pour réprimer brutalement les soulèvements, les brèches dans les blocs cellulaires ou les tentatives d'évasion. Leurs tactiques se concentrent sur la peur et une puissance de feu écrasante à courte portée, chargeant souvent dans des couloirs étroits pour faire exploser des cibles à bout portant.

L'utilisation de fusils de chasse dans les prisons est délibérée : leur pouvoir destructeur en espace restreint garantit que les prisonniers survivent rarement aux tentatives d'évasion. Leur armure plus légère, comparée à celle des soldats fusils de chasse de première ligne, les rend plus agiles dans les couloirs confinés de Nova Prospekt, bien qu'ils restent bien plus résistants que l'infanterie standard.

Fidèles au Combine, ces soldats agissent comme des bourreaux lors des émeutes et comme des troupes de choc lors du « traitement » massif des prisonniers. En rencontrer un dans les couloirs sombres de la prison signifie généralement que la mort est à quelques secondes.

TIPS: 

- Gardez vos distances : leurs fusils de chasse sont dévastateurs de près.
- Utilisez des explosifs ou des tirs rapides pour éclaircir leurs escouades avant qu'elles ne se rapprochent.
- Les fournitures sont parfois stockées dans des armureries à proximité des itinéraires de patrouille des unités de fusils de chasse.
