SERGENT STORMTROOPER DU KAMINO


CLASSE : Sergent Stormtrooper
MÉTABOLISME : humain cybernétique
PERCEPTION : Commandant d'élite, main gauche de Dark Vador
TAILLE : 6,5 pieds
ENDURANCE : Élevée
HOSTILITÉ : Absolue
ARMES : Minigun, grenades, RPG, canon laser, systèmes expérimentaux, bouclier énergétique, robots
RÉCOMPENSE : 10000 FC
MENACE : Absolue

DESCRIPTIF :

Ce guerrier cybernétique d'élite est l'assistant de confiance de Dark Vador. Il combine les talents d'un officier de terrain, d'un ingénieur hautement qualifié et d'un mercenaire. Il conçoit personnellement des armes expérimentales, qu'il appelle ses « jouets ».

Sa carrure puissante et athlétique est renforcée par une armure assistée massive en métal sombre. Les plaques angulaires, les pointes et les segments mobiles de l'armure offrent non seulement une formidable protection, mais servent également d'excellent camouflage la nuit. L'identité du sergent reste un mystère : son visage est solidement dissimulé sous un casque high-tech à visière transparente, équipé de modules de capteurs supplémentaires.

L'arsenal du Sergent est impressionnant par son potentiel destructeur. Ses armes principales sont un minigun à plusieurs canons et de puissants systèmes d'assaut, complétés par des grenades et des RPG. Au combat, il fait preuve d'une agressivité débridée, commandant des escouades d'infanterie et déployant ses propres robots sur les lignes de front. Sa supériorité technologique est soulignée par l'utilisation de boucliers énergétiques avancés et d'un certain nombre de systèmes secrets, faisant de lui un adversaire imprévisible.

Il est officiellement répertorié comme « la main gauche de Vador », mais la moitié de ses mises à niveau ont été installées illégalement.

Son casque n’a jamais été retiré en public – même les médecins ne peuvent le voir qu’à travers des capteurs.

CONSEILS :

- N'essayez pas d'attaquer le Sergent de front. Ses robots et son minigun vous transformeront en passoire.
- Le Sergent coordonne les escouades de stormtroopers.
- Ne vous approchez pas de ses « jouets », ses robots de combat auto-assemblés.
- La mobilité est la clé dans un duel avec lui. Il est lent en raison de son armure lourde, mais ses armes couvrent une large zone.