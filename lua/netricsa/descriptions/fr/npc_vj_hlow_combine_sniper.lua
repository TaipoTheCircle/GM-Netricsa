COMBINEZ UN SNIPER SUPÉRIEUR D'OVERWATCH


CLASS: Combinez Overwatch Superior Sniper
METABOLISM: Humain augmenté, mâle
PERCEPTION: Unité de reconnaissance et de frappe à longue portée
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Combinez un fusil de précision à lunette et à action de phase
REWARD: 700FC
THREAT: Très élevé

DESCRIPTION: 

L'Overwatch Superior Sniper est une version expérimentale du Combine Sniper standard, conçu pour une efficacité maximale à toutes les distances. Son casque blanc est équipé de deux visières optiques bleues situées au centre. La première visière est optimisée pour le combat rapproché et les portées urbaines, tandis que la seconde est destinée à l'observation à très longue portée et au tir à des distances extrêmes.

L'armure du soldat est légère, bleue et blanche, ce qui réduit la protection mais augmente la mobilité et la vitesse. Le Combine estime que le meilleur tireur d'élite est celui sur lequel on ne peut pas tirer dessus à temps.

La principale caractéristique de l'arme est son faisceau de visée énergétique. En mode normal, il est bleu, mais une fraction de seconde avant le tir, il passe au rouge, signalant la phase critique de ciblage.

TIPS: 

- Si le faisceau devient rouge, changez immédiatement de position.
- Son armure est faible, mais sa précision est parfaite.
- Ne vous tenez jamais dans des zones ouvertes s'il se trouve à proximité.
