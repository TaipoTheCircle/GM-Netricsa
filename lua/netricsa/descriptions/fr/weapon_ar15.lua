FUSIL SEMI-AUTOMATIQUE AR-15


WEAPON: AR-15
AMMUNITION: 5,56x45mm
ROF: ~950 tours/min

DESCRIPTION: 

AR-15 est le nom d'une famille de fusils semi-automatiques basés sur le modèle Colt AR-15 ou similaire. À l'origine, l'ArmaLite AR-15, son prédécesseur, était un dérivé réduit de la conception ArmaLite AR-10 d'Eugene Stoner et possédait un sélecteur de tir. ArmaLite a vendu le brevet et les marques à Colt en 1959, ce qui a donné naissance au Colt AR-15, sans sélecteur de tir. Après l'expiration de la plupart des brevets du Colt AR-15 en 1977, de nombreux fabricants d'armes à feu ont commencé à produire des copies de ce fusil sous différents noms. Jusqu'à l'expiration des brevets, Colt a conservé la marque AR-15 et était le seul fabricant à pouvoir étiqueter ses armes à feu comme « AR-15 ».

NOTES: 

- Idéal pour le tir défensif ou récréatif.
- Le fusil le plus répandu aux États-Unis.
- L'oreille de Trump a été touchée par ce fusil.
