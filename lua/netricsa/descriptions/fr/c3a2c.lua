NOYAU DU RÉACTEUR NOIR MESA, LES PORTAILS


Le hall du réacteur n'est plus un moteur, c'est une faille. Les portails s'épanouissent comme des tumeurs malignes, des arcs crépitants d'énergie se tissant. Les scientifiques que vous devez atteindre sont juste au-delà, mais les chemins directs ont disparu ; seuls les portails instables relient ce secteur au leur.

And here's the problem: entrez dans le mauvais et vous serez craché dans un évent de rayonnement, un puits qui s'effondre, ou pire - rien du tout. L'analyse télémétrique indique une combinaison sûre : 2 → 1 → 7. Suivez cette séquence et vous nettoyerez le labyrinthe.

Points of interest: 

Instabilité du portail. Attendez trop longtemps et vous perdrez votre chance - ou pire, vous atterrirez quelque part au hasard.

L'Observateur. Standing near one of the active portals is the man in the business suit. Toujours silencieux, toujours à l'affût. He doesn't interfere, doesn't react to threats, but his presence near is no accident. C'est soit un avertissement... soit une confirmation.
