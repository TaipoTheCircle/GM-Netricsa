NEMESIS DE LA SOCIÉTÉ UMBRELLA


CLASS: Arme biologique (B.O.W.), Tyrant de type T
METABOLISM: Organisme muté infecté par le parasite NE-α
PERCEPTION: Assassin tactique, chasseur d'élite
SIZE: 8,2 pieds
ENDURANCE: Extrême
HOSTILITY: Absolu
WEAPONS: Lance-roquettes, minigun M-134, lance-flammes, tentacules, poings, objets environnementaux
REWARD: 10000FC
THREAT: Absolu

DESCRIPTION: 

This is no ordinary bioweapon. It is a living nightmare, created by the European branch of Umbrella Corporation with one sole purpose: pour éliminer tous les membres survivants de l'élite S.T.A.R.S. équipe chargée de dénoncer les crimes de la société.

Nemesis est le résultat de l'infection d'un Tyrant T-103, le même modèle que M. X, avec un parasite expérimental appelé NE-α (Nemesis Alpha). Le parasite se développe littéralement dans le système nerveux de l'hôte, formant son propre cerveau et prenant le contrôle du corps. Cela confère au monstre non seulement une force surhumaine, mais également la capacité de prendre des décisions, de suivre des ordres complexes et, plus terrifiant encore, d'utiliser des armes.

En raison de la difficulté d’intégration du parasite à l’hôte, le taux de réussite était négligeable – environ un spécimen réussi sur 60 millions de candidats. Chacun de ces spécimens est incroyablement précieux pour Umbrella.

Dans sa forme de base, Nemesis est une gigantesque carcasse humanoïde musclée, vêtue d'un long manteau noir et imperméable. Le manteau faisait à l'origine partie de leur équipement standard, conçu pour inhiber les mutations et se protéger contre les balles.

Son visage est dépourvu de lèvres et de dents dénudées et les muscles sont visibles sous la peau tendue. Son œil droit est fermé par des points de suture chirurgicaux grossiers. Son corps est couvert de cicatrices et de marques de dissection. Il porte un pantalon noir, des bottes et des gants, et sur le côté gauche de sa poitrine se trouve un dispositif de retenue métallique visible, un dispositif qui empêche son parasite de muter de manière incontrôlable.

When his coat is damaged, Nemesis mutates: de longs et puissants tentacules commencent à sortir de son corps, qu'il utilise pour attraper ses victimes, les poignarder et même se déplacer rapidement en s'accrochant aux murs des bâtiments.

La principale caractéristique qui distingue Nemesis des autres Tyrans est son intelligence, qui lui permet de manipuler des armes complexes.

Son arme emblématique est un lance-roquettes lourd ou un lance-grenades à plusieurs canons monté sur son bras gauche. Nemesis fait preuve d'une précision incroyable, frappant facilement des cibles en mouvement à de grandes distances.

Nemesis utilise également un puissant lance-flammes à dos, capable de cracher des jets de flammes et de lancer des boules de feu. Même après la destruction du réservoir de carburant, il continue de l'utiliser comme une massue enflammée.

La mitrailleuse à six canons que Nemesis utilise lui permet de faucher des foules entières d'ennemis.

Même désarmé, Nemesis est mortel. Ses coups peuvent percer des murs en béton et arracher des morceaux d'asphalte. D'un seul coup, il peut envoyer une personne voler ou lui briser le cou.

Les tentacules de Nemesis font bien plus qu'infliger des dégâts physiques. Lorsqu'elle est injectée, la victime est infectée par le virus T, provoquant une mutation ou une mort rapide. Il peut également implanter le parasite NE-α dans les corps des défunts, les transformant en horribles monstres contrôlables.

Nemesis est un véritable monstre dans tous les sens du terme.

Il est capable de percer des murs en béton d'un seul coup, d'arracher les portes de leurs gonds, de projeter des voitures et de briser les sols des immeubles.

Il peut courir à des vitesses supérieures à 100 km/h (63 mph), esquivant instantanément les tirs de grenades et de lance-roquettes.

Son principal avantage est la régénération. Les balles, les éclats d’obus et les brûlures guérissent sous ses yeux. Nemesis peut être temporairement neutralisé par une explosion ou une arme lourde, mais il se relèvera rapidement et poursuivra la poursuite.

Il peut résister aux tirs de lance-grenades, aux explosions de réservoirs de carburant, aux chutes de grande hauteur et aux aspersion d'acide et de métal en fusion.

Nemesis n’est pas une machine à tuer stupide. Il a un système opérationnel et des priorités. Sa principale et unique cible est le S.T.A.R.S. équipe.

Toute sa stratégie est axée sur le traçage et la destruction. Il ne reculera pas jusqu'à ce que sa cible soit morte ou qu'il soit lui-même détruit.

Nemesis est capable d'évoluer à la volée, en passant par plusieurs étapes terrifiantes.

First Form: Un Nemesis basique en manteau, armé d'un lance-roquettes/lance-flammes. Intelligent, rapide et dangereux.

Second Form: Après avoir détruit son manteau et subi des dégâts critiques, Nemesis mute en une créature encore plus monstrueuse. Il se met à quatre pattes, devient comme une bête gigantesque, se couvre d'excroissances osseuses et développe de longs tentacules en forme de fouet.

Third Form (Final): Le corps de Nemesis perd finalement le contrôle. Il se transforme en une énorme masse amorphe de chair, d'os et de tentacules, se déplaçant dans un mouvement rampant. Sous cette forme, il est pratiquement immunisé contre les armes conventionnelles, continue de poursuivre sa cible et crache des nuages ​​d'acide toxique.

TIPS: 

- Tirez et courez. Nemesis est (presque) impossible à tuer avec des armes conventionnelles. Votre objectif est de le désactiver temporairement pour obtenir un avantage et vous échapper.
- Utiliser l'environnement. Des barils explosifs, des générateurs, des portes, des antennes largables : tout cela peut le ralentir.
- Ne reste pas immobile. Il sait toujours où vous êtes. Tout arrêt signifie la mort.
- Esquivez les missiles. Un coup direct de son lance-roquettes tue instantanément, même en armure complète.
- Surveillez vos armes. Le lance-roquettes est une menace à longue portée, le lance-flammes est une menace à moyenne portée. S'il court avec ses poings, vous devez augmenter votre distance.
- N'oubliez pas les tentacules. S'il est proche, il peut vous attraper avec un tentacule même s'il ne vous touche pas avec sa main.
