MANCUBUS, BEHEMOTH DE L'ENFER


CLASS: Mancubus, Béhémoth
METABOLISM: Mammifère démoniaque
DEFINITION: Personnification affaiblie de la gourmandise
SIZE: 13 pieds
ENDURANCE: Faible
HOSTILITY: Haut
WEAPONS: Deux RPG
REWARD: 1000FC
THREAT: Moyen

DESCRIPTION: 

Il s’agit d’un Démon affaibli, personnifiant l’imperfection humaine, l’avidité, la gourmandise et l’oisiveté. Il a une peau incroyablement épaisse, des pattes éléphantines massives et une petite tête. Dans le but de détruire et de faire exploser ses victimes, deux RPG sont implantés dans les avant-bras du Mancubus, tirant des roquettes.

Mental les a achetés au Seigneur de l'Enfer, ainsi que des soldats zombies et d'autres morts-vivants, pour renforcer sa Horde.

Les yeux du Behemoth ne sont pas verts comme ceux des Mancubus ordinaires, mais rouge vif, brûlant du feu de la haine.

Sa peau a une teinte bleue, mais au lieu du sang rouge, une substance verte toxique coule de ses veines, signe certain d'un rituel raté.

Au lieu de lance-flammes intégrés, le Behemoth a des lance-roquettes attachés à ses coudes, remplaçant ses membres. Contrairement au Mancubus ordinaire, qui tire des boules de feu, le Behemoth tire deux roquettes à la fois. Ces roquettes volent jusqu'à ce que la cible soit détruite.

Le Behemoth lui-même est nettement plus faible en défense que le Mancubus classique. Quelques coups de fusil de chasse peuvent le renvoyer en enfer.

TIPS: 

- Malgré sa taille, le Behemoth n'est pas particulièrement résistant.
- Deux tirs à bout portant et le démon ne sera plus qu'une tache humide.
- Évitez les missiles et ne restez pas à découvert.
