UGH-ZAN "?", LE PITIFUL SORCIER


CLASS: Ugh-Zan "?", le Pitoyable Sorcier
METABOLISM: Expérience synthétique-organique
PERCEPTION: Hybride Ug-Zan Clone III
SIZE: 150 pieds
ENDURANCE: Faible
HOSTILITY: Extrême
WEAPONS: Fusées faibles, appel de renforts, lévitation
REWARD: 698000FC
THREAT: Moyen

DESCRIPTION: 

Ugh-Zan "?", le Pitiful Sorcerer, est une abomination grise et tordue à quatre bras née d'une expérience de clonage ratée du légendaire Ugh-Zan III. Quand il n’y avait pas encore de candidats pour Ugh-zan. Lorsque les scientifiques de Mental ont tenté de reproduire l'ADN du grand seigneur de guerre, quelque chose s'est terriblement mal passé : le résultat a été un être malformé et instable doté d'un pouvoir risible et d'une amertume sans fin. Et comme Mental ne savait pas comment l'appeler, il lui a donné un surnom en l'honneur de sa silhouette "?".

En raison de sa force pathétique et de son incapacité à se régénérer, Mental l'a dépouillé de tout honneur et l'a enfermé dans une capsule d'exposition au sein du Musée d'Ugh-Zans, une exposition vivante de l'échec. Pourtant, après des siècles d’humiliation, le Pitoyable Sorcier s’est échappé de sa détention, alimenté par la haine et la folie.

Désormais obsédé par la vengeance, il cherche à détruire le « sérieux » Sam Stone, celui qui regardait autrefois d'un air moqueur sa prison de verre.

Malgré sa faiblesse, Ugh-Zan "?" n'est pas inoffensif : il peut flotter, déclencher des volées de faibles roquettes et appeler de petits renforts dans des tentatives désespérées pour prouver sa valeur à son maître oublié depuis longtemps.

TIPS: 

- Ses fusées sont faibles mais imprévisibles : continuez à avancer.
- Évitez d'être entouré par ses serviteurs invoqués.
- Ne sous-estimez pas sa rage, il peut se battre avec une férocité surprenante.
