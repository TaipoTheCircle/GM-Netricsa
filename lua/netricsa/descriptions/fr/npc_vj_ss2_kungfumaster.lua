KUNG FU ZOMBIE, MAÎTRE DU CHI-FANG


CLASSE : Kung Fu Zombie, Maître
MÉTABOLISME : Mort-vivant
PERCEPTION : Artiste martial zombie
TAILLE : 12 pieds
ENDURANCE : Faible
HOSTILITÉ : Élevée
ARMES : Invocation d'esprit
RÉCOMPENSE : 150 FC
MENACE : Élevée

DESCRIPTIF :

Alors que la plupart des serviteurs de Mental reçoivent une formation minimale, les Kung Fu Zombies ont reçu une formation approfondie en regardant des films d'action des années 80 et 90.

Certains de ces zombies maîtrisent l’art du kung-fu et ont appris les voies de la magie. On peut voir des maîtres planer dans les airs, invoquant des esprits de dragon pour détruire leurs ennemis. Ces esprits poursuivront l'ennemi de leur maître et exploseront, libérant de l'énergie magique au contact.

Les Maîtres étaient à l'origine des acteurs de la planète Chi-Fang. Avec l'arrivée de Mental, les Masters se sont révélés être des combattants fiables, mais Mental a décidé d'augmenter leur puissance. Les Maîtres ont été transformés en adversaires volants mortels : leurs jambes ont été coupées (qui ont ensuite été données aux Soldats Taureaux) et ils ont reçu la capacité de lancer des sorts magiques mortels de dragon (lévitation, créant un faisceau d'énergie de force).

Malheureusement, ces changements ont donné aux Maîtres une étrange envie de chanter constamment. Ils chantaient mal. En fait, ils étaient dégoûtants. À cause de ce défaut, Mental a dû attacher des masques en acier sur leurs visages afin que personne ne puisse entendre leur chant et que cela n'endommage pas son audition.

CONSEILS :

- S'ils s'approchent suffisamment, écrasez-les contre le mur avec un fusil à pompe à double canon.
- Ils essaieront de garder leurs distances afin de pouvoir invoquer des esprits en toute sécurité pour attaquer.
- Les esprits vous poursuivront, alors continuez à avancer si vous voulez les éviter.