MINI TOURELLE DEMI-QUAKE SHOW


CLASS: Mini-tourelle de spectacle HALF-QUAKE
METABOLISM: Mécanique
PERCEPTION: Unité de défense automatisée
SIZE: ~2 pieds
ENDURANCE: Faible-Moyen
HOSTILITY: Extrême
WEAPONS: HALF-QUAKE Show Crabe, Dragon
REWARD: 250 FC
THREAT: Modéré

DESCRIPTION: 

When the HALF-QUAKE Sadistic Show realized that live actors weren't dying enough, the decision was made to purchase a batch of automated turrets from Black Mesa. The contract was signed and the equipment was issued, but with one condition: tout l'équipement doit être correctement marqué.

La Mini Tourelle est une version compacte de la tourelle blindée standard, adaptée pour une installation dans des espaces à plafond bas, des puits de ventilation et d'autres endroits où une version pleine grandeur ne peut tout simplement pas tenir.

Après le transfert de propriété à HALF-QUAKE, le logo ">)" a été apposé au pochoir sur le dessus de chaque tourelle avec de la peinture blanche.

Les mini tourelles peuvent être montées au sol, au mur ou au plafond. Ils peuvent attendre des heures dans l'obscurité, alimentés par une batterie de secours, et ne sont activés que lorsque des capteurs de mouvement détectent une cible en approche.

Grâce à sa rotation à 360 degrés, il est impossible de se cacher derrière : il tourne simplement et continue de tirer.

TIPS: 

- Des explosifs ou des tirs précis peuvent rapidement le désactiver.
- Vérifiez l'espace au-dessus et en dessous de vous : même les petites tourelles peuvent couvrir des angles inattendus.
- Évitez de vous tenir dans des couloirs ou des espaces étroits où il pourrait vous cibler efficacement.
- Utilisez une couverture dans l'environnement pour minimiser l'impact des tirs de sa mitrailleuse.
