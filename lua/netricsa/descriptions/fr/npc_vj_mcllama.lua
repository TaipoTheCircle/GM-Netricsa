LAMA DU MONDE MINECRAFT


CLASS: Lama
METABOLISM: Mammifère, ongulé à doigts égaux
PERCEPTION: Animal de troupeau neutre
SIZE: 6,3 pieds
ENDURANCE: Moyenne
HOSTILITY: Conditionnel
WEAPONS: Cracher
REWARD: 120 FC
THREAT: Minimal

DESCRIPTION: 

Dans la nature, les lamas se trouvent dans les savanes et les collines balayées par le vent.

Les lamas sont des créatures neutres par nature. Ils n'attaquent pas, mais s'ils sont touchés, le chameau offensé se retourne et crache directement au visage de l'attaquant. Le crachat vole comme un projectile, inflige un point de dégâts et ne prend pas feu. Parfois, un lama rate et frappe un autre lama, puis des querelles éclatent au sein de la meute pour déterminer qui doit à qui.

Les lamas ont une haine particulière envers les loups. En voyant un chien sauvage, un lama crache sans prévenir, qu'il ait été touché ou non. Les loups, à leur tour, craignent les lamas puissants et les fuient toujours.

Les lamas peuvent être apprivoisés. Pour ce faire, montez-les à plusieurs reprises jusqu'à ce qu'ils arrêtent de vous projeter.

Un lama apprivoisé est une bête de somme idéale.

Outre un coffre, vous pouvez décorer un lama avec un tapis.

TIPS: 

- Ne frappe pas les lamas en premier. Les dégâts causés par la broche sont négligeables, mais ils laissent un mauvais goût en bouche.
- Si vous rencontrez un marchand ambulant avec des lamas, vous pouvez prendre les lamas pour vous en détachant la laisse.
