TEC-9 SELF-LOADING PISTOL


WEAPON: Intratec TEC-DC9
MUNITIONS Cartouches 9x19
ROF: ~20 tours/min

DESCRIPTION:

Un pistolet à chargement automatique développé en Suède dans les années 1980. Sorti sur le marché en 1984. Il était populaire auprès des gangs de Los Angeles jusqu'à la fermeture d'Intratec aux États-Unis.

NOTES:

- Un bon pistolet pour la guerre des bidonvilles.




