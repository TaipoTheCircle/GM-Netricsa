ENDERMAN DE LA FIN DE MINECRAFT


CLASS: Enderman
METABOLISM: Indéfini
PERCEPTION: Foule de téléportation neutre / extraterrestre mystérieux
SIZE: 9,5 pieds
ENDURANCE: Haut
HOSTILITY: Neutre
WEAPONS: Punch
REWARD: 1000FC
THREAT: Haut

DESCRIPTION: 

One of the most mysterious creatures, whose true origins are still debated. Its name is a combination of the words "End" (the dimension it supposedly inhabits) and "men" (human). According to fan theories, endermen were once ordinary people who fled to the End to escape a catastrophe. There, feeding on chorus fruit, they mutated: ils ont grandi, ont perdu leur couleur de peau et ont appris à se téléporter et à construire des villes finales.

The endermen's appearance is terrifying: un corps long, mince, complètement noir, des yeux violets brillants et une bouche d'où émanent des particules violettes, semblables à celles d'un portail vers le Nether. Les Endermen parcourent le monde calmement jusqu'à ce que vous les regardiez directement dans les yeux.

Endermen are unique in that they are the only mob that can naturally spawn in all three dimensions: 

Overworld: La nuit dans la plupart des biomes.

Netherworld: Le plus souvent dans la forêt déformée.

The End: Leur habitat principal. Ici, ils apparaissent en grands groupes.

Endermen behavior boils down to two things: téléportation et déplacement de blocs. Il peut soulever et déplacer certains blocs (terre, sable, cactus, fleurs), laissant le chaos sur son passage. Si un Enderman tient un bloc, il le lâchera à sa mort.

When idle, an Enderman simply walks around, ignoring the player. However, aiming a crosshair at it or striking it causes it to become enraged. The Enderman's body begins to shake, its mouth opens, and it lets out a terrifying, electronically distorted scream. Then the fight begins: 

L'Enderman se téléporte derrière l'ennemi ou à ses côtés, délivrant de puissants coups avec ses mains.

Tout projectile (flèche, boule de neige, œuf) provoque une téléportation avant impact. Il est impossible de tuer un Enderman avec un arc.

L'eau et la pluie ont endommagé Endermen. Lorsqu'ils sont exposés à l'eau ou sous la pluie, ils paniquent et se téléportent.

TIPS: 

- Équipez une citrouille sculptée sur votre casque. Votre vision sera altérée, mais l'Enderman ne s'aggravera pas, peu importe combien vous le regardez.
- Les Endermen mesurent près de 3 pâtés de maisons. Construisez un appentis à 2 pâtés de maisons du sol. Vous pouvez frapper les jambes de l'Enderman, mais il ne pourra pas vous atteindre.
- Appuyez votre dos contre un mur ou un coin. Cela empêchera l'Enderman de se téléporter derrière vous.
- Frapper les jambes de l'Enderman l'empêchera de se téléporter dans la douleur.
- Jeter de l'eau à vos pieds créera une barrière. Les Endermen ont peur de l'eau et ne s'en approchent pas.
- La meilleure arme est une épée. Frappez rapidement en essayant de garder l'ennemi devant vous.
- Les Endermen détestent les endermites (petits insectes violets) et les poursuivront même si vous les regardez.
- Si un Enderman meurt alors qu'il tient un bloc, ce bloc est abandonné. L'exception concerne les blocs qui ne sont pas du butin (comme le substrat rocheux). Il existe des légendes de fans sur le White Enderman, qui apparaîtrait rarement, mais officiellement, il ne s'agit que d'un bug de texture ou d'un mod.
