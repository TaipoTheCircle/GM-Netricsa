HUGO BOSS DU SIRIUS


CLASS: Hugo Boss
METABOLISM: Mécanique, Robot Boss
PERCEPTION: Robot-destructeur de combat géant programmé et jouet de Mental
SIZE: 29,5 pieds
ENDURANCE: Extrême
HOSTILITY: Absolu
WEAPONS: Poings, lasers à antenne, lance-roquettes montés sur l'épaule, canon à bras, bélier Jetpack
REWARD: 5000FC
THREAT: Absolu

DESCRIPTION: 

The most advanced robot in Mental's entire army. It is the "pinnacle of Horde engineering": blindé au point d’être quasi invulnérabilité, armé jusqu’aux dents et capable de voler. En apparence, il ressemble à un jouet qui prend vie : un énorme robot avec une antenne sur la tête, des lumières clignotantes et des sons semblables à ceux d'un jouet d'enfant. Mais ce jouet tue.

Lorsque Mental a décidé de créer une armée capable de supprimer toutes les poches de résistance dans l’univers, il s’est rendu compte que les soldats ordinaires ne suffisaient pas. Il fallait quelque chose de plus. Il fallait un symbole.

Le projet s'appelait à l'origine "CSS-00" - "Combat Siege System". Il s’agissait d’un robot capable à lui seul de détruire des armées entières et de résister aux attaques d’une flotte spatiale.

Mais Mental, qui n'a jamais eu bon goût, a insisté sur le changement.

"Faites-en un jouet", a-t-il dit à l'ingénieur en chef.

"Mais, Maître, c'est une machine de combat !"

"J'ai dit, fais-le ressembler à un jouet."

La deuxième exigence de Mental était une certaine « boîte ».

"Le patron devrait sortir des sentiers battus. Cela ajoute du drame."

"Nous pouvons simplement le mettre dans le hangar."

"Hors de la boîte ! Et écrivez dessus "Boss in the Box", pour que tout le monde le sache."

Les ingénieurs soupirèrent et retroussèrent leurs manches. La boîte était faite. L'écriture a été appliquée. Hugo était emballé.

Plus tard, Hugo a été déployé sur le satellite Kronor, où il garde le système de contrôle du canon laser qui pénètre dans le bouclier Sirius.

Et il n’épargnera aucune âme.

TIPS: 

- Hugo est invulnérable dans la première phase.
- Attaquez-le pendant le vol.
- Attention aux missiles et aux boulets de canon.
