MUTANT OMEGA DE L'ARCHIPEL KRIEGER


CLASS: Mutant Oméga
METABOLISM: Mutation finale du trigène
PERCEPTION: Plateforme de combat
SIZE: 12 pieds
ENDURANCE: Maximum
HOSTILITY: Absolu
WEAPONS: Lanceurs de missiles, mini-missiles
REWARD: 5000FC
THREAT: Catastrophique

DESCRIPTION: 

Omega is the culmination of Krieger's experiments, the final and most advanced form of Trigens. Visually reminiscent of the Fat Man, but superior in every way. Equipped with heavy armor and a built-in arsenal, it turns into a walking fortress. It uses a combination of weapons: des missiles tirés à la main, tandis que des lanceurs de missiles montés sur l'épaule couvrent une zone avec des mini-missiles.

Il agit comme un point de tir mobile, alternant entre les modes d'attaque pour engager des cibles à n'importe quelle distance. Les fusils de chasse sont efficaces contre les ennemis proches, les roquettes contre les groupes éloignés et les volées de mini-roquettes ne laissent aucune place à la couverture. Son armure absorbe des dégâts importants, le rendant pratiquement invulnérable aux armes standards.

TIPS: 

- Utilisez des armes lourdes - seuls les lance-grenades et les explosifs sont efficaces contre son armure.
- Continuez à bouger - rester immobile équivaut à la mort d'une volée de roquette.
- Attaque depuis les flancs - ses armes principales sont concentrées dans l'hémisphère avant.
- Recherchez une couverture contre les volées de mini-fusées en vous déplaçant à l'intérieur ou derrière des structures solides.
