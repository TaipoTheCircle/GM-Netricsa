DRONE DE PIT DE LA DIMENSION X


CLASS: Drone de fosse
METABOLISM: Carnivore
PERCEPTION: Infanterie légère de la race X
SIZE: 5-6 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Griffes de rasoir, pointes de tête (projectile), attaque à charge rapide
REWARD: 200FC
THREAT: Moyen

DESCRIPTION: 

Les Pit Drones sont des créatures d'assaut vicieuses élevées par Race X comme troupes de choc. Avec des traits reptiliens, des griffes crochues et des crêtes osseuses bordant leur crâne, ils sont conçus à la fois pour la brutalité au corps à corps et la létalité à distance. Leur capacité caractéristique consiste à lancer des pointes pointues ressemblant à des os depuis leur front, capables de percer l'armure et la chair à mi-portée. En combat rapproché, ils frappent sans relâche avec leurs serres, écrasant souvent les soldats légèrement armés.

Malgré leur férocité, les Pit Drones ne sont pas lourdement blindés et comptent sur leur vitesse et leur agressivité pour tuer leurs proies. Ils sont déployés en essaims, ce qui les rend particulièrement dangereux dans les espaces confinés où l'évasion est limitée. Sur Terre, ils ont été rencontrés pour la première fois lors de l'incident de Black Mesa, chassant souvent en meute aux côtés des Shock Troopers.

TIPS: 

- Continuer à bouger et rester immobile fait de vous une cible facile pour leurs pointes de tête.
- Utilisez SHOTGUN ou MP5 pour les couper rapidement avant qu'ils ne se rapprochent.
- Les grenades ou explosifs sont efficaces contre les groupes.
- Ne les laissez pas vous flanquer : ils fonctionnent mieux en nombre.
