STROGOS


PLANET: Stroggos
TYPE: Planète terrestre, monde industriel
CLIMATE: Atmosphère dure, aride et respirable pour les humains
MOONS: 3 lunes (une avec son propre système d'anneaux)
TERRAIN: Vastes friches industrielles, cratères, chaînes de montagnes, petits plans d'eau
LOCATION: Système stellaire inconnu
NATIVE RACES: Strogg, vestiges d'une faune disparue et assimilée
ALLIANCE: Empire Strogg
LOCATIONS: Palais Macron, zone d'atterrissage du Colossus, cratères Majoris et Minor, base lunaire secrète Strogg

DESCRIPTION: 

Un monde inhospitalier, mais respirant, pour les humains, abritant la race cybernétique agressive des Strogg. La planète est similaire en taille, en gravité et en composition atmosphérique à la Terre, mais son apparence a été complètement défigurée par des millénaires d’industrialisation incessante. Depuis son orbite, la planète apparaît comme une friche brun-orange, dépourvue d'espaces verts importants et d'eau libre.

Stroggos likely once boasted a rich biosphere with oceans, vegetation, and diverse fauna. However, the Stroggos, pursuing total militarization, exhausted virtually all natural resources: les métaux, les minéraux et les combustibles fossiles étaient extraits et traités. La plupart des formes de vie indigènes ont disparu, ont muté ou ont été assimilées de force par les Stroggos, transformées en nourriture ou transformées en nouveaux soldats à travers l'horrible processus de « Stroggification ».

Le paysage de la planète est une vaste friche industrielle, parsemée d'usines, d'usines de transformation et de bases militaires.

L'activité géologique est élevée, avec des cratères d'impact (tels que Majoris et Minor) restants et un système d'anneaux d'astéroïdes en orbite autour de la planète - peut-être les restes d'une quatrième lune détruite lors d'une ancienne catastrophe.

Stroggos est entouré de trois lunes et d'une ceinture d'astéroïdes, qui contient la résidence secrète du chef Strogg, Macron, accessible uniquement par téléporteur depuis la surface.

KEY LOCATIONS: 

- Macron's Palace: La structure centrale à la surface de Stroggos, couronnée par un téléporteur massif menant à la retraite astéroïde du chef. Le cœur de la machine de guerre Strogg.
- Landing Zone "Colossus": Le site des premiers débarquements massifs des forces d'invasion de la Terre. Un site de combats acharnés, jonché de restes de véhicules et de cadavres.
- Craters Majoris and Minor: Deux énormes cratères d'impact, probablement associés à la formation de la ceinture d'astéroïdes. D'importantes installations industrielles sont situées à proximité.
- Secret Strogg Lunar Base: Un immense complexe souterrain sur la troisième lune, contenant des usines, des quais navals et un bunker de commandement. Détruit lors d'une opération audacieuse menée par les Earth Marines.
