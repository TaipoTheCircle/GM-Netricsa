LE MONDE XEN FRONTIER, LE TUNER XEN


Voilà, Sam. Trouvé. Sain et sauf. Et, selon les normes Xen, il semble même accueillant.

Il ne reste plus qu'à l'allumer.

When the Black Mesa scientists were tinkering with teleportation, they quickly realized one thing: Xen a été impliqué dans le processus.

La solution semblait élégante. Prenez une énorme formation de cristaux de Xenium, connectez-y l'appareil et transformez-le en point focal. Ainsi, le Xen Tuner est né. Un nœud qui dirigerait la téléportation, comme une balise dans un monde où la direction elle-même est un concept relatif.

L'équipe de recherche envoyée ici pour installer l'appareil a disparu peu de temps après avoir terminé ses travaux. Rien de dramatique. Ils ont simplement arrêté de communiquer. Les scientifiques sur Terre ont tiré les conclusions appropriées, ont conçu une méthode de ciblage différente et... ont abandonné l'appareil. Xen, comme d'habitude, ne s'y opposa pas.

Devant vous se trouve un accordeur. Un gros appareil. Une paire de tiges mobiles reliées à un cristal. Un petit générateur qui alimente tout. Les cannes sont contrôlées via une console et il est préférable de les laisser seules, sauf en cas d'absolue nécessité. Ce qui, bien sûr, est exactement ce que nous nous apprêtons à faire.

Ce relais faisait partie d'un réseau à trois éléments.

Le premier est le cœur du complexe Lambda.

La seconde est une fusée en orbite terrestre. (L'autre, vous l'avez déjà fait.)

Le troisième, c'est lui. Ici. À Xen.

Alors vas-y, Sam.

Allumez l'appareil.

Faites savoir à Xen que vous respectez toujours ses règles.

Et où cela nous mènera-t-il...

Eh bien, ce ne sont que des détails.
