BULLSQUID AMPHIBIE DE XEN


CLASS: Calmar-taureau, amphibie
METABOLISM: Amphibien carnivore
PERCEPTION: Prédateur sous-marin
SIZE: 8-9 pieds
ENDURANCE: Moyen-élevé
HOSTILITY: Extrême
WEAPONS: 4 tentacules, griffes, cil de queue, crachat toxique
REWARD: 450 FC
THREAT: Haut

DESCRIPTION: 

Le Calmar amphibie est une sous-espèce aquatique rare du Calmar sauvage de Xen. Contrairement à ses parents terrestres, cette bête s'est adaptée aux eaux profondes, avec une charpente musclée semblable à celle d'un crocodile, une peau écailleuse verte et seulement quatre puissants tentacules nourriciers autour de sa gueule. Ses poumons amphibies lui permettent de prospérer sous l'eau et sur terre, bien qu'il préfère se cacher dans les rivières, les canaux et les cavernes inondées.

Lorsqu'il est provoqué, le Calmar amphibie attaque avec des griffes vicieuses, des coups de queue et des jets de crachats toxiques, même lorsqu'il est immergé. Sa construction crocodilienne lui confère une forte vitesse de nage, ce qui en fait un prédateur suprême dans les deux domaines.

TIPS: 

- Évitez d'entrer dans l'eau lorsque des calamars amphibies sont présents : ils tendent une embuscade par le bas.
- Les crachats toxiques se dispersent rapidement dans l'eau, alors gardez vos distances même lorsque vous nagez.
- Les explosifs et les armes de type harpon sont plus efficaces sous l'eau.
- Le combat terrestre est plus sûr, mais leur morsure chargeante peut briser les os en quelques secondes.
