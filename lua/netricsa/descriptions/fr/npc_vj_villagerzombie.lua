VILLAGER ZOMBIE DU MONDE MINECRAFT


CLASS: Villageois zombie
METABOLISM: Villageois infecté
PERCEPTION: Foule hostile
SIZE: 6,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Mains
REWARD: 200FC
THREAT: Moyenne

DESCRIPTION: 

C'est le résultat d'une rencontre tragique entre un villageois normal et un zombie normal. Une seule bouchée suffit au marchand imprudent pour se transformer en ce pâle simulacre, ne conservant qu'un nez reconnaissable et les restes de vêtements. Extérieurement, il diffère d'un zombie normal par son museau caractéristique et sa taille plus grande - 0,9 cm de plus que ses congénères zombies, ce qui lui permet parfois de rester coincé dans les portes.

Les villageois zombies apparaissent aux mêmes endroits que les zombies ordinaires, mais avec moins de chances. On peut parfois les rencontrer dans la taïga et dans les iglous, où l'un de ces zombies est enfermé dans la cave derrière les barreaux.

Zombie villagers behave similarly to regular zombies: ils poursuivent les victimes, les villageois et les golems de fer, peuvent enfoncer les portes et ramasser des objets. Mais il existe une différence importante, pour laquelle beaucoup sont prêts à risquer leur vie : ils peuvent être guéris.

The healing process goes like this: d'abord, le villageois zombie est affaibli avec une potion ou une citrouille explosive, puis reçoit une pomme d'or. Après quelques minutes, les tremblements commencent et après encore une ou deux minutes, le villageois revient à la normale. Le villageois guéri offre des réductions sur les échanges, parfois symboliques, allant jusqu'à une émeraude pour tout.

TIPS: 

- Si vous voyez un villageois zombie, ne le tuez pas tout de suite. Isolez-le des autres zombies pour pouvoir le guérir plus tard.
- Pour en guérir, il vous faut une potion de faiblesse et une pomme d'or. Les deux peuvent être fabriqués ou trouvés dans des coffres.
- Les sous-sols des igloos contiennent toujours un villageois zombie et tout le nécessaire pour le soigner.
- Un villageois guéri donne de bonnes réductions. Si vous avez de la chance, vous pouvez obtenir un outil diamanté pour quelques émeraudes.
- Il existe également des bébés villageois zombies. Ils sont tout aussi rapides et guérissent selon les mêmes règles.
- Les villageois zombies peuvent apparaître en chevauchant un poulet. C'est rare, mais ça arrive.
