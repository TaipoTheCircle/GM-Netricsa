BOOMER D'ESSAI


CLASSE : Swarm Boomer
MÉTABOLISME : Xénobiologique
PERCEPTION : Explosifs vivants
TAILLE : 7,9 pieds
ENDURANCE : Moyenne
HOSTILITÉ : Absolue
ARMES : Capsules explosives auto-détonantes
RÉCOMPENSE : 600 FC
MENACE : Élevée

DESCRIPTIF :

Le Boomer est un spécimen spécialisé de Swarm, dont toute la biologie est dédiée à une seule tâche : délivrer le maximum d'explosifs aussi près que possible de l'ennemi.

Au repos, le Boomer semble relativement inoffensif pour un spécimen d'Essaim de sa taille. Cependant, dès qu’une proie est détectée, ses organes internes commencent à produire rapidement des composés chimiques instables. Le corps de la créature gonfle, se transformant en un réservoir vivant de biomasse explosive.

Pour réduire la distance, Boomer peut effectuer des attaques rapides et glissantes, renversant les adversaires et bloquant les passages avec son propre corps. En arrivant à courte distance, l'organisme explose, se détruisant ainsi que tout ce qui se trouve à proximité.

Les capsules explosives secondaires présentent un danger particulier. Après la mort d'un Boomer gonflé, ils se dispersent autour du site de l'explosion et explosent quelques secondes plus tard. Par conséquent, même réussir à détruire la créature ne signifie pas toujours la fin de la menace.

Selon les xénobiologistes, Boomer est l’un des exemples les plus frappants de jusqu’où peut aller l’évolution lorsque l’instinct de conservation est complètement abandonné.

CONSEILS :

- Détruisez les baby-boomers avant qu'ils ne gonflent.
- Après l'explosion, quittez immédiatement la zone dangereuse.
- Ne leur permettez pas de s'approcher du groupe.
- Si la créature semble sur le point d'éclater, il est préférable de ne pas tester la littéralité de cette affirmation.