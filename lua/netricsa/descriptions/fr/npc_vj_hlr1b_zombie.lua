ZOMBIE CRABE VIOLET


CLASS: Tête de crabe zombie, violet
METABOLISM: Hôte humain + crabe violet
PERCEPTION: Entité zombifiée
SIZE: ~6 pieds
ENDURANCE: Moyen-élevé
HOSTILITY: Élevé (lent mais implacable)
WEAPONS: Attaques de mêlée (griffes, grappin)
REWARD: 250 FC
THREAT: Modéré individuellement, élevé en groupe

DESCRIPTION: 

Lorsqu'un crabe violet réussit à infecter un humain, il crée une variante de zombie qui est plus forte et plus résistante que les zombies crabes communs. Le parasite violet modifie les tissus de l'hôte, lui donnant une endurance accrue et une apparence extraterrestre grotesque. Ces zombies sont lents mais implacables, formant souvent des hordes coordonnées.

TIPS: 

- Les armes à feu ou explosifs sont efficaces en raison de leur vitesse lente.
- Évitez les contacts rapprochés - les attaques au corps à corps sont dangereuses.
- Les groupes peuvent se coincer ; prioriser les cibles.
