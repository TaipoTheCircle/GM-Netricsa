BLACK MESA, AVANT L'INCIDENT


La téléportation se termine... mais pas là où vous l'espériez. Pas de Xen ruiné, pas de conséquences. Au lieu de cela, vous vous trouvez à Black Mesa avant la catastrophe. Les laboratoires sont intacts, les scientifiques sont calmes et tout sent le mauvais café et l’ozone.

Et encore une fois, ils vous prennent pour Freeman. Je me plaindrais, mais apparemment, dans chaque chronologie, les gens adorent ce nom. Quelque part ailleurs dans cette installation tentaculaire, un autre « Sam, c'est vous mais pas vous » se prépare à activer le spectromètre anti-masse. Nous savons tous les deux comment cela se passe.

Pendant ce temps, vous êtes entraîné dans un autre projet. Les chercheurs parlent ici d'un test parallèle - un prototype de portail, indépendant de l'expérience principale. Ils veulent que vous le surveilliez pendant que le spectromètre bourdonne dans une autre aile.

Deux expériences. Deux catastrophes qui attendent d'entrer en collision. L’une est l’histoire. L'autre... est sur le point d'être écrit.

Le bon côté des choses, cela signifie que vous ne serez blâmé que pour la moitié de l'apocalypse. Progrès!