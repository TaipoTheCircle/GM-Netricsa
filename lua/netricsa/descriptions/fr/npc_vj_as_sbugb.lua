BOUCLIER D'ESSAI "BÊTA"


CLASSE : Swarm Shieldbug "Bêta"
MÉTABOLISME : Xénobiologique
PERCEPTION : Organisme d'assaut lourd
TAILLE : 9,2 pieds
ENDURANCE : Très élevée
HOSTILITÉ : Élevée
ARMES : membres blindés, frappe à l'éperon
RÉCOMPENSE : 1000 FC
MENACE : Très élevée

DESCRIPTIF :

Le Swarm Shieldbug "Beta" est un spécimen d'assaut spécialisé conçu pour briser les défenses ennemies et protéger les membres de l'Essaim les moins résilients. Sa défense repose sur deux membres blindés massifs, qui peuvent couvrir complètement la majeure partie du devant de la créature.

Lorsqu'il attaque, le Shieldbug maintient instinctivement l'ennemi devant lui, utilisant son bouclier naturel pour dévier les attaques. Grâce à sa conception unique, son blindage frontal peut résister à des dommages qui seraient mortels pour la plupart des autres organismes Swarm.

Ces plaques de protection servent également d'armes. Avec un mouvement brusque de ses membres, le Shieldfoot délivre des coups puissants capables de repousser ses proies sur une distance considérable. Cette attaque est particulièrement dangereuse dans les espaces confinés, sur les ponts et à proximité des falaises.

Contrairement aux membres les plus agiles de Swarm Beta, ce spécimen ne repose pas sur la vitesse, mais sur un mouvement incessant vers l'avant. Après avoir localisé une cible, le Shieldfoot réduira progressivement la distance jusqu'à ce qu'il ait une ouverture pour frapper.

Comme la plupart des créatures trop confiantes dans leur propre invulnérabilité, ses organes les plus vulnérables se trouvent précisément là où elles s’attendent le moins à une attaque.

CONSEILS :

- N'attaquez pas le Shieldfoot de front.
- Utiliser sa lenteur pour le flanquer.
- Évitez les passages étroits pendant les combats.
- Plus son bouclier paraît grand, moins il aime se faire attaquer par derrière.