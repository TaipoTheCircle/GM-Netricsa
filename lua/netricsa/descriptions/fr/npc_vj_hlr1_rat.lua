DESCRIPTION:

Rats Black Mesa standards trouvés dans les couloirs des installations et les zones de maintenance. Généralement passif et s'éloigne des humains, provoquant des nuisances mineures.

TIPS:

- Inoffensif – ignorer sauf si cela est nécessaire pour les objectifs.
