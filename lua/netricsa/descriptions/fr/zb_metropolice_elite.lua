COMBINEZ L'ÉLITE DE LA PROTECTION CIVILE


CLASSE : Combiner l'élite de la protection civile
MÉTABOLISME : Humain fortement conditionné, mâle (stade pré-Overwatch)
PERCEPTION : Application de la loi transhumaine (unité d'élite)
TAILLE : 5,9 pieds
ENDURANCE : Moyenne-Élevée
HOSTILITÉ : Élevée
ARMES : Stun Stun-Stick, pistolet 9 mm, SMG1, barils explosifs, Manhack
RÉCOMPENSE : 350 FC
MENACE : moyenne à élevée

DESCRIPTIF :

Le Combine Civil Protection Elite représente l'échelon supérieur des forces de la Metropolice, servant d'étape de transition entre la protection civile standard et les soldats d'Overwatch complets. Equipés de blindages renforcés et d'équipements de communication plus avancés, ils sont déployés dans la répression des émeutes, des patrouilles à grande échelle et des raids hautement prioritaires contre des cellules de résistance.

Contrairement aux Metro Cops ordinaires, ces élites ont subi un conditionnement et une augmentation plus lourds, les rendant plus forts, plus disciplinés et plus meurtriers au combat. Leurs masques comportent des filtres renforcés et des tons plus sombres, souvent avec des optiques améliorées pour leur donner une apparence intimidante.

Au combat, les élites de la protection civile opèrent avec une meilleure coordination et une puissance de feu plus importante, utilisant des tactiques de suppression pour chasser les ennemis de leur abri. Ils ne sont toujours pas aussi durables que la véritable infanterie d’Overwatch, mais représentent une sérieuse escalade lorsqu’ils sont déployés dans des villes occupées.

CONSEILS :

- Diriger souvent des escouades d'officiers réguliers du CP.
- S'ils sont rencontrés, d'autres renforts du Combine pourraient ne pas être loin derrière.