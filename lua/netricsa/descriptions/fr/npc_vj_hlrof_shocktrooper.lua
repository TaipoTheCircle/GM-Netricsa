SHOCK TROOPER DE LA DIMENSION X


CLASS: Soldat de choc
METABOLISM: Carnivore / Bio-électrique
PERCEPTION: Infanterie lourde de la race X
SIZE: 7-8 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Shock Roach (fusil bioélectrique), coups de griffes, coups de pied
REWARD: 400FC
THREAT: Haut

DESCRIPTION: 

Les Shock Troopers constituent la principale infanterie de la Race X, formés en tant que soldats d'élite et commandants de champ de bataille. Imposants et musclés, leurs corps sont blindés de chitine naturelle, ce qui leur confère une résilience impressionnante contre les armes conventionnelles. Leur arme emblématique est le Shock Roach, un bio-organisme vivant qui sert de lanceur d'énergie. Le soldat et le cafard partagent un lien symbiotique : lorsque le soldat tombe, le cafard se détache et continue de se battre comme une créature indépendante.

Les Shock Troopers emploient également des Pit Drones comme unités de soutien, les faisant avancer en tant qu'éclaireurs et attaquants de première ligne. Leurs tactiques sont organisées et efficaces, contrairement à l'agression sauvage de la faune Xen. Au combat, les Shock Troopers avancent régulièrement, déclenchant des tirs bioélectriques et utilisant leur masse pour écraser leurs ennemis au corps à corps.

Leur présence sur Terre lors de l'incident de Black Mesa était un signe que la Race X cherchait une invasion à grande échelle, rivalisant avec Xen lui-même.

TIPS: 

- Donnez la priorité au Shock Roach : l'arme est puissante mais a un schéma de tir limité.
- Les explosifs et les armes lourdes fonctionnent mieux contre leur épaisse armure.
- Soyez prêt à affronter les cafards de choc détachés après la mort du soldat.
- Évitez les combats rapprochés : ils excellent en mêlée s'ils sont autorisés à se rapprocher.
- De rares Shock Troopers à la peau orange peuvent apparaître ; ceux-ci sont plus solides et plus résistants que leurs homologues verts.
