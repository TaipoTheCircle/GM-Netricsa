Bonhomme en pain d'épice des îles douces du Big Lolly


CLASS: Bonhomme en pain d'épice de Sweet Island
METABOLISM: Construction comestible vivante
PERCEPTION: Protecteur et originaire des Îles Douces
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Combat rapproché (coups de poing et coups au corps)
REWARD: 450 FC
THREAT: Moyenne

DESCRIPTION: 

Le Bonhomme en pain d'épice de Sweet Island est l'un des principaux défenseurs de cette terre écoeurante mais mortelle. Son corps est constitué d'une matière dense en pain d'épices, donnant une impression trompeuse de douceur et de rondeur. En réalité, il est bien plus fort qu’il n’y paraît.

In appearance, he resembles a living Gingerbread Man: une tête ronde avec de grands yeux ronds, un nez rouge, un sourire figé en permanence et trois boutons rouges sur le ventre. Une bordure en sucre blanc longe le bord de ses « vêtements », et des éléments ronds rouges ressemblant à des inserts de bonbons sont situés sur ses bras et ses jambes. Ses membres semblent mous, mais lorsqu'ils sont frappés, ils agissent comme des massues élastiques.

Le Bonhomme en pain d'épice préfère le combat rapproché. Il essaie de réduire la distance et d'abaisser ses « poings » sur son adversaire, en utilisant sa masse corporelle et sa structure élastique.

Malgré son apparence « amicale », il est un défenseur dévoué de son territoire.

TIPS: 

- Ne vous fiez pas aux apparences, les coups font mal.
- Attaquer à distance est plus efficace.
- Au corps à corps, il donne rapidement le ton.
- Son sourire n'indique pas la gentillesse.
