CHEVAL SQUELETTE DU MINECRAFT OVERWORLD


CLASS: Cheval squelette
METABOLISM: Cheval mort-vivant
PERCEPTION: Véhicule / Foule Passive
SIZE: 5,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Neutre
WEAPONS: Aucun
REWARD: 300FC
THREAT: Faible

DESCRIPTION: 

L'une des deux variétés de chevaux morts-vivants, avec les chevaux zombies. Contrairement à leurs homologues vivants, ces destriers squelettiques ne paissent pas dans les prairies, mais sont créés par la foudre lors d'orages.

Leur apparition s'explique par une condition particulière appelée « piège à squelette ». Lorsqu'un cheval normal est frappé par la foudre pendant un orage, il se transforme en cheval squelette doté d'une étiquette spéciale appelée "SkeletonTrap". Un cheval squelettique est un squelette de cheval entièrement formé flottant au-dessus du sol. Il émet les sons caractéristiques d'un cheval normal, mais à un ton plus grave avec un effet d'écho.

TIPS: 

- Ne vous approchez pas trop d'un cheval squelettique solitaire pendant un orage, à moins que vous ne soyez prêt à combattre quatre archers squelettiques.
- La meilleure stratégie pour obtenir une telle monture est de tuer les cavaliers mais de laisser les chevaux indemnes. Une fois les ennemis partis, les chevaux deviendront passifs et pourront être apprivoisés.
- Un cheval squelette est une monture aquatique idéale. Contrairement aux chevaux normaux, il ne déloge pas son cavalier lorsqu'il est immergé et ne se noie pas.
- C'est une excellente monture pour explorer les Enfers et la Fin, car contrairement aux autres monstres, les chevaux squelettiques ne sont pas affectés par la lumière du soleil et ne brûlent pas.
- N'oubliez pas qu'en tant que créature morte-vivante, les chevaux squelettiques subissent des dégâts grâce aux potions de guérison et sont restaurés par les potions dommageables.
