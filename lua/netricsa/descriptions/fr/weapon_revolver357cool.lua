REVOLVER COLT KING COBRA


WEAPON: Colt King Cobra
MUNITIONS .357 Magnum
ROF: 15 tours/min

DESCRIPTION: 

Le revolver Colt King Cobra est un revolver à six coups doté d'un mécanisme de détente à double action. Il a été introduit pour la première fois en 1986. Il a été développé et fabriqué par Colt's Manufacturing Company. Il était destiné à être utilisé par les forces de l'ordre et à être vendu aux amateurs d'armes à feu. Disponible en bleui et en acier inoxydable.

NOTES: 

- Le revolver est une arme très fiable.
- Son gros calibre permet de tuer un ennemi très rapidement.
- Vous pouvez jouer à la roulette russe avec si vous n'avez pas peur.
