TRONÇONNEUSE P-LAH "BONECRACKER"


WEAPON: Tronçonneuse P-LAH "Bonecracker"
AMMO: Carburant provenant d'un synthétiseur autonome de carburant à microfusion
WEIGTH: 27 livres

DESCRIPTION: 

Cette arme familière semble seulement avoir été fabriquée sur Terre.  Il semble que seuls sa conception et son objectif aient été tirés du concept de conception familière des tronçonneuses.  À l’intérieur du boîtier en alliage de titane, vous trouverez un synthétiseur autonome de carburant à microfusion capable de produire suffisamment de carburant pour 10 000 ans de fonctionnement non-stop.  Malheureusement, il n'y a qu'une garantie de 6 mois pour toutes les pièces mobiles et elles ont tendance à se briser très peu de temps après cette période.  Vraiment une merveille d’ingénierie et d’économie modernes.


NOTES: 

- N'essayez pas d'utiliser ce bébé lorsque vous êtes attaqué par des adversaires à longue distance.
