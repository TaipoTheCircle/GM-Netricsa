SNARK NEST, WEAPON


WEAPON: Nid de Snark, Arme
AMMO: Créatures snark extraterrestres
ROF: ~1 par seconde (vitesse de lancer)

DESCRIPTION:

De petits organismes extraterrestres agressifs jetés comme des armes vivantes. Les Snarks attaquent sans relâche les ennemis proches jusqu'à ce qu'ils explosent.

NOTES:

- Agressivité infinie une fois libérée.
- Peut nuire à l'utilisateur si aucune cible n'est présente.
- Mieux utilisé pour le déni de zone ou la distraction.