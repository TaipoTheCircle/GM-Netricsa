AKS-74U

WEAPON: AKS-74U
AMMUNITION: 7,62x39
ROF: ~600 tours/min

Description: 

Le fusil d'assaut Kalachnikov modernisé est une version modifiée de l'AK-47, développée par Mikhaïl Timofeevich Kalachnikov en 1950 et adoptée par l'URSS en 1959. Il est très populaire parmi les terroristes et les guérilleros. Il a été utilisé dans presque toutes les guerres à travers le monde, comme la guerre du Vietnam, la guerre en Afghanistan, la guerre en Tchétchénie et la guerre en Irak, et est actuellement utilisé dans le SVO. C'est un fusil d'assaut très fiable et populaire.

Notes: 

- Il a eu un impact énorme sur le monde, engendrant d'innombrables copies et modifications.
- Il est très bien adapté aux opérations d'assaut.
- Dans les forêts du Vietnam, on peut entendre le bruit de ses tirs résonner sur les arbres.
