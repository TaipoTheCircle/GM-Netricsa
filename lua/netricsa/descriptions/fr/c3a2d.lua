NOYAU DE RÉACTEUR NOIR MESA, LANCEMENT LAMBDA


C'est ça. La dernière étape avant Xen. Les scientifiques se sont rassemblés sur la plate-forme supérieure, observant nerveusement les lectures d'un appareil qui n'est pas censé exister ici - un Time-Lock, issu de l'ingénierie inverse à partir de la Clé Sirienne de l'Éternité et truqué par un jury dans la grille du réacteur de Black Mesa. Une fois stabilisé, il ouvrira un pont contrôlé vers Xen.

The path to them is almost insultingly simple: éliminez trois crabes, montez à l'échelle et l'équipe vous rencontrera. Mais ne vous laissez pas tromper : le véritable défi commence lorsque la défense démarre.

Mission Parameters: 

Protégez le scientifique principal. He is the only one trained to operate the modified Time-Lock. S'il meurt, la passerelle ne se stabilise jamais.

Attendez-vous à une forte incursion. Les forces de Mental et les créatures de Xen sentent la faille et lanceront tout ce dont elles disposent pour arrêter l'activation.

Tenir jusqu'à ce qu'il soit stable. The device requires continuous energy flow for synchronization; interrupting it mid-cycle could cause a catastrophic resonance.

Complications: 

Des visages familiers. Justin Barnes, Adrian Shephard et Gordon Freeman lui-même ont été aperçus en train de se rendre ici. Le vrai Freeman... pas vous, pas la confusion. Mais ils sont en retard et vous avez déjà fait le gros du travail. S’ils arrivent, il ne sera pas temps de contribuer à ce combat.

Limited support. The scientists can only provide you with one tool: un prototype de jetpack, conçu pour des sauts prolongés. Cela sera nécessaire une fois le portail stabilisé. Traitez-le comme de l'or.
