BUREAUX BLACK MESA, CHEMIN VERS L'ASCENSEUR


Nous y sommes presque. Les escaliers sont derrière nous, nous avons contourné les forces de Mental et nous voilà face au dernier tronçon avant l'ascenseur. Un couloir. Long et sinueux. Au bout, une porte ouverte. Dans la cage d'ascenseur.

Problem: H.E.C.U. est dans le couloir. Ils sont là. Ni à l'étage, ni derrière les barricades, ici même, entre nous et le puits. Ils savent que l’ascenseur est le seul moyen rapide de monter. Et ils ne veulent pas que quiconque l'utilise. Ils ne se soucient pas de qui. Un scientifique, un extraterrestre, un spectateur. L’ordre est le même : personne ne passe.

Mais il y a de bonnes nouvelles. Les forces mentales ne viennent pas ici. Je ne sais pas pourquoi. Ils n'ont pas besoin d'ascenseur, ou le couloir est trop étroit, ou ils ont simplement décidé que les Marines étaient le problème de Sam Stone, pas le leur. Donc c'est juste toi et H.E.C.U.

L'ascenseur est notre ticket pour le sommet. Peut-être vers la sortie. Peut-être au prochain niveau de ce cirque. Mais il faut y arriver. Parce que là-haut, il y a soit le salut, soit un autre combat. Et là-bas, il n'y a qu'un couloir rempli de marines et un puits sans fond.

L'ascenseur attend. J'espère que ça marche toujours.
