INGÉNIEUR REBELLE


CLASS: Ingénieur rebelle
METABOLISM: Les humains
PERCEPTION: Technicien en Résistance Humaine
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Arc de charge, fusil de chasse SPAS-12, tourelles combinées déployables
REWARD: 350FC
THREAT: Moyen-élevé

DESCRIPTION: 

L'Ingénieur Rebelle est une unité de soutien cruciale au sein de la Résistance humaine. Formés au combat et à l'ingénierie, ils manient un arc de charge pour des attaques de précision à longue portée et peuvent déployer des tourelles Combine reprogrammées pour défendre les positions rebelles. Ces tourelles agissent comme un appui-feu lourd stationnaire, fournissant un multiplicateur de force pour les petites escouades.

Les ingénieurs rebelles sont agiles et ingénieux, flanquant souvent les ennemis pour installer des tourelles ou sécuriser des positions stratégiques. Leur expertise technique leur permet de pirater et de réutiliser les machines Combine, ce qui les rend indispensables aux opérations sur le terrain.

TIPS: 

- Donnez la priorité à l'élimination des ingénieurs ennemis en premier : ils peuvent retourner les défenses contre vous.
- Protégez les tourelles déployées : elles fournissent un appui-feu continu.
- Utilisez leur mobilité pour placer des tourelles dans des endroits inattendus.
- Charge Bow est mortel à distance, capable de pénétrer une armure légère.
