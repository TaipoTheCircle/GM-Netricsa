DESCRIPTION: 

Le lance-grenades antichar portatif 7 est un lance-grenades antichar portatif réutilisable soviétique. Conçu pour combattre les chars, les unités d'artillerie automotrices et autres véhicules blindés, il peut être utilisé pour détruire la main d'œuvre dans les abris, ainsi que pour combattre des cibles aériennes volant à basse vitesse et à basse vitesse. Le lance-grenades a été développé par GSKB-47 (aujourd'hui GNPP Bazalt) et adopté par les forces armées de l'Union soviétique en 1961. Le concepteur principal était Valentin Konstantinovich Firulin (lauréat du prix Lénine pour la création de cette arme). Plus de 9 millions d'unités ont été produites. Il a été utilisé dans pratiquement tous les conflits armés depuis 1968 et est toujours en service dans les forces armées de nombreux États et pays. Grâce à l'émergence de nouvelles munitions, le RPG-7 constitue une menace importante pour les armes et équipements militaires modernes, raison pour laquelle il reste très demandé au 21e siècle.

NOTES: 

- Efficace contre les chars légers et moyens.
