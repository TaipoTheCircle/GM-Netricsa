SCORPION DÉMON BESTIAL, SOLDAT DE LA PLANÈTE TROPICALE MORTE


CLASS: Scorpion Démon Bête, Soldat
METABOLISM: Exobiologique, Arthropode
PERCEPTION: Scorpion de combat
SIZE: 10 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Piqûre de queue semblable à celle d'un scorpion
REWARD: 6000FC
THREAT: Haut

DESCRIPTION: 

Soldier Beastial Demon Scorpions - Jeunes spécimens de créatures agressives provenant de planètes tropicales, autrefois indépendantes, mais personnellement attirées par Solgar, le bras droit de Mental. Depuis lors, ils sont devenus une partie à part entière de la Horde, servant de créatures d'assaut au corps à corps.

L'apparence du scorpion ressemble à un arachnoïde qui a décidé de devenir un tank. La coquille verte lisse et brillante, le corps segmenté, les trois paires de pattes et la tête massive créent l'impression d'un bélier vivant. Au sommet de sa tête se trouve un casque vert en forme de casque, étroitement fusionné avec la coque. Deux paires d'yeux sont situées sur les côtés de la tête, offrant une excellente vision même dans des conditions de faible luminosité. Dans le noir, les yeux brillent d’un vert menaçant.

Le casque améliore la protection de la tête et rend la créature particulièrement résistante aux tirs frontaux.

La queue incurvée vers le haut, qui rappelle les scorpions de type terrestre, est utilisée pour des frappes puissantes.

Le Beast Demon Scorpion manque d'attaques à distance et s'appuie uniquement sur la vitesse, la pression et la force physique, cherchant à réduire la distance le plus rapidement possible.

TIPS: 

- Se précipite immédiatement vers la cible.
- Ne se cache pas, ne recule pas et ne considère pas l'avenir.
- Les explosifs C4 sont la meilleure méthode de dialogue.
- Un scorpion est mortel au corps à corps.
- Si le scorpion est déjà à proximité, vous avez tout mal fait.
