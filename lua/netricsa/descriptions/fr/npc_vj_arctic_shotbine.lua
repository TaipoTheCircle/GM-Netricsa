COMBINEZ LE SHOTGUNNER ARCTIQUE D'OVERWATCH


CLASS: Combinez Overwatch Arctic Shotgunner
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner l'assaut au corps à corps
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Fusil de combat Spas-12, Grenades
REWARD: 700FC
THREAT: Haut

DESCRIPTION: 

Le Combine Overwatch Arctic Shotgunner est une version modifiée du fusil d'assaut alternatif standard Combine, adapté aux opérations dans des froids extrêmes, des blizzards et des infrastructures glacées.

Dans le passé, bon nombre d'entre eux étaient des soldats, des policiers ou des militaires qui servaient dans les régions du nord avant l'arrivée du Combine. Après la conquête de la Terre, leur expérience fut jugée précieuse et leurs corps furent jugés aptes à une augmentation importante. Leurs personnalités ont été supprimées et leur physiologie a été reconstruite pour résister au froid constant.

Il porte un camouflage arctique blanc et gris avec des accents orange. Des systèmes de thermorégulation sont intégrés au blindage, permettant au soldat de se déplacer sans ralentir dans des zones où une personne normale gelerait en quelques minutes.

Comme son homologue non arctique, ce soldat n’est pas conçu pour le combat à longue distance. Son rôle consiste à mener des combats rapprochés agressifs, à percer les défenses et à nettoyer les pièces, les bunkers et les tunnels gelés.

TIPS: 

- De près, il est tout aussi mortel que la version standard.
- Le camouflage blanc et gris le rend difficile à repérer dans la neige.
- Dans les couloirs glacés étroits, la retraite est souvent la seule chance de survie.
