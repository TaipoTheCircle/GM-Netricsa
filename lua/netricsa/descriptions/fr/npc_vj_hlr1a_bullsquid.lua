ALPHA BULLSQUID DE XEN


CLASS: Calmar-bull, Alpha
METABOLISM: sommet omnivore
PERCEPTION: Prédateur terrestre
SIZE: 10-12 pieds
ENDURANCE: très élevé
HOSTILITY: extrême
WEAPONS: griffes, coup de queue, 9 mâchoires tentacules, crachat toxique (plus fort)
REWARD: 750 FC
THREAT: très élevé

DESCRIPTION: 

L'Alpha Bullsquid est un cousin colossal et plus terrifiant du Bullsquid standard. Marqué par sa carapace jaune et noire, ses yeux rouges brillants et ses neuf puissantes mâchoires tentacules (par rapport aux six de ses petits parents), ce prédateur domine son territoire. Ses vrilles buccales blanches se tordent de manière menaçante avant de se jeter, tandis que son crachat toxique amélioré brûle avec une plus grande puissance, capable de fondre à travers l'armure et la chair. Plus grand et plus fort qu'un Bullsquid normal, craint même par les autres espèces exotiques. En rencontrer un n’est pas seulement dangereux, c’est généralement mortel.

TIPS: 

- Son crachat toxique est plus précis et plus mortel que la ligne de vue directe d'un Bullsquid normal.
- Continuez à avancer ; son balayage de queue élargi a une portée étendue.
- Les explosifs lourds ou les armes de gros calibre sont le seul moyen sûr de l'abattre.
