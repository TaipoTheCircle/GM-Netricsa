ZOMBIE CREEPER DU MONDE MINECRAFT


CLASS: Zombie rampant
METABOLISM: Mort-vivant
PERCEPTION: Foule hostile
SIZE: 5,5 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrêmement élevé
WEAPONS: S'autodétruire, infecter les villageois
REWARD: 110 FC
THREAT: Très élevé

DESCRIPTION: 

Il s’agit d’un hybride qui se produit lorsque deux ennemis familiers se combinent en un cocktail cauchemardesque.

The Zombie Creeper looks like a classic creeper, but with zombie elements: peau verte, vêtue de haillons de zombie, et son visage emblématique. Il n'explose pas immédiatement en voyant le joueur, mais se comporte initialement comme un zombie.

Tel un zombie, il traque les villageois. Comme une plante grimpante ordinaire, elle explose lorsqu'elle s'approche de sa cible.

Contrairement aux zombies ordinaires, les plantes grimpantes zombies n'ont pas peur de la lumière du jour. Ils ne brûlent pas et peuvent donc être rencontrés à tout moment de la journée.

Ils ignorent la mécanique des creepers (se faufiler) et courent simplement pour mordre et exploser.

Comme les zombies, ils peuvent attaquer en groupe, créant une réaction en chaîne d'explosions.

TIPS: 

- Gardez vos distances. La meilleure arme contre eux est un arc ou une arbalète. Ne les laissez pas s'approcher.
- Utiliser l'environnement. Des passages étroits, de l'eau ou de la lave peuvent contribuer à les ralentir ou à les détruire avant qu'ils n'explosent.
- Run and attack. If you're forced to fight in close combat, use the "hit and run" technique: courez, frappez et sautez immédiatement en arrière.
- Bloquez avec votre épée, mais n'oubliez pas que bloquer n'arrêtera pas l'explosion.
- Une fois tué, il peut lâcher jusqu'à 4 blocs de dynamite (TNT).
