TOURELLE DE MITRAILLEUSE MH-110


CLASS: Tourelle de mitrailleuse MH-110
METABOLISM: Mécanique
PERCEPTION: Stationnaire
SIZE: ~3,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Varie selon le tireur
WEAPONS: Mitrailleuses jumelées de 110 mm
REWARD: 300FC
THREAT: Moyenne

DESCRIPTION: 

When Mental designed his bases, he based his thinking on a simple principle: "Tout ce qui bouge doit être soit un esclave, soit un cadavre". Pour ceux qui refusaient de devenir l'un ou l'autre, il développa des tourelles de mitrailleuses fixes - des canons jumeaux capables de broyer la chair plus rapidement que l'ennemi ne s'en rendait compte.

La plupart étaient d’accord sur la première option.

Il dispose de deux mitrailleuses, jumelées ensemble, et d'une réserve infinie de munitions (la caisse est la source de la création de munitions magiques), ainsi que d'un système de ciblage qui permet même à ceux qui n'ont jamais vu d'arme auparavant de tirer.

La cadence de tir de la tourelle est telle qu'en quelques secondes, elle peut transformer une meute de Gnaars en un désastre sanglant, ou un Cyclope en une passoire.

Les tourelles sont généralement installées dans les bases de Mental, qui peuvent être capturées et utilisées contre leur maître.

Si un ennemi se trouve derrière la tourelle, il fera pleuvoir des balles sur tout ce qui bouge. Si vous délogez l'ennemi et sautez vous-même, vous pouvez déclencher un feu d'artifice.

TIPS: 

- Si vous voyez une tourelle avec un ennemi, éliminez-le d'abord. Une tourelle sans tireur n'est qu'un morceau de métal.
- Contre les créatures rapides, une tourelle est indispensable. Les balles vous atteignent instantanément.
- Si vous voyez les jambes d'un orque dépasser de derrière une tourelle, cela signifie qu'il y a quelqu'un là-bas.
