BLACK MESA, ACCÈS EN LIAISON MONTANTE


Sam, j'espère que tu es prêt, car ce n'est pas juste un autre couloir rempli de soldats. Dès que les portes de l'ascenseur s'ouvriront, vous vous retrouverez face à un H.E.C.U. campement.

Ils ont installé des sacs de sable, monté des mitrailleuses et superposé des champs de tir. De toute évidence, ils ne veulent personne à proximité de ce bâtiment. Malheureusement, c'est exactement là que se trouve l'antenne de liaison montante.

Your task remains the same: atteignez la salle de contrôle, ajustez le réseau et envoyez le signal. Sans cette transmission, le complexe Lambda reste scellé et la dernière chance de survie de Black Mesa disparaît.
