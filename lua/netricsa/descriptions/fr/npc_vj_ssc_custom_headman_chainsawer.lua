Tronçonneuse décapitée


CLASS: Tronçonneuse décapitée
METABOLISM: Cyber-Zombie
PERCEPTION: Insensible, contrôlé par LCU
SIZE: 6 pieds
ENDURANCE: Faible
HOSTILITY: Extrême
WEAPONS: Tronçonneuse
REWARD: 400FC
THREAT: Très élevé

DESCRIPTION: 

Un soldat syrien exécuté, ressuscité des morts par Mental et contrôlé à distance par une unité de contrôle de la vie (LCU).

He no longer needs a head: il n'y a rien à penser ni rien à faire. Sa tâche est simple : tout couper pendant que les moteurs de ses scies tournent.

Après la décapitation, son système nerveux a été grossièrement recâblé via le LCU, et ses mains sont désormais fermement attachées à ses tronçonneuses.

Lorsqu'une tronçonneuse détecte une cible, elle accélère brusquement, comme si quelqu'un venait de tourner l'accélérateur jusqu'au sol, ce qui rend presque impossible toute fuite.

Les groupes de tronçonneuses sont particulièrement dangereux. Lorsqu'ils sont regroupés, ils se transforment en une tornade mouvante de lames, de sang et de hurlements de moteurs – un véritable ouragan de mort qui écrase tout sur son passage, y compris la couverture et les alliés de Mental.

TIPS: 

- Extrêmement dangereux à cause de sa bêtise. Ne le laissez jamais s'approcher.
- Le meilleur choix est un fusil à double canon ou un lance-roquettes.
- En groupe, ils forment une grêle de violence meurtrière.
