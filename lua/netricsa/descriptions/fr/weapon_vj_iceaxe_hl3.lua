PIOLET


WEAPON: Piolet
AMMO: Aucun
ROF: ~1 coup/seconde
WEIGHT: 1,2 kg

DESCRIPTION: 

Une arme de mêlée utilisée pour franchir et détruire les obstacles, ainsi que pour se défendre au corps à corps. Le piolet a une vitesse de frappe élevée et peut pénétrer les structures légères et les blindages ennemis. Sa conception permet des frappes ciblées tout en conservant la maniabilité de l'utilisateur.

NOTES: 

- Efficace contre des adversaires isolés et des cibles légèrement blindées.
- Peut être utilisé pour briser la glace et les barrières en bois.
- Sa conception compacte et légère le rend facile à transporter dans des conditions difficiles.
