PERSEPOLIS, LA POUSSIÈRE DES SIÈCLES SUR LES OS DES EMPIRES


Sam, Voici Persépolis, l'ancienne capitale de l'empire achéménide, la « Cité des Perses », qu'Alexandre le Grand transforma en un gigantesque feu de joie en 330 avant JC.

Il ne reste plus que désert, vent et ruines qui rappellent la marche des peuples du Nil à l'Indus.

Imaginez une plaine sablonneuse sans fin, sur laquelle flotte une brume. Au milieu, sur une plate-forme massive en pierre, s'élèvent les squelettes de colonnes, d'escaliers et de portes.

The ruins retain traces of their former grandeur: 

Apadana - la salle de cérémonie de Darius I. Il était une fois 72 colonnes s'élevant ici, soutenant le toit. Il n’en reste plus que 13. Mais même eux sont stupéfiants. Des escaliers géants y mènent, autrefois utilisés par les chars et les délégations des peuples conquis portant des cadeaux.

Tachara est le palais résidentiel de Darius I. Il ne reste que les fondations, les portes et les parties inférieures des murs avec des bas-reliefs.

La salle des Cent Colonnes est une immense salle dont il ne reste que les bases des colonnes et les portes en pierre avec des scènes de bataille.

Le harem de Xerxès est composé de vingt-deux petites pièces où auraient pu vivre des femmes et des enfants.

Le tombeau de Darius III est le tombeau inachevé et naturellement dévasté du dernier roi de la dynastie, qui a fui Alexandre et a été tué par son propre peuple.

Cette ville n’était pas seulement une capitale, mais le symbole d’un empire s’étendant de l’Égypte à l’Inde. Des artisans du monde entier – Babyloniens, Égyptiens, Ioniens et Assyriens – l’ont construit. Cependant, le travail des esclaves n’était pas utilisé ici, ce qui était rare à l’époque. La ville disposait d'un système complexe d'approvisionnement en eau et d'assainissement. Alexandre l'a entièrement brûlé, soit dans une stupeur ivre, incité par la courtisane athénienne Thais, soit délibérément, comme un acte de vengeance pour l'incendie de l'Acropole.

Des exemplaires inestimables de l'Avesta, écrits à l'encre d'or sur des peaux de bœuf, périrent dans l'incendie. Après cela, la ville a perdu de son importance et Istakhr, le centre spirituel du zoroastrisme, s'est développée à proximité. Les ruines de Persépolis sont désormais inscrites au patrimoine mondial de l'UNESCO.
