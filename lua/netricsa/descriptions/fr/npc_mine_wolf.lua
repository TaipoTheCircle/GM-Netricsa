LOUP DU MONDE MINECRAFT


CLASS: Loup
METABOLISM: Mammifère à sang chaud
PERCEPTION: Animal neutre / apprivoisé
SIZE: 2,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Modéré
WEAPONS: Crocs
REWARD: 60 FC
THREAT: Moyenne

DESCRIPTION: 

Les loups sont des prédateurs sauvages du Minecraft Overworld, habitant les forêts, la taïga et les biomes enneigés. Dans leur état neutre, ils n'attaquent pas, mais deviennent extrêmement agressifs s'ils sont provoqués ou attaqués par un autre loup.

Un loup apprivoisé devient un fidèle compagnon de combat. Il suit son maître, le protège au combat et attaque les cibles touchées par son maître.

A wolf's health can be easily determined by the position of its tail: plus la queue est basse, plus elle est gravement blessée.

TIPS: 

- Utilisez des os pour apprivoiser.
- Nourrissez la viande de loup pour la guérison et la reproduction.
- Les loups sont particulièrement efficaces contre les squelettes et les zombies, mais sont vulnérables à la lave et aux explosions.
