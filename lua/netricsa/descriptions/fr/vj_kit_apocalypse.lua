KIT BODEGA


CLASS: Kit Bodega
METABOLISM: Chat anthropomorphe, Cyborg
PERCEPTION: Membre de l'équipe "Support"
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Variable
WEAPONS: Griffes, Blaster, PDA, Kibble Star Star Cannon
REWARD: 200FC
THREAT: Variable

DESCRIPTION: 

C'est un chat anthropomorphe avec une fourrure beige, des cheveux bruns, des yeux roses et des lunettes à monture jaune distinctive avec des lentilles rouges.

Elle porte une combinaison jaune avec une bande blanche en zigzag, complétée par des sous-armures noires, des brassards épais et des cretons dont les cretons rappellent les pattes de chat. Son nom complet se traduit par « Equipment Warehouse » (Kit - kit, Bodega - entrepôt), reflétant sa capacité à matérialiser des objets.

Kit est originaire d'un monde de jeu habité par des chats anthropomorphes, où elle et son partenaire, Kaboodle, étaient les personnages principaux. Ils ont juré de protéger leur village natal des méchants Malice et Mayhem, mais à la suite de leur victoire, le monde a été détruit par un cataclysme, tuant tout le monde sauf Kit et Kaboodle. Au dernier moment, le mystérieux Twilight les a sauvés en les recrutant dans l'équipe "Support".

Now Kit and Kaboodle's task is to travel through different worlds and prevent the local heroes from defeating the villains. The irony of fate: ils sont obligés de jouer le rôle de méchants pour sauver les mondes de la destruction qui suit inévitablement la victoire du héros. Voyagent avec eux les héros sauvés d'autres mondes : Gobbles (un dinosaure) et Flappers (un dauphin).

Kit is distinguished by her courage, optimism, and resourcefulness. The destruction of her homeworld left her with deep psychological trauma: tout monde ressemblant à son monde natal évoque des souvenirs de la destruction de son village, la poussant parfois à agir de manière imprudente. Elle se reproche son incapacité à contrôler ses émotions, mais cette même émotion lui donne l'énergie nécessaire pour sauver ceux qui peuvent encore l'être.

Au combat, Kit utilise un blaster, un PDA pour changer son apparence, et dans une situation critique, elle se combine avec Kaboodle pour former l'armure "Kibble Star", devenant rapide comme l'éclair et ayant accès au Kibble Star Star Cannon, un faisceau de puissance destructrice.

TIPS: 

- Le monde sera détruit après la victoire du héros, pas après la défaite. N'oubliez pas cela lorsque vous choisissez un camp.
- Protégez-la, car le sort du monde repose sur elle.
