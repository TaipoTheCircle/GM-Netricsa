DAEDABUS DE L'ENFER


CLASS: Grenades à main
METABOLISM: Mammifère démoniaque mutant
DEFINITION: Béhémoth modifié
SIZE: 13 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Deux canons à gaz explosifs verts
REWARD: 5000FC
DANGER: Absolu

DESCRIPTION: 

Un démon personnifiant l'imperfection humaine, l'avidité, la gourmandise et l'oisiveté. Il a une peau incroyablement épaisse, des pattes éléphantines massives et une petite tête. Dans le but de détruire et de rôtir ses victimes, le Daedabus possède deux canons implantés dans ses avant-bras qui tirent des boules d'acide explosives.

Au lieu de la chair démoniaque habituelle, son corps a acquis une teinte vert marécageux, et à la place de ses deux yeux, il y a maintenant un seul grand œil vert brillant d'énergie Argent. Cette énergie n'a pas changé sa personnalité - il est toujours aussi méchant, stupide et destructeur - mais elle l'a rendu physiquement plus fort.

Contrairement au Mancubus ordinaire avec ses lance-flammes, le Daedabus tire un gaz explosif vert, acide, la même substance qui remplit les barils explosifs des bases de l'UAC.

Comme beaucoup d'autres habitants de l'Enfer, le Daedabus a été acheté par Mental au Seigneur de l'Enfer pour renforcer sa Horde. Désormais, ces démons verts parcourent les planètes contrôlées par Mental, semant la mort partout où marchent leurs pieds lourds.

TIPS: 

- Ils tirent au hasard, alors restez à couvert.
- Ne conservez pas de munitions à moins que vous ne vouliez mourir.
