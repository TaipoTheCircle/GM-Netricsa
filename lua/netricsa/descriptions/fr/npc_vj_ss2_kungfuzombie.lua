KUNG FU ZOMBIE, APPRENTI DU CHI-FANG


CLASS: Kung Fu Zombie, apprenti
METABOLISM: Mort-vivant
PERCEPTION: Artiste martial zombie
SIZE: 6 pieds
ENDURANCE: Faible
HOSTILITY: Très élevé
WEAPONS: Poings, scie
REWARD: 100 FC
THREAT: Moyen

DESCRIPTION: 

Ils servent à la fois d'application de la loi et de serviteurs du prince Chang. Mental a créé ces zombies en extrayant leurs entrailles et leur cerveau, et leur a inculqué une hostilité envers tout ce qui bouge.

Soon, Mental had amassed a sizable army of martial arts enthusiasts. They mastered only one move: un coup de pied volant. Et comme les zombies avaient de la chair pourrie, un coup de pied pourri les ferait s'effondrer.

Mental a décidé qu'il serait plus facile d'y attacher des sabots.

Alors que la plupart des serviteurs de Mental reçoivent une formation minimale, les Kung Fu Zombies ont suivi une formation approfondie en regardant des films de kung-fu des années 80 et 90.

Mais même cela ne suffisait pas, et chacun d'eux reçut bientôt une paire de shurikens mortels pour combattre les menaces à longue portée.

Désormais, armés de leurs connaissances des scies et du cinéma centenaire, ils se lancent dans la bataille et écrasent quiconque s'oppose à leur grand professeur, Sensei Mental.

Ils vous lanceront leurs scies de loin et vous donneront des coups de pied à leur approche.

TIPS: 

- Esquivez leurs coups de pied en les contournant.
- Éliminez-les avec un fusil de chasse à double canon à bout portant.
