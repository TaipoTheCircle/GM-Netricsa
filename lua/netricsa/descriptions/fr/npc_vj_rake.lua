RÂTEAU


CLASS: Râteau
METABOLISM: Charognard
PERCEPTION: Prédateur anthropomorphe nocturne
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Moyen
WEAPONS: Les griffes, facteur de surprise
REWARD: 100 FC
THREAT: Haut

DESCRIPTION: 

Le Rake est une créature qui n'existe que dans la mesure où une personne est prête à y croire. Une légende urbaine, étoffée par des témoignages oculaires, des discussions sur des forums et des expéditions de harceleurs. Pas une seule attaque mortelle documentée, mais des milliers d'histoires de quelque chose de mince, pâle et griffu se tenant dans le coin d'une chambre et regardant jusqu'à ce que vous ouvriez les yeux.

Selon la mythologie, le Reik est un humanoïde mince avec une peau pâle, de longues griffes acérées et parfois des traits canins. Il habite les forêts et les égouts, évitant la lumière du soleil en raison de sa peau fine. Il est actif exclusivement la nuit. Il se nourrit de charognes, que l'on voit le plus souvent au bord des routes, où il attrape les victimes de la route. Il a peur des gens et essaie d’éviter de les rencontrer en personne.

Il existe plusieurs sous-espèces. Les Reiks forestiers vivent au cœur de forêts denses, sont censés parler et faire des gestes, sont faciles à communiquer et ne présentent aucun danger. Les Reiks des égouts sont plus gris, ont une queue et un museau allongé, et se déplacent à quatre pattes, en s'appuyant sur leurs pattes antérieures, comme les gorilles. Ils sont considérés comme dangereux, bien qu’il n’existe aucune preuve à l’appui.

Nightly visiting creatures occupy a special place in the legend. The Reik supposedly enters houses, stands by the bed, and watches the sleeping person, waiting for them to awaken. Scientists attribute this to sleep paralysis: une personne rêve de ce qu'elle craint, et comme le Rake fait peur, il apparaît. Les harceleurs, quant à eux, parcourent des bâtiments abandonnés et des forêts équipés de caméras thermiques, dans l'espoir de le filmer.

TIPS: 

- Si vous êtes dans la forêt et voyez une silhouette pâle à quatre pattes, ne courez pas. Le râteau est plus rapide.
- Dans les égouts, éloignez-vous des branches sombres, les sous-espèces des égouts sont plus agressives que celles des forêts.
- Après avoir rencontré le Rake, vous ferez probablement des cauchemars. C'est normal. Cela passera dans une semaine. Ou ce ne sera pas le cas.
