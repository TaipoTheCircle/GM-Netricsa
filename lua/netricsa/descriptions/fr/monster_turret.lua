TOURELLE BLINDÉE MESA NOIRE


CLASS: Tourelle blindée Mesa noire
METABOLISM: Construction mécanique
PERCEPTION: Unité de défense automatisée
SIZE: ~4 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Mitrailleuse lourde à tir rapide
REWARD: 600 FC
THREAT: Haut

DESCRIPTION: 

La tourelle blindée Black Mesa est une unité de défense automatisée fortement renforcée conçue pour sécuriser les couloirs, les laboratoires et les installations sensibles. Contrairement aux tourelles de sentinelle standard, elle peut être montée au sol ou inversée au plafond, ce qui permet des angles de tir imprévisibles.

Son blindage offre une résistance aux armes légères, tandis que sa mitrailleuse lourde à tir rapide délivre un tir de suppression mortel. Déployée dans des points d'étranglement ou des couloirs étroits, la tourelle peut contrôler l'accès avec une efficacité dévastatrice, rendant les embuscades et les assauts frontaux extrêmement dangereux.

TIPS: 

- Viser les points faibles structurels ; l'armure protège la plupart des zones frontales.
- Les explosifs ou la puissance de feu lourde sont efficaces contre lui.
- Surveillez les tourelles montées au plafond – vérifiez toujours ci-dessus.
- Évitez de rester dans des espaces ouverts ; leurs champs de tir sont mortels.
