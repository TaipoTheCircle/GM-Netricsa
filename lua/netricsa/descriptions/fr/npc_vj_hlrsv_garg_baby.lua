BÉBÉ GARGANTUA DE XEN


CLASS: Gargantua, bébé
METABOLISM: prédateur xénobiologique miniature
PERCEPTION: Stade précoce de Gargantua
SIZE: 5-6 pieds de haut
ENDURANCE: moyen
HOSTILITY: modéré
WEAPONS: un petit lance-flammes éclate, piétine, hurle fort
REWARD: 300FC
THREAT: faible individuellement, modéré en groupe

DESCRIPTION: 

Le bébé Gargantua est ce qui se produit lorsqu'un Gargantua saute quelques étapes de son évolution. Il a toujours un penchant pour le feu et pour briser des objets, mais son caractère colérique est généralement plus hilarant que mortel. Considérez-le comme un tout-petit pyromane doté d'une super force.

On voit souvent des bébés Gargantuas se promener après les plus grands ou les imiter dans une destruction chaotique et adorable. en meute, ces petites punaises de feu peuvent créer le chaos presque aussi efficacement que leurs parents.

TIPS: 

- Ne sous-estimez pas la gentillesse – elle s'accompagne de fumée et de feu.
- Les regrouper peut rendre une petite rencontre étonnamment compliquée.
- Les risques environnementaux tels que l'effondrement de structures peuvent aider à égaliser les chances.
