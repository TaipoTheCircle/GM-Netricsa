BLACK MESA, PANNES DANS LE SECTEUR "C"


D'accord, vous êtes dans le secteur "C". Il y a du bruit au niveau du panneau de commande. Un agent de sécurité et deux scientifiques tentent de réparer quelque chose. Les boutons ne répondent pas, les écrans clignotent, les fils fument. Le panneau est sérieusement cassé - quelqu'un a tiré les leviers non conformément aux instructions, quelqu'un était pressé.

L'odeur de l'ozone et de l'isolation brûlée fait mal au nez. Le sol tremble, et il y a ce froid, pas physique, qui me fait picoter la nuque. Il est là.

L’homme en costume d’affaires se trouve quelque part à proximité. Vous ne pouvez pas le voir, mais vous savez qu'il est là. Valise à la main, souriez comme une lame. Regarder. N'interfère pas. Il attend que le mécanisme tombe complètement en panne.
