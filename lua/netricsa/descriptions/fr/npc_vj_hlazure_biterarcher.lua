MORDANT DE XEN


CLASS: Créature aquatique Xen (prédateur)
METABOLISM: Carnivore amphibie extraterrestre
PERCEPTION: Prédateur sous-marin extraterrestre
SIZE: ~3,5 pieds de long
ENDURANCE: Moyen-Faible
HOSTILITY: Haut
WEAPONS: Morsure pointue (mêlée uniquement)
REWARD: 200FC
THREAT: Faible-Modéré

DESCRIPTION: 

Le Biter est la variante verte de la même espèce Xen que l'Archer. Contrairement à son parent rouge, il manque de capacités à distance et repose entièrement sur la morsure. Les mordeurs sont rapides sous l'eau et tenteront d'assaillir leurs proies, les submergeant de nombre plutôt que de cracher de l'acide.

TIPS: 

- Tenez-les à distance avec des armes à distance : ils ne peuvent pas attaquer à distance.
- Les fusils de chasse ou les armes à tir rapide sont efficaces s'ils se rapprochent.
- Plus facile à envoyer que les Archers, mais dangereux s'ils sont ignorés en groupe.
