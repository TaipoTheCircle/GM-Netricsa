SQUELETTE KLEER, GRAND


CLASS: Squelette Kleer, grand
METABOLISM: mort-vivant
PERCEPTION: créature magique
SIZE: 15 pieds
ENDURANCE: faible
HOSTILITY: moyen
WEAPONS: projectiles à deux billes, griffes, cornes
REWARD: 1000FC
THREAT: moyen

DESCRIPTION: 

Le squelette mort-vivant d'un membre d'une race éteinte de la planète Clear dans le système Alpha Canis Majoris. Il a des griffes acérées sur ses pattes avant et une longue queue.

A distance, il lance des boules métalliques reliées par une chaîne, et au corps à corps, il utilise ses griffes. Au cours des expériences de Mental visant à modifier la taille des créatures, ce squelette a été amélioré et agrandi par magie jusqu'à la taille d'un reptiloïde des plaines.

TIPS: 

- Sortez du champ de vision de l'attaque.
