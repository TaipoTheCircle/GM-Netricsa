CLAUDOVIC


WEAPON: Clawdovic
AMMO: Lui-même
ROF: ~60 Clawdovics/minute

DESCRIPTION: 


Le Clawdovic est un perroquet kamikaze psychotique armé d'une bombe. Malgré son apparence quelque peu gênante, cet oiseau est capable de renverser même les rhinocéros. En vol, il crie quelque chose d'inintelligible (Kurly...), mais cela semble lisible...

This is a bird-bioweapon from planet M'Digbo, used by the Simbas. It has been modified using a poisonous flower to turn its stomach into a bomb that explodes on impact. When thrown, Clawdovic acts like a homing projectile: il vole vers l'ennemi que l'utilisateur regarde.

NOTES: 

- Homing ability is imperfect: sur des distances moyennes à longues, un mouvement soudain de la cible peut faire rater ou dévier Clawdovic.
- Son utilisation à courte distance peut entraîner des blessures causées par l'explosion.
- Très efficace contre les ennemis de type boss et les cibles de grande taille ou aéroportées comme le Cyclope Albinos ou les hélicoptères d'attaque.
- Un approvisionnement limité en munitions fait tout.
