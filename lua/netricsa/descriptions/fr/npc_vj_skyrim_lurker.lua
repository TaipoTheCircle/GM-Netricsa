LURKER D'APOCRYPA


CLASS: Rôdeur
METABOLISM: Apparition Daedra, amphibie
PERCEPTION: Gardien des livres noirs
SIZE: 17 pieds
ENDURANCE: Haut
HOSTILITY: Très élevé
WEAPONS: Griffes, pieds piétinants, tentacules venimeux, crachat acide
REWARD: 1000FC
THREAT: Haut

DESCRIPTION: 

Créatures daedriques originaires des eaux brumeuses d'Apocrypha, le plan d'Hermaeus Mora. Dans la nature, ils gardent des tomes de connaissances interdites, où ils attaquent désormais les habitants et les voyageurs aléatoires.

Les rôdeurs ressemblent à un hybride de poisson, de crapaud et de gorille, avec des éléments de quelque chose de profond. Ils ont trois orteils à chaque pied et des pattes postérieures massives avec lesquelles ils piétinent, secouant le sol. Des tentacules peuvent sortir de leur bouche pour des attaques à moyenne portée, et leur crachat acide peut corroder l'armure en quelques secondes.

TIPS: 

- La broche acide a une longue portée. Esquivez ou mettez-vous à couvert.
- Si un rôdeur attaque avec ses tentacules, reculez.
- Il est préférable de les tuer à distance avant qu'ils ne s'approchent trop.
- Ils sont vulnérables au froid.
