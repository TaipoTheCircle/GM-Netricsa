VOYAGE EN TRAIN, IL EST TEMPS DE PARTIR


Sam, nous sommes incroyablement chanceux d'être ici. Ce train est une aubaine, même s'il tient, comme disent les ingénieurs locaux, « aux ficelles et aux prières ». Montez, allons voir ce qu'il y a ici.

The train is everything. It pulls a bunch of cars: trois voitures à compartiments, six sièges réservés et un vide, pour plus de variété. À l’intérieur, vous pouvez ouvrir les portes, allumer les lumières et même grimper sur les échelles de toit incluses.

The main lever in the driver's cabin is the stop/start switch, so you decide when to go and when to get out and explore the surroundings. By the way, there's more than one route: il y a trois boucles interchangeables, donc le décor va changer.

Nous traversons la campagne jusqu'à la ville. Et ainsi de suite, sans fin. La boucle est fermée, vous pouvez donc vous détendre et regarder par la fenêtre.

Features: 

Le train regorge de boutons pour tout allumer et éteindre. Aimez-vous appuyer sur des boutons? C'est l'endroit pour vous.

Il y a des secrets. Ils ne diront pas où exactement, mais il y a certainement des endroits cachés.

Montez dans un compartiment ou une voiture-lits, tirez le levier d'arrêt chaque fois que vous voulez regarder autour de vous et n'oubliez pas les boutons. Si la technologie commence à avoir des problèmes, soyez simplement reconnaissants que nous puissions voyager. Allons-y, Sam !
