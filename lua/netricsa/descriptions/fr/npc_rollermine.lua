COMBINER ROLLERMINE


CLASS: Combiner Rollermine
METABOLISM: Entièrement synthétique (noyau énergétique combiné)
PERCEPTION: Engin explosif autonome
SIZE: ~1,5 pied de diamètre
ENDURANCE: Faible
HOSTILITY: Haut
WEAPONS: Décharge de choc magnétique, auto-détonation explosive, attaque par bélier
REWARD: 75 FC
THREAT: Faible-Moyen

DESCRIPTION: 

Le Combine Rollermine est une arme autonome petite mais dangereuse déployée par le Combine comme outil de déni de zone. Conçus pour rouler rapidement sur le terrain, ces appareils sphériques utilisent l'attraction magnétique pour s'accrocher aux véhicules ou aux ennemis, les électrocutant avec des éclats d'électricité à haute tension.

Bien qu'individuellement faibles, les Rollermines excellent dans les essaims, perturbant les mouvements de résistance, tendant des embuscades aux escouades et arrêtant les véhicules en s'accrochant à elles. Leur coque légère les rend vulnérables aux tirs d'armes légères, mais leur agilité et leur comportement implacable à tête chercheuse les rendent frustrants à éliminer dans le feu de l'action.

Une fois détruit, leur noyau instable de Combine peut exploser.

TIPS: 

- Utilisez le pistolet à gravité pour repousser les Rollermines ou les renvoyer sur les ennemis.
- Continuez à avancer ; ils sont rapides mais prévisibles lorsqu’ils rentrent.
