NID DE FRELONS DU XEN


CLASS: Nid de frelons
METABOLISM: Organisme biologique Xen
PERCEPTION: Générateur stationnaire, tourelle organique
SIZE: 6,5 pieds
ENDURANCE: Haut
HOSTILITY: Absolu
WEAPONS: Frelons
REWARD: 150 FC
THREAT: Haut

DESCRIPTION: 

Il s'agit d'une énorme croissance organique trouvée sur les murs, les sols et les plafonds (et probablement dans d'autres endroits où les créatures de Xen ont atteint). Cela ressemble à un cône. À l’intérieur de cette croissance, des créatures petites mais extrêmement méchantes – les frelons – pullulent.

Le mécanisme du Nest est simple et terrifiant. Dès qu'une cible vivante s'approche du Nest, celui-ci s'active. L'un des Hornets suicides sort de la végétation et vole droit sur la cible.

Un Suicide Hornet ne vit que le temps nécessaire pour lancer une seule attaque. Il meurt après une attaque, mais le Nest en produit un nombre illimité. Une seconde après la destruction d'un Hornet, un autre s'envole hors du nid. Cela continue à l'infini, jusqu'à ce que le nid soit détruit ou que la cible quitte la zone de mise à mort.

TIPS: 

- Détruisez le Nid à distance. Les explosifs ou les armes lourdes sont vos meilleurs amis.
- Ne vous approchez pas trop du Nest. Les frelons s'envolent avec un léger retard, mais si vous êtes dans la zone de destruction, ils sont difficiles à esquiver.
- Un seul Hornet est faible, mais le flux sans fin est épuisant. Tuez le Nest et le problème disparaît.
- Une fois détruit, le Nid se désintègre en morceaux de chair, libérant parfois un nuage d'acide.
- Le Nest ne bouge pas et ne s'enfuit pas. Il s’agit d’un piège stationnaire, mais très efficace.
