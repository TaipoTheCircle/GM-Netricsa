FOUR MÉTAL SONIC DU MOBIUS


CLASS: Four Métal Sonic
METABOLISM: Robot tueur mécanique
PERCEPTION: Machine de destruction et de cuisson / Chef personnel d'Eggman
SIZE: 3,3 pieds
ENDURANCE: Très élevé
HOSTILITY: Absolu
WEAPONS: Four thoracique intégré, boules de feu, griffes, super vitesse
REWARD: 10000FC
THREAT: Absolu

DESCRIPTION: 

Il s'agit de Metal Sonic, reconverti à ses propres fins cannibales par le fou Docteur Eggman, connu sous le nom de Hungry. Au lieu de simplement vaincre Sonic, Furnace doit le capturer, le faire cuire dans le four situé dans sa poitrine et le servir à Eggman pour le dîner.

Furnace est un tueur de sang-froid et calculateur. Son objectif premier est d'obéir aux ordres de son créateur, et il aborde cela avec le plus grand sérieux. Ce n'est pas seulement une machine stupide ; il est capable d'analyser le comportement de ses proies, de repérer leurs faiblesses et de les utiliser contre elles. Dans un scénario, Furnace simule même sa propre dépression pour endormir Sonic dans un faux sentiment de sécurité avant l'attaque décisive.

Visuellement, Furnace diffère du Metal Sonic classique. Il semble plus industriel et utilitaire, construit à partir de couches brutes de métal et de boulons. L'élément le plus frappant est son coffre, qui est une véritable fournaise dotée d'une grille contenant un feu qui fait rage. Contrairement à Metal Sonic, il n'essaie pas de prouver sa supériorité dans une course ; il veut simplement tuer.

He's a formidable opponent, capable of fighting Sonic on equal terms. His arsenal includes: 

Furnace: Son arme principale. Peut s'ouvrir pour piéger une victime à l'intérieur ou émettre de puissants jets de flammes.

Super Speed: Capable d'atteindre des vitesses suffisamment rapides pour poursuivre un Sonic en marche.

Fireballs: Peut déclencher des jets de flammes à distance.

Sharp Claws: Utilisé en combat rapproché.

Physical Strength: Beaucoup plus fort que le Metal Sonic ordinaire.

Durability: Très résistant et capable de résister à des attaques puissantes.

TIPS: 

- Ne le sous-estimez jamais. Il est intelligent et peut prédire vos mouvements.
- On dit que Furnace est capable de détruire même son créateur si les circonstances l'exigent. Gardez toujours cela à l’esprit.
