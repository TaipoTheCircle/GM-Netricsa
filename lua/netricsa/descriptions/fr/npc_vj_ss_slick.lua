JAKE DE LA VILLE DES SURFEURS DU MÉTRO


CLASS: Jacques
METABOLISM: Humain, mâle
PERCEPTION: Type glissant, Parkourer
SIZE: ~5,2 pieds
ENDURANCE: Infini
HOSTILITY: Faible
WEAPONS: Peinture En Aérosol, Skateboard, Jetpack, Exécuter
REWARD: 500FC
THREAT: Zéro

DESCRIPTION: 

A typical teenager in bright clothes: un sweat à capuche orange, un jean bleu, des baskets blanches et une casquette de baseball à l'envers. Il a les cheveux foncés, les yeux foncés et un sourire distinctif : il apprécie clairement chaque course.

IL est un graffeur qui, avec son ami Tricky et d'autres enfants, décore des wagons de métro dans des villes du monde entier. Son thème de prédilection est celui des pandas, qui apparaissent souvent dans ses dessins.

Son objectif est de s'enfuir. Pour échapper à l'inspecteur et à son chien, qui maintiennent l'ordre sur la voie ferrée. Pour une durée infinie. Sur les rails, sur les toits des trains, dans les tunnels et sur les quais.

Pendant la course, Jake utilise une planche à roulettes pour la vitesse, un jetpack pour voler et un aimant pour attirer les pièces. Ses principaux ennemis sont l'approche des trains et les impasses.

TIPS: 

- Fonctionne vite.
- Utilise des power-ups.
