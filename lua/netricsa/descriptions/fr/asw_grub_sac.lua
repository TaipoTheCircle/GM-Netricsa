SAC À LAVERS D'ESSAI


CLASSE : Sac de larves d'essaim
MÉTABOLISME : Xénobiologique
PERCEPTION : Organisme stationnaire
TAILLE : 4 pieds
ENDURANCE : Moyenne
HOSTILITÉ : Faible
ARMES : Décharge de larves
RÉCOMPENSE : 75 FC
MENACE : Faible

DESCRIPTIF :

Le Swarm Grub Sac est un sac organique stationnaire dans lequel Swarm Grub se développe. Sa coque extérieure est solide et élastique, capable de résister à des dommages mécaniques importants et aux fluctuations de température.

Le Swarm Grub Sac contient quatre Grub matures, prêts à éclore. Lorsqu'elle est exposée au feu, à de fortes vibrations ou à l'approche de gros objets vivants, la coque s'ouvre et se divise en plusieurs rabats. Les Grub sont expulsés et commencent immédiatement à chercher une cible.

Les sacs à larves se trouvent généralement en groupes sur les murs, les plafonds et dans les endroits difficiles d'accès. Leur fonction principale est d'assurer le développement sécuritaire de la progéniture de l'essaim jusqu'à l'apparition des proies.

Malgré la faible menace directe, un grand nombre de sacs de larves actifs au même endroit est le signe d'une forte densité d'essaims et de l'émergence imminente de formes de vie plus dangereuses.

CONSEILS :

- Détruisez le Grub Sack avant qu'il ne s'ouvre.
- Un seul Grub Sack n'est rien. Les problèmes commencent lorsqu’ils sont nombreux.
- Le feu et les explosifs sont le meilleur moyen contre les Grub Sacs.
- Une fois le Grub Sack ouvert, ne perdez pas de temps sur le Grub.
- Il suffit du bruit et du mouvement pour réveiller la couvée.