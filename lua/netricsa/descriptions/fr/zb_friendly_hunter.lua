CHASSEUR REBELLE DE LA RÉSISTANCE


CLASSE : Chasseur rebelle de la Résistance
MÉTABOLISME : Biomécanique Synthétique
PERCEPTION : Unité de soutien de la Résistance Lourde
TAILLE : 7,8 pieds
ENDURANCE : Élevée
HOSTILITÉ : Extrême
ARMES : Needler, Ram, Blades
RÉCOMPENSE : 700 FC
MENACE : Très élevée

DESCRIPTIF :

Le Resistance Rebel Hunter est un Combine synthétique standard qui a été capturé et reprogrammé avec succès par les ingénieurs de la Résistance. Après avoir été piratés, ses systèmes de contrôle ont été complètement réécrits, l'obligeant à abandonner l'Alliance et à rejoindre la Rébellion.

Ce Hunter se distingue facilement de l’original en apparence. Sa coque a été repeinte en noir mat, la lueur bleue familière du capteur a été remplacée par un orange vif et le dessus de la coque porte le symbole reconnaissable Lambda, devenu l'emblème de la Résistance.

Malgré le changement de propriétaire, les capacités de combat du véhicule restent pratiquement inchangées. Le Hunter est toujours capable d'effectuer des sauts longs et à grande vitesse, de surmonter les obstacles et de réduire instantanément la distance avec l'ennemi. Ses puissants membres antérieurs sont utilisés pour délivrer des coups puissants et perçants, capables de pénétrer les armures légères et de neutraliser même l'infanterie lourde.

L'arme principale reste le lanceur d'aiguilles intégré, mais le système reprogrammé utilise des fletchettes orange. Lors de l'impact, ces projectiles s'incrustent dans la cible ou la surface, puis explosent après un court délai, posant un danger à la fois au personnel et aux véhicules.

Contrairement aux synthés Combine, les Chasseurs reprogrammés reconnaissent les combattants de la Résistance comme des alliés et les soutiennent activement au combat. Ils accompagnent les équipes d'assaut, défendent les positions fortifiées et sont capables de poursuivre indépendamment les unités combinées jusqu'à ce que la menace soit complètement éliminée.

Bien que ces machines combattent désormais aux côtés de l’humanité, elles restent les mêmes synthés de combat mortels. Toute erreur lors de la reprogrammation peut entraîner un retour aux protocoles d'origine, c'est pourquoi chaque Chasseur est constamment surveillé par les ingénieurs de la Résistance.

CONSEILS :

- Évitez de vous attarder dans les zones ouvertes : les Fletchettes explosent avec un léger retard et affectent une grande surface.
- Après avoir percuté, le Hunter perd brièvement de la vitesse, ce qui lui donne la possibilité de riposter.
- La zone la plus vulnérable reste la tête et le capteur central.
- Un armement explosif puissant perturbe efficacement la stabilité du synthé et permet sa neutralisation plus rapide.
- Malgré le symbolisme de la Résistance, le spécimen hostile conserve toute la létalité du Chasseur d'Alliance original.