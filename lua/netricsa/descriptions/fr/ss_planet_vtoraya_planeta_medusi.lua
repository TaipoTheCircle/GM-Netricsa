LA DEUXIÈME PLANÈTE DU SYSTÈME MÉDUSE


PLANET: La deuxième planète du système Medusa
TYPE: Planète terrestre
CLIMATE: Atmosphère tempérée et irrespirable, pluies occasionnelles
MOONS: Inconnu
TERRAIN: Plaine désertique, falaises scintillantes, pierres rondes éparses
LOCATION: Le système stellaire Méduse, à la périphérie de la Voie lactée
NATIVE RACES: BLACK MESA, BRIEFING SCIENTIFIQUE - PARTIE 6
AFFILIATION: Inconnu
LOCATIONS: Plaine d'atterrissage "Pegasus", sites d'apparition d'illusions

DESCRIPTION: 

C'est un monde silencieux et désolé, sa plaine rocheuse parsemée de pierres rondes ressemblant à des pommes de terre. Vu de l'orbite, il semble sombre et inhospitalier. L'atmosphère de la planète est totalement impropre à la respiration humaine ; le climat est tempéré et les précipitations sont abondantes. Des sources d'hydrocarbures lourds ont été découvertes dans le sous-sol.

La caractéristique la plus unique de la planète réside dans ses habitants autochtones. La flore locale est clairsemée, mais les véritables maîtres du monde sont des êtres intelligents d'origine inorganique, extérieurement impossibles à distinguer des pierres ordinaires.

These beings possess astonishing abilities: Ils sont capables de se souvenir de toutes les images qu’ils ont déjà vues. Les pierres peuvent reproduire ce qu'elles ont vu, voire ce qu'elles ont lu dans la tête des visiteurs, sous forme de mirages tridimensionnels et réalistes. On pense qu'ils le font par ennui, pour animer d'une manière ou d'une autre la vie quotidienne sans joie sur la planète désertique.

When the "Pegasus" landed on this planet, the passengers witnessed a series of ghostly apparitions: un cortège de mousquetaires des "Trois Mousquetaires", puis d'eux-mêmes, et plus tard des pirates de l'espace qu'ils poursuivaient. Toutes ces images étaient des illusions créées par les locaux.

Le mystère a été résolu lorsque la fille du professeur Seleznev a ramassé l'une des pierres, dont la lueur s'estompait lentement, et la pierre s'est transformée en capitaine de l'espace, l'un de ceux qui avaient déjà subi une catastrophe sur cette planète et avaient été "enregistrés" dans la mémoire de la pierre.

KEY LOCATIONS: 

- "Pegasus" Landing Plain: Une section de la plaine rocheuse désolée où le navire de recherche a atterri. C'est ici que des manifestations massives d'illusions ont été observées.
- Zones of Increased Illusory Activity: Zones d'amas de pierres sensibles, où la probabilité de mirages est la plus élevée. Les coordonnées de ces zones sont instables, car les pierres peuvent se déplacer lentement.
- Captain's Finding Location: Le point de la plaine où a été découverte la pierre qui contenait l'image de l'un des capitaines disparus.
