ALPHA H.E.C.U. MARIN


CLASS: Alpha H.E.C.U. Marin
METABOLISM: Humain, mâle
PERCEPTION: Escouade expérimentale américaine
SIZE: ~6 pieds
ENDURANCE: Moyen
HOSTILITY: Extrême
WEAPONS: Carabine Colt M4, Grenades.
REWARD: 700FC
THREAT: - Ne vous tenez jamais dans des zones ouvertes s'il se trouve à proximité.

DESCRIPTION: 

Alpha H.E.C.U. Grunts are the first wave of soldiers deployed into resonance zones. Their equipment and combat training differ from the standard units: une armure plus légère, des protocoles tactiques simplifiés et une concentration sur les assauts de choc rapides. Ils se déplacent de manière agressive et imprudente, chargeant souvent au combat sans coordination adéquate.

Contrairement au principal H.E.C.U. divisions, ces unités s'appuient rarement sur des embuscades ou des manœuvres avancées. Leur rôle principal est de pénétrer rapidement et de nettoyer les zones hostiles à tout prix, même si les pertes sont élevées. Pour cette raison, Alpha H.E.C.U. sont souvent considérées comme des forces remplaçables, envoyées en tant que sondes vivantes pour affaiblir la résistance avant l'arrivée des escouades principales.

TIPS: 

- Susceptible d'être supprimé sous un feu nourri.
- Utilisez des grenades plus fréquemment que les soldats ordinaires.
- Dangereux en combat rapproché mais mal adapté aux engagements prolongés.
