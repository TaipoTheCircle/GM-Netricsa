MESA NOIRE, TRAIN - PARTIE 4


Le tramway traverse désormais un secteur de fret. Je détecte une activité industrielle lourde : grues automatisées, robotique et plates-formes de transport. Une unité particulière – un grand robot jaune – semble chargée de déplacer des conteneurs de fret surdimensionnés.
Sam, compte tenu de votre histoire avec des adversaires mécaniques géants, je vous déconseille de le provoquer. Celui-là n'a pas l'air armé... mais je vous connais, et tôt ou tard vous testerez cette théorie.
