DRONE ESSAIS "BETA", GRAND


CLASSE : Drone essaim « Bêta », grand
MÉTABOLISME : Xénobiologique
PERCEPTION : Prédateur lourd
TAILLE : 9,8 pieds
ENDURANCE : Élevée
HOSTILITÉ : Très élevée
ARMES : Griffes, Dents, Masse corporelle
RÉCOMPENSE : 600 FC
MENACE : Élevée

DESCRIPTIF :

Le Grand Drone est un spécimen inhabituellement surdimensionné de l'Essaim "Bêta". Contrairement aux faux-bourdons d’élite, qui se distinguent par leur coloration, cette sous-espèce semble presque identique aux spécimens standards. Il possède la même peau verte, les mêmes trois yeux et le même besoin insatiable de déchirer tout être vivant. La différence n’apparaît que lorsque la créature se tient à côté d’un drone standard.

Les raisons d’une telle croissance anormale restent inconnues. Une théorie suggère que certaines larves reçoivent beaucoup plus de nutriments que d’autres. Une autre théorie postule que l’Essaim n’a tout simplement pas la capacité d’arrêter sa croissance au bon moment.

Sa taille accrue a conféré à la créature une musculature plus puissante et des organes internes considérablement renforcés. Par conséquent, le Grand Drone peut résister à des dégâts qui auraient depuis longtemps tué un spécimen standard et délivrer des coups suffisamment puissants pour envoyer une victime voler sur plusieurs mètres.

Malgré sa taille imposante, le Grand Drone conserve une grande partie de l'agilité de ses petits congénères. C'est une surprise particulièrement désagréable pour quiconque s'attend à ce qu'un tel monstre se déplace lentement et lourdement.

CONSEILS :

- Ne vous laissez pas tromper par sa taille. C'est plus rapide qu'il n'y paraît.
- Gardez vos distances et utilisez des armes puissantes.
- Si des drones ordinaires sont repérés à proximité, éliminez d'abord la cible la plus dangereuse.
- Lorsque Swarm "Beta" décide de créer un drone plus grand, tout le monde à proximité en souffre généralement.