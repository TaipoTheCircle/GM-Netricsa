COMBINEZ LE TIREUR TACTIQUE OVERWATCH


CLASS: Combinez le fusil de chasse tactique Overwatch
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner une unité d'assaut lourd en mêlée
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Fusil de combat SPAS-12, grenades
REWARD: 800 FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine Overwatch Tactical Shotgun Soldier est une version renforcée de l'Assault Trooper, conçue pour percer les défenses là où les unités hybrides commencent à subir des pertes. Bien qu'il ressemble à un Hybrid Trooper en apparence, la principale différence réside sous l'armure.

Le casque tactique neutralise complètement la vulnérabilité aux tirs à la tête. Même des tirs à la tête précis ne neutralisent pas le soldat. Le Combine a délibérément surchargé ces soldats d'armures, les transformant en béliers avançant sans relâche.

Le SPAS-12 est utilisé comme outil de pression et de dégagement. Le soldat avance sous le feu, ignorant les coups, se rapprochant de la cible pour assurer la distance de mise à mort. Les grenades servent à dégager des passages et à déloger l'ennemi des positions fortifiées.

TIPS: 

- Les tirs à la tête sont inefficaces - visez le corps ou utilisez des explosifs.
- Ne le laissez pas réduire la distance – il est presque assuré d'être mortel à courte portée.
- Les armes lourdes et les pièges fonctionnent mieux.
- La retraite et la distance sont les clés de la survie.
