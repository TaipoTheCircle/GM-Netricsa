MÉDECIN REBELLE


CLASS: Médecin rebelle
METABOLISM: Humain
PERCEPTION: Médecin de la Résistance
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONALS: Pistolet, SMG, trousses de premiers secours
REWARD: 250 FC
THREAT: Faible

DESCRIPTION: 

Les Rebel Medics sont des combattants de la Résistance qui ont décidé que la survie de leur équipe est plus importante que leur nombre de victimes. Ils apparaissent là où la bataille a déjà mal tourné et font tout leur possible pour changer ce plan. Leur arme principale est une trousse de premiers secours, mais si nécessaire, ils peuvent fournir un tir de couverture pour eux-mêmes et les blessés.

Unlike regular soldiers, medics tend to stay slightly behind the line of fire, constantly moving among their allies and assessing who can still fight and who needs to be urgently brought back into the fold. Their training is limited, but their practice is endless: chaque bataille est une nouvelle leçon d'anatomie sous le feu.

Bien qu'ils ne soient pas conçus pour des attaques directes, la présence d'un médecin augmente considérablement la capacité de survie de l'équipe. Les soldats de la Résistance combattent avec plus de confiance en sachant qu'il y a quelqu'un à proximité qui peut les remettre sur pied même après un coup dur.

TIPS: 

- Protect them: la perte d'un médecin réduit considérablement l'efficacité au combat du groupe.
- Un médecin sous le feu est le signe que le combat s'éternise.
