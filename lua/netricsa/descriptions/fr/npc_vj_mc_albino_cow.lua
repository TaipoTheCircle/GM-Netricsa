VACHE ALBINOS DU MONDE MINECRAFT


CLASS: Vache albinos
METABOLISM: Mammifère à sang chaud
PERCEPTION: Animal passif
SIZE: ~4,6 pieds
ENDURANCE: Moyenne
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 50 FC
THREAT: Minimal

DESCRIPTION: 

Une variété de vache rarissime, citée comme exemple de « la pâleur chez les animaux est assez rare ». Elle se distingue des vaches ordinaires par sa fourrure entièrement blanche et sa peau pâle. Ses sabots sont brun clair, ses cornes sont beiges, son nez est rosé et ses yeux sont rouge pâle, ce qui le caractérise comme un véritable albinos.

An albino cow behaves no differently from a normal cow: il erre dans les prairies, meugle, respire et évite l'eau, la lave et les falaises. Lorsqu'il est endommagé, il s'enfuit pendant quelques secondes puis reprend ses activités passives.

Sinon, c'est une vache normale, juste très pâle et avec les yeux rouges, ce qui lui donne un air un peu fou, même si elle l'ignore.

TIPS: 

- Une excellente exposition pour un collectionneur de monstres rares.
- Si vous avez un enclos avec de telles vaches, prenez-en soin.
- Ils traitent, produisent de la viande et du cuir, et meuglent.
- Les yeux rouges ne brillent pas dans le noir.
