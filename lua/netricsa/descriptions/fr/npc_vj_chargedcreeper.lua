CREEPER CHARGÉ DU MINECRAFT OVERWORLD


CLASS: Creeper chargé
METABOLISM: Électromagnétique-Végétal-Chimique
PERCEPTION: Une version renforcée du Creeper standard
SIZE: 5,5 pieds
ENDURANCE: Moyen
HOSTILITY: Extrême
WEAPONS: Explosion électrique plus grande
REWARD: 700FC
THREAT: Moyen-élevé

DESCRIPTION: 

Le Charged Creeper est la même figurine verte que le Creeper standard, seul son corps est désormais rempli d'énergie électrique débridée.

Il explose avec une force plus grande que la normale, endommageant tous les blocs et monstres dans un rayon.

Les Creepers chargés sont créés par des éclairs.

TIPS: 

- Le rayon de dégâts et la défense sont deux fois supérieurs à ceux du Creeper standard.
