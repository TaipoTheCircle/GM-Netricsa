COCKTAIL MOLOTOV


WEAPON: Cocktail Molotov (incendiaire improvisé)
AMMO: Solution inflammable
ROF: ~6 lancers/min (dépend de l'utilisateur)

DESCRIPTION: 

Un dispositif incendiaire improvisé - une bouteille remplie de liquide inflammable et une mèche - utilisé pour enflammer des cibles, créer des zones de feu persistantes et empêcher le mouvement de l'ennemi. Lors de l'impact, la bouteille se brise, enflammant le carburant déversé et créant une zone brûlante qui inflige des dégâts au fil du temps et obstrue les mouvements.

NOTES: 

- Crée une zone brûlante avec des dégâts au fil du temps et un potentiel d'incendie des objets inflammables à proximité.
- Idéal pour adoucir ou canaliser des groupes, bloquer les points d'étranglement et détruire la couverture lumineuse ; dangereux dans les espaces confinés en raison du risque d’automutilation.
- Approvisionnement limité à usage unique ; inefficace contre les armures lourdes et les ennemis résistants au feu.
