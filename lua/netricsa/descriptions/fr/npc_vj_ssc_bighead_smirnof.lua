DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais au niveau de difficulté « Je suis Ugh-Zan », ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans des zones secrètes, bloquant les passages grâce à leur grand nombre.

S-199 Smirnof isn't from Croteam. He grew up in the gmod workshop. He started out making simple nextbots, then learned to import exact replicas of Call of Duty weapons into GMod. His brain works like this: il voit quelque chose dans le jeu et comprend comment l'assembler.

He met Jack the Indifferent, and it all started. Together, they're putting together the entire Far Cry 1 game in GMod. His famous work is "(VJ BASE) Quake 4 SNPCs." He's the one who writes clear descriptions for his weapons for GM-Netricsa by Typo. He helped with Serious Sam: SNPC classiques (EXTRA), corrigeant certains PNJ. Apprend à Typo comment corriger les textures et les ragdolls dans Blender.

Lui et Jack ont ​​construit le Sniper Assassin mis au rebut de The Suffering. Ils se disputent parfois sur des choses insignifiantes, mais ils se réconcilient toujours : l'amitié est plus importante.

He doesn't like making new acquaintances. His description clearly states: "Ne m'envoie pas simplement un message en tant qu'ami. Dites-moi d'abord pourquoi. Si vous voulez juste être amis, je refuserai." Son cercle est composé de Jack, Typo et de son vieil ami Barsik, avec qui il joue à des jeux. Il est poli avec les autres, mais garde ses distances. Il n'est pas méchant ; il valorise simplement son temps et son énergie pour ceux qui lui tiennent déjà à cœur.

TIPS: 

- Utilisez un couteau militaire ou un pistolet pour dégager un chemin.
- En difficulté "I Am Ugh-Zan", détruisez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
