TERRE


PLANET: Terre
TYPE: Planète terrestre, planète natale de l'humanité
CLIMATE: Varié (de l'Arctique au Tropical)
MOONS: 1 lune
TERRAIN: Continents, océans, chaînes de montagnes, déserts
LOCATION: Système solaire, bras d'Orion (Voie lactée)
NATIVE RACES: Les humains
ALLIANCE: Espace humain indépendant ; Partiellement occupé par la Horde Mentale (à différents moments)
LOCATIONS: Egypte, Toungouska, Carcassonne, Pompéi, Teotihuacan, Krvavice

DESCRIPTION: 

La troisième planète du système solaire. Monde natal de l'humanité et centre de la civilisation humaine. Elle possède un satellite naturel, la Lune.

The planet's landscape is extremely diverse: des calottes glaciaires polaires aux jungles équatoriales, des fosses océaniques profondes aux plus hauts sommets des montagnes. Environ 70 % de la surface est recouverte d'eau.

La Terre attire depuis longtemps l’attention des civilisations extraterrestres. Il y a des milliers d'années, elle a été visitée par les Siriens, qui ont influencé le développement des cultures anciennes, notamment en Égypte. Plus tard, la planète est devenue à plusieurs reprises la cible d’invasions de la Horde Mentale, cherchant à détruire l’humanité.

Pendant la guerre, les Forces de défense unies ont utilisé d'anciens artefacts siriens, dont le Gardien du temps, pour résister aux envahisseurs.

KEY LOCATIONS: 

- Egypt: Une région où subsistent de nombreuses preuves de la présence syrienne. Les grandes pyramides, Karnak, Louxor, la Vallée des Rois et des complexes souterrains contenant des générateurs siriens se trouvent ici. - Toungouska : Le site d'une invasion massive par les forces de Mental, où un gigantesque portail de la Horde s'est ouvert lors de l'offensive de Toungouska.
- Carcassonne: Une ville fortifiée où était basé le groupe "Troïka-A" lors d'une des opérations contre les forces de Mental.
- Pompeii: Une ancienne ville romaine à proximité de laquelle une arme satellite était activée pour dissuader les forces ennemies.
- Teotihuacan: Une ancienne cité maya où les Siriens ont laissé l'un de leurs portails temporels.
- Krwavice: Une ville médiévale polonaise où un artefact important connu sous le nom de « Livre de la Sagesse » a été découvert.
