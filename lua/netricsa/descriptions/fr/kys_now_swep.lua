PISTOLET MJOLNIR


WEAPON: Pistolet Mjolnir
AMMO: Munitions 9 mm / Énergie de foudre
ROF: ~60 tr/min

DESCRIPTION: 

Un pistolet compact imprégné de la puissance d'un ancien artefact du tonnerre. Bien qu'elle ressemble à une arme standard de 9 mm, sa véritable puissance ne se révèle qu'entre les mains d'un utilisateur digne de ce nom.

Le mode de tir principal déclenche un éclair ciblé sur un point ciblé. L'éclair descend du ciel avec une force dévastatrice, frappant instantanément la cible et laissant une zone de dégâts carbonisée.

The alternate fire mode is used primarily for demonstration purposes: des éclairs traversent le ciel sans causer de dégâts importants, créant un effet visuel terrifiant et exerçant une pression psychologique sur l'ennemi.

NOTES: 

- Le tir principal déclenche un puissant éclair sur la cible.
- Tirer sous l'utilisateur est extrêmement dangereux - l'utilisateur n'a pas de défense de niveau Thor.
- Les tirs alternés créent des éclairs dans le ciel sans intimidation significative.
- Efficace à n'importe quelle distance avec une ligne de vue dégagée vers le ciel.
- Ne fonctionne qu'entre les mains d'une personne « digne ».
