FUSIL DE SNIPER GIAT FR F2


WEAPON: Fusil de précision GIAT FR F2
AMMO: 7,62x51mm OTAN
ROF: ~15 tours/min
WEIGHT: ~5,8 kg avec optique
LENGTH: ~1 200 millimètres

DESCRIPTION: 

Le GIAT FR F2 est le fusil de précision standard de l'armée française, développé comme une amélioration majeure du FR F1. Ses principales caractéristiques comprennent un canon flottant plus épais avec un manchon thermique qui réduit la distorsion thermique pendant le tir, ainsi qu'une ergonomie de crosse améliorée. Le fusil est conçu pour un engagement précis de cibles à moyenne et longue portée et est apprécié pour sa stabilité et sa fiabilité sur le terrain.

NOTES: 

- Haute précision dans les tirs ciblés à un seul coup ; idéal pour les engagements à longue distance.
- La conception à verrou offre une excellente précision, mais limite la cadence de tir.
- Efficace contre les effectifs et les cibles légèrement blindées ; contre une armure lourde, des coups précis sont nécessaires.
