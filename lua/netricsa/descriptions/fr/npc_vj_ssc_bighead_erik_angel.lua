DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils font preuve d'un enthousiasme obsessionnel et vous poursuivent avec des cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Erik Wolpaw est une légende vivante de l'industrie du jeu vidéo, co-fondateur du site satirique Old Man Murray (OMM) et l'un des écrivains de jeux vidéo les plus renommés depuis Marc Laidlaw. Croteam, avec son humour d'autodérision caractéristique, l'a immortalisé dans le jeu sous forme de camée, lui attribuant la PLUS GROSSE TÊTE de tous les fans secrets. Son crâne surdimensionné est le résultat direct de son énorme influence et de son esprit sarcastique, qu'il a d'abord utilisé pour critiquer l'industrie via OMM, puis pour la transformer en tant qu'écrivain chez Valve.

He penned the dialogue and plot twists in hits like Portal (and his iconic song "Still Alive"), Portal 2, and Half-Life 2: Épisode un et épisode deux.

Lorsque vous l'avez tué deux fois dans l'Egypte ancienne, il s'est transformé en ange.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", éliminez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
