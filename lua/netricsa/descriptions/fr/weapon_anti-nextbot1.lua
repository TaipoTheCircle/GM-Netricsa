LASER ANTI-NEXTBOT V1


WEAPON: PISTOLET À CHARGEMENT AUTOMATIQUE AR-15
AMMO: Énergie d'impulsion AR2 / Charges laser
ROF: Faisceau continu

DESCRIPTION: 

Une version améliorée du fusil à impulsion AR2, spécifiquement adaptée pour combattre les Nextbots. Il dispose d'un système de priorisation des cibles, permettant la destruction efficace des entités anormales sans destruction inutile de l'environnement.

Le mode 1 active un laser focalisé qui détruit instantanément toutes les cibles vivantes et hostiles, ignorant complètement les accessoires et les objets environnementaux. Cela le rend idéal pour un nettoyage précis dans des espaces confinés.

Le mode 2 désactive le filtrage et permet d'endommager à la fois les ennemis et les accessoires. Il est utilisé contre les Nextbots résistants ou non conventionnels lorsque la force brute et la destruction complète de zone sont nécessaires.

NOTES: 

- Mode 1: Détruit les cibles, ignorant les accessoires et les objets environnementaux.
- Mode 2: Inflige des dégâts à tout, y compris les objets et la couverture.
- Très efficace contre les Nexbots standards.
- Le mode 2 est dangereux pour l'environnement.
- Efficace à moyenne et longue portée.
