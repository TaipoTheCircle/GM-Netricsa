LE MONDE FRONTIÈRE DE XEN, LES GROTTES DE XEN


Xen a décidé de révéler son fonctionnement interne. Pas métaphoriquement. Littéralement.

Grottes. Labyrinthes. Des couloirs en pierre qui semblent avoir été conçus par quelqu'un qui détestait véritablement les lignes droites et les visiteurs.

It's full of Xen creatures. They move, breathe, spit, and generally consider you an intrusion on the landscape. A horde of Mental wanders among them, confidently exploring this alien world, as if Xen had always been part of the occupation. Two kinds of chaos colliding in a confined space. The result is predictable: tout le monde tire.

Les grottes s’étendent vers l’intérieur et l’extérieur, confondant les repères et les pensées. Il est facile de se perdre ici. Il est encore plus facile de tomber sur quelque chose que vous préféreriez ne pas découvrir. Il semble que Xen lui-même ne se souvienne pas vraiment où mènent ses tunnels. Il continue simplement à les construire.

Et oui, il y a une autre grotte sous l'eau. Caché. Calme. Les Chumtoads y vivent. Des créatures que l’on découvre habituellement après coup.

Alors fais attention, Sam.
