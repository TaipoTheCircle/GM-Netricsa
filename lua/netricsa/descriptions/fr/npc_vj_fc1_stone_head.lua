VISAGE DE PIERRE MALÉFIQUE DE L'ARCHIPEL DE KRIEGER


CLASS: Visage de pierre maléfique
METABOLISM: Pierre vivante
PERCEPTION: Moai non payé
SIZE: ~14,7 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Extrême (en raison de salaires impayés)
WEAPONS: 2 M4 géants modifiés, tête écrasante
REWARD: 0 (pas de salaire)
THREAT: Très élevé

DESCRIPTION: 

Une ancienne idole de pierre, Moai, qui a pris vie dans des circonstances inconnues et qui vit au plus profond de la jungle. Sa principale motivation est trois jours de salaire impayés comme tailleur de pierre, ce qui la met en colère. Malgré son manque d'armes, il manie et tire d'une manière ou d'une autre deux énormes fusils d'assaut M4 montés sur ses côtés. Son attaque principale est un piétinement écrasant avec sa tête massive, provoquant une onde de choc et renversant des arbres et des sangliers. Il possède une durabilité colossale.

TIPS: 

- Les tirs nourris de deux M4 de gros calibre et une attaque dévastatrice qui provoque des tremblements au sol et des étourdissements font de lui un adversaire dangereux.
- Les explosifs (lance-grenades, C4) sont efficaces contre les structures en pierre.
- Potentiellement, il pourrait être « apaisé » en retrouvant et en restituant son trésor, son « salaire impayé ». Mais vous ne l'avez pas.
- Vous pouvez l'apaiser avec des pastèques.
