LE MONDE FRONTIÈRE DE XEN, RETOUR


Bien sûr, Sam. Xén. Encore.

Les Houndeyes sont déjà là. Toujours là. Ils nous accueillent comme si nous n'étions jamais partis. C'est peut-être vrai.

Pour que le téléporteur fonctionne enfin comme prévu, nous devons activer le dispositif de contrôle de téléportation. Où est-il? Bonne question. La réponse, comme d'habitude, est dispersée sur les plateformes et sur les restes de ceux qui ont déjà essayé. Tout d’abord, trouvez-le. Puis activez-le. J'espère ensuite qu'il ne se souviendra pas de la façon dont cela s'est terminé la dernière fois.

Mais il y a un piège.

La horde de Mental s'empare déjà de Xen.

Monde extraterrestre, écosystème extraterrestre, règles extraterrestres. Et tout cela redevient une zone de transit. Mental, semble-t-il, ne se soucie pas de la dimension qu'il brise, tant qu'il y a quelque chose à conquérir.

Le plan est donc simple.

Trouvez l'appareil.

Activez-le.

Survivre.
