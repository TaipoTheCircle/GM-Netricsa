FUSIL D'ASSAUT AK-200


WEAPON: Fusil d'assaut AK-200
AMMUNITION: 5,45×39 millimètres
ROF: ~900 tours/min

DESCRIPTION: 

Une modification moderne du fusil d'assaut emblématique, connu pour sa fiabilité et sa durabilité. Repensé pour tirer des cartouches de 5,45 × 39 mm. Différents modules peuvent y être attachés.

NOTES: 

- Haute durabilité et facilité d'utilisation.
- Recul modéré ; contrôlable lors de courtes rafales.
- Efficace contre les cibles non blindées et légèrement blindées.
- La capacité du chargeur nécessite un rechargement périodique lors de combats prolongés.
- Ce fusil d'assaut peut être modifié avec différents modules à volonté.
