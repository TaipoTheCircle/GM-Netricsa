ABBAYE, BASILIQUE DE SAN PIETRO APOSTOLO


Sam, à en juger par ce que nous avons déplacé, cet endroit était autrefois un centre de pèlerinage, et maintenant c'est devenu le décor idéal pour une embuscade et une fusillade. Devant nous se trouve une ancienne basilique, ou plutôt ce qu'il en reste après des années de négligence... ou après la visite d'hier de l'artillerie de Mental.

L'architecture locale s'inspire clairement des grandes cathédrales, peut-être de la basilique Saint-Pierre elle-même.

Aujourd'hui, ce ne sont plus que des ruines...
