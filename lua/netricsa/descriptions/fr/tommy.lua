PISTOLET-MACHINE THOMPSON M1928


WEAPON: Mitraillette Thompson M1928
AMMO: .45APS
RR: 600 tr/min
LENGTH: 25,4 cm

DESCRIPTION: 

La mitraillette Thompson (également connue sous le nom de « Tommy Gun »), popularisée dans les années 1930 par Al Capone et d'autres gangsters, offre une puissance d'arrêt décente, se recharge rapidement et constitue la mitraillette incontournable de l'île. Moins apprécié des officiers que le revolver, il n’en reste pas moins une arme courante. On le trouve pour la première fois en haut des escaliers à côté d'un petit bureau dans le chronomètre de la Seconde Guerre mondiale, où Tork se souvient du colonel criant après un soldat.

