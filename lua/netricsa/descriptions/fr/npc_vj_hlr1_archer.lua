ARCHER DE XEN


CLASS: Archer
METABOLISM: Carnivore amphibie extraterrestre
PERCEPTION: Prédateur sous-marin extraterrestre
SIZE: ~4 pieds de long
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Crachat acide (à distance), morsure pointue (mêlée)
REWARD: 300FC
THREAT: Modéré

DESCRIPTION: 

L'Archer est un prédateur aquatique de couleur rouge de Xen. Il se cache dans les mares et les passages inondés, tendant une embuscade à ses proies avec des jets de crachats acides. S’il est approché de trop près, il se jette avec ses mâchoires acérées. Les archers sont agressifs, territoriaux et attaquent souvent à vue, crachant des volées avant de se rapprocher pour tuer.

TIPS: 

- Gardez vos distances et bougez de manière imprévisible pour éviter de cracher.
- Il est préférable d'utiliser des explosifs ou des tirs rapides tout en restant hors de l'eau.
- Dangereux en groupe - isolez-les si possible.
