Y.S.M. ASSASSIN FEMELLE CRABE ZOMBIE, COMMUN


CLASS: Y.S.M. Assassin femelle zombie headcrab, commun
METABOLISM: Hôte humain + crabe parasite de Xen
PERCEPTION: Entité zombifiée, ancien agent d'élite
SIZE: 5,6 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrême
WEAPONS: Griffes, Morsure, Saisie Furtive
REWARD: 700FC
THREAT: Très élevé

DESCRIPTION: 

C'est techniquement le même que le crabe zombie commun de la base Delta, juste dans un uniforme différent. Mais les survivants affirment qu’il existe des différences, et qu’elles ne sont pas qu’apparentes.

Only a faint shimmer gives her away. When she attacks, her invisibility fades: elle devient visible juste assez longtemps pour enfoncer ses griffes dans sa victime, puis disparaît dans l'obscurité. Un tir dans la tête détache la tête de la victime.

TIPS: 

- Elle devient visible lorsqu'elle attaque.
- Un fusil de chasse à dispersion est plus efficace qu'une arme de précision : il est plus facile de toucher la silhouette chatoyante.
- Elle n'a pas d'armure, juste du latex. Un ou deux coups et elle tombe. Mais la frapper est un défi.
- Elle est particulièrement dangereuse dans une meute avec d'autres zombies.
