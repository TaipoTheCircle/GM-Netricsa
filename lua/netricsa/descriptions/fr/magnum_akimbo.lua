DEUX REVOLVERS SMITH & WESSON MODÈLE 28


GUN: DEUX REVOLVERS SMITH & WESSON MODÈLE 28
CALCULATION: .357 Magnum
ROF: 40 tr/min

DESCRIPTION: 

Le pistolet Smith & Wesson modèle 28, ou Highway Patrolman, est l'arme de poing standard des policiers d'Abbott et de nombreux autres organismes d'application de la loi du Maryland. Il tire la cartouche .357 Magnum et contient six cartouches. C'est une arme extrêmement meurtrière. Ceux qui sont experts dans l'utilisation d'une telle arme peuvent être beaucoup plus dangereux en maniant deux d'entre elles. Étant donné que la réglementation sur les armes à feu à Abbott est assez laxiste, ces pistolets se trouvent en abondance partout où les agents les stockent. Les munitions sont également assez faciles à obtenir. Vous pouvez d'abord les obtenir auprès de l'officier principal, soit en le tuant alors qu'il quitte la salle de bain, soit en l'électrocutant dans la salle des chaises électriques. Un deuxième revolver, à double usage, se trouve dans le chapitre 2, "Descente", après avoir rencontré les trois fantômes de l'île et exploré la folie et la créature, mais avant de rencontrer les premiers tireurs d'élite qui exécutent l'officier ligoté. Tirez sur le levier pour faire exploser le générateur dans la pièce inondée que vous traversez, et le deuxième revolver se trouve caché dans le mur derrière lui.

