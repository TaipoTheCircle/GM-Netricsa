COMBINEZ LE BOUCLIER D'OVERWATCH


CLASS: Combiner Overwatch Shieldier
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner l'infanterie protectrice d'assaut
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Combiner pistolet et bouclier tactique
REWARD: 450 FC
THREAT: Moyen

DESCRIPTION: 

L'Overwatch Shieldier est un mur mobile de tirs combinés, conçu pour avancer sous le feu. Il ne porte pas d'armure lourde, s'appuyant sur un énorme bouclier noir avec des marques CMB qui couvre presque tout son corps. Le bouclier est recouvert d'un revêtement composite et peut résister aux tirs de fusils automatiques et aux éclats d'obus.

Les visières bleues identifient la variante standard d'Overwatch, mais le comportement de ce soldat est différent. Il ne cherche pas à se cacher, il se met à couvert. Le soldat avance lentement, couvrant ses alliés et permettant aux unités d'assaut d'avancer derrière lui.

Le pistolet est utilisé derrière le bouclier avec une seule main, le plus souvent pour la suppression à courte portée. En combat rapproché, le soldat peut enfoncer le pistolet, renverser les ennemis et les ouvrir pour finir.

Ces soldats sont utilisés dans les couloirs, lors du nettoyage des pièces, de la répression des émeutes et des opérations de rue où la défense frontale est essentielle. Le Combine les utilise comme barricades mobiles sous lesquelles le reste de l'infanterie avance.

TIPS: 

- Ne tirez pas sur le bouclier, il est préférable de flanquer ou de passer au-dessus.
- Les explosifs et le feu sont efficaces pour l'assommer.
- Il est vulnérable sur les côtés et derrière, surtout sans soutien. - S'il s'approche, préparez-vous aux coups.
