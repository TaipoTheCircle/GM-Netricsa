TYRANNOSAURUS REX DE LA PLANÈTE TERRAFORMÉE


CLASS: Tyrannosaure Rex
METABOLISM: Carnivore, Théropode
PERCEPTION: Prédateur suprême
SIZE: 17 pieds
ENDURANCE: Extrême
HOSTILITY: Absolu
WEAPONS: Crâne, mâchoires, dents, queue, griffes
REWARD: 5000FC
THREAT: Absolu

DESCRIPTION: 

Ce n'est pas n'importe quel dinosaure. C’est le seul dinosaure qui n’a pas besoin d’être présenté. Quand les gens disent "Dinosaure", c'est celui-là qui leur vient à l'esprit. Le roi des lézards tyrans.

The T-Rex's appearance is familiar to everyone: un crâne massif large à l'arrière et rétréci à l'avant, ce qui fait que les yeux sont tournés vers l'avant, offrant une vision binoculaire meilleure que celle d'un faucon. Le cou est court et épais, sinon il ne pourrait pas supporter une telle tête.

La question de savoir si un Tyrannosaurus rex chasse ou récupère est un débat qui dure depuis une décennie.

TIPS: 

- N'essayez pas de vous enfuir en ligne droite. C'est plus rapide qu'il n'y paraît.
