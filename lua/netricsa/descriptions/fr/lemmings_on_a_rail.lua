MESA NOIR, FERROVIAIRE DE FRET


Sam, the power is back online, and the cargo rail system is operational again. Against all odds, you've managed to secure yourself a ride out of that deathtrap. The good news: le train fonctionne. La mauvaise nouvelle : tout le reste.

Dès que vous montez à bord et appuyez sur les commandes, le wagon de marchandises avance d'un coup avec un gémissement de roues rouillées. La piste s'étend au plus profond du labyrinthe souterrain de Black Mesa, promettant à la fois évasion et nouveaux cauchemars.

Complications ahead: 

H.E.C.U. les patrouilles ont fortifié des sections de la voie ferrée. Attendez-vous à des embuscades aux points d'étranglement, des tourelles près des tunnels et des explosifs placés sur les voies.

L'activité extraterrestre augmente : la faune Xen se dirige directement vers les voies, soit trop stupide pour bouger, soit assez intelligente pour vous barrer la route.
