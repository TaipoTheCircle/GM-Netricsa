PARASITE ESSAIS "BÊTA"


CLASSE : Parasite d'essaim "bêta"
MÉTABOLISME : Xénobiologique
PERCEPTION : Parasite
TAILLE : 3,2 pieds
ENDURANCE : Très faible
HOSTILITÉ : Très élevée
ARMES : infecter, sauter, griffer
RÉCOMPENSE : 60 FC
MENACE : Élevée

DESCRIPTIF :

Le parasite "Beta" Swarm est une sous-espèce petite mais extrêmement agressive, spécialisée dans l'infection de plus grands organismes. Il éclot des œufs et commence presque immédiatement à rechercher la proie la plus proche, en utilisant ses longs membres postérieurs pour des sauts rapides.

Lors d'une attaque, le parasite tente de s'attacher à la victime et d'injecter du matériel infectieux dans son corps. Si un organisme infecté meurt avant d’avoir éliminé l’infection, une nouvelle génération de parasites mûrit rapidement en son sein et éclate bientôt.

Comparés aux parasites d'autres essaims, les individus de Beta Swarm se distinguent par leurs têtes allongées, certaines avec une carapace verte, et des membres plus saisissants. Malgré ces différences, leur cycle de vie et leurs méthodes d’attaque sont pratiquement identiques.

Seuls, les parasites constituent rarement une menace sérieuse. Cependant, lorsqu’ils apparaissent en masse, ils peuvent rapidement propager l’infection à l’ensemble de l’équipe, en particulier lorsqu’ils travaillent de concert avec d’autres organismes Swarm.

CONSEILS :

- Détruisez les parasites avant qu'ils ne se rapprochent.
- En cas d'infection, trouvez immédiatement un moyen d'éliminer l'infection de l'organisme.
- Ne laissez pas les parasites entourer le groupe, notamment dans les passages étroits.
- Détruisez d'abord les œufs pour empêcher les parasites de se reproduire.
- Les armes automatiques et le feu sont particulièrement efficaces contre ces créatures.