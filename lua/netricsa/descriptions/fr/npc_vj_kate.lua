KATE, LA TRAQUEUSE


CLASS: Kate, la traqueuse
METABOLISM: Humain, Femelle
PERCEPTION: Proie obsédée
SIZE: 5,3 pieds
ENDURANCE: Haut
HOSTILITY: Absolu
WEAPONS: Force physique, vitesse, instinct animal
REWARD: 300
THREAT: Haut

DESCRIPTION: 

Elle était autrefois une fille ordinaire, explorant les bois derrière sa maison avec son amie Lauren et son ami d'enfance Carl. Mais une rencontre a tout changé.

Lorsqu'ils étaient enfants, Kate et Carl ont rencontré Slenderman dans les bois. Carl réprima l'horreur, mais Kate, apparemment, s'en souvenait toujours.

Des années plus tard, ils sont retournés dans les bois pour confirmer ou infirmer leurs peurs d'enfance. Carl a eu de graves hallucinations et Kate a disparu. Elle a été retrouvée inconsciente dans les bois, avec un appareil photo cassé et une pile de dessins étranges. Deux semaines à l'hôpital, puis un retour à la maison qui n'était pas un retour à une vie normale.

Puis vint la mort de sa mère. Elle a essayé de vendre la maison et de partir.

Karl envisageait de se suicider et d'emporter ses cauchemars avec lui. Kate ne pouvait pas. Elle s'est enfuie, laissant son amie mourir seule.

Après une autre attaque de Slenderman, Kate est devenue la harceleuse. Elle vit dans les anciennes mines de Kullman, où elle chasse tous ceux qui s'y aventurent.

Une partie de Kate vit toujours quelque part à l’intérieur et essaie d’aider ceux qui suivent ses traces.

TIPS: 

- Elle est plus rapide que toi.
- La lumière de la lampe de poche l'aveugle momentanément. Utilisez-le pour vous échapper.
- Ce qui est écrit sur les murs ne ment pas. Lisez et faites ce qu'on vous dit.
