GNAAR PRIMITIF, MÂLE


CLASS: Gnaar primitif, mâle
METABOLISM: Mammifère
PERCEPTION: Animal de guerre reproducteur dressé
SIZE: 4 pieds
ENDURANCE: Faible
HOSTILITY: Faible
WEAPONS: Morsures, coups de poing
REWARD: 700FC
THREAT: Très faible

DESCRIPTION: 

Un petit animal bipède sans tête, avec un œil et une grande bouche. Originaire de Sirius et entraîné par Mental pour attaquer les forces adverses. Les mâles portent des pantalons en peau de léopard et sont plus petits et plus faibles que les femelles.

These are neighbors of the tropical Gnaars, who were once part of the same tribe but took a different path. While the tropical branch stagnated in its development, these gnaars found a new faith: elles servent fidèlement les femmes chamanes, accomplissant leur volonté et gagnant le droit de porter des marques distinctives.

Un mâle primitif diffère de son homologue tropical non pas par sa taille ou sa forme, mais par sa coloration. Les primitifs ne portent que des motifs rouges partiels, appliqués sur leur corps sous forme de tatouages ​​et de rayures rituelles. Le détail le plus remarquable est le pantalon à imprimé léopard, porté uniquement par les mâles de cette sous-espèce.

Pendant leur temps libre, les gnaars primitifs se livrent à des activités inhabituelles. Ils peignent des tatouages ​​de serpents jaunes et des égratignures sur des taureaux-garous, s'appropriant ainsi le bétail. Les taureaux-garous ne s'y opposent apparemment pas, ou ont simplement peur de désobéir aux chamans. De plus, les Gnaars primitifs font souvent du commerce avec leurs voisins tropicaux, échangeant des biens volés contre ce dont ils ont besoin.

Malgré toutes ces différences culturelles, les Gnaars primitifs restent de fidèles serviteurs du Mental.

TIPS: 

- Ils n'ont pas d'armes à distance, restez simplement à l'écart.
- Certains spécimens peuvent léviter.
- De rares individus seraient invisibles.
- La sous-espèce primitive est plus organisée que la sous-espèce tropicale, mais pas plus puissante.
