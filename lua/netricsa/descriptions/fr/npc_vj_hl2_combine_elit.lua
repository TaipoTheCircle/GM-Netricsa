COMBINEZ L'ÉLITE DES SOLDATS D'OVERWATCH


CLASS: Combinez Overwatch Soldier Elite
METABOLISM: Humain augmenté, mâle
PERCEPTION: Infanterie transhumaine (unité d'élite)
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, boules d'énergie
REWARD: 900 FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine Overwatch Soldier Elite est l'unité terrestre de rang le plus élevé déployée par le Combine sur Terre. Reconnaissables à leur armure blanche et à leur unique œil rouge brillant, ils servent à la fois de forces de l'ordre et de troupes de choc dans les opérations à grande échelle. Leurs implants synthétiques avancés leur confèrent des réflexes, une endurance et une conscience du champ de bataille supérieurs à ceux des soldats Overwatch standard.

Les élites ne sont déployées que lorsque l’activité de résistance dépasse la capacité des unités ordinaires. Ils sont craints pour leur efficacité brutale, leur utilisation intensive du fusil à impulsion AR2 et leurs tactiques coordonnées, combinant souvent des tirs de suppression avec des boules d'énergie pour chasser les ennemis de leur couverture.

Leur blindage est renforcé contre les tirs d'armes légères, ce qui en fait des adversaires très résistants. Leur présence indique souvent que le Combine est passé à une extermination ou à une répression à grande échelle, laissant aux rebelles peu de marge de retraite.

TIPS: 

- Les tirs du fusil à impulsion sont dévastateurs - utilisez un abri et continuez à avancer.
- Les explosifs et les armes lourdes constituent le moyen le plus efficace de les abattre.
- Ils opèrent souvent en escouades - ne présumez jamais qu'une élite travaille seule.
- La visière rouge peut révéler leur position en cas de faible luminosité.
