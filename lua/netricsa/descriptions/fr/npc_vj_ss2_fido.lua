FIDO LE BOULEDOGUE À PIQUANTS


CLASS: Fido le bouledogue hérissé
METABOLISM: Mammifère
PERCEPTION: Chien de garde
SIZE: 3 pieds
ENDURANCE: faible
HOSTILITY: haut
WEAPONS: morsures
REWARD: 50 FC
THREAT: faible

DESCRIPTION: 

Les chiens Fido font partie des races les plus populaires au sein de Mental's Empire et sont largement utilisés comme chiens de garde et d'attaque. Ou du moins, c'est ce que pensent la plupart des humains.

Les chiens Fido ne sont en réalité rien de plus que des animaux domestiques qui attaqueront ceux qui menacent leurs propriétaires. Les forces mentales sont autorisées à amener leurs chiens au combat, un peu comme la façon dont les humains promènent leurs chiens, mais avec plus d'armes et de sang.

Heureusement, leur aboiement est pire que leur morsure ; restez à l'écart et abattez-les.

TIPS: 

- Éviter leurs charges en les contournant ou en sautant par-dessus.
- Essayez de les aligner ; ils se piétineront en essayant de vous atteindre.
- Utilisez des armes à tir rapide ou à dégâts de zone pour les éliminer rapidement.
