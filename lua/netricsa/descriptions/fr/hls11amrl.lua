BLACK MESA, ASSAUT DE PONT


La télémétrie confirme les lourdes signatures du rotor : un hélicoptère de combat Apache AH-64 s'est verrouillé sur votre position. Le point d'embuscade ? Un pont suspendu s'étendant au-dessus d'un profond ravin industriel. Super endroit pour faire du tourisme... si vous ignorez l'hélicoptère d'attaque.

Situational analysis: 

Threat: l'Apache est armé de fusils à chaîne, de roquettes et de la patience d'une guêpe très en colère. Rester à l'air libre est une condamnation à mort.

Environment: le pont est étroit, partiellement endommagé et exposé de tous côtés. Une couverture limitée signifie que vous devrez continuer à vous déplacer – esquiver, serpenter et ne vous arrêter que lorsque vous aurez trouvé une position de tir stable.

Secondary hazards: H.E.C.U. les troupes sont retranchées des deux côtés du ravin, fournissant un appui-feu au sol à l'hélicoptère de combat. Leur coordination suggère que cette embuscade était planifiée.

Tactical advice: 

Utilisez les poutres structurelles et les sections cassées du pont comme couverture partielle, mais ne vous attardez pas.

Donnez la priorité à la mobilité : laissez l'hélicoptère de combat vous poursuivre au lieu de tourner en rond pour tuer facilement.

Si vous avez sécurisé le canon Tau, c'est l'une des rares armes capables de faire pression sur un avion. Sinon, la meilleure solution est de récupérer les munitions explosives.

Conclusion: traverser ce pont ne consiste pas seulement à atteindre l'autre côté. Il s'agit de prouver que vous êtes plus difficile à tuer qu'un char volant d'un milliard de dollars.
