FUSIL D'ASSAUT HK416


WEAPON: HK416
AMMUNITION: Cartouches OTAN 5,56 × 45 mm
ROF: ~850 tours/min

DESCRIPTION: 

Le HK416 a été développé à l'origine comme module destiné à être installé sur l'avant des fusils d'assaut M4 ou M16, mais a ensuite évolué pour devenir une carabine à part entière, puis un fusil. L'objectif était de remplacer les fusils d'assaut M4 et M16, qui constituaient la base de l'arsenal des forces armées et du Corps des Marines américains, en raison de leur faible fiabilité, de leurs dysfonctionnements fréquents, de leur surchauffe et de leur vulnérabilité à la contamination.

NOTES: 

- Ce fusil est efficace à moyennes et longues portées.
- Bien adapté aux opérations intérieures ou urbaines.
- A une cadence de tir élevée.
