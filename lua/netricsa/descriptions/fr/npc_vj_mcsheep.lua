MOUTONS DU MONDE MINECRAFT


CLASS: Mouton
METABOLISM: Mammifère à sang chaud
PERCEPTION: Domestique / Sauvage
SIZE: 3 pieds
ENDURANCE: Moyenne
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 40 FC
THREAT: Faible

DESCRIPTION: 

Les moutons sont des herbivores paisibles qui paissent dans les plaines, les forêts et les montagnes de Minecraft. Ils existent dans une variété de couleurs de laine (blanc, noir, gris, gris clair, marron, rose), leur permettant de produire une variété de laine teinte.

Les moutons peuvent être tondus avec des ciseaux pour obtenir de la laine, et ils peuvent être élevés avec du blé. Après la tonte, la laine repousse progressivement.

Les moutons se déplacent en groupes et paissent lentement, ce qui en fait des cibles faciles pour la récolte des ressources.

TIPS: 

- Tondez les moutons au lieu de les tuer : leur laine peut être régénérée.
- L'agneau constitue une bonne source de nourriture.
- La laine peut être utilisée pour construire un lit et permettre de se reposer après un travail acharné.
