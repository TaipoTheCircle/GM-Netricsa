GAUNTLET


WEAPON: Gant
AMMO: Aucun (mêlée)
ROF: ~150 tranches/min

DESCRIPTION:

Le Gauntlet est une arme de mêlée semblable à une scie.

Au contact, il tranche la cible et applique un effet de recul pour repousser l'ennemi.

NOTES:

- Inflige 50 dégâts par tranche.
- Ne nécessite aucune munition – toujours disponible pour utilisation.
- Idéal pour les manœuvres furtives, les embuscades ou les combats rapprochés agressifs.
- Avec Quad Damage, une seule tranche peut tuer un ennemi en pleine santé.