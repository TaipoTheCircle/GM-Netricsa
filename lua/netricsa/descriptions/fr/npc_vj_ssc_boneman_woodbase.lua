SOCLE EN BOIS KLEER


CLASS: Socle en bois Kleer
METABOLISM: mort-vivant
PERCEPTION: créature magique
SIZE: 9 pieds
ENDURANCE: faible
HOSTILITY: moyen
WEAPONS: griffes de kleer en bois, griffes, cornes
REWARD: 520 FC
THREAT: moyen

DESCRIPTION: 

Un squelette mort-vivant d'un être d'une race éteinte de la planète Kleer, d'Alpha Canis Majoris. Il a de longues griffes acérées sur les mains et une longue queue.

Ils attaquent soit en invoquant des griffes de bois, soit en coupant avec leurs griffes à bout portant.

Comme on le sait, les Kleers désossés subissent une mutation qui rend leurs os en bois, c'est pourquoi on l'appelle base en bois.

TIPS: 

- Il est préférable d'éviter leurs attaques sautées en les contournant.
- Lancer des griffes également à bout portant.
