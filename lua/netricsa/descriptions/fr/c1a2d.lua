BLACK MESA,  SECURITY CHECKPOINT


Sam, cette section illustre parfaitement l'expression « mauvais moment ». Un agent de sécurité est coincé derrière un portail verrouillé. Malheureusement, son timing est impeccable, car juste derrière lui se tient un zombie, se balançant avec ce look indubitable « J'ai raté le déjeuner ».

Les chances ? Disons simplement que le régime de retraite du gardien semble plus solide que ses chances de survie. Vous pourriez intervenir.