DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils reçoivent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Damir Perovic est l'un des quatre piliers sur lesquels repose tout l'empire Croteam. Son crâne surdimensionné contient non seulement le cerveau du développeur, mais aussi l'idée même, le germe du studio, né en 1989 au sein d'une équipe scolaire réalisant le jeu à défilement latéral 2D "Kids". Avec trois camarades de classe, il est passé du statut d'étudiant de l'école Nikola Tesla de Zagreb à celui de cofondateur de l'un des studios de jeux les plus emblématiques de Croatie.

While the other co-founders became the public faces of the company, Damir remained in the shadows, serving as a support artist and additional programmer in the early stages. His contribution is fundamental: il était là lorsque Croteam est né dans un appartement ordinaire, établissant les principes de camaraderie et de passion qui ont permis au studio de survivre des décennies et de créer les mondes de Serious Sam et The Talos Principe. Sa présence parmi les Croteamers est un hommage à leurs origines mêmes, au génie discret dont le travail dans l'ombre a jeté les bases de tout le chaos bruyant et insensé qui a suivi.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", éliminez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
