CYCLOPE ALBINOS DE MAGNOR


CLASS: Cyclope albinos
METABOLISM: Carnivore
PERCEPTION: Mutant ressemblant à un ver
SIZE: ~22 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Orbe d'énergie à destination, claquement de main
REWARD: 800 FC
THREAT: Haut

DESCRIPTION: 

Originally giant worms native to the planet Magnor, Albino Cyclopes were horrifyingly transformed by Mental-given arms and legs in a twisted genetic experiment. Their bodies didn't quite get the memo, so they remain gruesomely large and oddly proportioned. They strike fear into the hearts of even the bravest with two signature attacks: 

Orb blast: Tire une sphère d'énergie à tête chercheuse qui se verrouille sur les joueurs - les manœuvres d'évasion sont essentielles.

Arm smash: Si vous êtes assez stupide pour vous approcher trop près, préparez-vous à vous faire matraquer par une force caverneuse.

TIPS: 

- Leur grande taille en fait des cibles faciles pour les lance-roquettes et les lance-grenades.
- Utilisez des armes comme le Clawdovic, des roquettes ou des armes à feu lourdes pour des éliminations rapides.
- Un tir parfait du canon SBC peut en anéantir un si vous avez de la chance.
