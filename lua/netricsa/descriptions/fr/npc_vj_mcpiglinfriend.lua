PIGLIN SWORDSMAN DE MINECRAFT NETHER


CLASS: Épéiste Piglin
METABOLISM: Humanoïde ressemblant à un cochon
PERCEPTION: Race guerrière primitive du Nether
SIZE: 6,3 pieds
ENDURANCE: Moyenne
HOSTILITY: Conditionnel
WEAPONS: Épée d'or (parfois enchantée)
REWARD: 400FC
THREAT: Moyenne

DESCRIPTION: 

L'une des deux principales races de créatures ressemblant à des cochons habitant le Nether. Contrairement à leurs parents zombifiés, ils conservent une certaine intelligence, la capacité de parler leur propre langue et, plus important encore, une passion pour l'or.

This passion is so strong that it defines their entire behavior: les piglins ne sont neutres qu'à l'égard de ceux qui portent au moins une pièce d'armure dorée.

Sans cela, ils deviennent immédiatement agressifs.

Les épéistes représentent exactement la moitié de la population adulte de piglins (l'autre moitié préfère les arbalètes). Ils sont armés d'épées dorées, qui peuvent être enchantées, et attaquent au corps à corps, balançant leurs armes aussi violemment que les champions des demeures forestières.

Les Piglins habitent les forêts cramoisies et les friches du Nether, et habitent également des bastions, d'énormes forteresses où ils gardent des trésors et des blocs d'or. Dans les bastions, ils sont souvent rejoints par de féroces frères piglins, plus forts, dotés de haches dorées et immunisés contre les distractions impliquant l'or.

Les Piglins ont peur du feu de l'âme et de leurs frères zombifiés, à la vue desquels ils s'enfuient.

Ils ne savent pas nager, se noient dans l’eau et ne sont pas à l’abri du feu ou de la lave, même s’ils vivent en enfer.

TIPS: 

- Portez une armure dorée. Même un seul morceau empêchera les piglins de vous attaquer.
- N'ouvrez pas les coffres et ne cassez pas de blocs d'or devant eux - cela provoque une agression même à travers l'armure.
- Les épéistes sont dangereux au corps à corps, mais plutôt lents.
