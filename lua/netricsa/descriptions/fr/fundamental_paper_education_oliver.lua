OLIVER DE L'ÉDUCATION FONDAMENTALE DU PAPIER


CLASS: Olivier
METABOLISM: Organisme modifié anthropomorphe en papier
PERCEPTION: Chef intimidateur
SIZE: ~6 pieds
ENDURANCE: Haut
HOSTILITY: Sadique
WEAPONS: Crayon au lieu de la main droite, intelligence, connexion avec ∆lice
REWARD: 999 FC
THREAT: Très élevé

DESCRIPTION: 

Le principal tyran de l'école et l'un des "favoris" de Miss Circle, un ami d'enfance et petit ami de ∆lice. Contrairement aux étudiants ordinaires, lui et ∆lice ne sont pas des étudiants de Paper School au sens traditionnel du terme, mais plutôt ses « invités spéciaux » ou une partie d'un système caché. Reconnaissable à ses longs cheveux blancs jusqu'aux chevilles, ses cornes noires, une note « A+ » dans ses cheveux et un crayon à la place de sa main droite. Sa langue est fourchue comme celle d'un serpent. Il porte une chemise noire avec un symbole en diamant (identique au symbole sur le ∆lice). C'est un tyran sadique, arrogant et enjoué, dépourvu d'empathie. Il aime tourmenter les autres et est capable de tuer pour s'amuser. Il possède clairement une durabilité accrue et éventuellement d'autres capacités (manger des objets non comestibles comme du savon).

TIPS: 

- C'est un maître de la manipulation et des pièges.
- Il est motivé par l'ennui, le sadisme et la dévotion aux ∆lices. Ne cherchez pas de logique ou de pitié dans ses actions.
