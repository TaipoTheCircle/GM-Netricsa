DESCRIPTION: 

The V-22 Osprey is a tiltrotor aircraft used by the H.E.C.U. to rapidly deploy squads into combat zones. Its design allows vertical takeoff like a helicopter, but with the speed and range of a plane. While not heavily armed itself, its true danger lies in the payload it delivers: des escouades de marines prêtes à submerger les survivants avec des tactiques coordonnées.

Contrairement à l'Apache, l'Osprey s'engage rarement directement. Au lieu de cela, il tourne au-dessus du champ de bataille, dégorgeant les soldats via des cordes de rappel vers des points chauds. Le détruire en cours d'opération peut paralyser les renforts ennemis, mais son blindage et ses manœuvres d'évasion en font une cible difficile.

TIPS: 

- Concentrez le feu lorsqu'il plane pour déployer des troupes.
- Attendez-vous à des vagues de renforts partout où ils apparaissent.
- Les explosifs et les roquettes sont les contre-attaques les plus efficaces.
- En éliminer un prive les renforts ennemis - cela vaut le risque.
