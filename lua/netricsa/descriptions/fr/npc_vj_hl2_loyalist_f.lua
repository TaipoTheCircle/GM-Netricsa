COMBINEZ LES LOYALISTES


CLASS: Combiner Loyaliste
METABOLISM: Les humains
PERCEPTION: Auxiliaire civil
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Mitraillette (SMG), pistolet, paralysant Stun-Stick
REWARD: 200FC
THREAT: Faible

DESCRIPTION: 

Les Loyalistes du Cartel sont des civils qui se sont volontairement rangés du côté du Cartel, échangeant sécurité, rations et illusion de stabilité contre loyauté envers le régime. Ils sont facilement reconnaissables à leurs vêtements noirs et à la démarche trop confiante de ceux à qui on a promis « tout ira bien ».

Contrairement aux soldats d’Overwatch, les Loyalistes ne portent pas d’armure et ne suivent pas d’entraînement au combat complet. Leurs armes se limitent aux SMG, aux pistolets et aux Stun-Sticks, ce qui en fait davantage une force de soutien que de véritables combattants. En combat direct, ils sont vulnérables et meurent rapidement. Le Combine le sait et ne considère pas cela comme un problème.

Cependant, tous les loyalistes ne sont pas pareils. Beaucoup d’entre eux prétendent simplement être des partisans du régime. Ces agents doubles transmettent des informations à la Résistance, sabotent les ordres et tirent parfois « accidentellement » dans la mauvaise direction. Le Combine s’en doute, mais préfère fermer les yeux.

TIPS: 

- Sans armure, ils sont extrêmement vulnérables. Ne prolongez pas le combat.
- Tous les loyalistes ne sont pas vraiment des ennemis, mais le découvrir au combat est une mauvaise idée.
- Le Stun-Stick est dangereux de près, gardez vos distances.
