SHOW SADISTE DEMI-SÉMEMENT


CLASS: HALF-QUAKE Show Sadique
METABOLISM: Humain, mâle
PERCEPTION: Animateur de spectacle sadique
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Passif / Contemplatif
WEAPONS: Accès à d'énormes marteaux, spectacles de systèmes de contrôle, indifférence
REWARD: 10000FC
THREAT: Moyen

DESCRIPTION: 

Il ne s'agit pas seulement des animateurs de l'émission HALF-QUAKE, mais aussi des ingénieurs, scénaristes, bourreaux et spectateurs, tous réunis en un seul. Ils recherchent des victimes partout dans le monde, les arrachant à leur vie ordinaire et les livrant dans des arènes où elles doivent souffrir « pour leur terrible erreur dans la vie ». Que cette erreur soit réelle ou inventée sur place, les sadiques s'en moquent.

L'essentiel est que le processus se déroule selon le scénario.

Les sadiques ont créé Somos, le fils du sadisme, une intelligence supérieure qui contrôle tout ce qui se passe dans la série.

Lorsqu'ils manquent de victimes, ils engagent des gardes et les font subir des tests, puis les regardent mourir d'épuisement ou sous le coup d'un marteau géant. Pour le public, bien sûr.

Ils ne participent pas eux-mêmes au combat. Pourquoi? C'est à cela que servent les tourelles, les crabes et les vortigaunts.

