TRIGEN STEALTH (INVISIBLE) DE L'ARCHIPEL KRIEGER


CLASS: Berserker mutant (foutu)
METABOLISM: Mutation trigène intermédiaire
PERCEPTION: Combattant imparable
SIZE: 8,1 pieds
ENDURANCE: Très élevé
HOSTILITY: Absolu
WEAPONS: Lame en os (main droite)
REWARD: 1200FC
THREAT: Critique

DESCRIPTION: 

Le Berserker est un lien évolutif entre les primates Trigen et les mutants humains. De stature gigantesque, avec une lame osseuse massive remplaçant sa main droite et une cavité buccale supplémentaire sur son visage. Il combine la fureur animale des petits Trigens avec le pouvoir destructeur des mutations à un stade avancé. Il se dirige constamment vers sa cible, ignorant les obstacles.

Agit comme un bélier vivant. En combat rapproché, il délivre des frappes de lame dévastatrices, capables de détruire une cible en 2-3 coups. Sa taille repousse les ennemis, limitant leur maniabilité. Il est vulnérable lors de son attaque.

TIPS: 

- Courez et tirez avant qu'il ne se rapproche.
- Esquivez en zigzag, pas en ligne droite.
