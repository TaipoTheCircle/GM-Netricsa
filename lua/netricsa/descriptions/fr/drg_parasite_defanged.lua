ESSAI DE XÉNOMITE


CLASSE : Essaim Xenomite
MÉTABOLISME : Xénobiologique
PERCEPTION : Organisme Symbiotique
TAILLE : 1,3 pi
ENDURANCE : Très faible
HOSTILITÉ : Absolue
ARMES : Attaque sautée, Explosion d'acide
RÉCOMPENSE : 25 FC
MENACE : Moyenne

DESCRIPTIF :

Le Xenomite est une petite forme de vie parasite élevée par les Faucheurs pour se défendre et s'attaquer. Il passe la majeure partie de sa courte vie près de son parent, en attendant qu'une proie convenable émerge.

Malgré sa taille modeste, le Xenomite est extrêmement dangereux. Après avoir localisé une proie, il fait un bond rapide et tente de se rapprocher. Contrairement à la plupart des créatures Essaim, son objectif principal n'est pas de tuer son adversaire, mais de se détruire à proximité.

Le corps du Xenomite est rempli de composés acides instables. En cas de mort ou de dégâts critiques, la créature explose, dispersant de la biomasse corrosive. Le contact avec ces substances peut provoquer de graves brûlures chimiques même quelques secondes après la détonation.

La durée de vie du Xenomite est extrêmement courte. Apparemment, l'évolution de l'organisme a complètement abandonné la survie à long terme au profit d'une efficacité maximale lors d'une seule attaque.

CONSEILS :

- Détruisez les Xénomites à distance.
- Ne les laissez pas s'approcher trop près.
- Après la mort de la créature, restez à l'écart des restes acides.
- Si la créature ne vit que pour exploser près de chez vous, il est trop tard pour contester le but de sa vie.