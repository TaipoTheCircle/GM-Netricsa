COMBINER LA PISTE


CLASS: Combiner la piste
METABOLISM: Plateforme mécanisée avec commandes intégrées
PERCEPTION: Combinez le transport lourd
SIZE: 29,5 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Extrême
WEAPONS: Tourelle de mitrailleuse intégrée (contrôlée de l'intérieur)
REWARD: 3200FC
THREAT: Critique

DESCRIPTION: 

Le Combine Track est un transport Combine spécialisé, de conception similaire au transporteur APC, mais adapté au transport de soldats réplicants Combine. Sa forme est plus épurée et fermée, et la coque apparaît solide et solide, comme si elle était coulée. La principale différence est l’absence totale de verre, de cockpits ouverts et de postes de contrôle externes.

Le véhicule est contrôlé exclusivement de l'intérieur. La navigation s'effectue via un système d'unités de capteurs externes, de caméras tactiques et de modules analytiques Combine. L'équipage ne voit pas le monde directement, mais uniquement à travers un filtre de données. Cela réduit la vulnérabilité du véhicule et élimine complètement les erreurs humaines.

La tourelle du Combine Track est également contrôlée de l'intérieur. L'opérateur est directement connecté au système de ciblage.

TIPS: 

- Pas de verre signifie pas de points faibles évidents.
- Déploie des soldats.
