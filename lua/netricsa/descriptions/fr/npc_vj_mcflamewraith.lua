FLAME WRAITH DE MINECRAFT NETHER


CLASS: Spectre de flammes
METABOLISM: Mort-vivant, entité Wraithly enflammée
PERCEPTION: Foule hostile, volant Flame Wraith
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrêmement élevé
WEAPONS: Toucher de flamme
REWARD: 400FC
THREAT: Très élevé

DESCRIPTION: 

Il s'agit d'une variante infernale du Wraith commun, que l'on trouve uniquement dans le Nether. Contrairement à son pâle cousin de l'Overworld, cet esprit ardent ne se contente pas d'infliger des dégâts : il envoie sa victime sur Flame, transformant la bataille en une course à mort de Flame.

Le Flame Wraith est d’apparence plus agressive et dangereuse. Leur contact provoque une brûlure de leur adversaire pendant au moins 15 secondes, soit à peu près le même temps qu'il faut pour tomber dans la lave. En Enfer, où il fait déjà chaud, une telle brûlure est presque toujours mortelle, surtout sans eau ni potion antidéflagrante à proximité.

Malgré leur nature fougueuse, ces spectres sont paradoxalement vulnérables à la lumière du soleil. Comme les spectres normaux, ils meurent sous la lumière directe du soleil, même s'ils ne s'enflamment pas. Ils sont complètement immunisés contre les flammes (ce qui est logique), mais sont tués par la lumière du jour normale.

Otherwise, their behavior is identical to normal wraiths: ils planent, émettent des gémissements étranges, attaquent au toucher et n'entrent pas en collision avec d'autres créatures.

TIPS: 

- Si vous vous dirigez vers le Nether, préparez-vous à rencontrer ces créatures.
- Un spectre de flammes est une cible prioritaire. Cela ne doit pas être ignoré, car 15 secondes de combustion en Enfer sont presque garanties de vous tuer. - Emportez avec vous des potions de résistance aux flammes. Ils vous immuniseront contre la Flamme et annuleront l'arme principale du Wraith.
- Un seau d'eau ne sert à rien dans le Nether, l'eau s'évapore instantanément.
- Utilisez un arc ou une arbalète pour garder vos distances. En combat rapproché, il vous mettra sur Flame.
- Si vous êtes pris par la Flamme, essayez de vous retirer dans un endroit sûr et mangez rapidement une pomme d'or ou buvez une potion de guérison pour survivre aux dégâts de la Flamme.
- Contrairement aux Wraiths normaux, ceux-ci n'apparaissent pas dans l'Overworld, même la nuit. L'enfer est leur seule maison.
