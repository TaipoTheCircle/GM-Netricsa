COQUE DU MONDE MINECRAFT


CLASS: Décortiquer
METABOLISM: Mort-vivant du désert
PERCEPTION: Foule hostile
SIZE: 6,2 pieds
ENDURANCE: Haut
HOSTILITY: Haut
WEAPONS: Mains, effet "Faim"
REWARD: 160 FC
THREAT: Moyen

DESCRIPTION: 

Il s’agit d’un zombie qui a été exposé au soleil pendant trop longtemps et qui est désormais immunisé contre tout danger. Contrairement à leurs homologues habituels, ces créatures ne brûlent pas dans le désert pendant la journée car leur peau séchée a depuis longtemps perdu sa sensibilité aux rayons ultraviolets. Visuellement, un Husk mesure 2 cm de plus qu'un zombie normal, ce qui l'empêche de se faufiler à travers des espaces de deux blocs.

Ils terrorisent les déserts, les savanes et autres biomes chauds. Ils se déplacent en groupes de 2 à 4 individus lorsque les niveaux de luminosité sont inférieurs à 7, exclusivement à l'extérieur.

The Husk's main characteristic is its "Hunger" attack. If it's empty-handed (no weapons), its touch drains your hunger faster than sand drains water. The duration of the effect depends on the difficulty and regional difficulty: de 7 à 30 secondes. Un bouclier bloque à la fois les dégâts et la faim, alors gardez-le prêt.

Otherwise, the Husk behaves like a normal zombie: il traque les victimes, les villageois, les golems de fer et les œufs de tortues à une distance de 40 mètres. Il peut ramasser des objets, enfoncer des portes et tendre des embuscades. Si un Husk entre dans l’eau, il commencera à trembler après 30 secondes et après 15 secondes supplémentaires, il se transformera en zombie normal. Ce processus est irréversible, même si vous le déposez sur la terre ferme. Ce zombie peut alors devenir un zombie noyé s'il continue à rester assis dans l'eau.

À la mort, une coque laisse de la chair pourrie et parfois une carotte, une pomme de terre ou un lingot de fer. Une pomme de terre au four tombe si la foule est brûlée. Si la coque était équipée, elle tombera avec 100 % de chances. À Halloween, ils peuvent apparaître avec une citrouille sur la tête.

TIPS: 

- Les coques ne brûlent pas au soleil. Ne vous attendez pas à ce qu'elles meurent le matin.
- Frapper une balle provoque la faim - ayez toujours une réserve de nourriture ou un bouclier.
- Poursuivez une coque dans l'eau : après 45 secondes, elle se transformera en un zombie ordinaire, plus facile à contrôler.
- Les nouveau-nés sont rapides et peuvent tout monter. Ne les laissez pas vous entourer.
- Pour un drop garanti, utilisez l'enchantement "Loot III".
- Les carcasses sont légèrement plus grandes que les zombies : elles restent parfois coincées dans des espaces où les morts-vivants ordinaires peuvent passer.
