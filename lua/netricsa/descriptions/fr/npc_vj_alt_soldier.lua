COMBINEZ LE SOLDAT ALTERNatif D'OVERWATCH


CLASS: Combiner Overwatch Alt Soldier
METABOLISM: Humain augmenté, mâle
PERCEPTION: Infanterie combinée standard
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, SMG
REWARD: 600 FC
THREAT: Haut

DESCRIPTION: 

Le Combine Overwatch Alt Soldier est une version modifiée du soldat Overwatch standard, conçu pour les opérations dans des environnements où l'armure bleue des unités est un désavantage. Sa tenue combine un camouflage sombre avec un torse et des protège-manches noirs, rendant sa silhouette moins visible dans les zones industrielles, les rues nocturnes et les environnements intérieurs.

TIPS: 

- Travaille strictement en groupe et échoue rarement seul.
- L'AR2 est dangereux même à moyenne portée, évitez donc de vous tenir à découvert.
- La visière bleue est visible dans l'obscurité.
