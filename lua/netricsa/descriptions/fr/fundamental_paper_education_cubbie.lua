CUBBIE DE L'ÉDUCATION FONDAMENTALE DU PAPIER


CLASS: Casier
METABOLISM: Organisme en papier, construction hybride
PERCEPTION: Apprenti chat anthropomorphe
SIZE: 4 pieds
ENDURANCE: Faible
HOSTILITY: Aucun
WEAPONS: Aucun
REWARD: 1 FC
THREAT: Aucun

DESCRIPTION: 

One of the most unusual students at Paper School, Cubby is an anthropomorphic black cat whose body is a large pencil sharpener. His tail is a long, curved shaving, and his head hovers above his "torso", on which rests an open book. He has large, expressive eyes. Personality-wise, he's a typical nerd: sympathique mais préfère faire profil bas, plongé dans ses études ou ses propres pensées. On le retrouve souvent aux côtés de son ami Kevin. Il ne participe pas aux conflits, préférant rester observateur. Comme toutes les créatures de papier, il est extrêmement vulnérable aux dommages physiques.

TIPS: 

- Pas une menace.
- Son design unique (un taille-crayon au lieu d'un corps) fait de lui l'un des personnages les plus mémorables, symbolisant la nature créative et décalée de Paper School.
