MINIGUN XM214-A


WEAPON: Minigun XM214-A
AMMO: Balles de 5,56 mm
ROF: 1200 balles/min
WEIGHT: 30 livres

DESCRIPTION: 

Évoluant de Gatling à Vulcan et M134, le XM214-A est la dernière nouveauté en matière d'armement d'infanterie. Auto-alimentée et dotée d'un chargeur intégré et d'un adaptateur de recul, c'est l'arme personnelle la plus puissante disponible.

NOTES: 

- À utiliser pour une confrontation à courte ou longue distance avec des ennemis coriaces ou pour l'élimination massive d'un grand nombre d'adversaires plus petits.
- Dépense des munitions rapidement.
