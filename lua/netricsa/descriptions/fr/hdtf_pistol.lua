M9 PISTOL


WEAPON: Pistolet M9
AMMO: Rondes Parabellum 9x19mm
ROF: Semi-automatique, ~15 coups/min efficaces

DESCRIPTION:

Arme de poing militaire et policière largement utilisée. Offre une bonne précision et un faible recul, ce qui le rend fiable pour des tirs de suivi rapides. Sert d'arme de poing standard pour de nombreux H.E.C.U. soldats dans le conflit de Black Mesa.

NOTES:

- Précis et facile à contrôler.
- Léger mais limité en puissance de freinage.
- Surclassé par les armes de poing de gros calibre comme le .357 Magnum ou le Desert Eagle.