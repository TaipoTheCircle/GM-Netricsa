LANCEUR PORTABLE


WEAPON: Lanceur portable
AMMO: Missiles-fusées chauffants (ogives HEAT)
ROF: ~17,6 tours/min.

DESCRIPTION: 

Une arme d'infanterie lourde utilisée par les raiders lance-missiles pour détruire les véhicules blindés, les fortifications et les concentrations denses de cibles. Il s'agit d'un système de lancement portable qui tire des projectiles de fusée chauffés avec un pouvoir de pénétration élevé.

L'ogive à charge creuse est conçue pour détruire les fortifications et pénétrer les blindages lourds, tandis que l'explosion crée une zone de dégâts supplémentaire dangereuse pour le personnel. Malgré sa cadence de tir relativement faible, chaque missile possède une puissance destructrice importante et peut renverser le cours de la bataille d'un seul coup précis.

En raison de sa masse et de sa cadence de tir limitée, l'arme est plus souvent utilisée à distance ou à couvert, permettant aux raiders lance-missiles de soutenir les équipes d'assaut avec un tir nourri.

NOTES: 

- Efficace contre les véhicules blindés, les fortifications et les cibles lourdes.
- L'explosion inflige des dégâts de zone et crée une onde de choc.
- Plus efficace à moyenne et longue portée.
- Dangereux à courte distance ; tirer à proximité de l’utilisateur entraîne souvent des conséquences fatales.
- L'ogive à charge creuse est capable de détruire les fortifications et de pénétrer les blindages lourds.
- La lenteur de la cadence de tir pratique est compensée par sa puissance destructrice élevée.
