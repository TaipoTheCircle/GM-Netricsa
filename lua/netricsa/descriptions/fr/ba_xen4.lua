LE MONDE FRONTIÈRE DE XEN, CASCADES DE XEN


Sam, arrête une seconde. Oui, maintenant.

J'ai détecté l'appareil. Le signal est clair. Fiable. Indubitable. Je pourrais jurer sur mes neurones que c'est ça. Celui-là. Contrôle de téléportation. La dernière pièce du puzzle.

C'est juste un peu plus près. Sur une échelle Xen, c'est presque là. Sur l’échelle de survie, c’est comme toujours.

Entre nous et le but se trouvent des grottes avec des cascades.

Mais nous sommes proches, Sam. Si proche que même Xen semble devenir nerveux. L'espace se déforme davantage, les signaux sont plus stables et la résistance est plus désespérée.

Traversez les grottes.

Ne perdez pas vos repères.

Si je me trompe, vous pouvez attribuer cela à une interférence.

Mais si j'ai raison...

alors nous aurons bientôt une chance de nous échapper d'ici.
