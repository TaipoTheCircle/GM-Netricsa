ENTREPÔT BLACK MESA, UNE TROUVE AVEC UNE DATE LIMITE


Sam, tu es sorti des conduits de ventilation et je suis immédiatement tombé sur une tragédie. Quelque part à côté de vous se trouve un scientifique nommé Harold. Il a été abattu par H.E.C.U. militaires. Il y a du sang sur sa robe, sa respiration est irrégulière, mais ses yeux... Mes yeux brûlent. Il tient toujours. Et il semble savoir ce dont vous avez le plus besoin.

Harold s'accroche à ses dernières forces pour reprendre conscience.

Your task now is: 

Pour écouter Harold. Il sait comment sortir d'ici. Mais son temps est compté : chaque seconde compte.

Posez la question principale : « Comment sortir d'ici ? - et écoutez attentivement.

Rappelez-vous cela. Agissez vite.

Allez, Sam. Le temps presse. Harold respire toujours. Cela signifie que vous avez encore une chance.
