VILLAGER DU MINECRAFT OVERWORLD


CLASS: Villageois
METABOLISM: Humanoïde à Sang Chaud
PERCEPTION: Humanoïde Passif Intelligent
SIZE: 6,2 pieds
ENDURANCE: Faible
HOSTILITY: Très faible
WEAPONS: Aucun
REWARD: 100 FC
THREAT: Minimal

DESCRIPTION: 

Les villageois sont les principaux représentants de la civilisation intelligente dans Overworld de Minecraft. Ces humanoïdes sont reconnaissables à leur grand nez, leurs sourcils froncés et leur habitude invariable de croiser les bras, comme si le monde leur devait quelque chose. Ils communiquent en utilisant des sons caractéristiques, que certains considèrent comme une forme simplifiée de discours ou une agression passive.

Villagers live in villages where they lead a measured life: ils travaillent, font du commerce, dorment et paniquent à la vue des zombies. Chaque villageois peut avoir un métier déterminé par son bloc de travail, qui constitue la base de l'économie du village. La principale monnaie est l'émeraude, qui a beaucoup plus de valeur pour les villageois que pour le reste du monde.

Les villageois sont incapables de combattre et comptent entièrement sur les golems de fer, qu'ils craignent et considèrent à la fois comme des héros.

TIPS: 

- Échanger activement et changer de métier.
- Évitez les conflits directs.
- Craignez les foules hostiles.
- Un villageois peut pardonner, mais n'oublie jamais.
