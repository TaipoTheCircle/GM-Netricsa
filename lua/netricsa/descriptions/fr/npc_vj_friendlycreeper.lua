CREEPER AMICAL DU MONDE MINECRAFT


CLASS: Grimpante amical
METABOLISM: Poudre à canon végétale
PERCEPTION: Foule passive, animal domestique apprivoisé
SIZE: 5,5 pieds
ENDURANCE: Moyen
HOSTILITY: Faible
WEAPONS: Microexplosion
REWARD: 50 FC
THREAT: Moyen

DESCRIPTION: 

C'est une plante grimpante rose. Contrairement à ses frères verts et agressifs, cette variété grimpante est complètement passive et même amicale. Sa couleur rose n'est pas seulement une texture, mais un marqueur de son caractère paisible, une sorte de « label de qualité » pour ceux qui en ont assez d'exploser toutes les cinq minutes.

Contrairement aux autres membres de son espèce, cette plante grimpante prend tout avec calme. Il ne siffle pas dans le noir, ne se faufile pas derrière vous et ne détruit pas les structures. Même si quelqu'un le heurte accidentellement, il ne réagira pas par une agression, mais s'éloignera simplement.

Pour apprivoiser un Friendly Creeper, vous devez lui donner de la poudre à canon. Ensuite, un cœur apparaît sur sa poitrine et la plante grimpante devient un fidèle animal de compagnie.

Il suit son maître comme un loup apprivoisé.

Il attaque les foules hostiles en utilisant des microexplosions qui blessent les ennemis.

La plante grimpante elle-même reste indemne.

Pour soigner une plante grimpante amicale, vous pouvez lui donner des fleurs.

TIPS: 

- N'essayez pas de tuer un Creeper amical. C'est non seulement cruel, mais aussi inutile.
- Si vous trouvez une plante grimpante rose, apprivoisez-la immédiatement. C'est l'un des rares monstres qui vous protégera au lieu de vous tuer.
- L'apprivoisement nécessite de la poudre à canon, qui peut être obtenue à partir de plantes grimpantes ordinaires.
- En combat, le Friendly Creeper est efficace contre des hordes de petits monstres comme les zombies et les squelettes.
- N'essayez pas de l'utiliser pour l'extraction de ressources. Les explosions ne détruisent pas les blocs.
- Si vous possédez plusieurs lianes roses apprivoisées, elles peuvent fonctionner en meute. Celui-ci agit comme une petite escouade explosive rose qui vous protège de tous les malheurs du Nether et de l'Overworld.
