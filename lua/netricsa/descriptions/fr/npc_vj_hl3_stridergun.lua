COMBINER LA PROTECTION CIVILE STRIDER MINI METROCOP


CLASS: Combinez la protection civile Strider Mini Metrocop
METABOLISM: Humain, mâle
PERCEPTION: Opérateur de synthétiseur de combat lourd intégré
SIZE: 0,9 pied
ENDURANCE: Très faible
HOSTILITY: Haut
WEAPONS: Contrôle des armes principales de Strider
REWARD: 400FC
THREAT: Faible

DESCRIPTION: 

Le Strider Mini Metrocop est une forme encore plus radicale d'intégration humaine dans le synthétiseur de combat Combine. Contrairement aux opérateurs MiniStrider, ces Metrocops sont entièrement intégrés au compartiment biomécanique central du Strider et n'ont aucune autonomie en dehors de sa coque.

Leurs corps sont soumis à une compression, une contrainte et une synchronisation neuronale constantes avec le système de contrôle du marcheur. Toutes les fonctions vitales sont prises en charge uniquement par les systèmes embarqués du Strider. La vision, l'audition et l'orientation spatiale sont complètement remplacées par les informations sensorielles de la machine, qui alimentent directement le système nerveux de l'opérateur.

Contrairement à la version intégrée au MiniStrider, l'opérateur Strider ne peut pas être libéré, même théoriquement. Leur capsule de compression fait partie de la structure porteuse. La destruction du déambulateur entraîne immédiatement une perte du système de survie, une rupture des connexions neuronales et une destruction critique du corps de l'opérateur. Le retour à la taille normale est impossible.

TIPS: 

- La destruction du Strider garantit la destruction de l'opérateur à l'intérieur.
