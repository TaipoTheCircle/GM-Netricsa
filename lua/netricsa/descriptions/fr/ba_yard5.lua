ENTREPÔT BLACK MESA, PARC DE MARCHANDISES


Alors, récapitulons, Sam.

Le conteneur bleu s'est avéré rempli de Vortigaunts. Le rouge, les scientifiques. La logique a suivi, les surprises découvertes, la confiance dans le codage couleur complètement détruite.

Un autre conteneur nous accueille dehors. Bien sûr, c'est rouge. Si le système a fonctionné une fois, il doit absolument être inspecté. Il est garé dans le train, dernier espoir de sortir de ce trajet. Est-ce que Rosenberg est là ? C'est inconnu. Mais la probabilité est plus élevée que les deux précédentes, et à Black Mesa, c'est une raison d'être optimiste.

Bloquez soigneusement l'entrée du conteneur avec les tapis et les bobines. Pas symboliquement, littéralement. Quelqu'un a décidé que la meilleure façon de le protéger était de placer quelque chose de lourd et de le clouer avec des planches. Un plan conçu pour ceux qui ne peuvent pas perturber l'environnement au nom du progrès. Malheureusement, vous n’en faites pas partie.

La mécanique est simple. Vous cassez les planches et les bobines tombent. Les bobines tombent et le passage s'ouvre. Les lois de la physique, contrairement aux lois de la raison humaine, s’appliquent toujours ici.

Le secteur est gardé par H.E.C.U. militaires. Les mêmes visages, les mêmes fusils, la même certitude qu'ils ont déjà traversés ici. Dans la pièce voisine, derrière la tablette, se trouve un char Abrams. A l'intérieur se trouvent des soldats. Apparemment, ils considèrent le char comme un argument convaincant avec un homme qui a déjà vécu la fin du monde.

Si Rosenberg est vraiment dans ce conteneur, il a beaucoup de chance.

Dans le cas contraire, nous avons simplement déplacé à nouveau des objets lourds pour le bien du moral.

En tout cas, Sam, le choix est clair.

Cassez-le. Laissez tomber les bobines.

Le chemin de fer n'avait pas vu un tel passager depuis longtemps.
