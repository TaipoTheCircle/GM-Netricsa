PISTOLET-MACHINE SMG2 (MP7)


WEAPON: PISTOLET-MACHINE SMG2 (MP7)
AMMO: 4,6×30mm.
ROF: ~900 tr/min
WEIGHT: 2,0 kg.

DESCRIPTION: 

Le SMG2 est la mitraillette standard pour les unités d'assaut, utilisée au début des conflits d'unification. Conçu pour la suppression rapide des ennemis dans les zones densément peuplées et à bord des centres de transport.

La cadence de tir élevée garantit un tir concentré et dissuade efficacement les forces numériquement supérieures. L'arme est conçue pour le combat mobile et les changements de position fréquents, sacrifiant la portée au profit du contrôle à courte portée.

NOTES: 

- Optimal pour les combats rapprochés et le dégagement des couloirs
- Efficace pour supprimer la main-d'œuvre ennemie
- Sa grande capacité de munitions en fait une arme de soutien fiable
- Précision légèrement inférieure à longue portée par rapport aux armes à canon long
