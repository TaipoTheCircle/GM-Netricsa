TOURELLE PLASMA RXZ-200
\

CLASS: Tourelle à plasma RXZ-200
METABOLISM: Construction alimentée à l'énergie
PERCEPTION: Emplacement stationnaire, un hybride d'un canon à plasma et d'un lecteur laser
SIZE: ~3,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Varie selon le tireur
WEAPONS: Sphères plasmatiques
REWARD: 400FC
THREAT: Haut

DESCRIPTION: 

This is the result of Mental's engineering, who decided that conventional machine guns weren't enough and it was time to switch to energy. The design is simple: un corps ovale avec un cercle violet au centre, équipé d'un canon à plasma et d'un système de ciblage copié des tourelles laser.

Contrairement à la version mitrailleuse, la tourelle à plasma tire des sphères uniques de plasma violet, et non des rafales. Une seule sphère est parfaitement précise et peut détruire presque n'importe quelle cible d'un seul coup, qu'il s'agisse d'un Orc ordinaire, d'un hélicoptère de combat ou même de Mental lui-même, en cas de malchance. Lors de l'impact, le plasma explose, endommageant tout ce qui se trouve à proximité.

Mental combined his plasma cannon with the laser turret's control system, creating a hybrid weapon that can be used by anyone: Soldats de la Horde et Orcs de la planète Orazio.

Les tourelles sont généralement construites dans les bases de Mental sur Sirius et d'autres planètes.

En combat, la tourelle à plasma est dangereuse à n'importe quelle distance. Son seul inconvénient est sa faible cadence de tir. Quelques secondes s'écoulent entre les tirs, et s'il y a beaucoup d'ennemis et qu'ils se rapprochent, la tourelle n'aura peut-être pas le temps de recharger.

Mais si un tireur se trouve à l'intérieur, il est protégé par la coque et peut utiliser la tourelle comme source de santé supplémentaire : elle absorbe les dégâts et, si elle est détruite, éjecte simplement le pilote.

TIPS: 

- Si vous voyez un ennemi derrière une tourelle, éliminez-le en premier. Une tourelle sans tireur n'est qu'un morceau de métal.
- Une tourelle est indispensable contre les cibles blindées.
- Les explosifs détruisent une tourelle plus rapidement que les tirs d'armes légères.
- Si vous voyez des pattes d'orques dépasser de derrière une tourelle, il y a quelqu'un là-bas.
