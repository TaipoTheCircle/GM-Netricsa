PISTOLET AIGLE DU DÉSERT


WEAPON: Aigle du désert IMI
AMMO: 357 cartouches Magnum
ROF: Semi-automatique, ~30 coups/min

DESCRIPTION: 

Arme de poing semi-automatique de gros calibre délivrée à H.E.C.U. agents. Dispose d'un viseur laser intégré pour une précision accrue. Doté d'une grande puissance d'arrêt, ce qui en fait l'une des armes de poing les plus efficaces sur le terrain.

NOTES: 

- Dégâts supérieurs par rapport au pistolet standard de 9 mm.
- La visée laser intégrée permet une précision extrême.
- Capacité de chargeur limitée et munitions rares.
