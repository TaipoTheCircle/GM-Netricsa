COCON D'ARAIGNÉE ANTARESIENNE D'ANTARES PRIME


CLASS: Cocon d'araignée antarésienne
METABOLISM: Capsule stationnaire biologique
PERCEPTION: Capsule protectrice
SIZE: 1,6 pi
ENDURANCE: Faible
HOSTILITY: Passif
WEAPONS: Libère une araignée antarésienne lors de sa destruction
REWARD: 10 FC
THREAT: Moyen

DESCRIPTION: 

Une structure organique qui sert d'incubateur à une seule araignée antarésienne. Il s'agit d'un réseau de bioracines solides et nerveuses qui l'ancrent aux surfaces, le plus souvent aux plafonds, aux sols ou aux murs, dans les endroits où les araignées se sentent en sécurité (grottes, sous-sols, tunnels abandonnés). Le cocon n'attaque pas. Lorsqu'elle est détruite (par un tir, une explosion ou un impact), une araignée éclate et attaque immédiatement l'intrus.

TIPS: 

- Après la destruction, préparez-vous à une attaque immédiate d'araignée. Prenez au préalable une position favorable.
