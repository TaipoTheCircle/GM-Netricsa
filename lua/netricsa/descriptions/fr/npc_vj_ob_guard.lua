LEGIONNAIRE IMPÉRIAL DU NIRN


CLASS: Légionnaire Impérial
METABOLISM: Impérial, mâle
PERCEPTION: Soldat professionnel de la Légion Impériale
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Conditionnel
WEAPONS: Acier / Épée impériale, Arc impérial avec flèches en acier, Bouclier impérial
REWARD: 300FC
THREAT: Haut

DESCRIPTION: 

Il s'agit de la principale force militaire de l'Empire, une armée professionnelle qui maintient l'ordre dans tout Tamriel depuis des centaines d'années. Ses origines remontent à la Première Ère, lorsque les Légions Alessiennes unifièrent Cyrodiil sous la domination humaine. À l’ère moderne, malgré les guerres et les crises, la Légion demeure une force disciplinée et redoutable.

La structure de la Légion est organisée et sévère. Elle est divisée en légions et cohortes numérotées. Les candidats doivent posséder non seulement la force brute, mais aussi l’endurance, la vertu et la fiabilité. La Légion est réputée pour sa politique inclusive : les membres de toutes races et confessions sont les bienvenus dans ses rangs. Bien que le noyau de la Légion soit composé d'Impériaux, de Nordiques et de Rougegardes, on trouve parmi ses soldats des Brétons, des Orsimers et même des Dunmers.

L'équipement d'un légionnaire dépend de l'époque et du lieu de service. Pendant la crise d'Oblivion en Cyrodiil, ils portaient une lourde armure d'acier avec des éléments d'armure de plaques romaine, armés d'épées longues. Dans Skyrim, la situation est plus complexe : les ressources sont rares et la majorité des soldats portent une armure légère en cuir avec des inserts en acier. C'est une mesure nécessaire, car la Légion reçoit peu de renforts en province et est contrainte de recruter des milices locales, abaissant parfois leurs standards.

Legionnaires are subject to strict discipline. They patrol roads, guard forts, and participate in full-scale battles. In Skyrim, they wage a protracted war against rebels. Legionnaires employ "sword and shield" tactics: ils encaissent les coups ennemis avec leurs boucliers puis contre-attaquent.

Pendant la crise d'Oblivion, ils constituaient l'élite, mais la guerre avec les Thalmor et les années de crise ont paralysé l'armée. Dans Skyrim, les soldats sont souvent mécontents de l'Empire, mais ils le servent soit par tradition, soit parce qu'ils le considèrent comme le seul bastion contre les Thalmor.

TIPS: 

- Si vous envisagez de combattre aux côtés de l'Empire, votre équipement sera supérieur à celui de votre adversaire. Les légionnaires sont bien blindés et organisés.
- Lorsque vous combattez un légionnaire, souvenez-vous de sa tactique. Ne vous précipitez pas de front dans un mur de boucliers, utilisez des manœuvres de flanc, de la magie ou des unités à distance.
- Les meilleures armes contre les légionnaires lourdement blindés sont les sorts et les armes qui ignorent l'armure.
- Dans la province de Skyrim, les légionnaires sont plus vulnérables en raison de leur armure légère. Utilisez des armes lourdes pour drainer rapidement leur santé.
- Si vous êtes un criminel, éloignez-vous des légionnaires. Contrairement aux gardes municipaux, ils ont le droit de vous arrêter en dehors des villes.
- Lorsque vous rejoignez la Légion, vous prêtez un serment sacré d'allégeance à l'Empereur. Le briser sera considéré comme une trahison.
