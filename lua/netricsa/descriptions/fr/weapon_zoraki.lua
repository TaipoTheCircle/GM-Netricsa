ZORAKI STALKER M906 SIGNAL PISTOL


WEAPON: Traqueur Zoraki M906
MUNITIONS Cartouches 9MM PAK
ROF: ~15 tours/min

DESCRIPTION:

Pistolet de signalisation à chargement automatique de la marque turque Zoraki. Convient pour l'autodéfense, pour effrayer les animaux sauvages et pour lancer des feux d'artifice (à l'aide d'un lance-roquettes).

NOTES:

- Ce n'est pas un pistolet de combat et ne peut tuer qu'à bout portant.
- Peut aveugler une personne si des cartouches 9MM PAK Flash Defense sont insérées dans le chargeur.
