COMBINEZ OFFICIER DE PROTECTION CIVILE


CLASS: Combiner Officier de la Protection Civile
METABOLISM: Les humains
PERCEPTION: État-major de commandement de la protection civile
SIZE: 6 pieds
ENDURANCE: Moyen
HOSTILITY: Moyen
WEAPONS: Pistolet, Revolver, SMG, AR2, SPAS-12, OICW
REWARD: 350FC
THREAT: Moyen

DESCRIPTION: 

Les agents de la protection civile font le lien entre les flics de rue et les loyalistes. Contrairement aux soldats réguliers de la Protection Civile, ils évitent les masques à gaz, préférant garder le visage ouvert.

Leur apparence varie. Certains portent une armure standard de la protection civile, tandis que d'autres préfèrent des bérets, des insignes, des épaulettes et d'autres bibelots qui mettent l'accent sur le rang et les privilèges personnels. L'uniforme est moins standardisé, mais il souligne parfaitement la différence entre ceux qui donnent des ordres et ceux qui reçoivent des charges Stun-Stick.

Les officiers sont formellement tenus de maintenir l'ordre, de coordonner les patrouilles et de rendre compte directement aux structures du Combine. Mais de manière informelle, les choses sont plus compliquées. Certains, utilisant leur accès à l’information et aux communications, transmettent secrètement des données à la Résistance.

Au combat, un officier de la Protection Civile est plus dangereux qu'un métrocop ordinaire grâce à son expérience et son sang-froid, mais il reste humain sans augmentations significatives.

TIPS: 

- Ne sous-estimez pas leur expérience, même s'ils ont l'air trop « civils ».
- Certains officiers en savent plus qu'ils ne le prétendent.
