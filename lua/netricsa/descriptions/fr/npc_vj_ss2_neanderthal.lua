BRUTE PRIMITIVE DE M'DIGBO


CLASS: Brute primitive
METABOLISM: Mammifère
PERCEPTION: Être primitif
SIZE: 6,8 pieds
ENDURANCE: Moyen
HOSTILITY: Moyen
WEAPONS: Massue à pointes, bouclier en bois
REWARD: 150 FC
THREAT: faible

DESCRIPTION: 

M'Digbo est parsemé d'innombrables tribus primitives, qui affichent toutes un comportement très territorial et agressif. Ils attaquent presque tout le monde de manière impulsive et ne sont pas plus intelligents qu’un tas de briques.

Il s'agit d'une race humanoïde dirigée par la mystérieuse "Maman".

Lorsque les forces de Mental colonisèrent M'Digbo, les primitifs lancèrent des attaques contre eux. Ils sont tous morts. Après cela, ils ont commencé à adorer Mental comme un dieu de la guerre et lui ont prêté allégeance. Cela a été fait par peur et par respect pour Mental, ainsi que pour les culottes léopard stylistiques que Sirius Fashion Inc leur a fournies.

Leur arme la plus puissante est une simple massue à pointes, bien que certains portent également des boucliers en bois fabriqués sur mesure à partir de la planète Chi-Fang. Les tactiques sont inexistantes parmi eux, alors attendez-vous à ce qu’ils se précipitent après vous tout en balançant avec colère leurs massues.

TIPS: 

- Ils se déplacent lentement et peuvent être éliminés relativement facilement, ils ne devraient pas avoir une priorité parmi les autres ennemis.
- Ne sous-estimez pas leurs clubs, une grosse claque sur la tête et vous vous retrouverez avec bien plus qu'un simple mal de tête. Gardez vos distances.
