DESCRIPTION: 

Il s'agit de la troisième incarnation du vicieux Ugh-Zan, le dernier des géants Warlock. Ugh-Zan a le rang de grand amiral dans l'armée du Mental et sert le mal depuis des millions d'années.
Aussi grand qu'une montagne, suffisamment bien armé pour affronter un croiseur intergalactique, et doté de pouvoirs magiques invisibles et d'une capacité de régénération, ce grand guerrier est l'une des créatures les plus dangereuses de l'univers.
Ugh-Zan a été envoyé par Mental pour superviser le nettoyage final de la planète Terre.

