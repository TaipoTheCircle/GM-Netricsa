ALYX VANCE


CLASS: Alyx Vance
METABOLISM: Humain, Femelle
PERCEPTION: Agent de la Résistance Humaine
SIZE: ~5,5 pieds
ENDURANCE: Moyen
HOSTILITY: Faible
WEAPONS: Alyxgun, Shotgun, SMG occasionnel, outils de piratage
REWARD: 1000FC
THREAT: Moyen-élevé

DESCRIPTION: 

Alyx Vance est l'un des principaux dirigeants de la Résistance humaine et la fille d'Eli Vance, scientifique en chef de Black Mesa East. Ayant grandi sous l'oppression du Combine, elle a développé à la fois un esprit vif et des compétences de survie exceptionnelles. Agile, ingénieux et mortellement précis avec les armes à feu, Alyx n'est pas seulement un combattant mais aussi un ingénieur, capable de pirater les interfaces du Combine et de reprogrammer leurs appareils.

Malgré le monde sombre dans lequel elle habite, Alyx reste optimiste, compatissante et farouchement loyale envers ses alliés. Son lien étroit avec Gordon Freeman renforce le mouvement de Résistance, inspirant d’autres à se soulever. Souvent accompagnée de son fidèle chien robot de compagnie, elle représente l'espoir dans les moments les plus sombres.

Son style de combat privilégie la mobilité entre les abris, l'élimination des ennemis avec des tirs précis et l'utilisation de l'environnement à son avantage. Bien qu'elle ne soit pas lourdement blindée, ses réflexes rapides et ses tactiques intelligentes en font l'un des adversaires les plus dangereux du Combine.

TIPS: 

- Excellente alliée de soutien au combat – ne sous-estimez pas sa précision.
- Peut pirater les machines Combine pour retourner les armes de l'ennemi contre eux.
- Sans armure lourde, elle compte sur l'agilité et se protège lorsqu'elle est submergée.
