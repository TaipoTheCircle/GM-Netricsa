SOLDAT ORC, COMMANDANT DE L'ORAZIO


CLASS: Soldat orc, commandant
METABOLISM: Mammifère
PERCEPTION: Cornes
HEIGHT: 6,3 pieds
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Fusil à plasma, grenades à impulsions
REWARD: 40 FC
THREAT: Moyen

DESCRIPTION: 

C'est un exemple clair du dicton « ils voulaient le meilleur, mais cela s'est avéré comme d'habitude ». Ils faisaient autrefois partie de la grande race Zorg de Bilmez IV, forts, féroces et, surtout, bronzés. Mais les bains de soleil constants ont fait perdre aux Zorgs presque toute leur peau. Cherchant une solution, ils ont créé un mutagène qui leur redonnerait leur ancienne apparence. Au lieu de cela, le mutagène les a transformés en orcs...

La drogue fut immédiatement interdite et les expérimentateurs, ainsi que toutes les victimes, furent exilés sur la planète isolée d'Orazio. Là, les exilés ont fait bon usage de leur temps, fondant la société minière Altani et ont commencé à extraire du minerai, tombant finalement entre les mains de Mental.

Mental recruta des orcs dans ses armées ; la plupart se sont retrouvés dans les usines militaires de Mental, où ils ont contribué à produire des armes et des véhicules de combat. Les autres ont été envoyés à l'Académie militaire de Solgar sur Kronor, où ils ont été formés aux tactiques de base et équipés d'armures et d'armes modernes.

Orcs who graduated with honors from the academy were promoted to commanders. They wear composite armor, which provides greater protection than standard orc armor, and use two types of weapons: un fusil à plasma et des grenades à impulsions.

Ils dirigent généralement des groupes d'autres orques et sont rarement seuls. Certains commandants utilisent également des gyrocoptères, qui leur permettent de voler, ainsi que des montures en forme de lézard, qui augmentent leur mobilité.

TIPS: 

- Contrairement à la plupart des serviteurs de Mental, ils essaieront de garder une distance lorsqu'ils seront proches de vous.
- Leurs projectiles ont des capacités de guidage mineures - restez loin d'eux.
- Les commandants utilisant des gyrocoptères n'utilisent pas de grenades et disposent d'un blindage légèrement plus solide.
