ZOMBIE DU MONDE MINECRAFT


CLASS: Zombi
METABOLISM: Mort-vivant
PERCEPTION: Humanoïde hostile
SIZE: 6,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONALS: Les mains, l'entêtement et l'équipement de quelqu'un d'autre
REWARD: 800 FC
THREAT: Moyenne

DESCRIPTION: 

Les zombies sont d'anciens humains du monde Minecraft qui ont décidé un jour de ne pas s'endormir et l'ont profondément regretté. Lents, persistants et étonnamment déterminés, ils errent la nuit à la recherche de tout être vivant capable encore de penser.

Les zombies manquent d'intelligence, mais ils compensent par leur nombre. Ils peuvent ramasser des objets, porter des armures et avoir parfois l'air d'avoir simplement cambriolé l'entrepôt d'un aventurier. Ils s’illuminent lorsqu’ils sont exposés au soleil, ce qui les amène cependant rarement à reconsidérer leurs priorités dans la vie.

TIPS: 

- Le soleil est votre liberté - utilisez la matinée à bon escient.
