CARTE CUBE


WEAPON: Carte cubique

DESCRIPTION: 

Utilitaire de développement qui génère un ensemble de sphères de test réfléchissantes (sondes cubemap) utilisées pour prévisualiser et capturer les réflexions de l'environnement. Lorsqu'il est activé, l'outil place plusieurs sphères avec différents préréglages de matériaux dans le monde afin que les artistes et les concepteurs de niveaux puissent vérifier l'éclairage spéculaire, les réflexions et le mélange des cartes cubiques. Ce n'est pas un objet de combat - purement diagnostique et visuel.

NOTES: 

- Vérifier l'éclairage spéculaire.
