VEILLEUR IMPÉRIAL DU NIRN


CLASS: Gardien impérial
METABOLISM: Impérial, mâle
PERCEPTION: Gardien de l'Ordre de la Cité Impériale / Agent d'application de la loi
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Conditionnel
WEAPONS: Épée longue en argent, magie parfois destructrice
REWARD: 200FC
THREAT: Moyenne

DESCRIPTION: 

Ce sont les piliers de l’ordre et de la loi dans la cité impériale de Cyrodiil. Ces combattants, vêtus de l'armure distinctive de la Légion, sont des représentants de la Légion Impériale et accomplissent la difficile tâche de maintenir la loi et l'ordre dans la capitale provinciale.

L'apparence du Gardien inspire le respect et une certaine intimidation. Ils sont vêtus d'une armure complète en acier de la Légion, comprenant un casque massif, et manient une épée longue en argent. Leur travail consiste à être visible, afin que les criminels potentiels réfléchissent à deux fois avant d'enfreindre la loi. Certains Gardiens maîtrisent les sorts de Destruction, ce qui les rend dangereux non seulement en combat rapproché mais aussi à distance.

A Watchman's primary function is justice. If you commit a crime, be it murder, theft, or assault, the nearest guard will immediately rush to your side, shouting, "Halt, criminal abomination!" You will have a choice: payer une amende, aller en prison ou résister. Si vous décidez de vous rendre, le garde confisquera tous les biens volés et vous accompagnera jusqu'au château pour purger votre peine.

Résister à l'arrestation est une mauvaise idée. Les gardiens se battent avec habileté et cohésion, et si vous refusez de vous rendre, ils n'hésiteront pas à dégainer leurs épées en criant : « Alors payez avec votre sang !

Il existe plusieurs unités spécialisées de la Garde Impériale. Les gardiens de prison gardent la prison impériale et utilisent une masse d'argent au lieu d'une épée. Ils portent toujours sur eux des clés de cellule, qui peuvent s'avérer utiles pour un détenu possédant une bonne dextérité manuelle.

Malheureusement, certains gardes sont sujets à la corruption. Par exemple, le capitaine Audens Avidius a été surpris en train d'extorquer de l'argent à des marchands. Les gardes honnêtes, comme le capitaine Itius Hein, sont prêts à aider à arrêter les criminels, mais ils auront besoin de preuves irréfutables.

TIPS: 

- Si vous envisagez de vous livrer à une activité criminelle, faites-le dans des zones isolées et évitez d'être vu par les gardes. Leur vue et leur ouïe sont excellentes.
- Si vous êtes repéré en train de voler et qu'un garde s'approche, votre meilleure option est de payer une amende. Résister à une arrestation entraîne presque toujours la mort.
- Emportez suffisamment d'or pour couvrir l'amende. Si vous n’avez pas d’argent, vos seules options sont la prison ou le combat.
- N'oubliez pas qu'après avoir purgé votre peine, tous les objets volés seront confisqués. Cachez-les dans un endroit sûr avant de vous rendre aux autorités.
- Écoutez ce que disent les gardes. Ils offrent souvent des conseils utiles sur la ville, ses quartiers et les derniers événements.
