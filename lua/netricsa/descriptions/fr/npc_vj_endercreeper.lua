ENDER CREEPER DE LA FIN DE MINECRAFT


CLASS: Ender Creeper
METABOLISM: Poudre à canon hybride
PERCEPTION: Ender Créature Hybride, Chasseur d'Élite
SIZE: 5,5 pieds
ENDURANCE: Haut
HOSTILITY: Absolu
WEAPONS: Téléportation, référencement de cible, explosion, morsure
REWARD: 300FC
THREAT: Absolu

DESCRIPTION: 

C'est le résultat d'une mutation d'une plante grimpante normale qui a été exposée trop longtemps à l'énergie de l'Ender à la Fin.

L'Ender Creeper diffère d'une plante grimpante normale non seulement par sa couleur, mais aussi par sa vitesse et son intelligence visible. Lorsqu'il se déplace, il laisse une traînée de particules violettes, semblable à la téléportation de l'Enderman.

The Ender Creeper inherits the Enderman's abilities: il peut se téléporter sur de courtes distances, esquivant les flèches et les attaques. Lorsqu'on lui tire dessus avec un arc ou une arbalète, il disparaît instantanément et réapparaît derrière ou sur le côté, prêt à attaquer. Parfois, il peut se téléporter juste devant sa victime, lui bloquant ainsi le chemin.

Il traque sa cible, se téléporte pour attaquer depuis une direction inattendue, puis explose.

TIPS: 

- N'essayez pas de tirer sur l'Ender Creeper avec un arc ou une arbalète. Il se téléportera et vous gaspillerez simplement vos flèches.
- Utilisez de l'eau pour désactiver sa téléportation.
- En combat au corps à corps, frappez rapidement avec votre épée, sans lui laisser le temps de réagir.
- L'Ender Creeper n'explose pas instantanément. Si vous entendez un sifflement, sautez ou utilisez un bouclier.
- Tuer un Ender Creeper a une chance de laisser tomber une Ender Pearl. C'est un excellent moyen de cultiver des perles si vous ne voulez pas tuer des Endermen.
