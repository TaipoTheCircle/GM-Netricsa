BARNACLE DREAMCAST DE XEN


CLASS: Balane Dreamcast, Xen
METABOLISM: prédateur embuscade carnivore
PERCEPTION: Animal prédateur de plafond
SIZE: 6 à 8 pieds (longueur de langue jusqu'à 20 pieds)
ENDURANCE: moyen
HOSTILITY: embuscade passive
WEAPONS: langue collante, mâchoires écrasantes
REWARD: 150 FC
THREAT: moyen (élevé dans les espaces confinés)

DESCRIPTION: 

La balane est un prédateur stationnaire originaire de Xen, que l'on trouve souvent suspendu aux plafonds, aux grottes et aux structures industrielles. Il étend une longue langue collante qui pend vers le sol, attendant que quelque chose l'effleure. Une fois le contact établi, la victime est remontée vers une gueule de dents dentelées, où elle est écrasée et dévorée entière. Bien qu'immobiles et prévisibles, les balanes sont mortelles pour les imprudents, en particulier dans les couloirs étroits ou lorsque les victimes sont distraites par d'autres menaces.

TIPS: 

- Recherchez les balanes qui sont plus faciles à repérer de loin.
- Use them against enemies: attirer les zombies ou combiner dans leurs langues.
- Un tir court et contrôlé les tuera rapidement.
