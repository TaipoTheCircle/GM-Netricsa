CLONER STORMTROOPER AVEC KAMINO


CLASS: Cloner un Stormtrooper
METABOLISM: Clone humain, mâle
PERCEPTION: Stormroopers d'élite de l'Empire Galactique
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrême
WEAPONS: Fusil E-101
REWARD: 800 FC
THREAT: Haut

DESCRIPTION: 

Des stormtroopers d'élite de l'Empire Galactique, incarnant la volonté de fer de l'Empereur et servant à intimider la population. Ce soldat est vêtu d'une armure ork blanche, mais il est en réalité humain et lié aux orks uniquement par l'armure. Il fait partie de l'Empire Galactique et est allié à Mental. Ils ont obtenu leur armure auprès de la coentreprise Mental-Altani sur Oratio, où les Orks extraient du minerai et les ingénieurs impériaux le transforment en équipement de combat. D'où l'armure Ork portant l'insigne de Mental.

Le soldat lui-même est un clone de l'un des guerriers à succès, Jack Fat. Ils portent un fusil E-101, qu'ils utilisent pour éliminer les ennemis à moyenne et longue portée.

L'armure se compose de 18 segments, offrant une grande mobilité et une protection efficace contre la plupart des types d'armes légères. Les filtres de casque intégrés protègent contre les armes chimiques et biologiques et permettent de respirer sous vide. Un système de communication intégré au casque permet une coordination en temps réel.

Ces serviteurs sans visage du Nouvel Ordre, par la force et la brutalité, ont porté la volonté de l'Empereur sur des milliers de systèmes stellaires à travers la galaxie. À l’apogée de l’Empire, les stormtroopers sont devenus l’incarnation vivante de sa puissance, de sa cruauté et de sa peur. À de rares exceptions près, tous les stormtroopers portaient une armure blanche, ce qui les distinguait des autres unités militaires impériales.

Les stormtroopers impériaux ont évolué à partir des soldats clones de la Grande Armée de la République. À la fin de la Guerre des Clones, en 19 av.BY, la République Galactique fut réorganisée en Premier Empire Galactique. La Grande Armée a été transformée en Stormtrooper Corps, et les clones ont été rebaptisés « Stormtroopers ».

Ils sont alliés de Mental et reçoivent de sa part des renforts, des transports, de l'équipement et d'autres fournitures. En échange, ils fournissent leurs troupes pour des opérations qui nécessitent une infanterie massive et hautement disciplinée.

TIPS: 

- Leur fusil E-101 a une cadence de tir élevée mais est imprécis à longue portée.
- Leur armure est blanche, ce qui les rend invisibles dans la neige.
- N'essayez pas de négocier.
- Si un stormtrooper monte à bord d'un transport, détruisez le transport.
