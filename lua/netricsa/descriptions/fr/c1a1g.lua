BLACK MESA, LE PROBLÈME DU SCIENTIFIQUE


Sam, scan visuel à venir... oh, ça n'a pas l'air bien. Il y a un scientifique debout sur ce qui reste d'un podium. Et quand je dis « ce qui reste », je veux dire trois boulons rouillés, une prière et beaucoup de gravité.

I've run the survival probability through all my models. Result: 1% de chances qu'il y parvienne. Et ce n'est que si un miracle se produit - comme un crabe se transformant soudainement en trampoline. Spoiler : peu probable.

Soyez prudent, car si vous vous approchez trop près, les vibrations pourraient suffire à l'envoyer dans les abysses. D'un autre côté... si vous ne vous approchez pas, il va probablement tomber de toute façon. C’est l’un de ces scénarios classiques perdant-perdant, sauf que ce n’est pas vous qui perdez.

Strategic suggestion: ne gaspillez pas trop de munitions ou d'efforts ici. Si vous essayez de le sauver et que vous réussissez d'une manière ou d'une autre, félicitations, vous venez de battre les mathématiques. Mais je ne parierais pas là-dessus mes charmantes baskets rouges.
