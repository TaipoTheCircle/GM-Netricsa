KINGPIN R, ARCHPSION DE XEN


CLASS: Kingpin R, Archipsion de Xen
METABOLISM: Essence psionique
PERCEPTION: Entité psionique de Xen
SIZE: 12-15 pieds
ENDURANCE: Haut
HOSTILITY: Très élevé
WEAPONS: Explosions psioniques, contrôle mental, projectiles d'énergie orange, frappes de foudre (incinère les cibles en cendres)
REWARD: 10000FC
THREAT: Extrêmement élevé

DESCRIPTION: 

Connu sous le nom d'Archpsion de Xen, le Kingpin est l'un des seigneurs de guerre psioniques les plus terrifiants de Xen. Sa forme grotesque – un cerveau massif exposé avec quatre tiges oculaires et une queue en forme de ver pour la locomotion – est un moteur vivant de domination psychique et de destruction élémentaire.

La créature est constamment entourée d'un bouclier psionique, rendant les attaques directes presque inutiles jusqu'à ce que son énergie soit perturbée. Il lance des éclairs de plasma orange brûlant et invoque des arcs de foudre capables de réduire la chair et la pierre en cendres.

What makes the ArchPsion even more dangerous is its ability to summon hordes of Xenian beasts to fight by its side. Among these are unique variants of Bullsquids, each mutated for elemental combat: 

Devilsquid - Basé sur le feu, crache des projectiles incendiaires qui enflamment tout.

Frostsquid - À base de glace, pulvérise un liquide glacial qui immobilise les ennemis.

Poisonsquid - Violet, sécrète un acide venimeux qui corrode l'armure et empoisonne la chair.

Calmar toxique - Vert, propage des nuages ​​​​de bile corrosifs qui s'attardent et s'étouffent.

Calmar hybride - Blanc, agile, crache des éclairs de plasma blanc à longue portée à grande vitesse.

Standard Bullsquid - Prédateur classique, agressif et nombreux.

Les légendes parmi les créatures Xen décrivent le Kingpin comme un « lien vivant » de tempêtes psioniques – un commandant qui incarne à la fois le contrôle et la destruction.

TIPS: 

- Concentrez le feu sur la perturbation de son bouclier psionique avant d'infliger de réels dégâts.
- Restez mobile pour éviter ses éclairs et ses impulsions de contrôle mental.
- Préparez-vous à des vagues de créatures invoquées : le contrôle des foules est vital.
- Un équipement résistant au feu et aux toxines est fortement recommandé.
