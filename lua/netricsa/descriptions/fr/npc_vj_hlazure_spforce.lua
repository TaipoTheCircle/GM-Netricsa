GROGNEMENT DES FORCES SPÉCIALES DU SECTEUR DELTA


CLASS: Grunt des forces spéciales
METABOLISM: Humain, mâle
PERCEPTION: Unité d'opérations spéciales d'élite
SIZE: ~5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Beretta 92FS, fusil à impulsion Armat M41A, mitrailleuses, munitions expérimentales
REWARD: 4000FC
THREAT: Catastrophique

DESCRIPTION: 

Les Forces Spéciales du Secteur Delta sont une unité militaire d'élite sous le commandement direct de l'homme en costume d'affaires qui porte une valise. Ils sont hostiles à toutes les factions – le personnel de Black Mesa, les HECU et les créatures Xen. Hautement entraînés et blindés, ils opèrent avec une efficacité impitoyable, prenant souvent d'assaut les installations de recherche ou les avant-postes Xen pour sécuriser des artefacts anormaux.

Ils emploient une coordination supérieure, un armement avancé et un soutien important, notamment des véhicules et des mitrailleuses. Les affrontements avec eux sont marqués par une puissance de feu écrasante et une précision tactique, ce qui en fait l'une des menaces les plus meurtrières du conflit.

TIPS: 

- Utiliser largement la couverture - ils sont précis et disciplinés.
- Perturbez leurs formations pour réduire l'efficacité de l'équipe.
- Préparez-vous aux armes lourdes et aux tourelles.
- Attendez-vous à un homme en costume d'affaires qui porte une valise là où il apparaît.
