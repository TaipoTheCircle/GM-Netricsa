CHIEN D'ATTAQUE CERBÈRE


CLASS: Chien d'attaque Cerbère
METABOLIZATION: Mammifère
PERCEPTION: Animal d'attaque dressé
SIZE: 3,5 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Mordre
REWARD: 150 FC
THREAT: Moyen

DESCRIPTION: 

Il s'agit d'un chien d'attaque agressif à trois têtes utilisé pour garder les bases de Mental ou accompagner les chasseurs lors de leurs voyages de chasse.

Cerbère a été créé grâce à l'intérêt malsain de Mental pour la mythologie grecque. Après que Mental ait trouvé du temps libre, il s'est précipité pour créer un chien à trois têtes – Cerbère.

But after the new animal was born, unexpected problems arose: le cerveau de chaque tête ordonnait des tâches différentes au reste du corps.

En fin de compte, le chien ne représentait aucun danger, simplement un « pouf » précieux dans la vie de Mental, car le conflit entre les têtes interférait avec son objectif principal : la douleur et la colère. Par conséquent, le créateur rusé a choisi de supprimer complètement le cerveau de toutes les têtes.

And to ensure the little beast wouldn't go to waste, it was instinctively instilled with a simple task: Les chiens Fido ne sont en réalité rien de plus que des animaux domestiques qui attaqueront ceux qui menacent leurs propriétaires. Les forces mentales sont autorisées à amener leurs chiens au combat, un peu comme la façon dont les humains promènent leurs chiens, mais avec plus d'armes et de sang.

Cerbère, tel un petit taureau, enfonce l'ennemi. Et lorsque l'ennemi, repoussé par le chien, reprend ses esprits, les trois têtes de la créature tentent de lui arracher un morceau savoureux.

Il est nettement plus dangereux que n'importe quel chien sur Terre et sa morsure est bien plus douloureuse. Que peut-on attendre d'autre d'un chien à trois têtes dont la tête est en feu ?

Cependant, comme la plupart des serviteurs de Mental, il est aussi stupide qu'un tas de briques. Tuez-les à distance ou attirez-les et tirez-lui soudainement une balle dans le visage avec un fusil de chasse à double canon.

TIPS: 

- Évitez ses attaques en esquivant sur le côté ou en sautant.
- Utilisez le fusil de chasse à double canon à bout portant pour éliminer de petits groupes.
- Utilisez une arme à tir rapide pour éliminer des groupes plus importants à distance.
- Les explosifs sont très efficaces contre lui, à condition qu'il ne soit pas trop proche.
