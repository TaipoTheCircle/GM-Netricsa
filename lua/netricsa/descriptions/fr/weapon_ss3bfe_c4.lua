C-4 EXPLOSIFS


WEAPON: C-4 Explosifs
AMMO: Frais C-4
ROF: Détonation manuelle
WEIGHT: 1,1 livres

DESCRIPTION: 

Explosif plastique de qualité militaire. Extrêmement stable et fiable. N'explosera pas suite à un impact ou à un incendie, nécessitant un détonateur pour l'initiation.
Possède un énorme pouvoir destructeur. Peut être moulé dans n'importe quelle forme pour un placement efficace sur des véhicules, des structures ou des groupes ennemis.

NOTES: 

- Idéal pour tendre des embuscades, éliminer des cibles lourdes et créer des chemins à travers les débris.
- La détonation contrôlée permet des dommages précis et minimise les risques pour l'utilisateur.
