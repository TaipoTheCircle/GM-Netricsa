Engloutit LEARNOSAURE


CLASS: Gobe ​​Learnosaure
METABOLISM: Dinosaure, Learnosaure
PERCEPTION: Membre de l'équipe "Support"
SIZE: 5,6 pieds
ENDURANCE: Moyenne
HOSTILITY: Aucun
WEAPONS: Cravate, connaissance, courage entraîné
REWARD: 120 FC
THREAT: Haut

DESCRIPTION: 

Il s'agit d'un dinosaure violet avec des yeux roses, un ventre blanc et de rares plaques roses sur le dos. Il porte une cravate bleue avec un « G » blanc au milieu. Il provient du jeu éducatif préscolaire "Gobbles & the Learnosaurs", dont il était le personnage principal.

Dans son monde natal, Gobbles et son ami Stegosaurus se sont aventurés dans la grotte du méchant Cromugn pour récupérer les consonnes volées. Ils réussirent, mais la victoire du héros entraîna la destruction du monde. Cromugn a été dévoré par une colonne de feu, Stegosaurus a été écrasé (coupé en deux), et Gobbles a été sauvé au dernier moment par Kit et Kaboodle, qui l'ont recruté dans l'équipe "Support".

Gobbles est un dinosaure nerveux, timide et non préparé qui panique constamment. Kaboodle le considère comme un fardeau et suggère de le laisser à son sort, mais Kit est gentil avec lui, lui offrant du jus et le rassurant constamment. Gobbles essaie de prouver son utilité, mais c'est difficile. Pourtant, derrière cette façade lâche se cache une personnalité sensible et capable de courage.

Gobbles est un dinosaure prêt à tout apprendre sous le soleil. Il a appris à Flappers à lire et à écrire, et il a lui-même appris le courage du monde qui l'entourait et de ses héros.

TIPS: 

- Gobbles n'est pas un combattant. Sa force réside dans sa capacité à remarquer les détails et à prendre soin de ses amis.
