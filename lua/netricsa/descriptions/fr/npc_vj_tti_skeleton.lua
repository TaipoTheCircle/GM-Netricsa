DESCRIPTION: 

"Painty", également connu simplement sous le nom de BurgerBeard, est un mort-vivant maudit de l'atoll de Bikini. Dans la vie, il était le fils d'un pirate sévère qui rêvait de faire de lui un véritable voleur de mer. Cependant, BurgerBeard lui-même ne rêvait pas d'or et de pension, mais de créer le burger parfait.

Ses rêves ont fait de lui la risée de l'école des pirates. Le ridicule et l'humiliation l'ont conduit à la folie. Dans une tentative de changer son destin, Burger Beard a volé un livre magique au gardien squelettique de l'atoll de Bikini, dans l'intention de réécrire l'histoire de Bikini Bottom et d'obtenir la formule secrète du Krabby Patty.

Ayant réalisé son désir, il s'est dirigé vers la terre ferme, où il a commencé à vendre des Krabby Patties depuis une Burgermobile. Cependant, son plan a été ruiné après une longue bataille avec Bob l'éponge et ses amis, qui avaient pris l'apparence de super-héros. Plankton a finalement renvoyé Burger Beard sur l'atoll de Bikini, où il a été maudit.

Même mort, Burger Beard n’a pas trouvé la paix. Son squelette chante encore "qui vit dans un ananas sous la mer ?" au début de chaque journée, incapable de m'arrêter. Il porte un pantalon rouge en lambeaux et un bandana de pirate rouge, les derniers vestiges de son rêve non réalisé de devenir plus qu'un simple bouffon de pirate.

TIPS: 

- Détruire le squelette arrête le chant.
- Certaines créatures dansent sur son chant.
