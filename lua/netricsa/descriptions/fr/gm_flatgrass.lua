FLAT GRASS


Flat Grass is one of the oldest and most iconic locations. It lives up to its name: un vaste champ d'herbe plat entouré d'un ciel bleu sans fin.

Il n'y a aucune anomalie. Flat Grass est utilisé pour fabriquer des appareils, tester des armes, contrôler des véhicules ou simplement faire des ravages sans limites.

Key Features:

A sense of infinity: une vaste surface plane, parfaite pour la construction à grande échelle et les tests physiques.

Minimalist architecture: un seul bâtiment apparaît, avec une pièce cachée et un toit pour l'observation.