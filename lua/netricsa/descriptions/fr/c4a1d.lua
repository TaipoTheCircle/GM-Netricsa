XEN, CAPSULES D'INFANTERIE


Des rangées et des rangées de biocapsules s'étendent, chacune berçant des troupes à moitié formées, certaines endormies, d'autres tremblantes de vie. Les murs bourdonnent d’énergie, l’air est chargé de l’odeur de la boue génétique et de l’ozone.

Chaque grognement, chaque soldat, chaque lame est né ici.
