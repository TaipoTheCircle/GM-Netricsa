COMBINEZ LE SOLDAT SNIPER D'OVERWATCH


CLASS: Combinez Overwatch Sniper Soldier
METABOLISM: Humain augmenté, mâle
PERCEPTION: Infanterie transhumaine (spécialiste à longue portée)
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Extrême
WEAPONS: Combinez le fusil de sniper MK2
REWARD: 500FC
THREAT: Haut-Extrême

DESCRIPTION: 

Le Combine Overwatch Sniper Soldier est une unité transhumaine spécialisée conçue pour l’élimination et la suppression à longue portée. Contrairement aux autres unités d'infanterie du Combine, leur apparence est plus irrégulière et atténuée : leurs visières n'ont pas les optiques lumineuses emblématiques pour éviter de révéler leur position. Cela leur permet de se fondre dans les ruines, les toits ou les tours de guet des prisons, ce qui les rend difficiles à détecter jusqu'à ce qu'il soit trop tard.

Ils sont maîtres en patience et en précision, occupant généralement des positions de surveillance pendant des heures tout en suivant les mouvements de l'ennemi. Leurs fusils de sniper peuvent éliminer des cibles d’un seul coup, semant la panique dans les escouades et forçant les survivants à se mettre à couvert.

Les soldats tireurs d'élite sont souvent déployés pour protéger les ressources clés du Combine, garder les extérieurs des prisons comme Nova Prospekt et créer des zones de mise à mort dans les rues de la ville. Silencieux, mortel et presque invisible jusqu'à ce qu'ils tirent.

TIPS: 

- Évitez de rester à l'air libre - déplacez-vous toujours entre les abris.
- Des grenades fumigènes ou des explosifs peuvent les débusquer.
