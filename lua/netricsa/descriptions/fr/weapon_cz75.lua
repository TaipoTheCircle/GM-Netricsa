PISTOLET À CHARGEMENT AUTOMATIQUE CZ 75


WEAPON: 75 CZK
AMMUNITION: Cartouches 9x19
ROF: ~20 tours/min

DESCRIPTION: 

Pistolet à chargement automatique développé en Tchécoslovaquie en 1975. Le pistolet a été conçu par les frères Josef et Frantisek Koucky dans la ville d'Uhersky Brod. Initialement, la conception était destinée à l'exportation et se positionnait comme un remplacement moderne des pistolets "lourds" de la police et de l'armée chambrés pour la cartouche 9×19 Parabellum.

NOTES: 

- Facile à transporter.
- Cette version du pistolet tire des coups simples.







