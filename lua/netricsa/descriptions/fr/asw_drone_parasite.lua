DRONE PORTEUR D'ESSAI


CLASSE : Drone porteur d'essaim
MÉTABOLISME : Xénobiologique
PERCEPTION : spécimen d'assaut
TAILLE : 6,5 pieds
ENDURANCE : Moyenne
HOSTILITÉ : Très élevée
ARMES : Griffes, Parasite Symbiote
RÉCOMPENSE : 120 FC
MENACE : Élevée

DESCRIPTIF :

Le Swarm Carrier Drone est une variante rouge rare du drone commun, qui se distingue par la présence d'un parasite adulte fermement attaché au haut de son corps. Contrairement à la plupart des cas de parasitisme connus, les deux organismes ne se nuisent pas, agissant comme une seule créature de combat.

Pendant le combat, le Swarm Carrier se comporte comme un drone ordinaire, réduisant rapidement la distance et attaquant sa proie avec de longues griffes en forme de faucille. Le parasite reste immobile, attendant la mort de son hôte.

Après la mort du drone, le parasite se détache immédiatement du corps et attaque la créature vivante la plus proche, tentant de terminer son cycle de vie de la manière typique de son espèce. Ainsi, la destruction de l’hôte à courte distance n’est souvent que le début d’une nouvelle menace.

L'origine de cette symbiose reste inconnue. Certains xénobiologistes considèrent qu'il s'agit d'un stade de développement naturel des faux-bourdons, tandis que d'autres pensent que les parasites utilisent les abeilles adultes uniquement comme moyen de transport protégé vers des proies potentielles. Quoi qu’il en soit, cette coopération augmente considérablement le taux de survie des deux organismes.

CONSEILS :

- Ne vous approchez pas de l'hôte après sa mort.
- Après avoir tué le drone, éliminez immédiatement le parasite séparé.
- Les armes automatiques vous permettent d'éliminer rapidement les deux menaces.
- Si possible, détruisez l'hôte dans une zone dégagée pour qu'il soit plus difficile pour le parasite de se rapprocher.