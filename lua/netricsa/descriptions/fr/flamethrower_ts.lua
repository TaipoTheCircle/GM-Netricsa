UN LANCE-FLAMMES FABRIQUÉ MAISON


WEAPON: Lance-flammes fait maison
AMMO: Essence
ROF: Feu continu
WEIGHT: 9 kg (vide),
14 kg (plein)

DESCRIPTION: 

Un lance-flammes fait maison. Créé en combinant une pompe à vélo, un bidon d'essence et des fusées éclairantes. Il consomme rapidement des munitions, mais s'avère très utile contre les monstres vulnérables au feu. Cette arme expérimentale utilise de l'essence pour créer une puissante flamme. Le lance-flammes est très efficace contre de grandes concentrations de troupes ennemies (vivantes et non vivantes), avec un jet de flammes atteignant même ceux qui se cachent. Le fait qu’ils continuent à brûler pendant quelques secondes avant de mourir ne fait qu’en renforcer l’effet. Certains appellent ce type d'arme « posez-la et oubliez-la ».

NOTES: 

- Efficace contre de grandes concentrations de troupes ennemies
