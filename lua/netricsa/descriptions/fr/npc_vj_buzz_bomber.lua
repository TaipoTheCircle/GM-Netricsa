BUZZ-BOMBER DU MOBIUS


CLASS: Buzz-Bombardier
METABOLISM: Animal-Ion-Carburant
PERCEPTION: Bombardier tactique
SIZE: 4 pieds
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Détonateur ferroviaire, coque
REWARD: 400FC
THREAT: Moyen

DESCRIPTION: 

Lorsque le Dr Eggman a décidé de conquérir le ciel, il a passé beaucoup de temps à choisir un prototype. Les guêpes ordinaires étaient trop vicieuses, les libellules trop rapides et les moustiques trop petits. En fin de compte, le choix s'est porté sur la Shrew Wasp. Ainsi, le Buzz-Bomber est né.

Après le tir, il s'envole, pour revenir d'une direction différente après un court délai.

TIPS: 

- Attaquez en survolant. Pendant qu'il pose l'aiguillon.
