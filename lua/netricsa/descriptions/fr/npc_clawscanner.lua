SCANNER DE COMBAT À GRIFFES COMBINÉES


CLASS: Combinez le scanner de griffes
METABOLISM: Synthé
PERCEPTION: Synthé de reconnaissance/combat, scanner de type II
SIZE: ~2,5 pieds de diamètre
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Déploiement de la mine Hopper, caméra flash aveuglante
REWARD: 450 FC
THREAT: Haut

DESCRIPTION: 

Le Combine Claw Scanner, également connu sous le nom de Shield Scanner ou Scanner Type II, est une sinistre variante prête au combat du City Scanner standard. Équipé d'un bras de tenaille caché, il peut déployer des mines à trémie mortelles dans des couloirs étroits, des ruelles et d'autres zones stratégiques pour bloquer ou tendre une embuscade aux forces de résistance.

Comme son homologue civil, il peut émettre un flash aveuglant grâce à son système de caméra, utilisé pour désorienter les cibles ou les forcer à sortir de leur abri. Ces drones soutiennent les Striders et les unités terrestres en marquant les cibles et en restreignant les mouvements des civils, donnant ainsi au Combine un œil implacable dans le ciel.

TIPS: 

- Le flash du scanner peut devenir incapacitant – évitez de regarder directement lorsqu'il se charge.
- Des tirs intenses ou des explosifs le désactiveront et empêcheront de nouveaux largages de mines.
- Ces unités sont souvent vues en groupe ou à proximité des positions défensives des Combines.
