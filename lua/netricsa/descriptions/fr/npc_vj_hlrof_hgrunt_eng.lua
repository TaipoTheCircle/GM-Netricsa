H.E.C.U. INGÉNIEUR DE COMBAT


CLASS: H.E.C.U. Ingénieur de combat
METABOLISM: Humain, mâle
PERCEPTION: Spécialiste en ingénierie
SIZE: ~6 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Pistolet, couteau de combat, explosifs déployables, chalumeau
REWARD: 600 FC
THREAT: Modéré individuellement, élevé dans les situations tactiques

DESCRIPTION: 

Il s'agit de spécialistes entraînés au combat qui manipulent activement l'environnement pour contrôler l'accès et piéger les intrus. À l'aide de chalumeaux de soudage et d'explosifs, ils peuvent franchir des portes verrouillées pour entrer dans des zones restreintes ou des sorties SEA, forçant ainsi les ennemis à pénétrer dans des espaces confinés.

Bien qu'ils ne soient pas des combattants de première ligne, ils se défendent de manière agressive avec des pistolets et des couteaux, et leur capacité à manipuler les portes les rend étonnamment dangereux dans les couloirs et les installations fermées. Ils opèrent souvent en paires ou en petits groupes, sécurisant les points d'étranglement pendant que le reste de l'équipe avance.

TIPS: 

- Empêchez les ingénieurs d'atteindre les portes - ils pourraient vous piéger ou vous prendre à revers.
- Des explosifs ou des attaques à distance peuvent les neutraliser avant qu'ils aient fini de souder.
- Soyez conscient de l'environnement : une fois qu'une porte est scellée, les voies d'évacuation sont limitées.
