ROUTE, CHEMIN VERS LA CLÔTURE


Nous l'avons trouvé. Un lieu où le minimalisme est élevé à l'absolu, et où le mot « design » a tout simplement été effacé du dictionnaire.

Devant nous se trouve une ROUTE. Juste une route. Il s'étend tout droit, disparaissant dans l'horizon brumeux et ne montrant aucun signe de virage nulle part.

De chaque côté se trouve une GRANDE CLÔTURE. Grand, gris, infranchissable.

Au début se trouve une clôture. Au bout se trouve une clôture. Sur les côtés se trouve une clôture. Au dessus, c'est le ciel. Ci-dessous se trouve l'asphalte. Et toi.

Rien. Absolument rien. Pas de panneaux, pas de maisons, pas d'arbres, pas même une flaque d'eau égarée. Juste vous, la route, les clôtures et la réalisation que vous êtes enfermé dans ce couloir POUR TOUJOURS.

Si vous êtes déjà là, suivez la route. Lentement. Comptez vos pas. Puis faites demi-tour et revenez. Là encore. Et encore. Peut-être qu'après mille passages, la clôture paraîtra un peu plus proche ?
