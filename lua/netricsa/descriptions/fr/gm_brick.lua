BRIQUE, TOUR DE BRIQUE DE L'ENFER


Sam, nous l'avons trouvé. L'endroit où l'architecte a finalement cessé d'être timide et s'est contenté d'écraser des briques les unes sur les autres.

Alors que les versions précédentes concernaient l'expansion horizontale (maisons, garages, piscines), ici tout est devenu vertical. Sérieusement.

Tout cela est mélangé dans une structure verticale. Ce n'est pas une maison, ni une tour, mais plutôt un labyrinthe de briques. Murs, plates-formes, passerelles, tous en brique. Et des escaliers partout. Beaucoup d'escaliers.

Main Mechanics: 

There's one key feature here that turns the map into a philosophical experiment: 

À un moment donné, vous déciderez que vous en avez assez vu et sauterez.

Et là-bas, vous êtes accueilli par... les mêmes escaliers.

You land and realize: les escaliers que vous venez de monter sont juste devant vous. Vous pouvez recommencer à grimper. Et sautez à nouveau. Et nous retrouver à nouveau à ses pieds.

Just let's make a deal: si dans une heure nous nous retrouvons au pied de ces escaliers, je commencerai à soupçonner que nous sommes en enfer. Brique, enfer gris-rouge-jaune.
