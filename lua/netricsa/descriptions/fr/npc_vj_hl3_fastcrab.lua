TÊTE RAPIDE ARCTIQUE DE XEN


CLASS: Crabe rapide (Arctique)
METABOLISM: Parasite résistant au grand froid
PERCEPTION: Arme biologique
SIZE: 1,3 pi
ENDURANCE: Très faible
HOSTILITY: Haut
WEAPONS: Sauts ultra-rapides, morsure tranchante
REWARD: 180 FC
THREAT: Moyen

DESCRIPTION: 

La variété arctique du ZEN Fast Headcrab est une évolution encore plus alarmante d’un parasite déjà agressif. Ayant été introduite dans les biomes glacials de la Terre, cette espèce a développé un pelage court et glacé qui protège ses muscles de l'hypothermie.

Son corps reste mince et flexible, et ses membres sont développés pour des sauts rapides et répétés, même sur la glace et la neige. Grâce à sa musculature modifiée, le crabe arctique est capable de repousser les surfaces glissantes et de changer de direction en plein vol, ce qui rend ses attaques particulièrement difficiles à prévoir.

Lorsqu'il infecte un hôte, il crée un zombie Arctic Swift, plus résistant au froid et moins sensible à la douleur que son homologue modéré. Ces zombies sont capables de traverser rapidement des zones enneigées et d'escalader des structures glacées là où des infectés normaux perdraient leur adhérence.

TIPS: 

- Don't stand still: leur vitesse est conçue pour une cible stationnaire.
- Shoot ahead: ils changent souvent leur trajectoire de saut.
