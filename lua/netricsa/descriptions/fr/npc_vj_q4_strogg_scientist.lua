SCIENTIFIQUE STROGG DE STROGGOS


CLASS: Scientifique Strogg
METABOLISM: Hybride Cybernétique / Humain-Strogg
PERCEPTION: Personnel scientifique de Strogg
SIZE: ~ 5,5 pieds (hauteur du survol)
ENDURANCE: Moyen
HOSTILITY: Moyen (combat défensif / réticent)
WEAPONS: Bras de scie mécanique, aiguilles sur les membres supplémentaires, grenade à gaz toxique
REWARD: 700FC
THREAT: Moyen

DESCRIPTION: 

Le Scientifique est une unité Strogg chargée de la recherche médicale et du processus de Stroggification. Le bas de son corps est remplacé par un survol, lui permettant de flotter silencieusement dans les laboratoires médicaux. Le torse est humain, mais avec un respirateur sur la bouche et des yeux orange vif. Autour de son corps sont greffés des outils tranchants - une scie pour une main droite, des membres mécaniques tenant des aiguilles - utilisés à l'origine pour l'expérimentation, mais mis en service lorsqu'ils sont attaqués.

Une fois acculé, le scientifique lancera une grenade à gaz toxique, qui explosera et libérera un nuage toxique qui endommagera au fil du temps et contournera l'armure. Ses attaques au corps à corps avec des scies et des aiguilles sont brutales à courte portée. Il se faufile souvent dans les coins ou flotte au-dessus pour surprendre les intrus dans les installations médicales de Strogg.


TIPS: 

- Gardez vos distances - ses outils de mêlée sont mortels en gros plan.
- Évitez les nuages ​​​​de poison ; ils désorientent et contournent l'armure.
- Utilisez des armes à distance/automatiques pour l'envoyer rapidement.
- Soyez attentif aux mouvements planants ; le hover-pad rend plus difficile la détection sonore.
