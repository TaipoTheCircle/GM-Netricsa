SNARK DE XEN


CLASS: Snark
METABOLISM: Insectoïde carnivore (origine Xen)
PERCEPTION: Insecte extraterrestre
SIZE: 6 po
ENDURANCE: extrêmement faible
HOSTILITY: absurdement élevé
WEAPONS: morsure, tactiques d'essaim, autodestruction (par accident)
REWARD: 50 FC
THREAT: moyen (en groupe)

DESCRIPTION: 

Le Snark est essentiellement un cafard qui a bu trop de café et a décidé que la violence était un mode de vie. Originaires de Xen, ces minuscules créatures sont libérées des œufs comme des grenades organiques. Une fois libérés, ils se précipitent immédiatement vers la forme de vie la plus proche, mordant furieusement sans aucun sentiment d’auto-préservation.

Malheureusement pour eux (mais heureusement pour vous), ils ont également une durée de vie hilarante : s'ils ne vous tuent pas rapidement, ils explosent simplement dans une bouffée d'enthousiasme sanglant. Les résistants plaisantent souvent en disant que les Snarks sont des « kamikazes atteints de TDAH ».

TIPS: 

- Ne pas paniquer. One Snark n’est qu’un soulagement comique.
- Une douzaine de Snarks ? La comédie se transforme en horreur.

PRO TIP: 

- Jetez-les dans une foule d'ennemis pour un maximum de chaos.
