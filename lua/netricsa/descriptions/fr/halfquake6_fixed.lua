SHOW DEMI-QUAKE, SAUTER ET COURIR


Les sadiques semblent avoir décidé de changer de genre.

Maintenant c'est le parkour. Dans le noir.

Les pièces sont plongées dans une pénombre, celle où les murs apparaissent au moment où l'on tombe déjà vers eux. Le sol est criblé de fosses de lave. Non marqué. Non éclairé. Tout simplement parce que les surprises donnent vie au spectacle. Ou vice versa.

Il ne s'agit pas de tirer. Il s’agit de mouvement. Sautez, courez, arrêtez-vous brusquement, sautez à nouveau, accroupissez-vous. La lave en dessous est patiente et sans hâte, sachant que tôt ou tard, quelqu'un trébuchera.

Pour ouvrir la voie, vous devez trouver deux boutons pour deux générateurs. Ils sont cachés dans différentes parties de la pièce principale. Lorsque les deux générateurs sont activés, la voie à suivre s’ouvrira.

Et oui, conformément à la bonne vieille tradition, il y a des grenades cachées derrière l'un des murs. L'émission appelle cela « une récompense pour la curiosité ».

Alors saute, Sam.

Courir. Saut. Danse.

Surveillez vos pas, et un peu plus loin que vous ne le pensez.

Bonne chance.
