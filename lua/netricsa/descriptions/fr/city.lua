VILLE, LA DERNIÈRE CARTE DE ZEROZEROZERO


La carte city.bsp est considérée comme une continuation directe de xmasxmas.bsp et fuck.bsp et comme l'une des dernières œuvres connues du mystérieux mappeur zerozerozero, disparu d'Internet en 2013.

The file was found in an encrypted archive, the password to which turned out to be simple: "Noël". Dès l'ouverture, le joueur se voit présenter une ville vide.

Pas de fenêtres. Pas de portes. Pas de vie. Les bâtiments s'étendent vers le ciel gris sans fin, comme le paysage inachevé du rêve de quelqu'un d'autre.

Quelque chose, une forme étrange, habite le centre de la ville. Le monde semble tourner autour de lui. Si vous restez là trop longtemps, alors...
