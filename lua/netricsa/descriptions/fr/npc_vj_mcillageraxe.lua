VINDICATEUR DU MONDE MINECRAFT


CLASS: Indicateur de redressement
METABOLISM: Villageois malveillant, bandit
PERCEPTION: Combattant de mêlée vicieux, infanterie lourde des Illagers
SIZE: 6,2 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: - La meilleure défense contre les crocs est de tuer l'Evoker avant qu'il ne puisse les invoquer.
REWARD: 200FC
THREAT: Très élevé

DESCRIPTION: 

Ce psychopathe en robe grise prouve que les villageois peuvent être non seulement utiles mais aussi mortels. Ce sont des « méchants villageois » ou, comme on les appelle officiellement, des Illagers. Tandis qu'un villageois normal commerce avec vous et construit des fermes, un redresseur de torts veut simplement vous couper en deux avec une hache. Et il ne se soucie pas que vous soyez simplement en visite.

Ces personnages sombres sont apparus pour la première fois comme gardes des manoirs forestiers, où ils hantent encore certaines pièces. Plus tard, ils ont été repérés lors de patrouilles de bandits et, ce qui est le plus inquiétant, lors d'attaques dans des villages.

Le Vindicator porte une veste sombre, un pantalon et des bottes. Il a une expression sérieuse et colérique, qui rappelle sa parenté avec les villageois. Lorsqu'il est inactif, il se tient debout, les bras croisés sur la poitrine, tout comme son cousin épris de paix. Mais dès qu'il repère un ennemi (vous ou un villageois), il dégaine sa hache de fer et charge.

La principale caractéristique du Vindicator est une agressivité et une vitesse incroyables. Il court vers sa cible plus vite que la vitesse de marche du joueur, délivrant des coups dévastateurs avec sa hache. Cette hache n'est pas seulement dangereuse : elle peut pénétrer les boucliers et les désactiver pendant 5 secondes.

Contrairement à leurs parents Parjures, les Vindicators peuvent briser les portes en bois lors de raids en difficulté normale et difficile (comme les zombies, mais plus rapidement).

TIPS: 

- Si vous n'êtes pas blindé, ne vous approchez pas. Un coup de hache peut être mortel.
- Utilisez un arc ou une arbalète. Garder vos distances contre un Vindicator est la meilleure stratégie.
- Don't rely on your shield: son attaque le pénétrera et vous subirez des dégâts.
- Dans le Woodland Mansion, surveillez les coins. Les redresseurs de torts sont souvent en embuscade dans de petites pièces.
- Si vous voyez un Vindicator nommé Johnny, courez immédiatement dans la direction opposée. Il détruira non seulement vous, mais aussi tous les autres monstres sur son passage.
