CHAR ANNIHILATEUR DU CYBERSTAN


CLASSE : Char annihilateur
MÉTABOLISME : Mécanique
PERCEPTION : Armure Lourde, Destructeur
TAILLE : 13 pieds
ENDURANCE : Extrême
HOSTILITÉ : Absolue
ARMES : Canon de combat à pression terminale, cycleur de fusion coaxial, répéteur de fusion lourd monté sur la coque
RÉCOMPENSE : 3000 FC
MENACE : Absolue

DESCRIPTIF :

Il s'agit d'une forteresse en acier sur rails, que les Automates ont copiée sans vergogne à partir des modèles de la Super-Terre, mais, comme d'habitude, ils l'ont aggravée. Le char est un véhicule de combat massif, lourdement armé, capable de détruire une escouade entière de Helldivers en quelques secondes. Vu de face, il paraît quasiment invulnérable, mais comme tout véhicule, il présente des points faibles.

Le char est armé de trois types d'armes :

Le canon de combat à pression terminale est l'arme principale. Ce puissant canon est monté sur la tourelle et tire une fois toutes les 6 secondes. Il clignote en rouge avant de tirer. Un coup direct tue instantanément un Helldiver.

Le Coaxial Fusion Cycler est une mitrailleuse jumelée avec une cadence de tir élevée (1 200 tr/min). Il tire par rafales de 2,7 secondes, suivies d'un temps de recharge de 1,5 seconde.

Le Hull-Mounted Heavy Fusion Repeater est une mitrailleuse à tir vers l'avant intégrée à la coque. Il tire plus lentement (400 tr/min) et a un angle de tir limité.

Le Char Annihilator possède plusieurs points faibles qui peuvent être exploités pour une mise à mort rapide :

Le moteur arrière, le radiateur de la tourelle arrière et les chenilles. (Détruire une chenille immobilise le char mais ne le détruit pas.) Il est également vulnérable au feu.

CONSEILS :

- Lorsque vous attaquez un char, approchez-vous toujours par l'arrière ou par le côté. Il est quasiment invulnérable de face.
- Détruire les chenilles immobilise le char mais ne le détruit pas. Utilisez-le si vous ne pouvez pas le tuer directement.
- Avant que le canon principal ne tire, il clignote en rouge. Dès que vous voyez le flash, mettez-vous immédiatement à couvert ou plongez au sol.
- Le char a une vitesse de rotation de tourelle lente. Utilisez-le pour vous placer derrière le feu ennemi.
- Un char détruit peut être utilisé comme couverture.
- Une fois détruit, le char explose dans un rayon de 8 mètres. Éloignez-vous.
- N'essayez pas de détruire un char avec un fusil standard. Vous gaspillerez simplement vos munitions.