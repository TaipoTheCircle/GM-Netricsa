ZOMBIE CRABE, TERRE


CLASS: TêteCrab Zombie, Terre
METABOLISM: hôte parasite
PERCEPTION: Entité zombifiée
SIZE: 6-7 pieds
ENDURANCE: moyen
HOSTILITY: haut
WEAPONS: griffes, morsure, saut de crabe
REWARD: 200FC
THREAT: moyen

DESCRIPTION: 

Ces malheureuses créatures étaient autrefois des humains, jusqu'à ce qu'un crabe s'accroche à leur crâne et prenne le contrôle total de leur système nerveux. Leurs corps tordus avancent en titubant avec des mouvements saccadés et contre nature, motivés uniquement par la faim du parasite. L'esprit de l'hôte est encore vaguement conscient, obligé de regarder, impuissant, le crabe utiliser son corps comme une marionnette. Leurs gémissements et cris déformés sont les derniers vestiges de l’humain emprisonné à l’intérieur.

TIPS: 

- Visez le crabe pour neutraliser rapidement l'hôte.
- Gardez vos distances : ils se jettent sans prévenir.
