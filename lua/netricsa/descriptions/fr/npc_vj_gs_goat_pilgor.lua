PILGOR LA CHÈVRE DE GOATVILLE


CLASS: Pilgor la chèvre
METABOLISM: Biologie caprine standard
PERCEPTION: Mammifère ongulé
SIZE: ~2,5-3 pieds aux épaules
ENDURANCE: Normal, mais étrangement durable
HOSTILITY: Moyen
WEAPONS: Coup de tête, physique du ragdoll, destruction aléatoire
REWARD: 100 FC
THREAT: Moyen

DESCRIPTION: 

Le Pilgor ressemble peut-être à une simple chèvre de ferme, mais les apparences sont trompeuses. C’est sans aucun doute la chèvre la plus téméraire qui existe.

Contrairement à ses variantes « normales », le Pilgor est l'original : le générateur de chaos qui a tout déclenché. Chaque moment de bug, de problème et de ragdoll semble amplifié lorsque cette chèvre est impliquée.

TIPS: 

- Ne le laissez jamais seul à proximité d'objets fragiles.
- Les coups de tête peuvent envoyer des objets (et des personnes) voler comme des missiles.
- Cette chèvre est la physique elle-même : sauvage, brisée et imparable.
