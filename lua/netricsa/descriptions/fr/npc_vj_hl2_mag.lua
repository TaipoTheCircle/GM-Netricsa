DR. ARNE MAGNUSSON


CLASS: Dr Arne Magnusson
METABOLISM: Humain, mâle
PERCEPTION: Scientifique humain / Commandant de la Résistance
SIZE: ~6 pieds
ENDURANCE: Moyen (plus âgé, mais dur d'esprit)
HOSTILITY: Faible (pour les alliés), Élevé (pour combiner)
WEAPONS: Appareil Magnusson, langue pointue
REWARD: 1000FC
THREAT: Moyen (directement), Élevé (en tant que stratège et inventeur)

DESCRIPTION: 

Le Dr Arne Magnusson est l'un des hauts dirigeants de la Résistance, supervisant les opérations depuis la base de la Forêt Blanche. Bourru, impatient et souvent critique, il tolère peu les erreurs, mais sous son extérieur abrasif se cache un dévouement inébranlable à la survie de l'humanité.

Vétéran de Black Mesa, Magnusson est un survivant de la Cascade de résonance et porte à la fois les cicatrices et la connaissance de ce désastre. Son génie en ingénierie a donné naissance au dispositif Magnusson, une arme explosive expérimentale conçue spécifiquement pour détruire les Combine Striders. Malgré sa nature non conventionnelle, l'appareil s'est avéré être l'un des plus grands outils de la Résistance pour renverser le cours de la bataille.

Bien qu’il ne soit pas un soldat de première ligne, Magnusson suscite le respect et la peur dans une mesure égale dans les rangs de la Résistance. Son génie n'a d'égal que son caractère colérique, et pourtant, dans les moments critiques, son leadership est indispensable.

TIPS: 

- Ses appareils Magnusson sont la clé pour détruire les Striders : utilisez-les à bon escient.
- Son aboiement est pire que sa morsure. Ignorez les cris, suivez le plan.
- Gardez-le en vie : sa connaissance du Cartel est irremplaçable.
- Préparez-vous à des critiques constantes concernant le micro-ondes à Black Mesa.
