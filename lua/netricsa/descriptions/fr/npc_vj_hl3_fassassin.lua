COMBINEZ L'ASSASSIN FÉMININ D'OVERWATCH


CLASS: Combiner Overwatch Femme Assassin
METABOLISM: Augmentations humaines, féminines / biomécaniques améliorées
PERCEPTION: Agent transhumain d'élite, Assassin
HEIGHT: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Pistolets, Grenades
REWARD: 800 FC
THREAT: Très élevé

DESCRIPTION: 

La Female Combine Assassin est un agent transhumain spécialisé conçu pour éliminer des cibles clés, supprimer la résistance et mener des opérations secrètes derrière les lignes ennemies. Sa formation acrobatique et neurochimique fait d'elle une chasseuse, une espionne et une force de frappe à la fois.

Les origines de l'assassin sont liées aux programmes d'Overwatch visant à créer des agents capables d'opérer indépendamment des unités principales. Comme les autres combattants transhumains, elle a subi des modifications biomécaniques, mais sa spécialisation nécessite des adaptations accélérées du réseau neuronal, lui permettant d'anticiper les mouvements de l'ennemi, d'opérer dans un silence complet et de s'adapter aux conditions de combat changeantes.

Son équipement tactique est adapté aux attaques ultra-rapides et furtives.

TIPS: 

- Dans la plupart des cas, elle lance des Grenades avant d'attaquer.
- Ses pas sont silencieux, alors garde les yeux ouverts.
- Son œil rouge la trahit dans le noir.
