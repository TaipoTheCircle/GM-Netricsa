Contrairement au principal H.E.C.U. divisions, ces unités s'appuient rarement sur des embuscades ou des manœuvres avancées. Leur rôle principal est de pénétrer rapidement et de nettoyer les zones hostiles à tout prix, même si les pertes sont élevées. Pour cette raison, Alpha H.E.C.U. sont souvent considérées comme des forces remplaçables, envoyées en tant que sondes vivantes pour affaiblir la résistance avant l'arrivée des escouades principales.


CLASS: Agent de sécurité, Alpha
METABOLISM: Humain, mâle
PERCEPTION: Guerrier de sécurité Black Mesa
TAILLE ~6 pieds
ENDURANCE: moyen (gilet blindé léger)
HOSTILITY: neutre envers le personnel de Black Mesa, hostile aux créatures Xen et aux menaces extérieures
WEAPONS: pistolet de service, bâton.
REWARD: 150 FC
THREAT: modéré

DESCRIPTION: 

The Alpha Security Guards were the first wave of government forces deployed to Black Mesa following the Resonance Cascade. Unlike later H.E.C.U. units, their orders prioritized containment and survival, not eradication of witnesses. Upon arrival, they quickly realized the critical danger beneath the facility: une installation de missiles dissimulée, armée et instable. Toute erreur de calcul pourrait faire exploser l’ogive et anéantir entièrement Black Mesa.
Conscients de la gravité de la situation, les Alpha Guards ont mis de côté leurs divergences et ont travaillé côte à côte avec les scientifiques et le personnel de sécurité sur place. Ensemble, ils tentèrent de tenir tête aux créatures Xen et de maintenir le contrôle des secteurs vitaux de l'installation, dans l'espoir d'empêcher une destruction totale. Bien que légèrement équipés par rapport aux renforts ultérieurs, leur détermination à stabiliser la crise en a fait un élément essentiel de la fragile défense de Black Mesa.

