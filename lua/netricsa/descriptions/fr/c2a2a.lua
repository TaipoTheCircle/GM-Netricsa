BLACK MESA, DIRECTIVE MISSION FERROVIAIRE


Avant de monter à bord du train, l'un des gardes de sécurité survivants vous a transmis un message. Selon lui, un scientifique plus âgé – quelqu'un qui semble en savoir beaucoup plus qu'il ne le devrait – a insisté pour que Freeman atteigne les silos de missiles situés à l'extrémité de l'installation.

L'objectif ? Lancez une fusée satellite en orbite. Le scientifique pense que cela permettra au secteur Lambda de stabiliser la cascade de résonance, ou au moins d’affaiblir le lien dimensionnel qui inonde la base de formes de vie extraterrestres.

Now, whether he mistook you for this "Freeman" person, or simply doesn't care as long as someone gets the job done, doesn't matter. The mission is clear: 

Restez sur le rail jusqu'à ce que vous atteigniez le complexe de missiles.

Naviguez à travers n'importe quel défi que le H.E.C.U. et les forces extraterrestres mises sur votre chemin.

Trouvez un moyen de lancer la fusée.

Of course, the catch is obvious: si l'armée réalise ce que vous essayez de faire, elle fera tout ce qu'elle peut pour vous arrêter. Et à en juger par les mouvements croissants de troupes le long des voies ferrées, ils le savent déjà.
