TAILLEUR SIRIEN, SOURIS


CLASS: Taureau-garou sirien, souris
METABOLISM: mammifère
PERCEPTION: animal de travail
SIZE: 0,8 pied
ENDURANCE: faible
HOSTILITY: moyen
WEAPONS: cornes
REWARD: 2000 FC
THREAT: moyen

DESCRIPTION: 

Croisé avec des gènes mixtes de Siriens et d'une sorte de bétail. Le taureau-garou se comporte comme un taureau ordinaire et attaque avec ses cornes en chargeant.

Mental, tentant d'améliorer leur adaptabilité, a accidentellement ajouté des gènes de souris, entraînant une réduction spectaculaire de leur taille. Les hybrides miniatures se sont échappés des laboratoires et ont infesté les donjons de la Perse antique, s'adaptant à la vie dans les trous de souris. Conservant les habitudes de leurs ancêtres, ils attaquent depuis l'obscurité de leurs tunnels, dans le but de piéger leurs proies avec leurs cornes acérées mais microscopiques. Ils sont dangereux principalement en raison de leur surprise et de leur nombre.

TIPS: 

- Éloignez-vous de la ligne d'attaque.
- Si vous entendez un grondement dans le terrier, c'est bien lui.
