MESA NOIRE, CATACOMBES


Sam, you've made it out of the collapsing blocks and into the underground catacombs. Good news: il y a beaucoup de pierre et de béton entre vous et les bombes. Mauvaise nouvelle : les militaires savent déjà que vous êtes ici et ont décidé de redoubler d'efforts.

Un autre V-22 Osprey a été envoyé après que le dernier n'ait pas réussi à contenir la situation. Il tourne au-dessus du ciel. Si vous éternuez trop fort, vous recevrez une nouvelle pluie de plomb venant d'en haut.
