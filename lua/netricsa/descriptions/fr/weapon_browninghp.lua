PISTOLET À CHARGEMENT AUTOMATIQUE BROWNING HI-POWER


WEAPON: Browning haute puissance
AMMUNITION: Cartouches .40 SW
ROF: ~30 tours/min

DESCRIPTION: 

Un pistolet à chargement automatique conçu par John Browning et Dieudonné Saëver avec un chargeur à capacité étendue. Le nom reflète la grande capacité du chargeur - 13 coups. Le pistolet a été développé pour répondre aux spécifications techniques de l'armée française, mais n'a pas été adopté pour le service en France. Après la mort de Browning en 1926, le design fut affiné par Sev, designer en chef de la société belge Fabrique Nationale.

NOTES: 

- Bien que la description indique que le chargeur contient 13 cartouches, il en contient en réalité 10.
- Néanmoins, le pistolet est bon et puissant.



