FUSÉE CREEPER DU MINECRAFT OVERWORLD


CLASS: Fusée rampante
METABOLISM: Produits chimiques végétaux
PERCEPTION: Créature végétale à poudre à canon
SIZE: 5,5 pieds
ENDURANCE: Faible
HOSTILITY: Extrêmement élevé
WEAPONS: Auto-détonation de saut et de suivi
REWARD: 160 FC
THREAT: Moyen

DESCRIPTION: 

C’est la preuve que la nature adore expérimenter les techniques d’auto-détonation. Alors qu'une plante grimpante ordinaire s'approche et explose simplement, une plante grimpante de fête lance des explosifs, la meilleure façon d'infliger des dégâts est d'atterrir de manière spectaculaire, tête en bas, sur sa victime.

Le comportement du Rocket Creeper est unique. Comme une plante grimpante ordinaire, il poursuit sa proie. Mais lorsque la distance se rapproche, il ne siffle pas et ne s'enfuit pas. Au lieu de cela, il saute dans les airs puis tombe tête première, directement sur sa cible. L'explosion brise tout autour d'elle. L'explosion est aussi puissante que celle d'une plante grimpante ordinaire, suffisamment pour tuer une cible non blindée.

Si un Rocket Creeper est frappé par la foudre, il se charge. Dans ce mode, la puissance de l'explosion double, tout comme une plante grimpante chargée.

TIPS: 

- Peut se faufiler inaperçu.
- Le Rocket Creeper n'explose qu'à la fin de son saut. Esquiver au dernier moment peut éviter la plupart des dégâts.
- Ne reste pas immobile. Pendant qu'il vole, vous avez le temps de vous déplacer sur le côté.
- Un Rocket Creeper chargé est extrêmement dangereux. Son explosion peut même détruire des structures fortifiées. Si vous le voyez, il vaut mieux rester à l'écart.
- Comme toutes les plantes grimpantes, elle ne meurt pas à cause du soleil.
