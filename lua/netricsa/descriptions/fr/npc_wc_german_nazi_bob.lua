BOB, NAZI SOLDIER


CLASS: Bob, soldat nazi
METABOLISM: Humain, mâle
PERCEPTION: Infanterie de la Wehrmacht
SIZE: 6 pieds
ENDURANCE: Haut
HOSTILITY: Faible
WEAPONS:    MP-40
REWARD:     300 FC
THREAT: Moyen

DESCRIPTION:

Le Führer a donné le soldat Bob à Ghostface. Depuis ce jour, Bob n'est plus seulement un nazi : il est le compagnon et ami éternel de Ghostface. Loyal, discipliné et armé de son MP-40, Bob suit partout son nouveau maître, non par peur, mais par camaraderie indestructible.

TIPS:

- Bob agit rarement seul - il est toujours près de Ghostface.
- Son MP-40 est le plus dangereux à courte portée.
