SOLDAT REBELLE


CLASS: Soldat rebelle
METABOLISM: Les humains
PERCEPTION: Combattant de la Résistance Humaine
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Pistolet, SMG, fusil de chasse, grenades
REWARD: 200FC
THREAT: Moyen

DESCRIPTION: 

Les soldats rebelles sont des membres de la Résistance humaine, organisés pour combattre l'occupation du Combine. Ce sont des humains ordinaires entraînés rapidement au maniement des armes à feu et à la coordination des attaques, improvisant souvent des tactiques dans les zones de combat urbaines et rurales.

Ils opèrent en escouades, couvrent les civils, sécurisent les routes d'approvisionnement et fournissent un soutien essentiel aux principaux agents comme Gordon Freeman ou Alyx Vance. Bien que moins avancées que les forces combinées, leur adaptabilité, leur moral et leur nombre en font de redoutables adversaires.

Rebels rely on teamwork and hit-and-run tactics rather than brute force. Leur résilience vient de leur engagement en faveur de la survie de l'humanité plutôt que d'un armement supérieur, et on les voit souvent en coordination avec des véhicules tels que le char Merkava ou en train d'aider les scientifiques et les médecins sur le terrain.

TIPS: 

- Protégez-les : ils sont plus efficaces en groupe.
- Fournissez-leur des munitions et des kits médicaux pour maintenir la pression sur la ligne de front.
- Utilisez-les pour explorer les zones et tendre une embuscade aux patrouilles du Combine.
- Les escouades rebelles peuvent submerger même les unités combinées légèrement blindées si elles sont coordonnées.
