LAVE GNAAR MÂLE DE LA PLANÈTE DE LAVE


CLASSE : Lava Gnaar, mâle
MÉTABOLISME : Mammifère
PERCEPTION : Bête de guerre dressée
TAILLE : 4 pieds
ENDURANCE : Moyenne
HOSTILITÉ : Élevée
ARMES : Morsures, coups de poing
RÉCOMPENSE : 600 FC
MENACE : Faible

DESCRIPTIF :

Un petit animal bipède sans tête, avec un œil et une grande bouche. Originaire de Sirius et entraîné par Mental pour attaquer les forces adverses. Les mâles sont plus petits, de couleur rouge vif et moins coriaces que les femelles.

Les Gnaars de lave sont étroitement liés aux Gnaars ordinaires, mais au fil de milliers de générations, ils se sont pleinement adaptés à la vie sur une planète volcanique couverte de mers de lave, de roches brûlantes et d'éruptions constantes. Leur peau a acquis une teinte rouge vif et est devenue nettement plus épaisse, protégeant leur corps des températures extrêmement élevées.

Ils rôdent constamment dans les friches volcaniques à la recherche de proies et défendent farouchement leur territoire.

Malgré leur intelligence primitive, ils n'hésiteront pas à se jeter sur leurs adversaires, tentant de les déchirer avec leurs puissantes mâchoires et leurs longues griffes.

CONSEILS :

- Ils n'ont pas d'armes à distance, gardez simplement vos distances.
- Certains spécimens peuvent voler.
- Des Gnaars invisibles ont été signalés.