FANTÔME DU MONDE DE FATUM


CLASS: Le fantôme du monde de Fatum
METABOLISM: Soutenu par l'énergie de Fatum
PERCEPTION: Créature éthérée, gardienne des cimetières de Fatum
SIZE: 8,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Projectiles de lave, griffes de mêlée
REWARD: 300FC
THREAT: Moyenne

DESCRIPTION: 

Les fantômes du monde de Fatum sont des créatures vertes et translucides vêtues de longues robes rappelant celles des grands sorciers. Leur patrie, ce sont les cimetières de Fatum, des nécropoles déformées où la frontière entre la vie et la mort a depuis longtemps perdu son sens. Là, ils servaient de gardiens.

Ces créatures servent inconditionnellement Fatum. Et puisque Doom agit dans l'intérêt de Mental, les spectres sont formellement considérés comme faisant partie de sa Horde, bien qu'ils ne fassent eux-mêmes preuve ni d'individualité ni d'hésitation.

Au combat, les spectres utilisent des éclairs d'énergie en fusion qui ressemblent à de la lave. Ils lancent ces projectiles à moyenne portée et attaquent avec leurs griffes à courte portée. Malgré leur caractère éphémère, leurs frappes infligent de réels dégâts physiques et énergétiques.

TIPS: 

- Don't linger in open areas: les projectiles de lave sont efficaces dans une trajectoire rectiligne.
- Avoid close combat: race primitive entraînée
