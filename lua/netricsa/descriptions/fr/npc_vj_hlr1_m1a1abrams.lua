H.E.C.U. M1A1 ABRAMS


CLASS: H.E.C.U. M1A1 Abrams
METABOLISM: Mécanique, piloté par l'homme
PERCEPTION: Véhicule blindé lourd
SIZE: ~ 32 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Extrême
WEAPONS: Lance-roquettes
REWARD: 8000FC
THREAT: Catastrophique

DESCRIPTION: 

Le M1A1 Abrams est un char lourdement armé déployé par H.E.C.U. forces, modifiées pour lancer des barrages de roquettes dévastateurs à la place. Son blindage renforcé protège contre la plupart des armes légères et des explosifs légers, tandis que les roquettes peuvent détruire aussi bien les véhicules, les structures et les groupes d'infanterie. Son avance lente mais délibérée lui permet de contrôler des zones clés, d'aplanir les obstacles et de dominer les combats au sol.

TIPS: 

- Gardez toujours un abri ; les roquettes ont un large rayon de souffle et peuvent frapper à distance.
- Utilisez des armes explosives ou perforantes pour l'endommager.
- Les manœuvres de flanc sont essentielles - les assauts frontaux sont quasiment suicidaires.
- Évitez les terrains ouverts où le char peut tirer librement des roquettes.
