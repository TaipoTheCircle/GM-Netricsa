COCHON SALE (GÂTÉ) DU MONDE MINECRAFT


CLASS: Cochon sale (gâté)
METABOLISM: Mammifère à sang chaud
PERCEPTION: Animal sauvage/domestique amical, amateur de bains de boue
SIZE: ~ 3 pieds
ENDURANCE: Moyenne
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 35 FC
THREAT: Minimal

DESCRIPTION: 

Il ne s’agit pas d’une espèce à part entière, mais d’un état d’esprit. N'importe quel cochon ordinaire peut devenir sale s'il trouve une flaque d'eau appropriée et y passe quelques secondes, se salissant avec diligence.

Après l'intervention, presque tout son corps est couvert de saleté, en particulier ses jambes, et une fleur repose sur sa tête, se moquant de la propreté.

Dirt can be washed off a pig in two ways: attirez-la dans un lac ou un autre plan d’eau, ou arrosez-la simplement avec un seau d’eau. Après cela, le cochon retrouve son apparence normale, rose et propre, mais pas pour longtemps. S'il y a de la saleté à proximité, elle reviendra certainement et répétera la procédure. Apparemment, ils aiment ça.

TIPS: 

- N'essayez pas de laver un cochon sale à moins que vous ne vouliez un cochon normal. La saleté n'est pas une maladie, c'est le mode de vie du porc.
- Un tel cochon sent... enfin, comme un cochon dans la boue.
- Si vous élevez des porcs et que vous voulez une progéniture sale, laissez simplement une flaque d'eau près de l'enclos. Ils régleront le problème eux-mêmes.
