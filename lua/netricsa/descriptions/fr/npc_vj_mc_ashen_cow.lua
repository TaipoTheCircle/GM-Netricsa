VACHE CENDRÉE DU MINECRAFT OVERWORLD


CLASS: Vache cendrée
METABOLISM: Mammifère à sang chaud
PERCEPTION: Animal passif
SIZE: ~4,6 pieds
ENDURANCE: Moyenne
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 60 FC
THREAT: Minimal

DESCRIPTION: 

Une variété rare de vache commune, originaire de la steppe. Contrairement à ses cousines tachetées, cette vache est entièrement grise, avec une bande longitudinale plus foncée le long du dos et le bout de ses cornes est également gris.

The Ashen Cow's behavior is no different from that of a common cow: il broute, erre sans but et évite la lave et les falaises. Lorsqu’il est endommagé, il s’enfuit quelques secondes puis se calme.

But it has one unique characteristic, confirmed by thousands of survivors: les plantes grimpantes l'évitent. Aucune plante grimpante n'explosera jamais près d'une vache cendrée.

Ils ne s'approchent tout simplement pas. Ils en font le tour, changent de trajectoire, même si la vache est directement sur leur chemin. Personne ne sait pourquoi. Certains disent que sa peau grise sent quelque chose qui effraie les créatures végétales et poudrières.

TIPS: 

- Si vous trouvez une vache cendrée, ne la tuez pas. Rassemblez-le dans votre base.
- Vous pouvez construire en toute sécurité des structures en bois autour ; les plantes grimpantes ne feront pas l'affaire.
- Élevez-les – Les vaches cendrées se reproduisent comme les vaches normales, produisant la même progéniture.
- Inutile au combat, mais inestimable en agriculture.
- Protège la base des Creepers.
