MESA NOIRE, PARTIE AVEC ASSAINISSEMENT


Sam, anomalous energy spikes are going crazy here! Multiple teleport signatures detected: des yeux de chien, des zombies crabes et suffisamment de bruit pour réveiller la moitié de Xen. Toute la zone est instable.

To move forward, you'll need to get into the sewer system. But here's the trick: la vanne en bas n'est pas destinée au drainage, mais à l'inondation. Une fois que vous le tournez, le niveau de l'eau monte et vous pourrez alors nager à travers un puits de sortie plus élevé. Un design efficace, vous ne trouvez pas ? Les humains aiment vraiment leurs solutions trop compliquées.

Et cerise sur le gâteau, mes capteurs l'ont repéré à nouveau. Costume d'affaires, mallette, calme comme un roc dans un ouragan. Je reste là, comme si le chaos n'existait même pas.

So: vous êtes en infériorité numérique, les eaux usées jusqu'aux genoux, sur le point de vous inonder délibérément pour vous échapper... et sous la surveillance de l'employé de bureau le plus suspect du multivers. Aucune pression !
