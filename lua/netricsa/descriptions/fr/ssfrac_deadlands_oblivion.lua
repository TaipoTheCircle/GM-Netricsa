DAEDRA DES TERRES MORTES DE L'OBLIVION


Les Daedra des Terres Mortes d'Oblivion sont une armée de destruction, née dans les flammes du royaume de Mehrunes Dagon, prince du chaos et de la destruction. Ils sont des incarnations de la fureur de leur maître, des créatures forgées à partir de cendres, d'acier et de haine.

Chaque guerrier des Terres Mortes porte la flamme de son prince dans son cœur. Ils ne connaissent ni la peur, ni la pitié, et ne font pas de distinction entre le bien et le mal. Pour eux, seul l’ordre de la destruction existe : le cycle sacré de la fin et de la renaissance.

Lorsque les portes d'Oblivion s'ouvrent, leurs armées font irruption dans le monde des mortels comme une coulée de lave brûlant tout sur leur passage. Ils ne sont pas liés par le temps et ne craignent pas la mort, car pour eux, la fin est le début d’un nouvel incendie.
