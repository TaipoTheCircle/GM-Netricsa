TOURELLE COMBINÉE PIRATÉE PAR LES REBELLES


CLASS: Tourelle de moissonneuse-batteuse piratée par les rebelles
METABOLISM: Mécanisme entièrement synthétique et reprogrammé
PERCEPTION: Système de défense automatisé capturé
SIZE: 4,5 pieds
ENDURANCE: Moyen
HOSTILITY: Extrême
WEAPONS: Canons à double impulsion
REWARD: 400FC
THREAT: Moyen-élevé

DESCRIPTION: 

La tourelle Rebel-Hacked est une tourelle portable standard du Combine, capturée, grossièrement recodée et réintroduite par les forces de la Résistance. Son extérieur présente un travail de peinture modifié et des signes de réparation sur le terrain, mais ses internes mortels restent les mêmes.

Après la reprogrammation, le système de reconnaissance de cible ne considère plus les combattants du Combine comme des « amis ». Désormais, tout soldat Metrocop ou Overwatch est automatiquement désigné comme cible prioritaire. La tourelle ouvre immédiatement le feu dès qu'elle détecte un mouvement ennemi ou une signature thermique.

Les rebelles utilisent activement ces tourelles pour défendre les couvertures, les bases souterraines, les couloirs et les points d'étranglement. Grâce à sa taille compacte et à sa puissance de feu élevée, une seule tourelle peut occuper une position qui nécessiterait autrement une escouade entière.

Malgré le changement d'allégeance, la tourelle reste une machine Combine par nature. Il ne fait aucune distinction entre les tactiques, n'économise pas les munitions et ne connaît aucune peur. La seule chose qui a changé est la liste des cibles.

TIPS: 

- Placez les tourelles dans les points d'étranglement et sur les flancs : c'est là qu'elles sont les plus efficaces.
- Surveillez la ligne de tir : la tourelle ne fait aucune distinction entre les alliés s'ils s'interposent entre elle et la cible.
