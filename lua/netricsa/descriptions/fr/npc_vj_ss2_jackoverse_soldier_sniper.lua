CLONE STORMTROOPER, SNIPER DU KAMINO


CLASSE : Clone Stormtrooper, Sniper
MÉTABOLISME : Clone humain, mâle
PERCEPTION : Tireur d'élite de l'Empire Galactique
TAILLE : 5,9 pieds
ENDURANCE : Moyenne
HOSTILITÉ : Extrême
ARMES : Fusil de précision « RAPTOR » amélioré avec indicateur laser
RÉCOMPENSE : 1200 FC
MENACE : Très élevée

DESCRIPTIF :

Les tireurs d'élite constituent le niveau suivant dans la hiérarchie des Stormtrooper Corps et sont recrutés exclusivement parmi les clones les plus disciplinés et les plus précis. Ils sont facilement identifiables grâce à leur armure grise, délivrée aux soldats ayant suivi un entraînement spécial pour le combat à longue distance.

L'arme principale du tireur d'élite est un fusil "RAPTOR" modernisé, équipé d'une optique de haute précision et d'un désignateur laser. Le laser permet de suivre des cibles en mouvement et réduit considérablement le temps de visée, tandis que ses puissantes munitions perforantes peuvent pénétrer la plupart des types d'équipements de protection individuelle connus.

Contrairement aux troupes d'assaut standards, les tireurs d'élite s'engagent rarement dans des combats ouverts. Ils occupent des hauteurs dominantes, des toits ou des positions fortifiées, d'où ils éliminent les cibles les plus dangereuses, laissant le reste à l'infanterie de ligne. Même après avoir été détectés, ils gardent leur sang-froid et continuent de tirer jusqu'au dernier moment possible.

Alliés au Mental, ces soldats sont appréciés pour leur capacité à éliminer l’ennemi avant que les forces principales ne s’engagent. De nombreux commandants préfèrent d’abord envoyer quelques tireurs d’élite avant d’engager des unités entières dans l’attaque. Perdre un de ces tireurs est désagréable, mais perdre un commandant à cause d'un seul tir bien ciblé est bien plus désagréable.

CONSEILS :

- Si vous remarquez un laser rouge, quelqu'un vous a déjà repéré.
- Ne restez pas trop longtemps dans des espaces ouverts.
- La fumée, la couverture et les mouvements constants réduisent considérablement leur efficacité.
- L'armure grise est moins visible que la blanche. Malheureusement, ils tirent à reculons.