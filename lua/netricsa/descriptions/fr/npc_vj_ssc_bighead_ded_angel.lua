GROSSE TÊTE, DED123_228


CLASS: Grosse tête, DED123_228
METABOLISM: Humanoïde, mâle
PERCEPTION: Fanatique enthousiaste
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Neutre
WEAPONS: Aucun (sauf en difficulté I Am Ugh-Zan)
REWARD: 500FC
THREAT: Ennui

DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes avec des crânes exagérés contenant le cerveau des développeurs. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils reçoivent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

DED123_228 is a rare breed of fan-enthusiast, whose oversized skull contains not only a love for the game but also an exaggerated sense of justice and respect for the creativity of others. He is the creator of the "TROPICAL GNAARS - Enemies Reskin" mod for Serious Sam Classics: Revolution, où il a donné aux Gnaars classiques un camouflage tropical vibrant.

When Taipo asked permission to port his tropical Gnaars to GMod for the "Serious Sam: Le module complémentaire "Classic SNPC (EXTRA)", DED123_228, n'a pas fait obstacle, n'a pas exigé de commission ni ignoré la demande. Il a généreusement donné son feu vert, comprenant que cela élargirait l'univers de sa création et ravirait davantage de joueurs.

Le croquis et le design originaux appartiennent à son ami Froel Hoates.

GROSSE TÊTE, JOHN J. DICK "BOOGER"

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager un chemin.
- En difficulté "I Am Ugh-Zan", détruisez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
