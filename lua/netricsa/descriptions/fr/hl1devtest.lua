BLACK MESA DEVTEST, ÉTRANGE


Au lieu d'expériences de routine et de bavardages inutiles, on plonge directement dans le chaos. Presque immédiatement, les installations de Black Mesa sont soumises à de violents bombardements, avec des explosions secouant les couloirs, des dalles de plafond s'effondrant et des sirènes d'urgence retentissantes.

Au même moment, des créatures Xen commencent à apparaître - des crabes tombant de nulle part, les scientifiques parviennent à peine à pousser quelques cris avant d'être déchiquetés, et les gardes de sécurité tentent de se battre mais sont rapidement dépassés.
