VICTIME DU DEMI-QUAKE SHOW


CLASS: Victime
METABOLISM: Humain, Cyborg, Mâle
PERCEPTION: Prisonnier du spectacle, pion rebelle
SIZE: ~6,2 pieds
ENDURANCE: Variable
HOSTILITY: Haut
WEAPONS: MP5, fusil de chasse SPAS-12
REWARD: 500FC
THREAT: Haut

DESCRIPTION: 

À première vue, c'est un robot sans âme. Armure verte, formes anguleuses, démarche lourde. Mais derrière l’armure se cache de la chair humaine. Du sang, des muscles, du désespoir et de la haine. Son visage est caché derrière un masque métallique, mais si vous regardez attentivement, vous pouvez voir quelque chose de vivant. Quelque chose qui a depuis longtemps perdu espoir, mais qui n’a pas perdu sa rage.

Il s'est retrouvé dans l'émission Half-Quake pour "sa terrible erreur dans la vie". Les sadiques ne diront pas lequel. Peut-être qu'il a tué la mauvaise personne. Peut-être qu'il a croisé le mauvais sadique. Ou peut-être qu’il n’y a eu aucune erreur. Ils avaient simplement besoin d'un corps pour un nouvel épisode. Et il a été choisi pour ce corps.

Il est armé d'un MP5 et d'un fusil de chasse SPAS-12. Pas parce qu’ils lui ont donné des armes. Mais parce qu'il les a pris aux gardes qu'il a tués dans les épisodes précédents. Il déteste le spectacle. Il veut s'échapper. Il veut détruire cet endroit. Mais surtout, il ne veut plus jamais mourir.

La victime est décédée à plusieurs reprises. Il a été coupé, brûlé, noyé, explosé, poignardé, donné à manger aux monstres. Mais il se relève. Encore et encore. Les sadiques pensent que cela rend le spectacle plus intéressant. La victime elle-même croit qu'il s'agit d'une malédiction. Mais chaque fois qu'il ouvre les yeux après un nouveau décès, il prend les armes et continue son chemin, déterminé à mettre fin aux cauchemars.

TIPS: 

- Ne tirez pas sur la victime. Ce n'est pas votre ennemi.
- Si vous le croisez dans les couloirs de Shaw, restez à proximité. Il sait où sont les gardes, où se trouvent les pièges et où se trouve la sortie.
- La victime ne fait confiance à personne. Ne vous attendez pas à ce qu'elle partage ses munitions avec vous. Mais elle peut vous couvrir lors d'un combat.
- Les sadiques le détestent. Somos aussi. Le fait qu'elle soit toujours en vie (dans un sens) les exaspère plus que n'importe quelle rébellion.
- N'essayez pas de le sauver. Il n'a pas besoin d'être sauvé. Il a besoin de vengeance. Et à la fin, soit il détruira Shaw, soit il mourra pour la dernière fois. Pour de bon.
