FERRAILLE


CLASS: ScrapJack
METABOLISM: Cadavre cousu cybernétiquement modifié
PERCEPTION: Guerrier fusée cousu
SIZE: 8 pieds
ENDURANCE: Haut
HOSTILITY: Haut
WEAPONS: 2 lance-roquettes, coup écrasant
REWARD: 5000FC
THREAT: Haut

DESCRIPTION: 

A cheap and mass-produced heavy infantryman of the Mental Horde, literally assembled from the body parts of fallen soldiers and equipped with powerful rocket launchers. Extremely slow and clumsy, but compensates for this with a huge reserve of fat and devastating firepower. The rockets fly in a predictable pattern: l'un directement sur la cible, les autres tirent de manière préventive sur les flancs. Lorsqu’il est enragé, il tire plus fréquemment et avec plus de précision. À bout portant, il délivre un coup dévastateur, capable de tuer d'un seul coup.

TIPS: 

- Ses missiles peuvent pénétrer dans des murs minces. Une frappe rapprochée est fatale.
- A-24 "Devastator", pistolet lance-fusées, C4 - Excellentes options pour le combattre.
