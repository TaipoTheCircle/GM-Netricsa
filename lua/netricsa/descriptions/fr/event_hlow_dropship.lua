COMBINEZ LE DROPSHIP ÉLITE


CLASSE Combiner Elite Dropship
MÉTABOLISME Entièrement synthétique
PERCEPTION Livraison directe à grande vitesse d'unités d'élite et standard
TAILLE ~55-65 pieds (avec conteneur)
ENDURANCE Extrême
HOSTILITÉ Critique
ARMES Aucune
RÉCOMPENSE 6000 FC
MENACE Très élevée

DESCRIPTION

Le Combine Elite Dropship est un transporteur de troupes synthétique avancé conçu pour le transport rapide d'unités d'élite du Combine, y compris des soldats d'élite et des équipes de frappe standard. Contrairement aux Dropships standards, ce modèle est optimisé pour une vitesse maximale et un profil de combat minimal.

La coque du Synthetic est peinte en noir et blanc avec des panneaux composites renforcés, ce qui la distingue visuellement des Dropships standards. Le conteneur est conçu pour transporter 3 à 5 soldats à la fois. Après le déploiement, le conteneur reste ouvert même lorsque le Dropship se retire, accélérant le cycle de déploiement et réduisant le temps passé dans la zone de destruction.

Il n'y a pas de modules d'armes, car le concept de la plate-forme est basé sur le principe d'entrée, de déchargement et de sortie rapides. L'Elite Dropship est pratiquement invulnérable aux armes standard et vulnérable uniquement aux missiles RPG V2, où deux coups suffisent pour le détruire ou le désactiver de manière critique.

CONSEILS

- Utiliser le RPG V2 ; les autres armes sont inefficaces.
- Deux coups précis suffisent pour assommer le Dropship.
- Attaque lors de la descente et du débarquement, lorsque la trajectoire est prévisible.
- Le conteneur ne se ferme pas lors du retrait, le laissant vulnérable aux incendies du compartiment interne.
- La destruction d'un vaisseau d'élite Dropship pourrait perturber le déploiement des troupes d'élite et changer le cours de la bataille.
