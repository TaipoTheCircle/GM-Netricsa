COMBINEZ LE SOLDAT ANTI-VÉHICULE OVERWATCH


CLASS: Combinez Overwatch Anti-Vehicle Trooper
METABOLISM: Humain augmenté, mâle
PERCEPTION: Unité spécialisée anti-véhicule
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Très élevé
WEAPONS: SMG amélioré, lance-roquettes et missiles anti-véhicules
REWARD: 750 FC
THREAT: Haut

DESCRIPTION: 

L’Overwatch Anti-Vehicle Trooper est pratiquement impossible à distinguer d’un Combine Trooper standard. Presque la même armure, la même posture, la même visière bleu froid. Une énorme erreur : confondez-le avec « ordinaire ». Son véritable rôle n'est révélé que lorsque des véhicules apparaissent sur le terrain.

Un lance-roquettes est monté sur le dos du soldat et peut être récupéré instantanément dès que le système reconnaît une cible dans un véhicule. Les véhicules, blindés et blindés improvisés de la résistance sont tous sa priorité absolue.

La mitraillette améliorée est utilisée pour l'autodéfense et la répression de l'infanterie tandis que les soldats prennent position pour les véhicules d'attaque. Cependant, les échanges de tirs prolongés ne lui posent aucun problème. Il agit rapidement, frappe fort et tente de changer de position immédiatement après le lancement d'une fusée, sachant que la riposte suivra immédiatement.

Le Combine utilise ces soldats pour neutraliser la mobilité de la résistance. Un soldat anti-véhicule a réduit un convoi de véhicules en ferraille enflammée en quelques secondes.

TIPS: 

- Ne l'approchez jamais dans un véhicule.
- Tuez-le d'abord, puis utilisez le véhicule.
