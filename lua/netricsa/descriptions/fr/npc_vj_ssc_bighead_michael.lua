METABOLISM: Humanoïde, mâle
PERCEPTION: Fanatique enthousiaste
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Neutre
WEAPONS: Aucun (sauf en difficulté I Am Ugh-Zan)
REWARD: 100 FC
THREAT: Ennui

DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Michael Harris (à ne pas confondre avec le criminel du même nom) est l'un des principaux bêta-testeurs de Croteam, ayant apporté une contribution significative au débogage et au peaufinage des jeux les plus importants de la série. Son crâne surdimensionné est le résultat direct de centaines d'heures passées à rechercher de minuscules bugs, à vérifier l'équilibre et à effectuer des tests de stabilité sur The First Encounter, The Second Encounter, Serious Sam 2 et leurs remasters HD.

Contrairement aux développeurs célèbres, les noms des testeurs restent souvent dans l’ombre, mais leur rôle est inestimable. Ce sont des spécialistes comme Michael Harris qui ont servi de dernière ligne de défense entre une construction préliminaire et le produit final, détectant les erreurs de collision, les problèmes de performances et les incohérences logiques qui pourraient autrement gâcher l'expérience du joueur. Ses nombreuses années de travail sont créditées sous le nom de « Tests et support supplémentaires », une fonctionnalité modeste mais vitale, sans laquelle aucun jeu Serious Sam ne serait aussi perfectionné et stable.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", éliminez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
