DELTA BASE ZOMBIE HEADCRAB DANS LE COSTUME DANGER XEN, COMMUN


CLASS: Crabe-tête zombie de la base Delta dans le costume Hazard Xen, commun
METABOLISM: Hôte humain + crabe parasite de Xen
PERCEPTION: Entité zombifiée blindée
SIZE: 6,2 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Griffes, Morsure
REWARD: 600 FC
THREAT: Très élevé

DESCRIPTION: 

This specimen is a former Delta Base researcher who was careless enough to travel to Xen or work in particularly hazardous environments wearing the signature purple HAZARD SUIT. Functionally, it is completely identical to the H.E.V. From Black Mesa: l'étanchéité, l'amplification des impacts, la radioprotection et autres plaisirs du travail scientifique aux confins de la réalité.

Même cela n’a pas sauvé certaines âmes malheureuses du crabe. Le parasite a trouvé un moyen de pénétrer dans le casque – ou peut-être que l’hôte l’a retiré à un moment critique – et a fait son sale boulot. Désormais, la carcasse mobile en costume violet est un zombie, mais un zombie avec des avantages.

La combinaison de protection lui confère un double avantage. Premièrement, la capacité de survie. Les balles et les éclats d'obus sont absorbés par le tissu et les plaques renforcés de la combinaison. Deuxièmement, la force. Les servos qui aidaient les scientifiques à soulever des équipements lourds amplifient désormais les coups des zombies, les rendant dévastateurs.

La combinaison violette est tachée de sang et de saleté, le casque est cassé ou manquant. Un tir dans la tête décapite la tête.

TIPS: 

- Ce zombie possède l'armure la plus élevée de toutes les variétés standards. Un pistolet ordinaire est presque inutile.
- Visez la tête. La combinaison protège le corps, mais pas la tête.
- Utiliser un fusil de chasse à bout portant ou une arme automatique de gros calibre.
- Un tir à la tête détachera la tête, tout comme les autres.
- Ne pense pas que tu puisses enlever le costume et le remettre. Cela fait désormais partie du monstre.
