MANCUBUS, LA FERRAILLE DE L'ENFER


CLASS: Mancubus, ScrapJack
METABOLISM: Mammifère démoniaque reconstruit
DEFINITION: Cadavre Behemoth modifié cousu
SIZE: 13 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Deux RPG
REWARD: 3000FC
THREAT: Haut

DESCRIPTION: 

Un démon personnifiant l'imperfection humaine, l'avidité, la gourmandise et l'oisiveté. Il a une peau incroyablement épaisse, des pattes éléphantines massives et une petite tête. Afin de détruire et faire exploser les victimes, deux RPG tirant des roquettes sont implantés dans les avant-bras du mancubus.

Les Mancubus tués de la manière la plus horrible ont été refusionnés, devenant écarlates. Leur peau prend la couleur du sang séché et leur essence change. Mental, toujours à la recherche de moyens de renforcer sa Horde, a acheté plusieurs dizaines de ces créatures au Seigneur de l'Enfer (ainsi que des soldats zombies et d'autres morts-vivants) et les a envoyées pour être réutilisées.

Au lieu de lance-flammes, des lance-roquettes ont été greffés sur leurs avant-bras. Deux RPG tirent en continu jusqu'à la mort de l'ennemi. Même si l'ennemi se cache derrière un mur, les roquettes voleront encore et encore, réduisant méthodiquement le couvercle en tamis puis le tireur en viande hachée.

Contrairement à un Mancubus lance-flammes classique, le ScrapJack Mancubus est plus faible en défense. Soudé à partir de morceaux de divers démons, il manque d'armure monolithique et peut être tué plus rapidement. Mais seulement si vous parvenez à y arriver avant que tout ne se transforme en un cratère d’explosions.

TIPS: 

- Mettez-vous à l'abri. Les fusées sont aléatoires et un mur de pierre peut vous sauver la vie, mais seulement pour une courte période.
- Ils tirent sans fin. N'attendez pas qu'ils soient à court de munitions. Ils n'en ont pas.
- Tuez-les vite. Oui, ils sont plus faibles qu'un Mancubus ordinaire, mais chaque seconde que vous attendez augmente les chances que vous vous transformiez en un désastre sanglant.
- Ne restez pas à l'air libre. Les roquettes sont largement répandues et se cacher dans les coins est le seul moyen de survivre.
- Ne les laissez pas viser. Si vous voyez un Mancubus pointer ses lance-roquettes sur vous, déplacez-vous immédiatement sur le côté. Ils ne tirent pas instantanément, mais une fois lancés, il est presque impossible de les arrêter.
- Utilisez des explosifs. Un C4 bien placé peut le détruire plus rapidement que 5 coups de fusil de chasse.
