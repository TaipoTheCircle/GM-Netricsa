ROBOT COMPAGNON G-2 DU LOEK-III


CLASS: Robot compagnon G-2
METABOLISM: Mécanique
PERCEPTION: Robot de défense automatisé
SIZE: ~ 3 pieds
ENDURANCE: Haut
HOSTILITY: Haut
WEAPONS: Grèves
REWARD: 1200
THREAT: Haut

DESCRIPTION: 

Le robot compagnon G-2 est un défenseur mécanisé, faisant partie de l'équipe Core Guardian, utilisé lors des attaques de la horde Lum. Son corps est peint par Sweet Autumn.

Il est armé d'armes capables de détruire les créatures extraterrestres Lum en combat rapproché. Son IA lui permet de coopérer efficacement avec les Core Guardians, en couvrant les points d'étranglement et en assurant la suppression si nécessaire.

TIPS: 

- Gardez le G-2 à proximité des points d'étranglement pour une efficacité maximale.
- Fonctionne mieux avec les Core Guardians et les tourelles.
