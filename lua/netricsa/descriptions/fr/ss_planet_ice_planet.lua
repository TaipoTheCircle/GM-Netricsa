PLANÈTE DE GLACE


PLANET: Planète de glace
TYPE: Planète terrestre, Cryo-Monde
CLIMATE: Congélateur
MOONS: Inconnu
TERRAIN: Couverture de glace solide, plans d'eau non gelés, ponts de glace
LOCATION: Système stellaire inconnu
NATIVE RACES: Élémentaires de glace, Mamutman des neiges, Mamuts des neiges, Bêtes-garous des neiges
ALLIANCE: Horde mentale
LOCATIONS: Pont de glace, monolithes noirs, camps de Mamutman

DESCRIPTION: 

La planète de glace est un monde entièrement recouvert de glace, rappelant visuellement les régions polaires de la Terre, comme l'Antarctique. Sa surface est constituée de champs de glace sans fin, entrecoupés de zones ouvertes d'eau non gelée.

Les habitants indigènes de la planète sont des créatures élémentaires de glace composées de glace pure.

Les Mamutman des neiges, une race humanoïde primitive adaptée aux conditions de froid rigoureux, habitent également la planète. Les Mamutman utilisent les Snow Mamuts, des créatures massives et hirsutes ressemblant à des mammouths, comme bêtes de somme et pour le combat. Les taureaux-garous des neiges constituent une menace distincte.

Au plus profond des champs de glace s'élèvent deux mystérieuses structures de monolithes noirs d'origine inconnue, accessibles uniquement par un long pont de glace enjambant des eaux non gelées.

KEY LOCATIONS: 

- Ice Bridge: Une traversée sur des eaux non gelées menant à deux mystérieux monolithes noirs.
- Black Monoliths: Deux structures mystérieuses d'origine et de destination inconnues, situées de l'autre côté du pont de glace.
- Mamutman Camps: Colonies de la race humanoïde locale, où sont gardés des Mamuts des neiges apprivoisés.
