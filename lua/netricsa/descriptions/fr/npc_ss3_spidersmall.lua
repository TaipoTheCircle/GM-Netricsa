ARAIGNÉE ANTARESIENNE HACHÉE D'ANTARES PRIME


CLASS: Araignée antarésienne éclos
METABOLISM: Extraterrestre, Arachnide
PERCEPTION: Créature arachnide
SIZE: 1 pied
ENDURANCE: Très faible
HOSTILITY: Moyenne
WEAPONS: Crachat acide, morsure
REWARD: 100 FC
THREAT: Faible

DESCRIPTION: 

La forme nouveau-née des araignées antarésiennes – des arachnides extraterrestres géants de la planète Antares Prime. Un adversaire extrêmement faible avec un exosquelette fin, mais qui compense sa vulnérabilité par le nombre et la surprise. Le plus souvent, ils éclosent à partir de cocons sensibles au bruit. Ils ont la capacité de ramper le long des murs et des plafonds, d'attaquer de loin avec de l'acide empoisonné ou de s'accrocher à leurs victimes de près. Leurs boules d'acide peuvent ricocher sur les surfaces. Même si elles ne sont pas dangereuses individuellement, un essaim de dizaines de ces araignées peut rapidement encercler et épuiser même un combattant expérimenté.

TIPS: 

- Ils peuvent émerger sous leurs pieds, depuis le plafond, ou sortir de leur cocon après un fort coup de feu.
- À courte portée, ils peuvent être instantanément écrasés avec votre pied. Cela ne gaspille pas de munitions et est rapide.
- Le fusil à pompe est efficace contre les groupes.
- Le marteau tue d'un seul coup, ne gaspille pas de munitions, mais vous laisse vulnérable pendant toute la durée du coup.
