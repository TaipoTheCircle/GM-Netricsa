Bug mortel de l'essaim "bêta"


CLASSE : Bug de mortier d'essaim "Bêta"
MÉTABOLISME : Xénobiologique
PERCEPTION : Unité d'artillerie
TAILLE : 9 pieds
ENDURANCE : Moyenne
HOSTILITÉ : Élevée
ARMES : Projectiles biologiques explosifs
RÉCOMPENSE : 650 FC
MENACE : Élevée

DESCRIPTIF :

Le Swarm Mortar Bug « Beta » est une variante de siège spécialisée conçue pour éliminer les ennemis à longue distance. Contrairement aux Drones ou aux Rangers, son corps est presque entièrement dédié à une seule tâche : la production et le lancement de munitions organiques.

Une gueule massive domine la moitié avant de son torse ; à l’intérieur, des biocapsules explosives se forment. Lors de l'impact, ceux-ci adhèrent aux surfaces et explosent au bout de quelques secondes. Cela permet au Mortar Bug de bloquer les passages, de forcer les proies à sortir de leur abri et de disperser les formations défensives.

La physiologie de la créature diffère considérablement de celle de la plupart des autres membres de l'essaim « bêta ». Ses organes visuels sont sous-développés ou dissimulés sous des couches de tissus ; il navigue probablement à l'aide de vibrations, d'odeurs et d'autres capteurs biologiques. Cela explique pourquoi le Mortar Bug opère rarement seul, préférant rester sous la protection d'unités plus mobiles.

Malgré sa létalité, la créature n'est pas adaptée au combat rapproché. Son volume massif et son anatomie spécialisée rendent le Mortar Bug beaucoup moins maniable que la plupart des autres unités Swarm.

CONSEILS :

- Regardez où les projectiles atterrissent.
- Ne laissez pas le Mortar Bug maintenir un tir soutenu.
- Exploiter son manque de mobilité contre lui.
- Si une créature de la taille d'un petit véhicule ouvre sa gueule dans votre direction, mieux vaut ne pas attendre le résultat de l'expérience.