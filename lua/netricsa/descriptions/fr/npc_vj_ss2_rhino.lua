CYBERJOUET RHINO


CLASS: Cyber-jouet Rhino
METABOLISM: Mammifère cybernétiquement amélioré
PERCEPTION: Animal de guerre dressé
SIZE: 6,5 pieds
ENDURANCE: Moyen
HOSTILITY: Extrême
WEAPONS: Cornes
REWARD: 300FC
THREAT: Haut

DESCRIPTION: 

L'introduction des Cyber-Rhinos a été une malheureuse surprise pour l'armée humaine pendant la guerre contre le mental.

Après avoir conquis de nombreuses planètes et placé des milliards d'êtres vivants sous son contrôle, Mental voulait garder le pouvoir entre ses mains et il a donc été décidé de donner à chaque famille de son gigantesque empire un jouet rhinocéros à manivelle.

La production de masse a commencé.

La joie et le bonheur remplissaient le monde de souffrance et de chagrin que Mental avait créé.

Mais dès que la guerre avec la Terre a commencé, dès que Sam Stone a commencé à mordiller les talons du méchant, libérant une planète après l'autre, les rhinocéros ont été équipés d'un moteur et d'un réservoir de carburant et jetés au combat !

Les rhinocéros sont plus agressifs et plus rapides que leurs cousins ​​taureaux-garous, grâce aux améliorations cybernétiques. La façon de les gérer est la même ; restez à l’écart et utilisez des explosifs. Il faudra cependant être encore plus prudent.

TIPS: 

- Faites un pas de côté pour esquiver leurs attaques.
- Utilisez des explosifs pour les éliminer.
