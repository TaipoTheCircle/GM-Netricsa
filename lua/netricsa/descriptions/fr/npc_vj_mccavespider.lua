ARAIGNÉE DES GROTTES DU MONDE MINECRAFT


CLASS: Araignée des cavernes
METABOLISM: Arthropode
PERCEPTION: Prédateur souterrain venimeux
SIZE: 1,9 pi
ENDURANCE: Faible
HOSTILITY: Haut
WEAPONS: Morsure venimeuse
REWARD: 600 FC
THREAT: Moyen

DESCRIPTION: 

L'araignée des cavernes est une version compacte, colérique et extrêmement mécontente de l'araignée commune, que l'on trouve exclusivement dans les mines abandonnées. Il est plus petit, plus rapide et, plus ennuyeux encore, plus venimeux.

TIPS: 

- Ne sous-estimez pas sa taille : le venin ne se soucie pas de ce que vous pensez.
- Les épées sont les meilleurs arguments face à cette créature.
