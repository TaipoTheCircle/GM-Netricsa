ZOMBIE CRABE


CLASS: TêteCrab Zombie, Torse
METABOLISM: hôte parasite (restes sectionnés)
PERCEPTION: Entité zombifiée
SIZE: 3 pieds
ENDURANCE: très faible
HOSTILITY: haut
WEAPONS: coups de griffes, morsures, sauts courts
REWARD: 100 FC
THREAT: faible

DESCRIPTION: 

Le torse zombie est le reste mutilé d'un hôte humain, animé par un crabe même après avoir perdu la majeure partie de son corps. Souvent rencontrées en train de ramper à travers les décombres ou rejetées au combat, ces créatures se traînent vers l'avant avec une vitesse surprenante, hurlant d'agonie tandis que le parasite les pousse en avant. Malgré leur état pitoyable, ils conservent suffisamment de force pour griffer leurs proies ou tenter de les mordre. Leur persistance grotesque les rend plus inquiétants que dangereux.

TIPS: 

- Un seul coup bien placé suffit généralement.
- Ne les laissez pas vous distraire lors de batailles plus importantes : ils sont souvent utilisés comme « bruit » aux côtés de zombies plus puissants.
- Surveillez vos déplacements dans les zones sombres ; ils ont tendance à sortir soudainement des débris.
