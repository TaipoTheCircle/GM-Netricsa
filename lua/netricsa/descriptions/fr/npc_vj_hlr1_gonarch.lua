GONARCH DE XEN


CLASS: Gonarque
METABOLISM: suzerain parasite
PERCEPTION: Matriarche du crabe
SIZE: ~ 20 pieds de haut
ENDURANCE: extrêmement élevé
HOSTILITY: extrême
WEAPONS: pattes écrasantes, bébés crabes, projectiles acides
REWARD: 2000 FC
THREAT: catastrophique

DESCRIPTION: 

Le Gonarch est l’étape finale et la plus terrifiante du cycle de vie du crabe. C'est une matriarche colossale ressemblant à une araignée avec un sac à œufs grotesque pendant sous son corps, produisant un nombre infini de crabes. Adoré – ou redouté – par les parasites de moindre importance, il sert à la fois de reproducteur et de commandant de l'infestation. Rencontré dans les royaumes les plus profonds de Xen, le Gonarch charge sa proie avec ses pattes imposantes, crache de l'acide et libère des essaims de crabes nouveau-nés. Ses cris déformés résonnent dans les cavernes comme un cri de guerre cauchemardesque.

Les légendes suggèrent que chaque infestation de crabes remonte finalement à un Gonarch. En tuer un n’est pas une mince affaire : rares sont ceux qui ont survécu à une telle rencontre. Mais vous ne faites pas partie de ces chanceux, n’est-ce pas ?

TIPS: 

- Concentrez le feu sur le sac abdominal pour l'affaiblir.
- Continuez à bouger, ses charges sont mortelles dans les espaces confinés.
- Soyez prêt à affronter des vagues infinies de crabes pendant le combat.
