DREAMCAST HOUNDEYE DE XEN


CLASS: Oeil de chien, Dreamcast
METABOLISM: xénofaune chasseuse en meute
PERCEPTION: Oeil de chien adulte
SIZE: ~ 3 pieds de haut, 4 pieds de long
ENDURANCE: moyen
HOSTILITY: riche en paquets
WEAPONS: ondes de choc soniques, vitesse, coordination du pack
REWARD: 400FC
THREAT: modéré individuellement, élevé en groupe

DESCRIPTION: 

L'Alpha Houndeye est un petit prédateur Xen ressemblant à un croisement entre un chien et un porc-épic surexcité. Il chasse en meute, s'appuyant sur des impulsions sonores pour désorienter ses proies avant de se rapprocher pour l'attaque. Malgré son apparence apparemment maladroite et ses cris aigus, une meute de Houndeyes peut submerger même les intrus bien armés.

Ces créatures sont très sociales et communiquent via des cris ultrasoniques qui peuvent briser les tympans à courte distance. Les scientifiques pensent que leurs attaques sonores explosives sont une adaptation évolutive destinée à compenser la faiblesse de leurs griffes et de leurs dents.

TIPS: 

- Engagez les Houndeyes à distance pour éviter leurs ondes de choc.
- Tirer sur un seul peut provoquer toute la meute - méfiez-vous.
- Exploit their pack mentality: les attirer vers des risques environnementaux peut retourner leur coordination contre eux.
