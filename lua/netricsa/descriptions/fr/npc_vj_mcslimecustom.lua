CUSTOMIZABLE SLIME FROM THE MINECRAFT OVERWORLD


CLASS: Slime personnalisable
PERCEPTION: Monstre visqueux
METABOLISM: Organisme amorphe
SIZE: ~3,2-6,5 pieds
ENDURANCE: Moyenne
HOSTILITY: Moyenne
WEAPONS: Sauter, Crash
REWARD:     50 FC
THREAT: Moyenne

DESCRIPTION:

Le Slime personnalisable est une créature amorphe et visqueuse qui semble n'avoir aucun sens d'espace personnel. Il bondit, se jette sur tout ce qui vit et se divise parfois en morceaux plus petits lorsqu'il est endommagé, créant ainsi des copies encore plus ennuyeuses.

TIPS:

- Combattez des Slimes plus gros à distance.
- Les Slimes plus petits ne représentent aucune menace en eux-mêmes, mais en groupe, ils peuvent provoquer le chaos.