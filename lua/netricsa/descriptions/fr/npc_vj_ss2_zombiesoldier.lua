MITRAILLEUSE ZOMBIE, COMMANDO


CLASS: Mitrailleur zombie, Commando
METABOLISM: Mort-vivant diabolisé
PERCEPTION: Soldat zombie
SIZE: 11,5 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Minigun
REWARD: 150 FC
THREAT: Haut

DESCRIPTION: 

Après avoir constaté avec quelle efficacité les zombies dirigés par des démons détruisaient des hordes entières d'infanterie avec des miniguns, Mental a décidé d'incorporer ces soldats dans son arsenal.

Les Arachnoïdes étaient trop gros, encombrants et lents pour être utilisés en masse. Mental forme alors une alliance avec le Seigneur de l'Enfer, qui lui prête plusieurs espèces, dont ces créatures.

Ces zombies sont d'anciens marines qui ont subi un rituel de transformation démoniaque complexe. Contrairement aux zombies ordinaires, qui sont simplement possédés par des démons, les commandos ont été complètement refaits, devenant quelque chose entre humain et abyssal.

They still retain the remnants of their military uniform: un pantalon de camouflage et un casque allemand vert distinctif.

La meilleure façon de les gérer est de NE PAS se précipiter tête baissée dans leur feu.

TIPS: 

- Se mettre à l'abri de leur feu ; vous pourriez être tenté d'utiliser l'approche « Pas de couverture, un contre un », mais en utilisant cette tactique, vous vous retrouverez plus probablement dans une situation « Pas de couverture, mort ».
- Leur minigun est imprécis à longue portée ; plus on est loin, mieux c'est.
- Tirer à distance est un excellent moyen de les gérer.
- Les tirs de fusée éclairante à la tête sont une solution rapide.
