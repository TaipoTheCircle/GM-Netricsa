RPG-7


WEAPON: RPG-7 (Lance-grenades propulsé par fusée)
AMMO: Roquettes hautement explosives
ROF: ~4 fusées/min

DESCRIPTION: 

Lanceur anti-blindage portable capable de tirer des roquettes hautement explosives. Efficace contre les véhicules, les positions fortifiées et les groupes d'ennemis. Un système de visée simple permet une utilisation aussi bien par des soldats entraînés que par des insurgés.

NOTES: 

- Dégâts explosifs massifs dans la zone cible.
- Munitions limitées ; chaque fusée doit être orientée avec soin.
- Le retour de souffle peut être dangereux s'il est tiré dans des espaces confinés.
- Rechargement requis après chaque tir.
