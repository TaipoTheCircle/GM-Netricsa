DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Sean "mwadaibe" Center est un autre rouage clé du légendaire collectif de modding A Few Screws Loose, dont le travail a été officiellement reconnu et gravé à jamais dans l'histoire de la série. Son crâne exagéré témoigne de sa profonde compréhension de l'équilibre, de la conception des flux et de la dynamique pure et débridée des batailles en ligne. Avec l'équipe, il a travaillé sur le célèbre mod Seriously Warped Deathmatch pour The Second Encounter, créant et affinant ces 20 cartes insensées qui ont inspiré toute une génération de joueurs d'esports de Serious Sam.

His contribution goes beyond mere fan art: il s'agissait d'un projet collaboratif à part entière, où la passion de la communauté rencontrait la confiance du studio. Sean et ses collègues n'ont pas seulement donné au jeu du contenu supplémentaire - ils ont prolongé sa durée de vie, établi son statut culte en multijoueur et prouvé que les idées les plus brillantes peuvent naître à l'intersection du développement professionnel et du dévouement des fans.

GROSSE TÊTE, JOHN J. DICK "BOOGER"

TIPS: 

