PROTECTEUR DE FLUX STROGG DE STROGGOS


CLASS: Protecteur de ruisseau Strogg
METABOLISM: Guerrier organique/biomécanique
PERCEPTION: Unité de combat d'élite Strogg
SIZE: 8,2 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Canon à pointe de plasma, missiles à tête chercheuse
REWARD: 2000 FC
THREAT: Très élevé

DESCRIPTION: 

Le Stream Defector est un gardien Strogg d'élite créé pour protéger les installations énergétiques et stratégiques clés de la planète Stroggos. Ces créatures agissent en tant que défenseurs du Flow et du Nexus, chargés d'empêcher la destruction de complexes d'infrastructures complexes par les forces humaines envahissantes.

Au combat, le Stream Defector combine une combinaison réussie de tirs à longue portée et à courte portée. Il est armé d'un puissant canon à plasma, capable de délivrer des tirs sporadiques, et de missiles à tête chercheuse qui poursuivent l'ennemi au moindre mouvement.

TIPS: 

- Utilisez des armes lourdes pour détruire efficacement les attaques de missiles.
- Maneuvering and cover are critical: une attaque directe et positionnelle réussit rarement.
- Leurs missiles peuvent être détruits en vol si vous agissez rapidement et avec précision.
