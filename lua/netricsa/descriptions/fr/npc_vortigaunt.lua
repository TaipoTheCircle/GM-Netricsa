VORTIGAUNT DE XEN


CLASS: Vortigaunt
METABOLISM: mammifère intelligent (réfugié Xen)
PERCEPTION: Race extraterrestre intelligente
SIZE: 6 pieds
ENDURANCE: moyen-élevé
HOSTILITY: faible (allié de la Résistance)
WEAPONS: éclair vert, rituels de guérison, connaissance de l'esprit de la ruche
REWARD: 520 FC
THREAT: haut

DESCRIPTION: 

Après la chute du Nihilanth, les Vortigaunts furent libérés de l'esclavage psychique. Beaucoup ont fui vers la Terre, où ils ont rejoint la Résistance humaine contre le Combine. Philosophiques et profondément spirituels, ils vénèrent la « Vortessence », un champ psionique partagé qui relie leur espèce. Ils apparaissent comme des alliés, guérissant les blessés et brandissant même leurs éclairs contre les soldats et les fourmilions du Combine. Autrefois ennemis, ils sont désormais des compagnons de confiance.

TIPS: 

- Protégez-les au combat ; ils constituent un soutien inestimable.
- Surveillez leurs rituels : ils peuvent soigner et renforcer les alliés.
- Ils en savent peut-être plus sur le destin et le Combine qu'ils ne le révèlent.
