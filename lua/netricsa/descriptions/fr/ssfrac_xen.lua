LES CRÉATURES DE XEN


Les créatures de Xen sont un empire d'êtres arrachés à leurs dimensions et fusionnés par une volonté extraterrestre. Autrefois un monde frontalier, Xen était un refuge pour ceux qui fuyaient les menaces de leur monde natal.

At the head of Xen stands the Nihilanth, a gigantic psionic overlord who has gathered all sentient inhabitants, from Vortigaunts to Bullsquids, under his control. His power is maintained not by fear, but by mind control: Les êtres de Xen ressentent les pensées des uns et des autres, vivant dans un seul nœud énergétique.
