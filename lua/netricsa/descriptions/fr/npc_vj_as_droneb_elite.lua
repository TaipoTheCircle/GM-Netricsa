DESCRIPTIF :

Les drones d'élite sont beaucoup moins courants au sein de l'Essaim « Bêta » que les spécimens standards. Ils sont facilement identifiables grâce à leur chitine noir comme charbon et à leurs trois yeux, capables de repérer leurs proies même dans l'obscurité presque totale.

Physiquement, le drone d'élite diffère peu de ses congénères, bien que sa musculature soit sensiblement plus dense et sa coque externe beaucoup plus résistante. De nombreux colons ont pris par erreur ces créatures pour une espèce distincte jusqu'à découvrir que des spécimens d'élite émergent des mêmes ruches et obéissent aux mêmes instincts.

Une théorie courante parmi les xénobiologistes est que la coloration noire signifie la maturité ou résulte d'une survie prolongée dans des conditions particulièrement dangereuses. En d’autres termes, rencontrer un drone d’élite signifie qu’il a déjà survécu à de nombreuses situations qui auraient dû le tuer.

Contrairement aux drones standards, les spécimens d’élite succombent rarement aux premiers coups et poursuivent leur progression même après avoir subi de graves blessures. Malheureusement, l’Essaim « Bêta » considère cette résilience comme une réussite exceptionnelle plutôt que comme une raison pour commencer à se préserver.

CONSEILS :

- Ne vous attendez pas à ce qu'il meure aussi vite qu'un drone standard.
- Donnez la priorité à l'élimination des spécimens d'élite face à une grande meute.
- L'obscurité joue à son avantage.
- Si un drone noir avec trois yeux brûlants vous charge, cela signifie que la phase d'avertissement d'introduction est déjà terminée.