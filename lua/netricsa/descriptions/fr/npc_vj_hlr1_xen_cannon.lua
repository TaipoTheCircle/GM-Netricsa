CANON DE SENTIELLE XEN


CLASS: Canon sentinelle Xen
METABOLISM: Énergie électromagnétique
PERCEPTION: Plateforme de défense autonome de Xen
SIZE: Variable
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Faisceau d'énergie pivotant
REWARD: 400FC
THREAT: Moyen-élevé

DESCRIPTION: 

Le Xen Sentry Cannon est une tourelle autonome déployée par les Alien Grunts au sein de Xen. Il comporte une base métallique avec quatre longues broches qui pivotent dans toutes les directions, libérant un formidable faisceau d'énergie bleue sur les intrus. Malgré sa conception extraterrestre, le canon se comporte comme une plate-forme d'armes artificielle : une fois détruit, sa structure s'effondre comme du béton.

TIPS: 

- Approchez-vous prudemment : le canon s'active lorsqu'il détecte un mouvement.
- Utilisez des armes à distance pour le détruire à distance de sécurité.
- Les explosifs sont particulièrement efficaces pour le neutraliser rapidement.
