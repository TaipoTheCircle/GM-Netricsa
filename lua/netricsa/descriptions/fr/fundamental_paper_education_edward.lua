EDWARD DE L'ÉDUCATION FONDAMENTALE SUR PAPIER


CLASS: Édouard
METABOLISM: Organisme anthropomorphe en papier
PERCEPTION: Inventeur-Bully
SIZE: ~3,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Appareils faits maison, griffes acérées, queue
REWARD: 30 FC
THREAT: Moyenne

DESCRIPTION: 

L'un des trois principaux intimidateurs de Paper School, avec Oliver et Zip. Reconnaissable à ses cheveux blancs hérissés, ses cornes noires courtes, ses lunettes rondes et sa queue noire ressemblant à une queue de diable. Il a les mains et les pieds noirs avec des doigts pointus. Il porte une chemise noire et blanche et de grandes bottes. Contrairement à la force brute d'Oliver, Edward est un tyran ingénieux et doué en technologie. Il a créé le drone qu'il a utilisé pour kidnapper Claire et la jeter dans la fosse. Il s'intéresse probablement aux sciences et à l'ingénierie. Comme ses complices, il est l'un des favoris de Miss Circinus et jouit de l'impunité. Il possède une résilience accrue.

TIPS: 

- Méfiez-vous de son intellect et de ses appareils technologiques.
