DYNJA L'ALCHIMISTE DE MAGNOR


CLASS: Dynja l'alchimiste
METABOLISM: Mammifère, ancienne beauté
PERCEPTION: Sorcière
SIZE: 4,6 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Crânes Magiques, Chaudron, Mains
REWARD: 50 FC
THREAT: Haut

DESCRIPTION: 

Elle était essentiellement l'une des plus belles filles de la galaxie, mais lorsqu'elle et sa sœur étaient prêtes à participer aux concours de beauté de Mental, elles se sont transformées en laides sorcières. Après cet incident, ils furent facilement enrôlés dans l'armée de Mental.

Dynja est la sœur jumelle de Yagoda ; ensemble, elles sont les sorcières les plus célèbres de la planète Magnor. Ils sont réputés pour leur nature sadique, la maîtrise de leur art et leur penchant pour l'alcool.

C'est une alchimiste qui maîtrisait l'art de créer des potions de clonage et les utilisait pour se reproduire (autoclonage). Voyant son potentiel, Mental lui a offert une réserve de vodka à vie en échange de ses services. Elle a accepté avec joie.

Dynja se cache au sol, invoquant des crânes magiques depuis son chaudron pour traquer ses ennemis.

TIPS: 

- Ils préfèrent attaquer autant que possible, plutôt que de se déplacer ou d'esquiver vos attaques.
- Les explosifs et les tirs de tireurs d'élite peuvent les éliminer facilement.
- Bien qu'un tir de fusil à double canon puisse les éliminer à bout portant, il est déconseillé de s'en approcher.
- Les crânes vous poursuivront, alors restez en mouvement si vous ne voulez pas avoir peur.
