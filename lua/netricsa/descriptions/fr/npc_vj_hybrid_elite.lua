COMBINEZ UN SOLDAT D'ÉLITE HYBRIDE OVERWATCH


CLASS: Combinez Overwatch Hybrid Elite
METABOLISM: Humain augmenté, mâle
PERCEPTION: Unité révolutionnaire d'élite polyvalente
SIZE: 5,9 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, grenades, bâton paralysant
REWARD: 1000FC
THREAT: Critique

DESCRIPTION: 

Le Combine Overwatch Hybrid Elite est le compromis du Combine entre élitisme ostentatoire et brutalité pratique. Il combine les meilleurs éléments des soldats d'élite standards et des unités alternatives, devenant ainsi un outil polyvalent pour supprimer la résistance dans n'importe quel environnement.

Leur armure est blanche, comme celle des soldats d'élite classiques, mais renforcée par des inserts composites noirs et un gilet pare-balles supplémentaire, donnant à leur silhouette un aspect plus lourd et menaçant. Ces soldats semblent avoir été rassemblés non pas pour des défilés, mais pour survivre là où d’autres élites se briseraient.

Le béret ne sert pas de décoration, mais de marqueur d'expérience et de droit à des décisions tactiques indépendantes.

Au combat, l'hybride agit de manière agressive et calculatrice. L'AR2 est utilisé pour la pression méthodique et la destruction de cibles, et les grenades sont utilisées pour détruire les couvertures et perturber les formations ennemies. Ces soldats mènent le plus souvent l’assaut, étant les premiers à entrer dans la zone de contact et les derniers à en sortir, laissant derrière eux le silence et l’odeur de l’ozone.

Alors que le soldat d'élite alternatif trompe les attentes et que le soldat standard submerge par son apparence, l'hybride fait les deux simultanément, ne laissant ni temps ni illusions à l'ennemi.

TIPS: 

- L'armure renforcée noire absorbe mieux les dégâts que l'armure standard.
- Les grenades sont utilisées avec précision et sang-froid.
