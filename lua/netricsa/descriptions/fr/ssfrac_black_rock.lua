ROCHE NOIRE


Black Rock est une grande société de recherche dans un univers alternatif, similaire à Black Mesa. Il se spécialise dans l'étude des artefacts extraterrestres et des créatures anormales, exploitant des navires de recherche pour mener des expériences.

L'entreprise dispose de sa propre force militaire, les « Bleus », qui gardent les installations, escortent les opérations et assurent la sécurité des navires de recherche. Ces soldats agissent comme une organisation militaire d'élite au sein de Black Rock, prêts à défendre l'entreprise contre toute menace et à contrôler les matériaux de valeur.
