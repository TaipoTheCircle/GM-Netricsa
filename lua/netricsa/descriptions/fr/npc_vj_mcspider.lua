ARAIGNÉE DU MONDE MINECRAFT


CLASS: Araignée
METABOLISM: Arthropode
PERCEPTION: Hostile
SIZE: 2,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Variable
WEAPONS: Morsures, sauts, escalade de murs
REWARD: 120 FC
THREAT: Moyenne

DESCRIPTION: 

L'araignée est un cauchemar classique d'Overworld. Pendant la journée, il se comporte presque décemment et ignore généralement tout le monde, comme s'il se faisait passer pour un élément paysager ordinaire. Mais à la tombée de la nuit, il se souvient qu’il s’agit en fait d’un prédateur et commence à attaquer activement.

La principale caractéristique de l’araignée est sa capacité à escalader des surfaces verticales. Les murs, les clôtures et les maisons ne sont pas un obstacle, mais une invitation.

Parfois, les araignées apparaissent avec des « passagers » supplémentaires, des squelettes, qui forment le fameux jockey d'araignées, ce qui augmente considérablement le niveau de problèmes par mètre carré.

TIPS: 

- Pendant la journée, l'araignée est neutre, vous pouvez attaquer en premier sans risque.
