COMBINER UN SOLDAT DE MÊLÉE OVERWATCH


CLASS: Combiner Overwatch Soldat de mêlée
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner Punisher de mêlée
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Très élevé
WEAPONS: Bâton paralysant modifié
REWARD: 400FC
THREAT: Moyenne

DESCRIPTION: 

Le Combine Overwatch Melee Soldier est une unité Combine rare mais quelque peu méchante, conçue pour les situations où le tir est inapproprié ou impossible. Il combat exclusivement au corps à corps, en s'appuyant sur la vitesse, l'agressivité et un bâton paralysant modifié capable d'étourdir, de briser les os et de paralyser les cibles.

This soldier's armor is deliberately simplified and practically nonexistent. The Combine sacrificed portable radio mobility: le soldat est plus rapide que les soldats standards, couvre facilement de courtes distances et peut rapidement engager d'autres soldats. Un casque blanc avec une visière bleue couvre le visage.

These soldiers were most often used in prisons, narrow corridors, and riot suppression. Their task is simple: atteindre la cible et les battre.

Malgré leur faible armure, les soldats de mêlée sont mortels au corps à corps.

TIPS: 

- Ne le laissez pas s'approcher trop près, son bâton paralysant le neutraliserait rapidement.
- Utilisez la distance et les armes à feu.
- Vulnérable en raison du blindage.
- Dans les espaces restreints, il est plus dangereux que les soldats standards.
