BLASTER MARIN


WEAPON: Blaster
AMMO: Aucun (énergie infinie)
ROF: ~360 tours/min

DESCRIPTION: 

Le Blaster est le pistolet à énergie standard offert à chaque Marine. Il tire de petites rafales d'énergie d'une simple pression sur la gâchette ; maintenir la gâchette enfoncée charge un tir plus fort. Une LED sur le panneau arrière s'allume lorsqu'elle est complètement chargée. Comprend également une lampe de poche sous le canon pour éclairer les zones sombres.

NOTES: 

- Le tir chargé inflige des dégâts beaucoup plus élevés (environ 50), mais nécessite du temps pour charger.
- N'utilise pas de munitions et ne nécessite pas de rechargement - énergie infinie.
- La lampe de poche intégrée aide dans les zones sombres sans changer d'arme.
