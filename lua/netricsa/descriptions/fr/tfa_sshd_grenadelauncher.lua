LANCE-GRENADE MKIII


WEAPON: Lance-grenades MKIII
AMMO: Cartouches hautement explosives de 40 mm
ROF: variable

DESCRIPTION: 

Un lance-grenades d'infanterie fiable avec une vitesse de lancement réglable. MKIII est conçu pour lancer des grenades hautement explosives au-dessus des obstacles et dans les coins, tout en réduisant le risque que l'opérateur soit blessé par le projectile. Un détecteur d'impact intelligent fait exploser la grenade lors de l'impact sur une cible réelle, mais pas sur un mur.

NOTES: 

- Appuyez sur la gâchette pour un lancement lent ou maintenez-la enfoncée pour délivrer à des distances plus longues.
- Les explosions à large portée sont efficaces contre de grandes masses d'ennemis.
- Faites rebondir les grenades dans les coins ou lancez-les par-dessus les murs.
