COMBINER OVERWATCH ELITE MEDIC


CLASS: Combiner Overwatch Elite Medic
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner un médecin de combat sur le terrain
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Combinez Elite Medic Gun (rayon de guérison et tir alternatif de mitrailleuse) et des flacons de guérison
REWARD: 1200FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine Overwatch Elite Medic n’est pas un sauveur, mais un membre du personnel de soutien d’un abattoir. Il porte une combinaison blanche avec des rayures noires sur les jambes et des empiècements noirs sur les épaules. Les rayures orange sur les épaules forment une croix - le symbole médical Combine, qui est plus un avertissement qu'une promesse d'aide.

Le casque est équipé d'un respirateur et de grandes lentilles bleues convexes, donnant au médecin une apparence d'insecte. Un tube s'étend du respirateur jusqu'à un sac médical situé à l'arrière, où est stocké le contenu des larves de fourmilions.

Le Medicannon du Combine tire un rayon de guérison qui délivre des œufs de larves de fourmilion transformés aux alliés. Le Combine extrait les sucs curatifs des larves et les utilise comme biocarburant pour la régénération des tissus.

Des flacons de guérison supplémentaires sont fixés sur le côté du médecin et sont utilisés pour la récupération d'urgence des soldats. Le mode alternatif du médecin active une mitrailleuse intégrée, permettant au médecin de se défendre et d'achever les ennemis directement sous le rayon de guérison.

Au combat, le Medic d'élite s'engage rarement dans des attaques directes. Il reste derrière les unités d'élite, les transformant en soldats quasi immortels pendant qu'il reste à l'abri.

TIPS: 

- Tuez d'abord le médecin, sinon les soldats d'élite deviendront pratiquement invincibles.
- Son armure est plus faible que celle des élites combattantes, mais il est généralement entouré de gardes.
- Son rayon de guérison révèle sa position avec une lueur vive, alors utilisez-le.
- Son tir secondaire est dangereux à courte portée, alors ne le sous-estimez pas.
