GRENADE À PLASMA


WEAPON: Grenade à plasma
AMMO: Noyau plasma auto-chargeant
ROF: À usage unique / jeté
WEIGHT: 1,6 livres

DESCRIPTION: 

Développée par les scientifiques de Lord Anton, la Grenade à Plasma est une arme expérimentale alimentée par un noyau électrique autonome. Lors de la détonation, il libère une violente décharge de plasma qui non seulement choque et immobilise les ennemis pendant une courte période, mais provoque également des brûlures d'énergie dévastatrices.

Instable et imprévisible, cette arme est considérée comme trop dangereuse pour être distribuée en masse, car même une mauvaise manipulation lors de l'armement pourrait conduire à une décharge accidentelle. Seules les unités fiables des forces d'Anton sont autorisées à utiliser une telle technologie : ses effets sur les cibles organiques et mécaniques en font l'un des appareils les plus redoutés sur le champ de bataille.

NOTES: 

- Paralyse temporairement les ennemis pris dans son rayon d'explosion.
- Ne le laissez PAS tomber entre les mains de l'ennemi.
- Efficace contre l'infanterie et la robotique légère.
