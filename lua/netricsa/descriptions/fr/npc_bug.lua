PUNAISE DE BRONZE DE ZENOZOIK


CLASS: Insecte de bronze
METABOLISM: Insecte, xénomorphe géant
PERCEPTION: Punaise prédatrice des forêts
SIZE: 1,9 pi
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Mandibules puissantes, longues et incurvées, capables de couper la chair
REWARD: 80 FC
THREAT: Haut

DESCRIPTION: 

Un insecte géant sauvage, ressemblant à un insecte terrestre en bronze, mais nettement plus gros et plus agressif que son cousin. Il a une carapace chitineuse brun foncé ou bleu violet, une paire d'ailes transparentes, de longues pattes fines et est couvert de poils fins sur la tête. Leur principale caractéristique distinctive est leurs énormes mandibules cornées et saillantes, capables de trancher facilement leurs proies. Leur habitat naturel est constitué par les forêts où vivent les corvidés, mais ces insectes sont capables de migrer vers d'autres endroits, y compris les déserts. Ils font preuve d'une agression sélective inexplicable, attaquant exclusivement Geth et ignorant toutes les autres créatures. Ce ne sont pas des animaux de compagnie dressés. Ils agissent toujours en meute, coordonnant les attaques aériennes.

TIPS: 

- Un filet ordinaire ne fonctionnera pas ; prenez une arme et tirez dessus ; ils sont plus maniables en vol.
- Ils attaquent toujours en meute ; essayez de toujours vous éloigner d'eux et de les éviter.
