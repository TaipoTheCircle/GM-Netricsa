VACHE MOOSHROOM BRUN DU MINECRAFT OVERWORLD


CLASS: Vache Mooshroom brune
METABOLISM: Mammifère à sang chaud
PERCEPTION: Animal sauvage/fongique
SIZE: 5,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 150 FC
THREAT: Faible

DESCRIPTION: 

La vache Brown Mooshroom est une espèce inhabituelle de créature du biome champignon, une fusion de chair de vache biologique et d'un écosystème fongique. Des champignons bruns avec des taches beiges poussent sur son dos et son corps est recouvert de peau de vache tachetée de beige rougeâtre.

TIPS: 

- Les soupes aux champignons sont un moyen rapide de retrouver santé et satiété.
- Après avoir coupé les champignons, elle se transformera en une vache ordinaire.
