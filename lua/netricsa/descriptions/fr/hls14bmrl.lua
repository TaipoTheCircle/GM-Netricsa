XEN, L'USINE DE NIHILANTH


After passing through the portal, we arrived at one of the most critical sites in Xen: l'Usine de Nihilanth. Il ne s’agit pas d’une simple forteresse, mais d’une machine vivante – un lieu où la technologie extraterrestre et la matière organique fusionnent en un cycle de production sans fin.

L'atmosphère est étouffante. Chaque mur palpite comme de la chair, les conduits palpitent d'énergie et l'air vibre avec le bourdonnement constant des machines. Les Vortigaunts, liés par des chaînes psychiques, sont obligés de faire fonctionner des dispositifs massifs : élever des plates-formes, alimenter des conduits et entretenir les cuves où se développent des armées entières.

C'est l'origine de l'infanterie extraterrestre. Les soldats de choc, les grognements et même les gardiens en armure sont élevés ici, façonnés dans des biocapsules et préparés pour une guerre sans fin.

As we entered, his voice struck the air: 

"Nous sommes tous des esclaves... Nous sommes tous des esclaves... Nous sommes tous..."

Nihilanth lui-même connaît la vérité : il est autant prisonnier que ceux qu'il asservit.
