SHELEZYAKA


PLANET: Shelezyaka
TYPE: Planète terrestre, planète usine
CLIMATE: Espace sans air, fluctuations de température
MOONS: Inconnu
TERRAIN: Une plaine sulfurique plate couverte de poussière
LOCATION: Une partie lointaine de la Voie Lactée, hors des sentiers battus, près de la planète Sheshineru
NATIVE RACES: Robots (descendants de l'équipage d'un navire robotique écrasé), Robobeasts
ALLIANCE: Civilisation robotique indépendante
LOCATIONS: Port spatial du désert, colonies de robots, habitats de Robobeast

DESCRIPTION: 

Shelezyaka est une planète qui est un exemple clair de la transformation complète (et de l’épuisement) du monde par une civilisation technologique. C'était autrefois un monde ordinaire avec une atmosphère, de l'eau, des mers, des montagnes, de la végétation et des animaux. Il y a de nombreuses années, un navire automatisé transportant des robots du service universel obsolètes s'est écrasé ici.

The surviving robots began to actively reproduce, using the local mineral resources. During the process of industrialization, they completely depleted the planet's resources: l'oxygène était brûlé dans les fourneaux, l'eau était éliminée (pour éviter la rouille et utilisée pour refroidir les moteurs), les montagnes étaient rasées et les mers asséchées. À présent, Shelezyaka est devenue une sphère grise et plate, recouverte de poussière, sans atmosphère ni signe de vie.

A famous entry in the galactic directory reads: "Pas de ressources minérales. Pas d'eau. Pas de végétation. Habité par des robots.".

Les indigènes sont les descendants de ces mêmes robots. Ils sont courts, lourds, solides et durables (« construits pour durer »). Ils sont simples, mais capricieux et facilement offensés. Chacun a un voyant lumineux sur le front. Ce sont des artisans qualifiés, mais leur civilisation reste à un niveau technologique primitif, sans ordinateurs ni agriculture. Leur mets préféré est l’huile de tournesol.

Lorsque la faune de la planète s'est éteinte en raison de conditions inappropriées, les robots ont créé des animaux mécaniques pour combler le vide. Plus tard, lorsque les pièces ont manqué, les animaux robotiques ont été démantelés et ils se sont retirés dans un état semi-sauvage. Ils sont divisés en types « souris » et « chat » ; tous se déplacent sur des roues et peuvent grincer, tandis que les robots-chats peuvent lécher avec une langue de fer. Ils sont apprivoisés.

KEY LOCATIONS: 

- Deserted Cosmodrome: Le seul site d'atterrissage, toujours poussiéreux et désert. Au fil des années, un seul visiteur est venu ici : le pirate de l'espace Rat.
- Robot Settlements: Zones habitées par les habitants indigènes de la planète. Les robots adorent donner des conseils et, lorsqu’ils ne travaillent pas, préfèrent s’asseoir ou s’allonger.
- Robo-beast habitats: Territoires habités par des animaux mécaniques sauvages. Ici, vous pouvez rencontrer des troupeaux de robots-souris à roues ou de robots-chats furtifs.
