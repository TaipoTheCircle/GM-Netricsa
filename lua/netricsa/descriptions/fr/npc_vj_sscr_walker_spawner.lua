BIO-MÉCANOÏDE, GAGNEUR


CLASS: Bio-mécanoïde, générateur
METABOLISM: cyborg endotherme
PERCEPTION: génétiquement programmé
SIZE: 20 pieds debout
ENDURANCE: moyen
HOSTILITY: haut
WEAPONS: nids de larves des marais
REWARD: 4500FC
THREAT: haut

DESCRIPTION: 

Un mécanisme biologique cultivé dans les bio-réservoirs de Mental. Son génome est programmé pour lui donner des pièces mécaniques cultivées biologiquement et des fentes latérales où des nids de larves de marais améliorés cybernétiquement sont attachés et directement connectés à son système nerveux.
Ajusté pour attaquer sans réfléchir. On suppose que ses nids sont liés spatialement et dimensionnellement à la plus grande frayère de larves de marais de la galaxie, de sorte qu'il est capable de libérer des quantités infinies de larves de marais, jusqu'à ce qu'elles soient détruites.

TIPS: 

- S'il n'est pas éliminé rapidement, il envahira bientôt tout l'endroit avec des larves des marais.
- Une fois tué, il libérera une plus grande quantité de larves des marais à sa mort, alors faites attention.
