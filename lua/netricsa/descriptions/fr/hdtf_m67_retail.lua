GRENADE À FRAGMENTATION M67


WEAPON: Grenade à main à fragmentation M67
AMMO: Remplissage explosif : Composition B (6,5 oz / ≈184 g)
ROF: délai de 4 à 5 s
WEIGHT: 400g

DESCRIPTION: 

La M67 est la grenade à fragmentation standard américaine « baseball » introduite à la fin des années 1960. Son corps sphérique en acier contient une doublure de fragmentation pré-encochée et environ 6,5 oz (≈184 g) d'explosif de composition B ; Lors de la détonation, les fragments de corps produisent l'effet mortel/éclat d'obus. La grenade est armée en retirant la goupille de sécurité et en relâchant le levier de sécurité (cuillère), après quoi la fusée pyrotechnique M213 brûle pendant environ 4 à 5 secondes avant la détonation.

NOTES: 

- Rayon mortel communément cité ~5 m ; perte/rayon effectif ~15 m ; de petits fragments peuvent être projetés à des centaines de mètres dans de rares cas.
- Distance de projection moyenne ≈30-35 m ; soyez prudent lorsque vous lancez des arcs dans des espaces confinés pour éviter des blessures amicales ou personnelles.
- Classée comme munition antipersonnel/à effet de zone - efficace pour dégager la couverture et supprimer l'infanterie.
