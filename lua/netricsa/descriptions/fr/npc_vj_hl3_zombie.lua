ZOMBIE DE CRABE DE TERRE, ARCTIQUE


CLASS: Crabe-tête zombie, Terre (Arctique)
METABOLISM: Hôte parasite, adapté au froid
PERCEPTION: Entité zombifiée
SIZE: ~6-7 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Griffes, morsure, saut de crabe
REWARD: 220 FC
THREAT: Moyenne

DESCRIPTION: 

Le crabe zombie arctique est un humain infesté par un crabe terrestre laineux et adapté au froid constant. Le parasite conserve le contrôle du système nerveux de l'hôte, mais le froid modifie considérablement le comportement et l'apparence de la créature.

Le corps de l'hôte est souvent enveloppé dans des morceaux de vêtements d'hiver, des uniformes gelés ou des costumes fusionnés à la chair. Le crabe, recouvert d'une épaisse fourrure, s'enfouit profondément dans le crâne, se protégeant ainsi davantage du froid.

TIPS: 

- Visez le crabe : la fourrure le protège partiellement, mais les coups précis restent efficaces.
