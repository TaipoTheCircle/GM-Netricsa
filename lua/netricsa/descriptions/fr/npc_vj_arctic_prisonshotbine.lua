COMBINER LE SHOTGUNNER DE LA PRISON ARCTIQUE D'OVERWATCH


CLASS: Combinez le fusil de chasse de la prison arctique d'Overwatch
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner l'unité d'assaut punitif
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Fusil de combat Spas-12, Grenades
REWARD: 700FC
THREAT: Haut

DESCRIPTION: 

L'Overwatch Arctic Prison Soldat with Shotgun est un soldat du Combine initialement affecté au complexe Nova Prospekt mais temporairement redéployé dans les régions arctiques. Cette pratique est utilisée lorsque les zones carcérales connaissent un excès de main-d’œuvre, tandis que les secteurs éloignés souffrent d’une pénurie d’unités d’assaut.

Après retraitement dans le système Nova Prospekt, leur psychisme est presque complètement supprimé et l'obéissance est assurée par une surveillance constante et des impulsions douloureuses. Au combat, ils agissent sans hésitation ni instinct de conservation.

Il porte un camouflage arctique blanc et gris, modifié par rapport à l'équipement de prison standard. La visière du casque brille en orange, une caractéristique des soldats de prison, révélant leur origine même dans l'obscurité enneigée. "Nova Prospekt" est clairement écrit sur son dos en bleu, servant à la fois de marquage et de rappel de l'endroit où le soldat sera renvoyé s'il désobéit.

Comme les autres fusils de chasse Combine, ce soldat est conçu pour le combat rapproché. Dans les stations arctiques, les tunnels de glace et les complexes polaires abandonnés, il est utilisé pour un nettoyage brutal, la répression des émeutes et l'élimination de cibles là où l'infanterie standard est considérée comme inefficace.

TIPS: 

- Ne comptez pas sur sa prudence, les soldats des prisons y vont souvent de front.
- La visière orange est visible, mais il réagit plus vite qu'il n'y paraît.
- Le panneau Nova Prospekt signifie que le soldat n'a rien à perdre.
- Dans les espaces confinés, le fusil de chasse le rend mortel.
