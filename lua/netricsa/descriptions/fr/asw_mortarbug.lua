Bug de mortier d'essaim


CLASSE : Insecte de mortier d'essaim
MÉTABOLISME : Xénobiologique
RÔLE : Unité d'artillerie
TAILLE : 9,2 pieds
DURABILITÉ : Élevée
HOSTILITÉ : Élevée
ARMES : Projectiles biologiques explosifs
RÉCOMPENSE : 700 FC
MENACE : Élevée

DESCRIPTIF :

Le Mortar Bug est une variante d'artillerie spécialisée du Swarm, conçue pour engager des cibles à longue portée. Au lieu des glandes acides typiques des Rangers, cette créature utilise des organes spécialisés qui produisent des charges explosives instables.

Lors de l'attaque, le Mortar Bug lance une volée de trois projectiles selon une trajectoire à arc élevé. Lors de l'impact, ils adhèrent à la surface et explosent au bout de quelques secondes. Cela permet à l'Essaim de restreindre les mouvements de l'ennemi, de bloquer les passages et de forcer ses proies à sortir de leur abri.

Malgré sa taille imposante et sa carapace épaisse, le Mortar Bug est mal adapté au combat rapproché. Sa principale menace réside dans son armement à longue portée ; par conséquent, ces créatures restent généralement derrière les principales forces de l'Essaim, protégées par les drones et autres sous-espèces agressives.

La peau du Mortar Bug est saturée de composés biologiques caustiques. Même après la mort, ses tissus restent dangereux au toucher pendant un certain temps, ce qui fait du prélèvement des carcasses une tâche extrêmement impopulaire parmi les chercheurs sur le terrain.

CONSEILS :

- Ne vous attardez pas à proximité des projectiles tombés.
- Éliminez les insectes de mortier avant qu'ils ne puissent obtenir une position avantageuse.
- Soyez prudent lorsque vous utilisez une couverture contre les arcs de feu.
- Si trois sphères organiques suspectes apparaissent soudainement sur le sol, il est préférable de quitter immédiatement les lieux.