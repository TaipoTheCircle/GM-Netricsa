SPITTER RAPTOR DE LA PLANÈTE TERRAFORMÉE


CLASS: Raptor cracheur
METABOLISM: Prédateur, théropode
PERCEPTION: Utahraptor génétiquement modifié
SIZE: ~8,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrêmement élevé
WEAPONS: Griffes, dents, crachats venimeux, pointes dorsales
REWARD: 600 FC
THREAT: Haut

DESCRIPTION: 

Cette espèce est apparue lorsque la société Mendel-Gruman a décidé que les Utahraptors normaux de la planète n'étaient pas suffisants et s'est tournée vers le génie génétique. La société souhaitait créer une arme de destruction massive plus efficace en produisant des rapaces capables de cracher du venin tout en conservant toutes les habitudes des prédateurs sauvages.

Le rapace Spitter se distingue de ses frères par sa coloration grise, son absence totale de plumes et une rangée de courtes pointes dépassant le long de son dos. En raison de ces pointes et du sac situé sous sa gorge, on l'a initialement appelé à tort un bébé spinosaure.

Ils ont un sac jaune et coriace sous le cou qui stocke les toxines. Lorsque le sac est plein, il gonfle visiblement, avertissant un tireur attentif que le plaisir est sur le point de commencer.

TIPS: 

- Ne reste pas immobile. La broche vole en ligne droite.
- En combat rapproché, il est aussi dangereux qu'un rapace normal. Ne vous laissez pas encercler.
