CYBORG ARACHNOÏDE, ADULTE DU SCORPIUS


CLASS: Arachnoïde cyborg, adulte
METABOLISM: Arthropode ectotherme et amélioré cybernétiquement
PERCEPTION: Race sensible primitive
SIZE: 20-25 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Pistolet à chaîne, piqûre de queue
REWARD: 8500FC
THREAT: Haut

DESCRIPTION: 

Un guerrier sacré issu d'une race primitive a évolué à partir d'arthropodes. Extrêmement hostile en raison de leurs convictions religieuses.

Porte un Chaingun avec un réapprovisionnement en munitions dans une main. L'exosquelette couvre tout le corps de l'arachnoïde et offre une bonne protection contre les balles, les explosifs et le feu.

Les adultes sont des guerriers adultes, dotés d'un exosquelette rouge fort et "mûr" et d'une taille beaucoup plus grande.

Un arachnoïde cyborg adulte - un Scorpius Holy Warrior, volontairement (ou non) amélioré par la technologie de Mental.

Une partie du corps a été remplacée par des segments mécaniques, tels que des membres renforcés, des servos, des stabilisateurs de recul et un exosquelette. Cette armure peut résister à plus de dégâts qu’un exosquelette arachnoïdien standard.

TIPS: 

- Piqûre dangereuse - ne vous approchez pas de trop près.
- Tireront sur leurs propres rangs avec fureur.
- Le combat rapproché est une mauvaise idée, une très mauvaise idée.
