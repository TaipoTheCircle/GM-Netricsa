LOBBY BLACK MESA, DÉMO DE SILO


Sam, nous sommes dans le silo. Pas une version live, pas une version complète, une démo. Un endroit où Black Mesa testait autrefois des moteurs de fusée et met désormais nos nerfs à rude épreuve.

Ils ont grandi ici. Ou descendu ici. Ou ont été emportés par les vents d’un effondrement interdimensionnel. Trois créatures de classe Tentacule. Bien que les tentacules eux-mêmes constituent un organisme unique, les tentacules fonctionnent séparément.

Problem: pas de lance-flammes. Pas de moteur-fusée pour les viser. Rien qui puisse les faire frire. Et sans feu, ces créatures sont immortelles. Cela ne sert à rien de faire quoi que ce soit, c'est écrit dans les protocoles. Et les protocoles de Black Mesa sont généralement écrits avec le sang de ceux qui les ont ignorés.

La seule façon qui a fonctionné (parfois accidentellement, à cause de bugs et du désespoir) est d'obtenir le bon timing et de tirer continuellement avec le pistolet à faisceau à la racine des tentacules. Pas le tronc, pas les ventouses, mais là où la chair rencontre le sol. Ils y ont une sorte de centre nerveux. Cela pourrait fonctionner. Ce n’est peut-être pas le cas. Mais il n'y a pas le choix.

Actuellement, trois gardes les combattent. Ils n’ont pas envie de mourir, mais ils n’ont nulle part où se retirer. Ils tirent avec tout ce qu'ils ont. Les tentacules s'en moquent. Les gardes sont de la viande. Ce n'est qu'une question de temps avant qu'ils ne tombent.

Et il y a des H.E.C.U. enfermés dans l'une des pièces.

Ils sont juste assis à couvert et attendent. Pour quoi? Peut-être des renforts. Peut-être notre mort. Peut-être que tout se résoudra tout seul. Mais cet endroit ne mène nulle part. C'est une impasse. Au-delà, il n’y a que des murs, des tuyaux et des tentacules.

La seule issue est de tuer les créatures. Ou mourir.
