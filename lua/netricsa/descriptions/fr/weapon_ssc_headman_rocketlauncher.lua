LANCE-MISSILE FUSÉE


WEAPON: Lanceur de missiles-fusées
AMMO: Boulons Technomagiques
ROF: ~90 missiles/min

DESCRIPTION: 

Armement rudimentaire mais omniprésent parmi l'infanterie morte-vivante de Mental, le lance-missile canalise l'énergie arcanique en une volée de missiles pétillants. Produite en série et presque en apesanteur, chaque unité exploite un réservoir caché de flux magique, bien qu'un éclair à la fois.

NOTES: 

- Faibles dégâts par tir, donc peu fiable pour les cibles hautement prioritaires ou blindées.
