MESA NOIR, SORTIE VERROUILLÉE


So, some "good news" from one of the surviving guard: apparemment, il y a une sortie vers la surface juste au-dessus de nous. La lumière du soleil, l’air frais, plus de presses à convoyeurs, ce genre de choses. Ça a l’air génial, non ? Eh bien, naturellement, il y a un piège.

The doors are sealed with high-level clearance. Seuls les scientifiques de Black Mesa possèdent les codes, ce qui signifie que nous ne pouvons pas simplement enfoncer la porte et sortir valsé. Nous devrons les retrouver si nous voulons revoir le ciel.

Passons maintenant aux mauvaises nouvelles : H.E.C.U. les forces rampent déjà dans ce secteur. Ils ne sont pas là pour sauver qui que ce soit. Ils ont ordre de nettoyer, et au cas où vous ne l'auriez pas remarqué, nous faisons partie du désordre.

So, here's the plan: 

Step one: trouvez les scientifiques avec l'autorisation.

Step two: obtenez les codes de sortie.

Step three: sortez avant que les soldats décident que nous sommes plus beaux comme cible d'entraînement.

Oh, et une dernière remarque : ne vous fiez pas trop à l'optimisme du garde. "Juste au-dessus de nous", en termes de Black Mesa, pourrait encore signifier un demi-mile de pièges mortels, d'infestations extraterrestres et probablement une autre porte verrouillée. Mais bon, c'est ça qui rend la vie passionnante, non ?
