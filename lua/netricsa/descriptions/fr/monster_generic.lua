DESCRIPTION: 

Monster Generics sont les toiles vierges de la vie prédatrice de Xen et Black Mesa. Personne ne connaît leur vraie nature, car ils existent principalement comme modèles dans le monde – pourtant, lorsqu'ils « apparaissent » dans le monde réel, ils prennent la forme de véritables monstres, obéissant aux règles de leur environnement.

They can be anything: un extraterrestre vif, une créature qui se précipite ou un hybride bizarre auquel personne ne s'attendait.

Certains chercheurs murmurent que ces créatures sont les marionnettistes sous-jacents de tous les autres monstres et acteurs génériques – les archétypes invisibles à partir desquels chaque prédateur à griffes, à crocs ou à tentacules est copié. En rencontrer un, c'est comme rencontrer une créature à la fois générique et particulièrement terrifiante.

TIPS: 

- Expect the unexpected: l'apparence et les attaques varient énormément.
- Exploitez les pièges environnementaux, car les Monster Generics ont tendance à suivre tout le monde littéralement.
- Ne présumez pas que vous êtes familier : chaque rencontre peut être différente.
