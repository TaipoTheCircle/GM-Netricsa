NAPALM LAUNCHER


WEAPON: Lanceur de Napalm
AMMO: Boules de napalm (sphères de feu)
ROF: ~30 tirs/min

DESCRIPTION:

arme qui tire des boules de feu qui s'enflamment en heurtant quelque chose. Utilisé pour le refus de zone et le contrôle des mouvements dans les couloirs ou les points d'étranglement. Pas très efficace contre les cibles blindées mais utile pour les ennemis faibles ou moyens.

NOTES:

- Projectile de type balistique - la visée doit tenir compte de l'arc et de la chute.
- Ennemis faibles ou lourdement blindés ; mieux utilisé pour supprimer plutôt que pour finir.