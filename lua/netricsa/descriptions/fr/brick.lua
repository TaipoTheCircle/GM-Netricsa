BRICK, LE JEUNE FRÈRE DE BRICK


Sam, on dirait que nous sommes tombés sur un parent minimaliste de ces demeures en briques que nous avons explorées. Si la version précédente était un « village urbain » avec une piscine et des immeubles à deux étages, celle-ci est un carré de désespoir de banlieue austère.

Deux maisons, de la brique, de l'herbe et une absence totale de trace de civilisation. Ils n'ont même pas creusé d'étang.

Before us is a smooth, green lawn, on which stand exactly two buildings: 

Deux maisons carrées en brique. Sans portes. C'est déjà une tradition. Apparemment, l'architecte s'est inspiré du concept selon lequel « l'hospitalité est une illusion, les ouvertures sont la réalité ».

Tous deux sont en brique rouge, tous deux carrés, tous deux silencieux.

Rien d'autre. Pas de clôture, pas d'arbres, pas de voitures. Juste la maison n°1, la maison n°2 et vous.

First House (aka "Staircase"): 

Il y a un escalier vers le deuxième étage. Ça marche, ça mène. Elle n'est ni cassée, ni clouée, ni gardée par un gnome. Juste des escaliers.

Au deuxième étage, il y a une fenêtre. Pas de verre. Un courant d'air, une vue, l'opportunité de se pencher et de crier : "Qui es-tu !? Allez à f..." Ou tout simplement de vous pencher et de vous faire tirer une balle dans le front. Une fenêtre est une chose.

Second House (aka "Empty"): 

Il n'y a pas de deuxième étage. Aucun.

Il n'y a rien à l'intérieur.

Cet endroit est de pure géométrie. Deux maisons, herbe, ciel. Pas de distractions, pas de fournitures pour Sophia, pas de piscines. Juste vous, l'ennemi et une brique. Ici, tout dépend de votre rapidité de réaction et de votre capacité à utiliser l'unique fenêtre du deuxième étage.

Nous pouvons commencer.
