SLENDERMAN, L'HOMME MINCEUR (20 DOLLARS )


CLASS: Slenderman, l'homme mince (20 dollars)
METABOLISM: Non biologique
PERCEPTION: Légende urbaine
SIZE: 12 pieds
ENDURANCE: Incommensurable
HOSTILITY: Absolu
WEAPONS: Tentacules, téléportation, saut, télépathie, manipulation de la mémoire, simple présence
REWARD: 2009 FC
THREAT: Absolu

DESCRIPTION: 

C'est un homme maigre vêtu d'un costume funéraire noir avec une chemise blanche. Sa peau est pâle, ses membres sont anormalement longs et peuvent se plier dans n'importe quelle direction. Des tentacules ou appendices noirs et flexibles poussent parfois sur son dos. Et surtout, il est complètement sans visage. Là où devrait être son visage, il y a un vide lisse. Pas d'yeux, pas de nez, pas de bouche.

Slenderman voyage par téléportation. Il apparaît et disparaît silencieusement. Au contact, il provoque de graves maux de tête, des interférences électroniques et un sentiment de terreur inexplicable. Il peut effacer ou modifier des souvenirs, transformer des personnes en mandataires (marionnettes) et emmener des enfants dans un lieu inconnu.

TIPS: 

- Si tu le vois, il te voit déjà. Courir ne sert à rien, il se téléporte.
- Ne le regarde pas dans les yeux. Il n'en a pas.
- L'électricité statique sur votre appareil photo ou votre téléphone est le premier signe qu'il est à proximité. Éteignez votre appareil et partez.
- La seule façon de survivre est de ne pas penser à lui. Mais maintenant tu penses déjà à lui.
