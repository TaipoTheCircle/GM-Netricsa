POGON


PLANET: Pogon
TYPE: Planète terrestre, Cryo-monde, Base militaire
CLIMATE: Gel profond, gel sévère, vent froid constant
MOONS: Inconnu
TERRAIN: Hautes chaînes de montagnes, plaines enneigées, plateaux glacés
LOCATION: Quelque part entre les planètes Stroggos et la Terre
NATIVE RACES: Strogg (garnison)
ALLIANCE: Empire Strogg
LOCATIONS: Base du Général Faron, Cols de Montagne, Plaines Enneigées

DESCRIPTION: 

A harsh, snowy world located at a strategic point between Stroggos and Earth. Extreme climatic conditions make this planet a natural fortress: de fortes gelées, des vents violents et des chaînes de montagnes abruptes éliminent pratiquement toute possibilité de raids aériens ou d'attaques d'artillerie réussies depuis l'espace.

Le paysage de la planète est formé de hautes montagnes inaccessibles, entre lesquelles s'étendent d'interminables plaines enneigées et des plateaux glacés. Les blizzards constants et la faible visibilité rendent la navigation extrêmement difficile, et toute trace de leur présence est rapidement masquée par la neige.

C'est ici, au cœur de la chaîne de montagnes, que les Strogg ont établi l'une de leurs principales bases militaires. Le commandement de l'installation est exercé par le général Faron, un char Strogg d'élite installé sur une plate-forme antigravité, une formidable unité de combat parfaitement adaptée à la guerre dans des conditions hivernales rigoureuses.

Le choix de Pogon comme base a été dicté non seulement par son emplacement stratégique en route vers la Terre, mais aussi par la protection naturelle apportée par le climat. Alors que les blizzards et les températures glaciales entravent toute tentative d'invasion, les Strogg mènent leur travail sans entrave dans des complexes bien abrités et chauffés creusés dans la roche.

KEY LOCATIONS: 

- General Faron's Base: Le principal bastion des Strogg sur la planète, un complexe de tunnels et de hangars cachés au plus profond de la chaîne de montagnes. De là, ils coordonnent leurs opérations et contrôlent le secteur.
- Mountain Passes: Les seules voies potentielles de pénétration au sol vers la base sont constamment patrouillées et minées. Impraticable la majeure partie de l'année à cause des congères.
- Snowy Plains: Des espaces ouverts au pied des montagnes, utilisés pour tester des équipements dans des conditions météorologiques extrêmes et lancer des drones de reconnaissance.
