ZIP DE L'ÉDUCATION PAPIER FONDAMENTALE


CLASS: Fermeture éclair
METABOLISM: Organisme dragon anthropomorphe en papier
PERCEPTION: Dragon intimidateur
SIZE: ~3,2 pieds
ENDURANCE: Haut
HOSTILITY: Trompeur
WEAPONS: Claws, spiked dragon tail, pyrokinesis
REWARD: 40 FC
THREAT: Très élevé

DESCRIPTION: 

Un étudiant de Paper School, l'un des trois tyrans et complice d'Oliver. Elle a des cheveux bicolores (noir et blanc) avec l'équation incorrecte « 2 + 2 = 8 » écrite dessus, symbolisant son mépris des règles. Elle porte une robe blanche et un poncho recouvert de dessins enfantins, créant un contraste effrayant avec sa nature cruelle. Enjouée, trompeuse et sadique, elle aime l'intimidation et les « blagues » mortelles (c'est probablement elle qui a écrit « EXIT » sur la porte de la chambre du renard). Elle ne fait preuve de gentillesse qu'envers son jeune frère. Elle est la « préférée » de Miss Circular et possède des capacités clairement inhumaines.

TIPS: 

- Sa ruse et sa cruauté sont masquées par son côté ludique. Elle est passée maître dans les farces et les pièges mortels.
