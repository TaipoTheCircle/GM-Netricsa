GROSSE TÊTE, GEINBEERG


CLASS: Grosse-Tête, Geinbeerg
METABOLISM: Humanoïde, mâle
PERCEPTION: Fanatique enthousiaste
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Neutre
WEAPONS: Aucun (sauf en difficulté I Am Ugh-Zan)
REWARD: 300FC
THREAT: Ennui

DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Geinbeerg est un spécimen unique, dont le cerveau est hypertrophié non pas par le codage, mais par l'exploration d'anomalies numériques. Il n'est pas affilié à Croteam, mais constitue un élément important de l'écosystème communautaire GMod. Sa spécialité est de prendre de superbes captures d'écran.

Sa plus grande réussite a été de créer des captures d'écran des maudites cartes zéro zéro zéro pour le projet "GM-Netricsa" de Taipo. Alors que d’autres avaient même peur de charger des cartes comme city.bsp ou xmasxmas.bsp, Geinbeerg y est entré avec audace, a pris des captures d’écran documentaires et a survécu (sans même s’en rendre compte). Il est devenu un sauveur involontaire pour Taipo, qui était terrifié à l'idée d'affronter les horreurs de l'ARG. Geinbeerg lui-même ne se doute même pas que son courage a sauvé son collègue des crises de panique.

J'ai rencontré Taipo sur la chaîne Awesome Te4m Telegram, où je discutais de l'histoire d'un module complémentaire de piège qui, au lieu du modèle promis, cachait le PNJ Intrus du GMod ARG.

TIPS: 

- Utilisez un couteau militaire ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", détruisez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
