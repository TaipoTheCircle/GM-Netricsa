UNE MAISON EN BRIQUE, FRICHE ET ARBRES


Sam, cet endroit sent le harceleur qui a décidé de s'installer et de remplir son placard pour l'hiver nucléaire. Devant nous se trouve une maison solitaire en briques, posée sur l'herbe verte, comme si elle avait simplement été placée là et oubliée.

Il n'y a pas de ville à proximité, pas de voisins. Seulement 11 arbres.

The house is sturdy, red brick, the roof is in place. But there are some nuances: 

Il n'y a pas de porte. Du tout. Juste une ouverture, béante d'un vide noir. Soit ils l'ont supprimé, soit il n'a jamais été là.

Deux fenêtres de part et d'autre de l'entrée.

To the right are three cars. They used to be able to drive away, but now they're just rusty boxes without wheels. They stand there as a reminder: il n'y a pas d'échappatoire d'ici.

Inside is a warehouse, not a home. Someone was clearly preparing for the end of the world: 

Deux lampes suspendues au plafond. La lumière est juste suffisante pour voir ce qu'il y a à l'intérieur.

Five drawers: un grand, quatre petits. Bien scellé. Ils ne s'ouvrent pas, ne cassent pas, ne bougent pas. Ils restent assis là, à se moquer.

Sur le mur de droite se trouve une horloge. Est-ce que ça marche ? Est-ce que ça tourne ? Est-ce qu'il indique l'heure ? Inconnu. Mais c'est là.

Il y a aussi une armoire avec deux armes. Mais ne t'inquiète pas, Sam. Ils sont cloués. Quelqu'un est venu, a suspendu les armes et les a sécurisées pour que vous ne puissiez pas les déplacer. En guise d'avertissement. Ou comme une raillerie.

Supplies-that's where the gold is: 

And here's where the fun begins. Someone has taken a very careful approach to survival: 

"Patates douces" - un aliment de base.

"Les pommes, les meilleures du monde." Les meilleures pommes du monde. C'est ce qui est dit.

"Les fours à micro-ondes de Sofia" - 112 points. Les repas au micro-ondes de Sofia. 112 pièces. Sofia est soit un génie de la logistique, soit simplement une vraie cuisinière.

"Jabber's Assorted Snacks" - assortiment de collations de Jabber. Il y en a pour tous les goûts.

"Rolee Juice Drinks" - jus de fruits. Vitamines.

"Vanilla Drink" - boisson à la vanille. Pour la variété.

"Diet Soda" - soda diététique. Un chiffre est important, même dans l’apocalypse.

"Pommes de terre fraîches" - pommes de terre fraîches. Juste des pommes de terre. Frais.

Une maison est un point de défense idéal, sinon pour un « mais ». Il n'y a pas de porte. Tout le monde peut entrer. C'est donc plus un piège qu'une forteresse. Si des ennemis apparaissent, la seule issue est de passer par la même ouverture que celle où se trouvait la porte.

Les fournitures sont une grande motivation pour défendre ce lieu. Avec cet ensemble, vous pouvez survivre à un hiver, à deux hivers et à quelques mois supplémentaires. Mais les armes sont clouées, nous devrons donc riposter avec ce que nous avons apporté.

Si nous décidons de nous installer ici, la première chose que nous faisons est de chercher une porte. Ou des planches pour barricader l'entrée. Et puis nous essaierons d'ouvrir ces boîtes. Parce que qui sait ce qu'il y a à l'intérieur ? Peut-être qu'ils contiendront des clés de voiture. Ou les clés de ces armes. Ou simplement plus de nourriture en conserve. Mais la curiosité, Sam, aura raison de nous.

Entrons à l'intérieur. Mais gardez le silence.
