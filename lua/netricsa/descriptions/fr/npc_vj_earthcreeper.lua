EARTH CREEPER DU MINECRAFT OVERWORLD


CLASS: Plante grimpante de la terre
METABOLISM: Terre inorganique
PERCEPTION: Hostile
SIZE: 5,5 pieds
ENDURANCE: Faible
HOSTILITY: Haut
WEAPONS: Explosion de terre
REWARD: 60 FC
THREAT: Moyen

DESCRIPTION: 

Ce type de plante grimpante résout le problème des explosions un peu différemment. Son corps est brun et sa tête ressemble à un bloc de terre avec une pointe verte, ce qui lui permet de se fondre dans le paysage et de se faufiler sur ses ennemis sans être détecté.

Au lieu de tout détruire autour de lui ou de causer des dégâts, le Earth Creeper crée un tas chaotique de blocs de terre lorsqu'il explose. Cette explosion n'endommage pas votre santé, mais ses effets peuvent être dévastateurs sur votre terrain et vos bâtiments. Il est souvent décrit comme « inutile » (relativement parlant) au combat, mais très ennuyeux en raison de la façon dont il transforme une pelouse soignée en un tas de terre dispersée au hasard.

TIPS: 

- Cette plante grimpante n'est pas nocive pour ta santé, mais elle constitue une menace sérieuse pour ta base et tes bâtiments.
- Ne le laissez jamais exploser près de chez vous, sauf si vous souhaitez qu'il soit enterré sous terre. Il est préférable de le tuer à distance.
- Il est facilement reconnaissable à son corps brun et à sa tête verte qui se fond dans l'herbe.
- À votre mort, vous pouvez piller de la terre ordinaire.
- Les creepers apprivoisés peuvent être utiles en tant que défenseurs, mais le Earth Creeper laisse toujours un désordre même après avoir été apprivoisé.
