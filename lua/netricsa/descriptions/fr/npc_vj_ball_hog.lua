COCHON À BOULE DU MOBIUS


CLASS: Cochon boule
METABOLISM: Badnik mécanique
PERCEPTION: Bombardier robot de combat
SIZE: ~4 pieds
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Bombes à billes métalliques
REWARD: 150 FC
THREAT: Moyen

DESCRIPTION: 

Le Ball Pig est un robot Badnik créé par le méchant Dr Eggman dans le cadre de son armée de machines. Sa conception ressemble à un organisme mécanique ressemblant à un cochon, au corps arrondi, reposant sur deux pattes mécaniques et armé d'un dispositif intégré directement dans son ventre. C'est à travers cette armure circulaire qu'il tire des bombes à billes métalliques qui rebondissent et endommagent les cibles.

Its attack method is simple but effective in narrow corridors and on inclines: le Ball Pig grimpe souvent plus haut sur les pentes ou au bord des plates-formes et lâche ses bombes, où elles rebondissent sur le sol et se tordent, créant un piège mortel pour quiconque tente de passer.

Malgré son apparence inspirée d'un vrai cochon, c'est une machine de la guerre froide. Il faisait partie des premiers modèles Badnik utilisés lors des premières invasions Eggman, et il apparaît encore aujourd'hui dans divers mondes.

TIPS: 

- Il est préférable de l'attaquer à distance ou par le haut.
- Les bombes rebondissent, anticipent leur trajectoire et esquivent.
- Une fois détruit, le Ball Pig libère souvent un animal en otage, comme les autres Badniks.
