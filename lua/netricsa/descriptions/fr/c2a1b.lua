MESA NOIRE, ASCENSION DE LA FOSSE ACIDE


Sam, pour rétablir l'alimentation électrique du rail de chargement, tu devras traverser l'une des sections les plus impitoyables de l'installation. Les niveaux inférieurs ont été complètement inondés de déchets toxiques, formant une fosse acide incandescente qui ronge les grilles métalliques comme du papier. Un faux pas et vous vous dissoudrez plus vite qu’un morceau de sucre dans un café chaud.

La seule façon d'avancer est de monter dans un labyrinthe de passerelles étroites, de tuyaux rouillés et d'échelles de secours. Malheureusement, vous ne grimpez pas en paix. H.E.C.U. Les soldats se sont déjà retranchés aux niveaux supérieurs, utilisant leur avantage vertical pour localiser tout ce qui bouge en dessous.

Hazards: 

Acid pit beneath you: mort instantanée si vous tombez.

Grenades et suppression des tirs des soldats au-dessus.
