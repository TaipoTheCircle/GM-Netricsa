GREEN-CUT MACAW FROM THE KRIEGER ARCHIPELAGO


CLASS: Ara coupé en vert
METABOLISM: Oiseau
PERCEPTION: Perroquet vert
SIZE: ~ 3 pieds (y compris la queue)
ENDURANCE: Très faible
HOSTILITY: Aucun
WEAPON: Bec
REWARD:     10 FC
THREAT: Zéro

DESCRIPTION:

Oiseau tropical aux couleurs vives originaire des jungles de l’archipel, l’ara vert est un oiseau inoffensif et ne représentant aucune menace. On le voit souvent voler haut dans le ciel. Le perroquet n'attaque pas et n'interagit pas avec d'autres ennemis, faisant partie de l'environnement pour créer une atmosphère.

TIPS:

- Pas un ennemi. Il n’y a ni besoin ni avantage à l’attaquer.
