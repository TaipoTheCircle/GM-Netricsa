APC 404-08180 LIVRAISON DIRECTE


CLASS: APC 404-08180 Livraison directe
METABOLISM: Unité de transport aérien, module de largage blindé
PERCEPTION: Transport terrestre / Transport de troupes blindé aéroporté
SIZE: 19,7 pieds
ENDURANCE: Très élevé
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 100 FC
THREAT: Haut

DESCRIPTION: 

This is a true flying armored vehicle with five-star comfort. Mental, who usually doesn't care about the well-being of his soldiers, has somehow been generous here. Inside, the ship has everything: un jacuzzi, des fauteuils en cuir, une télévision à écran plasma et un espace pour deux douzaines de soldats blindés. Les murs sont si épais qu’aucune attaque ne gêne ceux qui se trouvent à l’intérieur.

De l’extérieur, le Dropship ressemble à une énorme boîte volante gris-orange avec des moteurs. Il n'y a aucune arme à bord du navire : il n'est pas conçu pour le combat ; son but est de livrer les soldats à destination puis de s'envoler.

TIPS: 

- Le Dropship n'a pas d'armes. Vous pouvez vous rapprocher et tirer sur les soldats qui débarquent directement sur la rampe.
- Détruire le Dropship est possible, mais difficile. Cela nécessite plusieurs tirs du lance-roquettes. La meilleure arme est le CH-181 Hoverbike avec sa cadence de tir élevée.
- Le meilleur moment pour attaquer est pendant que le Dropship vole. Il se déplace lentement en ligne droite et ne peut pas réagir.
- Ne perdez pas de temps à détruire le navire lors du débarquement. Il est plus facile de tuer les soldats qui sautent dehors : vous économiserez ainsi vos munitions et ne serez pas distrait de la menace principale.
- Remember: il y a un bain à remous à l'intérieur. Si jamais vous capturez ce Dropship, vérifiez d’abord si Mental y a laissé une bouteille de champagne.
