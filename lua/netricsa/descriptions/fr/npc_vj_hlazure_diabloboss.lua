LE DIABLO


CLASS: Le Diablo
METABOLISM: Fusion artificielle (anciens scientifiques + tissu Chumtoad + ADN Vortigaunt + intégration de cristaux Xen)
PERCEPTION: Chimère Xen expérimentale (hybride Pantereye)
SIZE: ~9 pieds de haut
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Projection de cristal Xen : tire des projectiles d'énergie cristalline concentrée depuis sa crête dorsale.
REWARD: 5000FC
THREAT: Catastrophique

DESCRIPTION: 

Le Diablo est une abomination imposante, artificiellement conçue, créée par un horrible épissage de scientifiques humains, de Chumtoads, d'ADN de Vortigaunt et de matière cristalline de Xen. Son corps brille faiblement d'énergie violette, tandis que son sang reste d'un rouge inquiétant, trahissant son origine humaine.

Contrairement à ses cousins ​​Pantereye, le Diablo n'est pas seulement un prédateur : c'est une arme vivante, conçue pour combiner la sauvagerie sauvage avec des attaques à distance d'énergie cristalline. Sa silhouette massive domine des créatures plus petites, presque deux fois plus grandes qu'un Pantereye, avec un dos large et blindé couronné par une crête de cristal palpitante qui canalise des projectiles destructeurs.

The Diablo's presence on the battlefield is overwhelming: sa taille, sa coloration violet foncé et son sang cramoisi le rendent à la fois impressionnant et horrible. Ce n'est pas une bête Xen naturelle, mais une expérience délibérée d'évolution militarisée, un prédateur de stade final sans rival naturel.

TIPS: 

- Donnez la priorité à la couverture - ses explosions de cristaux sont mortelles à distance.
- Les munitions lourdes (roquettes, explosifs) sont indispensables pour l'abattre.
- Continuez à bouger - sa portée en mêlée et son attaque sautée sont énormes
