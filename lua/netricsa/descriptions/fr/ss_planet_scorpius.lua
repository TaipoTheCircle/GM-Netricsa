SCORPIUS


PLANET: Scorpion
TYPE: Planète terrestre, Wasteland
CLIMATE: Aride, dur
MOONS: Aucun
TERRAIN: Désert aride, nombreux sommets déchiquetés
LOCATION: Dans la constellation du Scorpion
NATIVE RACES: Arachnoïdes
ALLIANCE: Anciennement un monde arachnoïde indépendant, désormais Mental Horde (toutes les races)
LOCATIONS: Salle du Haut Conseil, Fanatic Wastes

DESCRIPTION: 

La surface de la planète est un désert sans vie et brûlé, qui rappelle les paysages martiens, mais avec une brume rouge foncé inquiétante. Un épais brouillard enveloppe les sommets acérés des montagnes qui émergent du désert. Cet environnement hostile a façonné le caractère des habitants et alimenté leur fanatisme extrême.

Sur cette planète, Mental a découvert une race primitive mais intelligente, les Arachnoïdes. Se présentant à eux comme un messie, Mental rassembla parmi eux une faction de fanatiques religieux.

Avec son aide, ces fanatiques détruisirent le Haut Conseil du Scorpius et effaçèrent des millénaires de leur propre culture, rendant illégales les reliques du passé.

Cela a conduit à une guerre civile brutale, au cours de laquelle les Arachnoïdes se sont exterminés.

La jeune génération a commencé à être enrôlée de force dans l'armée de Mental, déclenchant un mouvement de résistance parmi les survivants cherchant à saboter les plans de Tah-Um de l'intérieur.

KEY LOCATIONS: 

- High Council Chamber: L'ancien centre de la civilisation arachnoïde, où étaient préservés des savoirs millénaires.
- Fanatic Wastes: Régions contrôlées par des cultistes, où ont lieu des fouilles actives et un recrutement forcé.
