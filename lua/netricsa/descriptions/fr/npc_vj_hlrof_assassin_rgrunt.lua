ASSASSIN DE ROBOT BLACK OPS


CLASS: Assassin de robots Black Ops
METABOLISM: Systèmes mécanisés
PERCEPTION: Unité Cybernétique Black Ops
SIZE: ~6 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Extrême
WEAPONS: SMG, grenades, servos de mêlée renforcés
REWARD: 3000FC
THREAT: mortel

DESCRIPTION: 

Le Robot Assassin Black Ops est un soldat mécanisé expérimental - une fusion de tactiques de combat humaines et d'endurance robotique. Contrairement aux agents Black Ops ordinaires, ces machines sont conçues pour zéro hésitation, zéro peur et une fidélité absolue aux paramètres de la mission.

Leur élégant exosquelette blindé imite la silhouette d'un assassin humain mais cache des réseaux de ciblage avancés et des fibres musculaires synthétiques. Ils se déplacent avec une précision étonnante, combinant la furtivité de leurs prédécesseurs humains avec l’efficacité infatigable des machines.

Les rumeurs suggèrent que ces unités ont été prototypées par des projets fantômes liés, destinés à remplacer les agents consommables. On ignore si les esprits humains sont encore cachés dans leurs coques métalliques...

TIPS: 

- Traitez-les à la fois comme des soldats et des machines : ils sont intelligents, mais aussi lourdement blindés.
- Les balles conventionnelles les ralentissent à peine ; les explosifs ou les armes énergétiques fonctionnent mieux.
- Ne sous-estimez jamais leur agilité : ils peuvent sprinter et sauter comme leurs homologues humains.
