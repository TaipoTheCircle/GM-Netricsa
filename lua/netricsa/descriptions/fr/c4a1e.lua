XEN, MONTÉE AU PORTAIL


Vous y êtes presque. Tout en haut de cette abomination d'usine se trouve le portail qui mène directement au chemin menant à Nihilanth lui-même. La seule façon de monter ? Ascenseurs organiques. Oui, vous avez bien entendu : les locaux en ont apparemment eu assez des boutons et des câbles normaux, alors ils ont créé des plates-formes géantes qui vous transportent vers le haut lorsque vous vous tenez dessus.


Mais bien sûr, Nihilanth ne se contentera pas de dérouler le tapis de bienvenue. Le portail crache des contrôleurs à intervalles réguliers, chacun désireux de vous faire frire avec des explosions psychiques. Considérez-les comme les pires portiers de l'univers.

Objective: survivez au trajet en ascenseur, évitez le comité d'accueil et atteignez le portail. L’autre côté est l’endroit où commence – ou se termine la salle du trône de Nihilanth, selon votre performance.
