... C'est intéressant, Sam.

Le portail a fonctionné. Terre. La gravité est familière. Mais il n’y a aucune trace de Black Mesa ici. Mais ça sent le spectacle.

Bienvenue à Half-Quake. Un spectacle sadique où la douleur est présentée comme un divertissement et la survie est considéré comme une mauvaise forme. A en juger par le monologue d'ouverture de l'annonceur, trois étapes de « tests » nous attendent. Bien sûr, il parlait de torture.

Le salon est depuis longtemps un fervent acheteur d’équipement Black Mesa. Tourelles, créatures de type Xen, matériel expérimental. Tout ce qui peut être utilisé pour infliger de la souffrance et susciter des applaudissements. Quelqu’un a clairement décidé qu’une catastrophe était une opportunité commerciale.

Regardez autour de vous.

Derrière vous se trouve un gros bouton avec un symbole ">)". Cela ressemble à une invitation. Il y a une porte devant. C'est verrouillé pour l'instant. La cage dans laquelle vous vous trouvez attend le signal. Ils veulent que vous soyez nerveux. Attendre. Souffrir d’avance.

And the announcer also said the following: 

"Nous ne sommes pas là pour sauver le monde." Freeman, dit-il, le fait déjà. Ou, plus précisément, le premier Sam.

Et tu es là pour autre chose.

Vous êtes ici pour souffrir.

Pour « une énorme erreur dans votre vie ». Ils ne précisent pas quoi exactement. Apparemment, le format de l'émission ne permet pas de donner plus de détails. Peut-être pour exister. Ou pour avoir exterminé des créatures Xen et avoir sérieusement l'intention d'en finir avec Mental, au lieu de mourir en beauté selon le scénario.

Bad news: le spectacle est construit là-dessus.

Good news: ils ont choisi le mauvais candidat.

Il y a encore une nuance. Mental a déjà conclu un accord avec les organisateurs. Ses troupes font désormais partie du programme. Officiellement. Avec pour objectif « d’en finir avec Sam une fois pour toutes ». C'est bon de savoir que vous avez une réputation stable.

Alors voici mon conseil, Sam.

Ils veulent de la souffrance, donnez-la-leur.

Mais pas le vôtre.
