MESA NOIRE, LABORATOIRES INONDÉS


Nous sommes dans une impasse. Littéralement. Le couloir se termine, ne laissant que quelques pièces, chacune un petit enfer personnel.

First room: une piscine de style Black Mesa

Le sol est inondé. L'eau est trouble et sent l'ozone et quelque chose de brûlé. Au milieu, en plein milieu de la flaque d’eau, flotte une lampe cassée. Ça fait des étincelles. Toutes les quelques secondes, il y a un flash, un crépitement et l'eau s'anime d'une lumière bleue. Vous ne pouvez pas rester là-dedans. Natation? N'y pensez même pas.

The reason: la lampe est tombée dans l'eau et a transformé cette pièce en soupe électrique. Il ne produit presque aucune lumière, mais la tension est suffisante pour faire frire n'importe qui. Même toi, Sam. Même dans votre armure.

The solution: il y a un interrupteur sur le mur à droite de l'entrée. C'est vieux, rouillé, mais apparemment fonctionnel. Un clic, et la lampe s'éteindra, l'eau se transformera en eau pure et nous pourrons passer. Question : pouvez-vous l'atteindre à temps avant que les étincelles ne transforment tout le sol en une seule grande prise électrique ?

Vient ensuite la dernière pièce. Et là, Sam, ils nous attendent.

Des yeux de chien. Pas seulement un, pas deux. Tout un nid. Bruissement, cri, attente. Et parmi eux se trouvent des crabes. Petit, agile, du genre à tomber du plafond au moment le plus inopportun.

La pièce en est couverte. Comme un tapis. Chaque pas est un risque de marcher sur une queue, chaque mouvement est une invitation au dîner. Ils ne dorment pas, ils ne sont pas partis à la recherche d'autres territoires. Ils sont là. Et ils ont faim.

Strategy Notes: 

Briseur. Tout d’abord, coupez l’alimentation. Nous pouvons essayer d'allumer la lampe, mais cela ne fera qu'aggraver l'eau. Le mode manuel est plus fiable. Et plus vite. J'espère.

Eau. Après la fermeture, n’y allez de toute façon pas. Qui sait ce qui flottait là devant nous.

La pièce avec les créatures. Il n'y a pas de choix ici. C'est soit nous, soit eux. L'étroite ouverture à l'entrée est l'occasion de créer un couloir de la mort, mais les chiens savent comment le contourner. Crabes - sautez. La meilleure tactique consiste à lancer une grenade au centre et à achever les survivants. À moins, bien sûr, que la grenade ne roule dans un coin et ne les énerve encore plus.

Le changement attend.
