COMBINEZ LE SOLDAT DE PRISON HYBRIDE OVERWATCH


CLASS: Combinez le soldat de prison hybride Overwatch
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combinez une unité d'assaut punitif à longue et moyenne portée
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, SMG, Grenades
REWARD: 650 FC
THREAT: Haut

DESCRIPTION: 

Le Combine Overwatch Hybrid Prison Soldier est une version renforcée du soldat Nova Prospekt, déployé temporairement ou définitivement à l’extérieur des complexes pénitentiaires. Le Combine utilise ces soldats dans des situations où les forces standards sont insuffisantes et où le déploiement de forces d’élite n’est pas pratique.

Le soldat est vêtu d'une armure renforcée de type prison et équipé d'un casque tactique avec une visière de protection. La visière jaune souligne son affiliation à la garde et aux forces d'assaut de Nova Prospekt. Malgré son aspect protecteur, la visière n'est pas totalement à l'épreuve des balles : les tirs à la tête précis restent efficaces.

TIPS: 

- Ne surestimez pas la visière - les tirs à la tête fonctionnent toujours.
- La meilleure option est la distance et le tir précis.
