H.E.C.U. AH-64 APACHE GUNSHIP


CLASS: H.E.C.U. AH-64 Apache Gunship
METABOLISM: aucun (mécanique, piloté par l'homme)
PERCEPTION: Hélicoptère d'attaque militaire
SIZE: ~ 58 pieds de long
ENDURANCE: très haut (blindé, évasif)
HOSTILITY: extrême (chercher et détruire)
WEAPONS: canon à chaîne, roquettes, missiles à tête chercheuse
REWARD: 5000FC
THREAT: catastrophique

DESCRIPTION: 

L'Apache est un vaisseau de combat militaire lourdement armé déployé par le H.E.C.U. pour éliminer les cibles de grande valeur et supprimer la résistance dans les zones ouvertes. Son blindage composite résiste aux tirs d'armes légères et son agilité dans les airs le rend difficile à toucher, même avec des armes lourdes. Armé d'un canon à chaîne à tir rapide, de roquettes non guidées et parfois de missiles à tête chercheuse, il peut facilement dévaster l'infanterie et les véhicules.

Le rugissement des pales du rotor suivi des tirs entrants est une condamnation à mort pour toute personne prise à découvert. Les Apaches se coordonnent généralement avec les troupes terrestres, agissant comme des bourreaux aéroportés pendant que les marines avancent. Une confrontation directe sans explosifs ni armes énergétiques est vaine.

TIPS: 

- La couverture est votre meilleur allié - ne combattez jamais un Apache à l'air libre.
- Les explosifs (RPG ou armes montées) sont le seul véritable contre-attaque.
- Les missiles à tête chercheuse sont mortels - continuez à vous déplacer de manière imprévisible.
- En cas d'abattage, restez à l'écart - les débris et les explosions secondaires peuvent être mortels.
