BLACK MESA, LUI ENCORE



Sam, vous venez d'entrer dans la pièce avec le réservoir d'ichtyosaure géant et la pièce où se trouve l'énorme machine à piston poussant le métal comme si l'endroit lui appartenait.

Quelque chose de trop calme. Là, de l’autre côté de la pièce, un homme en costume-mallette à la main regarde. Il ne bouge pas, ne crie pas. Il suffit d'observer. Lui, le fantôme en costume, réapparaît.

Il sait que tu es là. Je t'attends probablement. Ou non...
