BIOMASSE D'ESSAI


CLASSE : Essaim de biomasse
MÉTABOLISME : Xénobiologique
PERCEPTION : Structure d'essaim organique
TAILLE : Variable
ENDURANCE : pratiquement invulnérable
HOSTILITÉ : Passif
ARMES : Tissus corrosifs, phéromones défensives
RÉCOMPENSE : 150 FC
MENACE : Moyenne

DESCRIPTIF :

La biomasse est le tissu organique vivant à partir duquel l'Essaim construit des essaims, des tunnels et d'autres grandes structures. Il s'étend progressivement, recouvrant les murs, les sols et les plafonds d'une couche dense de chair fibreuse, transformant l'environnement environnant en une extension de l'Essaim lui-même.

On pense que la biomasse traite toutes les matières organiques disponibles pour se développer. Des plantes, des animaux et même des êtres intelligents disparus peuvent devenir des matériaux de construction pour de nouvelles sections de l'Essaim, perdant à jamais leur forme originale.

Malgré leur incapacité à se déplacer de manière autonome, la biomasse reste dangereuse. Sa surface est recouverte de sécrétions caustiques qui provoquent des brûlures chimiques au contact. De plus, certaines variétés sont capables de sécréter des phéromones spéciales qui, lorsqu'elles sont endommagées ou brûlées, attirent les membres de l'Essaim à proximité pour défendre la zone infestée.

Les armes conventionnelles ne causent pratiquement aucun dommage à la biomasse. Son tissu résiste aux munitions cinétiques, aux explosions et à la plupart des armes énergétiques. La méthode de destruction la plus efficace reste une exposition prolongée à des températures élevées, qui brûlent littéralement la structure organique.

L’apparition de la biomasse est considérée comme l’un des premiers signes de la formation d’un essaim à part entière. Si l’infestation n’est pas stoppée à temps, ses défenseurs suivront bientôt la chair immobile.

CONSEILS :

- Brûlez la biomasse avec des lance-flammes ou d'autres armes incendiaires.
- Ne touchez sa surface que si cela est nécessaire.
- Soyez prêt à ce qu'un essaim apparaisse après avoir détruit de gros clusters.
- Plus il y a de biomasse, plus la probabilité qu'un noyau Swarm se trouve à proximité est élevée.