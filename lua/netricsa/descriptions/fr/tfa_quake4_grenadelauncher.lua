GRENADE LAUNCHER


WEAPON: Lance-grenades
AMMO: Grenades
ROF: ~20 grenades/min


DESCRIPTION:

Le lance-grenades tire des grenades à impact ou à retardement qui explosent après avoir été lancées. Il inflige d'importants dégâts de zone d'effet, ce qui le rend idéal pour éliminer des groupes ou détruire une couverture. Avec une visée précise, les grenades peuvent être lancées sur ou autour des obstacles.

NOTES:

- Les explosions peuvent blesser l'utilisateur si elles sont utilisées trop près.
- Efficace contre les clusters et pour briser les barrières.
- L'utilisation du rebond et des angles est avantageuse dans les combats.
- Soyez prudent dans les zones confinées en raison des dégâts causés par les éclaboussures.