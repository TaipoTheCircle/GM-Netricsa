CARABINE À CHARGEMENT AUTOMATIQUE RUGER 10 / 22


WEAPON: RUGGER 10/22
MUNITIONS Cartouches .22 LR
ROF: ~40-60 tours/min

DESCRIPTION: 

Un fusil semi-automatique compact chambré pour les cartouches .22 Long Rifle, introduit pour la première fois par Sturm, Ruger & Co. en 1964. Basé sur une action de boulon fiable à course courte avec un cadre de boulon rotatif ; est livré en standard avec un chargeur rotatif de 10 coups. Connu pour sa durabilité, sa conception simple et son prix abordable, il est populaire pour l'entraînement, le tir récréatif, la chasse au petit gibier et aux nuisibles, ainsi que dans les disciplines sportives. Il est disponible dans de nombreuses variantes et configurations (Carbine, Target, Tactical, Ruger 10/22 Takedown, etc.) et dispose d'un vaste marché de réglage et d'accessoires (systèmes de visée, crosses, devants, compensateurs de canon, chargeurs étendus, etc.).

NOTES: 

- Largement utilisé comme arme d’entraînement et « récréative » ; dans certains pays, des versions civiles sportives/chasse sont disponibles avec des restrictions.
- Provoque très peu de dégâts grâce à son petit calibre 22. LR.
