THE PROTOZOAN FROM XEN


CLASS: Le protozoaire
METABOLISM: filtreur gazeux
PERCEPTION: Microbe aérien de Xen
SIZE: 3-4 pieds de diamètre
ENDURANCE: indéfini
HOSTILITY: aucun
WEAPONS: aucun
REWARD:     15 FC
THREAT: aucun

DESCRIPTION:

Le protozoaire est un organisme passif et non hostile originaire de Xen. Il apparaît comme un orbe translucide bleuâtre avec une plus petite sphère rougeâtre à l’intérieur, en orbite doucement au-dessus du sol. Le protozoaire ne remplit aucune fonction de combat et ne représente aucune menace.

TIPS:

- Aucune action n'est requise ; le protozoaire ne s'engage pas et ne présente aucun danger.
