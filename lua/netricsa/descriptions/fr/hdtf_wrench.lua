CLÉ À TUYAU


WEAPON: Clé à tube
AMMO: Aucun
ROF: Variable - swing rapide (~60 swings/min) ou frappe chargée (~20 swings/min)

DESCRIPTION: 

Clé à tube industrielle robuste reconvertie en arme de mêlée lors de l'incident de Black Mesa. Inflige un puissant traumatisme contondant avec des frappes rapides ou un coup aérien chargé dévastateur.

NOTES: 

- Primary attack: des balançoires rapides pour des dégâts modérés.
- Secondary attack: frappe chargée avec des dégâts et un recul accrus.
- Arme silencieuse, utile pour économiser les munitions.
- Inefficace contre les ennemis éloignés ou lourdement blindés.
- Durable et toujours disponible, ce qui en fait un outil fiable de dernier recours.
