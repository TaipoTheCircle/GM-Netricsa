DRONE FLOTTEUR DU SERGENT STORMTROOPER


CLASSE : Drone flottant du sergent Stormtrooper
MÉTABOLISME : contrôlé par l'IA, avion
PERCEPTION : Reconnaissance Tactique, Scanner Aérien
TAILLE : 1 pied
ENDURANCE : Très faible
HOSTILITÉ : Moyenne
ARMES : Canon laser dans "Eye"
RÉCOMPENSE : 25 FC
MENACE : Élevée

DESCRIPTIF :

Un jour, Stormtrooper Sergeant aperçut le droïde de reconnaissance standard de Mental et, en quelques secondes, en dessina mentalement une version miniature. Contrairement à la version originale, ce drone est doté d'un « œil » rouge, un scanner capable d'analyser le terrain et de transmettre des données en temps réel.

Le Sergeant's Floater Drone est un avion léger conçu pour la reconnaissance tactique et la surveillance. Il plane dans les airs, scrutant la zone et identifiant les menaces. Son « œil » rouge lui permet de voir même dans des conditions de mauvaise visibilité, comme le brouillard, la nuit et la fumée.

Au combat, le drone utilise des lasers conçus non pas pour causer de graves dégâts, mais pour supprimer et distraire. Si le drone détecte un ennemi, il transmet immédiatement les coordonnées au Sergent et peut également appeler des renforts.

Le Floater Drone est également utilisé pour patrouiller les zones et surveiller les mouvements des troupes. Contrairement au Floater Drone original de Mental, ce modèle n'a pas de système de pénalité uniquement pour la recherche et la destruction.

CONSEILS :

- Le drone est facile à détruire avec n'importe quelle arme, mais une arme à tir rapide est préférable.
- Si le drone vous repère, il transmettra vos coordonnées au Sergent. Changez immédiatement de position ou détruisez le drone.
- L'"oeil" rouge est un point faible. Le frapper désactive le drone instantanément.
- Ne reste pas immobile. Le drone tire un laser, mais sa précision diminue à mesure que la cible se déplace.
- S'il y a plusieurs drones, ils peuvent coordonner les tirs. Détruisez-les un par un, en les empêchant de former un groupe.
- Le drone, ce sont les yeux du sergent. Si vous le voyez, cela signifie que le sergent vous connaît déjà. Il est temps de partir.