COMBINEZ L'INTERCEPTEUR DE LA PROTECTION CIVILE


CLASS: Combiner un intercepteur de la protection civile
METABOLISM: Humain, mâle
PERCEPTION: Combiner l'unité de protection civile de l'Arctique et l'intercepteur
SIZE: 6 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: SMG3 (HK MP7), Manhack
REWARD: 250 FC
THREAT: Moyen-élevé

DESCRIPTION: 

Le Combine Civil Protection Interceptor est une unité spécialisée de la protection civile, adaptée aux opérations dans les régions enneigées et arctiques. Ces unités sont utilisées pour patrouiller dans les villes glacées, les stations polaires et les zones reculées où les forces standard du PC perdent leur mobilité et leur orientation.

Il porte une combinaison de protection camouflage gris et blanc qui se fond dans le paysage enneigé. L'uniforme est renforcé par des ceintures, des pochettes et des supports d'équipement conçus pour les opérations longues et sans surveillance dans des froids extrêmes. Un gilet pare-balles noir protège le torse et de lourdes bottes noires assurent la stabilité sur la glace et la neige.

Leur principale caractéristique distinctive est un masque doté de deux grandes lentilles bleues brillantes qui servent de capteurs d’imagerie thermique et de vision nocturne. Ces lentilles permettent à l'Interceptor de détecter des cibles à travers les blizzards, le brouillard et les rideaux de neige, tournant ainsi la mauvaise visibilité à son avantage.

Ils sont armés d'un SMG3 (HK MP7), une arme compacte à tir rapide idéale pour les combats rapprochés et moyens dans des espaces confinés. Si nécessaire, l'Intercepteur peut déployer le Manhack, l'utiliser pour la reconnaissance, la poursuite des fugitifs ou la pression aérienne.

Les intercepteurs agissent rapidement, s'engagent rarement dans des combats prolongés et préfèrent les tactiques de pression, d'encerclement et de finition.

TIPS: 

- Ne comptez pas sur la couverture en cas de tempête de neige : ses capteurs sont meilleurs qu'il n'y paraît.
- Le Manhack est souvent utilisé pour désorienter avant une attaque.
- Le MP7 est mortel à courte portée en raison de sa cadence de tir élevée.
