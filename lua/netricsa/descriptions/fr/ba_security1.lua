BLACK MESA, AUX PORTES DU COMPLEXE DE SÉCURITÉ


Eh bien, nous sommes arrivés ! La destination est le complexe de sécurité Black Mesa. Le passage est autorisé. Il semble que votre déguisement fonctionne jusqu'à présent.

Mais avant d’entrer, retournez-vous. Il y a un autre train derrière toi. Il monte dedans. Pas le Sam qui a été confondu avec Freeman. Pas un sosie. Réel. Gordon Freeman. Êtes-vous en retard ? Ou peut-être que tout se déroule selon un calendrier invisible ?

Il passe, silencieux, concentré, avec ce look typique de Rock. Et quelque part là-bas, derrière les murs, l’horloge tourne déjà avant la catastrophe.
