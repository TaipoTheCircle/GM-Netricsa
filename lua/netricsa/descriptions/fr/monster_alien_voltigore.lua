VOLTIGORE DE LA DIMENSION X


CLASS: Voltigore
METABOLISM: Prédateur bioélectrique
PERCEPTION: Bête d'assaut lourd de la race X
SIZE: 10-12 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Griffes, force d'écrasement, décharge électrique de plasma
REWARD: 600 FC
THREAT: Très élevé

DESCRIPTION: 

Le Voltigore est une énorme bête quadrupède élevée par la Race X comme artillerie lourde vivante. Son corps musclé est recouvert d'une épaisse armure chitineuse qui offre une excellente protection contre les armes conventionnelles. Les Voltigores sont extrêmement agressifs, chargeant leurs proies avec leurs énormes griffes et leur taille écrasante.

Leur capacité la plus dévastatrice est l’émission d’explosions de plasma bioélectrique, tirées depuis un organe situé sur leur dos. Ces projectiles explosent à l'impact avec une puissance dévastatrice, capable de détruire des véhicules blindés et d'anéantir l'infanterie. Leurs rugissements terrifiants et leurs motifs lumineux signalent une attaque imminente, leur donnant une présence presque démoniaque sur le champ de bataille.

Bien qu'assez intelligents pour combattre en coordination avec d'autres créatures de la Race X, les Voltigores sont principalement déployés comme armes de choc de terreur et de destruction.

TIPS: 

- Gardez une distance maximale : leurs explosions de plasma sont dévastatrices en combat rapproché.
- Des explosifs, des roquettes ou des mitrailleuses lourdes sont nécessaires pour les abattre rapidement.
- Utilisez une couverture pour appâter leurs attaques de plasma avant de contrer.
- Ne sous-estimez jamais leur puissance en mêlée : s'ils se rapprochent, les chances de survie diminuent fortement.
