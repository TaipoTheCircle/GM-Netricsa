CRABE DU ZENOZOIK


CLASS: Crabe
METABOLISM: Arthropode, xénomorphe
PERCEPTION: Arthropode
SIZE: ~1,3 pied
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Projectiles organiques (amas de biomasse éjectés ou vase minéralisée)
REWARD: 25 FC
THREAT: Faible

DESCRIPTION: 

Un arthropode côtier qui habite les plans d'eau du Zenozoik : étangs, lacs et plages. Il a une carapace bleue et des membres rouge vif. Il est territorial et attaque toute créature qu'il perçoit comme une menace avec des projectiles organiques. Son attaque consiste à éjecter une substance visqueuse avec suffisamment de force pour causer des dégâts à moyenne portée. Ils ne sont pas particulièrement mobiles au combat, préférant tirer depuis une seule position. On les trouve souvent en groupes de 2 à 4, ce qui peut poser problème sur les sentiers étroits près de l'eau.

TIPS: 

- Évitez l'acide corrosif craché par les crabes.
- Une arbalète ou toute autre arme à distance est efficace contre eux. Les coups de pied sont utiles en combat rapproché.
