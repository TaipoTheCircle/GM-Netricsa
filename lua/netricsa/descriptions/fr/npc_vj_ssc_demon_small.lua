DÉMON REPTILOÏDE FIENDIEN, PETIT


CLASS: Démon reptiloïde fiien, minuscule
METABOLISM: à sang froid, ressemblant à un reptile
PERCEPTION: race primitive modifiée
SIZE: 0,4 pied
ENDURANCE: moyen
HOSTILITY: haut
WEAPONS: projectile boule de lave, griffes
REWARD: 5000FC
THREAT: moyen

DESCRIPTION: 

Une autre mutation hideuse, gracieuseté de Notorius Mental. Un simple organisme ressemblant à un lézard de Fiendia Prime a été croisé et muté sur plusieurs générations, et a finalement été renforcé par un squelette en titane. Il a la capacité unique de former un trou noir miniature à travers lequel il peut faire exister des boules flottantes de lave en fusion. De faibles forces magiques maintiendront ce projectile enflammé cohérent pendant une courte période de temps. En volant dans les airs, il se refroidira et finalement explosera.

Au cours d'expériences de réduction de créatures, ce micro-reptilien a conservé toutes les capacités dangereuses de son prototype géant, notamment la création de trous noirs miniatures et le lancement de jets de lave en fusion. Son squelette en titane est devenu une micro-armure pratiquement indestructible. Étant si petit, il est pratiquement invisible dans les herbes hautes, les crevasses et les débris. Il peut être tué en marchant littéralement dessus, mais sa taille même le rend mortel.

TIPS: 

- Les projectiles sont indestructibles, alors esquivez-les jusqu'à ce qu'ils explosent.
- Marchez dessus et il mourra.
