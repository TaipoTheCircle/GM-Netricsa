DESCRIPTION: 

A genetic hybrid of a Sirian and a bovine species. The Werebull's behavior is similar to that of a normal bull: il charge à toute vitesse, dans le but d'encorner son adversaire.

The Werebull Meat Bull is the result of Mental's devising: il a écorché plusieurs montures de taureaux-garous pour ses vêtements et ceux de ses collègues. Le taureau est entièrement écorché, laissant apparaître les muscles et les tendons. Ses yeux sont absents, mais il reste incroyablement sensible aux menaces qui l'entourent. Il est aussi vicieux qu'un taureau normal, car chaque cellule de son corps ressent la douleur et la colère lorsqu'elle est exposée à l'air.

Au combat, il attaque avec une charge courante, projetant sa victime sur ses cornes et lui infligeant des lacérations. Malgré son apparence terrifiante, ce taureau est considéré comme un « mets délicat » sur la planète Sirius, même si personne l'ayant rencontré sur le champ de bataille n'a été pressé de l'essayer.

