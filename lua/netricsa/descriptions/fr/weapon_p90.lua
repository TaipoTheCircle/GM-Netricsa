MITRAILLEUSE MITRAILLEUSE FN P90


WEAPON: FN P90
MUNITIONS 5,7x28 mm
ROF: ~900 tours/min

DESCRIPTION: 

Le FN P90 est une arme de défense personnelle compacte développée par la société belge FN Herstal à la fin des années 1980. Il a été créé pour les équipages de véhicules blindés et les unités arrière, où une capacité de chargeur élevée et un faible recul sont importants.

Il utilise des cartouches spéciales de 5,7x28 mm capables de pénétrer dans les gilets pare-balles modernes tout en maintenant un faible recul. Grâce à son chargeur supérieur de 50 cartouches et à sa conception bullpup, l'arme reste courte mais très efficace en combat rapproché.

NOTES: 

- Haute précision et faible recul - idéal pour les combats rapprochés et moyens.
- Pénètre les gilets pare-balles de classe II et IIIA.
- Souvent utilisé par les forces spéciales du monde entier.
- Conception ergonomique et commandes ambidextres.
