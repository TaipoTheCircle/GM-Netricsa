COLLINES SOLITAIRES, ROUTE VERS NULLE PART


Sam, nous sommes arrivés à un endroit qui semble tout droit sorti d'une carte postale qu'ils ont oublié d'envoyer. Des collines verdoyantes, de l'air pur, une route qui s'étend jusqu'à l'horizon... et pas une âme. Juste du vent, de l'herbe et le sentiment que le monde avait tout simplement été éteint sans prévenir les habitants.

Imaginez des champs verts sans fin, se fondant doucement dans des collines basses. Et l’artère principale de ce lieu, c’est la route. Il serpente entre les collines, gravit les collines, plonge dans les plaines et... se termine simplement. Il ne heurte pas un mur, ne mène pas à un portail, mais disparaît dans l'herbe, comme si quelqu'un l'avait dessiné et avait oublié de le terminer.

What's here: 

Les collines constituent d’excellentes positions de couverture naturelle et d’embuscade.

La route est le point de repère idéal. Si vous êtes perdu, suivez-le. Cela ne mènera nulle part, mais cela vous donnera l'illusion d'une destination.

Le silence ici est en quelque sorte... contre nature. Même le vent bruisse comme s'il craignait de réveiller les collines. Garde les yeux ouverts, Sam. Si quelqu'un se présente dans un endroit aussi désert, c'est soit un fantôme, soit une très mauvaise nouvelle. Ou peut-être simplement un harceleur local qui apprécie également le calme et la tranquillité. Mais il y a peu d'espoir pour un harceleur.

Allons-y, explorons où mène cette route.

Spoiler: nulle part.
