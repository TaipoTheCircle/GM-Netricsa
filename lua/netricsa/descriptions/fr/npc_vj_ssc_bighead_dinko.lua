GROSSE TÊTE, DINKO PAVICIC


CLASS: Grosse tête, Dinko Pavicic
METABOLISM: Humanoïde, mâle
PERCEPTION: Fanatique enthousiaste
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Neutre
WEAPONS: Aucun (sauf en difficulté I Am Ugh-Zan)
REWARD: 100 FC
THREAT: Ennui

DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Dinko Pavicic est l'un des piliers du style visuel de Croteam. Son crâne exagéré est le résultat direct de sa combinaison unique de génie artistique et de réflexion stratégique. Ayant débuté en tant qu'artiste 2D sur les premiers jeux Serious Sam, Dinko a contribué à façonner les visuels saisissants et reconnaissables de la franchise à ses débuts.

Cependant, son talent s’étend bien au-delà de l’industrie du jeu vidéo. En 2006, il a reçu un Emmy Award pour la conception exceptionnelle de séquences de titres lors de la 78e cérémonie des Oscars, confirmant ainsi son statut de maître de classe mondiale en matière de design visuel. Cette expérience unique – de la création de pixel art pour un jeu de tir emblématique à la collaboration sur un événement hollywoodien majeur – fait de lui l'un des esprits les plus polyvalents et les plus influents du panthéon Krotimovtsy.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", éliminez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
