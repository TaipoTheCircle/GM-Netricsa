GROS GARÇON DE L'ARCHIPEL KRIEGER


CLASS: Gros garçon
METABOLISM: Mutant trigène
PERCEPTION: Combattant simple
SIZE: 7,2 pieds
ENDURANCE: Grand
HOSTILITY: Agressif
WEAPONS: Lance-missile (intégré), Griffes
REWARD: 1500FC
THREAT: Humain muté par trigène

DESCRIPTION: 

Fat Boys get their name from their gigantic build and heavy stomps, audible tens of meters away. Most specimens are equipped with a missile launcher built into their right arm, which has an infinite supply of ammunition but fires slow-firing missiles. Their chests are exposed to allow ventilation of the glowing implant, their primary vulnerable area. Despite their military uniform (trousers and boots), their intelligence is extremely primitive: ils n'évitent pas les obstacles, mais les rongent littéralement, mourant parfois de leurs propres missiles dans des passages étroits.

Remarquablement, ce sont les seuls Trigens à conserver la capacité de parler. Au combat, ils aboient des menaces du type « Vous avez échoué ! » et "Vous avez fini !", bien que leurs tactiques se limitent à des assauts frontaux. Ils ne comprennent pas le concept de la mort parmi les leurs – ils pourraient crier « Quelqu'un est mort ! » en assistant à la mort d'un autre mutant. Ils réagissent aux bruits et organisent des patrouilles, mais deviennent plus dangereux dans les espaces confinés en raison de l'impossibilité d'esquiver leurs missiles.

TIPS: 

- Visez l'implant lumineux sur la poitrine. Les balles normales font peu de dégâts à la chair mutée.
- Utilisez des missiles lents et maniables qui sont faciles à éviter dans les zones ouvertes.
- Méfiez-vous des couloirs : leurs missiles y atteindront presque à coup sûr leur cible.
- Il existe une variété non armée.
