DOMESTIC PIG FROM THE ZENOZOIK


CLASS: Cochon domestique
METABOLISM: Mammifère, ongulé à doigts égaux
PERCEPTION: Animal sauvage/domestique
SIZE: 1,6 pi
ENDURANCE: Faible
HOSTILITY: Aucun
WEAPONS: Aucun
REWARD:     3 FC
THREAT: Aucun

DESCRIPTION:

Un porc domestique commun trouvé dans les colonies et les fermes de Zenozoik. Pas différent de ses homologues terrestres, sauf qu’il habite un monde bien plus étrange. Totalement inoffensif, il n’attaque ni n’interagit avec les adversaires.

TIPS:

- L'attaquer est inutile et peut attirer une attention inutile.
- Lorsqu'on lui donne un coup de pied, il émet un cri caractéristique et se retourne maladroitement. Il ne tente pas de riposter.
