MÉTABOLISME : Xénobiologique
PERCEPTION : Prédateur lourd
TAILLE : 9,8 pieds
ENDURANCE : Très élevée
HOSTILITÉ : Extrême
ARMES : Griffes, Dents, Masse corporelle
RÉCOMPENSE : 1200 FC
MENACE : Très élevée

DESCRIPTIF :

Le Grand Drone d'Élite est considéré comme l'une des variantes les plus dangereuses de l'Essaim "Bêta". Il combine la taille d'un grand spécimen avec la résilience d'un drone d'élite, créant une créature que de nombreuses garnisons coloniales préféreraient ne jamais rencontrer.

Il ne diffère de ses congénères que par sa coloration noir charbon et sa taille imposante. Trois yeux scrutent les environs, tandis que son corps massif est enveloppé d'une chitine dense capable de résister à des dommages qui s'avéreraient mortels pour un drone ordinaire. Certains témoins oculaires ont confondu ces créatures avec de jeunes Gardes de la Ruche, pour ensuite rapidement se désintéresser des questions de classification.

L’apparition d’un tel spécimen au sein d’une ruche indique généralement que la colonie existe depuis longtemps et possède des ressources importantes. En termes simples, si l’Essaim a réussi à produire un monstre comme celui-ci, il prospère. On ne peut pas en dire autant de ses voisins.

Malgré son immense masse, le Large Elite Drone conserve l'agressivité et la vitesse caractéristiques de l'Essaim "Beta". Il ne possède ni intelligence complexe ni capacités spéciales ; il n’en a pas besoin. L'évolution a conclu il y a longtemps que quatre griffes massives sont souvent plus convaincantes que n'importe quelle tactique.

CONSEILS :

- Ne vous engagez en aucun cas dans un combat rapproché avec lui.
- Des armes lourdes sont recommandées ; les armes légères sont simplement sympathiques.
- Éliminez ce type de spécimen avant qu'il ne s'approche de l'escouade.
- Si un drone standard pose problème, un drone lourd d'élite ressemble plus à une catastrophe naturelle avec des griffes.