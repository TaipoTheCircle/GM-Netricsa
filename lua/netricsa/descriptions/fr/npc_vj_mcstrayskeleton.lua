SQUELETTE ERRABLE DU MONDE MINECRAFT


CLASS: Squelette errant
PERCEPTION: Mort-vivant
METABOLISM: Mort-vivant magiquement alimenté par la glace
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Arc, flèches à effet lent
REWARD: 150 FC
THREAT: Moyen

DESCRIPTION: 

Le Stray Skeleton est un squelette qui a oublié que la neige est un piège mortel pour ceux qui ne sont pas préparés.

Tout squelette pris dans la neige frissonne d'abord comme s'il était gelé, et après quelques secondes se transforme en Straya - une version gelée de lui-même avec un arc et des flèches qui ralentissent les ennemis pendant 30 secondes.

TIPS: 

- N'essayez pas de jouer aux boules de neige avec Stray - vous perdrez.
- Ses flèches vous ralentiront lorsqu'elles frapperont.
