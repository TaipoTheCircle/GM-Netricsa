DESCRIPTION: 

Il ressemble à un poulet ordinaire, moyen, mais regardez dans son âme... même si, bien sûr, c'est une affaire personnelle.

Cela ressemble à un poulet, c'est pourquoi c'est un poulet. Utilisé par Mental pour faire du shawarma.

TIPS: 

- Le poulet est inoffensif. Ne gaspillez pas de munitions, sauf si vous souhaitez accumuler des points supplémentaires.
