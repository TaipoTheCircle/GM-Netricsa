VENDEUR DE BLACK ROCK


CLASS: Vendeur de Black Rock
METABOLISM: Humain
PERCEPTION: Marchand de Black Rock
SIZE: ~5,9 pieds
ENDURANCE: Faible
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 40 FC
THREAT: Très faible

DESCRIPTION: 

Le vendeur est un représentant de Black Rock Corporation, travaillant à bord du navire de recherche dans l'univers UH-1. Contrairement aux scientifiques, Seller se spécialise dans le commerce, l'acquisition et la logistique interne des projets de Black Rock. Il apparaît souvent dans les zones réglementées, négociant des transferts de ressources ou supervisant le transport de formes de vie Xen capturées.

Bien qu'il ne soit pas un combattant, Seller est important pour les opérations de l'entreprise Black Rock, garantissant que les technologies rares, les échantillons génétiques et les organismes Xen soient livrés entre de bonnes mains. Comme beaucoup d'employés de Black Rock, il reste dans l'ignorance de l'ampleur des transactions de l'entreprise, étant à la fois commerçant.

TIPS: 

- Le vendeur évite le combat et fuira s'il est menacé.
- Le vendeur protecteur peut accorder l'accès à des fournitures rares ou à des informations restreintes.
- S'il est éliminé, Black Rock perdra de précieux liens avec le marché des entreprises.
