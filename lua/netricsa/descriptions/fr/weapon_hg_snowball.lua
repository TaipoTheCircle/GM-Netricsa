BOULE DE NEIGE


WEAPON: BOULE DE NEIGE
ROF: ~1 lancer/sec.

DESCRIPTION: 

Des morceaux de neige moulés ensemble à la main. Les boules de neige peuvent être lancées sur n’importe quoi et n’importe où. On dit que si vous ajoutez une pierre ou de la neige jaune à l’intérieur, vous pouvez rendre la boule de neige plus dangereuse.

NOTES: 

- Objet inutile, ne peut être utilisé que pour peindre les joueurs et les murs.
- Mais si un joueur est en mode ragdoll, vous pouvez lui casser le cou avec une boule de neige (apparemment, cela a peut-être été corrigé).
