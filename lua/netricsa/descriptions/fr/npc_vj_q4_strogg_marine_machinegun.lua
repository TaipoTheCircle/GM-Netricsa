GARDE DE MITRAILLEUSE STROGG DE STROGGOS


CLASS: Variante de la Garde Strogg
METABOLISM: Cybernétique (Hybride Humain-Strogg)
PERCEPTION: Ciblage visuel + suivi de mouvement
SIZE: ~6 pieds
ENDURANCE: Moyen-Faible
HOSTILITY: Haut
WEAPONS: Mitrailleuse intégrée (bras droit), tir de balle rapide
REWARD: 500FC
THREAT: Moyen

DESCRIPTION: 

Les gardes de mitrailleuses sont l'un des types d'infanterie Strogg les plus courants. Ces anciens soldats humains ont été chirurgicalement transformés en drones semi-mécaniques, leur bras droit remplacé par des mitrailleuses lourdes. Malgré leur cybernétique rudimentaire, ce sont des combattants efficaces, déclenchant des tirs de suppression avec une intensité effrayante.

Leurs systèmes de ciblage sont imprécis, ce qui fait que leurs tirs balayent de manière erratique d'un côté à l'autre, mais leur volume de balles les rend mortels à proximité ou à mi-portée. Ils patrouillent souvent dans les couloirs industriels ou gardent des installations clés, servant ainsi de troupes de première ligne remplaçables.

TIPS: 

- Bien qu'il soit faible, le Machine Gun Guard peut infliger une quantité décente de dégâts si vous en êtes proche. Essayez de vous en éloigner si vous le voyez, puis attaquez.
- N'importe quelle arme fonctionne contre elle, mais la mitrailleuse doit être utilisée pour conserver les munitions pour des armes plus puissantes. Le fusil de chasse est bon s'ils se rapprochent.
- Un groupe de gardes mitrailleuses peut être assez puissant, alors essayez de les détruire dès que possible.
- Les explosifs peuvent éliminer la majeure partie d'un groupe en un seul coup.
- Avec d'autres ennemis, le Machine Gun Guard est de la chair à canon. Cela ne fera que vous distraire pendant que vous attaquerez des cibles plus fortes, alors essayez de vous en débarrasser dès que possible.
