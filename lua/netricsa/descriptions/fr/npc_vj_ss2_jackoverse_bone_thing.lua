DESCRIPTIF :

Cette... chose est l'une des formes de vie connues les plus mystérieuses et les plus dangereuses de la planète Kleer. Malgré de nombreux témoignages oculaires, l'origine de la créature reste inconnue. Il n'appartient à aucune sous-espèce connue de l'armée de Mental et ne ressemble à aucun représentant de la faune locale.

À la première rencontre, la Chose est facilement confondue avec une statue inhabituelle assemblée à partir des os de diverses créatures Kleer. Il est totalement immobile et peut le rester indéfiniment. Cependant, si l’observateur se détourne ou perd de vue la créature, même pour un instant, celle-ci change de position ou apparaît nettement plus proche.

Après plusieurs de ces mouvements, le comportement de la créature change radicalement. Il émet un cri assourdissant et perçant et se met à poursuivre sa cible à toute vitesse. La vitesse de la Chose est si élevée qu'il n'existe aucun cas connu d'évasion réussie.

Le contact physique avec la créature entraîne invariablement la mort instantanée de la victime.

Les tentatives pour détruire la Chose ont échoué. Les armes à feu, les explosifs, les armes énergétiques et les systèmes de combat lourds n’ont eu aucun effet notable sur lui. Selon les données disponibles, la créature est indestructible par les moyens connus.

Son apparence combine les caractéristiques du squelette de Kleer et du serpent osseux de Kleer. Cela a conduit les chercheurs à émettre l’hypothèse que la Chose pourrait être une forme évolutive inconnue de ces espèces ou leur descendant commun. Cependant, aucune preuve n’a été trouvée pour étayer cette théorie.

Les habitants de Kleer évitent les zones où le Bone Thing a été repéré. Même les chasseurs et les vétérans les plus expérimentés préfèrent ne pas s'engager avec lui, et les informations faisant état de son apparition entraînent l'évacuation immédiate des colonies voisines.

CONSEILS :

- Si vous pensez que ce n'est qu'une statue, ne tirez pas de conclusions hâtives.
- Tant que la créature reste en vue, elle reste inactive.
- Ne le perdez pas de vue une seule seconde.
- S'il commence à bouger et à vous poursuivre, la seule solution raisonnable est de quitter la zone immédiatement. Il n’existe actuellement aucun moyen connu de détruire le Bone Thing.