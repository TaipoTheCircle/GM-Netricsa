ALUDRAN REPTILOÏDE, NOËL (ROUGE)


CLASS: Aludran Reptiloïde, sous-espèce de vacances
METABOLISM: À sang froid, ressemblant à un reptile
DEFINITION: Race primitive, membre du culte du Nouvel An
SIZE: 15 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Missiles magiques à tête chercheuse (boules de feu rouges)
REWARD: 5500FC
THREAT: Moyenne

DESCRIPTION: 

Un grand reptile à quatre mains vivant dans le système Aludran dans la constellation de Canis Major. Cette race primitive a accepté de se battre pour Mental en échange des pouvoirs de lancement de sorts qu'elle leur a donnés.

Ils lancent des boules de feu automotrices dotées de capacités de guidage primitives.

The Christmas Red Aludran Reptiloid is a rare and disturbingly grotesque mutation of the plains species. Unlike its brethren, this subspecies has been doubly influenced: par la magie du Mental et par une entité connue sous le nom de Bonhomme de neige géant, qui a promis une nouvelle année éternelle. Le prix de l'accord s'est avéré symbolique et humiliant : le port obligatoire du chapeau du Père Noël pour le Nouvel An.

They are easily recognizable in appearance: des boules magiques rouges au lieu des vertes habituelles, du sang rouge et un arc rouge vif sur leurs poitrines massives, sécurisés par une magie grossière. Malgré leur apparence comique, ils restent extrêmement dangereux au combat.

Les reptiliens eux-mêmes croient sincèrement qu'ils se battent pour des vacances éternelles. Ironiquement, la nouvelle année est vraiment sans fin pour eux, mais seulement jusqu'à ce qu'ils meurent en combattant pour la guerre de quelqu'un d'autre.

TIPS: 

- Gardez-les sous le feu constant et essayez d'abattre les missiles en l'air.
- Les boules rouges sont plus visibles, tirez de manière préventive.
