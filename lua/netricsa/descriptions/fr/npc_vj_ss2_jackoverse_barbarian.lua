ONAN ARTHUR


CLASS: Onan Arthur
METABOLISM: Mammifère
PERCEPTION: Guerrier intelligent et fort
SIZE: 8,9 pieds
ENDURANCE: Haut
HOSTILITY: Conditionnel
WEAPONS: Hache magique ancienne "Excalibur"
REWARD: 400FC
THREAT: Haut

DESCRIPTION: 

Membre du clan Onan Librarian, une rare combinaison de gardiens du savoir et de guerriers. Contrairement à beaucoup de ses semblables, il n’était pas idiot et a fait preuve d’intelligence et de force dès sa jeunesse. Malgré son apparence menaçante, Arthur est connu comme l'un des héros les plus gentils et aide souvent ceux qui sont en difficulté.

Le point culminant de son voyage a été la bataille au sommet du volcan, où il a vaincu le grand puissant Golem de lave et s'est emparé de l'ancienne hache magique, un artefact au pouvoir destructeur incroyable, qui entre ses mains ne sert pas à la destruction, mais à la défense. Cette hache est considérée comme l'un des artefacts les plus dangereux, mais Arthur l'utilise à bon escient.

Onan Arthur a combattu les sorcières, mais lors d'un affrontement, il a été contraint de battre en retraite pour sauver sa peau. Plus tard, il vainquit les Romains et Sanya, prouvant une fois de plus que sa férocité et sa gentillesse vont de pair avec sa volonté de défendre son monde. Sa victoire sur le golem, les Romains et Sanya a fait de lui une légende parmi les bibliothécaires d'Onan.

TIPS: 

- Arthur n'attaque pas en premier ceux qui ne représentent aucune menace.
- Si vous voyez Arthur battre en retraite, ne présumez pas que c'est de la lâcheté. Il est peut-être en train de sauver quelqu'un ou son propre cul.
- N'essayez pas de lui voler sa hache. C'est inutile et mortel.
- Si tu es en difficulté et qu'Arthur est à tes côtés, considère-toi chanceux.
