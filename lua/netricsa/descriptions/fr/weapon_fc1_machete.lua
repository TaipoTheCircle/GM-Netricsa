MACHETTE


WEAPON: Machette
AMMO: Aucun (arme de mêlée)
ROF: ~75 balançoires/min

DESCRIPTION: 

La machette est un couteau long et large à un seul tranchant, généralement utilisé comme outil agricole dans la jungle et dans les régions tropicales, et également reconverti comme arme de mêlée. Fonction polyvalente : couper la végétation, préparer de la nourriture ou infliger des coups mortels en cas de besoin.

NOTES: 

- Aucune munition nécessaire - purement au corps à corps.
- Excellent en combat rapproché, en particulier contre des ennemis non armés ou légèrement blindés.
- Utile aussi bien pour les tâches de survie que pour le combat.
- Son poids léger et sa grande lame le rendent efficace mais peut être encombrant dans les espaces restreints.
