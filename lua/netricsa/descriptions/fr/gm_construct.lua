CONSTRUCT, OBSERVATION


À première vue, ce n'est qu'une construction. Des murs vierges. Ciel ouvert. Des formes familières. Mais quelque chose ne va pas.

Les capteurs détectent une anomalie de faible consommation d'énergie dans l'une des salles de maintenance sombres. Aucun son, aucun mouvement, mais les signatures thermiques scintillent – ​​comme si quelqu'un se tenait parfaitement immobile dans l'ombre. Regarder.

Chaque fois que vous tournez le dos, vous pouvez jurer que l’air change. La porte semble un peu plus sombre. Une silhouette se tient peut-être là - ou peut-être s'agit-il simplement de votre esprit qui assemble une forme dans la statique.

Recommendation: continuez à avancer. Ne vous attardez pas dans les couloirs sombres. Et si vous sentez que quelqu'un vous regarde, vous n'avez probablement pas tort.