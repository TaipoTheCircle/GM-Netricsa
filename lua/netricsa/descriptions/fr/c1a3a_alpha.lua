COMPLEXE DE SÉCURITÉ BLACK MESA, OÙ EST TOUT LE MONDE ?


Des Houndeyes errent ici, des crabes et quelque part dans les profondeurs, à en juger par le son, un Bullsquid. Ils ne se cachent pas. Ils sont là. Ils se sentent en confiance, chez eux. Parce que c'est leur maison maintenant. Et ils n’attendaient pas de visiteurs.

Et entre eux se trouvent les forces de Mental. Des soldats en armure familière, des fusils à plasma prêts. Ils ne combattent pas les créatures. Ils divisent le territoire. Comme des propriétaires venus prendre la relève.

L'ascenseur dont nous avons besoin est suspendu au-dessus.

Mais pour y parvenir, il nous faudra traverser plusieurs secteurs supplémentaires. L'entrée de l'ascenseur est gardée par une tourelle au plafond. Celui du haut, encastré dans le béton, regarde en bas et attend que quelqu'un apparaisse dans la zone de mise à mort. Intelligent, rapide, avec une vision à 360 degrés.

Heureusement, nous avons Zipper. Et il y a la scie circulaire et bien d'autres armes. Et voilà ce qu'ils peuvent faire avec un morceau de fer qui est resté trop longtemps suspendu au plafond.
