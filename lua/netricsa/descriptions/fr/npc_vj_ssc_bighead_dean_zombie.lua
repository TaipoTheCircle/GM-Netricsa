DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Dean Sekulic est l'un de ces titans cachés dont le génie technique a fait briller et fluidifier le monde de Serious Sam même pendant les moments les plus intenses des batailles avec des milliers d'ennemis. Son crâne exagéré est le résultat direct de sa profonde immersion dans l'architecture du moteur de rendu et de la bataille sans fin pour l'optimisation. En tant que programmeur principal et concepteur de rendu chez Croteam depuis le premier jeu, il a joué un rôle clé dans la création de Serious Engine 2 pour Serious Sam 2 (2005), où ses efforts comprenaient la mise en œuvre de technologies de pointe telles que le HDR, le bloom, le mappage de parallaxe et un système d'ombre complexe basé sur des tampons d'ombre.

Son approche pragmatique et pleine d'esprit du développement - "Je n'achète pas les conneries de relations publiques que NVidia et ATI crachent" - est devenue légendaire parmi les passionnés de technologie de jeu. Maître de l'optimisation, Dean pensait que les joueurs voulaient un jeu qui « fonctionne et se joue bien », et non une lente démo des dernières fonctionnalités. Ses contributions à la série, jusqu'à son travail sur Serious Sam 4 (2020), ont assuré le style visuel caractéristique, vibrant et incroyablement dynamique de la série qui perdure depuis des années.

Il était autrefois vivant, mais après que vous l'ayez tué dans le temple d'Hatchepsout, il est ressuscité des morts, devenant un zombie démoniaque.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", détruisez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
