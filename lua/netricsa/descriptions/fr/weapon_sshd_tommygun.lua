MITRAILLEUSE M1-A2 THOMPSON


WEAPON: Mitraillette M1-A2 Thompson
AMMO: Balles de 5,56 mm
ROF: 600 balles/min
BARREL: 10"

DESCRIPTION: 

Inventée à l'origine par le général Thompson, cette mitraillette a survécu à deux guerres mondiales et, grâce à de récentes améliorations, elle est
retour sur la piste. Cet ancien a été modifié pour accepter des munitions de 5,56 mm afin d'être compatible avec d'autres armes d'infanterie standard, et son conteneur de tambour universel peut être rempli à partir de n'importe quel pack de munitions standard.

NOTES: 

- Fournit à la fois un mode de tir rapide et des rafales courtes contrôlées.
