Scientifique en combinaison noire MESA Clean


CLASS: Scientifique en costume propre Black Mesa
METABOLISM: Humain, mâle
PERCEPTION: Personnel de recherche de Black Mesa (Division des environnements dangereux)
SIZE: ~5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Aucun
WEAPONS: Aucun
REWARD: 50 FC
THREAT: Négligeable

DESCRIPTION: 

Les Clean Suit Scientists sont une branche spécialisée du personnel de recherche de Black Mesa affecté aux environnements dangereux. Équipés de combinaisons de protection intégrale, ils sont principalement déployés dans des zones présentant des dangers biologiques, chimiques ou radiologiques, où une tenue normale de laboratoire entraînerait une mort certaine. Leurs casques sont scellés et dotés de systèmes respiratoires limités pour filtrer l'air contaminé.

Ces scientifiques travaillaient souvent sur des projets sensibles dans des zones réglementées, entretenaient des équipements ou manipulaient des matières volatiles. Lorsque la Cascade de Résonance a frappé, leurs combinaisons leur ont donné un léger avantage de survie contre les toxines et les spores de Xen, bien qu'ils soient restés tout aussi vulnérables aux menaces physiques.

Despite their professional training, they are not combatants - their survival instinct is to flee and seek shelter, leaving combat roles to security and H.E.C.U. forces. Their knowledge, however, is invaluable: beaucoup ont accès à des codes d’accès, à des systèmes de confinement et à des laboratoires de développement d’armes cachés au plus profond de l’installation.

TIPS: 

- Protégez-les si possible : ils disposent souvent d'informations utiles.
- Ne vous attendez pas à ce qu'ils se battent ; évitez de les utiliser comme boucliers.
- Écoutez attentivement leurs cris : ils pourraient indiquer un danger à proximité.
