REINE DE L'ESSAI "BÊTA"


CLASSE : Reine de l'Essaim "Bêta"
MÉTABOLISME : Xénobiologique
PERCEPTION : Reine de l'Essaim
TAILLE : 26,2 pieds
ENDURANCE : Extrêmement élevée
HOSTILITÉ : Écrasante
ARMES : tentacules fouisseurs, projectiles acides, membres écrasants
RÉCOMPENSE : 5000 FC
MENACE : Apocalyptique

DESCRIPTIF :

La Swarm Queen "Beta" est la plus grande forme de vie connue de cet essaim et l'organisme central de toute la colonie. L'Essaim se forme autour d'elle, et tous les autres individus existent uniquement pour protéger leur ancêtre et poursuivre le cycle de vie de l'essaim. La destruction de la Reine entraîne la désintégration progressive de la colonie, privée d'un centre unique de coordination.

Son corps massif est recouvert d'une épaisse armure chitineuse et soutenu par plusieurs membres puissants, lui permettant de se déplacer en toute confiance même sur des surfaces inégales. Malgré sa taille, la reine est capable de changer rapidement de position corporelle, choisissant la position d'attaque la plus avantageuse.

La caractéristique la plus distinctive de la reine sont ses longs tentacules souterrains. En les enfouissant dans le sol, elle peut soudainement faire surface sous son adversaire, les capturer et les maintenir en place tandis que de puissantes contractions des tentacules infligent des dégâts dévastateurs. Même les combattants expérimentés peuvent avoir du mal à échapper à un tel piège.

À longue portée, la Reine adopte une position stable et tire une volée de trois gros projectiles acides. Chaque impact s'accompagne d'une puissante libération de fluide biologique corrosif, capable de détruire rapidement les armures et les tissus vivants. Lors de la préparation de la volée, sa mobilité est considérablement réduite, ce qui en fait le moment le plus opportun pour une contre-attaque.

En combat rapproché, la reine utilise des membres massifs capables d'écraser pratiquement n'importe quel adversaire d'un seul coup. Grâce à sa combinaison d'immense durabilité, de contrôle spatial et d'attaques dévastatrices, elle est considérée comme le membre le plus dangereux du Beta Swarm.

CONSEILS :

- Ne restez pas trop longtemps au même endroit : les tentacules souterrains peuvent atteindre une cible stationnaire.
- Pendant qu'elle prépare sa volée d'acide, la Reine change à peine la direction de son attaque. Profitez de ce moment pour vous repositionner.
- La Reine contrôle efficacement la majeure partie du champ de bataille grâce à ses tentacules.