DESCRIPTION: 

Il est le fils du biomécanoïde rouge. Après que Sam ait tué ce dernier, le mécanosaure a juré de tuer notre sauveur.

T-Mechs - Un Tyrannosaurus rex aux pattes de poulet qui ronge des cigares. Lourdement blindé. Armé de deux lance-roquettes.

Les T-Mechs sont les biomécanoïdes de nouvelle génération – même si leur rôle de combat ne diffère pas beaucoup, ce sont les progrès scientifiques qui sont impressionnants. Auparavant, les biomécanoïdes étaient des robots génétiquement programmés dont les armes étaient directement connectées à leur système nerveux. Ils ne pensaient pas vraiment par eux-mêmes, c'était essentiellement leur réflexe. Les T-Mechs, cependant, sont capables d'avoir des pensées semi-cohésives et décident eux-mêmes quand ils souhaitent attaquer. Ces connaissances peuvent être utilisées pour améliorer de nombreux anciens modèles biomécaniques de Mental.

Comme les principaux biomécanoïdes, les T-Mechs tireront deux missiles contre leurs cibles. Cependant, ils sont à moitié dinosaures, ce qui les rend d’autant plus intimidants. Ils ont même inspiré les réalisateurs syriens à réaliser un nouveau film, "Primordial Park". Depuis sa sortie, le film bat des records au box-office.

TIPS: 

- Leurs missiles sont parmi les plus puissants que vous rencontrerez dans Mental's Horde - évitez-les ou abattez-les.
