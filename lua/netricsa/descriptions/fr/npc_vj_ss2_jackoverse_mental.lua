MENTAL (TAH-UM)


CLASSE : Mental (Tah-Um)
MÉTABOLISME : Dieu immortel
PERCEPTION : Dernier de la course Hum-Tah
TAILLE : Inconnue
ENDURANCE : pratiquement illimitée
HOSTILITÉ : Absolue
ARMES : capacités psioniques, magie ancienne, technologie Hum-Tah, innombrables armées
RÉCOMPENSE : Paix
MENACE : maximale

DESCRIPTIF :

Mental, également connu sous son vrai nom, Tah-Um, est considéré comme la plus grande menace pour la vie intelligente dans la galaxie connue. Il est le dernier représentant connu de l'ancienne race d'êtres immortels et divins, les Hum-Tah, qui possédaient autrefois des connaissances bien supérieures à la technologie moderne de la plupart des civilisations.

On estime que le mental a des millions d'années. Durant cette période, il a conquis ou détruit d’innombrables mondes, effaçant des civilisations entières de la surface de l’univers. Les peuples capturés sont soit complètement exterminés, soit font partie de son armée sans fin par modification biologique, mécanique ou génétique.

La plupart des armées connues de Mental ne sont pas composées de volontaires, mais d'êtres esclaves. De nombreuses formes de vie ont été artificiellement modifiées spécifiquement pour la guerre. Les captifs sont souvent soumis à des expériences ou à des exécutions cruelles, après quoi leurs corps sont utilisés pour créer de nouvelles unités de combat. C’est ainsi que furent créés de nombreux soldats sans tête, kamikazes et autres membres de ses forces.

Malgré son pouvoir pratiquement illimité, Mental lui-même apparaît rarement en personne sur le champ de bataille. Au lieu de cela, il dirige la guerre par l’intermédiaire de généraux, de commandants et de nombreux gouverneurs, coordonnant les actions de ses armées grâce à des capacités psioniques colossales. Sa forme observable n'est qu'une gigantesque projection sombre, tandis que la véritable apparence de Tah-Um reste inconnue.

En plus de son incroyable intellect, Mental possède l'ancienne magie de Hum-Tah, la technologie d'une civilisation disparue et des capacités psioniques qui lui permettent d'influencer l'esprit des autres, de contrôler de vastes armées et de coordonner des invasions à travers plusieurs systèmes stellaires.

Au cours des millénaires de guerre, le seul ennemi qui a contrecarré ses plans à plusieurs reprises était le « sérieux » Sam Stone (vous). C'est cette incapacité à éliminer complètement un seul individu qui a contraint Mental à recourir aux méthodes de guerre les plus radicales, y compris la destruction de planètes entières.

Malgré toutes les informations recueillies, les origines de Hum-Tah, les limites des pouvoirs de Tah-Um et sa véritable localisation restent inconnues.

CONSEILS :

- Si vous rencontrez Mental en personne, les événements ont pris une tournure très défavorable.
- Ne sous-estimez pas l'ennemi simplement parce qu'il s'engage rarement personnellement dans la bataille.
- Esquivez ses attaques.
- Il ne peut pas se lancer la Lune sur lui-même ; laissez libre cours à votre imagination !