FUSIL D'ASSAUT AK-12


WEAPON: Fusil d'assaut AK-12
AMMUNITION: 5,45×39 millimètres
ROF: ~900 tours/min

DESCRIPTION: 

Une modification moderne du fusil d'assaut emblématique, connu pour sa fiabilité et sa durabilité. Repensé pour tirer des cartouches de 5,45 × 39 mm. Différents modules peuvent y être attachés. Développé en 2011 et présenté comme le fusil d'assaut du futur pour les forces armées russes.

NOTES: 

- Haute durabilité et facilité d'utilisation.
- Recul modéré ; contrôlable lors de courtes rafales.
- Efficace contre les cibles non blindées et légèrement blindées.
- La capacité du chargeur nécessite un rechargement périodique lors de combats prolongés.
- Ce fusil d'assaut peut être modifié avec différents modules à volonté.
- Ergonomie améliorée, le fusil d'assaut est plus facile à tenir en main.
