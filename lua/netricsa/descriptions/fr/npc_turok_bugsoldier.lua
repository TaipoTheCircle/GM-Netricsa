SOLDAT ANTI-INSECTES DE LA PLANÈTE TERRAFORMÉE


CLASS: Soldat anti-insectes
METABOLISM: Insecte
PERCEPTION: Prédateur d'embuscade souterraine
SIZE: Grand
ENDURANCE: Moyenne
HOSTILITY: Extrêmement élevé
WEAPONSL: Tenailles, dard, toxine
REWARD: 300FC
THREAT: Haut

DESCRIPTION: 

Ces créatures ressemblent à un scorpion plus grand que nature, mais avec une expression plus vicieuse et un manque total d'intérêt pour autre chose que tuer.

Les Bug Soldiers vivent profondément sous la surface, où ils creusent des tunnels et attendent que quelque chose de chaud et de charnu tombe d'en haut. Parfois, ils émergent pour chasser ou simplement rappeler aux dinosaures locaux qui commande.

Au combat, les Bug Soldiers se précipitent vers leur cible et tentent de la coincer. Le combat rapproché est leur point fort. Leurs pinces agrippent, leur dard pique, et comme si cela ne suffisait pas, chaque Bug contient une toxine capable de tuer un humain en quelques secondes.

TIPS: 

- Un fusil de chasse à bout portant est le moyen le plus rapide de tuer un Bug tombé.
- N'approchez pas un insecte tombé avant d'être sûr qu'il est vraiment mort.
