COCHON FROID DU MINECRAFT OVERWORLD


CLASS: Cochon froid
METABOLISM: Sang chaud, résistant au froid
PERCEPTION: Animal sauvage/domestique
SIZE: 3 pieds
ENDURANCE: Moyenne
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 30 FC
THREAT: Faible

DESCRIPTION: 

Il s'agit d'une variante rare du cochon commun, adaptée à la vie dans les biomes de Coldy. Contrairement à leurs homologues roses des régions tempérées, ces porcs ont développé une épaisse fourrure blanche ou gris clair qui les protège du froid.

Ils errent paisiblement dans le froid en groupes de 2 à 4, grognant, creusant dans les Colddrifts à la recherche de nourriture comestible, et sont totalement non agressifs. Comme c'est généralement le cas avec les cochons, ils n'attaquent pas même lorsqu'ils sont blessés, préférant simplement fuir et devenir apparentés aux injustices du monde.

Parce qu'ils vivent dans des régions froides, les cochons froids sont légèrement plus résistants au froid que leurs parents logiques : ils ne subissent pas de dégâts du froid et peuvent traverser en toute sécurité des biomes glacés sans risquer de geler.

Ils peuvent être élevés, sellés et utilisés comme source de porc et de cuir.

TIPS: 

- Utilisez des carottes sur un bâton pour diriger un cochon froid sur une selle - c'est plus pratique par temps froid que de courir.
- Élevez-les avec des carottes, de la laitue ou des bougies pour une source de nourriture constante dans les biomes froids.
