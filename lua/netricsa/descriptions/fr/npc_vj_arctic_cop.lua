COMBINER LA PROTECTION CIVILE ARCTIQUE METROCOP


CLASS: Combiner la protection civile Arctic MetroCop
METABOLISM: Humain, mâle
PERCEPTION: Combiner la Protection Civile
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Bâton paralysant, pistolet, SMG
REWARD: 150 FC
THREAT: Faible

DESCRIPTION: 

L'Arctic MetroCop est une variante spécialisée du soldat de la protection civile combinée, adaptée pour le service dans les régions froides et enneigées. Vêtu d'un camouflage gris et blanc, destiné à le camoufler contre la glace et la neige, même si le plus souvent, il ne cache que la peur et un mauvais entraînement.

Par-dessus son uniforme, il porte un masque à gaz pour se protéger du froid, de la poussière, des gaz et, selon la rumeur, de l'odeur de ses propres camarades après de longues journées de travail. Comme les policiers réguliers du métro, il patrouille, disperse les civils, garde les installations et maintient « l'ordre », en particulier dans les zones où l'ordre a longtemps fait défaut.

TIPS: 

- Il n'est presque pas dangereux seul, mais il est rarement seul.
