SHOFIELD .45 AVEC TMAR


WEAPON: Shofield .45 avec TMAR
AMMO: Cartouches hautement explosives de 40 mm
ROF: simple action, rechargement manuel
MAGAZINE: 6 balles + TMAR
WEIGHT: 2 livres, 5 onces
LENGTH: 12 3/4"

DESCRIPTION: 

Réplique du modèle original de cavalerie Shofield de 1875, modifié pour une précision accrue. Une arme solide et fiable, mais normalement peu efficace pour les situations de combat en raison de sa faible cadence de tir.
Celui-ci est équipé d'un petit réapprovisionnement de munitions techno-magique (TMAR) capturé par les forces de Mental, pour fournir un approvisionnement infini en munitions. Utile comme défense de dernier recours et pour éliminer les ennemis les plus faibles à longue portée.

NOTES: 

- Peut être utilisé en combinaison avec le système de détection de cible de NETRICSA pour un tir précis à longue distance.
