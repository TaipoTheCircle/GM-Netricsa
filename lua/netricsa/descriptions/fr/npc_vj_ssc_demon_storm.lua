DÉMON REPTILOÏDE FIENDIEN, STORMCALLER


CLASSE : Démon reptiloïde fiendien, Stormcaller
MÉTABOLISME : à sang froid, semblable à celui d'un reptile
PERCEPTION : Race primitive modifiée
TAILLE : 30 pieds
ENDURANCE : Élevée
HOSTILITÉ : Élevée
ARMES : Tornades, Griffes
RÉCOMPENSE : 8000 FC
MENACE : Moyenne

DESCRIPTIF :

Une autre mutation hideuse, gracieuseté de Notorius Mental. Un simple organisme ressemblant à un lézard de Fiendia Prime a été croisé et muté sur plusieurs générations, et a finalement été renforcé par un squelette en titane. Il a une capacité unique à tordre l’air ambiant, atteignant des vitesses de vent allant jusqu’au niveau des tornades F2. Ces tornades miniatures finiront par se stabiliser et se dissiper, mais elles feront des ravages entre-temps.

CONSEILS :

- Les tornades projetteront des objets et redirigeront les projectiles.
- Il n'y a aucun moyen de détruire les tornades du Démon... Évitez-les simplement jusqu'à ce qu'elles se dissipent.