BLACK MESA, DÉRAILLEMENT


Sam, félicitations, la fusée est partie. Les capteurs confirment un allumage réussi, un guidage stable, une trajectoire propre. Pour une fois, quelque chose dans cette installation maudite fonctionnait comme prévu. Ne soyez pas trop à l'aise, cependant : la victoire à Black Mesa ne dure jamais plus de cinq minutes.

Vous êtes monté à bord d'une autre ligne de fret pour continuer vers Lambda. Au début, le trajet semble presque paisible comparé au chaos derrière vous. Les tunnels résonnent avec le grondement des roues, et pendant une seconde, cela ressemble presque à un progrès.

Les analyses structurelles montrent que le rail devant est compromis. Avant que vous puissiez réagir, les voies cèdent et le wagon tout entier plonge dans l'eau noire en contrebas.

The good news: tu es toujours en vie. Mauvaise nouvelle : la voiture coule rapidement et des signatures hostiles convergent à la surface de l'eau. Vous devrez vous frayer un chemin pour sortir avant de vous noyer ou de vous laisser entraîner.
