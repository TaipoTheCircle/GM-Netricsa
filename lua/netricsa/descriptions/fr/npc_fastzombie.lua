ZOMBIE DE CRABE RAPIDE


CLASS: Zombie crabe, rapide
METABOLISM: Hôte parasite
PERCEPTION: Entité zombifiée
SIZE: 6 pieds
ENDURANCE: très vite
HOSTILITY: Extrême
WEAPONS: Griffes, sauts, grande vitesse
REWARD: 250 FC
THREAT: Moyen-élevé

DESCRIPTION: 

Contrairement à leurs frères plus lents, les zombies rapides sont animés par une énergie agitée. Autrefois humain, son corps a été déformé par le crabe en une forme squelettique et nerveuse qui se déplace avec une agilité terrifiante. Ils sprintent à quatre pattes et sautent sur les toits en hurlant comme des prédateurs en chasse. Leur endurance est faible, mais leur vitesse et leur poursuite incessante en font l’un des ennemis les plus déconcertants à affronter.

TIPS: 

- Ne les laissez pas réduire la distance.
- De courtes rafales de feu peuvent les arrêter à mi-charge.
