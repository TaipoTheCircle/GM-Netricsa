CENTRE TECHNOLOGIQUE BLACK MESA, COULOIR DE LA FOLIE SCIENTIFIQUE


Sam, nous sommes dans un endroit qui, même selon les standards de Black Mesa, semble étrange. Un long couloir. Avec une courbe. Et des portes. Beaucoup de portes. Derrière chacun, un laboratoire. Petit, mais chacun avec son propre thème. Sa propre obsession. C’est sa propre petite catastrophe, qui attend d’être déclenchée.

Nous marchons dans le couloir. Une porte à gauche. Une porte à droite. Au bout, un virage, et au-delà, encore des portes. Pas de fenêtres. Aucun signe, à l'exception des plaques sur les portes elles-mêmes. Black Mesa a étudié ici tout ce qui ne rentre pas dans d'autres secteurs. Tout ce qui semblait trop inutile pour le budget de base, mais trop intéressant pour être jeté.

Qu'y a-t-il derrière les portes ?

Lumière colorée. Un laboratoire où ils ont étudié les effets de différentes longueurs d'onde sur le psychisme. Les résultats se trouvent probablement dans des coffres-forts quelque part. Ou sur les murs, sous forme de taches laissées par ceux qui ont trop longtemps regardé.

Échantillons de droïdes. Unités de test de robots. Certains ne sont que des blancs. Certains sont alimentés.

Transparence. Recherche de matériaux qui laissent passer la lumière mais pas les balles. Ou vice versa. Ou ne rien laisser passer sauf la peur.

Pantherai. A living creature was discovered in one of the rooms. Not a specimen. Not an exhibit. Just a pantherai. Where did it come from? Why was it brought here? Why is it still here? There are more questions than answers. The answer is simple: n'y entrez pas sans armes.

PolyRobot Zentraedi. Module de combat tactique. Créé ici. Testé ici. Probablement considéré comme un succès. Ou non. Mais le module est quelque part, derrière l’une des portes. En attendant. Regarder. Écoute. Et danser...

N'ouvrez pas toutes les portes. Si le signe semble menaçant, c’est probablement le cas. Si le panneau semble inoffensif, il s'agit probablement d'un piège.
