COMBINEZ H.E.C.U. SOLDAT


CLASS: Combinez H.E.C.U. Soldat
METABOLISM: Humain augmenté
PERCEPTION: Infanterie d'assaut
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Haut
WEAPONS: OIAC, AR2, SPAS-12
REWARD: 850 FC
THREAT: Haut

DESCRIPTION: 

Combinez H.E.C.U. les soldats sont les Marines américains survivants qui ont survécu à l'enfer de Black Mesa. Ceux qui n'ont pas été déchirés par les créatures de Xen ont finalement affronté un ennemi bien plus impitoyable : le Combine. Certains ont été éliminés comme témoins potentiellement dangereux, mais les plus expérimentés, disciplinés et moralement brisés ont été recrutés de force.

Le Combine a évalué leur expérience de combat comme étant « exceptionnellement utile ». Contrairement aux soldats d'Overwatch standards, les H.E.C.U. conservaient un degré important d'autonomie de pensée et de flexibilité tactique, juste assez pour être utiles et sûrs. Ils ont subi une augmentation partielle, améliorant leurs réflexes et supprimant le stress post-traumatique, mais leur personnalité n’a pas été complètement réécrite.

Ils portent un camouflage bleu. Certains portent des respirateurs noirs, d'autres des cagoules et des casques tactiques à l'ancienne, légèrement modifiés avec la technologie Combine.

TIPS: 

- Ne les sous-estimez pas : ce ne sont pas des recrues d'Overwatch, mais des survivants vétérans.
- Ils sont dangereux dans les espaces confinés, surtout avec SPAS-12.
- Ils agissent parfois de manière non conventionnelle : attendez-vous à des manœuvres de flanc et à des embuscades.
- Derrière leur sang-froid, les souvenirs de Black Mesa... Freeman, et la colère se cachent encore.
