XEN, APRÈS


Le Nihilanth est mort. La faille est fermée. Xen se tait...

Mais avant même que vous puissiez reprendre votre souffle, la réalité vacille et vous êtes retiré du champ de bataille. Vous réapparaissez dans un vide stérile et sans fin. Un homme vêtu d’un costume bon marché se tient devant vous, une mallette à la main, un sourire faible mais acéré.

Il vous informe que toutes vos armes, votre H.E.V. votre costume - même vos sous-vêtements - ont été confisqués. "Bien de l'État", dit-il, "appartenant à une entité, Prospero". Son ton est poli, mais chaque mot ressemble à un contrat que vous n'avez jamais signé.

Puis il commence à parler de toi. Il vous appelle "FREEMAN", un nom que vous commencez à détester. Il loue votre performance, admire votre efficacité, et pourtant chaque compliment sonne comme une cage.

He offers you a choice: 

Restez ici, sans armes, entouré d'extraterrestres qui vont probablement vous déchirer.

Ou entrez dans un portail de stase lumineux et bourdonnant, où vous « attendrez » que ses employeurs décident de vous réveiller. Peut-être demain. Ce ne sera peut-être jamais.

L'homme attend. Le portail palpite. Il est temps de choisir.
