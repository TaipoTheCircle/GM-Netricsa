COMBINEZ UN OFFICIER D'ÉLITE D'OVERWATCH


CLASS: Combiner Officier d'élite d'Overwatch
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner l'unité de commandement et de reconnaissance
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrême
WEAPONS: Pistolet silencieux, pistolet lance-fusées à appel de renfort
REWARD: 800 FC
THREAT: Très élevé

DESCRIPTION: 

L'officier d'élite du Combine Overwatch est le commandant de terrain du Combine, combinant les fonctions de reconnaissance, de coordinateur et de suppression psychologique. Il porte un uniforme tactique sombre avec une armure renforcée aux genoux, des bottes noires et un béret noir, soulignant son statut parmi les soldats ordinaires.

Son casque a été remplacé par une version comportant une seule visière lumineuse blanche et jaune, qui sert de scanner tactique et d'interface de commande. Une cape grise est drapée sur ses épaules, servant d'élément de camouflage et de symbole de rang. Une radio longue portée est attachée à son dos, lui permettant d'appeler des renforts auprès des transports d'élite Combine.

Il est armé d'un pistolet silencieux pour l'assassinat furtif et la suppression de témoins. Lorsqu'il détecte une menace, l'officier utilise un pistolet lance-fusées pour appeler des escouades de soldats, de combattants d'élite ou de drones de soutien. Son travail ne consiste pas à s'engager dans un combat prolongé, mais à gérer la bataille, en transformant un incident localisé en une opération combinée à grande échelle.

TIPS: 

- Tuez l'officier avant qu'il n'appelle des renforts.
- Le pistolet silencieux fait de lui un chasseur presque invisible.
- La radio est son plus grand atout. Sans cela, il n'est qu'un simple commandant.
- Don't underestimate him: il se tire rarement une balle, mais il décide qui vous tirera dessus.
