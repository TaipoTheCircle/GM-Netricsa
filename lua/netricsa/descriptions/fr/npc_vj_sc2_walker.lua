MARCHEUR DE LOEK-III


CLASS: Marcheur
METABOLISM: Créature extraterrestre
PERCEPTION: Lumière au sol
SIZE: ~5-6 pieds de haut
ENDURANCE: Faible-Moyen
HOSTILITY: Haut
WEAPONS: Attaques de mêlée
REWARD: 300FC
THREAT: Moyen

DESCRIPTION: 

Le Walker est l’un des Lumes les plus courants. Ils viennent en hordes et poursuivent les Gardiens Noyaux chaque fois qu'ils les repèrent. Individuellement, ils sont faibles et mal blindés, mais leur danger vient du nombre. Ces créatures apparaissent souvent en grands groupes, submergeant les défenses par leur volume.

Les marcheurs n'ont pas de pouvoirs spéciaux ni d'attaques à distance ; ils avancent simplement, se balançant légèrement lorsqu'ils se déplacent, essayant d'atteindre et d'endommager le noyau. Leur point faible est une petite région oculaire rougeoyante sur le devant, qui est plus difficile à atteindre en raison de leur mouvement.

TIPS: 

- Utilisez des tours de contrôle des foules ou des dégâts de zone pour réduire leur nombre.
- Ciblez le point faible rouge pour des attaques plus rapides.
