FUSIL D'ASSAUT AK-203


WEAPON: Fusil d'assaut AK-203
AMMUNITION: 7,62x39mm
ROF: ~900 tours/min

DESCRIPTION: 

Une modification moderne du fusil d'assaut emblématique, connu pour sa fiabilité et sa durabilité. Il tire de puissantes cartouches de calibre 7,62 mm avec un recul modéré. Il est efficace à moyenne portée et capable d'effectuer des tirs soutenus pour la suppression ou l'engagement direct. Différents modules peuvent y être attachés.

NOTES: 

- Haute durabilité et facilité d'utilisation.
- Recul modéré ; contrôlable en courtes rafales.
- Efficace contre les cibles non blindées et légèrement blindées.
- La capacité du chargeur nécessite un rechargement périodique lors de combats prolongés.
- Ce fusil d'assaut peut être modifié avec différents modules à volonté.
