PISTOLET WALTHER P22


WEAPON: Walther P22
MUNITIONS .22 LR
ROF: 15 tours/min

DESCRIPTION: 

Le pistolet Walther P22 est un pistolet de sport et d'entraînement à chargement automatique allemand de calibre 22 fabriqué par Carl Walther Sportwaffen GmbH. Le cadre du pistolet est constitué de polymères résistants aux chocs, tandis que le canon et la glissière sont en acier. Il est équipé d'un mécanisme de déclenchement à double action. Les dispositifs de sécurité comprennent une sécurité mécanique, une sécurité automatique qui verrouille le mécanisme lorsque le chargeur n'est pas inséré correctement et un verrou qui verrouille le cadre coulissant, empêchant ainsi le tir et le démontage. Le modèle P22 ressemble au modèle précédent Walther P99, mais est environ un quart plus court. Le pistolet est livré avec des viseurs et des panneaux de poignée interchangeables pour permettre à l'arme d'être personnalisée en fonction de la main du tireur.

NOTES: 

- Vous pouvez y attacher un silencieux et devenir un tueur silencieux.
- Le fait qu'il s'agisse d'un pistolet de sport le rend moins efficace au combat que les pistolets classiques.
- Un lointain descendant du bon vieux Walther P38.
