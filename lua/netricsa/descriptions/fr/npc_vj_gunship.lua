NAVIRE DE PATROUILLE AÉRIENNE AUTOMATIQUE DU CYBERSTAN


CLASSE : Gunship de patrouille aérienne automatisée
MÉTABOLISME : Mécanique
PERCEPTION : Navire de patrouille volant, soutien aérien
TAILLE : 13 pieds
ENDURANCE : Moyenne
HOSTILITÉ : Absolue
ARMES : canons à tir rapide, missiles
RÉCOMPENSE : 2000 FC
MENACE : Élevée

DESCRIPTIF :

Il s'agit d'un navire de patrouille volant Automaton conçu pour soutenir les troupes au sol et détruire l'infanterie Super-Terre depuis les airs. Extérieurement, il ressemble à un drone agrandi avec deux moteurs principaux et des ailes delta distinctives. Des canons Rapid-Fire sont montés sur les côtés du fuselage et des lanceurs de missiles sont montés sur les ailes.

Il plane dans les airs, tire sur les positions des Helldiver et peut les poursuivre à basse altitude. Il peut être détruit avec des RPG, des canons automatiques ou des Stratagèmes.

Ils patrouillent dans les zones d'atterrissage, gardent les avant-postes des Automates et attaquent les Helldivers qui s'attardent trop longtemps au même endroit.

Il présente plusieurs points faibles : Moteurs et Ailes.

CONSEILS :

- Lorsque vous entendez le bruit des moteurs, cherchez à vous mettre à l'abri. Il peut attaquer depuis n’importe quelle direction.
- Ne restez pas à l'air libre. Il vous tirera dessus depuis les airs.
- Pour le détruire, utilisez des RPG ou des canons automatiques. Le fusil consomme trop de cartouches.
- Si le vaisseau est en vol stationnaire, c'est le meilleur moment pour attaquer. Tirez sur les moteurs ou les ailes.
- Ils arrivent souvent en groupe. Détruisez-les un par un pour éviter d'être pris entre deux feux.
- Si vous avez déclenché un Stratagème, assurez-vous que le Gunship ne perturbe pas votre largage de renforts.
- N'essayez pas de le distancer. C'est plus rapide que toi. Il vaut mieux essayer de le perturber.