BLACK MESA, AILE ADMINISTRATION


Sam, le bureau est devenu un champ de bataille interdimensionnel. D'un côté, des escouades de soldats de Mental prennent d'assaut les cabines, détruisant les cloisons sèches et la paperasse. De l’autre, les créatures Xen – crabes, vortigaunts et cauchemars assortis – affluent à travers de nouvelles failles, déchirant tout ce qui bouge.

Ce qui était autrefois une branche administrative ordonnée ressemble désormais à une guerre.

Ce n'est plus seulement votre combat, c'est une lutte à trois entre l'humanité, les forces de Mental et l'avant-garde de Nihilanth. L'avantage ? Ils sont trop occupés à s'entre-tuer pour vous remarquer immédiatement. L'inconvénient ? Vous êtes toujours coincé au milieu d’une zone de guerre au bureau.

Recommendation: laissez les deux factions s'adoucir, puis avancez rapidement à travers le chaos avant de se rappeler que le vrai problème, c'est vous.
