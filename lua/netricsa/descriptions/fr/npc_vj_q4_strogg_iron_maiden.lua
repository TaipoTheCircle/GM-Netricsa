STROGG JEUNE FILLE DE FER DE STROGGOS


CLASS: Strogg, la jeune fille de fer
METABOLISM: Soldat cybernétique/biomécanique
PERCEPTION: Unité d'assaut aérien Strogg
SIZE: 7,5 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Lanceur de missiles (à gauche "manipulateur"), Energy Wave Scream, Close Combat "Claw"
REWARD: 1800 FC
THREAT: Haut

DESCRIPTION: 

Le Strogg Iron Maiden est une unité de combat volante de l'armée Strogg, une créature cybernétique semi-vivante avec un haut du corps ressemblant à une figure féminine et des rubans d'énergie flottants à la place des jambes, lui permettant de planer dans les airs.

Elle est équipée d'un puissant lance-roquettes sur son bras gauche et peut tirer une série de roquettes avec un rayon d'explosion important. Sa grande maniabilité et sa capacité à se téléporter brièvement lui permettent d'échapper aux attaques directes, ce qui en fait un adversaire dangereux à moyenne et longue portée.

À courte portée, Iron Maiden utilise un cri d'énergie super-tonal de courte durée qui non seulement inflige des dégâts mais désoriente également la cible. Dans les cas extrêmes, elle peut attaquer avec sa « griffe », une griffe mécanique de mêlée.

TIPS: 

- En raison du rayon de souffle de la fusée, évitez les espaces ouverts et restez à couvert.
- Iron Maiden peut se téléporter et esquiver.
- Son cri de mêlée super-tonal désoriente la cible : gardez vos distances.
