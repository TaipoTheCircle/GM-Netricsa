FUSIL D'ASSAUT DE VODKONSON


WEAPON: Fusil d'assaut de Vodkonson
AMMO: Capsules de bouteilles de vodka
ROF: ~1800 tirs/min

DESCRIPTION: 

Le fusil d'assaut de Vodkonson est une arme primitive mais incroyablement efficace. La vodka accélère les mains de l'utilisateur jusqu'à des niveaux surhumains. L'utilisateur insère manuellement les bouchons dans la bouteille, mais il le fait si rapidement que l'arme semble arracher les bouchons de nulle part.

Les capsules s'envolent à une vitesse incroyable grâce à la force insensée des bras du tireur, se transformant en projectiles mortels capables de déchirer n'importe quelle créature ou structure.

NOTES: 

- L'utilisateur éprouve une force et une vitesse de bras incroyables après avoir bu de la vodka.
- Les casquettes déchirent la chair, les armures et les synthés grâce à une vitesse supersonique.
- Peut être utilisé pour jouer aux quilles avec une variété d'ennemis.
