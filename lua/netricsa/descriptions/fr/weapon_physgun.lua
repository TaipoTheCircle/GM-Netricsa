PISTOLET PHYSIQUE


WEAPON: Outil de manipulation physique (pistolet physique)
AMMO: Aucun (manipule les objets physiques)

DESCRIPTION: 

Outil avancé pour saisir, faire pivoter et déplacer des objets avec précision. Permet à l'utilisateur de manipuler l'environnement ou des objets à distance, notamment en soulevant, en tournant et en gelant des objets dans les airs. Volé à Aperture Science par Black Mesa.

NOTES: 

- Utilisation infinie ; aucune munition traditionnelle requise.
- Capable de lancer des objets comme armes improvisées.
- Utile pour la construction, les énigmes et l'improvisation de combat.
- Nécessite des compétences pour un positionnement et un contrôle précis.
