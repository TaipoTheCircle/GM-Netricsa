BLACK MESA, BLACK OPS APERÇUS


Sam, I need your full attention here. We've been tracking multiple heat signatures moving with inhuman precision - silent, fast, deliberate. Identification confirmed: Assassins féminins des Black Ops.

Unlike H.E.C.U. they specialize in stealth, ambush, and close-range elimination. Standard armor: léger, optimisé pour l’agilité plutôt que pour la protection. Arsenal standard : pistolets silencieux, SMG. Tactique standard : frappez depuis l'ombre, disparaissez avant de pouvoir viser.

Warning: ils ne se battent pas comme des soldats, Sam. Ils se battent comme des prédateurs. Les rapports montrent des acrobaties améliorées – flips, sauts muraux, esquives impossibles. Votre approche habituelle de course à pied ? Attendez-vous à ce qu'il échoue. Vous devrez observer les signaux sonores, les éclairs de bouche et les ombres pour les suivre.

Recommendation: continuez à avancer, forcez-les à sortir et ne les sous-estimez pas. Ce ne sont pas les brutes stupides de Mental ou les créatures sauvages de Xen et de Race X. Ce sont des professionnels formés pour effacer les problèmes comme vous.

Oh, and one more thing: leur mission c'est d'effacer les témoins. Ce qui signifie chaque scientifique, chaque garde, chaque grognement H.E.C.U... et vous.
