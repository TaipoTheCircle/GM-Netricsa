DESCRIPTION: 

Le Green Boid est une sous-espèce rare du familier Xen Boid, documenté pour la première fois à bord du navire de recherche UH-1. Сcolorés avec une luminescence verte vibrante, ils glissent à travers des espaces confinés en groupes synchronisés. Comme leurs cousins ​​Xen, ils sont totalement inoffensifs et servent de spectacles vivants plutôt que de prédateurs.

Les chercheurs pensent que leur lueur pourrait être une forme de communication ou de cohésion de groupe, leur permettant de former des schémas aériens plus serrés, même dans l'obscurité. Malgré leur apparence extraterrestre, ils restent des créatures curieuses et dociles, sans agressivité envers les humains ou les autres êtres.

TIPS: 

- Ne leur faites pas de mal, ils ne représentent aucune menace.
- Observez leurs schémas de rassemblement pour trouver des indices sur les dangers environnementaux à proximité.
