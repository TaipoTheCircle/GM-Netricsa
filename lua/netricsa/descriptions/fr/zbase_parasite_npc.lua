ESSAI PARASITE


CLASSE : Essaim parasite
MÉTABOLISME : Parasite xénobiologique
PERCEPTION : Organisme infectieux
TAILLE : 0,9 pi
DURABILITÉ : Très faible
HOSTILITÉ : Extrême
ARMES : Infection, attaque sautée
RÉCOMPENSE : 50 FC
MENACE : Élevée

DESCRIPTIF :

Le parasite est l’une des formes de vie en essaim les plus nombreuses et les plus dangereuses. Malgré leur petite taille et leur corps fragile, ces créatures constituent une menace sérieuse, même pour les combattants bien équipés.

Leur principale méthode de chasse consiste à sauter rapidement sur la victime. Lors d'une attaque réussie, le parasite implante des larves dans le corps de l'hôte ; ces larves commencent alors à se développer rapidement au sein des tissus vivants. Dans les premiers stades, l'infection peut passer pratiquement inaperçue, mais l'état de la victime se détériore rapidement avec le temps.

Le cycle de vie de la créature présente un danger particulier. Si l’hôte infecté meurt, les larves achèvent leur développement et émergent du corps, formant un nouvel essaim de parasites. Ainsi, même une seule attaque réussie peut entraîner une augmentation rapide de la population de l’Essaim.

Les parasites éclosent à partir d’œufs organiques, généralement déposés dans des endroits humides et abrités. L'intérieur des amas d'œufs est rempli d'un fluide biologique visqueux essentiel au développement des jeunes. Par conséquent, de grandes concentrations de parasites se trouvent le plus souvent dans les tunnels humides, les complexes souterrains et les colonies abandonnées.

CONSEILS :

- Détruisez les parasites avant qu'ils ne se trouvent à portée de bond.
- Si vous découvrez une grappe d'œufs, détruisez-la au plus vite.
- Un seul parasite pose rarement un problème. Les problèmes commencent généralement plus tard.