DESCRIPTION: 

La star de Broadside Animation et le visage de l'entreprise. Un grand castor brun avec un sourire permanent et deux grandes dents de devant. Il a été créé par Rex Broadside après avoir accidentellement perdu les droits de son premier personnage, Blot the Rabbit. L'inspiration est venue lorsque sa mère lui a apporté une gaufre qui lui rappelait une queue de castor.

Early Bucky cartoons were dark and violent-each episode featured the beaver's death at the end, which fueled his hatred for his creator. Later, the character was softened, and friends were added: Olivia la loutre, Giovanni l'oie et le frère de Walter le morse. Bucky est devenu un favori des fans, son visage reconnaissable par des millions de personnes et ses produits sous licence ont rempli les étagères des magasins.

Brandon Lester était un acteur engagé pour porter le costume de Bucky au parc à thème Broadside Beach. Il voulait le poste et l'a apparemment obtenu, en le prenant à un autre candidat plus joyeux. Mais un jour, Brandon est mort d'une overdose de fentanyl.

Au lieu d'enterrer son corps, Broadside Animation l'a utilisé pour une expérience macabre. Le corps de Brandon a été fusionné avec de la chair mystérieuse provenant des grottes de Nulla Terra, créant ainsi « Starling », un costume de Bucky vivant qui ne nécessitait pas d'acteur à l'intérieur. Ce fut le premier Starling public.

Après sa transformation, Brandon a commencé à tuer.

His Victims: 

Olivia Finch (Olivia the Otter): Brandon l'a noyée dans le lac près du complexe d'habitation des employés. Son corps a ensuite été utilisé pour créer l'Étourneau d'Olivia.

Gary Wilson (Giovanni the Goose): Brandon s'est fracassé le visage contre un mur, puis a enfoncé sa tête dans une friteuse en marche. Gary est devenu l'étourneau de Giovanni.

Nathan Stewart (Walter the Walrus): Brandon l'a matraqué à mort avec une chaise en bois dans les bureaux de Broadside Animation. Nathan est devenu l'étourneau de Walter.

