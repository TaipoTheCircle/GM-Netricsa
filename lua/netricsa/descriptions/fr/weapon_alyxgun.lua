PISTOLET D'ALYX "ALYXGUN"


WEAPON: Pistolet d'Alyx "Alyxgun"
AMMO: Rondes de 9x19mm
ROF: S emi-automatique, ~20 coups/min

DESCRIPTION: 

Arme de poing semi-automatique compacte utilisée par Alyx Vance. Offre une grande précision et une puissance d'arrêt modérée, adaptée à la fois aux tirs précis et aux attaques de suivi rapides. Léger et fiable pour les scénarios de combat urbain.

NOTES: 

- Excellente précision à courte et moyenne portée.
- Taille limitée du chargeur ; nécessite un placement minutieux des tirs.
- Arme de secours efficace lorsque les armes à feu plus lourdes ne sont pas disponibles.
- Favorisé par les tireurs d'élite qualifiés pour les engagements furtifs ou tactiques.
