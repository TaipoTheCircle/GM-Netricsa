COMBINEZ LE SYNTHÉ D'ASSAUT


CLASS: Combiner un synthétiseur d'assaut
METABOLISM: Synthé biomécanique
PERCEPTION: Petit synthétiseur d'assaut
SIZE: ~5 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Canon synthé intégré
REWARD: 300
THREAT: Moyen

DESCRIPTION: 

L'Assault Synth est une unité conceptuelle Combine - une construction biomécanique rapide à quatre pattes conçue pour des frappes coordonnées sur le champ de bataille. Mesurant environ 1,50 mètre de haut, ces synthés sont suffisamment compacts pour envahir et submerger les forces d'infanterie. Leurs quatre membres agiles leur permettent une mobilité exceptionnelle, leur permettant de courir à grande vitesse et de déjouer les défenseurs.

Ils sont souvent déployés aux côtés des Crab Synths, agissant comme des tirailleurs rapides qui complètent le rôle d'assaut lourd de leurs homologues plus grands. Bien qu'individuellement fragiles par rapport aux synthés majeurs, en groupe, ils deviennent une menace sérieuse, harcelant l'infanterie et affaiblissant les défenses des unités combinées plus lourdes.

TIPS: 

- Restez vigilant lorsque des Crab Synths sont présents - Les Assault Synths les accompagnent généralement.
- Les armes à zone d'effet (grenades, fusils à pompe) sont les contre-attaques les plus efficaces.
