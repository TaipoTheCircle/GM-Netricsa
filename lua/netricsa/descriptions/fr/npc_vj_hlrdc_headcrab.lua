CRABE DREAMCAST DE XEN


CLASS: Crabe, Dreamcast
METABOLISM: xénofaune parasitaire
PERCEPTION: Arme biologique
SIZE: ~1-2 pieds
ENDURANCE: médium individuellement
HOSTILITY: élevé (lors de l'attachement)
WEAPONS: morsure, attachement parasitaire permanent
REWARD: 150 FC
THREAT: modéré individuellement, élevé en groupe

DESCRIPTION: 

Les crabes communs sont les parasites Xen classiques connus pour s'attacher de manière permanente à la tête d'un hôte. Contrairement à la variante terrestre, qui peut sauter et se déplacer indépendamment, le crabe commun s'enfouit dans le crâne et reste lié pour la vie (ou jusqu'à ce qu'il soit tué), transformant l'hôte en un zombie de crabe commun ou un zombie de crabe coureur, évoluant finalement en un Gonome.

The fusion is horrifying: le crâne de l'hôte est visible sous la carapace extraterrestre et le contrôle de la créature est absolu. Une fois attaché, le Headcrab dicte ses mouvements, son agressivité et son comportement, faisant de son hôte un prédateur lent et implacable.

TIPS: 

- Évitez d'être mordu - une fois attaché, le retrait est presque impossible sans tuer l'hôte.
- Les armes à feu ou les explosifs sont efficaces simultanément contre l'hôte et le parasite.
- Gardez un œil sur les groupes ; des essaims de crabes communs peuvent rapidement submerger les intrus.
