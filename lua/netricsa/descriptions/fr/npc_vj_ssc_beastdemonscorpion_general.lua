ALUDRAN REPTILOÏDE, HIGHLANDER


CLASS: Scorpion démoniaque bestial, adulte
METABOLISM: Exobiologique, Arthropode
PERCEPTION: Scorpion de combat adulte
SIZE: 20 pieds
ENDURANCE: Extrême
HOSTILITY: Extrême
WEAPONS: Piqûre de queue mortelle
REWARD: 12000FC
THREAT: Absolu

DESCRIPTION: 

C'est ce que devient un jeune Scorpion s'il vit assez longtemps pour tuer quelques milliers de personnes supplémentaires. Solgar encadre personnellement les spécimens les plus prometteurs, sélectionnant ceux qui ont fait leurs preuves au combat et leur permettant de survivre jusqu'à l'âge adulte. Les résultats sont impressionnants.

Un scorpion adulte est remarquablement différent en apparence de son homologue juvénile. La carapace verte brillante du juvénile s'assombrit avec l'âge, devenant d'un vert noirâtre profond et sombre, se fondant presque dans l'obscurité.

La tête est protégée par un casque en forme de casque avec un subtil éclat métallique. Les yeux sur les côtés de la tête brillent dans le noir avec une lumière bleue et froide.

La queue de l'adulte est plus longue, plus épaisse et plus mortelle.

TIPS: 

- Utilisez des explosifs, en grande quantité. C4, grenades, roquettes, tout ce que vous avez.
- Ne le laisse pas s'approcher trop près, il te tuera plus vite que tu ne peux tirer.
