MESA NOIRE, HALLS SUPÉRIEURS


The elevator ride ends with a charming little scene: un scientifique terrifié frappant à une porte verrouillée, suppliant l'homme avec la mallette à l'intérieur de le laisser entrer. "Ils arrivent, s'il vous plaît, ouvrez la porte !"

Spoiler: un tentacule massif. Ci-dessus : des conduites de carburant, des pompes et une plate-forme de réacteur qui, si elle est mise en service dans le bon ordre, devient le lance-flammes le plus efficace au monde – et un moyen pratique de transformer un tentacule en un feu de joie très coûteux.

Et oui, cela signifie que l’armée est là maintenant. Pas pour sauver qui que ce soit, bien sûr, mais pour s'assurer que personne ne reparte vivant.
