WILLIAM AFTON, L'ÉLITE D'OVERWATCH DERRIÈRE LE MASSAGE



CLASS: William Afton, l'élite d'Overwatch derrière le massacre
METABOLISM: Humain augmenté, mâle
PERCEPTION: Tueur solitaire, chasseur de soldats de l'Alliance
SIZE: 5,9 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Absolu
WEAPONS: AR2, OICW, SPAS-12, grenades
REWARD: 9000FC
THREAT: Critique

DESCRIPTION: 

William Afton est un tueur en série de soldats de l'Alliance, un être qui a depuis longtemps cessé d'être simplement humain. Il portait autrefois une combinaison Overwatch Elite, mais elle était violette, ce qui le distinguait même parmi les Elites. Aux côtés de Wallace Breen, Afton était à l'avant-garde de la marque de cafétéria de la Citadelle, occupant divers postes dans les établissements de l'Alliance. Derrière la façade du « service », Afton cachait un prédateur, étudiant méthodiquement les faiblesses de son entourage.

Au fil du temps, les esprits enragés de ses victimes ont piégé Afton dans sa propre armure, transformant l'armure à la fois en prison et en sarcophage. A partir de ce moment, il est devenu un massacre vivant. Son sang est violet et son corps affiche une vitalité anormale, comme s'il avait déjà partiellement franchi la frontière entre la vie et la mort.

Afton est un sociopathe prononcé. Son narcissisme imprègne chacune de ses actions, alimentant sa nature manipulatrice. Il ne se contente pas de tuer, il domine. Ciblant habilement les points vulnérables, Afton attire les soldats de l'Alliance dans des pièces isolées où il les exécute en privé. Il se délecte de la douleur de ceux qui l’entourent, transformant la peur en un outil.

Deep down, Afton understands his own worthlessness, but compensates for this with a distorted sense of grandeur, exaggerating the achievements of others and claiming them for himself. He is obsessed with science and technological advances, seeing them as the path to his cherished goal - immortality. A religious fear of hell makes him even more cruel: Afton veut vivre éternellement, à tout prix.

Au combat, il utilise toutes les armes disponibles, variant son style de la suppression méthodique au combat rapproché sanglant. Il prend son temps. Il joue.

TIPS: 

- Ne sois jamais seul avec lui, c'est exactement ce qu'il veut.
- Sa capacité de survie élevée rend peu probable qu'il soit tué rapidement.
- Dangereux à n'importe quelle distance.
