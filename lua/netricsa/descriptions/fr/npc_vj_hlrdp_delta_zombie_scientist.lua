CRABE ZOMBIE À BASE DELTA, COMMUN


CLASS: Crabe zombie de la base Delta, commun
METABOLISM: Hôte humain + crabe parasite de Xen
PERCEPTION: Entité zombifiée, ancien scientifique
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Griffes, mordre, saisir
REWARD: 200FC
THREAT: Modéré

DESCRIPTION: 

This is almost the same as a Common Headcrab Zombie, but with one important difference: le corps de l'hôte appartient à un ancien scientifique qui travaillait à la base classée Delta dans le désert du Nevada. Au lieu des blouses blanches de leurs concurrents de Black Mesa, ces gars-là portent des uniformes gris en lambeaux, déchirés, trempés de sang et d’huile de machine.

The infection process is the same: un crabe s'enfouit dans le crâne, laissant un visage humain partiellement visible sous la carapace extraterrestre.

Ils sont tout aussi lents et acharnés que leurs homologues blancs. Si un tel zombie repère une personne vivante, il la poursuivra jusqu'à ce qu'il se rapproche.

Une balle dans la tête tue la tête d'un zombie.

TIPS: 

- Gardez vos distances. Ils sont lents, mais s'ils s'approchent, leurs griffes et leurs dents feront l'affaire.
- Les armes à feu sont efficaces. Ne gaspillez pas vos munitions en combat rapproché avec eux.
- Un tir dans la tête tue la tête d'un zombie.
- Ils sont dangereux en groupe. Un zombie n'est pas un problème. Trois personnes qui vous coincent est déjà un problème.
