SPIDER-JOCKEY DU MONDE MINECRAFT


CLASS: Spider-Jockey
METABOLISM: Arthropode + Mort-vivant
PERCEPTION: Tandem Tactique
SIZE: 11 pieds
ENDURANCE: Haut
HOSTILITY: Très élevé
WEAPONS: Saut d'araignée, précision du squelette, tir en hauteur
REWARD: 350FC
THREAT: Très élevé

Description: 

Le Spider-Jockey est un exemple rare mais extrêmement désagréable de deux ennemis relativement ordinaires décidant soudainement de travailler ensemble. L'araignée massive fonctionne comme une plate-forme mobile, une unité d'assaut et un véhicule de livraison, tandis que le squelette au-dessus fait ce qu'il fait de mieux : tirer de sang-froid.

Tips: 

- Séparez-les le plus rapidement possible – individuellement, ils sont beaucoup moins dangereux.
