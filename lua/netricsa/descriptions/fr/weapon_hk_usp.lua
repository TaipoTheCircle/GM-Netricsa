PISTOLET À CHARGEMENT AUTOMATIQUE HK USP


WEAPON: Heckler & Koch USP
AMMUNITION: Cartouches 9x19
ROF: ~20 tours/min

DESCRIPTION: 

Un pistolet à chargement automatique développé par la société allemande Heckler & Koch. Le pistolet USP est conçu pour être utilisé par la police et les forces militaires. Il est également utilisé pour l'autodéfense civile, le tir sportif et le tir récréatif.

NOTES: 

- Ce pistolet est de bonne qualité.
- La variante "HK USP MATCH" de ce pistolet est utilisée par les forces de l'Alliance et les rebelles.




