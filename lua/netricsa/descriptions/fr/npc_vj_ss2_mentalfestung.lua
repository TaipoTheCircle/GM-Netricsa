INSTITUTION MENTALE


CLASS: Établissement psychiatrique
METABOLISM: Mégastructure mécanique blindée
PERCEPTION: Capteurs de combat autonomes et intelligence artificielle
SIZE: 984 pieds
ENDURANCE: Immense
HOSTILITY: Extrême
WEAPONS: Lance-feu, canons d'artillerie, batteries de missiles, roues mortelles
REWARD: 5000FC
THREAT: Catastrophique

DESCRIPTION: 

Mental Institution est une gigantesque pyramide mobile qui sert de base centrale et de repaire personnel à Mental. Il s'agit d'une citadelle mécanisée autonome, conçue uniquement pour la guerre totale et la destruction de tout intrus.

La structure est entièrement recouverte de segments blindés et de plates-formes d'armes. Un noyau de contrôle blindé est logé à l’intérieur, activé dès la détection d’une menace. Une fois activée, la forteresse lance des vagues d'avions de combat, dont des chasseurs « fatso kamikaze » qui tombent sur des cibles et explosent à l'impact, provoquant des dégâts dévastateurs.

Dans le même temps, la forteresse bombarde les environs avec des boules de feu à plasma, des obus de canon lourds et d'énormes salves de missiles. Les bombardiers maniables de Chaika larguent des bombes à fragmentation, transformant la surface autour de la forteresse en une zone de destruction totale.

Lorsque les défenses sont affaiblies, le noyau central s’ouvre temporairement, permettant aux attaquants d’attaquer des composants clés de la structure. À ce moment-là, des roues rotatives massives - des modules de combat autonomes qui se déplacent le long de la surface - s'étendent depuis les compartiments inférieurs. Tout contact avec eux entraîne la destruction instantanée de la cible.

Mammifère cybernétiquement amélioré

TIPS: 

- Donnez la priorité à la destruction des « fatso kamikazes » : ce sont eux qui infligent le plus de dégâts.
- Les bombardiers agiles sont dangereux, mais secondaires.
- Lorsque le noyau est exposé, attaquez de la manière la plus agressive possible avec des armes lourdes.
- Ne restez jamais immobile pendant les tirs et les attaques d'artillerie, à moins que vous ne vouliez finir comme Kwongo.
