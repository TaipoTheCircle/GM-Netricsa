OUVRIER DE CONSTRUCTION NOIR MESA


CLASS: Ouvrier de construction de Mesa noir
METABOLISM: Humain, mâle
PERCEPTION: Entrepreneur civil de l’installation de Black Mesa
SIZE: ~5,9 pieds
ENDURANCE: faible
HOSTILITY: aucun
WEAPONS: outils improvisés
REWARD: 500FC
THREAT: négligeable

DESCRIPTION: 

Les ouvriers du bâtiment sont des entrepreneurs civils employés à Black Mesa pour la réparation des installations, l'entretien des structures et l'agrandissement des complexes souterrains. Reconnaissables à leurs gilets orange, leurs casques de sécurité et leurs outils de travail, ce sont des non-combattants pris dans le chaos de la Cascade de Résonance.

Bien qu'ils ne soient pas entraînés au combat, leur connaissance pratique de l'infrastructure de Black Mesa en fait des alliés étonnamment précieux. Ils peuvent naviguer dans des tunnels de maintenance restreinte, déverrouiller des zones scellées et, occasionnellement, apporter une aide improvisée avec des outils. Cependant, leur manque d'armure et d'expérience au combat les rend très vulnérables aux créatures Xen et aux unités militaires hostiles.

Certains rapports de témoins oculaires suggèrent que, malgré leur statut civil, quelques travailleurs ont défendu les scientifiques et le personnel de sécurité avec des équipements de construction, tenant bon jusqu'à ce que l'évacuation soit impossible.

