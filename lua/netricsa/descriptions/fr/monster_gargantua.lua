GARGANTUA DE XEN


CLASS: Gargantua
METABOLISM: prédateur xénobiologique massif
PERCEPTION: Le char blindé de Xen
SIZE: ~ 15 pieds de haut, 30 pieds de long
ENDURANCE: extrême
HOSTILITY: très élevé
WEAPONS: bras lance-flammes, force brute, piétinement de feu
REWARD: 5000FC
THREAT: catastrophique

DESCRIPTION: 

Le Gargantua est un imposant monstre crachant du feu de Xen. Son corps musclé et blindé lui permet d'ignorer les tirs d'armes légères tout en incinérant ses ennemis avec ses bras semblables à ceux d'un lance-flammes. Malgré sa taille, il est étonnamment rapide et peut bondir pour écraser les intrus avec une force dévastatrice.

Les gargantues sont des chasseurs solitaires mais incroyablement territoriaux. Ils semblent presque imparables, obligeant même les résistants les plus confiants à repenser les assauts frontaux. Leur combinaison terrifiante de puissance de feu et de force brute en fait des prédateurs de premier plan dans n’importe quel environnement Xen.

TIPS: 

- Keep distance: les lance-flammes peuvent faire fondre la plupart des couvertures.
- Utiliser des explosifs et des armes lourdes ; les armes conventionnelles sont presque inutiles.
- Les risques environnementaux tels que l'effondrement de structures peuvent aider à égaliser les chances.
