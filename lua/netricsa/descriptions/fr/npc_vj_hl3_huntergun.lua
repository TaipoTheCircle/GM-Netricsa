COMBINEZ LA PROTECTION CIVILE MINI METROCOP


CLASS: Combinez Mini Metrocop de la Protection Civile
METABOLISM: Humain, mâle
PERCEPTION: Opérateur de synthétiseur de combat embarqué
SIZE: 0,9 pied
ENDURANCE: Faible
HOSTILITY: Haut
WEAPONS: Opérateur de fusil à impulsion MiniStrider, pistolet
REWARD: 300FC
THREAT: Faible

DESCRIPTION: 

Le Mini Metrocop est une unité expérimentale du personnel du Combine, réduite et logée dans la coque du MiniStrider. Ces opérateurs effectuent la maintenance, le ciblage et le contrôle du fusil à impulsion intégré, agissant comme une partie vivante du synthétiseur de combat. Leurs corps sont soumis à une compression extrême, à une stabilisation biomécanique et à un attachement neuronal au système MiniStrider.

Contrairement aux plates-formes synthétiques entièrement autonomes, les MiniStriders dotés de tels opérateurs présentent un comportement tactique plus flexible. Les réflexes humains et la capacité d'improvisation leur permettent de sélectionner plus rapidement des cibles, d'ajuster le tir et de répondre aux menaces non conventionnelles. Essentiellement, il s’agit de la tentative du Combine de combiner une machine froide et un esprit humain asservi dans un seul corps.

In rare cases, when a MiniStrider is destroyed, the compression field partially destabilizes, and the operator can return to their normal size. However, this process is incompatible with life: la surcharge des tissus, des organes et du système nerveux entraîne une mort inévitable, souvent simultanément à la destruction du synthétique lui-même.

Ces Metrocops ne sont pas considérés comme des combattants à part entière et sont officiellement répertoriés comme « ressources de maintenance consomptibles ».

TIPS: 

- Détruire un MiniStrider neutralise automatiquement l'opérateur. - Un comportement plus précis du MiniStrider peut indiquer la présence d'un opérateur vivant à l'intérieur.
