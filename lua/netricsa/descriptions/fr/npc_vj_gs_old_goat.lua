VIEILLE CHÈVRE (T-REX)


CLASS: Vieille chèvre (T-rex)
METABOLISM: Carnivore (mais se nourrit étrangement de butin et d'humains)
PERCEPTION: Bête
SIZE: ~30 pieds
ENDURANCE: Très élevé
HOSTILITY: Comiquement extrême
WEAPONS: Mâchoires massives, coup de queue, "Goat Roar"
REWARD: 9999 FC + objet de quête cassé
THREAT: Absurdement élevé

DESCRIPTION: 

Connu dans les chroniques MMO sous le nom de « Vieille Chèvre », ce mutant préhistorique est apparu accidentellement dans des toilettes géantes dans un monde de RPG médiéval. Au départ, les joueurs pensaient qu'il ne s'agissait que d'un œuf de Pâques, jusqu'à ce qu'il s'avère que la créature pouvait anéantir un raid complet de 40 hommes en quelques secondes.

Il ressemble à un T-Rex classique et son style de combat mélange la fureur des dinosaures.

TIPS: 

- Si vous le voyez sur les toilettes, courez.
