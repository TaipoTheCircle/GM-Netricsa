FEMIKAZE DÉCAPÉE


CLASS: Femikaze décapitée
METABOLISM: Cyber-zombie
PERCEPTION: Insensible, contrôlé par LCU
SIZE: 6 pieds
ENDURANCE: Très faible
HOSTILITY: Extrême
WEAPONS: Grenades à main
REWARD: 350FC
THREAT: Haut

DESCRIPTION: 

Le Femikaze est une variante féminine du kamikaze décapité de Sirius, créé par Mental à l'aide de la LCU. Sa tête est manquante, il s'appuie donc entièrement sur la télécommande et sur des algorithmes d'agression intégrés. Son objectif principal est de se rapprocher le plus possible de sa cible et d'exploser avec les grenades qu'elle détient.

Outwardly, he looks "fashionable and menacing": son T-shirt imite un corps nu avec un maillot de bain rouge, un jean et des baskets rouge vif. Les bombes sont des accessoires, mais leur effet est bien moins décoratif.

TIPS: 

- Explose immédiatement après la mort.
- Gardez vos distances et utilisez des armes à longue portée.
- En groupe, il transforme n'importe quelle zone en un champ d'explosion chaotique.
- Looks can be deceiving: un T-shirt élégant et des baskets ne réduisent pas le danger.
