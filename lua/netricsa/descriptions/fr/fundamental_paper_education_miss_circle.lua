MISS CERCLE DE L'ÉDUCATION PAPIER FONDAMENTALE


CLASS: Miss Cercle
METABOLISM: Organisme de papier (enseignant mutant)
PERCEPTION: Professeur de mathématiques
SIZE: 9,7 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême (envers les étudiants sous-performants)
WEAPONS: Cercle prothétique géant, griffes acérées
REWARD: 10000FC
THREAT: Très élevé

DESCRIPTION: 

The main antagonist of "Fundamentals of Behavior", a math teacher at Paper School. Exceptionally tall, with long black hair tapering to horns. In place of her left hand, she has a giant, detachable Circle named "Mr. Circle", which serves both as a teaching tool and a deadly weapon. She displays extreme cruelty and cannibalism toward students who fail her exams (Abby, Lana). She has favorites (Oliver, Zip, Edward), but can even scold them for breaking the rules. She can change her size and possesses immense physical strength, allowing her to smash school furniture. Her personality is a frightening mixture of goofy playfulness and sudden, merciless rage. She is the only character to speak in the original animation (with a synthesized Japanese voice). She has one known weakness: un amour fanatique des biscuits Oreo.

TIPS: 

- Son Cercle est une arme à distance et au corps à corps avec d'immenses dégâts. Elle le manie magistralement.
- Elle attaque méthodiquement et sans pitié.
- Des biscuits Oréo. Une offre ou un lancer d'Oreo pourrait l'apaiser, déclenchant un éclat de joie et dévorant la friandise. Elle déteste les Kit-Kats.
- Quelques coups de fusil à double canon feront tomber ce professeur.
