DESCRIPTION:

Personne ne sait d'où ils viennent, quel est leur nom, ni pourquoi ils brûlent. Tout ce que nous savons, c'est que ce sont des sangliers noirs enveloppés de flammes, et qu'ils veulent vous tuer.

En apparence, ces créatures ressemblent aux Were-Bulls Siriens - corps massifs, mêmes mouvements frénétiques, même rage incontrôlable.

Les détruire est possible, mais demande des efforts. Ils sont très résistants, courent vite et ne réagissent pas aux attaques normales.

CONSEILS:

- N'essayez pas de bloquer. Ils tuent en un coup. Aucune armure ne vous sauvera.
- Si vous avez une Serious Bomb, utilisez-la immédiatement. C'est le seul moyen garanti d'éliminer rapidement le groupe.
- Ne vous faites pas coincer dans les coins. Ils vous auront à coup sûr.
