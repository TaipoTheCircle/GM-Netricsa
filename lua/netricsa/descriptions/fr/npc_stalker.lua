COMBINEZ LE STALKER


CLASS: Combiner Traqueur
METABOLISM: Cyborg transhumain
PERCEPTION: Travailleur transhumain asservi
SIZE: ~6,5 pieds
ENDURANCE: Moyen
HOSTILITY: Faible
WEAPONS: Découpe laser, Membres hydrauliques, Implants d'obéissance
REWARD: 200FC
THREAT: Faible-Moyen

DESCRIPTION: 

Les harceleurs sont parmi les résultats les plus horribles des expériences du Combine sur les prisonniers humains. Une fois hommes et femmes libres, ils ont été mutilés, lobotomisés et reconstruits en drones cyborgs. Leurs yeux sont remplacés par des volets métalliques, leurs bouches scellées et leurs corps tordus par des augmentations qui les privent de leur autonomie.

Principalement utilisés dans les installations de Nova Prospekt et de Citadel, les Stalkers servent de drones de construction et de gardiens de prison. Ils exploitent des machines, entretiennent la technologie et renforcent la domination du Combine. Lorsqu'ils sont menacés, ils libèrent des faisceaux brûlants de leurs découpeurs laser, capables de trancher les combattants de la résistance et même les couvertures légères.

Leur présence est un sinistre rappel de la cruauté du Cartel : le destin humain peut être réduit à un outil d'oppression, sans autre identité que l'obéissance.

TIPS: 

- Visez la tête et le torse exposés.
- Les harceleurs se trouvent souvent dans les zones fortifiées ; attendez-vous à ce que les unités de soutien Combine se trouvent à proximité.
