SCIENTIFIQUE ASSIS BLACK MESA


CLASS: Scientifique de Black Mesa, assis
METABOLISM: Humain, mâle
PERCEPTION: Personnel scientifique de Black Mesa
SIZE: ~ 6 pieds (assis)
ENDURANCE: faible
HOSTILITY: aucun
WEAPONS: aucun
REWARD: 100 FC
THREAT: négligeable

DESCRIPTION: 

Le Scientifique Assis est un non-combattant stationnaire rencontré dans les laboratoires et les salles de contrôle de Black Mesa. Habituellement assis à des bureaux ou à des terminaux, ces scientifiques donnent à l'environnement un réalisme, effectuant souvent des tâches telles que surveiller des équipements ou prendre des notes.

Ils ne participent pas au combat et sont extrêmement vulnérables en cas d'attaque.

TIPS: 

- Évitez de leur faire du mal si possible ; ils ne ripostent pas.
- Observer leur comportement peut fournir des indices sur les objectifs ou les dangers.
- Ils sont totalement sans défense : s'ils sont attaqués, ils ne peuvent pas s'échapper.
