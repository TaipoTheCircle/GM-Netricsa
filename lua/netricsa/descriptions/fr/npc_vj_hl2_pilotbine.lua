COMBINEZ LE PILOTE D'OVERWATCH


CLASS: Combiner le pilote Overwatch
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner l'unité aérienne
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: OIAC, SMG
REWARD: 850 FC
THREAT: Moyen

DESCRIPTION: 

Le Combine Overwatch Pilot est un combattant spécialisé chargé de contrôler les avions Combine. Contrairement aux soldats standards, leur valeur principale ne réside pas dans le tir de précision, mais dans le contrôle de l'espace aérien et le déploiement rapide des forces.

They wear a pilot helmet with an integrated respirator and integrated blue pilot goggles, directly linked to Combine navigation systems. Les lunettes affichent l'altitude, la vitesse, les cibles et les zones de défense aérienne, permettant au pilote d'opérer même dans des tempêtes de neige, des brouillages radio et l'obscurité totale.

Même si les pilotes ne sont pas conçus pour le combat en première ligne, le Combine les équipe d'armes polyvalentes. L'OICW permet de tirer à la fois sur l'infanterie et sur des cibles à couvert, tandis que le SMG est utilisé en cas d'atterrissage d'urgence ou lors de la défense de la zone d'atterrissage. Les augmentations améliorent la conscience spatiale et la résistance à la force G, transformant le pilote en une force avec laquelle il faut compter dans l'armée de l'air du Combine.

TIPS: 

