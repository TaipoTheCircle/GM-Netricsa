Mitraillette à impulsions de fusion


WEAPON: Mitraillette à impulsion Fusion
AMMO: Munitions balistiques à fusion/munitions à impulsion thermique
ROF: ~450 tr/min

DESCRIPTION: 

L'arme standard des automates, largement utilisée par les unités d'assaut de base, les pillards et les maraudeurs. Sa conception se caractérise par une exécution grossière, une mauvaise ergonomie et une précision médiocre, mais elle compense cela par une facilité de fabrication et une haute densité de tir.

L'arme est conçue pour de courtes rafales de tirs automatiques. Le mode de tir principal est une rafale de cinq coups, suivie d'un bref délai de stabilisation. Un mauvais système de dissipation thermique rend la mitraillette Fusion Pulse sujette à la surchauffe lors d'une utilisation prolongée, réduisant ainsi sa fiabilité lors d'engagements prolongés.

Malgré la puissance modérée des coups individuels, la densité du tir permet une suppression efficace des cibles et une pression constante sur le champ de bataille.

NOTES: 

- Utilisé par les automates de classe Assault, Raider et Marauder.
- Une rafale de 5 coups fournit un tir de suppression constant.
- Surchauffe lors d'une cuisson prolongée en raison d'un mauvais refroidissement.
- Efficace à courte et moyenne portée.
- Une mauvaise ergonomie et une dispersion élevée réduisent la précision à longue portée.
- Un rechargement (~4 secondes) est nécessaire après 30 tirs.
- Une faible pénétration du blindage permet de combattre des cibles légèrement blindées.
