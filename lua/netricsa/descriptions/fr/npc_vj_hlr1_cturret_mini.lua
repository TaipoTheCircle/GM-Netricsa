MINI TOURELLE MESA NOIRE


CLASS: Mini tourelle Mesa noire
METABOLISM: Construction mécanique
PERCEPTION: Unité de défense automatisée compacte
SIZE: ~2 pieds
ENDURANCE: faible-moyen
HOSTILITY: extrême
WEAPONS: petite mitrailleuse à tir rapide
REWARD: 250 FC
THREAT: modéré

DESCRIPTION: 

La mini tourelle est une variante compacte de la tourelle blindée Black Mesa standard. Il conserve des capacités de ciblage automatisé et une fonctionnalité de tir rapide, mais il est plus petit, plus léger et plus maniable. Comme son homologue grandeur nature, il peut être monté au sol ou au plafond, permettant un déploiement polyvalent dans des espaces restreints.

Bien que moins blindé que la version grandeur nature, il reste mortel contre les cibles non protégées, capable de supprimer les intrus et de défendre les points d'accès critiques. Sa petite taille le rend plus difficile à repérer jusqu'à ce qu'il ouvre le feu.

TIPS: 

- Des explosifs ou des tirs précis peuvent le désactiver rapidement.
- Vérifiez ci-dessus et ci-dessous : même les petites tourelles peuvent couvrir des angles inattendus.
- Évitez de vous tenir dans les couloirs ou les points d'étranglement où il peut vous cibler efficacement.
- Utilisez une couverture environnementale pour minimiser l'exposition aux tirs de mitrailleuses.
