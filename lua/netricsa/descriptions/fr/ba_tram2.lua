BLACK MESA, ENTRAÎNEZ-VOUS À LA CAFÉTÉRIA


Hey, Sam the guard! Get ready for a short tour of the "gastronomic attractions" of Black Mesa. Now we'll drive past the cafeteria, where Big Tony's Pizza and Tesla's Taco are nestled against a hole in the rock. The place is popular: Otis Lorey, par exemple, traîne souvent ici.

Keep your eyes peeled: il y a des « monstres génériques » et des « acteurs génériques » qui se cachent parmi les scientifiques et les gardes. On ne sait pas qui ils sont réellement. Là où ils vivent aussi. Pourquoi se déguisent-ils - encore plus. Mais ils ne sont évidemment pas venus prendre un café.

They copy the behavior of the staff: ils se tiennent devant des machines à café, feuilletant des journaux, faisant semblant d'être pressés par leurs affaires. Mais mon instinct me dit que ces « collègues » attendent dans les coulisses. Peut-être qu'ils regardent. Peut-être qu'ils préparent une surprise. Ou peut-être que l’un d’eux vous regarde déjà et décide d’appuyer ou non sur le bouton d’alarme.

So far, everything is quiet. The cafeteria is lit up, the customers (real or not) are chatting, and it smells of pizza and tacos. But remember: à Black Mesa, même une pause déjeuner peut être la dernière.

Gardez votre main près de l'étui. Et ne regardez pas les panneaux : et s'il ne s'agissait pas de nourriture, mais d'appâts ?

Allez-y, "garde". Le prochain arrêt est inconnu.
