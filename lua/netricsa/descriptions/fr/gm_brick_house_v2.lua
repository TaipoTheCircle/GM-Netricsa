MAISON EN BRIQUE, AMÉLIORATION POST-APOCALYPSE


Sam, pendant que nous voyagions, un harceleur local et propriétaire d'une datcha a rénové la maison en briques. Elle a acquis toutes les infrastructures nécessaires. C'est désormais un véritable country club pour survivants, doté d'une piscine et d'un garage.

C’est vrai, il n’y a toujours pas de portes, mais qui sommes-nous pour pinailler ?

The landscape has undergone significant changes. Civilization is advancing: 

Une quatrième voiture. Il y en a maintenant quatre. Il n'a pas non plus de roues, mais le groupe est devenu plus encombré. Ils se garent comme ils peuvent.

Garage en brique. Un garage à part entière de la même brique rouge a surgi derrière la maison. Il n'y a pas de porte – une tradition familiale, apparemment.

Inside is a mechanic's sacred place: une clé et un moteur reposent soigneusement sur une table en bois. Ni cloué, ni vissé. Je suis juste allongé là.

Piscine. A gauche de la maison, à même l'herbe, une piscine est apparue. C'est du plastique blanc, une forme rectangulaire classique. C'est plein d'eau. Mais on peut y nager. D’ailleurs, l’eau est propre. Je suppose.

Un arbre migrateur. L'un des onze arbres a été transplanté dans un autre coin de la propriété. Soit le paysagiste a fait des travaux, soit l'arbre les a demandés lui-même. Visuellement, rien n'a changé, mais le fait demeure : la flore vit désormais selon le nouveau plan directeur.

Inside the house, the same supplies we've already explored (hello, Sofia, with her 112 dinners). But a new door has appeared... or rather, a new opening: 

A new room. Without a door, naturally. Inside, spartan minimalism: un placard, une table et un lit vides. Pas de matelas. Un cadre nu. Apparemment, c'est une suite pour ceux qui n'ont pas peur de dormir sur du contreplaqué et d'accrocher des vêtements dans un espace vide.

Supplies are in place: patates douces, pommes (les meilleures du monde), 112 repas de Sofia, collations Jabber, jus de fruits, boisson à la vanille, soda light et pommes de terre fraîches. Les caisses (une grande, quatre petites) ne s'ouvrent toujours pas. L'horloge est suspendue. Les armes sont clouées dans le placard, se moquant de leur apparence.

Le garage est un atelier de réparation potentiel. Le moteur et la clé ne traînent pas. Avec un peu d'ingéniosité (et peut-être quelques roues pour ces voitures inutilisées), nous pouvons faire fonctionner au moins une des quatre épaves.

La piscine est une source d'eau. Inestimable lors d'un siège. Et c'est aussi agréable de se baigner après un combat avec des extraterrestres. Cependant, il n'y a nulle part où se réchauffer après : le lit n'a pas de matelas.

La nouvelle pièce peut être utilisée comme point de contrôle ou comme chambre. Si nous trouvons un matelas. Ou au moins un peu de foin.

En avant.
