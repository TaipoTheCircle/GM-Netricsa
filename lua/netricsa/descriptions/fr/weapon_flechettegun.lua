PISTOLET À FLECHETTE


WEAPON: Pistolet à Fléchette
AMMO: Fléchettes (petites fléchettes à grande vitesse)
ROF: ~250 tours/min

DESCRIPTION: 

Arme à distance qui tire des projectiles compacts en forme d'aiguilles à grande vitesse. Chaque fléchette pénètre l'armure et la chair avec un recul minimal, ce qui rend l'arme efficace pour une suppression précise ou des frappes chirurgicales.

NOTES: 

- Les projectiles à grande vitesse peuvent percer plusieurs cibles en ligne.
- Capacité de munitions modérée ; une visée prudente est recommandée.
- Efficace à moyenne portée ; moins utile dans des espaces rapprochés.
- Le faible recul permet des tirs de suivi rapides.
