MITRAILLEUSE MARINE


WEAPON: Mitrailleuse
AMMO: Balles
ROF: ~600 tours/min

DESCRIPTION: 

La Machine Gun est un fusil automatique compact à tir sélectif utilisé par le Space Marine Corps comme fusil de service principal. Il comprend une lampe de poche sous le canon et un mode lunette pour un tir de précision. L'arme peut être tirée en mode entièrement automatique depuis la hanche ou en semi-automatique lorsqu'elle est portée, ce qui la rend polyvalente aussi bien pour les combats rapprochés que pour les rencontres à moyenne portée.

NOTES: 

- Le mode Scoped permet un tir semi-automatique avec une précision améliorée.
- Damage per shot: ~ 15 normalement, ~ 20 en cas de portée.
- Bon contre les ennemis faibles et moyens ; Les cibles lourdes blindées ou les boss nécessitent de nombreuses balles.
- La mise à niveau du chargeur de 80 cartouches améliore considérablement la résistance lors des combats prolongés.
