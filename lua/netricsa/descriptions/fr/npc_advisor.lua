CONSEILLER EN COMBINAISON


CLASS: Conseiller en Combineurs
METABOLISM: Organisme psionique parasite
PERCEPTION: Combinez le contrôleur psionique du surveillant
SIZE: ~12 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Domination psionique, préhension télékinésique, trompe tentaculaire, claquement de lévitation
REWARD: 2200 FC
THREAT: Catastrophique

DESCRIPTION: 

Les Combine Advisors sont les véritables surveillants de l’occupation de la Terre, rarement vus par les soldats ordinaires mais profondément craints même parmi les Overwatch. Ces êtres grotesques ressemblant à des limaces flottent dans un champ d'énergie psionique, leurs corps gonflés et soutenus par une technologie extraterrestre.

Ils commandent les forces du Combine via des réseaux télépathiques, étendant leur volonté directement aux soldats transhumains et aux humains asservis. Au combat, ils libèrent de terrifiants pouvoirs psioniques : arrêtant leurs ennemis à mi-chemin, les écrasant avec une force invisible ou les projetant contre les murs. Leur trompe alimentaire est capable de percer les crânes et d'extraire de la matière neurale, un processus utilisé à la fois pour se nourrir et pour interroger.

Bien que rarement exposés sur le champ de bataille, lorsque des conseillers apparaissent, cela signale la domination absolue du groupe dans la zone. Leur présence signifie qu’aucune action ne passe inaperçue et qu’aucune résistance ne peut durer longtemps.

TIPS: 

- Évitez tout contact visuel direct ; le verrouillage psionique affaiblit la résistance.
