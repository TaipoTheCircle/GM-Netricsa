RÉCOLTEUSE D'ESSAIMS


CLASSE : Moissonneur d'essaims
MÉTABOLISME : Xénobiologique
PERCEPTION : Spécimen de soutien
TAILLE : 8,2 pieds
ENDURANCE : Faible
HOSTILITÉ : Élevée
ARMES : apparition de Xenomite, peau corrosive
RÉCOMPENSE : 500 FC
MENACE : Élevée

DESCRIPTIF :

Le Harvester est une forme spécialisée d'Essaim, conçue pour reproduire rapidement des organismes de combat directement sur le champ de bataille. Contrairement à la plupart des membres de son espèce, il attaque rarement directement les ennemis, préférant créer des Xénomites et les déployer contre des cibles détectées.

La caractéristique la plus remarquable du Moissonneur est un grand organe lumineux sur son dos. Les xénomites y éclosent et peu après avoir mûri, elles quittent le corps de l'hôte. Leur reproduction rapide permet à un seul Moissonneur de créer un essaim complet de ces créatures en peu de temps.

Lorsqu'il est menacé, le Moissonneur tente d'augmenter la distance qui le sépare de l'ennemi, continuant à se reproduire même en battant en retraite. Cela rend la destruction d’un Harvester souvent plus difficile qu’il n’y paraît au départ.

Après la mort, l'organisme subit une brève libération post-mortem, au cours de laquelle les xénotiques restants quittent le corps et commencent immédiatement à attaquer. Pour cette raison, détruire un Moissonneur ne signifie pas toujours la fin de la menace.

Malgré ses défenses relativement faibles, le tissu Harvester contient une grande quantité de substances corrosives. Le contact avec son corps peut causer de graves dégâts même après la mort de la créature.

CONSEILS :

- Détruisez les moissonneurs avant qu'ils ne créent un grand essaim.
- Ne les laissez pas se retirer dans des zones sûres.
- Soyez prêt à ce que des xénoticks apparaissent après la destruction de la cible.
- Si le Moissonneur a vécu assez longtemps, cela signifie qu'il devra bientôt affronter plus d'un ennemi.