BLACK MESA, BAD COMBO


Félicitations, l'univers vient de décider de mettre le pire combo possible dans votre assiette.

Situation: un tentacule met des objets au rebut dans la cour ; un Apache tourne en rond et fait pleuvoir des fusées ; Les hordes de Mental affluent de chaque rocher brisé ; les crabes inondent la couverture basse ; et le sol entre vous et l'objectif est un champ de mines avec des déclencheurs que Dieu seul connaît.