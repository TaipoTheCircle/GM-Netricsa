JOHN JEEWICH, LE BOXEUR INÉGALÉ


CLASS: Humain, champion bagarreur
METABOLISM: Humain, Mâle, Titan Anabolisant
PERCEPTION: Réflexes d'un prédateur
SIZE: 6,4 pieds
ENDURANCE: Colossal
HOSTILITY: Extrême (sur le ring - impitoyable)
WEAPONS: KO à une main, coups de poing écrasant les os, écrasement en corps à corps
REWARD: 9000FC
THREAT: Apocalyptique

DESCRIPTION: 

Ne défiez jamais John Jeewich, le boxeur sans égal, à moins que vous ne souhaitiez assister à l'effondrement de votre propre squelette. Cet homme peut écraser non seulement une noix, mais aussi des arbres entiers – et éventuellement vos côtes – d’une seule main. Connu comme la peur de toute l’Allemagne, ses poings frappaient comme des marteaux industriels forgés lors d’une tempête.

Legends say only one being can defeat him: Saitama - Un coup de poing, une mort.

Cependant, vos poings suffiront dans un combat à coups de poing équitable.

TIPS: 

- Ne restez pas immobile : le mouvement est votre seule défense.
- Si tu n'es pas Saitama, tu ne survivras pas à un coup direct.
- Ses coups de poing peuvent briser le métal et les os.
