ORAZIO


PLANET: Orazio
TYPE: Planète terrestre, Planète minière
CLIMATE: Instable, dur
MOONS: Inconnu
TERRAIN: Cratères profonds, mines à ciel ouvert, zones industrielles
LOCATION: Secteur inconnu de la galaxie
NATIVE RACES: Orcs, Horde mentale (toutes les races)
ALLIANCE: Altani CorpOrazion (anciennement), Mental-Altani CorpOrazion (actuellement)
LOCATIONS: Orazio Deep Mines, installation d'essai de véhicules de combat

DESCRIPTION: 

Un monde inhospitalier avec une écologie extrêmement instable. Les couches profondes du sol sont dominées par un environnement acide et radioactif, ce qui rend la planète impropre à la vie normale en surface. Le paysage est parsemé d'innombrables mines, carrières et terrasses industrielles, vestiges d'une extraction minière à grande échelle.

La planète est devenue la nouvelle demeure des orcs, exilés par l'Empire Zorg après l'interdiction du pool chimique mutagène. Les exilés fondèrent ici la société minière Altani, qui connut rapidement le succès grâce aux riches gisements.

Des machines spécialisées, les PRIME-209 et Tech-X, ont été créées pour fonctionner dans les profondeurs radioactives et mortelles.

Lorsque Mental apprit l'existence de la CorpOrazion florissante, il proposa une alliance avec les orcs. À la suite de la fusion, la corpOrazion a été rebaptisée Mental-Altani et ses véhicules miniers ont subi le « Programme de conversion de véhicules de combat », recevant des armes lourdes pour servir dans la Horde.

KEY LOCATIONS: 

- Orazio Deep Mines: Un réseau de tunnels et de puits s’étendant sur plusieurs kilomètres sous la surface. En raison de la forte radioactivité, les travaux y sont effectués exclusivement par des véhicules télécommandés.
- Combat vehicle testing Facility: Une ancienne carrière reconvertie en centre d'essais de versions armées de robots miniers dans le cadre du « Programme de Rénovation ».
