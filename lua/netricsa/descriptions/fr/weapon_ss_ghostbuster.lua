PISTOLET À FAISCEAU XL4-P "GHOSTBUSTER"


WEAPON: Pistolet à faisceau XL4-P "Ghostbuster"
AMMO: Cellules de puissance X7
ROF: Continu
WEIGTH: 21 livres

DESCRIPTION: 

Branche encore plus expérimentale de la recherche originale sur le XL2 Lasergun, cette arme incroyablement rare tire un faisceau continu mortel qui traverse tout. Utilisée uniquement par quelques soldats hautement qualifiés de l'EDF pour des tests secrets sur le terrain, l'arme a rapidement reçu le surnom de "Ghostbuster", en raison de sa nature similaire à une arme à faisceau d'un certain film.
Utilisant les mêmes cellules de puissance que le XL2 Lasergun, cette arme a été conçue à l'origine pour remplacer le Minigun encombrant et lourd, mais comme elle n'a jamais été produite en série, la plupart des soldats pensaient que cette « super arme » n'était qu'une légende.

NOTES: 

- Utilisez-le pour couper rapidement des masses d'ennemis de taille moyenne.
