SKORPION SAMOPAL VZ.61


WEAPON: Skorpion Samoral vz. 61
AMMO: .32 ACP (7,65 × 17 mm Browning SR).380 ACP et 9 × 19 mm
ROF: 850 tours/min
WEIGHT: 1,59 kg vide
LENGTH: 517 millimètres


DESCRIPTION: 

Le Skorpion vz. 61 est une mitrailleuse tchécoslovaque compacte développée pour les officiers, les équipages de véhicules et le personnel de sécurité. Sa crosse pliable et sa taille de poche en faisaient une arme idéale comme arme PDW/de secours dissimulable. Chambré à l'origine pour la cartouche .32 ACP de faible puissance, il s'appuie sur un taux cyclique élevé et une mécanique de rafale contrôlable (réducteur de taux) pour fournir un tir efficace à courte portée.

NOTES: 

- Adapté au CQB et à l'autodéfense rapprochée - petit, dissimulable et rapide à déployer.
- La faible puissance par tir est compensée par le volume de tir ; mieux utilisé dans de courtes rafales pour la contrôlabilité.
