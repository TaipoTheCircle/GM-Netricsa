COMBINER LA PROTECTION CIVILE BLACKLIGHT COP


CLASS: Combinez le flic Blacklight de la protection civile
METABOLISM: Humain sous le contrôle de Combine
PERCEPTION: Unité spéciale de répression de la police
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Revolver, SMG, Manhacks
REWARD: 400FC
THREAT: Moyen

DESCRIPTION: 

Le Combine Civil Protection Blacklight Cop est un agent de police urbain spécialisé conçu pour opérer dans des zones contaminées, chimiquement dangereuses et instables. Sa combinaison de protection complète ne met pas tant l'accent sur l'élitisme que sur la fonctionnalité et la capacité de survie dans des conditions où le flic moyen du métro ne tiendrait pas longtemps.

Le casque couvre entièrement le visage et est équipé d'une visière bleutée avec un système V-HUD. Des marquages ​​tels que « V-HUD », « PREX INDUSTRIAL » et « ARAVO-08 » sont visibles sur la surface, indiquant l'origine industrielle et la nature expérimentale de l'équipement. Un respirateur intégré filtre l'air, protégeant l'utilisateur des gaz, des spores et du rayonnement de la lumière noire, d'où le nom de l'unité.

The black body armor with white trim bears the marking "C17: i4o", reliant directement le soldat à City 17 et aux forces de sécurité internes du Combine. La tenue est complétée par un pantalon sombre, des bottes noires et des gants, et un étui est toujours présent sur la ceinture pour une réponse rapide.

Au combat, le Blacklight Cop agit durement, mais pas de manière militaire. Le revolver est utilisé pour des intimidations et des exécutions ciblées, le SMG pour le contrôle des foules et les Manhacks sont déployés dans des espaces confinés, transformant les rues et les entrées en zones de mort chaotiques. Ces flics travaillent souvent en équipe, éliminant les quartiers en silence... selon les normes du Combine.

TIPS: 

- Ne le sous-estimez pas à cause de son statut de « flic ». Les Manhacks changent rapidement le cours de la bataille.
- La visière protège contre la cécité, mais pas contre les coups directs.
- Dans les espaces confinés, la priorité est de le tuer avant le déploiement des Manhacks.
