La première maison est « Ouverte ». Vous pouvez le saisir. A l'intérieur se trouvent quatre boîtes. Et rien d'autre. Pas de chaises, pas de tables, pas de fournitures pour Sophia. Juste quatre cartons vides. J'attends que vous regardiez à l'intérieur et que vous soyez déçu.

La deuxième maison est « Fermée ». Vous ne pouvez pas entrer. La porte est-elle verrouillée ? Muré ? Tout simplement pas conçu ? Cela n'a pas d'importance. L’important est que derrière le mur de cette maison, dans un espace muré, repose un cadavre mutilé. Dans son propre sang. Quelqu'un n'est pas simplement mort, il l'a déchiré. Et je suis parti là, derrière le mur. Pour rappel. Ou comme avertissement.

On ne peut pas voir le cadavre directement. Mais maintenant tu sais que c'est là. Derrière le mur. Dans une maison, on ne peut pas entrer.

Le vent. Il ne s'agit pas ici d'un simple bruit de fond. C'est comme la voix de cet endroit. Il s'estompe, puis devient plus fort, puis se met à hurler si fort que la chair de poule me parcourt même le dos, et je n'ai plus de dos.

Un étang. Une eau sombre dans laquelle personne ne nage.

Cet endroit n'est pas pour vous. Et pas pour moi. C'est pour celui laissé derrière le mur. "Pour toujours" (Tazik29).
