BRICK POND, LE VENT SENT LA MORT


Sam, cet endroit est comme cette pièce d'un film d'horreur où tu n'es pas censé aller, mais tu l'es déjà parce que la porte est ouverte et que la curiosité est ton pire ennemi.

À première vue, c'est un quartier ordinaire. Des murs en briques, un étang, quelques maisons. Le silence, la douceur, le bonheur de la brique.

And then you notice the details. And you realize: quelqu'un est mort ici. Et pas seulement mort, mais il est resté ici pour toujours.

Les murs de briques forment une sorte de cour ou d’impasse. Au centre se trouve un petit étang. L'eau est calme, comme un miroir dans une maison hantée.

Tout autour est le silence. Seulement le vent. C'est le principal ingénieur du son ici. Souffler, hurler, chuchoter. Cela crée le sentiment que vous n’êtes pas seul, même s’il n’y a personne autour. Sauf un.

