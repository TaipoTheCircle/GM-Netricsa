DESCRIPTIF :

C'est l'une des créatures les plus mystérieuses des univers connus. Personne ne sait d’où ils viennent, qui les a créés ni pourquoi ils sont ici. Ils apparaissent comme des stickmen pixelisés de différentes couleurs : rouge, bleu, vert, jaune, etc. Ils sont plats, comme dessinés sur une feuille de papier, mais ils se déplacent dans un espace tridimensionnel, conservant leur forme 2D.

Les premières apparitions documentées ont été enregistrées dans des environnements de test, des simulations abandonnées et d’étranges espaces numériques qui défient toute classification. D'où le nom « environnements de test ». Le créateur, s’il existe, n’a laissé aucune trace.

Dès qu'il aperçoit une créature vivante, il commence à se déplacer vers la cible. Il ne court pas, ne se faufile pas et n'attaque pas ; il marche simplement à une vitesse constante, en empruntant le chemin le plus court. Si un obstacle se dresse sur son chemin, il le contournera. Si une porte est verrouillée, elle restera devant elle jusqu'à ce qu'elle soit ouverte.

Lorsque le Nextbot s'approche d'un objet d'intérêt, il s'arrête et commence à sauter. Il saute joyeusement, avec enthousiasme. Il peut sauter indéfiniment, ne montrant aucun signe de fatigue ou d’agressivité.

À la mort, il disparaît tout simplement. Cela ne laisse aucune trace.

La théorie la plus improbable est qu’ils essaient d’établir le contact mais ne savent pas comment le faire, alors ils sautent simplement.

CONSEILS :

- Ces Nextbots sont inoffensifs. Ne gaspillez pas de munitions avec eux.