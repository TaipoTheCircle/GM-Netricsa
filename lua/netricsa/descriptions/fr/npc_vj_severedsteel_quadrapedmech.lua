THOR, PDG D'EDENSYS BIOTECH


CLASSE : Thor, PDG d'EdenSys BioTech
MÉTABOLISME : humain, masculin
PERCEPTION : PDG d'EdenSys BioTech
TAILLE : 9,8 pieds
ENDURANCE : Très élevée
HOSTILITÉ : Extrême
ARMES : BattleMech quadrupède, lanceurs de missiles, champ de force, bélier
RÉCOMPENSE : 25000 FC
MENACE : Critique

DESCRIPTIF :

Thor Erickson, mieux connu sous le nom de PDG d'EdenSys BioTech, est le plus haut dirigeant de la société et l'homme responsable d'innombrables expériences de clonage, du contrôle total de l'entreprise et de l'élimination de toute personne constituant une menace pour les intérêts de l'entreprise.

Malgré sa position, Thor n'est pas un combattant professionnel. Il évite l'engagement direct dans la bataille et préfère commander ses troupes depuis un endroit sûr. Cependant, si les circonstances le forcent à se rendre sur le champ de bataille, il le fait à bord d'un lourd robot de combat quadrupède, spécialement conçu pour protéger la haute direction de l'entreprise.

Le robot est une immense plate-forme de marche équipée de puissants servos et d'armes lourdes. Sa principale méthode d'attaque consiste à lancer des volées successives de missiles à tête chercheuse, capables de couvrir de vastes zones et de forcer les adversaires à se repositionner constamment.

Si une cible se rapproche trop, le robot accélère brusquement et percute. La masse du véhicule est si grande qu'une collision peut détruire instantanément la plupart des adversaires ou les projeter sur une distance considérable.

La principale caractéristique du robot est son générateur de champ de force intégré. Une fois activée, une barrière d'énergie rouge se forme autour du robot, bloquant complètement tous les dégâts entrants. Tant que le champ est actif, Thor devient pratiquement invulnérable et continue de tirer sans risque. Le générateur fonctionne pendant une durée limitée, après quoi il doit être rechargé.

CONSEILS :

- Ne gaspillez pas de munitions lorsque le champ de force est actif. Attendez qu'il soit désactivé.
- Continuez à avancer pour éviter les salves de missiles.
- Ne vous tenez jamais devant le 'Mech pendant qu'il accélère : le percuter est presque toujours fatal.
- Utilisez une couverture entre les salves de missiles et l'attaque uniquement lorsque le champ de force est désactivé.
- Malgré la puissance de la machine, Thor lui-même est entièrement dépendant de son 'Mech. Une fois détruit, le directeur devient beaucoup plus vulnérable.