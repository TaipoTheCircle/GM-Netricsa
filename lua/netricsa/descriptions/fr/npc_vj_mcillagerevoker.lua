ÉVOCATEUR DU MONDE MINECRAFT


CLASS: Évocateur
METABOLISM: Illager magiquement doué
PERCEPTION: Sorcier / Évocateur d'élite
SIZE: 6,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrême
WEAPONS: "Crocs" magiques, invocation Vex, transformation rouge mouton bleu
REWARD: 600 FC
THREAT: Extrême

DESCRIPTION: 

C'est ce qui arrive lorsqu'un villageois abandonne le commerce, enfile une robe sombre et se retire dans la forêt pour étudier la magie. C'est l'une des variétés d'Illager les plus dangereuses et les plus ennuyeuses, capable de transformer une bataille en un enfer s'il n'est pas tué instantanément. Si le Vindicator est la force brute, alors l'Evoker est l'intellect, la magie et le pur chaos.

Les évocateurs habitent dans de mystérieuses demeures forestières, tapies au plus profond de ces sombres structures. Cependant, vous pouvez également les rencontrer lors des dernières étapes des raids de village.

Malgré leur constitution fragile, leurs mains cachent un pouvoir mortel.

The Evoker is a master of territory control. They always keep their distance, trying to keep enemies at bay. Their arsenal includes three primary spells, each capable of quickly dispatching you: 

Vex Summoning (Purple Aura): Lorsque l'Evoker lève la main et qu'elle brille en violet, il invoque 2 à 3 Vex volants. L'Evoker peut les réinvoquer s'il en reste moins de 4.

"Fangs" (Yellow Aura): Si l'Evoker lève les mains et qu'elles brillent en jaune, une rangée de "Crocs" épineux émerge du sol dans la direction ciblée ou autour de lui, entrant en collision et infligeant d'énormes dégâts. Cette attaque est rapide et douloureuse.

Sheep Transformation ("Volo-lo"): La capacité la plus drôle, mais la plus inutile. S'il y a un mouton bleu à proximité, l'Evoker peut perdre du temps à crier « Volo-lo ! pour le transformer en rouge.

TIPS: 

- Tuez-le d'abord. Dans toute lutte contre les illageois, l'Evoker devrait être la cible prioritaire numéro un. S'il reste en vie, il enverra des Vex partout sur le champ de bataille.
- Réduisez la distance. Il est impuissant au corps à corps. Dès que vous courrez vers lui, il cessera d'invoquer Vex et tentera de battre en retraite. Ne le laisse pas faire ça.
- Attaque pendant le sort. Pendant que ses mains brillent et qu'il prépare son sort, il est sans défense. C'est le meilleur moment pour attaquer.
- Esquivez les crocs. Leurs attaques sont visibles à travers les particules au sol. Dès que vous voyez les particules jaunes, éloignez-vous immédiatement. Une ligne droite de Crocs est très dangereuse.
- Ignorez les Vex au début. Les Vex ont une santé faible et disparaissent après un certain temps. Si vous commencez à vous concentrer sur eux, l'Evoker aura le temps d'en invoquer davantage. Tuez-le d'abord, puis occupez-vous du reste de son armée.
- Si vous tuez l'Evoker, il laissera tomber l'extension Totem. Cet artefact peut vous sauver la vie au tout dernier moment.
