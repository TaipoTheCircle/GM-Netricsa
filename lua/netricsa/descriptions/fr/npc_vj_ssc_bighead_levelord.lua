DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais au niveau de difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Richard Bailey « Levelord » Gray est un véritable patriarche de la conception de niveaux de jeu, dont le crâne surdimensionné contient des décennies d'expérience, de sarcasme et un incroyable sens du plaisir. Ce vétéran de la guerre froide, ancien ingénieur aérospatial et ancien élève des sous-marins soviétiques a trouvé sa véritable vocation dans la création d'espaces virtuels. C'est lui, avec Allen Bloom, qui a créé les meilleurs niveaux de l'emblématique Duke Nukem 3D, gravé à jamais dans l'histoire la phrase "Vous n'êtes pas censé être ici".

While Levelord himself modestly denies direct involvement in the development of Serious Sam: La Première Rencontre, son esprit et ses principes sont invisiblement présents dans le jeu. Croteam, comme l’ensemble du monde du jeu vidéo, a été grandement influencé par son travail. Sa philosophie - selon laquelle un jeu doit être avant tout amusant et qu'un niveau doit être thématiquement cohérent et équilibré comme un bon restaurant - est devenue la clé de voûte de générations de designers, y compris l'équipe croate. Sa vie ultérieure en Russie et son travail sur le 20e anniversaire du duc Nukem ne font qu'ajouter à son aura de « cerveau » le plus coloré et le plus durable de cette galerie.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", détruisez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
