KATE


CLASS: Kate
METABOLISM: Humain, Femelle
PERCEPTION: Agent de sécurité (niveau 3), centre de recherche de Black Mesa
SIZE: 5,6 pieds
ENDURANCE: Moyen
HOSTILITY: Neutre envers le personnel de Black Mesa ; hostile aux créatures extraterrestres et aux forces hostiles
WEAPONS: Pistolet Glock 17
REWARD: 500FC
THREAT: Modéré

DESCRIPTION: 

Kate, également connue sous son nom de code Azure Sheep. Elle sert comme agent de sécurité de niveau 3 au centre de recherche de Black Mesa et se distingue par son apparence distinctive par rapport aux autres membres du personnel de sécurité féminin. Portant un béret, des lunettes et un gilet en Kevlar, Kate est à la fois un officier compétent et une figure centrale du récit.

Lors des événements de l'incident de Black Mesa, Kate devient une alliée clé de Justin Barns. Elle aide à naviguer dans le chaos de l'installation, affrontant les créatures Xen et les forces hostiles des Black Ops. Leur partenariat évolue vers une relation plus profonde, Kate devenant l'intérêt amoureux de Barns.

TIPS: 

- Combat Support: Kate maîtrise son Glock 17 et peut participer aux échanges de tirs.
- Escort Missions: En raison de ses compétences au combat et de sa connaissance des installations, elle est précieuse lors des opérations d'escorte ou de sauvetage.
- Vulnerability: Bien que capable, elle n'est pas invincible ; s'assurer qu'elle est protégée lors de situations de combat intenses.
