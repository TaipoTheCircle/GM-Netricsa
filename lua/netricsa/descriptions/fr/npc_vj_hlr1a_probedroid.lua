DROÏDE SONDE MESA NOIR


CLASS: Droïde sonde Mesa noir
METABOLISM: noyau alimenté
PERCEPTION: Drone synthétique
SIZE: 4 pieds
ENDURANCE: moyen
HOSTILITY: modéré
WEAPONS: faisceau de guérison (alliés), lanceur de fléchettes (ennemis), explosion d'autodestruction
REWARD: 600 FC
THREAT: moyen-élevé

DESCRIPTION: 

Developed by Black Mesa as a multipurpose support drone, the Probe Droid is a hovering machine designed to aid combat units in the field. Its primary function is to scan for allied lifeforms and repair or stabilize them using a directed healing beam. However, its programming also includes offensive protocols: Lorsque des ennemis sont détectés, le droïde lance des fléchettes pointues recouvertes de toxines qui peuvent percer une armure légère. Lors de sa destruction, son noyau énergétique instable explose, provoquant une violente explosion, transformant la perte d'un droïde en une attaque finale contre les ennemis.

TIPS: 

- Gardez vos distances : l'explosion d'autodestruction est mortelle en combat rapproché.
