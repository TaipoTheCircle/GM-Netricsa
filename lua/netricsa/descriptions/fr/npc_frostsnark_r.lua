SNARK FROST DU XEN


CLASSE : Frost Snark
MÉTABOLISME : Insecte carnivore (mutation cryogénique)
PERCEPTION : Insecte extraterrestre, Glace vivante
TAILLE: 6 pouces
ENDURANCE : Faible
HOSTILITÉ : Absurdement élevée
ARMES : Morsure de givre
RÉCOMPENSE : 100 FC
MENACE : Élevée

DESCRIPTIF :

Il s’agit d’une mutation naturelle du snark commun, originaire des régions glacées de Xen. Là où les vents hurlent si fort qu'ils refroidissent les os et où la glace dure des milliers d'années, les snarks communs ne peuvent pas survivre. Mais l’évolution a trouvé un chemin : ceux qui survivent ont évolué en insectes bleus dont la coquille brille de l’intérieur d’une lumière froide et glaciale.

Contrairement à leurs frères, qui explosent avec un enthousiasme débordant quelques secondes après l'éclosion, les snarks de glace ne sont pas susceptibles de s'autodétruire. Ils peuvent vivre longtemps. Très longtemps. Et pendant tout ce temps, ils essaieront de vous geler. Ou te dévorer. Ou les deux.

Ils portent une carapace entièrement bleue avec des veines bleuâtres qui palpitent comme la glace au fond d'un glacier. De loin, ils scintillent et réfléchissent la lumière, ce qui les rend faciles à repérer. Mais dès que l’on s’en rapproche, ils disparaissent de la vue et se fondent dans la neige et la glace.

La morsure d'un snark de glace n'est pas seulement douloureuse : elle gèle sa cible. Un coup et vous commencez à ralentir. Deux, et vous ne pouvez plus courir. Trois, et vous restez simplement là, une statue impuissante de chair et de glace, jusqu'à ce que la meute vous achève. Les survivants affirment que ces créatures attaquent délibérément en groupe pour s'infliger un effet de gel les unes aux autres, paralysant leurs victimes avant qu'elles ne puissent tirer.

Comme leurs voisins de type feu, les snarks des glaces sont constamment en contradiction avec les calamars des glaces. Le conflit dure aussi longtemps qu'avec leurs homologues du feu. Personne ne se souvient de la cause. Peut-être que les calamars ont marché sur la banquise préférée de quelqu'un. Peut-être que les snarks ont mangé la réserve de cristaux de quelqu'un. Ou peut-être qu’ils ne s’aiment tout simplement pas à cause de la couleur de leur coquille.

En mourant, un snark de glace ne meurt pas simplement. Il se désintègre en morceaux de glace, laissant derrière lui une flaque d’eau gelée qui dure longtemps.

CONSEILS :

- Ne vous approchez pas trop d'un snark de glace. Il n'explose pas comme un snark normal, mais sa morsure gèle.
- On les repère de loin grâce aux reflets qu'ils produisent. Utilisez un fusil de sniper.
- Ils sont doublement dangereux à courte portée. Gardez vos distances.
- Les Frost Snarks ne meurent pas tout seuls.
- Si vous voyez un pack, utilisez des explosifs. Ils n'ont pas peur du froid, mais une explosion, c'est une autre affaire.
- Frost Snarks et Frost Bullsquids se détestent. Si possible, confrontez-les les uns aux autres. Laissez-les s'entre-tuer.
- Les grenades thermiques fonctionnent mieux contre eux que les grenades classiques. La chaleur et le froid ne font pas bon ménage.