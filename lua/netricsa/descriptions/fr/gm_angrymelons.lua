CITY 17, ASSASSINAT DE PASTÈQUES EN COLÈRE


Sam, I don't even know what to say. We've seen a lot: zombies dans les sous-sols, Mental dans l'espace, pièces en briques sans portes.

Mais que des pastèques habitent City 17... et qu'ils aient une fronde... et qu'ils se vengent... C'est un nouveau niveau d'absurdité, et j'aime ça.

A typical corner of City 17: des murs gris, quelques bâtiments, un petit magasin. Une journée typique dans la ville occupée. Mais quelque chose ne va pas. Près du magasin, au lieu de la patrouille habituelle du Combine, il y a une énorme fronde. Et tout autour se trouvent des pastèques. Beaucoup de pastèques. Ils restent là, attendant leur heure. Dans votre attente.

Vous prenez le lance-pierre (de préférence congelé pour qu'il ne s'envole pas).

Vous placez une pastèque dessus.

Soudez une pastèque à une fronde en réglant la tension à 2000 ou plus.

Saisissez la fronde, appuyez sur R, tirez et relâchez.

La pastèque s'envole pour détruire les murs de la Cité 17.

