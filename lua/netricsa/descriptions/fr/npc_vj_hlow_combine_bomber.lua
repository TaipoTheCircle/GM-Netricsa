COMBINEZ LE BOMBER D'OVERWATCH


CLASS: Combiner le bombardier Overwatch
METABOLISM: Humain augmenté, mâle
PERCEPTION: Soutenir la suppression des incendies et des zones
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Très élevé
WEAPONS: Lance-grenades à triple tir Overwatch
REWARD: 700FC
THREAT: Haut

DESCRIPTION: 

Le Combine Overwatch Bomber est un avertissement ambulant indiquant que la couverture n’est plus sûre. Il porte une armure légère tachetée de bleu ciel conçue pour les opérations dans des environnements enneigés, des complexes de grande hauteur et des zones industrielles stériles. Une visière bleue souligne son rôle d'agent de suppression à longue portée.

L'arme principale ressemble à un énorme fusil de chasse, mais au lieu d'une chevrotine, elle tire une rafale de trois grenades en une seule volée. Cette rafale couvre une zone à la fois, transformant les couloirs, les cages d'escalier et les barricades en un chaos instantané. Le Combine utilise ces soldats pour déloger l'ennemi des positions fortifiées sans échanges de tirs prolongés.

Les grenades sont programmées pour une détonation optimale, rendant la fuite presque inutile. Même si la première grenade manque la cible, la deuxième et la troisième terminent généralement le travail. Le soldat travaille méthodiquement, ciblant les concentrations ennemies et les voies de fuite potentielles.

TIPS: 

- Ne vous cachez pas dans une couverture standard.
- Il est moins dangereux dans les espaces ouverts qu'à l'intérieur.
