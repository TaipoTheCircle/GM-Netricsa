MAISON EN BRIQUE, SYNDROME DE LA BRIQUE


Sam, le projet de construction du siècle est en voie d'achèvement. Celui qui a conçu ce complexe s'est clairement inspiré de l'architecture des rêves, où tout est là, mais il n'y a pas de portes, et où la brique rouge coexiste paisiblement avec le blanc, comme si c'était censé être le cas.

Découvrez la version finale de la légendaire Brick House.

The landscape continues to evolve. There's an entire village here: 

Étang. A droite de la maison, juste sur l'herbe plate, un étang éclabousse désormais. Pas une piscine, pas une flaque d'eau, mais un étang à part entière. Il y a de l'eau, pas de poisson, mais qui sait ce qui la peuplera.

Un bâtiment en briques blanches de deux étages. Derrière la maison, à gauche, s'élève un colosse à deux étages. Brique blanche, parquet et... pas d'escalier menant au deuxième étage. Juste des murs, du sol et du plafond, et entre les sols, le vide et les rêves.

First floor: 8 fenêtres. Beaucoup de lumière, beaucoup de verre, beaucoup de points d'entrée potentiels pour les tireurs d'élite. Pour une raison quelconque, un mur est rouge. Apparemment, ils ont décidé de ne pas jeter les vieux stocks de briques.

Second floor: 6 fenêtres. Pas d'escalier.

Inside: vide. Le vide absolu. Pas de table, pas de chaise, pas même une boîte de conserve. Juste les murs, le sol et les fenêtres. Et le mur rouge du premier étage vous regarde avec une question silencieuse : « Pourquoi suis-je ici ?

The first brick house: les mêmes armes clouées, les mêmes boîtes verrouillées, les fournitures de Sofia et l'horloge accrochée au mur. Pas de porte ; ils n'attendaient pas.

Garage: le moteur et la clé attendent toujours leur moment sur la table en bois.

Swimming pool: plein d'eau, prêt pour les harceleurs fatigués.

Four broken cars: garé, rouillé.

Eleven trees: debout autour du périmètre, un toujours dans son nouvel emplacement.

Maintenant vous êtes prêt.
