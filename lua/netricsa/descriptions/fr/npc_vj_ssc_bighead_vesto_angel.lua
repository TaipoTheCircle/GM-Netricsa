ANGE À GROSSE TÊTE, VESTO


CLASS: Ange à grosse tête, Vesto
METABOLISM: Humanoïde, mâle
PERCEPTION: Fanatique enthousiaste
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Neutre
WEAPONS: Aucun (sauf en difficulté I Am Ugh-Zan)
REWARD: 100 FC
THREAT: Ennui

DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Vesto est l'une des figures les plus énigmatiques et les plus pures du générique de Croteam. Contrairement à « Wassboy », dont le rôle est clairement défini, Vesto reste simplement un nom, succinctement inscrit dans la catégorie Moral Boost. Son crâne surdimensionné est un vaisseau de soutien silencieux mais puissant. Peut-être était-il cet ami qui apportait le café à trois heures du matin, ou bien le collègue dont la confiance inébranlable dans la réussite du projet était contagieuse.

Sa présence au générique rappelle que le moral de l'équipe se construit sur des dizaines de contributions, grandes et petites, dont beaucoup ne peuvent être mesurées. Vesto est un symbole de tous ceux qui n'ont pas écrit de code ni sculpté de modèles, mais dont la foi, la patience ou la simple gentillesse humaine sont devenues le fondement invisible sur lequel s'est développé le monde fou et magnifique de Serious Sam.

GROSSE TÊTE, JOHN J. DICK "BOOGER"

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", détruisez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
