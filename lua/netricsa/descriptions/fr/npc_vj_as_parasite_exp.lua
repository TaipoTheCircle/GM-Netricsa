PARASITE EXPLOSIF ESSAIS "BETA"


CLASSE : Parasite explosif d'essaim "Bêta"
MÉTABOLISME : Xénobiologique
PERCEPTION : spécimen d'assaut
TAILLE : 3,2 pieds
ENDURANCE : Très faible
HOSTILITÉ : Très élevée
ARMES : Attaque sautée, Détonation biologique
RÉCOMPENSE : 70 FC
MENACE : moyenne à élevée

DESCRIPTIF :

Le parasite Explosive Swarm « Beta » est une variante parasitaire spécialisée, adaptée non pas pour infecter ses hôtes, mais pour détruire instantanément sa cible au prix de sa propre vie. Sa structure corporelle est pratiquement identique à celle d'un parasite essaim standard « Bêta », mais son abdomen élargi ressemble au sac abdominal d'un moissonneur et est rempli d'agents biologiques explosifs instables.

Ces créatures sont extrêmement agressives et se précipitent immédiatement vers la cible vivante la plus proche après leur apparition. En atteignant sa victime, le parasite fait un long bond et explose à l'impact, frappant l'ennemi avec une onde de souffle et dispersant des fragments organiques corrosifs.

Les parasites explosifs apparaissent le plus souvent après la mort d'un Swarm Harvester "Beta", qui libère plusieurs de ces créatures à la fois. Grâce à leur petite taille et leur vitesse élevée, ils peuvent rapidement surmonter les obstacles et constituer une menace sérieuse dans des espaces restreints ou lors de combats avec d'autres membres de l'Essaim.

Bien que chaque parasite ne dure que jusqu'à ce qu'il attaque, leur déploiement massif permet à l'Essaim de submerger les défenses ennemies, les obligeant à se concentrer constamment sur des cibles qui s'approchent rapidement.

CONSEILS :

- Détruisez les parasites à distance, en les empêchant de s'approcher.
- Après la mort du Moissonneur, attendez-vous à ce que plusieurs parasites explosifs apparaissent immédiatement.
- Une seule explosion peut infliger des dégâts à plusieurs combattants.
- Les armes avec une cadence de tir ou des dégâts de zone élevés sont les plus efficaces contre ces créatures.