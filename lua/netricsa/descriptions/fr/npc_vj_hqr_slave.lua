DEMI-QUAKE SHOW VORTIGAUNT (ESCLAVE)


CLASS: HALF-QUAKE Show Vortigaunt, esclave
METABOLISM: Mammifère intelligent
PERCEPTION: Race extraterrestre asservie
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Forcé
WEAPONS: Décharge bioélectrique, griffes
REWARD: 300FC
THREAT: Moyenne

DESCRIPTION: 

Lorsque le Sadistic HALF-QUAKE Show a signé un contrat avec Black Mesa, Vortigaunts est venu avec l'équipement et les crabes. Beaucoup d'entre eux. Ils ont été amenés par lots, stockés dans des entrepôts et pendant un certain temps, personne ne savait quoi en faire, car un mammifère intelligent est plus difficile à programmer qu'un parasite.

The problem was solved simply: Les anciens circuits psioniques de Nihilanth étaient maîtrisés, et chaque Vortigaunt avait une marque blanche appliquée sur son visage avec un marqueur ">)" avant de monter sur le plateau. La marque est permanente, non pas parce qu'elle est apposée, mais parce que les Vortigaunts ont peur de l'enlever.

La version non officielle est que les producteurs de HALF-QUAKE aiment simplement regarder ces choses sauter sur les gens avec leur logo sur la tête.

À la vue d'un ennemi, un Vortigaunt décharge son énergie bioélectrique accumulée, libérant des éclairs verts des deux mains.

TIPS: 

- La foudre frappe loin et fait mal. Ne regardez pas inutilement dans les coins.
- Ils sont plus effrayants en groupe que seuls.
