SOLDAT D'ÉLITE D'OVERWATCH COMBINE AUX YEUX JAUNES


CLASS: Overwatch aux yeux jaunes Combine Elite
METABOLISM: Humain augmenté, mâle
PERCEPTION: Force de frappe d'élite du groupement furtif
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, boules d'énergie
REWARD: 850 FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine Yellow-Eyed Overwatch Elite est un soldat d'élite déguisé en soldat Combine standard. Son armure a la palette de couleurs bleue et blanche standard des soldats réguliers, lui permettant de se fondre dans les formations et d'endormir les ennemis dans un faux sentiment de sécurité.

His only distinguishing feature is his yellow visor. While his appearance resembles Nova Prospekt's elite prison soldiers, his status is considered that of a regular unit. This is intentional: le Combine intègre ces soldats dans des unités standard pour améliorer discrètement leur efficacité au combat.

En termes d'implants, de temps de réaction et d'entraînement tactique, il est tout à fait équivalent à un soldat d'élite. Au combat, il agit avec sang-froid, coordonne les tirs des soldats réguliers et est le premier à engager des cibles particulièrement dangereuses. Les boules d'énergie sont utilisées pour chasser les ennemis hors de leur abri, après quoi il achève la cible avec un tir pulsé précis.

TIPS: 

- Si vous voyez une grande visière jaune parmi les soldats réguliers, c'est une mauvaise nouvelle.
- Il ressemble à un soldat, mais tire comme un élite.
- Détruisez-le d'abord : sa présence rend toute l'équipe plus dangereuse.
- Don't confuse him with a prison elite soldier: il opère sur le terrain et est plus mobile.
