CHI-FANG


PLANET: Chi-Fang
TYPE: Planète terrestre, Planète industrielle
CLIMATE: Tempéré
MOONS: 1 lune
TERRAIN: Villes
LOCATION: Système stellaire inconnu dans la Voie Lactée.
NATIVE RACES: Chi-Che, anciennement Mental Horde (toutes les races)
ALLIANCE: Ancien Empire du Prince Chan et Chi-Fang (anciennement), Nouvel Empire de Chi-Fang
LOCATIONS: Ville de Chi Che, port de Chi Che, parc d'attractions Chan, palais Chan

DESCRIPTION: 

L'architecture de cette planète s'inspire de l'esthétique de la Chine préindustrielle. Les villes sont construites avec des pagodes densément peuplées avec des toits incurvés distinctifs, indiquant une forte densité de population dans des vallées habitables entourées de sommets montagneux inhospitaliers. Elle dispose d'une infrastructure portuaire bien développée, ce qui signifie que ses principales routes commerciales se font par voie maritime.

La planète a été capturée par les forces de Mental grâce à l'assujettissement de son dirigeant. Le prince Chan, dirigeant de Chi-Fang, a conclu un accord avec Mental, recevant une armée de ninjas morts-vivants en échange de leur loyauté. Jusqu’à récemment, ces troupes patrouillaient dans les rues de la ville et dans les jardins du palais.

KEY LOCATIONS: 

- Port Chi-Che: Une plaque tournante des transports animée reliant la planète au monde extérieur.
- Chan Amusement Park: Un complexe construit sur ordre du prince Chan, comprenant trois grandes roues géantes.
- Prince Chan's Palace: La résidence du souverain. Les chambres intérieures comprennent un jardin entouré de murs de verre et une salle du trône. La zone extérieure du palais est décorée de statues de dragons et mène à une pagode.
