BORDER BEACH, OÙ TOUT VA MAL


Sam, j'ai analysé cet endroit et... je suis sans voix.

Et si je le fais, ils sont perdus quelque part entre "WTF !?" et "Pourquoi cet endroit est-il si effrayant ?".

Imaginez une plage qui n'a jamais vu le soleil, une piscine dans laquelle personne n'a jamais nagé et un gratte-ciel construit simplement parce qu'ils le pouvaient.

Ajoutez à cela une poupée en hauteur, qui vous surveille clairement, et vous obtenez Border Beach.

Le ciel est ici un personnage à part entière. Des nuages ​​lourds et épais pendent si bas qu’il semble que vous pourriez sauter d’un plongeoir pour les atteindre.

What's here: 

Une immense piscine, l'absurdité ultime. Pourquoi avoir une piscine sur une plage ? C'est comme apporter un thermos de thé dans un sauna. Mais c'est là. L'eau est noire, immobile, comme un miroir dans lequel on a peur de se regarder. A proximité se trouvent des tables et des chaises en bois.

Comme si quelqu’un avait prévu de pique-niquer aux confins de la réalité mais avait changé d’avis à la dernière minute.

L'obélisque-gratte-ciel est un monolithe s'élevant droit dans les nuages. C'est clairement la force dominante ici. Énorme, sombre, bouleversant. Vous le regardez et vous vous sentez comme une fourmi devant une chaussure. Il ne reste pas là, il... appuie. Avec sa présence.

La poupée là-haut... et ça, Sam, c'est la chose la plus étrange. Quelque part là-haut, sur un rebord, se trouve une petite poupée. Il ne bouge pas, il ne s'enfuit pas, il s'assoit et regarde vers le bas. Chez nous. Ou par notre intermédiaire. Je dirais que c'est une blague stupide, mais il n'y a pas de blagues dans un endroit comme celui-ci.

Je n'ai pas de données claires. Mes algorithmes ne peuvent pas traiter complètement cet endroit. La logique est brisée ici, la géométrie est brisée, le but même de l'existence de cette plage est brisé. Il s’agit d’un espace liminal dans sa forme la plus pure : un lieu qui devrait être familier mais qui semble étranger. Vous êtes censé vous détendre ici, mais vous voulez courir sans vous retourner.

Tu sais, Sam... traversons rapidement cette plage, allons ailleurs et faisons comme si nous n'étions pas là. Allons-y. Et pour l’amour du ciel, ne regarde pas en arrière.
