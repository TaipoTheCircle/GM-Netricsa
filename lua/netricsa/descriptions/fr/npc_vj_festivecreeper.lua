CLASS: Plante grimpante festive
METABOLISM: Poudre à canon végétale
PERCEPTION: Foule hostile, lanceur de dynamite
SIZE: 5,5 pieds
ENDURANCE: Faible
HOSTILITY: Extrêmement élevé
WEAPONS: Blocs TNT
REWARD: 200FC
THREAT: Moyen

DESCRIPTION: 

C'est ce qui arrive lorsqu'une plante grimpante ordinaire décide que se faire exploser sur place est ennuyeux et qu'il est bien plus amusant de lancer des explosifs à distance.

Contrairement à son homologue vert classique, qui se faufile et siffle, le Festive Creeper agit comme un pyromane lâche. Il apparaît la nuit, comme des creepers ordinaires, mais au lieu de percuter un ennemi, il commence à lancer des blocs de dynamite (TNT). Il recule constamment, essayant de maintenir sa distance.

Les explosions de ces TNT sont équivalentes en puissance aux boules de feu d'un horrible. Ils ne causent pas beaucoup de dégâts, mais si vous manquez quelques coups d'affilée, votre santé chutera. Parfois, lorsqu’ils sont lancés, les TNT enflamment le sol, aggravant ainsi le problème.

Si un Festive Creeper est frappé par la foudre, il se charge. Dans ce mode, la force de son explosion de TNT augmente jusqu'à celle d'une plante grimpante ordinaire. Rester près de soi est fortement déconseillé.

TIPS: 

- Le Festive Creeper n'a pas d'arme de mêlée, donc la meilleure tactique est de courir vers lui et de le frapper avec une épée. Il ne pourra pas lancer de dynamite à bout portant.
- Il est immunisé contre le feu, ce qui signifie qu'il est inutile d'y mettre le feu. La lave ne fera aucun dégât non plus.
- Ses TNT ne volent pas très vite, tu peux donc les esquiver si tu continues à avancer.
- Ne le laissez pas s'enfuir. Plus il est éloigné de vous, plus il a le temps de les lancer.
- Si vous voyez une plante grimpante festive chargée, notamment à proximité des bâtiments, tuez-la au plus vite ou fuyez. Des explosions avec la force d'une plante grimpante ordinaire peuvent détruire toutes vos créations.
