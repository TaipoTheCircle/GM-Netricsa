MISS THAVEL DE L'ÉDUCATION PAPIER FONDAMENTALE


CLASS: Mlle Thavel
METABOLISM: Organisme de papier / Entité mythique
PERCEPTION: Professeur de langues
SIZE: ~9 pieds
ENDURANCE: haut
HOSTILITY: Extrême
WEAPONS: Mains griffues géantes, dents pointues, règle (symbole d'autorité), transformation Wendigo
REWARD: 1000FC
THREAT: Très élevé

DESCRIPTION: 

Le plus jeune des trois professeurs de langues meurtriers. Dans sa forme normale, elle a la peau blanche comme neige, les cheveux noirs ébouriffés, trois cubes de lettres sur la tête (par ordre alphabétique) et porte une robe blanche en lambeaux. Son arme principale sont ses énormes mains noires et griffues, capables de couper des livres, et probablement des étudiants, en deux. Elle possède une forme cachée et plus terrifiante, le Wendigo, dans laquelle des bois poussent sur sa tête, son corps se couvre de fourrure et ses vêtements sont encore plus en lambeaux. Cette transformation fait allusion à sa nature mythique, insatiable et prédatrice, allant au-delà de la simple punition pour échec scolaire. Comme ses collègues, elle est cruelle et impitoyable.

TIPS: 

- Au corps à corps, elle est mortelle grâce à ses griffes. Ses attaques sont rapides et radicales. Sous sa forme Wendigo, sa force, sa vitesse et éventuellement sa férocité sont considérablement augmentées.
- Une peur intense chez la victime ou sa propre rage peuvent déclencher la transformation en Wendigo. (Ce n'est pas sûr). Rester calme peut retarder ou empêcher cette forme.
