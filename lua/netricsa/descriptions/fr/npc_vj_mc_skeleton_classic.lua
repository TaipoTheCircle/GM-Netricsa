ARCHER SQUELETTE CLASSIQUE DU MONDE MINECRAFT OVERWORLD


CLASS: Archer squelette classique
PERCEPTION: Mort-vivant, unité à distance
METABOLISM: Mort-vivant soutenu comme par magie
SIZE: 6,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Arc et flèches
REWARD: 200FC
THREAT: Moyenne

Description: 

Le Classic Skeleton Archer est un exemple classique de la façon dont un minimum de chair ne compromet pas une irritation maximale. C'est une collection vivante d'os, maintenus ensemble par la magie, la malice et un excellent sens de la distance. Il n'aime pas le combat rapproché, préférant transformer méthodiquement sa cible en coussin pour ses flèches.

À la lumière du jour, le squelette brûle, ce qui ne l'empêche cependant pas d'essayer de vous achever avec une dernière flèche car il brûle magnifiquement.

Tips: 

- Un bouclier est votre meilleure défense contre un archer squelette.
- Utilisez un abri, sinon la prise de vue sera unilatérale.
- Si un squelette se retrouve soudainement à chevaucher une araignée, considérez cela comme une promotion.
