Probabilité d'un parcours propre - 36%.

Probabilité de tomber et de décorer la fosse en dessous - 63%.

Probabilité que le conteneur écrase commodément un ennemi pour vous - 1%.

Recommendation: canalisez votre gymnaste intérieur, ignorez votre peur des hauteurs et rappelez-vous : les points de style ne comptent pas si vous éclaboussez en bas.
