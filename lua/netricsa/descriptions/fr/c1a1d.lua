MESA NOIR, CONTENEURS


Sam, pour atteindre l'ascenseur, tu devras traverser une brèche béante dans l'installation. Le seul « pont » disponible est une série de conteneurs suspendus, suspendus au-dessus du vide comme un parcours d’obstacles sadique.

Ils sont instables, ils oscillent et ils n’ont certainement pas été conçus pour la circulation piétonnière. Mais à moins que vous n'ayez secrètement emballé un jetpack dans votre sac à dos, sauter de conteneur en conteneur est votre seule option.

