LE SPECTACLE DEMI-SÉMEMENT, DÉBUT


Alors, Sam. Le premier essai.

Ou, comme on aime à le dire ici, la première « torture ».

Pour la série, vous n'êtes plus un héros, un soldat ou même une erreur de chronologie. Vous êtes la victime. C'est ainsi que l'annonceur vous présente, avec le même ton que vous entendriez lors de l'annonce d'une publicité ou d'un bulletin météo.

Le test commence par une série de pièces. Simple. Direct. Mortellement honnête.

Dans la première salle, les Vortigaunts sont libérés. Sans fioritures. Un classique. Les règles du spectacle ne leur ont pas été expliquées, mais ce n'est pas nécessaire. Leur travail est d'être un obstacle. Le vôtre est d’avancer et de ne pas vous attarder sur des réflexions humanistes.

The next room is more interesting. Spikes. A button. A door. To get through, you need to step inside and press the button. The door will open. The spikes will move toward the button. Right toward you. The organizers' plan is clear: donnez-leur de l'espoir, puis testez à quelle vitesse vous pouvez retourner dans la pièce précédente et plonger par une porte ouverte. Le timing est primordial. Une erreur est également décisive, mais cette fois elle est définitive.

La prochaine étape est une piscine de lave. Trois dalles. Sautable. Dès que l’on descend d’une dalle, celle-ci s’effondre. Le spectacle aime le mouvement et décourage le doute. Attardez-vous et vous ferez partie du décor.

Puis d'autres pointes. Parce que la tradition doit être respectée.

L'ascenseur vous mènera à un étage, où une salle pleine de tourelles vous attend. Oui, ces mêmes-là. Veuillez l'acheter chez Black Mesa. Les affaires sont les affaires.

Quelque part parmi cette splendeur se cache un bouton. Trouvez-le et vous pourrez passer à autre chose. Mais pas directement. Vous devrez marcher le long d'un mince rebord jusqu'à la porte. C'est miné. Vous pouvez vous en approcher, mais vous ne pouvez pas y entrer. Vous devrez faire demi-tour et repartir. Ensuite, un autre passage s'ouvrira. Autre part. Logique? Non. Efficace ? Absolument.

Et bien sûr, tout cela sera généreusement assaisonné par les troupes de Mental. Parce que le spectacle doit évoluer. Et les téléspectateurs aiment quand les enjeux montent.

Souviens-toi, Sam.

Ils veulent que vous échouiez.

Ils attendent que tu te casses.

Et vous continuez.

Le test ne fait que commencer.
