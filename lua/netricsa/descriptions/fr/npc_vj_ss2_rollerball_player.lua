HAM-Z-RR314 "ROLLERBALL", VÉHICULE DE RAMMING


CLASS: HAM-Z-rr314 "Rollerball", véhicule de frappe
METABOLISM: Mécanique
PERCEPTION: Transport de balle
SIZE: 6,5 pieds
ENDURANCE: Moyenne
HOSTILITY: Dépend du conducteur
WEAPONS: Bélier, pointes, masse
REWARD: 500
THREAT: Moyenne

DESCRIPTION: 

Le Rollerball est l'une de ces merveilles d'ingénierie qui vous font vous demander : "Qui est le génie qui a inventé cela et où devraient-ils lui ériger un monument ?" C'est une énorme boule, parsemée de pointes, avec une personne qui court à l'intérieur. Courez littéralement comme un hamster sur une roue, mais au lieu d'une roue de hamster, il y a une sphère de mort de plusieurs tonnes, écrasant tout sur son passage.

The controls are incredibly simple: si vous voulez bouger, courez. Si vous voulez vous retourner, penchez-vous. Si vous voulez tuer, accélérez et dirigez ce colosse vers le cyclope le plus proche. L'impact perce les pointes, brise la chair et le roller continue de rouler sans jamais ralentir.

The design allows for jumping. How exactly you can jump inside the ball and make this colossus lift off is a mystery, but the fact remains: le roller rebondit comme une balle en caoutchouc remplie de colère et de pointes.

This thing can be used in a variety of ways: comme arme, comme moyen de transport, comme moyen de s'échapper rapidement d'une zone dangereuse ou comme attraction pour ceux qui ont toujours voulu se sentir comme un Terminator. La principale chose à retenir est que la balle subit également des dégâts. Quelques collisions puissantes et tout ce qui reste, ce sont des pics et des souvenirs.

TIPS: 

- Accélérez et frappez.
- Le roller est rapide. Utilisez-le pour éviter les missiles et les lasers.
- Parfois, il vaut mieux sauter par-dessus un obstacle que d'essayer de le renverser.
- Si vous voyez une foule d'ennemis, roulez directement dessus. Une course coûte des milliers de balles.
- À ne pas confondre avec ses homologues IA.
