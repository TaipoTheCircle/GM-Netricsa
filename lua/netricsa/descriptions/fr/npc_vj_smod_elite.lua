COMBINEZ UN SOLDAT BLINDÉ D'ÉLITE LOURDE D'OVERWATCH


CLASS: Combiner Overwatch Combiner un soldat blindé d'élite lourd
METABOLISM: Humain augmenté dans un exosquelette
PERCEPTION: Combiner l'unité de suppression des assauts lourds
SIZE: 5,9 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, OICW
REWARD: 1200FC
THREAT: Critique

DESCRIPTION: 

Le Combine Overwatch Heavy Elite Armored Soldier est une plate-forme de combat ambulante, conçue pour briser les défenses et démontrer la domination absolue du Combine. Il est vêtu d'un énorme exosquelette robotique, noir et gris avec des accents rouges, à l'intérieur duquel l'humain devient simplement un élément de contrôle de la machine de guerre.

The suit completely covers the soldier's body, including the head. The helmet is sealed, leaving no vulnerable areas. The armor's structure clearly reveals mechanical components: servos, joints renforcés, plaques blindées et nœuds de puissance.

L'exosquelette améliore considérablement les capacités physiques du soldat, lui permettant de porter des armes lourdes sans sacrifier sa mobilité. L'AR2 est utilisé pour les tirs de suppression, tandis que l'OICW réduit la couverture et les concentrations ennemies à des déchets inutiles. Ce soldat ne recherche pas de positions avantageuses : il les crée lui-même, en avançant directement sur le champ de bataille.

Malgré son apparente invulnérabilité, la structure présente un point de rupture. Un coup direct d'un RPG détruit l'exosquelette, transformant le véhicule d'élite en un tas de métal tordu.

TIPS: 

- Les armes légères sont presque inutiles contre son armure.
- Utiliser des RPG ou des explosifs puissants - c'est sa principale et presque seule faiblesse.
- Ne vous gênez pas - il ne recule pas et ne s'arrête pas.
