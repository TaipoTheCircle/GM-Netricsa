BLENJÉL


PLANET: Blenjeel
TYPE: Planète terrestre et désertique
CLIMATE: Aride, dur
MOONS: Inconnu
TERRAIN: Vastes déserts mouvants, affleurements rocheux instables
LOCATION: Sables rouges rayonnants de lumière, ciels bleus, agglomérations urbaines
NATIVE RACES: Mineurs Sandburst (créatures géantes ressemblant à des vers)
ALLEGIANCE: Territoire neutre, répertorié dans les bases de données de la Nouvelle République comme planète dangereuse
LOCATIONS: Site de l'accident d'un navire marchand, site de l'accident de la navette Yavin Runner II

DESCRIPTION: 

A desert planet, lost far from the busy space lanes between the Ando and Kothlis systems. Its surface consists of endless sand seas, possessing a unique characteristic: le sable bouge constamment. Le paysage est en constante évolution : des affleurements rocheux qui semblaient un jour sûrs peuvent disparaître le lendemain dans les abysses sablonneux.

Blenjeel's main danger lurks beneath its shifting sands. The planet is inhabited by giant sandbursters-monstrous creatures that dwell in the desert depths. These creatures emerge swiftly from the ground, ambush unsuspecting prey. Their constant movement is the primary cause of the surface's instability: les tunnels creusés provoquent le déplacement du sable et engloutissent les affleurements rocheux.

Dix ans après la bataille d'Endor, un navire marchand fut attaqué par un raider impérial de classe Immobilizer-418 et s'écrasa sur Blenjeel. L'aspirant Jedi Jaden Korr a été envoyé en mission de sauvetage à bord du Yavin Runner II. Cependant, lors de l'atterrissage, sa navette a été frappée par la foudre et s'est également écrasée.

Jaden n'a pas pu sauver les membres d'équipage survivants : le dernier d'entre eux a été dévoré par un ver des sables sous ses yeux. Cependant, les Jedi ont réussi à survivre dans le désert, évitant constamment tout contact avec les prédateurs. Il atteint l'épave du navire marchand, récupère les pièces de rechange nécessaires, répare sa navette et quitte la planète. Après son retour sur Yavin 4, Blenjeel a été répertorié dans les bases de données de la Nouvelle République comme une planète dangereuse, déconseillée à la visite.

KEY LOCATIONS: 

- Merchant Ship Crash Zone: La zone désertique où le navire marchand s'est écrasé après avoir été attaqué par un raider impérial. Le lieu du décès de l'équipage et la source des pièces de réparation nécessaires.
- Yavin Runner II Crash Site: Le désert où la navette de secours de Jaden Korr s'est écrasée après avoir été frappée par la foudre. C’est là que son voyage vers la survie a commencé.
