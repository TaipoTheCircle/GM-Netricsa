COCHON PÂLE DU MINECRAFT OVERWORLD


CLASS: Cochon pâle
METABOLISM: Mammifère à sang chaud
PERCEPTION: Animal passif des biomes de neige
SIZE: 3 pieds
ENDURANCE: Moyenne
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 50 FC
THREAT: Minimal

DESCRIPTION: 

Une variété rare de cochon commun, adaptée à la vie dans les biomes de neige et qui se distingue de ses homologues roses par son teint plus terne et plus pâle. Le manque de pigmentation causé par un environnement hostile est plus que compensé par son charme, comme le notent à juste titre les habitants.

Pale pigs behave no differently from regular pigs: ils errent à travers les plaines enneigées, évitent la lave et les falaises et fuient pendant quelques secondes lorsqu'ils sont endommagés.

TIPS: 

- Recherchez les cochons pâles dans les biomes enneigés : ils s'y sentent comme chez eux.
- Their lack of pigment doesn't affect their usefulness: ils peuvent être élevés, montés et utilisés comme source de nourriture.
- Les cochons pâles sont un excellent camouflage dans la neige – faites attention à ne pas les manquer.
- Si vous élevez un cochon pâle avec d'autres variétés, les porcelets peuvent avoir n'importe quelle couleur chez les parents.
