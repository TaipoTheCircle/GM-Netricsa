COMBINER LE PILOTE APC


CLASS: Combiner le pilote APC
METABOLISM: Artificiel / Mécanique
PERCEPTION: Drone de contrôle synthétique (IA de type mine)
SIZE: ~1,5 pied
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Aucun directement ; contrôle les canons APC montés + les roquettes
REWARD: 250 FC
THREAT: Moyen-élevé

DESCRIPTION: 

Le Combine APC Driver est un drone sphérique spécialisé, visuellement similaire à une mine à rouleaux Combine, qui fonctionne comme un système de pilote automatique pour les véhicules blindés de transport de troupes lorsqu'aucun pilote humain ou transhumain n'est disponible. Une fois connectée, la sphère s'intègre aux systèmes de l'APC, accordant au véhicule un contrôle autonome de son armement et de ses mouvements.

Seul, le Conducteur est presque inoffensif, fragile et incapable d'attaquer. À l'intérieur d'un APC, cependant, il devient une extension dangereuse de la puissance du Combine, contrôlant les canons lourds, les mitrailleuses montées et les lance-roquettes avec une précision mécanique. Lorsqu'il est détruit ou éjecté, l'APC cesse de fonctionner et devient immobile.

TIPS: 

- Concentrez-vous sur la destruction de l'APC lui-même ; le chauffeur ne partira pas à moins d'être forcé.
- Les explosifs puissants peuvent désactiver le pilote plus rapidement que les tirs directs.
- S'il est attrapé à l'extérieur de son véhicule, il peut être neutralisé avec un minimum d'effort.
- Ne sous-estimez pas un APC contrôlé par le conducteur : ils ne manquent jamais.
