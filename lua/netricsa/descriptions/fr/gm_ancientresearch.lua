FOUILLES DE TEMPLES ANCIENS, RECHERCHES ANCIENNES


Nous nous trouvons devant une fouille archéologique. Un centre de recherche fouillant les ruines de quelque chose de très ancien. Et à en juger par ce qui est enterré ici, ils ne faisaient pas que creuser.

Au centre se trouvent les restes d’un ancien temple ou monument, construit avec des blocs de pierre massifs.

L’essentiel ici est autre chose : un secret qui mérite d’être creusé.

Quelque part sur le site de fouilles, caché sous le symbole de résistance lambda, se trouve un cube orange, clairement pas d'origine locale.

Cassez le cube. Brisez la coque extérieure et tombe... la légendaire The Orange Box. Oui, Sam, la même boîte qui a donné au monde Half-Life 2, Portal et Team Fortress 2.

Cassez la boîte. Et c'est alors que commence l'apocalypse. Une fois The Orange Box brisé en morceaux, Nuclear Mission Jam retentira dans les haut-parleurs, ce morceau épique qui transforme n'importe quelle bataille en un massacre légendaire.

Allons chercher le cube.

And remember: ne vous contentez pas de le briser, mais faites-le avec émotion. Avec un but. Avec arrangement.
