PISTOLET-MACHINE MP5K


WEAPON: Mitraillette MP5K
AMMO: Munitions de moyen calibre
ROF: ~700 tr/min

DESCRIPTION: 

Une mitraillette compacte conçue à l'origine pour une utilisation de dissimulation/sauvegarde. Il présente une précision équilibrée et une cadence de tir contrôlée, ce qui en fait un choix privilégié pour les combats rapprochés et les engagements mobiles.

NOTES: 

- Efficace contre les cibles légères et moyennes à courte portée.
- La conception compacte réduit le recul et facilite le tir dans des espaces confinés.
- Utilise des munitions moyennes standards, compatibles avec d'autres armes à feu.
