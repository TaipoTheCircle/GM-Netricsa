GARDE IMPÉRIALE DES DRAGONS DE NIRN


CLASS: Garde impériale du dragon
METABOLISM: Mer, mâle
PERCEPTION: Gardien de la zone interdite / Garde impériale d'élite
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Très élevé
WEAPONS: Épées longues, boucliers lourds, parfois magiques
REWARD: 10000FC
THREAT: Très élevé

DESCRIPTION: 

Il ne s’agit pas simplement d’une unité d’élite de la Légion impériale, mais plutôt d’un mythe entouré de mystère. Contrairement à la Garde Royale Impériale, qui garde visiblement l’Empereur et le Palais, ces gardes opèrent dans l’ombre. Leur travail consiste à patrouiller dans les fouilles interdites, les forts abandonnés, les tombes anciennes et d'autres endroits dangereux où l'entrée est interdite aux simples mortels. On pense que cette unité a été formée sur ordre personnel de l'empereur Uriel Septim VII avant la crise d'Oblivion pour garder des sites stratégiquement importants liés à des artefacts sombres et des passerelles vers d'autres mondes.

Leur caractéristique la plus distinctive est la légendaire armure impériale du dragon. Cette armure unique était traditionnellement portée uniquement par la garde spéciale qui protégeait personnellement l'empereur et sa famille. Forgée à partir des meilleurs matériaux dont dispose l'Empire, l'armure est considérée comme un symbole de la plus haute valeur et de la plus grande confiance. On sait qu'un de ces costumes a été présenté au héros de Kvatch en reconnaissance de ses services.

While information about the Imperial Dragon Guard is extremely limited, their origins are likely linked to an even older organization: 

Dragon Guard: Une organisation d'élite, prédécesseur des Lames, qui gardait les empereurs de la dynastie Réman. Ils étaient plus que de simples gardes, mais une force spéciale et une armée personnelle pour l'empereur.

TIPS: 

- Si vous voyez un garde en armure impériale de dragon, vous êtes dans une zone restreinte. Vous n'êtes pas le bienvenu. Partez avant qu'il ne soit trop tard.
- Ces gardes sont mortels au combat. Leur armure offre une résistance à la magie, au feu, au froid et à l'électricité. Les tactiques standards sont inutiles contre eux.
- La meilleure stratégie est la furtivité. Si vous êtes repéré, courez. Le combat direct avec une Garde Dragon Impériale est une mort certaine, même pour un aventurier chevronné.
