CARDARD DE CHOC, ARME


WEAPON: Cafard de choc (arme biologique)
AMMO: Charges bio-électriques (régénérantes)
ROF: ~120 chocs/min

DESCRIPTION: 

Bio-organisme extraterrestre capturé et transformé en arme Lorsqu'il est tenu, il s'accroche au bras de l'utilisateur et décharge des flux d'énergie bioélectrique sur les cibles. Fonctionne comme une arme vivante, régénérant constamment ses propres munitions.

NOTES: 

- Munitions illimitées grâce à la régénération naturelle.
- Primary fire: boulons électriques rapides efficaces à moyenne portée.
- Secondary fire: flux continu d’électricité pour des dommages durables.
