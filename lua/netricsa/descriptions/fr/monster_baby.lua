BÉBÉ DU FAVERSHOLM


CLASS: Bébé
METABOLISM: Apparition de cauchemar
PERCEPTION: Horreur psychologique hostile
SIZE: 2,6 pieds
ENDURANCE: Faible
HOSTILITY: Absolu
WEAPONS: Épine
REWARD: 50 FC
THREAT: Moyen

DESCRIPTION: 

Un reflet des recoins les plus sombres de la psyché. Ce petit adversaire rapide et extrêmement agressif symbolise l'irritation des pleurs des nouveau-nés. Innocent dans la forme, mais mortel dans l'essence.

Le bébé apparaît comme une petite silhouette pâle et nue.

It is found in apartments. It has no sophisticated tactics, no arsenal, not even a sense of self-preservation. He has only one goal: pour courir vers vous et s'empaler avec une pointe sortant de sa tête.

Oui, vous avez bien entendu. Le Kid se suicide pour essayer de vous tuer.

Il est assez rapide, plus rapide que la plupart des ennemis. S'il touche sa cible, une longue pointe pointue sort de sa tête, lui infligeant des dégâts. Le Kid lui-même est tué. Vous n’avez pas besoin de l’achever, il le fait lui-même.

Les dégâts d'une seule attaque ne sont pas significatifs. Mais le problème est que ces gars apparaissent généralement en groupe et non individuellement. Si deux ou trois d’entre eux vous frappent d’affilée, votre santé s’effondrera.

TIPS: 

- Le Kid meurt d'un seul coup avec n'importe quelle arme. Même un pistolet.
- Ne le laissez pas s'approcher. Gardez vos distances et tirez en premier.
- Ils sont particulièrement dangereux dans les couloirs étroits, car il n'y a pas de place pour esquiver. Utilisez un fusil de chasse à bout portant.
- Si vous entendez le cri d'un enfant et le crépitement des petits pieds, préparez votre arme. Ils courent déjà.
- N'attendez pas qu'ils arrivent. Courez vers eux et tirez-leur dessus à distance sûre.
