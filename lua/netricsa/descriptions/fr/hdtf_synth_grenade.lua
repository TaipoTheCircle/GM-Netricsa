GRENADE PLASMA SYNTHÉTIQUE


WEAPON: Grenade à plasma
AMMO: Noyau de plasma à chargement automatique
ROF: 950 tours/min
WEIGHT: 1,6 livres

DESCRIPTION: 

Développée par les scientifiques de Combine, la grenade à plasma est une arme expérimentale alimentée par un noyau électrique autonome. Lorsqu'il explose, il libère une puissante décharge de plasma qui non seulement étourdit et immobilise brièvement les ennemis, mais provoque également des brûlures d'énergie dévastatrices.

NOTES: 

- NE laissez PAS les mains ennemies atteindre cette arme.
- Efficace contre l'infanterie.
