COMBINEZ UN NAVIRE DE GUERRE


CLASS: Combiner un vaisseau de combat
METABOLISM: Synthétique / Biomécanique
PERCEPTION: Synthé d'assaut aérien
SIZE: ~40-45 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Canon à impulsion lourd (avant), canon à distorsion ventral (ventre)
REWARD: 5000FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine Gunship est un synthétiseur d'assaut volant déployé par le Combine. Il ressemble à un étrange hybride de conception organique et mécanique, avec un corps arrondi, deux ailes en forme de nageoires près de l'avant, une grande « queue » de rotor et des composants mécaniques (tubes exposés, pièces incandescentes) qui trahissent sa nature synthétique.

Il patrouille dans le ciel au-dessus des territoires critiques du Combine (Citadelle, Nova Prospekt, avant-postes clés), faisant pleuvoir des tirs précis de canon à impulsion et parfois des explosions de canon ventral plus lourdes (bien que le canon ventral soit pour la plupart inutilisé, sauf dans les commandes spéciales). Très maniable, capable de tourner à 180° en vol, il évite les engagements à courte distance et les combats à distance, utilisant sa vitesse et son agilité pour rester au-dessus des menaces.

TIPS: 

- Utilisez des armes explosives (RPG, grenades) plutôt que des armes légères - elles sont pour la plupart insensibles aux balles.
- Continuez à bouger et utilisez un abri. Les hélicoptères de combat préfèrent les attaques à longue portée et l'avantage en hauteur.
- Faites attention à son son d'avertissement avant qu'il ne commence à tirer - cela vous donne une brève chance de vous cacher.
- Évitez de vous exposer dans des zones ouvertes, notamment sous les trajectoires de vol.
- Essayez d'attaquer depuis les angles morts (en dessous, derrière ou sur les côtés) pour réduire l'exposition à son arme.
