BIKINI BOTTOM, UNDERWATER CITY


Sam, nous avons été aspirés par une anomalie qui nous a transportés directement dans la ville sous-marine de Bikini Bottom.

There's just one problem: soit l'armée de Mental vient de passer par ici, soit...

Ce ne sera donc ni calme ni paisible ici.

There's:

Le Krusty Krab - l'attraction principale, un restaurant qui sert des galettes de crabe.

Patrick's Rock - juste un rocher. Mais en dessous, il y a un trou plein de... Je ne sais même pas de quoi serait rempli un trou d'étoile de mer.

La maison de Squidward - en forme de tête de l'île de Pâques et le dôme transparent de Sandy. Les deux endroits sont des cachettes parfaites pour les embuscades.

