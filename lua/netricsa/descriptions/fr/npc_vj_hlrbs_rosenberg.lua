DR. ROSENBERG


CLASS: Dr Rosenberg
METABOLISM: Humain, mâle
PERCEPTION: Scientifique principal, Division de physique théorique
SIZE: ~6 pieds
ENDURANCE: Faible
HOSTILITY: Aucun
WEAPONS: Aucun
REWARD: 300FC
THREAT: Négligeable

DESCRIPTION: 

Le Dr Rosenberg est un scientifique senior de Black Mesa et l'un des esprits les plus expérimentés travaillant au sein de l'installation. Reconnu pour ses cheveux blancs et son attitude calme, Rosenberg se spécialise dans la théorie de la téléportation et la recherche interdimensionnelle, lui conférant un rôle crucial dans la compréhension de la technologie Xen et des phénomènes de résonance.

Lors de l'incident de Black Mesa, Rosenberg s'est avéré être l'un des rares scientifiques capables de rester calme sous une pression catastrophique. Sa priorité était la survie et l’évacuation, tant pour lui que pour ses collègues. Malgré son manque de capacité de combat, la connaissance de Rosenberg des systèmes de téléportation et des protocoles d'évacuation a fait de lui un allié essentiel du personnel de sécurité et du personnel survivant.

Bien que fragile et désarmé, la valeur de Rosenberg réside dans l'intellect et non dans le combat : sa survie garantit que les connaissances les plus sensibles de Black Mesa ne meurent pas avec l'incident.

