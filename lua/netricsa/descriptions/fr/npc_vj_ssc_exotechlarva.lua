LARVE EXOTECH


CLASS: Larve Exotech
METABOLISM: cyborg, arthropode
PERCEPTION: génétiquement programmé
SIZE: 30 pieds
ENDURANCE: extrême
HOSTILITY: haut
WEAPONS: canons à plasma, biopodes, laser haute énergie
REWARD: 750000
THREAT: haut

DESCRIPTION: 

The success of it's first abomination of nature - the Biomechanoid - pushed Mental even further this time.  In an attempt to create a perfect and obeying bodyguard, Mental decided to combine robotic parts with an even simpler organic creature - an insect larvae.  Larva was heavily armed and put into a 'strength multiplicator' device and was supposed to guard Mental.  Things did not work out as planned.  After the 17th assassination attempt by Larva on Mental and many hours spent teaching the cyborg the difference between it's Master and a common caterpillar, Mental finally gave up and found it's pet a new job: Très faible (meurt en subissant des dégâts)

TIPS: 

- La force de Larva découle de la force... des réacteurs. La créature rechargera ses batteries lorsqu'elle sera faible, alors assurez-vous de vérifier les environs à la recherche de tout appareil suspect.
- Utilisez les colonnes pour vous cacher de ses armes.
