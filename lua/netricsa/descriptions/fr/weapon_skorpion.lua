DESCRIPTION: 

Le Škorpion (Scorpion) est une mitraillette tchécoslovaque développée pour les équipages de chars, les signaleurs et autres militaires. Le Scorpion a été adopté par l'armée populaire tchécoslovaque en 1961 (d'où la désignation vz.61 - vzor 61, signifiant « modèle 1961 »). La modification moderne est le Scorpion EVO III. Il a été utilisé pendant la guerre du Vietnam, la guerre de Tchétchénie et est actuellement utilisé dans le cadre des opérations militaires spéciales.

NOTES: 

- Très compact et léger, peut être facilement utilisé dans un véhicule.
- La crosse peut être dépliée et tirée depuis l'épaule.
- Peut être utilisé comme arme de secours.
