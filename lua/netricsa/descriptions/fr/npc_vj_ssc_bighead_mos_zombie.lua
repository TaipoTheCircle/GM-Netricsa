DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages grâce à leurs numéros.

Nikola Mosettig est l’incarnation vivante de « l’autre voie ». Lorsqu'il rejoint Croteam avec son ami Ivan Mika, il rêve d'une carrière de level designer, mais le destin en a décidé autrement en l'envoyant au département programmation. Son crâne exagéré contient 15 années d'humilité et de croissance professionnelle - depuis un programmeur de contenu responsable des scripts, des déclencheurs et de la logique d'interaction dans les jeux, jusqu'au directeur des technologies de l'information. Pendant tout ce temps, il a regardé avec une tristesse tranquille Ivan créer des paysages monumentaux, tout en écrivant minutieusement ce qui devrait se passer lorsque le joueur marchait sur telle ou telle tuile.

C'est Nikola qui a été irrité par la vilaine couverture de l'éditeur, et c'est lui qui a assisté et participé au déménagement dans un bureau spacieux, marquant une nouvelle étape dans la vie du studio. Son histoire est un hymne à tous ceux dont les rêves en matière de développement de jeux n'étaient pas tout à fait ce qu'ils paraissaient, mais qui ont néanmoins trouvé leur place et sont devenus un élément indispensable de la machine qui a cimenté les plans grandioses des concepteurs en code fonctionnel. "Nous ne pouvons tout simplement pas nous débarrasser de lui", cette plaisanterie des développeurs cache un profond respect pour un homme dont le dévouement au projet s'est avéré plus fort que ses ambitions non réalisées.

Il était autrefois vivant, mais après que quelqu'un l'ait tué, il est ressuscité d'entre les morts, devenant un zombie démoniaque.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager un chemin.
- En difficulté "I Am Ugh-Zan", détruisez-le à distance grâce à leurs modificateurs. - En groupe, une masse à frappe horizontale est efficace.
