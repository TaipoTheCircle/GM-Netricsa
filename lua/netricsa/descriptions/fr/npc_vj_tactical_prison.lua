COMBINEZ UN SOLDAT DE PRISON TACTIQUE OVERWATCH


CLASS: Combinez le soldat de prison tactique Overwatch
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combinez une unité tactique lourde de sécurité et de suppression
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, SMG, Grenades
REWARD: 800 FC
THREAT: Très élevé

DESCRIPTION: 

Le Soldat de prison tactique Combine Overwatch est une version spécialisée du Soldat tactique, entraîné et équipé pour servir à Nova Prospekt et dans des complexes pénitentiaires similaires. Contrairement aux unités de terrain, ces soldats sont formés pour opérer dans des espaces confinés, parmi les cellules, les couloirs et les zones de répression massive des détenus.

His key feature is a yellow visor and a reinforced tactical helmet with a protective visor. The helmet's design completely neutralizes his vulnerability to headshots: les tirs à la tête n'ont aucun effet critique, transformant les tentatives de tir précis en un gaspillage de munitions. Le Combine a délibérément rendu ces soldats aussi résistants que possible aux embuscades et aux attaques surprises à l'intérieur des cellules de prison.

Leur armure est encore plus épaisse que celle des soldats tactiques standards. Les augmentations internes suppriment les réactions douloureuses et maintiennent l'efficacité du combat même en cas de blessures graves.

TIPS: 

- Les tirs à la tête sont complètement inutiles - ne perdez même pas votre temps avec eux.
- Les explosifs et les armes lourdes restent les seules solutions fiables.
- Il est particulièrement dangereux dans les couloirs étroits de Nova Prospekt.
- La visière jaune est très visible – mais ce n'est pas une faiblesse, c'est un avertissement.
