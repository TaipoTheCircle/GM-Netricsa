NOIR MESA H.E.V. GARDE DE SÉCURITÉ



CLASS: Black Mesa H.E.V. Agent de sécurité
METABOLISM: Humain, mâle
PERCEPTION: Guerrier de sécurité Black Mesa
SIZE: ~6 pieds
ENDURANCE: Moyen (combinaison de protection H.E.V.)
HOSTILITY: Variable (défensif, hostile lorsqu'il est menacé)
WEAPONS: Arme de poing Glock 17.
REWARD: 250 FC
THREAT: Faible à modéré

DESCRIPTION: 

Bleu H.E.V. Les agents de sécurité sont des gardes spécialisés de Black Mesa équipés de prototypes de combinaisons pour environnements dangereux. Leur équipement offre une durabilité supérieure à celle du personnel de sécurité standard, leur permettant de survivre plus longtemps dans des zones hostiles et de résister à de légers tirs extraterrestres ou militaires.

Bien que moins entraînés que les forces militaires, ils sont de fidèles défenseurs de Black Mesa et tiennent souvent tête aux créatures Xen, aux H.E.C.U. ou aux agents hostiles. Leur armure HEV bleue distincte les rend reconnaissables sur le terrain.

TIPS: 

- Ne comptez pas sur lui comme tank - il est courageux, mais humain.
- Écoutez attentivement leurs cris : ils pourraient indiquer un danger à proximité.
