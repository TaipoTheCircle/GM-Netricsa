DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils font preuve d'un enthousiasme obsessionnel et vous poursuivent avec des cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Zach Rand, known by the resonant nickname "Goldfinger", occupies a special place in the Croteam pantheon-a place cast in the gold of gratitude. His exaggerated cranium shines not so much with technical expertise as with the value of the support provided, the nature of which remains a mystery over the years. His name appears in the credits from the very first game in the series, The First Encounter, all the way through Classics: Révolution, signe d'une relation longue et significative pour le studio.

Peut-être a-t-il été l'un des premiers et des plus perspicaces testeurs, dont les conseils ont sauvé le projet d'erreurs fatales. Ou peut-être a-t-il apporté un soutien moral ou même matériel inestimable au cours des premières étapes, les plus difficiles. Le surnom de « Goldfinger » fait allusion à quelque chose d'exceptionnel, de « doré », qu'il s'agisse d'une compétence unique, d'une trouvaille rare ou simplement d'un cœur en or.

Il était autrefois vivant, mais après que quelqu'un l'ait tué, il est ressuscité d'entre les morts, devenant un zombie démoniaque.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", éliminez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
