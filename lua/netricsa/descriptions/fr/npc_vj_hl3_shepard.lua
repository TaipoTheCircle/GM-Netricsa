ADRIAN SHEPARD, CORPORAL, H.E.C.U.


CLASS: Adrian Shepard, caporal, H.E.C.U.
METABOLISM: Humain, mâle, combattant entraîné
PERCEPTION: Caporal, Unité des opérations spéciales du Corps des Marines des États-Unis (H.E.C.U.)
HEIGHT: 6,2 pieds
ENDURANCE: Haut
HOSTILITY: Moyen-élevé
WEAPONS: MP5, SMG1
REWARD: 800 FC
THREAT: Moyen-élevé

DESCRIPTION: 

Adrian Shepard est un caporal des Marines américains et membre de l'unité d'élite de combat pour environnements dangereux (H.E.C.U.), affecté au centre de recherche de Black Mesa, où un incident inattendu impliquant des extraterrestres et une brèche dans les barrières spatiales et énergétiques a éclaté.

Il fait partie d'une équipe d'atterrissage envoyée d'abord pour éliminer la menace extraterrestre, puis pour dissimuler l'incident et « neutraliser les témoins ». Cependant, après que son hélicoptère de transport ait été abattu, Shepard se retrouve séparé de son équipe et contraint de se battre pour survivre dans un chaos croissant.

He has standard Marine gear: un gilet pare-balles, un masque à gaz, des lunettes de vision nocturne et un H.E.C.U. arme, ce qui en fait un adversaire dangereux pour les forces spéciales de Black Mesa et de Black Ops, ainsi que pour d'autres forces qui émergent au cours des événements.

TIPS: 

- Son expérience du combat le rend dangereux à courte portée, mais sans équipement lourd, il est vulnérable.
