ROBOT CHASSEUR RC DU SERGENT STORMTROOPER


CLASSE : Robot chasseur RC du sergent Stormtrooper
MÉTABOLISME : Mécanique
PERCEPTION : Drone de Combat Tueur, le "Jouet" du Sergent
TAILLE: 5,2 pieds
ENDURANCE : Moyenne
HOSTILITÉ : Absolue
ARMES : Minigun intégré (à droite), fusil de chasse (à gauche), module de missile (sur la tête)
RÉCOMPENSE : 1200 FC
MENACE : Élevée

DESCRIPTIF :

C'est l'un des « jouets » que le sergent STORMTROOPER a personnellement conçu et mis en production. Ce drone de combat est conçu pour les missions les plus dangereuses : reconnaissance, suppression de points de tir, chasse de cibles uniques et soutien d'infanterie. Il n'est pas autonome, il est contrôlé à distance par le sergent, mais cela ne le rend pas moins dangereux.

Le noyau du robot est constitué de deux unités à chenilles vert foncé et noire. Les chenilles lui permettent de naviguer sur des terrains accidentés, en escaladant des rochers, des débris et même de petits murs. Un corps central est situé au-dessus des voies.

Deux bras manipulateurs articulés d'aspect mécanique s'étendent du corps. Le bras droit est équipé d'une mitrailleuse rotative et le gauche d'un fusil de chasse. Au-dessus de sa tête, un module sphérique avec un bord orange et un « œil » vert brillant porte un lance-missile. Le robot peut utiliser n'importe quel type d'arme en fonction de la situation : la mitrailleuse pour le tir de suppression, le fusil de chasse pour le combat rapproché et les missiles pour détruire les cibles blindées et les abris.

Le cou du robot est flexible, permettant au module de tête de tourner et de suivre des cibles sur une large plage.

CONSEILS :

- Le robot est mobile sur ses chenilles, mais pas très rapide. Utilisez un couvercle et flanquez-le.
- La mitrailleuse et le fusil de chasse ont une efficacité variable à différentes distances. Restez à moyenne portée pour minimiser les dégâts.
- Le module de tête est un point faible. Quelques coups bien placés et ce sera réduit à un tas de métal.
- N'essayez pas d'attaquer le robot de front. Son arme couvre une vaste zone.
- Le robot est le « jouet » du sergent. Si vous le voyez sur le champ de bataille, le sergent est à proximité. Soyez prêt à le rencontrer directement.