ONCLE PLUS RAPIDE DU FAVERSHOLM


CLASS: Oncle plus rapide
METABOLISM: Apparition de cauchemar
PERCEPTION: Tueur d'embuscade
SIZE: 5,6 pieds
ENDURANCE: Moyenne
HOSTILITY: Absolu
WEAPONS: Lames prothétiques pour bras et jambes
REWARD: 260 FC
THREAT: Haut

DESCRIPTION: 

C'est l'un des monstres les plus frappants et les plus effrayants rencontrés pour la première fois dans un immeuble résidentiel.

He is a disfigured man with cleaner, smoother spikes. His head is obscured by bars extending from his shoulders, forming a protective cage-a headshot is impossible. He lacks conventional arms and legs: au lieu de cela, il a de longues pointes métalliques pointues. Il utilise ces lames pour trancher et poignarder ses victimes. Parce que les jambes ont des appendices similaires, un bruit métallique distinctif se fait entendre lors de la marche.

Le Quick One préfère opérer en embuscade. Il se cache dans les coins sombres ou derrière des obstacles, pour ensuite bondir, enfoncer la porte et attaquer. Lorsqu’il attaque, il crie fort pour tenter d’intimider. Il court plutôt lentement, mais la soudaineté et les cris confondent l'ennemi.

Une fois tué, le Quick One ne tombe pas simplement. Il s'empale le cou avec des lames, émettant un cri douloureux et lugubre, et tombe au sol.

Comme la plupart des monstres, le Quick One symbolise la blessure. Les pointes métalliques à la place des membres représentent un fauteuil roulant en métal. La tête en cage reflète un esprit « enfermé » dans la folie et les problèmes émotionnels.

Il représente des personnes de l’environnement qui ont été source d’irritation et de douleur. L'acte de suicide du monstre après sa mort est un parallèle direct avec les tendances suicidaires.

TIPS: 

- Soyez prudent dans les longs couloirs comportant de nombreuses portes et des coins sombres. Le Quick One aime sauter soudainement.
- Le monstre court assez lentement, mais les cris et la soudaineté facilitent la panique. Ne pas paniquer.
- Frapper le Quick One est difficile en raison de sa vitesse. La meilleure tactique consiste à attendre qu'il essaie de frapper, puis à reculer et à commencer à tirer avec précaution.
- You can try hand-to-hand combat: Courez, frappez et fuyez. Mais c'est risqué.
- Si vous entendez un bruit métallique et un cri perçant, c'est à proximité. Préparez votre arme et regardez autour de vous.
