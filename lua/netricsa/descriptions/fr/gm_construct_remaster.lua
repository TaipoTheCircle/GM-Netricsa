CONSTRUCTION REMASTER, INFINI


Sam, tu te souviens de ce chantier de construction où nous avons commencé ? Vide, en béton, avec quelques hangars et un chantier de construction sans fin qui ne finirait jamais ? Eh bien, quelqu'un a décidé que ce n'était pas suffisant. Et j'ai fait un remaster.

Not a rebuild. Not a completion. A remaster, exactly. Like the old games: les mêmes emplacements, mais plus de textures, moins de bugs et quelques fonctionnalités supplémentaires pour divertir les vétérans.

Le chantier n'est toujours pas terminé. Mais maintenant, il y a des améliorations. Des sérieux. Et des étranges. Et comme d'habitude, avec un piège.

Quoi de neuf?

Le hangar a une porte.

Oui, Sam. Ce même trou dans lequel nous nous trouvions autrefois en attendant la fin des bombardements est en train de se refermer. Et peut-être que le vent ne souffle pas.

Une pièce sombre – la lumière et le bannissement d’une légende.

Vous souvenez-vous de cette pièce où il faisait toujours sombre et où nous avions peur que Celui dont on ne doit pas prononcer le nom saute du coin ? Eh bien, il y a un interrupteur là-bas maintenant. Vous le retournez et il n'y a pas d'obscurité. Et la rumeur veut que le Meat Man en ait été banni. Ne demandez pas qui c'est. Il n'y a rien à demander. Le principal c'est qu'il n'est plus là. Ou peut-être qu'il a simplement appris à mieux se cacher.

La Chambre Blanche – risque de cécité.

Il y a un avertissement affiché à l'entrée. Il est conseillé à toute personne entrant de porter des lunettes. Parce que la lumière y est si brillante, vous pourriez vous retrouver les yeux fermés pour toujours. Je ne sais pas qui a créé une pièce qui pourrait vous aveugler, ni pourquoi. Mais Sam, nous serions entrés.

Extincteurs.

Partout. Sur les murs, dans les coins, sur les tables. Apparemment, quelqu’un s’est rendu compte qu’ils étaient indispensables sur un chantier de construction. Ou peut-être qu’ils se sont simplement souvenus des précautions de sécurité. Quoi qu’il en soit, si quelque chose prend feu, vous n’aurez pas besoin de l’éteindre avec vos pieds.

Regards et trappes.

Oui.

Parking.

Enfin, vous pouvez garer votre voiture. Pas dans un champ, pas dans un hangar, mais dans un vrai parking. Avec marquages. Beaucoup d'espaces. Mais pas de voitures. Mais nous pouvons apporter les nôtres.

Et bien plus encore.

C'est trop long à énumérer. Trop longtemps.

Take a look around the construction site again. A lot has changed. Some things have gotten better, some are stranger. Find the glasses in front of the white room. Turn on the light in the dark room. Look into the well. Park the car. And remember: les extincteurs ne sont pas des jouets. Jusqu'à ce qu'il y ait un incendie.

C'est parti pour une promenade autour du chantier rénové. N'oubliez pas vos lunettes. Sinon, vous deviendrez aveugle. Qui tirera alors sur les crabes ?
