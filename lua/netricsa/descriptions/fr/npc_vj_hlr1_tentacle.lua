TENTACULE DE XEN


CLASS: Tentacule
METABOLISM: xénofaune carnivore
PERCEPTION: Tentacule de l'énorme créature
SIZE: 30-40 pieds (sort de la fosse)
ENDURANCE: extrêmement élevé
HOSTILITY: extrême
WEAPONS: détection sonore, frappes dévastatrices
REWARD: 1200FC
THREAT: catastrophique (dans des zones confinées)

DESCRIPTION: 

Le Tentacule est un énorme prédateur souterrain de Xen, rencontré dans les zones de cascade de résonance où il dépasse des fosses profondes. Aveugle, il s'appuie entièrement sur le son et les vibrations pour localiser ses proies. Tout bruit attirera instantanément son attention, suivi de coups fracassants provenant de ses appendices en forme de fouet.

Sa peau blindée le rend pratiquement invulnérable aux armes conventionnelles. Les victimes sont souvent jetées au sol avant même de se rendre compte qu’elles ont été détectées. La seule véritable façon de l’éliminer est de détruire son noyau, enfoui profondément sous la surface.

TIPS: 

- Restez silencieux - marcher, tirer ou même recharger trop fort vous fera tuer.
- Les armes explosives ou énergétiques sont inefficaces contre son blindage.
- La patience et la furtivité sont les clés de la survie.
