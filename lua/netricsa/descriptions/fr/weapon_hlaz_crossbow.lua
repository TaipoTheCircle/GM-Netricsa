ARBALÈTE


WEAPON: Arbalète
AMMO: Boulons en acier
ROF: Action de boulon, ~15 boulons/min

DESCRIPTION: 

Arme de précision à longue portée équipée d'une lunette. Tire des boulons en acier silencieusement, ce qui le rend efficace pour éliminer des cibles à distance avec une grande précision.

NOTES: 

- Précision mortelle à longue portée.
- Rechargement lent entre les tirs.
- Les boulons explosifs causent des dégâts de zone.
