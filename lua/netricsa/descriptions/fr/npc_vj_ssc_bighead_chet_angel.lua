PERCEPTION: Fanatique enthousiaste
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Neutre
WEAPONS: Aucun (sauf en difficulté I Am Ugh-Zan)
REWARD: 100 FC
THREAT: Ennui

DESCRIPTION: 

Les Big-Heads sont des humanoïdes maigres avec des crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Chet Falisek est le co-fondateur du site satirique Old Man Murray (OMM), qu'il a co-créé avec Eric. Ensemble, ils donnent le ton du journalisme de jeu avec leur langage pointu, cynique et incroyablement drôle. Chet fait partie d'un duo que Croteam jugeait digne d'immortalité en tant que fans secrets aux crânes gonflés. Même si Eric était souvent en première ligne, c'est leur collaboration qui a créé cette voix unique qui a tant amusé et inspiré les développeurs. Sa présence dans le jeu est un hommage au journaliste.

Lorsque vous l'avez tué dans les Cours Sacrées, il s'est transformé en ange.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager un chemin. - En difficulté "I Am Ugh-Zan", détruisez-les à distance grâce à leurs modificateurs.
- En groupe, une masse à frappe horizontale est efficace.
