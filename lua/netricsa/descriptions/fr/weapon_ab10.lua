AB-10 SELF-LOADING PISTOL


WEAPON:      AB-10
AMMUNITION: Cartouches 9x19
ROF: ~20 tours/min

DESCRIPTION:

Un pistolet semi-automatique fabriqué aux États-Unis, une modification du pistolet Intratec TEC-9. Sorti en 1995.

NOTES:

- Comme le TEC9, le pistolet était populaire auprès des gangsters, mais n'a pas atteint la même popularité dans la culture pop.






