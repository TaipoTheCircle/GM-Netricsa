H.E.C.U. VCI M2A3 BRADLEY


CLASS: H.E.C.U. VCI M2A3 Bradley
METABOLISM: Mécanique, piloté par l'homme
PERCEPTION: Véhicule de combat d'infanterie
SIZE: ~ 32 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Mitrailleuse principale, lance-roquettes, trappe de déploiement des troupes arrière
REWARD: 7000FC
THREAT: Catastrophique

DESCRIPTION: 

Le M2A3 Bradley est un véhicule de combat blindé polyvalent déployé par H.E.C.U. forces. Son armement principal est une mitrailleuse de gros calibre, capable de supprimer efficacement l'infanterie. De plus, le Bradley est équipé d'un lance-roquettes pour affronter les véhicules, les structures ou les forces ennemies concentrées.

A unique feature of the Bradley is its rear troop deployment hatch: il peut transporter des escouades de marines ou du personnel des Black Ops et les déployer directement dans les zones de combat, créant ainsi des menaces à plusieurs niveaux. Sa combinaison de puissance de feu, de mobilité et de transport de troupes en fait à la fois un véhicule d'assaut de première ligne et un multiplicateur de force.

TIPS: 

- Mettez-vous à couvert pour éviter les tirs de mitrailleuses et les roquettes.
- Détruire le Bradley empêche rapidement le déploiement des troupes.
- Le flanc ou les explosifs sont indispensables pour endommager le châssis blindé.
- Surveillez l'infanterie sortant du hayon arrière : même si le véhicule est détruit, les troupes déployées restent dangereuses.
