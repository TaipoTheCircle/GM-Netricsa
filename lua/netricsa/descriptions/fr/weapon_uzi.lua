PISTOLET-MACHINE IMI UZI


WEAPON: IMI UZI
AMMUNITION: Cartouches Parabellum 9 × 19 mm
ROF: ~600 tours/min

DESCRIPTION: 

Une mitraillette compacte à chargement automatique/automatique avec un boulon télescopique et un simple mécanisme de recul à inertie (mécanisme de tir à boulon ouvert dans les premières versions). Développé par Uzi Gal en Israël à la fin des années 40 et dans les années 50. Le chargeur est situé dans la poignée du pistolet, ce qui rend l'arme compacte et pratique pour un transport et un contrôle dissimulés lors du tir à une seule main. Il présente une conception simple, une grande fiabilité dans des conditions difficiles et une facilité d'entretien. Il a été largement utilisé par l'armée, la police et les forces de sécurité et a été produit en plusieurs versions (Standard, Mini, Micro) et modifications pour d'autres calibres.

NOTES: 

- La compacité et la cadence de tir cyclique élevée sont compensées par une précision relativement faible à des distances supérieures à 50-100 m.
- Simple à fabriquer et à entretenir, populaire au 20ème siècle ; des versions civiles et d'exportation avec des restrictions de mode de tir existent.


