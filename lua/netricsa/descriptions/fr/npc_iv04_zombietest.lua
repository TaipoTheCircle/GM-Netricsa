CRABE ZOMBIE COMMUN


CLASSE : Zombie crabe, commun
MÉTABOLISME : hôte humain + crabe commun parasitaire
PERCEPTION : Entité zombifiée
TAILLE : ~ 6 pieds de haut
ENDURANCE : moyenne
HOSTILITÉ : élevée (lente mais implacable)
ARMES : attaques au corps à corps (griffes, balançoires), grappin
RÉCOMPENSE : 200 FC
MENACE : modérée individuellement, élevée en groupe

DESCRIPTIF :

Le Zombie Headcrab commun est une fusion tragique d’un hôte humain et d’un Headcrab commun. Contrairement aux variantes terrestres, le crabe commun s'enfouit de manière permanente dans le crâne de l'hôte, laissant le crâne de l'humain visible sous sa carapace extraterrestre. Une fois infecté, l’hôte perd tout libre arbitre et devient un prédateur lent et implacable.

Ces zombies Headcrab sont encombrants et faciles à éviter à distance, mais ils sont implacables et poursuivront leurs proies jusqu'à ce qu'elles soient suffisamment proches pour les frapper. Le lien permanent du Headcrab fait de cette créature un horrible rappel de l'ingéniosité parasitaire de Xen - pas de pitié, pas d'évasion, juste un conduit ambulant et gémissant pour l'agression extraterrestre.

CONSEILS :

- Gardez vos distances ; les attaques de mêlée sont dangereuses si elles se connectent.
- Les armes à feu fonctionnent bien en raison de leur mouvement lent.
- Ne sous-estimez pas les groupes : bien qu'ils soient individuellement lents, ils peuvent facilement coincer leurs proies.