RPG


WEAPON: RPG (lance-grenades propulsé par fusée)
AMMO: Roquettes hautement explosives
ROF: ~4 fusées/min

DESCRIPTION: 

Système d'arme anti-blindage lourd avec ciblage guidé par laser. Délivre une puissance destructrice extrême, capable de détruire des véhicules, des tourelles et de grands organismes hostiles.

NOTES: 

- Rayon d'explosion énorme, mortel pour les groupes.
- Le guidage laser assure des frappes précises.
- Dangereux d'incendie dans les espaces confinés.
