GÉNÉRATEUR


CLASS: Générateur
METABOLISM: Véhicule contrôlé par l'IA
PERCEPTION: IA primitive
SIZE: 24 pieds
ENDURANCE: Très élevé
HOSTILITY: Très élevé
WEAPONS: Bombes à plasma
REWARD: 500FC
THREAT: Extrême

DESCRIPTION: 

Il s'agit d'un Rollerball modifié, conçu pour se téléporter et créer des ennemis faibles.

Contrairement aux autres drones mentaux, les Spawners sont des bastions lourds et lents dotés d'une immense puissance de feu. Il s’agit de l’un des véhicules les plus blindés et les plus lourdement armés.

Ils utilisent un canon à plasma pour tirer des bombes à plasma à longue portée sur leur cible. Ces bombes, tout en causant des dégâts de zone minimes, infligent des dégâts d'impact massifs à tout ce qu'elles touchent. Cela les rend excellents pour détruire des cibles stratégiques telles que les installations de communication, les dépôts de ravitaillement, les dépôts de munitions et les cuirassés. C'est l'une des principales raisons pour lesquelles les forces de Mental détruisent nos bases coloniales.

Utilisez une puissance de feu puissante pour le détruire rapidement et essayez de ne pas vous faire toucher. Si les structures et les grands navires ne peuvent pas résister à de nombreux coups, alors vous non plus.

TIPS: 

- Utilisez un canon ou des explosifs pour le détruire rapidement.
- En raison de son immense puissance de feu, il devrait être une cible prioritaire.
- Évitez ses attaques. Je sais que vous entendez souvent cela, mais sérieusement, ne vous faites pas frapper cette fois.
