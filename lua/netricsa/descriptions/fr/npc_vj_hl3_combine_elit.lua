COMBINEZ LA GARDE ARCTIQUE D'OVERWATCH ELITE


CLASS: Combinez Overwatch Elite Arctic Guard
METABOLISM: Humain augmenté, Homme
PERCEPTION: Infanterie transhumaine, unité d'élite de sécurité et d'assaut
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2 V2, boules d'énergie
REWARD: 900 FC
THREAT: Très élevé

DESCRIPTION: 

L'Overwatch Combine Elite Arctic Guard est une variante spécialisée de l'infanterie d'élite conçue pour protéger les actifs stratégiques, le personnel clé et les zones critiques dans des climats extrêmes. Contrairement aux commandos, les gardes sont moins susceptibles de s'engager dans des avancées rapides, mais sont presque aussi efficaces au combat.

They are easily recognizable by their dazzling white armor and single red visor. A cloak is optional: certains gardes le portent sur leurs épaules en signe de statut ou d'affiliation à une unité spécifique, tandis que d'autres y renoncent complètement pour une plus grande mobilité et une visibilité réduite en cas de vents violents et de tempêtes de neige.

Comme les autres soldats d’élite d’Overwatch, leur armure est équipée d’un système de survie intégré. Un respirateur est relié à un tube épais passant sur l'épaule gauche et relié à un sac à dos à oxygène sur le dos portant le logo Combine. Le système assure une respiration stable, filtre les toxines et protège les gardes des températures extrêmes, leur permettant de maintenir des positions dans les zones arctiques pendant des heures sans soutien extérieur.

TIPS: 

- L'absence de cape n'indique pas un rang inférieur ou une menace.
- Ils opèrent presque toujours en binôme ou au sein d'une équipe de sécurité périmétrique.
