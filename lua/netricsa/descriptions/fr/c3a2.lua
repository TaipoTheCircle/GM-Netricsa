RÉACTEUR NOIR MESA LAMBDA, CALME AVANT LA PROCHAINE TEMPÊTE


Le couloir s'ouvre sur une petite salle de contrôle. À l’intérieur, un scientifique solitaire se tient debout, pâle, nerveux et refusant absolument de jouer au héros. Il n'ouvrira pas la porte tant qu'il ne sera pas certain que la zone immédiate est dégagée - pas "pour la plupart morte", ni "probablement morte", mais définitivement morte. La paranoïa est un excellent trait de survie dans cet endroit, et il en a à revendre.

Ils veulent remettre le prototype de Gluon Cannon - une méchante petite réponse à de très gros problèmes. C’est dangereux, expérimental et c’est précisément le genre de chose que vous souhaitez lorsque la prochaine vague n’est pas simplement une autre série de grognements.

Mission snapshot: 

Objective: dégagez les couloirs immédiats et confirmez « aucune signature hostile » au scientifique.

Reward: accès au canon à gluons et entrée complète dans la zone du cœur du réacteur.
