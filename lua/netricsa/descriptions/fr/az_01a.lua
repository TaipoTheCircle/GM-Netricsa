BLACK MESA, SECTEUR ÉCHO


Vous avez plongé dans leur version improvisée de la Clé Sirienne de l'Éternité. Pendant un instant, cela a fonctionné : vous vous trouviez à nouveau à l'intérieur de Xen, sentant son air en apesanteur, entendant son bourdonnement sans fin. Mais avant que vous puissiez agir, les Vortigaunts sont intervenus. Ils vous ont ramené, vous jetant hors du portail et dans Black Mesa une fois de plus.

Au même moment, dans une autre aile, le spectromètre anti-masse s'effondre sous les soins de « l'autre Sam ». La cascade de résonance a traversé l'installation et votre expérience a également volé en éclats. Le prototype Time-Lock a explosé – instable, cassé, effacé. Un grognement extraterrestre a réussi à s'introduire avant l'explosion... pour ensuite être déchiré avec la machine elle-même.

Maintenant, tout s’écroule. Le spectromètre est détruit. Le Time-Lock détruit. L’ensemble de l’installation est en train de craquer. Quelque part là-bas, l’autre vous est déjà sur le chemin vers Nihilanth.

Ce qui signifie que vous avez une fenêtre étroite. Avancez tant que vous le pouvez encore.

Bonne nouvelle : vous n’êtes pas obligé de sauver le monde deux fois. Mauvaise nouvelle : vous devez encore vous sauver.
