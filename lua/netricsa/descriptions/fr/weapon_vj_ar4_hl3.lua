FUSIL DE SNIPER OVERWATCH MK1


WEAPON: Fusil de précision Overwatch MK1
AMMO: Combinez des obus de tireur d'élite à grande vitesse
ROF: Semi-automatique, ~30 coups/min

DESCRIPTION: 

Fusil de précision prototype développé pour les tireurs d'élite d'élite du Combine au début de l'occupation. Équipé d'une lunette de précision, il tire des obus à hyper-vitesse capables de neutraliser instantanément des cibles légèrement blindées ou non blindées. Contrairement au MK2 ultérieur, cette version avait une conception plus expérimentale.

NOTES: 

- Extrêmement mortel à longue portée ; souvent mortel d'un seul coup.
- Le tir semi-automatique permet des tirs de suivi plus rapides que le MK2.
- Rare et réservé aux unités de tireurs d'élite d'élite.
- Représente la transition vers le Overwatch Sniper Rifle MK2 finalisé.
