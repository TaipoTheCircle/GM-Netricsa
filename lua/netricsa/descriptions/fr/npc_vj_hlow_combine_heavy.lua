COMBINEZ LE GUNNER DE PRISON LOURDE D'OVERWATCH


CLASS: Combinez Overwatch Heavy Prison Gunner
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner une unité de tir suppressif
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Combiner Minigun
REWARD: 1200FC
THREAT: Critique

DESCRIPTION: 

Le Combine Overwatch Heavy Prison Gunner est un point de tir ambulant, construit à partir de la base du personnel de Nova Prospekt, mais conçu pour bien plus que de simples murs de prison. Son blindage est multicouche, lesté et rembourré d'amortisseurs, lui permettant de tirer en continu sans perdre en stabilité.

Outwardly, he resembles a standard Nova Prospekt prison guard: visières jaunes, armure distinctive. Cependant, il est immédiatement trahi par de nombreux éléments d'identification jaunes, avertissant les alliés qu'il ne s'agit pas d'un garde, mais d'un dispositif de suppression.

Ces soldats sont utilisés pour réprimer les émeutes, protéger les points stratégiques, escorter les convois et attaquer les positions fortifiées.

TIPS: 

- N'entrez jamais dans une ligne de tir ouverte - le minigun est impitoyable.
- Utiliser des explosifs ou des armes lourdes.
