Y.S.M. MARIN


CLASS: Y.S.M. Marin
METABOLISM: Humain, mâle
PERCEPTION: Yellow Shield PMC, maraudeur contractuel
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONATORY: Carabine M4A1, MP7, fusil de chasse SPAS-12
REWARD: 500FC
THREAT: Haut

DESCRIPTION: 

Il s'agit d'un membre d'une société militaire privée qui fonctionne sur le principe « Entendez un appel de détresse, récupérez les sacs de butin ». Ces mercenaires ont intercepté le signal de la base Delta et sont arrivés sur les lieux non pas pour sauver, mais pour piller.

Their appearance is typical of mercenaries of the late 1990s: gilets tactiques, gilets, bandanas, lunettes et autres gadgets. Leur palette de couleurs est gris foncé avec des accents jaunes (d'où son nom). Sur leurs épaules et leurs bérets se trouvent des écussons avec un bouclier et l'abréviation Y.S.M. Ils sont armés d'armes automatiques : MP7, M4A1 et parfois des fusils de chasse SPAS-12.

Their mission is to remove anything unsafe from the base: du matériel expérimental, des données de recherche, des échantillons de matériaux extraterrestres, des armes, des médicaments et même d'étranges champignons qui ont poussé après une cascade de résonance. Tout cela sera ensuite vendu sur le marché noir à des sociétés privées, à des gouvernements, à d’autres SMP ou à des syndicats criminels.

Y.S.M. a également été embauché spécifiquement pour saisir et détruire certaines données et appareils liés à la téléportation. Et créer un nouveau type d’armée privée, utilisant la technologie capturée pour simplement faire chanter le gouvernement et les entreprises, en utilisant des documents classifiés.

TIPS: 

- Les Marines ont une armure. Un pistolet ordinaire pourrait ne pas les pénétrer du premier coup. Visez la tête.
- Ils utilisent la couverture et les sprints. Ne tombez pas sur des balles à l'air libre.
- Les grenades et les explosifs sont efficaces contre les groupes.
- Un tir dans la tête le fera exploser.
- N'essayez pas de négocier. Ils ne se soucient pas de votre histoire. Ils veulent vos affaires et votre mort si vous les gênez.
