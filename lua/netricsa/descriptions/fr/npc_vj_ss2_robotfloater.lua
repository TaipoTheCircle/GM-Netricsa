DRONE FLOTTEUR DU SIRIUS


CLASS: Drone flottant
METABOLISM: Avion contrôlé par Al
PERCEPTION: Al primitif
SIZE: 2 pieds
ENDURANCE: Très faible
HOSTILITY: Moyen
WEAPONS: Pistolet laser
REWARD: 50 FC
THREAT: Faible

DESCRIPTION: 

Ce sont des unités de reconnaissance légères et volantes conçues pour le repérage, bien qu'elles soient également largement utilisées par la police syrienne, distribuant généralement des contraventions pour excès de vitesse aux conducteurs et pilotes imprudents.

Les flotteurs tourneront autour des ennemis et leur tireront dessus avec un pistolet laser à faible rendement. L'un d'eux représente une menace minime, cependant, ils transmettront un appel de détresse à tous les flotteurs à proximité, ainsi qu'au QG local du Commandement des Drones.

Si vous êtes repéré par un Floater, ne soyez pas surpris si vous voyez le gros des forces de Mental venir renforcer la zone.

TIPS: 

- Ils peuvent être facilement éliminés avec la plupart des armes, mais celles à tir rapide et à dégâts de zone fonctionnent mieux.
- Ils devraient être des cibles de faible priorité parmi les autres ennemis, tant que vous êtes en mouvement et que vous esquivez leurs lasers.
