BUZZER D’ESSAI


CLASSE : Buzzer d'essaim
MÉTABOLISME : Xénobiologique
PERCEPTION : Prédateur volant
TAILLE : 1,6 pi
ENDURANCE : Très faible
HOSTILITÉ : Élevée
ARME : Dard venimeux, attaque aérienne
RÉCOMPENSE : 75 FC
MENACE : Faible

DESCRIPTIF :

Les buzzers sont de petits organismes volants d’essaims adaptés pour traquer et harceler des adversaires plus grands. Individuellement, ils constituent rarement une menace sérieuse, mais leur apparition signifie généralement le début de nouveaux problèmes.

Leur principale méthode de chasse consiste à attaquer rapidement par le haut. Grâce à sa grande maniabilité, le Buzzer plonge brusquement vers sa cible, frappant avec son dard venimeux, puis remonte. Leur petite taille en fait une cible difficile, même pour les tireurs expérimentés.

Le venin du buzzer n'est pas particulièrement mortel, mais il peut provoquer de la douleur, une désorientation et une diminution de la concentration. C'est pourquoi ces créatures sont particulièrement dangereuses lors des rencontres avec d'autres membres de l'Essaim. Pendant que la proie est distraite par le ravageur volant, les plus gros prédateurs ont la possibilité de se rapprocher.

Les observations montrent que les Buzzers font rarement preuve de prudence et attaquent souvent des cibles beaucoup plus grandes qu'eux. Apparemment, l’évolution a compensé leur manque d’instinct de conservation par la capacité de se reproduire rapidement.

CONSEILS :

- N'ignorez pas les Buzzers, même lorsque vous combattez des adversaires plus dangereux.
- Utilisez des armes à cadence de tir élevée ou à visée automatique.
- Surveillez l'air au-dessus de vous.
- Si tu entends un bourdonnement gênant autour de toi, cela signifie que quelqu'un est déjà déterminé à gâcher ta journée.