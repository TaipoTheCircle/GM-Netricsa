GOLEM DE NEIGE DU MONDE MINECRAFT


CLASS: Golem des neiges
PERCEPTION: Gardien/défenseur contre les foules hostiles
METABOLISM: Citrouille des neiges
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Faible
WEAPONS: Boules de neige
REWARD: 100 FC
THREAT: Faible

DESCRIPTION: 

Le Golem des neiges est une foule passive construite créée par un joueur ou un dispensaire en plaçant deux blocs de neige surmontés d'une citrouille sculptée. Il lance des boules de neige sur les foules hostiles à proximité, les attirant et les repoussant, mais les boules de neige infligent rarement des dégâts directs, à l'exception des Blazes.

Le Golem des neiges n'attaquera pas ses alliés, mais il réagit calmement à l'agression d'autres créatures, les poussant avec des boules de neige jusqu'à 10 pâtés de maisons. Lors de son déplacement, il laisse une traînée de couches de neige si le climat et les règles mondiales le permettent. Il est également vulnérable dans les biomes humides ou chauds : l’eau, la pluie et les températures élevées l’endommageront ou le tueront lentement.

Si vous coupez sa « tête » de citrouille, il laissera tomber la citrouille, révélant son visage en dessous.

TIPS: 

- Le Golem des Neiges est excellent comme garde distrayant, surtout lorsqu'il est combiné avec les Golems de Fer.
- La neige qu'elle laisse derrière elle peut être utilisée pour créer un parc à neige.
- Gardez à l'esprit qu'il fond dans les biomes chauds, ce qui le rend moins adapté à la défense.
