A CONSTRUCT OF THE FLAT GRASS, A BEAUTIFUL PLACE


Devant vous, il n’y a pas que de l’herbe plate. C'est un plan si infini que l'herbe elle-même doute de sa propre existence, aussi large que l'esprit d'un crabe avant de bondir.

À l'horizon, au-dessus du sol, le panneau « FlattyWood » brille - en grandes lettres confiantes, comme si quelqu'un avait construit ici une cérémonie de remise de prix hollywoodienne pour la meilleure scène.

Quelques immeubles de grande hauteur et des constructions en béton, vides et gris, comme les décors oubliés d'un sombre film d'art et d'essai ;

Un étang que vous regardez et pensez reflète simplement votre fatigue causée par des batailles constantes...