BLACK MESA DEVTEST, RÉACTEUR


Cette carte de test projette le joueur directement dans la chambre du cœur du réacteur. À travers une grande fenêtre d'observation, on peut voir l'homme mystérieux à la mallette observant tranquillement le chaos, debout à côté d'un Vortigaunt, comme si aucun d'eux ne remarquait la bataille en cours.

A l’intérieur, c’est beaucoup moins calme. Les Houndeyes envahissent les couloirs.

Et juste au moment où vous pensez qu'il ne s'agit que de créatures Xen... l'armée agit. H.E.C.U. des soldats attendent dans l'une des pièces, prêts à vous tendre une embuscade et à vous capturer.
