VELOCIRAPTOR VULGARIS DU M'DIGBO


CLASS: Vélociraptor vulgaris
METABOLISM: Reptile
PERCEPTION: Monture / Compagnon de combat
SIZE: 5 pieds
ENDURANCE: Très faible
HOSTILITY: Varie selon le cavalier
WEAPONS: Boules de feu
REWARD: 200FC
THREAT: Haut

DESCRIPTION: 

Ce lézard extraterrestre vert semble tout droit sorti du film Jurassic Park de 1993, sans les plumes. Il vit sur la planète M'Digbo et, contrairement à ses frères terrestres, peut cracher du feu.

The Velociraptor's appearance resembles a classic pop culture dromaeosaurid: des écailles vertes, des griffes acérées, une longue queue et une bouche pleine de petites dents. Pas de plumes, pas d'intelligence de meute, juste une fureur pure et pure, exploitée et dirigée contre les ennemis de Mental.

Au combat, le Vélociraptor est indispensable. Il court vite, évite les attaques ennemies et tire des boules de feu depuis sa bouche. Une de ces boules de feu tue un ennemi faible en un seul coup, et les plus forts après quelques secondes de tir continu. Le principal problème est sa santé. Le Vélociraptor est aussi fragile que le verre. Quelques coups et le lézard chevaucheur est parti.

TIPS: 

- Pendant que vous roulez, gardez vos distances et tirez des volées de boules de feu sur les ennemis.
- Sa santé est faible. Si vous voyez quelqu'un vous tirer dessus, esquivez. Un coup peut envoyer un lézard dans l'autre monde.
- N'essayez pas de tuer des ennemis de près avec un lézard ordinaire, il mourra plus vite que vous ne pourrez infliger de dégâts.
