AKS-74U


WEAPON: AKS-74U
AMMUNITION: Fusil 5,45x39, lance-grenades sous canon de 40 mm.
ROF: 700 tours/min

DESCRIPTION: 

Le fusil d'assaut pliable Kalachnikov de 1974, version raccourcie - une version raccourcie du fusil d'assaut AKS-74 - a été développé à la fin des années 1970 et au début des années 1980 pour armer les équipages de véhicules et d'avions de combat des forces armées de l'URSS. Il a été utilisé en Afghanistan et en Tchétchénie et est maintenant utilisé dans le cadre de l'opération militaire spéciale. Il était également très populaire parmi les gangsters russes dans les années 1990. Il est également utilisé par les forces de l'ordre en URSS/RF et dans d'autres pays de la CEI.

NOTES: 

- Son design compact permet à cette arme d'être transportée inaperçue.
- La crosse peut être pliée pour la rendre encore plus courte.
- Très fiable au combat, il ne vous laissera pas tomber.
