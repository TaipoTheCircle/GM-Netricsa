POULE DE ZENOZOIK


CLASS: Poule
METABOLISM: Aviaire
PERCEPTION: Oiseau domestique/sauvage
SIZE: 1,5 pied
ENDURANCE: ~3,2 pieds
HOSTILITY: Quasiment absent
WEAPONS: Bec
REWARD: 2 FC
THREAT: Négligeablement faible

DESCRIPTION: 

Un oiseau sauvage commun dans les colonies et à la périphérie de Zenozoik. Contrairement aux corbeaux, les poulets se caractérisent par une intelligence extrêmement faible et une absence totale de capacité de combat. Leur comportement est caractérisé par une fuite chaotique face à toute menace. Ils sont célèbres pour être la cible d'un acte de force brute particulier dans le monde de Zenozoik : leurs têtes sont souvent arrachées. Ils ne se rassemblent pas en groupes agressifs, mais peuvent créer la panique en se mettant accidentellement en travers de leur chemin.

TIPS: 

- Les attaquer est inutile et peut attirer une attention inutile.
- Complètement sans défense. Ils ne représentent aucune menace, mais ils peuvent accidentellement provoquer l'agression d'autres créatures s'ils sont chassés.
- Il y a des rumeurs selon lesquelles des poulets auraient pris feu.
