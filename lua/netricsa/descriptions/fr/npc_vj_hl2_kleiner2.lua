DR. ISAAC KLEINER


CLASS: Dr Isaac Kleiner
METABOLISM: Humain, mâle
PERCEPTION: Scientifique humain/chercheur en résistance
SIZE: ~5,9 pieds
ENDURANCE: Faible (physiquement fragile, non combattant)
HOSTILITY: Faible (amical, défensif uniquement)
WEAPONS: Connaissances scientifiques, technologie de téléportation, fusil de chasse SPAS-12 occasionnel
REWARD: 1000FC
THREAT: Faible (directement), Élevé (indirectement via les inventions)

DESCRIPTION: 

Le Dr Isaac Kleiner est l'un des derniers scientifiques survivants de Black Mesa et une pierre angulaire des efforts scientifiques de la Résistance. Physicien excentrique mais brillant, Kleiner se consacre au développement de la technologie de téléportation et à la réutilisation des machines Combine au profit de l'humanité. Sa plus grande contribution est son laboratoire dans la Cité 17, qui sert de plaque tournante vitale pour les opérations de la Résistance.

Kleiner est distrait et souvent dispersé, enclin à divaguer sur des théories ou à se laisser distraire par son crabe de compagnie, Lamarr, qu'il garde affectueusement malgré sa tendance à semer le chaos. Malgré sa maladresse, l'esprit de Kleiner est extrêmement vif et ses inventions jouent un rôle crucial en donnant à la Résistance un avantage technologique.

Bien que physiquement fragile et inapte au combat, le courage de Kleiner réside dans sa détermination inébranlable à résister au Combine avec l'intellect plutôt que la force brute. Son mentorat auprès de Gordon Freeman reflète à la fois son admiration pour les compétences de Gordon et sa confiance en lui pour mettre en œuvre les plans les plus audacieux de la Résistance.

TIPS: 

- Protégez Kleiner à tout prix : ses inventions sont l'épine dorsale de la technologie de la Résistance.
- Ne laissez pas Lamarr errer librement ; le chaos a tendance à suivre.
- Bien qu'inoffensives, ses excentricités sont vitales pour le moral dans les moments sombres.
- Ne veut pas publier de mèmes sur le serveur Discord, donc il est dans la maison de fous.
