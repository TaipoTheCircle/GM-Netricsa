SAUVAGE DUBINOV DE M'DIGBO


CLASS: Sauvage Dubinov
METABOLISM: Mammifère
PERCEPTION: Sauvage amical, renégat
SIZE: 6,8 pieds
ENDURANCE: Moyenne
HOSTILITY: Moyenne
WEAPONS: Club à pointes, jumelles en os faites maison, compétences de chasse
REWARD: 400FC
THREAT: Faible

DESCRIPTION: 

Un représentant unique des Primitifs de la planète M'Digbo. Tandis que ses compatriotes, dirigés par la mystérieuse « Maman », concluaient un pacte avec Mental, acceptant de devenir ses soldats en échange d'une quantité inépuisable de lanières en peau de léopard, Dubinov choisit une voie différente. Il a volontairement renoncé aux normes sociales agressives de son peuple, faisant de lui un sauvage étonnamment amical.

Dubinov dépasse largement le Primitif moyen en termes de développement intellectuel et social. Il possède des jumelles artisanales uniques, habilement fabriquées à partir d'os d'ennemis et d'animaux tombés au combat. Contrairement à ses congénères, il préfère chasser le sanglier. Il vit sous les tropiques de la planète avec sa femme, menant une vie paisible, selon des normes sauvages.

Dubinov's morning begins in his cave, located in the heart of the tropics. First, he leaves his dwelling and heads to the nearest mountain stream. This ritual is unchangeable: il boit de l'eau propre et se lave soigneusement, se préparant pour une nouvelle journée de chasse et observant l'horizon à travers ses jumelles en os.

TIPS: 

- Dubinov n'est pas un ennemi. Ne l'attaquez pas.
- Si vous le rencontrez sous les tropiques, vous pourrez parler. Il vous racontera beaucoup de choses intéressantes sur la planète M'Digbo et ses habitants.
- Ses jumelles ne sont pas qu'un jouet. Dubinov peut repérer le danger bien avant qu'il ne s'approche.
- Si tu l'aides à chasser, il partagera de la viande et peut-être même t'apprendra quelques astuces simples.
- N'essayez pas de lui voler ses jumelles. C'est impoli et Dubinov est très attaché à ses biens.
- Pour la femme de Dubinov, c'est une autre histoire. Il y a des rumeurs à son sujet, mais il ne la montre jamais à personne.
