PLANÈTE DES TROIS CAPITAINES


PLANET: Planète des Trois Capitaines
TYPE: Planète terrestre, Memorial World
CLIMATE: Maintenu artificiellement, respirant
MOONS: Inconnu
TERRAIN: Rocheux à l'origine sans vie ; équipé de générateurs d'atmosphère
LOCATION: Système stellaire inconnu dans la Voie Lactée
NATIVE RACES: Aucun (planète touristique)
ALLIANCE: Complexe commémoratif intergalactique
LOCATIONS: Statue de soixante mètres des Trois Capitaines, Musée de l'astronautique

DESCRIPTION: 

Un monde commémoratif unique nommé en l'honneur de trois héros spatiaux légendaires qui ont accompli de nombreux exploits et découvertes remarquables au cours de leurs voyages à travers la galaxie. Au départ, c'était une planète sans nom, inhabitée, totalement inhabitable en raison de l'absence d'atmosphère.

Pour perpétuer la mémoire de ces célèbres capitaines, la planète a été spécialement équipée de puissants générateurs qui produisent de l'oxygène et créent des conditions respirables, la rendant accessible aux touristes de tous les coins de la galaxie.

L'attraction et le symbole principal de la planète est une statue de pierre colossale de soixante mètres de haut représentant les Trois Capitaines. Ce monument est visible de loin et sert de repère à tous ceux qui arrivent sur la planète.

À côté de la statue se trouve le musée de l'astronautique, une collection unique d'objets, de documents et de preuves de l'ère de la grande exploration spatiale. Le musée a été construit grâce à des dons volontaires collectés auprès de quatre-vingts planètes du système stellaire, ce qui en fait un véritable monument galactique.

KEY LOCATIONS: 

- The sixty-meter-tall statue of the Three Captains: Le monument principal de la planète, représentant trois héros spatiaux légendaires, sert de point de repère et de symbole pour la planète.
- Cosmonautics Museum: Une collection unique d'objets et de documents dédiés à l'ère des grandes découvertes spatiales. Construit grâce aux dons des habitants de quatre-vingts planètes.
