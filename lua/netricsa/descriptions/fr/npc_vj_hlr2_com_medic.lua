COMBINEZ LE SOLDAT MEDIC D'OVERWATCH


CLASS: Combiner Overwatch Soldier Medic
METABOLISM: Humain augmenté, mâle
PERCEPTION: Infanterie transhumaine, unité médicale de campagne
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Haut
WEAPONS: SMG1, Kit de santé médical
REWARD: 750 FC
THREAT: Moyen-élevé

DESCRIPTION: 

Le Combine Overwatch Soldier Medic est une unité de soutien spécialisée sur le champ de bataille, qui se distingue visuellement des soldats standards par son armure blanche avec des rayures médicales rouges sur les côtés des jambes, des lentilles de visière jaunes et l'inscription indubitable « MEDICAL ASSISTANCE » gravée sur son dos.

Contrairement à l'infanterie régulière, les médecins sont chargés de maintenir les escouades d'Overwatch en état de combat lors d'engagements prolongés. Équipés de bioscanners et d'injecteurs de guérison portables, ils peuvent rapidement stabiliser les soldats blessés, administrer des stimulants fabriqués par Combine et prolonger l'endurance au combat d'unités entières.

Bien qu'ils soient principalement orientés vers le soutien, ils restent lourdement armés et n'hésiteront pas à engager directement leurs ennemis s'ils sont menacés. Leur présence rend les escouades du Combine beaucoup plus difficiles à éliminer, obligeant souvent les résistants à des échanges de tirs interminables.

TIPS: 

- Ciblez d'abord les médecins : leurs soins prolongeront considérablement les combats.
- Surveillez-le : chaque soldat du syndicat blessé peut soudainement retrouver toutes ses forces.
