PINGOUIN À LA GRENADE


CLASS: Pingouin avec grenade
METABOLISM: Carnivore aviaire
PERCEPTION: Arme biologique
SIZE: 2 pieds
ENDURANCE: Faible-moyen
HOSTILITY: Haut
WEAPONS: Grenade attachée, attaque par picage
REWARD: 200FC
THREAT: Moyen

DESCRIPTION: 

Ce pingouin apparemment inoffensif a été transformé en arme. Une grenade est attachée à son dos, prête à exploser lorsqu'elle se rapproche d'une cible. Malgré sa petite taille, il est étonnamment rapide et agressif, se précipitant vers ses ennemis avec une combinaison mortelle de coups de bec et d'explosifs. Les rencontres imprudentes peuvent être fatales, car la détonation peut gravement endommager le terrain et les alliés proches.

TIPS: 

- Gardez vos distances ; la grenade peut exploser au contact.
- Utilisez des attaques à distance pour le neutraliser avant qu'il ne vous atteigne.
- Évitez de vous regrouper : un seul pingouin peut déclencher une réaction en chaîne.
