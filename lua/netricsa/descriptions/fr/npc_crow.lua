CORBEAU


CLASS: Corbeau
METABOLISM: Oiseau
PERCEPTION: Oiseau aviaire sauvage
SIZE: ~1,5 pied
ENDURANCE: Faible
HOSTILITY: Faible (défensif si menacé)
WEAPONS: Bec, griffes
REWARD:     1 FC
THREAT: Très faible

DESCRIPTION:

Les corbeaux sont des oiseaux intelligents et méfiants que l’on trouve souvent dans les zones urbaines et rurales. Ils se rassemblent en groupes et peuvent harceler les menaces potentielles, en utilisant des appels coordonnés pour alerter les autres. Bien qu’elles ne soient pas agressives par défaut, leur présence peut signaler un danger ou un changement environnemental.

TIPS:

- Les troupeaux peuvent indiquer des ennemis ou des dangers à proximité.
