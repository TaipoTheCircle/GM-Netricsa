Tache d'araignée de la plage Broadside


CLASS: Araignée tachetée
METABOLISM: Étourneau
PERCEPTION: Traqueur de prédateur
SIZE: 19,7 pieds
ENDURANCE: Infini
HOSTILITY: Absolu (vraisemblablement)
WEAPONS: Dents
REWARD: La vie ou la mort
THREAT: Très élevé

DESCRIPTION: 

Blot était autrefois le premier et le plus aimé personnage de Rex Broadside, un petit lapin portant une casquette et une salopette qui prenait vie à l'écran et volait le cœur du public. Mais Rex, inexpérimenté, signe un contrat cédant les droits sur son personnage, et Blot disparaît de sa vie. Des années plus tard, après la mort de Rex, Broadside Animation a racheté les droits, mais pas pour réaliser des dessins animés.

Dans un laboratoire secret, Blot est devenu la base d'une horrible expérience visant à créer des combinaisons vivantes pour Starlings dans lesquelles ils ont tenté d'implanter la conscience humaine afin de ressusciter Rex. La première tentative fut monstrueuse. La coquille grossière et imparfaite déformait l’image du mignon lapin au point de le rendre méconnaissable. Les restes d’un humain sont encore visibles à l’intérieur.

Alors que Starling subissait de nouvelles transformations au sein du monde déformé, une forme encore plus monstrueuse émergea : la Blot-Spider. Son corps a acquis de longues et fragiles pattes d'araignée, le transformant d'un chasseur tapi dans les coins en un prédateur rapide conçu pour terrifier ses proies.

Contrairement à d'autres formes, le Blot-Spider ne se cache pas et ne se déguise pas. Ça traque. Rapidement, sans relâche, forçant sa proie à fuir sans se retourner.

TIPS: 

- Cette forme de Blot ne se contente pas d'errer, elle chasse. Ne restez pas immobile.
- Ses pattes en forme d'araignée lui permettent de se déplacer rapidement. Cherchez un abri là où il ne peut pas vous atteindre.
- Contrairement au Blot classique, qui saute simplement des tonneaux, cette forme n'a probablement aucune faiblesse. Seulement le vol.
