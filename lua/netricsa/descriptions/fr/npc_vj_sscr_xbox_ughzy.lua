DESCRIPTION: 

Il a toujours été spéculé que le vicieux Ugh-Zan n'était pas une dynastie de monstres immortels, mais une seule entité incorporelle qui se réincarne dans différents corps (généralement avec au moins quatre bras), expliquant l'apparence différente de la créature entre ses incarnations. Les candidats Ugh-Zan en sont la preuve.
Formés par un culte fanatique d'adorateurs d'Ugh-Zan ("Zanlites"), les Candidats sont des clones produits en masse d'Ugh-Zan 3, à différents stades de croissance. Après la mort d'Ugh-Zan 3, ceux-ci ont été créés et suivent un entraînement de combat épuisant et constant pour atteindre la même taille et la même force que l'original. À mesure qu'ils grandissent, ils sont capables de manier de plus en plus d'armes d'Ugh-Zan 3.
En fin de compte, il ne restera qu'un seul candidat Ugh-Zan et l'esprit d'Ugh-Zan se réincarnera à nouveau en lui.

TIPS: 

- Utilise les armes et les sorts magiques les plus puissants connus
- Disponible en différentes tailles avec des armes variées
