LANCE-GRENADE MKIV


WEAPON: Lance-grenades MKIV
AMMO: Grenades
ROF: 60-200 tours/min selon la charge


DESCRIPTION: 

Le lance-grenades MKIV est le successeur amélioré du MKIII. Chaque pression sur la gâchette tire une seule grenade qui explose soit à l'impact avec un ennemi, soit trois secondes après le tir. Les grenades rebondissent sur les murs. En maintenant enfoncé le bouton de tir, vous chargez le tir, augmentant ainsi la vitesse et la distance de lancement ;


NOTES: 

- Les tirs chargés sont utiles pour les engagements à moyenne et longue portée, voyageant plus vite et plus loin.
- Les grenades sont dangereuses si elles manquent, surtout dans les zones restreintes - les dégâts causés par les éclaboussures peuvent blesser l'utilisateur.
- Très efficace contre les groupes d'ennemis faibles à moyens ; évitez d’utiliser des tirs chargés dans des espaces clos.
