HEADCRAB ZOMBIE COMMUN, GARDE DE SÉCURITÉ HORS SERVICE


CLASS: Zombie de crabe commun, garde de sécurité hors service
METABOLISM: Hôte humain + crabe parasite commun
PERCEPTION: Entité zombifiée
SIZE: ~6 pieds
ENDURANCE: Faible-Moyen
HOSTILITY: Modéré
WEAPONS: Griffes, grappin
REWARD: 100 FC
THREAT: Faible individuellement, modéré en groupe

DESCRIPTION: 

Les OffDuty Zombies sont d'anciens gardes de sécurité de Black Mesa qui n'étaient ni armés ni blindés. Infectés par les Headcrabs, ils se déplacent sans but mais attaquent tout ce qui se trouve à proximité avec des frappes de mêlée grossières. Leur manque d'armure les rend moins résistants que les H.E.C.U. ou les zombies en armure, mais ils constituent toujours une menace en groupe.

La transformation met l'accent sur la vulnérabilité tragique du personnel non préparé lors de l'épidémie de Xen, transformant des gardes endormis et inoffensifs en prédateurs lents.

TIPS: 

- Gardez vos distances ; leurs attaques sont lentes mais peuvent vous attraper.
- Les armes à feu sont efficaces ; la mêlée est risquée.
- Les groupes peuvent coincer leurs proies, alors priorisez soigneusement les cibles.
