PISTOLET À FAISCEAU


WEAPON: Pistolet à faisceau
AMMO: Cellules plasmatiques
ROF: ~300 tours/min

DESCRIPTION: 

Arme énergétique avec quatre cristaux bleus et un central. Tire un faisceau continu lorsque la gâchette est maintenue enfoncée, mais présente un délai de démarrage lorsque les barils de cristal tournent à pleine vitesse. Dégâts très élevés ; idéal pour éliminer des hordes d’ennemis faibles ou affronter des ennemis plus coriaces.

NOTES: 

- Faisceau Hitscan - pas de temps de trajet du projectile.
- Excellent contre les ennemis non blindés ou groupés.
