CITOYEN


CLASS: Citoyen
METABOLISM: Les humains
PERCEPTION: Résistance humaine / Civil
SIZE: ~5-6 pieds
ENDURANCE: Faible-Moyen
HOSTILITY: Faible (sauf si armé contre Combine)
WEAPONS: Varie : pistolet, SMG, fusil de chasse, armes trouvées
REWARD: Aucun
THREAT: Faible-Moyen

DESCRIPTION: 

Les citoyens sont la population humaine opprimée vivant sous le règne du Combine dans la ville 17 et au-delà. La plupart vivent dans des conditions misérables dans des immeubles d'habitation délabrés, sous la surveillance constante et la pression de la protection civile. Malgré leur apparente soumission, beaucoup soutiennent secrètement la Résistance, offrant des fournitures, des informations et des refuges aux rebelles.

Inspirés par des dirigeants comme Eli Vance ou Gordon Freeman, les citoyens prennent les armes et combattent aux côtés de la Résistance. Bien que mal entraînés et légèrement armés, leur nombre et leur détermination en font souvent une force décisive contre les patrouilles du Combine.

Their loyalty, however, comes at a cost: beaucoup sont capturés, « traités » et transformés en harceleurs ou en soldats d'Overwatch. Chaque citoyen armé sait qu’il risque plus que la mort : il risque de perdre entièrement son humanité.

TIPS: 

- Faible seul, mais dangereux en groupe avec des armes à feu.
- Ils peuvent fournir des munitions, des kits médicaux ou agir en tant que coéquipiers si vous gagnez leur confiance.
