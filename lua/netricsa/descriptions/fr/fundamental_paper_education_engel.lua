ENGEL DE L'ÉDUCATION FONDAMENTALE DU PAPIER


CLASS: Ange
METABOLISM: Organisme anthropomorphe en papier
PERCEPTION: Étudiant de l'Ecole du Papier
SIZE: 5 pieds
ENDURANCE: Moyen (plus fort que les étudiants standards)
HOSTILITY: Faible (uniquement les attaques en défense)
WEAPONS: Griffes acérées (4 doigts pointus), force physique
REWARD: 2
THREAT: Moyenne

DESCRIPTION: 

Student of the Paper School. Childhood friend and protector of Claire. Distinguished by his long, white, tousled hair, pulled back into a low ponytail, with two feathers (blue and red-orange) on his head. He wears the classic school uniform: une chemise blanche à manches rayées, une salopette noire et des jambières rayées. Ses mains sont noires, avec quatre doigts pointus en forme de griffes, et il a également une petite queue blanche. Il est gentil, attentionné et altruiste par nature, prêt à se risquer pour les autres, comme le démontre sa confrontation directe avec Miss Circle pour sauver Claire. Il possède une force physique et une résilience accrues par rapport aux étudiants normaux (il a résisté au coup de poing d'un professeur).

Il a le lien le plus étroit avec Claire (amis d'enfance). Il est également ami avec Bubble. Il respectait probablement Oliver lorsqu'il était enfant, mais ils sont désormais ennemis en raison de leurs visions du monde différentes.

TIPS: 

- Il est le plus physiquement capable des étudiants connus.
- Sa principale force motrice est de protéger Claire.
