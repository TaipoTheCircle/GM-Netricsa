GARDE SANGLANTE DU SPECTACLE DEMI-QUAKE


CLASS: Garde sanglante
METABOLISM: Humain, mâle
PERCEPTION: Gardien et victime des procès du spectacle
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Neutre
WEAPONS: Pistolet
REWARD: 60 FC
THREAT: Faible

DESCRIPTION: 

Il s'agit d'un garde en uniforme gris que le sadique HALF-QUAKE Show donne à ceux qui acceptent de travailler pour eux contre de la nourriture et la promesse de ne pas nuire à leur famille. L'uniforme est gris, indéfinissable, avec un logo ">)" caché par le sang dans le dos. Les gardes eux-mêmes portent des gilets pare-balles en lambeaux, des casques et plusieurs jours de chaume. Et du sang. Beaucoup de sang. Les leurs et ceux des autres.

Chaque garde doit éventuellement subir des tests sadiques. C'est une condition du contrat qu'ils ont signé sans lire la dernière page. Les sadiques aiment les tourmenter. C'est leur nature. Les gardes sont battus, empoisonnés, obligés de courir à travers des labyrinthes de la mort, puis, s'ils survivent, ils sont autorisés à entrer dans l'arène en tant que « personnel de service » pour les véritables victimes de la série. Ceux qui ne survivent pas (presque toujours) font partie du paysage.

C'est votre allié temporaire et réticent. Il déteste les sadiques autant que vous, mais il est trop brisé pour se rebeller. Au mieux, il pourrait se détourner au bon moment. Au pire, il s'assiéra simplement contre le mur et regardera jusqu'à ce qu'il soit abattu pour inaction.

Certains gardes sont armés. Mais même avec des armes, ce ne sont pas des combattants. Ce sont des victimes qui n'ont pas encore réalisé qu'elles sont déjà mortes.

TIPS: 

- Ne les tuez pas. Ce sont les seuls à pouvoir vous aider avec des conseils ou des munitions.
- Si un garde dit « Courez », alors courez. Il a vu beaucoup de choses.
- Les gardiens connaissent l'emplacement des cellules, des cachettes et des sorties. Suivez-les s'ils se dirigent quelque part dans un but précis.
- N'essayez pas de les sauver. Vous ne pouvez pas. Vous ne mourrez qu'avec eux. Et ils mourront de toute façon.
