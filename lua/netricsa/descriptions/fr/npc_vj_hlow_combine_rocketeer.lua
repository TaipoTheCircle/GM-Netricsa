COMBINEZ LE SOLDAT OVERWATCH ROCKET


CLASS: Combinez Overwatch Rocket Soldier
METABOLISM: Humain augmenté, mâle
PERCEPTION: Suppression des véhicules et des cibles lourdes
SIZE: 9,5 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrême
WEAPONS: Combinez RPG, arme de mêlée secondaire
REWARD: 700FC
THREAT: Très élevé

DESCRIPTION: 

The Combine Overwatch Rocket Soldier is a heavy fire unit designed to destroy vehicles, fortifications, and high-risk targets. His appearance is easily confused with that of a shotgunner: une armure similaire, une silhouette massive et une position. Cette erreur est souvent fatale.

The Soldier's primary weapon is an Combine Rocket Launcher, which he carries at all times and uses without hesitation. Unlike anti-tank soldiers, he doesn't wait for the perfect target: ses roquettes sont utilisées de manière agressive, éliminant les couvertures, brisant les murs et supprimant les concentrations ennemies.

Il travaille souvent en tandem avec des soldats réguliers qui le protègent pendant qu'il se prépare à tirer.

Bien que son armure ne soit pas de premier ordre, un lancement réussi peut instantanément renverser le cours de la bataille.

TIPS: 

- Ne le confondez pas avec le fusil à pompe Combine.
- Le Rocket Soldier est particulièrement dangereux dans les espaces confinés.
- Utilisez une couverture capable de résister à l'explosion, pas seulement de bloquer votre vue.
