BLACK OPS AH-64 APACHE GUNSHIP


CLASS: Black Ops AH-64 Apache Gunship
METABOLISM: aucun (mécanique, piloté par élite)
PERCEPTION: Hélicoptère d'attaque secret
SIZE: ~ 58 pieds de long
ENDURANCE: extrêmement haut (fortement blindé)
HOSTILITY: extrême
WEAPONS: Chaingun, nacelles de fusée, charge utile possible de missile
REWARD: 5000FC
THREAT: catastrophique

DESCRIPTION: 

Le Black Ops Apache est une variante fortement modifiée de l’hélicoptère d’attaque standard AH-64, déployé par les forces gouvernementales secrètes. Contrairement à son H.E.C.U. homologue, cette version comprend un blindage renforcé, une avionique améliorée et des charges utiles d'armes améliorées, lui permettant de dominer à la fois l'espace aérien et les rencontres au sol.

Peint en camouflage noir mat avec une signature radar réduite, il est conçu à la fois pour l'intimidation et l'approche furtive. Lorsqu'il est rencontré, il délivre une puissance de feu écrasante, balayant le champ de bataille avec des canons à chaînes et des salves de roquettes. Les survivants signalent souvent sa poursuite incessante et sa précision étrange, rendant la fuite presque impossible une fois verrouillée.

Contrairement à l'Osprey axé sur le transport, l'Apache Black Ops existe uniquement pour l'anéantissement. Là où il vole, il reste rarement des survivants.

TIPS: 

- Restez à l'abri - un terrain découvert est une mort certaine.
- Les roquettes sont la méthode la plus fiable pour l'abattre.
- Ne vous attardez pas après l'avoir endommagé, les représailles seront brutales.
- Écoutez l'écho distinct du rotor - c'est votre seul avertissement.
