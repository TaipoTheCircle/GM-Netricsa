COMBINEZ H.E.V. SOLDAT D'OVERWATCH


CLASS: Combinez H.E.V. Soldat de surveillance
METABOLISM: Humain augmenté, mâle
PERCEPTION: Unité d'assaut combinée expérimentale
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: OICW, fusil à impulsion AR2
REWARD: 1400 FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine H.E.V. Overwatch Soldier est la preuve directe que le Combine n’invente pas, il prend. Après avoir capturé la technologie de Black Mesa, le H.E.V. la technologie de la combinaison a été repensée. Le résultat est une exocombinaison de combat bleue avec l'insigne Combine, conçue à la fois pour protéger le porteur et pour maximiser l'efficacité du combat jusqu'à sa mort.

L'apparence du soldat ressemble à celle d'un scientifique dans un véhicule H.E.V. costume, mais la ressemblance est trompeuse. Le Combine considère le soldat comme une ressource consommable, mais extrêmement dangereuse.

Armés d'un OICW et d'un AR2, ces soldats sont capables de combattre à n'importe quelle distance, combinant tir automatique et grenades sous le canon. Ils sont souvent utilisés dans des situations où les unités Overwatch standard subissent des pertes excessives et où les forces d'élite sont toujours « redondantes ».

TIPS: 

- L'OICW le rend particulièrement dangereux à couvert.
- Une endurance élevée ne signifie pas invulnérabilité : les explosifs fonctionnent toujours aussi bien.
