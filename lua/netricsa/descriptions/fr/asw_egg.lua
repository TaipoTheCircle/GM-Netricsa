OEUF DE PARASITE D'ESSAI


CLASSE : Œuf de parasite en essaim
MÉTABOLISME : Xénobiologique
PERCEPTION : Incubateur de parasites
TAILLE : 6,5 pieds
ENDURANCE : Très faible
HOSTILITÉ : Moyenne
ARMES : Libération de parasites
RÉCOMPENSE : 50 FC
MENACE : Élevée

DESCRIPTIF :

Le Swarm Parasite Egg est une chambre d’incubation spécialisée conçue pour le développement de parasites. Il est recouvert d'une fine membrane élastique, à l'intérieur de laquelle se trouve un épais liquide nutritif nécessaire à la maturation de l'embryon.

Dans un état calme, les œufs restent immobiles, attendant l’émergence d’organismes vivants. Lorsqu'une proie potentielle s'approche, la coquille s'ouvre instantanément, libérant le parasite, qui attaque immédiatement la cible la plus proche. Une fois éclos, l’œuf perd rapidement sa viabilité et n’est plus capable de produire de nouveaux œufs.

Les œufs sont généralement situés en groupes à l’intérieur des ruches, des puits de ventilation, des tunnels et d’autres zones humides où sont maintenues des conditions favorables au développement de la progéniture. On les trouve souvent à côté de cocons de larves et d’autres incubateurs, formant ainsi des zones de reproduction en essaims à part entière.

Malgré leur immobilité, un grand nombre d’œufs peuvent transformer un passage sûr en piège mortel. Même si les adultes sont détruits, les couvées restantes permettent à l’Essaim de reconstituer rapidement ses effectifs.

CONSEILS :

- Détruisez les œufs avant de vous en approcher.
- Le feu vous permet de nettoyer en toute sécurité les zones contaminées avec un grand nombre d'embrayages.
- Après avoir ouvert la coquille, concentrez le feu sur le parasite éclos.
- S'il y a plusieurs œufs à proximité, essayez de ne pas les activer en même temps.