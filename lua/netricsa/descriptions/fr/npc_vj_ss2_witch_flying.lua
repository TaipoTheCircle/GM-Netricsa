YAGODA LA SORCIÈRE DE MAGNOR


CLASS: Yagoda la sorcière
METABOLISM: Mammifère, ancienne beauté
PERCEPTION: Sorcière
SIZE: 4,6 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: La cadence de tir de la tourelle est telle qu'en quelques secondes, elle peut transformer une meute de Gnaars en un désastre sanglant, ou un Cyclope en une passoire.
REWARD: 50 FC
THREAT: Haut

DESCRIPTION: 

Elle était essentiellement l'une des plus belles filles de la galaxie, mais lorsqu'elle et sa sœur étaient prêtes à participer aux concours de beauté de Mental, elles se sont transformées en laides sorcières. Après cet incident, ils furent facilement enrôlés dans l'armée de Mental.

Yagoda est la sœur jumelle de Dynja ; ensemble, elles sont les sorcières les plus célèbres de la planète Magnor. Ils sont réputés pour leur nature sadique, la maîtrise de leur art et leur penchant pour l'alcool.

C'est une sorcière qui a maîtrisé le sort Mirror Clone et l'a utilisé pour se reproduire. Voyant son potentiel, Mental lui a offert une réserve de vodka à vie en échange de ses services. Elle a accepté avec joie.

Yagoda vole de manière erratique, tentant d'encercler l'ennemi tout en faisant pleuvoir des attaques magiques.

TIPS: 

- Elle est très maniable et difficile à toucher ; pour la détruire, utilisez des armes à tir rapide ou visez soigneusement avec un fusil de sniper.
- Elle devrait être une cible prioritaire, car les tirs aériens peuvent être trop efficaces.
- Même si un coup de fusil à canon tronqué peut la tuer au corps à corps, s'approcher d'elle est extrêmement difficile.
- Si vous êtes encerclé, essayez de vous déplacer constamment à gauche et à droite tout en reculant pour tenter de briser son encerclement.
