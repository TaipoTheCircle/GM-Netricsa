COMBINER UN DROÏDE


CLASS: Combiner un droïde
METABOLISM: Entièrement synthétique
PERCEPTION: Combinez une plate-forme de combat robotique lourde
SIZE: ~8-10 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Extrême
WEAPONS: Force mécanique pure, lancer d'objets lourds, éperonnage
REWARD: 1200FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine Droid est la réponse directe, mais tordue, du Combine au Resistance Dog. Inspirés par les Rebels, les ingénieurs d'Overwatch l'ont repensé, supprimant l'improvisation « redondante » et la composante émotionnelle, transformant l'idée en une machine de combat parfaitement calibrée.

Unlike the Dog, the Droid is perfectly proportioned: son corps est symétrique, ses mouvements sont mesurés et sa mécanique est dépourvue de tout geste brusque et « vif ». Sa tête est tournée dans la direction opposée. Son extérieur est peint d'un noir profond avec des accents rouges et blancs, et le logo Combine est clairement visible sur sa carrosserie.

Its purpose is simple: briser, écraser et détruire. Il peut lancer des voitures, arracher des tourelles de leurs supports et percer des fortifications avec la même facilité que le Chien, mais il le fait de manière méthodique et précise.

Le droïde est intégré au réseau tactique Combine et travaille souvent en tandem avec les soldats d'Overwatch, fournissant une couverture, dégageant des passages ou semant la panique parmi l'ennemi.

TIPS: 

- Direct combat is highly discouraged - its armor can withstand enormous forces. - Use the environment: effondrements, pièges et explosions puissantes.
