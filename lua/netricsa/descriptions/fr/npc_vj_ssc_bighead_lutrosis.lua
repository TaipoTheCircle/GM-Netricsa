DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Matt « Lutrose » McGill est l'un de ces héros méconnus dont le travail constitue le fondement de la stabilité et de la finition du jeu. Son crâne surdimensionné est le résultat direct d'innombrables heures passées à rechercher des bugs, à tester des mécanismes et à envoyer des rapports détaillés aux développeurs de Croteam. Tandis que d'autres appréciaient le produit fini, Lutrose s'est battu contre les chutes de Kleers à travers les textures, les portes coincées et les échecs de progression pour éviter que la même chose n'arrive aux autres.

Son surnom au générique est le signe de la plus haute reconnaissance du studio. Il incarne une communauté dévouée de fans qui ne se contentent pas de jouer, mais participent activement à la création, se transformant de consommateurs en co-créateurs de qualité.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", détruisez-le à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
