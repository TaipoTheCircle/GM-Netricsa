COMBINEZ LE SOLDAT D'ÉLITE ARCTIQUE D'OVERWATCH


CLASS: Combinez le soldat d'élite arctique d'Overwatch
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner Strike Elite
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, boules d'énergie
REWARD: 1100 FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine Overwatch Arctic Elite est une version spécialisée du Combine Elite, conçue pour le combat dans le pergélisol, les blizzards et les villes polaires mortes. Son camouflage blanc se fond pratiquement dans la glace environnante, et les éléments gris-bleu de son équipement remplacent les détails rouges familiers, rendant sa silhouette froide, sans émotion et presque fantomatique.

Une seule visière bleue brille à travers la tempête de neige, détectant les signatures thermiques et les mouvements à une distance considérable. Des rayures bleues courent des deux côtés de ses jambes, servant d'éléments d'identification et techniques de son armure, ainsi que de marqueur visuel de son statut d'élite.

These soldiers are deployed in regions where conventional Combine forces quickly lose their combat effectiveness. Reinforced implants protect them from hypothermia, and their life support system allows them to fight for hours without cover or resupply. Their tactics are standard for Overwatch elite: tir coordonné, suppression avec des boules d'énergie et élimination méthodique de la résistance.

TIPS: 

- Leur camouflage blanc les rend difficiles à repérer dans la neige.
- Les boules d'énergie sont particulièrement dangereuses dans les passages glacés étroits.
