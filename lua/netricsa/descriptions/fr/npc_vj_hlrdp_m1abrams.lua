Y.S.M. M1 ABRAMS


CLASS: Y.S.M. M1 Abrams
METABOLISM: Mécanique, contrôlé par des mercenaires
PERCEPTION: Véhicule blindé moyen, soutien à l'infanterie
SIZE: 15,4 pieds
ENDURANCE: Moyen
HOSTILITY: Extrême
WEAPONS: Lanceur de missiles
REWARD: 5000FC
THREAT: Très élevé

DESCRIPTION: 

Il s'agit d'une version plus légère et plus petite du célèbre char utilisé par l'Y.S.M. pour des raids rapides et un soutien d'infanterie à la base Delta. Contrairement à son original lourd et encombrant, cette modification sacrifie l'armure et la taille au profit de la vitesse et de la mobilité.

The vehicle's appearance is distinctive: camouflage gris foncé avec des rayures jaune vif sur les côtés et la tourelle. Sa petite taille lui permet de manœuvrer dans des espaces urbains restreints et même de traverser des zones où un char ordinaire resterait coincé. Le blindage est plus fin : il est conçu pour protéger contre les armes légères et les explosifs légers, mais un coup direct d'un RPG ou d'un missile antichar réduira le véhicule à la ferraille.

Le Y.S.M. Abrams tire plus fréquemment, lançant un missile toutes les quelques secondes. Les missiles ont une bonne précision et un bon rayon de souffle, mais moins de puissance perforante que le modèle original.

Ils se déplacent rapidement, tirent avec précision et peuvent changer de position en quelques secondes.

TIPS: 

- Cette version est plus petite et plus rapide, mais son blindage est plus faible. Un ou deux tirs RPG bien ciblés peuvent le désactiver.
- Mettez-vous à couvert : les missiles arrivent rapidement, et si vous hésitez, le suivant arrivera dans quelques secondes.
- N'essayez pas d'attaquer de front. Optez pour le flanc ou l’arrière.
