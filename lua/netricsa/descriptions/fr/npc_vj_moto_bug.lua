BUG MOTO DU MOBIUS


CLASS: Bug de moto
METABOLISM: Batterie-Essence-Animale
PERCEPTION: Dépisteur d'actions
SIZE: 4 pieds
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Coque, Bélier
REWARD: 200FC
THREAT: Faible

DESCRIPTION: 

Lorsqu'il s'agissait de patrouiller dans des espaces ouverts, le Dr Eggman a décidé de ne pas réinventer la roue mais de combiner ce qui était toujours à portée de main. Il a pris une coque de coccinelle, un animal, un moteur de tondeuse à gazon, une roue de brouette et un processeur d'un jouet pour enfants appelé "Eh bien, attendez !" Le résultat fut le Moto Bug.

Il s'agit de la première tentative d'Eggman de créer des unités mobiles et du Badnik le plus reconnaissable de l'empire. Le Moto Bug ne vole pas, ne tire pas et n'explose pas. Il continue simplement. Aller et retour. Des allers-retours encore. S'il repère une cible, il essaie de la rattraper, mais il le fait avec la grâce d'un mille-pattes ivre.

TIPS: 

- Tout coup venant d'en haut le transforme en un tas de pièces.
- N'essayez pas de vous enfuir en ligne droite - ses roues valent mieux que son cerveau.

