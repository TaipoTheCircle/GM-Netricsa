ROBOT ENVAHISSEUR, ASSAUT DE SHAIJU GANDHI



CLASS: Robot envahisseur, assaut
METABOLISM: Artificiel (noyau mécanique)
PERCEPTION: Guerrier-machine
SIZE: 7-8 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Fusil d'assaut laser, grenades à plasma améliorées, armes écrasantes
REWARD: 1200FC
THREAT: Très élevé

DESCRIPTION: 

L'unité d'assaut représente l'une des variantes les plus dangereuses des robots envahisseurs de Shaidzu-Gandi. Contrairement aux modèles de base, ces machines sont entièrement équipées d'armes à distance avancées, notamment un fusil d'assaut laser à tir rapide, capable d'abattre des escouades de soldats en quelques secondes.

De plus, ils transportent des grenades à plasma améliorées, conçues pour exploser avec une force dévastatrice, annihilant l'infanterie et déstabilisant les cibles blindées. Lorsqu’ils sont rapprochés, ils peuvent toujours compter sur leurs bras mécaniques écrasants, ce qui les rend mortels à n’importe quelle distance.

Their arrival on Earth marked a turning point in the war: Ce ne sont plus seulement des machines brutales, ils sont devenus une force d’invasion entièrement armée, terrifiante à la fois par sa puissance de feu et sa résilience.

TIPS: 

- Traitez-les comme des cibles prioritaires : leurs grenades peuvent anéantir des groupes instantanément.
- Restez à couvert et utilisez des munitions lourdes pour briser leur armure.
- Their laser rifle has pinpoint accuracy: continuer à bouger pour réduire son efficacité.
