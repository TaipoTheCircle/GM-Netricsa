DRONE OUVRIER DE CUIVRE 9


CLASS: Villageois zombie
METABOLISM: Mécanique / Alimenté par l'énergie
PERCEPTION: Drone / Unité Civile
SIZE: ~4 pieds
ENDURANCE: Faible
HOSTILITY: Aucun
WEAPONS: Aucun
REWARD: 150 FC
THREAT: Très faible

DESCRIPTION: 

Les drones ouvriers sont des drones civils standards. Ils sont conçus pour la maintenance, la logistique et le travail manuel à bord des installations ou en milieu urbain. Totalement non combatifs, ils évitent activement les conflits et fuient toute entité hostile, à l'exception des leurs.

Bien que sans défense, les Worker Drones servent d’indicateur visuel des dangers environnementaux et de la présence de l’ennemi. Observer leur comportement peut avertir des menaces à proximité, ce qui les rend étonnamment utiles malgré leur nature passive.

TIPS: 

- Les drones ouvriers sont inoffensifs et peuvent fournir un contexte narratif ou environnemental.
