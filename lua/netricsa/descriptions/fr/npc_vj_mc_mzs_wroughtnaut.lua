WROUGHTNAUT FERREUX DU MINECRAFT OVERWORLD


CLASS: Ferreux Forgénaute
METABOLISM: Automate enchanté
PERCEPTION: Gardien oublié
SIZE: 11 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Absolu
WEAPONAUT: Hache aux mille métaux, piétinement magique
REWARD: 10000FC
THREAT: Maximum

DESCRIPTION: 

A forgotten guardian, imprisoned for centuries in the Forged Chambers. This is neither a man nor a statue - it is a piece of magically animated armor whose soul has long since faded, leaving only blind rage and an impossible duty. It has forgotten what it guards, who it waits for, and for whom it mourns. He remembers only one command: pour détruire quiconque ose troubler la paix éternelle de ses demeures. Il est totalement immunisé contre tous les dégâts, à l'exception des frappes sur un point vulnérable spécifique - une blessure au dos, qui n'est vulnérable que lors de son attaque la plus puissante, "Axe Strike", lorsqu'il plonge son arme dans le sol. En dehors du combat, son armure magique soigne instantanément tous les dégâts.

TIPS: 

- Les dégâts ne peuvent être infligés QUE lors de son attaque "Axe Strike", lorsqu'il fait une pause, plongeant sa hache dans le sol, exposant son dos. Visez précisément la blessure.
- L'attaque "Stomp" peut être esquivée en sautant au bon moment. Les coups de hache doivent être évités sur le côté.
- Provoquez-le en "Axe Strike" en restant à moyenne portée. Après un échec, déplacez-vous rapidement derrière lui et lancez des frappes précises. Évitez les combats rapprochés : ses boucliers sont inutiles contre ses attaques.
- Lorsque sa santé est faible, il effectue des combos de deux ou trois attaques dont la dernière est une attaque tournoyante qui ne laisse quasiment aucune chance d'esquiver. À ce stade, il est préférable de battre en retraite et d'attendre la prochaine occasion d'attaquer par derrière.
- En cas de défaite, vous recevez son casque forgé et la légendaire hache aux mille métaux.
