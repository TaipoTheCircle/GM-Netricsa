ENTREPÔT NOIR MESA, CONTENEURS


Bienvenue à l'intérieur, Sam. Il fait plus sombre, plus calme et ça sent comme si l'entrepôt avait compris depuis longtemps comment cela allait se terminer et s'y était résigné.

Au centre de la pièce se trouvent deux conteneurs. Énorme. Un bleu, un rouge. Un choix classique. Presque philosophique. Je dirais que l'une mène à la vérité, l'autre à une mort certaine, mais connaissant Black Mesa, les deux options pourraient être vraies simultanément.

Rosenberg est là ? C'est inconnu. Peut-être qu'il est à l'intérieur. Peut-être qu'il est sous le conteneur. Peut-être qu'il n'est pas là du tout, et c'est un autre test de votre foi dans la cause et l'effet. Les scientifiques aiment se cacher dans des endroits où aucune personne sensée n’irait volontiers. Et Rosenberg, semble-t-il, a déjà sacrifié sa raison pour le portail.

Les conteneurs sont gardés par H.E.C.U. militaires. On leur a clairement dit : « Gardez-le. Ce qu'il y a à l'intérieur ne vous regarde pas. S'ils le demandent, tirez. » Et donc ils le gardent. Les questions ne sont pas les bienvenues ici, pas plus que la survie des témoins.

Note: les gardes sont postés comme s'il y avait quelque chose de vraiment précieux à l'intérieur. Ou extrêmement dangereux. Et le plus souvent, à Black Mesa, c’est la même chose.

Bleu. Rouge. Deux conteneurs. Deux mauvaises décisions possibles.

Choisissez soigneusement, Sam.

