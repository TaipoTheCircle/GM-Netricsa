BRICK POND, LE VENT SENT LA MORT


Sam, nous nous sommes retrouvés dans un endroit qui peut difficilement être appelé un endroit.

Cela ressemble plus à un problème dans la matrice, à un paradoxe géométrique ou simplement à une blague vraiment bizarre de quelqu'un.

Imaginez une seule pièce en briques rouges. Sans toit, sans fenêtre, sans porte. Les murs s'élèvent, mais il n'y a pas de ciel au-dessus d'eux, juste un infini gris.

La pièce est entourée de vide. Pas l'obscurité, mais le vide. Aucun son, aucun sens.

What's here: 

Les murs de briques sont le seul objet de cet univers. Rouge, rugueux, réel. Vous pouvez les toucher, les frapper, voire essayer de les briser. Mais pourquoi ? Il n'y a rien derrière eux.

Un plafond ouvert, ou plutôt son absence. Les murs tombent et au-delà s’étend une obscurité sans fin. Aucune source, aucune direction. C'est tout simplement le cas.

Le sol est en brique ordinaire. Vous pouvez rester là.

Le vide au-delà des murs : si vous parvenez à vous échapper (et vous y parvenez, Sam), il n'y a rien là-bas. Rien du tout. Juste toi et un vide sans fin.

Et qu'est-ce qu'il y a là-dessous ?

Et c'est là que ça devient intéressant.

Si vous sautez (et vous le ferez certainement, je vous connais), l'infini commence.

Vous tombez, tombez, tombez. Les murs de la pièce ont depuis longtemps disparu de la vue. Seule la lumière noire l’entoure. Une minute, une heure, un jour, rien ne change.

Je tombe juste. Vers nulle part. Sans s'arrêter. Sans fond. Sans espoir de toucher un jour une surface solide.

Il n'y a aucune couverture ici, aucun moyen d'obtenir un point de vue, aucun moyen de se cacher. Soit vous êtes dans la pièce, soit dans une chute sans fin.

Si vous amenez des ennemis ici, la bataille sera courte. Soit quelqu'un tombe dans l'abîme, soit tout le monde reste dans la pièce et se tire dessus jusqu'à ce qu'il n'y ait plus de munitions. Et puis restez là et regardez les briques.

L’aspect psychologique est la véritable arme. Cet endroit vous rend fou plus vite que n'importe quel crabe. Le silence, le vide, l'infini au-delà de la pièce. Après une demi-heure ici, vous commencerez à parler aux briques. Et après une heure, les briques commenceront à répondre.

Eh bien, ne sautez pas. Sérieusement, Sam. Je sais que tu es curieux, je sais que tu veux vérifier s'il y a un fond. Mais il n’y en a pas. J'ai calculé la trajectoire, la vitesse de votre chute, les conséquences possibles. Il n'y a pas de fond. Vous tomberez pour toujours. Et je devrai t'entendre crier : "Quand allons-nous y arriver ?" pour toujours.

Nous ne survivrons pas tous les deux à ça.

Allons-y... mais où sommes-nous censés aller ? Il n'y a qu'une seule pièce ici.

OK, restons là et regardons les briques. Au fait, ils sont magnifiques. Rouge. Brique. Tout ce que nous avons dans ce monde...
