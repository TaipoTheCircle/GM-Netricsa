SORCIÈRE DU MONDE MINECRAFT


CLASS: Sorcière
METABOLISM: Humanoïde à sang chaud
PERCEPTION: Humanoïde Assistant Intelligent
SIZE: 6,2 pieds
ENDURANCE: Très élevé
HOSTILITY: Haut
WEAPONS: Potions (dégâts, lenteur, poison, faiblesse)
REWARD: 400FC
THREAT: Haut

DESCRIPTION: 

The Witch is an isolated representative of a village civilization who has chosen the path of potions, alchemy, and an outright disregard for public opinion. Outwardly, she resembles a villager, but differences are immediately noticeable: un nez verruqueux, des vêtements sombres et un regard sévère.

La sorcière utilise des potions non seulement pour attaquer mais aussi pour se défendre. Lorsqu'elle est menacée, elle boit instantanément des potions de résistance au feu, de résistance, de régénération et de respiration aquatique, se transformant en un adversaire extrêmement résistant.

Elle vit dans des huttes dans les marais ou apparaît lors de raids, où elle agit comme un artilleur alchimique mobile, inondant les ennemis de bouteilles au contenu extrêmement hostile.

TIPS: 

- Analyse constamment la menace et sélectionne la bonne potion.
- Préfère épuiser l'ennemi plutôt que de tuer rapidement.
- Ne panique jamais. Jamais.
- Le lait supprime les effets, mais ne résout pas complètement le problème de la sorcière.
