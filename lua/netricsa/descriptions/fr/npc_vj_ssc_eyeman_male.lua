GNAAR MÂLE


CLASS: Gnaar, mâle
METABOLISM: mammifère
PERCEPTION: animal de guerre dressé
SIZE: 4 pieds
ENDURANCE: faible
HOSTILITY: faible
WEAPONS: morsures, coups de poing
REWARD: 500FC
THREAT: très faible

DESCRIPTION: 

Un petit animal bipède sans tête, avec un œil et une grande bouche. Originaire de Sirius et entraîné par Mental pour attaquer les forces adverses. Les mâles sont plus petits, violets et moins coriaces que les femelles.

TIPS: 

- Ils n'ont pas d'armes de tir, restez simplement à l'écart.
- Certaines espèces peuvent léviter.
- Des spécimens rares seraient invisibles.
