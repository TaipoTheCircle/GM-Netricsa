VER GÈNE DE LA DIMENSION X


CLASS: Ver génétique
METABOLISM: Hybride parasitaire + bio-construction
PERCEPTION: Apex suzerain de la race X
SIZE: Plus de 100 pieds (sort d'une faille dimensionnelle)
ENDURANCE: Extrêmement élevé
HOSTILITY: Total
WEAPONS: Crachat acide, gousses de spores, onde de choc psionique, invocation de serviteurs de la Race X
REWARD: 9000FC
THREAT: Cataclysmique

DESCRIPTION: 

Le Gene Worm est le suzerain colossal de la Race X, une monstruosité issue de la bio-ingénierie qui sert d'arme ultime dans leur invasion. Émergeant d'une faille dimensionnelle, il apparaît comme un titan massif ressemblant à un ver avec des mandibules grotesques, des plaques blindées et des sacs palpitants. Son corps est conçu pour terraformer et dominer les environnements extraterrestres, ensemençant le terrain avec des formes de vie Race X tout en annihilant l'opposition avec des attaques dévastatrices.

La biologie de la créature est une fusion de tissus parasitaires et de génétique artificielle, ce qui en fait à la fois une arme vivante et une chambre d'accouchement mobile. Il projette de l'acide corrosif et génère des modules qui libèrent des serviteurs agressifs pour le protéger. Sa présence psychique peut perturber même les soldats entraînés, submergeant les esprits d'impulsions extraterrestres.

Lors de l'incident de Black Mesa, le Gene Worm était le fer de lance de l'invasion de la Race X. Son arrivée réussie aurait solidifié leur emprise sur Terre. Il a fallu une puissance de feu militaire importante et des sacrifices extrêmes pour le détruire avant que la brèche dimensionnelle ne se stabilise complètement.

TIPS: 

- Visez les points faibles de la tête (yeux) pour exposer le point faible.
- Préparez-vous à ce que des serviteurs apparaissent constamment ; détruisez-les rapidement.
- La seule façon de vaincre ce monstre est d'attaquer le point faible du ventre.
