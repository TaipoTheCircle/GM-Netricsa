PISTOLET-MACHINE MP-7


WEAPON: MP-7
AMMUNITION: 4,6x30mm
ROF: ~950 tours/min

DESCRIPTION: 

La mitraillette MP-7 est une mitraillette développée au début des années 2000 par la société allemande Heckler & Koch. Il s'agit d'une arme automatique chambrée pour la cartouche spécialement conçue de 4,6 × 30 mm. Le MP7 est conçu comme une petite mitraillette avec un chargeur inséré dans la poignée pistolet, une crosse télescopique pliable et une poignée auxiliaire pliable à l'avant. Le mécanisme automatique du MP7 est basé sur un moteur à gaz avec un piston à gaz à course courte, et le verrou est verrouillé en tournant la glissière. La conception intègre certaines caractéristiques du fusil d'assaut HK G36, telles que l'utilisation intensive de matériaux polymères, un mécanisme automatique similaire et une conception de mécanisme de déclenchement. Il a été utilisé pendant la guerre en Afghanistan, est également utilisé par les forces de l'ordre des pays de l'OTAN et est utilisé par les criminels.

NOTES: 

- Compact et léger.
- La crosse et la poignée tactique peuvent être pliées.
- Il peut être modifié en ajoutant un viseur ou un viseur laser.
