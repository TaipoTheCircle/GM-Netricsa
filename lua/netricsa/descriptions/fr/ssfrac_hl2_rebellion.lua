LA RÉSISTANCE


The Resistance is a loose network of rebels, humans and Vortigaunts, united by a common goal: pour libérer la Terre de la tyrannie du Combine et récupérer leur droit à la liberté et à la raison. Ils ne constituent pas une armée unifiée, mais des centaines de petites poches opérant dans l’ombre.

The Resistance is led by figures whose names evoke both hope and outrage: Eli Vance, Alyx Vance, Barney Calhoun et d'autres dirigeants chevronnés capables d'exploiter le pouvoir de la science.

Their actions are divided: Certains scientifiques travaillent sur des technologies qui pourraient briser la tyrannie du Combine, tandis que d'autres forment des unités armées prêtes à combattre directement, quelles que soient les pertes.
