MERCENAIRE ZORG DE BEELMEZ IV


CLASS: Mercenaire Zorg
METABOLISM: mammifère
PERCEPTION: race de guerrier
SIZE: 6,5 pieds
ENDURANCE: faible
HOSTILITY: faible
WEAPONS: laser éclaté
REWARD: 500FC
THREAT: faible

DESCRIPTION: 

Mental a transporté ces soldats sur Terre depuis une partie inconnue de l'univers.  Ils étaient autrefois une race de guerriers redoutables, mais comme ils ont conquis plus de planètes qu'ils ne savaient quoi faire, ils ont décidé de ralentir pendant un moment.  La plupart de la population a pris des vacances attendues depuis longtemps sur l'une des plus belles planètes tropicales de leur système ; mais comme ils ne prenaient le soleil qu'à partir de 10h du matin. À 16 heures, après quelques années, la plupart d'entre eux n'avaient plus de peau sur le corps.

TIPS: 

- Essayez d'éviter leurs faisceaux laser rapides.
