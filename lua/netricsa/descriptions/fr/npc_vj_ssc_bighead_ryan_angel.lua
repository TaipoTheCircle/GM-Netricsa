DESCRIPTION: 

Les Big_Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Ryan is one of the most enigmatic "brains" in the Croteam pantheon. Unlike his colleagues with detailed credits, his contributions remain undecipherable, like a faded artifact on an ancient scroll. His oversized skull holds a secret: peut-être a-t-il corrigé un bug critique la dernière nuit avant le Golden Master, écrit le scénario d'un œuf de Pâques oublié, ou simplement livré cette même pizza qui a sauvé l'équipe pendant la crise. Son nom est perdu dans les annales du développement, mais le fait qu'il figure sur la liste est une preuve irréfutable qu'il a apporté quelque chose.

Il rappelle que derrière chaque jeu emblématique se cache non seulement le génie des développeurs principaux, mais aussi les contributions discrètes de dizaines de « quelqu'un », sans qui le produit final serait juste un peu moins parfait.

GROSSE TÊTE, JOHN J. DICK "BOOGER"

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", détruisez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
