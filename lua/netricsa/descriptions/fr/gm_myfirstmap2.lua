A CITY FOR FRIENDS 2


La ville se compose de plusieurs bâtiments, dont un garage en brique et un groupe de gratte-ciel, constitués pour la plupart de fenêtres sans porte. Certaines routes longent les toits, reliant les bâtiments. L'atmosphère est minimaliste : le ciel gris, l'herbe verte et la ville vide créent une sensation d'espace et un léger isolement.