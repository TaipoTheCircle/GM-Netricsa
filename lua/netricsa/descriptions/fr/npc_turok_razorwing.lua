AILE DE RASOIR DE LA PLANÈTE TERRAFORMÉE


CLASS: Aile de rasoir
METABOLISM: Insectoïde
PERCEPTION: Insecte volant prédateur
SIZE: 5,9 pieds
ENDURANCE: Très faible
HOSTILITY: Extrêmement élevé
WEAPONS: Crachat acide, piqûre, vitesse
REWARD: 150 FC
THREAT: Haut

DESCRIPTION: 

Les Razor Wings sont ce que vous obtenez si vous croisez une libellule avec un scorpion, ajoutez quelques pattes supplémentaires et libérez le tout sur un continent volcanique.

Les Razor Wings vivaient autrefois profondément sous terre jusqu'à ce qu'une éruption volcanique fasse disparaître leur ruche. Depuis, ces créatures causent régulièrement des problèmes à quiconque s’aventure dans leur zone de nidification.

Au combat, les Razor Wings comptent sur la vitesse et le nombre. Ils volent dans les airs plus vite qu’un humain ne peut courir au sol et attaquent en essaim.

Leur arme principale est le crachat d'acide. L’acide ronge non seulement la chair, mais ralentit également les mouvements.

Les Razor Wings eux-mêmes meurent d'un seul coup de n'importe quelle arme, mais le problème est qu'il y en a toujours beaucoup. Beaucoup.

TIPS: 

- Un coup, un monstre. N'économisez pas de munitions, mais ne paniquez pas.
- Les armes à tir rapide sont la meilleure solution au problème des essaims.
- Si un Razor Wing crache, esquivez. L'acide colle et vous ralentit.
- Ne vous laissez pas encercler. Dans un champ ouvert, un essaim vous renversera en quelques secondes.
