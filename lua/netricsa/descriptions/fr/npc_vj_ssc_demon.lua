DÉMON REPTILOÏDE FIENDIEN


CLASS: Démon reptiloïde fiendien
METABOLISM: à sang froid, ressemblant à un reptile
PERCEPTION: race primitive modifiée
SIZE: 20 pieds
ENDURANCE: moyen
HOSTILITY: haut
WEAPONS: projectile boule de lave, griffes
REWARD: 5000FC
THREAT: moyen

DESCRIPTION: 

Une autre mutation hideuse, gracieuseté de Notorius Mental. Un simple organisme ressemblant à un lézard de Fiendia Prime a été croisé et muté sur plusieurs générations, et a finalement été renforcé par un squelette en titane. Il a la capacité unique de former un trou noir miniature à travers lequel il peut faire exister des boules flottantes de lave en fusion. De faibles forces magiques maintiendront ce projectile enflammé cohérent pendant une courte période de temps. En volant dans les airs, il se refroidira et finalement explosera.

TIPS: 

- Il n'y a aucun moyen de détruire les projectiles du Démon... évitez-les jusqu'à ce qu'ils explosent.
