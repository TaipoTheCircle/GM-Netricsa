GUERRE ARCTIQUE L96A1


WEAPON: Guerre arctique / L96A1
AMMO: 7,62 × 51 mm OTAN (.308 Winchester)
ROF: 12 tirs/min
WEIGHT: 7,5 kg
LENGTH: 1 200 millimètres

DESCRIPTION: 

Le L96A1 (Arctic Warfare) est un fusil de précision à verrou d'Accuracy International. Conçu pour la précision dans les environnements difficiles et froids, son châssis rigide, son canon martelé à froid et sa conception modulaire offrent une précision et une fiabilité constantes. Généralement équipé d'une crosse réglable, d'un bipied et d'une optique, il sert de plate-forme polyvalente pour les rôles anti-matériel et antipersonnel à longue portée.

NOTES: 

- Engagements de précision à longue portée – efficaces entre 800 et plus de 1 000 m selon les munitions et les conditions.
- Un mécanisme de boulon robuste et des composants modulaires simplifient la maintenance et l'adaptation sur le terrain.
- Largement utilisé par les unités de police et de tireurs d'élite militaires du monde entier ; adopté par plusieurs pays sous des désignations nationales.
