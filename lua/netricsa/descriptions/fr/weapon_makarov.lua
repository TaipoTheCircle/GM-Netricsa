PISTOLET MAKAROV


WEAPON: Pistolet Makarov
AMMUNITION: 9x18
ROF: 30 tours/min

DESCRIPTION: 

Le pistolet Makarov est un pistolet soviétique à chargement automatique conçu par l'ingénieur soviétique Nikolai Fedorovich Makarov en 1948. Il a été adopté pour le service en 1951. Il s'agit d'une arme personnelle d'attaque et de défense, conçue pour engager des ennemis à courte portée, utilisée par les forces armées et les forces de l'ordre, et l'un des pistolets les plus largement utilisés en URSS et dans l'espace post-soviétique. Il a été utilisé pendant la guerre du Vietnam, la guerre d’Afghanistan et la guerre civile syrienne, ainsi que dans presque tous les points chauds de l’espace post-soviétique.

NOTES: 

- Idéal pour un transport dissimulé.
- L'arme préférée des gangsters dans les années 90.
