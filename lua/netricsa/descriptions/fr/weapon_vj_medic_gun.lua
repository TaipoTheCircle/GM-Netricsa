PISTOLET MÉDICAL COMBINE ELITE


WEAPON: Combinez le pistolet médical Elite
AMMO: AR2 Pulse Rounds / Capsules Biomed (Larves de fourmis)
ROF: Faisceau de guérison + Feu à impulsion sous le canon

DESCRIPTION: 

Le Combine Elite Medical Gun est un outil de soutien tactique multifonctionnel qui combine la guérison pour les alliés et le tir de suppression pour l'ennemi. Son corps présente une palette de couleurs blanche et grise avec une poignée noire et une section de guérison verte brillante.

Une fine tige émettrice noire dépasse de l’avant du pistolet, ce qui génère un faisceau médical focalisé. Le premier mode de tir crée un flux de guérison soutenu qui restaure l’activité des tissus et des neuroimpulsions dans les organismes alliés en utilisant des biomatériaux recyclés provenant d’œufs de larves de fourmis.

Le deuxième mode active un module d'impulsion sous le canon qui tire des balles d'énergie AR2 standard directement sous la trajectoire du faisceau de guérison. Cela permet au médecin de soigner simultanément ses alliés et de supprimer ses ennemis sans changer d'arme.

Le système est conçu pour les unités d'élite Combine, où chaque soldat doit combiner soutien et puissance de feu.

NOTES: 

- La partie verte de la coque affiche le niveau d'agent de soin.
- Efficace dans les escouades mobiles et les équipes d'assaut d'élite.
- Dégâts inférieurs à ceux des fusils à impulsion à pleine puissance, mais compensent cela par sa fonction de guérison.
