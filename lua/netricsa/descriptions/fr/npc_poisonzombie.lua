TÊTE DE CRABE POISON ZOMBIE


CLASS: Zombie crabe, poison
METABOLISM: hôte parasite (plusieurs crabes)
PERCEPTION: Entité zombifiée
SIZE: 7-8 pieds
ENDURANCE: très élevé
HOSTILITY: très élevé
WEAPONS: griffes, crabes empoisonnés
REWARD: 400FC
THREAT: haut

DESCRIPTION: 

Cette créature grotesque est le résultat d'un hôte humain rattrapé non pas par un, mais par plusieurs crabes, en particulier la variété venimeuse. Leur silhouette gonflée et pourrie chancelle lentement mais est presque impossible à ignorer. Le parasite rend le corps frénétique, arrachant et lançant des crabes venimeux sur les ennemis. La chair de l'hôte pend librement à ses os, ce qui lui donne l'apparence d'un cadavre plutôt que d'un homme. Chaque gémissement qu’il pousse est un rappel effrayant de ce qui vivait autrefois à l’intérieur.

TIPS: 

- Restez à distance, les crabes venimeux peuvent vous paralyser instantanément.
- Ciblez les crabes accrochés à son corps pour les tuer plus facilement.
