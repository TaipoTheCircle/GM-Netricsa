TOR, LE COMMANDANT XEN


CLASS: Tor, le commandant Xen
METABOLISM: inconnu
PERCEPTION: Seigneur d'élite de Xen
SIZE: 9-10 pieds
ENDURANCE: haut
HOSTILITY: extrême
WEAPONS: bâton (explosion sonore, lévitation), invoque Alien Grunts
REWARD: 500FC
THREAT: épique

DESCRIPTION: 

Tor, le commandant Xen, est le bras droit du Nihilanth, doté d'un pouvoir et d'une intelligence stratégique considérables. Il porte un bâton énergétique capable de déclencher des explosions sonores dévastatrices et de faire léviter les ennemis pour infliger des dégâts ultérieurs. De plus, Tor peut invoquer des Alien Grunts sur le champ de bataille, renforçant ainsi sa présence et écrasant les intrus. Son approche est annoncée par des grognements profonds et des sons inquiétants, signalant un danger imminent. Tor est un lieutenant clé dans la hiérarchie Xen, imposant le respect et la peur de toutes les créatures de son domaine.

TIPS: 

- Concentrez le feu sur les Grunts invoqués pour réduire la pression sur le champ de bataille.
- Utilisez des armes à cadence de tir élevée pour interrompre ses attaques.
- Restez mobile pour éviter ses puissantes capacités sonores et de lévitation.
