DESCRIPTION: 

Mental a transporté ces soldats sur Terre depuis une partie inconnue de l'univers. Ils étaient autrefois une race de guerriers redoutables, mais comme ils ont conquis plus de planètes qu'ils ne savaient quoi faire, ils ont décidé de ralentir pendant un moment. La plupart de la population a pris des vacances attendues depuis longtemps sur l'une des plus belles planètes tropicales de leur système ; mais comme ils ne prenaient le soleil qu'à partir de 10 heures du matin. Entre 16 heures et 16 heures, après quelques années, la plupart d'entre eux n'avaient plus de peau sur le corps.

Their blue color comes from a tragic mistake: au lieu de crème solaire, ils ont accidentellement utilisé du colorant bleu industriel "Sin'ka". Cette tournure bizarre des événements les a tellement fait ressortir que Mental les a immédiatement réaffectés dans les rangs du redoutable corps LASER-GUNNER.

TIPS: 

- Essayez d'éviter leurs faisceaux laser rapides.
