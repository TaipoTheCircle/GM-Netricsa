ALTERNATIVE OVERWATCH COMBINE SHOTGUNNER


CLASS: Fusilier alternatif Overwatch Combine
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner une unité d'assaut au corps à corps
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Fusil de combat Spas-12, Grenades
REWARD: 700FC
THREAT: Haut

DESCRIPTION: 

L'Alternative Overwatch Combine Shotgunner est un Combine Soldier spécialisé conçu pour nettoyer les pièces, les tunnels et les environnements urbains denses. Contrairement aux unités d'élite standard, il porte un uniforme de couleur marécageuse conçu pour se camoufler dans les zones industrielles, les ruines et les zones contaminées. Sa visière brille en orange.

This type of soldier isn't designed for long-range firefights. His job is simple and extremely frustrating: réduisez rapidement la distance et transformez tout devant lui en un nuage de débris et de chair. Les augmentations améliorent ses réflexes et sa tolérance à la douleur, lui permettant d'avancer même sous un feu nourri.

TIPS: 

- A courte portée, un fusil de chasse est mortel.
- Dans les couloirs étroits, il vaut mieux reculer que de tester le nombre de tirs que l'on peut prendre.
- La visière orange est très visible dans l'obscurité – c'est parfois la seule chance de le voir en premier.
