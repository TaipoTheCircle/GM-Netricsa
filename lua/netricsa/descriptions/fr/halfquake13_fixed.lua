Spectacle de demi-séisme, faux espoir


Eh bien.... SOMOS est mort.

Le métal a refroidi. Les étincelles sont mortes. Les applaudissements, hélas, n’ont pas suivi.

Et maintenant, vous étiez dans le téléporteur, avec le sentiment que la fin était proche. Liberté, dénouement, moral... quoi qu'il arrive habituellement à la fin.

Erreur.

Vous êtes allongé sur une dalle de pierre. Vos bras et vos jambes sont immobilisés, comme des objets exposés dans un musée sur la souffrance d'autrui. Au-dessus de vous se trouve un plafond clairement conçu par quelqu'un qui déteste les surfaces horizontales. Devant, un pic. Grand. Convaincant. Avec l’intention philosophique d’entrer dans votre tête et de mieux me connaître.

La voix des haut-parleurs vous remercie. Poliment. Avec l’enthousiasme d’un service RH d’entreprise qui vous licencie pour toujours.

"Merci d'être mort".

Classique.

Quelque part au-delà des murs, des cris, des coups de feu et des fins étranges peuvent être entendus. Les forces mentales nettoient le spectacle. Les concurrents, le personnel, la sécurité : tout est mélangé dans un épisode majeur et très coûteux.

Et j'irai rédiger mon testament en code binaire... Merci, Sam. Au revoir.
