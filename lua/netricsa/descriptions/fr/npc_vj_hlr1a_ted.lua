TED BARNEY


CLASS: Ted Barney
METABOLISM: Humain, mâle
PERCEPTION: Agent de sécurité Black Mesa
SIZE: ~6 pieds
ENDURANCE: Moyen
HOSTILITY: Défensif / Allié
WEAPONS: Glock-17, Baton
REWARD: 40 FC
THREAT: Faible

DESCRIPTION: 

Ted Barney est un fidèle agent de sécurité de Black Mesa. Équipé d'armes standard et d'armures légères, il aide à défendre le personnel et les zones d'installation. Il réagit rapidement aux menaces mais n'est pas aussi bien entraîné ni résilient que H.E.C.U. ou des agents des forces spéciales.

Ted est fiable dans les situations de combat, mais devrait être soutenu lors de combats plus importants en raison de son endurance limitée.

TIPS: 

- Gardez-le à proximité pour vous soutenir.
- Utilisez une couverture : il ne peut pas survivre seul à un tir nourri.
- Efficace pour défendre les couloirs et les points de contrôle.
