MESA NOIRE, REPAIRE DE L'ICHTHYOSAURE



Sam, welcome to the flooded research basin. Scanners confirm you're not alone - deep below the surface, something massive is moving. Identification: Ichthyosaure, un prédateur aquatique de Xen. Vitesse : extrême. Appétit : sans fond. Probabilité de survie si vous nagez sans armes : ne la calculons pas.

Au-dessus de la ligne de flottaison, un petit pont de maintenance tient toujours. Là-dessus, un scientifique de Black Mesa agite désespérément, coincé entre le laboratoire inondé et les eaux infestées de monstres. Ses chances de survie sans votre intervention sont... mathématiquement sans importance.

L'ichtyosaure tourne comme un requin, attendant que vous plongez. Chaque éclaboussure le rapprochera, chaque seconde sous l'eau augmentera le risque de devenir son déjeuner. Les armes standard seront difficiles à utiliser ici : la visibilité est mauvaise et le recul n'est pas fiable. Vous devrez faire preuve de créativité si vous voulez le déjouer.
