DESCRIPTION: 

Les pigeons sont courants dans les zones urbaines et coexistent avec les humains et d’autres animaux sauvages. Ils sont passifs, souvent observés en groupes sur les toits, les rues et les places. Bien qu’inoffensifs, ils peuvent réagir à des perturbations soudaines, s’envoler et créer des repères visuels.

