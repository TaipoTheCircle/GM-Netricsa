UNE PLANÈTE QUI MATÉRIELLE LES PENSÉES


PLANET: Planète qui matérialise les pensées
TYPE: Planète terrestre, monde psychotrope
CLIMATE: Ambiance impropre à la respiration
MOONS: Inconnu
TERRAIN: Surface rocheuse, volcans, complètement dépourvus de vie
LOCATION: Système stellaire inconnu
NATIVE RACES: Aucun (des pensées matérialisées peuvent apparaître temporairement)
ALLIANCE: Inconnu
LOCATIONS: Charolet Crash Landing Site, réplique de Chamomile Valley

DESCRIPTION: 

L'atmosphère de la planète est totalement impropre à la respiration et la surface est un paysage rocheux sans vie parsemé de nombreux volcans. Il n’y a aucune végétation, aucune vie animale et aucun signe de vie organique.

La propriété unique de la planète est sa capacité à matérialiser les pensées de tout être sensible à sa surface. Ces pensées deviennent des objets très réels et tangibles existant dans l’espace proche de la planète. Or, ces objets se dématérialisent complètement dès que leur créateur quitte la planète.

Lorsque le Charolet, à court de carburant, effectua un atterrissage d'urgence sur cette planète, un pilote nommé Pin, qui se retrouva à bord, utilisa cette propriété pour résoudre le problème. Il a matérialisé une réplique exacte de sa vallée natale de Camomille, ainsi que de ses habitants, puis, à l'aide des plans stockés dans l'appareil, a créé un moteur ionique, lui permettant de quitter la planète.

As he left the planet, Pin left the materialized copies of his friends with advice: s'ils continuaient à penser l'un à l'autre, ils pourraient maintenir leur existence après son départ.

Les chercheurs pensent que la planète pourrait posséder un inconscient collectif ou une intelligence de terrain qui répondrait à l'activité mentale des visiteurs, mais la nature exacte de ce phénomène reste inconnue.

KEY LOCATIONS: 

- Charolet Crash Landing Site: La zone rocheuse où le navire écrasé a atterri.
- Replica of Chamomile Valley: Une réplique exacte et matérialisée du village natal de Pin, créée par le pouvoir de la pensée et existant grâce à l'énergie mentale de son créateur. Comprend des répliques exactes de bâtiments et d'habitants, possédant un semblant de conscience.
