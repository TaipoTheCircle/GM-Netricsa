SNARK DE GENMOD-262 DE L'UH-1


CLASS: Snark de Genmod-262
METABOLISM: Carnivore agressif
PERCEPTION: Insecte extraterrestre
SIZE: ~1 pied
ENDURANCE: Faible
HOSTILITY: Extrême
WEAPONS: Morsure, griffes, explosif à la mort
REWARD: 150 FC
THREAT: Haut

DESCRIPTION: 

Ces Snarks sont générés directement à partir de Genmod-262 dans l'univers UH-1. Contrairement aux snarks ordinaires, ils sont exceptionnellement rapides et imprévisibles, ce qui les rend extrêmement difficiles à frapper ou à contrôler. Ils se déplacent de manière chaotique, envahissant les cibles et explosant à leur mort, causant des dégâts supplémentaires aux ennemis proches.

Malgré leur petite taille, leur agressivité et leur nombre en font un danger sérieux lors de toute rencontre. Même les combattants expérimentés ont du mal à les contenir une fois qu’ils apparaissent.

TIPS: 

- Les cibles statiques en mouvement sont rapidement dépassées.
- Utilisez des armes à zone d'effet pour contrer les essaims.
- Évitez les espaces rapprochés ; leur mort explosive peut être mortelle dans des espaces restreints.
- Ciblez la source (Genmod-262) pour arrêter la reproduction.
