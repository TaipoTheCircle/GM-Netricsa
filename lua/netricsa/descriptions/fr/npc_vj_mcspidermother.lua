MÈRE ARAIGNÉE DU MONDE MINECRAFT


CLASS: Mère araignée
METABOLISM: Arthropode, Arachnide
PERCEPTION: Foule hostile, chef de famille
SIZE: 2,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Extrêmement élevé
WEAPONS: Morsure, saut, escalade de murs, coordination de meute
REWARD: 300FC
THREAT: Haut

DESCRIPTION: 

Ce n'est pas seulement un prédateur solitaire, mais une véritable matriarche, parcourant l'Overworld entourée de sa couvée. Contrairement aux araignées ordinaires, qui mènent une vie solitaire, la Mère-Araignée n'est jamais seule.

À la tombée de la nuit, cette bête dangereuse part à la chasse, accompagnée de 3 à 6 bébés araignées, dont l'un est assis sur son dos. L'araignée mère ressemble en apparence à une araignée normale, mais son comportement est radicalement différent. Elle est le centre de l’unité de combat et l’ensemble de l’essaim agit comme un seul organisme.

The mother spider behaves like a normal spider: elle attaque dans des conditions de faible luminosité ou lorsqu'elle est provoquée. Mais le principal danger réside dans ses bébés. Tous les bébés suivent leur mère sans relâche et attaquent n'importe quelle cible de son choix. Si vous frappez la mère araignée, tout l’essaim vous attaquera. Si vous frappez un bébé, la mère et le reste des frères et sœurs deviendront furieux.

Les bébés montés sur le dos de leur mère sont particulièrement traîtres : ils sont protégés par son corps et peuvent soudainement sauter au milieu d'une bagarre.

TIPS: 

- Tuez d'abord les nouveau-nés. Moins il reste de petites araignées en vie, plus l’attaque de l’essaim sera faible.
- Ne vous laissez pas encercler. La Mère Araignée et sa couvée tenteront de vous coincer.
- Utiliser des armes de destruction massive. Un coup d'épée bien ciblé avec le sort Blast Wave ou une potion de mal bien placée peut détruire plusieurs nouveau-nés à la fois.
- Soyez prudent la nuit. La Mère Araignée ne se trouve que la nuit et toujours avec sa couvée.
- La récompense pour avoir tué la Mère Araignée est petite - quelques fils et un œil d'araignée rare - mais la sécurité de la zone dégagée en vaut la peine.
