DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils reçoivent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Tomislav Pongrac is a walking encyclopedia of game development and the embodiment of Croteam's "mad scientist" philosophy. His oversized cranium houses an incredible range of skills, applied over decades. He started out working with pixel art on Amiga and DOS, progressed through 3D art and level design for the classic Serious Sam: The First and The Second Encounter, puis est devenu le concepteur principal de Serious Sam II.

Mais ses talents ne se limitent pas à une seule série. Il a été directeur créatif et concepteur de jeux pour le jeu de course d'arcade frénétique Gas Guzzlers, et a travaillé sur la conception sonore, la modélisation 3D, les textures et même les cinématiques. Son summum en tant que visionnaire était le simulateur de survie hardcore SCUM (2018), où il a été directeur, directeur créatif et artiste conceptuel original. Tomislav est la preuve vivante qu'un véritable développeur chez Croteam n'est pas obsédé par une seule discipline ; c'est le couteau suisse du studio, capable de donner vie à un projet à n'importe quelle étape et dans n'importe quel rôle.

Il était autrefois vivant, mais après que vous l'ayez tué dans le temple d'Hatchepsout, il est ressuscité des morts, devenant un zombie démoniaque.

TIPS: 

- Utilisez un couteau militaire ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", éliminez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
