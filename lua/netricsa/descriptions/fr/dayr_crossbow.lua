WEAPON: Arbalète de survie standard
AMMO: Carreaux d'arbalète
ROF: ~1 tir/seconde

DESCRIPTION: 

Une arbalète de survie basique, conçue pour un combat économique et fiable. Bien qu'il ne soit pas particulièrement puissant, il compense cela par la disponibilité de munitions et une conception simple.

L'arme tire des carreaux simples et nécessite peu d'efforts pour fonctionner, ce qui la rend populaire parmi les survivants novices. Avec la compétence Empoisonnement, elle peut être modifiée pour infliger des dégâts de poison supplémentaires, affaiblissant progressivement la cible.

NOTES: 

- Les boulons sont facilement fabriqués à partir de matériaux de récupération.
- Peut infliger des dégâts de poison supplémentaires avec la compétence appropriée.
- Efficace à courte et moyenne portée.
- Faibles dégâts de base par rapport aux arbalètes plus avancées.
- Un excellent choix en cas de pénurie de munitions pour armes à feu.
