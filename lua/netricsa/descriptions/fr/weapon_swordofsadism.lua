ÉPÉE DU SADISME


WEAPON: Épée du sadisme
AMMO: Résonance d'énergie cristalline
ROF: ~75 coups/min

DESCRIPTION: 

Une lame cristalline légendaire fabriquée à partir d'une émeraude verte rare d'origine inconnue. Le matériau possède un treillis ultra-dense, permettant à la lame de couper presque tous les matériaux, y compris les armures de l'Alliance et la chair de classe Grand-Pas.

Les organisateurs de HALF-QUAKE ont trouvé l'artefact dans des ruines antiques et l'ont adapté pour le spectacle. Lors de l'impact, l'épée crée une onde de résonance qui déchire la cible au niveau moléculaire.

NOTES: 

- Désintègre la plupart des entités d'un seul coup.
- Efficace à courte portée, mortellement catastrophique pour les adversaires dans les couloirs étroits.
- Nécessite un porteur fort.
