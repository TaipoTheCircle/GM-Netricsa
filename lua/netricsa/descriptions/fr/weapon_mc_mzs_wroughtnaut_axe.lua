HACHE DE MILLE METAUX


WEAPON: Hache aux mille métaux
ROF: ~0,9 coups/seconde
WEIGHT: 9,5 kg

DESCRIPTION: 

A massive melee weapon forged from alloys of multiple metals, possessing exceptional strength and heaviness. It combines deadly power with resistance to destruction: la hache ne se brise jamais au combat et reste efficace même avec une utilisation constante.

In combat, the weapon offers several attack types: des coupures standards, de larges mouvements pour les attaques de groupe et de puissantes frappes verticales au sol, créant une onde de choc qui se propage à travers le sol et impacte les ennemis à distance, les faisant voler. La portée de l'onde de choc dépend de la distance à la cible.

NOTES: 

- Efficace contre des cibles uniques et des groupes à moyenne portée.
- Un large coup endommage toutes les cibles dans un cône devant l'utilisateur.
- Un claquement vertical au sol crée une onde de choc, repoussant et endommageant les cibles derrière lui.
- Son indestructibilité absolue en fait un outil fiable pour les affrontements prolongés.
