DESCRIPTION: 

Le SSS Centerprice est un vaisseau spatial sirien. Il y a plusieurs milliers d'années, les Siriens utilisaient le SSS Centerprice comme l'un de leurs principaux moyens de transport lors de leurs voyages interstellaires. Finalement, ils atteignirent le système solaire et découvrirent la Terre. Ils utilisaient le navire pour visiter la Terre et survoler la Grande Pyramide en Égypte chaque fois que les Égyptiens participaient à leurs rituels religieux.

Peu de temps après que les Siriens aient appris l'attaque de Mental sur Sirius, les survivants ont ramené le Centerpice dans l'espace et l'ont abandonné sur Titan, la lune de Saturne, à titre temporaire. Ils pensaient que les humains étaient trop primitifs à l’époque pour maîtriser les voyages dans l’espace, ils abandonneraient donc le vaisseau jusqu’à ce que l’humanité ait suffisamment progressé technologiquement pour le trouver et que Mental soit entré en sommeil.

Les Siriens ont créé un autre vaisseau après que vous n'ayez pas répondu à vos attentes et que vous ayez perdu le premier, en l'écrasant au cours d'une mission, mais il vise désormais à vous éliminer.

Pour les Siriens, il ne s’agissait pas d’un simple « accident », mais d’une insulte à leur technologie.

