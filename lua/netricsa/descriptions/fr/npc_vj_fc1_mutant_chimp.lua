CHIMPANZE MUTÉ DE L'ARCHIPEL KRIEGER


CLASS: Chimpanzé muté
METABOLISM: Trigène - un primate mutant.
PERCEPTION: Chasseur tactique
SIZE: 4,1 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Griffes, attaques sautées.
REWARD: 400FC
THREAT: Haut

DESCRIPTION: 

The most monstrously deformed of all primates, trigenes. Retaining only a vague resemblance to chimpanzees, they have acquired reptilian features: un museau allongé, quatre yeux jaunes et des membres à trois doigts fourchus. Les restes de fourrure ne restent que le long de la colonne vertébrale. Idéalement adaptés aux attaques furtives, ils peuvent pénétrer dans les conduits de ventilation grâce à leur petite taille.

TIPS: 

- Très vulnérable aux tirs à la tête - malgré leur vitesse, les tirs de précision sont efficaces.
- Gardez vos distances : à courte portée, leurs attaques à grande vitesse sont presque imparables.
