LIBELLULE DE L'ARCHIPEL KRIEGER


CLASS: Libellule
METABOLISM: Insecte, prédateur
PERCEPTION: Insecte prédateur
SIZE: 0,13 pied
ENDURANCE: Faible
HOSTILITY: Aucun
WEAPONS: Aucun
REWARD: 1 FC
THREAT: Aucun

DESCRIPTION: 

Insecte volant prédateur, que l'on trouve souvent à proximité des plans d'eau et dans les espaces ouverts de l'archipel. Il se distingue par sa grande maniabilité et sa vitesse de vol. Il peut planer dans les airs et changer brusquement de direction. Cela ne représente aucune menace. Sa présence ajoute du dynamisme et du réalisme à l’atmosphère tropicale, surtout les jours ensoleillés près de l’eau.

TIPS: 

- Pas un ennemi ou une ressource. Cela ne sert à rien d’attaquer.
