Y.S.M. CRABE ZOMBIE, COMMUN (APERÇU)


CLASS: Technicien de crabe zombie de la base Delta, commun
METABOLISM: Hôte humain + crabe parasite de Xen
PERCEPTION: Entité zombifiée, ancien spécialiste de la technologie
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Griffes, mordre, saisir
REWARD: 200FC
THREAT: Haut

DESCRIPTION: 

C'est techniquement le même que le crabe zombie commun de la base Delta, juste dans un uniforme différent. Mais les survivants affirment qu’il existe des différences, et qu’elles ne sont pas qu’apparentes.

Si les zombies habituels de la base sont d'anciens scientifiques en blouse grise, alors les techniciens sont ceux qui réparaient les générateurs, grimpaient dans les systèmes de ventilation et savaient où se trouve chaque levier.

La principale différence entre un technicien et un scientifique n'est pas la force ou la vitesse, mais le comportement. Certains survivants jurent en avoir vu un réparer un climatiseur cassé. (Il s'est simplement cogné la tête contre le mur jusqu'à ce qu'il tombe.)

Un tir dans la tête détache la tête du Technicien.

TIPS: 

- Un zombie technicien n'est ni plus fort ni plus rapide qu'un zombie commun. Ils ont juste des vêtements différents.
- La tête se détache en tuant, tout comme celle d'un scientifique.
- Gardez vos distances, tirez et ne les laissez pas vous entourer.
