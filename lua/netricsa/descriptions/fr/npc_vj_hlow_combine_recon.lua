COMBINEZ LE SOLDAT DE RECONNAISSANCE D'OVERWATCH


CLASS: Combiner Overwatch Recon Soldier
METABOLISM: Humain augmenté, mâle
PERCEPTION: Reconnaissance et appel aux renforts du Combine
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Haut
WEAPONS: SMG modifié (single-shot), pistolet lance-fusées
REWARD: 350FC
THREAT: Très élevé

DESCRIPTION: 

Le Combine Overwatch Recon Soldier est les yeux et les oreilles du Combine sur les lignes de front. Il n’est pas conçu pour un combat prolongé ou une confrontation directe. Sa mission principale est de localiser l'ennemi, de déterminer sa position et de transformer immédiatement un incident localisé en une opération punitive à grande échelle.

Il est armé d'une mitraillette modifiée qui ne tire que des coups simples. Cela augmente la précision, réduit le bruit et économise les munitions. Cependant, l'outil principal du soldat est le pistolet lance-fusées bleu. Un coup de feu dans le ciel, et peu de temps après, un transport d'élite Combine transportant une troupe de soldats apparaît dans la zone.

L'armure du reconnaissance est intentionnellement faible. Il doit se déplacer rapidement, se cacher, changer de position et éviter les tirs directs. S’il n’est pas éliminé à temps, la situation deviendra rapidement incontrôlable.

La reconnaissance se bat rarement jusqu'à la mort. Son objectif est de survivre suffisamment longtemps pour appeler des renforts. Après cela, il peut soit battre en retraite, soit fournir un tir de flanc aux soldats qui arrivent.

TIPS: 

- Éliminez-le le plus rapidement possible : une seule fusée éclairante peut tout gâcher.
- Son armure faible le rend vulnérable même aux armes légères.
- Ne lui laissez pas de temps ni de distance pour manœuvrer.
