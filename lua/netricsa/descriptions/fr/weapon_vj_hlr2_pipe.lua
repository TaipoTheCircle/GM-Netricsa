TUYAU MÉTALLIQUE


WEAPON: Tuyau en métal
ROF: ~60 balançoires/min

DESCRIPTION: 

Arme de mêlée improvisée utilisée par les réfugiés et les civils de la ville 17. Simple mais robuste, le tuyau métallique inflige des dégâts contondants à courte portée. Principalement une arme de dernier recours pour l'auto-défense contre les forces combinées ou les crabes.

NOTES: 

- Utilisation illimitée ; aucune munition requise.
- Efficace uniquement en espace rapproché.
- Inflige des dégâts contondants modérés ; peut faire chanceler les ennemis faibles.
- Fonctionne comme un outil pratique dans les situations de survie.
