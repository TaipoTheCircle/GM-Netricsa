COMBINEZ LE SOLDAT TACTIQUE D'ÉLITE D'OVERWATCH


CLASS: Combinez le soldat tactique Overwatch Elite
METABOLISM: Humain augmenté, mâle
PERCEPTION: La force de frappe tactique la plus élevée du Combine
SIZE: 5,9 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Absolu
WEAPONS: Fusil à impulsion AR2, Grenades
REWARD: 1000FC
THREAT: Critique

DESCRIPTION: 

The Combine Overwatch Elite Tactical Soldier is the pinnacle of Overwatch's ground forces. He wears the standard white Elite Soldier suit, topped with a black, reinforced flak jacket. This combination makes the soldier virtually invulnerable to small arms fire and completely eliminates vulnerability to headshots: les tirs à la tête sont inefficaces en toutes circonstances.

La Visière Rouge n'est pas seulement une optique, mais une interface de combat liée aux protocoles centraux du Combine. Il suit les cibles, prédit les trajectoires et coordonne les actions avec d'autres unités d'élite. Un seul regard d’un tel soldat ressemble à une phrase prédéterminée.

Contrairement aux soldats d'élite standards, la variante Tactique ne repose pas sur la surprise ou l'agression démonstrative. Il avance méthodiquement, contrôlant l'espace, supprimant la résistance avec des tirs à impulsions AR2 précis et nettoyant les zones avec des grenades. Ses mouvements se font sans hâte et les décisions sont prises plus rapidement que l'ennemi ne peut même reconnaître la menace.

TIPS: 

- Oubliez les tirs à la tête de précision, cela ne sert à rien.
- Utilisez des armes lourdes, des explosifs et des pièges.
