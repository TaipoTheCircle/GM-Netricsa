BLACK MESA, À L'INTÉRIEUR DU COMPLEXE DE SÉCURITÉ


Well, you're inside. The walls are oppressive, the air smells of metal and disinfection - a typical security headquarters. Now we need to get a foothold: ici, chaque objet peut être la clé de la survie. Ou à l'échec.

What to do next: 

Gilet pare-balles

There are rows of vests in the armory. Put one on. Not for beauty: quand le désordre commencera, chaque assiette comptera. Oui, c'est dur, mais il vaut mieux transpirer que saigner.

Toilettes (au cas où)

Yes, even in the apocalypse, the superman needs his own space. But remember: Je peux tout voir. La puce dans votre crâne traduit chaque pas. Alors ne vous attardez pas et n'essayez pas de cacher quelque chose dans le réservoir de vidange. Je le sais déjà.

Pistolet

Sans armes, c'est comme ne pas avoir de mains. Il y a un pistolet de service standard dans l'armurerie. Prenez-le, vérifiez le clip, mettez la sécurité. Ne tirez pas inutilement, mais n'attendez pas non plus que quelqu'un vous vise.

Caméras de surveillance

Le panneau de contrôle est au deuxième étage. L'ascenseur est à proximité, le bouton d'appel clignote en rouge. Levez-vous, asseyez-vous devant les moniteurs et attendez que tout commence.
