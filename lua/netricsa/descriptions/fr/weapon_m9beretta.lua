PISTOLET BERETTA M9


Weapon: Beretta M9
Ammunition: 9x19mm
ROF: 15 tours/min

DESCRIPTION: 

La carabine M4A1 est une carabine très réputée et efficace. Il est utilisé par presque tous les pays de l’OTAN et a été développé aux États-Unis. Il est basé sur le M16A2 et était initialement destiné à armer les équipages de véhicules de combat et les équipages d'armes et d'équipements militaires. Cette arme a été utilisée dans presque tous les conflits modernes à travers le monde. Il peut être fortement modifié en ajoutant des viseurs, des viseurs laser, des poignées tactiques, etc.

NOTES: 

- C'est lourd et puissant.
- Vous pouvez le tenir à deux mains et sauter tout en filmant au ralenti.
- Vous pouvez être volé ou tué avec, mais vous pouvez aussi vous défendre avec.
