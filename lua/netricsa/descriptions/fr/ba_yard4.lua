PARKING DE L'ENTREPÔT BLACK MESA, H.E.C.U. SOLDATS DANS LA RUE


Eh bien, Sam. De l'air frais à nouveau. Si l’on considère un cocktail de poudre à canon, d’ozone et une équipe scientifique légèrement fuyante, bien sûr.

Before us is the Black Mesa warehouse parking lot. A place where people usually leave their cars. Today, bodies were left here. Scientists, Mental's soldiers, and H.E.C.U. soldiers among them, like an exhibit called "Situation Control." Spoiler: la situation est hors de contrôle.

Harold managed to say the main thing before he finally stopped talking. Doctor Rosenberg. The name sounds reassuring. People like that usually either save the world or accidentally blow it up again. Right now, he's busy with a more mundane task: étant retenu en otage. On ne sait pas exactement où. Mais si l'armée le détient, alors il est toujours en vie. H.E.C.U. Ils n'aiment pas jeter tout de suite les objets utiles.

Faites attention aux escaliers. Il y a des militaires là-bas. Beaucoup d'entre eux. Trop pour une opération « nous ne sommes là que pour peu de temps ». Et entre eux, les forces de Mental vacillent.

Le portail que Rosenberg est en train de construire pourrait être notre ticket de sortie.

Mais il n'y a plus le choix maintenant.

Nous devrons traverser la rue.
