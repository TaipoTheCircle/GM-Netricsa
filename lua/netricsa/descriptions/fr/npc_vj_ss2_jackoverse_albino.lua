YÉTI


CLASSE : Yéti
MÉTABOLISME : mammifère intelligent
PERCEPTION : Géant sympathique et homme de famille
TAILLE : ~22 pieds
ENDURANCE : Élevée
HOSTILITÉ : Faible
ARMES : Griffes, Force
RÉCOMPENSE : 300 FC
MENACE : Moyenne

DESCRIPTIF :

Ce père de famille attentionné vit avec sa famille dans des terres enneigées et ramène chaque jour à la maison des proies, principalement des sangliers. Il allie supériorité physique et tempérament paisible, préférant éviter les conflits autant que possible.

Un jour, un homme l'a rencontré par hasard et a commencé à prendre des photos. Le Yéti n'avait pas peur et a même posé, sans se rendre compte des conséquences. Après la parution des images aux informations, le Yéti a été considéré comme une menace et un chasseur a été envoyé pour le traquer. Cependant, en voyant la famille aimante du Yéti, le chasseur n'a pas pu terminer sa mission. Il a contacté le président des États-Unis et a signalé que l'alarme n'était pas fondée.

Malgré cela, la Zone 51 continue de chasser le Yéti, le considérant comme une créature rare et potentiellement dangereuse. Cela fait de sa vie et de celle de sa famille une cible constante pour les agences de renseignement. Chaque jour, le Yéti est obligé de vérifier la zone à la recherche de signes de présence non autorisée et de protéger le périmètre contre d'éventuelles interférences des agences de renseignement.

Le Yéti passe la plupart de son temps à rechercher des ressources dans des températures extrêmement froides. Il passe ses soirées en famille, assurant leur sécurité et leur confort. Sa priorité absolue est le bien-être de ses proches et elle est prête à tout pour y parvenir.

CONSEILS :

- Le Yéti n'est pas agressif. Si vous le rencontrez dans la neige, ne paniquez pas et n'essayez pas de le tuer.
- Ne vous approchez pas de son domicile ou de sa famille. C'est la seule chose qui peut l'irriter.
- Si vous le voyez par accident, partez. Cela ne traque pas les gens.
- N'essayez pas de le prendre en photo. Cela pourrait attirer l’attention de la zone 51.
- Dans un combat, le Yéti n'est dangereux que si sa famille est en danger. Si vous ne menacez pas ses proches, il ne vous touchera pas.