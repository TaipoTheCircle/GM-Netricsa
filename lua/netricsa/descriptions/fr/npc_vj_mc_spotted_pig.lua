COCHON IMPECCABLE DU MONDE MINECRAFT


CLASS: Cochon tacheté
METABOLISM: Mammifère à sang chaud
PERCEPTION: Animal passif joyeux
SIZE: 3 pieds
ENDURANCE: Moyenne
HOSTILITY: Faible
WEAPONS: Aucun
REWARD: 30 FC
THREAT: Minimal

DESCRIPTION: 

L'une des variétés les plus charmantes du cochon commun, ce cochon tacheté est incroyablement doux. Il aime vivre avec ses amis les cochons et chercher des friandises avec eux à travers les plaines herbeuses.

Ce cochon a une apparence touchante et reconnaissable. Sa couleur de base est aussi terne que celle du cochon pâle, mais des taches brunes rondes sont dispersées sur sa peau. On dirait qu'elle a été baignée de chocolat, mais pas complètement, juste par endroits, ou comme si le soleil l'avait embrassée à travers les feuilles.

She behaves no differently from a regular pig: elle erre dans les plaines herbeuses en compagnie de ses camarades cochons, évite la lave et les falaises et s'enfuit pendant quelques secondes lorsqu'elle est endommagée. Mais son activité principale est de chercher des friandises et de profiter de la vie parmi ses congénères cochons.

TIPS: 

- Le principal atout du cochon tacheté est sa gentillesse exceptionnelle.
- Élevez ces cochons pour obtenir une progéniture avec la même coloration joyeuse.
- Le cochon tacheté est superbe dans un enclos sur une plaine herbeuse, son habitat naturel.
- Si vous cherchez des friandises à travers les plaines en compagnie de cochons tachetés, vous êtes déjà au Zen.
