CANON DEPLACEMENT XV11382


WEAPON: Canon déplaceur (Projet XV11382)
AMMO: Cellules énergétiques dimensionnelles
ROF: ~1 tir/3 secondes

DESCRIPTION: 

Arme de téléportation expérimentale développée par Black Mesa avant l'incident survenu dans l'installation. Tire des explosions d'énergie concentrée qui peuvent déplacer des cibles vivantes et non vivantes sur de courtes distances. Le mode de tir alternatif téléporte l'utilisateur vers un emplacement aléatoire, permettant un repositionnement tactique ou une évasion. Considéré comme très instable mais extrêmement polyvalent.

NOTES: 

- Primary fire: téléporte les cibles à l'impact, causant des dégâts importants.
- Alternate fire: téléporte l'utilisateur à un point aléatoire sur la carte.
- Extrêmement puissant mais risqué ; une mauvaise utilisation peut nuire à l’utilisateur.
- Disponibilité limitée ; le projet est resté expérimental.
- Utilise un mini réacteur à uranium comme source d'énergie, similaire au Tau Cannon ou au Gluon Gun.
