SHREK, LE TANK ZOMBIE DU LOIN TRÈS LOIN


CLASS: Shrek, le char zombie
METABOLISM: Ogre mutant biologique
PERCEPTION: Grand combattant de mêlée infecté d'élite
SIZE: 10 pieds
ENDURANCE: Extrêmement élevé
HOSTILITY: Absolu
WEAPONS: Poings, pierres, béliers, tirets, rugissement
REWARD: 5000FC
THREAT: Absolu

DESCRIPTION: 

This specimen of Infected evokes mixed feelings in survivors: d’un côté, ils voient un énorme mutant vert et chauve arrachant des morceaux d’asphalte et les lançant. De l'autre, la chanson "Smash Mouth All Star" retentit depuis sa poitrine. Shrek a apparemment avalé une radio juste avant d'être infecté, et maintenant le haut-parleur est coincé quelque part dans son estomac, en train de lancer "Hé maintenant, tu es une star, lance ton jeu, va jouer!" à plein volume.

Otherwise, his behavior is identical to that of a classic Tank. He feels no pain, doesn't hesitate to stop at obstacles, and has one goal: pour écraser les survivants contre le mur. Ses mouvements préférés sont de déchirer un morceau de trottoir et de le lancer sur sa cible, et si la distance se rapproche, il l'écrase simplement avec sa main lourde. L'impact est si puissant que le survivant est projeté une bonne dizaine de mètres en arrière.

Parfois Shrek rugit. Un Tank ordinaire crie si fort que c'en est à couper le souffle. Celui-ci rugit tout aussi fort, mais après le rugissement, le haut-parleur hurle : « Je ne suis pas l'outil le plus tranchant du hangar ». Les survivants ne savent pas s'ils doivent rire ou pleurer. La plupart choisissent de pleurer.

L'origine de ce spécimen est inconnue. Certains disent que le laboratoire essayait de croiser le virus avec un écosystème marécageux. D'autres disent que c'est juste un grand type en cuir vert qui a trop aimé ce morceau et l'a emporté dans la tombe. D’autres encore tirent sans poser de questions.

TIPS: 

- Shrek jette des pierres en esquivant. Ils volent lentement, mais ils font mal.
- Il est mortel au corps à corps. Ne vous approchez pas trop, sauf si vous voulez vous transformer en boue des marais.
- Vous pouvez savoir à quel point Shrek est proche grâce au volume.
- Si vous entendez « Tant de choses à faire, tant de choses à voir », et que le sol sous vous tremble, courez. Courez maintenant.
- Vous pouvez le tuer, mais cela demandera une tonne de munitions et de chance.
- On dit que si vous écoutez attentivement, le refrain jouera. Mais vous n'écouterez probablement pas, vous serez simplement heureux s'il se tait.
