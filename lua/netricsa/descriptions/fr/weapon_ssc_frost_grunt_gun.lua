FUSIL LASER GIVRÉ


WEAPON: Fusil laser de givre
AMMO: Faisceaux d'énergie glacés
ROF: Coups doubles

DESCRIPTION: 

Le Frost Laser Rifle est une version modifiée du fusil laser standard, adapté pour une utilisation dans des conditions extrêmement froides. La structure de l'arme est renforcée avec des matériaux résistants à la chaleur et le corps présente un design mis à jour avec des éléments jaunes et bleus servant de marqueurs visuels de sa spécialisation par temps froid.

L'arme conserve le principe de fonctionnement du modèle de base, tirant deux impulsions d'énergie d'affilée à chaque pression sur la gâchette. Cependant, au lieu de faisceaux standard, le fusil tire des lasers de glace bleue infusés d'énergie cryonique. Lors de l'impact, ceux-ci infligent non seulement des dégâts, mais ralentissent également l'ennemi en refroidissant rapidement les tissus, l'armure et les mécanismes d'entraînement.

Le fusil laser Frost est alimenté par un noyau de plasma modifié avec cryostabilisation, empêchant la surchauffe et la dégradation des composants à des températures glaciales. Un réacteur intégré recharge en permanence l'arme via un chargeur, garantissant un fonctionnement stable lors de combats prolongés et dans des conditions arctiques.

NOTES: 

- Entièrement adapté au froid et aux basses températures.
- Chaque tir émet une double impulsion glaciale, ralentissant l'ennemi.
- L'effet cryo réduit la mobilité des cibles vivantes et le temps de réaction des adversaires mécaniques.
- Particulièrement efficace à moyenne portée et lors de tirs de groupe.
- Moins de pouvoir de pénétration contre les armures lourdes, mais compense cela par un effet ralentisseur.
- Destiné à l'infanterie légère et aux unités opérant en zones froides.
