CLASS: Kukulkán
METABOLISM: inorganique
PERCEPTION: ancienne divinité
SIZE: variable
ENDURANCE: extrême
HOSTILITY: faible
WEAPONS: tornades, boules de tornade
REWARD: 500000FC
THREAT: haut

DESCRIPTION: 

Ancienne divinité maya, Kukulkán n’a rien à voir avec le mental.  En fait, c’est vraiment un bon dieu.  On pense que Kukulkán contrôle le vent et est donc très apprécié des Mayas.  Mais il est aussi le gardien d'un portail laissé par les Siriens, et il faut dire très sérieux !  Kukulkán ne peut pas être détruit - mais c'est un joueur loyal - une fois vaincu, il s'évaporera dans les airs et ouvrira le portail au vainqueur en signe de respect.  Il est vraiment un dieu à tous points de vue.

TIPS: 

- Évitez les tornades ou vous serez plus près du paradis en un rien de temps.
- Kukulkán est très résistant aux balles, utilisez plutôt des armes explosives.

