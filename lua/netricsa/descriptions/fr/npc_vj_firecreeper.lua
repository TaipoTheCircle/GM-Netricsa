CREEPER AMICAL DU MONDE MINECRAFT


CLASS: Plante grimpante de feu
METABOLISM: Plante-Pyrotechnique
PERCEPTION: Foule hostile, Pyro ambulant
SIZE: 5,5 pieds
ENDURANCE: Faible
HOSTILITY: Extrême
WEAPONS: Auto-détonation avec propagation du feu
REWARD: 150 FC
THREAT: Moyen-élevé

DESCRIPTION: 

C'est ce qui arrive lorsqu'une plante grimpante ordinaire décide qu'exploser est trop ennuyeux et que le véritable élément est le feu. Cette variété de plantes grimpantes élémentaires diffère de leurs homologues à poudre par leur feuillage orange vif, qui ressemble à des braises et des flammes fumantes. Il est difficile d'en repérer un dans le désert ou dans le Nether, mais lorsqu'il y en a un à proximité, vous le saurez instantanément.

Fire creepers primarily inhabit hot biomes: les déserts, les savanes, les mesas et, bien sûr, le Nether (l'Enfer). Ils n'ont pas peur de la lave et peuvent prospérer dans des endroits où les plantes grimpantes ordinaires seraient incinérées.

Lorsqu'elle explose, une plante grimpante de feu ne détruit pas les blocs avec la même force qu'une plante grimpante ordinaire, mais son onde de souffle propage le feu sur une distance considérable. Toute la zone située dans le rayon de l'explosion est engloutie par des flammes, qui se propagent rapidement au bois, aux feuilles, à la laine et à d'autres matériaux inflammables. Une telle explosion peut transformer une forêt en un enfer ardent et une maison en bois en cendres en quelques secondes.

TIPS: 

- Ne combattez pas une plante grimpante à proximité de bâtiments en bois. Une explosion et votre maison sera réduite en cendres.
- Cette plante grimpante est particulièrement dangereuse en forêt. Les flammes se sont rapidement propagées aux arbres et vous pourriez vous retrouver entouré de feu.
- Si vous êtes dans le désert ou dans les enfers, soyez doublement prudent. Dans le désert, les plantes grimpantes se fondent dans le sable et dans le Nether, le feu est partout.
- Gardez un seau d'eau à portée de main. Une explosion peut rapidement éteindre un incendie qui se propage.
- Les lianes de feu n'octroient pas d'expérience et ne lâchent pas de ressources précieuses, autres que la poudre à canon ordinaire et parfois le charbon.
- Tuer une plante grimpante de feu a une chance de laisser tomber de la poudre de feu, qui est utilisée pour fabriquer des explosifs élémentaires. Les explosifs fabriqués à partir de poudre de feu créent une boule de feu lorsqu'ils explosent, enflammant tout ce qui les entoure.
