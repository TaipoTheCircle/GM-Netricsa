LA SORCIÈRE-ÉPOUSE D'ACHRIMAN


CLASS: Mariée-sorcière d'Ahriman
METABOLISM: Entité semi-corporelle
PERCEPTION: Sorcière télékinésique
SIZE: 7,4 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Strangulation télékinétique, lancement de débris/projectiles énergétiques, téléportation, déviation de projectile
REWARD: 8000FC
THREAT: Haut

DESCRIPTION: 

A former sorceress of the Grand Council, deceived and enslaved by Lord Achriman. An entity trapped in a semiotic limbo between dimensions, rendering it invulnerable until it materializes to attack. She has a humanoid female form with six tentacles on her back, a metal bikini, and stumps instead of legs. Her tactics involve constant teleportation and the use of powerful telekinesis for attack: elle étrangle ses victimes à distance, soulève et brise les os, et leur lance des débris et des projectiles énergétiques. Elle est extrêmement dangereuse lorsqu'elle est combinée avec d'autres ennemis, car elle distrait et restreint la mobilité.

TIPS: 

- Il est préférable d'utiliser l'A-24 "Devastator" et des fusils de sniper. Le canon SBC tuera d'un seul coup chargé.
- Son emprise est interrompue si la ligne de vue est rompue (se cachant derrière un abri) ou si elle est endommagée.
