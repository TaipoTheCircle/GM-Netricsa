PALADIN DE LA GARDE IMPÉRIALE DE NIRN


CLASS: Paladin de la Garde Impériale
METABOLISM: Impérial, mâle
PERCEPTION: Haute Garde du Palais Impérial / Infanterie d'élite
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Conditionnel
WEAPONS: Épée longue en acier fin, bouclier de la Garde Impériale
REWARD: 1000FC
THREAT: Haut

DESCRIPTION: 

Le palais impérial au cœur de Cyrodiil est gardé par l'élite de l'élite : les paladins de la garde impériale. Tandis que des gardes ordinaires patrouillent dans les rues de la Cité Impériale, ces guerriers en armure de cérémonie protègent la résidence de l'Empereur lui-même et les salles des Anciens.

Leur apparence force le respect. Un ensemble complet d'armures, comprenant un casque, des bottes, des jambières et des brassards, crée l'image d'un gardien inébranlable de la loi. Ils manient invariablement des armes de haute qualité : une épée longue en acier fin et un solide bouclier de la Garde Impériale.

These guards are generally friendly and greet visitors, reminding them of the rules of etiquette: "S'il vous plaît, comportez-vous tranquillement par respect pour le palais impérial" ou "N'oubliez pas que pénétrer dans le palais impérial est une infraction grave. Ne dépassez pas le premier et le deuxième étage." Ils sont disposés à indiquer les directions vers différentes parties de la ville et à signaler les guildes et les tavernes.

Mais leur hospitalité a ses limites.

Tuer un paladin n'est pas une tâche anodine. Leur armure est correcte et leur fine épée en acier inflige de sérieux dégâts. Bien que leur cuirasse et leur casque ne puissent pas être retirés du cadavre, les armes et la clé du palais peuvent leur être retirées.

TIPS: 

- Si vous êtes un citoyen respectueux des lois, profitez simplement de la compagnie et n'essayez pas de vous faufiler dans les zones restreintes du palais. Les paladins vous avertiront une fois.
- Si le combat est inévitable, utilisez la magie à distance ou un arc pour affaiblir le paladin avant qu'il ne s'approche.
- N'oubliez pas que leur armure ne vous permet pas de récupérer les pièces les plus précieuses du champ de bataille, alors ne les comptez pas comme butin.
