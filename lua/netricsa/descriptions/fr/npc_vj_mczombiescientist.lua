SCIENTIFIQUE ZOMBIE DU MONDE MINECRAFT


CLASSE : Scientifique zombie
MÉTABOLISME : Mort-vivant
PERCEPTION : Mort-vivant intelligent, lanceur de potions
TAILLE: 6,2 pieds
ENDURANCE : Moyenne
HOSTILITÉ : Élevée
ARMES : Potions de faiblesse et de dégâts
RÉCOMPENSE : 300 FC
MENACE : Moyenne

DESCRIPTIF :

Il s'agit d'une espèce mort-vivante rare dont le cerveau conserve suffisamment de matière grise pour se souvenir de ses vieilles habitudes. Contrairement à ses frères stupides, qui chargent simplement de front, ce spécimen préfère les tactiques d'un alchimiste. Il ne s'engage pas dans des combats rapprochés ; il bombarde sa cible de potions à distance sûre.

Il porte une blouse blanche sur une chemise bleue, des lunettes noires, une jolie cravate marron et une ceinture. Il ressemble presque à un employé vivant, si l’on ignore sa peau verte. Il semble qu'il soit mort juste à son bureau, puis s'est simplement levé et a continué ses expériences, cette fois sur des créatures vivantes.

Au combat, le Zombie Scientist utilise deux types de potions. Une potion de dégâts endommage directement la cible, tandis qu'une potion de faiblesse réduit la puissance d'attaque, laissant la victime presque sans défense contre les autres monstres. Le scientifique évite les combats au corps à corps : si un ennemi s'approche trop près, les morts-vivants préfèrent battre en retraite et reprendre le feu à distance.

Il ne brûle pas au soleil comme les zombies normaux. Apparemment, son pelage le protège aussi bien des rayons UV que de l'acide.

CONSEILS :

- Ne laissez pas le scientifique se mettre à portée de lancer. Fermez-vous rapidement et attaquez au corps à corps.
- Il est impuissant au corps à corps et ne pourra pas utiliser de potions.
- Esquivez les potions : elles volent en arc de cercle.
- Utilisez un bouclier. Cela ne bloque pas l'effet de la potion, mais réduit les dégâts directs des coups.
- Si vous êtes affaibli, reculez-vous à l'abri et attendez la fin de l'effet. Se battre dans cet état signifie une mort certaine.
- Les potions de dégâts vous infligent des dégâts ainsi qu'aux autres monstres.