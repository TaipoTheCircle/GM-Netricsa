SPECTACLE DEMI-SÉMEMENT, SOMOS


Nous avons donc enfin atteint le stand de l'enfer du réalisateur.

SOMOS se tient au centre de la pièce, entouré d'un bouclier énergétique, tel un roi effrayé par ses propres sujets. Les armes conventionnelles sont inutiles ici. Les balles rebondissent, comme les plaintes du public concernant la politique de la série.

Les sadiques se sont avérés non seulement des sadiques, mais aussi des ingénieurs. La machine maintient son état « divin » grâce à un système de boutons. Quatre nœuds. Quatre points douloureux. Quatre arguments contre son éternité.

Les boutons sont gardés par des Vortigaunts.

The irony: ils ne savent même pas que les forces de Mental sont considérées comme des alliées. Ils ont donc battu tout le monde en vue, y compris ceux soi-disant « sous contrat ».

Un chaos pur, l’environnement opérationnel parfait pour vous.

Désactivez les quatre premiers et la porte suivante s'ouvre. Il y a un bouton pour la deuxième étape du captcha.

Oui, Sam. Même cet enfer a mis en place une authentification à deux facteurs.

Après cela, la dernière porte s'ouvre. Le bouton de déverrouillage du bouclier. Silence. L'instant de vérité.

Et SOMOS ne restera qu’un gros tas de métal coûteux et très vulnérable.
