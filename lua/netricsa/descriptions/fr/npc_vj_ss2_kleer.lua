SQUELETTE DE KLEER, COMMUN


CLASSE : Squelette de Kleer, commun
MÉTABOLISME : Mort-vivant
PERCEPTION : Créature Magique
TAILLE : 9 pieds
ENDURANCE : Faible
HOSTILITÉ : Moyenne
ARMES : Projectiles à deux balles, griffes, cornes
RÉCOMPENSE : 100 FC
MENACE : Moyenne

DESCRIPTIF :

Un squelette mort-vivant d'un être d'une race éteinte de la planète Kleer, d'Alpha Canis Majoris. Il a de longues griffes acérées sur les mains et une longue queue.

Ils attaquent en invoquant des projectiles, en coupant avec leurs griffes au corps à corps ou en sautant vers leur ennemi à courte portée.

Ceux-là en particulier ont été ressuscités de la tombe récemment. Cela se voit à la couleur de leurs os et à l’absence d’odeur de cendre datant de 5000 ans.

CONSEILS :

- Il est préférable d'éviter leurs attaques sautées et à distance en évitant les attaques de côté.
- Un coup de fusil à double canon dans le visage les éliminera.
- Le minigun est votre meilleur ami et leur pire cauchemar.