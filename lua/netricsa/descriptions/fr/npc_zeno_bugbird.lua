BUGBIRD DU ZENOZOIK


CLASS: Insecte
METABOLISM: Xénobiologique
PERCEPTION: Xénobiologie formée
SIZE: 1,3 pi
ENDURANCE: Moyenne
HOSTILITY: Moyenne
WEAPONS: Bec déchiqueté, pattes griffues, attaques sautantes
REWARD: 40 FC
THREAT: Moyenne

DESCRIPTION: 

Une petite créature hybride populaire comme garde ou comme animal de compagnie ornemental dans les villes de Zenozoik. Il a une peau lisse gris-brun sur la tête, une bouche allongée en forme de bec bordée de petites dents et des yeux rouge vif. Son corps est recouvert d'une fourrure brune avec de légères rayures ondulées. Il possède quatre membres avec de grandes griffes et une queue pointue. Contrairement à ce que l'on pourrait attendre d'un animal de compagnie, il se déplace selon un motif en zigzag net et irrégulier, et lorsqu'il est menacé, il fait des bonds inattendus, ce qui en fait une cible difficile. Son régime alimentaire est vraisemblablement composé d'insectes (punaises), bien que cela n'ait pas été confirmé. En voyant un ennemi, il devient agressif, utilisant sa vitesse et ses sauts pour mordre et gratter rapidement.

TIPS: 

- S'il saute, vous pouvez lui donner un puissant coup de pied à la tête.
- Malgré sa vitesse, ses sauts sont prévisibles.
- Utiliser des armes à zone d'effet (fusils à pompe, grenades explosives à courte portée) ou des armes automatiques à tir rapide pour couvrir sa portée potentielle.
