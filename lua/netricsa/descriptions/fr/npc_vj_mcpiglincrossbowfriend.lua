ARBALIER PIGLIN DE MINECRAFT NETHER


CLASS: Arbalétrier Piglin
METABOLISM: Humanoïde ressemblant à un cochon
PERCEPTION: Race guerrière primitive du Nether
SIZE: 6,3 pieds
ENDURANCE: Moyenne
HOSTILITY: Conditionnel
WEAPONS: Arbalète
REWARD: 450 FC
THREAT: Moyenne

DESCRIPTION: 

Les arbalétriers piglins représentent exactement la moitié de la population adulte de piglins – l’autre moitié préfère les épées d’or et le combat rapproché.

Comme toute leur espèce, ils sont obsédés par l’or au point d’identifier ami ou ennemi uniquement par la présence d’une armure dorée sur leur corps.

Appearance: Un arbalétrier ne diffère d'un épéiste que par ses armes et ses tactiques. Au lieu de se précipiter dans la mêlée, il garde ses distances et tire des carreaux d'arbalète toutes les deux secondes. Il recule, essayant de ne pas laisser sa cible s'approcher trop près, mais s'il heurte un mur ou le bord d'une falaise, il continue de tirer.

Contrairement aux bandits Vindicator, les arbalétriers ne préchargent pas leurs armes ; ils rechargent seulement après être entrés en combat.

Les arbalétriers vivent aux mêmes endroits que les autres peuples : dans les forêts pourpres, les terres désolées des Pays-Bas et surtout dans les bastions, où ils protègent les trésors de tout intrus. Ils craignent le feu bleu des âmes, des frères zombifiés et des zoglins, mais cette lâcheté disparaît instantanément dès qu'ils repèrent un ennemi.

Si un groupe d'arbalétriers parvient à abattre une grande cible, ils se rassemblent sur les lieux de la mise à mort et organisent une petite fête avec des grognements caractéristiques. Ils aiment particulièrement danser après une chasse au hoglin réussie.

TIPS: 

- Portez une armure dorée. Un détail et ils ne vous toucheront pas.
- Ils sont nombreux dans les bastions, et ils n'aiment pas qu'on touche à leur trésor doré.
