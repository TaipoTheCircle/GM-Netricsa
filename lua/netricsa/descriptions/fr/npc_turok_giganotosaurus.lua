GIGANOTOSAURE DE LA PLANÈTE TERRAFORMÉE


CLASS: Giganotosaure
METABOLISM: Carnivore, Théropode
PERCEPTION: Prédateur suprême
SIZE: 23 pieds
ENDURANCE: Extrême
HOSTILITY: Absolu
WEAPONS: Coup de tête, coup de queue, mâchoires
REWARD: 7000FC
THREAT: Absolu

DESCRIPTION: 

L'un des plus grands prédateurs terrestres ayant jamais vécu sur la planète, dépassant même le célèbre Tyrannosaurus rex en taille. On le trouve dans la Vallée de la Mort.

En apparence, le Giganotosaurus est un corps brun massif avec un crâne étroit, des crêtes au-dessus des yeux et des rayures le long de la colonne vertébrale.

Le Giganotosaurus utilise les mêmes techniques que les autres grands prédateurs.

TIPS: 

- N'essayez pas de vous engager dans un combat.
- Très dangereux.
