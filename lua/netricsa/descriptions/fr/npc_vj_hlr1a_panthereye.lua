PROTO-PANTHEREYE DE XEN


CLASS: TED BARNEY
METABOLISM: Hybridation incomplète de l'ADN de la bête Xen
PERCEPTION: Prédateur Xen
SIZE: ~ 4 pieds de haut
ENDURANCE: Moyen-Faible
HOSTILITY: Modéré
WEAPONS: Morsure bondissante, frappes de griffes basiques, décharge d'énergie limitée
REWARD: 500FC
THREAT: Modéré

DESCRIPTION: 

Le Early Alpha Panthereye représente une version prototype instable de l’espèce Panthereye, observée au cours de ses étapes d’évolution formatrice. Contrairement au Panthereye pleinement développé, cette sous-espèce manque de musculature raffinée et sa peau semble inégale.

Son corps est plus mince et moins blindé, avec des excroissances osseuses irrégulières dépassant de ses membres – preuve d'une stabilité génétique incomplète. Bien que plus petit que ses homologues plus récents, le Early Alpha Panthereye est toujours dangereux, s'appuyant sur des fentes sauvages et des attaques frénétiques pour submerger ses proies.

On pense que des spécimens de ce type ont été les premières expériences de l'écosystème adaptatif de Xen, peut-être antérieures à la lignée perfectionnée de Panthereye. Certains chercheurs émettent l’hypothèse que des hybrides instables comme celui-ci ont ouvert la voie à des monstruosités plus avancées.

TIPS: 

- Moins durable qu'un Panthereye complet, mais plus rapide et plus erratique.
