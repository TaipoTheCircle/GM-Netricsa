STROGG GRUNT DE STROGGOS


CLASS: Grunt Strogg
METABOLISM: Organisme cybernétique / bio-augmenté
PERCEPTION: Infanterie de mêlée Strogg
SIZE: ~6 pieds de haut
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: Coup de poing / bond en mêlée, parfois mitrailleuse rotative montée sur l'épaule
REWARD: 400FC
THREAT: Moyen-élevé

DESCRIPTION: 

Grunt est un Strogg humanoïde, de taille humaine, avec une carrure semblable à celle d'un singe, des dents pointues, trois yeux et des cartouches de stroient vertes le long des bras et du dos.

Ils préfèrent charger au corps à corps ou bondir sur l'ennemi. Ils utilisent parfois un fusil d'épaule pour tirer à distance, mais c'est rare. Lorsqu'ils sont suffisamment endommagés, ils peuvent « se déchaîner » en injectant du stroyent à partir de leurs cartouches, augmentant ainsi leur santé et leurs dégâts en mêlée. Lorsqu'ils sont en frénésie, ils rugissent et brillent en vert.



TIPS: 

- Gardez vos distances ; leur charge au corps à corps est dangereuse.
- Utilisez un fusil de chasse ou une mitrailleuse, visez la tête.
- Ne leur permettez pas de se rapprocher, utilisez la mobilité et les attaques à distance.
