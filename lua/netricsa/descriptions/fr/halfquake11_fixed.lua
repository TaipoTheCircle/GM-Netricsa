Mais une audience avec le propriétaire de la douleur.

Et il s'appelle SOMOS. Fils des maîtres du sadisme.

Ce n'est pas un homme, Sam. C'est une machine. Un algorithme de torture avec la folie des grandeurs. Le chef d'orchestre central du spectacle, qui décide où vous sautez, où vous tombez et où le public doit applaudir.

Your task sounds almost romantic: 

percez le parkour grouillant de la horde de Mental, trouvez le portail, entrez dans l'espace des plates-formes volantes, appuyez sur tous ces jolis boutons, activez le portail principal et expliquez personnellement à SOMOS que les machines ont également des défauts de conception fatals. Généralement dans la tête.

