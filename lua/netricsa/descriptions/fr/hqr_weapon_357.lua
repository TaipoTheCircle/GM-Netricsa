REVOLVER DEMI-QUAKE 357 MAGNUM


WEAPON: Un temple antique, creusé dans la roche, nous regarde avec des fenêtres vides. Ce ne sont pas seulement des ruines : c'est l'endroit que les Siriens ont choisi comme refuge. Une base cachée. Un référentiel de données sur les armes capables de changer le cours de la guerre. Et tout cela, sous le nez des dieux du désert.
AMMO: 357 magnums
ROF: Revolver double action, ~30 coups/min.

DESCRIPTION: 

Un Colt Python modifié, adapté aux standards du salon HALF-QUAKE. Le cylindre est orné du logo du projet gravé ">)" comme élément décoratif.

Ce revolver est conçu pour éliminer des cibles prioritaires d’un seul coup précis.

NOTES: 

- Des coups simples extrêmement puissants.
- Très précis, efficace pour le tir à longue distance.
- La capacité limitée en munitions empêche une utilisation fréquente.
