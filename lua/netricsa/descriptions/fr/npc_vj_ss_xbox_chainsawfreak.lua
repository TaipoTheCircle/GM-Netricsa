DESCRIPTION: 

Ce monstre a été génétiquement modifié par Mental un week-end lorsque ses agents voyageant dans le temps sont revenus avec des échantillons de médias du 20e siècle.  Parmi les objets figuraient un CD d'un groupe dont le nom avait quelque chose à voir avec la destruction des légumes, un film mettant en vedette Brut Renyolds et un joueur de banjo souriant, ainsi qu'un vieil exemplaire de « Lumberjack Digest ». Alors qu'il insérait la dernière partie de 1979 dans cette abomination, celle-ci reprit vie et même le Notorius Mental vacilla à la vue de ce Taureau aux Choses Gourmandes. Juste un parfait exemple de Mental Rolling dans son Infinite Badness.


TIPS: 

- Ils sont plutôt bêtes mais dangereux lorsqu'ils commencent à te suivre.
- Évitez-les en les contournant.
