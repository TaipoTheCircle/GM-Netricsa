∆POUx DE L'ÉDUCATION FONDAMENTALE SUR PAPIER


CLASS: ∆Poux
METABOLISM: Entité emprisonnée/déformée
PERCEPTION: Entité emprisonnée
SIZE: ~ 5,5 pieds (forme normale), > 11,4 pieds (forme vraie)
ENDURANCE: Absolu
HOSTILITY: Extrême (envers tous sauf quelques élus)
WEAPONS: Pointes noires, télékinésie, griffes, tentacules, transformation
REWARD: 666666 FC
THREAT: Catastrophique

DESCRIPTION: 

La menace ultime, plus grande que le directeur de la Paper School lui-même, une entité emprisonnée dans une pièce séparée marquée de panneaux « NE PAS ENTRER ». Dans sa forme normale, elle apparaît comme une fille aux cheveux bruns courts, portant une couronne et un arc, mais ses vêtements sont déchirés et sa peau est couverte de cicatrices et d'une étrange éruption cutanée. La vraie nature des ∆Lice est une créature démoniaque ressemblant à une ombre avec une sclère rouge, d'énormes griffes, des pointes et des tentacules. Elle est agressive et meurtrière envers quiconque entre dans sa chambre, mais passive et même douce avec Oliver (son ami d'enfance et petit ami). Son pouvoir surpasse même les enseignants les plus dangereux, ce qui les fait craindre.

Elle a les pleins pouvoirs grâce à son pouvoir. (C'est pourquoi elle va parfois à la cafétéria avec Oliver =3) Son apparence transforme la réalité papier de l'école en quelque chose de plus tangible et terrifiant. Elle s'oppose à d'autres entités emprisonnées, comme HanseG. Elle considère l'école comme son territoire.

TIPS: 

- N'entrez JAMAIS dans sa chambre. Même si la porte ressemble à une sortie, c'est un piège.
- If you don't have the SBC Cannon lying around: Courir est votre seule option. Elle est plus rapide, plus forte et contrôle l'espace autour d'elle. La confrontation directe est vaine.
