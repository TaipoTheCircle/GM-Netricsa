CHIEN


CLASS: Chien
METABOLISM: Mécanique
PERCEPTION: Machine de guerre de la Résistance
SIZE: ~8-10 pieds
ENDURANCE: Très élevé
HOSTILITY: Faible
WEAPONS: Force pure, projectiles de rebut, armes improvisées
REWARD: 1000FC
THREAT: Haut

DESCRIPTION: 

Dog est un robot massif et fidèle construit par Eli Vance et continuellement amélioré par sa fille Alyx. Initialement conçu comme un « animal de compagnie » mécanique et un garde du corps, Dog est devenu une immense puissance de force et de résilience. Malgré son physique intimidant, il partage une personnalité enjouée, presque enfantine : il aime les jeux de récupération, lançant facilement d'énormes objets.

In combat, however, Dog transforms into a wrecking machine: déchirer Combinez les forces, lancez des voitures comme projectiles ou brisez des barrières fortifiées. Son mélange unique de force brute et de précision technique le rend indispensable à la Résistance.

La loyauté de Dog envers Alyx est absolue. Il la protège à tout prix, se lançant souvent dans des situations impossibles avec un élan imparable.

TIPS: 

- Le chien peut soulever et lancer des objets plus lourds que n'importe quel humain. Utilisez sa force pour dégager des chemins.
- Résistant à la plupart des tirs d'armes légères ; Il est préférable de le laisser attirer le feu ennemi.
- Lorsque Dog se déchaîne, restez à l'écart, ami ou ennemi, son élan peut écraser tout sur son passage.
