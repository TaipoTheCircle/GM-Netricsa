ARACHNOÏDE, NOUVEAU-NÉ DU SCORPIUS


CLASS: Arachnoïde, nouveau-né
METABOLISM: Ectotherme, arthropode
PERCEPTION: Race intelligente primitive
SIZE: 8 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Mitrailleuse, dard de queue
REWARD: 1000FC
THREAT: Moyenne

DESCRIPTION: 

Un nouveau-né d'une race primitive descendant des arthropodes. Extrêmement dangereux en raison de ses croyances religieuses.

Un jeune arachnoïde pas complètement formé de la planète Scorpius, qui adore Mental comme un messie. Il possède un exosquelette plus léger et moins durable que celui des adultes. Il est armé d'une mitrailleuse légère qui remplace l'un de ses membres.

Cette arme offre une grande précision et des munitions illimitées grâce à un régénérateur techno-magique, mais elle a tendance à surchauffer après une longue rafale, laissant aux ennemis le temps d'attaquer. Il peut également attaquer avec un dard venimeux en combat rapproché. Il s'agit d'une unité d'infanterie produite en masse dans l'armée de Mental, compensant sa faible capacité de survie par son nombre et sa puissance de feu dense.

TIPS: 

- Le dard sur sa queue est dangereux – ne vous approchez pas trop.
- Lorsqu'il est enragé, il tirera même à travers le sien.
- Après une longue rafale, il arrête de tirer pendant une seconde pour se refroidir – le moment idéal pour une contre-attaque.
- Utilisez un fusil de précision, un fusil d'assaut ou un lance-roquettes.
