BLACK MESA, FINALE DE LA LIAISON MONTANTE


Sam, le signal a été envoyé et les portes du réacteur sont désormais ouvertes. Le complexe Lambda attend.

Mais avant de célébrer, il y a un dernier problème. Un scientifique survivant nous prévient qu'un Gargantua a été aperçu errant dans les couloirs. Cela déchire la sécurité comme du papier, et il vous suggère fortement d'éviter tout contact direct, à moins que vous n'aimiez devenir un barbecue sérieux.

Pour ajouter à l'étrangeté, cet homme mystérieux avec la mallette a de nouveau été repéré, cette fois se tenant calmement à côté d'un garde, regardant le chaos se dérouler comme s'il s'agissait d'une pièce de théâtre.

Your next step is simple: Accédez à l'ascenseur, descendez-le et évadez-vous. Du moins, c'est le plan.
