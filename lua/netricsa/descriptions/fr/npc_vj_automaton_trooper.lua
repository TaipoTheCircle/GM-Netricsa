SOLDAT AUTOMATE DU CYBERSTAN


CLASS: Soldat automate
METABOLISM: Mécanique
PERCEPTION: Infanterie légère automate
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Absolu
WEAPONS: Mitraillette Fusion Pulse, grenades à fragmentation, coup de poing
REWARD: 200FC
THREAT: Faible-Moyen

DESCRIPTION: 

Il s'agit de l'unité la plus courante et la plus faible de la faction Automate.

Trooper Appearance: Un corps métallique mince, une forme humanoïde et des yeux rouges brillants. Le corps est gris et la tête est blanche avec les yeux rouges. Les Troopers sont en métal chromé et brillent à la lumière.

In combat, Troopers rely on numbers. Their tactics are simple: repérez un Helldiver, tirez des rafales de cinq coups, puis rechargez (temps de recharge de 4 secondes). Si le Helldiver se cache à couvert, le Trooper lance une grenade à fragmentation. Si le Helldiver s'approche trop près, il frappe. Lorsqu'il est réprimé par le feu, le Trooper commence à déplacer son arme de manière erratique, réduisant ainsi la précision.

TIPS: 

- Les soldats ne sont pas une cible prioritaire, mais il faut les tuer rapidement pour éviter qu'ils n'appellent des renforts.
- La tête tue d'un seul coup, mais c'est difficile à toucher. Il est plus facile de tirer sur le corps.
- Un bras coupé désarme le Trooper, l'obligeant à engager un combat rapproché.
- Une jambe coupée immobilise le Trooper.
- Les explosifs (grenades, RPG, frappes aériennes) sont efficaces contre les clusters.
- Les soldats qui exploitent des tourelles dans les avant-postes des Automates peuvent être tués et la tourelle peut être utilisée contre les ennemis.
