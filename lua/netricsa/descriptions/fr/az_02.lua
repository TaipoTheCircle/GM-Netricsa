MESA NOIRE, HALLS SUPÉRIEURS


Une fois de plus, l’homme à la mallette est là – silencieux, intouchable, observant depuis un pont au-dessus. C'est soit votre plus grand fan... soit votre pire recruteur RH.

This sector feels eerily familiar: couloirs, laboratoires, machines - comme si Black Mesa copiait et collait ses propres plans pour gagner du temps et de l'argent. Mais ne vous détendez pas trop. Certaines chambres sont différentes. Des angles qui ne correspondent pas. Des machines que vous n'avez jamais vues auparavant. De nouveaux pièges dans un vieux labyrinthe.

Pensez-y comme du déjà vu... mais avec un risque de mort légèrement plus élevé.

And there's a problem: pour atteindre les bureaux - tant pour vous que pour cet "autre Sam" - le courant doit être rétabli. Quelque part dans ce labyrinthe se cache un levier contrôlant l'électricité du secteur. Trouvez-le, ou vous êtes tous les deux coincés.

One more complication: un Panthereye rôde dans la zone. Il mange tout le monde
