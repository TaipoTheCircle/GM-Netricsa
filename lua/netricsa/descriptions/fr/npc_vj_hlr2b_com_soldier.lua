COMBINEZ LE SOLDAT D'OVERWATCH BÊTA


CLASS: Combiner la bêta d'Overwatch Soldier
METABOLISM: Humain augmenté, Homme
PERCEPTION: Infanterie transhumaine prototype
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPONS: XM29 OICW, grenades à fragmentation
REWARD: 300FC
THREAT: Moyen

DESCRIPTION: 

Le Combine Beta Overwatch Soldier représente une première étape dans la tentative du Combine de créer une armée transhumaine fiable. Contrairement aux unités Overwatch finalisées vues plus tard, ces prototypes conservaient des caractéristiques plus humaines et utilisaient un équipement plus volumineux et moins avancé. Leur armure est patchwork, offrant une protection modérée, et leur augmentation cybernétique est rudimentaire par rapport aux générations suivantes.

Bien qu'elles soient moins disciplinées et plus fragiles que les soldats d'Overwatch modernes, les unités bêta ont été déployées en grand nombre au cours des premières phases de l'occupation du Combine. Ils comptaient sur l’agressivité et le nombre plutôt que sur des tactiques précises, se précipitant souvent sur leurs adversaires avec des armes obsolètes et une mauvaise coordination.

Leur existence préfigurait le raffinement impitoyable de la conception transhumaine du Combine – les prédécesseurs imparfaits qui ont ouvert la voie aux forces d’élite perfectionnées.

TIPS: 

- Blindage plus faible que les unités Combine finalisées - visez la masse centrale.
- Moins précis à longue distance mais mortel en combat rapproché.
