FUSIL AUTOMATIQUE K-3


Fusil d'assaut WEAPON K-3 Bullpup
Munitions 5,45 x 39 mm (compatibles avec les chargeurs AK-74)
ROF ~ 600 tours

DESCRIPTION: 

Fusil d'assaut arménien bullpup développé dans les années 1990 sur la base du modèle AK-74. Destiné à remplacer les fusils plus anciens des forces armées arméniennes, principalement destinés aux unités d'opérations spéciales. Comprend une disposition bullpup compacte, un mobilier en polymère et une compatibilité avec les chargeurs AK-74 standard de 30 cartouches.

NOTES: 

- Modes de tir semi-automatique et entièrement automatique.
- Portée efficace jusqu'à 500 mètres.
- Mécanisme à gaz avec boulon rotatif et piston à longue course.
- Equipé de mires en fer; une lunette optique (PSO-1) peut être montée.
- Peut lancer des grenades à fusil à l'aide de l'adaptateur 3K-3.
- Conception légère et compacte adaptée aux combats rapprochés et urbains.
