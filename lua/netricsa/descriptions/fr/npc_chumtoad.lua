CHUM-TOAD DE XEN


CLASS: Chum-Crapaud
METABOLISM: Créature amphibie basée sur Xen
PERCEPTION: Amphibien Xen
SIZE: ~ 1 pied de long, corps trapu et rond
ENDURANCE: Faible
HOSTILITY: Faible (principalement passif)
WEAPONS: Morsure faible, peut sauter sur de courtes distances lorsqu'il est surpris
REWARD: 50 FC
THREAT: Minimal

DESCRIPTION: 

Le Chum-Toad est une petite créature Xen ressemblant à un amphibien avec un corps rond, un grand œil et des membres trapus. Bien qu'une fois théorisé par les scientifiques de Black Mesa comme étant apparenté aux crabes en raison de sa forme trapue, le Chum-Toad est plus une curiosité inoffensive qu'un prédateur. Sa peau épaisse et caoutchouteuse et sa légère coloration lui permettent de survivre dans les cavernes Xen humides et les enceintes de laboratoire.

Certains spécimens ont été amenés à Black Mesa pour étude, où ils ont été observés comme étant largement dociles. Contrairement à la plupart de la faune Xen, les Chum-Toads ne sont pas conçus pour le combat : ils se nourrissent de fond et sont des charognards.

Il est intéressant de noter que leur matériel génétique a été utilisé dans des expériences horribles (telles que la création des esclaves Diablo ou Magenta), prouvant que même l'organisme Xen le plus faible peut être transformé en arme.

TIPS: 

- Inoffensifs s'ils sont laissés seuls - ils ne représentent aucune menace pour le personnel qualifié.
- Peut être ignoré dans la plupart des rencontres.
