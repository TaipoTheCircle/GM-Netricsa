LE MONDE FRONTIÈRE DE XEN, IL EST TEMPS DE S'ÉVADER !


Et voilà, Sam. Cela a fonctionné.

Le portail s'est ouvert et, comme prévu, nous a ramenés là où tout a commencé. Les terres de Xen. Des paysages indigènes, un chaos familier et le sentiment que le monde est sur le point de se libérer complètement de ses amarres.

La guerre fait rage partout. Beaucoup de troupes mentales. Beaucoup. Ils combattent les forces de Xen, sans vraiment distinguer qui est indigène et qui est transitoire. L’air crépite d’énergie, l’espace tremble et la réalité essaie de ne pas regarder dans cette direction.

La voix de Rosenberg vient du portail. Il t'appelle "Calhoun". Il a tort. Mais honnêtement, ce n’est pas notre problème pour le moment. L'essentiel c'est qu'il soit là. Et le portail fonctionne. Pour l'instant.

La mauvaise nouvelle est autre chose. Le portail est instable. Il se déchire, scintille et agit comme s'il était sur le point d'abandonner l'existence. Nous devons battre en retraite. Oui je sais. Ce n'est pas ton style. Vous préférez rester, régler ça, en finir avec ça, et ensuite seulement partir.

Mais il n'y a pas le choix. Si nous n'avançons pas maintenant, le passage pourrait se fermer. Pour toujours. Et puis Xen redeviendra une impasse, pas un couloir.

On ne sait pas où cela mène.

La date de fermeture est également inconnue.

Alors agi, Sam.

Retraite.

Parfois, le meilleur coup est de prendre du recul.
