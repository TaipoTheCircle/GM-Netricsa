DR. RICHARD KELLER


CLASS: Dr Richard Keller
METABOLISM: Humain, mâle
PERCEPTION: Administrateur en chef, Division de recherche de Black Mesa
SIZE: ~6 pieds
ENDURANCE: faible
HOSTILITY: aucun
WEAPONS: aucun
REWARD: 500FC
THREAT: négligeable

DESCRIPTION: 

Le Dr Richard Keller est une autorité administrative et scientifique supérieure au sein du centre de recherche de Black Mesa à l'époque de la cascade de résonance. Se distinguant par son recours à un fauteuil roulant motorisé en raison d'un handicap physique, Keller suscite néanmoins un immense respect grâce à son intellect aiguisé, son leadership décisif et sa maîtrise de la théorie interdimensionnelle.

En tant que l'une des personnalités les plus haut placées, Keller supervise et dirige les actions du Dr Gina Cross et du Dr Colette Green, assurant la stabilité des expériences et le confinement des catastrophes au sein de Black Mesa. Son expertise en téléportation et en contrôle de résonance le rend indispensable au maintien de la structure opérationnelle lors des événements catastrophiques.

Bien qu'incapable de combattre directement, Keller exerce son influence en coordonnant les scientifiques, la sécurité et les systèmes depuis les postes de commandement. Ses connaissances, sa discipline et sa détermination aident à guider le personnel survivant à travers le chaos de l'incident.

La survie de Keller représente la préservation des plus hauts niveaux de leadership scientifique et administratif de Black Mesa, faisant de lui l'un des civils les plus stratégiques sur le plan stratégique pendant la crise.

