COMBINAISON ALTERNATIVE SOLDAT D'ÉLITE OVERWATCH


CLASS: Alternative Combiner Overwatch Elite Soldier
METABOLISM: Humain augmenté, mâle
PERCEPTION: Combiner Elite Shock Trooper
SIZE: 5,9 pieds
ENDURANCE: Très élevé
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, boules d'énergie
REWARD: 900
THREAT: Très élevé

DESCRIPTION: 

Les soldats d’élite alternatifs d’Overwatch sont des soldats d’élite qui choisissent de ne pas se montrer. Contrairement à leurs homologues standards en armure blanche, ces soldats portent une combinaison bleu foncé, visuellement presque impossible à distinguer de l'uniforme des soldats réguliers d'Overwatch. La seule caractéristique distinctive évidente est leur visière bleu froid.

Bien qu'apparemment un soldat ordinaire, il est en fait un combattant d'élite avec les mêmes implants, réflexes et entraînement tactique. Il opère souvent au sein d'unités standard, augmentant discrètement leur efficacité à un niveau déconcertant.

TIPS: 

- Travaille souvent en tandem avec les troupes régulières, les propulsant au niveau élite.
- La visière bleue peut révéler leur position dans des conditions de faible luminosité.
