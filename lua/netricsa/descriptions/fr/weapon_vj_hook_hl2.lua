HARPON


WEAPONS: Harpon
AMMO: Aucun
ROF: ~1 action de lancer/seconde
WEIGHT: 3,5 kg

DESCRIPTION: 

Le harpon est une arme de mêlée à longue portée utilisée pour engager des cibles à courte et moyenne portée. Il s'agit d'une tige de fer durable avec une pointe aiguisée, conçue pour percer des matériaux denses et créer un effet époustouflant lors de l'impact.

Lorsqu'il est lancé, le harpon perce la cible et s'enfonce fermement dans la surface, bloquant ainsi l'ennemi en place. Il est efficace contre des adversaires isolés et peut les immobiliser.

NOTES: 

- Peut être utilisé comme arme de jet improvisée en l'absence d'armes à feu.
- Lors de l'impact, il immobilise fermement la cible, rendant difficile toute résistance supplémentaire.
- Peut être combiné avec d'autres armes de mêlée pour le combat rapproché.
