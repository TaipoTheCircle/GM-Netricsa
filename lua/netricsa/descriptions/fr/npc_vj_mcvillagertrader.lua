COMMERÇANT ERRANT DU MONDE MINECRAFT


CLASS: Commerçant errant
METABOLISM: Humain, mâle
PERCEPTION: Commerçant errant / Fournisseur exotique
SIZE: 6,2 pieds
ENDURANCE: Moyenne
HOSTILITY: Passif
WEAPONS: Aucun
REWARD: 150 FC
THREAT: Minimal

DESCRIPTION: 

Un personnage mystérieux vêtu d'une robe bleue à capuche, dont les origines restent un mystère même pour les villageois. Cet énigmatique vagabond parcourt l'Overworld, à la recherche de clients prêts à échanger des émeraudes contre des biens rares et exotiques.

L'apparence du commerçant est sensiblement différente de celle des villageois ordinaires. Au lieu des vêtements traditionnels, il porte une longue robe bleue avec une capuche qui cache son visage et une cape de la même couleur.

On every journey, the Trader is accompanied by two Trader llamas, tethered to him by leashes. These hardy animals not only carry the load but also protect their master: si le commerçant est touché, les lamas commencent immédiatement à cracher sur le contrevenant. Les lamas eux-mêmes peuvent être apprivoisés après la mort de leur maître : il suffit d'attendre qu'ils se calment.

Contrairement aux villageois, le commerçant n'achète pas d'objets, il se contente de les vendre. Son inventaire est aléatoire et comprend des biens rares et difficiles à obtenir.

L'astuce principale du Trader est la survie. A la tombée de la nuit ou en apercevant une foule hostile, il boit une potion d'invisibilité, devenant presque invisible, même si les laisses de lama sont toujours visibles. A l'aube, il lave l'effet avec du lait. Si un zombie tue le commerçant, il ne se transformera pas en villageois zombie.

Tuer un commerçant diminue votre réputation : les commerçants suivants apparaîtront moins fréquemment et leurs prix augmenteront.

TIPS: 

- Ayez toujours une réserve d'émeraudes sous la main - vous ne savez jamais quelles choses intéressantes un commerçant apportera.
- Si vous avez besoin de ses lamas, il est préférable d'attendre que le commerçant disparaisse - les lamas resteront alors et pourront être apprivoisés.
- Lorsque vous attaquez un commerçant, soyez prêt à vous faire cracher dessus par ses lamas.
- Si vous ne voyez que des laisses flottantes, cela signifie que le commerçant a consommé une potion d'invisibilité et qu'il se trouve quelque part à proximité.
- Le commerçant est vulnérable la nuit - la potion d'invisibilité ne le rend pas complètement invulnérable, elle le cache simplement.
