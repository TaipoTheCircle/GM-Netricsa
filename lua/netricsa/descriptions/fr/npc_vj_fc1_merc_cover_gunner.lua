SOLDAT D'ASSAUT M134 DE L'ARCHIPEL KRIEGER


CLASS: Soldat d'assaut M134
METABOLISM: Humain, mâle
PERCEPTION: Mercenaire d'élite de Krieger
SIZE: 6 pieds
ENDURANCE: Haut
HOSTILITY: Haut
WEAPON: M134
REWARD: 700FC
THREAT: Haut

DESCRIPTION: 

Le soldat le plus commun et le plus reconnaissable de la compagnie militaire privée du Dr Krieger. Ce sont des professionnels grands et forts physiquement, recrutés dans le monde entier. Leur motivation est l’argent, pas l’idéologie. Ils portent divers uniformes et couvre-chefs (bérets, casques, casquettes), mais portent toujours des gilets pare-balles. Même si beaucoup sont mécontents des conditions de service (chaleur, insectes, mauvaise alimentation, isolement), ils restent des adversaires disciplinés et dangereux.

Un mercenaire Krieger spécialisé, faisant partie de l'équipage des hélicoptères de patrouille UH-60 Blackhawk. Il est monté à bord près de la trappe ouverte et contrôle un minigun rotatif à six canons. Sa mission est de supprimer les cibles au sol et d'assurer une couverture aérienne lors des atterrissages en vol. Il possède une excellente puissance de feu, mais est extrêmement vulnérable en raison de sa position fixe et de son champ de tir limité. Il n'est que partiellement protégé par la coque de l'hélicoptère. Le neutraliser prive l'hélicoptère de son armement offensif principal, le forçant à se retirer du champ de bataille.

TIPS: 

- L'armure protège la coque. La méthode la plus efficace est celle des tirs à la tête.
- Évitez les terrains découverts dans son champ de tir.
- Le tireur lui-même est pratiquement immunisé contre les tirs ciblés. Des tirs précis à la tête ou au corps avec n’importe quelle arme sont efficaces.
- Tuer le tireur est le moyen le plus rapide de neutraliser la menace des hélicoptères sans utiliser d'armes lourdes.
