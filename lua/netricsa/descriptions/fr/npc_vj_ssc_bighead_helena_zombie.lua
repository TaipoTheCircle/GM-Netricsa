THREAT: Ennui

DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais dans la difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Helena Hunski fait partie de ces « cerveaux » invisibles mais vitaux dont le travail reste en coulisses, mais sans qui tout le mécanisme connu sous le nom de Croteam ne fonctionnerait pas. Son crâne exagéré est le résultat d'un multitâche et d'une profonde compréhension du fonctionnement interne du studio. Tandis que les programmeurs et les concepteurs créaient le chaos à l'écran, Elena, en tant qu'assistante commerciale et membre de l'équipe, garantissait l'ordre et l'organisation dans le monde réel.

Her contributions to the series, acknowledged in the credits of Serious Sam: The Second Encounter et Serious Sam II rappellent que derrière chaque grand jeu de tir se cache non seulement un codeur ou un artiste brillant, mais aussi des personnes qui coordonnent le travail, résolvent d'innombrables problèmes d'organisation et maintiennent la cohésion de l'équipe. En tant que parente probable de l'un des fondateurs du studio, Davor Hunski, elle incarne l'esprit de famille et le dévouement qui caractérisent Croteam depuis des décennies.

Elle était autrefois en vie, mais après que quelqu'un l'ait tuée, elle est ressuscitée d'entre les morts, devenant une démone zombie.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", éliminez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.
