GROSSE TÊTE, TOM BAKER


CLASS: Grosse tête, Tom Baker
METABOLISM: Humanoïde, mâle
PERCEPTION: Fanatique enthousiaste
SIZE: 5,9 pieds
ENDURANCE: Faible
HOSTILITY: Neutre
WEAPONS: Aucun (sauf en difficulté I Am Ugh-Zan)
REWARD: 100 FC
THREAT: Ennui

DESCRIPTION: 

Les Big-Heads sont de minces humanoïdes aux crânes exagérés contenant le cerveau des développeurs de Croteam. Ils portent l'uniforme de Serious Sam, mais ont les traits du visage uniques de l'équipe de développement. Ils affichent un enthousiasme obsessionnel et vous poursuivent aux cris de « Bonjour ! » et "Hé, attends!" Ils sont totalement inoffensifs, mais au niveau de difficulté "Je suis Ugh-Zan", ils gagnent des modificateurs de feu et de glace. Ils apparaissent spontanément dans les zones secrètes, bloquant les passages par leur grand nombre.

Tom Baker est un pont vivant reliant le monde de Croteam au vaste univers de 2K Games et d'autres grands studios. Son crâne surdimensionné est une archive contenant une expérience de travail sur plus de 150 jeux, allant de succès cultes comme BioShock, Borderlands, XCOM, Civilization et Mafia à des projets de niche et des jeux pour enfants sous licence. Dans le cadre de Serious Sam, il a fait partie de l'équipe de conception de The Second Encounter et a ensuite travaillé sur l'édition de Serious Sam II au sein de l'équipe européenne de Take-Two Interactive.

Sa carrière illustre l'évolution d'un membre de l'équipe de conception à un directeur principal de la conception, supervisant les processus créatifs sur les projets AAA. Tom est l'incarnation d'un concepteur de jeux professionnel aux multiples talents, quelqu'un qui comprend les mécanismes de jeu à un niveau systémique et peut appliquer cette expérience à la fois au chaos indépendant de Croteam et aux superproductions de 2K.

TIPS: 

- Utilisez un couteau de combat ou un pistolet pour dégager le chemin.
- En difficulté "I Am Ugh-Zan", éliminez-les à distance grâce à leurs modificateurs.
- Une masse à frappe horizontale est efficace en groupe.

