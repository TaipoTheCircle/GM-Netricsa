HYPERBLASTER


WEAPON: Hyperblaster
AMMO: Cellules énergétiques
ROF: ~450 tours/min.

DESCRIPTION: 

L'Hyperblaster est une arme à énergie de grande puissance qui tire un plasma continu lorsque la gâchette est maintenue enfoncée. Il a une cadence de tir rapide et une portée solide. Le plasma apparaît comme une décharge de particules d'énergie bleue. Excellent pour les dommages et la suppression durables.

NOTES: 

- Utilise des cellules énergétiques au lieu de munitions conventionnelles.
- Le maintien continu de la gâchette permet un tir ininterrompu.
- Efficace à la fois contre des cibles uniques et contre le contrôle de zone.
