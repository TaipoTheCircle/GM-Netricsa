T-REX, JUVÉNILE DE LA PLANÈTE TERRAFORMÉE


CLASS: T-Rex, juvénile
METABOLISM: Prédateur à sang chaud
PERCEPTION: Prédateur au sommet
SIZE: 8,2 pieds
ENDURANCE: Haut
HOSTILITY: Extrêmement élevé
WEAPONS: Crocs, griffes, vitesse
REWARD: 800 FC
THREAT: Haut

DESCRIPTION: 

C'est la version adolescente du roi des dinosaures que tout le monde craint. Il n'a pas encore atteint neuf mètres et huit tonnes, mais il est déjà tout à fait capable de réduire un adulte à une tache sanglante sur le trottoir en quelques secondes.

En termes d'apparence, l'adolescent est très différent du prédateur adulte. Il est plus mince, aux jambes plus longues et plus lent. Ses membres postérieurs sont longs, ce qui lui permet d'atteindre une vitesse décente et de changer de direction rapidement.

Leurs mâchoires sont déjà puissantes, mais leurs dents ne sont pas encore celles qui écrasent les os des adultes.

Les tyrannosaures juvéniles vivent dans la même jungle que les adultes. Mais contrairement aux spécimens matures, qui préfèrent les embuscades et les sprints courts, les juvéniles sont actifs et agressifs. Ils peuvent poursuivre leurs proies pendant de longues périodes, en utilisant leur vitesse et leur endurance.

Dans un combat, un tyrannosaure juvénile est dangereux précisément en raison de sa mobilité. Il ne reste pas immobile : il se précipite, mord, se déchaîne avec sa queue et essaie de l'attraper avec ses pattes antérieures.

TIPS: 

- On les voit rarement seuls. Si vous en voyez un, cela signifie que leur mère est à proximité. Et tu ne veux certainement pas rencontrer leur mère.
