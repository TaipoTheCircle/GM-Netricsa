GRANDE VILLE, ÉTENDU URBAIN


Big city is exactly what its name suggests: une métropole tentaculaire conçue pour le chaos. De larges avenues, des gratte-ciel imposants, des blocs industriels et des toits sans fin forment un immense environnement bac à sable. Contrairement aux zones de test confinées de Construct ou au vide de Flat grass, la grande ville prospère grâce à la verticalité et à l’échelle.

Key features include: 

Centre-ville avec gratte-ciel et places ouvertes, idéal pour les poursuites sur les toits et les nids de tireurs d'élite.

Zones industrielles avec entrepôts, tunnels et passages souterrains cachés.

Autoroutes et ponts pour les essais de véhicules et les batailles à grande échelle.

Des toits et des intérieurs qui permettent des traversées créatives et des embuscades.

C'est la ville qui ne dort jamais, mais qui n'a jamais eu de vrais citoyens. Les rues vides résonnent du bruit de vos propres pas, laissant une impression troublante.
