CANON ANTI-NEXTBOT


WEAPON: Canon anti-Nextbot
AMMO: Énergie gravitationnelle suralimentée/faisceau d'annihilation
ROF: Faisceau continu

DESCRIPTION: 

L'arme anti-nextbot la plus avancée, basée sur le pistolet à gravité suralimenté amélioré. L'arme combine la technologie gravitationnelle et des explosions d'énergie destructrices, formant un faisceau de puissance colossale.

Le premier mode concentre l'énergie dans un point étroit, libérant un faisceau capable de détruire instantanément des groupes de cibles dans une seule zone. Il est utilisé pour éliminer les concentrations ennemies et maintenir des positions clés.

Le deuxième mode active une explosion d'énergie complète, couvrant toute la zone. A ce stade, l'utilisateur est automatiquement protégé par un champ spécial qui l'isole complètement des effets de l'arme. Tous les autres êtres vivants présents dans la zone d’explosion sont immédiatement détruits – sans exception.

NOTES: 

- Mode 1: Destruction massive de cibles dans une seule zone avec un faisceau.
- Mode 2: Efface complètement l’emplacement de toutes les créatures vivantes.
- L'utilisateur est protégé par un champ énergétique lors de l'activation du deuxième mode.
- Ne fait aucune distinction entre les cibles : les alliés et les neutres sont également détruits.
- Efficace à toutes les distances, notamment dans les espaces confinés.
