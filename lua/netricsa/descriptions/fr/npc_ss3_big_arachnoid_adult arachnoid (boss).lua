ARACHNOÏDE RENFORCÉ, ADULTE DU SCORPIUS


CLASS: Arachnoïde renforcée, adulte
METABOLISM: ectotherme, arthropode
PERCEPTION: race sensible primitive
SIZE: 20-25 pieds
ENDURANCE: haut
HOSTILITY: extrême
WEAPONS: pistolet à chaîne, piqûre de queue
REWARD: 5000FC
THREAT: haut

DESCRIPTION: 

Un guerrier sacré d'une race primitive descendant des arthropodes. Extrêmement dangereux en raison de ses croyances religieuses.
Les deux griffes de l'arachnoïde sont implantées de mitrailleuses équipées d'un dispositif de récupération de munitions. Son corps est recouvert d'un exosquelette qui le protège des balles, des explosifs et du feu.

TIPS: 

- Piqûre dangereuse - ne vous approchez pas de trop près.
- Tireront sur leurs propres rangs avec fureur.
