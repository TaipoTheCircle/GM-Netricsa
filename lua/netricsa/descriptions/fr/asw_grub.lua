VER D'ESSAI


CLASSE : Nuée de larves
MÉTABOLISME : Xénobiologique
PERCEPTION : Forme de vie immature
TAILLE : 0,98 pi
ENDURANCE : Très faible
HOSTILITÉ : Aucune
ARMES : Aucune
RÉCOMPENSE : 10 FC
MENACE : mineur

DESCRIPTIF :

Un Grub est l’un des premiers stades de développement des organismes Swarm. Ces créatures émergent le plus souvent de sacs incubateurs spécialisés qui s'ouvrent lors de la détection d'une proie ou d'une exposition à des températures élevées.

Contrairement à la plupart des membres de Swarm, les Grub sont incapables d'attaquer leurs adversaires et sont pratiquement sans défense. Leur système nerveux primitif leur permet uniquement de réagir au mouvement et de suivre de gros objets vivants.

Malgré l’absence de menace directe, la présence de Grub peut servir d’indicateur important de l’activité des Essaims à proximité. La découverte de sacs à couvain indique généralement la présence d'organismes plus dangereux dans la zone.

Au fur et à mesure que les larves se développent, elles subissent d'autres étapes de métamorphose, se transformant progressivement en représentants à part entière de leur espèce. Les formes de vie précises qui émergent de ce processus font encore l’objet de débats parmi les xénobiologistes.

La plupart des combattants éprouvent d'abord de la compassion pour les membres de l'Essaim lorsqu'ils rencontrent le Grub. Cela continue généralement jusqu'à ce qu'ils découvrent ce qu'ils deviennent.

CONSEILS :

- Ne vous laissez pas distraire par le Grub pendant le combat.
- Leur présence indique souvent que quelque chose de bien plus dangereux se trouve à proximité.
- Un petit organisme inoffensif d'aujourd'hui peut devenir un problème majeur demain.