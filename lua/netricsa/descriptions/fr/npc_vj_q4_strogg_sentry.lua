SENTINELLE STROGG DE STROGGOS


CLASS: Sentinelle Strogg
METABOLISM: Organisme cybernétique, symbiose
PERCEPTION: mitrailleur-garde volante
SIZE: ~5 pieds
ENDURANCE: Faible
HOSTILITY: Moyen
WEAPONS: Mitrailleuses jumelées de haute précision
REWARD: 150 FC
THREAT: Moyen

DESCRIPTION: 

Le Strogg est un Strogg volant spécialisé conçu pour garder des zones clés telles que les tunnels Nexus et Hub. Il s'agit d'un corps sans jambes, de forme ovale, planant dans les airs, avec une paire de mitrailleuses à la place des bras et un récipient rempli de liquide vert en dessous, abritant un symbiote humanoïde. Ces ennemis sont de difficulté moyenne et apparaissent en groupes de 2 à 4. Leur principale force réside dans leur cadence de tir, leur précision et leur capacité à contourner rapidement les projectiles lents. Malgré leurs faibles dégâts individuels, leur barrage constant peut rapidement épuiser votre santé et votre armure, vous distrayant des ennemis plus dangereux.

TIPS: 

- Concentrez-vous sur le conteneur vert ci-dessous - les attaques contre lui infligent le double de dégâts.
- Utilisez une mitrailleuse ou un fusil de chasse à bout portant. Le Lightning Cannon est idéal.
- Mettez-vous à l'abri pendant leurs rafales, en attaquant pendant les pauses. Lorsque vous apparaissez dans un groupe, éliminez d’abord les Sentinelles pour éviter les distractions.
- Ils ne poursuivent pas à travers les portes - vous pouvez vous retirer pour vous regrouper.
