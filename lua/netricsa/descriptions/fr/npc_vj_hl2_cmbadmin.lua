ADMINISTRATEUR DE COMBINAISON


CLASS: Administrateur de moissonneuse-batteuse
METABOLISM: À sang chaud, humain
PERCEPTION: Humains, hauts fonctionnaires
SIZE: 5,9 pieds
ENDURANCE: Moyenne
HOSTILITY: Moyenne
WEAPONS: Revolver, Glock 17, coup de crosse
REWARD: 700FC
THREAT: Moyenne

DESCRIPTION: 

Mâle humain amélioré / Augmentations biomécaniques limitées

Male and female administrators wear a standard uniform: une veste marron et une cravate orange. En cas de conflit personnel, ils sont armés de revolvers et de Glock 17, et en combat rapproché, ils utilisent une crosse comme arme. Malgré leur statut officiel, ils sont capables d’une défense agressive et n’hésitent pas à réprimer la résistance si nécessaire.

TIPS: 

- Les administrateurs gardent leurs distances et connaissent bien leurs itinéraires.
- Ils sont bien gardés.
