ROBOT SIRIEN, GARDE DU SIRIUS


CLASS: Robot Sirien, Garde
METABOLISM: Mécanique, Energie
PERCEPTION: Unité d'assaut lourd, plateforme de combat autonome
SIZE: 9,85 pieds
ENDURANCE: Extrême
HOSTILITY: Extrême
WEAPONS: Deux mitrailleuses rotatives multi-canons Minigun M134 de 7,62 mm
REWARD: 5000FC
THREAT: Absolu

DESCRIPTION: 

Il s'agit d'une plate-forme de combat autonome à chenilles avancée, créée par les ingénieurs de Mental sur la base du robot Sirian Sentry standard. Contrairement aux gardes standards qui patrouillent dans les niveaux inférieurs de Siriuspolis et dispersent les intrus curieux, la Garde est conçue pour la guerre. Une véritable guerre, où le but n'est pas d'effrayer mais de détruire.

The Guardian's appearance is appropriate: un corps gris, mais au lieu de rouge, les parties vertes sont plus grandes et les bandes de roulement sont plus larges et plus massives. Au lieu d'un pistolet photo bleu, deux mitrailleuses multi-canons M134 Minigun de 7,62 mm de Mental Electric sont montées de chaque côté de la tête, tirant jusqu'à 6 000 coups par minute. Chaque.

La tête est ovale, avec des yeux capteurs verts, une antenne et une crête de pointes sur la couronne. Le cou est segmenté, presque comme un ressort, permettant à la tête de tourner à 360 degrés. Cependant, cela n'est plus nécessaire pour viser : les mitrailleuses sont déjà montées des deux côtés, il suffit donc de faire simplement pivoter le corps.

The Guardian is widely used outside of Mental's main army. It's supplied to allies: l'Empire Galactique, diverses PMC et même certains gouvernements planétaires qui peuvent se permettre un tel luxe. Le concept est basé sur le T-1 Terminator, mais contrairement à ce tueur à métal liquide du futur, il est purement mécanique, sans la moindre goutte de matière organique.

Ce n'est pas un chien de garde. C'est une machine pour un nettoyage total de la zone.

TIPS: 

- N'essayez pas d'attaquer de front.
- S'il commence à tirer, cachez-vous derrière une épaisse couverture.
- Ne restez pas dans la ligne de mire.
