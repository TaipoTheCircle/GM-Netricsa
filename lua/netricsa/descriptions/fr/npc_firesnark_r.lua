SNARK DE FEU DU XEN

CLASSE : Snark de Feu
MÉTABOLISME : Insecte carnivore (Mutation du feu)
PERCEPTION : Insecte extraterrestre, braises vivantes
TAILLE: 6 pouces
ENDURANCE : Faible
HOSTILITÉ : Absurdement élevée
ARMES : Morsure de feu
RÉCOMPENSE : 100 FC
MENACE : Élevée

DESCRIPTIF :

Il s'agit d'une mutation naturelle du snark commun, apparue dans les régions ardentes de Xen. Là où la lave coule et où l’air est brûlant, les snarks communs ne peuvent pas survivre. Mais la nature de Xen a trouvé une solution : ceux qui survivent se sont transformés en insectes noirs et rouges dont la coquille brille de l'intérieur, comme des braises.

Contrairement à leurs frères, qui vivent quelques secondes puis explosent par simple enthousiasme, les snarks de feu ne sont pas sujets à l'autodestruction. Ils peuvent vivre longtemps. Très longtemps. Et pendant tout ce temps, ils vont essayer de vous dévorer. Ou vous mettre le feu. Ou les deux en même temps.

Ils ont un corps noir avec des veines rouge vif qui palpitent comme la lave dans un volcan. De loin, ils scintillent et réfléchissent la lumière, ce qui les rend faciles à repérer. Mais dès qu’on s’en rapproche, ils disparaissent de la vue, se fondant dans l’ombre et les roches chaudes.

La morsure d'un snark de feu n'est pas seulement douloureuse : elle enflamme sa cible. Un coup et tu es en feu. Deux, et vous n'aurez pas le temps de vous éteindre. Les survivants affirment que ces créatures attaquent délibérément en groupe afin que leurs victimes n'aient pas le temps de réagir à plusieurs incendies.

Les Fire Snarks sont en conflit constant avec les Devil Bullsquids, leurs voisins des régions de feu. Le conflit dure depuis des siècles. Personne ne se souvient comment tout a commencé. Peut-être que c'était pour une question de territoire. C'était peut-être à cause de la nourriture. Ou peut-être que les Devil Bullsquids ont simplement refusé de reconnaître que le rouge sur les carapaces des Snarks n'était pas « rose », comme ils le prétendaient.

Lorsqu'il est tué, un Fire Snark ne meurt pas simplement. Il se désintègre en morceaux de lave en fusion qui volent dans toutes les directions.

CONSEILS :

- Ne vous approchez pas trop d'un Fire Snark. Il n'explose pas comme un Snark ordinaire, mais sa morsure y met le feu.
- On les voit de loin grâce à leur lueur. Utilisez un fusil de sniper.
- Ils sont doublement dangereux en combat rapproché. Gardez vos distances.
- Les Fire Snarks ne meurent pas tout seuls.
- Si vous voyez un pack, utilisez des explosifs. Il y a déjà du feu partout, un peu plus ne ferait pas de mal.
- Les Fire Snarks et les Devil Bullsquids se détestent. Si possible, confrontez-les les uns aux autres. Laissez-les s'entre-tuer.