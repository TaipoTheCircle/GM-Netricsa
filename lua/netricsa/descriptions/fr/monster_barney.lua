OFFICIER DE SÉCURITÉ DE MESA NOIR


CLASS: Officier de sécurité de Black Mesa
METABOLISM: Humain, agent de sécurité, homme
PERCEPTION: Guerrier de sécurité Black Mesa
SIZE: ~6 pieds de haut
ENDURANCE: moyen
HOSTILITY: élevé envers les intrus, amical envers le joueur
WEAPONS: pistolet, fusil de chasse, complexe d'autorité
REWARD: 1 FC (Il promet de la bière et ne la rend pas)
THREAT: modéré individuellement, élevé si une sauvegarde arrive

DESCRIPTION: 

Barney est l'agent de sécurité par excellence de Black Mesa - un humain loyal et armé qui connaît les couloirs mieux que quiconque. Equipé d'un pistolet, parfois d'un fusil de chasse, et d'un sens du devoir tenace, il patrouille dans les couloirs et tente d'empêcher l'établissement de sombrer dans le chaos total.

Amical pour le joueur, Barney peut combattre les extraterrestres, sauvant ainsi les scientifiques en détresse. Ses compétences sont étonnamment polyvalentes, bien qu'il soit toujours humain et paniquera si un tentacule ou un Gargantua apparaît. Il a une étrange capacité à apparaître juste à temps pour sauver le joueur d'une grêle de Hornets ou d'un Alien Grunt voyou.

TIPS: 

- Restez près de Barney ; il peut aider lors des combats contre les incendies.
- Ne comptez pas sur lui comme tank - il est courageux, mais humain.
- Écoutez attentivement leurs cris : ils pourraient indiquer un danger à proximité.
