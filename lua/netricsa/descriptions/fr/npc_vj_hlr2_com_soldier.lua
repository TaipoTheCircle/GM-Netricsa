COMBINEZ LE SOLDAT OVERWATCH


CLASS: Combiner Overwatch Soldier
METABOLISM: Améliorations humaines augmentées, masculines / biomécaniques
PERCEPTION: Combiner l'infanterie
SIZE: 5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Fusil à impulsion AR2, Fusil de chasse, Grenades
REWARD: 450 FC
THREAT: Haut

DESCRIPTION: 

Les soldats d'Overwatch, également connus simplement sous le nom de Combine Soldiers, sont les exécuteurs du règne du Combine sur Terre. Une fois humains, ils ont été modifiés chirurgicalement et améliorés cybernétiquement pour servir d'infanterie transhumaine. Leurs corps sont modifiés pour l'endurance et la discipline, tandis que leurs esprits sont conditionnés pour une loyauté inébranlable envers le Combine Overwatch.

Ils portent une armure tactique sombre avec des respirateurs qui déforment leurs voix en tons mécaniques, leur donnant une présence inhumaine sur le champ de bataille. Très organisés et compétents sur le plan tactique, les soldats d'Overwatch coordonnent les assauts avec précision, faisant souvent appel au soutien de manhacks, de scanners ou d'unités plus lourdes.

Impitoyables et implacables, ils sont déployés partout où surgit une résistance à l’autorité de l’Alliance, écrasant l’opposition avec une puissance de feu écrasante.

TIPS: 

- Attendez-vous à des tactiques d'embuscade et à des tirs coordonnés - ne vous laissez pas flanquer.
- Les explosifs sont très efficaces contre leurs escouades.
- Les tirs à la tête peuvent contourner leur gilet pare-balles.
