ARTILLEUR STROGG DE STROGGOS


CLASS: Artilleur Strogg
METABOLISM: Organisme cybernétique (Strogg bio-augmenté)
PERCEPTION: Soutien à distance Strogg / Combattant de niveau intermédiaire
SIZE: ~7 pieds de haut
ENDURANCE: Élevé (durabilité de niveau intermédiaire)
HOSTILITY: Haut
WEAPONS: Nailgun (projectiles à rafale), lance-grenades (grenades rebondissantes)
REWARD: 600 FC
THREAT: Haut

DESCRIPTION: 

Le Gunner est une formidable unité Strogg, imposante par sa stature et mortelle en combat à distance. Il possède un Nailgun greffé à la place de son avant-bras droit et un lance-grenades qu'il utilise pour lancer des explosifs rebondissants vers sa cible. Lorsqu'il tire des grenades, il donne un avertissement visible en armant son bras lanceur avant de déclencher le tir.

Contrairement aux Stroggs orientés au corps à corps, le Gunner préfère s'asseoir, garder les couloirs ou les pièces, supprimer les ennemis avec des clous et soutenir les unités plus lourdes. Son mouvement plus lent le rend moins mobile que les autres, mais sa puissance à distance compense grandement. Sous-estimer un artilleur peut être mortel : son barrage de clous et ses grenades chronométrées peuvent détruire votre santé et votre armure si vous êtes pris à découvert.

TIPS: 

- Les armes lourdes ou les attaques explosives fonctionnent mieux : les tirs de mitrailleuses prennent de nombreux coups.
- Engage at mid-range: à courte portée signifie que vous risquez d'être touché à la fois par des tirs de pistolet à clous et par des grenades.
