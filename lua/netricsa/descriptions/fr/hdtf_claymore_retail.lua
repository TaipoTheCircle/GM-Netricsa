MINE ANTIPERSONNEL M18A1 CLAYMORE


WEAPON: M18A1 Claymore (mine antipersonnel directionnelle)
AMMO: Charge C-4 avec 700 billes d'acier
ROF: Appareil à usage unique
WEIGHT: 1,6kg

DESCRIPTION: 

Une mine antipersonnel directionnelle de conception américaine développée dans les années 1950 et largement utilisée pour la défense périmétrique et les embuscades. Lors de la détonation, la charge projette environ 700 billes d'acier vers l'avant, produisant un motif de fragments mortels en forme de feuille, ressemblant davantage à une chevrotine dense qu'à une fragmentation aléatoire. L'appareil est déployé courbé vers l'approche ennemie attendue et utilise un simple cadre de visée pour viser ; le boîtier porte la célèbre marque « FRONT TOWARD ENEMY ».

NOTES: 

- Effectiveness: env. Arc de 60° et efficace jusqu'à ~50 m ; mortalité la plus élevée à courte distance (probabilité de victimes notable dans un rayon d'environ 5 à 15 m, avec des blessures possibles plus loin).
- Safety distances: L'US FM 20-32 prescrit des directives de sécurité/espacement (exemples de distances de sécurité : 250 m vers l'avant pour les forces amies, 100 m sur les côtés/arrière - à utiliser lors de la mise en place).
- Limitations: à usage unique, dangereux pour les non-combattants dans les zones peuplées, nécessite un placement, un camouflage et un calcul de secteur minutieux.
