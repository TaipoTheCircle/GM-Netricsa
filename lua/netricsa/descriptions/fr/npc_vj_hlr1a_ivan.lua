IVAN LE "MOTARD DE L'ESPACE"


CLASS: Ivan le "Motard de l'espace"
METABOLISM: Humain, mâle
PERCEPTION: Sujet de test de Black Mesa
SIZE: ~6 pieds
ENDURANCE: moyen
HOSTILITY: aucun
WEAPONS: improvisé
REWARD: 1000FC
THREAT: négligeable

DESCRIPTION: 

Ivan le "Space Biker" est le premier candidat connu au test de Black Mesa, antérieur à la désignation donnée plus tard à Gordon Freeman. Reconnaissable à sa barbe robuste, à sa carrure massive et à sa combinaison de danger orange distinctive d'un modèle plus ancien, Ivan incarne la vision originale d'un sujet de test prêt à se lancer dans des expériences de résonance à haut risque.

Contrairement aux scientifiques ordinaires, l'apparence d'Ivan est étonnamment militante, ce qui donne lieu à des rumeurs internes selon lesquelles il a été choisi non seulement pour ses aptitudes techniques, mais aussi pour sa résilience face au danger. Bien qu'il n'ait pas de capacités extraordinaires au-delà de celles d'un humain ordinaire, son look imposant lui a valu le surnom de « Space Biker » parmi le personnel.

Ivan reste une figure symbolique dans l'histoire de Black Mesa.

