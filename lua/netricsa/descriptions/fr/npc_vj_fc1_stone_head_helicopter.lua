HÉLICOPTÈRE DE PATROUILLE UH-60 "BLACKHAWK" DE L'ARCHIPEL KRIEGER


CLASS: Hélicoptère de patrouille UH-60 « Blackhawk » de l'archipel Krieger
METABOLISM: Avions (HP, Jet)
PERCEPTION: Avion ennemi
SIZE: ~16,7 pieds
ENDURANCE: Haut
HOSTILITY: Haut
WEAPONATORY: Minigun embarqué contrôlé par un tireur
REWARD: 1000 (l'hélicoptère a une masse salariale)
THREAT: Haut

DESCRIPTION: 

Le principal hélicoptère polyvalent des forces Krieger. Utilisé pour les patrouilles, le déploiement rapide de renforts et l'appui-feu rapproché. Il présente un motif de camouflage standard avec le logo de l'entreprise.

Le maléfique Stone Face a capturé un hélicoptère de patrouille mercenaire Krieger UH-60. La colère du Moai non payé est à son paroxysme – cette tête de pierre volante est encore plus dangereuse. Il patrouille dans les points clés de l'archipel, exigeant en vain son salaire, et attaque toute personne en vue, qu'il s'agisse de mercenaires, de Trigens ou de Jack.

La tête du Moai est intégrée à la position du mitrailleur, sa masse de pierre servant d'armure et de renfort structurel. La tourelle M134 sur le côté gauche est contrôlée par le « viseur » magique de l'idole, tirant avec une précision et une vitesse monstrueuses. C’est l’incarnation d’une colère ancienne fusionnée avec la technologie militaire moderne.

TIPS: 

- Évitez le barrage de balles du M134, qui transperce une couverture légère et inflige des dégâts colossaux.
