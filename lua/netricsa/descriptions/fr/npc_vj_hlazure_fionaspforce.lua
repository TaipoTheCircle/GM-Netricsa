FIONA, LE COMMANDANT ASSASSIN DES BLACK OPS


CLASS: Fiona, le commandant assassin des Black Ops
METABOLISM: Humain, Femelle
PERCEPTION: Chef des assassins des Black Ops
SIZE: ~5,9 pieds
ENDURANCE: Haut
HOSTILITY: Extrême
WEAPONS: Beretta
REWARD: 5000FC
THREAT: Catastrophique

DESCRIPTION: 

Fiona est le commandant de la division Black Ops Assassin, un chef impitoyable dirigeant les opérations secrètes lors de l'incident de Black Mesa. Contrairement aux assassins standards, elle opère au plus haut niveau, en coordination directe avec les forces spéciales du secteur Delta et l'homme mystérieux en costume d'affaires avec une mallette.

Son équipe est spécialisée dans les frappes chirurgicales, la récupération de renseignements et l'élimination totale des témoins. Fiona elle-même est plus rapide, plus meurtrière et bien plus disciplinée que les assassins ordinaires, incarnant la précision du commandement des Black Ops. Son partenariat avec l'homme mystérieux en costume d'affaires suggère qu'elle est profondément ancrée dans le programme interdimensionnel plus large derrière l'incident.

TIPS: 

- Ne la combattez jamais de front ; elle est très évasive.
- Attendez-vous à des embuscades : elle attaque rarement sans le soutien de son équipe.
- Surveillez les frappes coordonnées avec les forces du secteur Delta.
- Son alliance avec le porte-documents laisse entrevoir des objectifs allant au-delà du simple combat.
