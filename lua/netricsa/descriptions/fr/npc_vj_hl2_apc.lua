PORTEUR D'ARMÉE COMBINE (APC)


CLASS: Combinez les transporteurs de l'armée (APC)
METABOLISM: Véhicule de transport
PERCEPTION: Combinez un véhicule blindé de transport de troupes
SIZE: 26 pieds
ENDURANCE: Très élevé
HOSTILITY: Haut
WEAPONS: Lanceur d'impulsions + Missiles guidés
REWARD: 1500FC
THREAT: Haut

DESCRIPTION: 

L'APC est un énorme véhicule blindé de transport de troupes Combine utilisé pour les patrouilles, le déploiement rapide de forces et le tir direct contre la résistance. Il combine une armure lourde, des armes redoutables et des systèmes mécaniques conçus pour supprimer tout signe d'insubordination. Ce véhicule est apparu au début du conflit et est particulièrement visible dans les zones urbaines et sur la côte, où il sert de colonne vertébrale aux unités de la Défense Civile et aux soldats d'Overwatch. Son blindage est conçu pour résister à des tirs nourris, tandis que de puissants missiles guidés et un lanceur à impulsions transforment l'APC d'un simple transport en une unité de frappe indépendante.

Dans la pratique, l'APC est utilisé à la fois comme moyen de fournir rapidement des forces vers des points chauds et comme forteresse mobile pour la résistance aux bombardements.

TIPS: 

- Utilisez des grenades ou du RPG V2.
- Les missiles guidés de l'APC infligent des dégâts importants dans la zone de destruction - déplacez-vous à couvert.
