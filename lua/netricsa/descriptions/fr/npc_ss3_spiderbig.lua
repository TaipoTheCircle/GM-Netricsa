ARAIGNÉE ANTARESIENNE JUVÉNILE D'ANTARES PRIME


CLASS: Araignée antarésienne juvénile
METABOLISM: Extraterrestre, arachnide
PERCEPTION: Créature arachnide
SIZE: 6,5 pieds
ENDURANCE: Moyenne
HOSTILITY: Haut
WEAPONS: Crachat acide amélioré, morsure puissante
REWARD: 1000FC
THREAT: Moyenne

DESCRIPTION: 

Le stade juvénile de l’araignée antarésienne est plus grand, plus fort et plus dangereux que ses parents nouveau-nés. Il possède un exosquelette développé qui le protège des tirs d'armes légères et des mâchoires plus puissantes. Comme les nouveau-nés, il peut escalader les murs et les plafonds et tendre des embuscades. Conserve la capacité de cracher de l'acide (plus puissant et avec une portée plus longue) et est également mortel en combat rapproché. Souvent trouvé en groupe, il agit comme un combattant hybride à distance et au corps à corps, distrayant et harcelant les ennemis.

TIPS: 

- Un seul coup précis d'un fusil de chasse à double canon ou un coup puissant d'une masse tue instantanément.
- Deux coups de fusil à pompe, de fusil d'assaut ou de fusil de sniper sont efficaces. Les boules d'acide peuvent être abattues dans les airs.
- Utilisez un pistolet lance-fusées, un lance-grenades ou un C4. À la rigueur, utilisez un pistolet SBC ou un minigun.
