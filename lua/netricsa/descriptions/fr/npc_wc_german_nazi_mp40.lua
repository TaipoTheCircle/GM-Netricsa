SOLDAT NAZI


CLASS: Soldat nazi
METABOLISM: Humain, mâle
PERCEPTION: Infanterie de la Wehrmacht
SIZE: 5,9 pieds
ENDURANCE: Moyen
HOSTILITY: Extrême (envers les ennemis du Reich)
WEAPONS: Mitraillette MP-40, grenades, couteau de combat
REWARD: 250 FC
THREAT: Moyen

DESCRIPTION: 

Classiques fantassins allemands de la Seconde Guerre mondiale, les soldats nazis sont fidèles à leur régime et entraînés à combattre avec une efficacité impitoyable. Armés principalement du MP-40, ils excellent dans les combats rapprochés et à moyenne portée et se déplacent souvent en escouades pour se couvrir mutuellement. Leur service inébranlable en Allemagne les rend à la fois déterminés et dangereux.

Au combat, ils s'appuient sur des tirs de suppression, des tactiques d'embuscade et un grand nombre. Même si leur équipement n’est pas aussi avancé que celui des futures armées, leur discipline et leur coordination en font de redoutables ennemis sur le terrain.

TIPS: 

- Gardez vos distances ; le MP-40 est plus efficace à courte portée.
- Les grenades peuvent perturber les escouades avant de vous submerger.
- Utilisez un couvercle pour éviter les éclats concentrés.
- Isoler les soldats de leur groupe pour les neutraliser plus facilement.
