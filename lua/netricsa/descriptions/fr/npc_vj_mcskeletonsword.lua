ÉPÉISTE SQUELETTE DU MONDE MINECRAFT


CLASS: Épéiste squelette
PERCEPTION: Guerrier de mêlée mort-vivant
METABOLISM: Mort-vivant soutenu comme par magie
SIZE: 6,2 pieds
ENDURANCE: Moyen
HOSTILITY: Haut
WEAPON: Épée de pierre
REWARD: 350FC
THREAT: Moyen-élevé

DESCRIPTION: 

Le Skeleton Swordsman est un squelette qui a décidé que les arcs étaient réservés aux faibles et aux lâches, et que les vrais morts-vivants devraient regarder leurs victimes dans les yeux... même si leurs yeux ont disparu depuis longtemps. Armé d'une épée et d'une expérience de combat douteuse acquise dans l'au-delà, il charge de front sans hésitation.

Parfois, il semble que le Squelette Épéiste en veut vraiment aux vivants, peut-être parce qu'ils ont encore de la peau, des nerfs et la capacité de ressentir la douleur.

TIPS: 

- Ne le laissez pas s'approcher trop près, il est plus dangereux qu'il n'y paraît.
