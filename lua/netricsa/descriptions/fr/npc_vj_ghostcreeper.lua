CREEPER FANTÔME DU MONDE MINECRAFT


CLASS: Creeper fantôme
METABOLISM: Entité éthérée d'un autre monde
PERCEPTION: Creeper's Ghost, mort-vivant éthéré
SIZE: 5,5 pieds
ENDURANCE: Moyenne
HOSTILITY: Varie
WEAPONS: Explosion d'énergie éthérée
REWARD: 200FC
THREAT: Moyenne

DESCRIPTION: 

Il s'agit d'une variante de l'Elemental Creeper qui apparaît comme une créature grise translucide. C'est l'incarnation de l'essence résiduelle d'un Creeper décédé, mais au lieu de disparaître dans l'oubli, il reste figé entre les mondes.

Il peut traverser des foules.

La principale caractéristique du Ghost Creeper est son explosion. Il ne détruit pas les blocs, mais émet plutôt une vague d'énergie éthérée pure lorsqu'il explose. Cette vague est mortelle pour toutes les créatures vivantes dans son rayon, mais est totalement sans danger pour l'environnement.

Malgré leur nature fantomatique, ces plantes grimpantes restent vulnérables à l'acier et à la magie, même si les attraper par surprise peut nécessiter une agilité remarquable ou l'utilisation d'un équipement spécial.

TIPS: 

- Si vous rencontrez un Ghost Creeper, ne paniquez pas. Vous pouvez essayer de l'éviter, car sa vitesse est lente, à moins qu'il ne soit en meute.
- Ne le combattez jamais à l'intérieur. Même s'il ne détruit pas les blocs, son attaque est difficile à esquiver dans un couloir étroit.
- Une arme à dégâts élevés ou l'enchantement "Force" sur un arc aidera à le tuer avant qu'il ne commence à charger son explosion.
