CLONE STORMTROOPER, BOUCLIER DU KAMINO


CLASSE : Clone Stormtrooper, Shielder
MÉTABOLISME : Clone humain, mâle
PERCEPTION : Unité d'assaut révolutionnaire
TAILLE : 5,9 pieds
ENDURANCE : Élevée
HOSTILITÉ : Extrême
ARMES : Bouclier énergétique, Électrobaton, Grenades, Pistolet Impérial
RÉCOMPENSE : 1800 FC
MENACE : Très élevée

DESCRIPTIF :

Les Shielders sont des vétérans du Stormtrooper Corps et occupent l'un des rangs les plus élevés de l'infanterie de ligne de l'Empire Galactique. Leur armure presque entièrement noire et leur longue cape sont le signe d'un statut élevé et d'une vaste expérience de combat. Ces combattants sont chargés de percer les fortifications, de dégager les espaces confinés et d'éliminer les adversaires les plus dangereux.

La principale défense d'un bouclier est un bouclier énergétique massif, capable de résister à pratiquement n'importe quel type de tir d'armes légères. Tant que le bouclier se trouve entre le combattant et la cible, la plupart des attaques sont inefficaces. Cependant, la conception du bouclier laisse les jambes du porteur exposées et n'offre aucune protection dorsale, un fait souvent exploité par les adversaires expérimentés.

En combat rapproché, un bouclier utilise une matraque électrique. Quelques coups puissants suffisent généralement à briser des os, à désactiver une armure ou à faire reculer la victime de plusieurs mètres. Lorsque l'ennemi maintient sa distance, le bouclier lance rapidement trois grenades à fragmentation par-dessus le bouclier, le forçant à quitter son abri.

Si la cible est hors de portée du bâton et que les grenades sont dépensées, le bouclier dégaine un pistolet impérial. Le bouclier tire une courte série de trois coups rapides avec une précision étonnante, puis, d'un mouvement exercé, fait tourner l'arme sur son doigt et la remet dans son étui. Cette manière ostentatoire de manipuler un pistolet est considérée comme une tradition particulière des unités d'élite de l'Empire et sert en même temps de démonstration de calme complet face au prochain ennemi.

CONSEILS :

- Ne tirez pas sur le bouclier. Le bouclier s'en fiche.
- Flancez ou visez les jambes.
- Ne le laissez pas s'approcher trop près. Une matraque paralysante met fin très rapidement à la dispute.
- Si vous voyez voler trois grenades, il est temps de changer de position.