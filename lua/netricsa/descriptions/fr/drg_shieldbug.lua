BOUCLIER D'ESSAI


CLASSE : Swarm Shieldbug
MÉTABOLISME : Xénobiologique
PERCEPTION : Organisme d'assaut lourd
TAILLE : 9,8 pieds
ENDURANCE : Très élevée
HOSTILITÉ : Élevée
ARMES : membres blindés, frappe à l'éperon
RÉCOMPENSE : 1000 FC
MENACE : Très élevée

DESCRIPTIF :

Le Shieldbug est l’une des formes de combat Swarm les plus blindées. L'avant de son corps est recouvert de plaques chitineuses massives capables de résister à des dégâts colossaux. Cela permet à la créature d'avancer sous un feu nourri, couvrant les membres de l'Essaim moins durables.

L'arme principale du Shieldbug sont ses membres antérieurs élargis. En les utilisant comme un bélier vivant, il délivre des coups puissants capables de repousser les adversaires sur une distance considérable. Il y a eu des cas où les victimes sont décédées non pas du coup lui-même, mais de collisions ultérieures avec des objets environnants ou de chutes de grande hauteur.

Malgré son blindage frontal pratiquement impénétrable, le corps présente des zones vulnérables. La zone la moins protégée reste l’arrière du corps, où se trouvent les principaux organes vitaux. Pour cette raison, le Shieldfoot essaie instinctivement de maintenir son adversaire devant lui et est extrêmement réticent à se retourner pendant le combat.

Contrairement à la plupart des membres de l'Essaim, le Shieldfoot agit rarement de manière chaotique. Une fois qu'il a localisé une cible, il peut la poursuivre méthodiquement pendant une longue période, ignorant les adversaires moins dangereux et se concentrant sur la proie qu'il a choisie.

CONSEILS :

- Ne gaspillez pas de munitions sur le blindage frontal.
- Essayez d'attaquer par les flancs ou par l'arrière.
- Ne laissez pas Shieldfoot vous conduire dans des précipices et des passages étroits.
- Si une créature de la taille d'un module de transport décide de vous suivre, ce n'est pas une raison d'être fier.