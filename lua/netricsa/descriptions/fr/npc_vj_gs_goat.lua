CHÈVRE DE GOATVILLE


CLASS: Chèvre
METABOLISM: Herbivore (mais agit comme le chaos incarné)
PERCEPTION: Mammifère ongulé
SIZE: ~ 3 pieds aux épaules
ENDURANCE: Élevé (immunisé contre la plupart des dégâts physiques)
HOSTILITY: Neutre chaotique
WEAPONS: Coup de tête, chaos de ragdoll, lécher
REWARD: 200FC
THREAT: Moyen

DESCRIPTION: 

La Crazy Goat est une chèvre qui défie la physique et qui transforme n'importe quel environnement en pur chaos. Il charge tête première sur les voitures, les objets et les personnes, les envoyant voler avec une force impossible. Bien qu'il ne s'agisse que d'une chèvre, sa capacité à ragdoll à volonté, à lécher et à traîner des objets et à survivre aux explosions en fait une catastrophe ambulante (et bêlante).

TIPS: 

- Ne sous-estimez pas le coup de tête ; il peut lancer même des objets lourds.
- Si c'est des ragdolls, ce n'est pas mort, c'est juste un jeu de physique.
- Le chaos suit partout où il va ; évitez de vous tenir à proximité d’objets fragiles.
