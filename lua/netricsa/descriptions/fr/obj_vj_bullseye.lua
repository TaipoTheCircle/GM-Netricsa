ŒIL DE BULLE


CLASS: CALMAR, TERRE
METABOLISM: Entité scriptée
PERCEPTION: Proxy cible/entité de redirection
SIZE: ~1 pied
ENDURANCE: Faible
HOSTILITY: Neutre
WEAPONS: Aucun
REWARD: 1 FC
THREAT: Très faible

DESCRIPTION: 

Bullseye is a special scripted entity used internally by the engine (and map makers) to redirect damage. Essentially, it is a target proxy: tout ce qui est lié à Bullseye verra ses dégâts ou son rayon de feu redirigés vers la position de Bullseye. Par exemple, vous pouvez placer un Bullseye invisible dans une pièce, y lier des modèles de PNJ et lancer des attaques visant les PNJ touchés à l'emplacement réel de Bullseye.

En termes de gameplay, Bullseye est invisible et intangible ; il ne se bat pas et ne réagit pas. Son rôle est purement technique : il est utilisé dans les cartes pour permettre de « tirer à travers les murs » ou de rediriger les attaques vers des énigmes, des pièges ou des événements scénarisés.

TIPS: 

- Comprendre Bullseye permet aux mappeurs de produire des balles de téléportation, des murs de verre ou des déclencheurs à distance.
