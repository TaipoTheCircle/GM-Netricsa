SAITAMA, ONE PUNCH MAN


CLASS: Saitama, One Punch Man
METABOLISM: Humain normal (bien qu’illimité)
PERCEPTION: Héros
SIZE: ~5,9 pieds
ENDURANCE: Infini (immunisé contre les dégâts)
HOSTILITY: Neutre (se bat uniquement lorsqu'il s'ennuie ou est ennuyé)
WEAPONS: Son poing (One Punch)
REWARD: ??? (aucun ne survit à la collecte, sauf le moustique)
THREAT: Au-delà de l'apocalypse

DESCRIPTION: 

Également connu sous le nom de "Caped Baldy", Saitama est un héros ridiculement maîtrisé qui passait par là et a décidé avec désinvolture d'aider l'Allemagne dans ses conflits. Pour la plupart, il ressemble à un homme chauve ordinaire vêtu d'un costume jaune avec une cape blanche - mais en vérité, il est capable de vaincre n'importe quel adversaire d'un seul coup de poing.

Contrairement aux autres combattants, Saitama n'a pas de véritable stratégie, pas de programme d'entraînement (autre que 100 pompes, 100 redressements assis, 100 squats et une course de 10 km chaque jour) et aucune peur. Ses combats se terminent souvent instantanément, laissant les alliés et les ennemis tout aussi confus.

Bien qu'invincible, sa plus grande faiblesse est son ennui : il prend rarement quoi que ce soit au sérieux et n'apprécie vraiment un combat que lorsqu'il rencontre un adversaire qui semble (au moins brièvement) difficile.

TIPS: 

- Ne vous attendez pas à un duel, le combat se terminera par un seul coup de poing.
- S'il est de ton côté, félicitations, tu as déjà gagné.
- Son plus grand ennemi est le monstre des moustiques.
