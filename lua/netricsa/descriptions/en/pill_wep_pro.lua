PROFESSIONAL RIFLE


WEAPON: Professional Rifle
AMMO:   7.62x51mm NATO
ROF:    ~12 rounds / min

DESCRIPTION:

The Professional Rifle is a high-precision bolt-action rifle based on the Accuracy International L96A1 / Arctic Warfare platform and designed for professional long-range combat. Its design retains all the advantages of the original: a heavy floating barrel, outstanding accuracy, and stability at long ranges.

However, the rifle has one frightening feature: a powerful speaker embedded inside the frame, recording hundreds of distorted, enraged human screams. With each shot, the speaker plays them simultaneously with the shot, creating a deafening acoustic shock. The speaker is permanently soldered to the internal frame and cannot be removed without completely destroying the weapon.

The weapon effectively eliminates targets, but poses a serious hearing risk to the shooter and anyone nearby.

NOTES:

- Exceptional accuracy and high damage at long ranges.
- Each shot creates extreme noise, capable of causing temporary or permanent hearing loss.
- Demoralizes enemies and disorients nearby targets.
- Use without hearing protection is highly discouraged.
- Considered a psychological weapon, not a simple sniper rifle.