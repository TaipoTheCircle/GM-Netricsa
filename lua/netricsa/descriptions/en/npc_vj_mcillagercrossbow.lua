PILLAGER ARBALIST FROM THE MINECRAFT OVERWORLD


CLASS:      Pillager Arbalist
METABOLISM: Human
PERCEPTION: Pillager with a crossbow, Illager light infantry
SIZE:       6,2 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Crossbow
REWARD:     100 FC
THREAT:     High

DESCRIPTION:

One of the most common and recognizable hostile villager varieties, armed with a crossbow and merciless. While a normal villager trades with you and builds farms, a Pillager Arbalist simply wants to turn you into an arrow cushion.

Unlike their Vindicator cousins, who prefer close combat, Pillager Arbalist are natural-born snipers. Their main and only weapon is a crossbow, which they fire every 3 seconds at a target within a 16-meter radius. When fully submerged, Pillager Arbalist switch to close combat, as the crossbow is ineffective underwater.

Pillager Arbalist inhabit three locations, each of which promises trouble. First, there are Pillager Arbalist patrols-groups of 2-5 marauders who roam the world in search of villages. Second, there are Pillager Arbalist outposts-watchtowers with cages for iron golems, around which these archers constantly reside. And third, there are raids, where Pillager Arbalist form the backbone of the first waves of invaders.

The main characteristic of Pillager Arbalist is their cowardly but effective tactics. They never engage in close combat, preferring to keep their distance and methodically shoot at their target. If a player approaches, the Pillager Arbalist backs away, trying to maintain their distance. In narrow corridors, this can be fatal, as it's difficult to dodge an arrow.

Among the Pillager Arbalist, there are marauder captains with an ominous flag on their heads. They are easily recognized by the black and white banner fluttering above their hood. Killing such a captain inflicts the "Bad Omen" status effect. The level of the effect depends on the number of captains killed. With this status effect, a raid begins upon entering a village-a series of waves in which Pillager Arbalist and other Ilagers attempt to eliminate all the villagers.

During raids, Pillager Arbalist are especially aggressive, specifically attacking villagers. If they manage to kill a villager or destroy a door, they let out a triumphant laugh and jump in place.

Pillager Arbalist can appear mounted on reavers-huge beasts that destroy everything in their path. A reaver rider is an especially dangerous opponent, as they must dodge both arrows and battering rams.

In raids, Pillager Arbalist sometimes drop emeralds. Captains always leave behind an ill-omened flag-a valuable trophy and a source of trouble.

If a Pillager Arbalist's crossbow breaks (crossbows have durability, like any tool), they can't fire and become helpless, though they remain technically hostile.

TIPS:

- A shield completely blocks arrows and allows you to close in without taking casualties.
- Don't run straight at a Pillager Arbalist. Shoot back with a bow or crossbow-your arrows deal the same damage as theirs.
- Don't stand out in the open in narrow corridors-the arrow will fly right in.
- If you see a captain with a flag, think twice before killing them. The "Ill Omen" buff will start a raid as soon as you enter the village.
- Milk removes the "Ill Omen" buff if you're not yet ready for a raid.
- In water, the Pillager Arbalist is helpless and switches to melee combat, which deals less damage.
- In raids, try to kill the Pillager Arbalist first-they prevent you from focusing on stronger enemies.
- Don't let them surround you-arrows from both sides are guaranteed to hit.
- If the Pillager Arbalist is riding a Ravager, kill the archer first-the beast is less dangerous without a rider.
