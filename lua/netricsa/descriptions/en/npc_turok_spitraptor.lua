SPITTER RAPTOR FROM THE TERRAFORMED PLANET


CLASS:      Spitter Raptor
METABOLISM: Predator, Theropod
PERCEPTION: Genetically Modified Utahraptor
SIZE:       ~8,2 ft
ENDURANCE:  Average
HOSTILITY:  Extremely High
WEAPONS:    Claws, Teeth, Venomous Spit, Back Spikes
REWARD:     600 FC
THREAT:     High

DESCRIPTION:

This species emerged when the Mendel-Gruman corporation decided that the normal Utahraptors on the planet weren't enough and turned to genetic engineering. The corporation wanted to create a more effective weapon of mass destruction by producing raptors that could spit venom while retaining all the habits of wild predators.

The Spitter raptor is distinguished from its brethren by its gray coloring, complete lack of feathers, and a row of short spikes protruding along its back. Because of these spikes and the sac under its throat, it was initially mistakenly called a baby spinosaurus.

They have a yellow, leathery sac under their neck that stores toxin. When the sac is full, it visibly swells, warning an attentive shooter that fun is about to begin.

TIPS:

- Don't stand still. The spit flies in a straight line.
- In close combat, it's just as dangerous as a normal raptor. Don't get surrounded.
