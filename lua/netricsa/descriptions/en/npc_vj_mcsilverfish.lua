SILVERFISH FROM THE MINECRAFT OVERWORLD


CLASS:      Silverfish
METABOLISM: Arthropod
PERCEPTION: Stone-destroying parasite
SIZE:       ~0,9 ft
ENDURANCE:  Very low
HOSTILITY:  High
WEAPONS:    Quick bites
REWARD:     500 FC
THREAT:     Medium

DESCRIPTION:

Silverfish are walking aberrations of evolution that have decided that stone is the best home. These small, nervous creatures dwell inside infested blocks and spring forth as soon as anyone dares to dig in the wrong place with a pickaxe.

They are especially fond of fortresses, where they feel like the elite of underground society and guard portals as if they were paid to do so.

TIPS:

- Kill quickly.