DESCRIPTION:

A self-loading sniper rifle developed by Eugene Stoner in the 1990s based on the AR-15 rifle design. Various modifications of the SR-25 are in service with the US Armed Forces and Marine Corps, as well as a number of law enforcement agencies in Australia, Israel, and Poland. Civilian versions are used in shooting sports, police and ranger services.

NOTES:

- This rifle is effective at medium to long ranges.
- It does not have an automatic fire mode.
- Some people use it to eliminate people they dislike, such as politicians.

