PALE PIG FROM THE MINECRAFT OVERWORLD


CLASS:      Pale Pig
METABOLISM: Warm-blooded mammal
PERCEPTION: Passive animal of Snow Biomes
SIZE:       3 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     50 FC
THREAT:     Minimal

DESCRIPTION:

A rare variety of the common pig, adapted to life in snow biomes and distinguished from its pink counterparts by its duller, paler skin tone. The lack of pigmentation caused by the harsh environment is more than compensated for by its charm, as the locals rightly note.

Pale pigs behave no differently from regular pigs: they wander across snowy plains, avoid lava and cliffs, and flee for a few seconds when damaged.

TIPS:

- Look for Pale pigs in snowy biomes—they feel right at home there.
- Their lack of pigment doesn't affect their usefulness: they can be bred, mounted, and used as a food source.
- Pale pigs are excellent camouflage in the snow—be careful not to miss them.
- If you breed a Pale pig with other varieties, the piglets can have any color from the parents.
