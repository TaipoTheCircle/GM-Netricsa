ORC SOLDIER, GUNNER FROM THE ORAZIO


CLASS:      Orc Soldier, Gunner
METABOLISM: Mammal
PERCEPTION: Primitive warrior race, former Zorgs
HEIGHT:     6,3 ft
ENDURANCE:  Very low
HOSTILITY:  Very low
WEAPONS:    Rocket Laucher GVR-57 MK.4
REWARD:     20 FC
THREAT:     Low

DESCRIPTION:

This is a clear example of the saying "they wanted the best, but it turned out as usual". They were once part of the great Zorg race from Bilmez IV, strong, ferocious, and, most importantly, tanned. But constant sunbathing resulted in the Zorgs losing almost all their skin. Seeking a solution, they created a mutagen that would restore their former appearance. Instead, the mutagen turned them into orcs...

The drug was immediately banned, and the experimenters, along with all the victims, were exiled to the secluded planet of Orazio. There, the exiles made good use of their time, founding the Altani mining corporation and began mining ore, eventually falling into Mental's hands.

Gunners are Orcs who loved to blow up everything in their class; as a result, they were issued rocket launchers GVR-57 MK.4, and now serve as Mental's primary elite anti-tank infantry.

TIPS:

- Unlike most of Mental's minions, they will try to keep their distance when close to you.
- You can dodge their missiles by moving to the side, or shoot them down.
