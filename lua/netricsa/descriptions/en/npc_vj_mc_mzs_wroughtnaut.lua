FERROUS WROUGHTNAUT FROM THE MINECRAFT OVERWORLD


CLASS:      Ferrous Wroughtnaut
METABOLISM: Enchanted Automaton
PERCEPTION: Forgotten Guardian
SIZE:       11 ft
ENDURANCE:  Extremely high
HOSTILITY:  Absolute
WEAPONAUT:  Axe of a Thousand Metals, Magical Stomp
REWARD:     10000 FC
THREAT:     Maximum

DESCRIPTION:

A forgotten guardian, imprisoned for centuries in the Forged Chambers. This is neither a man nor a statue - it is a piece of magically animated armor whose soul has long since faded, leaving only blind rage and an impossible duty. It has forgotten what it guards, who it waits for, and for whom it mourns. He remembers only one command: to destroy anyone who dares disturb the eternal peace of his halls. He is completely immune to all damage except for strikes to a specific vulnerable point-a wound on his back, which is only vulnerable during his most powerful attack, "Axe Strike", when he plunges his weapon into the ground. Outside of combat, his magical armor instantly heals any damage.

TIPS:

- Damage can ONLY be inflicted during his "Axe Strike" attack, when he pauses, plunging his axe into the ground, exposing his back. Aim precisely for the wound.
- The "Stomp" attack can be dodged by jumping at the right moment. Sweeping axe strikes should be avoided sideways.
- Provoke him into "Axe Strike" by staying at medium range. After a miss, quickly move behind him and deliver precise strikes. Avoid close combat-his shields are useless against his attacks.
- When his health is low, he performs combos of two or three attacks, the last of which is a spinning attack that leaves almost no chance to dodge. At this point, it's best to retreat and wait for the next opportunity to attack from behind.
- Upon defeat, you receive his Forged Helmet and the legendary Axe of a Thousand Metals.
