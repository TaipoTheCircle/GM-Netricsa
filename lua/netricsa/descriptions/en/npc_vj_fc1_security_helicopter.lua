DESCRIPTION:

The primary multi-purpose helicopter of the Krieger forces. Used for area security. The crew consists of a pilot, a gunner operating the onboard minigun, and up to three paratroopers. When spotted, the helicopter begins a frantic attack. It's impossible to shoot it down.

TIPS:

- It's best not to argue with this machine; it'll kill you in one shot.