BEHEADED FEMIKAZE


CLASS:      Beheaded Femikaze
METABOLISM: Cyber-zombie
PERCEPTION: Insentient, LCU controlled
SIZE:       6 ft
ENDURANCE:  Very Low
HOSTILITY:  Extreme
WEAPONS:    Hand Grenades
REWARD:     350 FC
THREAT:     High

DESCRIPTION:

The Femikaze is a female variant of the beheaded kamikaze from Sirius, created by Mental using the LCU. Its head is missing, so it relies entirely on remote control and built-in aggression algorithms. Its primary objective is to get as close to its target as possible and explode along with the grenades it holds.

Outwardly, he looks "fashionable and menacing": his T-shirt mimics a naked body with a red swimsuit, jeans, and bright red sneakers. Bombs are accessories, but their effect is far less decorative.

TIPS:

- Explodes immediately upon death.
- Keep your distance and use long-range weapons.
- In a group, he turns any area into a chaotic explosion field.
- Looks can be deceiving: a stylish T-shirt and sneakers don't reduce the danger.