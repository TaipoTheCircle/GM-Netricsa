POCKET KNIFE


WEAPON:    Pocket knife

DESCRIPTION:

A standard pocket knife. Designed for use in various situations where a knife is needed, including in fights.

NOTES:

- Pressing LMB + R allows you to change the type of attack. 
- In cutting mode, it is recommended to aim for the limbs to cause arterial bleeding.
- Pressing RMB on an item secured with tape allows you to start untying it. 