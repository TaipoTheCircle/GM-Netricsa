HUGO BOSS FROM THE SIRIUS


CLASS:      Hugo Boss 
METABOLISM: Mechanical, Boss Robot
PERCEPTION: Programmed Giant Battle Robot-Destroyer And Mental's Toy
SIZE:       29,5 ft
ENDURANCE:  Extreme
HOSTILITY:  Absolute
WEAPONS:    Fists, Antenna Lasers, Shoulder-Mounted Rocket Launchers, Arm-Cannon, Jetpack Ram
REWARD:     5000 FC
THREAT:     Absolute

DESCRIPTION:

The most advanced robot in Mental's entire army. It is the "pinnacle of Horde engineering": armored to the point of near invulnerability, armed to the teeth, and capable of flight. In appearance, it resembles a toy come to life - a huge robot with an antenna on its head, flashing lights, and sounds like those of a child's toy. But this toy kills.

When Mental decided to create an army capable of suppressing any pockets of resistance in the universe, he realized that ordinary soldiers weren't enough. Something more was needed. A symbol was needed.

The project was originally called "CSS-00" - "Combat Siege System." It was to be a robot capable of single-handedly destroying entire armies and withstanding the attacks of a space fleet.

But Mental, who never had good taste, insisted on changes.

"Make it look like a toy," he told the chief engineer.

"But, Master, it's a fighting machine!"

"I said, make it look like a toy."

Mental's second requirement was a certain "box."

"The boss should come out of the box. It adds drama."

"We can just put him in the hangar."

"Out of the box! And write on it 'Boss in the Box,' so everyone knows."

The engineers sighed and rolled up their sleeves. The box was made. The writing was applied. Hugo was packed.

Later, Hugo was deployed on the Kronor satellite, where he guards the control system of the laser cannon that penetrates the Sirius shield.

And He Will Not Spare a Soul.

TIPS:

- Hugo is invulnerable in the first phase.
- Attack him during flight.
- Watch out for missiles and cannonballs.
