SWARM BUZZER


CLASS:      Swarm Buzzer
METABOLISM: Xenobiological
PERCEPTION: Flying Predator
SIZE:       1,6 ft
ENDURANCE:  Very Low
HOSTILITY:  High
WEAPOON:    Venomous Sting, Air Attack
REWARD:     75 FC
THREAT:     Low

DESCRIPTION:

Buzzers are small flying Swarm organisms adapted to stalking and harassing larger opponents. Individually, they rarely pose a serious threat, but their appearance usually means the beginning of new problems.

Their primary hunting method is rapid attacks from above. Using their high maneuverability, the Buzzer dives sharply toward its target, striking with its venomous stinger, and then ascends again. Their small size makes them a difficult target even for experienced marksmen.

Buzzer venom isn't particularly lethal, but it can cause pain, disorientation, and decreased concentration. This is why these creatures are especially dangerous during encounters with other members of the Swarm. While the prey is distracted by the flying pest, larger predators have the opportunity to close in.

Observations show that Buzzers rarely show caution and often attack targets significantly larger than themselves. Apparently, evolution has compensated for their lack of self-preservation instinct with the ability to reproduce rapidly.

TIPS:

- Don't ignore Buzzers, even when fighting more dangerous opponents.
- Use weapons with a high rate of fire or automatic aiming.
- Watch the air above you.
- If you hear an annoying buzzing sound around you, it means someone is already determined to ruin your day.
