UZI


WEAPON: Uzi
AMMO:   5.56mm rounds
ROF:    600 rounds / min

DESCRIPTION:

The Uzi is a compact submachine gun. It features high accuracy and does not require reloading, allowing for effective long-range engagements with minimal accuracy drop.

NOTES:

- Ideal for dealing with numerous weak to medium-strength enemies, such as Beheaded Kamikazes and Clowns.