SIG SAUER SIG556 CLASSIC CARBINE


WEAPON: SIG Sauer SIG556 Classic
AMMO:   5.56x45 mm NATO (.223 Rem.)
ROF:    Gas-piston semi-automatic (two-position gas system)

DESCRIPTION:

The SIG556 Classic is a U.S. carbine styled to resemble the Swiss SG family. It features a triangular synthetic folding stock with three-position length adjustment, two-piece fore-end with provisions for Picatinny rails, and a piston-driven gas system with an adjustable valve for standard and emergency settings. Range testing showed very good accuracy with several commercial .223/5.56 loads, soft felt recoil and flawless function over several hundred rounds.

NOTES:

- Barrel heat can influence accuracy in sustained fire.