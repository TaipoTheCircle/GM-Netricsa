VODKONSON'S ASSAULT RIFLE


WEAPON: Vodkonson's Assault Rifle
AMMO:   Vodka Bottle Caps
ROF:    ~1800 shots/min

DESCRIPTION:

The Vodkonson's Assault Rifle is a primitive but insanely effective weapon. Vodka accelerates the user's hands to superhuman levels. The user manually inserts the caps into the bottle, but does so so quickly that the weapon seems to pluck the caps from thin air.

The caps fly out at incredible speed thanks to the shooter's insane arm strength, turning into deadly projectiles capable of tearing apart any creature or structure.

NOTES:

- The user experiences incredible strength and arm speed after drinking vodka.
- The caps rip through flesh, armor, and synths thanks to supersonic speed.
- Can be used to play Skittles with a variety of enemies.
