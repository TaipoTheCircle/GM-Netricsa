CUCURBITO THE PUMPKIN, SMALL


CLASS:      Cucurbito the Pumpkin, Small
METABOLISM: vegeterian
PERCEPTION: genetically programmed
SIZE:       0.8 ft
ENDURANCE:  low
HOSTILITY:  medium
WEAPONS:    chainsaw
REWARD:     1500 FC
THREAT:     medium

DESCRIPTION:

A terrifying product of Mental's accidental genetic experiment, created under the influence of strange 20th-century artifacts. Upon awakening, even its creator was shocked by this insatiable, frightening beast. However, the experiment continued. Inspired by the success (if you can call it that) of shrinking the Sirian bulls, Mental continued experimenting with size. Now you can encounter miniature Cucurbitos. The smaller versions retain the same dull aggression and are armed with tiny, but no less deadly, chainsaws. They are dangerous in packs and can attack from hiding places, such as pumpkins.

