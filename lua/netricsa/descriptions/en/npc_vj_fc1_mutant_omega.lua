OMEGA MUTANT FROM THE KRIEGER ARCHIPELAGO


CLASS:      Omega Mutant
METABOLISM: Final Trigen Mutation
PERCEPTION: Combat Platform
SIZE:       12 ft
ENDURANCE:  Maximum
HOSTILITY:  Absolute
WEAPONS:    Missile Launchers, Mini-Missiles
REWARD:     5000 FC
THREAT:     Catastrophic

DESCRIPTION:

Omega is the culmination of Krieger's experiments, the final and most advanced form of Trigens. Visually reminiscent of the Fat Man, but superior in every way. Equipped with heavy armor and a built-in arsenal, it turns into a walking fortress. It uses a combination of weapons: hand-fired missiles, while shoulder-mounted missile launchers blanket an area with mini-missiles.

He acts as a mobile firing point, alternating between attack modes to engage targets at any range. Shotguns are effective against nearby enemies, rockets against distant groups, and mini-rocket volleys leave no room for cover. His armor absorbs significant damage, making him virtually invulnerable to standard weapons.

TIPS:

- Use heavy weapons - only grenade launchers and explosives are effective against his armor.
- Keep moving - standing still equals death from a rocket volley.
- Attack from the flanks - his primary weapons are concentrated in the forward hemisphere.
- Seek cover from mini-rocket volleys by moving indoors or behind solid structures.