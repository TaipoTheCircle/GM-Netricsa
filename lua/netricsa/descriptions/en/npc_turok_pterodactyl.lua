PTERANODON FROM THE TERRAFORMED PLANET


CLASS:      Pteranodon
METABOLISM: Flying lizard, Pterosaur
PERCEPTION: Aerial predator
SIZE:       Enormous wingspan
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Beak, claws, dive attack
REWARD:     400 FC
THREAT:     Medium

DESCRIPTION:

One of the few flying lizards that doesn't just soar through the sky, but actively hunts anything that moves on the ground. This subspecies is aggressive and views humans as legitimate prey.

In combat, the Pteranodon uses a "dive-peck-fly" tactic. It appears from out of sight, dives at its target, striking with its beak or attempting to grab with its talons, and immediately rises to repeat the attack a few seconds later.

TIPS:

- Don't stand in the open.
- A sawed-off shotgun will kill it in two shots.
