MARSH-HOPPER FROM RIGIL KENTAURUS, HUGE


CLASS:      Marsh-Hopper from Rigil Kentaurus, Huge
METABOLISM: terrestrial amphibian
PERCEPTION: wild animal
SIZE:       26 ft (standing)
ENDURANCE:  none
HOSTILITY:  extreme
WEAPONS:    toxic slime splash
REWARD:     5000 FC
THREAT:     very low

DESCRIPTION:

A strange organism originating from marshy wastelands in the system of Rigil Kentaurus. They live in large groups and have a suicidal instinct for protecting their territory. In all-devastating havoc, all individuals try to leap at the intruder and explode, by over inflating themselves. Upon exploding, toxic slime contained in their intestines splashes the target.

Mental's favorite and the pinnacle of his size experiments is the Red Leviathan, twice the size of its already gigantic predecessor. Its crimson body ripples with thousands of liters of toxic fluid. But the main secret and surprise for anyone who decides to attack it lies within. Mental has placed approximately sixty microscopic Leapers in special incubator bubbles within its body. Killing the Leviathan is only the beginning of a true nightmare. Upon its death, the body explodes, releasing not only a sea of ​​poison but also a swarm of tiny, nearly invisible, incredibly fast, and deadly Leapers that instantly attack all living things within the blast radius.

TIPS:

- Don't let it get close. Attack from maximum range.
- Destroy the body. Be prepared to retreat immediately or use a full-damage weapon (flamethrower, grenade launcher) against the swarm of micro-Leapers that has escaped. - It's better to trigger its explosion from a distance, using remote explosives or decoys to destroy both the main body and the hidden swarm at the same time, without getting too close.