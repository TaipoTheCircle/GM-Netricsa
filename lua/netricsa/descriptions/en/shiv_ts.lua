SHIV


WEAPON: Shiv
AMMO:   None (melee)
ROF:    80 strikes/min

DESCRIPTION:

A shiv is a catch-all term for a small improvised stabbing or cutting implement used in close-quarters combat or as a makeshift melee weapon. It may be a sharpened piece of metal, broken glass, carved plastic or a small blade fitted into a handle.

NOTES:

- High damage on vital hits (head, neck, chest); reduced effectiveness vs. armor or hardened targets.
- Excels in stealth, ambushes and confined spaces; poor choice for ranged engagements.
- Fragile and short reach compared to larger melee weapons; requires close proximity to target.