ASSAULT TROOPER WITH M4 FROM KRIEGER ARCHIPELAGO 


CLASS:      M4 Assault Trooper, Basic Krieger Mercenary
METABOLISM: Human, Male
PERCEPTION: Standard vision, basic training
SIZE:       6 ft
ENDURANCE:  Low
HOSTILITY:  Average
WEAPON:     M4
REWARD:     150 FC
THREAT:     Low

DESCRIPTION:

The basic infantry of the Krieger mercenaries, the most numerous and least trained fighters. They resemble scouts in appearance, but lack body armor, making them vulnerable. They are most often found in barracks, headquarters, and offices, where they can rest or perform support tasks (paperwork, radio). Their tactics are simple: rely on their numerical superiority and use the cover of more armored assault troops. They don't possess exceptional skills, but in large groups they can pose a threat due to the density of their fire. Dressed in blue, or less commonly black or green, T-shirts, they are the "cannon fodder" of Krieger's army.

TIPS:

- They are encountered in groups. Focus on infantry first to quickly thin out the enemy's numbers, then switch to the more dangerous mercenaries.
- They often hide behind cover and allies. Use weapons with a high rate of fire or a shotgun at close range to quickly eliminate groups.
- They don't pose a serious threat individually, but can waste ammo and create a distraction in large skirmishes.