SPAS-12 SHOTGUN


WEAPON:      SPAS-12 Shotgun
AMMO:        12-gauge shells
ROF:         Pump-action, ~60 rds/min

DESCRIPTION:

Close-range shotgun delivering overwhelming stopping power. Capable of firing single shells for efficiency or double blasts for maximum damage at the cost of ammo consumption.

NOTES:

- Highly effective in close combat.
- Double-shot mode delivers devastating bursts.
- Very poor accuracy at long range.