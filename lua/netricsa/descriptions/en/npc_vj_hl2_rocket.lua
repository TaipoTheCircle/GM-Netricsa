COMBINE HOOK-UP MISSILE


CLASS:      Autonomous Guidance Weapon
METABOLISM: Artificial Intelligence
PERCEPTION: Friend or Foe Recognition, Thermal and Signal Sensors
SIZE:       1,6 ft
ENDURANCE:  Absolute
HOSTILITY:  Very High
WEAPONS:    Directional Explosive Charge
REWARD:     1 FC
THREAT:     Critical

DESCRIPTION:

The Combine Homing Missile is an autonomous munition controlled by an embedded artificial intelligence. After launch, it independently analyzes the surrounding area, identifying targets based on the "enemy" principle. If a target is deemed hostile, the missile locks on it and continues to pursue it until it is destroyed.

TIPS:

- Avoid it; it's impossible to shoot it down without the SBC Cannon.