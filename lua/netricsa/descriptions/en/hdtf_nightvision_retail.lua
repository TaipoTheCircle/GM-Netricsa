AN / PVS-7 NIGHT VISION GOGGLES


ITEM: AN/PVS-7 bi-ocular night vision device
WEIGHT: 680 g

DESCRIPTION:

The AN/PVS-7 is a single-tube bi-ocular night vision goggle: one image intensifier feeds two eyepieces, offering a lighter, simpler alternative to dual-tube systems. Fielded since the 1980s, it became a workhorse NVG for military and security forces due to its ruggedness and helmet-mount capability. 


NOTES:

- Automatic Brightness Control (ABC) and bright-light cutoff / auto-gating protect the tube and preserve image detail under changing light.
- Built-in IR illuminator enables operation in total darkness.
