AUTOMATON AERIAL PATROL GUNSHIP FROM THE CYBERSTAN


CLASS:      Automaton Aerial Patrol Gunship
METABOLISM: Mechanical
PERCEPTION: Flying Patrol Ship, Air Support
SIZE:       13 ft
ENDURANCE:  Medium
HOSTILITY:  Absolute
WEAPONS:    Rapid-Fire Cannons, Missiles
REWARD:     2000 FC
THREAT:     High

DESCRIPTION:

This is an Automaton flying patrol ship designed to support ground troops and destroy Super-Earth infantry from the air. Externally, it resembles an enlarged drone with two main engines and distinctive delta wings. Rapid-Fire cannons are mounted on the sides of the fuselage, and missile launchers are mounted on the wings.

It hovers in the air, firing at Helldiver positions, and can pursue them at low altitude. It can be destroyed with RPGs, autocannons, or Stratagems.

They patrol landing zones, guard Automaton outposts, and attack Helldivers who linger in one place for too long.

It has several weak points: Engines and Wings.

TIPS:

- When you hear the sound of engines, seek cover. It can attack from any direction.
- Don't stand in the open. It will fire at you from the air.
- To destroy it, use RPGs or autocannons. The rifle consumes too many rounds.
- If the Ship is hovering, this is the best time to attack. Shoot the engines or wings.
- They often arrive in groups. Destroy them one by one to avoid getting caught in crossfire.
- If you triggered a Stratagem, make sure the Gunship doesn't disrupt your reinforcement drop.
- Don't try to outrun it. It's faster than you. It's better to try to disrupt it.
