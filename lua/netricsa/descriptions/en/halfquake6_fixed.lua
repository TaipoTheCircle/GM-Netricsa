HALF-QUAKE SHOW, JUMP AND RUN


The sadists seem to have decided to change genres.

Now it's parkour. In the dark.

The rooms are immersed in semi-darkness, the kind where walls appear just as you're already falling toward them. The floor is riddled with lava pits. Unmarked. Unlit. Simply because surprises bring the show to life. Or vice versa.

This isn't about shooting. This is about movement. Jump, run, stop abruptly, jump again, crouch. The lava below is patient and unhurried, knowing that sooner or later someone will stumble.

To open the way forward, you need to find two buttons for two generators. They are hidden in different parts of the main room. When both generators are activated, the path forward will open.

And yes, in keeping with good old tradition, there are grenades hidden behind one of the walls. The show calls this "a reward for curiosity".

So jump, Sam.

Run. Jump. Dance.

Watch your step, and a little further than you think.

Good luck.