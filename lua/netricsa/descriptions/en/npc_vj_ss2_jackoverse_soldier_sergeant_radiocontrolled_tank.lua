STORMTROOPER SERGEANT'S RC HUNTER ROBOT


CLASS:      Stormtrooper Sergeant's RC Hunter Robot
METABOLISM: Mechanical
PERCEPTION: Killer Combat Drone, Sergeant's "Toy"
SIZE:       5,2 ft
ENDURANCE:  Average
HOSTILITY:  Absolute
WEAPONS:    Integrated Minigun (right), Shotgun (left), Missile Pod (on head)
REWARD:     1200 FC
THREAT:     High

DESCRIPTION:

This is one of the "toys" that Stormtrooper Sergeant personally designed and put into production. This combat drone is designed for the most dangerous missions: reconnaissance, suppression of fire points, hunting single targets, and infantry support. It's not autonomous-it's controlled remotely by Sergeant-but that doesn't make it any less dangerous.

The robot's core consists of two dark green and black tracked units. The tracks allow it to navigate rough terrain, climbing over rocks, debris, and even small walls. A central body is located above the tracks.

Two articulated, mechanical-looking manipulator arms extend from the body. The right arm is equipped with a rotating machine gun, and the left with a shotgun. Above its head, a spherical module with an orange rim and a glowing green "eye" carries a missile launcher. The robot can use any weapon type depending on the situation: the machine gun for suppressive fire, the shotgun for close combat, and missiles for destroying armored targets and cover.

The robot's neck is flexible, allowing the head module to rotate and track targets over a wide range.

TIPS:

- The robot is mobile on its tracks, but not very fast. Use cover and flank it.
- The machine gun and shotgun have varying effectiveness at different ranges. Stay at medium range to minimize damage.
- The head module is a weak point. A few well-placed hits and it will be reduced to a pile of metal.
- Don't try to attack the robot head-on. Its weapon covers a wide area.
- The robot is Sergeant's "toy". If you see it on the battlefield, Sergeant is nearby. Be prepared to meet it directly.
