COMBINE CIVIL PROTECTION TACTICAL METROCOP


CLASS:      Combine Civil Protecion Tactical MetroCop
METABOLISM: Human, Male
PERCEPTION: Combine Police Assault Unit
SIZE:       5,9 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Pistol, SMG
REWARD:     450 FC
THREAT:     Medium

DESCRIPTION:

The Tactical MetroCop is a transitional unit between the regular Civil Protecion and the Combine's paramilitary forces. He is less formally equipped, but noticeably more aggressive than standard patrolmen. Green military camouflage pants identify him as a street assault unit, and the lack of sleeves emphasizes his emphasis on mobility and close combat, rather than a formal appearance.

The black body armor covers vital organs and contains built-in shock absorbers, preventing a headshot from causing immediate death. This makes the Tactical Metrocop an unexpectedly resilient opponent for those accustomed to settling everything with a single, precise shot.

He carries the standard weaponry of a pistol and SMG, but is more aggressive, actively closing the distance and trying to outnumber his targets. He is often used for clearing buildings, riot control, and operations in areas that require a tough, yet still "police-like" approach.

TIPS:

- Don't rely on headshots.
- Dangerous at medium and close range, especially indoors.
- Best to eliminate him from a distance or incapacitate him with explosives.