STROGG TELEPORT DROPPER FROM STROGGOS


CLASS:      Strogg Teleport Dropper
METABOLISM: Cybernetic Quadruped
PERCEPTION: Tactical Relay / Living Factory
SIZE:       ~4 ft
ENDURANCE:  High
HOSTILITY:  Extremely High
WEAPONS:    Bite, Call Backup
REWARD:     2000  FC
THREAT:     High

DESCRIPTION:

The Strogg Teleport Dropper is one of the most terrifying creatures in the Strogg arsenal, not because it's powerful in itself, but because it can increase the number of enemies, even during combat.

In appearance, the DROPPER resembles a cross between a dog, an insect, and a machine the size of a large tiger. Four-legged, fast, and agile, with a predatory snout and cybernetic limbs.

The Resetter's main ability is to deploy miniature teleportation stations. He can deploy up to four of these at a time, from which reinforcements pour. Most often, these are Guardians and Grunts, sometimes other types of infantry, but fortunately, Gladiators and Tanks cannot pass through them.

TIPS:

- Kill first. The Resetter is more important than any Gladiator because he makes the enemy swarm.
- Don't let him run. Corner him and finish him off with rapid-fire weapons.
- If there are other Strogg nearby, kill the Resetter anyway. They'll wait.
