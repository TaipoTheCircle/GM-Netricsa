COMBINE H.E.C.U. SOLDIER


CLASS:      Combine H.E.C.U. Soldier
METABOLISM: Augmented Human
PERCEPTION: Assault Infantry
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  High
WEAPONS:    OICW, AR2, SPAS-12
REWARD:     850 FC
THREAT:     High

DESCRIPTION:

Combine H.E.C.U. soldiers are the surviving US Marines who survived the hell of Black Mesa. Those who weren't torn apart by Xen's creatures eventually faced a far more ruthless enemy - the Combine. Some were eliminated as potentially dangerous witnesses, but the most experienced, disciplined, and morally broken were forcibly recruited.

The Combine assessed their combat experience as "exceptionally useful." Unlike standard Overwatch soldiers, H.E.C.U.s retained a significant degree of autonomous thought and tactical flexibility-just enough to be useful and safe. They underwent partial augmentation, enhancing their reflexes and suppressing post-traumatic stress, but their personalities were not completely rewritten.

They wear blue camouflage. Some wear black respirators, others balaclavas and old-style tactical helmets, slightly modified with Combine technology.

TIPS:

- Don't underestimate them-they're not Overwatch recruits, but veteran survivalists.
- They're dangerous in confined spaces, especially with SPAS-12.
- They sometimes act unconventionally-expect flanking maneuvers and ambushes.
- Behind their composure, memories of Black Mesa... Freeman, and anger still lurk.