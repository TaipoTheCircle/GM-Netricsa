BIG AERO CITY, VIBE CITY


Sam, we've just been transported to a city of the future... from the 2000s.

Nature has conquered concrete. A third of the city has been demolished (literally "razed to the ground") to add greenery. Because in a bright future, people need to breathe.

Water has finally appeared. Canals that were once dry and sad are now filled with water. You can soak your enemies or just wet your feet.

A swimming pool. This is already becoming a tradition. Apparently, no self-respecting Aero reskin is complete without a jacuzzi.

The Twin Towers (WTC) are open. You can go up there and explore the surroundings. If you're not afraid of heights and airplane karma.

No horror atmosphere. The architect deliberately cleaned up everything scary, unpleasant, and oppressive. No more jump scares in dark alleys. Just light, greenery, and peace. Relative, of course.

Let's go for a swim... or rather, to look around.
