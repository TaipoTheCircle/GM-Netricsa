SPOTLESS PIG FROM THE MINECRAFT OVERWORLD


CLASS:      Spotted Pig
METABOLISM: Warm-blooded mammal
PERCEPTION: Cheerful Passive Animal
SIZE:       3 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     30 FC
THREAT:     Minimal

DESCRIPTION:

One of the most charming varieties of the common pig, this spotted pig is incredibly sweet. It enjoys living with its pig friends and foraging for treats with them across the grassy plains.

This pig has a touching and recognizable appearance. Its base color is as dull as that of the pale pig, but round brown spots are scattered throughout its skin. She looks like she's been bathed in chocolate, but not completely, just in spots, or as if the sun has kissed her through the leaves.

She behaves no differently from a regular pig: she wanders the grassy plains in the company of her fellow pigs, avoids lava and cliffs, and runs away for a few seconds when damaged. But her main activity is searching for sweets and enjoying life among her fellow pigs.

TIPS:

- The spotted pig's main asset is her exceptional cuteness.
- Breed these pigs to obtain offspring with the same cheerful coloring.
- The spotted pig looks great in a pen on a grassy plain-her natural habitat.
- If you're searching for sweets across the plains in the company of spotted pigs, you've already reached Zen.
