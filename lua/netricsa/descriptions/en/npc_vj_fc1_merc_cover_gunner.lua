M134 ASSAULT TROOPER FROM THE KRIEGER ARCHIPELAGO


CLASS:      M134 Assault Trooper, Krieger Elite Mercenary
METABOLISM: Human, Male
PERCEPTION: Standard Vision, Tactical Training
SIZE:       6 ft
ENDURANCE:  High
HOSTILITY:  High
WEAPON:     M134
REWARD:     700 FC
THREAT:     High

DESCRIPTION:

The most common and recognizable soldier of Dr. Krieger's private military company. These are tall, physically strong professionals recruited from all over the world. Their motivation is money, not ideology. They wear various uniforms and headgear (berets, helmets, caps), but always wear body armor. Although many are dissatisfied with the conditions of service (heat, insects, poor food, isolation), they remain disciplined and dangerous opponents.

A specialized Krieger mercenary, part of the crew of UH-60 Blackhawk patrol helicopters. He is mounted on board near the open hatch and controls a rotating six-barrel minigun. His mission is to suppress ground targets and provide air cover during airborne landings. He has excellent firepower, but is extremely vulnerable due to his fixed position and limited field of fire. He is only partially protected by the helicopter's hull. Disabling him deprives the helicopter of its primary offensive armament, forcing it to retreat from the battlefield.

TIPS:

- Armor protects the hull. The most effective method is headshots.
- Avoid open terrain within his field of fire.
- The gunner himself is virtually immune to aimed fire. Precise head or body shots from any weapon are effective.
- Killing the gunner is the fastest way to neutralize the helicopter threat without using heavy weapons.
