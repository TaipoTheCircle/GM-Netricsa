COMBINE OVERWATCH NAPALM BOMBER SOLDIER


CLASS:      Combine Overwatch Napalm Bomber Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Area denial and cover burning
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Combine Napalm Bomber (3-round burst)
REWARD:     750 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Napalm Bomber Soldier is a specialized variant of the standard Grenade Bomber, designed for total area denial. He wears desert camouflage, designed for operations in arid zones, industrial wastelands, and ruins, where fire is the Combine's most important ally.

Outwardly, he resembles a standard grenade Bomber, but his red visor immediately reveals his true purpose. His grenade Bomber fires in three-round bursts, but instead of the usual explosions, the grenades instantly explode into clouds of napalm, drenching everything in a sticky flame.

The napalm ignites immediately upon contact with a surface, leaving no time to react. Floors, walls, cover, and even bodies become sources of fire. These soldiers are used to clear trenches, the lower levels of buildings, ventilation shafts, and areas where the enemy is trying to take cover or dig in.

TIPS:

- Don't stand still.
- The red visor is easily visible through smoke and flames.
- Eliminate the soldier before the first salvo.