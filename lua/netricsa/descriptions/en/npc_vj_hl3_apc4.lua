COMBINE TRACK


CLASS:      Combine Heavy Transport
METABOLISM: Mechanized platform with integrated controls
PERCEPTION: External sensor units, panoramic tactical cameras, AI targeting
SIZE:       29,5 ft
ENDURANCE:  Extremely high
HOSTILITY:  Extreme
WEAPONS:    Integrated machine gun turret (controlled from inside)
REWARD:     3200 FC
THREAT:     Critical

DESCRIPTION:

The Combine Track is a specialized Combine transport, similar in design to the APC Transporter, but adapted for transporting Combine Replicant Soldiers. Its shape is more streamlined and closed, and the hull appears solid and solid, as if cast. The main difference is the complete absence of glass, open cockpits, and external control stations.

The vehicle is controlled exclusively from the inside. Navigation is via a system of external sensor units, tactical cameras, and Combine analytical modules. The crew doesn't see the world directly, only through a data filter. This reduces the vehicle's vulnerability and completely eliminates human error.

The Combine Track's turret is also controlled from the inside. The operator is directly connected to the targeting system.

TIPS:

- No glass means no obvious weak points.
- Deploys soldiers.