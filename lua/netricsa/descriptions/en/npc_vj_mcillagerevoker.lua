EVOKER FROM THE MINECRAFT OVERWORLD


CLASS:      Evoker
METABOLISM: Magically gifted Illager
PERCEPTION: Elite Sorcerer / Evoker
SIZE:       6,2 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Magical "Fangs", Vex Summoning, Blue Sheep Red Transformation
REWARD:     600 FC
THREAT:     Extreme

DESCRIPTION:

This is what happens when a villager abandons trading, dons a dark robe, and retreats into the forest to study magic. This is one of the most dangerous and annoying Illager varieties, capable of turning a battle into a living hell if not killed instantly. If the Vindicator is brute force, then the Evoker is intellect, magic, and pure chaos.

Evokers dwell in mysterious Woodland Mansions, lurking deep within these gloomy structures. However, you can also encounter them in the later stages of Village Raids.

Despite their frail build, their hands conceal deadly power.

The Evoker is a master of territory control. They always keep their distance, trying to keep enemies at bay. Their arsenal includes three primary spells, each capable of quickly dispatching you:

Vex Summoning (Purple Aura): When the Evoker raises their hands and they glow purple, they summon 2 to 3 flying Vex. The Evoker can resummon them if fewer than 4 remain.

"Fangs" (Yellow Aura): If the Evoker raises his hands and they glow yellow, a row of spiky "Fangs" emerges from the ground in the targeted direction or around him, colliding and dealing massive damage. This attack is fast and painful.

Sheep Transformation ("Volo-lo"): The funniest, but most useless ability. If there's a blue sheep nearby, the Evoker can waste time shouting "Volo-lo!" to transform it into a red one.

TIPS:

- Kill him first. In any fight against illagers, the Evoker should be the number one priority target. If left alive, he'll spam Vex all over the battlefield.
- Close the distance. He's helpless in close combat. As soon as you run up to him, he'll stop summoning Vex and try to retreat. Don't let him do that.
- Attack during the spell. While his hands glow and he's preparing his spell, he's defenseless. That's the best time to attack.
- Dodge the Fangs. Their attacks are visible through the particles on the ground. As soon as you see the yellow particles, jump away immediately. A straight line of Fangs is very dangerous.
- Ignore the Vex at first. The Vex have low health and disappear after a while. If you start to focus on them, the Evoker will have time to summon more. Kill him first, and then deal with the rest of his army.
- If you kill the Evoker, he'll drop the Totem Expansion. This artifact can save your life at the very last moment.
