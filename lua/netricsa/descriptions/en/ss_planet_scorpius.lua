SCORPIUS


PLANET:       Scorpius
TYPE:         Terrestrial planet, Wasteland
CLIMATE:      Arid, harsh
MOONS:        None
TERRAIN:      Barren desert, numerous jagged mountain peaks
LOCATION:     In the constellation Scorpius
NATIVE RACES: Arachnoids
ALLIANCE:     Formerly an independent Arachnoid world, now Mental Horde (All Races)
LOCATIONS:    High Council Chamber, Fanatic Wastes

DESCRIPTION:

The planet's surface is a lifeless, scorched wasteland, reminiscent of Martian landscapes, but with an ominous dark red haze. A thick fog shrouds the needle-sharp mountain peaks rising from the desert. This harsh environment has shaped the character of the locals and fueled their extreme fanaticism.

On this planet, Mental discovered a primitive but intelligent race, the Arachnoids. Presenting himself to them as a messiah, Mental rallied a faction of religious fanatics among them.

With his help, these fanatics destroyed the Scorpius High Council and erased millennia of their own culture, outlawing relics of the past.

This led to a brutal civil war, during which the Arachnoids exterminated each other.

The younger generation began to be forcibly conscripted into Mental's army, sparking a resistance movement among the survivors seeking to sabotage Tah-Um's plans from within.

KEY LOCATIONS:

- High Council Chamber: The former center of the Arachnoid civilization, where millennia-old knowledge was preserved.
- Fanatic Wastes: Regions controlled by cultists, where active excavations and forced recruitment take place.
