KLEER WOODENBASE


CLASS:      Kleer Woodenbase
METABOLISM: undead
PERCEPTION: magical creature
SIZE:       9 ft
ENDURANCE:  low
HOSTILITY:  medium
WEAPONS:    wooden kleer claws, claws, horns
REWARD:     520 FC
THREAT:     medium

DESCRIPTION:

An undead skeleton of a being from an extinct race from the planet Kleer, of Alpha Canis Majoris. It has long sharp claws on its hands and a long tail.

They either attack by conjuring wooden claws or by slashing with their claws from close range.

As known boned Kleers, this one get mutation that makes their bones made of wood, that's why it's called woodenbase.

TIPS:

- Their jumping attacks are best evaded by sidestepping.
- Throwing claws also at close range.