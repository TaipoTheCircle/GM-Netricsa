COMBINE SINTH SOLDIER


CLASS:      Combine Synth Soldier
METABOLISM: Synth / Biomechanical Organism
PERCEPTION: Early elite of the Combine military
SIZE:       5,9 ft
ENDURANCE:  Very high
HOSTILITY:  Extreme
WEAPONS:    AR2, SPAS-12, OICW
REWARD:     2000 FC
THREAT:     Very high

DESCRIPTION:

The Combine Synth Soldier is a forgotten early combat unit developed for the Combine during the Seven Hour War. It is a highly adapted biomechanical organism, combining an organic body with an advanced Synth structure, designed to replace standard soldiers with its combination of mobility, durability, and firepower.

Their tactics combined high maneuverability, surprise attacks, and the ability to withstand hits that would stop a normal soldier.

TIPS:

- Withstands multiple hits thanks to biomechanical armor; one or two shots won't stop them.
- In open spaces, it's best to flank them and try to isolate them.