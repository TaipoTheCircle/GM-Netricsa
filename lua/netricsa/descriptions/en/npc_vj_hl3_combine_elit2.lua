COMBINE OVERWATCH ELITE ARCTIC COMMANDO


CLASS:      Transhuman Infantry, Elite Assault Unit
METABOLISM: Augmented Human, Male
PERCEPTION: Tactical HUD + Infrared and Thermal Systems
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    AR2 V2 Pulse Rifle, Energy Balls
REWARD:     900 FC
THREAT:     Very High

DESCRIPTION:

Combine Overwatch Elite Commandos are the highest form of Combine ground infantry, designed to completely suppress and destroy resistance. Recognizable by their dazzling white armor, a cape over their right shoulder, and a single, ominously glowing red visor, these soldiers are a symbol of the Combine's abandonment of negotiations.

A key feature of the Elite Commando's equipment is their integrated life support system. A thick tube runs through the respirator's left shoulder and into the armor, where it connects to an oxygen pack on the back. This module, marked with the Combine logo, provides autonomous breathing, toxin filtration, and a stable oxygen supply even in completely hostile environments-from smoky ruins to contaminated zones.

TIPS:

- Don't underestimate their survivability-conventional fire is rarely effective.
- Elite Commandos almost never operate alone, even if it seems otherwise.