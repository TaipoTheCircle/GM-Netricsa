ASHEN COW FROM THE MINECRAFT OVERWORLD


CLASS:      Ashen Cow
METABOLISM: Warm-blooded mammal
PERCEPTION: Passive animal
SIZE:       ~4,6 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     60 FC
THREAT:     Minimal

DESCRIPTION:

A rare variety of the common cow, native to the steppe. Unlike its mottled relatives, this cow is entirely gray, with a darker longitudinal stripe along its back, and the tips of its horns are also gray.

The Ashen Cow's behavior is no different from that of a common cow: it grazes, wanders aimlessly, and avoids lava and cliffs. When damaged, it flees for a few seconds and then calms down.

But it has one unique characteristic, confirmed by thousands of survivors: creepers avoid it. No creeper will ever explode near an Ashen Cow.

They simply don't approach. They circle around it, change their trajectory, even if the cow is directly in their path. No one knows why. Some say its gray skin smells of something that scares the plant-and-powder creatures.

TIPS:

- If you find an Ashen Cow, don't kill it. Herd it into your base.
- You can safely build wooden structures around it; creepers won't do.
- Breed them – Ashen Cows reproduce like regular ones, producing the same offspring.
- Useless in combat, but invaluable in farming.
- Protects the base from Creepers.
