BIG-HEADS, CHET FALICEK AND ERIK WOLPAW


CLASS:      Big-Heads, Erik Falisek and Chet Wolpaw
METABOLISM: Humanoid, Male
PERCEPTION: Legends of Games Journalism
SIZE:       5,9 feet (each)
ENDURANCE:  Low
HOSTILITY:  Ironic Neutral
WEAPONS:    OMM-branded car (tactical vehicle)
REWARD:     200 FC
THREAT:     Cultural Phenomenon

DESCRIPTION:

In the heart of the secret pyramid in the "Sacred Courts" levels, lived the unique Big-Heads duo of Erik Wolpaw and Chet Falisek, founders of the satirical website Old Man Murray. Unlike their counterparts, they don't simply wander aimlessly through the corridors, but rather emerge dramatically from the depths of the pyramid in a vehicle emblazoned with the proud OMM logo.

This dynamic crew represents the ultimate Easter egg from the Croteam developers. Erik, the owner of the game's largest head, and Chet, his loyal companion, symbolize the very "venomous criticism" that inspired and entertained the developers in the franchise's early years. Their appearance on a vehicle is not only a reference to their status in the gaming industry but also a kind of "breaking the fourth wall", where the critics literally drive into the game they once reviewed.

TIPS:

- Don't attack the vehicle-it's a cultural treasure of the gaming industry.
- In "I Am Ugh-Zan" difficulty, the duo can develop a higher level of danger.