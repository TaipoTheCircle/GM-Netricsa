VPO-136 HUNTING CARABINE


WEAPON:      VPO-136
AMMUNITION   7.62×39 cartridges
ROF:         ~15 rounds / min

DESCRIPTION:

Self-loading hunting carbine chambered for 7.62×39 cartridges. Based on the Kalashnikov AKM assault rifle, modified to meet the requirements for civilian rifled hunting weapons.

NOTES:

- Accuracy compensates for the lack of automatic fire.