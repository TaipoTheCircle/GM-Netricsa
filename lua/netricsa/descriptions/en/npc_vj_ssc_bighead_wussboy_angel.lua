DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Serious Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They are completely harmless, but in the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They spontaneously appear in secret areas, blocking passages with their sheer numbers.

Koray "Wussboy" Peters is a unique specimen in the Croteam pantheon. His contribution is measured not in lines of code or polygons, but in an intangible yet vital currency â€“ morale (Moral Boost). While other testers were busy searching for bugs, Wussboy seemed to be busy seeking out sagging spirits and instantly recharging them. His oversized skull radiates waves of fierce optimism and support, which have likely saved the team more than once during grueling crunches.

His name, modestly listed in the credits of everything from The First Encounter to the HD remasters and Classics: Revolution, is a reminder that behind every great game lies not only technical labor but also a human atmosphere.

He was once alive, but after you killed him in the Temple of Hatshepsut, he was resurrected - becoming an Angel.

TIPS:

- Use a combat knife or pistol to clear the path.
