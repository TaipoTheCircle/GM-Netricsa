TERRAFORMED PLANET


PLANET:       Terraformed planet
TYPE:         Terrestrial planet, Artificially terraformed planet
CLIMATE:      Varied (tropical, arid, polar)
MOONS:        At least 2 moons
TERRAIN:      Dense jungles, mountain ranges, deserts, swamps, polar caps
LOCATION:     Unknown star system
NATIVE RACES: Humans, Wildmen, various dinosaur species and mutated creatures
ALLIANCE:     Mendel-Gruman Corporation
LOCATIONS:    Rainforests, Desert regions, Wetlands, Underground cave systems

DESCRIPTION:

From orbit, this planet resembles Earth—green continents surrounded by sparkling blue seas. However, its true nature lies in a catastrophic experiment. Originally, it was an unstable, desert world with raging volcanic activity, where molten lava lay just beneath a thin crust.

The Mendel-Gruman Corporation, led by Roland Kane, subjected the planet to massive terraforming. But the process took an unexpected turn: it triggered an exponential surge in evolutionary activity. The result was the sudden appearance of dinosaurs and other prehistoric creatures, many of which possessed anomalous mutations.

The planet's surface is covered primarily in impenetrable tropical jungle, with giant trees and towering rocky peaks casting long shadows. It is home to packs of ferocious Velociraptors, stealthy ambush lurkers, and larger predators such as T-Rex, Dilophosaurus, and Giganotosaurus. Peaceful herbivores such as Brachiosaurus and Parasaurolophus coexist with aggressive Triceratops.

Besides the jungles, the planet contains limited desert regions with dead trees and the remains of ancient creatures. The wetlands are vast swamps with unique aquatic life forms, including the amphibious Utahraptor. Ice caps lie at the poles, the fauna of which is unknown.

Deep beneath the surface, a vast cave system extends, from vast grottoes to cramped tunnels. Creatures adapted to life without light dwell here in complete darkness: blind predators, giant scorpion-like Soldier Bugs, and unidentified eel-like monsters.

The Mendel-Gruman Corporation exploited the planet's unique flora and fauna to develop biological weapons for sale to various clients. Roland Kane personally led the corporation's military forces on the planet until his death.

KEY LOCATIONS:

- Rainforests: The planet's primary biome, covering most of the surface. Dense jungles with multi-layered vegetation are home to most dinosaur species.
- Desert Regions: Limited, arid zones with harsh conditions. They are home to armored dinosaur species with rock-like scales.
- Wetlands: Endless swamps and marshes inhabited by unique aquatic lifeforms, including amphibious Utahraptors and gigantic T-Rex.
- Subterranean Cave Systems: An extensive network of tunnels beneath the planet's surface, home to creatures adapted to the eternal darkness.
- Mendel-Gruman's Facilities: Research stations and military bases of the corporation, including facilities built on the unstable, pre-terraformed surface.
