HALF-QUAKE SHOW VORTIGAUNT (SLAVE)


CLASS:      HALF-QUAKE Show Vortigaunt, Slave
METABOLISM: Intelligent Mammal
PERCEPTION: Enslaved Alien Race
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Forced
WEAPONS:    Bioelectric Discharge, Claws
REWARD:     300 FC
THREAT:     Average

DESCRIPTION:

When the Sadistic HALF-QUAKE Show contracted with Black Mesa, Vortigaunts came with the equipment and headcrabs. Lots of them. They were brought in batches, stored in storage rooms, and for a while, no one knew what to do with them, because an intelligent mammal is harder to program than a parasite.

The problem was solved simply: Nihilanth's old psionic circuits were subdued, and each Vortigaunt had a white mark applied to their face with a marker pen ">)" before going on set. The mark is permanent, not because it's affixed, but because the Vortigaunts are afraid to remove it.

Vortigaunts themselves don't want to fight. They still remember freedom, and their sounds are a plea for someone to finally shoot them.

Upon sight of an enemy, a Vortigaunt discharges its accumulated bioelectric energy, releasing green lightning bolts from both hands.

TIPS:

- Lightning strikes far and hurts. Don't peek around corners unnecessarily.
- They're scarier in groups than alone.
