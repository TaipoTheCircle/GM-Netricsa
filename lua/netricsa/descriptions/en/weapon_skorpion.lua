SCORPION VZ-61 SUBMACHINE GUN


WEAPON:    Scorpion VZ-61
AMMUNITION 7.65x17 MM
ROF:       ~900 rounds/min

DESCRIPTION:

The Škorpion (Scorpion) is a Czechoslovakian submachine gun developed for tank crews, signalmen, and other military personnel. The Scorpion was adopted by the Czechoslovak People's Army in 1961 (hence the designation vz.61 — vzor 61, meaning “model 1961”). The modern modification is the Scorpion EVO III. It was used in the Vietnam War, the Chechen War, and is currently used in the Special Military Operation.

NOTES:

- Very compact and lightweight, can be easily used in a vehicle.
- The stock can be folded out and fired from the shoulder.
- Can be used as a backup weapon.