BIG-HEAD ANGEL, JOHN J. DICK "BOOGER"


CLASS:      Big-Head Angel, John J. Dick "Booger"
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
STAMINA:    Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Cool Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Where are you going!?" They are completely harmless, but in the "I Am Ugh-Zan" difficulty, they gain fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their sheer numbers.

John J. Dick is the living embodiment of the spirit of Sam Stone. His oversized skull is a direct reference to that deep, raspy, and incredibly charismatic voice that has been the series' signature for decades. It's the voice that turned the character into an icon, whose cries of "Aaah, you're the one!" and "YEAH!" are familiar to millions of players.

John's biography is a story of true "serious" survival: from military service in the Persian Gulf and street life to a career as a DJ and, finally, immortality in the gaming industry. His pragmatic, "dollar a word" approach to work and love for indie developers reflect the same irrepressible, free-spirited, and slightly insane energy that characterizes his most famous character.

He was once alive, but after you killed him in the Temple of Hatshepsut, he was resurrected - becoming an Angel.

TIPS:

- Use a combat knife or pistol to clear the path.
- On "I Am Ugh-Zan" difficulty, eliminate them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.