FLINTLOCK PISTOL


WEAPON:     Flintlock Pistol
AMMUNITION: 17.5 to 21.5 mm
ROF:        1 shot/min

DESCRIPTION:

The flintlock pistol is a firearm with a flintlock mechanism, in which the charge is ignited by sparks. There were two types of flintlocks: wheel locks, where the flint, held in the jaws of the hammer, rubbed against a steel wheel that spun rapidly when the trigger was pulled, and percussion locks, where the hammer with the flint struck a steel firing pin. In the 16th-19th centuries, flintlock weapons were used in all countries of the world. In Russia, flintlock weapons with a caliber of 17.5 to 21.5 mm and weighing 4.0 to 5.6 kg were used. The effective range of a flintlock rifle against a single person was 40 to 100 meters. For a group of people, it was 100-200 meters. There were two types of flintlock guns: smoothbore and rifled. The rate of fire for smoothbore guns was 2-4 shots per minute, and for rifled guns, it was 1 shot per minute. In the mid-19th century, flintlock guns were replaced by caplock guns and rifles. Initially, the army rejected flintlock weapons because of their imperfections. The new type of gun was first mastered by hunters, for whom the rate of fire was not a determining factor. Unlike the pre-fired matchlock gun, the new flintlock weapon did not reveal the shooter's position, which gave a significant advantage when hunting.

NOTES:

- Popular among pirates.
- Used in all wars where flintlock weapons were employed.
- Extremely long reload time and only one shot; this weapon requires skill to use effectively.