ANCIENT TEMPLE EXCAVATIONS, ANCIENT RESEARCH


We've found ourselves at an archaeological dig. A research center, digging into the ruins of something very ancient. And judging by what's buried here, they weren't just digging.

In the center are the remains of an ancient temple or monument, constructed of massive stone blocks.

The main thing here is something else-a secret worth digging for.

Somewhere in the dig site, hidden beneath the resistance lambda symbol, is an orange cube, clearly not of local origin.

Break the cube. Break the outer shell, and out falls... the legendary The Orange Box. Yes, Sam, the very same box that gave the world Half-Life 2, Portal, and Team Fortress 2.

Break the box. And that's when the apocalypse begins. Once The Orange Box is shattered into pieces, Nuclear Mission Jam will blast through the speakers-that epic track that turns any battle into a legendary slaughter.

Let's go find the cube.

And remember: don't just smash it, but do it with feeling. With purpose. With arrangement.
