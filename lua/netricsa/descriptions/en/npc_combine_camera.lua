HANGING COMBINE CAMERA


CLASS:      Hanging Combine Camera
METABOLISM: Combine optical hardware
PERCEPTION: Surveillance / Alert Device
SIZE:       ~1,5-2 ft
ENDURANCE:  Low-Medium
HOSTILITY:  Low
WEAPONS:    None direct; acts as trigger for other units
REWARD:     30 FC
THREAT:     Low

DESCRIPTION:

The Hanging Combine Camera is a variant that hangs from ceilings or overhangs, giving it broad visual coverage. Because it's suspended, it can retract or swing slightly, making it harder to avoid its gaze. It often surveils large hallways, checkpoints, or high-value areas. It's used by Combine to monitor movement, catch smugglers, and protect restricted zones.

TIPS:

- Check ceilings and overhangs for these - they may be less obvious.
- Use thrown objects or long-range weapons to break them.
- Avoid standing in wide open spaces under them.
