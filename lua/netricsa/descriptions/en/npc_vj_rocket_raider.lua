ROCKET RAIDER FROM THE CYBERSTAN


CLASS:       Rocket Raider
METABOLISM:  Mechanical
PERCEPTION:  Anti-Tank, Anti-Infantry
SIZE:        8,2 ft
ENDURANCE:   High
HOSTILITY:   Absolute
WEAPONS:     Drum-Type Rocket Launcher
REWARD:      150 FC
THREAT:      Absolute

DESCRIPTION:

This is one of the most dangerous combat units of the Automata faction. Outwardly, it resembles a standard Raider-a bipedal robot. Mounted on its shoulder is a powerful drum-type Rocket launcher, ready to turn any Helldiver into a bloody mist.

Like all Automata, the Rocket Raider has a humanoid shape, a gray metal body, red sensor eyes, and a machine gun-mounted head. He may carry a shield on his left arm and a rocket launcher on his right. The main difference is the rocket launcher on his shoulder and the distinctive green indicators that indicate readiness for launch.

In combat, the Rocket Raider occupies a mid- to long-range position. He avoids close combat, preferring to fire rockets at opponents from cover. His rockets deal high damage, have a large blast radius, and can penetrate the armor of even the best-equipped Helldivers.

The rocket launcher fires in bursts. A single salvo can fire up to 4-6 rockets, which spread out in different directions, covering a large area.

The Rocket Raider operates in a group with other Automatons: Shielded Raiders, Commanders, and regular Automaton infantry. His job is to destroy Helldivers at long range, covering the advance of the close-range units.

The Rocket Raider has several weak points:

The engine on its back. Hitting the engine causes increased damage and can cause a detonation.

The head is vulnerable, but less so.

The rocket launcher - if hit, it can cause a premature explosion, killing the Raider and damaging everyone around it.

TIPS:

- When the Rocket Raider appears, don't stand in the open. Its rockets will kill you in a second.
- Use cover: buildings, rocks, craters, even Helldiver shields.
- The rockets have a launch sound. If you hear the distinctive whistle, immediately move to the side or dive to the ground.
- The best weapons against a Rocket Raider are: RPGs (EAT-17, Recoilless Rifle), sniper rifles (Anti-Materiel Rifle), laser cannons (like the LAS-98), or a shotgun with armor-piercing rounds at close range.
- Don't try to attack head-on with a regular rifle. You'll just waste your ammo.
- If the Raider fires a volley and reloads, that's your chance to attack.
- In a group with shield bearers, kill the Rocket Raider first, then deal with the nearby enemies.
