MARSH-HOPPER FROM RIGIL KENTAURUS, BIG


CLASS:      Marsh-Hopper from Rigil Kentaurus, Big
METABOLISM: terrestrial amphibian
PERCEPTION: wild animal
SIZE:       13 ft (standing)
ENDURANCE:  none
HOSTILITY:  extreme
WEAPONS:    toxic slime splash
REWARD:     500 FC
THREAT:     very low

DESCRIPTION:

A strange organism originating from marshy wastelands in the system of Rigil Kentaurus. They live in large groups and have a suicidal instinct for protecting their territory. In all-devastating havoc, all individuals try to leap at the intruder and explode, by over inflating themselves. Upon exploding, toxic slime contained in their intestines splashes the target.

Mental, inspired by his successes in size manipulation, watched with glee as this tiny swamp creature transformed into a colossal beast. However, he overlooked a key flaw: the jumper's physiology hadn't scaled. Its tissues remained fragile, its internal organs vulnerable. It was now a gigantic, yet incredibly flimsy, blob. Its leaps had become cumbersome and predictable, and its suicidal instinct caused it to explode on contact, making it a perfect target. Mental hadn't yet realized that a single well-aimed pistol shot was enough to bring down this colossus.

TIPS:

- Aim for any part of the body-damage will be critical due to its fragile nature.
- Don't waste heavy weapons on it-it's a waste of ammo.
- A single creature isn't dangerous, but a whole pack is best avoided.
- Shoot from a distance.