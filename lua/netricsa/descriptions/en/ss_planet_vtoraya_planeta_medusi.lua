THE SECOND PLANET OF THE MEDUSA SYSTEM


PLANET:       The second planet of the Medusa system
TYPE:         Terrestrial planet
CLIMATE:      Temperate, unbreathable atmosphere, occasional rainfall
MOONS:        Unknown
TERRAIN:      Desert plain, glittering cliffs, scattered round stones
LOCATION:     The Medusa star system, outskirts of the Milky Way Galaxy
NATIVE RACES: Rocks
AFFILIATION:  Unknown
LOCATIONS:    "Pegasus" Landing Plain, Illusion Spawn Sites

DESCRIPTION:

This is a silent and desolate world, its rocky plain strewn with round, potato-like stones. Viewed from orbit, it appears bleak and inhospitable. The planet's atmosphere is completely unsuitable for human breathing; the climate is temperate, and rainfall is abundant. Sources of heavy hydrocarbons have been discovered in the subsurface.

The planet's most unique feature is its indigenous inhabitants. The local flora is sparse, but the true masters of the world are intelligent beings of inorganic origin, outwardly indistinguishable from ordinary stones.

These beings possess astonishing abilities: They are able to remember any images they have ever seen. The stones can reproduce what they have seen, or even what they have read in the minds of visitors, in the form of three-dimensional, realistic mirages. It is believed that they do this out of boredom, to somehow enliven the joyless everyday life on the desert planet.

When the "Pegasus" landed on this planet, the passengers witnessed a series of ghostly apparitions: a procession of musketeers from "The Three Musketeers", then themselves, and later the space pirates they were chasing. All these images were illusions created by the locals.

The mystery was solved when Professor Seleznev's daughter picked up one of the stones, the glow around which was slowly fading, and the stone transformed into a space captain-one of those who had previously suffered a disaster on this planet and been "recorded" in the stone's memory.

KEY LOCATIONS:

- "Pegasus" Landing Plain: A section of the desolate rocky plain where the research ship landed. It was here that mass manifestations of illusions were observed.
- Zones of Increased Illusory Activity: Areas of clusters of sentient stones, where the likelihood of mirages is highest. The coordinates of these zones are unstable, as the stones can shift slowly.
- Captain's Finding Location: The point on the plain where the stone was discovered, which contained the image of one of the missing captains.
