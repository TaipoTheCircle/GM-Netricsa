RESISTANCE RECRUITER


CLASS:      Resistance Recruiter
METABOLISM: Human, Male
PERCEPTION: Tactical assessment, potential detection, basic combat experience
SIZE:       ~9 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    AR2 Pulse Rifle
REWARD:     400 FC
THREAT:     High

DESCRIPTION:

An experienced and charismatic Resistance fighter, fulfilling a dual role as lead fighter and field instructor. His primary role is not only to fight, but also to quickly train and integrate new recruits during missions. He is always on the front lines alongside regular rebels, leading by example in tactics, directing fire, and raising morale.

TIPS:

- Always stay close to him. He increases the effectiveness and survivability of nearby Rebel recruits.
- He's more valuable than a regular fighter. When assaulting Alliance positions, cover him while he establishes a position or coordinates the attack.