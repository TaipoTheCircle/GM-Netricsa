HIVEHAND


WEAPON:      Hivehand (Alien Organic Weapon)
AMMO:        Hornet larvae (self-replenishing)
ROF:         Semi-automatic or full-auto, ~300 rounds / min

DESCRIPTION:

Xen-organic weapon firing living hornets. Ammunition regenerates over time, granting infinite supply. Can be used for homing or rapid-fire attacks.

NOTES:

- Infinite ammo through natural regeneration.
- Homing fire is effective against mobile targets.
- Weak damage per projectile.