RXZ-200 PLASMA TURRET
\

CLASS:      RXZ-200 Plasma Turret
METABOLISM: Energy-Powered Construction
PERCEPTION: Stationary emplacement, a hybrid of a plasma cannon and a laser drive
SIZE:       ~3,2 ft
ENDURANCE:  Average
HOSTILITY:  Varies with the shooter
WEAPONS:    Plasma Spheres
REWARD:     400 FC
THREAT:     High

DESCRIPTION:

This is the result of Mental's engineering, who decided that conventional machine guns weren't enough and it was time to switch to energy. The design is simple: an oval body with a purple circle in the center, equipped with a plasma cannon and a targeting system copied from laser turrets.

Unlike the machine gun version, the plasma turret fires single spheres of purple plasma, not bursts. A single sphere is perfectly accurate and can destroy almost any target with a single hit—be it a regular Orc, a gunship, or even Mental himself, if unlucky. Upon impact, the plasma explodes, damaging everything nearby.

Mental combined his plasma cannon with the laser turret's control system, creating a hybrid weapon that can be used by anyone: Horde soldiers and Orcs from the planet Orazio.

Turrets are typically built into Mental's bases on Sirius and other planets.

In combat, the plasma turret is dangerous at any range. Its only drawback is its low rate of fire. A couple of seconds pass between shots, and if there are many enemies and they get close, the turret may not have time to reload.

But if a gunner is inside, they're protected by the hull and can use the turret as an additional source of health—it absorbs damage and, if destroyed, simply ejects the pilot.

TIPS:

- If you see an enemy behind a turret, take them out first. A turret without a gunner is just a piece of metal.
- A turret is indispensable against armored targets.
- Explosives destroy a turret faster than small arms fire.
- If you see orcish legs sticking out from behind a turret, there's someone there.
