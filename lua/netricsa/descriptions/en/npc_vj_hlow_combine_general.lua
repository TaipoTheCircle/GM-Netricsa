COMBINE OVERWATCH ELITE GENERAL


CLASS:      Combine Overwatch Elite General
METABOLISM: Augmented Human, Male
PERCEPTION: Combine High Command
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle
REWARD:     5000 FC
THREAT:     Extreme

DESCRIPTION:

The Overwatch Elite General is the Combine's highest field commander, combining tactical intelligence, combat experience, and full body augmentation. His appearance is deliberately intimidating and subservient: a black beret over a tactical helmet with a single glowing white visor creates a sense of cold, inhuman observation.

He wears a camouflage jacket with black and green patterns and a long dark cape with gold trim—a symbol of rank and absolute authority over his troops. The cape also conceals additional armor and command equipment.

In combat, the General doesn't limit himself to command. He actively participates in clashes, using an AR2 with modified firing modes and improved stabilization. His implants allow him to process tactical data in real time and adjust the actions of his squads.

The General's presence on the battlefield dramatically increases the effectiveness of all Overwatch units. The soldiers around him act faster, more accurately, and more aggressively, as if obeying not orders, but the algorithm of war itself.

TIPS:

- He is rarely unguarded—expect elite units and vehicles.
- Long-range combat and sniper weapons offer the best chance of inflicting critical damage.
- If the General engages in battle personally, the situation has already spiraled out of the resistance's control.
