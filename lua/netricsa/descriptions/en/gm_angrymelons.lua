CITY 17, ANGRY WATERMELONS ASSASSINATE


Sam, I don't even know what to say. We've seen a lot: zombies in basements, Mental in space, brick rooms without doors.

But for watermelons to inhabit City 17... and for them to have a slingshot... and for them to take revenge... That's a new level of absurdity, and I like it.

A typical corner of City 17: gray walls, a couple of buildings, a small store. A typical day in the occupied city. But something's off. Near the store, instead of the usual Combine patrol, there's a huge slingshot. And all around are watermelons. Lots of watermelons. They're just lying there, biding their time. Waiting for you.

You take the slingshot (preferably frozen so it doesn't fly away).

You place a watermelon on it.

Weld a watermelon to a slingshot, setting the tension to 2000 or more.

Grab the slingshot, press R, pull, and release.

The watermelon flies off to destroy the walls of City 17.

