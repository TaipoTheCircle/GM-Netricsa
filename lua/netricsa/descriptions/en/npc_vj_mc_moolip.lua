MOOLIP COW FROM THE MINECRAFT OVERWORLD


CLASS:      Passive Animal / Rare Cow
METABOLISM: Warm-blooded Mammal
PERCEPTION: Standard Vision and Smell
SIZE:       5,2 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     75 FC
THREAT:     Low

DESCRIPTION:

The Moolip Cow is a rare variety of cow. It resembles a regular cow in appearance, but has a pink coat, black eyes, and numerous pink daisies on its back and head.

A special trait of Moolip Cows is that they leave pink flowers on the blocks they walk on. Each flower has a chance of becoming an onion, lilac, or peony, making these cows a coveted item for flower collecting and world decoration.

TIPS:

- Can be sheared with scissors to obtain a single pink daisy.
- Moolip Cows can be milked with a bucket, just like regular cows.