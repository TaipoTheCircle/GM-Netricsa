BIG WHITE SHARK FROM KRIEGER ARCHIPELAGO 


CLASS:      Big White Shark
METABOLISM: Cartilaginous fish (apex predator)
PERCEPTION: Electroreception, olfactory sense, lateral line, vision
SIZE:       16 ft
ENDURANCE:  Very high
HOSTILITY:  High (in water), absent (on land)
WEAPONALS:  Jaws with multiple rows of serrated teeth
REWARD:     1100 FC
THREAT:     Extreme (in water, when approached)

DESCRIPTION:

The archipelago's apex predator. Found in open waters around the islands, especially in deep waters. Jack Carver mostly saw its suspended carcasses. However, a living specimen poses a mortal danger to anyone who ventures too far. Attacks without warning, inflicting massive damage with powerful bites. Its only "weakness" is its inability to leave the water.

TIPS:

- Avoid deep water. Don't swim far from shore.
- Quickly return to the boat or shore if a shark appears.
- Completely helpless on land.