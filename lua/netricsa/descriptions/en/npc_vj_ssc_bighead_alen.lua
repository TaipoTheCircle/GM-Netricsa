BIG-HEAD, ALEN LADAVAC


CLASS:      Big-Head, Alen Ladavac
METABOLISM: Humanoid (70% code)
PERCEPTION: Enthusiastic Fanatic
SIZE:       5.9 ft
ENDURANCE:  Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Cool Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They are completely harmless, but on the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They spontaneously appear in secret areas, blocking passages with their sheer numbers.

Alen Ladavac is the living foundation and one of the creators of everything you see on screen. He is the co-founder of Croteam and a technical guru who served as the studio's heart and soul for 25 years. He created the engine and technologies for the entire Serious Sam series, as well as the philosophical puzzler The Talos Principle. He led, coded, designed, and even consulted with programmers, becoming a true "Swiss Army knife" of game development. His oversized skull is fully justified in the game: it contained the knowledge of rendering, sound, graphics, and physics that gave the world the awesome Sam. After decades of work, he left the project to join Google Stadia and attempt to bring his grandiose ideas to the cloud, leaving behind a legacy that is hard to rival.

TIPS:

- Use a combat knife or pistol to clear a path.
- On the "I Am Ugh-Zan" difficulty, destroy them remotely due to their modifiers.
- In a group cluster, a sledgehammer with a horizontal strike is effective.