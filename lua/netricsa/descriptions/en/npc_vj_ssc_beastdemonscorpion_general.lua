DESCRIPTION:

This is what a young Scorpion becomes if allowed to live long enough to kill another couple thousand people. Solgar personally mentors the most promising specimens, selecting those who have proven themselves in combat and allowing them to survive to adulthood. The results are impressive.

An adult scorpion is strikingly different in appearance from its juvenile counterpart. The juvenile's glossy green carapace darkens with age, becoming a deep, somber blackish-green, almost blending into the darkness.

The head is protected by a helmet-like helm with a subtle metallic sheen. Eyes on the sides of the head glow in the dark with a cold, blue light.

The adult's tail is longer, thicker, and more deadly.

TIPS:

- Use explosives, lots of them. C4, grenades, rockets-whatever you have.
- Don't let it get too close, it'll kill you faster than you can shoot.
