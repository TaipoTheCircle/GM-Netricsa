COMBINE ARCTIC OVERWATCH SOLDIER


CLASS:      Transhuman Soldier (Combine Infantry, Arctic Squad)
METABOLISM: Enhanced Human, Male / Biomechanical Enhancements
PERCEPTION: Tactical Sensors + Enhanced Reflexes + Night/Thermal Vision
HEIGHT:     6,4 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, SMG3, Grenades
REWARD:     500 FC
THREAT:     High

DESCRIPTION:

The Arctic Overwatch Soldier is a specialized version of the standard Combine trooper, adapted for extremely cold and snowy regions. He wears a white and gray camouflage uniform consisting of a jacket and warm pants that cover the entire body. On their feet are massive black boots, providing stability on ice and snow.

The soldier's head is protected by a metal helmet with blue glowing lenses that provide night vision, thermal detection, and integration with the Combine tactical network. A camouflage-patterned flak jacket matches the uniform and provides a high level of protection against firearms.

Arctic soldiers operate according to the same tactical principles as standard Overwatch soldiers: they coordinate attacks, utilize Manhack and heavy units for support, hold positions, and suppress Combine resistance in conditions of limited visibility and snow cover. Their adaptation to the cold and camouflage make them virtually invisible against the icy landscape, enhancing the effectiveness of ambushes and stealth maneuvers.

TIPS:

- High endurance allows them to hold positions even in extreme conditions.
- Headshots are still effective, but require precision.