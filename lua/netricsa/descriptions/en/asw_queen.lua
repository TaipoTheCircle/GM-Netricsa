SWARM QUEEN "BETA"


CLASS:      Swarm Queen "Beta"
METABOLISM: Xenobiological
PERCEPTION: Swarm Queen
SIZE:       26,2 ft
ENDURANCE:  Extremely High
HOSTILITY:  Overwhelming
WEAPONS:    Burrowing Tentacles, Acid Projectiles, Crushing Limbs
REWARD:     5000 FC
THREAT:     Apocalyptic

DESCRIPTION:

The Swarm Queen "Beta" is the largest known life form of this swarm and the central organism of the entire colony. The Swarm is formed around her, and all other individuals exist solely to protect their progenitor and continue the swarm's life cycle. The destruction of the Queen leads to the gradual disintegration of the colony, deprived of a single coordinating center.

Her massive body is covered in thick chitinous armor and supported by several powerful limbs, allowing her to confidently move even over uneven surfaces. Despite her size, the Queen is capable of quickly shifting her body position, choosing the most advantageous attack position.

The Queen's most distinctive feature is her long, subterranean tentacles. By burying them in the ground, she can suddenly surface beneath her opponent, capturing them and holding them in place while powerful contractions of the tentacles inflict devastating damage. Even experienced fighters can find it difficult to escape such a trap.

At long range, the Queen assumes a stable position and fires a volley of three large acidic projectiles. Each impact is accompanied by a powerful release of corrosive biological fluid, capable of quickly destroying armor and living tissue. While preparing the volley, her mobility is significantly reduced, making this the most opportune moment for a counterattack.

In close quarters, the Queen uses massive limbs capable of crushing virtually any opponent with a single blow. Thanks to her combination of immense durability, spatial control, and devastating attacks, she is considered the most dangerous member of the Beta Swarm.

TIPS:

- Don't remain in one place for too long: underground tentacles can reach a stationary target.
- While preparing her acid volley, the Queen barely changes the direction of her attack. Use this moment to reposition.
- The Queen effectively controls most of the battlefield with her tentacles.