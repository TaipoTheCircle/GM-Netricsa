SKELETON HORSE FROM THE MINECRAFT OVERWORLD


CLASS:      Skeleton Horse
METABOLISM: Undead Horse
PERCEPTION: Vehicle / Passive Mob
SIZE:       5,2 ft
ENDURANCE:  Average
HOSTILITY:  Neutral
WEAPONS:    None
REWARD:     300 FC
THREAT:     Low

DESCRIPTION:

One of two varieties of undead horses, along with zombie horses. Unlike their living counterparts, these skeletal steeds do not graze in meadows, but are instead created by lightning strikes during thunderstorms.

Their appearance is explained by a special condition called "skeleton trap". When a normal horse is struck by lightning during a thunderstorm, it transforms into a skeleton horse with a special tag called "SkeletonTrap." A Skeletal Horse is a fully formed horse skeleton floating above the ground. It makes the characteristic sounds of a normal horse, but at a lower pitch with an echo effect.

TIPS:

- Don't get too close to a lone Skeletal Horse during a thunderstorm unless you're prepared to fight four Skeletal Archers.
- The best strategy for obtaining such a mount is to kill the riders but leave the horses unharmed. Once the enemies are gone, the horses will become passive and can be tamed.
- A Skeletal Horse is an ideal water mount. Unlike normal horses, it doesn't dislodge its rider when submerged and doesn't drown.
- It's an excellent mount for exploring the Underworld and the End, as unlike other mobs, Skeletal Horses are not affected by sunlight and won't burn in it.
- Remember that as an undead creature, Skeletal Horses take damage from healing potions and are restored by damaging potions.
