SHREDDER TANK FROM THE CYBERSTAN


CLASS:      Shredder Tank
METABOLISM: Mechanical
PERCEPTION: Heavy Armor, Infantry Destroyer
SIZE:       13 ft
ENDURANCE:  Extreme
HOSTILITY:  Absolute
WEAPONS:    Twin Heavy Machine Guns (Four-stage heavy fusion cyclers)
REWARD:     4000 FC
THREAT:     Absolute

DESCRIPTION:

This is a variant of the Automaton heavy tank, differing from its "Annihilator" counterpart in its armament. Instead of a single powerful intermittent cannon, the Shredder is equipped with a quadruple anti-aircraft mount, reconfigured for ground attack.

Externally, this tank is easily distinguished by its turret with four long barrels, reminiscent of the Flakpanzer IV Wirbelwind anti-aircraft gun. These guns, originally designed to destroy aircraft, now reduce Helldiver infantry to a bloody pulp.

The tank is armed with four twin heavy machine guns (four-stage heavy fusion cyclers). The rate of fire is 1200 RPM. The gun has two modes:

Precision Fire: A short burst lasting 1 second.

Cleansing: A long burst lasting 2 seconds with the turret traversing to the sides, mowing down all living things.

In combat, the Shredder Tank fires continuously, using one of two firing modes. Thanks to the twin turrets, it can rotate the turret very quickly, tracking fast targets.

Unlike the Annihilator, which leaves openings for attack between main battery shots, the Shredder sprays the area with lead almost continuously.

The Shredder Tank has the same weak points as the Annihilator:

Rear Engine, Rear Turret Radiator, Treads (Destroying a track immobilizes the tank, but does not destroy it.) It is also vulnerable to fire.

TIPS:

- Don't try to hide from it behind a thin wall. Its machine guns will pierce it.
- Don't stand in the open. The tank clears an area quickly.
- Use ravines and low spots to approach the tank from the rear.
- Destroying its tracks immobilizes the tank, but it continues to fire.
- When destroyed, the tank explodes within an 8-meter radius. Move away.
- The Shredder is more dangerous to infantry than the Annihilator. Suppress it with heavy weapons before it gets close to your position.
