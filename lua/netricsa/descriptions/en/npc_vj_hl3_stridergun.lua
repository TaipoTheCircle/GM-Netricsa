COMBINE CIVIL PROTECTION STRIDER MINI METROCOP


CLASS:      Integrated Heavy Combat Synth Operator
METABOLISM: Human, Male (critical biomechanical compression)
PERCEPTION: Full Strider neural interface, sensory organs suppressed
SIZE:       0,9 ft
ENDURANCE:  Very low
HOSTILITY:  High
WEAPONS:    Control of Strider's primary weapons
REWARD:     400 FC
THREAT:     Low

DESCRIPTION:

The Strider Mini Metrocop is an even more radical form of human integration into the Combine combat synth. Unlike MiniStrider operators, these Metrocops are fully integrated into the Strider's central biomechanical compartment and have no autonomy outside its hull.

Their bodies are subject to constant compression, restraint, and neural synchronization with the walker's control system. All vital functions are supported solely by the Strider's onboard systems. Vision, hearing, and spatial orientation are completely replaced by the machine's sensory input, which feeds directly into the operator's nervous system.

Unlike the version integrated into the MiniStrider, the Strider operator cannot be freed, even theoretically. Their compression capsule is part of the supporting structure. Destroying the walker immediately causes loss of life support, severed neural connections, and critical destruction of the operator's body. Returning to normal size is impossible.

TIPS:

- Destroying the Strider guarantees the destruction of the operator inside.