HALF-QUAKE SHOW, HELL


I scanned the area.

And you know, Sam, this is what really ruins the atmosphere of the show.

Off to the side, behind the backdrop of pain and designer madness, lies a security guard. Not an actor. Not a target. Alive. For now. Covered in blood, his armor looks more like a prop after a dress rehearsal of hell. His breathing is ragged. His movements are cautious, as if his body itself is afraid to remind anyone of its existence.

The local rules are simple and disgusting: Guards are also put through trials. For motivation. For discipline. For statistics. The show loves it when everyone suffers without exception. Equality, so to speak.

It's worth talking to him. He could see more than an announcer with a microphone. He knew where the buttons were, where the traps were, where the audience especially loved to applaud. And perhaps he's still alive precisely because he was waiting for someone who wouldn't finish him off out of habit.

And if you can help...

Well then.

This place calls itself Hell.