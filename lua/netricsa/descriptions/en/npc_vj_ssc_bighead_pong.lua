BIG-HEAD, TOMISLAV PONGRAC


CLASS:      Big-Head, Tomislav Pongrac
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
ENDURANCE:    Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Serious Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They are completely harmless, but in the "I Am Ugh-Zan" difficulty they receive fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their sheer numbers.

Tomislav Pongrac is a walking encyclopedia of game development and the embodiment of Croteam's "mad scientist" philosophy. His oversized cranium houses an incredible range of skills, applied over decades. He started out working with pixel art on Amiga and DOS, progressed through 3D art and level design for the classic Serious Sam: The First and The Second Encounter, and then became the lead level designer for Serious Sam II.

But his talents weren't limited to just one series. He served as creative director and game designer for the frantic arcade racing game Gas Guzzlers, and has worked on sound design, 3D modeling, textures, and even cinematics. His pinnacle as a visionary was the hardcore survival simulator SCUM (2018), where he served as director, creative director, and original concept artist. Tomislav is living proof that a true developer at Croteam isn't fixated on one discipline; he's the studio's Swiss Army knife, capable of breathing life into a project at any stage and in any role.

TIPS:

- Use an army knife or pistol to clear the path.
- On the "I Am Ugh-Zan" difficulty, eliminate them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.
