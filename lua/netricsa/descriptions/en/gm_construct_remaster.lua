CONSTRUCT REMASTER, UNFINISHED


Sam, remember that construction site where we started? Empty, concrete, with a couple of hangars and an endless construction site that would never end? Well, someone decided that wasn't enough. And made a remaster.

Not a rebuild. Not a completion. A remaster, exactly. Like the old games: the same locations, but more textures, fewer bugs, and a couple of added features to keep veterans entertained.

The construction site is still unfinished. But now there are improvements. Serious ones. And strange ones. And, as usual, with a catch.

What's new?

The hangar has a door.

Yes, Sam. That same hole we used to run into to wait out the shelling is now closing. And perhaps the wind isn't blowing.

A dark room - light and the banishment of a legend.

Remember that room where it was always dark, and we were afraid He Who Must Not Be Named would jump out of the corner? Well, there's a switch there now. You flip it, and there's no darkness. And rumor has it that the Meat Man was banished from there. Don't ask who it is. There's nothing to ask. The main thing is, he's not there anymore. Or maybe he's just learned to hide better.

The White Room - risk of blindness.

There's a warning posted at the entrance. Everyone entering is advised to wear glasses. Because the light there is so bright, you could be left with your eyes closed forever. I don't know who made a room that could blind you, or why. But Sam, we would have gone in.

Fire extinguishers.

Everywhere. On the walls, in the corners, on the tables. Apparently, someone realized they were essential on a construction site. Or maybe they just remembered safety precautions. Either way, if something catches fire, you won't have to put it out with your feet.

Manholes and hatches.

Yes.

Parking.

Finally, you can park your car. Not in a field, not in a hangar, but in a real parking lot. With markings. Plenty of spaces. No cars, though. But we can bring our own.

And much more.

It's too long to list. Too long.

Take a look around the construction site again. A lot has changed. Some things have gotten better, some are stranger. Find the glasses in front of the white room. Turn on the light in the dark room. Look into the well. Park the car. And remember: fire extinguishers are not toys. Until there's a fire.

Let's go for a walk around the renovated construction site. Just don't forget your glasses. Otherwise, you'll go blind - who'll shoot the headcrabs then?
