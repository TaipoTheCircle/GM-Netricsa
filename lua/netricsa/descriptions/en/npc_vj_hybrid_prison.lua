COMBINE OVERWATCH HYBRID PRISON SOLDIER


CLASS:      Combine Overwatch Hybrid Prison Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine long-range and mid-range punitive assault unit
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, SMG, Grenades
REWARD:     650 FC
THREAT:     High

DESCRIPTION:

The Combine Overwatch Hybrid Prison Soldier is a beefed-up version of the Nova Prospekt trooper, temporarily or permanently deployed outside of prison complexes. The Combine uses these soldiers in situations where standard forces are insufficient and the deployment of elite forces is impractical.

The soldier is clad in reinforced prison-style armor and equipped with a tactical helmet with a protective visor. The yellow visor emphasizes his affiliation with the Nova Prospekt guard and assault forces. Despite its protective appearance, the visor is not completely bulletproof - accurate headshots remain effective.

TIPS:

- Don't overestimate the visor - headshots still work.
- The best option is distance and accurate fire.