FLAME WRAITH FROM THE MINECRAFT NETHER


CLASS:      Flame Wraith
METABOLISM: Undead, fiery Wraithly entity
PERCEPTION: Hostile mob, flying Flame Wraith
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extremely high
WEAPONS:    Flame Touch
REWARD:     400 FC
THREAT:     Very high

DESCRIPTION:

This is a hellish variant of the common Wraith, found only in the Nether. Unlike its pale cousin from the Overworld, this fiery spirit doesn't just deal damage – it sets its victim on Flame, turning the battle into a race to the death by Flame.

The Flame Wraith is more aggressive and dangerous in appearance. Their touch causes their opponent to burn for at least 15 seconds-roughly the same amount of time it takes to fall into lava. In Hell, where it's already hot, such a burn is almost always fatal, especially without water or a Flameproof potion nearby.

Despite their fiery nature, these wraiths are paradoxically vulnerable to sunlight. Like normal wraiths, they die in direct sunlight, though they don't ignite. They are completely immune to Flame (which makes sense), but are killed by normal daylight.

Otherwise, their behavior is identical to normal wraiths: they hover, emit eerie moans, attack with touch, and don't collide with other creatures.

TIPS:

- If you're heading to the Nether, be prepared to encounter these creatures.
- A Flame wraith is a priority target. It shouldn't be ignored, as 15 seconds of burning in Hell is almost guaranteed to kill you. - Carry Flame resistance potions with you. They will make you immune to Flame and negate the Wraith's main weapon.
- A bucket of water is useless in the Nether-the water evaporates instantly.
- Use a bow or crossbow to keep your distance. In close combat, he'll set you on Flame.
- If you do get set on Flame, try retreating to a safe place and quickly eat a golden apple or drink a healing potion to survive the Flame damage.
- Unlike regular Wraiths, these ones don't appear in the Overworld, even at night. Hell is their only home.
