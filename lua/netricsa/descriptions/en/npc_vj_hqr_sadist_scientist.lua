CLASS:      HALF-QUAKE Show Sadist
METABOLISM: Human, Male
PERCEPTION: Sadistic Show Host
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  Passive / Contemplative
WEAPONS:    Access to enormous hammers, show control systems, indifference
REWARD:     10000 FC
THREAT:     Medium

DESCRIPTION:

These are not only the HALF-QUAKE show hosts, but also engineers, scriptwriters, executioners, and spectators all rolled into one. They search for victims all over the world, dragging them from their ordinary lives and delivering them to arenas where they must suffer "for their terrible mistake in life". Whether this mistake was real or invented on the spot-the sadists don't care.

The main thing is that the process goes according to script.

The Sadists created Somos, the Son of Sadism-a higher intelligence that controls everything that happens in the show.

When they run out of victims, they hire guards and force them to undergo tests, then watch them die of exhaustion or from the blow of a giant hammer. For the public, of course.

They don't engage in combat themselves. Why? That's what turrets, headcrabs, and vortigaunts are for.

