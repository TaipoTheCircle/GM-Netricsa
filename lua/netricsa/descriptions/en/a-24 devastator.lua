A-24 "DEVASTATOR" SHOTGUN


WEAPON: A-24 "Devastator" Shotgun
AMMO:   24-Gauge Shells
ROF:    300 rpm
WEIGHT: 24 lbs.

DESCRIPTION:

An automatic shotgun whose firepower lives up to its fearsome nickname. A modernized design based on the old Atchisson system. Fires high-piercing heavy-explosive (HPHE) rounds capable of penetrating multiple targets.
High muzzle velocity and optional scope attachment allow for effectiveness even at medium range.

NOTES:

- High piercing power effective against concentrated groups and heavy targets.
- Recommended for eliminating Technopolyps and Witch-Brides of Achriman.