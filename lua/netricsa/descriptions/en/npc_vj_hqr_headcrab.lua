HALF-QUAKE SHOW COMMON HEADCRAB


CLASS:      HALF-QUAKE Show Headcrab, Common
METABOLISM: Parasitic xenofauna
PERCEPTION: Biological weapon
SIZE:       1-2 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Bite, permanent attachment
REWARD:     150 FC
THREAT:     Medium

DESCRIPTION:

THE BLACK MESA bred these creatures specifically for the HALF-QUAKE Sadistic Show. The project's management wanted Common headcrabs, but with recognizable branding, so viewers would immediately understand that this wasn't just a parasite from Xen, but a corporate product.

Each specimen has a black brand burned directly onto the top of its head ">)". Apparently, they were burned while still in the larval stage-the chitin has baked on in even layers, and now the mark can't be washed off or scraped off. The official version is that the brand is for population control and to prevent theft.

The unofficial version is that the producers of HALF-QUAKE just enjoy watching these things jump on people with their logo on their heads.

Otherwise, they're just regular Xen headcrabs: they sense warmth, jump from the ceiling, burrow into your skull, and turn you into a vegetable. But now they're also walking advertisements.

TIPS:

- If you see the brand ">)"-know that they paid a fortune for this creature. It has something to prove.
- They jump from any surface taller than you. Watch your back.
- They're helpless on the ground-hit them before they jump.
