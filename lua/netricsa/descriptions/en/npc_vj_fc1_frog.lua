FROG FROM KRIEGER ARCHIPELAGO


CLASS:      Frog
METABOLISM: Amphibian
PERCEPTION: Movement, vibrations
SIZE:       0.6 ft
ENDURANCE:  Very low
HOSTILITY:  None
WEAPONS:    None
REWARD:     10 FC
THREAT:     None

DESCRIPTION:

A small amphibian, rarely found in damp areas of the archipelago, near bodies of water and in undergrowth. Easily startled and jumps away from approaching creatures. Does not interact with other enemies.

TIPS:

- Not an enemy or resource. Attacking is pointless.
- Killing provides no benefit.