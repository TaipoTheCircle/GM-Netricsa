TATOOINE


PLANET:       Tatooine
TYPE:         Terrestrial Planet, Desert Planet
CLIMATE:      Arid, extremely hot during the day, cold at night
MOONS:        None
TERRAIN:      Endless sand seas, rocky canyons, wastelands, plateaus
LOCATION:     Outer Rim Territories, Arkanis sector, Tatooine system (double star: Tatooine I and Tatooine II)
NATIVE RACES: Humans (settlers), Jawas, Tusken Raiders (Sand People), Hutts
ALLIANCE:     Hutt Space (de facto), Arkanis sector (de jure), Galactic Empire (formerly), New Republic (formerly), Galactic Union (currently)
LOCATIONS:    Mos Eisley, Mos Espa, Anchorhead, Bestine, Jabba's Palace, Dune Sea

DESCRIPTION:

An inhospitable desert planet orbiting a double yellow sun.

Once thriving with oceans and jungles, it was transformed into a desert millennia ago by orbital bombardments by the Rakata Infinite Empire. Today, its surface is endless sand seas, scorched by the double sun, occasionally cut by rocky canyons, such as Beggar's Canyon, formed approximately two million years ago.

The climate is harsh: unbearably hot during the day, and temperatures plummet at night. Water covers less than a percent of the surface, and the main source of moisture for the settlers is moisture harvesters that condense water from the dry air. Thanks to a unique combination of magnetic ores in the mantle and atmospheric winds, several temperate zones exist where temperatures rarely exceed 43 degrees Celsius—it is here that the few settlements are concentrated.

Tatooine lies at the crossroads of many hyperspace routes, making it a haven for smugglers, mercenaries, and bounty hunters on the run from the law. The planet's surface is literally strewn with the wreckage of ancient ships buried in the sand. While the planet is legally considered part of the Arkanis sector, it is de facto ruled by Hutt criminal clans, such as Jabba Desilijic's clan, which controls smuggling and other shady dealings.

The native population consists of two races descended from a single ancient people. The Jawas are small, bustling scavengers in distinctive robes, traveling through the desert on giant sandouls in search of scrap metal for resale. The Sand People, or Tuskens, are tall, warlike nomads, hostile to outsiders and fiercely protective of their territory. Human colonists survive primarily on moisture farming in settlements such as Mos Eisley, Mos Espa, Anchorhead, and the capital city of Bestine.

Despite the harsh conditions, life on Tatooine is diverse. It is home to the enormous furry banthas used by the Tusken Raiders, the slow-moving rontos, herds of eopie, and dangerous predators such as krayt dragons, whose stomachs contain valuable dragon pearls, and the monstrous sarlaccs, which take a thousand years to digest their prey. The most famous plant is the hubba gourd, the main food source for Jawas and Tusken Raiders.

KEY LOCATIONS:

- Mos Eisley: A major spaceport and the planet's criminal hub, with bars, hangars, and shady dealings.
- Mos Espa: A port city known for its hippodrome, home to the famous podracing events.
- Anchorhead: The oldest settlement on the planet, founded by the first colonists.
- Bestine: The official capital of Tatooine, located approximately two hundred kilometers west of Mos Eisley.
- Jabba's Palace: A powerful desert fortress serving as the residence of the crime lord Jabba the Hutt.
- Dune Sea: A vast expanse of shifting sands stretching for many kilometers.
