ANTLION, SPITTER FROM THE XEN


CLASS:      Antlion, Spitter
METABOLISM: Insectoid
PERCEPTION: Insectoid Alien Creature
SIZE:       4-5 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Blue Acid Spit, Toxic Claws, Toxic Bite, Short Dashes
REWARD:     250 FC
THREAT:     Medium

DESCRIPTION:

Insectoid predators from Xen, drawn to Earth before the Combine invasion. They roam sandy terrain in swarms, emerging from underground to ambush prey. Agile and relentless, they win not by individual strength, but by sheer numbers.

The spitter is a unique species of antlion, distinguished by its bright blue shell and venom glands. Unlike normal individuals, it prefers to stay behind the main pack, providing long-range support. Its main weapon is a single glob of caustic blue liquid, which, upon impact, causes a brief chemical burn to the skin and disorients its prey for a few moments.

In their behavior, spitters resemble worker antlions. They do not rush into reckless attacks, but instead try to take an advantageous position, constantly moving around the enemy and covering their more aggressive brethren. If an enemy gets too close, the creature attacks with sharp claws and powerful mandibles. Both the bite and claws contain a toxin that causes severe pain and temporary deterioration of the target.

Despite belonging to the antlion species, spitters completely ignore the effects of pheropods. Attempts to subdue or calm them prove futile, making them significantly more dangerous than normal swarmers. For this reason, they remain aggressive even after other antlions have ceased attacking.

TIPS:

- Avoid walking on sand; the vibrations immediately attract them.
- Avoid standing in the open.
- Don't rely on pheropods; spitters are completely immune to their effects.
- Avoid close combat.
