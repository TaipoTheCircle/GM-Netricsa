DESCRIPTION:

This is what the Delta Base security personnel become after a headcrab takes over. Unlike their gray-coated scientist colleagues or the technicians in oil-stained overalls, the guards were initially stronger, more muscular, and better prepared for physical exertion. Unfortunately, this didn't save them from infection—but it made them far more dangerous opponents in their zombified form.

The guards wear Delta Base security vests with holsters from which their service pistols have long since fallen. Their bodies are less deformed than those of the scientists—apparently, their strong muscles resist the parasitic transformation longer. But this doesn't make them any easier to deal with. They are stronger, more resilient, and more aggressive than regular headcrab zombies.

TIPS:

- A zombie guard is more resilient than a scientist. Don't skimp on bullets.
- He dies in the head the same way as everyone else: his head comes off.
- Don't think you can penetrate a bulletproof vest with one shot. Aim for the head or legs.
- In a group, a guard is a priority target because of his survivability.
