DESCRIPTION:

Combine Civil Protection GIGN are former fighters from the legendary French counter-terrorist unit "Groupe d'Intervention de la Gendarmerie Nationale" (GIGN), who survived the Occupation and were recruited by the Combine almost entirely. Their training, discipline, and experience in urban operations have been deemed by the Combine "perfectly suited to the task of suppressing human resistance."

GIGN troopers operate in small tactical groups, employing effective cover, assault maneuvers, and synchronized attacks. They don't rely on brute force, preferring precision, speed, and situational awareness. They communicate directly with Combine headquarters, allowing them to quickly receive orders, situational updates, and target coordinates.

Their equipment combines elements of ancient Terran tactics with Combine technology.

