SANDWORM FROM THE ZENOZOIK


CLASS:      Sandworm, Predatory Coprolite
METABOLISM: Xenobiology, Organosilicon
PERCEPTION: Vibrational, Chemical
SIZE:       3,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme (territorial)
WEAPONALS:  Drilling jaws, rapid lunge from beneath the sand
REWARD:     100 FC
THREAT:     High

DESCRIPTION:

Aggressive subterranean predators that inhabit the loose sands of desert dunes. Their presence is revealed by the characteristic shifting, "unstable" texture of the sand on the surface. The worms are almost constantly in motion underground, forming distinctive "sand rivers." They attack with lightning speed, bursting out of the sand like a tentacle, their mouths wide open and studded with bony plates.

TIPS:

- If you see a "hump" moving under the sand, it's a worm hunting.
- Attack immediately after its lunge-at that moment, it's vulnerable to attacks.