SPAWNER


CLASS:      Spawner
METABOLISM: AI-controlled vehicle
PERCEPTION: Primitive AI
SIZE:       24 ft
ENDURANCE:  Very high
HOSTILITY:  Very high
WEAPONS:    Plasma Bombs
REWARD:     500 FC
THREAT:     Extreme

DESCRIPTION:

This is a modified Rollerball, designed to teleport and create weak enemies.

Unlike other Mental drones, Spawners are heavy, slow-moving bastions with immense firepower. They are one of the most heavily armored and heavily armed vehicles.

They use a plasma cannon to fire long-range plasma bombs at their target. These bombs, while causing minimal area damage, inflict massive impact damage to anything they hit. This makes them excellent at destroying strategic targets such as communications facilities, supply depots, ammunition depots, and battleships. They are one of the main reasons why Mental's forces destroy our colonial bases.

Use powerful firepower to destroy it quickly, and try not to get hit. If structures and large ships can't withstand a lot of hits, then neither can you.

TIPS:

- Use a cannon or any explosives to destroy it quickly.
- Due to its immense firepower, it should be a priority target.
- Avoid its attacks. I know you hear this a lot, but seriously, don't get hit this time.
