WOLF FROM THE MINECRAFT OVERWORLD


CLASS:      Neutral / Tamable
METABOLISM: Warm-blooded mammal
PERCEPTION: Keen hearing, developed sense of smell
SIZE:       2,9 ft
ENDURANCE:  Average
HOSTILITY:  Moderate
WEAPOON:    Fangs
REWARD:     60 FC
THREAT:     Average

DESCRIPTION:

Wolves are wild predators of the Minecraft Overworld, inhabiting forests, taiga, and snowy biomes. In their neutral state, they do not attack, but become extremely aggressive if provoked or attacked by a fellow wolf.

A tamed wolf becomes a loyal battle companion. It follows its master, protects them in battle, and attacks targets hit by its master.

A wolf's health can be easily determined by the position of its tail: the lower the tail, the more severely it is injured.

TIPS:

- Use bones to tame.
- Feed the wolf meat for healing and breeding.
- Wolves are especially effective against skeletons and zombies, but are vulnerable to lava and explosions.