SWARM RANGER


CLASS:        Swarm Ranger
METABOLISM:   Xenobiological
PERCEPTION:   Ranged Predator
SIZE:         6,8 ft
ENDURANCE:    Medium
HOSTILITY:    High
WEAPONS:      Acid Blobs
REWARD:       350 FC
THREAT:       High

DESCRIPTION:

Rangers occupy the niche of short-range living artillery within the Swarm hierarchy. Unlike Drones, which prefer to tear prey apart with their own claws, these creatures have learned to deal with threats from a distance.

The Ranger's primary weapon consists of specialized acid glands located in the front of its body. Using these, the creature launches volleys of caustic biological matter capable of burning through flesh, light armor suits, and the morale of anyone caught in the line of fire.

Observations indicate that Rangers rarely act alone. They typically stay behind the main Swarm forces, bombarding the victim with acid while Drones close in for the attack. Such tactics appear surprisingly intelligent for a species that usually solves problems using claws.

Some researchers hypothesize that the Ranger is a specialized evolutionary offshoot of the standard Drone. Others consider it a distinct species. Unfortunately, neither side has yet managed to obtain a comment from the Rangers themselves.

TIPS:

- Do not stand still while under fire.
- Eliminate Rangers before standard Drones.
- The acid volley is more dangerous than it appears at first glance.
