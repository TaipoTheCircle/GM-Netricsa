DELTA BASE ZOMBIE HEADCRAB, COMMON


CLASS:      Delta Base Zombie Headcrab, Common
METABOLISM: Human Host + Parasitic Headcrab Of Xen
PERCEPTION: Zombified Entity, Former Scientist
SIZE:       5,9 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Claws, Bite, Grab
REWARD:     200 FC
THREAT:     Moderate

DESCRIPTION:

This is almost the same as a Common Headcrab Zombie, but with one important difference: the host's body belongs to a former scientist who worked at the classified Delta Base in the Nevada desert. Instead of the white lab coats of their Black Mesa competitors, these guys wear gray staff uniforms-tattered, torn, soaked in blood and machine oil.

The infection process is the same: a Headcrab burrows into the skull, leaving a human face partially visible beneath the alien shell.

They are just as slow as their white counterparts, and just as relentless. If such a zombie spots a living person, it will pursue them until it closes the distance.

A headshot kills a zombie's head.

TIPS:

- Keep your distance. They're slow, but if they get close, their claws and teeth will do the trick.
- Firearms are effective. Don't waste ammo on close combat with them.
- A headshot kills a zombie's head.
- They're dangerous in groups. One zombie isn't a problem. Three people cornering you is already a problem.
