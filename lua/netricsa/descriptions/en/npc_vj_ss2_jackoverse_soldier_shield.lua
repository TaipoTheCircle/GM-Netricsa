CLONE STORMTROOPER, SHIELDER FROM THE KAMINO


CLASS:      Clone Stormtrooper, Shielder
METABOLISM: Human Clone, Male
PERCEPTION: Breakthrough Assault Unit
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Energy Shield, Electrobaton, Grenades, Imperial Pistol
REWARD:     1800 FC
THREAT:     Very High

DESCRIPTION:

Shielders are veterans of the Stormtrooper Corps and occupy one of the highest ranks of the Galactic Empire's line infantry. Their almost entirely black armor and long cloak serve as a sign of high status and extensive combat experience. These fighters are tasked with breaching fortifications, clearing confined spaces, and eliminating the most dangerous opponents.

A shielder's primary defense is a massive energy shield, capable of withstanding virtually any type of small arms fire. While the shield is between the fighter and the target, most attacks are ineffective. However, the shield's design leaves the wielder's legs exposed and offers no back protection, a fact often exploited by experienced opponents.

In close combat, a shielder uses an electric baton. A few powerful blows are usually enough to shatter bones, disable armor, or knock the victim back several meters. When the enemy maintains their distance, the shielder quickly tosses three fragmentation grenades over the shield, forcing them to leave cover.

If the target is out of reach of the baton and the grenades are expended, the shielder draws an Imperial pistol. The shielder fires a short series of three quick shots with astonishing accuracy, then, with a practiced motion, spins the weapon on his finger and returns it to the holster. This ostentatious manner of handling a pistol is considered a peculiar tradition of the Empire's elite units and simultaneously serves as a demonstration of complete calm in the face of the next enemy.

TIPS:

- Don't shoot the shield. The shield doesn't care.
- Flank or aim for the legs.
- Don't let him get too close. A stun baton ends the argument very quickly.
- If you see three grenades flying, it's time to change position.
