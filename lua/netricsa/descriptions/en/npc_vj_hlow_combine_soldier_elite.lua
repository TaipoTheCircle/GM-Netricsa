YELLOW-EYED COMBINE OVERWATCH ELITE SOLDIER


CLASS:      Yellow-Eyed Overwatch Combine Elite
METABOLISM: Augmented Human, Male
PERCEPTION: Stealth Combine Elite Strike Force
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, Energy Balls
REWARD:     850 FC
THREAT:     Very High

DESCRIPTION:

The Combine Yellow-Eyed Overwatch Elite is an elite trooper disguised as a standard Combine soldier. His armor has the standard blue and white color scheme of the regular soldiers, allowing him to blend into formations and lull enemies into a false sense of security.

His only distinguishing feature is his yellow visor. While his appearance resembles Nova Prospekt's elite prison soldiers, his status is considered that of a regular unit. This is intentional: the Combine integrates such soldiers into standard units to discreetly enhance their combat effectiveness.

In terms of implants, reaction time, and tactical training, he is fully equivalent to an elite soldier. In combat, he acts coolly, coordinates the fire of regular soldiers, and is the first to engage particularly dangerous targets. Energy balls are used to flush enemies out of cover, after which he finishes off the target with precise pulse fire.

TIPS:

- If you see a large yellow visor among regular soldiers, it's bad news.
- He looks like a private, but shoots like an elite.
- Destroy him first-his presence makes the entire squad more dangerous.
- Don't confuse him with a prison elite soldier: he operates in the field and is more mobile.
