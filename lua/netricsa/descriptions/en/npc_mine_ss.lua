SPIDER-JOCKEY FROM THE MINECRAFT OVERWORLD


CLASS:      Spider-Jockey
PERCEPTION: Tactical Tandem
METABOLISM: Arthropod + Undead
SIZE:       11 ft
ENDURANCE:  High
HOSTILITY:  Very High
WEAPONS:    Spider Jumping, Skeleton Accuracy, Elevated Shooting
REWARD:     350 FC
THREAT:     Very High

Description:

The Spider-Jockey is a rare but extremely unpleasant example of two relatively ordinary enemies suddenly deciding to work together. The massive spider functions as a mobile platform, assault unit, and delivery vehicle, while the skeleton above does what it does best - cold-blooded shooting.

Tips:

- Separate them as quickly as possible - individually, they are much less dangerous.