T-REX, JUVENILE FROM THE TERRAFORMED PLANET


CLASS:      T-Rex, juvenile
METABOLISM: Warm-blooded predator
PERCEPTION: Apex predator
SIZE:       8,2 ft
ENDURANCE:  High
HOSTILITY:  Extremely high
WEAPONS:    Fangs, claws, speed
REWARD:     800 FC
THREAT:     High

DESCRIPTION:

This is the adolescent version of the very king of the dinosaurs that everyone fears. He hasn't yet reached nine meters and eight tons, but he is already quite capable of reducing an adult to a bloody stain on the pavement in a matter of seconds.

Appearance-wise, the adolescent is very different from the full-grown predator. He is leaner, longer-legged, and slower. His hind limbs are long, allowing him to reach a decent speed and change direction quickly.

Their jaws are already powerful, but their teeth aren't yet the bone-crushing ones of adults.

