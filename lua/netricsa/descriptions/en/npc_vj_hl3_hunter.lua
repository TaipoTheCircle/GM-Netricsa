COMBINE MINISTRIDER


CLASS:      Experimental Combine Combat Synth
METABOLISM: Fully mechanical, autonomous frontline drone
PERCEPTION: Advanced combat sensors, Combine communications link
SIZE:       9,8 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Pulse charges, energy darts, bladed arms
REWARD:     900 FC
THREAT:     High

DESCRIPTION:

The Ministrider is an early concept and prototype of the Hunter developed by the Combine.

In appearance, the Ministrider resembles a smaller, but no less menacing Hunter - it stands on three legs, has mechanical bladed arms, and is capable of automatic attacks. It fires pulsed energy blasts, making it dangerous to kill even from a distance.

The Ministerrider not only pursues its target but can also exploit its surroundings, react quickly to threats, and engage in aggressive combat in open areas.

