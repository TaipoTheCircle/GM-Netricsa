BIG-HEAD, DED123_228


CLASS:      Big-Head, DED123_228
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     500 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of developers. They wear the Serious Sam uniform, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, wait!" They are completely harmless, but in the "I Am Ugh-Zan" difficulty they receive fire and ice modifiers. They spontaneously appear in secret areas, blocking passages with their sheer numbers.

DED123_228 is a rare breed of fan-enthusiast, whose oversized skull contains not only a love for the game but also an exaggerated sense of justice and respect for the creativity of others. He is the creator of the "TROPICAL GNAARS - Enemies Reskin" mod for Serious Sam Classics: Revolution, where he gave the classic Gnaars a vibrant tropical camouflage.

When Taipo asked permission to port his tropical Gnaars to GMod for the "Serious Sam: Classic SNPC's (EXTRA)" add-on, DED123_228 didn't obstruct, demand commission, or ignore the request. He generously gave his go-ahead, understanding that it would expand the universe of his creation and delight more players.

The original sketch and design belong to his friend Froel Hoates.

He was once alive, but after someone killed him, he was resurrected - becoming an Angel.

TIPS:

- Use a combat knife or pistol to clear a path.
- On the "I Am Ugh-Zan" difficulty, destroy them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.
