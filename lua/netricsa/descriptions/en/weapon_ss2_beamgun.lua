BEAM GUN


WEAPON: Beam Gun
AMMO:   Plasma cells
ROF:    ~300 rounds / min

DESCRIPTION:

Energy weapon with four blue barrel crystals and a central one. Fires a continuous beam when trigger is held, but has a startup delay as the crystal barrels spin up to full speed. Very high damage; ideal for clearing hordes of weak enemies or dealing with tougher foes.

NOTES:

- Hitscan beam - no projectile travel time.
- Excellent against unarmored or grouped enemies.