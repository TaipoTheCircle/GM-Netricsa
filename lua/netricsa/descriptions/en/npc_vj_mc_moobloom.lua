MOOBLOOM COW FROM THE MINECRAFT OVERWORLD


CLASS:      Passive Animal / Rare Cow
METABOLISM: Warm-blooded Mammal
PERCEPTION: Standard vision and smell
SIZE:       5,2 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     75 FC
THREAT:     Low

DESCRIPTION:

The Moobloom Cow is a rare variety of cow covered in bright yellow Mooblooms instead of its normal fur.

It resembles a Mooshroom Cow, but instead of mushrooms, its skin grows yellow flowers, and when it moves, it can leave a trail of flowers on blocks around it. Every plant she plants has a chance of being a dandelion, sunflower, or another yellow flower.

TIPS:

- You can trim it with scissors to get a single Moobloom.
- The meat is nutritious.