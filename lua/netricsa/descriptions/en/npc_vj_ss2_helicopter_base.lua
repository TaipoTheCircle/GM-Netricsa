KOZAK AHA-C64 ATTACK HELICOPTER


CLASS:      Kozak aHa-C64 Attack Helicopter
METABOLISM: Manned Aircraft
PERCEPTION: Attack Helicopter
SIZE:       14 ft
ENDURANCE:  Medium 
HOSTILITY:  Medium 
WEAPONS:    Machine Guns, Rockets
REWARD:     100 FC
THREAT:     High

DESCRIPTION:

The Kozak aHa-C64 class infantry attack helicopter is the latest aircraft in Mental's line. Although lightly armored and lacking the regenerative capabilities of a Technopolip Helicopter, the Kozak is extremely manueverable and very speedy.

Kozaks will strafe around their target while gunning it down with their machineguns and rockets. They should be priority targets in the battlefield - your enemies won't last long without their air support.

TIPS:

- Its high speed allows it to dodge most of your projectile weapons.
- Shoot it down with bullets, preferrably with a minigun or sniper rifle.
- Take cover from its attacks and stay far away - it is inaccurate at long ranges.
