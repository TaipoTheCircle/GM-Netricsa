ILLUSION CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Illusion Creeper
METABOLISM: Plant-Gunpowder With Elements Of Illusionary Magic
PERCEPTION: Hostile mob, Tactical Deceiver
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  Absolute
WEAPONS:    Illusion Clones, Self-Destruct
REWARD:     60 FC
THREAT:     High

DESCRIPTION:

This is a creeper variant that doesn't rely on brute force, but rather uses deception. This creeper is of medium difficulty, dangerous not so much for its explosion as for its ability to confuse its victims before dying.

Like all creepers, the Illusion Creeper has low health, but its main power is creating copies of itself. This ability makes it one of the most insidious enemies: while the victim tries to figure out which creeper is real, the real one gets closer and explodes.

The Illusionary Creeper can create multiple copies of itself, which are visually indistinguishable from the original. The copies move, hiss, and simulate attacks, but disappear or deal no damage when touched.

TIPS:

- Don't panic when you see multiple creepers at once. Chances are, most of them are copies.
- Use ranged attacks (bow, crossbow, snowballs).
- Don't waste resources killing the copies. Simply ignore them and focus on the original.
- The Illusionary Creeper is especially dangerous in dark caves, where it's difficult to distinguish the copies due to poor visibility. Carry torches with you.
