ABBIE FROM FUNDAMENTAL PAPER EDUCATION


CLASS:      Abbie, Paper School Student
METABOLISM: Paper anthropomorphic organism
PERCEPTION: Vision, heightened alertness
SIZE:       ~5 ft
ENDURANCE:  Extremely low
HOSTILITY:  None
WEAPONS:    None
REWARD:     I will find YOU personally if you lay a finger on HIM FC
THREAT:     None

DESCRIPTION:

A 15-year-old Paper School student, but also timid, anxious, and not the brightest. His distinguishing feature is an apple stalk with a leaf on top. He wears a strict school uniform: a white shirt, a black tuxedo vest with a tie, and white shorts. His eyes are just small black slits. Canonically, he was brutally murdered by his teacher, Miss Circling, for failing an exam, but in an alternate scenario, he can be considered alive. He has an extremely low survivability rate due to his paper nature; he tears and breaks easily. He has tender feelings for his classmate, Lana. He poses no threat, being a victim of circumstances and the atmosphere of the school.

TIPS:

- Vulnerable to any physical impact, explosions, and fire.
- In case of danger, he will tremble, withdraw, and try to escape rather than resist.
- Very sensitive to criticism and pressure.