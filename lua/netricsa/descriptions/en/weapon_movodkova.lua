MOVODKOV'S COCKTAIL


WEAPON: Movodkov's Cocktail
AMMO:   Vodka processed by the Never-Drying Drunk
ROF:    1 throw per minute

DESCRIPTION:

An improvised incendiary weapon created through highly questionable, yet effective, "biochemistry". In the Never-Drying Drunk user, the vodka is processed by the body, resulting in an abnormally high alcohol concentration, after which it is excreted and collected in a bottle.

The resulting liquid is highly flammable. Once ignited, the bottle turns into a fully functional incendiary device, operating on the same principle as a Molotov cocktail. When smashed against a surface, it creates a persistent burning zone, damaging everyone within a radius.

The origin of this method remains questionable, but its effectiveness is beyond doubt.

NOTES:

- When smashed, it creates a fire zone similar to a Molotov cocktail.
- The "stronger" the source material, the longer and brighter the burn.
- Effective against groups of enemies and for controlling narrow passages.
- Most useful at close and medium ranges.
- High risk of self-ignition if used carelessly.
- Requires "ammunition" preparation, which can take an indefinite amount of time.
