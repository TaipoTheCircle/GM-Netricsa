COMBINE OVERWATCH ARCTIC SHOTGUNNER


CLASS:      Combine Overwatch Arctic Shotgunner
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Close Combat Assault
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Spas-12 Combat Shotgun, Grenades
REWARD:     700 FC
THREAT:     High

DESCRIPTION:

The Combine Overwatch Arctic Shotgunner is a modified version of the standard Combine alternative assault rifle, adapted for operations in extreme cold, blizzards, and icy infrastructure.

In the past, many of them were soldiers, police, or military personnel who served in the northern regions before the Combine's arrival. After the conquest of Earth, their experience was deemed valuable, and their bodies were deemed suitable for extensive augmentation. Their personalities were suppressed, and their physiology was rebuilt to withstand the constant cold.

He wears white and gray arctic camouflage with orange accents. Thermoregulation systems are built into the armor, allowing the soldier to move without slowing down in areas where a normal person would freeze in minutes.

Like his non-arctic counterpart, this soldier is not designed for long-range combat. His role is aggressive close-quarters combat, breaching defenses, and clearing frozen rooms, bunkers, and tunnels.

TIPS:

- Up close, he is just as deadly as the standard version.
- The white and gray camouflage makes him difficult to spot in the snow.
- In narrow icy corridors, retreat is often the only chance of survival.