FIRE CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Fire Creeper
METABOLISM: Plant-Pyrotechnic
PERCEPTION: Hostile Mob, Walking Pyro
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  Extreme
WEAPONS:    Self-Detonation with Fire Spread
REWARD:     150 FC
THREAT:     Medium-High

DESCRIPTION:

This is what happens when a regular creeper decides that exploding is too boring, and that fire is the true element. This variety of elemental creepers differs from their gunpowder counterparts in their bright orange foliage, which resembles smoldering embers and flames. It's hard to spot one in the desert or the nether, but when one's nearby, you'll know it instantly.

Fire creepers primarily inhabit hot biomes: deserts, savannas, mesas, and, of course, the Nether (Hell). They're not afraid of lava and can thrive in places where regular creepers would be incinerated.

When exploding, a fire creeper doesn't destroy blocks with the same force as a regular creeper, but its blast wave spreads fire over a considerable distance. The entire area within the blast radius is engulfed in flames, which quickly spread to wood, leaves, wool, and other flammable materials. One such explosion can turn a forest into a blazing inferno, and a wooden house into ashes in a matter of seconds.

TIPS:

- Don't fight a fire creeper near wooden buildings. One explosion and your house will be reduced to ashes.
- This creeper is especially dangerous in the forest. Flames quickly spread to the trees, and you could find yourself surrounded by fire.
- If you're in the desert or the netherworld, be doubly careful. In the desert, creepers blend into the sand, and in the Nether, fire is everywhere.
- Keep a bucket of water handy. An explosion can quickly extinguish a spreading fire.
- Fire creepers don't grant experience or drop valuable resources, other than regular gunpowder and sometimes coal.
- Killing a fire creeper has a chance to drop blaze powder, which is used to craft elemental explosives. Explosives made from blaze powder create a fireball when detonated, igniting everything around them.
