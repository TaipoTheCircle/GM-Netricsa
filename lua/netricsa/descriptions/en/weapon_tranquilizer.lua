TRANQUILIZER GUN


WEAPON:      Beretta M9
AMMUNITION   Tranquilizer darts
ROF:         ~10 rounds/min

DESCRIPTION:

A Beretta M9 that has been modified to fire darts containing a tranquilizing agent. This item is used by special services in various countries.

NOTES:

- If you want to subdue someone without killing them, this weapon is ideal.
- It is best to aim for the head, because when the dart hits the brain, the substance inside works faster.


