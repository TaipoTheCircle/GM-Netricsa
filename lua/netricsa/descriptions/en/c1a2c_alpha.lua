BLACK MESA OFFICES, PATH TO THE ELEVATOR


We're almost there. The stairs are behind us, we've bypassed Mental's forces, and now we're facing the last stretch before the elevator. A corridor. Long, winding. At the end, an open door. Into the elevator shaft.

Problem: H.E.C.U.'s in the corridor. They're here. Not upstairs, not behind the barricades—right here, between us and the shaft. They know the elevator is the only quick way up. And they don't want anyone using it. They don't care who. A scientist, an alien, a bystander. The order is the same: no one gets through.

But there's good news. Mental's forces aren't coming here. I don't know why. They don't need the elevator, or the corridor is too narrow, or they've simply decided that the Marines are Sam Stone's problem, not theirs. So it's just you and H.E.C.U.

The elevator is our ticket to the top. Maybe to the exit. Maybe to the next level of this circus. But we have to get there. Because up there, there's either salvation or another fight. And down there, there's only a corridor full of marines and a bottomless shaft.

The elevator is waiting. I hope it still works.
