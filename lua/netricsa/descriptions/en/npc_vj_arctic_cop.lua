COMBINE CIVIL PROTECTION ARCTIC METROCOP


CLASS:      Combine Civil Protection Arctic MetroCop
METABOLISM: Human, Male
PERCEPTION: Combine Civil Protection
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Stun-Stick, Pistol, SMG
REWARD:     150 FC
THREAT:     Low

DESCRIPTION:

The Arctic MetroCop is a specialized variant of the Combine Civil Protection trooper, adapted for service in cold and snowy regions. Dressed in gray and white camouflage, which is intended to camouflage him against ice and snow, though more often than not, it only conceals fear and poor training.

Over his uniform, he wears a gas mask to protect against cold, dust, gas, and, rumor has it, the smell of his own comrades after long shifts. Like regular metro cops, he patrols, disperses civilians, guards facilities, and maintains "order", especially in areas where order has long been absent.

TIPS:

- He's almost not dangerous alone, but he's rarely alone.