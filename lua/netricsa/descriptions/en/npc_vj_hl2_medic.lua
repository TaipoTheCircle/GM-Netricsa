REBEL MEDIC


CLASS:      Resistance Fighter, Medic
METABOLISM: Human
PERCEPTION: Medic
SIZE:       ~1.7-1.8 m
ENDURANCE:  Average
HOSTILITY:  High
WEAPONALS:  Pistol, SMG, First Aid Kits
REWARD:     250 FC
THREAT:     Low

DESCRIPTION:

Rebel Medics are Resistance fighters who have decided that the survival of the squad is more important than their personal kill count. They appear where the battle has already gone wrong and do everything possible to change that plan. Their primary weapon is a First Aid Kit, but if necessary, they can provide cover fire for themselves and the wounded.

Unlike regular soldiers, medics tend to stay slightly behind the line of fire, constantly moving among their allies and assessing who can still fight and who needs to be urgently brought back into the fold. Their training is limited, but their practice is endless: every battle is a new lesson in anatomy under fire.

While they are not designed for direct attacks, the presence of a medic significantly increases the squad's survivability. Resistance soldiers fight with greater confidence knowing there's someone nearby who can get them back on their feet even after a heavy hit.

TIPS:

- Protect them: the loss of a medic dramatically reduces the group's combat effectiveness.
- A medic under fire is a sign that the fight is dragging on.