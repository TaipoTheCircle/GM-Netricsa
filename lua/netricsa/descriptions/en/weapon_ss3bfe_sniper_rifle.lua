GIAT FR F2 SNIPER RIFLE


WEAPON: GIAT FR F2 Sniper Rifle
AMMO:   7.62x51mm NATO
ROF:    ~15 rounds/min
WEIGHT: ~5.8 kg with optics
LENGTH: ~1,200 mm

DESCRIPTION:

The GIAT FR F2 is the standard sniper rifle of the French Army, developed as a major upgrade of the FR F1. Its main features include a thicker, floating barrel with a thermal sleeve that reduces thermal distortion during firing, as well as improved stock ergonomics. The rifle is designed for accurate engagement of targets at medium and long ranges and is valued for its stability and reliability in the field.

NOTES:

- High accuracy in single-shot aimed fire; ideal for long range engagements.
- The bolt-action design provides excellent accuracy, but limits the rate of fire.
- Effective against manpower and lightly armored targets; against heavy armor, precise hits are required.