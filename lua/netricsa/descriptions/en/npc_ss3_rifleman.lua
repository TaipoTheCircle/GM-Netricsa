DESCRIPTION:

The basic infantry of Mental's army, created by cloning and cybernetically modifying captured or deceased Earth Defense Alliance soldiers. They have a distinctive metal helmet with five red sensors in place of a face, allowing them to see in poor visibility. Their free will is suppressed by an implanted life-control unit, leaving only basic combat instincts and the ability to receive orders. They wear standard camouflage and light armor. Despite their modifications, they retain a vestige of humanity: they feel pain, can limp when wounded, and agonize before death. In combat, they use cover, reload, and operate in groups.

