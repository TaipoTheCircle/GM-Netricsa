WRAITH FROM THE MINECRAFT OVERWORLD


CLASS:      Wraith
METABOLISM: Undead
PERCEPTION: Hostile mob, flying Wraith
SIZE:       5,9 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Touch
REWARD:     200 FC
THREAT:     Medium

DESCRIPTION:

This translucent, pale figure with a fluttering "mantra" wanders the Overworld at night, emitting eerie moans.

Unlike many other mobs, the Wraith has no physical body. It flies and doesn't collide with other creatures, making it a difficult target. However, it is quite fast and aggressive: it can spot enemies from 12-16 meters away and attack by touching them.

Regular Wraiths appear in the Overworld at light levels of 7 or lower, like zombies or skeletons. A regular Wraith deals damage simply by touching it.

However, they also have vulnerabilities. Like all undead, Wraiths fear the sun: in daylight, they begin to take damage and die, though they don't catch fire. Their translucency is a double-edged sword. On the one hand, it allows them to blend into the stone walls of caves, remaining undetected. On the other, it reveals their Wraithly nature, allowing their opponents to spot a threat in time.

TIPS:

- Wraiths are difficult to spot in caves due to their transparency, so be alert and listen for their eerie moans.
- Like all undead, they are easily killed with weapons enchanted with the "Celestial Smite" enchantment.
- Since they fly and don't collide with blocks, hiding in tight corners is useless. Better keep your distance and use a bow.
- Wraiths emit a sharp whimper when struck and a strange, unearthly cry when they die.
