GHAST FROM THE MINECRAFT NETHER


CLASS:      Ghast
METABOLISM: Undefined
PERCEPTION: Hostile flying mob
SIZE:       ~13 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Fireballs
REWARD:     350 FC
THREAT:     High

DESCRIPTION:

It resembles a huge white jellyfish with nine tentacles, floating aimlessly through the Nether, emitting heart-rending sounds reminiscent of a child's cry. The Ghast's eyes and mouth are usually closed, making it appear, if not peaceful, then at least indifferent. But this impression is deceptive.

Ghasts inhabit the wastelands of the Nether, basalt deltas, and soul sand valleys-anywhere with enough open space to spread out.

Ghasts have a unique way of detecting targets: they can spot them from up to 100 meters away, but only fire when they have a clear line of sight. Any solid blocks, including glass and foliage, interrupt their line of sight. Ghasts do not attempt to approach their victims.

The ghast's main weapon is a fireball, which flies in a perfectly straight trajectory at the speed of a projectile and explodes on impact. The fireball cannot be knocked off course, but can be deflected by any attack: a sword, a hand, an arrow, a snowball, or even an egg. A projectile that hits a ghast deals 1000 damage, guaranteeing instant death.

Despite its terrifying appearance and size, ghasts have less health than zombies. You can kill one with a bow, a sword (if you can get close), or a reflected fireball. A dead ghast drops gunpowder and a ghast tear, which is needed to make a regeneration potion and craft an end crystal.

Ghasts have many strange properties. They are not undead-they drown in water, take damage from damaging potions, and are healed by healing potions. They are also completely immune to fire and lava, can sit in minecarts (while still firing), and their famous cry is equally loud from any distance. If a ghast flies too close to a wall, it can blow itself up with its own projectile.

Lately, rumors have been circulating online about "happy ghasts", "baby ghasts", and "dried ghasts"-all fan fiction and mods; nothing of the sort exists in the official game.

TIPS:

- If you hear crying, look up and look for its source. The ghast may be far away, but you can always hear it.
- Don't stand on the edge of a netherrack cliff. One explosion and you'll fall into the lava.
- Fireballs can and should be deflected. This is the fastest way to kill a ghast.
- Hide under overhangs or in narrow tunnels - the ghast can't see you there.
- For a guaranteed kill, use a bow with a Strength enchantment.
- If the ghast is hanging directly over lava, be prepared for the drop to burn. It's better to lure it onto a solid surface.
- Glass, foliage, and portals block your view - you can hide behind them.
