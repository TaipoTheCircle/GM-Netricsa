FN P90 SUBMACHINE GUN


WEAPON:    FN P90 
AMMUNITION 5.7x28 mm 
ROF:       ~900 rounds/min

DESCRIPTION:

The FN P90 is a compact personal defense weapon developed by the Belgian company FN Herstal in the late 1980s. It was created for armored vehicle crews and rear units, where high magazine capacity and low recoil are important.

It uses special 5.7x28 mm cartridges capable of penetrating modern body armor while maintaining low recoil. Thanks to its 50-round top magazine and bullpup design, the weapon remains short but very effective in close combat.

NOTES:

- High accuracy and low recoil - ideal for close and medium range combat.
- Penetrates Class II and IIIA body armor.
- Often used by special forces around the world.
- Ergonomic design and ambidextrous controls.