APC 404-08180 DROPSHIP


CLASS:      APC 404-08180 Dropship
METABOLISM: Air Transport Unit, Armored Drop Pod
PERCEPTION: Landing Transport / Airborne Armored Personnel Carrier
SIZE:       19,7 ft
ENDURANCE:  Very High
HOSTILITY:  Low
WEAPONS:    None
REWARD:     100 FC
THREAT:     High

DESCRIPTION:

This is a true flying armored vehicle with five-star comfort. Mental, who usually doesn't care about the well-being of his soldiers, has somehow been generous here. Inside, the ship has everything: a jacuzzi, leather chairs, a plasma TV, and space for two dozen armored soldiers. The walls are so thick that no attacks bother those inside.

From the outside, the Dropship looks like a massive gray-orange flying box with engines. There are no weapons on the ship-it's not designed for combat; its purpose is to deliver soldiers to their destination and then fly away.

TIPS:

- The Dropship has no weapons. You can get close and shoot the disembarking soldiers right at the ramp.
- Destroying the Dropship is possible, but difficult. It requires several hits from the rocket launcher. The best weapon is the CH-181 Hoverbike with its high rate of fire.
- The best time to attack is while the Dropship is flying. It moves slowly in a straight line and cannot respond.
- Don't waste time destroying the ship during disembarkation. It's easier to kill the soldiers jumping out-that way you'll conserve ammo and won't be distracted from the main threat.
- Remember: there's a hot tub inside. If you ever capture that Dropship, first check to see if Mental left a bottle of champagne there.
