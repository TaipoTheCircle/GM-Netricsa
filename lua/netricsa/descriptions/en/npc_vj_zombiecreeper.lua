ZOMBIE CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Zombie Creeper
METABOLISM: Undead
PERCEPTION: Hostile mob
SIZE:       5,5 ft
ENDURANCE:  Average
HOSTILITY:  Extremely high
WEAPONS:    Self-destruct, infect villagers
REWARD:     110 FC
THREAT:     Very high

DESCRIPTION:

This is a hybrid that occurs when two familiar enemies combine into one nightmarish cocktail.

The Zombie Creeper looks like a classic creeper, but with zombie elements: green skin, dressed in zombie rags, and its iconic face. It doesn't explode immediately upon seeing the player, but initially behaves like a zombie.

Like a zombie, it stalks villagers. Like a regular creeper, it explodes when it gets close to its target.

Unlike regular zombies, zombie creepers aren't afraid of daylight. They don't burn, meaning they can be encountered at any time of day.

They ignore the creeper mechanic (sneaking) and simply run to bite and explode.

Like zombies, they can attack in groups, creating a chain reaction of explosions.

TIPS:

- Keep your distance. The best weapon against them is a bow or crossbow. Don't let them get close.
- Use the environment. Narrow passages, water, or lava can help slow them down or destroy them before they explode.
- Run and attack. If you're forced to fight in close combat, use the "hit and run" technique: run up, strike, and immediately jump back.
- Block with your sword, but remember that blocking won't stop the explosion.
- When killed, he can drop up to 4 blocks of dynamite (TNT).
