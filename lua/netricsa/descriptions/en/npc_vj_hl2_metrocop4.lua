COMBINE CIVIL PROTECTION METROCOP COMMANDER


CLASS:      Civil Protection, Unit Commander
METABOLISM: Human, Male (transhuman training)
PERCEPTION: Enhanced (tactical thinking, radio control)
SIZE:       9 ft
ENDURANCE:  Average
HOSTILITY:  Very high
WEAPONS:    AR3 (mounted pulse machine gun)
REWARD:     600 FC
THREAT:     High

DESCRIPTION:

The MetroCop Commander is a rare and dangerous member of the Civil Protection, appearing in high-resistance zones. His black uniform, reinforced body armor, and white mask with gas mask immediately distinguish him from ordinary Metropolitan Police officers. The face is completely concealed: the mask features distinctive breathing holes and round side panels, making identification impossible.

On the left sleeve is a red armband with a white circle and a black Combine symbol-a sign of authority and unconditional subordination to the highest hierarchy. This symbol doesn't so much denote rank as it warns: this is not a patrolman, but an order-follower with no right to question.

The Commander holds an AR3-a heavy pulse weapon whose true purpose and official designation are known only to a limited number of Combine officers. This weapon is designed not for crowd dispersal, but for the complete suppression of resistance.

TIPS:

- Don't engage in direct combat without cover-the AR3 suppresses space, not the target.