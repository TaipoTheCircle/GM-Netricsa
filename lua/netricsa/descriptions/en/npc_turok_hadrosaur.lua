PARASAUROLOPHUS FROM THE TERRAFORMED PLANET


CLASS:       Parasaurolophus
METABOLISM:  Herbivore
PERCEPTION:  Peaceful herd animal
SIZE:        10 ft
ENDURANCE:   Low
HOSTILITY:   None
WEAPONS:     Legs
REWARD:      50 FC
THREAT:      Minimal

DESCRIPTION:

These are peaceful, herbivorous duck-billed dinosaurs that spend most of their time searching for bushes to munch on.

They don't attack or harm anyone; they simply exist and brighten the landscape with their presence.

TIPS:

- Don't waste ammo.
- Use them for cover. Predators often switch to them, giving you time to escape.
