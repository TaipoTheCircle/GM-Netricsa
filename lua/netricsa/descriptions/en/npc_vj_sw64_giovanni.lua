DESCRIPTION:

A world-renowned chef and star of Broadside Animation's animated films. He is an anthropomorphic black-and-white Canada goose wearing a white chef's hat. In the cartoons, he is portrayed as a slightly arrogant but talented cook who prepares exquisite dishes and sometimes teases his friend Bucky the Beaver, calling him a "rodent".

One day, he and his friends, Bucky the Beaver and Olivia the Otter, went fishing, but their boat crashed off the coast of the mysterious island of Nulla Terra. The local wolf inhabitants, terrified by the invasion, tasked each of their friends with repairing the damage. Giovanni, as the chef, was tasked with preparing food for the wolf pack.

Like many Broadside characters, he was brought to life through the terrifying technology of "Starlings"-living suits into which humans were placed. In Giovanni's case, the man inside the suit was Gary Wilson.

According to the encrypted files, Gary was brutally murdered: his head was shoved into a running deep fryer. After this, Gary's consciousness apparently became trapped inside the goose suit, creating one of the corrupted entities.

