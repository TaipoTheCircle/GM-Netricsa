HIGHWAY 17, HUNTER HELICOPTER APPROACHING


We're in a dilapidated building clinging to a cliff face directly above a chasm. Below are rocks and, judging by the height, a guaranteed death if we fall into the water.

All around us is only sky, wind, and the sound of approaching rotor blades. The Hunter is already here. He's taking his time, circling, choosing his moment. He knows you're somewhere inside.

There are crates of RPG ammo here. Scattered around the building.

A flimsy platform outside. A wooden structure is attached to the building, hanging over the chasm. It looks like it could fall apart in a sneeze. But it offers a perfect view of the helicopter. Perfect, Sam, and very dangerous.

Time to show whose guns are more powerful.
