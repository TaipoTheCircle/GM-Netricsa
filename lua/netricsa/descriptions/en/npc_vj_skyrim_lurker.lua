LURKER FROM APOCRYPA


CLASS:      Lurker
METABOLISM: Daedric Spawn, Amphibious
PERCEPTION: Guardian of Black Books
SIZE:       17 ft
ENDURANCE:  High
HOSTILITY:  Very High
WEAPONS:    Claws, Stomping Feet, Poisonous Tentacles, Acid Spit
REWARD:     1000 FC
THREAT:     High

DESCRIPTION:

Daedric creatures native to the misty waters of Apocrypha, the plane of Hermaeus Mora. In the wild, they guard tomes of forbidden knowledge, where they now attack locals and random travelers.

Lurkers resemble a hybrid of fish, toad, and gorilla, with elements of something deep-sea. They have three toes on each foot and massive hind legs that they stomp with, shaking the ground. Tentacles can erupt from their mouths for medium-range attacks, and their acid spit can corrode armor in a matter of seconds.

TIPS:

- The acid spit has a long reach. Dodge or take cover.
- If a lurker attacks with its tentacles, jump back.
- It's best to kill them from a distance before they get too close.
- They're vulnerable to cold.
