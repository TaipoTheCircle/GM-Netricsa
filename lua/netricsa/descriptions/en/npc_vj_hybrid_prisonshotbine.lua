COMBINE OVERWATCH HYBRID PRISON SHOTGUNNER


CLASS:      Combine Overwatch Hybrid Prison Shotgunner
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Security and Extermination Assault Unit
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    SPAS-12 Combat Shotgun, Grenades
REWARD:     700 FC
THREAT:     High

DESCRIPTION:

The Combine Overwatch Hybrid Prison Soldier with Shotgun is a beefed-up version of the Nova Prospekt trooper, temporarily or permanently deployed outside of prison complexes. The Combine uses these soldiers in situations where standard forces are insufficient and the deployment of elite forces is impractical.

The soldier is clad in reinforced prison-style armor and equipped with a tactical helmet with a protective visor. A red visor underscores his affiliation with the Nova Prospekt guard and assault forces. Despite its apparent protection, the visor is not completely bulletproof - accurate shots to the head remain effective.

His primary weapon is a SPAS-12, designed for riot suppression and clearing cells, corridors, and technical areas. His tactics are simple and brutal: rapid approach, suppressive fire, and a complete lack of capture attempts unless otherwise directed.

TIPS:

- Don't overestimate the visor - headshots still work.
- In close combat, the SPAS-12 leaves virtually no chance.
- The best option is distance and accurate fire.
- He feels right at home in narrow corridors.