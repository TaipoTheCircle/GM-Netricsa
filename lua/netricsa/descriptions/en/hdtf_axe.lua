AXE


WEAPON: Axe
AMMO:   None (melee)
ROF:    ~60 strikes/min

DESCRIPTION:

The axe is a crude but reliable melee weapon. Its sharp blade and heavy poll deliver powerful chopping blows capable of ripping flesh and smashing wooden obstacles. Simple to use and effective when paired with mobility or ambush tactics: one or two well-placed strikes can dispatch weak foes or heavily wound larger enemies.

NOTES:

- High single-hit damage on direct impact; useful for shredding clustered enemies at close range.
- Can break wooden props and certain cover objects in the environment.
- Straightforward mechanics - low skill floor, but requires closing distance to the target.