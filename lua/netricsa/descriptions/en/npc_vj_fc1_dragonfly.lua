DRAGONFLY FROM THE KRIEGER ARCHIPELAGO


CLASS:      Dragonfly, Predatory Insect
METABOLISM: Insect, Predator
PERCEPTION: Compound eyes, high reaction speed
SIZE:       0.13 ft
ENDURANCE:  Low
HOSTILITY:  None
WEAPONS:    None
REWARD:     1 FC
THREAT:     None

DESCRIPTION:

A predatory flying insect, often found near bodies of water and in open spaces of the archipelago. It is distinguished by its high maneuverability and flight speed. It can hover in the air and change direction abruptly. It poses no threat. Its presence adds vibrancy and realism to the tropical atmosphere, especially on sunny days near water.

TIPS:

- Not an enemy or a resource. There is no point in attacking.