MENTAL (TAH-UM)


CLASS:      Mental (Tah-Um)
METABOLISM: Immortal God
PERCEPTION: Last of the Hum-Tah Race
SIZE:       Unknown
ENDURANCE:  Virtually Unlimited
HOSTILITY:  Absolute
WEAPONS:    Psionic Abilities, Ancient Magic, Hum-Tah Technology, Countless Armies
REWARD:     Peace
THREAT:     Maximum

DESCRIPTION:

Mental, also known by his true name, Tah-Um, is considered the greatest threat to intelligent life in the known galaxy. He is the last known representative of the ancient race of immortal, godlike beings, the Hum-Tah, who once possessed knowledge far superior to the modern technology of most civilizations.

Mental is estimated to be millions of years old. During this time, he has conquered or destroyed countless worlds, wiping entire civilizations from the face of the universe. Captured peoples are either completely exterminated or become part of his endless army through biological, mechanical, or genetic modification.

Most of Mental's known armies are composed not of volunteers, but of enslaved beings. Many life forms have been artificially modified specifically for war. Captives are often subjected to cruel experiments or executions, after which their bodies are used to create new combat units. This is how numerous headless soldiers, kamikazes, and other members of his forces were created.

Despite his virtually unlimited power, Mental himself rarely appears on the battlefield in person. Instead, he directs the war through generals, commanders, and numerous governors, coordinating the actions of his armies with colossal psionic abilities. His observable form is merely a gigantic dark projection, while Tah-Um's true appearance remains unknown.

In addition to his incredible intellect, Mental possesses the ancient magic of Hum-Tah, the technology of a vanished civilization, and psionic abilities that allow him to influence the minds of others, control vast armies, and coordinate invasions across multiple star systems.

Over millennia of warfare, the only enemy who has repeatedly thwarted his plans was "Serious" Sam Stone (You). It was this inability to completely eliminate a single individual that forced Mental to resort to the most radical methods of warfare, including the destruction of entire planets.

Despite all the information gathered, the origins of Hum-Tah, the limits of Tah-Um's powers, and his true location remain unknown.

TIPS:

- If you encounter Mental in person, events have taken a very unfavorable turn.
- Don't underestimate the enemy just because he rarely engages in battle personally.
- Dodge his attacks.
- He can't throw the Moon on himself; let your imagination run wild!
