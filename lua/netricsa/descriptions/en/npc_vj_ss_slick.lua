JAKE FROM THE SUBWAY SURFERS CITY


CLASS:      Jake
METABOLISM: Human, Male
PERCEPTION: Slippery Type, Parkourer
SIZE:       ~5,2 ft
ENDURANCE:  Infinite
HOSTILITY:  Low
WEAPONS:    Spray Paint, Skateboard, Jetpack, Run
REWARD:     500 FC
THREAT:     Zero

DESCRIPTION:

A typical teenager in bright clothes: an orange hoodie, blue jeans, white sneakers, and a backwards baseball cap. He has dark hair, dark eyes, and a distinctive smile—he clearly enjoys every run.

HE is a graffiti artist who, along with his friend Tricky and other kids, decorates subway cars in cities around the world. His favorite theme is pandas, which often appear in his drawings.

His goal is to run away. To escape from the inspector and his dog, who maintain order on the railway. For an infinite amount of time. On rails, on train roofs, through tunnels, and through platforms.

In the run, Jake uses a skateboard for speed, a jetpack for flight, and a magnet to attract coins. His main enemies are approaching trains and dead ends.

TIPS:

- Runs fast.
- Uses power-ups.
