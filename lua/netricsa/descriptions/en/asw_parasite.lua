SWARM PARASITE


CLASS:        Swarm Parasite
METABOLISM:   Xenobiological Parasite
PERCEPTION:   Infecting Organism
SIZE:         0,9 ft
DURABILITY:   Very Low
HOSTILITY:    Extreme
WEAPONS:      Infection, Leap Attack
REWARD:       50 FC
THREAT:       High

DESCRIPTION:

The parasite is one of the most numerous and dangerous forms of Swarm life. Despite their small size and fragile bodies, these creatures pose a serious threat even to well-equipped combatants.

Their primary hunting method involves a swift leap onto the victim. Upon a successful attack, the parasite implants larvae into the host's body; these larvae then begin to develop rapidly within the living tissue. In the early stages, the infection may go virtually unnoticed, but the victim's condition deteriorates rapidly over time.

The creature's life cycle presents a particular danger. If the infected host dies, the larvae complete their development and emerge from the body, forming a new swarm of parasites. Thus, even a single successful attack can lead to a rapid increase in the Swarm population.

Parasites hatch from organic eggs, usually deposited in damp, sheltered locations. The interior of the egg clusters is filled with a viscous biological fluid essential for the development of the young. Consequently, large concentrations of parasites are most often found in humid tunnels, underground complexes, and abandoned colonies.

TIPS:

- Destroy parasites before they get within leaping range.
- If you discover an egg cluster, destroy it as quickly as possible.
- A single parasite is rarely a problem. The trouble usually starts later.
