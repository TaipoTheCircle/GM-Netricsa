COMBINE OVERWATCH TACTICAL PRISON SOLDIER


CLASS:      Combine Overwatch Tactical Prison Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Heavy Tactical Security and Suppression Unit
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, SMG, Grenades
REWARD:     800 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Tactical Prison Soldier is a specialized version of the Tactical Soldier, trained and equipped for service in Nova Prospekt and similar penal complexes. Unlike field units, these soldiers are trained to operate in confined spaces, among cells, corridors, and mass inmate suppression zones.

His key feature is a yellow visor and a reinforced tactical helmet with a protective visor. The helmet's design completely neutralizes his vulnerability to headshots: headshots have no critical effect, turning attempts at accurate fire into a waste of ammunition. The Combine deliberately made these soldiers as resilient as possible to ambushes and surprise attacks inside prison cells.

Their armor is even thicker than that of standard tactical soldiers. Internal augmentations suppress pain responses and maintain combat effectiveness even with serious injuries.

TIPS:

- Headshots are completely useless - don't even waste your time on them.
- Explosives and heavy weapons remain the only reliable solutions.
- He's especially dangerous in the narrow corridors of Nova Prospekt.
- The yellow visor is highly visible - but it's not a weakness, it's a warning.