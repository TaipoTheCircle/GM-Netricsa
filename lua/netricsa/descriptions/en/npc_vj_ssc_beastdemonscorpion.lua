BESTIAL DEMON SCORPION, SOLDIER


CLASS:       Beastial Demon Scorpion, Soldier
METABOLISM:  Exobiological, Arthropod
PERCEPTION:  Advanced vision, bioluminescent eyes
SIZE:        3 m
ENDURANCE:   High
HOSTILITY:   Extreme
WEAPONS: Scorpion-like tail sting
REWARD:      6000 FC
THREAT:      High

DESCRIPTION:

Soldier Beastial Demon Scorpions - Young specimens of aggressive creatures from tropical planets, once independent, but personally lured away by Solgar, Mental's right-hand man. Since then, they have become a full-fledged part of the Horde, serving as melee assault creatures.

The scorpion's appearance resembles an arachnoid that has decided to become a tank. The smooth, glossy green shell, segmented body, three pairs of legs, and massive head create the impression of a living battering ram. Atop its head is a green helmet-like helm, fused tightly to the shell. Two pairs of eyes are located on the sides of the head, providing excellent vision even in low light. In the dark, the eyes glow an ominous green.

The helmet enhances head protection and makes the creature particularly resistant to frontal fire.

The upward-curving tail, reminiscent of Earth-type scorpions, is used for powerful strikes.

The Beast Demon Scorpion lacks ranged attacks and relies solely on speed, pressure, and physical strength, seeking to close the distance as quickly as possible.

TIPS:

- Immediately rushes to the target.
- Does not hide, retreat, or consider the future.
- C4 explosives are the best method of dialogue.
- A scorpion is deadly in close combat.
- If the scorpion is already nearby, you did everything wrong.