ANNIHILATOR TANK FROM THE CYBERSTAN


CLASS:      Annihilator Tank
METABOLISM: Mechanical
PERCEPTION: Heavy Armor, Destroyer
SIZE:       13 ft
ENDURANCE:  Extreme
HOSTILITY:  Absolute
WEAPONS:    Terminal Pressure Battle Cannon, Coaxial Fusion Cycler, Hull-Mounted Heavy Fusion Repeater
REWARD:     3000 FC
THREAT:     Absolute

DESCRIPTION:

This is a steel fortress on tracks, which the Automatons shamelessly copied from Super-Earth designs, but, as usual, they made it worse. The tank is a massive combat vehicle, heavily armed, capable of destroying an entire squad of Helldivers in seconds. From the front, it appears nearly invulnerable, but like any vehicle, it has weak points.

The tank is armed with three types of weapons:

The Terminal Pressure Combat Cannon is the primary weapon. This powerful cannon is mounted on the turret and fires once every 6 seconds. It flashes red before firing. A direct hit kills a Helldiver instantly.

The Coaxial Fusion Cycler is a twin-linked machine gun with a high rate of fire (1200 RPM). It fires in 2,7-second bursts, followed by a 1,5-second cooldown.

The Hull-Mounted Heavy Fusion Repeater is a forward-firing machine gun built into the hull. It fires more slowly (400 RPM) and has a limited firing angle.

The Annihilator Tank has several weak points that can be exploited for a quick kill:

The rear engine, the rear turret radiator, and the tracks. (Destroying a track immobilizes the tank but does not destroy it.) It is also vulnerable to fire.

TIPS:

- When attacking a tank, always approach from the rear or side. It is nearly invulnerable from the front.
- Destroying the tracks immobilizes the tank but does not destroy it. Use this if you can't kill it outright.
- Before the main cannon fires, it flashes red. As soon as you see the flash, immediately take cover or dive to the ground.
- The tank has a slow turret traverse speed. Use this to get behind enemy fire.
- A destroyed tank can be used as cover.
- When destroyed, the tank explodes within an 8-meter radius. Move away.
- Don't try to destroy a tank with a standard rifle. You'll just waste your ammo.
