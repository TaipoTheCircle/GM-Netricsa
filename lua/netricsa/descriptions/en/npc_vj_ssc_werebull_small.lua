SIRIAN WEREBULL, MOUSE


CLASS:      Sirian Werebull, Mouse
METABOLISM: mammal
PERCEPTION: labor animal
SIZE:       0,8 ft
ENDURANCE:  low
HOSTILITY:  medium
WEAPONS:    horns
REWARD:     2000 FC
THREAT:     medium

DESCRIPTION:

Crossbred with mixed genes of Sirians and some kind of cattle. The Werebull behaves much like an ordinary bull and attacks with its horns by charging.

Mental, attempting to enhance their adaptability, accidentally added mouse genes, resulting in a dramatic reduction in size. The miniature hybrids escaped from laboratories and infested the dungeons of Ancient Persia, adapting to life in mouse holes. Retaining their ancestor's habits, they attack from the darkness of their tunnels, aiming to snare their prey with their sharp but microscopic horns. They are dangerous primarily due to their surprise and numbers.

TIPS:

- Move away from the attack line.
- If you hear a rumble in the burrow, that's him.