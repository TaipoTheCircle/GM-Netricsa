AKM

WEAPON:     AKM
AMMUNITION: 7.62x39
ROF:        ~600 rounds / min

Description:

The Modernized Kalashnikov Assault Rifle is a modified version of the AK-47, developed by Mikhail Timofeevich Kalashnikov in 1950 and adopted by the USSR in 1959. It is very popular among terrorists and guerrillas. It has been used in almost all wars around the world, such as the Vietnam War, the Afghan War, the Chechen War, and the Iraq War, and is currently used in the SVO. It is a very reliable and popular assault rifle.

Notes:

- It has had a huge impact on the world, spawning countless copies and modifications.
- It is very well suited for assault operations.
- In the forests of Vietnam, you can hear the sound of its shots echoing off the trees.