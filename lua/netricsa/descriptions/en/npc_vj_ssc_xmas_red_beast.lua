ALUDRAN REPTILOID, CHRISTMAS (RED)


CLASS:      Aludran Reptiloid, holiday subspecies
METABOLISM: Cold-blooded, reptile-like
DEFINITION: Primitive race, member of the New Year cult
SIZE:       15 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Magic homing missiles (red fireballs)
REWARD:     5500 FC
THREAT:     Average

DESCRIPTION:

A large fourhanded reptile living in the Aludran system in the constellation of Canis Major. This primitive race has agreed to fight for Mental in exchange for the spell-casting powers it gave them.

They cast self-propelled fireballs with primitive homing abilities. 

The Christmas Red Aludran Reptiloid is a rare and disturbingly grotesque mutation of the plains species. Unlike its brethren, this subspecies has been doubly influenced: by the magic of the Mental and by an entity known as the Giant Snowman, who promised an Eternal New Year. The price of the deal turned out to be symbolic and humiliating - the obligatory wearing of a Santa Claus New Year's hat.

They are easily recognizable in appearance: red magic balls instead of the usual green ones, red blood, and a bright red bow on their massive chests, secured with crude magic. Despite their comical appearance, they remain extremely dangerous in battle.

