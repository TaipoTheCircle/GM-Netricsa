DILOPHOSAURUS FROM THE TERRAFORMED PLANET


CLASS:      Dilophosaurus
METABOLISM: Predator, Theropod
PERCEPTION: Ambush predator
SIZE:       ~8,2 ft
ENDURANCE:  High
HOSTILITY:  Extremely high
WEAPONS:    Head ram, tail slam, jaws
REWARD:     1000 FC
THREAT:     High

DESCRIPTION:

One of the most dangerous predators on the planet, occupying an intermediate position in the food chain between pack-dwelling Velociraptors and giants like Tyrannosaurus. Contrary to popular belief, the local Dilophosaurus species does not spit venom and possesses a fairly typical set of hunting skills for its size.

This lizard poses a serious threat even to well-armed humans thanks to three key characteristics. First, it can reach high speeds and catch a running person. Second, its attack involves a rapid dash followed by a head-on ram, capable of knocking someone off their feet. Third, the Dilophosaurus actively uses its powerful tail as a flexible weapon, spinning its entire body for a lashing blow.

TIPS:

- Dangerous alone, deadly in pairs.
- If a Dilophosaurus attacks, dodge.
