"BETA" SWARM MORTAL BUG


CLASS:        "Beta" Swarm Mortar Bug
METABOLISM:   Xenobiological
PERCEPTION:   Artillery Unit
SIZE:         9 ft
ENDURANCE:    Medium
HOSTILITY:    High
WEAPONS:      Explosive biological projectiles
REWARD:       650 FC
THREAT:       High

DESCRIPTION:

The "Beta" Swarm Mortar Bug is a specialized siege variant designed to eliminate enemies from long range. Unlike Drones or Rangers, its body is almost entirely dedicated to a single task: the production and launch of organic munitions.

A massive maw dominates the front half of its torso; inside, explosive bio-capsules are formed. Upon impact, these adhere to surfaces and detonate after a few seconds. This allows the Mortar Bug to block passageways, force prey out of cover, and scatter defensive formations.

The creature's physiology differs significantly from most other "Beta" Swarm members. Its visual organs are underdeveloped or concealed beneath layers of tissue; it likely navigates using vibrations, scents, and other biological sensors. This explains why the Mortar Bug rarely operates alone, preferring to remain under the protection of more mobile units.

Despite its lethality, the creature is ill-suited for close-quarters combat. Its massive bulk and specialized anatomy make the Mortar Bug far less maneuverable than most other Swarm units.

TIPS:

- Watch where the projectiles land.
- Do not allow the Mortar Bug to maintain sustained fire.
- Exploit its lack of mobility against it.
- If a creature the size of a small vehicle opens its maw in your direction, it's best not to wait for the outcome of the experiment.
