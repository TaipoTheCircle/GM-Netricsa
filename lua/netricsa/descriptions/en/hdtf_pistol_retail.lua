M9 PISTOL


WEAPON:      M9 Pistol
AMMO:        9x19mm Parabellum rounds
ROF:         Semi-automatic, ~15 rds/min effective

DESCRIPTION:

Widely used military and law enforcement sidearm. Offers good accuracy and low recoil, making it reliable for rapid follow-up shots. Serves as a standard-issue handgun for many H.E.C.U. soldiers in the Black Mesa conflict.

NOTES:

- Accurate and easy to control.
- Lightweight but limited in stopping power.
- Outclassed by heavy-caliber sidearms like the .357 Magnum or Desert Eagle.