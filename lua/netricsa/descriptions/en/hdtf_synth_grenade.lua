SYNTH PLASMA GRENADE


WEAPON: Plasma Grenade
AMMO:   Self-Loading Plasma Core
ROF:    Disposable / Throwable
WEIGHT: 1.6 lbs

DESCRIPTION:

Developed by Combine scientists, the Plasma Grenade is an experimental weapon powered by a self-contained electrical core. When detonated, it releases a powerful plasma discharge that not only stuns and briefly immobilizes enemies, but also causes devastating energy burns.

NOTES:

- DO NOT allow enemy hands to reach this weapon.
- Effective against infantry.