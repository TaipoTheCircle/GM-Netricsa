BIG-HEAD, GEINBEERG


CLASS:      Big-Head, Geinbeerg
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     300 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of the developers. They wear the uniform of the Serious Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They're completely harmless, but in the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They spontaneously appear in secret areas, blocking passages with their sheer numbers.

Geinbeerg is a unique specimen, whose brain is hypertrophied not by coding, but by exploring digital anomalies. He's not affiliated with Croteam, but is an important part of the GMod community ecosystem. His specialty is taking awesome screenshots.

His greatest achievement was creating screenshots of the cursed zerozerozero maps for Taipo's "GM-Netricsa" project. While others were afraid to even load maps like city.bsp or xmasxmas.bsp, Geinbeerg boldly entered them, took documentary screenshots, and survived (without even realizing it). He became an unwitting savior for Taipo, who was terrified of facing ARG horrors. Geinbeerg himself doesn't even suspect that his courage saved his colleague from panic attacks.

I met Taipo on the Awesome Te4m Telegram channel, where I was discussing a story about a trap addon that, instead of the promised model, hid the Interloper NPC from the GMod ARG.

TIPS:

- Use an army knife or pistol to clear the path.
- On the "I Am Ugh-Zan" difficulty, destroy them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.