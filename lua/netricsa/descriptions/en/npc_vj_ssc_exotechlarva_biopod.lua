BIOPOD


CLASS:      Arthropod (Mental's Horde)
METABOLISM: Insectoid organic
PERCEPTION: Visual + motion sensing
SIZE:       3 ft tall 
ENDURANCE:  Very Low (dies when taking any damage) 
HOSTILITY:  High
WEAPONS:    Ram attack
REWARD:     500 FC
THREAT:     Low (individually)

DESCRIPTION:

Biopods are small arthropod-like creatures created by the Mental's horde. They are spawned by the Exotech Larva during, limb-less creatures that slither or dash across the ground. Their two long antennae and bluish-green eyes mark their distinctive appearance.

While individually weak, they spawn in numbers and charge, quickly, ramming into them to deal damage. The smallest scratch can kill a Biopod, but their sheer number can overwhelm if ignored.

TIPS:

- Biopods have very low health; a single shot from the Schofield .45 or Thompson submachine gun will kill them easily.
- For large groups of Biopods, the P-LAH Chainsaw is extremely effective at destroying them if the you wants to deal with them quickly.
- Since each one dies easily, manage crowd control rather than focusing on each individually.