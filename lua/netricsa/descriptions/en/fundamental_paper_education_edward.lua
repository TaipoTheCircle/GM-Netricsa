EDWARD FROM FUNDAMENTAL PAPER EDUCATION


CLASS:      Edward, the Inventor-Bully
METABOLISM: Paper anthropomorphic organism
PERCEPTION: Vision, tech savvy
SIZE:       ~3,2 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Homemade devices, sharp claws, tail
REWARD:     30 FC
THREAT:     Average

DESCRIPTION:

One of the three main bullies at Paper School, along with Oliver and Zip. Recognizable by his spiky white hair, short black horns, round glasses, and a black tail resembling a devil's tail. He has black hands and feet with pointed fingers. He wears a black and white shirt and large boots. In contrast to Oliver's brute strength, Edward is a resourceful and tech-savvy bully. He created the drone he used to kidnap Claire and throw her into the pit. He is likely interested in science and engineering. Like his accomplices, he is a favorite of Miss Circinus and enjoys impunity. He possesses enhanced resilience.

TIPS:

- Beware of his intellect and technological devices.