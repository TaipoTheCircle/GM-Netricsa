MOTHER SPIDER FROM THE MINECRAFT OVERWORLD


CLASS:      Mother Spider
METABOLISM: Arthropod, Arachnid
PERCEPTION: Hostile mob, family leader
SIZE:       2,9 ft
ENDURANCE:  Average
HOSTILITY:  Extremely high
WEAPONS:    Bite, jumping, wall climbing, pack coordination
REWARD:     300 FC
THREAT:     High

DESCRIPTION:

This is not just a solitary predator, but a true matriarch, traveling the Overworld surrounded by her brood. Unlike ordinary spiders, which lead a solitary life, the Spider-Mother is never alone.

At nightfall, this dangerous beast goes hunting, accompanied by 3-6 baby spiders, one of which sits on her back. The mother spider resembles a normal spider in appearance, but her behavior is radically different. She is the center of the fighting unit, and the entire swarm acts as a single organism.

The mother spider behaves like a normal spider: she attacks in low light or when provoked. But the main danger lies in her babies. All the babies follow their mother relentlessly and attack any target she chooses. If you strike the mother spider, the entire swarm will attack you. If you strike a baby, the mother and the rest of the siblings will become enraged.

Babies riding on their mother's back are especially treacherous – they are protected by her body and can suddenly jump off in the midst of a fight.

TIPS:

- Kill the hatchlings first. The fewer little spiders left alive, the weaker the swarm's attack will be.
- Don't let yourself be surrounded. The Mother Spider and her brood will try to pin you down.
- Use weapons of mass destruction. One well-aimed sword strike with the Blast Wave spell or a well-placed Harm Potion can destroy several hatchlings at once.
- Be careful at night. The Mother Spider is only found at night and always with her brood.
- The reward for killing the Mother Spider is small—a few threads and a rare spider eye—but the safety of the cleared area is worth it.
