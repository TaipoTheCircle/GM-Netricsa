HANDS


WEAPON:      HANDS
AMMUNITION:  None (hand-to-hand combat)
ROF:         ~90 strikes/min

DESCRIPTION:

Evolution gave primates hands with fingers. These hands can be used to grab objects, explore the world around them, and, of course, fight.

NOTES:

- Hitting too much can cause fatigue.
- For effective combat, it is best to use a variety of techniques. For example, after blocking, follow up with a powerful blow to the jaw.
