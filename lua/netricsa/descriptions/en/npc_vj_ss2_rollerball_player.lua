HAM-Z-RR314 "ROLLERBALL", RAMMING VEHICLE


CLASS:      HAM-Z-rr314 "Rollerball", Ramming Vehicle
METABOLISM: Mechanical
PERCEPTION: Ball transport
SIZE:       6,5 ft
ENDURANCE:  Average
HOSTILITY:  Dependent on driver
WEAPONS:    Ram, spikes, mass
REWARD:     500
THREAT:     Average

DESCRIPTION:

The Rollerball is one of those engineering marvels that makes you wonder, "Who the genius was who came up with this and where should they put a monument to him?" It's a huge ball, studded with spikes, with a person running inside it. Literally run like a hamster on a wheel, only instead of a hamster wheel, there's a multi-ton sphere of death, crushing everything in its path.

The controls are incredibly simple: if you want to move, run. If you want to turn, lean. If you want to kill, accelerate and direct this colossus at the nearest cyclops. The impact pierces the spikes, shatters the flesh, and the rollerball rolls on, never slowing down.

The design allows for jumping. How exactly you can jump inside the ball and make this colossus lift off is a mystery, but the fact remains: the rollerball bounces like a rubber ball filled with anger and spikes.

This thing can be used in a variety of ways: as a weapon, as transportation, as a way to quickly escape a dangerous area, or as an attraction for those who've always wanted to feel like a Terminator. The main thing to remember is that the ball takes damage too. A few powerful collisions and all that's left is spikes and memories.

TIPS:

- Accelerate and hit.
- The rollerball is fast. Use this to dodge missiles and lasers.
- Sometimes it's better to jump over an obstacle than try to knock it down.
- If you see a crowd of enemies, roll straight into them. One run costs thousands of bullets.
- Not to be confused with AI counterparts.
