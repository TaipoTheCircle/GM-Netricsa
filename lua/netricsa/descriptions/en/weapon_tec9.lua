TEC-9 SELF-LOADING PISTOL


WEAPON:      Intratec TEC-DC9
AMMUNITION   9x19 cartridges
ROF:         ~20 rounds/min

DESCRIPTION:

A self-loading pistol developed in Sweden in the 1980s. Released on the market in 1984. It was popular with gangs in Los Angeles until Intratec closed down in the US.

NOTES:

- A good pistol for slum warfare.




