ALUDRAN REPTILOID, ELECTRIC


CLASS:      Aludran Reptiloid, Electric
METABOLISM: Cold-blooded, Reptile-like, Artificially Augmented
PERCEPTION: Genetically Engineered Primitive Race
SIZE:       15 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Large Electric Missiles
REWARD:     7500 FC
THREAT:     High

DESCRIPTION:

A large fourhanded reptile living in the Aludran system in the constellation of Canis Major. This primitive race has agreed to fight for Mental in exchange for the spell-casting powers it gave them.

Mental wanted to enhance them further but many reptiloids didn't want to be experimented on. However through a strangely convenient accident involving a malfunctioning electric socket, a portion of its reptiloid forces gained electrical powers.

The Electric Reptiloids are considered freaks among their own kind.

TIPS:

- Their high-speed projectiles can be difficult to dodge, so eleminate them from a distance.
- Always watch your surroundings, in case its projectile bounces back into you.
