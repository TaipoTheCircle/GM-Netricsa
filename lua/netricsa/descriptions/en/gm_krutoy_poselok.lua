A COOL VILLAGE, BASEMENT SECRETS


Sam, judging by the name, we've ended up in a village the locals have nicknamed "Cool", either ironically or defiantly.

But the coolness here is unique: either they were filming a village rap, or it was an experimental testing ground for crossing flies with antlions.

There's almost no information from official channels, but word of mouth thrives.

Word of mouth is more reliable than any documentation. They say that in this village:

Basements are not for potatoes. Locals say that someone keeps zombies in the dark, damp basements. Either for security, or as pets, or simply because the Russian hinterland is boring without zombies. If you hear a distinctive wheezing sound from the basement, don't go down without a shotgun.

The Wooden Toilet is an exclusion zone. A mutation has occurred in the toilets. The ordinary flies that were always plentiful here have transformed into antlions.

Imagine: you go to the bathroom, and a creature with mandibles swoops down on you from its hole. The locals are probably used to it by now and are using buckets in their gardens.

The houses are excellent shelters, but entering them is risky: there are zombies in the basements, and monsters in the toilets. The best tactic is to stick to open spaces and not look in the windows. And never, Sam, never use the local toilet. Even if you really need to. Wait until you reach the forest.

Let's go, let's take a look. Just don't sniffle.
