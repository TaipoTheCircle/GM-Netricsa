COMBINE CIVIL PROTECTION MILITARY COP


CLASS:      Combine Civil Protection Military Cop
METABOLISM: Human, Male
PERCEPTION: Paramilitary police unit
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Revolver, Alyx Gun, SMG
REWARD:     650 FC
THREAT:     Average

DESCRIPTION:

Civil Protection Military Cop - These soldiers are used in areas of high instability: quarantine sectors, suburban areas, prisons, temporary checkpoints, and "hot" streets where the average cop wouldn't survive for long.

They wear an all-black military uniform, including a tactical vest with multiple pouches, protective pants, and heavy boots. Most have the "MP" marking on their sleeve, emphasizing their military jurisdiction.

The helmets are compatible with gas masks, making them equally prepared for both gas and a full-blown chemical disaster.

TIPS:

- Don't underestimate them because of their "police" status - they're practically soldiers.
- Use your distance: in close combat, they like to shoot a lot and without much philosophy.
- If you see a black uniform with "MP", be prepared - it's only going to get tougher.