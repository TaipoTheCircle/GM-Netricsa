SCRAPJACK


CLASS:      ScrapJack
METABOLISM: Cybernetically modified stitched-together corpse
PERCEPTION: Basic vision (3 head variants), vibrational
SIZE:       8 ft
ENDURANCE:  High
HOSTILITY:  High
WEAPONS:    2 rocket launchers, crushing blow
REWARD:     5000 FC
THREAT:     High

DESCRIPTION:

A cheap and mass-produced heavy infantryman of the Mental Horde, literally assembled from the body parts of fallen soldiers and equipped with powerful rocket launchers. Extremely slow and clumsy, but compensates for this with a huge reserve of fat and devastating firepower. The rockets fly in a predictable pattern: one directly at the target, the others preemptively fire on the flanks. When enraged, it fires more frequently and accurately. At close range, he delivers a devastating blow, capable of killing in one hit.

TIPS:

- His missiles can penetrate thin walls. A close-up strike is fatal.
- A-24 "Devastator", flare gun, C4 - Excellent options for fighting him.