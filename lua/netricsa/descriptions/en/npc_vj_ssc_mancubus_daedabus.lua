DAEDABUS FROM HELL


CLASS:      Daedabus
METABOLISM: Mutant Demonic Mammal
DEFINITION: Modified Behemoth
SIZE:       13 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    Two Green Explosive Gas Cannons
REWARD:     5000 FC
DANGER:     Absolute

DESCRIPTION:

A demon personifying human imperfection and greed, gluttony, and idleness. It has incredibly thick skin, massive elephantine legs, and a small head. For the purpose of destroying and roasting its victims, the Daedabus has two cannons implanted in its forearms that fire explosive acid balls.

Instead of the usual demonic flesh, his body has acquired a swamp-green hue, and in place of his two eyes, there's now a single, large green eye glowing with Argent energy. This energy hasn't changed his personality-he's still as evil, stupid, and destructive as ever-but it has made him physically stronger.

Unlike the regular Mancubus with its flamethrowers, the Daedabus fires a green, acidic, explosive gas-the same substance that fills the explosive barrels at UAC bases.

Like many other denizens of Hell, the Daedabus was purchased by Mental from the Lord of Hell to strengthen his Horde. Now these green demons roam the planets controlled by Mental, sowing death wherever their heavy feet tread.

TIPS:

- They fire randomly, so stay behind cover.
- Don't conserve ammo unless you want to die.
