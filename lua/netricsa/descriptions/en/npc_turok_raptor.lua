UTHARAPTOR FROM THE TERRAFORMED PLANET


CLASS:      Utahraptor
METABOLISM: Predator, Theropod
PERCEPTION: Pack Hunter
SIZE:       ~8,2 ft
ENDURANCE:  Average
HOSTILITY:  Extremely High
WEAPONS:    Claws, teeth, speed, pack tactics
REWARD:     400 FC
THREAT:     High

DESCRIPTION:

This is the main fighting unit of the planet's dinosaur population and the main headache for anyone who ventures into the jungle without heavy weapons. If you imagine raptors as small, nimble lizards knee-high, forget it. These creatures are as tall as a man, weigh nearly half a ton, and have claws that can rip through body armor like cardboard.

Utahraptors resemble a hybrid of a bird and a lizard, with a distinct killer bent. Their long hind legs allow them to reach a respectable speed and perform sharp, sideways leaps.

Utahraptors' main characteristic is that they hunt in packs. A single raptor is dangerous, but the real hell begins when there are three, five, or even a dozen of them. They surround, flank, distract, and strike from behind. If you let them surround you, you can't even try to escape.

TIPS:

- Don't stand still. Raptors are faster than you, but their attack trajectory is predictable.
