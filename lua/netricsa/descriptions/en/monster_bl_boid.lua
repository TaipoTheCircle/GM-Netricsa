BUTTERFLY FROM THE SWEET ISLAND OF THE BIG LOLLY


CLASS:      Sweet Island Butterfly
METABOLISM: Imago
PERCEPTION: Passive flying fauna
SIZE:       2,9 ft
ENDURANCE:  Very low
HOSTILITY:  None
WEAPONS:    None
REWARD:     10 FC
THREAT:     None

DESCRIPTION:

The Big Lolly Sweet Island butterfly is a local analogue of birds, fulfilling the same ecological role. These creatures are completely passive and never show aggression, even in direct contact with other island inhabitants.

Their wings are large and a rich blue.

They usually fly in groups, following the stable air currents above the islands.

TIPS:

- They pose no threat.