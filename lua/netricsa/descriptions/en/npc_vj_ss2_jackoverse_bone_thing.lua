DESCRIPTION:

This...thing is one of the most mysterious and dangerous known life forms on the planet Kleer. Despite numerous eyewitness accounts, the creature's origin remains unknown. It does not belong to any known subspecies of Mental's army and does not resemble any representative of the local fauna.

At first encounter, the Thing is easily mistaken for an unusual statue assembled from the bones of various Kleer creatures. It is completely immobile and can remain so indefinitely. However, if the observer turns away or loses sight of the creature, even for a moment, it changes position or appears significantly closer.

After several such movements, the creature's behavior changes dramatically. It emits a deafening, piercing scream and begins to pursue its target at full speed. The Thing's speed is so high that there are no known cases of successful escape.

Physical contact with the creature invariably results in the instant death of the victim.

Attempts to destroy the Thing have been unsuccessful. Firearms, explosives, energy weapons, and heavy combat systems have had no noticeable effect on it. According to available data, the creature is indestructible by known means.

Its appearance combines features of the Skeleton from Kleer and the Bone Snake from Kleer. This has led researchers to hypothesize that the Thing may be an unknown evolutionary form of these species or their common descendant. However, no evidence has been found to support this theory.

Residents of Kleer avoid areas where the Bone Thing has been spotted. Even the most experienced hunters and veterans prefer not to engage with it, and reports of its appearance prompt the immediate evacuation of nearby settlements.

TIPS:

- If you think it's just a statue, don't jump to conclusions.
- As long as the creature remains in sight, it remains inactive.
- Don't lose sight of it for a second.
- If it begins to move and pursue you, the only sensible course of action is to leave the area immediately. There are currently no known ways to destroy the Bone Thing.
