ZOMBIE HORSE FROM THE MINECRAFT OVERWORLD 


CLASS:        Zombie Horse
METABOLISM:   Undead
PERCEPTION:   Hostile / Passive
SIZE:         5,2 ft
ENDURANCE:    High
HOSTIMISSION: High
WEAPONS:      Bite
REWARD:       400 FC
THREAT:       Low-Medium

DESCRIPTION:

This is a grim, green version of a normal horse with completely black eyes.

The zombie horse's behavior is unusual. It grazes in meadows like a living horse. But a piece of its rotten essence remains forever within it: it is undead. Sunlight causes it to burn, healing potions harm it, and harmful potions heal it.

TIPS:

- A tamed horse can be equipped with a saddle and even armor.
- It can gallop underwater, making it ideal for exploring oceans and rivers.
- It burns in the sun (unless it's wearing armor), and healing potions will harm it rather than heal it.
