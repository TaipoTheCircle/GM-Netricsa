REINFORCED ARACHNOID, ADULT FROM THE SCORPIUS


CLASS:      Reinforced Arachnoid, Adult
METABOLISM: ectothermous, arthropod
PERCEPTION: primitive sentient race
SIZE:       20-25 ft
ENDURANCE:  high
HOSTILITY:  extreme
WEAPONS:    chain-gun, tail sting
REWARD:     5000 FC
THREAT:     high

DESCRIPTION:

A holy warrior of a primitive race descended from arthropods. Extremely dangerous due to his religious beliefs.
The arachnoid's two claws are implanted with machine guns equipped with an ammunition recovery device. Its body is covered in an exoskeleton that protects it from bullets, explosives, and fire.

TIPS:

- Dangerous sting - do not approach too closely.
- Will shoot through their own ranks in fury.
