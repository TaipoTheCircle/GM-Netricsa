CLONE STORMTROOPER, SNIPER FROM THE KAMINO


CLASS:      Clone Stormtrooper, Sniper
METABOLISM: Human Clone, Male
PERCEPTION: Elite Marksman of the Galactic Empire
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Enhanced "RAPTOR" Sniper Rifle with Laser Designator
REWARD:     1200 FC
THREAT:     Very High

DESCRIPTION:

Snipers are the next level in the Stormtrooper Corps hierarchy and are recruited exclusively from the most disciplined and accurate clones. They are easily identified by their gray armor, issued to soldiers who have undergone special training for long-range combat.

The sniper's primary weapon is a modernized "RAPTOR" rifle, equipped with high-precision optics and a laser designator. The laser allows for tracking moving targets and significantly reduces aiming time, while its powerful armor-piercing ammunition can penetrate most known types of personal protective equipment.

Unlike standard assault troops, snipers rarely engage in open combat. They occupy commanding heights, rooftops, or fortified positions, from where they eliminate the most dangerous targets, leaving the rest to the line infantry. Even after detection, they maintain their composure and continue firing until the last possible moment.

Allied with Mental, these soldiers are valued for their ability to eliminate the enemy before the main forces engage. Many commanders prefer to send a few snipers first before committing entire units to the attack. Losing one such marksman is unpleasant, but losing a commander to a single well-aimed shot is far more unpleasant.

TIPS:

- If you notice a red laser, someone has already spotted you.
- Don't stand in open areas for too long.
- Smoke, cover, and constant movement greatly reduce their effectiveness.
- Gray armor is less visible than white. Unfortunately, they fire backwards.
