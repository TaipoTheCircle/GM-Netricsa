COMBINE CIVIL PROTECTION COP


CLASS:      Combine Civil Protection Cop
METABOLISM: Human, Male
PERCEPTION: Occupation Police / Law Enforcement
SIZE:       6 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Pistol, Stun Baton, Manhack
REWARD:     350 FC
THREAT:     Medium

DESCRIPTION:

Civil Protection Cop is the most recognizable "down-to-earth" representative of the Combine. Once upon a time, they were ordinary people: former police officers, security guards, volunteers, or simply those who decided a whistle and baton were better than becoming a Stalker. Now they and the Metrocops are the face of order in City 17 and other cities.

He wears a dark gray uniform: jacket, pants, gloves, and boots, all of which are uniformly uniform. Yellow identification stripes bearing the inscription "C17" are pinned to his sleeve. A black helmet with a gas mask robs him of individuality, while the orange stripe serves almost as a decorative symbol of his affiliation with a system that views the world through filters.

A manhack-a mechanical "assistant" that deploys with a click-is strapped to the cop's back. The manhack functions as a scarecrow, scout, and punisher all at once, and sometimes seems smarter than its owner. The cop himself often prefers threats, baton strikes, and displays of authority over actual combat.

They have almost no armor, and minimal training. Their strength lies not in their individual abilities, but in their numbers, fear, and the support of the Combine. However, paired with a manhack, such a cop can pose a serious problem, especially for unprepared civilians.

TIPS:

- A cop is weak on his own, but a manhack dramatically increases the danger.
- Destroy the manhack first - without him, a cop quickly loses power.