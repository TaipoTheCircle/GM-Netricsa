FASTER UNCLE FROM THE FAVERSHOLM


CLASS:      Faster Uncle
METABOLISM: Nightmare Spawn
PERCEPTION: Ambush Killer
SIZE:       5,6 ft
ENDURANCE:  Average
HOSTILITY:  Absolute
WEAPONS:    Prosthetic Blades for Arms and Legs
REWARD:     260 FC
THREAT:     High

DESCRIPTION:

This is one of the most striking and frightening monsters, first encountered in a residential building.

He is a disfigured man with cleaner, smoother spikes. His head is obscured by bars extending from his shoulders, forming a protective cage-a headshot is impossible. He lacks conventional arms and legs: instead, he has long, sharp metal spikes. He uses these blades to slash and stab his victims. Because the legs have similar appendages, a distinctive metallic clanking sound is heard when walking.

The Quick One prefers to operate from ambush. It hides in dark corners or behind obstacles, only to suddenly leap out, break down the door, and attack. When attacking, it screams loudly, attempting to intimidate. It runs rather slowly, but the suddenness and screams confuse the enemy.

When killed, the Quick One doesn't simply fall. It impales its neck with blades, emitting a painful, mournful cry, and falls to the ground.

Like most monsters, the Quick One symbolizes injury. The metal spikes in place of limbs represent a metal wheelchair. The caged head reflects a mind "locked" in madness and emotional problems.

It represents people from the environment who have been a source of irritation and pain. The monster's act of suicide upon death is a direct parallel to suicidal tendencies.

TIPS:

- Be careful in long corridors with many doors and dark corners. The Quick One likes to jump out suddenly.
- The monster runs quite slowly, but the screams and suddenness make it easy to panic. Don't panic.
- Hitting the Quick One is difficult due to its speed. The best tactic is to wait until it tries to strike, then back away and begin firing with careful aim.
- You can try hand-to-hand combat: run up, hit, and run away. But it's risky.
- If you hear a metallic clang and a piercing scream, it's nearby. Ready your weapon and look around.
