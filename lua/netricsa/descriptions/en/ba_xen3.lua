THE FRONTIER WORLD OF XEN, ISLANDS


Xen decided to go even further and abandon the concept of land.

Before us are floating islands, levitating in the void.

There are many enemies here. Vortigaunts, local fauna, and everything Xen grows for uninvited guests. Some shoot. Some jump. Some simply watch from the edge of the island, estimating how far you'll fall. They're all sure you're out of place.

Somewhere there's a path to the teleportation control device. Of course, you're not allowed to access it directly. First, you have to prove you can move through the air without wings.

The local Aflok birds swarm around, creating the illusion of life uncontaminated by war. An illusion that lasts precisely until the first shot.

