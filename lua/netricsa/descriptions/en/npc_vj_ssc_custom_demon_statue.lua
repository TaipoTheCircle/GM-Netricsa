ANIMATED STATUE OF A FIENDIAN REPTILOID DEMON


CLASS:      Animated Statue Of A Reptilioid Demon
METABOLISM: Magically animated stone
PERCEPTION: Magical construct
SIZE:       20 ft
ENDURANCE:  High
HOSTILITY:  High
WEAPONS:    Lavaball projectile, claws
REWARD:     6000 FC
THREAT:     Medium

DESCRIPTION:

After three thousand years of service, even the most powerful spellcaster occasionally experiences bouts of incoherent Latin babbling. Mordekai, whose brain had been slightly damaged by the botched resurrection, once, in yet another fit of nonsense, demanded of Mental: "Necesse est magnitudinem daemonum Daemonicorum Reptilianorum in lapide immortalizare, ut cubicula ab infidelibus custodiant!"

(The greatness of the fiendian Reptilioid Demons must be immortalized in stone, so that they will guard the chambers from the infidels!)

Mental, accustomed to carrying out even the most absurd orders of his prized Servant, ordered an exact replica of the six-meter-tall mutant carved from basalt and infused with magic.

The result is what it was.

Outwardly, it is a perfect replica of the original. The difference is that the statue stands in place and does not move from its pedestal until it spots its target. In its normal state, it is simply a sculpture that could be mistaken for a decorative element. But as soon as an enemy approaches a certain distance, the statue's eyes glow red, and its combat program is activated.

Like its living counterpart, the statue can create miniature black holes from which it draws balls of molten lava. A magical field holds the projectile just long enough to reach its target. As it flies, the ball gradually cools and eventually explodes, regardless of whether it hits anyone.

TIPS:

- The projectiles cannot be destroyed, so dodge them until they explode.
- Watch its eyes. If they glow red, you're within range.
- The statue doesn't move until it sees a target, even if you shoot at it.
