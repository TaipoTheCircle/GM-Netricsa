RESISTANCE REBEL HUNTER


CLASS:      Resistance Rebel Hunter
METABOLISM: Biomechanical Synthetic
PERCEPTION: Heavy Resistance support unit
SIZE:       7,8 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Needler, Ram, Blades
REWARD:     700 FC
THREAT:     Very High

DESCRIPTION:

The Resistance Rebel Hunter is a standard Combine synthetical that was successfully captured and reprogrammed by Resistance engineers. After being hacked, its control systems were completely rewritten, causing it to abandon the Alliance and defect to the Rebellion.

This Hunter is easily distinguishable from the original in appearance. Its hull has been repainted matte black, the familiar blue sensor glow has been replaced with a bright orange, and the top of the hull bears the recognizable Lambda symbol, which has become the emblem of the Resistance.

Despite the change in ownership, the vehicle's combat capabilities remain virtually unchanged. The Hunter is still capable of high speed, long jumps, overcoming obstacles, and instantly closing the distance to the enemy. Its powerful forelimbs are used to deliver heavy slashing and piercing blows, capable of penetrating light armor and incapacitating even heavy infantry.

The primary weapon remains the integrated needle launcher, but the reprogrammed system utilizes orange fletchettes. Upon impact, these projectiles embed themselves in the target or surface and then detonate after a short delay, posing a hazard to both personnel and vehicles.

Unlike Combine synths, reprogrammed Hunters recognize Resistance fighters as allies and actively support them in combat. They accompany assault teams, defend fortified positions, and are capable of independently pursuing Combine units until the threat is completely eliminated.

Although these machines now fight on humanity's side, they remain the same deadly combat synths. Any error during reprogramming can result in a reversion to the original protocols, so each Hunter is constantly monitored by Resistance engineers.

TIPS:

- Avoid lingering in open areas: Fletchettes explode with a slight delay and affect a large area.
- After ramming, the Hunter briefly loses speed, giving it the opportunity to retaliate.
- The most vulnerable area remains the head and central sensor unit.
- Powerful explosive armament effectively disrupts the synth's stability and allows for its quicker incapacitation.
- Despite the Resistance symbolism, the hostile specimen retains all the lethality of the original Alliance Hunter.
