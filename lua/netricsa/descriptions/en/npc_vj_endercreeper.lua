ENDER CREEPER FROM THE MINECRAFT END


CLASS:      Ender Creeper
METABOLISM: Hybrid-Gunpowder
PERCEPTION: Ender Creature Hybrid, Elite Hunter
SIZE:       5,5 ft
ENDURANCE:  High
HOSTILITY:  Absolute
WEAPONS:    Teleportation, Target Homing, Explosion, Bite
REWARD:     300 FC
THREAT:     Absolute

DESCRIPTION:

This is the result of a mutation of a normal creeper that was exposed to ender energy in the End for too long.

The Ender Creeper differs from a normal creeper not only in color but also in speed and visibly intelligence. When moving, it leaves a trail of purple particles, similar to the Enderman's teleportation.

The Ender Creeper inherits the Enderman's abilities: it can teleport short distances, dodging arrows and attacks. When shot with a bow or crossbow, it instantly disappears and reappears behind or to the side, ready to attack. Sometimes, it can teleport right in front of its victim, blocking their path.

It stalks its target, teleports to attack from an unexpected direction, and then explodes.

TIPS:

- Don't try to shoot the Ender Creeper with a bow or crossbow. It will teleport, and you'll just waste your arrows.
- Use water to disable its teleportation.
- In melee combat, strike quickly with your sword, giving it no time to react.
- The Ender Creeper doesn't explode instantly. If you hear a hissing sound, jump away or use a shield.
- Killing an Ender Creeper has a chance to drop an Ender Pearl. This is a great way to farm pearls if you don't want to kill Endermen.
