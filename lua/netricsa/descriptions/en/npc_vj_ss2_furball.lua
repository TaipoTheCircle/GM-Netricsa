FUR MONSTER


CLASS:      Fur Monster
METABOLISM: Carnivorous
PERCEPTION: Bioweapon (Clone / Jumping Unit)
SIZE:       2,7 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Bite, leap, claws
REWARD:     300 FC
THREAT:     Medium

DESCRIPTION:

A creature that everyone mistakenly believes to be fecal matter from the giant planet Magnor. It's proof that even the dark lord Mental has something that could be called "pity."

When Mental first saw this creature, he mistook it for a pile of shit. It was small, brown, and round, sitting on a rock. But Mental noticed it moving and stole it. In any case, he took it to his ship, cloned it, and trained each copy to jump and bite.

In appearance, the Fur Monster is a round, furry thing covered in short brown fur. It has small green eyes that glow in the dark and two large ears that stick out in opposite directions. Its entire face is a huge maw filled with sharp white teeth. It has two strong legs with white claws, on which it moves exclusively by jumping.

In combat, the Fur Monster has no complex tactics. It simply leaps at the enemy, trying to sink its teeth into their face or leg, and then tries again. It has no regard for self-preservation and feels no fear.

TIPS:

- They look like shit.
- They're not dangerous alone, but in a pack they can bite to death.
- They jump in a predictable trajectory, so hitting them with a shotgun or assault rifle is easy.
- Their bite is painful.
- They're vulnerable while jumping. Use your shotgun when they're flying toward you.
- Mental assures me they don't stink.
