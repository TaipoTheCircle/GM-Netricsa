PIGLIN SWORDSMAN FROM THE MINECRAFT NETHER


CLASS:       Piglin Swordsman
METABOLISM:  Pig-like humanoid
PERCEPTION:  Primitive warrior race of the Nether
SIZE:        6,3 ft
ENDURANCE:   Average
HOSTILITY:   Conditional
WEAPONS:     Golden sword (sometimes enchanted)
REWARD:      400 FC
THREAT:      Average

DESCRIPTION:

One of the two main races of pig-like creatures inhabiting the Nether. Unlike their zombified relatives, they retain some intelligence, the ability to speak their own grunting tongue, and, most importantly, a passion for gold.

This passion is so strong that it defines their entire behavior: piglins are neutral only toward those wearing at least one piece of golden armor.

Without it, they immediately become aggressive.

Swordsmen make up exactly half of the adult piglin population (the other half prefer crossbows). They are armed with golden swords, which can be enchanted, and attack in close combat, swinging their weapons as fiercely as the champions in the forest mansions.

Piglins inhabit the crimson forests and wastelands of the Nether, and also inhabit bastions-enormous fortresses where they guard treasures and gold blocks. In bastions, they are often joined by fierce piglins-stronger brethren with golden axes who are immune to distractions involving gold.

Piglins fear soulfire and their zombified brethren, at the sight of which they flee.

They can't swim, drown in water, and aren't immune to fire or lava, despite living in hell.

TIPS:

- Wear gold armor. Even one piece will keep the piglins from attacking you.
- Don't open chests or break gold blocks in front of them-this provokes aggression even through armor.
- Swordsmen are dangerous in close combat, but rather slow.
