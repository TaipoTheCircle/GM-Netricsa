OSIPR PULSE RIFLE


WEAPON:      OSIPR Pulse Rifle
AMMUNITION  verwatch Energy Cells / Secondary: Alliance Electric Balls
ROF:         ~600 rounds/min (primary), 1 grenade/sec (secondary)

DESCRIPTION:

The standard energy rifle of the Overwatch Combines. Fires high-velocity pulse beams for rapid suppression and can launch energy balls to inflict damage over a large area. Highly effective against both organic and mechanical targets.

NOTES:

- Primary fire delivers fast and accurate energy beams.
- Alternate fire fires electric balls that deal damage over a large area.
- Effective at medium range; accuracy decreases at longer ranges.