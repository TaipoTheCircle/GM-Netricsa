MOBENIX, AN INCREDIBLE PLACE


Sam, this complex is a real treat!

A huge story, filled with detail and interactive under the hood.

Look what's here: an entire station with a real train (and you can ride it!), a race track, a bowling alley with a dance floor and music, a mine where you can drill into walls, and even a few cozy cabins in the forest. In short, the designers clearly didn't skimp on their imagination.

And...

Before the start, we'll find a hallway with teleporters to various industries. The main area is a mix of urban buildings, caves, and a subway station.

But the main feature is the train. It can drive itself, or you can take control.

If you turn on autopilot, a gnome driver will appear at the wheel, closing the doors and even applying emergency brakes if anyone gets on the tracks.

By the way, if you're sitting in an elevator with a gnome in the forest, be prepared to fall down the shaft.

There are many secrets hidden here.
