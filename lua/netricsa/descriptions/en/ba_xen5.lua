When the Black Mesa scientists were tinkering with teleportation, they quickly realized one thing: Xen was involved in the process.

The solution seemed elegant. Take a huge Xenium crystal formation, connect the device to it, and turn it into a focal point. Thus, the Xen Tuner was born. A node that would direct teleportation, like a beacon in a world where direction itself is a relative concept.

The research team sent here to install the device disappeared shortly after completing their work. Nothing dramatic. They simply stopped communicating. The scientists on Earth drew the appropriate conclusions, devised a different targeting method, and... abandoned the device. Xen, as usual, didn't object.

Before you is a tuner. A large device. A pair of moving rods connected to a crystal. A small generator that powers it all. The rods are controlled via a console and look like they're best left alone unless absolutely necessary. Which, of course, is exactly what we're about to do.

This relay was part of a three-element network.

The first is the core of the Lambda complex.

The second is a rocket in Earth orbit. (The other You already did that.)

The third is him. Here. In Xen.

So go ahead, Sam.

Turn the device on.

