"BETA" SWARM PARASITE


CLASS:      "Beta" Swarm Parasite
METABOLISM: Xenobiological
PERCEPTION: Parasite
SIZE:       3,2 ft
ENDURANCE:  Very Low
HOSTILITY:  Very High
WEAPONS:    Infect, Jump, Claws
REWARD:     60 FC
THREAT:     High

DESCRIPTION:

The "Beta" Swarm Parasite is a small but extremely aggressive subspecies, specializing in infecting larger organisms. It hatches from eggs and almost immediately begins searching for the nearest prey, using its long hind limbs for rapid leaps.

During an attack, the parasite attempts to attach itself to the victim and inject infectious material into their body. If an infected organism dies before clearing the infection, a new generation of parasites quickly matures within it, soon bursting forth.

Compared to parasites from other swarms, Beta Swarm individuals are distinguished by their elongated heads, some with green carapace, and longer grasping limbs. Despite these differences, their life cycle and attack methods are virtually identical.

Alone, parasites rarely pose a serious threat. However, when they appear en masse, they can rapidly spread the infection throughout the entire squad, especially when working in concert with other Swarm organisms.

TIPS:

- Destroy parasites before they close the distance.
- If infected, immediately find a way to clear the infection from the organism.
- Do not allow parasites to surround the group, especially in narrow passages.
- Destroy the eggs first to prevent parasites from spawning.
- Automatic weapons and fire are especially effective against these creatures.
