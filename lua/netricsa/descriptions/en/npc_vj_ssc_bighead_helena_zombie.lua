BIG-HEAD, HELENA HUNSKI


CLASS:      Big-Head, Helena Hunski
METABOLISM: Humanoid
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
STAMINA:    Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Cool Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Where are you going!?" They are completely harmless, but in the "I Am Ugh-Zan" difficulty, they gain fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their sheer numbers.

Helena Hunski is one of those unseen but vital "brains" whose work remains behind the scenes, yet without whom the entire mechanism known as Croteam would not function. Her exaggerated cranium is the result of multitasking and a deep understanding of the studio's inner workings. While programmers and designers created chaos onscreen, Elena, as a business assistant and team member, ensured order and organization in the real world.

Her contributions to the series, acknowledged in the credits of Serious Sam: The Second Encounter and Serious Sam II, are a reminder that behind every great shooter is not only a brilliant coder or artist, but also people coordinating the work, solving countless organizational problems, and maintaining team cohesion. As a likely relative of one of the studio's founders, Davor Hunski, she embodies the family spirit and dedication that have been the hallmark of Croteam for decades.

She was once alive, but after someone killed her, she rose from the dead - becoming a Zombie Demoness.

TIPS:

- Use a combat knife or pistol to clear the path.
- On the "I Am Ugh-Zan" difficulty, eliminate them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.