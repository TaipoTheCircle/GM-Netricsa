RED GUMMY BEAR FROM THE CANDY ISLANDS OF BIG LOLLY


CLASS:      Red Gummy Bear
METABOLISM: Bear-like gelatinous organism
PERCEPTION: Small predator of the Candy Islands
SIZE:       1,9 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Jump, Bite
REWARD:     150 FC
THREAT:     Low-Medium

DESCRIPTION:

The red gummy bear is one of the most common, yet cunning, inhabitants of the Candy Islands of Big Lolly. Outwardly, it looks like an ordinary bear-shaped gummy bear: a red body, rounded paws, and a frozen expression. It's this appearance that most often leads to the deaths of unwary guests.

Despite their small stature, gummy bears are extremely aggressive. They move in short bursts and attack suddenly, leaping onto their prey with their entire body. After the leap, they bite down with their teeth.

They usually hunt in packs.

TIPS:

- Don't let them get too close-they jump almost instantly.
- Destroy them first when they appear.
- Don't confuse them with regular candy.