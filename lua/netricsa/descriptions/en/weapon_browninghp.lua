BROWNING HI-POWER SELF-LOADING PISTOL


WEAPON:     Browning Hi-Power
AMMUNITION: .40 SW cartridges
ROF:        ~30 rounds/min

DESCRIPTION:

A self-loading pistol designed by John Browning and Dieudonné Saëver with an extended-capacity magazine. The name reflects the high capacity of the magazine - 13 rounds. The pistol was developed to meet the technical specifications of the French army, but was not adopted for service in France. After Browning's death in 1926, the design was refined by Sev, chief designer of the Belgian company Fabrique Nationale.

NOTES:

- Although the description states that the magazine holds 13 rounds, it actually holds 10 rounds.
- Nevertheless, the pistol is good and powerful.



