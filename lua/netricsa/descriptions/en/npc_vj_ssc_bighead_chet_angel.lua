BIG-HEAD ANGEL, CHET FALISEK


CLASS:      Big-Head Angel, Chet Falisek
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5.9 ft
ENDURANCE:    Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are skinny humanoids with exaggerated skulls containing the brains of Croteam's developers. They wear the uniform of Serious Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They are completely harmless, but in the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their sheer numbers.

Chet Falisek is the co-founder of the satirical website Old Man Murray (OMM), which he co-created with Eric. Together, they set the tone for gaming journalism with their sharp, cynical, and incredibly funny language. Chet is part of a duo that Croteam deemed worthy of immortality as secret fans with bloated skulls. While Eric was often on the front lines, it was their collaboration that created the unique voice that so amused and inspired the developers. His presence in the game is a tribute to the journalist.

When you killed him in the Sacred Courts, he transformed into an Angel.

TIPS:

- Use a combat knife or pistol to clear a path. - On "I Am Ugh-Zan" difficulty, destroy them remotely due to their modifiers.
- In a group, a sledgehammer with a horizontal strike is effective.
