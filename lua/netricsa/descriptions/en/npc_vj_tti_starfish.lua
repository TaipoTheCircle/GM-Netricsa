CLASS:      Big Patrick Star
METABOLISM: Starfish organism
PERCEPTION: A living, Deadbeat Sea Star, dangerous to others
SIZE:       8,2 ft
ENDURANCE:  Average
HOSTILITY:  Very high
WEAPONS:    Club-like head
REWARD:     5000 FC
THREAT:     High

DESCRIPTION:

Big Patrick Star is SpongeBob's loyal friend and colleague, known for his incredible stupidity, which makes him deadly. He feeds on Krabby Patties, made from fish, and attacks with his main weapon - his head, which he uses to smash anything in sight.

He has no eyes, but he can "teleport" short distances when his victim turns away, similar to SCP-173. His mouth is huge and oval, filled with teeth, and purple blood spurts with every hit. He has no pants. Despite his apparent immobility, his teleportation makes him extremely unpredictable and deadly.

TIPS:

- Maintain eye contact - as soon as you turn away, Patrick will instantly move closer.
- Ranged combat is your best option; avoid direct contact.
- Plan your maneuvers and constantly keep an eye on him to avoid being in his kill zone.
- Patrick's stupidity is deadly - ​​don't underestimate him.