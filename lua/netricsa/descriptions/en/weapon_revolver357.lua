DESCRIPTION:

The Colt King Cobra revolver is a six-shot revolver with a double-action trigger mechanism. It was first introduced in 1986. It was developed and manufactured by Colt's Manufacturing Company. It was intended for use by law enforcement agencies and for sale to firearms enthusiasts. Available in blued and stainless steel.

