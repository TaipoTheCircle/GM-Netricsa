COMBINE OVERWATCH ELITE COMMANDER


CLASS:      Combine Overwatch Elite Commander
METABOLISM: Augmented Human, Male
PERCEPTION: Combine High Command, Anomalous Tactician
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Absolute
WEAPONS:    AR2 Pulse Rifle, Portal Capture Fields
REWARD:     2500 FC
THREAT:     Extreme

DESCRIPTION:

The Overwatch Elite Commander is one of the Combine's highest-ranking combat officers, combining tactical genius with experimental suppression technology.

He wears a camouflage suit with purple accents and a black cape with red trim, emphasizing his status and almost theatrical arrogance.

His face is hidden beneath a helmet with a single glowing orange "Eye", which serves not only as a visor but also as a sensor node for the portal system.

The Commander's primary ability is forced teleportation. He creates a yellow portal around the target, instantly transporting them directly in front of him. Cover, distance, and maneuvering become meaningless-the victim is literally delivered to the Commander's barrel.

The Commander rarely pursues enemies. He prefers to let them "Come" to him at the behest of space.

TIPS:

- If a yellow portal appears around you, prepare for instant contact with a pulse rifle.
- The longer he survives, the more perverse the battlefield becomes-he turns it into a personal execution chamber.
