CLONE STORMTROOPER WITH KAMINO


CLASS:      Clone Stormtrooper
METABOLISM: Human Clone, Male
PERCEPTION: Elite Stormroopers of the Galactic Empire
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    E-101 Rifle
REWARD:     800 FC
THREAT:     High

DESCRIPTION:

Elite stormtroopers of the Galactic Empire, embodying the Emperor's iron will and serving to intimidate the populace. This soldier is clad in white ork armor, but is actually human and related to the orks only through the armor. He is part of the Galactic Empire and is allied with Mental. They obtained their armor from the Mental-Altani joint venture on Oratio, where Orks mine ore and Imperial engineers transform it into combat gear. Hence the Ork armor bearing Mental's insignia.

The soldier himself is a clone of one of the successful warriors, Jack Fat. They carry an E-101 rifle, which they use to dispatch enemies at medium and long range.

The armor consists of 18 segments, providing high mobility and effective protection against most types of small arms. Integrated helmet filters protect against chemical and biological weapons and allow breathing in a vacuum. A communications system integrated into the helmet allows for real-time coordination.

These faceless servants of the New Order, through force and brutality, carried the Emperor's will to thousands of star systems across the galaxy. During the Empire's heyday, stormtroopers became the living embodiment of its power, ruthlessness, and fear. With rare exceptions, all stormtroopers wore white armor, distinguishing them from other Imperial military units.

Imperial stormtroopers evolved from the clone troopers of the Grand Army of the Republic. At the end of the Clone Wars, in 19 BBY, the Galactic Republic was reorganized into the First Galactic Empire. The Grand Army was transformed into the Stormtrooper Corps, and the clones were renamed "Stormtroopers".

They are allies of Mental and receive reinforcements, transport, equipment, and other supplies from him. In exchange, they provide their troops for operations that require highly disciplined, massed infantry.

TIPS:

- Their E-101 rifle has a high rate of fire but is inaccurate at long range.
- Their armor is white, making them invisible in the snow.
- Don't try to negotiate.
- If a stormtrooper boards a transport, destroy the transport.
