COMBINE LOYALIST


CLASS:      Combine Loyalist
METABOLISM: Humans
PERCEPTION: Civilian Auxiliary
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Submachine gun (SMG), pistol, stun baton
REWARD:     200 FC
THREAT:     Low

DESCRIPTION:

Combine Loyalists are civilians who voluntarily sided with the Combine, trading safety, rations, and the illusion of stability for loyalty to the regime. They are easily recognized by their black clothing and the overly confident gait of people who were promised "everything will be fine."

Unlike Overwatch soldiers, Loyalists do not wear armor or undergo full combat training. Their weapons are limited to SMGs, pistols, and stun batons, making them more of a support force than true combatants. In direct combat, they are vulnerable and die quicklyâ€”the Combine knows this and doesn't consider it a problem.

However, not all loyalists are alike. Many among them are merely pretending to be regime supporters. These double agents feed information to the Resistance, sabotage orders, and sometimes "accidentally" shoot in the wrong direction. The Combine suspects this, but prefers to turn a blind eye.

TIPS:

- Without armor, they are extremely vulnerable-don't prolong the fight.
- Not every loyalist is truly an enemy, but finding out in combat is a bad idea.
- The stun baton is dangerous up close, keep your distance.