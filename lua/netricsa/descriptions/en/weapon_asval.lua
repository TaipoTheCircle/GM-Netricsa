AS VAL


WEAPON:          AS Val
AMMUNITION:      9x39 mm
ROF:             ~900-960 rounds / min

DESCRIPTION:

The Special Val Automatic Rifle is a silent automatic rifle developed at the Klimov Central Research Institute of Precision Engineering by designers P. Serdyukov and V. Krasnikov in the second half of the 1980s, based on the silent VSS sniper rifle. The Val and VSS are visually similar and 70% identical. Until the 1970s, special forces units of the USSR mainly used modified models of small arms and grenade launchers for general military use, equipped with silencers and using special subsonic ammunition. Examples include the Tishina system based on the AKM and the Kanareyka system based on the AKS74U, as well as the PB and APB pistols. However, such solutions had their drawbacks (for example, a significant increase in the size of pistols with silencers, a sharp decrease in the effective firing range of assault rifles with PBS-1, and its limited service life), Therefore, in parallel, the Central Research Institute of Machine Building, under the leadership of the KGB and the GRU General Staff of the USSR, created special models for narrow-purpose use that could ensure greater secrecy for the actions of special forces. Used in: the Chechen War, the Georgian War, and currently used in the Special Military Operation.

