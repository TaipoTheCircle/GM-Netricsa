DESCRIPTION:

The Hand-held Anti-Tank Grenade Launcher 7 is a Soviet reusable hand-held anti-tank grenade launcher. Designed to combat tanks, self-propelled artillery units, and other armored vehicles, it can be used to destroy manpower in shelters, as well as to combat low-flying, low-speed air targets. The grenade launcher was developed by GSKB-47 (now GNPP Bazalt) and adopted by the Armed Forces of the Soviet Union in 1961. The lead designer was Valentin Konstantinovich Firulin (winner of the Lenin Prize for the creation of this weapon). More than 9 million units were produced. It has been used in virtually all armed conflicts since 1968 and is still in service with the armed forces of many states and countries. Thanks to the emergence of new ammunition, the RPG-7 poses a significant threat to modern weapons and military equipment, which is why it remains in demand in the 21st century.

NOTES:

