FAT BOY FROM THE KRIEGER ARCHIPELAGO


CLASS:      Fat Boy
METABOLISM: Trigen Mutant
PERCEPTION: Straightforward Fighter
SIZE:       7.2 ft
ENDURANCE:  Tall
HOSTILITY:  Aggressive
WEAPONS:    Missile Launcher (integrated), Claws
REWARD:     1500 FC
THREAT:     Tall

DESCRIPTION:

Fat Boys get their name from their gigantic build and heavy stomps, audible tens of meters away. Most specimens are equipped with a missile launcher built into their right arm, which has an infinite supply of ammunition but fires slow-firing missiles. Their chests are exposed to allow ventilation of the glowing implant, their primary vulnerable area. Despite their military uniform (trousers and boots), their intelligence is extremely primitive: they don't avoid obstacles, but literally gnaw their way through them, sometimes dying from their own missiles in narrow passages.

Remarkably, these are the only Trigens to retain the ability to speak. In combat, they bark threats like "You've failed!" and "You're finished!", though their tactics are limited to frontal assaults. They don't understand the concept of death among their own kindâ€”they might scream "Someone's dead!" when witnessing the death of another mutant. They respond to sounds and organize patrols, but become more dangerous in confined spaces due to the impossibility of dodging their missiles.

TIPS:

- Aim for the glowing implant on the chest-normal bullets do little damage to mutated flesh.
- Use maneuverability-slow missiles are easy to avoid in open areas.
- Beware of corridors-their missiles are almost guaranteed to hit their target there.
- There is an unarmed variety.