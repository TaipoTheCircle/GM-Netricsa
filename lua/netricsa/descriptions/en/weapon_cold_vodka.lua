COLD VODKA


WEAPON: Cold Vodka
AMMO:   Bottle Shards
ROF:    ~90-120 bpm

DESCRIPTION:

An improvised melee weapon created by Jack the Loafer after a dubious moment of inspiration in a bar. Hearing stories of the exploits of "Gnaar on his Werebull", he decided to go ahead and simply smashed a bottle of vodka, ignoring the existence of the more "classic Rose".

However, that wasn't the end of it. The weapon was enchanted with enchantments known as the blessings of Mehrunes Dagon and Marka Ragnos. As a result, the bottle acquired an anomalous property-it reforms if not looked at.

It is used in combat in two stages: the first strike is delivered with the full bottle, after which it shatters, transforming into a sharp weapon for subsequent attacks.

NOTES:

- The first strike is heavy and blunt, like a Cucurbito's head.
- After the strike, it transforms into a sharp weapon with slashing damage.
- If unobserved, it regenerates back into a whole bottle.
- Effective at close range.
