SPIDER BABY FROM THE MINECRAFT OVERWORLD


CLASS:      Spider Baby
METABOLISM: Arthropod, Arachnid
PERCEPTION: Neutral mob
SIZE:       1,5 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Bite, Speed, Coordination with siblings
REWARD:     50 FC
THREAT:     Medium

DESCRIPTION:

This small but extremely annoying creature follows its mother wherever she goes. Together with the mother spider and other siblings, they form a mobile spider army, ready to destroy anyone who approaches their matriarch.

As long as their mother is alive, the babies behave like disciplined soldiers. They follow her unquestioningly and attack any target she chooses. Her young attack in groups and surround their prey, using their speed and small size to confuse them.

But the real fun begins when the mother spider dies. The young, deprived of their leader, fall into despair. They begin to "cry", instantly abandon their current attack, and lose all interest in it. From merciless killers, they turn into frightened orphans. In this state, they are aggressive only in response to aggression-if struck, they will defend themselves, but will not attack.

The young do not wander alone, but appear only when accompanied by their mother. Furthermore, one young always sits on the mother's back, making her even more dangerous and protecting the young from the first blows.

TIPS:

- If you don't want to fight a bunch of little creatures, try not to attack the hatchlings until you kill the mother. They might scream and call for help from the others.
- Once you kill the mother, you don't have to finish off the hatchlings-they'll stop being hostile until you touch them. This is a great way to conserve resources.
- However, remember that if you decide to finish them off, they'll defend themselves fiercely.
- Due to their small size and high speed, they're difficult to hit at close range. Use weapons with a wide area of ​​effect or attack from above.
- When fighting a family of spiders, try to destroy the hatchlings first (especially the one on their back) to deprive the mother of her support.
