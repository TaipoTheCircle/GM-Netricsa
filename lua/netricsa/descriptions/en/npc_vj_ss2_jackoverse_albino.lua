YETI


CLASS:      Yeti
METABOLISM: Intelligent Mammal
PERCEPTION: Friendly Giant And Family Man
SIZE:       ~22 ft
ENDURANCE:  High
HOSTILITY:  Low
WEAPONS:    Claws, Strength
REWARD:     300 FC
THREAT:     Medium

DESCRIPTION:

This caring family man lives with his family in the harsh snowy lands and brings home prey every day - mostly boars. He combines physical superiority with a peaceful temperament, preferring to avoid conflict whenever possible.

One day, a man accidentally encountered him and began taking pictures. The Yeti was unafraid and even posed, not realizing the consequences. After the footage appeared in the news, the Yeti was considered a threat, and a hunter was sent to hunt him down. However, upon seeing the Yeti's loving family, the hunter was unable to complete his mission. He contacted the President of the United States and reported that the alarm had been unfounded.

Despite this, Area 51 continues to hunt the Yeti, considering it a rare and potentially dangerous creature. This makes its life and the lives of its family a constant target for intelligence agencies. Every day, the Yeti is forced to check the area for signs of unauthorized presence and guard the perimeter from possible interference by intelligence agencies.

The Yeti spends most of its time foraging for resources in extremely cold temperatures. It spends its evenings with its family, ensuring their safety and comfort. Its top priority is the well-being of its loved ones, and it is willing to do anything to achieve this.

TIPS:

- The Yeti is not aggressive. If you encounter it in the snow, do not panic or try to kill it.
- Do not approach its home or family. That is the only thing that can irritate it.
- If you see it by accident, just leave. It does not stalk people.
- Don't try to take a photo of him. It might attract the attention of Area 51.
- In a fight, the Yeti is only dangerous if his family is in danger. If you don't threaten his loved ones, he won't touch you.
