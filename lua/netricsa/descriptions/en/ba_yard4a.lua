BLACK MESA WAREHOUSE, CONTAINERS


Welcome inside, Sam. It's darker, quieter, and smells as if the warehouse has long since realized how this will end and resigned itself to it.

In the center of the room are two containers. Enormous. One blue, one red. A classic choice. Almost philosophical. I'd say one leads to truth, the other to certain death, but knowing Black Mesa, both options could be true simultaneously.

Is Rosenberg there? It's unknown. Perhaps he's inside. Perhaps he's under the container. Perhaps he's not there at all, and this is another test of your faith in cause and effect. Scientists like to hide in places no sane person would willingly go. And Rosenberg, it seems, has already sacrificed his sanity for the portal.

The containers are guarded by H.E.C.U. soldiers. They were clearly told, "Guard it. What's inside is none of your business. If they ask, shoot." And so they guard it. Questions are not welcome here, nor is the survival of witnesses.

Note: the guards are posted as if there's something truly valuable inside. Or extremely dangerous. And more often than not, in Black Mesa, those are the same thing.

Blue. Red. Two containers. Two possible wrong decisions.

Choose carefully, Sam.

Although... who am I kidding? You'll still open both.