WATER BEAST FROM THE TERRAFORMED PLANET


CLASS:      Water Beast
METABOLISM: Deep-sea predator
PERCEPTION: Ancient subterranean monster
SIZE:       60 ft (Prominent Part)
ENDURANCE:  Extreme
HOSTILITY:  Absolute
WEAPONS:    Tentacles, rock throws, stalactites
REWARD:     5000 FC
THREAT:     Absolute

DESCRIPTION:

No one knows how long this creature has spent in the underground caves, but judging by its size and aggressiveness, it's been there since the time of the dinosaurs, and it has never complained about a lack of companions.

In appearance, the beast is a hybrid of an eel, an octopus, and a dunkleosteus.

In combat, the beast is immobile-it's bound to its underwater lair and can't leave the water. But this doesn't make it any less dangerous. Its primary attack is a forward lunge, attempting to devour its target. Between attacks, the beast lashes out with its tentacles, rips rocks from the ground and throws them, and tears stalactites from the ceiling. The last is the most deadly.

With each successful explosion, the beast will bleed more and more until it perishes completely.

TIPS:

- Don't stand still.
- There's a pause between attacks-use it to reload and catch your breath.
- Use Explosives.
