FESTIVE CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Festive Creeper
METABOLISM: Plant-Gunpowder
PERCEPTION: Hostile mob, Dynamite Thrower
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  Extremely high
WEAPONS:    TNT Blocks
REWARD:     200 FC
THREAT:     Medium

DESCRIPTION:

This is what happens when a regular creeper decides that blowing itself up on the spot is boring and it's much more fun to hurl explosives from a distance.

Unlike its classic green counterpart, which slinks and hisses, the Festive Creeper acts like a cowardly pyromaniac. It appears at night, like regular creepers, but instead of ramming an enemy, it begins throwing blocks of dynamite (TNT). It constantly backs away, trying to maintain its distance.

The explosions of these TNTs are equivalent in power to a ghast's fireballs. They don't cause much damage, but if you miss a couple of hits in a row, your health will plummet. Sometimes, when thrown, the TNTs ignite the ground, adding to the problem.

If a Festive Creeper is struck by lightning, it becomes charged. In this mode, the force of its TNT explosion increases to that of a regular creeper. Staying close to one is highly discouraged.

TIPS:

- The Festive Creeper doesn't have a melee weapon, so the best tactic is to run up to it and hit it with a sword. It won't be able to throw dynamite at point-blank range.
- It is immune to fire, meaning setting it on fire is useless. Lava won't do any damage either.
- His TNTs don't fly very fast, so you can dodge them if you keep moving.
- Don't let him get away. The further he is from you, the more time he has to throw them.
- If you see a charged Festive creeper, especially near buildings, kill it as quickly as possible or run away. Explosions with the force of a regular creeper can destroy all your creations.
