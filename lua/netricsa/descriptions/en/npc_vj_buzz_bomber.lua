BUZZ-BOMBER FROM THE MOBIUS


CLASS:      Buzz-Bomber
METABOLISM: Animal-Ion-Fuel
PERCEPTION: Tactical Bomber
SIZE:       4 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Rail Detonator, Hull
REWARD:     400 FC
THREAT:     Medium

DESCRIPTION:

When Dr. Eggman decided to conquer the skies, he spent a long time choosing a prototype. Regular Wasps were too vicious, Dragonflies were too fast, and Mosquitoes were too small. Ultimately, the choice fell on the Shrew Wasp. Thus, the Buzz-Bomber was born.

After firing, it flies away, only to return from a different direction after a short delay.

TIPS:

- Attack while hovering. While he is laying down the sting.
