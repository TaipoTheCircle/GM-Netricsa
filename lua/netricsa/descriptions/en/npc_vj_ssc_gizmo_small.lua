DESCRIPTION:

A strange organism originating from marshy wastelands in the system of Rigil Kentaurus. They live in large groups and have a suicidal instinct for protecting their territory. In all-devastating havoc, all individuals try to leap at the intruder and explode, by over inflating themselves. Upon exploding, toxic slime contained in their intestines splashes the target.

The pinnacle of Mental's miniaturization technology are the microscopic hoppers. These microscopic organisms lack everything but a basic instinct: detect a target, reach it, and detonate. They move in incredibly fast, flea-like hops and are virtually invisible in the air and dust. Each one carries a droplet of concentrated neurotoxin, which is injected upon contact or sprayed out in a mini-explosion. Individually, they are almost harmless, but when released in a swarm of 60, they create a deadly fog capable of covering and poisoning everything within a ten-meter radius in seconds. They never attack individuallyâ€”they always swarm.

