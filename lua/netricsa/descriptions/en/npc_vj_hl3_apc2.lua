COMBINE APC CRAWLER


CLASS:      Combine Armored Assault Platform
METABOLISM: Mechanized Combat Vehicle
PERCEPTION: Tactical sensors, targeting system, remote control
SIZE:       26 ft
ENDURANCE:  Very high (reinforced armor, redistributed mass)
HOSTILITY:  Extreme
WEAPONS:    Pulse cannon, guided missiles
REWARD:     2000 FC
THREAT:     Critical

DESCRIPTION:

The Combine APC CRAWLER is a modified version of the standard Combine APC, designed for operations in areas where wheeled vehicles are ineffective. Swamps, ruins, rubble, dense forests, and destroyed city blocksâ€”these are the places where the Crawler replaces the standard APC.

The main design difference is the installation of track modules over the wheels, significantly increasing cross-country ability and stability on unstable surfaces. Due to the change in balance and tactical role, the vehicle's fenders have been completely removed, lowering the vehicle's silhouette and simplifying maintenance in the field.

The Crawler moves slower than the standard APC, but compensates for this with relentless forward momentum.

The armament has been changed, removing the missiles.

