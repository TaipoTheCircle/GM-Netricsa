MACHETE


WEAPON: Machete
AMMO:   None (melee weapon)
ROF:    ~75 swings/min

DESCRIPTION:

The machete is a long, broad single-edge knife typically used as an agricultural tool in jungle and tropical settings, and also repurposed as a melee weapon. Versatile in function - chopping vegetation, preparing food, or dealing lethal blows when needed.

NOTES:

- No ammunition needed - purely melee.
- Excellent in close combat, especially vs unarmed or lightly armored enemies.
- Useful both for survival tasks and combat.
- Light weight and large blade make it effective but can be cumbersome in tight spaces.