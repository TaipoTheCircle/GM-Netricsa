COLT ANACONDA


WEAPON: Colt Anaconda
AMMO:   .44 Magnum
ROF:    ~25-30 rounds / min

DESCRIPTION:

The Colt Anaconda is a large-frame, heavy-caliber revolver engineered for hunting and heavy-duty use. Its robust stainless-steel construction and six-round cylinder deliver exceptional stopping power and stable shooting characteristics. In game terms it functions as a high-punch, low-capacity sidearm for situations that require one-shot lethality.

NOTES:

- Rugged and reliable; heavy construction ensures durability.
- Best used as a precision, high-impact tool or as a last-resort heavy sidearm.