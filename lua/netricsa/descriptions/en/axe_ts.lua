FIRE AXE


WEAPON: Fire Axe
AMMO:   None (melee)
ROF:    ~60 hits/min

DESCRIPTION:

The Fire Axe is a crude but reliable melee weapon. Its sharp blade edge and massive back allow for powerful slashing blows, breaking flesh and destroying wooden barriers. Easy to use and effective when paired with quick movement or ambushes: one or two precise strikes can neutralize weaker opponents or seriously damage larger foes. Fun fact: In the first part of "The Suffering," a fire axe lies next to a decomposing corpse in the middle of the hedge maze in the asylum. This is a reference to Stephen King's book and film "The Shining."

NOTES:

- High damage on direct hits; good for breaking up dense groups of enemies at close range.
- Can destroy wooden objects and some cover in the environment.
- Simple mechanics - low difficulty to use, but requires close combat.