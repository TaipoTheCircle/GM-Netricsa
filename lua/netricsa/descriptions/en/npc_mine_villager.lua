VILLAGER FROM THE MINECRAFT OVERWORLD


CLASS:      Intelligent Passive Humanoid
METABOLISM: Warm-Blooded Humanoid
PERCEPTION: Standard Vision and Hearing
SIZE:       6,2
ENDURANCE:  Low
HOSTILITY:  Very Low
WEAPONS:    None
REWARD:     100 FC
THREAT:     Minimal

DESCRIPTION:

Villagers are the primary representatives of intelligent civilization in Minecraft's Overworld. These humanoids are recognizable by their large noses, furrowed brows, and their invariable habit of crossing their arms, as if the world owes them something. They communicate using characteristic sounds, which some believe are either a simplified form of speech or passive aggression.

Villagers live in villages where they lead a measured life: they work, trade, sleep, and panic at the sight of zombies. Each villager can have a profession determined by their work block, which forms the basis of the village economy. The main currency is emeralds, which are significantly more valuable to villagers than to the rest of the world.

Villagers are incapable of combat and rely entirely on iron golems, which they simultaneously fear and consider a hero.

TIPS:

- Actively trade and change professions.
- Avoid direct conflict.
- Fear hostile mobs.
- A villager can forgive, but never forget.