VODKA-BAZOOKA


WEAPON: Vodka-Bazooka
AMMO:   Full-size vodka bottles
ROF:    1 round per minute

DESCRIPTION:

The Vodka-Bazooka is a giant bottle of vodka that can hold other bottles of vodka. It works on the principle of a bottle within a bottle, a bottle within the enemy.

When fired, the inner bottle ejects with devastating force, shattering on the target and creating a zone of total vodka destruction. At close range, the effect is so deadly that it's not recommended to fire at all, as everyone will die: enemies, allies, and moral values.

The weapon was developed by an unknown, drunken engineer in the 19th century, nicknamed "Jack the Loafer", who wanted to resolve all conflicts once and for all.

NOTES:

- Deadly effective at close range. So much so that close range may never exist. - When hit, the bottle explodes in a cloud of glass and alcohol.
- Reloading occurs by inserting a new bottle into the larger one.
- High risk of self-destruction.
- Weapons are prohibited in all civilized dimensions, including those where civilization has ceased to exist.
