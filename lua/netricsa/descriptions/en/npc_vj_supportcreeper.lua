SUPPORT CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Support Creeper
METABOLISM: Plant-Gunpowder
PERCEPTION: Neutral
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  Neutral
WEAPONS:    Buffs for allies
REWARD:     120 FC
THREAT:     Medium

DESCRIPTION:

This is a yellow, more peaceful version of the classic Creeper, found in dark areas of the Overworld.

The Support Creeper is distinguished from the standard Creeper by its more yellow skin tone and single-pixel mouth, giving it an almost harmless appearance. But this appearance is deceiving.

Unlike its green counterpart, which sneaks up and explodes, the Support Creeper is a pure support. Its main purpose is to buff other hostile mobs. It finds the nearest monster, follows it, and provides powerful buffs while running away.

Creeper Buff: If the Support Creeper buffs another creeper, it "charges" it with electricity (like a lightning strike), granting it Fireproof. It also grants itself Fireproof and Speed ​​I.

Buffing any other monsters: It grants all other monsters (zombies, skeletons, etc.) three effects: Fireproof, Strength I, and Speed ​​I. It only grants itself Speed ​​I.

If its target dies, it searches for another. If there are no targets left, it simply flees.

Despite its neutral nature, the Support Creeper can be dangerous.

If it drops below half health, it stops running and begins behaving like a normal creeper-hissing and exploding.

Like a normal creeper, it can become charged if struck by lightning. This increases all its effects to level II, making it and its allies even more deadly.

Killing a support creeper rarely yields a Yellow Mystery Egg. If hatched, it will hatch into a tamable baby support creeper. As it matures, this tamed creeper will provide useful buffs to you, not to the mobs.

TIPS:

- Kill it first. In any battle with a group of mobs, if you see a yellow creeper, it should be your number one priority. Killing it deprives the entire horde of its buffs.
- Keep your distance. If you damage it but don't kill it, it will charge at you and explode.
- Use a ranged weapon (bow / crossbow). This is the safest way to kill him without letting him get close.
- Don't let him escape. He's fast, so try to corner him or use slowing effects (webs, slow potions).
- If you see a charged support creeper, run. His buffs are so powerful that a regular zombie can become a mini-boss.