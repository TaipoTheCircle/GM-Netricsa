FIREFLY FROM THE KRIEGER ARCHIPELAGO


CLASS:      Firefly, Bioluminescent Insect
METABOLISM: Insect
PERCEPTION: Photoreception, basic vision
SIZE:       0,06 ft
ENDURANCE:  Very low
HOSTILITY:  None
WEAPONS:    None
REWARD:     1 FC
THREAT:     None

DESCRIPTION:

Small flying insects that inhabit damp and dark areas of the archipelago (jungles, caves, coastlines at night). They possess bioluminescence, emitting a soft light in one of three colors: green (the most common), yellow, and blue. They pose no threat. Their presence serves to create atmosphere and can sometimes illuminate paths or hiding places in the dark.

TIPS:

- They are not enemies or resources. Destroying them is pointless and doesn't provide much benefit.