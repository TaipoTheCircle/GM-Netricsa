BACKROOMS, WELCOME TO THE LIMINAL SPACE


Sam, I don't know how to explain this. My sensors are going crazy. One second, we were in the reactor, smelling of ozone, iron, and sweat. The next, we're here. Where is "here"? I'd say "nowhere," but that's too simple. This is some kind of nonlinear space. A utility room. Vast and endless, where all the corridors are similar, but none are the same.

Yellow wallpaper. Damp carpets underfoot, soaked with something but not water. Not drinkable. I don't even recommend trying. Lights on the ceiling. Fluorescent. They flicker-erratically, unpredictably-and hum.

Visually, everything is the same. But at the same time, no two rooms are alike. A pattern? There is no pattern. The space is changing. You walk in a straight line and end up back where you started. You follow the same route and end up in a different set of rooms. Compasses don't work. GPS is silent. Radio communication is distorted, turning your voice into a hiss. Even you and I... I can hear you, but I feel like the signal is shaky. Like we're talking through an old telephone during a thunderstorm.

This is the main entrance to the Backrooms-the place from which no one returns. Or they return, but changed.

There have been no reports of contact with people inside. No one has ever encountered anyone. But there were people here. Lots of them. They were dying of thirst, hunger, and whatever happens to your mind when surrounded by endless yellow walls and humming lights. Sensory deprivation, isolation, lack of landmarks. Psychosis sets in quickly. And then-silence.

But there are no bodies. No one has ever found bodies. They just disappear.

I don't know. Seriously. For the first time, I don't have a plan.

Navigation is impossible. You can just walk. Anywhere. Until you get out.

There is a way out. It exists. But how to find it is unknown. Sometimes it appears by chance. Sometimes - if you stop looking. Sometimes - if you believe it doesn't exist.

Supplies? We have what we have. Little water. Almost no food. There's nothing to trade here. There's nothing here but yellow wallpaper and a hum.

Psychological stability. That's the main thing. Don't go crazy. Talk to me. I'll be with you as long as the signal holds. But if we lose each other... don't panic. Keep moving. Wait. There is a way out.

We'll get out, Sam. I don't know how, but we'll get out. Because you're a man who survived the Resonance Cascade, fought Mental, and walked through a space where reality flows like water. It's just yellow wallpaper. Nothing new. Believe me.
