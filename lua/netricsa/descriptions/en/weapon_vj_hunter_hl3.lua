MINISTRIDER PISTOL


WEAPON: MiniStrider Pistol
AMMO:   Pulse Rounds
ROF:    ~300 RPM
WEIGHT: 1,3 kg

DESCRIPTION:

A compact pulse weapon developed using Strider technology and adapted for a personal pistol. Its appearance is virtually identical to a standard firearm, allowing it to be used discreetly until the shot is fired.

The weapon uses compact pulse batteries and is designed for 7 shots before requiring a reload. Despite its compact size, each pulse round has moderate penetration, sufficient for effective damage.

NOTES:

- Magazine capacity: 7 pulse rounds.
- Effective at close to medium ranges.
- Compatible with Combine simplified pulse power supplies.
- Used by Mini-Metrocops and Autonomous Support Units.