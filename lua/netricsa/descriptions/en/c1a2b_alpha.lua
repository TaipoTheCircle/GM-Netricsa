BLACK MESA OFFICES, MENTAL IS BACK AGAIN


We're still in the offices, but the atmosphere has begun to resemble a supermarket during rush hour, only instead of customers, there are heavily armed guys on both sides. On one side are the H.E.C.U.s, who have already established themselves at the top and regard the stairs as their personal property. On the other, a fresh addition to the menu: Mental's powers. Yes, they're here. Finally.

Their encounter with the Marines hasn't escalated into a full-scale war yet, but the tension hangs in the air thicker than the smell of burnt wiring. Mental, it seems, wants to get up there too. Or maybe he just decided to grab a bite. Because we're now located exactly between the freezer and the cafeteria.

Freezer.

Cold. Racks of frozen food that no one will defrost now. The perfect place for an ambush, if you're Mental. The perfect place for an ambush if you're H.E.C.U. The perfect place to get shot in the back if you're Sam. Heavy doors, narrow passages, echoing footsteps. If gunfire starts here, the only survivors will be the one who gets out first. Or the one who never came in at all.

Cafeteria.

A typical cafeteria. But without tables, a counter where men in white caps once stood. Now, Mental's Orc Destroyer probably sits there with a plasma pistol at the ready, waiting for someone to poke their nose in. There's a second exit from the cafeteria. And that exit leads to a staircase. A staircase that goes up. Where the Marines are.

Don't go in first. Find cover at the edge of the freezer and the cafeteria. Keep an eye on them. When Mental's forces attack the stairs (and they will, they have orders), the Marines will respond. At that point, slip through. Don't be a hero, don't try to shoot everyone. Just get to the stairs and up.

There will be the next stage of this endless elevator to hell.
