ZOMBIE HEADCRAB, BLOOD RUNNER (ENHANCED)


CLASS:      Zombie Headcrab, Blood Runner (Enhanced)
METABOLISM: Human Host + Parasitic Headcrab Of Xen
PERCEPTION: Zombified Entity
SIZE:       6 ft
ENDURANCE:  Medium-High
HOSTILITY:  Extreme
WEAPONS:    Claws, Bite, Speed
REWARD:     600 FC
THREAT:     Very High

DESCRIPTION:

This is a special, Nevada variety of zombie headcrab, which has become widespread in the driest state in America – Nevada, at Delta Base. The Arizona desert and heat haven't killed these creatures, but only made them angrier, faster, and more deadly.

The Blood Runner's appearance differs from that of its normal counterpart. It is clad in dark, tattered clothing, completely coated in dried blood. Its body is distorted, its muscles unnaturally elongated, adapted for long and fast runs over rough terrain.

In combat, the Blood Runner is even more dangerous than usual. It doesn't just run-it sprints, avoiding obstacles and emerging from the darkness at the most unexpected moments.

When a Blood Runner is killed in the head, its head is torn from its body.

TIPS:

- The Blood Runner is faster than usual. Don't try to run away-it will catch up.
- Use narrow passages and traps.
- If killed by a headshot, the head is torn off.
- In dark clothing, it is difficult to spot in the night.
- Close combat is extremely risky. Keep your distance and use the shotgun at point blank range when he runs.
