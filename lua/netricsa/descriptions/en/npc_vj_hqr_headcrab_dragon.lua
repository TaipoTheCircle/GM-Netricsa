HALF-QUAKE SHOW HEADCRAB DRAGON


CLASS:      HALF-QUAKE Show Headcrab, Dragon
METABOLISM: Parasitic xenofauna
PERCEPTION: Biological weapon
SIZE:       ~2 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Bite, permanent attachment
REWARD:     200
THREAT:     Medium

DESCRIPTION:

When the producers of HALF-QUAKE decided to launch a medieval MMORPG-style zone, they faced a problem: the local bestiary wasn't up to par. Dragons were urgently needed, but the budget had already been spent on latex and paint for the gray vortintons.

They found a solution in their own back room. Regular headcrabs, already modified for the Dark World, were removed from storage and sent for further processing. Each one had the word "Dragon" carved into the top of their heads. Apparently, it was carved with a red-hot nail, so the letters remained.

After that, the sadistic development team spent several weeks training the creatures to pronounce a single phrase: "I am a Dragon".

Now these creatures guard quest locations, entrances to toilet dungeons, and other important locations.

TIPS:

- Don't try to engage in dialogue. He only has one phrase, and he's already said it.
- In a pack, they scream in unison. It sounds like a dragon party in a nursing home. Run.
