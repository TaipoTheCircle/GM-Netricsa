ROCKET CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Rocket Creeper
METABOLISM: Plant-Chemical
PERCEPTION: Plant-Gunpowder Creature
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  Extremely High
WEAPONS:    Jump and Follow-Up Self-Detonation
REWARD:     160 FC
THREAT:     Medium

DESCRIPTION:

This is proof that nature loves to experiment with self-detonation techniques. While a regular creeper simply approaches and explodes, a party creeper throws explosives, the best way to inflict damage is to spectacularly land head-down on its victim.

The Rocket Creeper's behavior is unique. Like a regular creeper, it pursues its prey. But when the distance closes, it doesn't hiss or run away. Instead, it leaps into the air and then falls headfirst, straight at its target. The explosion shatters everything around it. The explosion is as powerful as a regular creeper's, enough to kill an unarmored target.

If a Rocket Creeper is struck by lightning, it becomes charged. In this mode, the explosion's power doubles, just like a charged creeper.

TIPS:

- Can sneak up unnoticed.
- The Rocket Creeper explodes only at the end of its jump. Dodging at the last moment can avoid most of the damage.
- Don't stand still. While it's flying, you have time to move to the side.
- A charged Rocket Creeper is extremely dangerous. Its explosion can destroy even fortified structures. If you see it, it's best to stay away.
- Like all creepers, it doesn't die from sunlight.
