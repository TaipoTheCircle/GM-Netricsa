PURE VESSEL FROM THE ABYSS


CLASS:      Pure Vessel
METABOLISM: Magical, Void
PERCEPTION: Perfect Vessel, Mindless
SIZE:       8,2 ft
ENDURANCE:  Extreme
HOSTILITY:  Absolute
WEAPONS:    Nail, Shadow Attacks, Void Spears
REWARD:     1500 FC
THREAT:     Absolute

DESCRIPTION:

This is the version of the Vessel (Hollow Knight) as it was many years ago - at its peak, before the corruption, before the Radiance began to corrupt it from within. It is an "absolutely pure" specimen, a perfect shell that has never known emotion or doubt.

Outwardly, the Pure Vessel is strikingly different from its broken, "adult" self. He is taller, more toned, and moves smoothly, yet with an unnatural, mechanical grace. His cloak is intact and almost floating, and the movements of his Nail are lightning-fast.

The Pure Vessel demonstrates the Pale King's original plan for containing the Radiance. The Vessel was to become an "empty" body, devoid of mind, voice, and will. But even the Pure Vessel shows signs of "impurity" in combat -he breathes, and his movements, though lightning-fast, sometimes contain hints of barely restrained rage.

He is very fast and aggressive, and uses a variety of moves:

Rapid dashes with a slash of Nails. He covers almost half the arena in a second.

Triple slashing attacks in close combat.

Throwing spears of the Void. He creates spears from the void that pierce the ground and the arena, then releases a sphere (similar to Reaper's sphere) that expands and destroys platforms.

Ceiling attack: He leaps upward, then slams his Nail into the ground, creating a shockwave, or hovers and dives down on the opponent.

Control: He can block attacks with lightning speed, instantly switching to his own.

TIPS:

- Don't panic. His attacks are very fast, but predictable. The hardest part is getting used to the speed.
- Spear attack: When he freezes in midair and beams of light start erupting from the ground, simply stand between them. The expanding sphere attack requires jumping through it.
- When he blocks, he will almost always respond with an instant counterattack.
- He feels no pain and knows no mercy. He is the perfect weapon.