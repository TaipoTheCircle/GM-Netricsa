COMBINE OVERWATCH ROCKET SOLDIER


CLASS:      Combine Overwatch Rocket Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Anti-vehicle and heavy target suppression
SIZE:       9,5 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Combine RPG, secondary melee weapon
REWARD:     700 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Rocket Soldier is a heavy fire unit designed to destroy vehicles, fortifications, and high-risk targets. His appearance is easily confused with that of a shotgunner: similar armor, a massive silhouette, and a stance. This mistake is often fatal.

The Soldier's primary weapon is an Combine Rocket Launcher, which he carries at all times and uses without hesitation. Unlike anti-tank soldiers, he doesn't wait for the perfect target: his rockets are used aggressively, clearing cover, breaching walls, and suppressing enemy concentrations.

He often works in tandem with regular soldiers, who protect him while he prepares to fire.

Although his armor isn't top-notch, one successful launch can instantly turn the tide of battle.

TIPS:

- Don't confuse him with the Combine shotgunner.
- The Rocket Soldier is especially dangerous in confined spaces.
- Use cover that can withstand the blast, not just block your view.