PLANET OF THE THREE CAPTAINS


PLANET:       Planet of the Three Captains
TYPE:         Terrestrial planet, Memorial World
CLIMATE:      Artificially maintained, breathable
MOONS:        Unknown
TERRAIN:      Originally lifeless rocky; equipped with atmosphere generators
LOCATION:     Unknown star system in the Milky Way Galaxy
NATIVE RACES: None (tourist planet)
ALLIANCE:     Intergalactic Memorial Complex
LOCATIONS:    Sixty-meter statue of the Three Captains, Cosmonautics Museum

DESCRIPTION:

A unique memorial world named in honor of three legendary space heroes who accomplished many remarkable feats and discoveries during their travels across the galaxy. Initially, it was an unnamed, uninhabited planet, completely uninhabitable due to the lack of an atmosphere.

To perpetuate the memory of these famous captains, the planet was specially equipped with powerful generators that produce oxygen and create breathable conditions, making it accessible to tourists from all corners of the galaxy.

The planet's main attraction and symbol is a colossal sixty-meter-tall stone statue depicting the Three Captains. This monument is visible from afar and serves as a landmark for all those arriving on the planet.

Next to the statue is the Cosmonautics Museum—a unique collection of artifacts, documents, and evidence from the era of great space exploration. The museum was built with voluntary donations collected from eighty planets in the star system, making it a truly galactic landmark.

KEY LOCATIONS:

- The sixty-meter-tall statue of the Three Captains: The planet's main monument, depicting three legendary space heroes, serves as a landmark and symbol for the planet.
- Cosmonautics Museum: A unique collection of artifacts and documents dedicated to the era of great space discoveries. Built with donations from the inhabitants of eighty planets.
