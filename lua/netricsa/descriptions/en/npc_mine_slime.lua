SMALL SLIME FROM THE MINECRAFT OVERWORLD


CLASS:      Small Slime
PERCEPTION: Slime Monster
METABOLISM: Amorphous Organism
SIZE:       ~3,2 ft
ENDURANCE:  Average
HOSTILITY:  Average
WEAPONS:    Jumping, Crash
REWARD:     10 FC
THREAT:     Average

DESCRIPTION:

The Small Slime is an amorphous, slimy creature that seems to have no sense of personal space. It leaps, hurls itself at anything living, and sometimes splits into smaller pieces when damaged, creating even more annoying copies.

TIPS:

- Smaller Slimes pose no threat on their own, but in a group they can cause chaos.