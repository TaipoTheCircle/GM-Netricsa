BERETTA M9 PISTOL


Weapon:       Beretta M9
Ammunition:   9x19 mm
ROF:          15 rounds / min

DESCRIPTION:

The Beretta M9 pistol is a self-loading pistol chambered for the 9×19 mm Parabellum cartridge, a version of the Italian Beretta 92F pistol for the US armed forces. It is manufactured in the US by a subsidiary of Beretta. It differs from the Italian-made 92F series pistols in its simplified and shorter production cycle, automated production of a number of parts that are handmade by master gunsmiths in Italy, and a stamped inscription on the left side of the steel slide “U.S. 9mm M9-P.BERRETTA” on early models or “BERETTA U.S.A. CORP. ACKK. MD.-MADE IN USA” on later models. Since 1986, the M12 holster from Bianchi International has been used as the standard hip holster for the pistol. It has been used in almost all US wars since 1989. It is popular with gangsters. It is also a civilian weapon.

