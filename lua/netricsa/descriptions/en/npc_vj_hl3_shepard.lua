ADRIAN SHEPARD, CORPORAL, H.E.C.U.


CLASS:      Corporal, US Marine Corps Special Operations Unit (H.E.C.U.)
METABOLISM: Human, Male, trained combatant
PERCEPTION: Tactical skills, vision + radio
HEIGHT:     6,2 ft
ENDURANCE:  High
HOSTILITY:  Medium-high
WEAPONS:    MP5, SMG1
REWARD:     800 FC
THREAT:     Medium-high

DESCRIPTION:

Adrian Shepard is a US Marine Corporal and a member of the elite Hazardous Environment Combat Unit (H.E.C.U.), assigned to the Black Mesa research facility, where an unexpected incident involving aliens and a breach in the space-energy barriers has erupted.

He is part of a landing party sent first to eliminate the alien threat, then to cover up the incident and "neutralize witnesses." However, after his transport helicopter is shot down, Shepard finds himself separated from his team and forced to fight for survival in the escalating chaos.

He has standard Marine gear: body armor, gas mask, night vision goggles, and a H.E.C.U. weapon, making him a dangerous opponent for both Black Mesa and Black Ops Special Forces forces, as well as other forces that emerge during the events.

TIPS:

- His combat experience makes him dangerous at close range, but without heavy equipment, he is vulnerable.