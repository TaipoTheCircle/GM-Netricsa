ALBINO CAVE RAPTOR FROM THE TERRAFORMED PLANET


CLASS:       Albino Cave Raptor
METABOLISM:  Predator, Theropod
PERCEPTION:  Subterranean Hunter
SIZE:        ~8,2 ft
ENDURANCE:   Average
HOSTILITY:   High
WEAPONS:     Claws, teeth, dark adaptation
REWARD:      500 FC
THREAT:      Average

DESCRIPTION:

This species is the result of evolution in conditions of complete isolation from sunlight. Thousands of generations living in the pitch-dark caves have taken their toll: these creatures have lost their pigmentation and become albinos.

The cave raptor is distinguished from its land-dwelling relatives by its pale white skin with barely noticeable stripes. Its eyes glow an ominous red in the dark.

Unlike regular raptors, albinos are active only in dark places and never emerge into the sunlight on their own.

TIPS:

- A knife is the best weapon in tight spaces. One hit kills one.
- Red eyes give away their location.
