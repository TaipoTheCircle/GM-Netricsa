RED TROPICAL PIG FROM THE MINECRAFT OVERWORLD


CLASS:      Red Tropical Pig
METABOLISM: Warm-blooded mammal
PERCEPTION: Wild / Domestic Animal
SIZE:       3 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     25 FC
THREAT:     Low

DESCRIPTION:

Unlike their classic pink counterparts, these pigs have adapted to life in hot regions and have developed a characteristic red-red coat with tan markings, which helps them blend in with the reddish soil of savannas and deserts.

Tropical pigs are found exclusively in biomes with high temperatures.

In behavior, these are still the same peaceful pigs: they roam in groups of 2-4, munch on grass, are not aggressive, and prefer to quickly retreat when damaged.

Like regular pigs, tropical pigs are easy to breed (carrots, potatoes, beets), can be saddled, and controlled with a carrot-tipped fishing rod.

Riding a tropical pig in hot biomes is a great way to avoid wasting resources on walking.

TIPS:

- Look for them in savannas and jungles-they are most common there.
- Breed them with carrots, potatoes, or beets for a constant food source in hot regions.
- A saddled tropical pig is the best transport through the desert if a horse is unavailable.
- When crossing a tropical pig with other varieties, the piglets can have any color of the parents.
