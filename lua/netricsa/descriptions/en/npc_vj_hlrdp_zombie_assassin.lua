Y.S.M. ZOMBIE HEADCRAB FEMALE ASSASSIN, COMMON


CLASS:      Y.S.M. Zombie Headcrab Female Assassin, Common
METABOLISM: Human Host + Parasitic Headcrab Of Xen
PERCEPTION: Zombified Entity, Former Elite Operative
SIZE:       5,6 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Claws, Bite, Stealth Grab
REWARD:     700 FC
THREAT:     Very High

DESCRIPTION:

This is an elite operative of the private military company Y.S.M., whom Headcrab encountered. Her latex suit retained the invisibility technology, and the zombie headcrab learned to use it instinctively. She becomes almost completely invisible as she moves through the corridors of Base Delta. Her acrobatic training remains intact-she runs just as fast.

Only a faint shimmer gives her away. When she attacks, her invisibility fades: she becomes visible just long enough to sink her claws into her victim, then disappears back into the darkness. A headshot detaches the victim's head.

TIPS:

- She becomes visible when attacking.
- A shotgun with a spread is more effective than a precision weapon-it's easier to hit the shimmering silhouette.
- She has no armor, just latex. One or two hits and she goes down. But hitting her is a challenge.
- She's especially dangerous in a pack with other zombies.
