BLACK MESA WAREHOUSE, TV LOVER


Sam, you're still in the Black Mesa warehouse-but now it's pure surrealism. In the center of the room, among stacks of rusty crates and stacked televisions, stands a lone vortigaunt.

And no, he's not waiting for the evening news-he's studying this mountain of technology. The TVs are not connected, the screens are dead, but Vortigaunt seems to see something of his own in them. Maybe an echo of other worlds? Or maybe he's just having fun.

Your goal is ventilation upstairs. The way there is obvious... but, as always, there is one THING. And that "BUT" is fat, red, and damn explosive.

There are explosive barrels next to the vortigaunt, neatly between it and the stairs to the ventilation. Two pieces. Just one spark is enough - and this whole TV parade will turn into a firework display.