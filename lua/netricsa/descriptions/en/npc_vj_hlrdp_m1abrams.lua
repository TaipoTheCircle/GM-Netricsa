Y.S.M. M1 ABRAMS


CLASS:      Y.S.M. M1 Abrams
METABOLISM: Mechanical, Mercenary-Controlled
PERCEPTION: Medium Armored Vehicle, Infantry Support
SIZE:       15,4 ft
ENDURANCE:  Medium
HOSTILITY:  Extreme
WEAPONS:    Missile Launcher
REWARD:     5000 FC
THREAT:     Very High

DESCRIPTION:

This is a lighter, smaller version of the famous tank, used by the Y.S.M. for rapid raids and infantry support at Delta Base. Unlike its heavy, unwieldy original, this modification sacrifices armor and size for speed and mobility.

The vehicle's appearance is distinctive: dark gray camouflage with bright yellow stripes on the sides and turret. Its smaller size allows it to maneuver in tight street spaces and even pass through areas where a regular tank would get stuck. The armor is thinner-it's designed to protect against small arms and light explosives, but a direct hit from an RPG or anti-tank missile will reduce the vehicle to scrap metal.

The Y.S.M. Abrams fires more frequently, launching a missile every few seconds. The missiles have good accuracy and blast radius, but less armor-piercing power than the original model.

They move fast, shoot accurately, and can change position in seconds.

TIPS:

- This version is smaller and faster, but its armor is weaker. One or two well-aimed RPG shots can disable it. 
- Use cover-the missiles are coming in fast, and if you hesitate, the next one will arrive in a couple of seconds.
- Don't try to attack head-on. Go for the flank or the rear.