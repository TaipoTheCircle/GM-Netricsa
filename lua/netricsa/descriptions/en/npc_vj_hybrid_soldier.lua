COMBINE OVERWATCH HYBRID SOLDIER


CLASS:      Combine Overwatch Hybrid Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Versatile Combine combat unit
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Very High
WEAPONS:    AR2 Pulse Rifle, SMG, Grenades, Stun-Stick
REWARD:     600 FC
THREAT:     Medium-High

DESCRIPTION:

The Overwatch Hybrid Soldier is a standard blue-visor Combine soldier, enhanced with additional armor and equipment, making him a versatile fighter for a wide range of environments. Unlike elite units, he doesn't specialize in one type of combat, but is consistently dangerous in any.

His armor is partly decorative: reinforced plates, stripes, and psychological warfare elements create the appearance of a heavier, more "important" soldier than he appears on paper. However, his protection is genuinely improved compared to standard Overwatch soldiers.

A key feature is the helmet-mounted night vision device. It functions not only as a night vision device but also as binoculars, allowing the soldier to observe, mark targets, and adjust fire without air support. In fog, night, or polar darkness, such soldiers become especially dangerous.

The Stun-Stick is rarely used, but is intended for suppression, capture, and demonstrative brutality. The Combine often deploys hybrid soldiers for clearing missions, patrols, and population control operations, which require not only firepower but also demonstrative force.

TIPS:

- Don't underestimate him because of the blue visor - his armor and equipment make him more dangerous than a standard soldier.
- He sees better in the dark than you do.
- Destroy him from a distance, preventing him from getting close enough to use grenades.