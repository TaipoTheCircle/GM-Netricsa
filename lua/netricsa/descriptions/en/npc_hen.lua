HEN FROM ZENOZOIK


CLASS:      Hen
METABOLISM: Avian
PERCEPTION: Basic vision, sensitivity to sudden movements
SIZE:       1,5 ft
ENDURANCE:  Extremely low
HOSTILITY:  Virtually absent (panicked flight)
WEAPONS:    Beak
REWARD:     2 FC
THREAT:     Negligibly low

DESCRIPTION:

A wild bird common in settlements and on the outskirts of Zenozoik. Unlike crows, chickens are distinguished by extremely low intelligence and a complete lack of fighting ability. Their behavior is chaotic flight at any threat. They are famous for being the object of a peculiar "ritual" or act of brute force in the world of Zenozoik - their heads are often torn off. They don't gather in aggressive flocks, but they can create panic by swarming around.

TIPS:

- Attacking them is pointless and can attract unnecessary attention.
- Completely defenseless. They pose no threat, but they can accidentally provoke aggression from other creatures if they are hunted.
- There are rumors of chickens that burst into flames.