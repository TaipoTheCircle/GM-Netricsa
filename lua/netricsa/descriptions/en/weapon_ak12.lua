AK-12 ASSAULT RIFLE


WEAPON:      AK-12 assault rifle
AMMUNITION:  5.45×39 mm
ROF:         ~900 rounds/min

DESCRIPTION:

A modern modification of the iconic assault rifle, known for its reliability and durability. Redesigned to fire 5.45×39 mm cartridges. Various modules can be attached to it. Developed in 2011 and presented as the assault rifle of the future for the Russian Armed Forces.

NOTES:

- High durability and ease of use.
- Moderate recoil; controllable during short bursts.
- Effective against unarmored and lightly armored targets.
- Magazine capacity requires periodic reloading during prolonged combat.
- This assault rifle can be modified with various modules as desired.
- Improved ergonomics, the assault rifle is easier to hold in your hands.