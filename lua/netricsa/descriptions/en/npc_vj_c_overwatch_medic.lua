COMBINE OVERWATCH ELITE MEDIC


CLASS:      Combine Overwatch Elite Medic
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Field Combat Medic
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Combine Elite Medic Gun (healing beam and machine gun alt-fire), healing flasks
REWARD:     1200 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Elite Medic is not a savior, but a support staff member of a slaughterhouse. He wears a white jumpsuit with black stripes on the legs and black shoulder panels. The orange stripes on the shoulders form a cross - the Combine medical symbol, which is more of a warning than a promise of help.

The helmet is equipped with a respirator and large, convex blue lenses, giving the medic an insect-like appearance. A tube extends from the respirator to a medical pack on the back, where the contents of antlion larvae are stored.

The Combine's Medicannon fires a healing beam that delivers processed antlion larval eggs to allies. The Combine extracts the healing juices from the larvae and uses them as biofuel for tissue regeneration.

Additional healing flasks are attached to the medic's side, used for emergency recovery of soldiers. The medic's alternate mode activates a built-in machine gun, allowing the medic to defend himself and finish off enemies directly under the healing beam.

In combat, the Elite Medic rarely engages in direct attack. He hangs behind Elite units, turning them into near-immortal soldiers while he remains under cover.

TIPS:

- Kill the medic first, otherwise the elite soldiers will become practically unkillable.
- His armor is weaker than that of the combat elites, but he's usually surrounded by guards.
- His healing beam gives away his position with a bright glow, so use it.
- His secondary fire is dangerous at close range, so don't underestimate it.
