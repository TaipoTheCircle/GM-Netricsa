GONZO GUN


WEAPON: Gonzo Gun
AMMO:   Eggs
ROF:    quick single shots

DESCRIPTION:

The Gonzo Gun literally resembles a chicken and fires eggs. As an obvious developer, its eggs instantly one-hit kill virtually any non-boss malefactor, making it an absurdly powerful cleanup tool. Looks and sounds ridiculous - plays exactly like a gag weapon but with serious lethality.

NOTES:

- Each shot consumes one egg; the ammo pool is limited.
- One-hit kills non-boss enemies; typically ineffective versus final bosses.
- Best used sparingly on high-value targets or to clear particularly nasty encounters.