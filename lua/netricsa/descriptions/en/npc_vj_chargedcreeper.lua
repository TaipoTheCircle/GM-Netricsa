CHARGED CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Charged Creeper
METABOLISM: Electromagnetic-Plant-Chemical
PERCEPTION: A beefed-up version of the standard Creeper
SIZE:       5,5 ft
ENDURANCE:  Medium
HOSTILITY:  Extreme
WEAPONATAL: Larger Electric Blast
REWARD:     700
THREAT:     Medium-High

DESCRIPTION:

The Charged Creeper is the same green figure as the standard Creeper, only its body is now filled with unbridled electrical energy.

It explodes with greater force than normal, damaging all blocks and mobs within a radius.

Charged Creepers are created by lightning strikes.

TIPS:

- The damage radius and defense are twice that of the standard Creeper.