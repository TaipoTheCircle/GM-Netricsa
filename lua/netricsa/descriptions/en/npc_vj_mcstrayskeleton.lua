STRAY SKELETON FROM THE MINECRAFT OVERWORLD


CLASS:      Stray Skeleton
PERCEPTION: Undead
METABOLISM: Magically ice-powered undead
SIZE:       5,9 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Bow, arrows with Slow effect
REWARD:     150 FC
THREAT:     Medium

DESCRIPTION:

The Stray Skeleton is a skeleton that has forgotten that snow is a deadly trap for the unprepared.

Any skeleton caught in loose snow initially shivers as if frozen, and after a few seconds transforms into Straya - a frozen version of itself with a bow and arrows that slow enemies for 30 seconds.

TIPS:

- Don't try to play snowballs with Stray - you'll lose.
- His arrows will slow you down when they hit.