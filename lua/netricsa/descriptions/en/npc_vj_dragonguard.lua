IMPERIAL DRAGON GUARD FROM NIRN


CLASS:      Imperial Dragon Guard
METABOLISM: Mer, Male
PERCEPTION: Forbidden Zone Guardian / Elite Imperial Guard
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Very High
WEAPONS:    Longswords, heavy shields, sometimes magic
REWARD:     10000 FC
THREAT:     Very High

DESCRIPTION:

This is not simply an elite unit of the Imperial Legion, but rather a myth shrouded in mystery. Unlike the Royal Imperial Guard, which visibly guards the Emperor and the Palace, these guards operate in the shadows. Their job is to patrol forbidden excavations, abandoned forts, ancient tombs, and other dangerous places where mere mortals are forbidden to enter. This unit is believed to have been formed by personal order of Emperor Uriel Septim VII before the Oblivion Crisis to guard strategically important sites connected to dark artifacts and gateways to other worlds.

Their most distinctive feature is the legendary Imperial Dragon Armor. This unique armor was traditionally worn only by the special guard who personally protected the Emperor and his family. Forged from the finest materials available to the Empire, the armor is considered a symbol of the highest valor and trust. One such suit is known to have been presented to the Hero of Kvatch in recognition of his service.

While information about the Imperial Dragon Guard is extremely limited, their origins are likely linked to an even older organization:

Dragon Guard: An elite organization, the predecessor of the Blades, that guarded the emperors of the Reman dynasty. They were more than just guards, but a special force and personal army for the Emperor.

TIPS:

- If you see a guard in Imperial Dragon Armor, you're in a restricted area. You're not welcome. Leave before it's too late.
- These guards are deadly in combat. Their armor provides resistance to magic, fire, cold, and electricity. Standard tactics are useless against them.
- The best strategy is stealth. If you're spotted, run. Direct combat with an Imperial Dragon Guard is certain death, even for a seasoned adventurer.
