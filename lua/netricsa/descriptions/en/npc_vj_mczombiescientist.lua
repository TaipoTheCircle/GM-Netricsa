ZOMBIE SCIENTIST FROM THE MINECRAFT OVERWORLD


CLASS:      Zombie Scientist
METABOLISM: Undead
PERCEPTION: Intelligent Undead, Potion Thrower
SIZE:       6,2 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Weakness And Damage Potions
REWARD:     300 FC
THREAT:     Average

DESCRIPTION:

This is a rare undead species whose brain retains enough gray matter to remember old habits. Unlike its mindless brethren, who simply charge head-on, this specimen prefers the tactics of an alchemist. It doesn't engage in close combat; it pelts its target with potions from a safe distance.

He wears a white lab coat over a blue shirt, dark glasses, a neat brown tie, and a belt. He looks almost like a living employee, if you ignore his green skin. It seems he died right at his desk, then simply stood up and continued his experiments-this time on living creatures.

In combat, the Zombie Scientist uses two types of potions. A damage potion directly damages the target, while a weakness potion reduces attack power, leaving the victim almost defenseless against other mobs. The scientist avoids melee combat-if an enemy gets too close, the undead prefer to retreat and resume firing from a distance.

He doesn't burn in the sun like normal zombies-apparently his coat protects him from UV rays just as well as from acid.

TIPS:

- Don't let the scientist get within throwing range. Close quickly and attack in melee combat.
- He's helpless in close combat and won't be able to use potions.
- Dodge potions-they fly in an arc.
- Use a shield. It doesn't block the potion's effect, but it does reduce direct damage from hits.
- If you're weakened, retreat behind cover and wait out the effect. Fighting in this state means certain death.
- Damage potions damage both you and other mobs.
