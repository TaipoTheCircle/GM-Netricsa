ANTI-NEXTBOT CANNON


WEAPON: Anti-Nextbot Cannon
AMMO:   Supercharged Gravitational Energy / Annihilation Beam
ROF:    Continuous Beam

DESCRIPTION:

The most advanced anti-nextbot weapon, based on the improved supercharged gravity gun. The weapon combines gravity technology and destructive energy blasts, forming a beam of colossal power.

The first mode concentrates the energy in a narrow spot, releasing a beam capable of instantly destroying groups of targets within a single area. It is used to clear enemy concentrations and hold key positions.

The second mode activates a full energy blast, covering the entire area. At this point, the user is automatically protected by a special field that completely isolates them from the weapon's effects. All other living beings within the blast zone are immediately destroyed – no exceptions.

NOTES:

- Mode 1: Mass destruction of targets in a single area with a beam.
- Mode 2: Completely clears the entire location of all living creatures.
- The user is protected by an energy field when activating the second mode.
- Does not discriminate between targets - allies and neutrals are also destroyed.
- Effective at any range, especially in confined spaces.
