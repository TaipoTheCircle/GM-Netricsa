DETONATION SENTRY FROM THE MINECRAFT AETHER WORLD


CLASS:      Detonation Sentry
METABOLISM: Mechanical, Stone Golem
PERCEPTION: Hostile Mob, Stationary Trap
SIZE:       3,2 ft
ENDURANCE:  Average
HOSTILITY:  Absolute
WEAPONS:    High Knockback Explosion
REWARD:     90 FC
THREAT:     Average

DESCRIPTION:

This is a type of Ethereal Realm security mechanism. It appears as a stone block with a carved symbol or eye, attached to the floor, wall, or ceiling. It remains motionless for a long time, blending into its surroundings, but when an enemy approaches, it activates.

Unlike other Sentinels, which fire lasers or projectiles, this one specializes in self-destruction. Upon detecting a target, the Detonation Sensate begins to flash, emit a beep, and then explodes. The explosion itself deals minor damage, but its main danger is the knockback it causes. The victim is launched high into the air, and if there's no ceiling above, a fatal fall from a height can be fatal.

Detonation Sentrys are primarily found in Slider dungeons, where they appear from carved trap blocks or through special spawners. They can sometimes be summoned by Sentinel Guardians.

TIPS:

- Before entering a dungeon, inspect the walls, floor, and ceiling. If you see a block with a suspicious symbol, avoid it.
- If fighting indoors, make sure there's a ceiling above you. This will protect you from fall damage.
- Use a bow or crossbow to destroy the Sentinel from a distance before it activates.
- If the Detonation Sentry is already flashing, run back and wait for the explosion. Don't try to rush past it.
