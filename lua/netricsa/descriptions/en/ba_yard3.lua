BLACK MESA WAREHOUSE, A FIND WITH A DEADLINE


Sam, you got out of the ventilation ducts, and I immediately stumbled upon a tragedy. Somewhere next to you lies a scientist named Harold. He was shot by H.E.C.U. soldiers. There is blood on his robe, his breathing is ragged, but his eyes... My eyes are burning. He's still holding on. And he seems to know what you need most.

Harold is clinging to consciousness with the last of his strength.

Your task now is:

To listen to Harold. He knows how to get out of here. But his time is running out - every second counts.

Ask the main question - "How to get out of here?" - and listen carefully.

Remember that. Act fast.

Come on, Sam. Time is ticking. Harold is still breathing. That means you still have a chance.