COMBINE OVERWATCH ELITE OFFICER


CLASS:      Combine Overwatch Elite Officer
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Command and Reconnaissance Unit
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Silenced Pistol, Reinforcement Call Flare Gun
REWARD:     800 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Elite Officer is the Combine's field commander, combining the functions of reconnaissance, coordinator, and psychological suppression. He wears a dark tactical uniform with reinforced knee armor, black boots, and a black beret, emphasizing his status among ordinary soldiers.

His helmet has been replaced with a version featuring a single white and yellow glowing visor, which serves as a tactical scanner and command interface. A gray cape is draped over his shoulders, serving as a camouflage element and a symbol of rank. A long-range radio is strapped to his back, allowing him to call for reinforcements from elite Combine transports.

He is armed with a silenced pistol for stealthy assassination and the suppression of witnesses. Upon detecting a threat, the officer uses a flare pistol to call in squads of soldiers, elite fighters, or support drones. His job is not to engage in prolonged combat, but to manage the battle, turning a localized incident into a full-scale Combine operation.

TIPS:

- Kill the officer before he calls for reinforcements.
- The silenced pistol makes him a nearly invisible hunter.
- The radio is his greatest asset. Without it, he's a mere commander.
- Don't underestimate him: he rarely shoots himself, but he decides who will shoot at you.
