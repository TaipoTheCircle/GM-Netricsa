OVERWATCH FEMALE COMBINE ASSASSIN


CLASS:      Elite Transhuman Operative, Assassin
METABOLISM: Enhanced Human, Female / Biomechanical Augmentations
PERCEPTION: Enhanced Sensors + Neuro-Reactive Target Recognition
HEIGHT:     5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Pistols, Grenades
REWARD:     800 FC
THREAT:     Very High

DESCRIPTION:

The Female Combine Assassin is a specialized transhuman operative designed to eliminate key targets, suppress resistance, and conduct covert operations behind enemy lines. Her acrobatic and neurochemical training makes her a hunter, a spy, and a strike force all in one.

The assassin's origins are linked to Overwatch's programs to create operatives capable of operating independently of the main units. Like other transhuman fighters, she has undergone biomechanical modifications, but her specialization requires accelerated neural network adaptations, allowing her to anticipate enemy movements, operate in complete silence, and adapt to changing combat conditions.

Her tactical equipment is adapted for lightning-fast attacks and stealth.

TIPS:

- In most cases, she throws Grenades before attacking.
- Her footsteps are silent, so keep your eyes peeled.
- Her red eye gives her away in the dark.