CLAIRE FROM FUNDAMENTAL PAPER EDUCATION


CLASS:      Claire, Paper School Student
METABOLISM: Paper anthropomorphic organism
PERCEPTION: Vision, survival instinct
SIZE:       5,3 ft
ENDURANCE:  Low
HOSTILITY:  None
WEAPONS:    None
REWARD:     1 FC
THREAT:     None

DESCRIPTION:

A diligent and hardworking student at Paper School. Easily recognizable by her bob haircut with a bow. She wears the standard school uniform: a white shirt, shorts, socks, and shoes. Calm and thoughtful, she is easily angered by the constant bullying of bullies like Zip, Oliver, Edward, and Alekoth. She possesses a strong instinct for self-preservation, which manifests itself in her incredible agility and resilience for a paper creature - she can survive falls from heights, evade killer teachers for long periods, and even briefly resist the ∆lice entity.

She trusts Engel (her childhood friend). She is friendly with Bubble, Biscuit, and Anova. She has a cat, Mo-Mo (Moe-Moe). She is a victim of bullies and teachers.

TIPS:

- Any serious attack from teachers is fatal for her.
- When in danger, she relies on flight and evasion. She does not attack. She can be saved by the intervention of others.
- She can accidentally unleash an even greater threat (∆lice).
- In alternate versions, she may have a different appearance (hair color, clothing), but the essence of the character remains unchanged – a conscientious student trapped in a nightmare.