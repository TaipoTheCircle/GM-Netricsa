COMBINE ARCTIC SCANNER


CLASS:      Autonomous organic Combine reconnaissance drone
METABOLISM: Fully synthetic, battery-powered energy system
PERCEPTION: Multi-range sensors: visual, thermal, Doppler
SIZE:       1 ft
ENDURANCE:  Medium
HOSTILITY:  Medium-high
WEAPONS:    Pulse laser beam
REWARD:     350 FC
THREAT:     Medium-high

DESCRIPTION:

The Combine Arctic Scanner is a modified version of the Wasteland Scanner, adapted for the extremely cold temperatures and snowy environments of northern territories.

Like the standard Wasteland Scanner, it is equipped with multi-band sensors: visual synthetic cameras, thermal imaging, and Doppler motion sensors. However, unlike the basic model, the Arctic Scanner is equipped with a pulsed laser, allowing it not only to observe but also to actively suppress soft targets at close range.

The laser beam is not designed to combat heavy armor or armored units, but it can cut through soft tissue, destroy small vehicles, and disrupt rebel operations in open spaces.

TIPS:

- Against a snowy background, its body visually assimilates into the landscape, making it difficult to detect.
- Avoid the laser.