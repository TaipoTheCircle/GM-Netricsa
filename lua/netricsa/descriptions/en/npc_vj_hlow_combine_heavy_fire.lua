HEAVY COMBINE OVERWATCH FLAMETHROWER SOLDIER


CLASS:      Heavy Overwatch Armored Soldier with Alliance Flamethrower
METABOLISM: Augmented Human, Male
PERCEPTION: Fire Clearance and Suppression Unit
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Combine Flamethrower, Fuel Tank
REWARD:     800 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Flamethrower Soldier is a heavily armored a walking irony of Combine engineering. He's clad in a bright orange hazmat suit with a massive respirator, making him look more like a wrecker than a combat unit. But that color was chosen deliberately-he's meant to be seen. And feared.

A large fuel tank powering a flamethrower is attached to the soldier's back. The tank is unstable and highly explosive: multiple accurate hits cause a detonation that kills both the soldier and anyone nearby with equal indifference. The Combine considers this an acceptable price for effectiveness and intimidation.

In combat, this soldier doesn't care about his own survival. He slowly advances, engulfing the area in flames, flushing the enemy out of cover and turning corridors into furnaces. His mission is to break through defenses at any cost, even at the cost of his own life.

TIPS:

- Shoot the tank on his back - an explosion is guaranteed.
- Don't get close, even if he's already wounded.
- The explosion kills everyone within range - use cover.
- The bright suit is highly visible, but there's little time to react.