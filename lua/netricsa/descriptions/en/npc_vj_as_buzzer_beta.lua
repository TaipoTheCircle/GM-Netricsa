BETA SWARM BUZZER


CLASS:      Beta Swarm Buzzer
METABOLISM: Xenobiological
PERCEPTION: Flying Scout
SIZE:       2,3 ft
ENDURANCE:  Very Low
HOSTILITY:  Average
WEAPONS:    Piercing Proboscis, Venomous Bite
REWARD:     100 FC
THREAT:     Low

DESCRIPTION:

Unlike its later brethren, this specimen has an elongated body and large membranous wings, allowing it to remain airborne for long periods without landing.

The creature's main weapon is its long, stabbing wings. When attacking, the Buzzer dives at its target, aiming for a quick stab and then immediately retreating to a safe distance. This action injects the victim with a toxic substance, causing pain and temporary loss of coordination.

A light-sensitive visual organ located in the front of the head can detect movement even in low-light conditions. This allows Buzzers to frequently appear in caves, technical tunnels, and other places where larger Swarm creatures have difficulty detecting prey.

Despite their aggressive behavior, this species is characterized by extremely weak defenses. Most individuals die after a few hits, compensating for this with their speed, numbers, and exceptional persistence.

Some xenobiologists believe that modern Buzzers evolved from these creatures. Fortunately for science, Buzzers themselves are not interested in scientific debate.

TIPS:

- Watch the ceiling and open spaces above your head.
- Don't let the pack circle around you.
- The toxin is more dangerous than the bite itself.
