NINJA CHICKEN FROM THE CHI-FANG


CLASS:        NInja Chicken
METABOLISM:   Warm-Blooded Bird
PERCEPTION:   Stealthy Rabbit-Slayer, Feathered Avenger
SIZE:         1,64 ft
ENDURANCE:    Medium
HOSTILITY:    Medium
WEAPONS:      Katana, Saws
REWARD:       1000 FC
THREAT:       Medium

DESCRIPTION:

At first glance, it looks like an ordinary, average chicken, but beneath that feathered body lies a true ninja's path-paved with blood and tears.

Since early childhood, its village had been plagued by invasions of ninja rabbits that slaughtered everyone in their path. Having lost everything it held dear-family, friends, its favorite roost, and even its grain bowl-

the bird swore an oath: every rabbit it encountered would be killed.

TIPS:

- Ninja rabbits fear the dark, for they know what lurks within it.
- Never ask a ninja chicken where the eggs are.
