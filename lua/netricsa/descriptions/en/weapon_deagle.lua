DESERT EAGLE LARGE-CALIBER SEMI-AUTOMATIC PISTOL


WEAPON:      Desert Eagle
AMMUNITION:  .50 Action Express cartridges
ROF:         ~20 rounds/min

DESCRIPTION:

Large-caliber semi-automatic pistol. It was developed in 1983 in the USA by Magnum Research and subsequently refined by Israel Military Industries. Due to its size and intimidating appearance, it is often used.

NOTES:

- This pistol is very powerful and has tremendous penetration.
- Extremely powerful recoil makes it impossible to shoot with one hand.




