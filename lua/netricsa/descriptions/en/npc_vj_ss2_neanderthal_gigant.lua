PRIMITIVE BEHEMOTH FROM M'DIGBO


CLASS:      Primitive Behemoth
METABOLISM: Mammal
PERCEPTION: Primitive being
SIZE:       50 ft
ENDURANCE:  High
HOSTILITY:  Medium
WEAPONS:    Giant Spiked Club
REWARD:     500 FC
THREAT:     Medium

DESCRIPTION:

M'Digbo is dotted with countless primitive tribes, all of which display highly territorial and aggressive behavior. They attack almost everyone on impulse and are no smarter than a bunch of bricks.

They are a humanoid race led by the mysterious "Mommy".

When Mental's forces colonized M'Digbo, the primitives launched attacks against them. They all died. After that, they started worshipping Mental as a god of war and pledged their allegience to him. This was done out of their fear and respect for Mental, as well as the stylistic leopardic panties that Sirius Fashion Inc provided them with.

Having found a bigger stick in this cruel world, the Primeval Giant sets out in search of adventure on his prehistoric backside. Life in a cave is ideal for a Primeval Giant: plenty of food, warmth, safety... and so these possible ancestors of humanoid creatures grew to gigantic proportions.

Troglodytes have gray skin and, to demonstrate their grandeur, tattoo themselves with red tattoos. To further intimidate others, they wear necklaces of teeth, kindly taken from the skulls of slain enemies. They also wear rings on their hands and feet.

TIPS:

- Although they move slowly, they are very dangerous if they are close up, keep your distance.
- They should be high priority targets in close environments with not much space to move.
