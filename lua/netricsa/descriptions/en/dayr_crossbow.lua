STANDARD SURVIVALIST CROSSBOW


WEAPON: Standard Survivalist Crossbow
AMMO:   Crossbow Bolts
ROF:    ~1 shot / second

DESCRIPTION:

A basic survivor crossbow, designed for economical and reliable combat. While not particularly powerful, it makes up for this with the availability of ammunition and a simple design.

The weapon fires single bolts and requires little effort to operate, making it popular among novice survivors. With the Poisoning skill, it can be modified to deal additional poison damage, gradually weakening the target.

NOTES:

- Bolts are easily crafted from scrap materials.
- Can deal additional poison damage with the appropriate skill.
- Effective at close and medium ranges.
- Low base damage compared to more advanced crossbows.
- An excellent choice when there is a shortage of firearms ammunition.
