COMBINE OVERWATCH ELITE PRISON SOLDIER


CLASS:      Combine Overwatch Elite Prison Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Prison Guard
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    AR2, Grenades
REWARD:     900 FC
THREAT:     Very High

DESCRIPTION:

The Overwatch Combine Elite Prison Soldier is a specialized operative designed to guard prisons and manage dangerous inmates. His armor is standard Nova Prospekt, providing enhanced protection, and his helmet is taken from the Combine Elite Soldier, but with a yellow visor, allowing him to observe prisoners and quickly respond to threats.

This type of fighter is equipped with a combination of long-range and close-range weapons: the AR2 pulse rifle allows for suppression of resistance at various distances, while grenades are effective at knocking out cover. Augments provide endurance, reaction speed, and the ability to fight in confined prison spaces.

TIPS:

- The yellow visor reveals his position in the dark, but helps him aim accurately.
- Keep his distance to avoid close combat.
- Grenades are dangerous in confined prison corridors; use cover.