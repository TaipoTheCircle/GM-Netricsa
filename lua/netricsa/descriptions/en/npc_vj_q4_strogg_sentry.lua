STROGG SENTRY FROM STROGGOS


CLASS:      Strogg, flying guard-gunner
METABOLISM: Cybernetic organism, symbiosis
PERCEPTION: Standard vision, precise target scanning
SIZE:       ~5 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    High-precision twin machine guns
REWARD:     150 FC
THREAT:     Medium

DESCRIPTION:

The Strogg is a specialized flying Strogg designed to guard key areas like the Nexus and the Hub tunnels. It is a legless, oval-shaped body hovering in the air, with a pair of machine guns in place of arms and a container of green liquid beneath, housing a humanoid symbiote. These enemies are medium difficulty, appearing in groups of 2-4. Their main strength lies in their rate of fire, accuracy, and the ability to quickly sidestep slow-moving projectiles. Despite their low individual damage, their constant barrage can quickly deplete your health and armor, distracting you from more dangerous enemies.

TIPS:

- Focus on the green container below - attacks against it deal double damage.
- Use a machine gun or shotgun at close range. The Lightning Cannon is ideal.
- Take cover during their bursts, attacking during pauses. When appearing in a group, eliminate the Sentinels first to avoid distractions.
- They don't pursue through doors - you can retreat to regroup.