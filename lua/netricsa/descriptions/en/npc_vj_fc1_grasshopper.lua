TROPICAL GRASSHOPPER FROM THE KRIEGER ARCHIPELAGO


CLASS:      Tropical Grasshopper
METABOLISM: Insect, Orthoptera
PERCEPTION: Compound eyes, vibration sensitivity
SIZE:       0.03 ft
ENDURANCE:  Extremely low
HOSTILITY:  None
WEAPONS:    None
REWARD:     1 FC
THREAT:     None

DESCRIPTION:

A small tropical insect that lives in the grass and shrubs of the archipelago. It behaves similar to a fly: when threatened, it makes a short, noisy jump-flight for several meters, then freezes again. It does not attack or interact with other creatures.

TIPS:

- Attacking is pointless. It is not a resource.