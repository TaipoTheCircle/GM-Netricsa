YAGODA THE WITCH OF MAGNOR


CLASS:      Yagoda The Witch
METABOLISM: Mammal, Former Beauty
PERCEPTION: Witch
SIZE:       4,6 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Magic Projectiles
REWARD:     50 FC
THREAT:     High

DESCRIPTION:

She was essentially one of the most beautiful girls in the galaxy, but when she and her sister were ready to compete in Mental's beauty pageants, they turned into ugly witches. After this mishap, they were easily drafted into Mental's army.

Yagoda is Dynja's twin sister; together, they are the most famous witches on the planet Magnor. They are renowned for their sadistic nature, mastery of their art, and fondness for alcohol.

She's a witch who mastered the Mirror Clone spell and used it to reproduce herself. Seeing potential in her, Mental offered her a lifetime supply of vodka in exchange for her services. She happily accepted.

Yagoda flies around erratically, attempting to surround the enemy while simultaneously raining down magical attacks.

TIPS:

- She's very maneuverable and difficult to hit; to destroy her, use rapid-fire weapons or carefully aim with a sniper rifle.
- She should be a priority target, as aerial fire can be too effective.
- While a shot from a sawed-off shotgun can kill her in close combat, getting close to her is extremely difficult.
- If you're surrounded, try to constantly move left and right while retreating backward to try to break her encirclement.
