UPSIDE DOWN CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Upside Down Creeper
METABOLISM: Gravity-Plant-Gunpowder
PERCEPTION: Hostile Mob, Reality Distorter
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  Extreme
WEAPONS:    Anti-Gravity Blast
REWARD:     200 FC
THREAT:     Medium-High

DESCRIPTION:

This is one of the strangest of the elemental creepers. It looks like a normal creeper, but its head is upside down. This makes it look like someone took a normal creeper, flipped its head over, and forgot to flip the body back over.

When approached by a victim, an Upside Down creeper behaves like a normal creeper: it hisses, swells, and explodes. However, its explosion effect is unique.

When an Upside Down creeper explodes, it doesn't destroy blocks or deal direct damage. Instead, it creates a gravity field that alters the gravity of all creatures within the explosion radius.

All enemies within the blast radius instantly "fall" upward.

Creepers rise vertically until they reach a ceiling (if there is one) or are blown into the open sky or space.

If there is no ceiling, the victim is blown upward tens of hundreds of blocks until the effect wears off.

The effect lasts for a few seconds, after which gravity returns to normal. If the victim was high enough when they fell, they will fall back down and take fall damage. If they are blown into space, they are considered dead.

TIPS:

- An Upside Down creeper is not dangerous in rooms with low ceilings. You'll just hit your head on the ceiling and fall back down.
- It's deadly in open areas. One blast and you'll be sent flying into the stratosphere.
- To avoid being launched, hug a block above you. If you're standing under an overhang, the gravity field won't activate.
- Use a shield to block the shockwave. A shield doesn't stop the gravity shift, but it does reduce the distance you're thrown back.
- Killing an upside-down creeper has a chance to drop an upside-down shard. You can craft a levitation potion from it, allowing you to rise at will.
- Don't try to run away. It hisses just like a regular creeper. If you hear it hiss, jump to the side, not up.
