CBRN S10 NBC RESPIRATOR


ITEM:  S10 CBRN / NBC Respirator (gas mask)

DESCRIPTION:

The S10 CBRN respirator is a British military gas mask introduced in 1986 as a replacement for the S6, manufactured by Avon Rubber. It provides protection against chemical, biological, radiological and nuclear (CBRN / NBC) threats and uses a detachable filter canister. The mask features a fail-safe drinking device, an inner oro-nasal mask to reduce lens fogging, and optical insert hooks allowing the use of corrective lenses.

NOTES:

- Fitted with a Secondary Speech Transmitter (SST) and side mounting for radio microphone integration.