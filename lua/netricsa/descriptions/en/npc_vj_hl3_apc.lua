ARCTIC COMBINE ARMY CARRIER (APC)


CLASS:      Arctic Combine Armored Personnel Carrier
METABOLISM: Transport Vehicle
PERCEPTION: Targeting Sensors + Automatic Warhead
SIZE:       26 ft.
ENDURANCE:  Very High
HOSTILITY:  High
WEAPONS:    Pulse Launcher + Guided Missiles
REWARD:     1500 FC
THREAT:     High

DESCRIPTION:

The APC is a massive Combine Arctic armored personnel carrier used for patrols, rapid force deployment, and direct fire against resistance. It combines heavy armor, formidable weapons, and mechanical systems designed to suppress any signs of insubordination. This vehicle appeared early in the conflict and is particularly noticeable in urban areas and on the coast, where it serves as a backbone for both Civil Defense units and Overwatch soldiers. Its armor is designed to withstand heavy fire, while powerful guided missiles and a pulse launcher transform the APC from a simple transport into an independent strike unit.

In practice, the APC is used both as a means of quickly delivering forces to hot spots and as a mobile fortress for shelling resistance.

TIPS:

- Use grenades or RPG V2.
- The APC's guided missiles inflict significant damage within the kill zone - move around cover.