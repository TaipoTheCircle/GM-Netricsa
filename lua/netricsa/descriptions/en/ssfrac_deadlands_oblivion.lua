DAEDRA OF THE OBLIVION'S DEADLANDS


The Daedra of the Oblivion's Deadlands are an army of destruction, born in the flames of the realm of Mehrunes Dagon, Prince of Chaos and Destruction. They are embodiments of their master's fury, creatures forged from ash, steel, and hatred.

Every warrior of the Deadlands bears the flame of their Prince within their heart. They know no fear, know no pity, and do not distinguish between good and evil. For them, only the order of destruction exists-the sacred cycle of end and rebirth.

When the gates of Oblivion open, their armies erupt into the mortal world like a lava flow-burning everything in their path. They are not bound by time and do not fear death, for to them, the end is the beginning of a new fire.