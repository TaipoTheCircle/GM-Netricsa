GEONOSIS


PLANET:       Geonosis
TYPE:         Terrestrial planet, Industrial world
CLIMATE:      Arid, severe sandstorms, frequent meteor showers
MOONS:        None (has a dense asteroid belt)
TERRAIN:      Rocky wastelands, spires, stalactites, stone formations; most structures are located underground
LOCATION:     Outer Rim Territories, less than a parsec from Tatooine
NATIVE RACES: Geonosians
ALIGNMENT:    Confederacy of Independent Systems (formerly); Galactic Empire (later); Rebel Alliance (Current)
LOCATIONS:    Geonosis Arena, Droid Mines, Hive Towers, Asteroid Belt

DESCRIPTION:

A harsh, rocky planet in the Outer Rim Territories, located just a parsec from Tatooine. Despite its relatively large diameter, the planet has weak gravity and a dense, heavy atmosphere. Its main distinguishing feature is a massive asteroid field that forms a dense ring around the planet. Geonosis's surface is constantly bombarded by meteorites and powerful sandstorms.

The planet's landscape consists of endless rocky wastelands dotted with bizarre stone spires, stalactites, and other erosional formations. This inhospitable environment has forced its native inhabitants, the insect-like Geonosians, to seek refuge underground. They built their famous hive towers, the main portions of which extend deep beneath the planet's rocky crust. It was there, in the depths of Geonosis, that the vast underground battle droid factories were located, forming the heart of the Confederacy of Independent Systems' war machine.

Geonosian society has a complex, insect-like hierarchical structure. For a long time, it was believed that they were ruled by an Archduke, but during the Second Battle of Geonosis, it was revealed that Queen Karina, who controlled the entire planet, was the true ruler. A unique ability of special Geonosian worms—the so-called "hive spirits"—to control the bodies of creatures, including the dead, was also discovered.

Shortly before the Battle of Yavin, the Alliance to Restore the Republic took advantage of the planet's remote location and rich resources to build secret shipyards, where, with the support of locals, they began producing ships for the Rebels.

KEY LOCATIONS:

- Geonosis Arena: A massive amphitheater carved into the rock where mock executions and combat were held. The site of the first major battle of the Clone Wars, when the clone army came to the rescue of the Jedi.
- Droid Mines: Massive underground complexes where the Geonosians mass-produced battle droids for the Confederacy of Independent Systems. The heart of the planet's military industry.
- Hive Towers: Above-ground sections of Geonosian dwellings, extending deep underground. They serve as entrances to underground cities and factories.
- Asteroid Ring: A dense ring of rock debris and meteoroids surrounding the planet. It poses a constant threat to orbital travel and serves as a natural defense against invasion.
