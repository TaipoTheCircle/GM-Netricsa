Y.S.M. ZOMBIE HEADCRAB, COMMON


CLASS:      Y.S.M. Zombie Headcrab, Common
METABOLISM: Human Host + Parasitic Headcrab Of Xen
PERCEPTION: Zombified Entity, Former Contractor
SIZE:       6,2 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    Claws, Bite
REWARD:     500 FC
THREAT:     Very High

DESCRIPTION:

This is what the mercenaries of the private military company Y.S.M. turn into after an unfortunate encounter with a headcrab. Unlike scientists in gray coats, technicians in coveralls, or even the Delta Base guards, these guys were originally professional soldiers. They wore body armor, tactical helmets, and knew how to handle weapons. This didn't save them from infection, but it made them much more dangerous opponents in their zombified form.

Their armor partially protects their bodies from gunfire, making them more resilient than regular zombies. The headcrab perched on their head looks the same as that of other variants, but its host is a trained, muscular body that doesn't fall apart from a couple of blows.

They are larger than the Delta Base guards, their armor is thicker, and their blows are heavier. Even after the headcrab takes control, the former mercenary retains muscle memory: they move faster than regular zombies. Their claws pierce armor, and their bites tear out chunks of flesh.

A headshot detaches the head.

TIPS:

- Y.S.M. zombies are more resilient than all other standard zombies. Don't skimp on ammo.
- His armor protects his chest and shoulders. Aim for the head-it's vulnerable, like everyone else's.
- Keep your distance-he's a bit faster than a normal zombie.
- Don't think that the remains of his tactical gear are trophies. He doesn't need them anymore, and you certainly don't.
