ALUDRAN ARACHNOID FROM THE ALUDRAN SYSTEM


CLASS:      Aludran Arachnoid
METABOLISM: Ectotherm, Arthropod
PERCEPTION: Primitive Sapient Race
SIZE:       10 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONATAL: Magical Flame Machine Gun, Stinger Tail
REWARD:     2000 FC
THREAT:     High

DESCRIPTION:

This crawling, eight-legged enemy is one of Mental's main adversaries in the war against humanity.

A scorpion hybrid, its sting attack at close range is powerful, but not as deadly as its quickly reloading flame minigun. The Arachnoid isn't particularly fast and prefers to keep its opponents at bay, opening fire as early as possible.

In one hand, the arachnoid holds a magical flame machine gun equipped with a plasma regenerator. Its body is covered in an exoskeleton that protects it from bullets, explosives, and fire.

It is unknown how the Arachnoids from Scorpius arrived in the Aludran system.

TIPS:

- The stinger on its tail is dangerous-don't get too close.
- When enraged, it will shoot even through its own.