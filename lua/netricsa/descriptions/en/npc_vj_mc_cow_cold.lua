COLD COW FROM THE MINECRAFT OVERWORLD


CLASS:      Wild / Domestic Animal
METABOLISM: Warm-blooded Mammal
PERCEPTION: Standard vision and smell
SIZE:       5,2 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     50 FC
THREAT:     Low

DESCRIPTION:

The Cold cow lives exclusively in Coldy biomes. It is distinguished by its uniform orange fur, lack of spots, and dense shagginess, which helps it retain heat in the cold environment.

A distinctive feature of this species is its straight, forward-facing horns, which give the cow a majestic and stern appearance.

Cold cows are peaceful, grazing in small groups, and are well adapted to life in cold, Coldy conditions.

TIPS:

- The milk is safe for hunting.
- The meat is nutritious.
- Their shaggy coat stands out against the Cold, making them an easy target for hunters but attractive for breeding in Cold-covered enclosures.