FAST ZOMBIE HEADCRAB, ARCTIC


CLASS:      Zombie Headcrab, Fast (Arctic)
METABOLISM: Parasitic host, resistant to cold
PERCEPTION: Heightened reflexes + heat sensitivity
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Claws, leaping, extreme speed
REWARD:     280 FC
THREAT:     Medium-high

DESCRIPTION:

The Arctic Fast Zombie Headcrab is one of the most frightening results of infestation in the perpetual cold. Its body, broken by the parasite, retains its characteristic skeletal shape, but is now covered in ice fragments and frozen flesh. The fast headcrab burrows deeply into the skull, further protecting itself from the cold and damage.

Unlike regular fast zombies, the Arctic doesn't lose speed on snow and ice. The creature moves on all fours, pushing off sharply with its clawed limbs and clinging to surfaces with frightening confidence. It can climb icy structures and make long leaps, using the surrounding terrain as a hunting ground.

TIPS:

- Don't let it get within jumping range.
- Short bursts of fire can stop it mid-attack.