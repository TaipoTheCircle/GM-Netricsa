HORNET NEST FROM THE XEN


CLASS:      Hornet Nest
METABOLISM: Xen Biological Organism
PERCEPTION: Stationary Spawner, Organic Turret
SIZE:       6,5 ft
ENDURANCE:  High
HOSTILITY:  Absolute
WEAPONS:    Hornets
REWARD:     150 FC
THREAT:     High

DESCRIPTION:

This is a huge organic growth found on walls, floors, and ceilings (and likely other places where Xen's creatures have reached). It resembles a cone. Inside this growth, small but extremely nasty creatures—Hornets—swarm.

The Nest's mechanism is simple and terrifying. As soon as any living target approaches the Nest, it activates. One of the suicide Hornets bursts out of the growth and flies straight at the target.

A Suicide Hornet lives only as long as it takes to launch a single attack. It dies after an attack, but the Nest produces an unlimited number of them. A second after one Hornet is destroyed, another one flies out of the Nest. This continues ad infinitum, until the Nest is destroyed or the target leaves the kill zone.

TIPS:

- Destroy the Nest from a distance. Explosives or heavy weapons are your best friends.
- Don't get too close to the Nest. Hornets fly out with a slight delay, but if you're in the kill zone, they're difficult to dodge.
- A single Hornet is weak, but the endless stream is exhausting. Kill the Nest, and the problem disappears.
- When destroyed, the Nest disintegrates into pieces of flesh, sometimes releasing a cloud of acid.
- The Nest doesn't move or flee. This is a stationary trap, but a very effective one.
