CRAB MEAT FROM THE MOBIUS


CLASS:      Crab meat
METABOLISM: Animal-Battery
PERCEPTION: Firing point
SIZE:       4 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Energy spheres
REWARD:     500 FC
THREAT:     Medium

DESCRIPTION:

When it came to organizing a defense in open spaces, Dr. Eggman turned his attention to crabs. Their sideways movement and snapping claws perfectly suited the concept of a mobile shooting drone. Thus, the Crab meat was born.

Their body is red, their belly is yellow, and their four legs with clawed feet allow them to crawl across any surface. Two flexible antennae and eyestalks with photoreceptors protrude from their shell. But the main attraction is their claws. Each one contains a hidden pulse generator. The Crab meat doesn't chase its prey; it methodically paces back and forth, waiting. As soon as it detects movement, it stops, aims its pincers, and fires either two spherical projectiles that fly in an arc.

TIPS:

- Attack while it's aiming. At this point, it doesn't react to external stimuli.
- Don't stand in the line of fire-the spheres fall from above.
