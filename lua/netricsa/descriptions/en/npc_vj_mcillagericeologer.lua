ICEOLOGER FROM THE MINECRAFT OVERWORLD

CLASS:      Iceologer
METABOLISM: Magically Gifted Illager
PERCEPTION: Ice Witch / Spellcaster
SIZE:       6,2 ft
ENDURANCE:  High
HOSTILITY:  Absolute
WEAPONS:    Summon Ice Block
REWARD:     700 FC
THREAT:     Very High

DESCRIPTION:

One of the most dangerous mages among Illagers, he uses powerful ice magic in combat. While a normal rogue simply shoots, and a summoner creates fangs and Vex, the Iceologer summons a block of ice onto the victim's head, which not only deals massive damage but also stuns the enemy for several seconds.

The Iceologer's appearance is strikingly different from his Rogue and Vindicator counterparts. He wears a long blue robe with white trim, and his eyes are a reddish-brown. Blue patterns-ritual markings-adorn his face, emphasizing his connection to magic.

In combat, the Iceologer is a classic support mage. He never engages in close combat, preferring to keep his distance and rain spells upon his enemies.

The Sorcerer's main and only combat ability is summoning a huge ice block directly above the enemy's head. The block doesn't fall instantly: it hovers for 1,75-4 seconds, pursuing the target, and then crashes down. Upon impact with the ground or a victim, the block explodes, dealing massive damage and stunning everyone within its radius.

Technically, the block is a triple cube of ice covered in snow.

He hates Glow Squids.

TIPS:

- The Iceologer is a priority target. While he's alive, an ice block will constantly hang over your head.
- While casting, he's motionless-the perfect window for an attack.
- Don't stand still. The block follows the target, but falls where you were when it locked on. Constant movement reduces the risk of a direct hit.
- If you hear the distinctive sound of a spell, move to the side immediately.
- Use cover. The block only falls where you have a clear line of sight.
