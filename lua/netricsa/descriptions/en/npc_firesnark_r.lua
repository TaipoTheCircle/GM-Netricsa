FIRE SNARK FROM THE XEN


CLASS:      Fire Snark
METABOLISM: Carnivorous Insect (Fire Mutation)
PERCEPTION: Alien Insect, Living Embers
SIZE:       6 inches
ENDURANCE   Low
HOSTILITY:  Absurdly High
WEAPONS:    Fire Bite
REWARD:     100 FC
THREAT:     High

DESCRIPTION:

This is a natural mutation of the common snark, which arose in the fiery regions of Xen. Where lava flows and the air is scorching hot, common snarks cannot survive. But Xen's nature has found a solution: those that survive have transformed into black and red insects whose shells glow from within, like smoldering embers.

Unlike their brethren, who live for a few seconds and then explode from sheer enthusiasm, fire snarks are not subject to self-destruction. They can live a long time. A very long time. And all this time, they will try to devour you. Or set you on fire. Or both at the same time.

They have a black body with bright red veins that pulsate like lava in a volcano. From a distance, they sparkle and reflect light, making them easy to spot. But as soon as you get closer, they disappear from sight, blending into the shadows and hot rocks.

A fire snark's bite isn't just painful-it ignites its target. One hit, and you're on fire. Two, and you won't have time to extinguish yourself. Survivors say these creatures deliberately attack in groups so their victims don't have time to react to multiple fires.

Fire Snarks are in constant conflict with the Devil Bullsquids, their neighbors in the Fire Regions. The conflict has been going on for centuries. No one remembers how it all began. Perhaps it was over territory. Perhaps it was over food. Or perhaps the Devil Bullsquids simply refused to acknowledge that the red on the Snarks' shells wasn't "pink," as they claimed.

When killed, a Fire Snark doesn't simply die. It disintegrates into chunks of molten lava that fly in all directions.

TIPS:

- Don't get too close to a Fire Snark. It doesn't explode like a regular Snark, but its bite sets it on fire.
- They can be seen from a distance by their glimmer. Use a sniper rifle.
- They're doubly dangerous in close combat. Keep your distance.
- Fire Snarks don't die on their own.
- If you see a pack, use explosives. There's already fire all around, a little more wouldn't hurt.
- Fire Snarks and Devil Bullsquids hate each other. If possible, set them against each other. Let them kill each other.
