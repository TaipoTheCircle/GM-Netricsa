BLENJEEL


PLANET:       Blenjeel
TYPE:         Terrestrial, Desert Planet
CLIMATE:      Arid, harsh
MOONS:        Unknown
TERRAIN:      Vast shifting deserts, unstable rock outcrops
LOCATION:     Between the planets Ando and Kothlis, far from major hyperspace routes
NATIVE RACES: Sandburst miners (giant worm-like creatures)
ALLEGIANCE:   Neutral territory, listed in New Republic databases as a dangerous planet
LOCATIONS:    Merchant ship crash site, Yavin Runner II shuttle crash site

DESCRIPTION:

A desert planet, lost far from the busy space lanes between the Ando and Kothlis systems. Its surface consists of endless sand seas, possessing a unique characteristic: the sand is constantly shifting. The landscape is constantly changing: rocky outcrops that seemed secure one day may vanish into the sandy abyss the next.

Blenjeel's main danger lurks beneath its shifting sands. The planet is inhabited by giant sandbursters-monstrous creatures that dwell in the desert depths. These creatures emerge swiftly from the ground, ambush unsuspecting prey. Their constant movement is the primary cause of the surface's instability: the dug tunnels cause the sand to shift and engulf rock outcrops.

Ten years after the Battle of Endor, a merchant ship was attacked by an Imperial Immobilizer-418-class raider and crashed on Blenjeel. Aspiring Jedi Jaden Korr was sent on a rescue mission aboard the Yavin Runner II. However, during landing, his shuttle was struck by lightning and also crashed.

Jaden was unable to save the surviving crew members-the last of them was devoured by a sandworm right before his eyes. However, the Jedi managed to survive in the desert, constantly avoiding contact with predators. He reached the wreckage of the merchant ship, salvaged the necessary spare parts, repaired his shuttle, and left the planet. After his return to Yavin 4, Blenjeel was listed in New Republic databases as a dangerous planet, not recommended for visitation.

KEY LOCATIONS:

- Merchant Ship Crash Zone: The desert area where the merchant ship crashed after being attacked by an Imperial raider. The location of the crew's death and the source of the necessary repair parts.
- Yavin Runner II Crash Site: The desert where Jaden Korr's escape shuttle crashed after being struck by lightning. This is where his journey to survival began.
