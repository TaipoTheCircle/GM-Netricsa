DIRTY (SPOILED) PIG FROM THE MINECRAFT OVERWORLD


CLASS:       Dirty (spoiled) Pig
METABOLISM:  Warm-blooded Mammal
PERCEPTION:  Friendly Wild / Domestic Animal, Mud Bath Lover
SIZE:        ~3 ft
ENDURANCE:   Average
HOSTILITY:   Low
WEAPONS:     None
REWARD:      35 FC
THREAT:      Minimal

DESCRIPTION:

This is not a separate species, but a state of mind. Any ordinary pig can become dirty if it finds a suitable puddle and spends a few seconds in it, diligently getting dirty.

After the procedure, almost her entire body is covered in dirt, especially her legs, and a flower sits on her head, mocking cleanliness.

Dirt can be washed off a pig in two ways: lure her into a lake or other body of water, or simply douse her with a bucket of water. After this, the pig returns to its normal, pink, and clean appearance-but not for long. If there's dirt nearby, she'll definitely return and repeat the procedure. Apparently, they enjoy it.

TIPS:

- Don't try to wash a dirty pig unless you want a normal pig. Dirt isn't a disease, it's a pig's way of life.
- Such a pig smells... well, like a pig in mud.
- If you breed pigs and want dirty offspring, just leave a puddle near the pen. They'll sort it out themselves.
