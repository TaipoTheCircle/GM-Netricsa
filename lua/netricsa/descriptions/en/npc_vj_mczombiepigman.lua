ZOMBIFIED PIGLIN SWORDSMAN FROM THE MINECRAFT NETHER


CLASS:       Zombified Piglin Swordsman
METABOLISM:  Pig-like humanoid, Undead
PERCEPTION:  Neutral Nether mob
SIZE:        6,3 ft
ENDURANCE:   Average
HOSTILITY:   Conditional
WEAPONS:     Golden Sword
REWARD:      300 FC
THREAT:      Average

DESCRIPTION:

A bipedal, zombified pig-like carcass, clad in scraps of once-golden armor.

This is what regular Piglin Swordsmen turn into if they remain in the Overworld or the End for more than 15 seconds. They were previously called Piglin Zombies, but that name was later changed.

Unlike their living relatives, these piglins don't care about gold. They don't care about anything as long as they're left alone.

They can also appear from regular pigs if they're struck by lightning. Or directly from Nether portals in the Overworld-the larger the portal, the more often.

Zombified piglins are immune to fire and lava-they burn but don't combust. However, they drown in water, as befits undead.

They heal from damaging potions and take damage from healing.

TIPS:

- Never strike first unless you're prepared to hit everyone. One hit and the whole pack is yours.
- Keep your distance. Up close, they swing their swords quickly and painfully.
- They're helpless in water.
