ICE AXE


WEAPON: Ice axe
AMMO:   None
ROF:    ~1 hit / second
WEIGHT: 1,2 kg

DESCRIPTION:

A melee weapon used for breaking through and destroying obstacles, as well as for defense in hand-to-hand combat. The ice axe has a high striking speed and can penetrate light structures and enemy armor. Its design allows for targeted strikes while maintaining the user's maneuverability.

NOTES:

- Effective against single opponents and lightly armored targets.
- Can be used to break ice and wooden barriers.
- Compact and lightweight design makes it easy to carry in challenging conditions.