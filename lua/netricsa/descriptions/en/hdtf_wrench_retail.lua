PIPE WRENCH


WEAPON:      Pipe Wrench
AMMO:        None
ROF:         Variable - quick swing (~60 swings/min) or charged strike (~20 swings/min)

DESCRIPTION:

Heavy-duty industrial pipe wrench repurposed as a melee weapon during the Black Mesa Incident. Delivers powerful blunt-force trauma with quick strikes or a devastating charged overhead blow.

NOTES:

- Primary attack: fast swings for moderate damage.
- Secondary attack: charged strike with increased damage and knockback.
- Silent weapon, useful for conserving ammunition.
- Ineffective against distant or heavily armored enemies.
- Durable and always available, making it a dependable last-resort tool.