PERCEPTION: Minimal sensory cycle
SIZE:       Variable (tied to the selected form)
ENDURANCE:  Very low
HOSTILITY:  None
WEAPONS:    None by default
REWARD:     ?
THREAT:     Low

DESCRIPTION:

An anomaly related to Generic Monsters and Generic-Actors an entity without a stable form of its own. The Cycler's existence consists of an endless simulation of other beings and objects: from holograms and inanimate objects to key individuals (the Alliance Advisor, Noriko, etc.). Its purpose is not direct attack, but infiltration, observation, and disorientation. When damaged or exposed, its form "loops" - it randomly cycles through its stored pose templates, unable to stabilize until it takes another hit or escapes.

TIPS:

- Check suspicious objects with a light strike or scan. The Cycler will reveal itself by malfunctioning.