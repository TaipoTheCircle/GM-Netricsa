ARBUZINO THE WATERMELON


CLASS:      Arbuzino The Watermelon
METABOLISM: Vegeterian
PERCEPTION: Genetically Programmed
SIZE:       7,5 ft
ENDURANCE:  Medium
HOSTILITY:  Medium
WEAPONS:    Chainsaw
REWARD:     2500 FC
THREAT:     Medium

DESCRIPTION:

This monster was genetically engineered by Mental one weekend when his time travelling agents returned with samples of 20th century media.  Among the items were a CD from a band whose name had something to do with destroying vegetables, a movie featuring Brut Renyolds and a grinning banjo player, and an old copy of 'Lumberjack Digest'. As he inserted the final, 1979th part into this abomination, it sprang to life and even the Notorius Mental faltered at the sight of this Watermelon with Gluttonous Things. Just a perfect example of Mental Rolling in his Infinite Badness.

TIPS:

- They are pretty dumb but dangerous when they start following you.
- More dangerous than pumpkin.
- Avoid them by sidestepping.
