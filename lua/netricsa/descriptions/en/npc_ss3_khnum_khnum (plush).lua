BLIND KHNUM FROM THE LAMBDA ARIES SYSTEM


CLASS:      Blind Khnum, Space Titan
METABOLISM: Alien hypertrophied life form
PERCEPTION: Poor vision, tactical thinking
SIZE:       45 ft
ENDURANCE:  Extremely high
HOSTILITY:  High
WEAPONS:    Plasma projectiles, crushing blows, ramming
REWARD:     10000 FC
THREAT:     Very high

DESCRIPTION:

A massive bipedal creature and a direct relative of the Barons Of Hell from a planet in the Lambda Aries system, whose race, believing themselves to be gods, voluntarily joined Mental's army, occupying high command positions.

Recognizable by his giant goat horns, thick gray skin, hooves instead of feet, and glowing eyes, his appearance may have inspired the ancient Egyptian god Khnum.

He possesses high intelligence and tactical skills, often commanding squads of smaller enemies. In combat, he combines powerful ranged attacks, specifically throwing blobs of plasma that are impossible to shoot down in mid-air, with deadly close-range attacks.

TIPS:

- His fireballs deal colossal damage, and he is nearly invulnerable at close range.
- Completely immune to bullets. Use only explosive weapons, such as C4 or a flare gun.
- Predict the trajectory of his fireballs and dodge at the last moment.