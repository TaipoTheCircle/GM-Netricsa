MANCUBUS, BEHEMOTH FROM THE HELL


CLASS:      Mancubus, Behemoth
METABOLISM: Demonic Mammal
DEFINITION: Weakened Personification of Gluttony
SIZE:       13 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Two RPGs
REWARD:     1000 FC
THREAT:     Medium

DESCRIPTION:

This is a weakened Demon, personifying human imperfection and greed, gluttony, and idleness. It has incredibly thick skin, massive elephantine legs, and a small head. For the purpose of destroying and exploding its victims, two RPGs are implanted in the Mancubus's forearms, firing rockets.

Mental bought them from the Lord of Hell, along with Zombie Soldiers and other undead, to strengthen his Horde.

The Behemoth's eyes aren't green like those of regular Mancubuses, but bright red, burning with the fire of hatred.

His skin has a blue tint, but instead of red blood, a toxic green substance flows from his veins-a sure sign of a failed ritual.

Instead of built-in flamethrowers, the Behemoth has rocket launchers attached to its elbows, replacing its limbs. Unlike the regular Mancubus, which fires fireballs, the Behemoth fires two rockets at a time. These rockets fly until the target is destroyed.

The Behemoth itself is significantly weaker in defense than the classic Mancubus. A couple of shotgun blasts can send it back to hell.

TIPS:

- Despite its size, the Behemoth isn't particularly durable.
- Two point-blank shots and the demon will be nothing but a wet spot.
- Avoid the missiles and don't stand in the open.
