KUNG FU ZOMBIE, MASTER FROM THE CHI-FANG


CLASS:      Kung Fu Zombie, Master
METABOLISM: Undead
PERCEPTION: Zombie Martial Artist
SIZE:       12 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Spirit Summoning
REWARD:     150 FC
THREAT:     High

DESCRIPTION:

While most of Mental's minions receive minimal training, Kung Fu Zombies have received extensive training from watching action films from the 80s and 90s.

Some of these zombies have mastered the art of kung fu and learned the ways of magic. Masters can be seen hovering in the air, summoning dragon spirits to destroy their enemies. These spirits will pursue their master's enemy and explode, releasing magical energy upon contact.

The Masters were originally actors on the planet Chi-Fang. With the arrival of Mental, the Masters proved themselves to be reliable fighters, but Mental decided to increase their power. The Masters were transformed into deadly flying opponents: their legs were severed (which were then given to the Bull Soldiers) and they were given the ability to cast deadly dragon magic spells (levitation, creating a force beam of energy).

Unfortunately, these changes gave the Masters a strange urge to constantly sing. They sang poorly. In fact, they were disgusting. Because of this flaw, Mental had to attach steel masks to their faces so that no one could hear their singing and it wouldn't damage his hearing.

TIPS:

- If they get close enough, smash them into the wall with a double-barreled shotgun.
- They'll try to keep their distance so they can safely summon spirits to attack.
- The spirits will pursue you, so keep moving if you want to avoid them.
