COMBINE OVERWATCH ANTI-VEHICLE SOLDIER


CLASS:      Combine Overwatch Anti-Vehicle Trooper
METABOLISM: Augmented Human, Male
PERCEPTION: Specialized Anti-Vehicle Unit
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Very High
WEAPONS:    Enhanced SMG, Rocket Launcher, Anti-Vehicle Missiles
REWARD:     750 FC
THREAT:     High

DESCRIPTION:

The Overwatch Anti-Vehicle Trooper is virtually indistinguishable from a standard Combine trooper. Almost the same armor, the same posture, the same cold blue visor. A huge mistake - mistake him for "ordinary." His true role is revealed only when vehicles appear on the field.

A rocket launcher is mounted on the soldier's back, and it can be retrieved instantly as soon as the system recognizes a target in a vehicle. Vehicles, armored vehicles, and improvised armored vehicles of the resistance are all his top priority.

The upgraded submachine gun is used for self-defense and infantry suppression while soldiers take up positions for attack vehicles. However, protracted firefights are no problem for him. He acts quickly, hits hard, and tries to change position immediately after launching a rocket, knowing that return fire will follow immediately.

The Combine uses these soldiers to neutralize resistance mobility. One anti-Vehicle soldier reduced a convoy of vehicles to flaming scrap metal in a matter of seconds.

TIPS:

- Never approach him in a vehicle.
- Kill him first, then use the vehicle.