ENGEL FROM FUNDAMENTAL PAPER EDUCATION


CLASS:      Engel, Student of the Paper School
METABOLISM: Paper anthropomorphic organism
PERCEPTION: Vision, heightened awareness of friends
SIZE:       5 ft
ENDURANCE:  Average (stronger than standard students)
HOSTILITY:  Low (only attacks in defense)
WEAPONS:    Sharp claws (4 pointed fingers), physical strength
REWARD:     2
THREAT:     Average

DESCRIPTION:

Student of the Paper School. Childhood friend and protector of Claire. Distinguished by his long, white, tousled hair, pulled back into a low ponytail, with two feathers (blue and red-orange) on his head. He wears the classic school uniform: a white shirt with striped sleeves, black overalls, and striped leg warmers. His hands are black, with four sharp, claw-like fingers, and he also has a small white tail. He is kind, caring, and selfless by nature, willing to risk himself for others, as demonstrated by directly confronting Miss Circle to save Claire. He possesses enhanced physical strength and resilience compared to normal students (he withstood a teacher's punch).

He has the closest bond with Claire (childhood friends). He is also friends with Bubble. He likely respected Oliver as a child, but they are now enemies due to their different worldviews.

TIPS:

- He is the most physically capable of the known students.
- His main driving force is protecting Claire.