SNOWBALL


WEAPON:      SNOWBALL
ROF:         ~1 throw/sec.

DESCRIPTION:

Lumps of snow that have been molded together by hand. Snowballs can be thrown at anything and anywhere. They say that if you add a stone or yellow snow inside, you can make the snowball more dangerous.

NOTES:

- Useless item, can only be used to paint players and walls.
- But if a player is in ragdoll mode, you can break their neck with a snowball (apparently, this may have been fixed).