ARCTIC RESISTANCE REBEL, FEMALE


CLASS:      Arctic Resistance Fighter, Female
METABOLISM: Human, Female (trained in basic combat and tactics)
SIZE:       5,9 ft
ENDURANCE:  Medium (light armor or improvised protection)
HOSTILITY:  High (towards Alliance forces)
WEAPONS:    Pistol, SMGs, SPAS-12 Shotgun, "Annabelle" Shotgun, Crossbow, Grenades, RPG V2, AR2, AR1
REWARD:     200 FC
THREAT:     Medium

DESCRIPTION:

Arctic Rebels are members of the human Resistance, organized to fight the Alliance occupation. These are ordinary people, quickly trained in the use of firearms and coordinated attacks, often improvising tactics in urban and rural combat zones.

They operate in squads, providing cover for civilians, securing supply routes, and providing critical support to key operatives like Gordon Freeman or Alyx Vance. Although less advanced than Alliance forces, their adaptability, morale, and numerical superiority make them formidable opponents.

The Rebels rely on teamwork and hit-and-run tactics rather than brute force. Their resilience stems from a dedication to human survival, not superior weaponry. They can often be seen manning vehicles like the Merkava tank or assisting scientists and medics on the battlefield.

TIPS:

- Protect them - they are most effective in groups. - Supply them with ammunition and first aid kits to maintain pressure on the front lines.
- Use them to scout the area and ambush Alliance patrols.
- Rebel squads can crush even lightly armored Alliance units if they coordinate well.