SLEDGEHAMMER


WEAPON: Sledgehammer
AMMO:   Not required
ROF:    Depends on attack type
WEIGHT: 13 lbs

DESCRIPTION:

A powerful melee weapon and a universal tool for "opening" barricades and enemy skulls. Features three attack types: overhead smash, wide swing, and a spinning attack effective against surrounding hostiles.
Deals significant damage at close range, capable of eliminating weak enemies in a single hit and conserving ammunition.

NOTES:

- Effective against hordes of Gnaars, Hatchling Spiders and Kleer Skeletons.
- The spin attack is invaluable when surrounded.
- Not recommended for use against enemies that explode upon death.