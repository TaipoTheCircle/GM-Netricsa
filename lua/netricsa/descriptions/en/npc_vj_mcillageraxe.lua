VINDICATOR FROM THE MINECRAFT OVERWORLD


CLASS:      Vindicator
METABOLISM: Malevolent villager, bandit
PERCEPTION: Vicious melee fighter, heavy infantry of the Illagers
SIZE:       6,2 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Iron Axe
REWARD:     200 FC
THREAT:     Very High

DESCRIPTION:

This gray-robed psychopath proves that villagers can be not only useful but also deadly. These are "evil villagers", or, as they are officially called, Illagers. While a normal villager trades with you and builds farms, a Vindicator simply wants to chop you in half with an axe. And he doesn't care that you're just visiting.

These dark figures first appeared as guards of the Forest Mansions, where they still haunt certain rooms. Later, they were spotted on bandit patrols and, most disturbingly, raiding villages.

The Vindicator wears a dark jacket, pants, and boots. He has a serious and angry expression, reminiscent of his kinship with the villagers. When idle, he stands with his arms crossed over his chest, just like his peace-loving cousin. But as soon as he spots an enemy (you or a villager), he draws his iron axe and charges.

The Vindicator's main characteristic is incredible aggression and speed. He runs toward his target faster than the player's walking speed, delivering devastating blows with his axe. This axe isn't just dangerous—it can penetrate shields, disabling them for 5 seconds.

Unlike their Forsworn relatives, Vindicators can break down wooden doors during raids on normal and hard difficulties (like zombies, only faster).

TIPS:

- If you're unarmored, don't approach. One axe strike can be fatal.
- Use a bow or crossbow. Keeping your distance against a Vindicator is the best strategy.
- Don't rely on your shield: its attack will penetrate it, and you'll take damage.
- In the Woodland Mansion, watch the corners. Vindicators often lie in ambush in small rooms.
- If you see a Vindicator named Johnny, immediately run in the opposite direction. He will destroy not only you, but also all other mobs in his path.
