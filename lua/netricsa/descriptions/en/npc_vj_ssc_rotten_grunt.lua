ROTTEN ZORG FROM BEELMEZ IV


CLASS:      Rotten Zorg
METABOLISM: Mammal
PERCEPTION: Warrior Race Veteran
SIZE:       6,5 ft
ENDURANCE:  Low
HOSTILITY:  Extreme
WEAPONS:    SBC Cannon
REWARD:     700 FC
THREAT:     High

DESCRIPTION:

Mental transported these soldiers to Earth from an unknown part of the universe.  They were once a race of fearsome warriors but as they conquered more planets than they possibly knew what to do with, they decided to slow down for a while.  Most of the population took a long overdue vacation on one of the most beautiful tropical planets in their system; but as they only sunbathed from 10A.M. to 4P.M., after a few years most of them had no skin left on their bodies.

After a few years, the effects became irreversible. The skin literally rotted, the tissue began to decompose, and the once-mighty warriors were reduced to walking, half-decayed carcasses.

Any sensible commander would have written such fighters off. But Mental was never known for his common sense.

Having discovered an entire army of half-decayed veterans, he decided to bring them back into service and armed them with a heavy SBC Cannon. This powerful weapon fires a massive cannonball capable of destroying virtually any target with a single hit. The only problem is that the Rotten Zorgs themselves are far from perfect in wielding this weapon. It's not uncommon for a misplaced shot to result in the death of the shooter.

Despite their horrific condition, these creatures have retained the combat experience of dozens of military campaigns. They are slow, easily mortally wounded, and significantly less durable than even Mental's ordinary mercenaries, but a single well-aimed hit from their cannon can instantly change the outcome of a battle.

Ironically, these decayed veterans are among the oldest soldiers in Mental's army. They participated in the Horde's first conquests long before the invasion of Earth and were transferred here from a distant, unknown sector of the galaxy.

TIPS:

- Don't stand in the way of the cannonballs.
