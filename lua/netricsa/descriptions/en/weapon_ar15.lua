AR-15 SEMI-AUTOMATIC RIFLE


WEAPON:     AR-15
AMMUNITION: 5.56x45 mm
ROF:        ~950 rounds / min

DESCRIPTION:

AR-15 is the name of a family of semi-automatic rifles based on the Colt AR-15 design or similar. Originally, the ArmaLite AR-15, its predecessor, was a scaled-down derivative of Eugene Stoner's ArmaLite AR-10 design and had a fire selector. ArmaLite sold the patent and trademarks to Colt in 1959, resulting in the Colt AR-15, without the fire selector. After most of the patents on the Colt AR-15 expired in 1977, many firearms manufacturers began producing copies of this rifle under various names. Until the patents expired, Colt retained the AR-15 trademark and was the only manufacturer that could label its firearms as "AR-15".

