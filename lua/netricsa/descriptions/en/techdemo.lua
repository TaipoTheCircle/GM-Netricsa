BLACK MESA TECHNOLOGY CENTER, CORRIDOR OF SCIENTIFIC MADNESS


Sam, we're in a place that, even by Black Mesa standards, seems strange. A long corridor. With a curve. And doors. Many doors. Behind each one, a laboratory. Small, but each with its own theme. Its own obsession. Its own little catastrophe, waiting to be unleashed.

We walk down the corridor. A door on the left. A door on the right. At the end, a turn, and beyond that, more doors. No windows. No signs, except for the plaques on the doors themselves. Black Mesa studied here everything that didn't fit in other sectors. Everything that seemed too useless for the core budget, but too interesting to throw away.

What's behind the doors?

Colored light. A laboratory where they studied the effects of different wavelengths on the psyche. The results are probably in safes somewhere. Or on the walls—in the form of stains left by those who stared too long.

Droid samples. Robot test units. Some are just blanks. Some are powered.

Transparency. Research into materials that let light through but not bullets. Or vice versa. Or let nothing through except fear.

Pantherai. A living creature was discovered in one of the rooms. Not a specimen. Not an exhibit. Just a pantherai. Where did it come from? Why was it brought here? Why is it still here? There are more questions than answers. The answer is simple: don't enter there unarmed.

PolyRobot Zentraedi. Tactical combat module. Created here. Tested here. Probably deemed a success. Or not. But the module is out there somewhere, behind one of the doors. Waiting. Watching. Listening. And Dancing...

Don't open all the doors. If the sign looks threatening, it probably is. If the sign looks harmless, it's probably a trap.
