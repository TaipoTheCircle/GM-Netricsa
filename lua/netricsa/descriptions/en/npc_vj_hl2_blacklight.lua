COMBINE CIVIL PROTECTION BLACKLIGHT COP


CLASS:      Combine Civil Protection Blacklight Cop
METABOLISM: Human under Combine control
PERCEPTION: Special police suppression unit
SIZE:       5,9 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Revolver, SMG, Manhacks
REWARD:     400 FC
THREAT:     Medium

DESCRIPTION:

The Combine Civil Protection Blacklight Cop is a specialized urban enforcer designed to operate in contaminated, chemically hazardous, and unstable areas. His full protective suit emphasizes not so much elitism as functionality and survivability in conditions where the average metro cop wouldn't last long.

The helmet completely covers the face and is equipped with a bluish visor with a V-HUD system. Markings such as "V-HUD", "PREX INDUSTRIAL", and "ARAVO-08" can be seen on the surface, indicating the equipment's industrial origin and experimental nature. A built-in respirator filters the air, protecting the wearer from gas, spores, and Blacklight radiation, hence the unit's name.

The black body armor with white trim bears the marking "C17:i4o", directly linking the soldier to City 17 and the Combine's internal security forces. The outfit is complemented by dark pants, black boots, and gloves, and a holster is always present on the belt for rapid response.

In combat, the Blacklight Cop acts harshly, but not in a military-like manner. The revolver is used for targeted intimidation and executions, the SMG is for crowd control, and the Manhacks are deployed in confined spaces, turning streets and entryways into chaotic death zones. These cops often work in teams, clearing out neighborhoods quietly... by Combine standards.

TIPS:

- Don't underestimate him because of his "cop" status-Manhacks quickly change the tide of battle.
- The visor protects against blindness, but not against direct hits.
- In confined spaces, the priority is to kill him before the Manhacks are deployed.
