COMBINE OVERWATCH "SKULLPIECE" SOLDIER


CLASS:      Combine Overwatch "Skullpiercer" Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: All-around Combine Assault Executioner
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, SPAS-12 Combat Shotgun, Grenades
REWARD:     1200 FC
THREAT:     Very High

DESCRIPTION:

The Combine Skullpiercer Soldier is a rare and extremely nasty hybrid Assault / Exorcist unit in the Combine Overwatch ranks. Unlike most units, which are strictly specialized for a single role, the Skullbreakers undergo extensive combat training, allowing them to operate equally effectively at medium range with an AR2 and at point-blank range with a SPAS-12 shotgun.

Their swamp-colored gear with dull blue Overwatch accents is designed for operations in industrial ruins, contaminated areas, cities, swamps, and underground sectors, where standard armor quickly reveals their position. Orange visors emphasize their status as assault suppression units.

TIPS:

- Dangerous at any range.
- Grenades are used for knocking out cover, not for finishing off.
- Orange visor is visible in the dark.