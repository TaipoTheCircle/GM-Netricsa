GLOCK 18C PISTOL


WEAPON:     Glock 18C
AMMUNITION: 9X19
ROF:        1200 rounds / min

DESCRIPTION:

The Glock 18C pistol is an Austrian automatic pistol manufactured by Glock GmbH in 9Ã—19 mm Parabellum caliber. It is designed for use by special army and police units. The Glock 18 attracted the interest of police and military structures, but it did not gain the expected popularity due to the low effectiveness of automatic fire compared to even fairly compact submachine guns. In general, the Glock 18 pistol inherited all the advantages of the basic Glock 17 pistol and is very similar in design. One of the differences is the fire mode selector lever on the left rear of the slide. In popular culture, models presented as Glock 18s are often actually converted (with automatic fire capability) Glock 17s, usually with a 17-round magazine. From a gaming perspective, this pistol is very popular.

NOTES:

