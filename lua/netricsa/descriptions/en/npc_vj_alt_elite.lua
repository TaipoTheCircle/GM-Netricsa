ALTERNATIVE COMBINE OVERWATCH ELITE SOLDIER


CLASS:      Alternative Combine Overwatch Elite Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Elite Shock Trooper
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, Energy Balls
REWARD:     900
THREAT:     Very High

DESCRIPTION:

The Alternative Overwatch Elite Soldiers are elite soldiers who choose not to show off. Unlike their standard white-armored counterparts, these soldiers wear dark blue overalls, visually almost indistinguishable from the uniform of regular Overwatch soldiers. The only obvious distinguishing feature is their cold blue visor.

While seemingly an ordinary soldier, he's actually a fully elite fighter with the same implants, reflexes, and tactical training. He often operates as part of standard units, quietly boosting their effectiveness to an unnerving level.

TIPS:

- Often works in tandem with regular troops, boosting them to elite levels.
- Blue visor can reveal their position in low-light conditions.