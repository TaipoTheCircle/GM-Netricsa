FASTER AUNT FROM THE FAVERSHOLM


CLASS:      Faster Aunt
METABOLISM: Nightmare Spawn
PERCEPTION: Ambush Killer
SIZE:       5,6 ft
ENDURANCE:  Average
HOSTILITY:  Absolute
WEAPONS:    Prosthetic Blades for Arms and Legs
REWARD:     250 FC
THREAT:     High

DESCRIPTION:

This is one of the most striking and frightening monsters, first encountered in a residential building.

She is a disfigured woman in a blue, blood-stained dress. She has no eyes or mouth-in their place are gaping black holes. She has no arms or legs as we know them; in their place are long, sharp metal spikes. With these blades, she slashes and stabs her victims. Because the legs have similar appendages, a distinctive metallic clanking sound is heard when walking.

The Quick prefers to operate from ambush. It hides in dark corners or behind obstacles, only to suddenly leap out, break down the door, and attack. When attacking, it screams loudly, attempting to intimidate. It runs rather slowly, but the suddenness and screams are disorienting.

When killed, the Quick doesn't simply fall. It impales its neck with blades, emitting a painful, mournful cry, and falls to the ground.

Like most monsters, the Quick symbolizes injury. The metal spikes instead of limbs represent a metal wheelchair.

It represents people in the environment who have been a source of irritation and pain. The monster's act of suicide upon death is a direct parallel to suicidal tendencies.

TIPS:

- Be careful in long corridors with many doors and dark corners. The Fast One likes to jump out suddenly.
- The monster runs quite slowly, but the screams and suddenness make it easy to panic. Don't panic.
- Hitting the Fast One is difficult due to its speed. The best tactic is to wait until it tries to attack, then back away and start firing with good aim.
- You can try hand-to-hand combat: run up, hit, then run away. But it's risky.
- If you hear a metallic clang and a piercing scream, it's nearby. Ready your weapon and look around.
