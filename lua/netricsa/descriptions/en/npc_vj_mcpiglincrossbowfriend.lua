PIGLIN CROSSBOWMAN FROM THE MINECRAFT NETHER


CLASS:      Piglin Crossbowman
METABOLISM: Pig-like humanoid
PERCEPTION: Primitive warrior race of the Nether
SIZE:       6,3 ft
ENDURANCE:  Average
HOSTILITY:  Conditional
WEAPONS:    Crossbow
REWARD:     450 FC
THREAT:     Average

DESCRIPTION:

Piglin Crossbowmen make up exactly half of the adult piglin population - the other half prefers golden swords and close combat.

Like all their kind, they are obsessed with gold to the point of identifying friend or foe solely by the presence of golden armor on their bodies.

Appearance: A crossbowman differs from a swordsman only in their weapons and tactics. Instead of rushing into the melee, he keeps his distance and fires crossbow bolts every two seconds. He backs away, trying not to let his target get too close, but if he hits a wall or cliff edge, he continues firing.

Unlike the Vindicator bandits, crossbowmen don't preload their weapons; they reload only after they enter combat.

Crossbowmen live in the same places as other folk—in crimson forests, Nether wastelands, and especially in bastions, where they guard treasures from any intruders. They fear the blue fire of souls, zombified brethren, and zoglins, but this cowardice instantly vanishes as soon as they spot an enemy.

If a group of crossbowmen manages to bring down a large target, they gather at the scene of the kill and hold a small celebration with characteristic grunting cries. They especially love to dance after a successful hoglin hunt.

TIPS:

- Wear golden armor. One detail and they won't touch you.
- There are many of them in the bastions, and they don't like it when their golden treasure is touched.
