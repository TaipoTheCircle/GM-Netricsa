CHOPPER FROM THE MOBIUS


CLASS:      Chopper
METABOLISM: Hydro-Animal-Battery
PERCEPTION: Ambush Perimeter Guard
SIZE:       4 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Hull, Jaws
REWARD:     300 FC
THREAT:     Low

DESCRIPTION:

When Dr. Eggman faced the problem of guarding bridges and waterways, he turned his attention to piranhas. Their lightning-fast attacks from the water became the basis for the Chopper project. The first models were assembled from literally whatever was floating around: the hull of a red parrotfish, fins from an old fan, teeth from a garden rake, and the motor from a Mriya coffee grinder.

Chopper is the only Badnik that doesn't pursue its target, but waits patiently underwater. It hangs in the water, baring its teeth, watching the surface. As soon as a silhouette appears over the bridge, Chopper leaps straight up, aiming straight for the hapless traveler's stomach. If the attack fails, it splashes back down and tries again after a while. In later modifications, it learned to swim horizontally and even set up group ambushes.

An oily liquid sloshes inside its hull, replacing its brain. The only command stored in its memory is: "If it's dark above, bite".

TIPS:

- Attack as it leaps out, while it's suspended in the air.
- Don't stand still when crossing the bridge - Chopper aims for the spot you were a second ago.
- Some models are capable of pursuing targets underwater.
