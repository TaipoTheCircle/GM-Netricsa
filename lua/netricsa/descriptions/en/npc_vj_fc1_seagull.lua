SEAGULL


CLASS:      Wild Avian
METABOLISM: Bird
PERCEPTION: Moderate (scavenger instincts, good eyesight)
SIZE:       ~1.5-2 ft
ENDURANCE:  Low
HOSTILITY:  Low
WEAPONS:    Beak, claws
REWARD:     1 FC
THREAT:     Very Low

DESCRIPTION:

Seagulls frequent coastlines, rivers, and waterways, scavenging for food near human activity. They are mostly passive but can react defensively if approached too closely. Their presence adds realism to water-adjacent areas.

TIPS:

- Observing seagulls may indicate nearby water or food sources.