ARMORED OFF-ROAD VEHICLE Y.S.M. HMMWV, BLACK


CLASS:      Armored Off-Road Vehicle Y.S.M. HMMWV, Black
METABOLISM: Mechanical, Operated By Mercenary
PERCEPTION: Vehicle, Armored Capsule
SIZE:       6 ft
ENDURANCE:  High
HOSTILITY:  Driver dependent
WEAPONS:    Ram
REWARD:     1000 FC
THREAT:     Medium

DESCRIPTION:

This is an armored capsule for the safe delivery of mercenaries to their destination. Unlike military versions, which are armed with machine guns and rocket launchers, this version is a standard military SUV, reinforced with armored panels and bulletproof glass.

The vehicle is unarmed. None at all. No machine gun on the roof, no grenade launcher on the turret. Y.S.M. drivers prefer to rely on armor and speed, and if a threat arises, they simply run over enemies with their wheels. The HMMWV moves slowly - the armor is heavy, and the engine isn't rubbery. But it's enough to get from point A to point B.

The vehicle has two rows of seats: the driver and passenger in front, and up to four mercenaries in full combat gear in the back. The windows are armored, but tinted, so visibility from inside is almost impossible.

At Delta Base, Y.S.M. uses the HMMWV for perimeter patrols, supply deliveries, and, of course, for robbery - there's not always room for the vehicle inside, so you have to drag the goods to the vehicle by hand and then load them.

TIPS:

- Unarmed, the HMMWV is harmless unless you stand in its way. Don't stand in front of the vehicle - it will run you over.
- The armor is strong, but the underbody is vulnerable to explosives. A grenade under the wheels and the car turns into a pile of metal.
- The driver doesn't get out to shoot.
- On a dark road, the HMMWV is easy to spot by its headlights.
