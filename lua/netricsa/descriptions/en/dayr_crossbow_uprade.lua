IMPROVED SURVIVAL CROSSBOW


WEAPON: Improved Survival Crossbow
AMMO:   Crossbow Bolts
ROF:    ~1 shot per second

DESCRIPTION:

An upgraded version of the standard crossbow, offering increased power and more consistent effectiveness in combat. Its reinforced design allows for greater damage without significantly increasing the difficulty of use.

The weapon remains simple and reliable, but is more effective against tougher opponents. With the appropriate skill, it can use poisoned bolts, dealing additional damage over time.

NOTES:

- Deals increased damage compared to the standard crossbow.
- Can use poisoned bolts, which deal damage over time.
- Effective at close to medium range.
- Ammunition is easily crafted from scrap materials.
