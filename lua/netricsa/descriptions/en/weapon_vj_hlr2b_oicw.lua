OICW ASSAULT RIFLE


WEAPON:      Objective Individual Combat Weapon (OICW)
AMMO:        5.56x45mm rounds / 20mm grenades
ROF:         ~650 rounds / min (primary), 1 grenade / 2 sec (secondary)

DESCRIPTION:

Prototype assault rifle combining rapid-fire ballistic rounds with an integrated grenade launcher. Intended for Combine overwatch troops as a next-generation combat weapon. Provides both precision automatic fire and explosive support, allowing soldiers to adapt instantly between ranged suppression and anti-armor engagements.

NOTES:

- Primary fire: automatic 5.56mm rounds, accurate at medium range.
- Secondary fire: underbarrel 20mm grenades with high explosive damage.
- Designed as an all-in-one infantry weapon.