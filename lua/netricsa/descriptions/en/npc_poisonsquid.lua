TOXICSQUID


CLASS:      Bullsquid Mutant, Acid
METABOLISM: Omnivore (acidic digestion)
PERCEPTION: Land Predator Of Xen
SIZE:       8 ft
ENDURANCE:  High
HOSTILITY:  Very High
WEAPONS:    Acid bile clouds, corrosive bite, tail smash
REWARD:     550 FC
THREAT:     Very High

DESCRIPTION:

The Toxicsquid emits green acid clouds that linger in the air, suffocating and burning anyone inside. Its saliva melts through steel, and its thick hide resists most conventional bullets.

TIPS:

- Gas masks or hazmat suits recommended.
- Explosives are best to avoid close combat.