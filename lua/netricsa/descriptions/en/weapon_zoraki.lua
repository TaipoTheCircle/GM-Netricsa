ZORAKI STALKER M906 SIGNAL PISTOL


WEAPON:      Zoraki Stalker M906
AMMUNITION   9MM PAK cartridges 
ROF:         ~15 rounds / min

DESCRIPTION:

Self-loading signal pistol from the Turkish brand Zoraki. Suitable for self-defense, scaring away wild animals, and launching fireworks (using a rocket launcher attachment).

NOTES:

- Not a combat pistol and can only kill at point-blank range.
- Can blind a person if 9MM PAK Flash Defense cartridges are inserted into the magazine.
