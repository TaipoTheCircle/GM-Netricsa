PINK FOOTED PIG FROM THE MINECRAFT OVERWORLD


CLASS:      Pink Footed Pig
METABOLISM: Warm-blooded Mammal
PERCEPTION: Friendly Wild / Domestic
SIZE:       3 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     35 FC
THREAT:     Minimal

DESCRIPTION:

One of the most stylish variations of the standard pig. Pigs are diverse and unique. This one has pink foots.

This pig's appearance is truly remarkable. The main body is dark, almost black, with a small light gray spot on the left side of the belly. The legs are bright pink, separated by light gray areas at the knees. The ears are dark, the snout is light gray, and the snout itself is pink. It looks as if the pig dipped its feet in pink paint when no one was looking and now proudly shows off its manicured features.

Behaviorally, it's no different from a normal pig: it wanders the world, avoids lava and cliffs, and when damaged, runs away for a few seconds before returning to its usual routine.

TIPS:

- This pig's value lies in its appearance.
- Breed these pigs to obtain offspring with the same unusual coloring.
- Riding a saddle, this pig looks especially striking-its pink legs contrast with its dark body.
- If you encounter one in the wild, consider yourself lucky.
