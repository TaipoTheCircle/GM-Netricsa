THE HALF-QUAKE SHOW, BEGINNING


So, Sam. The first test.

Or, as they like to say here, the first "torture."

For the show, you're no longer a hero, a soldier, or even a timeline error. You're the Victim. That's how the announcer introduced you, with the same tone you'd hear when announcing a commercial or a weather report.

The test begins with a series of rooms. Simple. Straightforward. Deadly honest.

In the first room, the Vortigaunts are released. No frills. A classic. The rules of the show haven't been explained to them, but that's not necessary. Their job is to be an obstacle. Yours is to move on and not dwell on humanistic musings.

The next room is more interesting. Spikes. A button. A door. To get through, you need to step inside and press the button. The door will open. The spikes will move toward the button. Right toward you. The organizers' plan is clear: give them hope, and then test how fast you can run back to the previous room and dive through an open door. Timing is everything. A mistake is also decisive, but this time it's final.

Next up is a lava pool. Three slabs. Jumpable. As soon as you step off a slab, it collapses. The show loves movement and discourages doubt. Linger, and you'll become part of the decor.

Then more spikes. Because tradition must be respected.

The elevator will take you up one floor, where a room full of turrets awaits. Yes, those very same ones. Kindly purchased from Black Mesa. Business is business.

Somewhere among this splendor is a button hidden. Find it, and you'll be able to move on. But not directly. You'll have to walk along a thin ledge to the door. It's mined. You can approach it, but you can't enter it. You'll need to turn around and go back. Then another passage will open. Elsewhere. Logical? No. Effective? Absolutely.

And, of course, all of this will be generously seasoned with Mental's troops. Because the show must evolve. And viewers like it when the stakes are raised.

Remember, Sam.

They want you to fail.

They're waiting for you to break.

And you just move on.

The test has only just begun.