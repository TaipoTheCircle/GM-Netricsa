ANTI-NEXTBOT LASER V1


WEAPON: Anti-Nextbot Laser V1
AMMO:   AR2 Pulse Energy / Laser Charges
ROF:    Continuous Beam

DESCRIPTION:

An upgraded version of the AR2 pulse rifle, specifically adapted for combating Nextbots. It features a target prioritization system, allowing for the effective destruction of anomalous entities without unnecessary environmental destruction.

Mode 1 activates a focused laser that instantly destroys all living and hostile targets, completely ignoring props and environmental objects. This makes it ideal for pinpoint clearing in confined spaces.

Mode 2 disables filtering and allows damage to both enemies and props. It is used against resistant or unconventional Nextbots when brute force and complete area destruction are required.

NOTES:

- Mode 1: Destroys targets, ignoring props and environmental objects.
- Mode 2: Deals damage to everything, including objects and cover.
- Highly effective against standard Nexbots.
- Mode 2 is environmentally hazardous.
- Effective at medium and long range.
