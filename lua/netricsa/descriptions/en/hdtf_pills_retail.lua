PAINKILLERS


ITEM: Painkillers (consumable tablets)
USE TIME: full benefit delivered

DESCRIPTION:

When taken, painkillers provide rapid health recovery: a portion of health is restored instantly. Intended as an emergency field treatment when a medical kit is unavailable. They do not repair lost limbs or severe internal trauma - they function as temporary medical support.

NOTES:

- Instant heal.
- Do not cure poisoning / infection / radiation - specialized meds required.