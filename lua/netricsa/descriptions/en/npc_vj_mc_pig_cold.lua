COLD PIG FROM THE MINECRAFT OVERWORLD


CLASS:      Cold Pig
METABOLISM: Warm-blooded, cold-resistant
PERCEPTION: Wild / Domestic Animal
SIZE:       3 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     30 FC
THREAT:     Low

DESCRIPTION:

This is a rare variation of the common pig, adapted to life in Coldy biomes. Unlike their pink, temperate counterparts, these pigs have developed a thick coat of white or light gray fur that protects them from the cold.

They peacefully wander through the Cold in groups of 2-4, grunting, digging through Colddrifts in search of edible food, and are completely non-aggressive. As is typical with pigs, they don't attack even when wounded, preferring to simply flee and become kin to the world's injustices.

Because they live in cold regions, Cold pigs are slightly more cold-hardy than their logical relatives - they don't take damage from Cold and can safely walk across icy biomes without risking freezing.

They can be bred, saddled, and used as a source of pork and leather.

TIPS:

- Use carrots on a stick to steer a Cold pig on a saddle - this is more convenient in the Cold than running.
- Breed them with carrots, lettuce, or candles for a constant food source in cold biomes.
