DESCRIPTION:

The SSS Centerprice is a Sirian spacecraft. Several thousand years ago, the Sirians used the SSS Centerprice as one of their primary means of transportation during their interstellar travels. Eventually, they reached the Solar System and discovered Earth. They used the ship to visit Earth and fly over the Great Pyramid in Egypt whenever the Egyptians participated in their religious rituals.

Shortly after the Sirians learned of Mental's attack on Sirius, the survivors took the Centerpice back into space and abandoned it on Saturn's moon Titan as a temporary measure. They believed that humans were too primitive at the time to master space travel, so they would abandon the ship until humanity had advanced enough technologically to find it and Mental had gone dormant.

The Sirians created another ship after you failed to live up to expectations and lost the first one, crashing it during a mission, but now it's aimed at eliminating you.

For the Sirians, this wasn't just some "accident," but an insult to their technology.

