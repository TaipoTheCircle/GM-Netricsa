BRONZE BUG FROM ZENOZOIK


CLASS:      Bronze Bug, Forest Predatory Bug
METABOLISM: Insect, giant xenomorph
PERCEPTION: Faceted vision, sensitivity to vibrations and heat
SIZE:       1,9 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Powerful, long, curved mandibles capable of cutting flesh
REWARD:     80 FC
THREAT:     High

DESCRIPTION:

A wild giant Bug, resembling a terrestrial bronze Bug, but significantly larger and more aggressive than its cousin. It has a dark brown or violet-blue chitinous carapace, a pair of transparent wings, long thin legs, and is covered in fine hairs on its head. Their main distinguishing feature is their enormous, protruding, horny mandibles, capable of easily slicing through prey. Their native habitat is the forests where Corvids live, but these Bugs are capable of migrating to other locations, including deserts. They exhibit inexplicable selective aggression, attacking exclusively Geth and ignoring all other creatures. They are not trained pets. They always act in a pack, coordinating aerial attacks.

TIPS:

- A regular net won't work; grab a weapon and shoot at them; they're more maneuverable in flight.
- They always attack in a pack; try to always move away from them and avoid them.