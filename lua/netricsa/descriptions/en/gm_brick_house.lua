A BRICK HOUSE, WASTELAND AND TREES


Sam, this place smells like a stalker who decided to settle down and stock his closet for the nuclear winter. Before us is a lonely brick house, standing on green grass, as if it was simply placed there and forgotten.

There's no city nearby, no neighbors. Just 11 trees.

The house is sturdy, red brick, the roof is in place. But there are some nuances:

There's no door. At all. Just an opening, gaping with a black void. Either they removed it, or it was never there.

Two windows on either side of the entrance.

To the right are three cars. They used to be able to drive away, but now they're just rusty boxes without wheels. They stand there as a reminder: there's no escape from here.

Inside is a warehouse, not a home. Someone was clearly preparing for the end of the world:

Two lamps hanging from the ceiling. The light is just enough to see what's inside.

Five drawers: one large, four small. Sealed tight. They don't open, don't break, don't move. They just sit there, taunting.

On the right wall is a clock. Does it work? Is it ticking? Does it ever tell time? Unknown. But it's there.

There's also a cabinet with two guns. But don't worry, Sam. They're nailed down. Someone came, hung the guns, and secured them so you can't move them. As a warning. Or as a taunt.

Supplies—that's where the gold is:

And here's where the fun begins. Someone has taken a very careful approach to survival:

"Sweet potatoes"—a staple of the diet.

"Apples—the world's finest." The best apples in the world. It says so.

"Sofia's microwave ovens"—112 count. Sofia's Microwave Meals. 112 pieces. Sofia is either a logistics genius or just a real cook.

"Jabber's Assorted Snacks" - assorted snacks from Jabber. Something for every taste.

"Rolee Juice Drinks" - juices. Vitamins.

"Vanilla Drink" - vanilla drink. For variety.

"Diet Soda" - diet soda. A figure is important, even in the apocalypse.

"Fresh Potatoes" - fresh potatoes. Just potatoes. Fresh.

A house is an ideal defense point, if not for one "but". There's no door. Anyone can enter. So it's more of a trap than a fortress. If enemies show up, the only way out is through the same opening where the door was.

Supplies are a great motivation to defend this place. With this set, you can survive a winter, two winters, and a couple more months. But the guns are nailed down, so we'll have to fight back with what we brought.

If we decide to settle here, the first thing we do is look for a door. Or some boards to board up the entrance. And then we'll try opening those boxes. Because who knows what's inside? Maybe they'll contain car keys. Or keys to those guns. Or just more canned food. But curiosity, Sam, will get the better of us.

Let's get inside. But keep it quiet.
