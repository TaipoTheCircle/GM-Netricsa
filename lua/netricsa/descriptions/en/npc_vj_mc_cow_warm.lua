RED-RED WARM COW FROM THE MINECRAFT OVERWORLD


CLASS:      Wild / Domestic Animal
METABOLISM: Warm-blooded Mammal
PERCEPTION: Standard vision and smell
SIZE:       5,2 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     50 FC
THREAT:     Low

DESCRIPTION:

This Warm cow inhabits warm biomes-savannas, lowlands, and rolling plains. It is distinguished by its bright red-red coloring without spots.

A distinctive feature of this species is the lack of spots and L-shaped horns standing straight on its head, giving the cow a proud and slightly wild appearance.

Red-and-red cows graze in groups, feed on grass and leaves, have a calm temperament, and are well adapted to hot climates.

TIPS:

- Milking is safe and produces milk.
- Red-and-red cow meat is nutritious, providing satiety and health.
- They are excellent for keeping in paddocks in warm biomes - they are heat-tolerant and graze well.