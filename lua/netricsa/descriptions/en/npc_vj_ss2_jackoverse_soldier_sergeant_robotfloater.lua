STORMTROOPER SERGEANT'S FLOATER DRONE


CLASS:      Stormtrooper Sergeant's Floater Drone
METABOLISM: AI-Controlled, Aircraft
PERCEPTION: Tactical Reconnaissance, Aerial Scanner
SIZE:       1 ft
ENDURANCE:  Very Low
HOSTILITY:  Average
WEAPONS:    Laser Cannon in "Eye"
REWARD:     25 FC
THREAT:     High

DESCRIPTION:

One day, Stormtrooper Sergeant saw Mental's standard reconnaissance droid and, in a matter of seconds, mentally sketched out a miniature version. Unlike the original version, this drone features a red "eye"-a scanner capable of analyzing the terrain and transmitting data in real time.

The Sergeant's Floater Drone is a light aircraft designed for tactical reconnaissance and surveillance. It hovers in the air, scanning the area and identifying threats. Its red "eye" allows it to see even in poor visibility conditions, such as fog, night, and smoke.

In combat, the drone uses lasers designed not to cause serious damage, but to suppress and distract. If the drone detects an enemy, it immediately transmits the coordinates to the Sergeant and can also call for reinforcements.

The Floater Drone is also used for patrolling areas and monitoring troop movements. Unlike Mental's original Floater Drone, this model has no penalty system-only search and destroy.

TIPS:

- The drone is easy to destroy with any weapon, but a rapid-fire weapon is best.
- If the drone spots you, it will transmit your coordinates to the Sergeant. Change position immediately or destroy the drone.
- The red "eye" is a weak point. Hitting it disables the drone instantly.
- Don't stand still. The drone fires a laser, but its accuracy decreases as the target moves.
- If there are multiple drones, they can coordinate fire. Destroy them one by one, preventing them from forming a group.
- The drone is the Sergeant's eyes. If you see it, it means the Sergeant already knows about you. Time to leave.
