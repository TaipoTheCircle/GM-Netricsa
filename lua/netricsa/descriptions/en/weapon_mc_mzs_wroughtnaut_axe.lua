AXE OF A THOUSAND METALS


WEAPON: Axe of a Thousand Metals
ROF:    ~0.9 hits/second
WEIGHT: 9,5 kg

DESCRIPTION:

A massive melee weapon forged from alloys of multiple metals, possessing exceptional strength and heaviness. It combines deadly power with resistance to destruction: the axe never breaks in combat and remains effective even with constant use.

In combat, the weapon offers several attack types: standard slashes, wide swings for group attacks, and powerful vertical strikes on the ground, creating a shockwave that spreads across the ground and impacts enemies at a distance, causing them to fly. The shockwave's range depends on the distance to the target.

NOTES:

- Effective against both single targets and groups at medium range.
- A wide swing damages all targets in a cone in front of the user.
- A vertical slam on the ground creates a shockwave, pushing back and damaging targets behind it.
- Absolute indestructibility makes it a reliable tool for prolonged clashes.