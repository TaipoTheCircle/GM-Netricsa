MARKSMAN SLAYER FROM CARNATE ISLAND


CLASS:      Marksman Slayer
METABOLISM: Undead / Mutant
PERCEPTION: Four-legged Undead
SIZE:       ~9 ft (at the shoulder)
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Built-in cannons on the left side (5 barrels), powerful clawed paws
REWARD:     2200 FC
THREAT:     High

DESCRIPTION:

A terrifying relic of a forgotten experiment or curse that has found its way into reality. This enormous, muscular creature moves on four powerful limbs. Its body is covered in decaying skin, riddled with bullet wounds, scars, and traces of violent surgeries. Despite the bloody blindfold, he somehow retains the ability to see his target. His main strength lies in the heavy, short-barreled cannons built into his left side, which he uses to unleash a barrage of fire. In close combat, he uses his metal claws for devastating blows. He combines speed, survivability, and a powerful artillery strike.

TIPS:

- His main threat is his cannons. Take cover during volleys.
- His left side, where the cannons are located, has extra armor. Focus fire on his right side or head to slow him down.
- Be prepared for him to suddenly switch to close combat at medium range with a quick dash. Always keep your distance.
- Despite his blindness, he has uncanny accuracy. Don't rely on simply dodging his shots-use proper cover.