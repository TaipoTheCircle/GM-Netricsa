CONSTRUCT, OBSERVATION


At first glance, it's just construct. Blank walls. Open sky. Familiar shapes. But something feels... off.

Sensors are picking up a low-energy anomaly in one of the dark maintenance rooms. No sound, no movement, yet heat signatures flicker - like someone standing perfectly still in the shadows. Watching.

Every time you turn your back, you can swear the air changes. The doorway seems a little darker. A figure might be standing there - or maybe it's just your mind stitching a shape into the static.

Recommendation: keep moving. Don't linger in the dark corridors. And if you feel someone watching, you're probably not wrong.