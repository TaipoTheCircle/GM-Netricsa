BIG-HEAD ANGEL, VESTO


CLASS:      Big-Head Angel, Vesto
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
STAMINA:    Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Serious Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Where are you going!?" They are completely harmless, but in the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their sheer numbers.

Vesto is one of the most enigmatic and pure figures in Croteam's credits. Unlike "Wassboy", whose role is clearly defined, Vesto remains simply a name, succinctly written into the Moral Boost category. His oversized skull is a vessel of silent yet powerful support. Perhaps he was that friend who brought coffee at three in the morning, or the colleague whose unwavering confidence in the project's success was infectious.

His presence in the credits is a reminder that the team's morale is built on dozens of large and small contributions, many of which cannot be measured. Vesto is a symbol of all those who didn't write code or sculpt models, but whose faith, patience, or simple human kindness became the invisible foundation upon which the crazy and beautiful world of Serious Sam grew.

He was once alive, but after someone killed him, he was resurrected - becoming an Angel.

TIPS:

- Use a combat knife or pistol to clear the path.
- On the "I Am Ugh-Zan" difficulty, destroy them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.
