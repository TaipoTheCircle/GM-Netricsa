BLACK MESA WAREHOUSE, ALMOST THERE


The scientist was laconic. But the hint he gave was clear: Rosenberg is here. Locked in. In a red container. Behind enormous doors that look like they were designed for an apocalypse, not just for the evacuation of one useful person.

To open these doors, you need to press a security button. Small. Discreet. Almost innocent. These are the very buttons that trigger chains of events in Black Mesa.

As soon as you approach it, H.E.C.U. will suddenly remember that this sector was strategically important. Mental's forces, in turn, will decide this is the perfect place to settle things with all living things. The familiar scene will unfold: everyone is shooting at everyone else, and you, of course, are to blame.

The doors will open. Behind them will be a container. Red. Again. I'd say color means nothing anymore, but it's precisely where someone's last hope lies.

When the way is clear, there's one more thing to do. Push the train back. Because even rescuing a brilliant scientist can't be done without a small logistical challenge. Black Mesa respects tradition: first the fight, then work as a loader, without pay.

So be prepared, Sam.