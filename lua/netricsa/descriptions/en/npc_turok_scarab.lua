SCARAB FROM THE TERRAFORMED PLANET


CLASS:      Scarab
METABOLISM: Insect
PERCEPTION: Armored ramming beetle
SIZE:       ~15 ft (in perspective)
ENDURANCE:  High
HOSTILITY:  Extremely High
WEAPONS:    Head spike, ram, mandibles, body mass
REWARD:     600 FC
THREAT:     High

DESCRIPTION:

This is what a soldier Bug turns into when fed steroids and ordered to abandon all tactics. Six massive legs, an armored carapace of the same blue and yellow color, and the main distinguishing feature is the enormous spike on its head, which this creature uses to crush anything that hasn't managed to curl up.

In combat, the Scarab is dumb but assertive. It won't spit acid or feign death. Its job is to accelerate and smash into its target at full speed. A head-on shot from a ship will penetrate even the strongest armor.

TIPS:

- Don't stand in a single line. The Scarab accelerates quickly, but once it's accelerated, it's difficult to dodge.
- Aim for the claws. Its speed and maneuverability are reduced.
