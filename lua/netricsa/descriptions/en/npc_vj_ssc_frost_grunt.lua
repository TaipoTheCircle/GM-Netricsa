FROST ZORG GRUNT FROM BEELMEZ IV


CLASS:      Frost Zorg Grunt
METABOLISM: Extremely Hypothermic Mammal
DEFINITION: Assault warrior race
SIZE:       6,5 ft
ENDURANCE:  Medium
HOSTILITY:  Medium
WEAPON:     Frost Laser Rifle
REWARD:     1200 FC
THREAT:     Medium

DESCRIPTION:

The Frost Zorg Grunt is the result of a rare event where Zorgs decided not to sunbathe, but rather to get thoroughly chilled. These units were deployed by Mental to icy planets and adapted to permafrost conditions. Their bodies are covered in thick, ice-like armor, frozen in layers directly onto the skin, acting as a natural protective shell.

In combat, frost Zorgs use frost laser guns that fire two blue pulses in quick succession. A hit doesn't so much kill as freeze the target, temporarily hindering their movement and turning them into a motionless target. The Zorg itself acts leisurely and calmly, as if in no hurry-which, in fact, is true.

TIPS:

- Avoid Lasers!
- Freezing them eliminates any chance of maneuvering.