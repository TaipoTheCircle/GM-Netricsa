MOTO BUG FROM THE MOBIUS


CLASS:      Moto Bug
METABOLISM: Animal-Gasoline-Battery
PERCEPTION: Stock Scout
SIZE:       4 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Hull, Ram
REWARD:     200 FC
THREAT:     Low

DESCRIPTION:

When it came to patrolling open spaces, Dr. Eggman decided not to reinvent the wheel but to combine what was always at hand. He took a ladybug hull, an animal, a lawnmower engine, a wheelbarrow wheel, and a processor from a children's toy called "Well, Just You Wait!" The result was the Moto Bug.

This is Eggman's first attempt at creating mobile units and the most recognizable Badnik in the empire. The Moto Bug doesn't fly, doesn't shoot, and doesn't explode. He just keeps going. Back and forth. Back and forth again. If he spots a target, he tries to catch up, but he does so with the grace of a drunken centipede.

TIPS:

- Any blow from above turns him into a pile of parts.
- Don't try to run away in a straight line - his wheels are better than his brains.

