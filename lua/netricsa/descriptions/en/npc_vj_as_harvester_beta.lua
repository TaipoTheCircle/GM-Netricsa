"BETA" SWARM HARVESTER


CLASS:     "Beta" Swarm Harvester
METABOLISM: Xenobiological
PERCEPTION: Swarm Support
SIZE:       8,2 ft
ENDURANCE:  Average
HOSTILITY:  Very High
WEAPONS:    Explosive Parasite Spawn, Ram, Corrosive Skin
REWARD:     30 FC
THREAT:     High

DESCRIPTION:

The Harvester "Beta" Swarm is a specialized support specimen designed to supply the Swarm with living ammunition. Its massive abdomen serves as an incubation chamber, where Explosive Parasites continuously develop. Unlike most Swarm Beta specimens, the Harvester rarely engages in direct combat and tends to stay behind the main attacking specimens.

During combat, the Harvester regularly releases Explosive Parasites of the Swarm Beta. These creatures behave like normal parasites: they leap swiftly at the nearest target and pursue it with great persistence. However, instead of infecting, they detonate on contact, rupturing their bodies and damaging everything around them with an explosion and biological tissue.

Even after death, a Reaper remains dangerous. The destruction of its body causes an emergency opening of the incubation chamber, from which four explosive "Beta" Swarm parasites burst forth, immediately launching an attack. If a Reaper is destroyed in close proximity, the surviving parasites are able to finish what their host left unfinished.

Despite its relatively weak defenses, a Reaper poses a serious threat when accompanied by drones, shieldlegs, or other Swarm fighters. While the enemy is distracted by close combat, it manages to create a new wave of parasites, gradually turning the battlefield into chaos.

TIPS:

- Destroy the Reaper as early as possible before it releases too many parasites.
- After his death, immediately prepare for the appearance of four explosive parasites.
- Keep your distance.
