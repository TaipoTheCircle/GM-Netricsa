TYRANNOSAURUS REX FROM THE TERRAFORMED PLANET


CLASS:      Tyrannosaurus Rex
METABOLISM: Carnivore, Theropod
PERCEPTION: Apex Predator
SIZE:       17 ft
ENDURANCE:  Extreme
HOSTILITY:  Absolute
WEAPONS:    Skull, jaws, teeth, tail, claws
REWARD:     5000 FC
THREAT:     Absolute

DESCRIPTION:

This isn't just any dinosaur. This is the one dinosaur that needs no introduction. When people say "Dinosaur", this is the one that comes to mind. The king of the tyrant lizards.

The T-Rex's appearance is familiar to everyone: a massive skull wide at the back and narrowed at the front, causing the eyes to face forward, providing binocular vision better than a hawk's. The neck is short and thick-otherwise it wouldn't be able to support such a head.

Whether a Tyrannosaurus rex hunts or scavenges is a decade-long debate.

TIPS:

- Don't try to run away in a straight line. It's faster than it looks.
