AB-10 SELF-LOADING PISTOL


WEAPON:      AB-10
AMMUNITION:  9x19 cartridges
ROF:         ~20 rounds/min

DESCRIPTION:

A semi-automatic pistol manufactured in the United States, a modification of the Intratec TEC-9 pistol. Released in 1995.

NOTES:

- Like the TEC9, the pistol was popular with gangsters, but did not achieve the same popularity in pop culture.






