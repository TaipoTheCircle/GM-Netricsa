QUIVER FROM XEN


CLASS:      Alien, Friendly
METABOLISM: unknown
PERCEPTION: curious wanderer
SIZE:       5 ft
ENDURANCE:  low
HOSTILITY:  none
WEAPONS:    none
REWARD:     150 FC
THREAT:     none

DESCRIPTION:

These little Quivers were sent from Xen to... just chill, really. They wander around making weird alien noises, open doors like they own the place, and have absolutely no interest in attacking anything. Most of them are experts at avoiding conflict and at politely observing humans.

TIPS:

They're the friendliest creatures you'll meet: no combat, just curiosity.
Listen to their alien noises - it's their way of saying "hello."
They can open doors, so don't be surprised if one helps you out.
