BETA ZOMBIE FROM THE MINECRAFT OVERWORLD


CLASS:      Beta Zombie
METABOLISM: Undead
PERCEPTION: Hostile Humanoid
SIZE:       6,2 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONALS:  Hands, stubbornness, and someone else's equipment
REWARD:     800 FC
THREAT:     Average

DESCRIPTION:

Beta Zombies are former humans from the Minecraft world who once decided not to go to sleep and deeply regretted it. Slow, persistent, and surprisingly determined, they roam the night in search of any living thing that can still think.

Beta Zombies lack intelligence, but they make up for it in numbers. They can pick up items, wear armor, and sometimes look like they just robbed an adventurer's warehouse. They light up when exposed to sunlight, which, however, rarely makes them reconsider their priorities in life.

TIPS:

- The sun is your free all - use the morning wisely.