GHOST CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Ghost Creeper
METABOLISM: Otherworldly Ethereal Entity
PERCEPTION: Creeper's Ghost, Ethereal Undead
SIZE:       5,5 ft
ENDURANCE:  Average
HOSTILITY:  Varies
WEAPONS:    Ethereal Energy Burst
REWARD:     200 FC
THREAT:     Average

DESCRIPTION:

This is a variant of the Elemental Creeper that appears as a translucent gray creature. It is the embodiment of the residual essence of a Creeper that died, but instead of vanishing into oblivion, it remains frozen between worlds.

It can pass through mobs.

The Ghost Creeper's main feature is its explosion. It does not destroy blocks, but rather emits a wave of pure ethereal energy when it explodes. This wave is lethal to all living creatures within its radius, but is completely safe for the environment.

Despite their ghostly nature, these creepers are still vulnerable to steel and magic, though catching them off guard may require remarkable agility or the use of special equipment.

TIPS:

- If you encounter a Ghost Creeper, don't panic. You can try to avoid it, as its speed is slow, unless it's in a pack.
- Never fight it indoors. Although it doesn't destroy blocks, its attack is difficult to dodge in a tight corridor.
- A high-damage weapon or the "Strength" enchantment on a bow will help kill it before it begins charging up its explosion.
