FROZEN ZOMBIE FROM THE MINECRAFT OVERWORLD


CLASS:      Frozen Zombie
METABOLISM: Cryogenic Undead
PERCEPTION: Support Rifleman / Freeze Infantry
SIZE:       ~6,3 ft tall
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Snowballs, Punch
REWARD:     200 FC
THREAT:     Average

DESCRIPTION:

This is the result of normal undead remaining in icy biomes for so long that they are frozen to the bone.

A frozen zombie resembles a normal zombie in appearance, but with one important difference: its entire skin has acquired a pale blue tint, and its head, shoulders, chest, arms, and legs are covered in small caps of snow. He looks like he just crawled out of a snowdrift and decided that since he's already dead, he might as well be cold.

In combat, frozen zombies behave differently than their regular counterparts. Instead of immediately charging into melee combat, they first attack from a distance with snowballs. One well-placed blow, and the victim is slowed for a few seconds. At this point, all other frozen zombies nearby rush in, trying to finish off the immobilized target.

After the slow wears off, frozen zombies retreat to a safe distance and repeat the cycle: snowball, attack, retreat. This tactic makes them dangerous in groups, especially in narrow corridors where there's no room to dodge flying snowballs.

Armored versions of these zombies also exist, which are even more dangerous than regular ones.

TIPS:

- If you see a frozen zombie, don't stand still. Snowballs fly accurately, but they can be avoided by rolling.
- If you're slowed, don't panic. Try to move with your back to the wall to avoid being surrounded.
- Kill frozen zombies first in the group. Their crowd control is worse than direct damage.
- A long-range weapon solves the problem before they can throw a snowball.
