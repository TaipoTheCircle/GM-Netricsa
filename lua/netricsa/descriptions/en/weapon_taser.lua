TASER STUN GUN


WEAPON:          Taser
AMMUNITION       lectric cartridges
ROF:             1 shot/min

DESCRIPTION:

The Taser stun gun is a non-lethal electric shock weapon. The main difference between Taser stun guns and similar devices is their patented technology, which minimizes injury and fatalities when apprehending offenders, and their ability to hit targets at a distance of 4.5 to 10 meters. Tasers are manufactured by Taser International (now Axon) in Scottsdale, Arizona. The Taser is positioned as a non-lethal and almost completely safe device for human health, intended for use by the police in cases of pursuit of a criminal, when resistance is offered during arrest or detention, and for use on potentially dangerous individuals against whom, in a standard situation, firearms could be used. In a number of countries, the use of tasers is included in national principles on the use of force as one of the levels of influence on a suspect. However, despite the manufacturer's claims about the safety of tasers, many organizations and individuals dispute this view. For example, the human rights organization Amnesty International recorded 334 deaths resulting from the use of tasers between 2001 and 2008.

