COOKIE CREEPER, MINECRAFT OVERWORLD CREEPER


CLASS:      Cookie Creeper
METABOLISM: Pastry
PERCEPTION: Unique Artifact Element
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  Passive
WEAPONS:    Explosion
REWARD:     50 FC
THREAT:     Medium

DESCRIPTION:

This is the result of a strange genetic experiment, somewhere between magic, pastry, and perhaps some crazy modder's bad joke.

Unlike its green, gunpowder-fueled brethren, this one is completely harmless in terms of damage. It resembles a shortbread cookie.

This mob's main and only feature is its explosion. When it hits an enemy, it doesn't destroy structures or deal damage. Instead, it quietly crunches into a multitude of cookie-shaped shards that fly in all directions. These cookies can be picked up, and each one instantly restores a small amount of hunger and health.

Unlike regular food, mob cookies don't have any negative effects, other than a slight feeling of guilt for having just "killed" a living sweet.

TIPS:

- Don't worry. It doesn't explode blocks or deal damage. But if you hate sweets, you can ignore it.
- A best friend on an empty stomach. If you're out of food and far from home, this is a real gift from heaven.
- Remember: these cookies have an expiration date of about 3 months, after which they go stale and turn into a regular zombie. Just kidding.
