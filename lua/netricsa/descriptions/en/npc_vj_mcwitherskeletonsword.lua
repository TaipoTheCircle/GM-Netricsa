WITHER SKELETON SWORDMAN FROM THE MINECRAFT NETHER


CLASS:      Wither Skeleton, Swordsman
PERCEPTION: Undead, melee unit
METABOLISM: Magically sustained undead
SIZE:       7,8 ft
ENDURANCE:  High
HOSTILITY:  Very high
WEAPONS:    Stone Sword
REWARD:     3000 FC
THREAT:     Very high

DESCRIPTION:

The Wither Skeleton Swordsman is a skeleton who decided the mundane life was too rosy and moved to the Nether to advance his career. Taller, darker in personality, and noticeably angrier than his bony relatives, he patrols the nether fortresses with the air of someone who isn't paid for his work.

In short...Poor Yorick...

Armed with a stone sword and a special Wither effect that gradually drains enemy health. Immunity to fire allows the Wither Skeleton to thrive in places where other mobs have long since perished.

Sometimes, upon death, it drops a Wither Skull - a rare trophy that can be used to summon the Wither, and for which many are willing to endure all of the above without complaint.

TIPS:

- In close combat, the Wither Skeleton explains the rules quickly and painfully.
- Milk, oddly enough, protects against the Wither effect.
- Recognizable by its black bones.