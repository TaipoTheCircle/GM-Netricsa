OIL ELEMENTAL


CLASS:      Oil Elemental
METABOLISM: Inorganic
PERCEPTION: Magical Creature
SIZE:       54 ft
ENDURANCE:  High
HOSTILITY:  High
WEAPONS:    Oil Balls, Punches
REWARD:     22750 FC
THREAT:     High

DESCRIPTION:

A collection of inanimate materials brought to life. Oil Elementals are composed of oil and use magical power to hurl oil balls and other projectiles at enemies.

Their main difference from other Elementals species is their inability to split into smaller specimens. Instead, upon death, they drop small health packs and armor.

TIPS:

- Large specimens can be extremely strong and dangerous.
