SWARM XENOMITE


CLASS:      Swarm Xenomite
METABOLISM: Xenobiological
PERCEPTION: Symbiotic Organism
SIZE:       1,3 ft
ENDURANCE:  Very Low
HOSTILITY:  Absolute
WEAPONS:    Jump Attack, Acid Blast
REWARD:     25 FC
THREAT:     Medium

DESCRIPTION:

The Xenomite is a small parasitic life form raised by Reapers for defense and offense. It spends most of its short life near its parent, waiting for a suitable prey to emerge.

Despite its modest size, the Xenomite is extremely dangerous. Upon locating a prey, it makes a swift leap and attempts to close in. Unlike most Swarm creatures, its primary goal is not to kill its opponent, but to destroy itself in close proximity.

The Xenomite's body is filled with unstable acidic compounds. Upon death or critical damage, the creature explodes, scattering corrosive biomass. Contact with these substances can cause severe chemical burns even seconds after detonation.

The Xenomite's lifespan is extremely short. Apparently, the organism's evolution has completely abandoned long-term survival in favor of maximum effectiveness during a single attack.

TIPS:

- Destroy Xenomites from a distance.
- Don't let them get too close.
- After the creature's death, stay away from the acidic remnants.
- If the creature lives only to explode near you, it's too late to argue with its purpose in life.
