DESCRIPTION:

The Combine Elite Beta Soldier is the standard white Overwatch Combine Elite soldier, designed to suppress resistance and maintain order in any environment. Distinguished by their armor, which is entirely white with black accents for camouflage. Unlike other Elite soldiers, he has two red eyes built into his helmet and three vents in his respirator that filter air.

This soldier type is designed for medium and long range combat, combining accurate pulse rifle fire with the use of energy grenades to suppress cover. Augments enhance reaction time and endurance, allowing the soldier to withstand intense fire and fight in difficult conditions.

