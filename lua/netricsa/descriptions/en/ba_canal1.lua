THE CANALS OF BLACK MESA, THE EXPLOSION OF REALITY


Well, Sam, here we go! Not quietly, not neatly - with a bang, screams and complete chaos.

Nihilanth decided to stop waiting. His creatures surged into the Black Mesa like water into a ruptured dam.

A freight train with guards derailed right next to a group of scientists. Metal is grinding, sparks are flying, and THEY are already climbing out of the wreckage.

The Houndai, these eternally hungry creatures, rush after the scientist as if they had seen a snack. Run, smartass, run!

And the one who froze at the window... Surprise! Vortigaunt. He just stands there and watches. Well, yes, it's probably an ordinary Tuesday for them.

The elevator you were just riding in with the "nice" scientists couldn't stand the strain of reality. He shot down like a stone. Two of them are confused. You're miraculously alive.

You look around, and here's an oil painting: a guard who seemed so brave a minute ago is now... well, let's just say he's becoming a dinner for a hound. Delicious, you can't say anything.

