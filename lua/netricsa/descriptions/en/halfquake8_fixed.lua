HALF-QUAKE SHOW, ESCAPE FROM THE LABYRINTH


Regarding the Guard...

The stone block decided everything faster than any dialogue. One guard, two rats, and a whole host of unspoken phrases were now evenly distributed across the floor. The show doesn't like witnesses. And it certainly doesn't like them getting a chance to tell something.

You entered through the security room. The irony is dense, almost tangible. This is where the labyrinth begins.

It's alive.

Not in a poetic sense, but in a practical one.

The corridors twist, the walls meet at odd angles. Mental's forces are already here. The Vortigaunts, too.

Somewhere above are the test control buttons. The central control panel. The brain of this meat grinder. If logic is to be believed, and logic is a rare guest here, it's from there that the traps are triggered, the paths are opened and closed, and who "delighted the audience" today is decided.

The task is simple in formulation and unpleasant in execution:

Get out.

And if possible, also shut down this place, at least for a couple of seconds.

Move, Sam.

Labyrinths don't tolerate doubt.

And this one especially dislikes those who think too long.