EDENSYS BIOTECH SOLDIER


CLASS:      EdenSys BioTech Soldier
METABOLISM: Human Clone, Male
PERCEPTION: EdenSys Corporate Security Force
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Various Small Arms
REWARD:     600 FC
THREAT:     Average

DESCRIPTION:

These soldiers form the core combat force of the EdenSys BioTech Corporation's security force. Most are clones, bred and trained exclusively for combat, facility security, and the suppression of any internal or external threats.

Their equipment is designed for use in extreme cold conditions. Insulated uniforms, thick masks, and visors allow them to operate in arctic environments while maintaining high mobility and effectiveness even in extremely low temperatures.

Warfare depends on the assigned mission. Soldiers are capable of using a wide range of weapons: assault rifles, shotguns, submachine guns, rifles, machine guns, and other modern corporate weaponry. Thanks to constant training, they confidently handle virtually any type of firearm.

EdenSys's training is considered extremely rigorous, but most soldiers learn one thing best from the entire course: an endless supply of obscenities. In combat, they constantly shout curses, threats, and insults, which has long become a hallmark of the corporate army.

Despite this, they should not be underestimated. They act in an organized manner, quickly occupying advantageous positions, actively exploiting cover, and delivering concentrated fire, posing a serious threat even to well-trained opponents.

TIPS:

- The weapons used are unknown in advance, so assess the threat immediately upon spotting the enemy.
- Soldiers prefer to fight from cover and rarely remain in the open.
- Their arctic gear doesn't make them any more protected than ordinary people.
- Don't be distracted by their constant cursing: they shoot much better than they talk.
