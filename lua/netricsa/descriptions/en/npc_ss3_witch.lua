WITCH-BRIDE OF ACHRIMAN


CLASS:      Witch-Bride of Ahriman, Telekinetic Sorceress
METABOLISM: Semi-corporeal entity
PERCEPTION: Extrasensory scanning, vision through obstacles
SIZE:       7,4 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Telekinetic strangulation, throwing debris/energy projectiles, teleportation, projectile deflection
REWARD:     8000 FC
THREAT:     High

DESCRIPTION:

A former sorceress of the Grand Council, deceived and enslaved by Lord Achriman. An entity trapped in a semiotic limbo between dimensions, rendering it invulnerable until it materializes to attack. She has a humanoid female form with six tentacles on her back, a metal bikini, and stumps instead of legs. Her tactics involve constant teleportation and the use of powerful telekinesis for attack: she strangles her victims from a distance, lifts and breaks bones, and hurls debris and energy projectiles at them. She is extremely dangerous when combined with other enemies, as she distracts and restricts mobility.

TIPS:

- It's best to use the A-24 "Devastator" and sniper rifles. The SBC Cannon will kill in one charged shot.
- Her stranglehold is interrupted if line of sight is broken (hiding behind cover) or if she is damaged.