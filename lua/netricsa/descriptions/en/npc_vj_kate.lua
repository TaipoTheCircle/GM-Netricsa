KATE, THE STALKER


CLASS:      Kate, the Stalker
METABOLISM: Human, Female
PERCEPTION: Obsessed Prey
SIZE:       5,3 ft
ENDURANCE:  High
HOSTILITY:  Absolute
WEAPONS:    Physical strength, speed, animal instincts
REWARD:     300
THREAT:     High

DESCRIPTION:

She was once an ordinary girl, exploring the woods behind her house with her friend Lauren and childhood friend Carl. But one encounter changed everything.

As children, Kate and Carl encountered Slenderman in the woods. Carl repressed the horror, but Kate, apparently, always remembered.

Years later, they returned to the woods to confirm or deny their childhood fears. Carl experienced severe hallucinations, and Kate disappeared. She was found unconscious in the woods, with a broken camera and a stack of strange drawings. Two weeks in the hospital—and then a return home that wasn't a return to normal life.

Then came her mother's death. She tried to sell the house and leave.

Karl was planning to commit suicide and take the nightmares with him. Kate couldn't. She ran away, leaving her friend to die alone.

After another Slenderman attack, Kate became the Stalker. She lives in the old Kullman Mines, where she hunts anyone who ventures there.

A part of Kate still lives somewhere inside and tries to help those who follow in her footsteps.

TIPS:

- She's faster than you.
- The flashlight's light blinds her momentarily. Use it to escape.
- The writing on the walls doesn't lie. Read and do as you're told.
