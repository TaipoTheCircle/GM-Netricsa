BIG-HEAD ZOMBIE, NIKOLA MOSETTIG


CLASS:      Big-Head Zombie, Nikola Mosettig
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
ENDURANCE:    Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Serious Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They are completely harmless, but in the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their numbers.

Nikola Mosettig is the living embodiment of "the other way." When he joined Croteam with his friend Ivan Mika, he dreamed of a career as a level designer, but fate decreed otherwise, sending him to the programming department. His exaggerated cranium contains 15 years of humility and professional growth – from a content programmer responsible for scripts, triggers, and interaction logic in games, to the Director of Information Technology. All this time, he watched with quiet sadness as Ivan created monumental landscapes, while he painstakingly wrote out what should happen when the player stepped on this or that tile.

It was Nikola who was irritated by the publisher's ugly release cover, and it was he who witnessed and participated in the move to a spacious office, marking a new stage in the studio's life. His story is a hymn to all those whose dreams in game development weren't quite what they seemed, but who nevertheless found their place and became an indispensable part of the machine that cemented the designers' grandiose plans into working code. "We just can't get rid of him"—this developer joke conceals deep respect for a man whose dedication to the project proved stronger than his unfulfilled ambitions.

He was once alive, but after someone killed him, he rose from the dead—becoming a demonic Zombie.

TIPS:

- Use a combat knife or pistol to clear a path.
- On the "I Am Ugh-Zan" difficulty, destroy him remotely due to their modifiers. - In a group cluster, a sledgehammer with a horizontal strike is effective.