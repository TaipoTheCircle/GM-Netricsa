OMEGA


PLANET:       Omega
TYPE:         Terrestrial planet, Prison Planet
CLIMATE:      Deep freeze, permafrost
MOONS:        Unknown
TERRAIN:      A giant ice crater, a fractured surface with cliffs and chasms, rare isolated mountains, a network of subglacial caves and tunnels
LOCATION:     Dimension Omega (connected by a portal to the planet Andros)
NATIVE RACES: White Snake Guardian, Ice Snake Guardians, rare surface birds, marauding gangs
ALLIANCE:     Penitentiary System of the Magic Dimension
LOCATIONS:    Ice crater, White Snake Labyrinth, Valtor's Dungeon, Prison Cells

DESCRIPTION:

This is a harsh prison planet, created for The eternal imprisonment of the Magical Dimension's most dangerous criminals. Its surface is completely encased in age-old ice and shrouded in a dense cloud cover, with gaps in it being extremely rare. The landscape is a fractured icy wasteland with deep chasms, cliffs, and lonely rocky peaks, completely devoid of vegetation.

The planet's unique topography is formed by a gigantic ice crater-a visual concept inspired by the circles of hell from Dante's "Divine Comedy". The entrance to the dimension is located at the top of this rift, reached by a portal connected to the planet Andros. Along the walls of the crater's central spiral are thousands of prison cells-magicians and witches-frozen into the ice right along the rocky outcrops.

At its deepest point, like Lucifer in an icy lake, lies Valtor's prison-the most secure place in the entire dimension.

The planet's internal structure is permeated with a complex network of caves, tunnels, and passages, forming the White Snake Labyrinth. Omega's core contains powerful magical energy that feeds the White Snake, the planet's guardian, which shares it with the guardian snakes patrolling the labyrinth.

The planet's surface and subsurface are littered with technical debris-the remains of ships that transported criminals, as well as equipment fragments. During the portal's destruction, many prisoners escaped and organized themselves into bloodthirsty gangs of marauders, scouring the planet, using this debris to build shelters and weapons. Only rare birds, which do not descend into the dangerous depths, inhabit the surface itself.

The prison system on Omega relies on magical freezing. A super-strong ice cocoon, resistant even to bombardment, forms around the criminal's body. These mirror blocks are delivered to the vortex by spaceships and dropped into the cone, where they are forever frozen into the rocky walls.

KEY LOCATIONS:

- Ice Vortex: A gigantic spiral rift that forms the planet's primary structure. It serves as the entrance to the dimension and the prisoner dumping site.
- White Snake Labyrinth: A complex network of subglacial caves and tunnels that permeates the planet. Guarded by Ice Snake Guardians, the offspring of the White Snake.
- Valtor's Prison: The deepest and most secure point of the vortex, where one of the most powerful Dark Mages was imprisoned.
- Prisoner Cells: Thousands of ice cocoons containing criminals, frozen into the vortex's walls at various levels.
