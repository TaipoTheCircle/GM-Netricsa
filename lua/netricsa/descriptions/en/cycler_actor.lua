DESCRIPTION:

Cycler Actors are a reduced, stasis-bound subspecies of Generic Actors, believed to be early experimental "failed mimics" that never achieved full behavioral replication.

Where classic Generic Actors can act, think, and use abilities based on the form they copy, Cycler Actors are locked into limited animation loops, imitating only movement patterns, idle behaviors, or scripted gestures.

Some xenobiologists believe Cycler Actors are proto-mimics, creatures that scanned a biological form only once and then became stuck in a perpetual "display mode", unable to evolve or adapt. Others propose that they were intentionally created-biological mannequins designed for training, psychological conditioning, or infiltration rehearsals.

Cycler Actors cannot fight, cannot flee, and cannot improvise. They simply repeat whatever behavioral cycle their nervous tissue was "frozen" into.

However, because they share the same morphic biology as Generic Actors, their resemblance to humans, aliens, or other entities can be disturbingly perfect... until they move.

TIPS:

- Don't panic. Cycler Actors are harmless unless externally controlled.
- Look for repeated movement loops - unlike Generic Actors, they cannot adapt.
- Destroying them is easy, but often unnecessary-they pose no natural threat.