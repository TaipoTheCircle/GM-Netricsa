CREATURES OF THE SWEET ISLANDS OF THE BIG LOLLY


The creatures of the Sweet Islands are inhabitants of an isolated dimension known as the Big Lolly. At first glance, their homeworld appears harmless, but behind this veneer lies an aggressive ecosystem that does not tolerate foreign intrusions.

To them, any outsider is a threat that must be destroyed immediately.

Their society consists of a variety of life forms, from small Gummy Bears to Gingerbread Men, composed of sugar, dough, and caramel.

They do not negotiate or retreat. But this does not mean they lack intelligence, as they have learned to use Xen HoundEyes as mounts.