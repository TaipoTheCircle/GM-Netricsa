GIGANOTOSAURUS FROM THE TERRAFORMED PLANET


CLASS:      Giganotosaurus
METABOLISM: Carnivore, Theropod
PERCEPTION: Apex Predator
SIZE:       23 ft
ENDURANCE:  Extreme
HOSTILITY:  Absolute
WEAPONS:    Headbutt, Tail Strike, Jaws
REWARD:     7000 FC
THREAT:     Absolute

DESCRIPTION:

One of the largest land predators ever to have lived on the planet, surpassing even the famous Tyrannosaurus rex in size. It is found in Death Valley.

In appearance, the Giganotosaurus is a massive brown body with a narrow skull, ridges above the eyes, and striped markings along the spine.

The Giganotosaurus uses the same techniques as other large predators.

TIPS:

- Don't try to engage in combat.
- Very dangerous.
