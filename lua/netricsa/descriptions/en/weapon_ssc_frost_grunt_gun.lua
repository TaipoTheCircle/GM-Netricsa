FROST LASER RIFLE


WEAPON: Frost Laser Rifle
AMMO:   Icy Energy Beams
ROF:    Double Shots

DESCRIPTION:

The Frost Laser Rifle is a modified version of the standard laser rifle, adapted for use in extremely cold conditions. The weapon's structure is reinforced with heat-resistant materials, and the body features an updated design with yellow and blue elements serving as visual markers of its cold-weather specialization.

The weapon retains the basic model's operating principle, firing two energy pulses in a row with each pull of the trigger. However, instead of standard beams, the rifle fires blue ice lasers infused with cryonic energy. Upon impact, these not only inflict damage but also slow the enemy by rapidly cooling tissue, armor, and drive mechanisms.

The Frost Laser Rifle is powered by a modified plasma core with cryostabilization, preventing overheating and component degradation in freezing temperatures. An integrated reactor continuously reloads the weapon through a magazine, ensuring stable operation in protracted combat and arctic conditions.

NOTES:

- Fully adapted to cold and low temperatures.
- Each shot emits a double icy pulse, slowing the enemy.
- The cryo effect reduces the mobility of living targets and the reaction time of mechanical opponents.
- Particularly effective at medium ranges and during group fire.
- Less penetrating power against heavy armor, but compensates for this with a slowing effect.
- Intended for light infantry and units operating in cold zones.