FLECHETTE GUN


WEAPON:      Flechette Gun
AMMO:        Flechettes (small, high-velocity darts)
ROF:        ~250 rounds / min

DESCRIPTION:

Ranged weapon that fires compact, needle-like projectiles at high speed. Each flechette penetrates armor and flesh with minimal recoil, making the weapon effective for precise suppression or surgical strikes.

NOTES:

- High-velocity projectiles can pierce multiple targets in line.
- Moderate ammo capacity; careful aiming recommended.
- Effective at medium range; less useful in close quarters.
- Low recoil allows rapid follow-up shots.