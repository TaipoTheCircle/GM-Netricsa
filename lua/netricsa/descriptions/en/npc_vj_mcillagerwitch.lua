WITCH FROM THE MINECRAFT OVERWORLD


CLASS:      Minecraft Witch
METABOLISM: Warm-blooded humanoid
PERCEPTION: Enhanced, with tactical self-preservation
SIZE:       6,2 ft
ENDURANCE:  Very high
HOSTILITY:  High
WEAPOON:    Potions (damage, slow, poison, weakness)
REWARD:     400 FC
THREAT:     High

DESCRIPTION:

The Witch is an isolated representative of a village civilization who has chosen the path of potions, alchemy, and an outright disregard for public opinion. Outwardly, she resembles a villager, but differences are immediately noticeable: a warty nose, somber clothing, and a stern gaze.

The Witch uses potions not only for attack but also for defense. When threatened, she instantly drinks potions of fire resistance, resistance, regeneration, and water breathing, turning into an extremely resilient opponent.

She lives in huts in the swamps or appears during raids, where she acts as a mobile alchemical artilleryman, showering enemies with bottles of extremely unfriendly contents.

TIPS:

- Constantly analyzes the threat and selects the right potion.
- Prefers to wear down the enemy rather than kill quickly.
- Never panics. Never.
- Milk removes the effects, but does not completely solve the problem with the witch.