BRICK HOUSE, BRICK SYNDROME


Sam, the construction project of the century is nearing completion. Whoever designed this complex was clearly inspired by the architecture of dreams—where everything is there, but there are no doors, and red brick peacefully coexists with white, as if it were meant to be.

Meet the final version of the legendary Brick House.

The landscape continues to evolve. There's an entire village here:

Pond. To the right of the house, right on the flat grass, a pond now splashes. Not a pool, not a puddle, but a full-fledged pond. There's water, no fish, but who knows what will populate it.

A two-story white brick building. Behind the house, to the left, a two-story colossus has risen. White brick, wooden floors, and... no stairs to the second floor. Just walls, floor, and ceiling, and between the floors—emptiness and dreams.

First floor: 8 windows. Lots of light, lots of glass, lots of potential entry points for snipers. One wall, for some reason, is red. Apparently, they decided not to throw out the old brick stock.

Second floor: 6 windows. No stairs.

Inside: emptiness. Absolute emptiness. No table, no chair, not even a tin can. Just walls, floor, and windows. And the red wall on the first floor stares at you with a silent question: "Why am I here?"

The first brick house: the same nailed-down guns, locked boxes, Sofia's supplies, and the clock on the wall. No door; they weren't waiting.

Garage: the engine and wrench still await their moment on the wooden table.

Swimming pool: full of water, ready for tired stalkers.

Four broken cars: parked, rusting.

Eleven trees: standing around the perimeter, one still in its new location.

Now you're ready.
