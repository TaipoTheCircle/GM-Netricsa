COMBINE ALIEN ASSASSIN


CLASS:      Experimental Combine strike unit
METABOLISM: Partially modified alien organism
PERCEPTION: Enhanced motion sensors and tactical awareness
SIZE:       7,5 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Claws, knives, decoy grenades, smoke devices
REWARD:     800 FC
THREAT:     Very high

DESCRIPTION:

The Alien Assassin is designed as a highly mobile Combine soldier. It is a humanoid alien with extremely pale skin, red spikes on its head, and a breathing mask. Tubes protrude from its eye sockets, and its arms end in long, sharp claws capable of slicing through its prey in a single swipe. Its feet have only two toes.

The Assassin is distinguished by extreme speed and durability. In combat, he constantly changes positions, performing jumps, rolls, and sharp dashes, rarely staying in one place. He can create a smoke screen, suddenly disappearing from sight, and then attack from the flank or rear. His arsenal includes throwing knives and trap grenades, presumably placed on his back.

The Assassin's left arm bears a distinctive orange sphere, visually resembling a Cremator. The similarity in the structure of the forearms and shoulders suggests a possible bioengineering relationship between these species, although the Assassin's purpose was more stealthy and targeted.

TIPS:

- Keep your distance - close combat with him is almost always a losing proposition.
- Don't stand still - he likes to attack from cover.