DESCRIPTION:

The Cover Mutant is distinguished by its developed organic carapace on its shoulders and a double row of teeth. It retains high intelligence and can produce complex speech patterns. It demonstrates flexibility in combat, using a variety of weapons, including a guided missile launcher.

