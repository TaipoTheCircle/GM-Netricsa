EARTH


PLANET:       Earth
TYPE:         Terrestrial planet, Humanity's homeworld
CLIMATE:      Varied (from arctic to tropical)
MOONS:        1 moon
TERRAIN:      Continents, oceans, mountain ranges, deserts
LOCATION:     Solar System, Orion Arm (Milky Way)
NATIVE RACES: Humans
ALLIANCE:     Independent human space; Partially occupied by the Mental Horde (at various times)
LOCATIONS:    Egypt, Tunguska, Carcassonne, Pompeii, Teotihuacan, Krvavice

DESCRIPTION:

The third planet in the Solar System. Humanity's homeworld and the center of human civilization. It has one natural satellite, the Moon.

The planet's landscape is extremely diverse: from polar ice caps to equatorial jungles, from deep ocean trenches to the highest mountain peaks. Approximately 70% of the surface is covered by water.

Earth has long attracted the attention of extraterrestrial civilizations. Thousands of years ago, it was visited by the Sirians, who influenced the development of ancient cultures, particularly in Egypt. Later, the planet repeatedly became the target of invasions by the Mental Horde, seeking to destroy humanity.

During the war, the United Defense Forces used ancient Sirian artifacts, including the Guardian of Time, to resist the invaders.

KEY LOCATIONS:

- Egypt: A region where numerous evidence of Sirian presence remains. The Great Pyramids, Karnak, Luxor, the Valley of the Kings, and underground complexes containing Sirian generators are located here. - Tunguska: The site of a massive invasion by Mental's forces, where a gigantic Horde portal opened during the Tunguska Offensive.
- Carcassonne: A fortified city where the "Troika-A" group was based during one of the operations against Mental's forces.
- Pompeii: An ancient Roman city near which a satellite weapon was activated to deter enemy forces.
- Teotihuacan: An ancient Mayan city where the Sirians left one of their time portals.
- Krwavice: A medieval Polish city where an important artifact known as the "Book of Wisdom" was discovered.
