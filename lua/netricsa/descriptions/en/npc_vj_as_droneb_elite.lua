"BETA" SWARM ELITE DRONE, SMALL


CLASS:        "Beta" Swarm Elite Drone, Small
METABOLISM:   Xenobiological
PERCEPTION:   Pack Predator
SIZE:         7,2 ft
ENDURANCE:    High
HOSTILITY:    Very High
WEAPONS:      Claws, Teeth
REWARD:       450 FC
THREAT:       High

DESCRIPTION:

Elite drones are far less common within the "Beta" Swarm than standard specimens. They are easily identified by their coal-black chitin and three eyes, capable of spotting prey even in near-total darkness.

Physically, the elite drone differs little from its kin, though its musculature is noticeably denser and its outer shell far tougher. Many colonists mistakenly took these creatures for a separate species until discovering that elite specimens emerge from the same hives and obey the same instincts.

A common theory among xenobiologists is that the black coloration signifies maturity or results from prolonged survival in particularly hazardous conditions. In other words, encountering an elite drone means it has already survived numerous situations that should have killed it.

Unlike standard drones, elite specimens rarely succumb to initial hits and continue their advance even after sustaining severe wounds. Unfortunately, the "Beta" Swarm views such resilience as an outstanding achievement rather than a reason to start preserving itself.

TIPS:

- Do not expect it to die as quickly as a standard drone.
- Prioritize eliminating elite specimens when facing a large pack.
- Darkness works to its advantage.
- If a black drone with three burning eyes is charging at you, it means the introductory warning phase is already over.
