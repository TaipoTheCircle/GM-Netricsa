HOSTILITY:  extreme
WEAPONS:    homing missiles
            thermal bombs
            rocket launcher
            pulse lasers
            beamgun
REWARD:     15000 FC
THREAT:     medium to high

DESCRIPTION:

It has been always speculated that the vicious Ugh-Zan is not a dynasty of immortal monsters, but a single incorporeal entity that reincarnates into different bodies (usually ones with at least four arms), explaining the creature's different appearance between its incarnations. The Ugh-Zan candidates are proof of this.
Trained by a fanatic cult of Ugh-Zan worshippers ("Zanlites"), the Candidates are mass produced clones of Ugh-Zan 3, in different stages of growth. After the death of Ugh-Zan 3, these were created and are in constant gruelling battle training to grow to the same size and strength as the original. As they grow, more and more weapons of Ugh-Zan 3 they are able to wield.
In the end, only one Ugh-Zan Candidate will remain, and Ugh-Zan's spirit will reincarnate inside it once more. 

TIPS:

- Employs most powerful weapons and magic spells known
- Comes in different sizes with varied weaponry
