KUNG FU ZOMBIE, APPRENTICE FROM THE CHI-FANG


CLASS:      Kung Fu Zombie, Apprentice
METABOLISM: Undead
PERCEPTION: Zombie Martial Artist
SIZE:       6 ft
ENDURANCE:  Low
HOSTILITY:  Very High
WEAPONS:    Fists, Saw
REWARD:     100 FC
THREAT:     Medium

DESCRIPTION:

They serve both as law enforcement and as Prince Chang's servants. Mental created these zombies by extracting their entrails and brains, and instilled in them a hostility toward anything that moves.

Soon, Mental had amassed a sizable army of martial arts enthusiasts. They mastered only one move: a flying kick. And since the zombies had decayed flesh, a kick from a rotten foot would cause them to fall apart.

Mental decided it would be easier to attach hooves to them.

While most of Mental's minions receive minimal training, the Kung Fu Zombies had undergone extensive training by watching kung fu films from the 80s and 90s.

But even that wasn't enough, so soon each of them received a pair of deadly shurikens to combat long-range threats.

Now, armed with knowledge of saws and century-old cinema, they rush into battle and crush anyone who opposes their great teacher, Sensei Mental.

They will throw their saws at you from afar and kick you as they approach.

TIPS:

- Dodge their kicks by sidestepping.
- Take them out with a double-barreled shotgun at close range.
- Larger groups can be dealt with using rapid-fire weapons and explosives.
