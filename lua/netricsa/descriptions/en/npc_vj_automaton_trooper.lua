AUTOMATON TROOPER FROM THE CYBERSTAN 


CLASS:      Automaton Trooper
METABOLISM: Mechanical
PERCEPTION: Automaton Light Infantry
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  Absolute
WEAPONS:    Fusion Pulse Submachine Gun, Frag Grenades, Punch
REWARD:     200 FC
THREAT:     Low-Medium

DESCRIPTION:

This is the most common and weakest unit of the Automaton faction.

Trooper Appearance: A thin metal body, humanoid shape, and glowing red eyes. The body is gray, and the head is white with red eyes. Troopers are made of chromed metal and gleam in the light.

In combat, Troopers rely on numbers. Their tactics are simple: spot a Helldiver, fire five-round bursts, then reload (4-second cooldown). If the Helldiver hides behind cover, the Trooper throws a frag grenade. If the Helldiver gets too close, it punches. When suppressed by fire, the Trooper begins to move its weapon erratically, reducing accuracy.

TIPS:

- Troopers are not a priority target, but they need to be killed quickly to prevent them from calling in reinforcements.
- The head kills in one shot, but it's difficult to hit. It's easier to shoot the body.
- A severed arm disarms the Trooper, forcing them to engage in close combat.
- A severed leg immobilizes the Trooper.
- Explosives (grenades, RPGs, airstrikes) are effective against clusters.
- Troopers operating turrets in Automaton outposts can be killed and the turret can be used against enemies.
