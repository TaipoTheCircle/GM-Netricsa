BIG-HEAD, DAVOR HANSKI


CLASS:      Big-Head, Davor Hanski
METABOLISM: Humanoid
PERCEPTION: Enthusiastic Fanatic
SIZE:       5.9 ft
ENDURANCE:  Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Cool Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They are completely harmless, but on the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They spontaneously appear in secret areas, blocking passages with their sheer numbers.

Davor Hanski is one of the pillars of the franchise. He oversaw the production of the iconic trilogy, from its inception with Serious Sam: The First Encounter (2001) and The Second Encounter (2002), to the series' resurgence with Serious Sam 3 (2011). His role often extended beyond management; he was one of the primary visionaries, pouring his heart and soul into the scripts of the first game and its massive sequel, and then returned to polish the story for the third installment.

He was once alive, but after you killed him in the Temple of Hatshepsut, he rose from the dead - becoming a demonic Zombie.

TIPS:

- Use a combat knife or pistol to clear a path.
- On the "I Am Ugh-Zan" difficulty, destroy them remotely due to their modifiers.
- In a group cluster, a sledgehammer with a horizontal strike is effective.