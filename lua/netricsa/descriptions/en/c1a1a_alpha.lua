BLACK MESA ALIEN CHAMBERS, A FIERCE ZOO


Sam, remember what I said about "maybe it'll be okay"? Forget it. The resonance cascade is already here. We're too late. Or maybe we arrived on time-it depends on how you feel about a situation where there's ruins all around and an alien hissing coming from the vents.

We took the elevator down and found ourselves in a sector the locals apparently called the "zoo". This is where they kept aliens. In cells. For study, for analysis, for a tick in the report. And now the cells are open. And the scientists who were poking headcrabs with chopsticks yesterday are locking themselves in these same cages today, hoping the concrete walls and bars will save them from what they unleashed.

A brilliant plan, no doubt about it. "We can't stop them, so we'll join them as live rations".

Houndeyes whine in their cages, headcrabs leap from around corners, and somewhere deep down, judging by the sound, someone large has decided it's open house.

Situation: We're in the containment sector, between us and the exit is a labyrinth of cells, sealed corridors, and crazed scientists who either stare at us through the bars with pleading eyes or are looking nowhere.

Objective: get to the offices. More offices in Black Mesa, of which there seem to be more than ammo in your backpack. Because the offices, according to the logic of those who survived, should contain communications, maps, keycards, and perhaps the miracle of a working elevator to the surface. Or at least to another level of this hell.

Specific: Mental's forces are not yet visible. Not their soldiers, not their equipment. Only the Zen creatures, only the scientists who'd chosen the wrong place to hide, and only you and me. And silence. Heavy, tense, broken only by the dripping of water from burst pipes and the sounds of what were once specimens and are now hunters.

Where are all Mental's forces? Because their absence here frightens me more than their presence.

Let's go. And don't look into the eyes of those behind bars. They've already made up their minds.
