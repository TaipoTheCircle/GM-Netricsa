SHEEPUFF FROM THE MINECRAFT AETHER WORLD


CLASS:      Sheepuff
METABOLISM: Biological, Floating
PERCEPTION: Peaceful Floating Animal
SIZE:       4 ft
ENDURANCE:  Low
HOSTILITY:  None
WEAPONS:    None
REWARD:     50 FC
THREAT:     Zero

DESCRIPTION:

These furry creatures inhabit floating islands in the sky and are among the most peaceful creatures of this strange world.

They resemble regular sheep in appearance, but their wool appears fluffier and lighter, as if woven from clouds. Their coloring can vary – white, blue, pink, lime, and cyan, with colored sheep being much rarer than white ones.

This is the main difference from regular sheep. They can "inflate" and launch themselves high into the air (like a rocket), then slowly glide downward. This sometimes causes them to fall off the island and crash.

They graze on cloud grass, eating it and turning it into dirt.

They can be bred with wheat, oranges, or blueberries.

When sheared with shears, they drop Cloud Wool, a special material of this dimension. Once sheared, a sheep loses its ability to fly until it regrows its wool by eating grass.

TIPS:

- To get a lot of Cloud Wool, herd sheep into a pen with grass and shear them every few minutes.
- Once a sheep is shaved, it cannot fly -this is the perfect time to herd it to safety.
- Unlike regular sheep, this species can be a valuable source of resources for crafting items from the Ethereal Dimension.
- Don't try to use them for transport. They're uncontrollable.
