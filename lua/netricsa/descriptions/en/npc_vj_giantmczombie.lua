ZOMBIE GIANT FROM THE MINECRAFT OVERWORLD


CLASS:      Zombie Giant
METABOLISM: Undead
PERCEPTION: Colossal Zombie
SIZE:       ~60 ft
ENDURANCE:  Extreme
HOSTILITY:  High
WEAPONS:    Stomp, Punch
REWARD:     1000 FC
THREAT:     Absolute

DESCRIPTION:

This is an exact replica of a normal zombie, six times larger. The same rotting flesh, the same rags, but the size of a village house. While a normal zombie barely reaches your face, the giant stares down at you from eighteen meters above. Its footsteps shake the earth, and a single attack can destroy an opponent.

The giant has other oddities, too. It is not undead, although it looks like a zombie. He is affected by harmful potions, healed by healing potions, immune to the "Heavenly Punishment" enchantment, and is immune to the sun. He doesn't drown in water, but slowly suffocates. Iron golems will still attack him like any other enemy.

If you put a giant in a minecart, he will float above it like a ghost, never touching the rails.

TIPS:

- To kill him, you need a long-range weapon. Only shoot at the legs—hits to the body and above don't count.
- The giant doesn't burn in the sun, doesn't drown, and doesn't respond to zombie summons.
- Don't try to leash him. It won't work.
