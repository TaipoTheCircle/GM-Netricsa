BLACK MESA, INSIDE THE SECURITY COMPLEX


Well, you're inside. The walls are oppressive, the air smells of metal and disinfection - a typical security headquarters. Now we need to get a foothold: here every object can be the key to survival. Or to failure.

What to do next:

Bulletproof vest

There are rows of vests in the armory. Put one on. Not for beauty: when the mess starts, every plate will count. Yes, it's hard, but it's better to sweat than to bleed.

Toilet (just in case)

Yes, even in the apocalypse, the superman needs his own space. But remember: I can see everything. The chip in your skull translates every step. So don't linger-and don't try to hide something in the drain tank. I already know.

Gun

Without weapons, it's like having no hands. There is a standard service pistol in the armory. Take it, check the clip, put the safety on. Don't shoot unnecessarily, but don't wait for someone to aim at you either.

Surveillance cameras

The control panel is on the second floor. The elevator is nearby, the call button is flashing red. Get up, sit at the monitors and wait for everything to start.