EARTH HEADCRAB ZOMBIE, ARCTIC 


CLASS:      Zombie Headcrab, Earth (Arctic)
METABOLISM: Parasitic host, adapted to cold
PERCEPTION: Poor, relies on host instincts and warmth
SIZE:       ~6-7 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Claws, bite, headcrab leap
REWARD:     220 FC
THREAT:     Average

DESCRIPTION:

The Arctic Zombie Headcrab is a human infested with a woolly Terrestrial headcrab and adapted to the constant cold. The parasite retains control of the host's nervous system, but the cold significantly alters the creature's behavior and appearance.

The host's body is often wrapped in scraps of winter clothing, frozen uniforms, or suits fused to the flesh. The headcrab, covered in thick fur, burrows deep into the skull, further protecting itself from the cold.

TIPS:

- Aim for the headcrab-the fur partially protects it, but accurate hits are still effective.