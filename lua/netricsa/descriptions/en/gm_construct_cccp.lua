USSR CONSTRUCT SITE, DAY-TIME SOCIAL BLOCK


A familiar construct site has been transported to the 1970s, to the countries of the Socialist Bloc.

And you know, it's even cozy here. None of those gloomy "depressive dumps of the 90s".

Just bright (literally) retro.

All this takes place in the summer. Lots of greenery, a lake, a field, a village on the horizon. And yes, there's music playing.

If it starts to distract you from saving humanity, just click on the sound source, and it'll turn off.
