DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Serious Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They are completely harmless, but on the "I Am Ugh-Zan" difficulty level they gain fire and ice modifiers. They spontaneously appear in secret areas, blocking passages with their sheer numbers.

Christopher "LanThief" Kroeger is the living embodiment of the spirit of the gaming community, rising to the level of an official developer. His exaggerated cranium is a testament to his deep understanding of online gameplay and the balance required for perfect Deathmatch. As part of the legendary modding team "A Few Screws Loose," he was a key contributor to the official Seriously Warped Deathmatch mod for Serious Sam: The Second Encounter (2002).

It was Christopher and his team who gave players twenty insane maps, new weapons, and the classic Capture the Flag mode, transforming Sam's already chaotic multiplayer into a veritable testing ground for mastering skill. His contribution is a bridge between passionate fans and the development studio, a shining example of how a talented community can become part of a game's official story.

He was once alive, but after someone killed him, he was resurrected - becoming an Angel.

TIPS:

- Use a combat knife or pistol to clear the path.
- On "I Am Ugh-Zan" difficulty, eliminate them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.