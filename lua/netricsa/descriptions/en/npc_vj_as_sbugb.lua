SWARM SHIELDBUG "BETA"


CLASS:      Swarm Shieldbug "Beta"
METABOLISM: Xenobiological
PERCEPTION: Heavy Assault Organism
SIZE:       9,2 ft
ENDURANCE:  Very High
HOSTILITY:  High
WEAPONS:    Armored Limbs, Ramming Strike
REWARD:     1000 FC
THREAT:     Very High

DESCRIPTION:

The Swarm Shieldbug "Beta" is a specialized assault specimen designed to breach enemy defenses and protect less resilient Swarm members. Its defense is based on two massive armored limbs, which can completely cover most of the creature's front.

When attacking, the Shieldbug instinctively holds the enemy in front of it, using its natural shield to deflect attacks. Thanks to its unique design, its frontal armor can withstand damage that would be fatal to most other Swarm organisms.

These protective plates also serve as weapons. With a sharp movement of its limbs, the Shieldfoot delivers powerful blows capable of knocking prey back a considerable distance. This attack is especially dangerous in confined spaces, on bridges, and near cliffs.

Unlike the more agile members of Swarm Beta, this specimen relies not on speed, but on relentless forward movement. Having located a target, the Shieldfoot will steadily close the distance until it has an opening to strike.

Like most creatures overconfident in their own invulnerability, its most vulnerable organs are located precisely where it least expects an attack.

TIPS:

- Don't attack the Shieldfoot head-on.
- Use its slowness to flank it.
- Avoid narrow passages during combat.
- The larger his shield looks, the less he likes being attacked from behind.
