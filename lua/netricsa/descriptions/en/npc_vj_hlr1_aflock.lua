AFLOCK FROM XEN


CLASS:      Aflock
METABOLISM: Unknown
PERCEPTION: Curious flyer
SIZE:       3 ft wingspan
ENDURANCE:  Low
HOSTILITY:  None
WEAPONS:    None
REWARD:     50 FC
THREAT:     None

DESCRIPTION:

Aflocks are small, peaceful creatures from Xen. They spend most of their time flying in loose groups, making strange alien sounds, and observing their surroundings. Completely harmless, they often act as scouts or mood-enhancers for the Xen ecosystem.

TIPS:

- They never attack.
- Listen for their alien chirps to locate them.
- Can be found flying in groups or near open areas.
