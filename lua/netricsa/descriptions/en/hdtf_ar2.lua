AR2 PULSE RIFLE


WEAPON:      AR2 Pulse Rifle
AMMO:        Overwatch energy cells / Secondary: Combine electrical balls
ROF:         ~600 rounds/min (primary), 1 grenade/sec (secondary)

DESCRIPTION:

Standard-issue energy rifle of the Combine Overwatch. Fires high-velocity pulse beams for rapid suppression and can launch a secondary energy ball for high-impact area damage. Highly effective against both organic and mechanical targets.

NOTES:

- Primary fire delivers fast, precise energy beams.
- Secondary electical balls cause dust area damage.
- Effective at medium range; reduced accuracy at long distances.