FERAL RAT FROM EARTH


CLASS:      Feral Rat
METABOLISM: Mammalian
PERCEPTION: Hazardous Environment Rodent
SIZE:       ~1 ft
ENDURANCE:  Low-Medium
HOSTILITY:  Moderate
WEAPONS:    Bite (moderate), jump attacks
REWARD:     10 FC
THREAT:     Low-Moderate

DESCRIPTION:

Feral Rats are aggressive variants of standard rats, mutated or stressed by the Cascade Resonance environment. They leap to bite targets and attack in small groups. While individually weak, swarms can harass intruders effectively.

TIPS:

- Keep distance - watch for jump attacks.
- Shotguns or melee attacks work best in confined areas.
- Dangerous only in numbers.
