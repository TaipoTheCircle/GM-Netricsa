A CONSTRUCT OF THE FLAT GRASS, A BEAUTIFUL PLACE


Before you is not just flat grass. It's a plane so infinite that the grass itself doubts its own existence, as wide as a headcrab's mind before a leap.

On the horizon, high above the ground, the sign "FlattyWood" shines - in large, confident letters, as if someone had built a Hollywood awards show for best scene here.

A few high-rise buildings and concrete construct, empty and gray, like the forgotten sets of a grim arthouse film;

A pond that you look at and think simply reflects your fatigue from constant battles...