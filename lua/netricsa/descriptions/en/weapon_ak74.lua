AK-74 ASSAULT RIFLE


WEAPON:     AK-74
AMMUNITION: 5.45x39 rifle, 40 mm underbarrel grenade launcher.
ROF:        700 rounds/min

DESCRIPTION:

The AK-74 Assault Rifle is the primary weapon of the Soviet/Russian army. It was developed by Mikhail Timofeevich Kalashnikov in 1970 and adopted by the Soviet army in 1974, hence its name. This assault rifle is renowned for its reliability and effectiveness; it rarely jams and can fire even after being completely submerged in mud. It saw action in the Afghan War and the Chechen War and is currently used in the Special Military Operation. There is a modified version with the letter M in its designation; this version has different characteristics and is black in color. This assault rifle is also famous for being used almost everywhere in the world, mainly by terrorist organizations, but also by the armies of various countries. It also has a GP-25 underbarrel grenade launcher that fires 40 mm grenades.

NOTES:

- Use it to storm buildings or trenches.
- Use the underbarrel grenade launcher to smoke the enemy out of cover.
- You can fire at close, medium, and long ranges.