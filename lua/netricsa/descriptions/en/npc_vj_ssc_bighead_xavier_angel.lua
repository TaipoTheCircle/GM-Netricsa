BIG-HEAD ANGEL, NICK "XAVIER" MACRON


CLASS:      Big-Head Angel, Nick "Xavier" Macron
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
STAMINA:    Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the uniform of Serious Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They are completely harmless, but in the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their sheer numbers.

Nick "Xavier" Macron is one of the most technically savvy members of the legendary modding team "A Few Screws Loose." His oversized cranium houses not only the design vision he applied to the iconic Seriously Warped Deathmatch mod for The Second Encounter, but also the deep engineering knowledge that later led him to become the Lead Engineer on projects like *Area-51* and *Blacksite: Area-51*, and subsequently the Director of Tools and Technology for the NBA 2K series.

His journey from a fan modder improving Serious Sam's online battles to a Lead Engineer and Director of Tools in the AAA industry is a story of how passion for games and technical talent can lead to the pinnacle of a career. Nick Macron is living proof that the modding community is a thriving talent pool for the entire industry.

He was once alive, but after someone killed him, he was resurrected - becoming an Angel.

TIPS:

- Use a combat knife or pistol to clear a path.
- On the "I Am Ugh-Zan" difficulty, eliminate them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.
