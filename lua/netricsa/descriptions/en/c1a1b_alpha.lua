BLACK MESA, FLOODED LABS


We've reached a dead end. Literally. The corridor ends, leaving only a few rooms, each a little personal hell.

First room: a Black Mesa-style swimming pool

The floor is flooded. The water is murky and smells of ozone and something burning. In the middle, right in the puddle, a broken lamp floats. It sparks. Every few seconds, there's a flash, a crackle, and the water comes alive with a blue light. You can't stand in it. Swimming? Don't even think about it.

The reason: the lamp fell into the water and has now turned this room into an electric soup. It produces almost no light, but the voltage is enough to fry anyone. Even you, Sam. Even in your armor.

The solution: there's a switch on the wall to the right of the entrance. It's old, rusty, but apparently functional. One click, and the lamp will go out, the water will turn to just water, and we'll be able to pass. Question: can you reach it in time before the sparks turn the entire floor into one big electrical outlet?

Next comes the last room. And here, Sam, they're waiting for us.

Houndeyes. Not just one, not two. A whole nest. Rustling, squealing, waiting. And among them are headcrabs. Small, nimble, the kind that usually fall from the ceiling at the most inopportune moment.

The room is covered with them. Like a carpet. Every step is a risk of stepping on a tail, every movement is an invitation to dinner. They're not sleeping, they haven't gone off to find other territories. They're here. And they're hungry.

Strategy Notes:

Breaker. First, turn off the power. We can try shooting the lamp, but that might only make the water worse. Manual mode is more reliable. And faster. I hope.

Water. After the shutdown, don't go in there anyway. Who knows what was floating around there before us.

The room with the creatures. There's no choice here. It's either us, or them. The narrow opening at the entrance is a chance to create a death corridor, but the houndeyes know how to get around it. Headcrabs - jump. The best tactic is to throw a grenade in the center and finish off the survivors. Unless, of course, the grenade rolls into a corner and angers them even more.

The switch awaits.
