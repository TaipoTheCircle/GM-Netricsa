MISS BLOOMIE FROM FUNDAMENTAL PAPER EDUCATION


CLASS:      Miss Bloomie, Science Teacher
METABOLISM: Paper Organism (mutant teacher)
PERCEPTION: Sight, Chemical Sense
SIZE:       ~8,2 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Box cutter blade (left arm prosthesis), sharp claws and teeth, forked snake tongue
REWARD:     1000 FC
THREAT:     High

DESCRIPTION:

One of three deadly teachers at Paper School, she teaches science (biology, chemistry, physics). Recognizable by her short hair, black horns of varying lengths, and a white bow. Her left hand is replaced by a giant, sharp box cutter blade, and her right hand has pointed fingers. She possesses a forked snake tongue, hinting at a predatory nature. She is more reserved and stern than Miss Circle, displaying less "playfulness." Her attacks are precise.

TIPS:

- Her prosthetic blade is a weapon for quick, slashing attacks. It is longer than it appears and can be used for both melee and lunging attacks.
- She usually works in a trio with Miss Circle and Miss Tavel. She is the shortest of the three, but no less dangerous.