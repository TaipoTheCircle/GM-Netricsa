RESISTANCE APC 404-818 DROPSHIP


CLASS:      Resistance APC 404-818 Dropship
METABOLISM: Air Transport Unit, Armored Drop Pod
PERCEPTION: Landing Transport / Resistance Airborne Armored Personnel Carrier
SIZE:       19,7 ft
ENDURANCE:  Very High
HOSTILITY:  Low
WEAPONS:    None
REWARD:     100 FC
THREAT:     High

DESCRIPTION:

This is a trophy captured by the Resistance from Mental's army. Externally, it is almost identical to the original, but has now been completely repainted in blue-the signature color of the Rebels. All the intimidating Horde logos have been carefully erased, and in their place are the Resistance "H" markings.

Inside, it's all the same armored comfort that Mental once designed: a jacuzzi, leather chairs, a plasma TV. But now it's occupied not by Mental's armored soldiers, but by Russian rebels. They fly on missions, transport supplies, and sometimes simply cruise around, enjoying a comfort they've never had before.

The shuttle is unarmed. Its purpose is to deliver troops to their destination and then depart. However, the rebels sometimes use it as a mobile command post or even as a refuge during combat.

TIPS:

- The shuttle is unarmed. Its purpose is to transport rebels, not to fight.
- If you're fighting Mental and the Galactic Empire, this shuttle is your friend. It's bringing reinforcements.
- If you see a blue shuttle approaching your position, don't shoot right away. Chances are, there are allies inside.
- The jacuzzi is still working. If you're tired of the war, you can climb into the shuttle and rest, but there's no champagne there, the rebels drank it.
