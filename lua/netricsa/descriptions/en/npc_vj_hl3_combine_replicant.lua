COMBINE REPLICANT SOLDIER


CLASS:      Combine Replicant Soldier
METABOLISM: Modified human, male / incomplete biomechanical implants
PERCEPTION: Transhuman soldier, experimental / transitional infantry
HEIGHT:     6,5 ft
ENDURANCE:  Medium-high
HOSTILITY:  Very high
WEAPONS:    SMG2, grenades
REWARD:     300 FC
THREAT:     Medium

DESCRIPTION:

The Combine Replicant soldiers are an incomplete stage of a person's transformation into a full-fledged Combine Overwatch soldier. They undergo accelerated implantation and psychocorrection, but are not yet equipped with the full protective gear and neural interfaces typical of standard Overwatch units.

The main distinguishing feature of a Replicant is the lack of a helmet. Their faces are partially or completely exposed, often bearing visible signs of surgical intervention: implants, scars, pale skin, a glassy stare. This makes them vulnerable, but also allows the Combine to conserve resources on mass operations.

Their behavior is less coordinated than that of Overwatch. Despite this, Replicants are still dangerous, especially when outnumbered.

TIPS:

- The lack of a helmet makes headshots especially effective.
