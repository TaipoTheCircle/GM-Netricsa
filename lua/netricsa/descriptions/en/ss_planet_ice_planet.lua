ICE PLANET


PLANET:       Ice Planet
TYPE:         Terrestrial Planet, Cryo-World
CLIMATE:      Deep Freeze
MOONS:        Unknown
TERRAIN:      Solid ice cover, unfrozen bodies of water, ice bridges
LOCATION:     Unknown star system
NATIVE RACES: Ice Elementals, Snow Mamutman, Snow Mamuts, Snow Werebull-Beasts
ALLIANCE:     Mental Horde
LOCATIONS:    Ice Bridge, Black Monoliths, Mamutman Camps

DESCRIPTION:

The Ice Planet is a world completely covered in ice, visually reminiscent of Earth's polar regions, such as Antarctica. Its surface consists of endless ice fields, interspersed with open areas of unfrozen water.

The planet's native inhabitants are Ice Elementals-elemental creatures composed of pure ice.

Snow Mamutman-a primitive humanoid race adapted to the harsh cold conditions-also inhabit the planet. The Mamutman use Snow Mamuts-massive, shaggy creatures resembling mammoths-as pack animals and for battle. Snow Werebulls pose a distinct threat.

Deep within the ice fields rise two mysterious Black Monoliths-structures of unknown origin, accessible only by a long Ice Bridge spanning unfrozen waters.

KEY LOCATIONS:

- Ice Bridge: A crossing over unfrozen waters leading to two mysterious black monoliths.
- Black Monoliths: Two mysterious structures of unknown origin and purpose, located on the other side of the ice bridge.
- Mamutman Camps: Settlements of the local humanoid race, where tamed Snow Mamuts are kept.
