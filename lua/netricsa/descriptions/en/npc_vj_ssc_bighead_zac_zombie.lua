BIG-HEAD ZOMBIE, ZACH RAND "GOLDFINGER"


CLASS:      Big-Head Zombie, Zach Rand "Goldfinger"
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
ENDURANCE:    Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     100 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam developers. They wear the Serious Sam uniform, but have the unique facial features of the development team. They exhibit an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, wait!" They are completely harmless, but in the "I Am Ugh-Zan" difficulty, they gain fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their sheer numbers.

Zach Rand, known by the resonant nickname "Goldfinger", occupies a special place in the Croteam pantheon—a place cast in the gold of gratitude. His exaggerated cranium shines not so much with technical expertise as with the value of the support provided, the nature of which remains a mystery over the years. His name appears in the credits from the very first game in the series, The First Encounter, all the way through Classics: Revolution, indicating a long and significant relationship for the studio.

Perhaps he was one of the first and most insightful testers, whose advice saved the project from fatal errors. Or perhaps he provided invaluable moral or even material support during the early, most difficult stages. The nickname "Goldfinger" hints at something exceptional, something "golden"—be it a unique skill, a rare find, or simply a heart of gold.

He was once alive, but after someone killed him, he rose from the dead—becoming a demonic Zombie.

TIPS:

- Use a combat knife or pistol to clear the path.
- On the "I Am Ugh-Zan" difficulty, eliminate them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.