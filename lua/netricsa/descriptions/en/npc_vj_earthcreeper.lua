EARTH CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Earth Creeper
METABOLISM: Inorganic-Earth
PERCEPTION: Hostile
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Earth Blast
REWARD:     60 FC
THREAT:     Medium

DESCRIPTION:

This creeper type solves the problem of explosions a little differently. Its body is brown, and its head resembles a block of earth with a green tip, allowing it to blend into the landscape and sneak up on enemies undetected.

Instead of destroying everything around it or causing damage, the Earth Creeper creates a chaotic pile of earth blocks when it explodes. This explosion doesn't damage your health, but its effects can be devastating to your terrain and buildings. It's often described as "useless" (relatively speaking) in combat, but very annoying due to the way it turns a neat lawn into a pile of randomly scattered dirt.

TIPS:

- This creeper isn't harmful to your health, but it poses a serious threat to your base and buildings.
- Never let it explode near your home unless you want it buried under the Earth. It's best to kill it from a safe distance.
- It's easily recognizable by its brown body and green head, which blends in with the grass.
- Upon death, you can loot regular dirt.
- Tamed creepers can be useful as defenders, but the Earth Creeper still leaves a mess even after being tamed.
