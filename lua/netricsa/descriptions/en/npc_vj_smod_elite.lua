COMBINE OVERWATCH HEAVY ELITE ARMORED SOLDIER


CLASS:      Combine Overwatch Combine Heavy Elite Armored Soldier
METABOLISM: Augmented human in an exoskeleton
PERCEPTION: Combine Heavy Assault Suppression Unit
SIZE:       5,9 ft
ENDURANCE:  Extremely High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, OICW
REWARD:     1200 FC
THREAT:     Critical

DESCRIPTION:

The Combine Overwatch Heavy Elite Armored Soldier is a walking combat platform, designed to breach defenses and demonstrate the Combine's absolute dominance. He is clad in a massive robotic exoskeleton, black and gray with red accents, inside which the human becomes merely a control element of the war machine.

The suit completely covers the soldier's body, including the head. The helmet is sealed, leaving no vulnerable areas. The armor's structure clearly reveals mechanical components: servos, reinforced joints, armored plates, and power nodes.

The exoskeleton significantly enhances the soldier's physical capabilities, allowing him to carry heavy weapons without sacrificing mobility. The AR2 is used for suppressive fire, while the OICW reduces cover and enemy concentrations to useless junk. This soldier doesn't seek advantageous positions-he creates them himself, advancing directly through the battlefield.

Despite its apparent invulnerability, the structure has a breaking point. A direct hit from an RPG destroys the exoskeleton, turning the elite vehicle into a pile of twisted metal.

TIPS:

- Small arms are almost useless against his armor.
- Use RPGs or powerful explosives - that's his main and almost only weakness.
- Don't get in his way - he doesn't retreat or stop.