COMBINE OVERWATCH ARCTIC PRISON SOLDIER


CLASS:      Combine Overwatch Arctic Prison Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine long-range and mid-range punitive assault unit
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, SMG
REWARD:     650 FC
THREAT:     High

DESCRIPTION:

The Combine Overwatch Arctic Prison Soldier is a repurposed Nova Prospekt Facility Guard, repurposed by the Combine for combat in extreme climates. When standard Overwatch forces are insufficient, the Combine unhesitatingly draws soldiers from the prison reserve and deploys them to the most inhospitable regions of the planet.

Unlike the shotgun variant, this soldier serves as a suppression and fire control agent. He is equipped with an AR2 pulse rifle or an SMG submachine gun, allowing him to operate effectively at medium range, covering the advance of other units and clearing approaches to objectives.

The equipment is adapted for the Arctic: white and gray camouflage reduces visibility against ice and snow, but elements of the prison uniform are still visible over it.

Like other prison soldiers, he has blue "Nova Prospekt" markings on his back.

TIPS:

- The AR2 allows him to keep his distance and suppress cover with energy fire.
- The yellow visor is easily visible in the dark, but difficult to spot in snowstorms.
- It's best to take him out from a distance before he reaches a favorable position.