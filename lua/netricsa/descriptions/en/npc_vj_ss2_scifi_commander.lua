ORC SOLDIER, COMMANDER FROM THE ORAZIO


CLASS:      Orc Soldier, Commander
METABOLISM: Mammal
PERCEPTION: Primitive warrior race, former Zorgs
HEIGHT:     6,3 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Plasma Rifle, Pulse Grenades
REWARD:     40 FC
THREAT:     Medium

DESCRIPTION:

This is a clear example of the saying "they wanted the best, but it turned out as usual". They were once part of the great Zorg race from Bilmez IV, strong, ferocious, and, most importantly, tanned. But constant sunbathing resulted in the Zorgs losing almost all their skin. Seeking a solution, they created a mutagen that would restore their former appearance. Instead, the mutagen turned them into orcs...

The drug was immediately banned, and the experimenters, along with all the victims, were exiled to the secluded planet of Orazio. There, the exiles made good use of their time, founding the Altani mining corporation and began mining ore, eventually falling into Mental's hands.

Mental recruited orcs into his armies; most ended up in Mental's military factories, where they helped produce weapons and combat vehicles. The rest were sent to the Solgar Military Academy on Kronor, where they were trained in basic tactics and equipped with modern armor and weapons.

Orcs who graduated with honors from the academy were promoted to commanders. They wear composite armor, which provides greater protection than standard orc armor, and use two types of weapons: a plasma rifle and pulse grenades.

They usually lead groups of other orcs and are rarely seen alone. Some commanders also use gyrocopters, which allow them to fly, increasing their mobility.

TIPS:

- Unlike most of Mental's minions, they will try to keep a distance when close to you.
- Their projectiles have minor homing capabilities - stay away from them.
- Commanders using gyrocopters don't use grenades and have slightly stronger armor.
