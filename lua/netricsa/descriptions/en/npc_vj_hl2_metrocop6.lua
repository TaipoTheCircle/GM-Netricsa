COMBINE CIVIL PROTECTION METROCOP SHIELD GUARD


CLASS:      Civil Protection, Assault Guard
METABOLISM: Human, Male
PERCEPTION: Standard (mask visor + radio)
SIZE:       9 ft
ENDURANCE:  High
HOSTILITY:  High
WEAPONS:    Tactical shield, stun Stun-Stick
REWARD:     350 FC
THREAT:     Medium

DESCRIPTION:

The Shield MetroCop is a specialized Civil Protection operative, designed to suppress riots and control dense crowds of citizens. Unlike regular patrolmen, he operates on the front lines, absorbing blows, gunfire, and desperate attempts at resistance.

His black tactical uniform is adorned with yellow panels on the shoulders and forearms, serving as visual markers of his role and unit. A white mask completely conceals his face, preventing enemies from reading his emotions or intentions.

The primary piece of equipment is a gray tactical shield with a yellow Combine symbol. The shield is used not only for defense but also as a means of pressure: it can be used to push back, pin opponents against walls, and break crowd formations. Behind the shield, the Guardian advances slowly and relentlessly, pushing opponents into pre-determined areas.

The appearance of Guardians with shields almost always marks the beginning of the active suppression phase.

TIPS:

- Attack from the flanks or from behind - a frontal assault is ineffective.
- The shield reduces damage but limits the wearer's line of sight.