BROWN MOOSHROOM COW FROM THE MINECRAFT OVERWORLD


CLASS:      Brown Mooshroom Cow
METABOLISM: Warm-blooded Mammal
PERCEPTION: Wild / Fungal Animal
SIZE:       5,2 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     150 FC
THREAT:     Low

DESCRIPTION:

The Brown Mooshroom Cow is an unusual species of Mushroom Biome creature, a fusion of organic cow flesh and a fungal ecosystem. Brown mushrooms with beige spots grow on its back, and its body is covered in reddish-beige spotted cowhide.

TIPS:

- Mushroom soups are a quick way to replenish health and satiety.
- After cutting the Mushrooms, she will turn into an ordinary Cow.
