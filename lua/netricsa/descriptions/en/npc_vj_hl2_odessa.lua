ODESSA CUBBAGE


CLASS:      Resistance Field Coordinator
METABOLISM: Human, Male
PERCEPTION: Resistance Leader
SIZE:       9,5 ft
ENDURANCE:  Medium
HOSTILITY:  Medium
WEAPONS:    RPG-V2
REWARD:     10 FC
THREAT:     Low

DESCRIPTION:

Odessa Cubbage is a man who calls himself a colonel with such confidence that most people prefer not to elaborate. He leads a small Resistance cell on the coast and directs combat operations from a safe distance using a radio and a well-established commanding tone.

Cubbage's primary contribution to the fight against the Combine is logistics. He hands out RPGs, explains where to fire them, and then oversees the process without interfering. His military uniform is adorned with patches whose origins are questionable, but in the face of a total invasion, such trifles are considered insignificant.

Despite his limited participation in combat, Cubbage sincerely believes in the Resistance's victory and that effective communication over the radio is sometimes more effective than personal presence on the battlefield.

