OLIVIA FINCH THE OTTER FROM THE BROADSIDE BEACH


CLASS:      Olivia Finch the Otter
METABOLISM: Anthropomorphic Otter
PERCEPTION: Bucky's cheerful companion, Crime Victim
SIZE:       5 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    Cooking skills, coconut foraging
REWARD:     100 FC
THREAT:     Minimal

DESCRIPTION:

This is Bucky the Beaver's cheerful companion, one of the main stars of Broadside Animation. A light brown otter in a striped swimsuit, she smiles, and she's almost always smiling. At presentations at the Frontier Street Museum, she's described as "sparkling" and "bubbly". She's always ready to help her friends, catches fish for Bucky's fish stand, and rarely appears without a smile on her face.

She went fishing with Bucky and Giovanni the Goose, but their boat was wrecked off the coast of the mysterious island of Nulla Terra. The local wolf leader tasks Olivia with collecting coconuts to make up for the damage.

Behind Olivia's cheerful cartoonish image lies a terrifying reality. The actress who voiced the character was Olivia Finch. She moved to Broadside Beach Island for the tropical weather and worked there, occasionally donning an otter costume for performances. There, she crossed paths with Brandon Lester, the actor who dressed as Bucky the Beaver.

Brandon began stalking Olivia. He stood at her window for hours, banged on her door, and once left a bloody stain on the glass when he banged his head on it. Olivia complained, but the situation only worsened. One night, as she was trying to record her complaint yet again, Brandon broke down the door, attacked her, and dragged her unconscious body to the lake. Olivia drowned.

To cover up the crime, Broadside Animation used Olivia's body to create "Starling"-a living Olivia the Otter costume intended to replace the missing actress.

TIPS:

- She's Harmless.
