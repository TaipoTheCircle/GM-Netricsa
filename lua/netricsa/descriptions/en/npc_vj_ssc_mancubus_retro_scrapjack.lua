MANCUBUS, THE SCRAPJACK FROM THE HELL


CLASS:      Mancubus, ScrapJack
METABOLISM: Reconstructed Demonic Mammal
DEFINITION: Modified Behemoth Corpse-Sewn
SIZE:       13 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Two RPGs
REWARD:     3000 FC
THREAT:     High

DESCRIPTION:

A demon personifying human imperfection and greed, gluttony, and idleness. It has incredibly thick skin, massive elephantine legs, and a small head. In order to destroy and explode victims, two RPGs that fire Rockets are implanted in the forearms of the mancubus.

Those Mancubuses killed in the most horrific ways were re-fused, turning scarlet. Their skin takes on the color of dried blood, and their essence changes. Mental, always looking for ways to strengthen his Horde, bought several dozen of these creatures from the Lord of Hell (along with Zombie Soldiers and other undead) and sent them for repurposing.

Instead of flamethrowers, rocket launchers were grafted onto their forearms. Two RPGs fire continuously until the enemy dies. Even if the enemy hides behind a wall, the rockets will fly again and again, methodically reducing the cover to a sieve and then the shooter to mincemeat.

Unlike a regular Flamethrower Mancubus, the ScrapJack Mancubus is weaker in defense. Welded together from the pieces of various demons, it lacks monolithic armor and can be killed faster. But only if you can get there before it turns everything around into a crater of explosions.

TIPS:

- Take cover. The rockets are random, and a stone wall can save your life, but only for a short time.
- They fire endlessly. Don't wait for them to run out of ammo. They don't have any.
- Kill them quickly. Yes, they're weaker than a regular Mancubus, but every second you wait increases the chance of you turning into a bloody mess.
- Don't stand in the open. The rockets have a wide spread, and hiding around corners is the only way to survive.
- Don't let them aim. If you see a Mancubus aiming its rocket launchers at you, move to the side immediately. They don't fire instantly, but once they start, they're almost impossible to stop.
- Use explosives. One well-placed C4 can destroy it faster than 5 shotgun blasts.
