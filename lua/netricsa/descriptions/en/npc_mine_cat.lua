CAT FROM THE MINECRAFT OVERWORLD


CLASS:      Domestic / Neutral Animal
METABOLISM: Warm-blooded Mammal
PERCEPTION: Acute hearing and vision
SIZE:       2,6 ft
ENDURANCE:  Low
HOSTILITY:  Low
WEAPONALS:  None
REWARD:     10 FC
THREAT:     Low

DESCRIPTION:

Cats are tameable neutral creatures in the Minecraft Overworld, most often found in jungles, villages, and witch huts. Once tamed, they become loyal companions and follow you around, periodically sitting, lying down, or curling up nearby.

Cats have an innate repellent instinct: creepers and phantoms avoid cats, making them not only decorative but also strategically useful. Cats come in a variety of colors, from black and orange to spotted and Siamese.

