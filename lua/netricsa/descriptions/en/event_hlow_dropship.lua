COMBINE ELITE DROPSHIP


CLASS      Combine Elite Dropship
METABOLISM Fully Synthetic
PERCEPTION High-speed Dropship of elite and standard units
SIZE       ~55-65 ft (with container)
ENDURANCE  Extreme
HOSTILITY  Critical
WEAPONS    None
REWARD     6000 FC
THREAT     Very High

DESCRIPTION

The Combine Elite Dropship is an advanced synthetic troop carrier designed for the rapid Dropship of elite Combine units, including elite soldiers and standard strike teams. Unlike standard Dropships, this model is optimized for maximum speed and a minimal combat profile.

The Synthetic's hull is painted black and white with reinforced composite panels, visually distinguishing it from standard Dropships. The container is designed to carry 3-5 troops at a time. After deployment, the container remains open even when the Dropship is withdrawing, speeding up the deployment cycle and reducing the time spent in the kill zone.

There are no weapons modules, as the platform's concept is based on the quickly enter, unload, and get out principle. The Elite Dropship is virtually invulnerable to standard weapons and vulnerable only to RPG V2 missiles, where two hits are enough to destroy or critically disable.

TIPS

- Use the RPG V2; other weapons are ineffective.
- Two accurate hits are enough to knock out the Dropship.
- Attack during descent and disembarkation, when the trajectory is predictable.
- The container does not close during withdrawal, leaving it vulnerable to fire on the internal compartment.
- The destruction of an elite Dropship ship could disrupt the deployment of elite troops and change the course of the battle.
