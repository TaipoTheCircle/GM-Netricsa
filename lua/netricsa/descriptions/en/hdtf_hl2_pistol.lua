9MM PISTOL


WEAPON:      9mm Pistol
AMMO:        9x19mm rounds
ROF:         Semi-automatic, ~15 rds/min effective

DESCRIPTION:

Standard-issue semi-automatic sidearm. Reliable, lightweight, and always the first weapon at hand. Lacks stopping power but effective for precision shots against weak or distant enemies.

NOTES:

- Moderate accuracy, low recoil.
- Ineffective against armored targets.
- Useful for conserving heavier ammo types.