BRICK, BRICK'S YOUNGER BROTHER


Sam, it looks like we've stumbled upon a minimalist relative of those brick mansions we've been exploring. If the previous version was an "urban village" with a swimming pool and two-story buildings, this one is a stark, suburban despair squared.

Two houses, brick, grass, and a complete lack of hints of civilization. They haven't even dug a pond.

Before us is a smooth, green lawn, on which stand exactly two buildings:

Two square brick houses. Without doors. This is already a tradition. Apparently, the architect was inspired by the concept of "hospitality is an illusion, openings are reality."

Both are made of red brick, both square, both silent.

Nothing else. No fence, no trees, no cars. Just house #1, house #2, and you.

First House (aka "Staircase"):

There's a staircase to the second floor. It works, leads up. It's not broken, not nailed down, not guarded by a gnome. Just stairs.

On the second floor, there's one window. No glass. A draft, a view, the chance to lean out and shout, "Who are you!? Go to f..." Or just lean out and get shot in the forehead. A window is a thing.

Second House (aka "Empty"):

There's no second floor. None at all.

There's nothing inside.

This place is pure geometry. Two houses, grass, sky. No distractions, no Sophia's supplies, no swimming pools. Just you, the enemy, and a brick. Here, everything depends on your reaction speed and your ability to use the only window on the second floor.

We can begin.
