SKELETON SWORDSMAN FROM THE MINECRAFT OVERWORLD


CLASS:      Skeleton Swordsman
PERCEPTION: Undead melee warrior
METABOLISM: Magically sustained undead
SIZE:       6,2 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPON:     Stone Sword
REWARD:     350 FC
THREAT:     Medium-High

DESCRIPTION:

The Skeleton Swordsman is a skeleton who decided that bows are for weaklings and cowards, and that true undead should look their victims in the eye... even if their eyes are long gone. Armed with a sword and some dubious combat experience from the afterlife, he charges head-on without hesitation.

Sometimes it seems the Skeleton Swordsman genuinely resents the living-perhaps because they still have skin, nerves, and the ability to feel pain.

TIPS:

- Don't let him get too close-he's more dangerous than he seems.