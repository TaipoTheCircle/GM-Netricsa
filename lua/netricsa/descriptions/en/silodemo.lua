BLACK MESA LOBBY, SILO DEMO


Sam, we're in the silo. Not a live version, not a full version-a demo. A place where Black Mesa once tested rocket engines, and now tests our nerves.

They grew here. Or descended here. Or were blown in by the winds of an interdimensional collapse. Three Tentacle-class creatures. Although the tentacles themselves are a single organism with tentacles operating separately.

Problem: no flamethrower. No rocket engine to aim at them. Nothing that can fry them. And without fire, these creatures are immortal. There's no point in doing anything-it's written in the protocols. And Black Mesa protocols are usually written in the blood of those who ignored them.

The only way that's ever worked (sometimes accidentally, due to bugs and desperation) is to get the timing right and fire the beam gun continuously at the root of the tentacles. Not the trunk, not the suction cups, but right where the flesh meets the floor. They have some kind of nerve center there. It might work. It might not. But there's no choice.

Currently, three guards are fighting them. They're not eager to die, but they have nowhere to retreat. They're firing with everything they have. The tentacles don't care. The guards are meat. It's only a matter of time before they fall.

And there are H.E.C.U.s holed up in one of the rooms.

They're just sitting in cover and waiting. For what? Maybe reinforcements. Maybe our death. Maybe for everything to resolve itself. But this place leads nowhere. It's a dead end. Beyond that, there are only walls, pipes, and tentacles.

The only way out is to kill the creatures. Or die.
