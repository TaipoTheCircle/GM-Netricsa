RIPPED STRIDER CANNON


WEAPON: Ripped Strider Cannon
AMMO:   AR2 Pulse Batteries
ROF:    ~120 RPM
WEIGHT: ~35 kg

DESCRIPTION:

A heavy pulse cannon ripped from a Strider's combat module and adapted for handheld use. Despite the crude modification, the system retains compatibility with the energy batteries of a standard AR2 pulse rifle.

The weapon is designed to destroy armored targets and vehicles at medium to long range. Each shot emits a powerful energy pulse capable of penetrating cover and inflicting critical damage. Due to its mass and lack of full stabilization, it requires considerable physical effort to use.

NOTES:

- Compatible with AR2 batteries.
- Effective against heavy synths and mechanized targets.