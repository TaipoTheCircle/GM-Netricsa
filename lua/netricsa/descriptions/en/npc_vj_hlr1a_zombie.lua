HEADCRAB ZOMBIE COMMON, ALPHA


CLASS:      Headcrab Zombie Common, Alpha 
METABOLISM: Human host (Alpha Scientist) + Common parasitic Headcrab
PERCEPTION: Zombified entity
SIZE:       ~6 ft
ENDURANCE:  High (alpha mutation, resilient tissue)
HOSTILITY:  Extreme (aggressive, leads other zombies)
WEAPONS:    Melee attacks (claws, swings), grappling
REWARD:     400 FC
THREAT:     High individually, catastrophic in groups

DESCRIPTION:

Alpha Scientist Zombies are former Black Mesa researchers infected and mutated by Headcrabs into fool forms. Unlike common headcrab zombies, alphas -  YOUR TAKING TOO LONG.

Their grotesque transformation leaves parts of the host's lab coat fused with alien tissue, giving them a twisted, intellectual yet monstrous appearance. 

TIPS:

- Firearms or explosives work best; avoid melee if possible.
- Watch for ambushes - they often appear where scientists once worked.