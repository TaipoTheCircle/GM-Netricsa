T-REX, SCARFACE MOM FROM A TERRAFORMED PLANET


CLASS:      T-Rex, Scarface Mom
METABOLISM: Apex Predator
PERCEPTION: Dominant Lifeform
SIZE:       20 ft
ENDURANCE:  Extreme
HOSTILITY:  Absolute
WEAPONS:    Jaws, claws, tail
REWARD:     5000 FC
THREAT:     Absolute

DESCRIPTION:

Scarface Mom. And she didn't get that name for her pretty eyes. She had a whole brood of young to feed, and she fed them with everything she could reach. She has a deep Scarface line down the right side of her snout, through her eye, and into her skull. The eye beneath the Scarface was cloudy and likely blind, but that didn't stop her from finding her prey again and again.

In combat, Scarface Mama is a force of nature. She won't chase you like a velociraptor. She'll simply go where she wants, and if you get in her way, that's your problem. She's quite fast and agile.

TIPS:

- Don't get in the open. She's faster than you and stronger than any cover.
- You can take Scarface Mama out with grenades right in the eye wound.
- Use Heavy Weapons.
