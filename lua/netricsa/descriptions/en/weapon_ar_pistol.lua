AR-15 SELF-LOADING PISTOL


WEAPON:      AR-15
AMMUNITION:  5.56x45 MM cartridges
ROF:         ~20 rounds/min

DESCRIPTION:

The AR-15 pistol is a short-barreled weapon assembled using the receiver from an AR-15 rifle, but with parts to create a pistol that can be held and fired with one hand.

NOTES:

- Due to its caliber, this pistol has good penetration.
- Not to be confused with the AR-15 rifle.






