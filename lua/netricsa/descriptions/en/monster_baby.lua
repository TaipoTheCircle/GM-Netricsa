BABY FROM THE FAVERSHOLM


CLASS:      Baby
METABOLISM: Nightmare Spawn
PERCEPTION: Hostile Psychological Horror
SIZE:       2,6 ft
ENDURANCE:  Low
HOSTILITY:  Absolute
WEAPONS:    Thorn
REWARD:     50 FC
THREAT:     Medium

DESCRIPTION:

A reflection of the darkest corners of the psyche. This small, fast, and extremely aggressive adversary symbolizes the irritation of the crying of newborns. Innocent in form, but deadly in essence.

The Baby appears as a small, pale, naked figure.

It is found in apartments. It has no sophisticated tactics, no arsenal, not even a sense of self-preservation. He has only one goal: to run up to you and impale himself with a spike emerging from his head.

Yes, you heard that right. The Kid kills himself to try to kill you.

He's quite fast-faster than most enemies. If he hits his target, a long, sharp spike shoots out of his head, dealing damage. The Kid himself is killed. You don't need to finish him off-he does it himself.

The damage from a single attack isn't significant. But the problem is that these Guys usually appear in groups, not individually. If two or three of them hit you in a row, your health will plummet.

TIPS:

- The Kid dies in one hit from any weapon. Even a pistol.
- Don't let him get close. Keep your distance and shoot first.
- They're especially dangerous in narrow corridors, as there's no room to dodge. Use a shotgun at point-blank range.
- If you hear a child's cry and the patter of little feet, get your gun ready. They're already running.
- Don't wait for them to get there. Run toward them and shoot them from a safe distance.
