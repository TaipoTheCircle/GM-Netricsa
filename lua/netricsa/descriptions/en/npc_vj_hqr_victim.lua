VICTIM FROM THE HALF-QUAKE SHOW


CLASS:      Victim
METABOLISM: Human, Cyborg, Male
PERCEPTION: Prisoner of the Show, Rebellious Pawn
SIZE:       ~6,2 ft
ENDURANCE:  Variable
HOSTILITY:  High
WEAPONS:    MP5, SPAS-12 Shotgun
REWARD:     500 FC
THREAT:     High

DESCRIPTION:

At first glance, this is a soulless robot. Green armor, angular forms, a heavy gait. But behind the armor lies human flesh. Blood, muscle, despair, and hatred. Its face is hidden behind a metal mask, but if you look closely, you can see something alive. Something that long ago lost hope, but has not lost its rage.

He ended up on the Half-Quake show for "his terrible mistake in life". The sadists won't say which one. Maybe he killed the wrong person. Maybe he crossed the wrong sadist. Or maybe there was no mistake at all. They simply needed a body for a new episode. And he was chosen for that body.

He's armed with an MP5 and a SPAS-12 shotgun. Not because they gave him guns. But because he took them from the guards he killed in previous episodes. He hates the show. He wants to escape. He wants to destroy this place. But most importantly, he wants to never die again.

The Victim has died many times. He's been cut, burned, drowned, blown up, stabbed, fed to monsters. But he rises again. Again and again. The sadists think this makes the show more interesting. The Victim herself believes it's a curse. But every time he opens his eyes after another death, he takes up arms and moves on, determined to put an end to the nightmares.

TIPS:

- Don't shoot the Victim. He's not your enemy.
- If you meet him in the corridors of Shaw, stay close. He knows where the guards are, where the traps are, and where the exit is.
- The Victim trusts no one. Don't expect her to share her ammo with you. But she can cover you in a fight.
- The sadists hate him. Somos does too. The fact that she's still alive (in a sense) infuriates them more than any rebellion.
- Don't try to save him. He doesn't need saving. He needs revenge. And in the end, he will either destroy Shaw or die for the last time. For good.
