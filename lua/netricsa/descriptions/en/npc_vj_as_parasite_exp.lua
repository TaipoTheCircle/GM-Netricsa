"BETA" SWARM EXPLOSIVE PARASITE


CLASS:      "Beta" Swarm Explosive Parasite
METABOLISM: Xenobiological
PERCEPTION: Assault Specimen
SIZE:       3,2 ft
ENDURANCE:  Very Low
HOSTILITY:  Very High
WEAPONS:    Jump Attack, Biological Detonation
REWARD:     70 FC
THREAT:     Medium-High

DESCRIPTION:

The Explosive Swarm Parasite "Beta" is a specialized parasite variant, adapted not to infect its hosts, but to instantly destroy its target at the cost of its own life. Its body structure is virtually identical to that of a standard Swarm Parasite "Beta," but its enlarged abdomen resembles the abdominal sac of a Harvester and is filled with unstable explosive biological agents.

These creatures are extremely aggressive and immediately rush toward the nearest living target after appearing. Upon reaching their victim, the parasite makes a long leap and detonates on impact, striking the enemy with a blast wave and scattering corrosive organic fragments.

Explosive parasites most often appear after the death of a Swarm Harvester "Beta", which releases several of these creatures at once. Thanks to their small size and high speed, they can quickly overcome obstacles and pose a serious threat in tight spaces or during combat with other Swarm members.

While each parasite only lasts until it attacks, their mass deployment allows the Swarm to overwhelm enemy defenses, forcing them to constantly focus on rapidly approaching targets.

TIPS:

- Destroy parasites from a distance, preventing them from getting close.
- After the death of the Harvester, expect several Explosive Parasites to spawn immediately.
- A single explosion can inflict damage on multiple fighters.
- Weapons with a high rate of fire or area damage are most effective against these creatures.
