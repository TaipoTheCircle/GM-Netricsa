BLACK MESA ALIEN RESEARCH LAB, SMALL WAREHOUSE


A small warehouse. Cramped, crammed with boxes, with only one exit-a narrow tunnel leading into the unknown.

We're on the freight elevator. Heavy, slow, taking forever to get here, while we shifted from foot to foot and wondered who would meet us at the top. A warehouse met us. And an exit.

But the exit, Sam, isn't just a hole in the wall. It's a small tunnel. Narrow, winding. And it leads to the alien research labs.

Where they once took samples, analyzed them, and possibly conducted experiments no one was supposed to see.

Now, judging by the data I'm picking up, it's an active war zone.