HORNS FROM THE BROADSIDE BEACH


CLASS:      Horns
METABOLISM: Cursed Creature
PERCEPTION: Forest Monster
SIZE:       5,9 ft
ENDURANCE:  Infinite
HOSTILITY:  Absolute
WEAPOON:    Touch
REWARD:     Life or Death
THREAT:     Absolute

DESCRIPTION:

No one knows where he came from or what he even is. His appearance resembles a hunchbacked animal with stone-black skin. Instead of a face, he has a skull with horns.

Rumor has it that Horns was once a rabbit who insulted God and every time he went outside, lightning would strike him, burning him but never killing him, which eventually led to him taking his current form.

He slowly patrols the abandoned fortress and the Sleepy Forest, trying to prevent Bucky from finding the pieces of his wrecked ship. He moves very slowly-even slower than Bucky-but he always knows where his target is and never stops. If Horns grabs Bucky, he will die.

TIPS:

- Horns can't be killed. Don't try. Just run.
- He always knows where you are. Hiding is useless.
- Move, but don't run unnecessarily. There's a rhythm to his slow gait that you can exploit.
- Don't try to understand his nature. He's just a shadow Forest Monster.
