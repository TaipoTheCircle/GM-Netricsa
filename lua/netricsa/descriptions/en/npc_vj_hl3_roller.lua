COMBINE HIDDEN ROLLER MINE


CLASS:      Autonomous Hidden Interceptor Mine
METABOLISM: Synthetic Engine with Phase Cloaking
PERCEPTION: Advanced Motion Sensors + Close-Range Target Scan
SIZE:       1 ft
ENDURANCE:  Average
HOSTILITY:  Extremely High
WEAPONS:    Jump Detonator with Directional Explosion
REWARD:     500 FC
THREAT:     High

DESCRIPTION:

The Hidden Roller Mine is an advanced version of the standard Combine Roller Mine, designed for ambush and instant target elimination. When active, it is completely invisible, using phase cloaking and reflection suppression, making it undetectable visually.

When movement is detected within the kill zone, the mine makes a sharp leap toward the target and detonates in mid-air, leaving no time for reaction or retreat.

A distinctive design feature is the complete isolation of its internal systems. Water, moisture, and even complete immersion have no effect on its functionality. This allows concealed Roller Mines to be placed in flooded areas, under snow, in ice, and in swampy areas.

TIPS:

- Cover is ineffective - detonation occurs during a jump, above ground level.
- It takes 3-4 Inferno missiles to defuse the mine.