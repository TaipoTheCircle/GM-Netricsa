BURROBOT FROM THE MOBIUS


CLASS:      Burrobot
METABOLISM: Animal-Battery
PERCEPTION: Underground Ambush
SIZE:       4 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Nose Drill, Hull
REWARD:     400 FC
THREAT:     Low

DESCRIPTION:

When Dr. Eggman needed to monitor underground communications and labyrinths, he turned his attention to moles. Their ability to dig tunnels and suddenly emerge from underground was ideal for setting ambushes. Thus was born Burrobot - a distant ancestor of the more famous Grunder.

In place of ears are gray disks, in place of a nose is a massive cone-shaped drill. On top of its head is a yellow helmet. Instead of legs, it has treads with two wheels on the sides: one small, one larger.

In later modifications, it received yellow goggles and the ability to pursue targets underground.

TIPS:

- Watch your nose. If a gray drill is sticking out of the ground, there's a Burrobot underneath you.
- Attack as it jumps out, before it lands.
- On the surface, it's helpless and slow-there's no need to rush.
- It's best not to aim for the treads-they're stronger than the body.
