ECHINDON FROM THE TERRAFORMED PLANET


CLASS:      Echindon
METABOLISM: Omnivorous
PERCEPTION: Small gregarious animal
SIZE:       3 ft
ENDURANCE:  Very low
HOSTILITY:  Average
WEAPONS:    Bite, claws
REWARD:     50 FC
THREAT:     Average

DESCRIPTION:

These are close relatives of mini-raptors, only darker, with smaller eyes and less pronounced claws. Their brown skin, small natural eyes, and lack of bright feather growths make them look like miniature versions of something larger and more dangerous. Overall, they are nondescript, gray, and inconspicuous.

The main difference between echindons and mini-raptors is their personality. Mini-raptors are aggressive by default and attack anything that moves. Echindons are passive and don't bother people unless they're bothered. If you pass by a pack of Echindons, they'll just sit and watch. But if you shoot one or accidentally hit one, the entire pack instantly goes berserk and attacks.

Otherwise, Echindons behave similarly to mini-raptors: they jump, bite, and try to surround and pin down their prey. Alone, they're helpless and die with a single knife strike or shot. In a pack, they can be a nuisance, especially if caught off guard in the jungle, where there's plenty of cover and little room to maneuver.

TIPS:

- Don't shoot first. If you leave the pack alone, they'll pass by.
- A knife works great. One strike kills one.
- Echindons are small but fast. Don't let them surround you.
