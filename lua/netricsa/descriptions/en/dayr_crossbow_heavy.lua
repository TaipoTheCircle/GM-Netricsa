HEAVY SURVIVAL CROSSBOW


WEAPON: Heavy Survival Crossbow
AMMO:   Crossbow Bolts
ROF:    ~0,7 shots per second

DESCRIPTION:

A reinforced version of the crossbow, designed to inflict maximum damage per shot. The design is significantly heavier and reinforced, allowing the bolts to fire with destructive force superior to standard and enhanced models.

This weapon is effective against lightly armored and medium-durability opponents, allowing them to be quickly neutralized with powerful single shots. With the appropriate skills, it can use poisoned bolts, dealing additional damage over time.

NOTES:

- High damage.
- Can inflict additional poison damage when using poisoned bolts.
- Slow rate of fire is compensated for by its power.
- Effective at close to medium ranges.
- Has no armor penetration, reducing its effectiveness against heavy armor.
- Uses standard bolts, which can be crafted from scrap materials.
