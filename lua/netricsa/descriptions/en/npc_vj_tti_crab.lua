BIG EUGENE HAROLD KRABS FROM THE BIKINI BOTTOM


CLASS:        Big Eugene Harold Krabs
METABOLISM:   Mutated crustacean
PERCEPTION:   Predatory strategist, organizer of mass murder
SIZE:         6 ft
ENDURANCE:    Very high
HOSTILITY:    Absolute
WEAPONS:      Giant claws, devastating physical strength
REWARD:       1200 FC
THREAT:       Critical

DESCRIPTION:

Big Eugene Harold Krabs, better known as Mr. Krabs, is not just the owner of the establishment, but the mastermind behind the entire meat-fueled scheme. He is the mastermind behind the Krusty Krab's transformation into a hidden cannibal factory. Krabby Patties were made from fish, which were then eaten by other fish. Lonely customers disappeared without a trace, becoming part of the menu. Business was perfect.

Krabs is a money fanatic. Not just greedy, but obsessive. For him, profit is more important than morality, logic, and even self-preservation. For the sake of profit, he's willing to exploit anyone, turning life into a commodity and fear into sauce. His mind is cold, calculating, and completely devoid of doubt.

Physically, he's an overgrown crab with a massive body and claws capable of crushing an opponent in seconds. He doesn't negotiate. Any approach is punishable by instant reprisal.

TIPS:

- Close combat is fatal for you.
- He doesn't panic or run away.