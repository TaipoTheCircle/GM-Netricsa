SPIDER FROM THE MINECRAFT OVERWORLD


CLASS:      Spider
PERCEPTION: Hostile
METABOLISM: Arthropod
SIZE:       2,9 ft
ENDURANCE:  Average
HOSTILITY:  Variable
WEAPONS:    Bites, jumping, wall climbing
REWARD:     120 FC
THREAT:     Average

DESCRIPTION:

The spider is a classic Overworld nightmare. During the day, it behaves almost decently and usually ignores everyone, as if pretending to be an ordinary landscape element. But with the onset of darkness, it remembers that it is, in fact, a predator and begins to actively attack.

The spider's main characteristic is its ability to climb vertical surfaces. Walls, fences, and houses are not an obstacle for it, but an invitation.

Sometimes spiders appear with additional "passengers"-skeletons-forming the famous spider jockey, which dramatically increases the level of problems per square meter.

TIPS:

- During the day, the spider is neutral-you can attack first without risk.