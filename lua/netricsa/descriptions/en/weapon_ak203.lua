AK-203 ASSAULT RIFLE


WEAPON:      AK-203 assault rifle
AMMUNITION:  7.62x39 mm
ROF:         ~900 rounds / min

DESCRIPTION:

A modern modification of the iconic assault rifle, known for its reliability and durability. It fires powerful 7.62 mm caliber cartridges with moderate recoil. It is effective at medium ranges and capable of sustained fire for suppression or direct engagement. Various modules can be attached to it.

NOTES:

- High durability and ease of use.
- Moderate recoil; controllable in short bursts.
- Effective against unarmored and lightly armored targets.
- Magazine capacity requires periodic reloading during prolonged combat.
- This assault rifle can be modified with various modules as desired.