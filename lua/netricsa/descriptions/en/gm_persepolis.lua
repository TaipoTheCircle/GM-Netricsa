PERSEPOLIS, THE DUST OF CENTURIES ON THE BONES OF EMPIRES


Sam, This is Persepolis, the ancient capital of the Achaemenid Empire, the "City of the Persians", which Alexander the Great turned into a gigantic bonfire in 330 BC.

Now there is only desert, wind, and ruins that remember the march of peoples from the Nile to the Indus.

Imagine an endless sandy plain, over which a haze floats. In the middle of it, on a massive stone platform, rise the skeletons of columns, staircases, and gates.

The ruins retain traces of their former grandeur:

Apadana - the ceremonial hall of Darius I. Once upon a time, 72 columns rose here, supporting the roof. Now only 13 remain. But even they are astounding. Giant staircases lead up to them, once used by chariots and delegations of conquered peoples bearing gifts.

Tachara is the residential palace of Darius I. Only the foundation, doorways, and lower sections of the walls with bas-reliefs remain.

The Hall of a Hundred Columns is a huge chamber, of which only the column bases and stone gates with battle scenes remain.

The Harem of Xerxes is twenty-two small rooms where women and children could have lived.

The Tomb of Darius III is the unfinished and naturally devastated tomb of the last king of the dynasty, who fled from Alexander and was killed by his own people.

This city was not just a capital, but a symbol of an empire stretching from Egypt to India. Craftsmen from all over the world—Babylonians, Egyptians, Ionians, and Assyrians—built it. However, slave labor was not used here, a rarity at the time. The city had a complex water supply and sewerage system. Alexander burned it to the ground—either in a drunken stupor, incited by the Athenian courtesan Thais, or deliberately, as an act of revenge for the burning of the Acropolis.

Priceless copies of the Avesta, written in gold ink on ox hides, perished in the fire. After this, the city lost its significance, and Istakhr, the spiritual center of Zoroastrianism, grew up nearby. The ruins of Persepolis are now a UNESCO World Heritage Site.
