ROYAL IMPERIAL GUARD FROM THE NIRN


CLASS:      Royal Imperial Guard
METABOLISM: Mer, Male
PERCEPTION: The Emperor's personal guard / Elite special forces of the Imperial Legion
SIZE:       5,9 ft
ENDURANCE:  Very high
HOSTILITY:  Conditional
WEAPONS:    Long swords of thin steel, heavy claymores, Imperial shield
REWARD:     7000 FC
THREAT:     Absolute

DESCRIPTION:

These are not just soldiers. They are "the greatest warrior-mages in Tamriel", the Emperor's personal shadow, the elite of the Imperial Legion. While an ordinary legionnaire defends the borders and catches bandits, the Royal Guard is responsible for one thing only: the life and safety of the Emperor and his inner circle. Their barracks and training halls are located directly within the Imperial Palace, so they can respond to threats instantly.

Becoming a guardsman is incredibly difficult. Candidates are selected in childhood, called "Potentials." They are trained in secret academies like the Forgotten Legion, where they train not just fighters, but warrior-mages, skilled with sword and spell. Every year, a Tournament of Potentials is held, where the winners are awarded the right to be tested. Only a few survive and are selected for the Royal Guard.

The Royal Guard existed for centuries, protecting the Septim Dynasty. However, their history is tragic. During the Imperial Farce (3E 389), General Talin Warhaft and many guardsmen were banished to Oblivion, and their places were taken by duplicates created by Jagar Tharn. Later, during the Oblivion Crisis, when Emperor Uriel Septim VII was assassinated by the Mythic Dawn, the Royal Guard joined the investigation into his death alongside the Imperial Legion, the Elders, and the Mages Guild.

After the fall of the Septims and the establishment of the Median Dynasty, the status of the Royal Guard is unclear.

The Royal Guard reports directly to the Emperor and his closest military commanders. Their structure includes the following ranks:

General of the Guard - the supreme commander.

Centurion - commands units.

Captain - responsible for specific installations and operations.

Lieutenant - assistant to the captain.

Sergeant - junior commander.

Guardsman - the main combat personnel.

TIPS:

- If you plan to break the law in the Imperial City, do not do so near the White-Gold Tower. The Guardsmen will kill you faster than you can draw your weapon. Guardsmen are not easily persuaded. Unlike regular guards, they cannot be bribed or persuaded.
- When fighting a guard, use long-range magic and avoid close combat.
- If you see a guard captain, be especially careful. They have a higher level and better equipment.
- Unlike regular guards, guard armor cannot be removed from corpses. Don't waste time trying to loot them.
- If you encounter a guard armed with a claymore instead of a longsword, run.
- Even after the Emperor's death, guardsmen continue to guard the Palace. Their duty does not end with the Emperor's death.
