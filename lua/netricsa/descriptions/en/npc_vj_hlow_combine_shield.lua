COMBINE OVERWATCH SHIELDIER


CLASS:      Combine Overwatch Shieldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Assault Protective Infantry
SIZE:       5,9 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    Combine Pistol, Tactical Shield
REWARD:     450 FC
THREAT:     Medium

DESCRIPTION:

The Overwatch Shieldier is a mobile wall of Combine fire, designed to advance under fire. He wears no heavy armor, relying on a massive black shield with CMB markings that covers almost his entire body. The shield is covered in a composite coating and can withstand automatic rifle fire and shrapnel.

Blue visors identify the standard Overwatch variant, but this soldier's behavior is different. He doesn't seek coverâ€”he becomes cover. The soldier advances slowly, covering his allies and allowing assault units to advance behind him.

The pistol is used from behind the shield with one hand, most often for suppression at close range. In close combat, the soldier can ram the pistol, knocking enemies down and opening them up for finishing.

These soldiers are used in corridors, during room clearing, riot suppression, and street operations where frontal defense is essential. The Combine uses them as moving barricades under which the rest of the infantry advances.

TIPS:

- Don't shoot the shield-it's better to flank or overhead.
- Explosives and fire are effective at knocking him out.
- He's vulnerable from the sides and behind, especially without support. - If he comes close, get ready for blows.