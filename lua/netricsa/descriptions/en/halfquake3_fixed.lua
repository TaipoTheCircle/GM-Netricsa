HALF-QUAKE SHOW, BUTTONS


We're in a room, Sam. Still a room. Very soon, it will begin to spin and stop pretending to be architecture. The walls will shift, the floor will twist, and everything around will turn into a carefully mixed mess of metal, flesh, and the show's design solutions.

Two headcrabs sit behind one wall. Patiently. As if they know they're meant to play a small supporting role. Grenades and snarks lie behind them. A nice bonus for those used to testing the walls for honesty. When they created it, the organizers called it "opportunity." I call it a miscalculation.

Ahead is a tunnel leading to the next rooms. Everything here is built on the principle of "there and back." The buttons are arranged so that you're constantly running: press one, go back. Press the second, back again. And so it goes until the system finally admits you've suffered enough to open the door.

And yes. At the very end, behind a pipe, there's a secret button hidden. Small. Unobtrusive. For those who pay a little more attention than expected. The show doesn't like it when such things are found, but it doesn't dare remove them either. The illusion of fair play must be maintained.

So run, Sam.

Press. Return. Hurry.

And try not to become part of the scenery while the stage is spinning.