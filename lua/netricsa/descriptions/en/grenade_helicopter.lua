COMBINE HUNTER CHOPPER MINE


CLASS:      Combine Hunter Chopper Mine
METABOLISM: Mechanical Ammunition
PERCEPTION: Airdropped Anti-Personnel Mine
SIZE:       ~1,7 ft
ENDURANCE:  Very Low
HOSTILITY:  Absolute
WEAPONS:    Contact Explosion, Shrapnel
REWARD:     10 FC
THREAT:     High

DESCRIPTION:

This is the Combine's primary clearing weapon when they want to wipe out the enemy. It is a timed explosive dropped in large quantities by Hunter Choppers. These mines are especially effective when a helicopter suffers critical damage and begins to panic, pelting the attacker with dozens of small, deadly toys.

Mines float in water, making them an effective weapon against watercraft.

Mines have a short timer before they explode after being dropped. However, if you grab them with the Gravity Gun, the timer increases by a few seconds. If you release them, the timer resets to zero and returns to its original value.

TIPS:

- Don't wait for them to explode. Use the Gravity Gun to catch them and throw them back.
- Mines float in water and don't sink.
- One shot is enough to detonate a mine in mid-air.
