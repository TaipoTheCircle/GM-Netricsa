EARLY MUTANT KRIEGER FROM THE KRIEGER ARCHIPELAGO


CLASS:      Early Mutant Krieger
METABOLISM: Genetically modified human, Male
PERCEPTION: Enhanced vision, retained intelligence
SIZE:       ~8,2 ft
ENDURANCE:  High
HOSTILITY:  Absolute
WEAPONS:    OICW, grenades, superhuman strength, jumping
REWARD:     100000 FC
THREAT:     Maximum

DESCRIPTION:

The final form of Dr. George Krieger, achieved through the administration of an advanced Trigen serum. Unlike normal mutants, he retained full intelligence and reason, becoming the "perfect" Trigen - an apex predator. Combines technological weaponry (OICW, grenades) with phenomenal mutant abilities: leaping vast distances and enhanced survivability. It's the culmination of his insane philosophy about the superiority of strength and accelerated evolution.

TIPS:

- Despite his abilities, his health is not unlimited. It's best to use powerful weapons.
- Keep moving. His jumps allow him to quickly close the distance.