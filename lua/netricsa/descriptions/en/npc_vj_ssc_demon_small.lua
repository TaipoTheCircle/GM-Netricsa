FIENDIAN REPTILOID DEMON, TINY


CLASS:      Fiendian Reptiloid Demon, Tiny
METABOLISM: cold-blooded, reptile-like
PERCEPTION: modified primitive race
SIZE:       0.4 ft
ENDURANCE:  medium
HOSTILITY:  high
WEAPONS:    lavaball projectile, claws
REWARD:     5000 FC
THREAT:     medium

DESCRIPTION:

Another hideous mutation, courtesy of Notorius Mental. A simple lizard-like organism from Fiendia Prime was crossbred and mutated through several generations, and was finally reinforced with a titanium skeleton. It has a unique ability of forming a miniature black hole through which it can summon floating balls of molten lava into existence. Weak magical forces will hold this fiery projectile coherent for a short period of time. As it flies through the air, it will cool down and finally explode.

During experiments with creature reduction, this micro-reptilian retained all the dangerous abilities of its giant prototype, including creating miniature black holes and hurling gouts of molten lava. Its titanium skeleton has become virtually indestructible micro-armor. Being so tiny, it is virtually invisible in tall grass, crevices, and debris. It can be killed by literally stepping on it, but its very size makes it deadly.

TIPS:

- The projectiles are indestructible, so dodge them until they explode.
- Step on it and it will die.