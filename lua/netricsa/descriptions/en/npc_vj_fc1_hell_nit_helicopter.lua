DESCRIPTION:

This isn't any company's weapon. It's the archipelago itself, coming for what's owed to it. And it won't stop until it takes everything. This is the ultimate manifestation of the Unpaid Moai's wrath. An immortal spirit of rage, materialized in a distorted UH-60 Blackhawk helicopter, glowing with crimson volcanic plasma. The entire hull is repainted a hellish crimson, unquenchable flames blaze from the cracks, and the standard hum of engines is replaced by the roar of ancient, unquenchable hatred and genocidal music. His main weapon is a hypercharged minigun, whose bullets inflict not damage but the absolute concept of death. His goal is not patrolling, but the complete cleansing of the archipelago of all life, including Krieger's mercenaries, the Trigens, and, of course, Jack Carver. He is flying retribution for three days of unpaid wages, elevated to the absolute.

TIPS:

- STAY OUT OF FIRE. One burst, one shell, one hit - guaranteed death. Cover, other than massive, indestructible rocks or underground bunkers, is useless-his shots burn through concrete and steel.
- It's impossible to destroy him.
- Lure him to mercenary bases or Trigen clusters. His rage is blind, and he'll attack everything, distracting him from you.