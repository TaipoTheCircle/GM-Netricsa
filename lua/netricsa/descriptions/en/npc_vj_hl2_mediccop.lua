COMBINE CIVIL PROTECTION MEDIC


CLASS:      Combine Civil Protection Medic
METABOLISM: Augmented Human
PERCEPTION: Suppression Support Personnel
SIZE:       5,9 ft
ENDURANCE:  Medium
HOSTILITY:  Low
WEAPONS:    Pistol, stun baton, submachine gun, medical supplies.
REWARD:     450 FC
THREAT:     Low

DESCRIPTION:

The Combine Civil Protection Medic is a mobile command module for Combine personnel. Most were paracs, military medics, field doctors, or medical students. After the Combine's arrival, their knowledge was considered active.

Medics wear modified Civil Protection uniforms with the red insignia of the Combine medical sector.

TIPS:

- Eliminating the medical team dramatically reduces the Combine squad's survivability.