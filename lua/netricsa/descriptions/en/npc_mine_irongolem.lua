IRON GOLEM FROM THE MINECRAFT OVERWORLD


CLASS:      Iron Golem
PERCEPTION: Village guardian / defensive structure
METABOLISM: Mechanical / artificially created
SIZE:       8,8 ft
ENDURANCE:  Very high
HOSTILITY:  Average
WEAPONS:    Powerful fists, enemy throws
REWARD:     100 FC
THREAT:     Average

DESCRIPTION:

The Iron Golem is a huge, massive structure created to protect villagers from hostile mobs. Despite its imposing appearance, it is friendly to humans and villagers.

It patrols the village, responding to any threats, be they zombies, skeletons, or other hostile creatures.

It can lift and throw mobs over distances using the strength of its metal arms.

Possesses immense physical strength and high survivability, making him virtually invincible to most common mobs.

TIPS:

- Use the Golem as a shield - it distracts enemies and deals significant damage.
- Several Golems working together transform a village into a nearly impenetrable fortress.