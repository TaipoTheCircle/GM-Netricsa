AGS-30 AUTOMATIC GRENADE LAUNCHER


WEAPON:      AGS-30
AMMUNITION:  30 mm cartridges
ROF:         ~400 rounds/min

DESCRIPTION:

Russian automatic 30 mm machine grenade launcher. Developed in the early 1990s at the Tula Instrument Design Bureau as a lighter replacement for the AGS-17 grenade launcher.

NOTES:

- You can rain grenades down on the enemy.
- Better be careful with this thing, you could blow yourself up.



