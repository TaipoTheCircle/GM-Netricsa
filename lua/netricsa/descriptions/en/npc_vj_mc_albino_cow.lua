ALBINO COW FROM THE MINECRAFT OVERWORLD 


CLASS:      Albino Cow
METABOLISM: Warm-blooded mammal
PERCEPTION: Passive animal
SIZE:       ~4,6 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     50 FC
THREAT:     Minimal

DESCRIPTION:

An extremely rare variety of cow, listed as an example of "paleness among animals is quite rare." It differs from ordinary cows in its completely white fur and pale skin. Its hooves are light brown, its horns are beige, its nose is pinkish, and its eyes are pale red, marking it as a true albino.

An albino cow behaves no differently from a normal cow: it wanders through meadows, moos, breathes, and avoids water, lava, and cliffs. When damaged, it runs away for a few seconds and then returns to its passive activities.

Otherwise, it's a normal cow, just very pale and with red eyes, which gives it a slightly mad look, although it's unaware of it.

TIPS:

- An excellent exhibit for a collector of rare mobs.
- If you have a pen of such cows, take care of them.
- They milk, produce meat and leather, and moo.
- The red eyes don't glow in the dark.
