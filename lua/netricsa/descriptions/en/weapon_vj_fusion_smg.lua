FUSION PULSE SUBMACHINE GUN


WEAPON: Fusion Pulse Submachine Gun
AMMO:   Fusion Ballistic Ammunition / Thermal Pulse Ammunition
ROF:    ~450 RPM

DESCRIPTION:

The standard weapon of automatons, widely used by rank-and-file assault units, raiders, and marauders. Its design is characterized by crude execution, poor ergonomics, and mediocre accuracy, but it compensates for this with ease of manufacture and a high density of fire.

The weapon is designed for short bursts of automatic fire. The primary fire mode is a five-round burst, followed by a brief stabilization delay. A poor heat dissipation system makes the Fusion Pulse Submachine Gun prone to overheating during prolonged use, reducing its reliability in protracted engagements.

Despite the moderate power of individual hits, the density of fire allows for effective suppression of targets and constant pressure on the battlefield.

NOTES:

- Used by Assault, Raider, and Marauder-class automatons.
- A 5-shot burst provides consistent suppressive fire.
- Overheats during prolonged firing due to poor cooling.
- Effective at close and medium ranges.
- Poor ergonomics and high dispersion reduce accuracy at long ranges.
- A reload (~4 seconds) is required after 30 shots.
- Low armor penetration allows for combat against lightly armored targets.
