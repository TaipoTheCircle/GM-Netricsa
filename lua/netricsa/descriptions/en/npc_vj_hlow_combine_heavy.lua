COMBINE OVERWATCH HEAVY PRISON GUNNER


CLASS:      Combine Overwatch Heavy Prison Gunner
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Suppressive Fire Unit
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    Combine Minigun
REWARD:     1200 FC
THREAT:     Critical

DESCRIPTION:

The Combine Overwatch Heavy Prison Gunner is a walking fire point, built from the Nova Prospekt personnel base, but designed for much more than just prison walls. Its armor is multi-layered, weighted, and padded with shock absorbers, allowing it to fire continuously without losing stability.

Outwardly, he resembles a standard Nova Prospekt prison guard: yellow visors, distinctive armor. However, he is immediately given away by numerous yellow identification elements, warning allies that they are not dealing with a guard, but a suppression device.

These soldiers are used to suppress riots, protect strategic points, escort convoys, and assault fortified positions.

TIPS:

- Never enter an open line of fire - the minigun is unforgiving.
- Use explosives or heavy weapons.