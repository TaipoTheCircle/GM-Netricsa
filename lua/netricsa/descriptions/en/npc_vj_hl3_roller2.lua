COMBINE ROLLER BOLTER MINE


CLASS:      Combine Active Explosive Mine
METABOLISM: Fully Synthetic
PERCEPTION: Motion Sensors + Heat Seeking
SIZE:       1 ft
ENDURANCE:  Low
HOSTILITY:  Very High
WEAPONS:    Contact Blast, Jump Attack, Shrapnel Damage
REWARD:     250 FC
THREAT:     Very High

DESCRIPTION:

The Combine Roller Bolter Mine is a two-phase weapon of destruction. Its spherical body, reminiscent of a naval mine, is completely covered in massive bolts and spikes, angled for maximum dispersion upon detonation.

Upon detecting a target, the mine immediately enters attack mode, accelerating and performing sharp leaps. She doesn't retreat, doesn't freeze-her behavior resembles that of a predator without a self-preservation instinct.

When she detonates, the central charge ruptures the hull, sending all the bolts and spikes flying in all directions at incredible speed. The blast zone expands dramatically, turning into a dense cloud of metallic shrapnel.

These bolts are capable of penetrating light cover, body armor, and unprotected parts of synthetics. Even outside the primary shockwave's radius, the target can be struck by a secondary "bolter rain".

TIPS:

- Maintain maximum distance-bolts travel farther than expected.