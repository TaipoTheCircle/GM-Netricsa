SPIDER BLOT FROM THE BROADSIDE BEACH 


CLASS:      Blot Spider
METABOLISM: Starling
PERCEPTION: Predator-Stalker
SIZE:       19,7 ft
ENDURANCE:  Infinite
HOSTILITY:  Absolute (presumably)
WEAPONATAL: Teeth
REWARD:     Life or Death
THREAT:     Very High

DESCRIPTION:

Blot was once Rex Broadside's first and most beloved character, a little bunny in a cap and overalls who came to life on screen and stole the hearts of audiences. But Rex, inexperienced, signed a contract giving up the rights to his character, and Blot disappeared from his life. Years later, after Rex's death, Broadside Animation bought the rights back, but not to make cartoons.

In a secret laboratory, Blot became the basis for a horrific experiment to create Starlings-living suits into which they attempted to implant human consciousness in order to resurrect Rex. The first attempt was monstrous. The crude, imperfect shell distorted the image of the cute rabbit beyond recognition. Remnants of a human are still visible within.

As Starling underwent further transformations within the distorted world, an even more monstrous form emerged-the Blot-Spider. Its body acquired long, brittle spider legs, transforming it from a hunter lurking in corners into a swift predator designed to terrify its prey.

Unlike other forms, the Blot-Spider doesn't hide or disguise itself. It stalks. Swiftly, relentlessly, forcing its prey to flee without looking back.

TIPS:

- This form of Blot doesn't just wander-it hunts. Don't stand still.
- His spider-like legs allow him to move quickly. Seek cover where he can't reach you.
- Unlike the regular Blot, which simply jumps out of barrels, this form likely has no weaknesses. Only flight.
