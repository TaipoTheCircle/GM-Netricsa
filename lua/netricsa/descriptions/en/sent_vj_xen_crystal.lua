XEN CRYSTAL, XENIUM


CLASS:      Xen Crystals, Xenium
METABOLISM: Energy-conductive (non-organic)
PERCEPTION: Alien Mineral Of Xen
SIZE:       Varies (shards: ~2-5 ft; clusters: up to 20 ft)
ENDURANCE:  Extremely High (dense crystalline lattice)
HOSTILITY:  None (passive)
WEAPONS:    None (but can emit psionic energy when linked)
REWARD:     200 FC
THREAT:     Indirect (when controlled by Xen entities)

DESCRIPTION:

Xen Crystals, also known as Xenium, are powerful mineral formations native to Xen's alien landscapes. These glowing crystalline structures radiate raw psionic and dimensional energy, acting as both a natural fuel source and a stabilizer for interdimensional rifts.

Most notably, Xenium serv as the primary power source of the Nihilanth, amplifying his psionic strength and maintaining his protective barriers. Shards of the crystal were embedded across his lair, acting as conduits that channeled vast amounts of energy. Destroying these shards during battle proved critical in weaken his defenses.

Due to their volatile energy signatures, Combine forces and Black Mesa researchers alike sought Xenium as a means of tapping into interdimensional travel and weaponization - with catastrophic results.

TIPS:

- Crystals can empower Nihilanth, making them more dangerous.
- Destroy crystal nodes to disable psionic shields or disrupt portals.
- Xenium remains highly unstable - handle fragments with extreme caution.
