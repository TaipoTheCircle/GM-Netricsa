COMBINE CIVIL PROTECTION MINI METROCOP


CLASS:      Embedded Combat Synth Operator
METABOLISM: Human, Male (high biomechanical compression)
PERCEPTION: MiniStrider interface + limited vision
SIZE:       0,9 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    MiniStrider Pulse Rifle Operator, pistol
REWARD:     300 FC
THREAT:     Low

DESCRIPTION:

The Miniature Metrocop is an experimental Combine personnel unit, scaled down and housed within the MiniStrider hull. These operators perform maintenance, targeting, and control of the embedded pulse rifle, acting as a living part of the combat synth. Their bodies are subjected to extreme compression, biomechanical stabilization, and neural tethering to the MiniStrider system.

Unlike fully autonomous synthetic platforms, MiniStriders with such operators exhibit more flexible tactical behavior. Human-like reflexes and the ability to improvise allow them to more quickly select targets, adjust fire, and respond to unconventional threats. Essentially, this is the Combine's attempt to combine a cold machine and a subservient human mind in a single body.

In rare cases, when a MiniStrider is destroyed, the compression field partially destabilizes, and the operator can return to their normal size. However, this process is incompatible with life: overload of tissue, organs, and the nervous system leads to inevitable death, often simultaneously with the destruction of the synthetic itself.

Such Metrocops are not considered full-fledged combatants and are officially listed as "expendable maintenance resources."

TIPS:

- Destroying a MiniStrider automatically incapacitates the operator. - More precise behavior of the MiniStrider may indicate the presence of a living operator inside.