CHI-FANG


PLANET:       Chi-Fang
TYPE:         Terrestrial planet, Industrial planet
CLIMATE:      Temperate
MOONS:        1 moon
TERRAIN:      Cities
LOCATION:     Unknown star system in the Milky Way.
NATIVE RACES: Chi-Che, Formerly Mental Horde (All races)
ALLIANCE:     Old Empire of Prince Chan and Chi-Fang (formerly), New Empire of Chi-Fang
LOCATIONS:    Chi Che Town, Chi Che Port, Chan Amusement Park, Chan Palace

DESCRIPTION:

This planet's architecture is inspired by the aesthetics of pre-industrial China. Cities are built with densely packed pagodas with distinctive curved roofs, indicating a high population density in habitable valleys surrounded by forbidding mountain peaks. It has a well-developed port infrastructure, meaning its main trade routes are by water.

The planet was captured by Mental's forces through the subjugation of its ruler. Prince Chan, ruler of Chi-Fang, struck a deal with Mental, receiving an army of undead ninjas in exchange for loyalty. Until recently, these troops patrolled the city streets and the palace grounds.

KEY LOCATIONS:

- Port Chi-Che: A bustling transportation hub connecting the planet to the outside world.
- Chan Amusement Park: A complex built by order of Prince Chan, including three giant Ferris wheels.
- Prince Chan's Palace: The ruler's residence. The inner chambers include a garden surrounded by glass walls and a throne room. The outer area of ​​the palace is decorated with dragon statues and leads to a pagoda.
