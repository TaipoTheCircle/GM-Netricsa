BOMBHEADED KAMIKAZE


CLASS:      Bombheaded Kamikaze
METABOLISM: Cyber-Zombie
PERCEPTION: Insentient, LCU controlled
SIZE:       8 ft
ENDURANCE:  Very Low
HOSTILITY:  Extreme
WEAPONS:    Giant Bomb
REWARD:     50 FC
THREAT:     High

DESCRIPTION:

Unlike typical Kamikazes, the Bombheaded Kamikaze is typically used for "charity" events such as marathons to raise money for sick Fido puppies.

However, the truth is, all the money generated from these events goes straight into Mental's war machine.

When not attending these events, the Kamikazes charge into battle and explode on anyone who opposes their overlord.

TIPS:

- Explodes when killed.
- They're dangerous when mixed with other enemies.
- Stay away; they don't have ranged weapons.
- If there are a lot of them, the SBC Cannon and Minigun will help.
- If they're grouped with other enemies, the explosion can kill them.
- Against a crowd of kamikazes, a shotgun, Uzi, or any other weapon is effective, but only if the group consists solely of kamikazes, as blowing up one will cause the others to explode.
