MAC-11 SUBMACHINE GUN


WEAPON:      MAC-11
AMMUNITION:  9x19 mm cartridges 
ROF:         ~1200 rounds/min

DESCRIPTION:

A versatile automatic weapon for close and medium range combat in urban environments. It is mainly used by Los Angeles gangs such as the Bloodz and Crips. It was also featured in the 1992 "El Mariachi".

NOTES:

- Rapidly depletes ammunition.
- Accuracy is significantly reduced at long ranges.
