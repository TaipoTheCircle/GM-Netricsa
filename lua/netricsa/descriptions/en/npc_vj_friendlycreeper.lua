FRIENDLY CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Friendly Creeper
METABOLISM: Plant-Gunpowder
PERCEPTION: Passive Mob, Tamable Pet
SIZE:       5,5 ft
ENDURANCE:  Medium
HOSTILITY:  Low
WEAPONS:    Microexplosion
REWARD:     50 FC
THREAT:     Medium

DESCRIPTION:

This is a pink creeper. Unlike its green, aggressive brethren, this creeper variety is completely passive and even friendly. Its pink color isn't just a texture, but a marker of its peaceful nature, a sort of "quality seal" for those tired of exploding every five minutes.

Unlike other members of its species, this creeper takes everything calmly. It doesn't hiss in the dark, doesn't sneak up behind you, and doesn't destroy structures. Even if someone accidentally bumps into it, it won't respond with aggression, but will simply move away.

To tame a Friendly Creeper, you need to feed it gunpowder. Afterward, a heart appears on its chest, and the creeper becomes a loyal pet.

It follows its master around like a tamed wolf.

It attacks hostile mobs using microexplosions that damage enemies.

The creeper itself remains unharmed.

To heal a Friendly Creeper, you can feed it flowers.

TIPS:

- Don't try to kill a Friendly Creeper. It's not only cruel, but also pointless.
- If you find a pink creeper, tame it immediately. It's one of the few mobs that will protect you instead of killing you.
- Taming requires gunpowder, which can be obtained from regular creepers.
- In combat, the Friendly Creeper is effective against hordes of small mobs like zombies and skeletons.
- Don't try to use it for resource mining. Explosions don't destroy blocks.
- If you have several tamed pink creepers, they can work in a pack. This acts as a small pink explosive squad that protects you from all the misfortunes of the Nether and Overworld.
