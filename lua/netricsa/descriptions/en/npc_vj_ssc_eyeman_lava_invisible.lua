LAVA GNAAR MALE FROM THE LAVA PLANET


CLASS:      Lava Gnaar, Male
METABOLISM: Mammal
PERCEPTION: Trained War Beast
SIZE:       4 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Bites, Punches
REWARD:     600 FC
THREAT:     Low

DESCRIPTION:

A small bipedal animal with no head, one eye and a large mouth. Originating from Sirius, and trained by Mental to attack opposing forces. The males are smaller, bright red in color and not as tough as females.

Lava Gnaars are closely related to regular Gnaars, but over thousands of generations they have fully adapted to life on a volcanic planet covered in lava seas, scorching rocks, and constant eruptions. Their skin has acquired a bright red hue and become significantly thicker, protecting their bodies from the extremely high temperatures.

They constantly prowl the volcanic wastelands in search of prey and fiercely defend their territory.

Despite their primitive intelligence, they will not hesitate to pounce on their opponents, attempting to tear them apart with their powerful jaws and long claws.

TIPS:

- They have no ranged weapons—just keep your distance.
- Some specimens can fly.
- There are reports of invisible Gnaars.
