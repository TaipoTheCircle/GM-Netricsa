MISS THAVEL OF FUNDAMENTAL PAPER EDUCATION


CLASS:      Miss Thavel, Language Teacher
METABOLISM: Paper Organism / Mythical Entity
PERCEPTION: Sight, hearing, possibly fear and language senses
SIZE:       ~9 ft
ENDURANCE:  high
HOSTILITY:  Extreme
WEAPONS:    Giant clawed hands, sharp teeth, ruler (as a symbol of authority), Wendigo transformation
REWARD:     1000 FC
THREAT:     Very high

DESCRIPTION:

The youngest of three murderous language teachers. In her normal form, she has snow-white skin, tousled black hair, three letter cubes on her head (in alphabetical order), and wears a tattered white dress. Her main weapon is her enormous, black, clawed hands, capable of cutting books, and likely students, in half. She possesses a hidden, more terrifying form-the Wendigo-in which antlers grow on her head, her body becomes covered in fur, and her clothing becomes even more tattered. This transformation hints at her mythical, insatiable, and predatory nature, extending beyond mere punishment for academic failure. Like her colleagues, she is cruel and merciless.

TIPS:

- In close combat, she is deadly thanks to her claws. Her attacks are swift and sweeping. In her Wendigo form, her strength, speed, and possibly ferocity are greatly increased.
- Intense fear in the victim or their own rage can trigger the transformation into a Wendigo. (This is not certain). Remaining calm can delay or prevent this form.