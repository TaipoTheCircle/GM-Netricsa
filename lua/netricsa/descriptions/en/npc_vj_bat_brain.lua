BAT BRAIN FROM THE MOBIUS


CLASS:      Bat brain
METABOLISM: Nickel-cadmium
PERCEPTION: Ambush drone
SIZE:       4 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Hull, ram
REWARD:     300 FC
THREAT:     Low

DESCRIPTION:

When Doctor Eggman realized that maintaining caves and underground levels was more expensive than above-ground levels, a tender was announced for the creation of the most cost-effective security system. The winning design was based on old archives: bat blueprints, a toy airplane engine, and a processor from an Elektronika MK-52 calculator.

Batbrain is the only robot in Eggman's empire that doesn't chase targets, but simply waits for them to come to him. In standby mode, it hangs upside down, blending in with the ceiling. When a living object appears, it takes off, emitting a rattling sound similar to a broken doorbell. It attacks directly from above, aiming for the top of the head. If it misses, it flapped its wings helplessly for a moment, trying to gain altitude, and then clung to the first ledge it came across, pretending nothing had happened.

TIPS:

- Dangerous only when near chasms and narrow bridges.
- After attacking, it has a one-and-a-half-second pause. This is enough time to fix its hair and move on.
