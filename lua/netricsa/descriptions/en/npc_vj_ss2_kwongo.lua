KWONGO, THE GIANT GORILLA OF M'DIGBO


CLASS:       Kwongo, the Giant Gorilla
METABOLISM:  Giant Primate
PERCEPTION:  Deity of the Simba tribe
SIZE:        427 ft
ENDURANCE:   Extreme
HOSTILITY:   Absolute
WEAPONS:     Fists, throwing buildings
REWARD:      50000 FC
THREAT:      Absolute

DESCRIPTION:

A giant gorilla, taller than city skyscrapers, with gray fur and red eyes.

It was once a local deity worshiped by the Simba tribe on the planet M'Digbo. But at some point, Kwongo acquired a piece of the Medallion, an artifact needed to fight Mental.

In combat, Kwongo is virtually invulnerable to conventional weapons. Rifles, shotguns, and even rocket launchers leave only scratches on his hide. The only way to inflict serious damage is to use special catapults, which the Simbas load with lava balls. While Kwongo slowly approaches you, he will periodically hurl houses and crush them with his fists.

After death, Kwongo will be eaten—the entire tribe will roast his remains over a huge bonfire.

TIPS:

- Manage the turrets. This is the only way to protect the Simbas and shoot down the flying houses.
- If the catapults aren't enough, finish them off with the most powerful weapon you have left.
