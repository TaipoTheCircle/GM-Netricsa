RTB COMBINE CIVIL PROTECTION


CLASS:      Transhuman Law Enforcement (CP / Metropolice)
METABOLISM: Human, Male (lightly augmented)
PERCEPTION: Standard vision + radio uplink
SIZE:       5.9-6.2 ft
ENDURANCE:  Low-Medium
HOSTILITY:  High
WEAPONS:    Stun Stun-Stick, 9mm Pistol, SMG1, Explosive Barrels, Manhack
REWARD:     200 FC
THREAT:     Low-Medium

DESCRIPTION:

RTB Combine Civil Protection, often referred to as Metropolice or Metro Cops, serve as the first line of enforcement in Combine-occupied cities. They are ordinary human recruits, conditioned and loyal to the Combine hierarchy, granted limited augmentations and equipment to maintain control over the civilian population.

Their tasks include patrolling urban zones, enforcing curfews, conducting arrests, and suppressing resistance activity. CP units rely heavily on intimidation, brutality, and the use of their iconic stun Stun-Sticks to subdue citizens. In combat, they function as lightly armed infantry, more dangerous in numbers than as individuals.

Despite their limited armor and weaker endurance compared to Overwatch Soldiers, Civil Protection units are essential to the Combine's urban dominance. Their white masks and cold, radio-filtered voices have become a symbol of oppression across Earth.

TIPS:

- Individually weak - take advantage of their low endurance.
- They rely on pistols and SMGs; close combat means heavy stun Stun-Stick use.
- Expect ambushes and backup calls via radio uplinks.
- Supplies are often found in their patrol outposts.