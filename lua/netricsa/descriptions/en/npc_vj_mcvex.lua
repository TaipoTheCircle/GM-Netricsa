VEX FROM THE MINECRAFT OVERWORLD


CLASS:      Vex
METABOLISM: Magical Summon
PERCEPTION: Flying hostile mob, servant of the Evoker
SIZE:       2,6 ft
ENDURANCE:  Average
HOSTILITY:  Absolute
WEAPONS:    Iron Sword
REWARD:     20 FC
THREAT:     High

DESCRIPTION:

This small but extremely annoying flying creature is summoned by the Evoker when he wants to make your life a living hell. Despite their diminutive size, these creatures can pass through walls and deliver devastating blows. If the Vindicator is brute force and the Evoker is intellect, then the Vex is pure, unadulterated malice, trapped in miniature form.

The Vex appears as a small, gray, winged wraith armed with an iron sword. When it attacks, its body glows red, heralding a deadly charge. Vex are corrupted Alai. Evokers are believed to use dark magic to transform friendly Alai (item-gathering helpers) into these aggressive flyers.

The Vex has three key features that make it one of the most dangerous mobs:

The Vex can fly through any block—dirt, stone, obsidian, and even bedrock. You can't hide from it behind cover.

When the Vex attacks, it emits a piercing sound, its body glows red, and it lunges at its target.

Vex don't last forever. Once summoned by a Evoker, they begin to take damage and die on their own after 30-119 seconds (ranging from 30 seconds to just under 2 minutes).

The Vex attack quickly, trying to get behind or flank them, making it difficult to block with a shield. They don't burn in lava, don't drown in water, and ignore webs except for honey blocks.

TIPS:

- Kill the Evoker first. This is the most important tip. As long as the Evoker is alive, it will summon new Vex again and again.
- Don't stand still. The Vex are fast and aggressive. Constant movement and dodging are key.
- Use a ranged weapon (Bow / Crossbow). Dodging their attacks at close range is much more difficult.
- If you're surrounded by Vex, use explosives (like Fireworks). Their health is low, and the explosion can quickly clear the area.
- Remember to self-destruct. If you feel like you're overwhelmed, try simply dodging and waiting it out. In a minute and a half they will start to die themselves.
