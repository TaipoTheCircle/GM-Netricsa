IMPERIAL WATCHMAN FROM THE NIRN


CLASS:      Imperial Watchman
METABOLISM: Imperial, Male
PERCEPTION: Guardian of the Order of the Imperial City / Law Enforcer
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Conditional
WEAPONS:    Silver Longsword, sometimes destructive magic
REWARD:     200 FC
THREAT:     Average

DESCRIPTION:

These are the pillars of order and law in the Imperial City of Cyrodiil. These fighters, clad in distinctive Legion armor, are representatives of the Imperial Legion and perform the difficult duty of maintaining law and order in the provincial capital.

The Watchman's appearance inspires respect and a certain intimidation. They are clad in a full suit of Legion steel armor, including a massive helmet, and wield a silver longsword. Their job is to be visible, so potential criminals think twice before breaking the law. Some Watchmen master Destruction spells, making them dangerous not only in close combat but also at range.

A Watchman's primary function is justice. If you commit a crime, be it murder, theft, or assault, the nearest guard will immediately rush to your side, shouting, "Halt, criminal abomination!" You will have a choice: pay a fine, go to prison, or resist. If you decide to surrender, the guard will confiscate all stolen goods and escort you to the castle to serve your sentence.

Resisting arrest is a bad idea. Watchmen fight skillfully and cohesively, and if you refuse to surrender, they will not hesitate to draw their swords, shouting, "Then pay with your blood!"

There are several specialized units of the Imperial Watch. Prison guards guard the Imperial Prison and use a silver mace instead of a sword. They always carry cell keys, which can be useful for a prisoner with good manual dexterity.

Some guards, unfortunately, are susceptible to corruption. For example, Captain Audens Avidius was caught extorting money from merchants. Honest guards, like Captain Itius Hein, are willing to help catch criminals, but will require irrefutable evidence.

TIPS:

- If you plan to engage in criminal activity, do so in secluded areas and avoid being seen by guards. Their sight and hearing are excellent.
- If you are spotted stealing and a guard approaches, your best option is to pay a fine. Resisting arrest almost always results in death.
- Carry enough gold to cover the fine. If you have no money, your only options are prison or combat.
- Remember that after you serve your sentence, all stolen items will be confiscated. Hide them in a safe place before turning yourself in to the authorities.
- Listen to what the guards say. They often offer helpful advice about the city, its districts, and the latest events.
