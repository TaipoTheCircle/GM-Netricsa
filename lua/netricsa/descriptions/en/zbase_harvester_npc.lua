SWARM HARVESTER


CLASS:      Swarm Harvester
METABOLISM: Xenobiological
PERCEPTION: Support Specimen
SIZE:       8,2 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Xenomite Spawn, Corrosive Skin
REWARD:     500 FC
THREAT:     High

DESCRIPTION:

The Harvester is a specialized form of Swarm, designed to quickly reproduce combat organisms directly on the battlefield. Unlike most members of its kind, it rarely attacks enemies directly, preferring to create Xenomites and deploy them against detected targets.

The Harvester's most noticeable feature is a large, glowing organ on its back. Xenomites hatch within it, and soon after maturing, they leave the host's body. Their rapid reproduction allows a single Harvester to create an entire swarm of these creatures in a short period of time.

When threatened, the Harvester attempts to increase the distance from the enemy, continuing to reproduce even while retreating. This makes destroying a Harvester often more challenging than it might initially appear.

After death, the organism undergoes a brief postmortem release, during which the remaining xenoticks leave the body and immediately begin attacking. For this reason, destroying a Harvester does not always mean the end of the threat.

Despite its relatively weak defenses, Harvester tissue contains a large amount of corrosive substances. Contact with its body can cause serious damage even after the creature's death.

TIPS:

- Destroy Harvesters before they create a large swarm.
- Do not allow them to retreat to safe areas.
- Be prepared for xenoticks to appear after the target is destroyed.
- If the Harvester has lived long enough, it means that soon he will have to deal with more than one enemy.
