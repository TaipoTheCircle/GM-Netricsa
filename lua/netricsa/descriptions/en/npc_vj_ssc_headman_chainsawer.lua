BEHEADED CHAINSAWER


CLASS:      Beheaded Chainsawer
METABOLISM: Cyber-Zombie
PERCEPTION: Insentient, LCU-controlled
SIZE:       6 ft
ENDURANCE:  Low
HOSTILITY:  Extreme
WEAPONS:    Chainsaw
REWARD:     400 FC
THREAT:     Very High

DESCRIPTION:

An executed Syrian soldier, raised from the dead by Mental and remotely controlled by a Life Control Unit (LCU).

He no longer needs a head: there's nothing to think with and nothing to do. His task is simple: cut everything while his saws' motors are spinning.

After decapitation, his nervous system was crudely rewired via the LCU, and his hands are now firmly attached to his chainsaws.

When a Chainsawer detects a target, it accelerates sharply, as if someone had just turned the throttle all the way to the floor, making it nearly impossible to escape.

