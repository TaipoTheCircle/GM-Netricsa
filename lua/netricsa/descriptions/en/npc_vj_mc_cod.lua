COD FROM THE MINECRAFT OVERWORLD


CLASS:      Fish
METABOLISM: Cold-blooded aquatic creature
PERCEPTION: Primary vision + aquatic movement
SIZE:       1,6 ft
ENDURANCE:  Low
HOSTILITY:  Low
WEAPONS:    None
REWARD:     10 FC
THREAT:     Low

DESCRIPTION:

Cod is a peaceful fish native to freshwater bodies of Minecraft's overworld.

Cod almost never react aggressively.

They are one of the most common aquatic inhabitants and can be found both alone and in large groups.

TIPS:

- Catching cod is an easy way to get food.
- Cooking cod restores hunger and health.
- Vulnerable on land.