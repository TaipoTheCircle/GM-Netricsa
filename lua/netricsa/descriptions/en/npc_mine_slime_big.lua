BIG SLIME FROM THE MINECRAFT OVERWORLD


CLASS:      Big Slime
PERCEPTION: Slime Monster
METABOLISM: Amorphous Organism
SIZE:       ~4,9 ft
ENDURANCE:  Average
HOSTILITY:  Average
WEAPONS:    Jumping, Crash
REWARD:     30 FC
THREAT:     Average

DESCRIPTION:

The Big Slime is an amorphous, slimy creature that seems to have no sense of personal space. It leaps, hurls itself at anything living, and sometimes splits into smaller pieces when damaged, creating even more annoying copies.

TIPS:

- Fight larger Slimes from a distance.
- Smaller Slimes pose no threat on their own, but in a group they can cause chaos.