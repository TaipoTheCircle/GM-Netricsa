NEMESIS FROM THE UMBRELLA CORPORATION


CLASS:      Bioweapon (B.O.W.), Tyrant T-Type
METABOLISM: Mutated organism infected with the NE-α parasite
PERCEPTION: Tactical Assassin, Elite Hunter
SIZE:       8,2 ft
ENDURANCE:  Extreme
HOSTILITY:  Absolute
WEAPONS:    Rocket Launcher, M-134 Minigun, Flamethrower, Tentacles, Fists, Environmental Objects
REWARD:     10000 FC
THREAT:     Absolute

DESCRIPTION:

This is no ordinary bioweapon. It is a living nightmare, created by the European branch of Umbrella Corporation with one sole purpose: to eliminate all surviving members of the elite S.T.A.R.S. team responsible for exposing the corporation's crimes.

Nemesis is the result of infecting a T-103 Tyrant-the same model as Mr. X-with an experimental parasite called NE-α (Nemesis Alpha). The parasite literally grows into the host's nervous system, forming its own brain and taking control of the body. This grants the monster not only superhuman strength, but also the ability to make decisions, follow complex orders, and, most terrifyingly, use weapons.

Due to the difficulty of integrating the parasite with the host, the success rate was negligible-approximately one successful specimen in 60 million candidates. Each such specimen is incredibly valuable to Umbrella.

In its basic form, Nemesis is a gigantic, muscular humanoid hulk, clad in a long, black, waterproof coat. The coat was originally part of their standard equipment, designed to inhibit mutations and protect against bullets.

His face is devoid of lips and nose-bared teeth and muscles are visible beneath the taut skin. His right eye is closed with crude surgical stitches. His body is covered in scars and dissection marks. He wears black pants, boots, and gloves, and on the left side of his chest is a visible metal restraint-a device that prevents his parasite from mutating uncontrollably.

When his coat is damaged, Nemesis mutates: long, powerful tentacles begin to erupt from his body, which he uses to grab victims, stab, and even move quickly by clinging to the walls of buildings.

The main distinguishing feature of Nemesis from other Tyrants is his intelligence, which allows him to handle complex weaponry.

His signature weapon is a heavy rocket launcher or multi-barreled grenade launcher mounted on his left arm. Nemesis demonstrates incredible accuracy, easily hitting moving targets at great distances.

Nemesis also uses a powerful backpack flamethrower, capable of spitting jets of flame and launching fireballs. Even after the fuel tank is destroyed, he continues to use it as a flaming club.

The six-barreled machine gun Nemesis wields allows him to mow down entire crowds of enemies.

Even unarmed, Nemesis is deadly. His blows can pierce concrete walls and tear out chunks of asphalt. With a single blow, he can send a person flying or break a neck.

Nemesis's tentacles do more than just inflict physical damage. When injected, the victim is infected with the T-virus, causing mutation or rapid death. He can also implant the NE-α parasite into the bodies of the deceased, transforming them into horrific, controllable monsters.

Nemesis is a true monster in every sense of the word.

He is capable of breaking through concrete walls with a single blow, ripping doors off their hinges, hurling cars, and smashing through the floors of buildings.

He can run at speeds over 100 km/h (63 mph), instantly dodging grenade and rocket launcher fire.

His main advantage is regeneration. Bullets, shrapnel, and burns heal before his eyes. Nemesis can be temporarily incapacitated by an explosion or heavy weapon, but he will quickly get up and continue the chase.

He can withstand grenade launcher hits, fuel tank explosions, falls from great heights, and douses of acid and molten metal.

Nemesis is not a mindless killing machine. He has an operational system and priorities. His primary and only target is the S.T.A.R.S. squad.

His entire strategy is focused on tracking and destruction. He will not retreat until his target is dead or he himself is destroyed.

Nemesis is capable of evolving on the fly, passing through several terrifying stages.

First Form: A basic Nemesis in a coat, armed with a rocket launcher/flamethrower. Smart, fast, and dangerous.

Second Form: After destroying his coat and receiving critical damage, Nemesis mutates into an even more monstrous creature. He drops to all fours, becomes like a gigantic beast, becomes covered in bony growths, and grows long, whip-like tentacles.

Third Form (Final): Nemesis's body finally loses control. He transforms into a huge, amorphous mass of flesh, bone, and tentacles, moving in a crawling motion. In this form, he is virtually immune to conventional weapons, continues to pursue his target, and spews clouds of toxic acid.

TIPS:

- Shoot and run. Nemesis is (almost) impossible to kill with conventional weapons. Your goal is to temporarily disable him to gain an advantage and escape.
- Use the environment. Exploding barrels, generators, doors, dropable antennas-all of these can slow him down.
- Don't stand still. He always knows where you are. Any stop means death.
- Dodge the missiles. A direct hit from his rocket launcher kills instantly, even in full armor.
- Watch your weapons. The rocket launcher is a long-range threat, the flamethrower is a medium-range one. If he's running with his fists, you need to increase your distance.
- Remember the tentacles. If he's close, he can grab you with a tentacle even if he doesn't touch you with his hand.
