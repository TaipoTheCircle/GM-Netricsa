BIG SQUIDWARD J. QUINCY TENTACLES FROM THE BIKINI BOTTOM


CLASS:      Big Squidward J. Quincy Tentacles
METABOLISM: Octopus-like creature
PERCEPTION: Intellectual, forced to survive among idiots
SIZE:       6 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    Personality
REWARD:     600 FC
THREAT:     Low

DESCRIPTION:

Big Squidward J. Quincy Tentacles is an octopus who is intellectually superior to his surroundings and deeply suffers because of it. Forced to endure years of companionship with SpongeBob SquarePants and Big Patrick Star, which has led to chronic irritability, cynicism, and emotional exhaustion.

He works as a cashier at a place he despises. Art isn't a hobby for him, but a last-ditch attempt to prove his worth to the world. He considers himself a genius, underestimated by the crowd.

Despite his outward passivity, Squidward is extremely educated, can operate almost any vehicle, and is capable of logical thinking. In rare moments of inspiration, he demonstrates genuine talent, especially in music, but almost always sabotages his own success.

Under constant pressure and bullying, he's capable of cruel acts, but after realizing the consequences, he almost always feels guilty and tries to make amends.

TIPS:

- Don't provoke him - Squidward is vindictive, but smart.
- Don't touch his art - it's personal.
- He doesn't like it when people don't pay him for an order.