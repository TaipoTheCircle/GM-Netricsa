KLEER SKELETON, BIG


CLASS:      Kleer Skeleton, Big
METABOLISM: undead
PERCEPTION: magical creature
SIZE:       15 ft
ENDURANCE:  low
HOSTILITY:  medium
WEAPONS:    two-ball projectiles, claws, horns
REWARD:     1000 FC
THREAT:     medium

DESCRIPTION:

The undead skeleton of a member of an extinct race from the planet Clear in the Alpha Canis Majoris system. It has sharp claws on its front paws and a long tail.

From a distance, it throws metal balls connected by a chain, and in close combat, it uses its claws. During Mental's experiments in altering the size of creatures, this skeleton was magically enhanced and enlarged to the size of a Plains Reptiloid.

TIPS:

- Move out of the attack's line of sight.