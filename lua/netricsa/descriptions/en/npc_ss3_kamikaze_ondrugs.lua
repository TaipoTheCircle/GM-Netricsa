BEHEADED KAMIKAZE, ON DRUGS


CLASS:      Beheaded Kamikaze, On Drugs
METABOLISM: cyber-zombie
PERCEPTION: insentient, LCU controlled
SIZE:       6 ft
ENDURANCE:  very low
HOSTILITY:  extreme
WEAPONS:    hand grenades
REWARD:     2500 FC
THREAT:     high

DESCRIPTION:

An executed Syrian soldier, resurrected from the dead using Mental Drugs and remotely controlled by a Life Control Unit (LCU).
The head of the Kamikaze has been ruined, so he is employed for suicidal attacks. Carries two bombs in his hands and activates them when close enough to the target. 

TIPS:

- Explodes when killed.