SOP38 PISTOL


WEAPON: SOP38 Pistol
AMMO:   .45 ACP Rounds
ROF:    Semi-Automatic
WEIGHT: 2.4 lbs

DESCRIPTION:

Standard-issue Special Operations Pistol, renowned for its reliability across the galaxy. A simple and effective semi-automatic system allows for a high rate of fire.
Features unlimited ammunition, making it an ideal weapon of last resort for dispatching weak targets or conserving ammo for primary weapons.

NOTES:

- Effective against lone Cyclops, Beheaded Kamikaze and other weak hostiles.
- Aiming down sights increases accuracy at medium and long range.