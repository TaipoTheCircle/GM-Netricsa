STROGGOS


PLANET:       Stroggos
TYPE:         Terrestrial planet, Industrial world
CLIMATE:      Harsh, arid, atmosphere breathable for humans
MOONS:        3 moons (one with its own ring system)
TERRAIN:      Vast industrial wastelands, craters, mountain ranges, small bodies of water
LOCATION:     Unknown star system
NATIVE RACES: Strogg, remnants of extinct and assimilated fauna
ALLIANCE:     Strogg Empire
LOCATIONS:    Macron Palace, Colossus Landing Zone, Craters Majoris and Minor, Secret Strogg Lunar Base

DESCRIPTION:

An inhospitable, but breathable, world for humans, home to the aggressive cybernetic Strogg race. The planet is similar in size, gravity, and atmospheric composition to Earth, but its appearance has been completely disfigured by millennia of relentless industrialization. From orbit, the planet appears as a brown-orange wasteland, devoid of significant green areas and open water.

Stroggos likely once boasted a rich biosphere with oceans, vegetation, and diverse fauna. However, the Stroggos, pursuing total militarization, exhausted virtually all natural resources: metals, minerals, and fossil fuels were extracted and processed. Most native life forms either died out, mutated, or were forcibly assimilated by the Stroggos-processed into food or turned into new soldiers through the horrific process of "Stroggification".

The planet's landscape is a vast industrial wasteland, dotted with factories, processing plants, and military bases.

Geological activity is high, with impact craters (such as Majoris and Minor) remaining, and a system of asteroid rings orbiting the planet-possibly the remains of a fourth moon destroyed in an ancient catastrophe.

Stroggos is surrounded by three moons and an asteroid belt, which contains the secret residence of the Strogg leader, Macron, accessible only via teleporter from the surface.

KEY LOCATIONS:

- Macron's Palace: The central structure on the surface of Stroggos, crowned by a massive teleporter leading to the leader's asteroid retreat. The heart of the Strogg war machine.
- Landing Zone "Colossus": The site of the first massive landings of Earth's invasion forces. A site of fierce fighting, littered with the remains of vehicles and bodies.
- Craters Majoris and Minor: Two enormous impact craters, likely associated with the formation of the asteroid belt. Important industrial facilities are located in their vicinity.
- Secret Strogg Lunar Base: A massive underground complex on the third moon, containing factories, naval docks, and a command bunker. Destroyed in a daring operation by Earth Marines.
