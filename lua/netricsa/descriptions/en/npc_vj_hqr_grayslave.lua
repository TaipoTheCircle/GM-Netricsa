HALF-QUAKE SHOW GRAY VORTIGAUNT (SLAVE)


CLASS:      HALF-QUAKE Show Gray Vortigaunt, Slave
METABOLISM: Intelligent Mammal
PERCEPTION: Enslaved Alien Race
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Forced
WEAPONS:    Bioelectric Discharge, Claws
REWARD:     400 FC
THREAT:     Average 

DESCRIPTION:

When the HALF-QUAKE Sadistic Show's theme shifted from industrial ruins to a dark, depressing world, the old Vortigaunts no longer fit in. Their natural colors were reminiscent of Xena, while the aesthetic needed was one of hopelessness, unification, and corporate uniformity.

The solution came from the hardware department: black latex paint, forty liters, rollers, and sprayers. The Vortigaunts were herded into the hangar, given respirators (not for them, but for the sadists, to prevent them from breathing in the fumes), and each individual was given a fresh coat. The paint went on unevenly, with streaks and some factory blue spots, but overall, the job was done: the Vortigaunts became a shiny black-gray, and the poor creatures' voices became squeaky.

The new model's eyes are white-either from the paint or from what they've seen enough of in their lives. The paint itself, it turns out, acts as an additional protection. Now these creatures not only look terrifying, but also live slightly longer than their unpainted counterparts.

They serve the new version of the show's deity-Somos V2, the Son of Sadism.

The Vortigaunts themselves don't know about this; they don't seem to care. They simply enter the arena, their new bodies gleaming, and shoot lightning at anything that moves.

TIPS:

- Their white eyes can be seen from a mile away in the dark. Use this to pinpoint their position before they see you.
- Their paint is shiny-if a Vortigaunt is standing under light, they can be seen by the glare.
- They have higher defense than regular Vortigaunts. Aim for the head or use explosives.
