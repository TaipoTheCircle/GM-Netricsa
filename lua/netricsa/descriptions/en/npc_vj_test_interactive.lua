INTERACTIVE CITIZEN


CLASS:      Interactive Citizen
METABOLISM: Human, Male
PERCEPTION: Human Civilian / Experimental
SIZE:       ~5,9 ft
ENDURANCE:  Low (fragile, unarmed)
HOSTILITY:  Very Low (non-combatant)
WEAPONS:    None
REWARD:     18 FC
THREAT:     Minimal

DESCRIPTION:

The Interactive Citizen is a bizarre experimental Citizen designed for sandbox interaction. Unlike ordinary citizens, this variant can be commanded by to commit kill yourself, providing darkly humorous gameplay options.

TIPS:

- Can be used for third-person exploration or creative scenarios.
- Triggering its kill-yourself action can provide experimental or humorous.