MARSH FIEND FROM RIGIL KENTAURUS


CLASS:      Marsh Fiend
METABOLISM: Amphibian reptile
PERCEPTION: Wild animal
SIZE:       3 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Fangs, claws
REWARD:     50 FC
THREAT:     Low to medium (depending on number)

DESCRIPTION:

Marsh-Fiends originate from the marsh-filled wasteland worlds of Rigil Kentaurus. They typically lurk near water, waiting for their prey to come close and drink before they strike.
Mental assimilated these creatueres into its Horde alongside their Marsh-Hopper cousins. Due to the extreme rate at which they breed, Mental can afford to send swarm after swarm against its foes.
Their only methods of attack are dashing forward and biting. So long as you keep your distance and gun them down, you'll be fine.

TIPS:

- You cannot outrun them, they'll eventually catch up with you. Shoot them down while they're afar.
- Use your shotguns to deal with small groups of them, and explosives to take care of larger swarms.
