HK USP SELF-LOADING PISTOL


WEAPON:     Heckler & Koch USP
AMMUNITION: 9x19 cartridges
ROF:        ~20 rounds / min

DESCRIPTION:

A self-loading pistol developed by the German company Heckler & Koch. The USP pistol is designed for use by police and military forces. It is also used for civilian self-defense, sports shooting, and recreational shooting.

