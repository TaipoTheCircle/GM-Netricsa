THOR, CEO OF EDENSYS BIOTECH


CLASS:      Thor, CEO of EdenSys BioTech
METABOLISM: Human, Male
PERCEPTION: CEO of EdenSys BioTech
SIZE:       9,8 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    Quadrupedal BattleMech, Missile Launchers, Force Field, Ram
REWARD:     25000 FC
THREAT:     Critical

DESCRIPTION:

Thor Erickson, better known as the CEO of EdenSys BioTech, is the corporation's top executive and the man responsible for countless clone experiments, total corporate control, and the elimination of anyone who poses a threat to the company's interests.

Despite his position, Thor is not a professional fighter. He avoids direct engagement in battle and prefers to command his troops from a safe location. However, if circumstances force him onto the battlefield, he does so from within a heavy, quadrupedal combat mech, designed specifically to protect the corporation's top management.

The mech is a massive walking platform equipped with powerful servos and heavy weapons. Its primary attack method is launching successive volleys of homing missiles, capable of covering large areas and forcing opponents to constantly reposition.

If a target gets too close, the mech accelerates sharply and rams. The vehicle's mass is so great that a collision can instantly destroy most opponents or throw them a considerable distance.

The mech's main feature is its built-in force field generator. Once activated, a red energy barrier forms around the mech, completely blocking all incoming damage. While the field is active, Thor becomes virtually invulnerable and continues firing without risk. The generator operates for a limited time, after which it requires recharging.

TIPS:

- Don't waste ammo while the force field is active. Wait until it's deactivated.
- Keep moving to avoid missile salvos.
- Never stand in front of the 'Mech while it's accelerating: ramming it is almost always fatal.
- Use cover between missile salvos and attack only when the force field is deactivated.
- Despite the machine's power, Thor himself is completely dependent on his 'Mech. Once it's destroyed, the Director becomes significantly more vulnerable.
