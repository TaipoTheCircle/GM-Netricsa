BOB, NAZI SOLDIER


CLASS:      Bob, Nazi Soldier
METABOLISM: Human, Male
PERCEPTION: Wehrmacht Infantry 
SIZE:       6 ft
ENDURANCE:  High
HOSTILITY:  Low 
WEAPONS:    MP-40
REWARD:     300 FC
