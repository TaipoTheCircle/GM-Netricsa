DESCRIPTION:

The Fuhrer gave the soldier Bob to Ghostface. Since that day, Bob is no longer just a Nazi - he is Ghostface's eternal companion and friend. Loyal, disciplined, and armed with his MP-40, Bob follows his new master everywhere, serving not out of fear, but out of unbreakable camaraderie.

TIPS:

- Bob rarely acts alone - he is always near Ghostface.
- His MP-40 is most dangerous at close range.
