AR3 MOUNTED MACHINE GUN


WEAPON: AR3 MOUNTED MACHINE GUN
AMMO:   Energy Pulse Bullets (Dark Energy)
ROF:    ~1800-2200 RPM
WEIGHT: ~45 kg

DESCRIPTION:

The names of this weapon are known only to a few. The AR3 is a stationary version of the AR2 OSIPR pulse rifle, adapted for prolonged fire and position holding. Typically, the machine gun is mounted on special armor shields with an active force field protecting the operator's lower body.

Unlike the AR2, which has a limited ammunition supply, the AR3 is constantly powered by an external battery charged with dark energy. When connected to the shield system, the battery is continuously charged, providing a stable generation of energy projectiles. This makes the AR3 a weapon with a virtually inexhaustible ammunition supply.

Its extremely high rate of fire allows it to effectively destroy large groups of enemies at medium range. However, the weapon has a noticeable spread, which reduces the effectiveness of accurate fire at long ranges. The AR3 is less powerful than the AR2 and AR1 due to differences in battery types, but a hit almost always results in severe injury or death.

To enhance combat effectiveness in low-light conditions or at night, compact spotlights are mounted on the weapon's body to detect and track targets.

NOTES:

- Does not require reloading when connected to energy shields.
- Effective for suppression and holding territory.
- Most of the rebels do not know the official name of this weapon.