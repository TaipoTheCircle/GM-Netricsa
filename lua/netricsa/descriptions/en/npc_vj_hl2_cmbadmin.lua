COMBINE ADMINISTRATOR


CLASS:      Combine Administrator
METABOLISM: Warm-blooded, Human
PERCEPTION: Humans, high-ranking bureaucrats
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Average
WEAPONS:    Revolver, Glock 17, butt strike
REWARD:     700 FC
THREAT:     Average

DESCRIPTION:

Combine Administrators are high-ranking officials who reside in their city's citadels. They are responsible for governing individual cities on Earth, such as City 10, City 8, and others. Unlike Wallace Breen, who governs all of Earth and City 17, these administrators operate at the regional level, strictly adhering to bureaucratic procedures and maintaining order.

Male and female administrators wear a standard uniform: a brown jacket and an orange tie. In case of personal conflict, they are armed with revolvers and Glock 17s, and in close combat, they use a buttstock as a weapon. Despite their official status, they are capable of aggressive defense and do not hesitate to suppress resistance if necessary.

TIPS:

- Administrators keep their distance and know their routes well.
- They are well guarded.
