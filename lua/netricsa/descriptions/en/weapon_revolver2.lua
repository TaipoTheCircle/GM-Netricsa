MANURHIN MR-96 REVOLVER


WEAPON:    Manurhin MR-96
AMMUNITION .38 Special
ROF:       15 rounds / min

DESCRIPTION:

The Manurhin MR-96 revolver is a French large-caliber double/single-action revolver designed for powerful .357 Magnum cartridges (also compatible with .38 Special). It was produced as a shorter and more affordable alternative to the well-known MR series (MR-73, etc.) and features a sturdy steel construction, accuracy, and an exportable design.

