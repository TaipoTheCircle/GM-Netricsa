COMBINE GRENADE


WEAPON:      Combine Grenade
AMMO:        Explosive charge
ROF:         Manual throw, ~10 throws/min

DESCRIPTION:

Standard-issue explosive device used by Combine soldiers. Designed for tactical suppression, it detonates on impact or after a short fuse, producing high damage in a small radius. Ideal for clearing rooms or disrupting enemy formations.

NOTES:

- Effective for area denial and anti-personnel use.
- Short fuse requires careful timing.
- Can be dangerous if thrown at close range.