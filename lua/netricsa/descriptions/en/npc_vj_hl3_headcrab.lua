ARCTIC HEADCRAB FROM EARTH


CLASS:      Headcrab, Earth (Arctic)
METABOLISM: Parasite adapted to low temperatures
PERCEPTION: Thermal sense + motion detection
SIZE:       ~1,5 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Jump, bite, parasitic infestation
REWARD:     120 FC
THREAT:     Low-medium

DESCRIPTION:

The Arctic Earth variety of headcrab is the result of a parasite's rapid and brutal adaptation to the perpetual cold. After spreading across Earth's northern regions, these creatures developed a thick layer of coarse fur that covers most of their bodies and protects their internal organs from frostbite.

Underneath the fur, the familiar anatomy remains: powerful hind limbs, a beak, and hooked appendages designed to grip the skull of prey.

TIPS:

- They often hide under the snow.
- They let them attach themselves to your head.