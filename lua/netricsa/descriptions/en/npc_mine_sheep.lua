SHEEP FROM THE MINECRAFT OVERWORLD


CLASS:      Domestic / Wild
METABOLISM: Warm-blooded mammal
PERCEPTION: Standard vision and smell
SIZE:       3 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     40 FC
THREAT:     Low

DESCRIPTION:

Sheep are peaceful herbivores that graze the plains, forests, and mountains of Minecraft. They come in a variety of wool colors (white, black, gray, light gray, brown, pink), allowing them to produce a variety of dyed wool.

Sheep can be sheared with scissors to obtain wool, and they can be bred using wheat. After shearing, the wool gradually grows back.

Sheep roam in groups and graze slowly, making them easy targets for resource harvesting.

TIPS:

- Shear sheep instead of killing them - their wool can be regenerated.
- Lamb provides a good source of food.
- Wool can be used to build a bed and provide a rest from hard work.