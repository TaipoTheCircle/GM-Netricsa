M4A1 CARBINE


WEAPON:       M4A1
AMMUNITION:   Ammunition: 5.56x45 MM
ROF:          950 rounds/min

DESCRIPTION:

The M4A1 carbine is a very famous and effective carbine. It is used by almost all NATO countries and was developed in the USA. It is based on the M16A2 and was originally intended to arm combat vehicle crews and weapon and military equipment crews. This weapon has been used in almost all modern conflicts around the world. It can be heavily modified by adding sights, laser sights, tactical grips, etc.

NOTES:

- This carbine is very good for storming buildings or trenches.
- It has a quick-close bolt button, which greatly speeds up reloading.
- Modify this weapon whenever possible, as modifications can be very helpful in combat.
- Use the fire mode switch to conserve ammunition.