HYPORI


PLANET:       Hypori
TYPE:         Terrestrial planet, Industrial desert world
CLIMATE:      Dry, arid
MOONS:        Unknown
TERRAIN:      Desert plains, rocky uplands, industrial zones, spaceship wrecks
LOCATION:     Ferra Sector, Outer Rim Territories
NATIVE RACES: Humans (refugees, Alliance members), Sith and their loyalists (during occupation)
ALLIANCE:     Confederacy of Independent Systems (formerly); the last stronghold of the Alliance, threatened by Sith invasion
LOCATIONS:    Alliance City, Queen Mothma's chambers, Grand Ballroom, planetary defense system

DESCRIPTION:

A harsh, rocky desert world located in the Ferra sector of the Outer Rim Territories. During the Clone Wars, the planet was the site of a fierce battle between the Galactic Republic and the Confederacy of Independent Systems, when the Republic fleet fell into a trap set by General Grievous. The wreckage of Republic ships, including Acclamator-class cruisers, still litter the surface, a reminder of that battle.

During the Jedi Wars, it became the last stronghold of the Alliance—the resistance formed by Queen Mothma after the fall of Yavin IV. Hypori is home to the last Alliance city, sheltering surviving Jedi, refugees, and resistance forces. It houses Queen Mothma's chambers, the Grand Ballroom, and a planetary defense system designed by Jaden Korr himself.

The city was built with a potential siege in mind, but its defenses were not prepared for a full-scale invasion. When a Sith fleet, commanded by a Sith Lord of the Kel Dor species, reaches Hypori's orbit, the battle for the Alliance's last stronghold begins. The crash of a Sith command ship causes catastrophic damage to the city, but this event proves to be a turning point in the war.

The planet has served as a refuge for various forces throughout history: during the Galactic Empire, Rebels hid there, Separatist garrisons were stationed there during the Clone Wars, and during the Sith Empire, Hypori became a symbol of the last resistance.

Despite its strategic importance, the planet remains little-known and is located off the main hyperspace routes, making it an ideal refuge for those seeking to remain undetected.

KEY LOCATIONS:

- Alliance City: The last stronghold of resistance on Hypori. It houses administrative buildings, refugee quarters, and the Alliance headquarters. - Queen Mothma's Chambers: The Alliance leader's residence in the northern part of the city, adjacent to the Grand Ballroom. During the Sith invasion, the final defense took place here.
- Planetary Defense System: A network of control switches and defense mechanisms developed by Jaden Korr. Capable of countering a Sith armada, but requires activation from multiple points in the city.
- Grand Ballroom: The city's central building, located opposite the Queen's chambers. It was the site of the final meetings of the New Jedi Council before the invasion.
