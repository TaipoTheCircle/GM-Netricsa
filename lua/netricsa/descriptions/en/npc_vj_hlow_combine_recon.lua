COMBINE OVERWATCH RECON SOLDIER


CLASS:      Combine Overwatch Recon Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Reconnaissance and calling in Combine reinforcements
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPONS:    Modified SMG (single-shot), flare pistol
REWARD:     350 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Recon Soldier is the Combine's eyes and ears on the front lines. He is not designed for protracted combat or direct confrontation. His primary mission is to locate the enemy, pinpoint their position, and immediately turn a localized incident into a full-scale punitive operation.

He is armed with a modified submachine gun that fires only single shots. This increases accuracy, reduces noise, and conserves ammunition. However, the soldier's primary tool is the blue flare gun. One shot into the sky, and a short time later, an elite Combine transport carrying a troop of soldiers appears in the area.

The recon's armor is intentionally weak. He must move quickly, hide, change positions, and avoid direct fire. If he isn't eliminated in time, the situation will quickly spiral out of control.

The recon rarely fights to the death. His goal is to survive long enough to call for reinforcements. After that, he can either retreat or provide flanking fire for the arriving soldiers.

TIPS:

- Eliminate him as quickly as possible-one flare can ruin everything.
- His weak armor makes him vulnerable even to light weapons.
- Don't give him time or distance to maneuver.