SPORE LAUNCHER


WEAPON:      Spore Launcher
AMMO:        Explosive spores
ROF:         ~6 rounds / min

DESCRIPTION:

Biological weapon that fires explosive spores capable of detonating on impact or after a short delay. Represents an early developmental stage of the Shock Trooper, combining organic growth with offensive capability. Primarily used to clear confined spaces or disrupt enemy formations.

NOTES:

- Explosive payload deals area-of-effect damage.
- Low rate of fire; requires careful aim.
- Can be dangerous if detonated too close to the user.