COMBINE OVERWATCH ARCTIC ELITE SOLDIER


CLASS:      Combine Overwatch Arctic Elite Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Strike Elite
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, Energy Balls
REWARD:     1100 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Arctic Elite is a specialized version of the Combine Elite, designed for combat in permafrost, blizzards, and dead polar cities. His white camouflage practically blends with the surrounding ice, and gray-blue elements of his gear replace the familiar red details, making his silhouette cold, emotionless, and almost ghostly.

A single blue visor glows through the snowstorm, detecting heat signatures and movement at a considerable distance. Blue stripes run along both sides of his legs, serving as identification and technical elements of his armor, as well as a visual marker of his elite status.

These soldiers are deployed in regions where conventional Combine forces quickly lose their combat effectiveness. Reinforced implants protect them from hypothermia, and their life support system allows them to fight for hours without cover or resupply. Their tactics are standard for Overwatch elite: coordinated fire, suppression with energy balls, and methodical elimination of resistance.

TIPS:

- Their white camouflage makes them difficult to spot in the snow.
- Energy balls are especially dangerous in narrow icy passages.