SQUIRREL (JUMPER) FROM THE KRIEGER ARCHIPELAGO


CLASS:      Squeaker (Jumper)
METABOLISM: Trigen-mutated human
PERCEPTION: Tactical saboteur
SIZE:       5.9 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    AG36, P90 automatic rifles, jump attacks
REWARD:     800 FC
THREAT:     High

DESCRIPTION:

Squeakers are the most intelligent variety of Trigen, retaining tactical thinking. Based on soldiers, they are recognizable by their military trousers, haircuts, and lack of shirts. Their limbs are hyper-muscled, and their mouths stretch to their chins. Metal arm guards above their elbows are a testament to their experiments. They are capable of coordinating the actions of other mutants, using them as living saboteurs.

Their primary tactic is mobility. They leap to distances other Trigens can't reach, constantly changing positions. In close combat, they use weak but distracting attacks. They are critically vulnerable when their jump is interrupted—at this point, they lose speed and cannot counterattack.

TIPS:

- Shoot while preparing to jump—this immobilizes them for 2-3 seconds.
- Use weapons with a high rate of fire—their mobility makes aimed shots ineffective.
- Control the heights—they often attack from higher levels and rooftops.