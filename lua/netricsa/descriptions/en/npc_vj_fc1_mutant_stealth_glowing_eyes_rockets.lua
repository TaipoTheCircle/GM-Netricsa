STEALTH TRIGEN (INVISIBLE) FROM THE KRIEGER ARCHIPELAGO


CLASS:      Stealth Trigen
METABOLISM: Failed Trigen Mutation
PERCEPTION: Tactician-Saboteur
SIZE:       5,8 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONATAL: Silent MP5, grappling hooks
REWARD:     1000 FC
THREAT:     High

DESCRIPTION:

Stealth Trigens are the result of failed experiments to create the perfect soldier. Unlike the muscular Squeakers and Fatties, they have an emaciated frame, lacking hair and lips, revealing clenched teeth. Their chests are covered in inflamed implants that power an experimental cloaking device. They are capable of remaining invisible for long periods, revealing themselves only when attacked or killed.

They operate from ambush, using invisibility for surprise attacks. They attack from long range with a silent MP5, and when the enemy approaches, they attempt to capture and immobilize them. They rarely operate in groups, preferring to eliminate key targets.

TIPS:

- Use CryVision biothermal goggles for detection.
- Listen carefully to the sounds of footsteps and weapon reloads.
- Keep your distance - their grabs are deadly at close range.
- Use shotguns or grenades at suspected hidden locations.