ONAN THE LIBRARIAN


CLASS:      Onan the Librarian
METABOLISM: Mammal
PERCEPTION: Primitive Being
SIZE:       9-10 ft
ENDURANCE:  Medium
HOSTILITY:  Medium
WEAPONS:    Axe
REWARD:     200 FC
THREAT:     Medium

DESCRIPTION:

Although most of M'Digbo's inhabitants are primitives, some (slightly) more advanced societies exist.

While the Simba people are undoubtedly the most civilized, there is another society that serves as a middle-ground between the caveman-esque brutes and the civil Simbas.

These have been nicknamed "Onans" by human researchers. Onans are taller and stronger than most primitives, yielding iron axes instead of wooden clubs.

Unlike primitives, Onans choose to slowly walk towards their prey, while flinging their axes towards it.

TIPS:

- Don't get too close, they may prefer ranged combat, but they are dangerous up close They are very slow, it is very easy to outmanuever them by strafing around.
- They don't think before they throw. They just hope for the best with no regard of their enemy's movements or any allies in the way.
