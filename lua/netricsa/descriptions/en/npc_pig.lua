DOMESTIC PIG FROM THE ZENOZOIK


CLASS:      Domestic Pig
METABOLISM: Mammal, Even-toed Ungulate
PERCEPTION: Basic vision, olfactory
SIZE:       1,6 ft
ENDURANCE:  Low
HOSTILITY:  None
WEAPONS:    None
REWARD:     3 FC
THREAT:     None

DESCRIPTION:

A common domestic pig found in Zenozoik settlements and farms. No different from its terrestrial counterparts, except that it inhabits a much stranger world. Completely harmless, it does not attack or interact with opponents.

TIPS:

- Attacking it is pointless and may attract unnecessary attention.
- When kicked, it makes a characteristic squeal and awkwardly rolls over. It does not attempt to retaliate.