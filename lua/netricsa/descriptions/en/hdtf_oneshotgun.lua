DOUBLE SHOTGUN


WEAPON: Double Shotgun
AMMO:   12-gauge shells
ROF:    ~46.5 blasts/min 

DESCRIPTION:

The Double Shotgun is a side-by-side double-barreled shotgun. When triggered, it fires both barrels simultaneously, delivering a powerful spread of pellets. It has a relatively tight spread for this kind of weapon, making it viable at medium range. After firing, there's a brief delay before it can fire again.

NOTES:

- Best used against medium-strength enemies like Kleer Skeletons and Levitators. 
- Close-range with full pellet spread can eliminate many weak enemies in one shot.