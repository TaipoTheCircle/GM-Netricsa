TRANSLOCATOR


WEAPON: Translocator, AR2-based
AMMO:   Self-Replenishing Dark Matter Energy
ROF:    Instant-fire

DESCRIPTION:

The Translocator is an experimental Combine device based on the AR2 assault rifle, but completely lacks direct-kill capabilities. Instead, the weapon uses a focused pulse of dark energy to spatially displace objects. After locking onto a target with the energy matrix, the object can be instantly transported to the user's aiming point, bypassing physical obstacles and gravity.

NOTES:

- Can teleport any physical objects: crates, barrels, vehicles, turrets, and some creatures.
- Used by Combine engineers and field technicians for tactical spatial control.