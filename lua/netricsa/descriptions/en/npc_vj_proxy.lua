MEMBER OF THE PROXY ORGANIZATION


CLASS:      Member of the Proxy Organization
METABOLISM: Human, Male
PERCEPTION: Obsessed with Slenderman
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Any sharp object
REWARD:     1000 FC
THREAT:     High

DESCRIPTION:

This is the name given to people who have fallen under Slenderman's influence. They are recruited, broken, reprogrammed, and released back into the world with one purpose: to search, track, and kill.

Proxies' appearance is standardized to the level of a corporate standard. Black clothing is practical, doesn't stain, and hides blood well. A black balaclava is worn to prevent anyone from seeing the face, because it may no longer be there. The logo is painted on the balaclava with white paint or marker.

The proxy's behavior combines traits of a stalker and a maniac. It can sit in the dark for hours, observing its victim. It can stalk them for days without showing aggression, then strike when the victim is relaxed. It feels no pain, feels no fear, and doesn't respond to persuasion.

TIPS:

- It's a skilled weapon user. Don't try to cross blades with it unless you're a pro.
- Proxies don't speak.
