"BETA" SWARM DRONE, LARGE


CLASS:        "Beta" Swarm Drone, Large
METABOLISM:   Xenobiological
PERCEPTION:   Heavy Pack Predator
SIZE:         9,8 ft
ENDURANCE:    High
HOSTILITY:    Very High
WEAPONS:      Claws, Teeth, Body Mass
REWARD:       600 FC
THREAT:       High

DESCRIPTION:

The Large Drone is an unusually oversized specimen of the "Beta" Swarm. Unlike elite drones, which are distinguished by their coloration, this subspecies looks almost identical to standard specimens. It possesses the same green hide, the same three eyes, and the same insatiable urge to tear any living thing apart. The difference only becomes apparent when the creature stands next to a standard drone.

The reasons for such anomalous growth remain unknown. One theory suggests that certain larvae receive significantly more nutrients than others. Another theory posits that the Swarm simply lacks the ability to stop growing at the right time.

Its increased size has granted the creature more powerful musculature and significantly reinforced internal organs. Consequently, the Large Drone can withstand damage that would have long since killed a standard specimen and deliver blows powerful enough to send a victim flying several meters.

Despite its imposing size, the Large Drone retains much of the agility of its smaller kin. This is a particularly unpleasant surprise for anyone expecting such a monster to move slowly and ponderously.

TIPS:

- Don't let its size fool you. It is faster than it looks.
- Keep your distance and use powerful weaponry.
- If ordinary drones are spotted nearby, eliminate the most dangerous target first.
- When Swarm "Beta" decides to create a larger drone, everyone nearby usually suffers.
