MJOLNIR GUN


WEAPON: Mjolnir Gun
AMMO:   9mm Ammo / Lightning Energy
ROF:    ~60 RPM

DESCRIPTION:

A compact pistol imbued with the power of an ancient thunder artifact. While it looks like a standard 9mm weapon, its true power is only revealed in the hands of a worthy user.

The primary fire mode unleashes a focused lightning strike at a targeted point. The bolt descends from the sky with devastating force, striking the target instantly and leaving a charred area of damage.

The alternate fire mode is used primarily for demonstration purposes: lightning flashes across the sky without causing significant damage, creating a terrifying visual effect and exerting psychological pressure on the enemy.

NOTES:

- The primary fire unleashes a powerful lightning strike on the target.
- Firing under the user is extremely dangerous - the user does not have Thor-level defense.
- Alternate fire creates lightning in the sky without significant intimidation.
- Effective at any range with a clear line of sight to the sky.
- Only works in the hands of a "worthy" person.
