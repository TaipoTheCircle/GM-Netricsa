WANDERING TRADER FROM THE MINECRAFT OVERWORLD


CLASS:      Wandering Trader
METABOLISM: Human, Male
PERCEPTION: Wandering Trader / Exotic Supplier
SIZE:       6,2 ft
ENDURANCE:  Average
HOSTILITY:  Passive
WEAPONS:    None
REWARD:     150 FC
THREAT:     Minimal

DESCRIPTION:

A mysterious figure in a blue hooded robe, whose origins remain a mystery even to villagers. This enigmatic wanderer roams the Overworld, seeking customers willing to exchange emeralds for rare and exotic goods.

The Trader's appearance is noticeably different from that of ordinary villagers. Instead of traditional clothing, he wears a long blue robe with a hood that conceals his face, and a cape of the same color.

On every journey, the Trader is accompanied by two Trader llamas, tethered to him by leashes. These hardy animals not only carry the load but also protect their master: if the Trader is hit, the llamas immediately begin spitting at the offender. The llamas themselves can be tamed after their master's death—just wait until they calm down.

Unlike villagers, the Trader doesn't buy items—he only sells them. His inventory is random and includes rare goods that are difficult to obtain.

The Trader's main trick is survival. At nightfall or upon seeing a hostile mob, he drinks an invisibility potion, becoming almost invisible, although the llama leashes are still visible. At dawn, he washes off the effect with milk. If a zombie kills the Trader, he will not turn into a zombie villager.

Killing a Trader lowers your reputation - subsequent Traders will appear less frequently, and their prices will increase.

TIPS:

- Always have a supply of emeralds on hand - you never know what interesting things a Trader will bring.
- If you need his llamas, it's best to wait until the Trader disappears - then the llamas will remain and can be tamed.
- When attacking a Trader, be prepared to be spat upon by his llamas.
- If you only see floating leashes, it means the Trader has consumed an invisibility potion and is somewhere nearby.
- The Trader is vulnerable at night - the invisibility potion doesn't make him completely invulnerable, it just hides him.