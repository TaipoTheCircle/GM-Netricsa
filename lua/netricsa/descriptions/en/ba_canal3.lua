CHANNELS OF THE BLACK MESA, A SPECIAL AREA


Sam, listen carefully. The path to the explosive box is not as easy as it seems. You need to take a detour - where you are standing now, the "small area" is fraught with a surprise.

They can come out of the shadows:

cartridges - so as not to be left without fire;

Armor - it might come in handy in close combat;

weapons - maybe your old friend "Whizz" will get a company.

But along with the supplies will come the Mental horde. They will come out of all the spatial cracks as soon as they sense your presence. Be ready to attack!

It gets scarier further.

In the distance, behind the rusty pipes, you will see a guard on the bridge. The poor guy is trapped: zombies are dragging him in different directions, like children arguing over a toy. He screams, but no one will come to help. 

After the bridge, there is a path to the box.