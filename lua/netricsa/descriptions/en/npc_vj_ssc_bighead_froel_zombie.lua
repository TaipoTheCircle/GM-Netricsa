BIG-HEAD, FROEL HOATES


CLASS:      Big-Head, Froel Hoates
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     6666 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of the developers. They wear the uniform of Cool Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Where are you going!?" They're completely harmless, but in the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They spontaneously appear in secret areas, blocking passages with their sheer numbers.

Froel Hoates is a special case. He's not a modder, programmer, or tester in the classic sense. His oversized skull is a universe of original characters (OCs), fan art, and endless fangame ideas that will likely remain just concepts. He's an artist at heart, a jack-of-all-trades who draws whenever he wants and creates content simply for fun.

He's the one who designed those tropical gnaars that DED123_228 implemented in the Revolution mod and that Taipo ported to GMod with permission. But his greatest legacy is his own virtual world.

He has "Froel Gnaar" - his fursona projection in the form of a gnaar, which he loves dearly. He has a whole gallery of OCs for the Serious Sam: from the poisonous Larcarian Prowler and the parasitic Elugat to a friendly NPC, Alien, who can be traded with. His OC, Jimmothy, is a sweet Gnaar, around whom an entire AU plot is built about the joint adventures of Serious Sam and Duke Nukem.

Froel is a walking encyclopedia of niche interests (Transformers, Jonny Quest, Postal, The Tick), a man with ADHD who loves "silly games, puzzles, and shooters" and hates drawing complex backgrounds. He embodies the pure, effortless joy of fan art. He creates not out of duty, fame, or money, but because his imagination constantly demands an outlet.

He was once alive, but after someone killed him, he rose from the dead - becoming a demonic Zombie.

TIPS:

- Use a combat knife or pistol to clear a path.
- On "I Am Ugh-Zan" difficulty, destroy them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.

