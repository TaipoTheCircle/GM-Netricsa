MALE GNAAR


CLASS:      Gnaar, Male
METABOLISM: Mammal
PERCEPTION: Trained War Animal
SIZE:       4 ft
ENDURANCE:  Low
HOSTILITY:  Low
WEAPONS:    Bites, Punches
REWARD:     500 FC
THREAT:     Very Low

DESCRIPTION:

A small bipedal animal with no head, one eye and a large mouth. Originating from Sirius, and trained by Mental to attack opposing forces. The males are smaller, purple and not as tough as females.

TIPS:

- They have no range weapons, just keep away.
- Some species can levitate.
- Rare specimens have been reported to be invisible.
