FIENDIAN REPTILOID DEMON, STORMCALLER


CLASS:      Fiendian Reptiloid Demon, Stormcaller
METABOLISM: Cold-Blooded, Reptile-Like
PERCEPTION: Modified Primitive Race
SIZE:       30 ft
ENDURANCE:  High
HOSTILITY:  High
WEAPONS:    Tornadoes, Claws
REWARD:     8000 FC
THREAT:     Medium

DESCRIPTION:

Another hideous mutation, courtesy of Notorius Mental. A simple lizard-like organism from Fiendia Prime was crossbred and mutated through several generations, and was finally reinforced with a titanium skeleton. It has a unique ability of twisting the surrounding air, reaching wind speeds up to the level of F2 tornadoes. These miniature tornadoes will eventually stabilize and dissipate, but they will wreak havoc in the meantime.

TIPS:

- The tornadoes will throw objects around and redirect projectiles.
- There is no way to destroy the Demon's tornadoes... Just avoid them until they dissipate.
