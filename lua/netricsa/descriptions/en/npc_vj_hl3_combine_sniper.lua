OVERWATCH COMBINE FEMALE SNIPER


CLASS:      Transhuman Combine Sniper
METABOLISM: Enhanced human, female / neuro-biomechanical implants
PERCEPTION: Optical enhancements, ballistic computers, tactical telemetry
HEIGHT:     5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Overwatch Mk1 Precision Sniper Rifle
REWARD:     800 FC
THREAT:     Very High

DESCRIPTION:

The Overwatch Combine Female Sniper is a specialized transhuman operative designed exclusively for long-range assassination. Unlike standard Overwatch soldiers, these operatives do not engage in open assaults or operate in groups. Their mission is to control key areas, suppress resistance, and eliminate commanders, leaders, and high-risk targets.

Snipers are deployed on rooftops, towers, industrial structures, and other hard-to-reach locations. They can remain motionless for hours, awaiting orders or target confirmation.

TIPS:

- Never stand in open spaces without cover.
- The best way to survive is to remain undetected.