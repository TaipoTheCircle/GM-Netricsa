COMBINE OVERWATCH PILOT


CLASS:      Combine Overwatch Pilot
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Air Unit
SIZE:       5,9 ft
ENDURANCE:  Medium
HOSTILITY:  High
WEAPONS:    OICW, SMG
REWARD:     850 FC
THREAT:     Medium

DESCRIPTION:

The Combine Overwatch Pilot is a specialized combatant responsible for controlling Combine aircraft. Unlike standard soldiers, their primary value lies not in precision shooting, but in controlling airspace and quickly deploying forces.

They wear a pilot helmet with an integrated respirator and integrated blue pilot goggles, directly linked to Combine navigation systems. The goggles display altitude, speed, targets, and air defense zones, allowing the pilot to operate even in snowstorms, radio jamming, and complete darkness.

While pilots are not designed for frontline combat, the Combine equips them with versatile weapons. The OICW allows for firing at both infantry and targets behind cover, while the SMG is used in the event of an emergency landing or while defending the landing zone. Augments improve spatial awareness and G-force resistance, turning the pilot into a force to be reckoned with in the Combine's air force.

