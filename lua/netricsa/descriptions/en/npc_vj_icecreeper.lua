ICE CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Ice Creeper
METABOLISM: Cryogenic, Snow Composite
PERCEPTION: Free Snow Supplier
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  Low
WEAPONS:    Cold
REWARD:     40 FC
THREAT:     Medium

DESCRIPTION:

This is a white snow creeper that leaves a chill in its wake. If you encounter one in the tundra or on icy plains, you might not even notice it: its white skin blends perfectly with the snowy landscape.

Unlike its green and gunpowder-type counterparts, this type of creeper doesn't try to kill you with a shockwave. Its main strength is its terrain control. Wherever it passes, it leaves behind a layer of snow covering the ground. When it explodes, instead of causing a destructive blast, it freezes everything around it. Water turns to ice, and if it detonates near a lava source, the lava will harden into obsidian.

The peculiarity of its explosion is that it deals no damage to enemies or blocks. Instead, it simply covers the area around it with a layer of snow and freezes water surfaces.

The Ice Creeper can be found in cold biomes: tundra, icy deserts, seasonal forests, and, of course, snow biomes.

TIPS:

- This is the perfect way to get free snow.
- Don't be afraid of its explosion. It won't destroy your house or kill you, but your entire yard will turn into a white field.
- Keep it away from lava sources or valuable water structures. Freezing your tropical fish tank would be a sad experience.
- It's easily recognizable by its white color, especially against the normal grass and rock in caves.
- Killing it with regular weapons is pointless if you're specifically hunting for snow -you need to trigger an explosion.
- Frozen lava turns into obsidian. This can be useful if you've fallen into the netherworld and a creeper like this happens to be nearby.
