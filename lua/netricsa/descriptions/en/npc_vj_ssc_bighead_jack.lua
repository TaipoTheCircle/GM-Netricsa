BIG-HEAD, JACK THE POFIGIST


CLASS:      Big-Head, Jack the Pofigist
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic (Golden-Handed Creator)
SIZE:       5,9 ft
ENDURANCE:    Low
HOSTILITY:  Neutral
WEAPONS:    None (except on I Am Ugh-Zan difficulty)
REWARD:     9999 FC
THREAT:     Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of developers. They wear the uniform of Serious Sam, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They're completely harmless, but on the "I Am Ugh-Zan" difficulty level, they gain fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their sheer numbers.

Jack never worked at Croteam, but his contribution to popularizing the series among a new generation is invaluable. He's the bridge between the classic 2001 shooter and the endless sandbox of 2006. Thanks to his Serious Sam Classic add-on, Mental's armies invaded thousands of user-created GMod scenarios: from silly RPG servers to epic animations. He didn't just port the model pack-he painstakingly recreated enemy behavior, sounds, and even balance to ensure the chaos felt authentic.

His oversized skull is an archive that preserves the very soul of beloved universes: the tactical freedom of Far Cry 1, the boundless creativity of Minecraft, the pure adrenaline of Serious Sam, and the absurd chaos of Goat Simulator. It's this unique blend of inspiration that makes his GMod addons so vibrant and multifaceted-he doesn't simply copy enemies, but imbues them with an understanding of the gameplay of his favorite games.

But his greatest superpower is mentorship. For Taipo, he's not just a fellow modder, but "the best friend who ever lived." Every tip on Blender or script logic is a lesson, and the fan community grows and strengthens. Jack is the core of that creative chain where experience and passion are passed on, ensuring the perpetual life of game universes. He is the embodiment of genius. With his "golden hands", masterful use of Blender, and tireless energy, he created the iconic Serious Sam Classic's NPCs addon for Garry's Mod. It was thanks to him and his friends (Taipo and S199-Smirnof) that the madness of Serious Sam - Gnaars, Kleers, Headless Kamikazes, and even the developers themselves in the form of the Croteam - HEADS: escaped the original games and took up residence in the GMod sandbox to terrorize and amuse new generations of players.

His story-from a Steam-restricted enthusiast to a respected content creator-is a modern-day fairy tale for the gaming community. Jack the Indifferent proves that his legacy lives on not only in official games but also in the hands of talented fans who continue to sculpt, animate, and spread his spirit.

TIPS:

- Use an army knife or pistol to clear a path.
- On "I Am Ugh-Zan" difficulty, destroy them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.
