TEST 2D NEXTBOT, STICKMAN FROM THE ????


CLASS:      Test 2D Nextbot, Stickman
METABOLISM: Pixelated
PERCEPTION: Mysterious Digital Entity, Stickman Observer
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  Zero
WEAPONS:    None
REWARD:     100 FC
THREAT:     Zero

DESCRIPTION:

This is one of the most mysterious creatures in the known universes. No one knows where they came from, who created them, or why they are here. They appear as pixelated stickmen of various colors: red, blue, green, yellow, etc. They are flat, as if drawn on a piece of paper, but they move in three-dimensional space, maintaining their 2D form.

The first documented appearances were recorded in test environments, abandoned simulations, and strange digital spaces that defy classification. Hence the name "test environments." The creator, if one exists at all, left no trace.

Upon sighting any living creature, it begins moving toward the target. It doesn't run, sneak, or attack-it simply walks at a constant speed, taking the shortest path. If an obstacle stands in its way, it will go around it. If a door is locked, it will stand in front of it until it's opened.

When the Nextbot approaches an object of interest, it stops and begins jumping. It jumps joyfully, enthusiastically. It can jump indefinitely, showing no signs of fatigue or aggression.

Upon death, it simply disappears. It leaves no trace.

The most unlikely theory is that they are trying to establish contact but don't know how, so they simply jump.

TIPS:

- These Nextbots are harmless. Don't waste ammo on them.
