FLARE GUN


WEAPON:      Flare Gun
AMMO:        Signal flares
ROF:         ~6 rounds/min

DESCRIPTION:

Single-shot firearm that launches incandescent flares. Primarily used for signaling, illumination, or temporary distraction of enemies. Can set flammable objects on fire if aimed carefully.

NOTES:

- Limited ammo; each shot consumes one flare.
- Effective for lighting dark areas or marking positions.
- Can cause minor fire damage to enemies and environment.
- Not suitable for sustained combat; primarily utility weapon.