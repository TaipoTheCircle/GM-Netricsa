YELLOW-EYED COMBINE OVERWATCH ELITE SHIELD SOLDIER


CLASS:      Yellow-Eyed Combine Overwatch Elite Combine Shield Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Assault Shield Suppression Unit
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    CMB Tactical Shield, Silenced Pistol
REWARD:     700 FC
THREAT:     High

DESCRIPTION:

The Yellow-Eyed Elite Shield Soldier is an Elite unit, intentionally degraded in equipment but not in skill. Unlike standard Elites, he does not wear heavy armor to appear like a standard shield bearer and avoid causing panic until contact is made.

His yellow visor betrays his Elite status, but most resistance fighters realize it too late. His demeanor deliberately mimics that of a standard shield-wielding soldier: slowly advancing, covering allies, suppressing targets.

However, in close combat, his true nature is revealed. His silenced pistol is used not only for shooting but also as an improvised baton. He pushes the enemy back, knocks them down, creates distance, and then finishes them off with a precise point-blank shot. His tactics appear mockingly calm, as if the fighter is confident in the outcome of the encounter.

The Combine uses such soldiers for psychological pressure and crowd control in urban areas, where maintaining control while demonstrating absolute superiority is crucial.

TIPS:

- Don't try to penetrate his shield head-on - go around it or use explosives.
- His weak armor is deceptive: his Elite skills make him extremely dangerous.
- If he gets too close, prepare for a push and a finishing shot.
- Destroy him from the flanks or from above, where the shield is useless.
