DELTA BASE ZOMBIE HEADCRAB IN THE HAZARD XEN SUIT, COMMON


CLASS:      Delta Base Zombie Headcrab In The Hazard Xen Suit, Common
METABOLISM: Human Host + Parasitic Headcrab Of Xen
PERCEPTION: Armored Zombified Entity
SIZE:       6,2 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    Claws, Bite
REWARD:     600 FC
THREAT:     Very High

DESCRIPTION:

This specimen is a former Delta Base researcher who was careless enough to travel to Xen or work in particularly hazardous environments wearing the signature purple HAZARD SUIT. Functionally, it is completely identical to the H.E.V. From Black Mesa: sealing, impact amplification, radiation protection, and other delights of scientific work on the edge of reality.

Even this didn't save some unfortunate souls from the headcrab. The parasite found a way to penetrate the helmet-or perhaps the host removed it at a critical moment-and did its dirty work. Now the moving carcass in the purple suit is a zombie, but a zombie with advantages.

The protective suit gives it a double advantage. First, survivability. Bullets and shrapnel are absorbed by the suit's reinforced fabric and plates. Second, strength. Servos that helped scientists lift heavy equipment now amplify the zombie's blows, making them devastating.

The purple suit is stained with blood and dirt, the helmet is broken or missing. A headshot decapitates the head.

TIPS:

- This zombie has the highest armor of all the standard varieties. A regular pistol is almost useless.
- Aim for the head. The suit protects the body, but not the head.
- Use a shotgun at point-blank range or a high-caliber automatic weapon.
- A headshot will detach the head, just like the others.
- Don't think you can take the suit off and put it back on. It's part of the monster now.
