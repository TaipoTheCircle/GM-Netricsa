MUTANT BERSERKER (SCREWED) FROM THE KRIEGER ARCHIPELAGO


CLASS:      Mutant Berserker (Screwed)
METABOLISM: Intermediate Trigen Mutation
PERCEPTION: Unstoppable Fighter
SIZE:       8.1 ft.
ENDURANCE:  Very High
HOSTILITY:  Absolute
WEAPONATAL: Bone Blade (Right Hand)
REWARD:     1200 FC
THREAT:     Critical

DESCRIPTION:

The Berserker is an evolutionary link between Trigen primates and human mutants. Of gigantic stature, with a massive bone blade replacing its right hand and an additional oral cavity on its face. It combines the animalistic fury of smaller Trigens with the destructive power of late-stage mutations. It constantly moves toward its target, ignoring obstacles.

Acts like a living battering ram. In close combat, it delivers devastating blade strikes, capable of destroying a target in 2-3 hits. Its size pushes enemies back, limiting their maneuverability. It's vulnerable during its attack swing.

TIPS:

- Run and shoot before it gets close.
- Dodge in a zig-zag pattern, not in a straight line.