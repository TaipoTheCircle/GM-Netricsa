COMBINE OVERWATCH ELITE ARCTIC GUARD


CLASS:      Transhuman infantry, elite security and assault unit
METABOLISM: Augmented human, male
PERCEPTION: Tactical HUD + infrared and thermal systems
SIZE:       5,9 ft
ENDURANCE:  Very high
HOSTILITY:  Extreme
WEAPONS:    AR2 V2 pulse rifle, energy balls
REWARD:     900 FC
THREAT:     Very high

DESCRIPTION:

The Overwatch Combine Elite Arctic Guard is a specialized variant of elite infantry designed to protect strategic assets, key personnel, and critical areas in extreme climates. Unlike Commandos, Guards are less likely to engage in rapid advances, but are almost as effective in combat.

They are easily recognizable by their dazzling white armor and single red visor. A cloak is optional: some Guards wear it on their shoulders as a sign of status or affiliation with a specific unit, while others forgo it entirely for greater mobility and reduced visibility in strong winds and snowstorms.

Like other elite Overwatch soldiers, their armor is equipped with a built-in life support system. A respirator is connected to a thick tube running over the left shoulder and connected to an oxygen backpack on the back bearing the Combine logo. The system ensures stable breathing, filters out toxins, and protects Guards from extreme temperatures, allowing them to hold positions in arctic zones for hours without external support.

TIPS:

- The absence of a cloak does not indicate a lower rank or threat.
- They almost always operate in pairs or as part of a perimeter security team.