COMBINE OVERWATCH HYBRID SHOTGUNNER


CLASS:      Combine Overwatch Hybrid Shotgunner
METABOLISM: Augmented Human, Male
PERCEPTION: All-purpose close-combat assault unit for the Combine
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    SPAS-12 Combat Shotgun, Grenades, Stun-Stick
REWARD:     650 FC
THREAT:     High

DESCRIPTION:

The Combine Overwatch Hybrid Shotgun Soldier is a modification of the standard Shotgun Soldier, tailored for aggressive close-range assault. He retains all the equipment of the standard Hybrid Soldier, including enhanced armor and night vision, but his role is radically altered.

The orange visor immediately identifies the soldier's intended purpose. The SPAS-12 is used for lightning-fast enemy suppression in corridors, tunnels, living quarters, and utility rooms. The Combine employs these soldiers in situations where speed and psychological impact are crucial.

The night vision device allows the soldier to operate in complete darkness, and the binocular mode helps quickly locate cover and enemy routes. The Stun-Stick is used less frequently, but is used for finishing off, intimidating, or capturing live targets.

In combat, the hybrid shotgun wastes no time in firefights. It closes the distance, flanks, and turns confined spaces into deadly traps. Its tactics are simple but extremely effective.

TIPS:

- Never let it get close - the SPAS-12 is almost guaranteed to be lethal.
- The orange visor is highly visible in the dark.
- In tight spaces, retreat is safer than attack.