THE SMALL BLACK MESA WAREHOUSE, OLD AND CRAMPED


Sam, we've ended up in a place that, even by Black Mesa standards, looks like a forgotten closet. A small warehouse. Old. Dusty.

There are crates all around. Lots of crates. Of varying heights. Some stacked them neatly, others haphazardly. Some stand in neat rows, creating narrow passages. Others are piled on top of each other, creating a maze of shadows and dead spots. Still others are simply in the middle of the room, as if they were brought in and abandoned, their purpose forgotten.

This warehouse is likely old. Very old. Perhaps it was built before Black Mesa became "Black Mesa". Back when the complex was simply a secret bunker, not the epicenter of interdimensional chaos. But time hasn't been kind to the walls, and disasters have added soot and cracks.
