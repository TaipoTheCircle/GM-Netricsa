FLOATER DRONE FROM THE SIRIUS


CLASS:      Floater Drone 
METABOLISM: Al-Controlled Aircraft
PERCEPTION: Primitive Al
SIZE:       2 ft
ENDURANCE:  Very Low
HOSTILITY:  Medium
WEAPONS:    Lasergun
REWARD:     200 FC
THREAT:     Low

DESCRIPTION:

Are light, flying reconnaissance units designed for scouting, although they are extensively used by the Sirian police as well, typically handing out speeding tickets to reckless drivers and pilots.

Floaters will circle around enemies and shoot them with a low-yield lasergun. One of them is a minimal threat, however, they will transmit a distress call to all nearby floaters, as well as the local Drone Command HQ.

If you are spotted by a Floater, don't be surprised if you see the bulk of Mental's forces coming to reinforce the area.

TIPS:

- They can be easily dispatched with most weapons, but rapid-fire and area-damage ones work best.
- They should be low-priority targets among other foes, as long as you are on the move and dodge their lasers.
