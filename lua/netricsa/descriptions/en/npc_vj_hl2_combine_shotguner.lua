COMBINE OVERWATCH SHOTGUNNER


CLASS:      Combine Overwarch Shotgunner
METABOLISM: Augmented Human, Male
PERCEPTION: Enhanced auditory + tactical vision
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    SPAS-12 Shotgun, Frag Grenades
REWARD:     450 FC
THREAT:     High

DESCRIPTION:

The Combine Overwatch Shotgunner is a brutal close-range enforcer designed for urban suppression and corridor combat. Outfitted with heavier body armor and enhanced muscle augmentations, these soldiers thrive in confined environments where their devastating shotguns can instantly annihilate unprepared enemies.

Unlike standard Overwatch units, Shotgunners are programmed for relentless aggression, often charging headlong into enemy positions to maximize the destructive potential of their weaponry.

They form the spearhead of Combine assaults, breaking resistance lines before the more organized transhuman infantry moves in to secure territory. While their heavy armor and devastating firepower make them formidable, they are far less effective at medium or long range, relying heavily on ambushes and aggressive maneuvers.

TIPS:

- Keep your distance - their shotguns dominate at close range.
- Explosives are effective against groups of Shotgunners.
- Avoid tight hallways if you know they're nearby.