WALTHER P22 PISTOL


WEAPON:           Walther P22
AMMUNITION        .22 LR
ROF:              15 rounds/min

DESCRIPTION:

The Walther P22 pistol is a German 22 caliber self-loading sports and training pistol manufactured by Carl Walther Sportwaffen GmbH. The pistol's frame is made of impact-resistant polymers, while the barrel and slide are made of steel. It is equipped with a double-action trigger mechanism. Safety devices include a mechanical safety, an automatic safety that locks the mechanism when the magazine is not inserted properly, and a lock that locks the slide frame, preventing firing and disassembly. The P22 model resembles the earlier Walther P99 model, but is about a quarter shorter. The pistol comes with interchangeable sights and grip panels to allow the weapon to be customized to the shooter's hand.

NOTES:

