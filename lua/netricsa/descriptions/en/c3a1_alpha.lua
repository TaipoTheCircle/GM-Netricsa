BLACK MESA REACTOR X-LAB, LOADING BAY


Sam, the scientists have brought us here. Not to the exit, as you might think, but to a new batch of problems. We're in the reactor's X-lab. The place where Black Mesa tested its most terrifying theories. Judging by the situation, the theory failed, the rig held, and we're left to deal with the consequences.

The facility is in a high-security state. This means whoever was here before us really didn't want anyone coming or going. The only entrance to the reactor lab is locked. Next to it is the elevator leading to the control center. Also locked. Everything is locked. Except for one way.

The sewers. Under the elevator shaft. Where no one wants to go, but where there's always a way in. Because in Black Mesa, if the door is locked, you have to go through the pipes. Rule number one.

If we make our way through the sewers and emerge on the other side, we'll reach the center. There's a large number of aliens there. Not a patrol, but a whole group. And a small detachment of H.E.C.U.s, who apparently wandered in here looking for a way out and got stuck.

The chief scientist, it turns out, is locked in the control room. He's probably asking to be found, because without him, we might not be able to unlock the exit. Or we won't know how to shut this circus down.

We need to neutralize the guard in the reception area. He has an access card. Without it, we won't get anywhere.

In the communications center, we need to find a console. Use it to lower the security level and unlock the remaining doors. The ones that lead to the reactor. And, possibly, to the exit.

If we do everything correctly, the doors will open, the scientist will come out, and we'll learn how to escape this nightmare. If we make a mistake, we'll stay here "Forever", as Tazik29 likes to say, among the infantry, the marines, and our own hopes.
