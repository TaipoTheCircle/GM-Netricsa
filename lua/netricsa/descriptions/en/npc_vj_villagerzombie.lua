ZOMBIE VILLAGER FROM THE MINECRAFT OVERWORLD


CLASS:      Zombie Villager
METABOLISM: Infected Villager
PERCEPTION: Hostile mob
SIZE:       6,2 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Hands
REWARD:     200 FC
THREAT:     Average

DESCRIPTION:

This is the result of a tragic encounter between a normal villager and a normal zombie. A single bite is enough for the unwary merchant to transform into this pale simulacrum, retaining only a recognizable nose and the remains of clothing. Outwardly, it differs from a normal zombie in its characteristic muzzle and larger size - 0,9 cm taller than its fellow zombies, which sometimes allows it to get stuck in doorways.

Zombie villagers spawn in the same places as regular zombies, but with a lower chance. They can sometimes be encountered in the taiga and in igloos, where one of these zombies is locked in the basement behind bars.

Zombie villagers behave similarly to regular zombies: they pursue victims, villagers, and iron golems, can break down doors, and pick up items. But there's one important difference, one that many are willing to risk their lives for: they can be cured.

The healing process goes like this: first, the zombie villager is weakened with a potion or explosive pumpkin, then given a golden apple. After a few minutes, shaking begins, and after another minute or two, the villager returns to normal. The cured villager offers discounts on trade, sometimes symbolic ones—up to one emerald for everything.

TIPS:

- If you see a zombie villager, don't kill it right away. Isolate it from other zombies so you can cure it later.
- To cure it, you need a potion of weakness and a golden apple. Both can be crafted or found in chests.
- Igloo basements always contain one zombie villager and everything needed to heal it.
- A cured villager gives good discounts. If you're lucky, you can get a diamond tool for a couple of emeralds.
- Baby zombie villagers also exist. They're just as fast and heal according to the same rules.
- Zombie villagers can spawn riding a chicken. It's rare, but it happens.
