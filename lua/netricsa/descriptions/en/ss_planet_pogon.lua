POGON


PLANET:       Pogon
TYPE:         Terrestrial planet, Cryo-world, Military base
CLIMATE:      Deep freeze, severe frost, constant cold wind
MOONS:        Unknown
TERRAIN:      High mountain ranges, snowy plains, icy plateaus
LOCATION:     Somewhere between the planets Stroggos and Earth
NATIVE RACES: Strogg (garrison)
ALLIANCE:     Strogg Empire
LOCATIONS:    General Faron's Base, Mountain Passes, Snowy Plains

DESCRIPTION:

A harsh, snowy world located at a strategic point between Stroggos and Earth. Extreme climatic conditions make this planet a natural fortress: severe frosts, piercing winds, and sheer mountain ranges virtually eliminate the possibility of successful air raids or artillery attacks from space.

The planet's landscape is formed by high, inaccessible mountains, between which stretch endless snow-covered plains and icy plateaus. Constant blizzards and low visibility make navigation extremely difficult, and any traces of their presence are quickly obscured by snow.

It is here, deep in the mountain range, that the Strogg have established one of their key military bases. Command of the facility is exercised by General Faron, an elite Strogg Tank on an antigravity platform, a formidable combat unit ideally suited for warfare in harsh winter conditions.

The choice of Pogon as the base was dictated not only by its strategic location en route to Earth, but also by the natural protection provided by the climate. While blizzards and freezing temperatures hamper any invasion attempts, the Strogg conduct their work unhindered in well-sheltered and heated complexes carved into the rock.

KEY LOCATIONS:

- General Faron's Base: The Strogg's primary stronghold on the planet, a complex of tunnels and hangars hidden deep within the mountain range. From here, they coordinate their operations and control the sector.
- Mountain Passes: The only potential ground penetration routes to the base, constantly patrolled and mined. Impassable for most of the year due to snowdrifts.
- Snowy Plains: Open spaces at the foot of the mountains, used for testing equipment in extreme weather conditions and launching reconnaissance drones.
