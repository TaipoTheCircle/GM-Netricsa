ALTERNATIVE OVERWATCH COMBINE SHOTGUNNER


CLASS:      Alternative Overwatch Combine Shotgunner
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Close-Combat Assault Unit
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    Spas-12 Combat Shotgun, Grenades
REWARD:     700 FC
THREAT:     High

DESCRIPTION:

The Alternative Overwatch Combine Shotgunner is a specialized Combine Soldier designed to clear rooms, tunnels, and dense urban environments. Unlike standard elite units, he wears a swamp-colored uniform designed for camouflage in industrial zones, ruins, and contaminated areas. His visor glows orange.

This type of soldier isn't designed for long-range firefights. His job is simple and extremely frustrating: quickly close the distance and turn everything in front of him into a cloud of debris and flesh. Augmentations enhance his reflexes and pain tolerance, allowing him to move forward even under heavy fire.

TIPS:

- At close range, a shotgun is deadly.
- In narrow corridors, it's better to retreat than to test how many shots you can take.
- The orange visor is highly visible in the dark - sometimes it's the only chance to see him first.