REWARD:     300 FC
THREAT:     Absolute

DESCRIPTION:

The star of Broadside Animation and the face of the company. A large brown beaver with a permanent smile and two large front teeth. He was created by Rex Broadside after he accidentally lost the rights to his first character, Blot the Rabbit. Inspiration came when his mother brought him a waffle that reminded him of a beaver tail.

Early Bucky cartoons were dark and violent-each episode featured the beaver's death at the end, which fueled his hatred for his creator. Later, the character was softened, and friends were added: Olivia the Otter, Giovanni the Goose, and Walter the Walrus's brother. Bucky became a fan favorite, his face recognizable to millions, and his licensed merchandise filled store shelves.

Brandon Lester was an actor hired to wear the Bucky costume at the Broadside Beach theme park. He wanted the job and apparently got it, taking it from another, more cheerful applicant. But one day, Brandon died of a fentanyl overdose.

Instead of burying his body, Broadside Animation used it for a grisly experiment. Brandon's body was fused with mysterious flesh from the caves of Nulla Terra, creating "Starling"-a living Bucky costume that didn't require an actor inside. This was the first public Starling.

After his transformation, Brandon began killing.

His Victims:

Olivia Finch (Olivia the Otter): Brandon drowned her in the lake near the employee housing complex. Her body was later used to create Olivia's Starling.

Gary Wilson (Giovanni the Goose): Brandon smashed his face into a wall and then shoved his head into a running deep fryer. Gary became Giovanni's Starling.

Nathan Stewart (Walter the Walrus): Brandon bludgeoned him to death with a wooden chair in the Broadside Animation offices. Nathan became Walter's Starling.

