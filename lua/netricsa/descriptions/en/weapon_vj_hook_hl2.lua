HARPOON


WEAPOON: Harpoon
AMMO:    None
ROF:     ~1 throwing action/second
WEIGHT:  3,5 kg

DESCRIPTION:

The harpoon is a long throwing melee weapon used to engage targets at close to medium range. It is a durable iron rod with a sharpened tip, designed to pierce dense materials and create a stunning effect upon impact.

When thrown, the harpoon pierces the target and firmly embeds itself in the surface, locking the enemy in place. It is effective against single opponents and can immobilize them.

NOTES:

- Can be used as an improvised throwing weapon in the absence of firearms.
- Upon impact, it firmly immobilizes the target, making further resistance difficult.
- Can be combined with other melee weapons for close combat.