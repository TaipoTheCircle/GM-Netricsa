BLACK MESA BLUE ALIEN RESEARCH LAB, A CHOICE WITHOUT A CORRECT ANSWER


Sam, we're inside. Thanks to the scientists. Those they found, coaxed, and put us through gunfire, explosions, and their own panic attacks. They led us to the blue entrance, opened the doors... and hit a wall. Because they know where the exit is. Roughly. But they don't know how to get there. And those who know for sure have locked themselves in on the other side. In the blue lab. In the center. Surrounded by H.E.C.U.

And now the irony: the scientists we need have locked themselves in. Not from us and the Marines specifically, but from everyone. They slammed the door on the side where we should be, and they themselves were stuck on the side where the exit is. But the problem is, they can't open the door from the inside. It's locked. By someone. Maybe it's the security system. Maybe it was accidental. Or maybe they locked it themselves when they heard the gunfire, and now they're sitting there, afraid. Basically, it's a classic.

Now we have two options. Both are tricky. Both are life-threatening. Both are pretty bad. But there's no choice.

Option one: press the button.

Somewhere on the bridge, right within the Marines' line of sight, hangs the coveted button. The one that will open the door. It's easy to press. The problem is, you have to walk to it. And the Marine guarding it isn't sleeping, isn't smoking, and isn't looking the other way. He's standing there. Watching. And ready to shoot anyone who comes near.

Pros: fast. Cons: noise, gunfire. But the door will open, the scientists will be able to get out, and we'll move on.

Option two: crawl through the sewers and vents.

A detour. Jump down, into the water. Cold, murky, with unknown contents. Swim through the sewers, find the ladder, climb up, enter the ventilation system, and then through the grate into the room with the scientists.

Problem: the door still can't be opened from the inside. You need to press the emergency button. The one that sets off the sirens. And it opens everything that was locked. Including two capsules with alien infantry, which are stationed right in this room.

So: we press the button, the door opens, the sirens wail, the capsules hiss, the infantry emerge, and the scientists find themselves face to face with them.

Pros: quiet, unnoticed, no unnecessary corpses (for now). Cons: very high risk for the scientists. They're not fighters. They're targets.

What exactly should we do?

Sam, I've calculated both options. The scientists' chances of survival in the first option are about 70%, if we quickly eliminate the Marine and take them. In the second, it's about 40%, because the Marines will be closer to them than we are.

But the first option is a direct fight with the H.E.C.U. The second is almost guaranteed death for the scientists if we don't make it in time. I'd choose the first. You like to shoot. I like it when we don't risk the people we're saving.
