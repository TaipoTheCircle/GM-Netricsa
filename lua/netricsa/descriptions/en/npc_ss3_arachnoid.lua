ARACHNOID, HATCHLING FROM THE SCORPIUS


CLASS:      Arachnoid, Hatchling
METABOLISM: Ectotherm, arthropod
PERCEPTION: Primitive intelligent race
SIZE:       8 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Machine gun, tail stinger
REWARD:     1000 FC
THREAT:     Average

DESCRIPTION:

A holy hatchling of a primitive race descended from arthropods. Extremely dangerous due to its religious beliefs.

A young, not fully formed arachnoid from the planet Scorpius, who worship Mental as a messiah. It has a lighter and less durable exoskeleton than adults. It is armed with a light machine gun that replaces one of its limbs.

This weapon boasts high accuracy and unlimited ammunition thanks to a techno-magical regenerator, but it's prone to overheating after a long burst, giving enemies time to attack. It can also attack with a poisonous stinger in close combat. It's a mass-producing infantry unit in Mental's army, compensating for its low survivability with its numbers and dense firepower.

TIPS:

- The stinger on its tail is dangerous - don't get too close.
- When enraged, it will shoot even through its own.
- After a long burst, it stops firing for a second to cool down - the perfect moment for a counterattack.
- Use a sniper rifle, assault rifle, or rocket launcher.