FURNACE METAL SONIC FROM THE MOBIUS


CLASS:      Furnace Metal Sonic
METABOLISM: Mechanical Killer Robot
PERCEPTION: Destruction and cooking machine / Eggman's personal chef
SIZE:       3,3 ft
ENDURANCE:  Very high
HOSTILITY:  Absolute
WEAPONS:    Built-in chest furnace, fireballs, claws, super speed
REWARD:     10000 FC
THREAT:     Absolute

DESCRIPTION:

This is Metal Sonic, repurposed for his own cannibalistic purposes by the mad Doctor Eggman, known as Hungry. Instead of simply defeating Sonic, Furnace must capture him, bake him in the furnace within his chest, and serve him to Eggman for dinner.

Furnace is a cold-blooded and calculating killer. His primary goal is to obey his creator's orders, and he approaches this with the utmost seriousness. He's not just a mindless machine; he's capable of analyzing his prey's behavior, spotting their weaknesses, and using them against them. In one scenario, Furnace even fakes his own breakdown to lull Sonic into a false sense of security before the decisive attack.

Visually, Furnace differs from classic Metal Sonic. He appears more industrial and utilitarian, constructed from rough layers of metal and bolts. The most striking feature is his chest, which is a veritable furnace with a grate containing a raging fire. Unlike Metal Sonic, he doesn't try to prove his superiority in a race; he simply wants to kill.

He's a formidable opponent, capable of fighting Sonic on equal terms. His arsenal includes:

Furnace: His primary weapon. Can open to trap a victim inside or emit powerful jets of flame.

Super Speed: Capable of reaching speeds fast enough to chase a running Sonic.

Fireballs: Can fire gouts of flame over a distance.

Sharp Claws: Used in close combat.

Physical Strength: Much stronger than regular Metal Sonic.

Durability: Highly resilient and able to withstand powerful attacks.

TIPS:

- Never underestimate him. He's intelligent and can predict your moves.
- They say Furnace is capable of destroying even his creator if the circumstances require it. Always keep that in mind.
