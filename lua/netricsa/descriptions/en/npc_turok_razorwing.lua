RAZOR WING FROM THE TERRAFORMED PLANET


CLASS:      Razor Wing
METABOLISM: Insectoid
PERCEPTION: Predatory flying insect
SIZE:       5,9 ft
ENDURANCE:  Very low
HOSTILITY:  Extremely high
WEAPONS:    Acid spit, sting, speed
REWARD:     150 FC
THREAT:     High

DESCRIPTION:

Razor Wings are what you get if you cross a dragonfly with a scorpion, add a couple extra legs, and unleash the whole thing on a volcanic continent.

Razor Wings once lived deep underground until a volcanic eruption pushed their hive out of existence. Since then, these creatures have regularly caused problems for anyone who ventures into their nesting area.

In combat, Razor Wings rely on speed and numbers. They fly through the air faster than a human can run on the ground, and attack in a swarm.

Their primary weapon is acid spit. The acid not only eats away at flesh but also slows movement.

Razor Wings themselves die from one shot from any weapon, but the problem is that there are always a lot of them. A lot.

TIPS:

- One shot, one monster. Don't save ammo, but don't panic.
- Rapid-fire weapons are the best solution to the swarm problem.
- If a Razor Wing spits, dodge. The acid sticks and slows you down.
- Don't let yourself get surrounded. In an open field, a swarm will knock you down in a couple of seconds.
