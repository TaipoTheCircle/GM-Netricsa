COMBINE OVERWATCH SUPERIOR SNIPER


CLASS:      Combine Overwatch Superior Sniper
METABOLISM: Augmented Human, Male
PERCEPTION: Long-range reconnaissance and strike unit
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Combine Phase-Action Scoped Precision Sniper Rifle
REWARD:     700 FC
THREAT:     Very High

DESCRIPTION:

The Overwatch Superior Sniper is an experimental version of the standard Combine Sniper, designed for maximum effectiveness at all ranges. His white helmet is equipped with two blue optic visors located in the center. The first visor is optimized for close combat and urban ranges, while the second is for ultra-long-range observation and shooting at extreme distances.

The soldier's armor is lightweight, blue and white, which reduces protection but increases mobility and speed. The Combine believes that the best sniper is the one who can't be shot at in time.

The weapon's main feature is its energy aiming beam. In normal mode, it is blue, but a split second before firing, it changes to red, signaling the critical targeting phase.

TIPS:

- If the beam turns red, change position immediately.
- It has weak armor, but perfect accuracy.
- Never stand in open areas if it's nearby.
