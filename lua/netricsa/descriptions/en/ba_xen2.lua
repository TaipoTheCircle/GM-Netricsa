THE FRONTIER WORLD OF XEN, THE CAVES OF XEN


Xen decided to reveal its inner workings. Not metaphorically. Literally.

Caves. Labyrinths. Stone corridors that look like they were designed by someone who genuinely hated straight lines and visitors.

It's full of Xen creatures. They move, breathe, spit, and generally consider you an intrusion on the landscape. A horde of Mental wanders among them, confidently exploring this alien world, as if Xen had always been part of the occupation. Two kinds of chaos colliding in a confined space. The result is predictable: everyone shoots.

The caves stretch inward and outward, confusing landmarks and thoughts. It's easy to get lost here. Even easier to stumble upon something that would prefer you not to. It seems Xen himself doesn't fully remember where his tunnels lead. He just keeps building them.

And yes, there's another cave underwater. Hidden. Quiet. The Chumtoads live there. Creatures you usually discover after the fact.

So be careful, Sam.