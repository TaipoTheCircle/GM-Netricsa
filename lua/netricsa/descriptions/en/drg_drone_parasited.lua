SWARM CARRIER DRONE


CLASS:      Swarm Carrier Drone
METABOLISM: Xenobiological
PERCEPTION: Assault Specimen
SIZE:       6,5 ft
ENDURANCE:  Average
HOSTILITY:  Very High
WEAPONS:    Claws, Symbiote Parasite
REWARD:     120 FC
THREAT:     High

DESCRIPTION:

The Swarm Carrier Drone is a rare red variant of the common drone, distinguished by the presence of an adult parasite firmly attached to its upper body. Unlike most known cases of parasitism, the two organisms do not harm each other, acting as a single combat creature.

During combat, the Swarm Carrier behaves like a common drone, rapidly closing the distance and attacking its prey with long, sickle-shaped claws. The parasite remains motionless, awaiting the death of its host.

After the drone's death, the parasite immediately detaches from the body and attacks the nearest living creature, attempting to complete its life cycle in the manner typical of its species. Thus, the destruction of the host at close range is often only the beginning of a new threat.

The origin of this symbiosis remains unknown. Some xenobiologists consider it a natural developmental stage of individual drones, while others believe that the parasites use adult bees solely as a protected means of transport to potential prey. In any case, this cooperation significantly increases the survival rate of both organisms.

TIPS:

- Do not approach the host after its death.
- After killing the drone, immediately eliminate the separated parasite.
- Automatic weapons allow you to quickly eliminate both threats.
- If possible, destroy the host in an open area to make it more difficult for the parasite to close the distance.
