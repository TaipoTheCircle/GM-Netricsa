COMBINE OVERWATCH ELITE TACTICAL SOLDIER


CLASS:      Combine Overwatch Elite Tactical Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: The Combine's highest-ranking tactical strike force
SIZE:       5,9 ft
ENDURANCE:  Extremely high
HOSTILITY:  Absolute 
WEAPONS:    AR2 Pulse Rifle, Grenades
REWARD:     1000 FC
THREAT:     Critical

DESCRIPTION:

The Combine Overwatch Elite Tactical Soldier is the pinnacle of Overwatch's ground forces. He wears the standard white Elite Soldier suit, topped with a black, reinforced flak jacket. This combination makes the soldier virtually invulnerable to small arms fire and completely eliminates vulnerability to headshots: headshots are ineffective under any circumstances.

The Red Visor isn't just a optic, but a combat interface linked to the Combine's central protocols. It tracks targets, predicts trajectories, and coordinates actions with other elite units. One glance from such a soldier feels like a predetermined sentence.

Unlike standard elite soldiers, the Tactical variant doesn't rely on surprise or demonstrative aggression. It methodically advances forward, controlling space, suppressing resistance with precise AR2 pulse fire, and clearing areas with grenades. Its movements are unhurried, and decisions are made faster than the enemy can even recognize the threat.

TIPS:

- Forget about precision headshots-it's useless.
- Use heavy weapons, explosives, and traps.