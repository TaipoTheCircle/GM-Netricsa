COMBINE OVERWATCH ALTERNATIVE SOLDIER


CLASS:      Combine Overwatch Alt Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Standard Combine Infantry
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, SMG
REWARD:     600 FC
THREAT:     High

DESCRIPTION:

The Combine Overwatch Alt Soldier is a modified version of the standard Overwatch soldier, designed for operations in environments where the units' blue armor is a disadvantage. His outfit combines dark camouflage with a black torso and sleeve protectors, making his silhouette less visible in industrial zones, nighttime streets, and indoor environments.

TIPS:

- Works strictly in a group and rarely fails alone.
- The AR2 is dangerous even at medium range, so avoid standing in the open.
- The blue visor is visible in the dark.
