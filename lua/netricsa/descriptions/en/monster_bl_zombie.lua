GINGERBREAD MAN FROM THE SWEET ISLANDS OF THE BIG LOLLY


CLASS:       Sweet Island Gingerbread Man
METABOLISM:  Living Edible Construct
PERCEPTION:  Protector and native of the Sweet Islands
SIZE:        5,9 ft
ENDURANCE:   Average
HOSTILITY:   High
WEAPONS:     Close combat (punches and body blows)
REWARD:      450 FC
THREAT:      Average

DESCRIPTION:

The Sweet Island Gingerbread Man is one of the main defenders of this cloying but deadly land. His body is made of a dense gingerbread material, giving the deceptive impression of softness and plumpness. In reality, he is much stronger than he appears.

In appearance, he resembles a living Gingerbread Man: a round head with large, round eyes, a red nose, a permanently frozen smile, and three red buttons on his stomach. A white sugar border runs along the edge of his "clothing," and round red elements resembling candy inserts are located on his arms and legs. His limbs appear soft, but when struck, they act like elastic clubs.

The Gingerbread Man prefers close combat. He tries to close the distance and bring his "fists" down on his opponent, using his body mass and springy structure.

Despite his "friendly" appearance, he is a dedicated defender of his territory.

TIPS:

- Don't be fooled by appearances - blows hurt.
- Attacking from a distance is more effective.
- In close combat, he quickly sets the pace.
- His smile doesn't indicate friendliness.