RESISTANCE REFUGEE


CLASS:      Resistance Refugee
METABOLISM: Human
PERCEPTION: Civilian / Displaced Survivor
SIZE:       5,9 ft
ENDURANCE:  Low
HOSTILITY:  Low
WEAPONS:    Improvised
REWARD:     100 FC
THREAT:     Very Low

DESCRIPTION:

Refugees are ordinary civilians who have fled Alliance occupation zones. Untrained, they survive by scavenging for food and medicine. Many are families and elderly people forced to navigate war-torn cities under constant threat from Alliance patrols and synths.

While not combatants, refugees can provide valuable information on enemy movements or terrain features. They often travel in groups for safety and may be guarded or accompanied by Resistance fighters. Their morale is fragile, and witnessing violence can lead to panic or disorientation.

TIPS:

- Protect refugees whenever possible - they are vulnerable but critical to missions.
- They can follow you or Resistance units, creating a temporary threat of crowd control for enemies.
- Refugees sometimes carry small supplies or knowledge of hidden, safe routes.
