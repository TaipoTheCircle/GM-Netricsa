VELOCIRAPTOR VULGARIS FROM THE M'DIGBO


CLASS:      Velociraptor Vulgaris
METABOLISM: Reptile
PERCEPTION: Mount / Combat Companion
SIZE:       5 ft
ENDURANCE:  Very Low
HOSTILITY:  Varies With Rider
WEAPONS:    Fireballs
REWARD:     200 FC
THREAT:     High

DESCRIPTION:

This green alien lizard looks like it was pulled straight out of the 1993 Jurassic Park movie without the feathers. It lives on the planet M'Digbo and, unlike its Earthly brethren, can breathe fire.

The Velociraptor's appearance resembles a classic pop culture dromaeosaurid: green scales, sharp claws, a long tail, and a mouth full of tiny teeth. No feathers, no pack intelligence-just pure, unadulterated fury, harnessed and directed at Mental's enemies.

In combat, the Velociraptor is indispensable. It runs fast, dodges enemy attacks, and fires fireballs from its mouth. One such fireball kills a weak enemy in one hit, and stronger ones after a couple of seconds of continuous fire. The main problem is its health. The Velociraptor is as fragile as glass. A couple of hits and the riding lizard is gone.

TIPS:

- While riding, keep your distance and fire volleys of fireballs at enemies.
- Its health is low. If you see someone shooting at you, dodge. One hit can send a lizard to the other world.
- Don't try to kill enemies up close with a regular lizard-it'll die faster than you can deal damage.
