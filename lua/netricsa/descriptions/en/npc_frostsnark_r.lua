FROST SNARK FROM THE XEN


CLASS:      Frost Snark
METABOLISM: Carnivorous Insect (Cryogenic Mutation)
PERCEPTION: Alien Insect, Living Ice
SIZE:       6 inches
ENDURANCE:  Low
HOSTILITY:  Absurdly High
WEAPONS:    Frost Bite
REWARD:     100 FC
THREAT:     High

DESCRIPTION:

This is a natural mutation of the common snark, which originated in the icy regions of Xen. Where the winds howl so hard that they chill the bones and the ice lasts for thousands of years, common snarks cannot survive. But evolution has found a way: those that survive have evolved into blue insects whose shells glow from within with a cold, icy light.

Unlike their brethren, who explode with overflowing enthusiasm seconds after hatching, frost snarks are not susceptible to self-destruction. They can live a long time. A very long time. And all that time, they will try to freeze you. Or devour you. Or both.

They wear a completely blue shell with bluish veins that pulsate like ice deep within a glacier. From a distance, they sparkle and reflect light, making them easy to spot. But as soon as you get closer, they disappear from view, blending in with the snow and ice.

A frost snark's bite isn't just painful-it freezes its target. One hit, and you begin to slow down. Two, and you can no longer run. Three, and you simply stand there, a helpless statue of flesh and ice, until the pack finishes you off. Survivors say these creatures deliberately attack in groups to inflict a freezing effect on each other, paralyzing their victims before they can fire.

Like their fire-type neighbors, frost snarks are constantly at odds with frost bullsquids. The conflict lasts as long as with their fire counterparts. No one remembers the cause. Perhaps the bullsquids stepped on someone's favorite ice floe. Perhaps the snarks ate someone's stash of crystals. Or perhaps they simply dislike each other because of their shell color.

When dying, a frost snark doesn't simply die. It disintegrates into chunks of ice, leaving behind a puddle of frozen water that lasts for a long time.

TIPS:

- Don't get too close to a frost snark. It doesn't explode like a normal snark, but its bite freezes.
- You can spot them from a distance by the glint they produce. Use a sniper rifle.
- They're doubly dangerous at close range. Keep your distance.
- Frost Snarks don't die on their own.
- If you see a pack, use explosives. They're not afraid of the cold, but an explosion is a different matter.
- Frost Snarks and Frost Bullsquids hate each other. If possible, set them against each other. Let them kill each other.
- Thermal grenades work better against them than regular ones. Heat and cold are bad bedfellows.
