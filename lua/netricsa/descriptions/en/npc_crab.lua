CRAB FROM THE ZENOZOIK 


CLASS:      Crab
METABOLISM: Arthropod, xenomorph
PERCEPTION: Basic vision, chemoreception
SIZE:       ~1,3 ft
ENDURANCE:  Low
HOSTILITY:  Medium
WEAPONS:    Organic projectiles (ejected clumps of biomass or mineralized slime)
REWARD:     25 FC
THREAT:     Low

DESCRIPTION:

A coastal arthropod that inhabits Zenozoik water bodies-ponds, lakes, and beaches. It has a blue carapace and bright red limbs. It is territorial, attacking any creature it perceives as a threat with organic projectiles. Its attack consists of ejecting a viscous substance with enough force to cause damage at medium range. They aren't particularly mobile in combat, preferring to fire from a single position. They're often found in groups of 2-4, which can be problematic on narrow paths near water.

TIPS:

- Avoid the corrosive acid spewed by the Crabs.
- A crossbow or any ranged weapon is effective against them. Kicks are useful in close combat.