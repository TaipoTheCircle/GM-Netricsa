COMBINE CIVIL PROTECTION OFFICER


CLASS:      Combine Civil Protection Officer
METABOLISM: Humans
PERCEPTION: Civil Protection Command Staff
SIZE:       6 ft
ENDURANCE:  Medium
HOSTILITY:  Medium
WEAPONS:    Pistol, Revolver, SMG, AR2, SPAS-12, OICW
REWARD:     350 FC
THREAT:     Medium

DESCRIPTION:

Civil Protection Officers are the link between street cops and loyalists. Unlike regular Civil Protection soldiers, they avoid gas masks, preferring to keep their faces open.

Their appearance varies. Some wear standard Civil Protection armor, while others prefer berets, insignia, shoulder pads, and other trinkets that emphasize rank and personal privileges. The uniform is less standardized, but it perfectly emphasizes the difference between those who give orders and those who receive Stun-Stick charges.

Officers are formally obligated to maintain order, coordinate patrols, and report directly to Combine structures. Informally, however, things are more complicated. Some, using their access to information and communications, secretly transmit data to the Resistance.

In combat, a Civil Protection officer is more dangerous than a regular metrocop thanks to their experience and composure, but they are still human without significant augmentations.

TIPS:

- Don't underestimate their experience, even if they look too "civilian."
- Some officers know more than they let on.