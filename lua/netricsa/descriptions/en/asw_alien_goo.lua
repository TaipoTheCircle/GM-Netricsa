SWARM BIOMASS


CLASS:      Swarm Biomass
METABOLISM: Xenobiological
PERCEPTION: Organic Swarm Structure
SIZE:       Variable
ENDURANCE:  Virtually Invulnerable
HOSTILITY:  Passive
WEAPONS:    Corrosive Tissues, Defensive Pheromones
REWARD:     150 FC
THREAT:     Medium

DESCRIPTION:

Biomass is the living organic tissue from which the Swarm constructs Swarms, tunnels, and other large structures. It gradually expands, covering the walls, floors, and ceilings with a dense layer of fibrous flesh, turning the surrounding environment into an extension of the Swarm itself.

It is believed that Biomass processes any available organic materials to grow. Extinct plants, animals, and even intelligent beings can become building material for new sections of the Swarm, forever losing their original form.

Despite their inability to move independently, Biomass remains dangerous. Its surface is covered in caustic secretions that cause chemical burns upon contact. Furthermore, some varieties are capable of secreting special pheromones that, when damaged or burned, attract nearby Swarm members to defend the infested area.

Conventional weaponry causes virtually no damage to Biomass. Its tissue is resistant to kinetic munitions, explosions, and most energy weapons. The most effective method of destruction remains prolonged exposure to high temperatures, which literally burns away the organic structure.

The appearance of Biomass is considered one of the first signs of the formation of a full-fledged Swarm. If the infestation is not stopped in time, its defenders will soon follow the immobile flesh.

TIPS:

- Burn Biomass with flamethrowers or other incendiary weapons.
- Don't touch its surface unless necessary.
- Be prepared for a Swarm to appear after destroying large clusters.
- The more Biomass there is, the higher the likelihood that a Swarm core is located nearby.
