COMBINE OVERWATCH MELEE SOLDIER


CLASS:      Combine Overwatch Melee Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Melee Punisher
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Very High
WEAPONS:    Modified Stun Baton
REWARD:     400 FC
THREAT:     Average

DESCRIPTION:

The Combine Overwatch Melee Soldier is a rare but somewhat nasty Combine unit, designed for situations where shooting is inappropriate or impossible. He fights exclusively in close combat, relying on speed, aggression, and a modified stun baton capable of stunning, breaking bones, and crippling targets.

This soldier's armor is deliberately simplified and practically nonexistent. The Combine sacrificed portable radio mobility: the soldier is faster than standard soldiers, easily covers short distances, and can quickly engage other soldiers. A white helmet with a blue visor covers the face.

These soldiers were most often used in prisons, narrow corridors, and riot suppression. Their task is simple: reach the target and beat them.

Despite their weak armor, melee soldiers are deadly in close combat.

TIPS:

- Don't let him get too close-his stun baton will quickly incapacitate him.
- Use distance and firearms.
- Vulnerable due to armor plating.
- In tight spaces, he's more dangerous than standard soldiers.