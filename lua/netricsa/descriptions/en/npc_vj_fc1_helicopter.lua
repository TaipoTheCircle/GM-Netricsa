UH-60 "BLACKHAWK" PATROL HELICOPTER FROM KRIEGER ARCHIPELAGO


CLASS:       Krieger Mercenary UH-60 "Blackhawk" Patrol Helicopter
METABOLISM:  Aircraft (HP, Jet)
PERCEPTION:  Enemy Aircraft
SIZE:        ~16,7 ft
ENDURANCE:   High
HOSTILITY:   Medium-High
WEAPONATORY: Onboard minigun controlled by a gunner
REWARD:      1000 FC
THREAT:      High

DESCRIPTION:

The primary multi-role helicopter of the Krieger forces. Used for patrols, rapid reinforcement deployment, and close fire support. It has a standard camouflage pattern with the company logo. The crew consists of a pilot, a gunner operating an onboard minigun, and up to three paratroopers. When detected, the helicopter begins an active search and attack, acting as a mobile firing point and troop landing platform.

TIPS:

- Pilot and gunner. Killing the pilot causes the helicopter to crash. Killing the gunner deprives them of their primary weapon, after which the helicopter usually retreats.
- Underbarrel grenade launcher shots and accurate sniper rifle shots into the cockpit are the most effective.
- The helicopter cannot pursue under rooftops.
