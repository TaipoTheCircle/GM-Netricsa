MONOLOPHOSAURUS FROM THE TERRAFORMED PLANET


CLASS:      Monolophosaurus
METABOLISM: Carnivore, Theropod
PERCEPTION: Ambush predator
SIZE:       ~8,2 ft
ENDURANCE:  High
HOSTILITY:  Extremely high
WEAPONS:    Jaws, claws, single crest on head
REWARD:     1000 FC
THREAT:     High

DESCRIPTION:

The Monolophosaurus resembles a smaller version of the Dilophosaurus, but with one key difference: instead of two crests on its head, it has a single crest.

Hence the name "single-crested lizard". Otherwise, it is a typical medium-sized predator, lean, fast, and, judging by its family relationships, more advanced than primitive Dilophosaurus.

TIPS:

- There's not much to recommend.
- Don't let them get close, hit them with explosives, don't let them kill you.
