MAGNOR


PLANET:       Magnor
TYPE:         Terrestrial planet, World of giant flora
CLIMATE:      Moderately rainy, warm year-round
MOONS:        Unknown
TERRAIN:      Giant swamps, forests with giant trees, valleys, clearings
LOCATION:     Unknown star system
NATIVE RACES: Zixie, Giants
ALLIANCE:     Independent world
LOCATIONS:    Lowland swamps, Abandoned base, Vetvetia city, Giant cartoon-like terrain

DESCRIPTION:

A unique planet where all life has reached colossal proportions. Gigantic swamps with putrid water, forests with trees soaring to the sky and obscuring the light with their crowns, grass as tall as multi-story buildings, enormous mushrooms and insects—every landscape element here is overwhelming in its scale.

The planet is believed to be a partially formed gas giant, but natural terraforming has made it habitable. The climate is similar to Earth's, but the air density is lower, allowing giant life forms to exist without being crushed by their own weight.

The low-lying swamps of Magnor are the most inhospitable place on the planet. The water here smells of putrefaction, and animals regularly get stuck in the quagmire. Rare patrols comb these areas in search of lost travelers, risking becoming prey themselves to the giant predators lurking in the dark water.

In contrast to the swamps, Magnor's forests are teeming with life. The local Zixie race, significantly shorter than the giant flora, have learned to exploit it for their own ends. Their main city, Vetvetia, is located high above the ground, almost at the very canopy of the trees. The inhabitants hunt the creatures that live in the branches and collect rainwater. This way of life allows them to avoid the dangers lurking below and enjoy the mild, warm climate.

However, the most mysterious area of ​​Magnor is the so-called Giant Cartoon Terrain. Here, scattered among the forests and clearings are objects of clearly man-made origin, but of monstrous size: tables, chairs, knives, bottles, wrenches, soda cans, and even car tires. Who created these artifacts and why remains a mystery, raising many questions and providing no answers.

KEY LOCATIONS:

- Lowland Marshes: A vast swampy area with rotting water and decomposing animal carcasses. An extremely dangerous area patrolled to search for survivors.
- Abandoned Base: An abandoned settlement deep in the swamps. The reason for its abandonment is the stifling stench, dampness, and constant threat of attack by giant creatures.
- Vetvetia City: A settlement of Zixie, built high in the branches of giant trees. It is the primary defense against ground-level dangers and the center of civilization on the planet.
- Gigantic Cartoon Terrain: An anomalous zone littered with enormous household items of unknown origin.
