SWARM SHIELDBUG


CLASS:      Swarm Shieldbug
METABOLISM: Xenobiological
PERCEPTION: Heavy Assault Organism
SIZE:       9,8 ft
ENDURANCE:  Very High
HOSTILITY:  High
WEAPONS:    Armored Limbs, Ramming Strike
REWARD:     1000 FC
THREAT:     Very High

DESCRIPTION:

The Shieldbug is one of the most heavily armored Swarm combat forms. The front of its body is covered in massive chitinous plates capable of withstanding colossal damage. This allows the creature to advance under heavy fire, covering less durable Swarm members.

The Shieldbug's main weapon is its enlarged forelimbs. Using them as a living battering ram, it delivers powerful blows capable of knocking opponents back a considerable distance. There have been cases where victims have died not from the blow itself, but from subsequent collisions with surrounding objects or falls from great heights.

Despite its virtually impenetrable frontal armor, the body has vulnerable areas. The least protected area remains the rear of the body, where the main life-support organs are located. For this reason, the Shieldfoot instinctively tries to keep its opponent in front of it and is extremely reluctant to turn during combat.

Unlike most members of the Swarm, the Shieldfoot rarely acts chaotically. Once it has located a target, it can pursue it methodically for a long time, ignoring less dangerous opponents and concentrating on its chosen prey.

TIPS:

- Don't waste ammunition on frontal armor.
- Try to attack from the flanks or rear.
- Don't let Shieldfoot drive you into precipices and narrow passages.
- If a creature the size of a transport module decides to follow you, that's no reason to be proud.
