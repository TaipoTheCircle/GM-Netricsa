DESCRIPTION:

The Civil Protection Rebel is a rare but valuable traitor from the Metropolitan Police. Unlike active CP operatives, he does not wear a respirator mask, which immediately distinguishes him from others and emphasizes his break with the Combine - both symbolic and practical. His face is open, and his voice is unfiltered. Many of them defect to the Resistance not out of heroism, but out of fatigue, fear, or a belated realization of their role in the oppressive system.

