COMBINE ELITE SUPER SOLDIER


CLASS:      Synth Elite Heavy Assault Unit
METABOLISM: Synth
PERCEPTION: Tactical sensors + integrated tracking systems
HEIGHT:     13 ft
ENDURANCE:  Very high
HOSTILITY:  Extreme
WEAPONS:    Primarily heavy weapons / integrated missiles, sometimes cannons
REWARD:     1500 FC
THREAT:     Critical

DESCRIPTION:

The Combine Elite Super Soldier is a combat synth unit, originally designed as an elite infantry trooper.

Essentially a bipedal creature with massive armor and an emphasis on close-quarters attack.

TIPS:

- It's best not to argue with such a colossus.
- Use RPG V2, it will take 2-3 perfect hits to completely eliminate him.