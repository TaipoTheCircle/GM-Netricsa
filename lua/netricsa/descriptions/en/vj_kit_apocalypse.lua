KIT BODEGA


CLASS:      Kit Bodega
METABOLISM: Anthropomorphic Cat, Cyborg
PERCEPTION: Member of Team "Support"
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Variable
WEAPONS:    Claws, Blaster, PDA, Kibble Star Star Cannon
REWARD:     200 FC
THREAT:     Variable

DESCRIPTION:

She is an anthropomorphic cat with beige fur, brown hair, pink eyes, and distinctive yellow-framed glasses with red lenses.

She wears a yellow jumpsuit with a white zigzag stripe, complemented by black underarmor, thick bracers, and greaves with greaves reminiscent of cat paws. Her full name translates as "Equipment Warehouse" (Kit - kit, Bodega - warehouse), reflecting her ability to materialize objects.

Kit hails from a game world inhabited by anthropomorphic cats, where she and her partner, Kaboodle, were the main characters. They swore to protect their home village from the villains Malice and Mayhem, but as a result of their victory, the world was destroyed by a cataclysm, killing everyone except Kit and Kaboodle. At the last moment, the mysterious Twilight saved them, recruiting them into the "Support" team.

Now Kit and Kaboodle's task is to travel through different worlds and prevent the local heroes from defeating the villains. The irony of fate: they are forced to play the role of villains to save worlds from the destruction that inevitably follows the hero's victory. Traveling with them are the rescued heroes of other worlds: Gobbles (a dinosaur) and Flappers (a dolphin).

Kit is distinguished by her courage, optimism, and resourcefulness. The destruction of her homeworld left her with deep psychological trauma: any world resembling her homeworld evokes memories of her village's destruction, sometimes pushing her to act rashly. She blames herself for her inability to control her emotions, but this very same emotion gives her the energy to save those who can still be saved.

In battle, Kit uses a blaster, a PDA to change her appearance, and in a critical situation, she combines with Kaboodle to form the "Kibble Star" armor, becoming lightning fast and gaining access to the Kibble Star Star Cannon -a beam of destructive power.

TIPS:

- The world will be destroyed after the hero's victory, not after defeat. Remember this when choosing a side.
- Protect her, for the fate of the worlds lies with her.
