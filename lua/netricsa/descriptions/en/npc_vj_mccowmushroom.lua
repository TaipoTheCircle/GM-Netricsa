MOOSHROOM COW MINECRAFT OVERWORLD


CLASS:      Wild / Fungal Animal
METABOLISM: Warm-blooded Mammal
PERCEPTION: Standard Vision + Smell
SIZE:       5,2 ft
ENDURANCE:  Average
HOSTILITY:  Low
WEAPONS:    None
REWARD:     150 FC
THREAT:     Low

DESCRIPTION:

The Mooshroom Cow is an unusual species of Mushroom Biome creature, a fusion of organic cow flesh and a fungal ecosystem. Mushrooms grow on its back, and its body is covered in spotted cowhide.

TIPS:

- Mushroom soups are a quick way to replenish health and satiety.
- After cutting the Mushrooms, she will turn into an ordinary Cow.