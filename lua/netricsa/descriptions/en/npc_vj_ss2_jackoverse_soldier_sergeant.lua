STORMTROOPER SERGEANT FROM THE KAMINO


CLASS:      Stormtrooper Sergeant
METABOLISM: Cybernetic Human
PERCEPTION: Elite commander, Darth Vader's Left Hand
SIZE:       6,5 ft
ENDURANCE:  High
HOSTILITY:  Absolute
WEAPONS:    Minigun, grenades, RPG, Laser Cannon, Experimental Systems, Energy Shield, Robots
REWARD:     10000 FC
THREAT:     Absolute

DESCRIPTION:

This elite cybernetic warrior serves as Darth Vader's trusted aide. He combines the talents of a field officer, a highly skilled engineer, and a mercenary. He personally designs experimental weaponry, which he calls his "toys".

His powerful, athletic build is enhanced by massive, dark-metal power armor. The angular plates, spikes, and movable segments of the armor not only provide tremendous protection but also serve as excellent camouflage at night. The Sergeant's identity remains a mystery: his face is securely concealed beneath a high-tech helmet with a transparent visor, equipped with additional sensor modules.

The Sergeant's arsenal is impressive in its destructive potential. His primary weapons are a multi-barreled minigun and powerful assault systems, supplemented by grenades and RPGs. In combat, he displays unbridled aggression, commanding infantry squads and deploying his own robots to the front lines. His technological superiority is emphasized by the use of advanced energy shields and a number of secret systems, making him an unpredictable opponent.

He's officially listed as "Vader's Left Hand", but half of his upgrades were installed illegally.

His helmet has never been removed in public-even medics can only see it through sensors.

TIPS:

- Don't try to attack the Sergeant head-on. His robots and minigun will turn you into a sieve.
- The Sergeant coordinates the stormtrooper squads.
- Don't get close to his "toys"-his self-assembled combat robots.
- Mobility is key in a duel with him. He's slow due to his heavy armor, but his weapons cover a wide area.
