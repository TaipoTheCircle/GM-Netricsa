BUG SOLDIER FROM THE TERRAFORMED PLANET


CLASS:      Bug Soldier
METABOLISM: Insect
PERCEPTION: Subterranean ambush predator
SIZE:       Large
ENDURANCE:  Average
HOSTILITY:  Extremely high
WEAPONSL:   Pincers, stinger, toxin
REWARD:     300
THREAT:     High

DESCRIPTION:

These creatures resemble a larger-than-life scorpion, only with a more vicious expression and a complete lack of interest in anything other than killing.

Bug Soldiers dwell deep beneath the surface, where they dig tunnels and wait for something warm and fleshy to fall from above. Occasionally, they emerge to hunt or simply remind the local dinosaurs who's in charge.

In combat, Bug Soldiers rush toward their target and try to corner them. Close combat is their forte. Their pincers grab, their sting stings, and if that's not enough, each Bug contains a toxin capable of killing a human in seconds.

TIPS:

- A shotgun at point-blank range is the quickest way to kill a fallen Bug.
- Don't approach a fallen Bug until you're sure it's truly dead.
