COMBINE OVERWATCH BOMBER


CLASS:      Combine Overwatch Bomber
METABOLISM: Augmented Human, Male
PERCEPTION: Support Fire and Area Suppression
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Very High
WEAPONS:    Overwatch Triple-Shot Grenade Launcher
REWARD:     700 FC
THREAT:     High

DESCRIPTION:

The Combine Overwatch Bomber is a walking warning that cover is no longer safe. He sports light, sky-blue mottled armor designed for operations in snowy environments, high-rise complexes, and sterile industrial areas. A blue visor emphasizes his role as a long-range suppression operative.

The primary weapon resembles a massive shotgun, but instead of buckshot, it fires a burst of three grenades in a single volley. This burst covers an area at once, turning corridors, stairwells, and barricades into instant chaos. The Combine uses these soldiers to dislodge the enemy from fortified positions without protracted firefights.

The grenades are programmed for optimal detonation, making escape nearly pointless. Even if the first grenade misses the target, the second and third usually finish the job. The soldier works methodically, targeting enemy concentrations and potential escape routes.

TIPS:

- Don't hide in standard cover.
- He's less dangerous in open areas than indoors.