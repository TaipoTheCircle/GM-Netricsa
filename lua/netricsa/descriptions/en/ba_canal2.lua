CANALS OF BLACK MESA, RECYCLING AREA


Sam, welcome to the most "fragrant" place of Black Mesa - the garbage disposal zone. It stinks of burnt plastic, metal screeches, and... yes, the Mental creatures are already here. Not guests, but full-fledged hosts.

A huge garbage disposal in the water - chews and crushes everything that gets into the mouth. 

The emergency reset lever is rusty, as if it's about to fall off. If you pull, the freight elevator will lower an explosive box, it must be shoved directly into the mechanism.

Come on, Sam. The lever is waiting. An explosion is on the way.