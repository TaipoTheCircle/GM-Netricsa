PRIMITIVE GNAAR, MALE


CLASS:      Primitive Gnaar, Male
METABOLISM: Mammal
PERCEPTION: Trained Breeding War Animal
SIZE:       4 ft
ENDURANCE:  Low
HOSTILITY:  Low
WEAPONS:    Bites, Punches
REWARD:     700 FC
THREAT:     Very Low

DESCRIPTION:

A small bipedal animal with no head, one eye and a large mouth. Originating from Sirius, and trained by Mental to attack opposing forces. Males wear leopard-skin trousers and are smaller and weaker than females.

These are neighbors of the tropical Gnaars, who were once part of the same tribe but took a different path. While the tropical branch stagnated in its development, these gnaars found a new faith: they faithfully serve the Female Shamans, fulfilling their will and earning the right to wear distinctive marks.

A primitive male differs from his tropical counterpart not in size or shape, but in coloring. Primitives wear only partial red patterns, applied to their bodies in the form of ritual tattoos and stripes. The most noticeable detail is the leopard-print pants, worn only by males of this subspecies.

In their free time, primitive gnaars engage in unusual activities. They paint tattoos of yellow snakes and scratches on werebulls, thereby claiming the cattle for their own. The werebulls apparently don't object, or are simply afraid to disobey the shamans. Furthermore, primitive Gnaars often trade with their tropical neighbors, exchanging stolen goods for what they need.

Despite all these cultural differences, primitive Gnaars remain loyal servants of Mental.

TIPS:

- They have no ranged weapons, just keep away.
- Some specimens can levitate.
- Rare individuals have reportedly been invisible.
- The primitive subspecies is more organized than the tropical subspecies, but not more powerful.
