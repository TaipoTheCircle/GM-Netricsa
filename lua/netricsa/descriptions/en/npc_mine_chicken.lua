CHICKEN FROM THE MINECRAFT OVERWORLD


CLASS:      Domestic / Wild
METABOLISM: Bird
PERCEPTION: Standard Vision
SIZE:       2,2 ft
ENDURANCE:  Low
HOSTILITY:  Low
WEAPONS:    None
REWARD:     20 FC
THREAT:     Low

DESCRIPTION:

Chickens are small, peaceful birds found in almost all Minecraft Overworld biomes. They wander aimlessly, occasionally flapping their wings, and lay eggs over time.

Chickens can glide while falling, slowly descending without taking damage from altitude.

Chickens can be bred using wheat seeds.

TIPS:

- Eggs can be used to breed chickens or as throwable weapons with a chance of spawning a chick.
- Feathers are needed to craft arrows.
- Chickens are ideal for automated farms due to their simple behavior.