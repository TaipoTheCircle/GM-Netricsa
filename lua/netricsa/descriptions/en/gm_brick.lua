BRICK, BRICK TOWER OF HELL


Sam, we found it. The place where the architect finally stopped being shy and just smashed bricks onto each other.

While previous versions were about horizontal expansion (houses, garages, swimming pools), here everything has gone vertical. Seriously.

It's all jumbled into a vertical structure. It's not a house, not a tower, but more like a brick labyrinth. Walls, platforms, walkways—all made of brick. And stairs everywhere. Lots of stairs.

Main Mechanics:

There's one key feature here that turns the map into a philosophical experiment:

At some point, you'll decide you've seen enough and jump down.

And down there, you're met by... the same stairs.

You land and realize: the stairs you just climbed are right in front of you. You can start climbing again. And jump again. And find ourselves at its foot again.

Just let's make a deal: if in an hour we're standing at the foot of these stairs again, I'll start to suspect we're in hell. Brick, gray-red-yellow hell.
