COMBINE OVERWATCH GHILLIE SOLDIER


CLASS:      Combine Overwatch Ghillie Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine long-range reconnaissance and sniper unit
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Extreme
WEAPONS:    Overwatch MK2 Sniper Rifle, AR2 Pulse Rifle
REWARD:     1600 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Ghillie Soldier is a shadow you only notice when you make a mistake. The Combine uses these soldiers to hunt Resistance commanders, monitor key routes, and quietly suppress activity in areas where overt Overwatch presence is undesirable.

Unlike most Combine soldiers, he lacks a glowing visor. Instead, he has optical lenses that obscure his line of sight and prevent him from being unmasked. His armor is completely covered in artificial bushes, grass, and fibers of a ghillie suit adapted to the surrounding environment. From a distance, he looks like a piece of the landscape that suddenly decided to shoot.

His primary weapon is the Overwatch MK2 sniper rifle, capable of taking out a target before they even realize they're in danger. The AR2 is used as a secondary weapon in case of detection or close combat, although at that point, he prefers to reposition rather than try to be a hero.

TIPS:

- Never linger in the open - that's what he's waiting for.
- Change position after the first shot.
- Explosives and fire are effective against cloaking, even if you're not sure he's there.