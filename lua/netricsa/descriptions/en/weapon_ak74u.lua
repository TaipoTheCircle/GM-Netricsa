AKS-74U


WEAPON:     AKS-74U
AMMUNITION: 5.45x39 mm
ROF:        700 rounds / min

DESCRIPTION:

The 1974 folding Kalashnikov assault rifle, shortened - a shortened version of the AKS-74 assault rifle - was developed in the late 1970s and early 1980s to arm the crews of combat vehicles and aircraft of the USSR Armed Forces. It saw action in Afghanistan and Chechnya and is now used in the Special Military Operation. It was also very popular among Russian gangsters in the 1990s. It is also used by law enforcement agencies in the USSR/RF and other CIS countries.

NOTES:

- Its compact design allows this weapon to be transported unnoticed.
- The stock can be folded to make it even shorter.
- Very reliable in combat, it will not let you down.