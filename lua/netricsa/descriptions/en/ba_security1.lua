BLACK MESA, AT THE GATE OF THE SECURITY COMPLEX


Well, we've arrived! The destination is the Black Mesa Security Complex. Passage is allowed. It looks like your disguise is working so far.

But before you step inside, turn around. There's another train behind you. He's riding in it. Not the Sam who was mistaken for Freeman. Not a doppelganger. Real. Gordon Freeman. Are you late? Or maybe everything is going according to some invisible schedule?

He drives by, silent, focused, with that characteristic Rock-like look. And somewhere out there, behind the walls, the clock is already ticking before the disaster.