DESCRIPTION:

The HK416 was originally developed as a module for installation on the fore-end of M4 or M16 assault rifles, but later evolved into a full-fledged carbine, and then a rifle. The goal was to replace the M4 and M16 assault rifles, which formed the basis of the US Armed Forces and Marine Corps' arsenal, due to their low reliability, frequent malfunctions, overheating, and susceptibility to contamination.

NOTES:

- This rifle is effective at medium to long ranges.
- Well suited for indoor or urban operations.
- Has a high rate of fire.
