"BETA" SWARM DRONE, SMALL


CLASS:        "Beta" Swarm Drone, Small
METABOLISM:   Xenobiological
PERCEPTION:   Pack Predator
SIZE:         7,2 ft
ENDURANCE:    Low
HOSTILITY:    Very High
WEAPONRY:     Claws, Teeth
REWARD:       250 FC
THREAT:       Medium

DESCRIPTION:

The "Beta" Swarm is considered one of the most primitive evolutionary branches of the alien infestation discovered on the fringes of settled space. Its drones differ markedly from those of better-known swarms: they are taller, possess elongated limbs, and lack protective chitinous plating, though they do have three eyes.

The absence of heavy armor makes these creatures vulnerable to small-arms fire, yet they compensate for this lack of protection with agility. "Beta" Swarm drones can rapidly cover great distances and assault their prey with a flurry of quick strikes, preventing the target from focusing on aiming.

Xenobiologists still debate whether the "Beta" Swarm is an ancestor of modern swarms or an evolutionary dead end. The drones themselves do not share this scientific interest and usually cut such research short while it is still in the observation phase.

Despite their gaunt appearance, these creatures possess surprising resilience and continue to attack even after sustaining severe wounds. It seems the instinct for self-preservation was deemed an unnecessary organ and vanished over time.

TIPS:

- Don't let them get close.
- They are particularly dangerous in open spaces.
- Precise fire is more effective than indiscriminate shooting.
- If a drone is charging straight at you, it has likely already made up its mind.
