RUGER 10 / 22 SELF-LOADING CARBINE


WEAPON:     RUGER 10/22
AMMUNITION  .22 LR cartridges 
ROF:        ~40-60 rounds / min 

DESCRIPTION:

A compact semi-automatic rifle chambered for .22 Long Rifle cartridges, first introduced by Sturm, Ruger & Co. in 1964. Based on a reliable short-stroke bolt action with a rotating bolt frame; comes standard with a 10-round rotary magazine. Known for its durability, simple design, and affordability, it is popular for training, recreational shooting, hunting small game and pests, as well as in sporting disciplines. It is available in numerous variants and configurations (Carbine, Target, Tactical, Ruger 10/22 Takedown, etc.) and has an extensive market for tuning and accessories (sighting systems, stocks, forends, barrel compensators, extended magazines, etc.).

NOTES:

- Widely used as a training and "recreational" weapon; in some countries, civilian sporting / hunting versions are available with restrictions.
- Causes very little damage due to its small 22 caliber. LR.