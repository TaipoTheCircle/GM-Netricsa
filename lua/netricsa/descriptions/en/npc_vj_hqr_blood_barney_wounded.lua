WOUNDED BLOODY GUARD FROM THE HALF-QUAKE SHOW


CLASS:      Wounded Bloody Guard
METABOLISM: Human, Male
PERCEPTION: Guard And Victim of the Show's Trials
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  Neutral
WEAPONS:    Pistol
REWARD:     60 FC
THREAT:     Low

DESCRIPTION:

This is a Wounded guard in the gray uniform that the sadistic HALF-QUAKE Show gives to those who agree to work for them for food and a promise not to harm their families. The uniform is gray, nondescript, with a ">)" logo hidden by the blood on the back. The guards themselves wear tattered body armor, helmets, and several days' worth of stubble. And blood. Lots of blood. Their own and others'.

Every guard must eventually undergo sadistic tests. It's a condition of the contract they signed without reading the final page. The Sadists enjoy tormenting them. It's their nature. The guards are beaten, they're poisoned, they're forced to run through labyrinths of death, and then, if they survive, they're allowed into the arena as "service personnel" for the show's real victims. Those who don't survive (almost always) become part of the scenery.

This is your temporary, reluctant ally. He hates the Sadists as much as you do, but he's too broken to rebel. At best, he might turn away at the right moment. At worst, he'll simply sit against the wall and stare until he's shot for inaction.

Some guards are armed. But even with weapons, they're not fighters. They're victims who haven't yet realized they're already dead.

TIPS:

- Don't kill them. They're the only ones who can help you with advice or ammo.
- If a guard says "Run", then run. He's seen a lot.
- The guards know the locations of cells, hiding places, and exits. Follow them if they're heading somewhere with a purpose.
- Don't try to save them. You can't. You'll only die with them. And they'll die anyway.
