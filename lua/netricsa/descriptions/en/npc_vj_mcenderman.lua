ENDERMAN FROM THE MINECRAFT END


CLASS:      Enderman
METABOLISM: Undefined
PERCEPTION: Neutral teleport mob / mysterious alien
SIZE:       9,5 ft
ENDURANCE:  High
HOSTILITY:  Neutral
WEAPONS:    Punch
REWARD:     1000 FC
THREAT:     High

DESCRIPTION:

One of the most mysterious creatures, whose true origins are still debated. Its name is a combination of the words "End" (the dimension it supposedly inhabits) and "men" (human). According to fan theories, endermen were once ordinary people who fled to the End to escape a catastrophe. There, feeding on chorus fruit, they mutated: they grew taller, lost their skin color, and learned to teleport and build ender cities.

The endermen's appearance is terrifying: a long, thin, completely black body, glowing purple eyes, and a mouth from which purple particles emanate, similar to those of a portal to the Nether. Endermen wander the world calmly until you look them directly in the eye.

Endermen are unique in that they are the only mob that can naturally spawn in all three dimensions:

Overworld: At night in most biomes.

Netherworld: Most often in the Warped Forest.

The End: Their primary habitat. Here, they spawn in huge groups.

Endermen behavior boils down to two things: teleportation and moving blocks. It can lift and move certain blocks (dirt, sand, cacti, flowers), leaving chaos in its wake. If an Enderman is holding a block, it will drop it when it dies.

When idle, an Enderman simply walks around, ignoring the player. However, aiming a crosshair at it or striking it causes it to become enraged. The Enderman's body begins to shake, its mouth opens, and it lets out a terrifying, electronically distorted scream. Then the fight begins:

The Enderman teleports behind the enemy or to their sides, delivering powerful blows with its hands.

Any projectile (arrow, snowball, egg) causes teleportation before impact. It is impossible to kill an Enderman with a bow.

Water and rain damage Endermen. When exposed to water or during rain, they panic and teleport away.

TIPS:

- Equip a carved pumpkin on your helmet. Your vision will be impaired, but the Enderman won't aggro no matter how much you stare at it.
- Endermen are almost 3 blocks tall. Build a lean-to 2 blocks off the ground. You can hit the Enderman's legs, but it won't be able to reach you.
- Press your back against a wall or corner. This will prevent the Enderman from teleporting behind you.
- Hitting the Enderman's legs will prevent it from teleporting in pain.
- Throwing water at your feet will create a barrier. Endermen are afraid of water and won't approach.
- The best weapon is a sword. Strike quickly, trying to keep the enemy in front of you.
- Endermen hate endermites (small purple bugs) and will chase them even if you're looking at them.
- If an Enderman dies while holding a block, that block is dropped. The exception is non-loot blocks (like bedrock). There are fan legends about the White Enderman, which supposedly appears rarely, but officially this is just a texture bug or a mod.