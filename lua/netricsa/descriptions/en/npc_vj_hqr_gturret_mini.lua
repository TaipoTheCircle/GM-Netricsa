HALF-QUAKE SHOW MINI TURRET


CLASS:      HALF-QUAKE Show Mini Turret
METABOLISM: Mechanical
PERCEPTION: Automated Defense Unit
SIZE:       ~2 ft
ENDURANCE:  Low-Medium
HOSTILITY:  Extreme
WEAPONS:    Small Rapid-Fire Machine Gun
REWARD:     250 FC
THREAT:     Moderate

DESCRIPTION:

When the HALF-QUAKE Sadistic Show realized that live actors weren't dying enough, the decision was made to purchase a batch of automated turrets from Black Mesa. The contract was signed and the equipment was issued, but with one condition: all equipment must be properly marked.

The Mini Turret is a compact version of the standard armored turret, adapted for installation in low-ceilinged spaces, ventilation shafts, and other places where a full-size version simply cannot fit.

After transferring ownership to HALF-QUAKE, the ">)" logo was stenciled onto the top of each turret in white paint.

Mini Turrets can be mounted on the floor, wall, or ceiling. They can wait for hours in the dark, powered by a backup battery, and are activated only when motion sensors detect an approaching target.

Thanks to its 360-degree rotation, it's impossible to hide behind it—it simply turns and continues firing.

TIPS:

- Explosives or precise shots can quickly disable it.
- Check the space above and below you—even small turrets can cover unexpected angles.
- Avoid standing in corridors or narrow spaces where it can effectively target you.
- Use cover in the environment to minimize the impact of her machine gun fire.
