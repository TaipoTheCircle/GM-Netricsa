ZOMBIE HEADCRAB DELTA BASE TECHNICALLY, COMMON


CLASS:      Zombie Headcrab Delta Base Technician, Common
METABOLISM: Human Host + Parasitic Headcrab Of Xen
PERCEPTION: Zombified Entity, Former Tech Specialist
SIZE:       5,9 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Claws, Bite, Grab
REWARD:     200 FC
THREAT:     High

DESCRIPTION:

This is technically the same as the Common zombie headcrab from Delta Base, just in a different uniform. But survivors claim there are differences, and they're not just in appearance.

If the regular zombies at the base are former scientists in gray coats, then the Technicians are the ones who used to fix generators, climb through ventilation systems, and know where every lever is.

The main difference between an Technician and a scientist isn't strength or speed, but behavior. Some survivors swear they saw one fixing a broken air conditioner. (He simply banged his head against the wall until he fell.)

A headshot detaches the Technician's head.

TIPS:

- An Technician zombie isn't stronger or faster than a Common one. They just have different clothing.
- The head detaches on kill just like a scientist's.
- Keep your distance, shoot, and don't let them surround you.
