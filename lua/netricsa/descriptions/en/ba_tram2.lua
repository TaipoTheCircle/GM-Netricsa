BLACK MESA, TRAIN THROUGH THE CAFETERIA


Hey, Sam the guard! Get ready for a short tour of the "gastronomic attractions" of Black Mesa. Now we'll drive past the cafeteria, where Big Tony's Pizza and Tesla's Taco are nestled against a hole in the rock. The place is popular: Otis Lorey, for example, often hangs out here.

Keep your eyes peeled: there are "Generic Monsters" and "Generic Actors" lurking among the scientists and guards. Who they really are is unknown. Where they live, too. Why are they disguising themselves - even more so. But they obviously didn't come for coffee.

They copy the behavior of the staff: they stand at coffee machines, leafing through papers, pretending to be in a hurry on business. But my gut tells me that these "colleagues" are waiting in the wings. Maybe they're watching. Maybe they're planning a surprise. Or maybe one of them is already looking at you and deciding whether to press the alarm button.

So far, everything is quiet. The cafeteria is lit up, the customers (real or not) are chatting, and it smells of pizza and tacos. But remember: in Black Mesa, even a lunch break can be the last.

Keep your hand close to the holster. And don't look at the signs - what if it's not food, but bait?

Go ahead, "guard". The next stop is unknown.