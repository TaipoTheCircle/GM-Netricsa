C-4 EXPLOSIVES


WEAPON: C-4 Explosives
AMMO:   C-4 Charges
ROF:    Manual Detonation
WEIGHT: 1.1 lbs

DESCRIPTION:

Military-grade plastic explosive. Extremely stable and reliable. Will not detonate from impact or fire, requiring a detonator for initiation.
Possesses tremendous destructive power. Can be molded into any shape for effective placement on vehicles, structures, or enemy groups.

NOTES:

- Ideal for setting ambushes, eliminating heavy targets, and creating paths through debris.
- Controlled detonation allows for precise damage and minimizes risk to the user.
