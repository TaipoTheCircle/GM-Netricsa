CLASS:      Mutant Sponge
METABOLISM: Distorted biology, stimulant-dependent
PERCEPTION: Intelligent predator
SIZE:       ~2.5 m
ENDURANCE:  Very high
HOSTILITY:  Absolute
WEAPONS:    Physical strength, grappling, meat grinder
REWARD:     ??? FC
THREAT:     Critical

DESCRIPTION:

"Meat" Bob is a mutated version of the once naive and cheerful good guy. In the past, he worked side by side with Eugene Harold Krabs, better known as Mr. Krabs, and was considered his best employee. It was Bob who prepared the Krabby Patties that made the establishment famous. No one ever knew the recipe for the dish. And no one was supposed to know.

Under the influence of steroids and unknown additives, Bob's body mutated. His body swelled to monstrous proportions, muscles bursting through the spongy structure, and the yellow surface became dense and rotting. His smile remained, but now it lacked every ounce of kindness.

Bob is fanatically devoted to the secret formula. Anyone who approaches it will be destroyed. Anyone who tries to escape will become an ingredient.

Krabby Patties weren't made from crabs. They were made from customers. From fish. The fish ate their own kind, unaware that they were becoming part of an endless recycling cycle. Meaty Bob chases his victims, then directs them toward an industrial meat grinder. There's no escape inside. Resistance is futile.

The resulting meat is sold.

TIPS:

- He has extraordinary physical strength, capable of tearing a man in half.
- He feels virtually no pain.
- Avoid close combat.
- Only Shrek can kill him.