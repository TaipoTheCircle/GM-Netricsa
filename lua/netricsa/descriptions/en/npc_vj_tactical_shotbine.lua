COMBINE OVERWATCH TACTICAL SHOTGUNNER


CLASS:      Combine Overwatch Tactical Shotgunner
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Heavy Melee Assault Unit
SIZE:       5,9 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    SPAS-12 Combat Shotgun, Grenades
REWARD:     800 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Tactical Shotgun Soldier is a beefed-up version of the Assault Trooper, designed to breach defenses where Hybrid units begin to suffer losses. While it resembles a Hybrid Trooper in appearance, the key difference lies beneath the armor.

The tactical helmet completely neutralizes vulnerability to headshots. Even accurate headshots do not incapacitate the soldier. The Combine deliberately overloaded these soldiers with armor, turning them into relentlessly advancing battering rams.

The SPAS-12 is used as a pressure and clearing tool. The soldier advances under fire, ignoring hits, closing on the target to ensure kill distance. Grenades are used to clear passages and dislodge the enemy from fortified positions.

TIPS:

- Headshots are ineffective - aim for the body or use explosives.
- Don't let him close the distance - he's almost guaranteed to be deadly at close range.
- Heavy weapons and traps work best.
- Retreat and distance are the keys to survival.