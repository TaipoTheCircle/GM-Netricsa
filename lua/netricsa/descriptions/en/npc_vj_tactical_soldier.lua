COMBINE OVERWATCH TACTICAL SOLDIER


CLASS:      Combine Overwatch Tactical Soldier
METABOLISM: Augmented Human, Male
PERCEPTION: Combine Heavy Tactical Unit
SIZE:       1.8 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, SMG, Grenades
REWARD:     750 FC
THREAT:     Very High

DESCRIPTION:

The Combine Overwatch Combine Tactical Soldier is a beefed-up and upgraded version of the standard trooper, designed for protracted combat and direct pressure on the resistance. While the standard trooper is a tool for control, the Tactical Soldier is a tool for total suppression.

His distinctive features are his blue visor and highly protective helmet. The helmet's design and internal augmentations completely eliminate vulnerability to headshots: headshots don't have an immediate effect. The Combine intentionally designed these soldiers to hold their ground under heavy fire.

The armor is multilayered and reinforced, with internal shock absorbers and synthetic inserts. The Tactical Soldier doesn't rely on speed - they advance methodically, using the AR2 for suppressive fire, the SMG for close combat, and grenades to clear cover.

In combat, they act calmly and deliberately. Their goal is not to flank, but to break the enemy's line, forcing them to retreat or die. When such a soldier advances, combat ceases to be a mobile affair and becomes a test of survival.

TIPS:

- Headshots are useless - aim for the body or use explosives.
- The AR2 is dangerous even at medium range - cover is essential.
- The best way to deal with him is with heavy weapons or a coordinated attack.