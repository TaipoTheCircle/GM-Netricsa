COMBINE OVERWATCH HYBRID ELITE SOLDIER


CLASS:      Combine Overwatch Hybrid Elite
METABOLISM: Augmented Human, Male
PERCEPTION: Versatile Elite Breakthrough Unit
SIZE:       5,9 ft
ENDURANCE:  Extremely High
HOSTILITY:  Extreme
WEAPONS:    AR2 Pulse Rifle, Grenades, Stun-Stick
REWARD:     1000 FC
THREAT:     Critical

DESCRIPTION:

The Combine Overwatch Hybrid Elite is the Combine's compromise between ostentatious elitism and practical brutality. It combines the best elements of standard Elite soldiers and alternative units, becoming a versatile tool for suppressing resistance in any environment.

Their armor is white, like that of classic elite soldiers, but reinforced with black composite inserts and an additional flak jacket, giving their silhouette a heavier and more menacing appearance. These soldiers look as if they were assembled not for parades, but for survival where other elites would break.

The beret serves not as decoration, but as a marker of experience and the right to independent tactical decisions.

In combat, the hybrid acts aggressively and calculatingly. The AR2 is used for methodical pressure and the destruction of targets, and grenades are used to destroy cover and disrupt enemy formations. These soldiers most often lead the assault, being the first to enter the contact zone and the last to leave, leaving behind silence and the smell of ozone.

While the alternative elite soldier deceives expectations and the standard one overwhelms with its appearance, the hybrid does both simultaneously, leaving the enemy neither time nor illusions.

TIPS:

- Black reinforced armor absorbs damage better than standard armor.
- Grenades are used with precision and coolness.