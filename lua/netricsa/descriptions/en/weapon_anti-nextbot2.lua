ANTI-NEXTBOT LASER V2


WEAPON: Anti-Nextbot Laser V2
AMMO:   Pulse Energy
ROF:    Continuous Beam

DESCRIPTION:

A radically redesigned version of the V1, abandoning selectivity in favor of absolute destruction. The weapon generates a stable purple beam that doesn't just deal damage, but completely obliterates everything it comes into contact with.

Unlike the previous model, this one has no modes—the beam operates on a single principle: eliminate the target from existence, be it a Nextbot, a prop, or any other object. It is used in situations where conventional methods fail or the target must be eliminated without a trace.

NOTES:

- Eliminates everything: enemies, props, structures, and cover.
- Does not discriminate between targets—high danger to allies.
- Effective at any range when aiming directly.
- Complete destruction without leaving any residue or debris.
