SAVAGE DUBINOV FROM M'DIGBO


CLASS:      Savage Dubinov
METABOLISM: Mammal
PERCEPTION: Friendly Savage, Renegade
SIZE:       6,8 ft
ENDURANCE:  Average
HOSTILITY:  Average
WEAPONS:    Spiked Club, Homemade Bone Binoculars, Hunting Skills
REWARD:     400 FC
THREAT:     Low

DESCRIPTION:

A unique representative of the Primitives from the planet M'Digbo. While his fellow tribesmen, led by the mysterious "Mommy," made a pact with Mental, agreeing to become his soldiers in exchange for an endless supply of leopard-skin thongs, Dubinov chose a different path. He voluntarily renounced the aggressive social norms of his people, making him a surprisingly friendly savage.

Dubinov significantly surpasses the average Primitive in intellectual and social development. He possesses a unique artifact-homemade binoculars, skillfully crafted from the bones of fallen enemies and animals. Unlike his kin, he prefers hunting wild boar. He lives in the planet's tropics with his wife, leading a peaceful life, by savage standards.

Dubinov's morning begins in his cave, located in the heart of the tropics. First, he leaves his dwelling and heads to the nearest mountain stream. This ritual is unchangeable: he drinks clean water and thoroughly washes himself, preparing for a new day of hunting and observing the horizon through his bone binoculars.

TIPS:

- Dubinov is not an enemy. Do not attack him.
- If you meet him in the tropics, you can talk. He will tell you many interesting things about the planet M'Digbo and its inhabitants.
- His binoculars aren't just a toy. Dubinov can spot danger long before it gets close.
- If you help him hunt, he'll share some meat and maybe even teach you a few simple tricks.
- Don't try to steal his binoculars. It's impolite, and Dubinov is very attached to his possessions.
- Dubinov's wife is a different story. There are rumors about her, but he never shows her to anyone.