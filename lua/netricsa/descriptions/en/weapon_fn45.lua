FNX45 SELF-LOADING PISTOL


WEAPON:     FN FNX-45
AMMUNITION: .45 ACP cartridges
ROF:        ~30 rounds / min

DESCRIPTION:

A self-loading pistol manufactured jointly by Belgium and the United States, part of the FNX line. Introduced in 2012 for the .45 ACP cartridge.

NOTES:

