SWARM GRUB SAC


CLASS:      Swarm Grub Sac
METABOLISM: Xenobiological
PERCEPTION: Stationary Organism
SIZE:       4 ft
ENDURANCE:  Medium
HOSTILITY:  Low
WEAPONS:    Grub Discharge
REWARD:     75 FC
THREAT:     Low

DESCRIPTION:

The Swarm Grub Sac is a stationary organic sac in which Swarm Grub develop. Its outer shell is strong and elastic, capable of withstanding significant mechanical damage and temperature fluctuations.

The Swarm Grub Sac contains four mature Grub, ready to hatch. When exposed to fire, strong vibration, or the approach of large living objects, the shell opens, splitting into several flaps. The Grub are expelled and immediately begin searching for a target.

Grub Sacs are usually found in groups on walls, ceilings, and in hard-to-reach places. Their main function is to ensure the safe development of the swarm's offspring until prey appears.

Despite the low direct threat, a large number of active Grub Sacs in one location is a sign of high swarm density and the imminent emergence of more dangerous life forms.

TIPS:

- Destroy the Grub Sack before they open.
- A single Grub Sack is nothing. The problems begin when there are many of them.
- Fire and explosives are the best means against Grub Sacs.
- Once the Grub Sack opens, do not waste time on the Grub.
- Noise and movement are all it takes to awaken the brood.
