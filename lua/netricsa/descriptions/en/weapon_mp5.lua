MP5 SUBMACHINE GUN


WEAPON:     MP5 submachine gun
AMMUNITION: 9x19 mm cartridges 
ROF:        ~700 rounds/min

DESCRIPTION:

A versatile automatic weapon for close and medium range combat. Provides continuous suppressive fire with acceptable accuracy. Used by special forces in counter-terrorism operations.

NOTES:

- Effective against lightly armored infantry.
- Accuracy decreases significantly at long ranges.
- Ideal for indoor operations.