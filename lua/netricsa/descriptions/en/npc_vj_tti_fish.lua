BIG FISH TOM FROM THE BIKINI BOTTOM


CLASS:      Big Fish Tom
METABOLISM: Organic humanoid, arm-wielding mutated fish
PERCEPTION: Extremely irritable civilian
SIZE:       6,5 ft
ENDURANCE:  Average
HOSTILITY:  High
WEAPONS:    Scream, Aggression
REWARD:     300 FC
THREAT:     Average

DESCRIPTION:

Big Tom Fish is a massive and extremely short-tempered inhabitant of Bikini Bottom, known for his insatiable passion for chocolate and pathological intolerance of others. Any interaction with him is almost guaranteed to end in screaming, threats, and outbursts of uncontrollable rage.

Tom is critical of poor service, especially at the Krusty Krab, where the slightest mistake by the staff can send him into a rage-fueled rage. He has no knowledge of the Krabby Patty's secret formula, making him dangerous but useless in terms of information.

His primary attack method is a deafening, aggressive, and furious scream.

TIPS:

- Don't approach him unless necessary - any attention irritates him.
- Keeping your distance and ignoring him are the best ways to avoid aggression.