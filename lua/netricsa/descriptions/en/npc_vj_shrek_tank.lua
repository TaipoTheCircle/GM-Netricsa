SHREK, THE ZOMBIE TANK FROM THE FAR FAR AWAY


CLASS:      Shrek, The Zombie Tank
METABOLISM: Biological Ogre Mutant
PERCEPTION: Large Elite Infected melee fighter
SIZE:       10 ft
ENDURANCE:  Extremely high
HOSTILITY:  Absolute
WEAPONS:    Fists, rocks, rams, dashes, roar
REWARD:     5000 FC
THREAT:     Absolute

DESCRIPTION:

This specimen of Infected evokes mixed feelings in survivors: on the one hand, they see a huge, green, bald mutant tearing up chunks of asphalt and hurling them. On the other, the song "Smash Mouth All Star" blares from its chest. Shrek apparently swallowed a radio right before becoming infected, and now the speaker is stuck somewhere in his stomach, blasting "Hey now, you're an all-star, get your game on, go play!" at full volume.

Otherwise, his behavior is identical to that of a classic Tank. He feels no pain, doesn't hesitate to stop at obstacles, and has one goal: to smash survivors into the wall. His favorite moves are ripping up a chunk of pavement and throwing it at his target, and if the distance closes, he simply crushes it with his heavy hand. The impact is so powerful that the survivor is thrown back a good ten meters.

Sometimes Shrek roars. A regular Tank screams so loudly it's ear-splitting. This one roars just as loudly, but after the roar, the speaker blares, "I ain't the sharpest tool in the shed". The survivors don't know whether to laugh or cry. Most choose to cry.

The origin of this specimen is unknown. Some say that the lab was trying to cross-breed the virus with a swamp ecosystem. Others say it's just some big guy in green leather who loved this track too much and took it to the grave. Still others just shoot without asking questions.

TIPS:

- Shrek throws rocks  dodge. They fly slowly, but they hurt.
- He's deadly in close combat. Don't get too close unless you want to turn into swamp muck.
- You can tell how close Shrek is by the volume.
- If you hear "So much to do, so much to see", and the floor beneath you trembles  run. Run right now.
- You can kill him, but it will require a ton of ammo and luck.
- They say if you listen carefully, the chorus will play. But you probably won't listen  you'll just be glad if he shuts up.
