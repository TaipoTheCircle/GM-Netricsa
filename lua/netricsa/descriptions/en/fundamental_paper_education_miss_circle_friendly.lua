APPEALED MISS CIRCLE FROM FUNDAMENTAL PAPER EDUCATION


CLASS:      Miss Circle, Math Teacher
METABOLISM: Paper Organism (mutant teacher)
PERCEPTION: Sight, hearing, sense of academic failure, sense of smell for Oreos
SIZE:       9,7 ft
ENDURANCE:  Very high
HOSTILITY:  Extreme (towards underachieving students)
WEAPONATAL: Giant prosthetic Circle, sharp claws
REWARD:     10000 FC
THREAT:     Very high

DESCRIPTION:

The main antagonist of "Fundamentals of Behavior", a math teacher at Paper School. Exceptionally tall, with long black hair tapering to horns. In place of her left hand, she has a giant, detachable Circle named "Mr. Circle", which serves both as a teaching tool and a deadly weapon. She displays extreme cruelty and cannibalism toward students who fail her exams (Abby, Lana). She has favorites (Oliver, Zip, Edward), but can even scold them for breaking the rules. She can change her size and possesses immense physical strength, allowing her to smash school furniture. Her personality is a frightening mixture of goofy playfulness and sudden, merciless rage. She is the only character to speak in the original animation (with a synthesized Japanese voice). She has one known weakness: a fanatical love of Oreo cookies.

TIPS:

- Her Circle is a ranged and melee weapon with immense damage. She wields it masterfully.
- She attacks methodically and mercilessly.
- Oreo cookies. An offer or toss of an Oreo might appease her, triggering a burst of joy and devouring the treat. She hates Kit-Kats.
- A few shots from a double-barreled shotgun will take this teacher down.