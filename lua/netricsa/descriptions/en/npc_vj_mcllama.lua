LLAMA FROM THE MINECRAFT OVERWORLD


CLASS:       Llama
METABOLISM:  Mammal, Even-toed Ungulate
PERCEPTION:  Neutral herd animal
SIZE:        6,3 ft
ENDURANCE:   Average
HOSTILITY:   Conditional
WEAPONS:     Spit
REWARD:      120 FC
THREAT:      Minimal

DESCRIPTION:

In the wild, llamas are found in savannas and windswept hills.

Llamas are neutral creatures by nature. They don't attack, but if struck, the offended camel turns and spits directly in the attacker's face. The spit flies like a projectile, deals one point of damage, and doesn't catch fire. Sometimes a llama misses and hits a fellow llama, and then squabbles break out within the pack to determine who owes whom.

Llamas have a particular hatred for wolves. Upon seeing a wild dog, a llama spits without warning, regardless of whether it's been touched or not. Wolves, in turn, fear strong llamas and always run away from them.

Llamas can be tamed. To do this, mount them repeatedly until they stop throwing you.

A tamed llama is an ideal pack animal.

Besides a chest, you can decorate a llama with a rug.

TIPS:

- Don't hit the llamas first. The damage from the spit is negligible, but it leaves a bad taste in the mouth.
- If you encounter a traveling merchant with llamas, you can take the llamas for yourself by untying the leash.
