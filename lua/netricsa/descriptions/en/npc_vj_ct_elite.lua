COMBINE SAS OVERWATCH SOLDIER


CLASS:       Combine SAS Overwatch Soldier
METABOLISM:  Augmented Human, Male
PERCEPTION:  Elite Reconnaissance and Infiltration Unit
SIZE:        5,9 ft
ENDURANCE:   Very High
HOSTILITY:   Extreme
WEAPOON:     AR2 Pulse Rifle, MP40, SSG-08 Sniper Rifle
REWARD:      1100 FC
THREAT:      Very High

DESCRIPTION:

The Combine SAS Overwatch is a reborn version of the legendary Special Air Service, a unit created by David Stirling during World War II. They once infiltrated deep behind enemy lines, carried out sabotage operations, and disappeared before the enemy could even realize what had happened. The Combine looked at this philosophy and said simply, "Sounds good."

After the occupation of Earth, SAS troopers were integrated into Overwatch and subjected to extensive cybernetic modification. Their current equipment consists of a white tactical suit with black body armor and a massive black helmet with a gas mask. The gas mask lenses are tinted an ominous red, concealing cybernetic eyes capable of infrared, thermal, and low-light detection.

Overwatch's SAS are masters of stealth combat, sabotage, and targeted assassinations. They are equally dangerous at long range, using the SSG-08, and in close combat with the AR2 and MP40. Their style is rapid infiltration, instant elimination of the target, and disappearance, leaving behind only silence and a report back to Combine headquarters.

The irony is that SAS training has hardly changed. As before, training remains deadly, due to the fact that real ammunition (and sometimes even real bombs and satchel charges) is used.

TIPS:

- Never stand still - Overwatch SAS soldiers like to operate from the shadows and flanks.
- Their snipers are dangerous even at extreme ranges.