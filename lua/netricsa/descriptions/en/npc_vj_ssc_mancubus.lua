MANCUBUS FROM THE HELL


CLASS:      Mancubus
METABOLISM: Demonic Mammal
DEFINITION: Modified Behemoth
SIZE:       13 ft
ENDURANCE:  Medium
HOSTILITY:  Medium
WEAPONS:    Two Flamethrowers
REWARD:     3000 FC
DANGER:     Medium

DESCRIPTION:

A demon that embodies human imperfection and greed, gluttony, and idleness. It has incredibly thick skin, massive elephantine legs, and a small head. For the purpose of destroying and roasting its victims, two flamethrowers are implanted in the mancubus's forearms, firing fireballs.

Mental bought several dozen of these creatures from the Lord of Hell, along with Zombie Soldiers and many other demons.

TIPS:

- They shoot randomly, so take cover.
