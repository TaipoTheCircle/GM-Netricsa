SLENDERMAN, THE SLIM MAN


CLASS:      Slenderman, the Slim Man
METABOLISM: Non-biological
PERCEPTION: Urban Legend
SIZE:       12 ft
ENDURANCE:  Immeasurable
HOSTILITY:  Absolute
WEAPONS:    Tentacles, teleportation, jumping, telepathy, memory manipulation, mere presence
REWARD:     2009 FC
THREAT:     Absolute

DESCRIPTION:

He is a skinny man in a black funeral suit with a white shirt. His skin is pale, his limbs are unnaturally long and can bend in any direction. Flexible black tentacles or appendages sometimes grow from his back. And most importantly, he is completely faceless. Where his face should be, there is a smooth void. No eyes, no nose, no mouth.

Slenderman travels by teleportation. He appears and disappears silently. Upon contact, he causes severe headaches, electronic interference, and a feeling of inexplicable terror. He can erase or edit memories, turn people into proxies (puppets), and take children to an unknown location.

TIPS:

- If you see him, he already sees you. Running is useless-he's teleporting.
- Don't look him in the eyes. He doesn't have any.
- Static on your camera or phone is the first sign he's nearby. Turn off your device and leave.
- The only way to survive is to not think about him. But now you're already thinking about him.
