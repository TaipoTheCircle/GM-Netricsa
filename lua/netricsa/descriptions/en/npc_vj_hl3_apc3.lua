COMBINE APC-TRANSPORTER


CLASS:      Combine Heavy Armored Personnel Carrier
METABOLISM: Mechanized Combat Platform
PERCEPTION: Tactical Sensors, Crew Vision Systems
SIZE:       29,5 ft
ENDURANCE:  Extremely High
HOSTILITY:  Extreme
WEAPONALS:  Heavy Machine Gun (Upper Cabin)
REWARD:     3000 FC
THREAT:     Critical

DESCRIPTION:

The Combine Heavy APC is a 1,5-times larger version of the standard Combine APC, redesigned to carry only Combine Guardsman Soldiers. Unlike the base model, this vehicle uses a six-wheel drive system: two steerable front axles and four rear wheels, providing maximum stability under heavy loads.

This chassis allows the Transporter to remain mobile even when fully loaded with troops and combat equipment, and to navigate ruined roads, rubble, and rugged urban terrain without significant loss of stability.

The vehicle is equipped with two cabins. The forward cabin is for the driver and is fully insulated by an armored capsule. The upper cabin serves as the machine gunner's station, providing all-round visibility and suppressive fire during movement and deployment.

TIPS:

- Don't let the Transporter stop at a key point.
- If it arrives, the operation has already begun.