HALF-QUAKE SHOW, HOUSE SEWER


The trials are over.

Not because the show decided to let us go-the sets simply ran out.

You've fallen into the sewer. No audience, no announcer, no applause. The smell of damp, old pipes, and other people's bad decisions. This isn't a stage anymore. This is the backstage of a world where no one pretends.

We escaped. But we didn't win, and we didn't ruin the show. We simply left, slipped between scripts, like a broadcast error no one had time to edit out.

A scan of the surroundings reveals an interesting detail: the sewer runs under a house. Someone's house. Judging by the structure, the belongings, and the number of strange objects-an artist's.

Somewhere above are living rooms, staircases, doors. And, most importantly, an exit. Not the front door, of course. Rather, one the owner prefers not to remember.

The task is clear and finally human:

Get into the house,

Don't get stuck in someone else's art,

Find a way out.

No tests.

No buttons.

No applause.

Just escape, Sam.

And sometimes that's the most enjoyable part of the journey.