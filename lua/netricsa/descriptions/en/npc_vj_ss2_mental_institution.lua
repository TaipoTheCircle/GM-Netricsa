MENTAL INSTITUTION


CLASS:      Mental Institution
METABOLISM: Armored mechanical megastructure
PERCEPTION: Autonomous combat sensors and artificial intelligence
SIZE:       984 ft
ENDURANCE:  Immense
HOSTILITY:  Extreme
WEAPONS:    Fire launchers, artillery cannons, missile batteries, deadly wheels
REWARD:     5000 FC
THREAT:     Catastrophic

DESCRIPTION:

Mental Institution is a gigantic mobile pyramid that serves as Mental's central base and personal lair. It is an autonomous mechanized citadel, designed solely for total warfare and the destruction of any intruders.

The structure is completely covered in armored segments and weapon platforms. A shielded control core is housed within, activated upon detection of a threat. Once activated, the fortress launches waves of fighter jets, including "fatso kamikaze" fighters that fall on targets and explode on impact, causing devastating damage.

At the same time, the fortress bombards the surrounding area with plasma fireballs, heavy cannon shells, and massive missile salvos. Maneuverable Chaika bombers drop cluster bombs, turning the surface around the fortress into a total destruction zone.

When the defenses are weakened, the central core temporarily opens, allowing attackers to attack key components of the structure. At this moment, massive rotating wheels-autonomous combat modules that move along the surface-extend from the lower compartments. Any contact with them results in the instantaneous destruction of the target.

Mental's Institution isn't an ordinary structure-it's a walking destruction machine, capable of overwhelming armies, razing cities, and functioning as a self-contained combat ecosystem.

TIPS:

- Prioritize destroying the "fatso kamikazes"-they inflict the most damage.
- Agile bombers are dangerous, but secondary.
- When the core is exposed, attack as aggressively as possible with heavy weapons.
- Never stand still during fire and artillery attacks, unless you want to end up like Kwongo.