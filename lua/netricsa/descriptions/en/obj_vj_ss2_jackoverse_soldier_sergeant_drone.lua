STORMTROOPER SERGEANT'S "KAMIKAZE" DRONE


CLASS:      Stormtrooper Sergeant's "Kamikaze" Drone
METABOLISM: Mechanical
PERCEPTION: Kamikaze Attack Drone, Suicide Bomber
SIZE:       1 ft
ENDURANCE:  Low
HOSTILITY:  Absolute
WEAPONS:    Self-Detonation
REWARD:     60 FC
THREAT:     Medium

DESCRIPTION:

This is one of Stormtrooper Sergeant's favorite "toys", crafted from old debris from previous battles. When long-dead Stormtroopers and enemies fell, Sergeant would scavenge their remains and transform them into something new. Thus was born this futuristic spherical drone with two retractable manipulators located on its sides, which it uses to move through the air.

The central part of the drone's body is black with a thin orange stripe encircling its equator. In the center of the front of the body is a red circular "eye".

When detecting an enemy, the Kamikaze drone rushes toward them at maximum speed. Its task is to fly to the target and simply crash into it. Upon contact with the target or upon receiving a signal from the Sergeant, the drone explodes, causing significant damage within its radius.

They have no ranged weapons-only self-destruct.

TIPS:

- Shoot down drones as they approach. One shot from a shotgun or assault rifle destroys them before they get close.
- Don't stand still.
- Use cover. Drones cannot fly around obstacles as quickly as they can attack directly.
