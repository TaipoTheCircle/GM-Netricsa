ZOMBIFIED PIGLIN CROSSBOWMAN FROM THE MINECRAFT NETHER


CLASS:       Zombified Piglin Crossbowman
METABOLISM:  Undead, pig-like humanoid
PERCEPTION:  Neutral Nether mob
SIZE:        6,3 ft
ENDURANCE:   Average
HOSTILITY:   Conditional
WEAPONS:     Golden Sword
REWARD:      350 FC
THREAT:      Average

DESCRIPTION:

A bipedal, zombified pig-like carcass, clad in scraps of once-golden armor.

This is what regular Piglin Crossbowmans turn into if they remain in the Overworld or the End for more than 15 seconds. They used to be called zombie piglins, but that name was later changed.

Unlike their living relatives, these piglins don't care about gold. They don't care about anything as long as they're left alone.

They can also emerge from regular pigs if they're struck by lightning. Or directly from Nether portals in the Overworld-the larger the portal, the more often.

Zombified piglins are immune to fire and lava-they burn but don't combust. However, they drown in water, as befits undead.

They heal from damage potions and take damage from healing.

TIPS:

- Never strike first unless you're ready to hit everyone. One hit and the whole pack is yours.
- They shoot quickly and painfully.
- They're helpless in water.
