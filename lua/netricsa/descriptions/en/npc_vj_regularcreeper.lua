CREEPER FROM THE MINECRAFT OVERWORLD


CLASS:      Creeper
METABOLISM: Plant-Chemical
PERCEPTION: Detect by vision
SIZE:       5,5 ft
ENDURANCE:  Low
HOSTILITY:  High
WEAPON:     Self-Detonation
REWARD:     100 FC
THREAT:     Medium-High

DESCRIPTION:

The Creeper is one of the most dangerous and recognizable creatures in the Overworld. It resembles a tall, green, armless creature with four short legs. Its body is covered in a camouflage pattern that allows it to blend easily with grass and foliage, making it a particularly cunning opponent.

The Creeper's main feature is its ability to detonate itself. When approaching a target, it begins to hiss and then explodes, causing serious damage to the enemy and the surrounding area. The creeper itself dies without showing any signs of self-preservation, making it more of a living weapon than a regular mob.

The origin of creepers remains a mystery.

TIPS:

- Can sneak up unnoticed
- Explosion destroys blocks and buildings
- When struck by lightning, transforms into a charged creeper, increasing the radius and power of the explosion
- Keep your distance and use a bow.
- A shield significantly reduces explosion damage.
- Avoid fighting creepers near buildings-they don't appreciate architecture.