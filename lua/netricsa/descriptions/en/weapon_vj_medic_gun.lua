COMBINE ELITE MEDICAL GUN


WEAPON: Combine Elite Medical Gun
AMMO:   AR2 Pulse Rounds / Biomed Capsules (Antlion Larvas)
ROF:    Healing Beam + Underbarrel Pulse Fire

DESCRIPTION:

The Combine Elite Medical Gun is a multifunctional tactical support tool that combines healing for allies and suppressive fire for the enemy. Its body features a white and gray color scheme with a black handle and a green glowing healing section.

A thin black emitter rod protrudes from the front of the gun, which generates a focused medical beam. The first fire mode creates a sustained healing stream that restores tissue and neuroimpulse activity in allied organisms using recycled biomaterials from antlion larval eggs.

The second mode activates an underbarrel pulse module that fires standard AR2 energy rounds directly under the healing beam's trajectory. This allows the medic to simultaneously heal allies and suppress enemies without switching weapons.

The system is designed for elite Combine units, where each soldier must combine support and firepower.

NOTES:

- The green section of the hull displays the healing agent level.
- Effective in mobile squads and elite assault teams.
- Inferior damage compared to full-power pulse rifles, but compensates for this with its healing function.
