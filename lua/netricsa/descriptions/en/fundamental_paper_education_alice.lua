DESCRIPTION:

The ultimate threat, greater than the principal of the Paper School itself, an entity imprisoned in a separate room marked with signs "DO NOT ENTER." In her normal form, she appears as a girl with short brown hair, wearing a crown and a bow, but her clothes are torn, and her skin is covered in scars and a strange rash. The true nature of the ∆Lice is a demonic, shadow-like creature with red sclera, huge claws, spikes, and tentacles. She is aggressive and murderous towards anyone who enters her room, but passive and even gentle with Oliver (her childhood friend and boyfriend). Her power surpasses even the most dangerous teachers, causing them to fear her.

She has full powers due to her power. (That's why she sometimes goes to the cafeteria with Oliver =3) Her appearance transforms the school's paper reality into something more tangible and terrifying. She opposes other imprisoned entities, such as HanseG. She considers the school her Territory.

