RAKE


CLASS:      Rake
METABOLISM: Scavenger
PERCEPTION: Nocturnal anthropomorphic predator
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Medium
WEAPONS:    Claws, surprise factor
REWARD:     100 FC
THREAT:     High

DESCRIPTION:

The Rake is a creature that exists only to the extent that a person is willing to believe it. An urban legend, fleshed out by eyewitness accounts, forum discussions, and stalker expeditions. Not a single documented fatal attack, but thousands of stories of something thin, pale, and clawed standing in the corner of a bedroom and watching until you open your eyes.

According to mythology, the Reik is a thin humanoid with pale skin, long, sharp claws, and sometimes canine features. It inhabits forests and sewers, avoiding sunlight due to its thin skin. It is active exclusively at night. It feeds on carrion, most often seen on the sides of roads, where it picks up roadkill. It is afraid of people and tries to avoid encountering them in person.

There are several subspecies. Forest Reiks live deep in dense forests, supposedly speak and gesture, are easy to communicate with, and pose no danger. Sewer Reiks are grayer, have a tail and an elongated snout, and move on all fours, leaning on their forepaws, like gorillas. They are considered dangerous, although there is no evidence to support this.

Nightly visiting creatures occupy a special place in the legend. The Reik supposedly enters houses, stands by the bed, and watches the sleeping person, waiting for them to awaken. Scientists attribute this to sleep paralysis: a person dreams of what they fear, and since the Rake is scary, he appears. Stalkers, meanwhile, travel through abandoned buildings and forests with thermal imaging cameras, hoping to catch him on camera.

