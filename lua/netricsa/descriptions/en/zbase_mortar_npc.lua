SWARM MORTAR BUG


CLASS:        Swarm Mortar Bug
METABOLISM:   Xenobiological
ROLE:         Artillery Unit
SIZE:         9,2 ft
DURABILITY:   High
HOSTILITY:    High
WEAPONS:      Explosive Biological Projectiles
REWARD:       700 FC
THREAT:       High

DESCRIPTION:

The Mortar Bug is a specialized artillery variant of the Swarm, designed to engage targets at long range. Instead of the acid glands typical of Rangers, this creature utilizes specialized organs that produce unstable explosive payloads.

When attacking, the Mortar Bug launches a volley of three projectiles in a high-arcing trajectory. Upon impact, they adhere to the surface and detonate after a few seconds. This allows the Swarm to restrict enemy movement, block passageways, and force prey out of cover.

Despite its imposing size and thick carapace, the Mortar Bug is ill-suited for close-quarters combat. Its primary threat lies in its long-range weaponry; consequently, these creatures usually remain behind the main Swarm forces, protected by Drones and other aggressive subspecies.

The Mortar Bug's hide is saturated with caustic biological compounds. Even after death, its tissues remain hazardous to touch for some time, making carcass harvesting an extremely unpopular task among field researchers.

TIPS:

- Do not linger near fallen projectiles.
- Eliminate Mortar Bugs before they can secure an advantageous position.
- Exercise caution when using cover against arcing fire.
- If three suspicious organic spheres suddenly appear on the floor, it is best to leave the area immediately.
