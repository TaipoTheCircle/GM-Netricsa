ILLUSON CREEPER CLONE FROM THE MINECRAFT OVERWORLD


CLASS:      Illusion Creeper Clone
METABOLISM: Illusory
PERCEPTION: Tactical Illusion, Living Deceiver
SIZE:       5,5 ft
ENDURANCE:  Infinite
HOSTILITY:  Absolute
WEAPONS:    Explosion Simulation
REWARD:     None
THREAT:     Psychological

DESCRIPTION:

This is not a true creeper, but a deceptive doppelganger created by the Illusion Creeper to intimidate its prey. It looks exactly like the original: the same proportions, the same texture, the same menacing hiss. But inside it, there's no gunpowder, no explosive flesh, not even the will to kill. There's only one purpose: to distract, confuse, and force your opponent to waste their energy on empty space.

The Clone has no physical form. When you try to strike it with a sword or shoot it with a bow, the weapon passes through it. It takes no damage, isn't knocked back, and doesn't explode.

However, the Clone is dangerous not physically, but tactically. In the chaos of battle, the victim may mistake it for a real creeper and either waste precious ammo on an empty space or, conversely, ignore the real one, mistaking it for a Clone. In the worst-case scenario, you can try to run away from the Clone and run into the real one waiting around the corner.

The Clone can simulate an explosion: it swells, hisses, and makes a characteristic sound, but upon detonation, it simply disappears without a trace, causing no damage or destroying any blocks.

TIPS:

- Don't waste resources on the Clone. You won't be able to kill it.
- Ignore the Clone and look for the real creeper.
- If there are multiple copies, the real one is most likely hiding somewhere to the side or behind your back.
- The Clone can't harm you. Don't be afraid of it.
