BLACK MESA, BREAKDOWNS IN THE SECTOR "C"


Okay, you're in Sector "C". There is a fuss at the control panel. A security guard and two scientists are trying to fix something. The buttons are not responding, the screens are blinking, the wires are smoking. The panel is seriously broken - someone pulled the levers not according to the instructions, someone was in a hurry.

The smell of ozone and burnt insulation hurts the nose. The floor is shaking, and there's this cold, not physical, that makes the back of my neck tingle. He's here.

The man in the business suit is somewhere nearby. You can't see him, but you know he's here. Suitcase in hand, smile like a blade. Watching. Does not interfere. He waits for the mechanism to break down completely.
