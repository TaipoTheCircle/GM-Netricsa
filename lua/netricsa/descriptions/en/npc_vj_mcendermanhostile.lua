GREEN-EYED ENDERMAN FROM THE MINECRAFT END


CLASS:      Green-Eyed Enderman
METABOLISM: Mysterious Alien
PERCEPTION: Neutral Teleporting Mob
SIZE:       9,5 ft
ENDURANCE:  High
HOSTITUDE:  Neutral
WEAPONS:    Punch
REWARD:     2000 FC
THREAT:     High

DESCRIPTION:

This is the original version of the same Enderman, but it was only available in the first pre-release of the Beta 1.8 update.

Its appearance was just as intimidating: a long, thin, completely black body, purple portal particles, and black smoke and eyes, but the eyes were green. Originally, when Notch first added the Enderman, he chose this color for them.

Why did the developer initially choose green and then change it to purple in the final Beta 1.8 release? It's not exactly clear. Jens Bergensten (Jeb) said this in an interview: "One morning, Notch said to me, 'Oh, I fixed the eye color, they should be purple now.' I said, 'OK.' And I'm still thinking about it. Why?" Most likely, this was done for consistency with the Ender portals and particles, which had just changed to purple.

Green eyes aren't just a simple color change. They trigger a whole chain of inconsistencies in the modern game. After all, it's because of them that Ender Pearls, dropped by Endermen, remain green to this day.

But green isn't the only difference with the Progenitor. Its behavior was far more destructive. Endermen could lift absolutely any block, including bedrock. Imagine this: an enderman comes at night and steals a chunk of your impenetrable obsidian wall or the world floor. Later, when players began complaining en masse about holes in the terrain, Notch had to seriously nerf this ability, allowing it to steal only certain types of blocks.

TIPS:

- We Broke the Fourth Wall.
- You won't encounter this enderman in the official version of the game. He was completely replaced by a purple version over ten years ago.
- If you're playing on the ancient Beta 1.8 pre-release, be careful: not only is he evil, but he can also steal your house block by block.
- A modern player can only see him through mods, texture packs, or in archival videos.
- Ender Pearls remain green as a tribute to this older version. It's a sort of "hello from the past".
- Despite the difference in eye color, the tactics for fighting him are the same as with a regular enderman: don't look into his eyes and he's afraid of water.