IMPERIAL LEGIONNAIRE FROM THE NIRN


CLASS:      Imperial Legionnaire
METABOLISM: Imperial, Male
PERCEPTION: Professional soldier of the Imperial Legion
SIZE:       5,9 ft
ENDURANCE:  High
HOSTILITY:  Conditional
WEAPONS:    Steel / Imperial sword, Imperial bow with steel arrows, Imperial shield
REWARD:     300 FC
THREAT:     High

DESCRIPTION:

This is the Empire's main military force, a professional army that has maintained order across Tamriel for hundreds of years. Its origins date back to the First Era, when the Alessian Legions first united Cyrodiil under human rule. In the modern era, despite wars and crises, the Legion remains a disciplined and formidable force.

The Legion's structure is organized and stern. It is divided into numbered legions and cohorts. Candidates are required to possess not only brute strength, but also endurance, virtue, and reliability. The Legion is renowned for its inclusive policy-members of all races and faiths are welcome into its ranks. Although the core of the Legion is Imperials, Nords, and Redguards, Bretons, Orsimer, and even Dunmer can be found among its soldiers.

A legionnaire's equipment depends on the era and location of service. During the Oblivion Crisis in Cyrodiil, they sported heavy steel armor with elements of Roman plate armor, armed with longswords. In Skyrim, the situation is more complex-resources are scarce, and the majority of soldiers wear lightweight leather armor with steel inserts. This is a necessary measure, as the Legion receives few reinforcements in the provinces and is forced to recruit local militias, sometimes lowering their standards.

Legionnaires are subject to strict discipline. They patrol roads, guard forts, and participate in full-scale battles. In Skyrim, they wage a protracted war against rebels. Legionnaires employ "sword and shield" tactics: they take enemy blows with their shields and then counterattack.

During the Oblivion Crisis, they were the elite, but the war with the Thalmor and the years-long crisis crippled the army. In Skyrim, soldiers are often dissatisfied with the Empire, but they serve it either out of tradition or because they consider it the only bastion against the Thalmor.

TIPS:

- If you plan to fight on the Empire's side, your equipment will be superior to that of your opponent. Legionnaires are well-armored and organized.
- When fighting a Legionnaire, remember their tactics. Don't rush head-on into a shield wall-use flanking maneuvers, magic, or ranged units.
- The best weapons against heavily armored legionnaires are spells and weapons that ignore armor.
- In the Province of Skyrim, legionnaires are more vulnerable due to their light armor. Use heavy weapons to quickly drain their health.
- If you're a criminal, stay away from the legionnaires. Unlike the city guard, they have the right to arrest you outside of towns.
- When you join the Legion, you swear a sacred oath of allegiance to the Emperor. Breaking it will be considered treason.
