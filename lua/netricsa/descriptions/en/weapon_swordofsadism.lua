SWORD OF SADISM


WEAPON: Sword of Sadism
AMMO:   Crystal Energy Resonance
ROF:    ~75 hits / min

DESCRIPTION:

A legendary crystalline blade crafted from a rare green emerald of unknown origin. The material possesses a super-dense lattice, allowing the blade to cut through almost any material, including Alliance armor and Strider-class flesh.

The HALF-QUAKE organizers found the artifact in ancient ruins and adapted it for the show. Upon impact, the sword creates a resonance wave that tears apart the target at the molecular level.

NOTES:

- Disintegrates most entities with a single strike.
- Effective at close range, catastrophically lethal to opponents in tight corridors.
- Requires a strong wielder.
