BLACK MESA WAREHOUSE PARKING LOT, H.E.C.U. SOLDIERS ON THE STREET


Well, Sam. Fresh air again. If you consider a cocktail of gunpowder, ozone, and a slightly leaky science team fresh, that is.

Before us is the Black Mesa warehouse parking lot. A place where people usually leave their cars. Today, bodies were left here. Scientists, Mental's soldiers, and H.E.C.U. soldiers among them, like an exhibit called "Situation Control." Spoiler: the situation is out of control.

Harold managed to say the main thing before he finally stopped talking. Doctor Rosenberg. The name sounds reassuring. People like that usually either save the world or accidentally blow it up again. Right now, he's busy with a more mundane task: being held hostage. Where exactly is unknown. But if the military is holding him, then he's still alive. H.E.C.U. They don't like to throw away useful things right away.

Pay attention to the stairs. There are soldiers there. Lots of them. Too many for a "we're only here for a short time" operation. And between them, Mental's forces flicker.

The portal Rosenberg is building might be our ticket out.

But there's no choice now.

We'll have to cross the street.