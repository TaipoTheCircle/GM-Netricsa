BIG-HEAD ZOMBIE, DINKO PAVICIC


CLASS:       Big-Head Zombie, Dinko Pavicic
METABOLISM: Humanoid, Male
PERCEPTION: Enthusiastic Fanatic
SIZE:        5,9 ft
ENDURANCE:     Low
HOSTILITY:   Neutral
WEAPONS:     None (except on I Am Ugh-Zan difficulty)
REWARD:      100 FC
THREAT:      Annoyance

DESCRIPTION:

Big-Heads are thin humanoids with exaggerated skulls containing the brains of Croteam's developers. They wear the Serious Sam uniform, but have the unique facial features of the development team. They display an obsessive enthusiasm, chasing you with cries of "Hello!" and "Hey, Wait!" They are completely harmless, but in the "I Am Ugh-Zan" difficulty they gain fire and ice modifiers. They appear spontaneously in secret areas, blocking passages with their sheer numbers.

Dinko Pavicic is one of the pillars of Croteam's visual style. His exaggerated skull is a direct result of his unique combination of artistic genius and strategic thinking. Starting out as a 2D artist on the first Serious Sam games, Dinko helped shape the franchise's striking and recognizable visuals in its early days.

However, his talent extended far beyond the gaming industry. In 2006, he was awarded an Emmy Award for Outstanding Title Sequence Design at the 78th Academy Awards, confirming his status as a world-class master of visual design. This unique experience-from creating pixel art for an iconic shooter to working on a major Hollywood event-makes him one of the most versatile and influential minds in the Krotimovtsy pantheon.

He was once alive, but after you killed him in the Temple of Hatshepsut, he rose from the dead, becoming a demonic Zombie.

TIPS:

- Use a combat knife or pistol to clear the path.
- On the "I Am Ugh-Zan" difficulty, eliminate them remotely due to their modifiers.
- A sledgehammer with a horizontal strike is effective in a group.