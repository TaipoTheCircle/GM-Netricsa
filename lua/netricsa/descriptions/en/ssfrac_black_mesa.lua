BLACK MESA


Black Mesa is a secret scientific corporation-a research complex located in the lifeless New Mexico desert. Funded by the US government, it's shrouded in secrecy: behind its pristine laboratories lie interdimensional experiments, advanced technology, and alien life research.

Black Mesa's staff is a fusion of scientists, security guards, maintenance staff, and administrators, unaware of the full scope of what's happening. They study everything: technology, biology, teleportation, theoretical physics, sometimes without fear of the consequences.