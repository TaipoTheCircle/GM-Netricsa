Y.S.M. AH-64 APACHE GUNSHIP


CLASS:      Y.S.M. AH-64 Apache Gunship
METABOLISM: Mechanical, Human-Piloted
PERCEPTION: Airborne Military Vehicle, Yellow Shield PMC
SIZE:       58 ft
ENDURANCE:  Very High
HOSTILITY:  Extreme
WEAPONS:    Machine Gun, Rockets, Homing Missiles
REWARD:     6000 FC
THREAT:     Catastrophic

DESCRIPTION:

The Y.S.M. Marines use this helicopter for the same purposes as the H.E.C.U.: destroying high-value targets, suppressing resistance in open areas, and providing security for raids on Delta Base.

It has a rapid-fire machine gun for clearing infantry, unguided rockets for destroying enemy groups, and sometimes homing missiles for hunting armored targets or enemy helicopters. The helicopter is piloted by a mercenary pilot, who likely has experience in the military or other PMCs. He knows what he's doing and isn't afraid to risk the aircraft to fulfill a contract.

Y.S.M. uses the Apache to provide cover for its raids and sometimes to intimidate surviving personnel.

The Apache can be shot down, but only with heavy weapons: RPGs, stationary large-caliber machine guns, or anti-tank missile systems. Small arms are useless.

TIPS:

- Cover is your best ally. Never fight the Y.S.M. Apache in the open.
- Explosives (RPGs, grenade launchers) are the only real countermeasure.
- Homing missiles are deadly. Move unpredictably and use cover with wide viewing angles.
- If the helicopter is shot down, stay away.
