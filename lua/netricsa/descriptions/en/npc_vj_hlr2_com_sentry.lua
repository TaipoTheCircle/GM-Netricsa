COMBINE SENTRY TURRET


CLASS:      Combine Sentry Turret
METABOLISM: Fully synthetic
PERCEPTION: Automated Defense System
SIZE:       ~4,5 ft tall 
ENDURANCE:  Medium 
HOSTILITY:  Extreme 
WEAPONS:    Dual Pulse Cannons
REWARD:     400 FC
THREAT:     Medium-High

DESCRIPTION:

The Combine Sentry Turret is an autonomous defense platform widely deployed across occupied territories. Mounted on a tripod base and powered by a compact Combine energy core, it can lock onto targets using motion and thermal detection systems, making it nearly impossible to sneak past.

Its dual pulse cannons unleash devastating suppressive fire, forcing enemy infantry into cover. Typically placed in choke points, corridors, and prison sectors like Nova Prospekt, these sentry Turrets provide relentless firepower and psychological intimidation, their humming and mechanical clicks signaling imminent death.

Though designed for maximum lethality, they lack mobility and depend on Combine Engineers for deployment and maintenance. Once positioned, however, they can hold entire sectors against human resistance.

TIPS:

- Use explosives or EMP-style attacks to disable them quickly.
- Avoid direct firefights; instead, flank their blind spots.
