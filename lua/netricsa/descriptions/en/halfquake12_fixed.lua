HALF-QUAKE SHOW, SOMOS


So, we've finally reached the director's booth of hell.

SOMOS stands in the center of the room, surrounded by an energy shield, like a king afraid of his own subjects. Conventional weapons are useless here. Bullets bounce off, like audience complaints about the show's politics.

The sadists turned out to be not only sadists, but also engineers. The machine maintains its "divine" state through a system of buttons. Four nodes. Four pain points. Four arguments against its eternity.

The buttons are guarded by Vortigaunts.

The irony: they don't even know that Mental's forces are considered allies. So they beat everyone in sight, including those supposedly "under contract".

Pure chaos, the perfect operating environment for you.

Disable the first four, and the next door opens. There's a button for the second stage of the captcha.

Yes, Sam. Even this hell has implemented two-factor authentication.

After that, the final door opens. The shield release button. Silence. The moment of truth.

And SOMOS will remain just a big, expensive, and very vulnerable pile of metal.