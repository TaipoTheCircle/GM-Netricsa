DESCRIPTION:

A compact self-loading/automatic submachine gun with a telescopic bolt and a simple inertia-recoil mechanism (open-bolt firing mechanism in early versions). Developed by Uzi Gal in Israel in the late 1940s and 1950s. The magazine is located in the pistol grip, which makes the weapon compact and convenient for concealed carry and control during single-handed firing. It features a simple design, high reliability in harsh conditions, and ease of maintenance. It was widely used by the military, police, and security forces and was produced in several versions (Standard, Mini, Micro) and modifications for other calibers.

NOTES:

- Compactness and high cyclic rate of fire are offset by relatively low accuracy at ranges over 50-100 m.
- Simple to manufacture and maintain, popular in the 20th century; civilian and export versions with fire mode restrictions exist.


