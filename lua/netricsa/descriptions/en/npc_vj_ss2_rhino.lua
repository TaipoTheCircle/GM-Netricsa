RHINO CYBER-TOY


CLASS:       Rhino Cyber-Toy
METABOLISM:  Cybernetically-Enhanced Mammal 
PERCEPTION:  Trained War Animal
SIZE:        6,5 ft
ENDURANCE:   Medium
HOSTILITY:   Extreme 
WEAPONS:     Horns
REWARD:      300 FC
THREAT:      High

DESCRIPTION:

The introduction of Cyber-Rhinos came as an unfortunate surprise to the human military during the War against Mental.

Having conquered numerous planets and brought billions of living beings under his control, Mental wanted to keep power in his own hands, and so it was decided to give every family in his gigantic empire a toy wind-up rhinoceros.

Mass production began.

Joy and happiness filled the world of suffering and sorrow Mental had created.

But, as soon as the war with Earth began, as soon as Sam Stone began to nip at the villain's heels, liberating one planet after another, the rhinoceroses were equipped with a motor and fuel tank and thrown into battle!

The rhinos are more aggressive and faster than their werebull cousins, thanks to cybernetic enhancements. The way to deal with them is the same; stay out of their way and use explosives. You'll have to be even more careful, though.

TIPS:

- Side-step to dodge their attacks.
- Use explosives to take them out.
