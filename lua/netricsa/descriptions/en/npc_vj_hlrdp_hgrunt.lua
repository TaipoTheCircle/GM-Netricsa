Y.S.M. MARINE


CLASS:       Y.S.M. Marine
METABOLISM:  Human, Male
PERCEPTION:  Yellow Shield PMC, Contract Marauder
SIZE:        5,9 ft
ENDURANCE:   Average
HOSTILITY:   High
WEAPONATORY: M4A1 Carbine, MP7, SPAS-12 Shotgun
REWARD:      500 FC
THREAT:      High

DESCRIPTION:

This is a member of a private military company that operates on the principle of "Hear a distress call, grab the loot bags". These mercenaries intercepted the signal from Delta Base and arrived at the facility not to rescue, but to loot.

Their appearance is typical of mercenaries of the late 1990s: tactical vests, vests, bandanas, goggles, and other gadgets. Their color scheme is dark gray with yellow accents (hence the name). On their shoulders and berets are patches with a shield and the abbreviation Y.S.M. They are armed with automatic weapons: MP7s, M4A1s, and occasionally SPAS-12 shotguns.

Their mission is to remove anything unsafe from the base: experimental equipment, research data, alien material samples, weapons, medicine, and even strange mushrooms that grew after a resonance cascade. All of this will then be sold on the black market to private corporations, governments, other PMCs, or criminal syndicates.

Y.S.M. was also hired specifically to seize and destroy certain data and devices related to teleportation. And to create a new type of private army, using captured technology to simply blackmail the government and corporations, using classified materials.

TIPS:

- Marines have armor. A regular pistol might not penetrate them the first time. Aim for the head.
- They use cover and sprints. Don't run into bullets in the open.
- Grenades and explosives are effective against groups.
- A headshot will blow it off.
- Don't try to negotiate. They don't care about your story. They want your things and your death if you're in the way.
