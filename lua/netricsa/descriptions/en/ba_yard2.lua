BLACK MESA SEWER, A WORKAROUND


Sam, you're in the heart of sewer hell. The air is thick as jelly, it smells of rot and something else... someone else's. It's not just pipes and drains - it's a maze.

You see cockroaches-they scurry across the floor, as if they know something you don't. So far, they're just watching, but it won't be for long. Headcrabs lurk in dark corners, sniffing, preparing to jump. Their movements are measured, like the ticking of a clock: one, two, three - and you are no longer alone. Somewhere in the depths there is a horde of Mentals hiding - you can't see them, but they are there: watching, waiting.

The hiss of steam from the smoking pipes is in the air.

The target ahead is a rusty elevator. It looks like it's about to fall apart, but this is your chance to rise higher.