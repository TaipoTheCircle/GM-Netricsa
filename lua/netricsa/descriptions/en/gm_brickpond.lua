BRICK POND, THE WIND SMELLS OF DEATH


Sam, this place is like that room in a horror movie where you're not supposed to go, but you already are because the door is open, and curiosity is your worst enemy.

At first glance, it's an ordinary neighborhood. Brick walls, a pond, a couple of houses. Silence, smoothness, the bliss of brick.

And then you notice the details. And you realize: someone died here. And not just died, but stayed here forever.

The brick walls form a kind of courtyard or dead end. In the center is a small pond. The water is still, like a mirror in a haunted house.

All around is silence. Only the wind. It's the main sound engineer here. Blowing, howling, whispering. It creates the feeling that you're not alone, even though there's not a soul around. Except for one.

What's here:

The first house is "Open." You can enter it. Inside are four boxes. And nothing else. No chairs, no tables, no Sophia's supplies. Just four empty boxes. Waiting for you to look inside and be disappointed.

The second house is "Closed." You can't enter. Is the door locked? Walled up? Simply not designed? It doesn't matter. The important thing is that behind the wall of this house, in a walled-up space, lies a mangled corpse. In its own blood. Someone didn't just die—they tore it apart. And left there, behind the wall. As a reminder. Or as a warning.

You can't see the corpse directly. But now you know it's there. Behind the wall. In a house you can't enter.

The wind. It's not just a background noise here. It's like the voice of this place. It fades, then grows stronger, then begins to howl so loudly that goosebumps run down even my spine, and I have no back.

A pond. Dark water in which no one swims.

This place is not for you. And not for me. It's for the one left behind the wall. "Forever" (Tazik29).
