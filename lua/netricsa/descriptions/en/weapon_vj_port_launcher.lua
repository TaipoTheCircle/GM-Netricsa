PORTABLE LAUNCHER


WEAPON: Portable Launcher
AMMO:   Heated Rocket Missiles (HEAT Warheads)
ROF:    ~17,6 rounds / min.

DESCRIPTION:

A heavy infantry weapon used by missile raiders to destroy armored vehicles, fortifications, and dense concentrations of targets. It is a portable launcher system that fires heated rocket projectiles with high penetrating power.

The shaped charge warhead is designed to destroy fortifications and penetrate heavy armor, while the explosion creates an additional damage zone dangerous to personnel. Despite its relatively low rate of fire, each missile possesses significant destructive power and can turn the tide of battle with a single, precise hit.

Due to its mass and limited rate of fire, the weapon is more often used from a distance or from cover, allowing missile raiders to support assault teams with heavy fire.

NOTES:

- Effective against armored vehicles, fortifications, and heavy targets.
- Explosion deals area damage and creates a shockwave.
- Most effective at medium and long range.
- Dangerous at close range; firing near the user often results in fatal consequences.
- The shaped-charge warhead is capable of destroying fortifications and penetrating heavy armor.
- The slow practical rate of fire is compensated for by its high destructive power.
