ПЕЩЕРНЫЙ ТРУТЕНЬ ИЗ ИЗМЕРЕНИЯ X


КЛАСС:        Пещерный Трутень
МЕТАБОЛИЗМ:   Плотоядный
ВОСПРИЯТИЕ:   Раса X, Лёгкая пехота
РАЗМЕР:       1,5-1,8 м
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Острые когти, шипы на голове (снаряд), быстрая атака
НАГРАДА:      200
УГРОЗА:       Средняя

ОПИСАНИЕ:

Пещерные Трутни - свирепые штурмовые существа, выведенные Расой X в качестве ударных войск. Обладая рептильными чертами, крючковатыми когтями и костяными гребнями на черепах, они предназначены как для жестокого ближнего боя, так и для смертоносного дальнего. Их отличительная особенность - метание острых костяных шипов изо лба, способных пробивать броню и плоть на средней дистанции. В ближнем бою они безжалостно рубят когтями, часто подавляя легковооружённых солдат.

Несмотря на свою свирепость, Пещерные Трутни не имеют тяжёлой брони и полагаются на чистую скорость и агрессию для уничтожения добычи. Они действуют стаями, что делает их особенно опасными в замкнутых пространствах, где уклонение ограничено. На Земле они впервые были обнаружены во время инцидента в Чёрной Мезе, часто охотясь стаями вместе с Шоковыми Солдатами.

СОВЕТЫ:

- Двигайся - неподвижное положение делает вас лёгкой мишенью для их головных шипов.
- Используй дробовик или MP5, чтобы быстро уничтожить их, прежде чем они подойдут близко.
- Гранаты или взрывчатка эффективны против групп.
- Не позволяй им заходить с фланга - они лучше всего действуют сообща.
