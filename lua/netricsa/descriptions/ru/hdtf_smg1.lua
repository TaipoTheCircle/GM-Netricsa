ПИСТОЛЕТ-ПУЛЕМЁТ HK MP7


ОРУЖИЕ:  HK MP7 
ПАТРОНЫ: 4.6x30 mm (паллеты высокой скорости, бронепробиваемые патроны)
СС:      950 выстр./мин
ВЕС:     1.9 кг.
ДЛИНА:   415 мм

ОПИСАНИЕ:

HK MP7 - компактный персональный оружейный комплекс (ПЗО), разработанный Heckler & Koch в конце 1990-х для замены пистолетов и лёгких пистолетов-пулемётов у экипажей и служб охраны. Стреляет специализированными 4.6x30 мм патронами высокой начальной скорости, которые обладают повышенной пробиваемостью современной лёгкой бронезащиты при сохранении низкого импульса отдачи. Конструктивно MP7 сочетает буллпап-подобную компактность, телескопический приклад (складывающийся/высувной), полностью амбидекстричные органы управления и рейки для навесного оборудования.

ПРИМЕЧАНИЯ:

- Отличается низкой отдачей и высокой скорострельностью — удобен для управления очередями в ближнем бою.
- 4.6x30 мм патрон специально разработан для пробития пластин лёгких бронежилетов при сопоставимых дистанциях.
- Компактность делает MP7 удобным для экипажей техники, спецназа и охраны VIP.
- Поддерживает множество аксессуаров: оптика, фонарик, лазер, глушители (в зависимости от модели).
- Эффективная дальность стрельбы примерно до ~200 м; вне этой дистанции точность и убойная сила снижаются по сравнению с полноразмерными винтовками.