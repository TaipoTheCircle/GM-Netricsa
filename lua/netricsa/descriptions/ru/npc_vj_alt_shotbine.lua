АЛЬТЕРНАТИВНЫЙ СОЛДАТ OVERWATCH С ДРОБОВИКОМ АЛЬЯНСА


КЛАСС:        Альтернативный солдат Overwatch с дробовиком Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Штурмовое подразделение ближнего боя Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Боевой дробовик Spas-12, гранаты
НАГРАДА:      700
УГРОЗА:       Высокая

ОПИСАНИЕ:

Альтернативный солдат Overwatch с дробовиком - это специализированный боец Альянса, созданный для зачистки помещений, тоннелей и плотной городской застройки. В отличие от стандартных элитных подразделений, он носит болотного цвета спецодежду, предназначенную для маскировки в индустриальных зонах, руинах и загрязнённых районах. Его визор светится оранжевым цветом.

Этот тип солдата не рассчитан на дальние перестрелки. Его задача проста и крайне неприятна для противника: быстро сократить дистанцию и превратить всё перед собой в облако обломков и плоти. Аугментации усиливают реакцию и болевой порог, позволяя бойцу идти вперёд даже под плотным огнём.

СОВЕТЫ:

- на ближней дистанции дробовик смертелен.
- В узких коридорах лучше отступить, чем проверять, сколько выстрелов ты выдержишь.
- Оранжевый визор хорошо заметен в темноте - иногда это единственный шанс увидеть его первым.