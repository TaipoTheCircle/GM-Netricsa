ТАКТИЧЕСКИЙ ПИСТОЛЕТ-ПУЛЕМЁТ STEYR TMP


ОРУЖИЕ:  Такческий Пистолет-Пулемёт Steyr TMP
ПАТРОНЫ: 9×19 mm Parabellum
СС:      900 выстр./мин 
ВЕС:     1.3 кг.
ДЛИНА:   282 мм

ОПИСАНИЕ:

Компактный австрийский пистолет-пулемёт разработанный Fried­rich Aigner и производившийся Steyr Mannlicher в 1990-х. Предназначен для высокомобильных тактических задач: лёгкий корпус из полимера, возможность установки глушителя (резьбовой ствол) и опциональный приклад для повышения управляемости. Дизайн обеспечивал жёсткую посадку в руке и хорошую эргономику для ведения очередями в коротких вспышках.

ПРИМЕЧАНИЯ:

- Высокая циклическая скорострельность делает TMP грозным в ближнем бою, но требует контроля короткими очередями.
- Эффективная дистанция примерно 100 м; нитевидная точка при установке приклада и оптики улучшает точность.
- Надёжность и компактность сделали его популярной базой для дальнейших модификаций, несмотря на относительную "нишевость" на рынке.