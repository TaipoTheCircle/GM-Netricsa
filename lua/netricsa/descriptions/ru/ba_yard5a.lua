СКЛАД ЧЁРНОЙ МЕЗЫ, ПОЧТИ У ЦЕЛИ


Учёный был немногословен. Но намёк он дал чёткий: Розенберг здесь. Заперт. В красном контейнере. За огромными дверями, которые выглядят так, будто их проектировали с расчётом на апокалипсис, а не на эвакуацию одного полезного человека.

Чтобы открыть эти двери, нужно нажать кнопку охраны. Маленькую. Скромную. Почти невинную. Именно такие кнопки в Чёрной Мезе отвечают за запуск цепочек событий.

Как только ты к ней приблизишься, H.E.C.U. внезапно вспомнят, что этот сектор был стратегически важен. Силы Ментала, в свою очередь, решат, что это отличное место для выяснения отношений со всем живым. Получится привычная сцена: все стреляют во всех, а виноват, разумеется, ты.

Как двери откроются. За ними будет контейнер. Красный. Опять. Я бы сказала, что цвет уже ничего не значит, но именно в нём снова чья‑то последняя надежда.

Когда путь будет открыт, придётся сделать ещё одну вещь. Отодвинуть поезд назад. Потому что даже спасение гениального учёного не может обойтись без небольшой логистической задачи. Чёрная Меза уважает традиции: сначала бой, потом работа грузчиком, без зарплаты.

Так что будь готов, Сэм.