ИМБИРНЫЙ ЧЕЛОВЕК С СЛАДКИХ ОСТРОВОВ БОЛЬШОГО ЛЕДЕНЦА


КЛАСС:        Имбирный человек Сладких Островов
МЕТАБОЛИЗМ:   Живая Съедобная Конструкция
ВОСПРИЯТИЕ:   Защитник и коренной житель Сладких Островов
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Ближний бой (удары руками и телом)
НАГРАДА:      450
УГРОЗА:       Средняя


ОПИСАНИЕ:

Имбирный человек Сладких Островов - один из главных защитников этой приторной, но смертельно опасной страны. Его тело изготовлено из плотного имбирного материала, создающего обманчивое впечатление мягкости и пухлости. На деле же он куда прочнее, чем кажется на первый взгляд.

Внешне он напоминает ожившее имбирное печенье: круглая голова с большими круглыми глазами, красный нос, неизменно застывшая улыбка и три красные пуговицы на животе. По краю "одежды" проходит белая сахарная кайма, а на руках и ногах расположены круглые красные элементы, похожие на карамельные вставки. Конечности выглядят мягкими, но при ударе действуют как упругие дубины.

Имбирный человек предпочитает ближний бой. Он старается сокращать дистанцию и обрушивать на противника свои "кулаки", используя массу тела и пружинистую структуру.

Несмотря на "дружелюбную" внешность, это полноценный защитник своей территории.

СОВЕТЫ:

- Не обманывайся внешним видом - удары болезненны.
- Эффективнее атаковать на дистанции.
- В ближнем бою он быстро навязывает темп.
- Его улыбка не означает дружелюбие.