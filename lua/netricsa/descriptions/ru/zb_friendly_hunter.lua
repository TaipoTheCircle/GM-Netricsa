ОХОТНИК ПОВСТАНЦЕВ СОПРОТИВЛЕНИЯ


КЛАСС:        Охотник Повстанцев Сопротивления
МЕТАБОЛИЗМ:   Биомеханический Cинтент
ВОСПРИЯТИЕ:   Тяжёлая единица поддержки Сопротивления
РАЗМЕР:       2,4 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Игломёт, Таран, Клинки
НАГРАДА:      700
УГРОЗА:       Очень Высокая

ОПИСАНИЕ:

Охотник Повстанцев Сопротивления представляет собой стандартного синтента Альянса, успешно захваченного и перепрограммированного инженерами Сопротивления. После взлома его системы управления были полностью переписаны, благодаря чему машина перестала подчиняться Альянсу и перешла на сторону восстания.

Внешне такой Охотник легко отличается от оригинала. Его корпус перекрашен в матовый чёрный цвет, привычное синее свечение сенсоров заменено ярко-оранжевым, а на верхней части корпуса нанесён хорошо узнаваемый символ Лямбды, ставший эмблемой Сопротивления.

Несмотря на смену владельца, боевые возможности машины практически не изменились. Охотник по-прежнему способен развивать высокую скорость, совершать длинные прыжки, преодолевать препятствия и мгновенно сокращать дистанцию до противника. Его мощные передние конечности используются для нанесения тяжёлых рубящих и колющих ударов, способных пробивать лёгкую броню и выводить из строя даже тяжёлую пехоту.

Основным оружием остаётся встроенный игломёт, однако перепрограммированная система использует оранжевые флетчетты. После попадания такие снаряды впиваются в цель или поверхность, а затем детонируют с небольшой задержкой, создавая опасность как для живой силы, так и для техники.

В отличие от синтентов Альянса, перепрограммированные Охотники распознают бойцов Сопротивления как союзников и активно поддерживают их в бою. Они сопровождают штурмовые группы, защищают укреплённые позиции и способны самостоятельно преследовать подразделения Альянса, пока угроза не будет полностью устранена.

Хотя эти машины теперь сражаются на стороне человечества, они остаются теми же смертоносными боевыми синтентами. Любая ошибка при перепрограммировании может привести к возвращению исходных протоколов, поэтому каждый такой Охотник постоянно находится под наблюдением инженеров Сопротивления.

СОВЕТЫ:

- Не задерживайся на открытой местности: флетчетты взрываются с небольшой задержкой и поражают большую область.
- После тарана Охотник ненадолго теряет скорость, что даёт возможность нанести ответный удар.
- Наиболее уязвимой частью остаётся область головы и центрального сенсорного блока.
- Мощное взрывное вооружение эффективно нарушает устойчивость синтента и позволяет быстрее вывести его из строя.
- Несмотря на символику Сопротивления, враждебный экземпляр сохраняет всю смертоносность оригинального Охотника Альянса.
