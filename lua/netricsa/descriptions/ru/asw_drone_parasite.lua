ТРУТЕНЬ-НОСИТЕЛЬ РОЯ


КЛАСС:        Трутень-Носитель Роя
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Штурмовая Особь
РАЗМЕР:       ~2 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Очень Высокая
ОРУЖИЕ:       Когти, Паразит-Симбионт
НАГРАДА:      120
УГРОЗА:       Высокая

ОПИСАНИЕ:

Трутень-Носитель является редкой красной разновидностью обычного трутня, отличающейся наличием взрослого паразита, прочно закрепившегося на верхней части его тела. В отличие от большинства известных случаев паразитизма, оба организма не наносят вреда друг другу, действуя как единое боевое существо.

Во время боя Носитель ведёт себя так же, как и обычный трутень, стремительно сокращая дистанцию и атакуя жертву длинными серповидными когтями. Паразит при этом остаётся неподвижным, ожидая гибели своего носителя.

После смерти трутня паразит мгновенно отделяется от тела и немедленно атакует ближайшее живое существо, пытаясь завершить жизненный цикл привычным для своего вида способом. Таким образом, уничтожение Носителя на близком расстоянии нередко оказывается лишь началом новой угрозы.

Происхождение подобного симбиоза остаётся неизвестным. Одни ксенобиологи считают его естественной стадией развития отдельных трутней, другие полагают, что паразиты используют взрослых особей исключительно как защищённое средство доставки к потенциальной добыче. В любом случае подобное сотрудничество заметно повышает выживаемость обоих организмов.

СОВЕТЫ:

- Не приближайся к Носителю после его гибели.
- После уничтожения трутня сразу же ликвидируй отделившегося паразита.
- Автоматическое оружие позволяет быстро устранить обе угрозы.
- Если есть возможность, уничтожай Носителя на открытой местности, чтобы паразиту было сложнее сократить дистанцию.
