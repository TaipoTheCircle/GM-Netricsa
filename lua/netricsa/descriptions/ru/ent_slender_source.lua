ОПИСАНИЕ:


Это тощий мужчина в чёрном похоронном костюме с белой рубашкой. Кожа у него бледная, конечности неестественно длинные и могут сгибаться в любых направлениях. Из спины иногда растут чёрные гибкие щупальца или придатки. А самое главное - полная безликость. На месте лица - гладкая пустота. Ни глаз, ни носа, ни рта.

Слендермен перемещается телепортацией. Появляется и исчезает бесшумно. При контакте вызывает сильные головные боли, помехи на электронике и чувство необъяснимого ужаса. Способен стирать или редактировать память, превращать людей в прокси (марионеток) и забирать детей в неизвестном направлении.

СОВЕТЫ:

- Если ты его видишь, значит, он уже видит тебя. Бежать бесполезно - он телепортируется.
- Не смотри в глаза. У него их нет.
- Помехи на камере или телефоне - первый признак того, что он рядом. Отключай технику и уходи.
- Единственный способ выжить - не думать о нём. Но теперь ты уже думаешь.
