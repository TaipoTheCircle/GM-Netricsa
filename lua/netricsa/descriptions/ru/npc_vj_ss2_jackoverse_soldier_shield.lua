ОПИСАНИЕ:

Щитовики являются ветеранами Штурмового корпуса и занимают одну из высших ступеней среди линейной пехоты Галактической Империи. Их почти полностью чёрная броня и длинный плащ служат знаком высокого статуса и богатого боевого опыта. Именно этим бойцам поручают прорыв укреплений, зачистку тесных помещений и устранение наиболее опасных противников.

Главным средством защиты щитовика служит массивный энергетический щит, способный выдерживать практически любые виды стрелкового вооружения. Пока щит находится между бойцом и целью, большинство атак оказываются бесполезными. Конструкция щита, однако, оставляет открытыми ноги владельца, а также не защищает его со спины, чем опытные противники нередко пользуются.

В ближнем бою щитовик применяет электродубинку. Несколько мощных ударов обычно достаточно, чтобы раздробить кости, вывести из строя броню или отбросить жертву на несколько метров. Когда противник держится на расстоянии, щитовик быстро перебрасывает через щит сразу три осколочные гранаты, вынуждая врага покинуть укрытие.

Если же цель оказывается вне досягаемости дубинки и гранаты уже израсходованы, боец достаёт имперский пистолет. Щитовик производит короткую серию из трёх быстрых выстрелов с удивительной точностью, после чего привычным движением проворачивает оружие на пальце и возвращает его в кобуру. Эта показная манера обращения с пистолетом считается своеобразной традицией элитных подразделений Империи и одновременно служит демонстрацией полного спокойствия перед следующим противником.

СОВЕТЫ:

- Не стреляй в щит. Щиту всё равно.
- Заходи с фланга или целься по ногам.
- Не подпускай его вплотную. Электродубинка заканчивает спор очень быстро.
- Если увидел три летящие гранаты, значит пора менять позицию.
