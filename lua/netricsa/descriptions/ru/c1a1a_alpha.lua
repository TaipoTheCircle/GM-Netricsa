КАМЕРЫ ДЛЯ ПРИШЕЛЬЦЕВ ЧЁРНОЙ МЕЗЫ, ЛЮТЫЙ ЗООПАРК


Сэм, помнишь, я говорила про "может, обойдётся"? Забудь. Каскадный резонанс уже здесь. Мы опоздали. Или прибыли вовремя - зависит от того, как ты относишься к ситуации, когда вокруг руины, а из вентиляции доносится чужое шипение.

Мы спустились на лифте и попали в сектор, который местные, видимо, называли "зоопарком". Здесь держали инопланетян. В камерах. Для изучения, для анализов, для галочки в отчёте. А теперь камеры открыты. И учёные, которые вчера тыкали в хедкрабов палочками, сегодня запираются в этих же клетках, надеясь, что бетонные стены и решётки спасут их от того, что они же и выпустили.

Гениальный план, ничего не скажешь. "Мы не можем их остановить, поэтому присоединимся к ним в качестве живого рациона".

Хаундаи скулят в клетках, хедкрабы прыгают из-за углов, а где-то в глубине, судя по звуку, кто-то крупный решил, что сегодня день открытых дверей.

Ситуация: мы в секторе содержания, между нами и выходом - лабиринт из камер, перекрытых коридоров и обезумевших учёных, которые либо смотрят на нас сквозь решётки умоляющими глазами, либо уже не смотрят никуда.

Задача: добраться до офисов. До очередных офисов в Чёрной Мезе, которых здесь, кажется, больше, чем патронов в твоём рюкзаке. Потому что именно в офисах, по логике тех, кто выжил, должна быть связь, карты, ключи-карты и, возможно, чудо в виде работающего лифта на поверхность. Или хотя бы на другой уровень этого ада.

Особенность: сил Ментала пока не видно. Ни их солдат, ни их техники. Только зеновская живность, только учёные, которые неудачно выбрали место для укрытия, и только мы с тобой. И тишина. Тяжёлая, напряжённая, нарушаемая только каплями воды из прорванных труб и звуками того, что когда-то было образцами, а теперь стало охотниками.

Где все силы Ментала? Потому что их отсутствие здесь пугает меня больше, чем их присутствие.

Погнали. И не смотри в глаза тем, кто остался за решёткой. Они уже всё решили за себя.
