ЧЁРНАЯ МЕЗА, ЗАТОПЛЕННЫЕ ЛАБОРАТОРИИ 


Мы упёрлись в тупик. Буквально. Коридор заканчивается, дальше только несколько комнат, и каждая - маленький персональный ад.

Первая комната: бассейн по-чёрномезовски

Пол затоплен. Вода мутная, пахнет озоном и чем-то горелым. Посередине, прямо в луже, плавает разбитая лампа. Она искрит. Каждые пару секунд - вспышка, треск, и вода оживает синим светом. Стоять в ней нельзя. Плыть? Даже не думай.

Причина - лампа упала в воду и теперь превратила это помещение в электрический суп. Света от неё почти ноль, зато напряжения хватит, чтобы поджарить кого угодно. Даже тебя, Сэм. Даже в твоей броне.

Решение - на стене, справа от входа, висит рубильник. Старый, ржавый, но, судя по всему, рабочий. Один щелчок - и лампа погаснет, вода станет просто водой, а мы сможем пройти. Вопрос: успеешь ли ты до него добраться, пока искры не превратили весь пол в одну большую розетку?

Дальше - последнее помещение. И здесь, Сэм, нас ждут.

Хаундаи. Не один, не два. Целое гнездо. Шуршат, повизгивают, ждут. А между ними - хедкрабы. Мелкие, юркие, те, которые обычно падают с потолка в самый неподходящий момент.

Комната усеяна ими. Как ковром. Каждый шаг - риск, что наступишь на хвост, каждое движение - приглашение к обеду. Они не спят, они не ушли искать другие территории. Они здесь. И они голодны.

Стратегические заметки:

Рубильник. Первым делом отключаем ток. Можно попробовать выстрелить по лампе, но от этого вода может стать только хуже. Ручной режим - надёжнее. И быстрее. Надеюсь.

Вода. После отключения - всё равно не лезь. Мало ли что там плавало до нас.

Комната с тварями. Тут без вариантов. Либо мы их, либо они нас. Узкий проём на входе - шанс устроить коридор смерти, но хаундаи умеют обходить. Хедкрабы - прыгать. Лучшая тактика - гранатой в центр и добивать тех, кто выжил. Если, конечно, граната не укатится в угол и не разозлит их ещё сильнее.

Рубильник ждёт.
