Красная лаборатория. Оправдывает свой цвет. Не стенами - содержимым. А в жидкости - те, кого сюда привезли, чтобы изучать.

Инопланетные пехотинцы. Когда-то их выловили, усыпили, залили формалином и поставили в ряд, как экспонаты. Но они не мертвы. Они ждут своего часа. И сегодня, кажется, их выходной.

Потому что вся лаборатория усеяна силами Ментала и H.E.C.U. Они здесь. Одновременно.

В одном помещении. Это похоже на пороховую бочку, где кто-то курит, кто-то чиркает зажигалкой, а кто-то принёс канистру бензина просто для компании.

Учёного ищем быстро, но не в ущерб безопасности. Когда найдёшь - оцени обстановку. Если путь к выходу чист - веди. Если нет - жди, пока враги разойдутся или отвлекутся.

