ТЕХНОЛОГИЧЕСКИЙ ЦЕНТР ЧЁРНОЙ МЕЗЫ, КОРИДОР НАУЧНОГО БЕЗУМИЯ


Сэм, мы в месте, которое даже по меркам Чёрной Мезы выглядит странно. Длинный коридор. С изгибом. И двери. Много дверей. За каждой - лаборатория. Небольшая, но у каждой своя тема. Своя одержимость. Своя маленькая катастрофа, которая ждёт, чтобы её выпустили.

Мы идём по коридору. Слева - дверь. Справа - дверь. В конце - поворот, а там - новые двери. Никаких окон. Никаких указателей, кроме табличек на самих дверях. Чёрная Меза изучала здесь всё, что не помещалось в другие сектора. Всё, что казалось слишком бесполезным для основного бюджета, но слишком интересным, чтобы выбросить.

Что за дверями?

Цветной свет. Лаборатория, где изучали влияние разных длин волн на психику. Результаты, вероятно, где-то в сейфах. Или на стенах - в виде пятен, которые остались после тех, кто смотрел слишком долго.

Пробы дроидов. Тестовые образцы роботов. Некоторые - просто болванки. Некоторые - с включённым питанием. 

Прозрачность. Исследования материалов, которые пропускают свет, но не пропускают пули. Или наоборот. Или ничего не пропускают, кроме страха.

Пантерай. В одной из комнат обнаружили живое существо. Не образец. Не экспонат. Просто пантерай. Откуда он взялся? Зачем его сюда привезли? Почему он до сих пор здесь? Вопросов больше, чем ответов. Ответ один: не входи туда без оружия.

PolyRobot Zentraedi. Тактический боевой модуль. Создан здесь. Испытан здесь. Наверное, признан успешным. Или нет. Но модуль где-то там, за одной из дверей. Ждёт. Смотрит. Слушает. И Танцует...

Не открывай все двери подряд. Если табличка выглядит угрожающе - возможно, так и есть. Если табличка выглядит безобидно - вероятно, это ловушка.
