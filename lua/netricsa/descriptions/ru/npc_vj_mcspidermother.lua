МАТЬ-ПАУК С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Мать-паук
МЕТАБОЛИЗМ:   Членистоногое, паукообразное
ВОСПРИЯТИЕ:   Враждебный моб, лидер семейства
РАЗМЕР:       0,9 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Чрезвычайно высокая 
ОРУЖИЕ:       Укус, прыжки, лазание по стенам координация стаи
НАГРАДА:      300
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это не просто одиночный хищник, а настоящий матриарх, путешествующий по Верхнему миру в окружении своего выводка. В отличие от обычных пауков, которые ведут одиночный образ жизни, мать-паук никогда не одна.

С наступлением темноты этот опасный зверь выходит на охоту в сопровождении 3–6 детёнышей пауков, один из которых сидит у неё на спине. Внешне мать-паук напоминает обычного паука, но её поведение кардинально отличается. Она - центр боевой единицы, и вся стая действует как единый организм.

Мать-паук ведёт себя так же, как и обычный паук: атакует противника при недостаточном освещении или будучи спровоцированной. Но главная опасность заключается в её детёнышах. Все малыши неотступно следуют за матерью и атакуют любую цель, которую выбрала она. Если ударить мать-паука, на вас набросится вся стая. Если ударить детёныша - мать и остальные братья и сёстры придут в ярость.

Детёныши, сидящие на спине матери, особенно коварны - они защищены её телом и могут внезапно спрыгнуть в самый разгар боя.

СОВЕТЫ:

- Убивай детёнышей первыми. Чем меньше маленьких пауков останется в живых, тем слабее будет атака стаи.
- Не давай окружить себя. Мать-паук и её выводок будут пытаться взять тебя в клещи.
- Используй оружие массового поражения. Один меткий удар мечом с заклинанием "Взрывная волна" или правильно брошенное зелье вреда могут уничтожить нескольких детёнышей за раз.
- Будь осторожен в тёмное время суток. Мать-паук водится только ночью и всегда со своим выводком.
- Награда за убийство матери невелика - несколько нитей и редкий паучий глаз, но безопасность очищенной от монстров территории того стоит.
