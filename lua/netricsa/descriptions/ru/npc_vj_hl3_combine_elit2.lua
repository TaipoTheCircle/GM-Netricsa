ЭЛИТНЫЙ АРКТИЧЕСКИЙ КОММАНДО OVERWATCH АЛЬЯНСА


КЛАСС:        Трансчеловеческий пехотинец, элитного штурмового отряда
МЕТАБОЛИЗМ:   Аугментированный человек, Мужчина
