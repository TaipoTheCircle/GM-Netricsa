ПЯТНИСТАЯ СВИНЬЯ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Пятнистая свинья
МЕТАБОЛИЗМ:   Теплокровное млекопитающее
ВОСПРИЯТИЕ:   Жизнерадостное Пассивное животное
РАЗМЕР:       ~1 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Отсутствует
НАГРАДА:      30
УГРОЗА:       Минимальная

ОПИСАНИЕ:

Одна из самых очаровательных разновидностей обычной свиньи. Эта пятнистая свинья невероятно мила. Ей нравится жить с друзьями-свиньями и разыскивать с ними сласти по травяным равнинам.

Внешность у этой свиньи трогательная и узнаваемая. Основной окрас такой же тусклый, как у бледной свиньи, но по всей шкуре разбросаны округлые коричневые пятна. Выглядит она так, будто искупалась в шоколаде, но не полностью, а только местами, или словно её поцеловало солнце через листву.

По поведению ничем не отличается от обычной свиньи: бродит по травянистым равнинам в компании сородичей, избегает лавы и обрывов, при получении урона убегает на несколько секунд, но главное её занятие - поиск сластей и радость от жизни в кругу друзей-свиней.

СОВЕТЫ:

- Главная ценность пятнистой свиньи - в её исключительной милоте.
- Разводи таких свиней, чтобы получить потомство с тем же жизнерадостным окрасом.
- Пятнистая свинья отлично смотрится в загоне на травянистой равнине - её естественной среде обитания.
- Если ты ищешь сласти по равнинам в компании пятнистых свиней, значит, ты уже достиг дзена.
