РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Оборонительная / Союзническая
ОРУЖИЕ:       Глок-17, Дубинка
НАГРАДА:      40
УГРОЗА:       Низкая

ОПИСАНИЕ:

Тэд Барни - верный охранник Чёрной Мезы. Вооружённый стандартным оружием и лёгкой броней, он помогает защищать персонал и помещения. Он быстро реагирует на угрозы, но не так хорошо подготовлен и вынослив, как бойцы H.E.C.U. или спецназа.

Тэд надёжен в боевых условиях, но из-за ограниченной выносливости ему требуется поддержка в крупных перестрелках.

СОВЕТЫ:

- Держи его рядом для поддержки.
- Используй укрытие - он не может выдержать сильный огонь в одиночку.
- Эффективен при обороне коридоров и контрольно-пропускных пунктов.
