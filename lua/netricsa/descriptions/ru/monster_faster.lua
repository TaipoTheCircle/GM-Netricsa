БЫСТРАЯ ТЁТЯ С ФЭВЕРСХОЛЬМА


КЛАСС:        Быстрая Тётя
МЕТАБОЛИЗМ:   Порождение кошмара
ВОСПРИЯТИЕ:   Засадный убийца
РАЗМЕР:       1,7 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Лезвия-протезы вместо рук и ног
НАГРАДА:      250
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это один из самых ярких и пугающих монстров, которого впервые встречается в жилом доме.

Это изуродованная женщина в синем окровавленном платье. У неё отсутствуют глаза и рот - вместо них зияют чёрные дыры. У неё нет рук и ног в привычном понимании: вместо них - длинные, острые металлические шипы. Этими лезвиями она рубит и вонзает в жертв. Из-за того что на ногах такие же придатки, при ходьбе слышится характерный металлический лязг.

Быстрая предпочитает действовать из засады. Она прячется в тёмных углах или за препятствиями, чтобы внезапно выскочить, сломать дверь и напасть. При атаке она громко вопит, пытаясь напугать. Бегает она довольно медленно, но из-за внезапности и криков сбивает с толку.

При убийстве Быстрая не просто падает. Она пронзает свою шею лезвиями, издавая болезненный, грустный вопль, и падает на землю.

Как и большинство монстров, Быстрая символизирует травмы. Металлические шипы вместо конечностей олицетворяют металлическое инвалидное кресло.

Она представляет собой людей из окружения, которые были источником раздражения и боли. Акт самоубийства монстра при смерти - прямая параллель с суицидальными наклонностями.

СОВЕТЫ:

- Будь внимателен в длинных коридорах со множеством дверей и тёмных углов. Быстрая любит выпрыгивать внезапно.
- Монстр довольно медленно бегает, но из-за криков и внезапности легко запаниковать. Не паникуй.
- Попасть в Быструю сложно из-за её скорости. Лучшая тактика - дождаться, пока она попытается нанести удар, отойти и начать обстрел с хорошим прицеливанием.
- Можно попробовать рукопашный бой: подбежать, ударить, отбежать. Но это рискованно.
- Если ты слышишь металлический лязг и пронзительный крик - она рядом. Приготовь оружие и осмотрись.
