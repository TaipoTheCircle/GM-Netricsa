КРАБМИТ C МОБИУСА


КЛАСС:        Крабмит
МЕТАБОЛИЗМ:   Животно-Аккумуляторный
ВОСПРИЯТИЕ:   Огневая точка
РАЗМЕР:       1,2 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Энергетические сферы
НАГРАДА:      500
УГРОЗА:       Средняя

ОПИСАНИЕ:

Когда потребовалось организовать оборону на открытых пространствах, доктор Эггман обратил внимание на крабов. Их манера передвигаться боком и щёлкать клешнями идеально легла в концепцию мобильного стрелкового дрона. Так появился Крабмит.

Корпус красный, брюхо жёлтое, четыре ноги с когтистыми ступнями позволяют перебираться по любой поверхности. Из панциря торчат две гибкие антенны и стебельки глаз с фоторецепторами. Но главное - это клешни. Внутри каждой спрятан генератор импульсов. Крабмит не бегает за жертвой, он методично расхаживает туда-сюда и ждёт. Как только замечает движение, останавливается, наводит клешни и выпускает либо два сферических заряда, летящих по дуге.

СОВЕТЫ:

- Атакуй, пока он целится. В этот момент он не реагирует на внешние раздражители.
- Не стой на линии выстрела - сферы падают сверху.
