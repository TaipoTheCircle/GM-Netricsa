ЧЁРНАЯ МЕЗА, У ВОРОТ ОХРАННОГО КОМПЛЕКСА


Ну что, приехали! Пункт назначения - охранный комплекс Чёрной Мезы. Проход разрешён. Похоже, твоя маскировка пока срабатывает.

Но прежде чем шагнуть внутрь, обернись. За тобой - ещё один поезд. В нём едет он. Не тот Сэм, которого спутали с Фрименом. Не двойник. Настоящий. Гордон Фримен. Опоздал? Или, может, всё идёт по какому‑то невидимому расписанию?

Он проезжает мимо - молчаливый, сосредоточенный, с этим своим характерным взглядом Скалы. И где‑то там, за стенами, уже тикают часы до катастрофы.