ЯЙЦО ПАРАЗИТА РОЯ


КЛАСС:        Яйцо Паразита Роя
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Инкубатор паразитов
РАЗМЕР:       2 м.
ВЫНОСЛИВОСТЬ: Очень Низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Выпуск Паразитов
НАГРАДА:      50
УГРОЗА:       Высокая

ОПИСАНИЕ:

Яйцо Паразита Роя представляет собой специализированную инкубационную камеру, предназначенную для развития паразитов. Оно покрыто тонкой эластичной оболочкой, внутри которой находится густая питательная жидкость, необходимая для созревания эмбриона.

В спокойном состоянии яйца остаются неподвижными, ожидая появления живых организмов. При приближении потенциальной добычи оболочка мгновенно раскрывается, выпуская паразита, который немедленно атакует ближайшую цель. После вылупления яйцо быстро теряет жизнеспособность и больше не способно производить новых особей.

Яйца обычно располагаются группами внутри ульев, вентиляционных шахт, тоннелей и других влажных участков, где поддерживаются благоприятные условия для развития потомства. Их нередко можно обнаружить рядом с коконами личинок и другими инкубаторами, образующими полноценные зоны размножения Роя.

Несмотря на неподвижность, большое количество яиц способно превратить безопасный проход в смертельно опасную ловушку. Даже если взрослые особи уничтожены, оставшиеся кладки позволяют Рою быстро восстановить численность.

СОВЕТЫ:

- Уничтожай яйца до того, как приблизишься к ним.
- Огонь позволяет безопасно очищать заражённые участки с большим количеством кладок.
- После раскрытия оболочки сосредоточь огонь на вылупившемся паразите.
- Если поблизости находится несколько яиц, старайся не активировать их одновременно.
