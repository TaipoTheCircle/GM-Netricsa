БЛЕДНАЯ СВИНЬЯ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Бледная свинья
МЕТАБОЛИЗМ:   Теплокровное млекопитающее
ВОСПРИЯТИЕ:   Пассивное животное Снежных Биомов
РАЗМЕР:       ~1 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Отсутствует
НАГРАДА:      50 
УГРОЗА:       Минимальная

ОПИСАНИЕ:

Редкая разновидность обычной свиньи, приспособившаяся к жизни в снежных биомах и отличающаяся от своих розовых собратьев более тусклым, бледным оттенком кожи. Недостаток пигментации, вызванный суровыми условиями обитания, с лихвой компенсируется её очарованием, как справедливо отмечают местные жители.

По поведению бледная свинья ничем не отличается от обычной: она бродит по заснеженным равнинам, избегает лавы и обрывов, при получении урона убегает на несколько секунд.

СОВЕТЫ:

- Ищи бледных свиней в снежных биомах - там они чувствуют себя как дома.
- Недостаток пигмента не влияет на полезность: их можно разводить, сажать на седло и использовать как источник пищи.
- Бледная свинья отлично маскируется на снегу - будь внимателен, чтобы не пройти мимо.
- Если скрещивать бледную свинью с другими вариантами, окрас поросят может быть любым из родительских.
