УЛЬТРАФИОЛЕТОВЫЙ КОП ГРАЖДАНСКОЙ ОБОРОНЫ АЛЬЯНСА


КЛАСС:        Ультрафиолетовый Коп Гражданской Обороны Альянса
МЕТАБОЛИЗМ:   Человек под контролем Альянса
ВОСПРИЯТИЕ:   Специальное полицейское подразделение подавления
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Револьвер, SMG, мэнхеки
НАГРАДА:      400
УГРОЗА:       Средняя


ОПИСАНИЕ:

Ультрафиолетовый Коп Гражданской Обороны Альянса - это специализированный городской силовик, предназначенный для работы в заражённых, химически опасных и нестабильных зонах. Его полный защитный костюм подчёркивает не столько элитность, сколько функциональность и выживаемость в условиях, где обычный метро-коп долго не протянет.

Шлем полностью закрывает лицо и оснащён голубоватым визором с системой V-HUD. На поверхности можно заметить маркировки "V-HUD", "PREX INDUSTRIAL" и "ARAVO-08", указывающие на промышленное происхождение оборудования и его экспериментальный характер. Встроенный респиратор фильтрует воздух, защищая носителя от газа, спор и ультрафиолетового излучения, отсюда и название подразделения.

Бронежилет чёрного цвета с белыми элементами несёт маркировку "C17:i4o", что напрямую связывает бойца с Сити 17 и внутренними силовыми структурами Альянса. Экипировка дополняется тёмными штанами, чёрными сапогами и перчатками, а на поясе всегда присутствует кобура для быстрого реагирования.

В бою ультрафиолетовый коп действует жёстко, но не по-военному. Револьвер используется для точечного устрашения и казней, SMG - для подавления толпы, а мэнхеки выпускаются в замкнутых пространствах, превращая улицы и подъезды в хаотичную зону смерти. Эти копы часто работают в связке, зачищая кварталы без лишнего шума... по меркам Альянса.

СОВЕТЫ:

- Не недооцени его из-за "полицейского" статуса - мэнхеки быстро меняют ход боя.
- Визор защищает от ослепления, но не от прямых попаданий.
- В закрытых помещениях приоритет - убить его до запуска мэнхеков.