ПИСКУН (ПРЫГУН) С АРХИПЕЛАГА КРИГЕРА


КЛАСС:        Пискун (Прыгун)
МЕТАБОЛИЗМ:   Триген-мутировавший человек
ВОСПРИЯТИЕ:   Тактический диверсант
РАЗМЕР:       5,9 фута
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокий
ОРУЖИЕ:       Автоматические винтовки AG36, P90, прыжковые атаки
НАГРАДА:      800
УГРОЗА:       Высокая

ОПИСАНИЕ:

Пискуны - наиболее интеллектуальная разновидность тригенов, сохранившая тактическое мышление. Созданные на основе солдат, они узнаются по военным брюкам, стрижкам и отсутствию рубашек. Их конечности гипертрофированно мускулисты, а ротовая щель растянута до подбородка. Металлические нарукавники выше локтей - следы экспериментов. Способны координировать действия других мутантов, используя их как живую диверсию.

Основная тактика - мобильность. Совершают прыжки на дистанции, недоступные другим тригенам, постоянно меняя позиции. В ближнем бою используют слабые, но отвлекающие удары. Критически уязвимы при прерывании прыжка - в этот момент теряют скорость и не могут контратаковать.

СОВЕТЫ:

- Стреляй при подготовке к прыжку - это обездвиживает их на 2-3 секунды.
- Используй оружие с высокой скорострельностью - их подвижность делает прицельные выстрелы неэффективными.
- Контролируй высоту - они часто атакуют с верхних уровней и крыш.