ЭЛИТНЫЙ ТРАНСПОРТНИК АЛЬЯНСА


КЛАСС:        Элитный транспортёр Альянса
МЕТАБОЛИЗМ:   Полностью синтетический
ВОСПРИЯТИЕ:   Высокоскоростная переброска элитных и стандартных юнитов
РАЗМЕР:       ~17-19 м. (с контейнером)
ВЫНОСЛИВОСТЬ: Экстремальная
ВРАЖДЕБНОСТЬ: Критическая 
ОРУЖИЕ:       Отсутствует
НАГРАДА:      6000
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Элитный Транспортник Альянса - усовершенствованный синтетический десантный носитель, предназначенный для быстрой переброски элитных подразделений Альянса, включая элитных солдат и стандартные ударные группы. В отличие от стандартных транспортёров, данная модель оптимизирована под максимальную скорость и минимизацию боевого профиля.

Корпус синтента окрашен в чёрно-белую схему с усиленными композитными панелями, что визуально отличает его от обычных транспортников. Контейнер рассчитан на доставку 3-5 бойцов за один заход. После высадки контейнер остаётся открытым даже при отходе транспортника, что ускоряет цикл переброски и снижает время нахождения в зоне поражения.

Оружейные модули отсутствуют, так как концепция платформы строится на принципе "быстро вошёл - выгрузил - свалил". Элитный транспортник практически неуязвим для стандартного вооружения и уязвим только для ракет RPG V2, где двух попаданий достаточно для уничтожения или критического вывода из строя.

СОВЕТЫ:

- Используй RPG V2, другие типы оружия неэффективны.
- Достаточно 2 точных попаданий, чтобы повалить транспортник.
- Атакуй во время снижения и высадки, когда траектория предсказуема.
- Контейнер не закрывается при отходе, что делает его уязвимым для выстрела по внутреннему отсеку.
- Уничтожение элитного транспортника может сорвать переброску элитных войск и изменить ход боя.
