ЭЛИТНАЯ МЕДИЦИНСКАЯ ПУШКА АЛЬЯНСА


ОРУЖИЕ:  Элитная медицинская пушка Альянса
ПАТРОНЫ: Импульсные снаряды AR2 / Биомед-капсулы (личинки муравьиных львов)
CC:      Луч лечения + подствольный импульсный огонь

ОПИСАНИЕ:

Элитная медицинская пушка Альянса - многофункциональный тактический инструмент поддержки, совмещающий лечение союзников и огневое подавление противника. Корпус выполнен в бело-серой цветовой схеме с чёрной рукояткой и зелёной светящейся секцией лечущего вещества.

Из передней части инструмента выступает тонкий чёрный стержень-излучатель, через который формируется направленный медицинский луч. Первый режим огня создаёт устойчивый лечебный поток, восстанавливающий ткани и нейроимпульсную активность союзных организмов с помощью переработанных биоматериалов из яиц личинок муравьиных львов.

Второй режим активирует подствольный импульсный модуль, стреляющий стандартными энергетическими снарядами AR2 прямо под траекторией лечебного луча. Это позволяет медику одновременно лечить союзников и подавлять противника без смены оружия.

Система разработана для элитных подразделений Альянса, где каждый боец должен совмещать функции поддержки и огневой мощи.

ПРИМЕЧАНИЯ:

- Зелёная секция корпуса отображает уровень запаса лечащего вещества.
- Эффективно в мобильных отрядах и элитных штурмовых группах.
- Уступает полноценным импульсным винтовкам по урону, компенсируя это функцией лечения.
