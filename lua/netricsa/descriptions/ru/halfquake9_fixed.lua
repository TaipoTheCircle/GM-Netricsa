ШОУ HALF-QUAKE, КАНАЛИЗАЦИЯ ДОМА


Испытания окончены.

Не потому что шоу решило нас отпустить - просто декорации закончились.

Ты вывалился в канализацию. Без зрителей, без диктора, без аплодисментов. Запах сырости, старых труб и чужих неудачных решений. Это уже не сцена. Это закулисье мира, где никто не притворяется.

Мы сбежали. Но не победили. и не сорвали шоу. Просто ушли, выскользнули между сценариями, как ошибка в трансляции, которую никто не успел вырезать.

Сканирование окружения даёт занятную деталь: канализация проходит под домом. Чьим-то домом. Судя по структуре, вещам и количеству странных предметов - художника.

Где-то сверху - жилые комнаты, лестницы, двери. И, что важнее, выход. Не парадный, конечно. Скорее тот, о котором хозяин предпочитает не вспоминать.

Задача ясна и наконец-то человеческая:

пробраться в дом,

не застрять в чужом искусстве,

найти дорогу наружу.

Без испытаний.

Без кнопок.

Без аплодисментов.

Просто побег, Сэм.

И иногда это самая приятная часть пути.