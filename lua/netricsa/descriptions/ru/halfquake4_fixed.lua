Ну конечно.

Повтор. Потому что если зрителю понравилось, значит, нужно сделать то же самое. Только хуже.

Мы снова там, где уже были. Те же стены, знакомые очертания, ощущение дежавю, которое здесь считают частью дизайна. Но теперь всё "улучшено". Лава больше не предлагает выбор из трёх плит. Вместо них - одна вертящаяся платформа. Крутится уверенно, без сомнений, как будто знает, что равновесие - понятие временное.

За лавой шипов стало больше. Заметно больше. Организаторы явно решили, что прошлый раз ты двигался слишком уверенно. Исправляют.

Лифта больше нет. Его убрали. Заменили другим проходом. Куда он ведёт - пока не ясно.

Войска Ментала, разумеется, никуда не делись. Они тоже получили апгрейд. Новые позиции, больше агрессии, меньше терпения. Им нравится, когда сценарий повторяется, но исход всё ещё не гарантирован.

Они проверяют, насколько хорошо ты усваиваешь боль.

Насколько быстро адаптируешься.

И насколько сильно их разочаруешь.