ГНИЛОЙ ЗОРГ С БИЛМЕЗА IV


КЛАСС:        Гнилой Зорг
МЕТАБОЛИЗМ:   Млекопитающее
ВОСПРИЯТИЕ:   Ветеран Расы Воинов
РАЗМЕР:       2 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       SBC Пушка
НАГРАДА:      700
УГРОЗА:       Высокая

ОПИСАНИЕ:

Ментал переправил этих солдат на Землю из неизвестной области вселенной. Раньше это была раса свирепых воинов, но когда они завоевали столько планет, что уже не знали, что с ними делать, то решили немного притормозить. Большая их часть отправилась в бессрочный отпуск на прекрасную тропическую планету в своей звёздной системе, но поскольку они беспечно загорали с десяти утра и до четырёх часов вечера, через несколько лет кожи у большинства зоргов просто не осталось. 

Через несколько лет последствия стали необратимыми. Кожа буквально сгнила, ткани начали разлагаться, а некогда могучие воины превратились в ходячие полуразложившиеся туши.

Любой здравомыслящий командир списал бы таких бойцов со службы. Но Ментал никогда не отличался здравомыслием.

Обнаружив целую армию полуразвалившихся ветеранов, он решил вернуть их в строй и вооружил тяжёлой SBC Пушкой. Это мощнейшее орудие выпускает огромное ядро, способное уничтожить практически любую цель одним попаданием. Проблема лишь в том, что сами Гнилые Зорги владеют этим оружием далеко не идеально. Нередки случаи, когда неудачный выстрел заканчивается гибелью самого стрелка.

Несмотря на своё ужасающее состояние, эти существа сохранили боевой опыт десятков военных кампаний. Они медлительны, легко получают смертельные ранения и значительно уступают по живучести даже обычным наёмникам Ментала, однако одно точное попадание из их пушки способно мгновенно изменить исход боя.

По иронии судьбы, именно эти разложившиеся ветераны являются одними из старейших солдат армии Ментала. Они участвовали в первых завоевательных походах Орды ещё задолго до вторжения на Землю и были переброшены сюда из далёкого, неизвестного людям сектора галактики.

