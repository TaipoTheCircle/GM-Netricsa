АНТИ-НЕКСТБОТ ЛАЗЕР V2


ОРУЖИЕ:  Анти-Некстбот Лазер V2
ПАТРОНЫ: Импульсная энергия
СС:      Непрерывный луч

ОПИСАНИЕ:

Радикально переработанная версия V1, отказавшаяся от избирательности в пользу абсолютного удаления. Оружие генерирует стабильный пурпурный луч, который не просто наносит урон, а полностью стирает всё, с чем соприкасается.

В отличие от предыдущей модели, здесь отсутствуют режимы - луч работает по одному принципу: удалить цель из существования, будь то Некстбот, проп или любой другой объект. Используется в ситуациях, когда обычные методы не дают результата или цель должна быть устранена без следов.

ПРИМЕЧАНИЯ:

- Удаляет всё: врагов, пропы, конструкции и укрытия.
- Не различает цели - высокая опасность для союзников.
- Эффективен на любой дистанции при прямом наведении.
- Полное уничтожение без остатков или обломков.

