СКРЫТАЯ ШАРОВАЯ МИНА АЛЬЯНСА


КЛАСС:        Автономная скрытая мина-перехватчик
МЕТАБОЛИЗМ:   Синтетический механизм с фазовой маскировкой
ВОСПРИЯТИЕ:   Продвинутые датчики движения + ближний скан цели
РАЗМЕР:       0,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Чрезвычайно высокая
ОРУЖИЕ:       Прыжковый подрыв с направленным взрывом
НАГРАДА:      500
УГРОЗА:       Высокая

ОПИСАНИЕ:

Скрытая Шаровая Мина - это усовершенствованная версия стандартной Шаровой Мины Альянса, созданная для засад и мгновенной ликвидации цели. В активном состоянии она полностью невидима, используя фазовую маскировку и подавление отражений, из-за чего её невозможно обнаружить визуально.

При фиксации движения в зоне поражения, мина совершает резкий прыжок в сторону цели и взрывается в воздухе, не давая времени на реакцию или отступление.

Особенностью конструкции является полная изоляция внутренних систем. Вода, влага и даже полное погружение не влияют на её работоспособность. Это позволяет размещать скрытые Шаровые Мины в затопленных секторах, под снегом, во льду и в болотистой местности.

СОВЕТЫ:

- Укрытия малоэффективны - подрыв происходит в прыжке, над уровнем земли.
- На обезвреживание Мины уходит 3-4 ракеты "Инферно"