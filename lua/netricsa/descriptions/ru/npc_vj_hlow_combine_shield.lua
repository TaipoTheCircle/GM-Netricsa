ОПИСАНИЕ:

Солдат Overwatch со щитом - это мобильная стена Альянса, созданная для продвижения под огнём. Он не носит тяжёлую броню, полагаясь на массивный чёрный щит с маркировкой CMB, который закрывает его почти полностью. Щит покрыт композитным покрытием и способен выдерживать автоматный огонь и осколки.

Синие визоры выдают стандартную модификацию Overwatch, но поведение этого бойца отличается. Он не ищет укрытия - он сам становится укрытием. Солдат продвигается медленно, прикрывая союзников, позволяя штурмовым подразделениям наступать за его спиной.

Пистолет используется из-за щита одной рукой, чаще всего для подавления на короткой дистанции. В ближнем бою солдат может таранить пистолетом, сбивая противников с ног и открывая их для добивания.

Такие бойцы применяются в коридорах, при зачистке помещений, подавлении бунтов и уличных операциях, где важна фронтальная защита. Альянс использует их как движущиеся баррикады, под которыми наступает вся остальная пехота.

СОВЕТЫ:

- Не стреляй в щит - лучше обходи с фланга или сверху.
- Взрывчатка и огонь эффективно выбивают его из строя.
- Он уязвим сбоку и сзади, особенно без поддержки.
- Если он приближается вплотную - готовься к ударам.