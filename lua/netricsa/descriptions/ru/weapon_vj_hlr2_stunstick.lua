ЭЛЕКТРОШОКЕР АЛЬЯНСА


ОРУЖИЕ:      Электрошокер Альянса
ПАТРОНЫ:                  Нет (рукопашное, электрическое)
CC:         ~90 ударов/мин

ОПИСАНИЕ:

Электрическое оружие ближнего боя, используемое солдатами Combine для ведения боя в непосредственной близости. Наносит мощные электрические удары, способные мгновенно вывести из строя большинство органических целей. Легкое и эффективное оружие для контроля толпы или в качестве оружия последней инстанции.

ПРИМЕЧАНИЯ:

- Высокий урон в ближнем бою с оглушающим эффектом.
- Неограниченное использование, боеприпасы не требуются.
- Эффективно только на близком расстоянии.
- Может комбинироваться с другими атаками для максимального контроля толпы.