ПОГРАНИЧНЫЙ МИР ЗЕН, ЗЕН-РЕТРАНСЛЯТОР


Вот он, Сэм. Найден. Цел. И, по меркам Зена, даже выглядит гостеприимно.

Осталось только включить.

Когда учёные Чёрной Мезы возились с телепортацией, они довольно быстро поняли одну вещь: Зен участвует в процессе.

Решение показалось элегантным. Взять огромное кристаллическое образование зениума, подключить к нему устройство и превратить его в точку фокусировки. Так появился Зен-ретранслятор. Узел, который должен был направлять телепортацию, как маяк в мире, где само направление - понятие условное.

Исследовательская команда, отправленная сюда для установки устройства, исчезла вскоре после завершения работ. Не драматично. Просто перестала выходить на связь. Учёные на Земле сделали соответствующие выводы, придумали другой способ нацеливания и... бросили устройство. Зен, как водится, возражать не стал.

Перед тобой настройщик. Крупный аппарат. Пара движущихся стержней, соединённых с кристаллом. Маленький генератор, который всё это подпитывает. Стержни управляются через консоль и выглядят так, будто их лучше не трогать без крайней необходимости. Что, разумеется, именно то, что нам предстоит сделать.

Этот ретранслятор был частью сети из трёх элементов.

Первый - ядро комплекса "Лямбда".

Второй - ракета на орбите Земли. (Другой Ты уже сделал это)

Третий - он. Здесь. В Зене.

Так что вперёд, Сэм.

Включи устройство.

Дай Зену понять, что ты всё ещё играешь по его правилам.

А куда нас вернёт...

Ну, это уже детали.
