АССАСИН ПРИШЕЛЕЦ АЛЬЯНСА


КЛАСС:        Экспериментальный ударный юнит Альянса
МЕТАБОЛИЗМ:   Инопланетный организм, частично модифицированный
ВОСПРИЯТИЕ:   Обострённые сенсоры движения и тактическое восприятие
РАЗМЕР:       2,3 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Экстремальная
ОРУЖИЕ:       Когти, ножи, гранаты-ловушки, дымовые устройства
НАГРАДА:      800
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Ассасин-пришелец - разработанный как высокомобильный солдат Альянса. Он представляет собой гуманоидного пришельца с крайне бледной кожей, красными шипами на голове и дыхательной маской. Из его глазниц выходят трубки, а руки заканчиваются длинными острыми когтями, способными рассекать жертву одним движением. На ногах - всего два пальца.

Ассасин отличается крайней скоростью и живучестью. В бою он постоянно меняет позиции, выполняя прыжки, перекаты и резкие рывки, редко задерживаясь на одном месте. Он способен выпускать дымовую завесу, внезапно исчезая из поля зрения, а затем атаковать с фланга или с тыла. В его арсенал входят метательные ножи и гранаты-ловушки, предположительно размещённые на спине.

На левой руке ассасина расположена характерная оранжевая сфера, визуально роднящая его с крематором. Сходство в строении предплечий и плеч предполагает возможное биоинженерное родство между этими видами, хотя назначение ассасина было более скрытным и точечным.

СОВЕТЫ:

- Держи дистанцию - ближний бой с ним почти всегда проигрышен.
- Не стой на одном месте - он любит атаковать из укрытий.