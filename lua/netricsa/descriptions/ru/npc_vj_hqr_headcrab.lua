ОБЫЧНЫЙ ХЕДКРАБ С ШОУ HALF-QUAKE


КЛАСС:         Хедкраб Шоу HALF-QUAKE, Обычный
МЕТАБОЛИЗМ:    паразитическая ксенофауна
ВОСПРИЯТИЕ:    Биологическое оружие
РАЗМЕР:        ~0,6 м.
ВЫНОСЛИВОСТЬ:  средняя
ВРАЖДЕБНОСТЬ:  высокая
ОРУЖИЕ:        укус, пожизненное прикрепление
НАГРАДА:       150
УГРОЗА:        Средняя

ОПИСАНИЕ:

Этих тварей ЧЁРНАЯ МЕЗА вывела специально под заказ Садистского Шоу HALF-QUAKE. Руководство проекта хотело получить обычных хедкрабов, но с узнаваемым брендированием, чтобы зритель с первого взгляда понимал: это не просто паразит с Зена, это корпоративный продукт.

На каждой особи, прямо на верхней части головы, выжжено чёрное клеймо ">)". Выжигали, судя по всему, ещё на стадии личинки - хитин запёкся ровными слоями, и теперь этот знак не смыть и не содрать. Официальная версия гласит, что клеймо нужно для учёта поголовья и предотвращения краж.

Неофициальная - что продюсерам HALF-QUAKE просто нравится смотреть, как эта хрень прыгает на лица с их логотипом на макушке.

В остальном это обычные зеновские хедкрабы: чуют тепло, прыгают с потолка, вгрызаются в череп и превращают человека в овощ. Но теперь они ещё и ходячая реклама.

СОВЕТЫ:

- Если увидел клеймо ">)" - знай, за эту тварь заплатили огромные деньги. Ей есть что доказывать.
- Прыгают с любой поверхности выше твоего роста. Смотри по сторонам.
- На земле беспомощны - бей, пока не прыгнули.

