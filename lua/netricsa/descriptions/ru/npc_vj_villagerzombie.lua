ЗОМБИ-ЖИТЕЛЬ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Зомби-житель
МЕТАБОЛИЗМ:   Инфицированный Деревенский Житель
ВОСПРИЯТИЕ:   Враждебный моб 
РАЗМЕР:       1,9 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Руки
НАГРАДА:      200
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это результат трагической встречи обычного жителя с обычным зомби. Достаточно одного укуса, чтобы зазевавшийся торговец превратился в это бледное подобие себя, сохранив лишь узнаваемый нос и остатки одежды. Внешне он отличается от обычного зомби характерной житейской мордой и более крупными размерами - на 0,9 см. выше собратьев, что иногда позволяет ему застревать в проёмах.

Зомби-жители появляются в тех же местах, что и обычные зомби, но с меньшим шансом. Иногда их можно встретить в тайге и в иглу, где один из таких зомби сидит в подвале за решёткой.

Поведение зомби-жителя копирует обычного зомби: он преследует жертв, жителей и железных големов, может ломать двери и подбирать предметы. Но есть одно важное отличие, ради которого многие готовы рисковать жизнью - его можно вылечить.

Процесс излечения выглядит так: сначала зомби-жителя ослабляют зельем или взрывной тыквой, потом дают золотое яблоко. Через несколько минут начинается тряска, а ещё через минуту-другую житель возвращается к нормальной жизни. Вылеченный житель предлагает скидки на торговлю, иногда символические - вплоть до одного изумруда за всё.

СОВЕТЫ:

- Если видишь зомби-жителя - не убивай сразу. Отгороди его от других зомби, чтобы потом вылечить.
- Для лечения нужно зелье слабости и золотое яблоко. И то и другое можно скрафтить или найти в сундуках.
- В подвалах иглу всегда есть один зомби-житель и всё необходимое для его излечения.
- Вылеченный житель даёт хорошие скидки. Если повезёт, можно получить алмазный инструмент за пару изумрудов.
- Детёныши-зомби-жители тоже существуют. Они такие же быстрые и лечатся по тем же правилам.
- Зомби-жители могут спауниться верхом на курице. Редко, но бывает.
