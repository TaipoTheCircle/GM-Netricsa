САМОЗАРЯДНЫЙ ПИСТОЛЕТ AR-15


ОРУЖИЕ:     AR-15
ПАТРОНЫ:    Патроны 5.56
CC:         ~20 выстрелов/мин

ОПИСАНИЕ:

Пистолет AR-15 - короткоствольное оружие, собранное с использованием ствольной коробки от винтовки AR-15, но с деталями для создания пистолета, который можно держать и стрелять одной рукой.

ПРИМЕЧАНИЯ:

- Из-За калибра данный пистолет имеет хорошую пробиваемость.
- Не путать с винтовкой AR-15.






