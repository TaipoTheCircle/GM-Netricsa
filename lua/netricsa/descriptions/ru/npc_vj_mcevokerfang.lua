КЛЫКИ ПРИЗЫВАТЕЛЯ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Клыки призывателя
МЕТАБОЛИЗМ:   Магический снаряд
ВОСПРИЯТИЕ:   Магический снаряд заклинания Призывателя
РАЗМЕР:       0,8 м.
ВЫНОСЛИВОСТЬ: Нет
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Укус
НАГРАДА:      10
УГРОЗА:       Высокая 

ОПИСАНИЕ:

это не оружие, не инструмент и не живое существо в привычном понимании. Это чистая магия, материализованная в форме зубов, вырывающихся из-под земли, чтобы сожрать тебя заживо. Они не существуют сами по себе, а являются частью арсенала Призывателя - одного из самых опасных колдунов среди Илладжеров.

Когда Призыватель готовится к атаке, его руки начинают светиться фиолетовым. Раздаётся низкий звук, похожий на трубный глас, и через долю секунды из земли прямо перед тобой вырастает ряд шипастых клыков. Они смыкаются, наносят урон и тут же исчезают, словно их и не было.

Клыки бывают двух типов. Если ты стоишь далеко от Призывателя, он призовёт 16 клыков в линию, которая вытянется прямо к твоей позиции. Если же ты осмелишься подойти ближе, чем на три блока, колдун изменит тактику: вокруг него появится два кольца клыков - внутреннее из пяти и внешнее из восьми. Эта атака не оставляет тебе пространства для манёвра, если ты оказался в зоне поражения.

Клыки наносят 6 единиц магического урона на любой сложности. Это значит, что они полностью игнорируют твою броню. Единственная защита - зачарование "Защита", которое способно поглотить часть магии. Клыки не наносят урон другим Илладжерам, поэтому Призыватель может бить сквозь толпу своих приспешников, не боясь задеть их.

Если ты сражаешься в узком коридоре или пещере, клыки могут не появиться - им нужна твёрдая поверхность для генерации. Однако в открытом поле они достанут тебя везде.

СОВЕТЫ:

- Как только увидел фиолетовые частицы вокруг рук Призывателя - немедленно уходи с прямой линии. Клыки идут по прямой.
- Не стой слишком близко к Призывателю, иначе попадёшь под кольцевую атаку. Дистанция - твой главный союзник.
- Клыки не пробивают стены, но могут достать через углы, если есть прямая видимость.
- Лучшая защита от клыков - это убить самого Призывателя до того, как он успеет их призвать.
- Используй щит. Удар клыков можно заблокировать, в отличие от многих других магических атак.
