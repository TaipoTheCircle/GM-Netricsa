СОЛДАТ OVERWATCH С НАПАЛМОВЫМ ГРАНАТОМЁТОМ АЛЬЯНСА


КЛАСС:        Солдат Overwatch с напалмовым гранатомётом Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, Мужчина
ВОСПРИЯТИЕ:   Подавление территорий и выжигание укрытий
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Напалмовый гранатомёт Альянса (очередь из 3 гранат)
НАГРАДА:      750
УГРОЗА:       Очень высокая


ОПИСАНИЕ:

Солдат Overwatch с напалмовым гранатомётом - это специализированная вариация стандартного гранатомётчика, созданная для тотального выжигания территории. Он носит пустынный камуфляж, предназначенный для операций в засушливых зонах, промышленных пустошах и руинах, где огонь становится главным союзником Альянса.

Внешне он напоминает обычного гранатомётчика, но красные визоры сразу выдают его истинное назначение. Его гранатомёт стреляет так же - очередями по три снаряда, однако вместо обычных взрывов гранаты моментально разрываются облаками напалма, заливая всё вокруг липким огнём.

Напалм воспламеняется сразу при контакте с поверхностью, не оставляя времени на реакцию. Пол, стены, укрытия и даже тела превращаются в источники огня. Такие солдаты используются для зачистки окопов, нижних уровней зданий, вентиляционных шахт и районов, где противник пытается укрыться или закрепиться.

СОВЕТЫ:

- Не стой на одном месте.
- Красный визор хорошо заметен сквозь дым и пламя.
- Ликвидируй бойца до первого залпа.