КРАСНЫЙ МАРМЕЛАДНЫЙ МИШКА С СЛАДКИХ ОСТРОВОВ БОЛЬШОГО ЛЕДЕНЦА


КЛАСС:        Красный мармеладный мишка
МЕТАБОЛИЗМ:   Медведе-подобный желатиновый организм
ВОСПРИЯТИЕ:   Мелкий хищник Сладких Островов
РАЗМЕР:       ~0,6 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Прыжок, укусы
НАГРАДА:      150
УГРОЗА:       Низко-Средняя


ОПИСАНИЕ:

Красный мармеладный мишка - один из самых распространённых, но коварных обитателей Сладких Островов Большого Леденца. Внешне он выглядит как обычная мармеладная конфета в форме медведя: красное тело, округлые лапы и застывшее выражение морды. Именно эта внешность чаще всего и становится причиной гибели неосторожных гостей.

Несмотря на небольшой рост, мармеладные мишки крайне агрессивны. Они передвигаются короткими рывками и атакуют внезапно, прыгая на жертву всем телом. После прыжка они вцепляются зубами.

Чаще всего охотятся стаями.

СОВЕТЫ:

- Не подпускай близко - прыжок происходит почти мгновенно.
- Уничтожай первыми при появлении.
- Не путай их с обычными сладостями.