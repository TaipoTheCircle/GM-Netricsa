МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Штурмовое подразделение ближнего боя Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Боевой дробовик Spas-12, гранаты
НАГРАДА:      700
УГРОЗА:       Высокая

ОПИСАНИЕ:

Арктический солдат Overwatch с дробовиком - это модифицированная версия стандартного альтернативного штурмовика Альянса, адаптированная для действий в условиях крайнего холода, снежных бурь и обледеневшей инфраструктуры.

В прошлом многие из них были солдатами, полицейскими или военными, служившими в северных регионах до Прихода Альянса. После захвата Земли их опыт был признан ценным, а тела - пригодными для глубокой аугментации. Личность была подавлена, физиология перестроена под постоянный холод.

Он носит бело-серый арктический камуфляж с оранжевыми вставками. Под бронёй встроены системы терморегуляции, позволяющие солдату двигаться без замедления там, где обычный человек замерзает за минуты.

Так же, как и его неарктический аналог, этот солдат не предназначен для дальнего боя. Его роль - агрессивное сближение, прорыв обороны и зачистка замёрзших помещений, бункеров и тоннелей.

СОВЕТЫ:

- Вблизи он так же смертоносен, как и стандартный.
- Бело-серый камуфляж делает его труднозаметным на снегу.
- В узких ледяных коридорах отступление часто единственный шанс выжить.