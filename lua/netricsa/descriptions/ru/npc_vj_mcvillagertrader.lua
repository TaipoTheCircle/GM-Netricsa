СТРАНСТВУЮЩИЙ ТОРГОВЕЦ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Странствующий Тторговец
МЕТАБОЛИЗМ:   Человек, Мужчина
ВОСПРИЯТИЕ:   Бродячий торговец / Экзотический поставщик
РАЗМЕР:       1,9 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Пассивная
ОРУЖИЕ:       Отсутствует
НАГРАДА:      150
УГРОЗА:       Минимальная

ОПИСАНИЕ:

Загадочная фигура в синем балахоне с капюшоном, скрывающим лицо, чьё происхождение остаётся тайной даже для жителей деревень. Этот таинственный странник бродит по Верхнему миру в поисках клиентов, готовых обменять изумруды на редкие и экзотические товары.

Внешность торговца заметно отличается от обычных жителей. Вместо традиционной одежды на нём длинный синий халат с капюшоном, скрывающим лицо, и накидка того же цвета.

В любом путешествии торговца сопровождают две торговые ламы, привязанные к нему поводками. Эти выносливые животные не только несут груз, но и защищают хозяина: если ударить торговца, ламы немедленно начинают плеваться в обидчика. Сами ламы после смерти хозяина могут быть приручены - достаточно подождать, пока они успокоятся.

В отличие от жителей, торговец не покупает предметы - только продаёт . Его ассортимент случаен и включает редкие товары, которые сложно получить.

Главная хитрость торговца - выживание. С наступлением ночи или при виде враждебного моба он выпивает зелье невидимости, становясь почти незаметным, хотя поводки к ламам по-прежнему видны. На рассвете он смывает эффект молоком. Если зомби убьёт торговца, тот не превратится в зомби-жителя.

Убийство торговца снижает твою репутацию - последующие торговцы будут появляться реже, а их цены станут выше.

СОВЕТЫ:

- Всегда имей при себе запас изумрудов - никогда не знаешь, что интересного принесёт торговец.
- Если нужны его ламы, лучше подождать, пока торговец исчезнет сам - тогда ламы останутся и их можно приручить.
- При нападении на торговца будь готов к плевкам от его лам.
- Если видишь только плавающие поводки - значит, торговец выпил зелье невидимости и где-то рядом.
- В тёмное время суток торговец уязвим - зелье невидимости не делает его полностью неуязвимым, просто скрывает.
