БИОМАССА РОЯ


КЛАСС:        Биомасса Роя
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Органическая Структура Улья
РАЗМЕР:       Переменный
ВЫНОСЛИВОСТЬ: Практически Неуязвима
ВРАЖДЕБНОСТЬ: Пассивная
ОРУЖИЕ:       Едкие Ткани, Защитные Феромоны
НАГРАДА:      150
УГРОЗА:       Средняя

ОПИСАНИЕ:

Биомасса представляет собой живую органическую ткань, из которой Рой строит ульи, тоннели и другие крупные структуры. Она постепенно разрастается, покрывая стены, пол и потолки плотным слоем волокнистой плоти, превращая окружающую среду в продолжение самого улья.

Предполагается, что для роста Биомасса перерабатывает любые доступные органические материалы. Исчезнувшие растения, животные и даже разумные существа могут стать строительным материалом для новых участков улья, навсегда утратив свою первоначальную форму.

Несмотря на отсутствие способности самостоятельно передвигаться, Биомасса остаётся опасной. Её поверхность покрыта едкими выделениями, вызывающими химические ожоги при контакте. Кроме того, некоторые её разновидности способны выделять особые феромоны, которые при повреждении или сжигании привлекают ближайших представителей Роя для защиты заражённой территории.

Обычное вооружение практически не причиняет Биомассе вреда. Её ткани устойчивы к кинетическим боеприпасам, взрывам и большинству видов энергетического оружия. Наиболее эффективным способом уничтожения остаётся длительное воздействие высоких температур, буквально выжигающих органическую структуру.

Появление Биомассы считается одним из первых признаков формирования полноценного улья. Если заражение не остановить вовремя, вскоре за неподвижной плотью последуют и её защитники.

СОВЕТЫ:

- Сжигай Биомассу огнемётами или другим зажигательным оружием.
- Не прикасайся к её поверхности без необходимости.
- Будь готов к появлению Роя после уничтожения крупных скоплений.
- Чем больше Биомассы вокруг, тем выше вероятность, что где-то поблизости находится сердце улья.
