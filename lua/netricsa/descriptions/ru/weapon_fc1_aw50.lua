ТОЧНАЯ МЕЖДУНАРОДНАЯ AW50


ОРУЖИЕ:   AW50
ПАТРОНЫ:  .50 BMG (12.7x99 mm NATO)
СС:       Одиночный выстрел
МАГАЗИН:  Съёмный коробчатый магазин, 5 патронов
ВЕС:      15 кг
ДЛИНА:    1,350 мм

ОПИСАНИЕ:

AW50 - крупнокалиберная противотехника/снайперская винтовка британского производства, созданная на базе платформы Arctic Warfare и рассчитанная на боеприпас .50 BMG. Предназначена для поражения материальной техники, лёгких бронеобъектов и удалённых целей на больших дистанциях; сочетает точность высоко-темповой винтовки с конструктивной надёжностью для работы в полевых условиях. 

ПРИМЕЧАНИЯ:

- Тактическая роль - антиматериальная/снайперская: вывод техники из строя, разрушение чувствительного оборудования и поражение живой силы на больших дистанциях. 
- Экипируется мощным дульным тормозом и сошкой; в базовой комплектации имеет складывающийся приклад для транспортировки. 
- Практическая дистанция стрельбы - порядка 1,500 м и более в зависимости от патрона и условий; для максимальной эффективности используются бронебойные и специализированные боеприпасы .50.
- Вес и габариты делают оружие требующим носимой поддержки (тренога/сообщная команда); не предназначено для мобильного ближнего боя.