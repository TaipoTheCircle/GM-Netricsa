ВЗРЫВНОЙ ПАРАЗИТ РОЯ "БЕТА"


КЛАСС:        Взрывной Паразит Роя "Бета"
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Штурмовая Особь
РАЗМЕР:       ~0,5 м.
ВЫНОСЛИВОСТЬ: Очень Низкая
ВРАЖДЕБНОСТЬ: Крайне Высокая
ОРУЖИЕ:       Атака В Прыжке, Биологическая Детонация
НАГРАДА:      70
УГРОЗА:       Средне-Высокая

ОПИСАНИЕ:

Взрывной Паразит Роя "Бета" представляет собой специализированую разновидность паразита, приспособленную не для заражения носителей, а для мгновенного уничтожения цели ценой собственной жизни. По строению тела он практически идентичен обычному паразиту Роя "Бета", однако его увеличенное брюшко напоминает брюшной мешок Жнеца и заполнено нестабильными взрывчатыми биологическими веществами.

Эти существа отличаются чрезвычайной агрессивностью и сразу после появления устремляются к ближайшей живой цели. Достигнув жертвы, паразит совершает длинный прыжок и детонирует при столкновении, поражая противника взрывной волной и разбрасывая вокруг едкие органические фрагменты.

Наиболее часто взрывные паразиты появляются после гибели Жнеца Роя "Бета", который выпускает сразу несколько таких существ. Благодаря небольшим размерам и высокой скорости они способны быстро преодолевать препятствия и представляют серьёзную опасность в тесных помещениях или во время боя с другими представителями роя.

Хотя каждый паразит живёт лишь до момента атаки, их массовое применение позволяет Рою перегружать оборону противника, вынуждая постоянно отвлекаться на быстро приближающиеся цели.

СОВЕТЫ:

- Уничтожай паразитов на расстоянии, не позволяя им приблизиться.
- После смерти Жнеца немедленно ожидай появления нескольких взрывных паразитов.
- Один взрыв способен поразить сразу нескольких бойцов.
- Оружие с высокой скорострельностью или уроном по площади наиболее эффективно против этих существ.
