АЛУДРАНСКИЙ РЕПТИЛОИД, РОЖДЕСТВЕНСКИЙ (КРАСНЫЙ)


КЛАСС:        Алудранский рептилоид, праздничный подвид
МЕТАБОЛИЗМ:   Хладнокровный, рептилия
ВОСПРИЯТИЕ:   Примитивная раса, член культа Нового Года
РАЗМЕР:       4,5 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Магические самонаводящиеся снаряды (красные огненные шары)
НАГРАДА:      5500
УГРОЗА:       средняя

ОПИСАНИЕ:

Крупные четверорукие рептилии, проживающие в системе Алудран, что в созвездии Большого Пса. Эта примитивная раса согласилась сражаться на стороне Ментала в обмен на магическую силу.

Они мечут самодвижущиеся огненные шары, снабжённые примитивной системой самонаведения. 

Рождественский Красный Алудранский Рептилоид - редкая и тревожно нелепая мутация равнинного вида. В отличие от своих собратьев, этот подвид оказался под двойным влиянием: магии Ментала и сущности, известной как Огромный Снеговик, пообещавшей Вечный Новый Год. Цена сделки оказалась символической и унизительной - обязательное ношение новогодней шапки "Деда Мороза".

Внешне они легко узнаваемы: красные магические шары вместо обычных зелёных, красная кровь, а на массивной груди - яркий красный бант, закреплённый грубой магией. Несмотря на комичный вид, в бою они остаются крайне опасными.

Сами рептилоиды искренне верят, что сражаются ради вечного праздника. По иронии судьбы, Новый Год для них действительно бесконечен - но лишь до момента, пока они не погибнут в бою за чужую войну.

СОВЕТЫ:

- Держи его под постоянным обстрелом и пытайся сбивать снаряды в воздухе.
- Красные шары более заметны, стреляй на упреждение.