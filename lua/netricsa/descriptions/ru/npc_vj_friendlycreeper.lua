ДРУЖЕЛЮБНЫЙ КРИПЕР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Дружелюбный Крипер 
МЕТАБОЛИЗМ:   Растительно-Пороховой
ВОСПРИЯТИЕ:   Пассивный Моб, Приручаемый Питомец
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Микровзрыв 
НАГРАДА:      50
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это розовый крипер. В отличие от своих зелёных, агрессивных собратьев, эта разновидность крипера полностью пассивна и даже дружелюбна. Его розовый цвет - это не просто текстура, а маркер мирного характера, своего рода "знак качества" для тех, кто устал взрываться каждые пять минут.

В отличие от других представителей своего вида, этот крипер относится ко всему спокойно. Он не шипит в темноте, не подкрадывается со спины и не разрушает постройки. Даже если кто-то случайно заденет его, он не ответит агрессией, а просто отойдёт в сторону.

Чтобы приручить Дружелюбного крипера, нужно дать ему порох. После этого на его груди появляется сердечко, и крипер становится верным питомцем.

Он ходит за хозяином по пятам, как прирученный волк.

Атакует враждебных мобов, используя микровзрывы, которые наносят урон противникам.

Сам крипер при этом остаётся цел и невредим.

Для лечения Дружелюбного крипера можно кормить его цветами.

СОВЕТЫ:

- Не пытайся убить Дружелюбного крипера. Это не только жестоко, но и бессмысленно.
- Если ты нашёл розового крипера, срочно приручай его. Это один из немногих мобов, который будет тебя защищать, а не убивать.
- Для приручения нужен порох, который можно добыть у обычных криперов.
- В бою Дружелюбный крипер эффективен против толп мелких мобов вроде зомби и скелетов.
- Не пытайся использовать его для добычи ресурсов. Взрывы не разрушают блоки.
- Если у тебя несколько прирученных розовых криперов, они могут работать в стае. Это выглядит как небольшой розовый взрывной отряд, который защищает тебя от всех несчастий Нижнего и Верхнего миров.
