ТЯЖЁЛЫЙ ЭЛИТНЫЙ БРОНИРОВАННЫЙ СОЛДАТ OVERWATCH АЛЬЯНСА


КЛАСС:        Тяжёлый элитный бронированный солдат Overwatch Альянса
МЕТАБОЛИЗМ:   Аугментированный человек в экзоскелете
ВОСПРИЯТИЕ:   Тяжёлое штурмовое подразделение подавления Альянса
РАЗМЕР:       ~1,9 м.
ВЫНОСЛИВОСТЬ: Экстремально высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Импульсная винтовка AR2, OICW
НАГРАДА:      1200
УГРОЗА:       Критическая


ОПИСАНИЕ:

Тяжёлый элитный бронированный солдат Overwatch - это ходячая боевая платформа, созданная для проламывания обороны и демонстрации абсолютного превосходства Альянса. Он облачён в массивный роботизированный экзоскелет чёрно-серого цвета с красными акцентами, внутри которого человек становится лишь управляющим элементом машины войны.

Костюм полностью покрывает тело бойца, включая голову. Шлем герметичен и не оставляет ни малейших уязвимых зон. В конструкции брони отчётливо видны механические компоненты: сервоприводы, усиленные суставы, бронированные пластины и энергетические узлы.

Экзоскелет значительно усиливает физические возможности солдата, позволяя ему нести тяжёлое вооружение без потери мобильности. AR2 используется для подавляющего огня, в то время как OICW превращает укрытия и скопления противников в бесполезный хлам. Этот солдат не ищет выгодные позиции - он сам создаёт их, продвигаясь напрямую через зону боя.

Несмотря на кажущуюся неуязвимость, у конструкции есть предел прочности. Прямое попадание из РПГ разрушает экзоскелет, превращая элитную машину в груду искорёженного металла.

СОВЕТЫ:

- Стрелковое оружие почти бесполезно против его брони.
- Используй РПГ или мощную взрывчатку - это его главный и почти единственный недостаток.
- Не стой на пути - он не отступает и не останавливается.