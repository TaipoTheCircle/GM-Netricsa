УБИЙЦА С ОСТРОВА КАРНАТЕ


КЛАСС:        Преступник / Ужасная сущность
МЕТАБОЛИЗМ:   Неизвестно (сверхъестественный)
ВОСПРИЯТИЕ:   Обостренные чувства + способность передвигаться по стенам / потолку
РАЗМЕР:       ~6 футов
ВЫНОСЛИВОСТЬ: Высокая (регенеративная, упругая)
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Конечности-клинки (руки и ноги - длинные, острые)
НАГРАДА:      1000
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Убийца - один из самых известных и ужасающих врагов на острове. Созданный как символ казни через обезглавливание, он жутко сросся с телом: голова отделена и удерживается над телом с помощью плоти и стальных приспособлений, а конечности превратились в длинные смертоносные клинки.

Убийцы двигаются с неестественной ловкостью, умело крадутся по стенам и потолкам, спрыгивать на жертв или рубить их "сверху". Их часто слышно раньше, чем видно - их конечности-клинки скрежещут и шуршат, выдавая их приближение.

СОВЕТЫ:

- Всегда стремись уничтожить голову - без неё убийцы теряют бдительность.
- Постоянно двигайся - неподвижность навлекает на них засаду.
- Внимательно слушай - их клинки на стенах и потолках часто предупреждают тебя, прежде чем ударить.