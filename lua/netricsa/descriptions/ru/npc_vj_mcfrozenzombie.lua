ЗАМОРОЖЕННЫЙ ЗОМБИ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Замороженный зомби
МЕТАБОЛИЗМ:   Криогенная Нежить
ВОСПРИЯТИЕ:   Стрелок поддержки / замораживающий пехотинец
РАЗМЕР:       ~1,9 м. в высоту
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Снежки, удар рукой
НАГРАДА:      200
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это результат того, что обычная нежить задержалась в ледяных биомах настолько долго, что промёрзла до костей.

Внешне замороженный зомби напоминает обычного зомби, но с одним важным отличием: вся его кожа приобрела бледно-голубой оттенок, а голова, плечи, грудь, руки и ноги покрыты небольшими шапками снега. Выглядит он так, будто только что вылез из сугроба и решил, что раз уж он всё равно мёртвый, то пусть будет ещё и холодным.

В бою замороженный зомби ведёт себя иначе, чем его обычные собратья. Вместо того чтобы сразу лезть в ближний бой, он сначала атакует снежками с дистанции. Один точный удар - и жертва получает эффект замедления на несколько секунд. В этот момент все остальные замороженные зомби, находящиеся рядом, бросаются в атаку, пытаясь добить обездвиженную цель.

После того как эффект замедления проходит, замороженные зомби отходят на безопасное расстояние и повторяют цикл: снежок - набег - отход. Такая тактика делает их опасными в группах, особенно в узких коридорах, где негде уворачиваться от летящих снежков.

Существуют так же бронированные версии этих зомби, которые ещё опаснее обычных.

СОВЕТЫ:

- Увидел замороженного зомби - не стой на месте. Снежки летят точно, но их можно избежать перекатом.
- Если получил замедление - не паникуй. Постарайся отойти спиной к стене, чтобы тебя не окружили.
- Убивай замороженных зомби первыми в группе. Их контроль хуже, чем прямой урон.
- Дальнобойное оружие решает проблему до того, как они успеют бросить снежок.
