КОКТЕЙЛЬ МОВОДКОВА


ОРУЖИЕ:  Коктейль Моводкова
ПАТРОНЫ: Переработанная Непросыхающим Алкашом водка
СС:      1 бросок в минуту

ОПИСАНИЕ:

Импровизированное зажигательное оружие, созданное в результате крайне сомнительной, но эффективной "биохимии". У непросыхающего пользователя водка перерабатывается организмом с аномальным повышением концентрации спирта, после чего выводится и собирается в бутылочку.

Полученная жидкость обладает повышенной горючестью. После поджигания бутылка превращается в полноценное зажигательное средство, действующее по принципу коктейля Молотова. При разбитии о поверхность создаёт устойчивую зону горения, наносящую урон всем в радиусе.

Происхождение метода остаётся под вопросом, но его эффективность не вызывает сомнений.

ПРИМЕЧАНИЯ:

- При разбитии создаёт область огня, аналогичную коктейлю Молотова.
- Чем "крепче" исходный материал, тем дольше и ярче горение.
- Эффективен против групп противников и для контроля узких проходов.
- Наиболее полезен на ближней и средней дистанции.
- Высокий риск самоподжога при неаккуратном использовании.
- Требует подготовки "боеприпаса", что может занять неопределённое время.
