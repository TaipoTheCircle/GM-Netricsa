ПАЛАДИН ИМПЕРАТОРСКОЙ ГВАРДИИ C НИРНА


КЛАСС:        Паладин Императорской гвардии
МЕТАБОЛИЗМ:   Имперец, Мужчина
ВОСПРИЯТИЕ:   Верховный страж Имперского дворца / Элитный пехотинец
РАЗМЕР:       1.8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Условная
ОРУЖИЕ:       Длинный меч из тонкой стали, щит Имперской стражи
НАГРАДА:      1000
УГРОЗА:       Высокая

ОПИСАНИЕ:

Имперский Дворец в самом сердце Сиродила охраняет элита среди элит - Паладины Императорской гвардии. В то время как обычные стражи патрулируют улицы Имперского города, эти бойцы в парадных доспехах защищают резиденцию самого Императора и залы Старейшин.

Их внешность внушает уважение. Полный набор брони включает шлем, сапоги, поножи и наручи, создавая образ непреклонного стража закона. В руках у них неизменно качественное оружие - длинный меч из тонкой стали и прочный щит Имперской стражи.

Как правило, эти стражи дружелюбны и приветствуют посетителей, напоминая им правила этикета: "Пожалуйста, ведите себя тихо из уважения к Императорскому дворцу" или "Помните, проникновение в Императорский дворец - это серьёзное преступление. Не заходите дальше первого и второго этажа". Они готовы подсказать дорогу в различные районы города и объяснить, где находятся гильдии или таверны.

Однако их гостеприимство имеет границы.

Убийство паладина - нетривиальная задача. Их броня неплоха, а меч из тонкой стали наносит серьёзный урон. Хотя их кирасу и шлем нельзя снять с трупа, у них можно забрать оружие и ключ от дворца.

СОВЕТЫ:

- Если ты будешь законопослушным гражданином, просто наслаждайся общением и не пытайся пробраться в закрытые части дворца. Паладины предупредят тебя один раз.
- Если бой неизбежен, используй дальнобойную магию или лук, чтобы ослабить паладина до того, как он подойдет вплотную.
- Помни, что их броня не позволяет вынести с поля боя самые ценные её части, так что не рассчитывай на доспех как на добычу.
