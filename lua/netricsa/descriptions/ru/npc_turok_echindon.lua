ЭХИНДОН С ТЕРРАФОРМИРОВАННОЙ ПЛАНЕТЫ


КЛАСС:        Эхиндон
МЕТАБОЛИЗМ:   Всеядное
ВОСПРИЯТИЕ:   Стайное мелкое животное
РАЗМЕР:       0,9 м.
ВЫНОСЛИВОСТЬ: Очень низкая 
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Укус, когти
НАГРАДА:      50
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это ближайшие родственники мини-рапторов, только более тёмные, менее глазастые и с менее выраженными когтями. Коричневая кожа, маленькие естественные глаза и отсутствие ярких перьевых наростов делают их похожими на уменьшенную копию кого-то более крупного и опасного. В целом - невзрачные, серые, незаметные.

Главное отличие эхиндонов от мини-рапторов - характер. Мини-рапторы агрессивны по умолчанию и нападают на всё, что движется. Эхиндоны же ведут себя пассивно и не трогают людей, пока их не тронут сами. Если пройти мимо стаи эхиндонов, они просто будут сидеть и смотреть. Но стоит выстрелить в одного или случайно задеть - вся стая мгновенно переключается в режим берсерка и начинает атаковать.

В остальном эхиндоны повторяют поведение мини-рапторов: они так же прыгают, так же кусаются, так же стараются окружить и зажать жертву. Поодиночке они беспомощны и дохнут с одного удара ножом или выстрела. В стае могут доставить неприятности, особенно если застанут врасплох в джунглях, где много укрытий и мало пространства для манёвра.

СОВЕТЫ:

- Не стреляй первым. Если не трогать стаю, она пройдёт мимо.
- Нож работает отлично. Один удар - одна тушка.
- Эхиндоны мелкие, но шустрые. Не дай себя окружить.
