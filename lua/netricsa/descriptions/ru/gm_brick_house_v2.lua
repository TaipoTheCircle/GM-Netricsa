КИРПИЧНЫЙ ДОМ, БЛАГОУСТРОЙСТВО ПОСТАПОКАЛИПСИСА


Сэм, пока мы путешествовали, местный сталкер-дачник провёл реновацию. Кирпичный дом обзавёлся инфраструктурой. Теперь это настоящий загородный клуб для выживальщиков с бассейном и гаражом.

Правда, дверей всё ещё нет, но кто мы такие, чтобы придираться к мелочам?

Ландшафт претерпел серьёзные изменения. Цивилизация наступает:

Четвёртый автомобиль. Теперь их четыре. Колёс у него тоже нет, но компания стала теснее. Паркуются как могут.

Кирпичный гараж. Взади дома вырос полноценный гараж из того же красного кирпича. Двери нет - традиция, видимо, фамильная.

Внутри - святое место механика: на деревянном столике аккуратно лежат гаечный ключ и двигатель. Не прибиты, не прикручены. Просто лежат.

Бассейн. Слева от дома, прямо на траве, появился бассейн. Из белого пластика, классической прямоугольной формы. Полон воды. Но искупаться можно. Вода, кстати, чистая. Наверное.

Дерево-мигрант. Одно из одиннадцати деревьев пересадили в другой угол участка. То ли ландшафтный дизайнер поработал, то ли дерево само попросилось. Визуально ничего не изменилось, но факт остаётся фактом - флора теперь живёт по новому генплану.

Внутри дома всё те же запасы, которые мы уже изучили (привет Софии с её 112 обедами). Но появилась новая дверь... точнее, новый проём:

Новая комната. Без двери, естественно. Внутри - спартанский минимализм: пустой шкаф, стол и кровать. Без матрасов. Голый каркас. Видимо, это гостевой сьют для тех, кто не боится спать на фанере и вешать одежду в пустоту.

Запасы на месте: сладкий картофель, яблоки (самые лучшие в мире), 112 обедов от Софии, снеки Джаббера, соки, ванильный напиток, диетическая газировка и свежая картошка. Ящики (один большой, четыре маленьких) по-прежнему не открываются. Часы висят. Ружья в шкафу прибиты и лишь насмехаются своим видом.

Гараж - это потенциальный ремонтный пункт. Двигатель и ключ не просто так лежат. Если применить смекалку (и, возможно, найти колёса для тех машин, что стоят без дела), можно заставить ездить хотя бы одну из четырёх развалюх.

Бассейн - источник воды. В осаде это бесценно. Да и просто приятно окунуться после боя с пришельцами. Правда, греться потом негде - кровать без матраса.

Новая комната - можно использовать как КПП или спальню. Если найдём матрас. Или хотя бы сена.

Вперёд.
