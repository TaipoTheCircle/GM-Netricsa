ОПИСАНИЕ:

Это то, во что превращаются наёмники частной военной компании Y.S.M. после неудачной встречи с хедкрабом. В отличие от учёных в серых халатах, техников в комбинезонах или даже охранников базы Дельта, эти ребята изначально были профессиональными солдатами. Они носили бронежилеты, тактические шлемы и знали, как обращаться с оружием. Это не спасло их от заражения, но сделало их гораздо более опасными противниками в зомбированном виде.

Их броня частично защищает тело от выстрелов, делая их более живучими, чем обычные зомби. Хедкраб, сидящий на голове, выглядит так же, как и у других разновидностей, но его носитель - это тренированное, мускулистое тело, которое не разваливается от пары ударов.

Они крупнее охранников базы Дельта, их броня толще, а удары тяжелее. Даже после того, как хедкраб взял контроль, в теле бывшего наёмника сохраняется мышечная память: они передвигаются быстрее обычных зомби. Их когти пробивают броню, а укусы вырывают куски плоти.

При убийстве в голову голова отрывается.

