МИНИАТЮРНЫЙ МЕТРОКОП ГРАЖДАНСКОЙ ОБОРОНЫ АЛЬЯНСА


КЛАСС:        Встроенный оператор боевого синтета
МЕТАБОЛИЗМ:   Человек, мужчина (сильная биомеханическая компрессия)
ВОСПРИЯТИЕ:   Интерфейс Министрайдера + ограниченное собственное зрение
РАЗМЕР:       ~0,3 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Управление импульсной винтовкой Министрайдера, Пистолет
НАГРАДА:      300
УГРОЗА:       Низкая

ОПИСАНИЕ:

Миниатюрный метрокоп - это экспериментальный персонал Альянса, уменьшенный и помещённый внутрь корпуса Министрайдера. Эти операторы выполняют функции обслуживания, наведения и контроля встроенной импульсной винтовки, действуя как живая часть боевого синтета. Их тела подвергнуты экстремальной компрессии, биомеханической стабилизации и нейронной привязке к системе Министрайдера.

В отличие от полностью автономных синтетических платформ, Министрайдеры с такими операторами обладают более гибким тактическим поведением. Человеческая реакция и способность к импровизации позволяют быстрее выбирать цели, корректировать огонь и реагировать на нестандартные угрозы. По сути, это попытка Альянса совместить холодную машину и подчинённый человеческий разум в одном корпусе.

В редких случаях, при уничтожении Министрайдера, происходит частичная дестабилизация компрессионного поля, и оператор может вернуться к своему нормальному размеру. Однако этот процесс несовместим с жизнью: перегрузка тканей, органов и нервной системы приводит к неминуемой смерти, зачастую одновременно с разрушением самого синтета.

Такие метрокопы не считаются полноценными бойцами и официально числятся как "расходный обслуживающий ресурс".

СОВЕТЫ:

- Уничтожение Министрайдера автоматически выводит оператора из строя.
- Более точное поведение Министрайдера может указывать на наличие живого оператора внутри.