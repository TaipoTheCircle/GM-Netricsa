ПАРАЗИТ РОЯ "БЕТА"


КЛАСС:        Паразит Роя "Бета"
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Паразит
РАЗМЕР:       ~1 м.
ВЫНОСЛИВОСТЬ: Очень низкая
ВРАЖДЕБНОСТЬ: Очень высокая
ОРУЖИЕ:       Инфицирование, прыжок, когти
НАГРАДА:      60
УГРОЗА:       Высокая

ОПИСАНИЕ:

Паразит Роя "Бета" является мелким, но чрезвычайно агрессивным подвидом, специализирующимся на заражении крупных организмов. Он вылупляется из яиц и практически сразу начинает искать ближайшую жертву, используя длинные задние конечности для стремительных прыжков.

Во время атаки паразит старается закрепиться на теле жертвы и внедрить в её организм инфекционный материал. Если заражённый организм погибает до очистки от инфекции, внутри него быстро созревает новое поколение паразитов, которое вскоре вырывается наружу.

По сравнению с паразитами других роёв, особи Роя "Бета" отличаются вытянутой головой, некоторые зелёной окраской панциря и более длинными хватательными конечностями. Несмотря на эти различия, их жизненный цикл и способы нападения практически идентичны.

Поодиночке паразиты редко представляют серьёзную угрозу. Однако при массовом появлении они способны стремительно распространить заражение по всему отряду, особенно если действуют совместно с другими организмами Роя.

СОВЕТЫ:

- Уничтожай паразитов до того, как они сократят дистанцию.
- При заражении немедленно найди способ очистить организм от инфекции.
- Не позволяй паразитам окружать группу, особенно в узких проходах.
- Сначала уничтожай яйца, чтобы не дать паразитам появиться.
- Автоматическое оружие и огонь особенно эффективны против этих существ.
