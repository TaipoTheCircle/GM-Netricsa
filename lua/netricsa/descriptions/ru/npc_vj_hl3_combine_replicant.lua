НАГРАДА:      300
УГРОЗА:       Средняя

ОПИСАНИЕ:

Солдаты-Репликанты - это незавершённая стадия превращения человека в полноценного солдата Overwatch Альянса. Они проходят ускоренную процедуру имплантации и психокоррекции, но ещё не оснащены полной защитной экипировкой и нейроинтерфейсами, характерными для стандартных подразделений Overwatch.

Главная отличительная черта Репликанта - отсутствие шлема. Их лица частично или полностью открыты, зачастую с заметными следами хирургического вмешательства: импланты, шрамы, бледная кожа, стеклянный взгляд. Это делает их уязвимыми, но также позволяет Альянсу экономить ресурсы на массовых операциях.

Их поведение менее скоординировано, чем у Overwatch. Несмотря на это, Репликанты всё ещё опасны, особенно в численном превосходстве.

СОВЕТЫ:

- Отсутствие шлема делает выстрелы в голову особенно эффективными.
