КОРОЛЕВА РОЯ "БЕТА"


КЛАСС:        Королева Роя "Бета"
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Матка улья
РАЗМЕР:       ~8 м.
ВЫНОСЛИВОСТЬ: Чрезвычайно высокая
ВРАЖДЕБНОСТЬ: Подавляющая
ОРУЖИЕ:       Подземные Щупальца, Кислотные Снаряды, Сокрушительные Конечности
НАГРАДА:      5000
УГРОЗА:       Апокалиптическая

ОПИСАНИЕ:

Королева Роя "Бета" является крупнейшей известной формой жизни данного роя и центральным организмом всей колонии. Именно вокруг неё формируется улей, а все остальные особи существуют исключительно для защиты своей прародительницы и продолжения жизненного цикла роя. Уничтожение Королевы приводит к постепенному распаду колонии, лишённой единого координирующего центра.

Её массивное тело покрыто толстой хитиновой бронёй и удерживается на нескольких мощных конечностях, позволяющих уверенно перемещаться даже по неровной поверхности. Несмотря на свои размеры, Королева способна быстро менять положение корпуса, выбирая наиболее выгодную позицию для атаки.

Главной особенностью Королевы являются длинные подземные щупальца. Погружая их в грунт, она способна неожиданно вывести их на поверхность под противником, захватывая жертву и удерживая её на месте, пока мощные сокращения щупалец наносят разрушительные повреждения. Даже опытным бойцам бывает трудно вырваться из такой ловушки.

На дальней дистанции Королева принимает устойчивое положение и выпускает очередь из трёх крупных кислотных сгустков. Попадание каждого такого снаряда сопровождается мощным выбросом едкой биологической жидкости, способной быстро разрушать броню и живые ткани. Во время подготовки залпа её подвижность значительно снижается, что делает этот момент наиболее удобным для контратаки.

В непосредственной близости Королева использует массивные конечности, способные раздавить практически любого противника одним ударом. Благодаря сочетанию огромной живучести, контроля пространства и разрушительных атак она считается наиболее опасным представителем Роя "Бета".

СОВЕТЫ:

- Не оставайся долго на одном месте: подземные щупальца способны настигнуть неподвижную цель.
- Во время подготовки кислотного залпа Королева почти не меняет направление атаки. Используй этот момент, чтобы сменить позицию.
- Королева эффективно контролирует большую часть поля боя своими щупальцами.
