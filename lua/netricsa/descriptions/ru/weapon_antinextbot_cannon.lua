АНТИ-НЕКСТБОТ ПУШКА


ОРУЖИЕ:  Анти-Некстбот Пушка
ПАТРОНЫ: Сверхзаряженная гравитационная энергия / аннигиляционный луч
СС:      Непрерывный луч

ОПИСАНИЕ:

Высшая стадия развития анти-некстботного вооружения, созданная на базе улучшенной заряженной грави-пушки. Оружие сочетает в себе гравитационные технологии и разрушительные энергетические выбросы, формируя луч колоссальной мощности.

Первый режим концентрирует энергию в узкой точке, выпуская луч, способный мгновенно уничтожать группы целей в пределах одной зоны. Используется для зачистки скоплений противников и удержания ключевых позиций.

Второй режим активирует полный выброс энергии, охватывающий всю локацию. В этот момент пользователь автоматически защищается специальным полем, которое полностью изолирует его от воздействия оружия. Всё остальное живое в зоне действия подлежит немедленному уничтожению - без исключений.

ПРИМЕЧАНИЯ:

- Режим 1: массовое уничтожение целей в одной области лучом.
- Режим 2: полная зачистка всей локации от всего живого.
- Пользователь защищён энергетическим полем при активации второго режима.
- Не различает цели - союзники и нейтралы также уничтожаются.
- Эффективен на любых дистанциях, особенно в замкнутых пространствах.
