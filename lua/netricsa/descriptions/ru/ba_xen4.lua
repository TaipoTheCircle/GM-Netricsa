ПОГРАНИЧНЫЙ МИР ЗЕН, ВОДОПАДЫ ЗЕНА


Сэм, остановись на секунду. Да, именно сейчас.

Я засекла устройство. Сигнал чёткий. Уверенный. Такой, который ни с чем не перепутаешь. Я готова поклясться своими нейронами, что это оно. То самое. Контроль телепортации. Финальный кусок головоломки.

До него осталось совсем чуть-чуть. В масштабах Зена - почти рядом. В масштабах выживания - как обычно.

Между нами и целью лежат пещеры с водопадами. 

Но мы близко, Сэм. Настолько близко, что даже Зен, кажется, начинает нервничать. Пространство искажается сильнее, сигналы стабильнее, а сопротивление - отчаяннее.

Пройди через пещеры.

Не теряй ориентиры.

Если я ошибаюсь - можешь списать это на помехи.

Но если я права...

то скоро у нас появится шанс уйти отсюда.