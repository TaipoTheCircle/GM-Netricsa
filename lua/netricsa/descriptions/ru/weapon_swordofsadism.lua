МЕЧ САДИЗМА


ОРУЖИЕ:  Меч Садизма
ПАТРОНЫ: энергетический резонанс кристалла
СС:      ~75 ударов / мин

ОПИСАНИЕ:

Легендарный кристаллический клинок, созданный из редкого зелёного изумруда неизвестного происхождения. Материал обладает сверхплотной решёткой, что позволяет лезвию прорезать почти любую материю, включая броню Альянса и плоть Страйдер-класса.

Организаторы HALF-QUAKE нашли артефакт в древних руинах и адаптировали его для шоу. При ударе меч создаёт резонансную волну, которая разрывает цель на молекулярном уровне.

ПРИМЕЧАНИЯ:

- Разрывает большинство сущностей одним ударом.
- Эффективен на ближней дистанции, катастрофически смертелен для противников в узких коридорах.
- Требует сильного носителя.
