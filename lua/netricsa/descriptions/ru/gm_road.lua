ДОРОГА, ПУТЬ В ЗАБОР


Мы нашли это. Место, где минимализм возведён в абсолют, а слово "дизайн" просто вычеркнули из словаря.

Перед нами ДОРОГА. Просто дорога. Она тянется прямо, теряется в дымке горизонта и даже не думает никуда сворачивать.

По бокам - БОЛЬШОЙ ЗАБОР. Высокий, серый, непреодолимый.

В начале - забор. В конце - забор. У боков - забор. Сверху - небо. Снизу - асфальт. И ты.

Ничего. Абсолютно. Ни указателей, ни домов, ни деревьев, ни даже случайной лужи. Только ты, дорога, заборы и осознание того, что ты заперт в этом коридоре НАВСЕГДА.

Если уж сюда попал - иди по дороге. Медленно. Считай шаги. Потом развернись и иди обратно. Потом ещё раз. И ещё. Может, через тысячу проходов забор покажется чуть ближе?
