ПРОТИВОПЕХОТНАЯ МИНА КЛЕЙМОРА M18A1


ОРУЖИЕ: M18A1 "Клеймора" 
ПАТРОНЫ: Заряд C-4 с 700 стальными шариками
СС: Одноразовое устройство
ВЕС: ≈1,6 кг

ОПИСАНИЕ:

Направленная противопехотная мина, разработанная в США в 1950-х и широко применяемая как управляемая мина для охраны периметра и засад. При подрыве заряд выбрасывает 700 стальных шариков вперёд, образуя профильную «панель» убойных элементов -  по эффекту ближе к дроби, чем к осколкам. Мина обычно устанавливается изогнутой стороной к предполагаемому маршруту противника и имеет прицел-рамку для наводки; на корпусе традиционно нанесена надпись "ФРОНТ НА ВРАГА".

ПРИМЕЧАНИЯ:

- Эффективность: сектор поражения ~60°, радиус поражения до ~50 м; фатальный/высокий риск ранения внутри ближней зоны (значительная вероятность поражения в пределах 5-15 м и травмы дальше). 
Википедия
- Безопасные расстояния: по полевому уставу FM 20-32 - 250 м вперёд (для собственных войск), 100 м в сторону и назад как ориентиры при постановке в боевых условиях. 
Википедия
- Ограничения: однократное применение, риск побочных потерь при установке в населённых пунктах; требует аккуратной маскировки и расчёта сектора огня.