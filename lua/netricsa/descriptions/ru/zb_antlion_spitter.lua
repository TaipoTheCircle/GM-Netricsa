МУРАВЬИНЫЙ ЛЕВ, ПЛЕВАЛЬЩИК С ЗЕНА


КЛАСС:         Муравьиный Лев, Плевальщик
МЕТАБОЛИЗМ:    Насекомоподобный
ВОСПРИЯТИЕ:    Насекомоподобное Инопланетное Существо
РАЗМЕР:        1,5 м.
ВЫНОСЛИВОСТЬ:  Средняя
ВРАЖДЕБНОСТЬ:  Высокая
ОРУЖИЕ:        Синий Кислотный Плевок, Токсичные Когти, Токсичный Укус, Короткие Рывки
НАГРАДА:       250
УГРОЗА:        Средняя

ОПИСАНИЕ:

Насекомоподобные хищники из Зена, привлеченные на Землю до вторжения Альянса. Они бродят по песчаным местностям роями, вырываясь из-под земли, чтобы заманить добычу в засаду. Проворные и неумолимые, они побеждают не благодаря индивидуальной силе, а благодаря своему количеству.

Плевальщик представляет собой особую разновидность муравьиного льва, отличающуюся ярко-синим панцирем и наличием ядовитых желез. В отличие от обычных особей, он предпочитает держаться позади основной стаи, оказывая ей дистанционную поддержку. Его главным оружием является одиночный сгусток синей едкой жидкости, который при попадании вызывает кратковременный химический ожог кожи и дезориентирует жертву на несколько мгновений.

По своему поведению плевальщики напоминают рабочих муравьиных львов. Они не бросаются в безрассудные атаки, а стараются занять выгодную позицию, постоянно перемещаясь вокруг противника и прикрывая более агрессивных собратьев. Если враг подходит слишком близко, существо атакует острыми когтями и мощными жвалами. И укус, и когти содержат токсин, вызывающий сильную боль и временное ухудшение состояния цели.

Несмотря на принадлежность к виду муравьиных львов, плевальщики полностью игнорируют действие фероподов. Попытки подчинить их или успокоить оказываются безрезультатными, что делает их значительно опаснее обычных представителей роя. По этой причине они сохраняют агрессивность даже тогда, когда остальные муравьиные львы прекращают нападение.

СОВЕТЫ:

- Избегай ходить по песку; вибрации мгновенно привлекают их.
- Не стой на открытом месте.
- Не рассчитывай на фероподы, плевальщики полностью невосприимчивы к их воздействию.
- Избегай ближнего боя.
