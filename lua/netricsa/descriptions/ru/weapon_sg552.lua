ШТУРМОВАЯ ВИНТОВКА SG-552


ОРУЖИЕ:     SIG SG-552 
ПАТРОНЫ:    5.56×45mm NATO
CC:         ~700 выстрелов/мин

ОПИСАНИЕ:

Швейцарская штурмовая винтовка, разработанная в 1998 году, как укороченная версия модели SG-550. Производство началось в 1998 году и продолжалось до 2008 года, когда винтовку сменил SG-553.

ПРИМЕЧАНИЯ:

- Данная винтовка эффективна на средних-дальних расстояниях.
- Хорошо используется для операций в помещениях или в городских условиях.

