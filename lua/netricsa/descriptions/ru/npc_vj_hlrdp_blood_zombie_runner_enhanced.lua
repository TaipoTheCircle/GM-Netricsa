ЗОМБИ-ХЕДКРАБ, КРОВАВЫЙ БЕГУН (УСИЛЕННЫЙ)


КЛАСС:        Зомби-хедкраб, Кровавый Бегун (Усиленный)
МЕТАБОЛИЗМ:   Человек-Носитель + Паразитический Хедкраб Зена
ВОСПРИЯТИЕ:   Зомбированная Сущность 
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Средне-высокая
ВРАЖДЕБНОСТЬ: Экстремальная
ОРУЖИЕ:       Когти, укус, Скорость
НАГРАДА:      600
УГРОЗА:       Очень Высокая

ОПИСАНИЕ:

Это особая, невадская разновидность зомби-хедкраба, которая получила широкое распространение в самом засушливом штате Америки - Неваде, на базе Дельта. Аризонская пустыня и жара не убили этих тварей, а только сделали их злее, быстрее и смертоноснее.

Внешне Кровавый Бегун отличается от своего обычного собрата. Он облачён в тёмные, рваные одежды, которые сплошь покрыты запёкшейся кровью. Тело носителя искажено, мышцы неестественно вытянуты, приспособлены для долгого и быстрого бега по пересечённой местности.

В бою Кровавый Бегун ещё опаснее обычного. Он не просто бежит - он несётся, избегая препятствий и появляясь из темноты в самый неожиданный момент.

Когда Кровавого Бегуна убивают в голову, его голова отрывается от тела.

СОВЕТЫ:

- Кровавый Бегун быстрее обычного. Не пытайся убежать - он догонит.
- Используй узкие проходы и ловушки.
- При смерти в голову, она отрывается.
- В тёмных одеждах его трудно заметить в ночи.
- Ближний бой крайне рискован. Держи дистанцию и используй дробовик в упор, когда он бежит.
