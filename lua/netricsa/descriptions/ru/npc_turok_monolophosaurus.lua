МОНОЛОФОЗАВР С ТЕРРАФОРМИРОВАННОЙ ПЛАНЕТЫ


КЛАСС:        Монолофозавр
МЕТАБОЛИЗМ:   Хищник, Теропод
ВОСПРИЯТИЕ:   Засадный хищник
РАЗМЕР:       ~2,5 м,
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Крайне высокая
ОРУЖИЕ:       Челюсти, когти, одиночный гребень на голове
НАГРАДА:      1000
УГРОЗА:       Высокая

ОПИСАНИЕ:

Внешне монолофозавр напоминает уменьшенную версию дилофозавра, но с одним ключевым отличием: вместо двух гребней на голове у него один.

Отсюда и название - "одногребневый ящер". В остальном это типичный хищник среднего размера, поджарый, быстрый и, судя по родственным связям, более продвинутый, чем примитивные дилофозавры.

СОВЕТЫ:

- Советовать особо нечего.
- Не подпускай близко, бей взрывчаткой, не давай себя убить.
