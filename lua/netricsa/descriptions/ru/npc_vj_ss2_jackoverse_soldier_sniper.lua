КЛОН СОЛДАТ-ШТУРМОВИК, СНАЙПЕР С КАМИНО


КЛАСС:        Клон Солдат-Штурмовик, Снайпер
МЕТАБОЛИЗМ:   Человек-Клон, Мужчина
ВОСПРИЯТИЕ:   Элитный Стрелок Галактической Империи
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Улучшенная Снайперская Винтовка "RAPTOR" С Лазерным Целеуказателем
НАГРАДА:      1200
УГРОЗА:       Очень Высокая

ОПИСАНИЕ:

Снайперы являются следующей ступенью в иерархии штурмового корпуса и набираются исключительно из наиболее дисциплинированных и метких клонов. Их легко узнать по серой броне, выдаваемой бойцам, прошедшим специальную подготовку для ведения боя на больших дистанциях.

Основным оружием снайпера служит модернизированная винтовка "RAPTOR", оснащённая высокоточной оптикой и лазерным целеуказателем. Лазер позволяет сопровождать движущиеся цели и значительно сокращает время прицеливания, а мощные бронебойные боеприпасы способны пробивать большинство известных типов индивидуальной защиты.

В отличие от обычных штурмовиков, снайперы редко вступают в открытый бой. Они занимают господствующие высоты, крыши зданий или укреплённые позиции, откуда устраняют наиболее опасные цели, оставляя остальную работу линейной пехоте. Даже после обнаружения они сохраняют хладнокровие и продолжают вести огонь до последнего возможного момента.

В союзе с Менталом эти бойцы ценятся за способность устранять противника ещё до того, как основные силы вступят в бой. Многие командиры предпочитают сначала отправить несколько снайперов, а уже затем бросать в атаку целые подразделения. Потерять одного такого стрелка неприятно, но потерять командира из-за единственного точного выстрела куда неприятнее.

СОВЕТЫ:

- Если заметил красный лазер, значит кто-то уже заметил тебя.
- Не стой долго на открытых участках.
- Дым, укрытия и постоянное движение резко снижают их эффективность.
- Серую броню видно хуже белой. К сожалению, стреляют они наоборот.
