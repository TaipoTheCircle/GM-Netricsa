ШТУРМОВАЯ ВИНТОВКА M16A1


ОРУЖИЕ:     Кольт M16A1
ПАТРОНЫ:           Патроны 5,56x45 мм НАТО + 40-мм гранаты
CC:         ~700 выстрелов/мин

ОПИСАНИЕ:

Надежное штурмовое ружье американского производства, широко используемое силами H.E.C.U. Обеспечивает непрерывный автоматический огонь с приемлемой точностью. Оснащено подствольным гранатометом, что позволяет вести как угнетающий огонь, так и оказывать взрывную поддержку с одной платформы.

ПРИМЕЧАНИЯ:

- Стандартное пехотное оружие с надежной производительностью.
- Гранатомет эффективен для контроля толпы и укрепленных позиций противника.
- Точность страдает при длительной стрельбе.