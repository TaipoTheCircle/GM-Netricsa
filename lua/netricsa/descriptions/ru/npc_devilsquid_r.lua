ДЬЯВОЛЬСКИЙ БУЛЛСКВИД С ЗЕНА


КЛАСС:        Буллсквид, Огненная разновидность
МЕТАБОЛИЗМ:   Хищник (пиро-аугментированный)
ВОСПРИЯТИЕ:   Наземный Хищник Зена
РАЗМЕР:       2,4 м.
ВЫНОСЛИВОСТЬ: Высокая (огненная шкура)
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Огненный плевок, когти, горящий хвостовой хлыст
НАГРАДА:      500
УГРОЗА:       Высокая

ОПИСАНИЕ:

Дьявольский Буллсквид - это огненная мутация буллсквида, плюющаяся зажигательной желчью, которая поджигает всё, к чему прикасается. Его тело излучает тепло, а удар хвостом оставляет на земле горящие следы. Эти звери часто поджигают целые охотничьи угодья.

СОВЕТЫ:

- Огнестойкая броня крайне важна.
- По возможности используйте ледяное или водяное оружие.
- Не проводите борьбу с огнем в закрытых, легковоспламеняющихся помещениях.