ОСКОЛОЧНАЯ ГРАНАТА M67


ОРУЖИЕ:  Осколочная ручная граната M67 
ПАТРОНЫ: Взрывчатое заполнение: Состав B
СС:      4-5 сек
ВЕС:     400 г
РАЗМЕРЫ: Диаметр 63.5 mm, длина 89.6 mm

ОПИСАНИЕ:

Классическая "бейсбольная" осколочная граната, принятая на вооружение США с конца 1960-х. Сферический металлический корпус содержит пред-надрезанную вставку и ≈6.5 oz (≈184 г) Состава B как основной заряд; при срабатывании корпуса образуются фрагменты, создающие зону поражения. Граната инициируется при снятии предохранительной скобы и отпускании рычага - после этого начинается пиротехническая задержка 4-5 секунд до взрыва.

ПРИМЕЧАНИЯ:

- Радиус фатального поражения (летальный радиус) оценивается примерно в 5 м, зона вероятных травм/каузалити - до 15 м; мелкие осколки могут разлетаться существенно дальше (до ~200-250 м). 
- Типичный фитиль/взрыватель - M213 (пиротехнический), с задержкой около 4–5 секунд; существует вариант M68 с ударно-детонационным взрывателем (ударным двигателем). 
- Граната относится к оборонительным/осколочным типам (эффективна при прикрытии и удержании позиций), но её основной эффект - поражение личного состава в зоне вокруг точки взрыва. 