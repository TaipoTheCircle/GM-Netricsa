СИГНАЛЬНЫЙ ПИСТОЛЕТ


ОРУЖИЕ:      Сигнальный пистолет
ПАТРОНЫ:                  Сигнальные ракеты
ТЕМП ПОТРЯСЕНИЯ:         ~6 выстрелов/мин.

ОПИСАНИЕ:

Однозарядное огнестрельное оружие, которое запускает ракеты с накаливающимся элементом. В основном используется для подачи сигналов, освещения или временного отвлечения внимания врагов. При тщательной прицельной стрельбе может поджечь горючие предметы.

ПРИМЕЧАНИЯ:

- Ограниченный боезапас; каждый выстрел расходует одну ракету.
- Эффективен для освещения темных областей или обозначения позиций.
- Может наносить незначительный урон от огня врагам и окружающей среде.
- Не подходит для длительного боя; в основном является вспомогательным оружием.