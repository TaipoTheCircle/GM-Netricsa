ВЕЛОЦИРАПТОР ВУЛЬГАРИС С М'ДИГБО


КЛАСС:        Велоцираптор Вульгарис
МЕТАБОЛИЗМ:   Рептилия
ВОСПРИЯТИЕ:   Ездовое животное / Боевой компаньон
РАЗМЕР:       ~1,5 м.
ВЫНОСЛИВОСТЬ: Очень низкая
ВРАЖДЕБНОСТЬ: Зависит от наездника
ОРУЖИЕ:       Огненные шары
НАГРАДА:      200
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это зелёная инопланетная ящерица, которая выглядит так, будто её вытащили из фильма "Парк Юрского периода" 1993 года, забыв добавить перья. Она живёт на планете М'Дигбо и, в отличие от своих земных собратьев, умеет дышать огнём.

Внешне велоцираптор напоминает классического дромеозаврида из поп-культуры: зелёная чешуя, острые когти, длинный хвост и пасть, полная мелких зубов. Никаких перьев, никакого интеллекта стаи - только чистая, незамутнённая ярость, оседланная и направленная на врагов Ментала.

В бою велоцираптор незаменим. Он быстро бегает, уворачивается от вражеских атак и стреляет огненными шарами из пасти. Один такой шар убивает слабого врага с одного попадания, а более сильных - после пары секунд непрерывного обстрела. Главная проблема - его здоровье. Велоцираптор хрупок, как стекло. Пара попаданий - и от ездовой ящерицы остаётся ни следа.

СОВЕТЫ:

- Сидя на нём - держись на дистанции и расстреливай врагов очередями огненных шаров.
- Его здоровье низкое. Если видишь, что в тебя стреляют - уворачивайся. Одно попадание может отправить ящерицу на тот свет.
- Не пытайся убивать врагов вблизи на обычной ящерице - она сдохнет быстрее, чем ты успеешь нанести урон.
