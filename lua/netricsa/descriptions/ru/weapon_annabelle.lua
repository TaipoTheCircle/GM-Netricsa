АННАБЕЛЬ


ОРУЖИЕ:      Дробовик «Аннабель»
ПАТРОНЫ:                  Патроны 357 Magnum
CC:                Помповое действие, ~60 выстрелов/мин.

ОПИСАНИЕ:

Мощный дробовик, способный стрелять стандартными стальными пулями. Разработанный для точных ударов на большие расстояния, «Аннабель» смертельно опасен как для пехоты, так и для тяжело бронированных целей. 

ПРИМЕЧАНИЯ:

- Чрезвычайно высокий урон за выстрел.