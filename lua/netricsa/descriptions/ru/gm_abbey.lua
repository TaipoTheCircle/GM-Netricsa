АББАТСТВО, БАЗИЛИКА САН-ПЬЕТРО-АПОСТОЛО


Сэм, судя по тому, куда нас занесло, это место когда-то было центром паломничества, а теперь стало идеальной декорацией для засад и перестрелок. Перед нами древняя базилика, точнее, то, что от неё осталось после долгих лет забвения... или после вчерашнего визита артиллерии Ментала.

Местная архитектура явно вдохновлялась великими соборами, возможно, той самой Базиликой ди Сан Пьетро.

Сейчас же тут сплошные Руины...
