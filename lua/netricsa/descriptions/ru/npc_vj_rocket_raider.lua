РАКЕТНЫЙ РЕЙДЕР С КИБЕРСТАНА


КЛАСС:        Ракетный Рейдер
МЕТАБОЛИЗМ:   Механический
ВОСПРИЯТИЕ:   Противотанковая, Противопехотная Единица 
РАЗМЕР:       ~2,5 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Ракетная Установка Барабанного Типа
НАГРАДА:      150
УГРОЗА:       Абсолютная

ОПИСАНИЕ:

Это одна из самых опасных боевых единиц фракции Автоматонов. Внешне он напоминает стандартного Рейдера - двуногого робота. У него на плече закреплена мощная ракетная установка барабанного типа, готовая превратить любого Хеллдайвера в кровавый туман.

Как и все Автоматоны, Ракетный Рейдер имеет человекоподобную форму, корпус из серого металла, красные глаза-датчики и пулемётную голову. На левой руке у него может быть щит, а на правой - ракетница. Основное отличие - ракетная установка на плече и характерные зелёные индикаторы, которые сигнализируют о готовности к пуску.

В бою Ракетный Рейдер занимает среднюю или дальнюю позицию. Он не лезет в ближний бой, предпочитая расстреливать противников ракетами из укрытия. Его ракеты обладают высоким уроном, большим радиусом поражения и могут пробивать броню Хеллдайверов даже с самой лучшей экипировкой.

Ракетная установка барабанного типа стреляет очередями. За один залп может выпустить до 4-6 ракет, которые разлетаются в разные стороны, покрывая значительную площадь.

Ракетный Рейдер действует в группе с другими Автоматонами: Рейдерами с щитами, Командирами и обычными пехотинцами-Автоматонами. Его задача - уничтожать Хеллдайверов на дальних дистанциях, прикрывая наступление ближних.

У Ракетного Рейдера есть несколько слабых мест:

Двигатель на спине. Попадание в двигатель наносит повышенный урон и может вызвать детонацию.

Голова - уязвима, но меньше.

Ракетная установка - если в неё попасть, можно вызвать преждевременный взрыв, который убьёт самого Рейдера и нанесёт урон окружающим.

СОВЕТЫ:

- При появлении Ракетного Рейдера не стой на открытом месте. Его ракеты убьют тебя за секунду.
- Используй укрытия: здания, скалы, кратеры, даже щиты Хеллдайверов.
- Ракеты имеют звук пуска. Если ты слышишь характерный свист - немедленно уходи в сторону или ныряй на землю.
- Лучшее оружие против Ракетного Рейдера: РПГ (EAT-17, Recoilless Rifle), снайперские винтовки (Anti-Materiel Rifle), стрелковые лазерные пушки (вроде LAS-98) или дробовик с бронебойными патронами на ближней дистанции.
- Не пытайся идти в лоб с обычной винтовкой. Ты просто потратишь патроны.
- Если Рейдер выпустил залп и перезаряжается - это твой шанс атаковать.
- В группе с щитоносцами сначала убей Ракетного Рейдера, потом разбирайся с ближними.
