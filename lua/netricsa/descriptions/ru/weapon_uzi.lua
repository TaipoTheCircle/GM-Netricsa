ПИСТОЛЕТ-ПУЛЕМЁТ IMI UZI


ОРУЖИЕ:    IMI UZI
ПАТРОНЫ:   Патроны 9×19 мм Парабеллум
CC:        ~600 выстрелов/мин

ОПИСАНИЕ:

Компактный самозарядно / автоматический пистолет-пулемёт с телескопическим затвором и простым инерционно-откатным механизмом (ударно-спусковой механизм с открытым затвором в ранних вариантах). Разработан Узиэлем Галом в Израиле в конце 1940–1950-х годов. Магазин размещён в рукояти-пистолетной рукояти, что делает оружие компактным и удобным для скрытого ношения и управления при одноморозном огне. Отличается простой конструкцией, высокой надёжностью в тяжёлых условиях и лёгкостью обслуживания. Широко применялся в военных, полицейских и охранных структурах; выпускался в нескольких вариантах (Standard, Mini, Micro) и модификациях под другие калибры.

ПРИМЕЧАНИЯ:

- Компактность и высокая цикловая скорострельность компенсируются относительно невысокой точностью на дальностях более 50–100 м.
- Простой в производстве и обслуживании, популярный в XX веке; существуют гражданские и экспортные версии с ограничениями по режимам огня.


