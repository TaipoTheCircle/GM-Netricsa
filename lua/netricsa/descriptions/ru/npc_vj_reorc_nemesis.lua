НЕМЕЗИС С КОРПОРАЦИИ "АМБРЕЛЛА"


КЛАСС:        Биологическое оружие (B.O.W.), Тиран T-типа
МЕТАБОЛИЗМ:   Мутировавший организм, заражённый паразитом NE-α
ВОСПРИЯТИЕ:   Тактический убийца, Элитный охотник
РАЗМЕР:       2,5 м.
ВЫНОСЛИВОСТЬ: Экстремальная
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Ракетная установка, миниган M-134, огнемёт, щупальца, кулаки, предметы окружения
НАГРАДА:      10000
УГРОЗА:       Абсолютная

ОПИСАНИЕ:

Это не просто биологическое оружие. Это живой ночной кошмар, созданный европейским отделением корпорации Амбрелла с одной единственной целью: уничтожить всех выживших членов элитного отряда S.T.A.R.S., виновных в разоблачении преступлений корпорации.

Немезис является результатом заражения Тирана T-103 - той же модели, что и Мистер Икс - экспериментальным паразитом под названием NE-α (Немезис Альфа). Паразит буквально прорастает в нервную систему хозяина, формируя собственный мозг и беря управление телом на себя. Это наделяет монстра не только нечеловеческой силой, но и способностью принимать решения, следовать сложным приказам и, что самое пугающее, использовать оружие.

Из-за сложности совмещения паразита с организмом носителя процент успешных созданий был ничтожно мал - примерно 1 успешный экземпляр на 60 миллионов кандидатов. Каждый такой экземпляр - это огромная ценность для Амбреллы.

В своей базовой форме Немезис - это гигантская, мускулистая гуманоидная туша, одетая в длинное чёрное непромокаемое пальто. Изначально пальто было частью их стандартной экипировки, предназначенной для сдерживания мутаций и защиты от пуль.

Его лицо лишено губ и носа - под натянутой кожей видны оскаленные зубы и мышцы. Правый глаз закрыт грубыми хирургическими стежками. Тело покрыто шрамами и следами препарирования. Он носит чёрные брюки, сапоги и перчатки, а на левом боку его груди виден металлический ограничитель - устройство, сдерживающее его паразита от неконтролируемой мутации.

При повреждении пальто Немезис мутирует: из его тела начнут прорываться наружу длинные, мощные щупальца, которые он использует для захвата жертв, нанесения колющих ударов и даже для быстрого перемещения, цепляясь за стены зданий.

Главное отличие Немезиса от других Тиранов - его интеллект, позволяющий обращаться со сложным вооружением.

Его визитная карточка - тяжелая ракетница или многоствольный гранатомёт, установленный на левой руке. Немезис демонстрирует невероятную точность, легко попадая в движущуюся цель на большом расстоянии.

Также Немезис использует мощный ранцевый огнемёт, способный выплёвывать струи пламени и запускать огненные шары. Даже после разрушения топливного бака он продолжает использовать его в качестве огненной дубины.

Шестиствольный пулемёт, используемый Немезисом, позволяет ему выкашивать целые толпы врагов .

Даже без оружия Немезис смертелен. Его удары способны пробивать бетонные стены и вырывать куски асфальта. Одним ударом он может отправить человека в полёт или сломать шею.

Щупальца Немезиса не просто наносят физический урон. При уколе жертва заражается Т-вирусом, вызывая мутацию или быструю смерть. Он также способен имплантировать паразита NE-α в тела погибших, превращая их в ужасных, управляемых монстров.

Немезис - настоящий монстр во всех смыслах этого слова.

Он Способен пробивать бетонные стены одним ударом, вырывать двери с петель, швырять автомобили и проламывать полы зданий.

Может бегать со скоростью более 100 км/ч (63 мили в час), мгновенно уклоняясь от выстрелов из гранатомёта и ракетницы.

Его главное преимущество - Регенерация. Пули, осколки и ожоги заживают на глазах. Немезис может быть временно выведен из строя взрывом или тяжёлым оружием, но через короткое время он встанет и продолжит погоню.

Выдерживает попадания из гранатомётов, взрывы цистерн с топливом, падения с огромной высоты, обливание кислотой и расплавленным металлом.

Немезис не является бездумной машиной для убийств. У него есть оперативная система и приоритеты. Его главная и единственная цель - отряд S.T.A.R.S..

Вся его стратегия подчинена выслеживанию и уничтожению. Он не отступит, пока его цель не будет мертва или пока его самого не уничтожат.

Немезис способен эволюционировать на лету, проходя через несколько ужасающих стадий.

Первая форма: Базовый Немезис в пальто, вооружённый ракетницей / огнемётом. Умён, быстр и опасен .

Вторая форма: После уничтожения пальто и получения критических повреждений Немезис мутирует в ещё более монструозное создание. Он опускается на четвереньки, становится похож на гигантского зверя, покрывается костяными наростами и отращивает длинные щупальца-хлысты.

Третья форма (Финальная): Тело Немезиса окончательно отказывается контролировать себя. Он превращается в огромную, аморфную массу плоти, костей и щупалец, передвигающуюся ползком. В этой форме он практически невосприимчив к обычному оружию, продолжает преследовать цель и изрыгает облака ядовитой кислоты.

СОВЕТЫ:

- Стреляй и беги. Немезиса (почти) невозможно убить обычным оружием. Твоя цель - временно вывести его из строя, чтобы получить преимущество и скрыться.
- Используй окружение. Взрывающиеся бочки, генераторы, двери, сбрасываемые антенны - всё это может его притормозить.
- Не стой на месте. Он всегда знает, где ты. Любая остановка - это смерть.
- Уворачивайся от ракет. Прямое попадание из его ракетницы убивает мгновенно, даже в полной броне.
- Следи за оружием. Ракетница - дальнобойная угроза, огнемёт - средняя. Если он бежит с кулаками, значит, дистанцию нужно увеличивать.
- Помни про щупальца. Если он рядом, он может схватить тебя щупальцем, даже если не коснулся рукой.
