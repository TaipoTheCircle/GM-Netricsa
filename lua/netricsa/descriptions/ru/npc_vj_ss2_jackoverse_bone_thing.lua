ОПИСАНИЕ:

Эта...штука - является одной из самых загадочных и опасных известных форм жизни планеты Клир. Несмотря на многочисленные свидетельства очевидцев, происхождение существа остаётся неизвестным. Оно не относится ни к одному из известных подвидов армии Ментала и не похоже ни на одного представителя местной фауны.

При первой встрече Нечто легко принять за необычную статую, собранную из костей различных существ Клира. Оно полностью неподвижно и способно оставаться в таком состоянии неопределённо долго. Однако стоит наблюдателю отвернуться или потерять существо из поля зрения хотя бы на мгновение, как оно меняет положение или оказывается значительно ближе.

После нескольких подобных перемещений поведение существа резко меняется. Оно издаёт оглушительный, пронзительный крик и переходит в стремительное преследование цели. Скорость Нечто настолько высока, что известные случаи успешного бегства отсутствуют.

Физический контакт с существом неизменно заканчивается мгновенной гибелью жертвы.

Попытки уничтожить Нечто не принесли никаких результатов. Огнестрельное оружие, взрывчатка, энергетическое вооружение и тяжёлые боевые системы не оказали на него заметного воздействия. По имеющимся данным, существо невозможно уничтожить известными средствами.

Его внешний вид сочетает признаки Скелета с Клира и Костяной змеи с Клира. Из-за этого среди исследователей возникла гипотеза, что Нечто может быть неизвестной формой эволюции этих видов или их общим потомком. Однако ни одного подтверждения этой теории обнаружить не удалось.

Жители Клира избегают мест, где замечали Костяное Нечто. Даже наиболее опытные охотники и ветераны предпочитают не вступать с ним в контакт, а сообщения о его появлении вызывают немедленную эвакуацию ближайших поселений.

