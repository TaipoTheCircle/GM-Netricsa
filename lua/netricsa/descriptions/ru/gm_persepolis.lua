ПЕРСЕПОЛИС, ПЫЛЬ ВЕКОВ НА КОСТЯХ ИМПЕРИЙ


Сэм, Это Персеполис - древняя столица империи Ахеменидов, "Город персов", который Александр Македонский в 330 году до нашей эры превратил в гигантский костёр.

Теперь здесь только пустыня, ветер и руины, помнящие шествие народов от Нила до Инда.

Представь бескрайнюю песчаную равнину, над которой плывёт марево. Посреди неё, на массивной каменной платформе, возвышаются остовы колонн, лестниц и ворот.

Руины хранят следы былого величия:

Ападана - церемониальный зал Дария I. Когда-то здесь возвышались 72 колонны, поддерживавшие крышу. Сейчас уцелело всего 13. Но даже они поражают воображение. К ним ведут гигантские лестницы, по которым когда-то поднимались колесницы и шествовали делегации покорённых народов с дарами.

Тачара - жилой дворец Дария I. От него остались только фундамент, дверные проёмы и нижние части стен с барельефами.

Зал ста колонн - огромное помещение, от которого остались только основания колонн и каменные ворота с батальными сценами.

Гарем Ксеркса - двадцать две небольшие комнаты, где могли жить женщины с детьми.

Гробница Дария III - незаконченная и разрушенная природой гробница последнего царя династии, который бежал от Александра и был убит своими же.

Этот город был не просто столицей, а символом империи, простиравшейся от Египта до Индии. Его строили мастера со всех концов света - вавилоняне, египтяне, ионийцы, ассирийцы. Но труд рабов здесь не использовали, что по тем временам редкость. У города была сложная система водопровода и канализации. Александр сжёг его дотла - то ли в пьяном угаре по подстрекательству афинской гетеры Таис, то ли сознательно, как акт мести за сожжение Акрополя.

В пожаре погибли бесценные экземпляры Авесты, написанные золотыми чернилами на бычьих шкурах. После этого город потерял своё значение, а неподалёку вырос Истахр, ставший духовным центром зороастризма. Сейчас руины Персеполя - объект Всемирного наследия ЮНЕСКО.
