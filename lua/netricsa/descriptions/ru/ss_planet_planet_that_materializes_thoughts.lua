ПЛАНЕТА, МАТЕРИАЛИЗУЮЩАЯ МЫСЛИ


ПЛАНЕТА:        Планета, материализующая мысли
ТИП:            Планета земного типа, Психотропный мир
КЛИМАТ:         Атмосфера непригодна для дыхания
СПУТНИКИ:       Неизвестно
ЛАНДШАФТ:       Скалистая поверхность, вулканы, полное отсутствие жизни
РАСПОЛОЖЕНИЕ:   Неизвестная звёздная система
МЕСТНЫЕ РАСЫ:   Отсутствуют (временно могут появляться материализованные мысли)
ПРИНАДЛЕЖНОСТЬ: Неизвестна
МЕСТА:          Место аварийной посадки Шаролёта, Копия Ромашковой долины

ОПИСАНИЕ:

Атмосфера планеты абсолютно непригодна для дыхания, а поверхность представляет собой безжизненный скалистый ландшафт с многочисленными вулканами. Здесь нет ни растительности, ни животных, ни признаков какой-либо органической жизни.

Уникальное свойство планеты заключается в её способности материализовывать мысли любого разумного существа, находящегося на её поверхности. Мысли становятся вполне реальными, осязаемыми объектами, существующими в пространстве вблизи планеты. Однако созданные таким образом объекты полностью дематериализуются, как только их автор покидает планету.

Когда на эту планету совершил вынужденную посадку Шаролёт, исчерпавший запасы топлива, оказавшийся на ней пилот по имени Пин использовал это свойство для решения проблемы. Он материализовал точную копию своей родной Ромашковой долины вместе с её обитателями, после чего по сохранённым в устройстве чертежам создал ионный двигатель, позволивший покинуть планету.

Покидая планету, Пин оставил материализованным копиям своих друзей совет: если они будут продолжать думать друг о друге, то смогут сохранить своё существование и после его отлёта.

Исследователи предполагают, что планета может обладать коллективным бессознательным или полевым разумом, реагирующим на ментальную активность посетителей, однако точная природа этого феномена остаётся неизученной.

КЛЮЧЕВЫЕ МЕСТА:

- Место аварийной посадки Шаролёта: Участок скалистой поверхности, где совершил посадку потерпевший крушение корабль.
- Копия Ромашковой долины: Точная материализованная реплика родного посёлка Пина, созданная силой мысли и существующая благодаря ментальной энергии своего создателя. Включает точные копии строений и обитателей, обладающих подобием сознания.
