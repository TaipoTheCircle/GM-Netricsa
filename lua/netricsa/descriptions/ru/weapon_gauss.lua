ТАУ-ПУШКА


ОРУЖИЕ:      Тау-пушка (экспериментальное энергетическое оружие)
ПАТРОНЫ:                  Ячейки урана-235
CC:                Непрерывный луч или заряженный выстрел

ОПИСАНИЕ:

Прототип ускорителя частиц, стреляющий высокоскоростными урановыми частицами. Может выпускать быстрые лучи низкой мощности или заряжаться для массивного разряда, способного мгновенно уничтожить цели.

ПРИМЕЧАНИЯ:

- Заряженный режим наносит огромный урон.
- Риск самоповреждения при разряде слишком близко.
- Эффективен как против органических, так и против бронированных врагов.