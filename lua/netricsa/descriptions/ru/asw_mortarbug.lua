ОПИСАНИЕ:

Жук Мортира является специализированной артиллерийской разновидностью Роя, предназначенной для поражения целей на больших дистанциях. Вместо кислотных желёз, характерных для рейнджеров, это существо использует особые органы, производящие нестабильные взрывчатые образования.

Во время атаки Жук Мортира выпускает серию из трёх снарядов по навесной траектории. После падения они прикрепляются к поверхности и детонируют спустя несколько секунд. Благодаря этому Рой способен ограничивать передвижение противника, перекрывать проходы и вынуждать добычу покидать укрытия.

Несмотря на внушительные размеры и плотный панцирь, Жук Мортира плохо приспособлена к ближнему бою. Основную угрозу представляет именно её дальнобойное вооружение, поэтому данные существа обычно держатся позади основных сил Роя под защитой трутней и других агрессивных подвидов.

Кожа Жука Мортиры пропитана едкими биологическими соединениями. Даже после гибели её ткани ещё некоторое время остаются опасными для контакта, что делает разделку туши крайне непопулярным занятием среди полевых исследователей.

СОВЕТЫ:

- Не задерживайся рядом с упавшими снарядами.
- Уничтожай Жуков Мортиры прежде, чем они займут выгодную позицию.
- Используй укрытия против навесного огня с осторожностью.
- Если на полу внезапно появились три подозрительных органических шара, лучше немедленно сменить место пребывания.
