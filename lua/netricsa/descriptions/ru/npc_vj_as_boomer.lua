БУМЕР РОЯ


КЛАСС:        Бумер Роя
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Живая Взрывчатка
РАЗМЕР:       2,4 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Самоподрыв, взрывные капсулы
НАГРАДА:      600
УГРОЗА:       Высокая

ОПИСАНИЕ:

Бумер представляет собой специализированную особь Роя, вся биология которой подчинена одной задаче: доставить максимальное количество взрывчатого вещества как можно ближе к противнику.

В спокойном состоянии Бумер выглядит сравнительно безобидно для представителя Роя такого размера. Однако при обнаружении добычи его внутренние органы начинают стремительно вырабатывать нестабильные химические соединения. Тело существа раздувается, превращаясь в живой резервуар со взрывчатой биомассой.

Для сокращения дистанции Бумер способен совершать стремительные скользящие броски, сбивая противников с позиции и блокируя проходы собственным телом. После сближения организм детонирует, уничтожая себя вместе со всем, что оказалось поблизости.

Особую опасность представляют вторичные взрывные капсулы. После гибели раздутого Бумера они разлетаются вокруг места взрыва и срабатывают спустя несколько секунд. Благодаря этому даже успешное уничтожение существа не всегда означает окончание угрозы.

По мнению ксенобиологов, Бумер является одним из наиболее ярких примеров того, насколько далеко может зайти эволюция, если полностью отказаться от инстинкта самосохранения.

СОВЕТЫ:

- Уничтожай Бумеров до того, как они раздуются.
- После взрыва немедленно покинь опасную зону.
- Не позволяй им приближаться к группе.
- Если существо выглядит так, будто сейчас лопнет, лучше не проверять насколько буквально это утверждение.
