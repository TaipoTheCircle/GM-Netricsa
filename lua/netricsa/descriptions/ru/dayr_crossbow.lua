СТАНДАРТНЫЙ АРБАЛЕТ ВЫЖИВАЛЬЩИКА


ОРУЖИЕ:  Стандартный арбалет Выживальщика
ПАТРОНЫ: Арбалетные болты
СС:      ~1 выстрел в секунду


ОПИСАНИЕ:

Простейший арбалет выживших, предназначенный для экономичного и надёжного ведения боя. Не обладает высокой мощностью, однако компенсирует это доступностью боеприпасов и простотой конструкции.

Оружие стреляет одиночными болтами и не требует сложных ресурсов для эксплуатации, что делает его популярным среди начинающих выживших. При наличии навыков отравления может быть модифицирован для нанесения дополнительного урона ядом, постепенно ослабляющего цель.

ПРИМЕЧАНИЯ:

- Болты легко крафтятся из подручных материалов.
- Может наносить дополнительный урон ядом при соответствующих навыках.
- Эффективен на ближней и средней дистанции.
- Низкий базовый урон по сравнению с более продвинутыми арбалетами.
- Отличный выбор при нехватке огнестрельных боеприпасов.
