ОДИНОКИЕ ХОЛМЫ, ДОРОГА В НИКУДА


Сэм, мы попали в место, которое словно сошло с открытки, которую забыли отправить. Зелёные холмы, чистый воздух, дорога, уходящая за горизонт... и ни души. Только ветер, трава и ощущение, что мир просто выключили, забыв предупредить жителей.

Представь себе бескрайние зелёные поля, плавно переходящие в невысокие холмы. И главная артерия этого места - дорога. Она петляет между холмами, поднимается на пригорки, ныряет в низины и... просто обрывается. Не упирается в стену, не ведёт к воротам, а исчезает в траве, будто её нарисовали и забыли дорисовать.

Что здесь есть:

Холмы - отличные естественные укрытия и позиции для засад. 

Дорога - идеальный ориентир. Если потерялся - просто иди по ней. Она никуда не приведёт, но даст иллюзию цели.

Тишина здесь какая-то... не естественная. Даже ветер шумит так, будто боится разбудить холмы. Смотри в оба, Сэм. Если в таком безлюдном месте кто-то появится - значит, это либо призрак, либо очень плохие новости. А может, просто местный сталкер, который тоже любит тишину и покой. Но на сталкера надежды мало.

Погнали, разведаем, куда ведёт эта дорога.

Спойлер: никуда.
