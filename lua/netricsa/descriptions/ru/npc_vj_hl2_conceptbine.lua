СОЛДАТ ОБЪЕДИНЁННОЙ ГВАРДИИ АЛЬЯНСА


КЛАСС:        Трансчеловеческий солдат, гвардия Альянса
МЕТАБОЛИЗМ:   Улучшенный человек, мужчина / ограниченные биомеханические модификации
ВОСПРИЯТИЕ:   Тактические датчики, улучшенные рефлексы, прямая связь с командованием
РАЗМЕР:       190-200 см.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Экстремальная
ОРУЖИЕ:       Тяжёлый дробовик SPAS-12, гранаты
НАГРАДА:      600
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Солдаты Объединённой гвардии Альянса - элитная разновидность трансчеловеческой пехоты, стоящая между обычными силами Overwatch и специализированными ударными подразделениями. В отличие от стандартных солдат Альянса, гвардейцы сохраняют большую долю физиологической автономии, но при этом подвергнуты более глубокой психологической обработке и селективным имплантационным улучшениям.

Их основная роль - удержание ключевых позиций, охрана стратегических объектов и сопровождение высокоценных транспортов, таких как APC Transporter и Combine Track. Именно гвардейцы первыми высаживаются и последними покидают зону операции.

Экипировка солдата Объединённой гвардии тяжелее стандартной: усилённая броня, стабилизаторы отдачи и встроенные системы контроля жизненных показателей. Их шлемы и респираторы искажают голос, придавая ему холодное, обезличенное звучание, лишённое эмоций и индивидуальности.

Гвардейцы не стремятся к быстрой зачистке - их цель в подавлении и удержании, даже если это требует затяжного боя.

СОВЕТЫ:

- Не вступай с ними в затяжной бой - выносливость гвардейцев выше стандартной.
- Гранаты и концентрированный огонь - лучший способ прорвать оборону.