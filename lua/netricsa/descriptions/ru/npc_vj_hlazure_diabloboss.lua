ДИАБЛО


КЛАСС:        Экспериментальная химера Зена, Гибрид Пантерая
МЕТАБОЛИЗМ:   Искусственное слияние нескольких Организмов
ВОСПРИЯТИЕ:   Усиленные чувства Зена, псионическое восприятие
РАЗМЕР:       ~3 м.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Экстремальная
ОРУЖИЕ:       Проекция кристалла Зена: Выстреливает концентрированными кристаллическими энергетическими снарядами из своего спинного гребня.
НАГРАДА:      5000
УГРОЗА:       Катастрофическая

ОПИСАНИЕ:

Диабло - это огромное, искусственно созданное чудовище, созданное путём ужасающего сращивания человеческих учёных, Чамтоада, ДНК Вортигонта и кристаллической материи Зена. Его тело слабо светится фиолетовой энергией, а кровь остаётся тревожно-красной, выдавая его человеческое происхождение.

В отличие от своих собратьев-пантераев, Диабло - не просто хищник, это живое оружие, созданное для сочетания дикой свирепости с дистанционными атаками кристаллической энергии. Его массивная фигура возвышается над низшими существами, почти вдвое больше Пантерая, с широкой бронированной спиной, увенчанной пульсирующим кристаллическим гребнем, направляющим разрушительные снаряды.

Присутствие Диабло на поле боя ошеломляет: его огромные размеры, тёмно-фиолетовый окрас и багровая кровь делают его одновременно внушающим благоговение и ужас. Это не природный зверь Зена, а преднамеренный эксперимент по превращению его в оружие, хищник последней стадии развития, не имеющий естественных конкурентов.

СОВЕТЫ:

- Отдавайте приоритет укрытию - его кристаллические взрывы смертоносны на расстоянии.
- Для его уничтожения крайне важно использовать тяжёлую артиллерию (ракеты, взрывчатку).
- Продолжайте двигаться - его дальность атаки в ближнем бою и прыжок огромны.
