ОПИСАНИЕ:

Тяжёлый тюремный солдат Overwatch с миниганом - это ходячая огневая точка, созданная на базе персонала Нова Проспекта, но предназначенная далеко не только для тюремных стен. Его броня многослойна, утяжелена и напичкана демпфирующими вставками, позволяющими вести непрерывный огонь без потери устойчивости.

Внешне он напоминает стандартного тюремного солдата Нова Проспекта: жёлтые визоры, характерная форма брони. Однако его сразу выдают многочисленные жёлтые опознавательные элементы, предупреждающие союзников - перед ними не охранник, а средство подавления.

Таких бойцов используют для подавления бунтов, защиты стратегических точек, сопровождения конвоев и штурма укреплённых позиций.

СОВЕТЫ:

- Никогда не выходи на открытую линию огня - миниган не прощает ошибок.
- Используй взрывчатку или тяжёлое вооружение.