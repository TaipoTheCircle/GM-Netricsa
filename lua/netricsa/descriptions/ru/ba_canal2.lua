КАНАЛЫ ЧЁРНОЙ МЕЗЫ, ЗОНА УТИЛИЗАЦИИ


Сэм, добро пожаловать в самое "ароматное" место Чёрной Мезы - зону уничтожения мусора. Тут воняет горелой пластмассой, скрежещет металл и... да, твари Ментала уже здесь. Не гости, а полноправные хозяева.

Громадный мусороуничтожитель в воде - жуёт и давит всё, что попадает в пасть. 

Рычаг аварийного сброса - ржавый, будто вот‑вот отвалится. Если дёрнуть - грузовой лифт спустит взрывоопасную коробку, её нужно сунуть прямо в механизм.

Вперёд, Сэм. Рычаг ждёт. Взрыв - на подходе. 