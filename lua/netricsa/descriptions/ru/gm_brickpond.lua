КИРПИЧНЫЙ ВОДОЁМ, ВЕТЕР ПАХНЕТ СМЕРТЬЮ


Сэм, это место - как та самая комната в хорроре, куда заходить нельзя, но ты уже зашёл, потому что дверь открыта, а любопытство - твой главный враг.

На первый взгляд - обычный райончик. Кирпичные стены, водоём, пара домов. Тишь, гладь, кирпичная благодать.

А потом ты заметишь детали. И поймёшь: здесь кто-то умер. И не просто умер, а остался тут навсегда.

Кирпичные стены образуют некое подобие двора или тупика. В центре - небольшой водоём. Вода, неподвижная, как зеркало в доме с привидениями.

Вокруг- тишина. Только ветер. Он здесь главный звукорежиссёр. Дует, завывает, шепчет. Создаёт ощущение, что ты не один, хотя вокруг ни души. Кроме одной.

Что здесь есть:

Первый дом - "Открытый". В него можно войти. Внутри - четыре ящика. И больше ничего. Ни стульев, ни столов, ни запасов Софии. Просто четыре пустых ящика. Ждут, когда ты заглянешь внутрь и разочаруешься.

Второй дом - "Закрытый". Войти нельзя. Дверь заперта? Замурована? Просто не предусмотрена? Неважно. Главное - за стеной этого дома, в замурованном пространстве, лежит растерзанный труп. В собственной крови. Кто-то не просто умер - его разорвали. И оставили там, за стеной. Как напоминание. Или как предупреждение.

Ты не видишь труп напрямую. Но теперь ты знаешь, что он там. За стеной. В доме, куда нельзя зайти.

Ветер. Он здесь не просто фон. Он здесь как голос этого места. То затихает, то усиливается, то начинает завывать так, что мурашки бегут по спине даже у меня, а у меня спины нет.

Водоём. Тёмная вода, в которой никто не купается.

Это место не для тебя. И не для меня. Оно для того, кто остался за стеной. "Навсегда" (Tazik29).
