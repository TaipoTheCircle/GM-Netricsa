БИНОКЛЬ


ПРЕДМЕТ:  Бинокль (оптический прибор)
ПАТРОНЫ:  Нет
ОБЪЕКТИВ: Обычно от 4× до 10× и более / диаметр объектива 25-50 мм 

ОПИСАНИЕ:

Оптический прибор, состоящий из двух параллельных зрительных труб для наблюдения двумя глазами одновременно; даёт стереоскопическое (объёмное) изображение и увеличивает удалённые объекты.  Бинокли применяются в быту, спорте, астрономии, морской службе и в военной сфере; некоторые модели комплектуются дальномером, компасом, ночным видением или гиростабилизацией. 

ПРИМЕЧАНИЯ:

- Ключевые параметры: кратность (увеличение) и диаметр объектива; показатель "10x40" означает ×10 увеличение и 40 мм объектив. 
- Выходной зрачок = диаметр объектива / кратность; важен для сумеречного видения (рекомендовано ≈4-7 мм в зависимости от условий и возраста наблюдателя). 
- Фактор сумерек (√(кратность × диаметр объектива)) даёт грубую оценку пригодности для сумерек, но не учитывает качество просветления оптики.