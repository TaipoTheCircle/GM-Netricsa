ТРУБНЫЙ РАЗВОДНОЙ КЛЮЧ


ОРУЖИЕ:      Трубный Разводной ключ
БОЕПРИПАСЫ:        Нет
СКОРОСТЬ СТРЕЛЬБЫ:         Переменная - быстрый размах (~60 размахов/мин) или заряженный удар (~20 размахов/мин)

ОПИСАНИЕ:

Тяжелый промышленный трубный ключ, переделанный в оружие ближнего боя во время инцидента в Чёрной Мезе. Наносит мощные тупые удары быстрыми замахами или сокрушительные заряженные удары сверху.

ПРИМЕЧАНИЯ:

- Основная атака: быстрые замахи, наносящие умеренный урон.
- Вторичная атака: заряженный удар с увеличенным уроном и отталкиванием.
- Бесшумное оружие, полезное для экономии боеприпасов.
- Неэффективно против удаленных или сильно бронированных врагов.
- Прочное и всегда доступное, что делает его надежным орудием последней надежды.