ПИСТОЛЕТ-ПУЛЕМЁТ MP5K


ОРУЖИЕ:  Пистолет-пулемёт MP5K
ПАТРОНЫ: Патроны среднего калибра
СС:      ~700 выстр./мин.

ОПИСАНИЕ:

Компактный пистолет-пулемёт первоначальной разработки, использовавшийся как скрытое / резервное оружие. Отличается сбалансированной точностью и контролируемой скорострельностью, что делает его предпочтительным выбором для ближних боёв и мобильных столкновений.

ПРИМЕЧАНИЯ:

- Эффективен против лёгких и средних целей на коротких дистанциях. 
- Компактность снижает отдачу и упрощает ведение огня в ограниченном пространстве. 
- Использует стандартный средний боеприпас, совместимый с другими стрелковыми системами.