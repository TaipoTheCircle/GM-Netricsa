ПОМПОВЫЙ ДРОБОВИК REMINGTON 870


ОРУЖИЕ:     Remington 870
ПАТРОНЫ:    12/70 калибра
CC:         ~60 выстрелов/мин

ОПИСАНИЕ:

Американское помповое ружьё (дробовик). Предназначено для самообороны, охоты, спортивной стрельбы и вооружения армейских и полицейских подразделений.

ПРИМЕЧАНИЯ:

- Данное ружье эффективно на низких-средних расстояниях.
- Для дальних расстояний, можно сменить тип патрон на SLUG.


