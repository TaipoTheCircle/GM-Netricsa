РАЗБОЙНИК С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Разбойник
МЕТАБОЛИЗМ:   Человек
ВОСПРИЯТИЕ:   Разбойник с арбалетом, лёгкая пехота Илладжеров
РАЗМЕР:       1,9 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная 
ОРУЖИЕ:       Арбалет 
НАГРАДА:      100
УГРОЗА:       Высокая

ОПИСАНИЕ:

Одна из самых распространённых и узнаваемых разновидностей враждебных жителей, вооружённая арбалетом и не знающая пощады. Если обычный житель торгует с тобой и строит фермы, то разбойник просто хочет превратить тебя в подушечку для стрел.

В отличие от своих родственников-виндикаторов, которые предпочитают ближний бой, разбойники - прирождённые снайперы. Их главное и единственное оружие - арбалет, из которого они стреляют каждые 3 секунды по цели в радиусе до 16 метров. При полном погружении в воду разбойники переходят на ближний бой, так как арбалет под водой не работает.

Обитают разбойники в трёх местах, и каждое сулит неприятности. Во-первых, это разбойничьи патрули - группы из 2-5 мародёров, которые бродят по миру в поисках деревень. Во-вторых, это аванпосты разбойников - сторожевые башни с клетками для железных големов, вокруг которых постоянно обитают эти стрелки. И в-третьих - рейды, где разбойники составляют костяк первых волн захватчиков.

Главная особенность разбойников - их трусливая, но эффективная тактика. Они никогда не лезут в ближний бой, предпочитая держать дистанцию и методично расстреливать цель. Если игрок приближается, разбойник пятится назад, стараясь сохранить дистанцию. В узких коридорах это может быть фатально, так как увернуться от стрелы сложно.

Среди разбойников встречаются капитаны - мародёры с зловещим флагом на голове. Их легко узнать по чёрно-белому знамени, развевающемуся над капюшоном. Убийство такого капитана накладывает эффект "Дурное знамение". Уровень эффекта зависит от количества убитых капитанов. С этим эффектом при входе в деревню начинается рейд - серия волн, где разбойники и другие илагеры пытаются уничтожить всех жителей.

В рейдах разбойники ведут себя особенно агрессивно, целенаправленно атакуя жителей. Если им удаётся убить жителя или разрушить дверь, они издают победный смех и подпрыгивают на месте.

Разбойники могут появляться верхом на разорителях - огромных зверях, которые крушат всё на своём пути . Всадник на разорителе - особенно опасный противник, так как нужно уворачиваться и от стрел, и от тарана.

В рейдах разбойники иногда дропают изумруды. Капитаны всегда оставляют зловещий флаг - ценный трофей и источник проблем.

Если у разбойника ломается арбалет (у арбалетов есть прочность, как и у любого инструмента), он не может стрелять и становится беспомощным, хотя формально остаётся враждебным.

СОВЕТЫ:

- Щит полностью блокирует стрелы и даёт возможность сблизиться без потерь.
- Не беги прямо на разбойника. Стреляй в ответ из лука или арбалета - урон от твоих стрел такой же, как и от его.
- В узких коридорах не стой на открытом месте - стрела прилетит точно.
- Если видишь капитана с флагом, подумай дважды, прежде чем его убивать. Эффект "Дурное знамение" запустит рейд, как только ты войдёшь в деревню.
- Молоко снимает эффект "Дурное знамение", если ты ещё не готов к рейду.
- В воде разбойник беспомощен и переходит на ближний бой, который наносит меньше урона.
- В рейдах старайся убивать разбойников первыми - они мешают сосредоточиться на более сильных врагах.
- Не давай им окружить себя - стрелы с двух сторон гарантированно попадут.
- Если разбойник верхом на разорителе, сначала убей лучника - без всадника зверь менее опасен.
