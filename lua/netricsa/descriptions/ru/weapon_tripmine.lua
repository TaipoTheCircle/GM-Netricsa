МИНА-ЛОВУШКА


ОРУЖИЕ:      Лазерная Мина-Ловушка
ПАТРОНЫ:           Нет (взрывное устройство, которое можно установить)

ОПИСАНИЕ:

Устанавливаемая энергетическая мина с видимым лазерным спусковым механизмом. Крепится к стенам и взрывается при пересечении лазера, нанося огромный урон по площади.

ПРИМЕЧАНИЯ:

- Чрезвычайно мощное оружие-ловушка.
- Видимый лазер может предупредить внимательных врагов.
- Рискованно использовать вблизи.