ЛЕДЯНАЯ ПЛАНЕТА


ПЛАНЕТА:        Ледяная планета
ТИП:            Планета земного типа, Крио-мир
КЛИМАТ:         Глубокие заморозки
СПУТНИКИ:       Неизвестно
ЛАНДШАФТ:       Сплошной ледяной покров, незамерзающие водоёмы, ледяные мосты
РАСПОЛОЖЕНИЕ:   Неизвестная звёздная система
МЕСТНЫЕ РАСЫ:   Ледяные Големы, Снежные Мамутмены, Снежные Мамуты, Снежные Быки-Оборотни
ПРИНАДЛЕЖНОСТЬ: Орда Ментала
МЕСТА:          Ледяной мост, Чёрные монолиты, Стойбища Мамутменов

ОПИСАНИЕ:

Ледяная планета представляет собой мир, полностью покрытый льдом, визуально напоминающий полярные регионы Земли, такие как Антарктида. Её поверхность состоит из бескрайних ледяных полей, среди которых встречаются открытые участки воды, не скованные замерзанием.

Коренными обитателями планеты являются Ледяные Големы - стихийные существа, состоящие из чистого льда.

Также здесь обитают Снежные Мамутмены - примитивная гуманоидная раса, приспособившаяся к суровым условиям холода. Мамутмены используют в качестве вьючных и боевых животных Снежных Мамутов - массивных лохматых созданий, напоминающих мамонтов. Отдельную угрозу представляют Снежные Быки-Оборотни.

В глубине ледяных полей возвышаются два загадочных Чёрных монолита - сооружения неизвестного происхождения, к которым ведёт единственный путь - длинный Ледяной мост, перекинутый через незамерзающие воды.

КЛЮЧЕВЫЕ МЕСТА:

- Ледяной мост: Переправа через незамерзающие воды, ведущая к двум загадочным чёрным монолитам.
- Чёрные монолиты: Два загадочных сооружения неизвестного происхождения и назначения, расположенные по ту сторону ледяного моста.
- Стойбища Мамутменов: Поселения местной гуманоидной расы, где содержатся прирученные Снежные Мамуты.
