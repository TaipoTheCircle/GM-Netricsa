ЗОМБИ-ХЕДКРАБ, ЗЕМНАЯ АРКТИЧЕСКАЯ РАЗНОВИДНОСТЬ


КЛАСС:         Зомби-Хедкраб, Земной (арктический)
МЕТАБОЛИЗМ:    Паразитический хозяин, адаптированный к холоду
ВОСПРИЯТИЕ:    Зомбированная сущность
РАЗМЕР:        1,8 м.
ВЫНОСЛИВОСТЬ:  Средняя
ВРАЖДЕБНОСТЬ:  Высокая
ОРУЖИЕ:        Когти, укус, прыжок хедкраба
НАГРАДА:       220
УГРОЗА:        Средняя

ОПИСАНИЕ:

Арктический зомби-хедкраб - это человек, заражённый шерстяной земной разновидностью хедкраба и приспособленный к условиям постоянного холода. Паразит сохраняет контроль над нервной системой хозяина, но холод заметно меняет поведение и внешний вид существа.

Тело хозяина часто укутано в обрывки зимней одежды, промёрзшую форму или костюмы, сросшиеся с плотью. Хедкраб, покрытый густой шерстью, глубоко вцепляется в череп, дополнительно защищая себя от холода.

СОВЕТЫ:

- Цельтесь в хедкраба - шерсть защищает его частично, но точные попадания всё ещё эффективны.