КЛАСС:        Гибридный солдат Overwatch Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Универсальное боевое подразделение Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Очень высокая
ОРУЖИЕ:       Импульсная винтовка AR2, SMG, гранаты, Электро дубинка
НАГРАДА:      600
УГРОЗА:       Средне-высокая

ОПИСАНИЕ:

Гибридный солдат Overwatch - это стандартный голубо-визорный солдат Альянса, усиленный дополнительной бронёй и оборудованием, превращающим его в универсального бойца для самых разных условий. В отличие от элитных подразделений, он не специализируется на одном типе боя, зато стабильно опасен в любом.

Его броня носит частично декоративный характер: усиленные пластины, нашивки и элементы психологического давления создают образ более тяжёлого и "важного" бойца, чем он кажется на бумаге. Тем не менее, защита действительно улучшена по сравнению с обычными солдатами Overwatch.

Ключевая особенность - устройство ночного видения, закреплённое на шлеме. Оно работает не только как ПНВ, но и как бинокль, позволяя солдату вести наблюдение, отмечать цели и корректировать огонь без поддержки с воздуха. В условиях тумана, ночи или полярной темноты такие бойцы становятся особенно неприятными.

Электрошёкер почти не используется, но предназначен для подавления, захвата и демонстративной жестокости. Альянс часто применяет гибридных солдат для зачисток, патрулей и операций по контролю населения, где требуется не только огневая мощь, но и показательная сила.

СОВЕТЫ:

- Не недооценивай его из-за голубого визора - броня и снаряжение делают его опаснее обычного солдата.
- В темноте он видит лучше тебя.
- Уничтожай с дистанции, не давая сблизиться и использовать гранаты.