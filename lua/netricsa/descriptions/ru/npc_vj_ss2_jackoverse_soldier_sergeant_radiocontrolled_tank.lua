РАДИОУПРАВЛЯЕМЫЙ РОБОТ-ОХОТНИК СЕРЖАНТА ШТУРМОВИКОВ


КЛАСС:        Робот-Охотник Сержанта Штурмовиков
МЕТАБОЛИЗМ:   Механический
ВОСПРИЯТИЕ:   Боевой Дрон-Убийца, "Игрушка" Сержанта
РАЗМЕР:       ~1,6 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Встроенный Миниган (справа), Дробовик (слева), Ракетный Блок (на голове)
НАГРАДА:      1200
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это одна из "игрушек", которую Сержант Штурмовиков лично сконструировал и запустил в производство. Этот боевой дрон создан для выполнения самых опасных задач: разведка, подавление огневых точек, охота на одиночные цели и поддержка пехоты. Он не является автономным - им управляет Сержант дистанционно, но это не делает его менее опасным.

Основа робота - два гусеничных блока тёмно-зелёного и чёрного цветов. Гусеницы позволяют ему передвигаться по пересечённой местности, преодолевая камни, обломки и даже небольшие стены. Над гусеницами расположен центральный корпус.

От корпуса отходят две шарнирные манипуляторные руки механического вида. Правая рука оснащена вращающимся пулемётом, а левая - дробовиком. Над головой, на сферическом модуле с оранжевым ободком и зелёным светящимся "глазом", закреплён блок с ракетами. Робот может использовать любой тип оружия в зависимости от ситуации: пулемёт - для подавления огнём, дробовик - для ближнего боя, ракеты - для уничтожения бронированных целей и укрытий.

Шея робота гибкая, что позволяет головному модулю вращаться и отслеживать цели в широком диапазоне.

СОВЕТЫ:

- Робот мобилен на гусеницах, но не очень быстр. Используй укрытия и обходи его с флангов.
- Пулемёт и дробовик имеют разную эффективность на разных дистанциях. Держись на средней дистанции, чтобы минимизировать урон.
- Головной модуль - слабое место. Несколько точных попаданий - и от него останется только груда металла.
- Не пытайся атаковать робота в лоб. Его оружие покрывает большую площадь.
- Робот - "игрушка" Сержанта. Если ты видишь его на поле боя, значит, Сержант где-то рядом. Будь готов к встрече с ним самим.
