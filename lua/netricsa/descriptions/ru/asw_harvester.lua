ЖНЕЦ РОЯ


КЛАСС:        Жнец Роя
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Поддерживающая Особь
РАЗМЕР:       2,5 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Порождение ксеноклещей, едкая кожа
НАГРАДА:      500
УГРОЗА:       Высокая

ОПИСАНИЕ:

Жнец представляет собой специализированную форму Роя, предназначенную для быстрого воспроизводства боевых организмов непосредственно на поле боя. В отличие от большинства представителей своего вида, он редко атакует противника самостоятельно, предпочитая создавать ксеноклещей и направлять их против обнаруженных целей.

Наиболее заметной особенностью Жнеца является крупный светящийся орган на спине. Внутри него формируются зародыши ксеноклещей, которые покидают тело носителя вскоре после созревания. Скорость размножения позволяет одному Жнецу за короткое время создать целую стаю этих существ.

При возникновении угрозы Жнец старается увеличить дистанцию до противника, продолжая производить потомство даже во время отступления. Благодаря этому уничтожение Жнеца нередко оказывается более сложной задачей, чем может показаться на первый взгляд.

После гибели организм переживает кратковременный процесс посмертного выброса, в ходе которого оставшиеся внутри ксеноклещи покидают тело и немедленно переходят в атаку. По этой причине уничтожение Жнеца далеко не всегда означает окончание угрозы.

Несмотря на сравнительно слабую защиту, ткани Жнеца содержат большое количество едких веществ. Контакт с его телом способен вызвать серьёзные повреждения даже после смерти существа.

СОВЕТЫ:

- Уничтожай Жнецов прежде, чем они создадут большую стаю.
- Не позволяй им отступать в безопасные зоны.
- Будь готов к появлению ксеноклещей после уничтожения цели.
- Если Жнец прожил достаточно долго, значит скоро придётся разбираться уже не с одним противником.
