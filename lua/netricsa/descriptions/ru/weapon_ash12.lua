ШТУРМОВАЯ ВИНТОВКА АШ-12


ОРУЖИЕ:      АШ-12
ПАТРОНЫ:     12,7 мм.
CC:          ~650  выстрелов/мин 

ОПИСАНИЕ:

Российский крупнокалиберный штурмовой автомат, созданный для нужд подразделений спецназначения ФСБ России.

ПРИМЕЧАНИЯ:

- Эффективна на средней дистанции; точность снижается на больших расстояниях.
- Хорошо пробивает стены и другие материалы.
