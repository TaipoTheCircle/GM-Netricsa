ОПИСАНИЕ:

Эта планета - оживлённый перекрёсток космических трасс, одна из ключевых торговых и туристических планет Галактики. В отличие от многих миров, она не замкнута на себе, а открыта для бесчисленных гостей со всех уголков космоса. Планета известна своим колоритным пейзажем, который поэты описывают как сочетание "рыжего неба и голубого песка".

Главным центром цивилизации на Блуке является город Палапутра, практически полностью застроенный складами и гостиницами, рассчитанными на приём самых разных форм жизни. Космодром Палапутры - один из крупнейших в секторе, способный принимать множество пассажирских и чартерных рейсов ежедневно.

Коренное население планеты - ушаны, разумные гуманоиды, получившие своё название из-за больших, подвижных ушей. Уши для них - не просто орган слуха, а важнейший инструмент коммуникации. Помимо обычной речи, ушаны владеют тайным языком, основанным на сложных движениях ушей. В минуты страха они поджимают уши, а когда им стыдно - прикрывают ими лицо. В спорах ушаны способны создавать порывы ветра, способные сдувать со столов тяжёлые предметы.

Блук также является родиной уникальных разумных птиц - говорунов. Эти создания обладают феноменальной памятью (запоминают всё, что видят и слышат) и способны к межзвёздным перелётам со сверхсветовой скоростью.

На планете расположен главный говоруний питомник во всём обитаемом космосе.

КЛЮЧЕВЫЕ МЕСТА:

- Город Палапутра: Столица и единственный крупный город планеты, построенный вокруг космодрома. Архитектура ориентирована на обслуживание туристов и торговцев: бесчисленные гостиницы, складские комплексы и места отдыха для существ с разных планет.
- Космодром Палапутры: Огромный транспортный узел, принимающий регулярные и чартерные рейсы со всей Галактики. Один из самых загруженных космопортов в секторе.
- Филателистический рынок: Крупнейший в Галактике рынок марок, уступающий лишь рынку в Туманности Андромеды. Каждую неделю здесь собираются миллиарды коллекционеров. Территория размечена на 14 300 столиков, за которыми филателисты с разных планет обмениваются марками. Здесь можно найти редчайшие экземпляры, включая легендарные марки с планеты Шешинеру (негашёные меняют изображение по чётным числам, а гашёные вообще пусты) и чудо инженерной мысли - альбомы с планеты Фикс, которые сами устанавливают марки на нужное место.
- Главный говоруний питомник: Центр разведения и сохранения уникальных птиц-говорунов. Единственное место в Галактике, где можно гарантированно приобрести или заказать эту редкую птицу.
