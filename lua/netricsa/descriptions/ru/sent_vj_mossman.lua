ОПИСАНИЕ:

Доктор Джудит Моссман - блестящий учёный, работающий на Сопротивление, хотя её лояльность никогда не очевидна. Выдавая себя за союзницу Элая Вэнса, Айзека Кляйнера и Гордона Фримена, она тайно общается с доктором Брином, предоставляя Альянсу разведданные об операциях Сопротивления. Амбициозная, расчётливая и движимая инстинктом самосохранения, Моссман балансирует на грани предательства и искупления.

Её истинные мотивы противоречивы: будь то прагматичная выжившая, ищущая расположения Альянса, или учёный, оказавшийся между двух огней, её поступки определяют курс Сопротивления. Несмотря на свою двуличность, Моссман в конечном счёте проявляет мужество - например, восстаёт против Брина, когда на кону жизнь Элая.

Моссман не солдат, но её интеллект и хитрость делают её опасной, как пули. Для Сопротивления она одновременно и обуза, и потенциальный спаситель, в зависимости от того, какая сторона её натуры возьмёт верх.

