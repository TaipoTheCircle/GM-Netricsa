АБЕРРИРОВАННАЯ ГОРИЛЛА С АРХИПЕЛАГА КРИГЕРА


КЛАСС:         Аберрированный Мутант, Горилла
МЕТАБОЛИЗМ:    Триген-мутировавший примат
ВОСПРИЯТИЕ:    Штурмовой боец
РАЗМЕР:        4,8 фута
ВЫНОСЛИВОСТЬ:  Высокая
ВРАЖДЕБНОСТЬ:  Крайне высокая
ОРУЖИЕ:        Клешневидные конечности, мощные удары
НАГРАДА:       600
УГРОЗА:        Критическая

ОПИСАНИЕ:

Наиболее устойчивая версия приматов-тригенов. Полностью лишены шерстяного покрова, с деформированным черепом, сросшимся с грудной клеткой. Передние конечности превратились в гигантские серповидные клешни, аналогичные конечностям богомола. Несмотря на меньшую прыгучесть по сравнению с шимпанзе, обладают повышенным запасом здоровья и наносят катастрофический урон в ближнем бою.

СОВЕТЫ:

- Требует концентрации огня - из-за повышенной живучести одиночные выстрелы малоэффективны.
- Используй взрывчатку - площадь поражения компенсирует их высокую скорость перемещения.
- Не допускай сближения - даже броня не спасает от их клешней.

