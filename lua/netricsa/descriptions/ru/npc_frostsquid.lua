МОРОЗНЫЙ БУЛЛСКВИД С ЗЕНА


КЛАСС:        Буллсквид, морозная разновидность
МЕТАБОЛИЗМ:   Хищник (криогенная мутация)
ВОСПРИЯТИЕ:   Наземный Хищник Зена
РАЗМЕР:       2,4 м.
ВЫНОСЛИВОСТЬ: Средняя (но устойчива к холоду)
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Замораживающий плевок, ледяные когти, леденящий удар хвостом
НАГРАДА:      500
УГРОЗА:       Высокая

ОПИСАНИЕ:

Морозные Буллсквиды эволюционировали, чтобы выпускать переохлаждённую жидкость, которая замораживает добычу намертво. Их укусы и удары хвостом замедляют жертву, делая побег практически невозможным. Их боятся за то, что они превращают поля сражений в ледяные могилы.

СОВЕТЫ:

- Тепловая защита помогает противостоять ледяному плевку.
- Сохраняйте подвижность, чтобы избежать обездвиживания холодом.
- Огнестрельное оружие чрезвычайно эффективно.