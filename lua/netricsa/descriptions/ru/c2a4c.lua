ЧЁРНАЯ МЕЗА, ДРОБИЛЬНЫЕ КОНВЕЙЕРЫ


Зал тянется бесконечно, и его доминируют широкие конвейерные ленты, движущиеся в разных направлениях, словно артерии какого-то механического зверя. Груз – или то, что от него осталось – мчится по лентам к огромным гидравлическим прессам, которые ритмично обрушиваются вниз. Один шаг – и вас постигнет та же участь, что и обломки, уже размазанные по лентам.

Данные об окружающей среде:

Параллельные конвейеры работают с разной скоростью, заставляя постоянно перескакивать с одного на другой.

Дробилки работают по расписанию, но непредсказуемо: датчики показывают, что некоторые прессы смещены, ударяя быстрее или сильнее, чем ожидалось.

Техобслуживающие проходы над системой обеспечивают временную безопасность, но они узкие и нестабильные.

Текущая угроза: существа из Зена рыщут по лентам, пугающе хорошо приспосабливаясь к движущимся платформам. Хедкрабы цепляются за механизмы, буллсквиды стреляют из-за конвейерных линий, а в тенях бесшумно крадутся более крупные хищники, выжидая момента, когда можно совершить ошибку.

Рекомендация: относись к механизмам так же, как и к существам, как к врагу. Движения должны быть точными, а время - идеальным. В этом секторе выжить означает подстроиться под ритм машины - или быть ею раздавленным.