СКАРАБЕЙ С ТЕРРАФОРМИРОВАННОЙ ПЛАНЕТЫ


КЛАСС:        Скарабей
МЕТАБОЛИЗМ:   Насекомое
ВОСПРИЯТИЕ:   Бронированный таранный жук
РАЗМЕР:       ~4,5 м. (в длину)
ВЫНОСЛИВОСТЬ: высокая
ВРАЖДЕБНОСТЬ: крайне высокая
ОРУЖИЕ:       Шип на голове, таран, жвала, масса тела
НАГРАДА:      600
УГРОЗА:       Высокая

ОПИСАНИЕ:

это то, во что превращается солдат-жук, если его кормить стероидами и приказать забыть про всякую тактику. Шесть массивных лап, бронированный панцирь того же сине-жёлтого цвета, и главное отличие - огромный шип на голове, которым эта тварь крушит всё, что не успело увернуться.

В бою скарабей туп, но напорист. Он не будет плеваться кислотой и притворяться мёртвым. Его задача - разогнаться и влететь в цель на полной скорости. Шип на голове пробивает даже самую силльную броню.

СОВЕТЫ:

- Не стой на одной линии. Скарабей разгоняется быстро, но если разогнался - увернуться сложно.
- Стреляй в лапы. Подбитая тварь теряет скорость и маневренность.
