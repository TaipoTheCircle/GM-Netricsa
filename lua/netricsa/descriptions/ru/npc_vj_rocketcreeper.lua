РАКЕТНЫЙ КРИПЕР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Ракетный Крипер
МЕТАБОЛИЗМ:   Растительно-Химический
ВОСПРИЯТИЕ:   Растительно-Пороховое Существо
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Чрезвычайно Высокая
ОРУЖИЕ:       Прыжок С Последующим Самоподрывом
НАГРАДА:      160
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это доказательство того, что природа любит экспериментировать с формами самоподрыва. Если обычный крипер просто подходит и взрывается, а праздничный кидается взрывчаткой, то для него лучший способ нанести урон - эффектно приземлиться на голову жертвы головой вниз.

Поведение ракетного крипера уникально. Как и обычный крипер, он преследует жертву. Но когда дистанция сокращается до нужной, он не шипит и не убегает. Вместо этого он резко взлетает в воздух, а затем камнем падает головой вниз, прямо на цель. Взрыв - и всё, что было вокруг, разлетается на куски. Сила взрыва такая же, как у обычного крипера, что достаточно, чтобы убить цель без брони.

Если ракетный крипер попадает под удар молнии, он становится заряженным. В этом режиме сила взрыва увеличивается вдвое - как у заряженного крипера.

СОВЕТЫ:

- Может подкрасться незаметно.
- Ракетный крипер взрывается только в конце своего прыжка. Если увернуться в последний момент, можно избежать большей части урона.
- Не стой на месте. Пока он летит, у тебя есть время отбежать в сторону.
- Заряженный ракетный крипер крайне опасен. Его взрыв может уничтожить даже укреплённые постройки. Если увидишь его, лучше держаться подальше.
- Как и все криперы, он не умирает от солнца.
