СНЕЖНАЯ СВИНЬЯ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Снежная свинья
МЕТАБОЛИЗМ:   Теплокровное холодоустойчивое млекопитающее
ВОСПРИЯТИЕ:   Дикое / домашнее животное 
РАЗМЕР:       ~1 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Отсутствует
НАГРАДА:      30 
УГРОЗА:       Низкая

ОПИСАНИЕ:

Это редкая разновидность обычных свиней, приспособившаяся к жизни в заснеженных биомах. В отличие от своих розовых собратьев с равнин, эти свиньи обросли густой белой или светло-серой шерстью, которая защищает их от холода.

Они мирно бродят по снегу группами по 2-4 особи, хрюкают, роются в сугробах в поисках чего-нибудь съедобного, и совершенно не агрессивны. Как и обычные свиньи, они не нападают даже будучи ранеными, предпочитая просто убежать подальше и пожаловаться сородичам на несправедливость мира.

Из-за жизни в холодных регионах снежные свиньи чуть более выносливы к холоду, чем их равнинные родственники - они не получают урон от снега и могут спокойно гулять по ледяным биомам без риска замёрзнуть.

Их можно разводить, сажать на них седло и использовать как источник свинины и кожи.

СОВЕТЫ:

- Используй морковь на палке, чтобы управлять снежной свиньёй на седле - в снегах это удобнее, чем бежать самому.
- Разводи их с помощью моркови, картофеля или свёклы для постоянного источника пищи в холодных биомах.
