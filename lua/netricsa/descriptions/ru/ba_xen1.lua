ПОГРАНИЧНЫЙ МИР ЗЕН, ВОЗВРАЩЕНИЕ


Ну конечно, Сэм. Зен. Снова.

Хаундаи уже здесь. Всегда здесь. Они встречают нас так, словно мы никуда и не уходили. Возможно, так оно и есть.

Чтобы телепорт наконец начал работать как задумано, нам нужно запустить устройство контроля телепортации. Где оно? Хороший вопрос. Ответ, как обычно, разбросан по платформам и остаткам тех, кто уже пытался. Сначала найти. Потом активировать. Потом надеяться, что оно не вспомнит, как всё закончилось в прошлый раз.

Но есть нюанс.

Орда Ментала уже захватывает Зен.

Чужой мир, чужая экосистема, чужие правила. И всё это снова превращается в плацдарм. Менталу, похоже, всё равно, какое измерение ломать, если там есть что подчинить.

Так что план простой.

Найти устройство.

Включить его.

Выжить.