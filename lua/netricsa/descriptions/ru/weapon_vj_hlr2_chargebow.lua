ЗАРЯЖАЕМЫЙ ЛУК


ОРУЖИЕ:      Заряжаемый лук
ПАТРОНЫ:                  Энергетические болты
CC:                ~20 болтов/мин (основной режим), 1 залп/5 сек (альтернативный режим)

ОПИСАНИЕ:

Экспериментальное энергетическое оружие, разработанное инженерами Сопротивления. Выпускает одиночные высокоскоростные энергетические болты с высокой точностью. Альтернативный режим стрельбы заряжает и выпускает залп из трех болтов одновременно, увеличивая урон по групповым целям или бронированным противникам.

ПРИМЕЧАНИЯ:

- Основной режим стрельбы выпускает точные, быстрые энергетические болты.
- Альтернативный режим стрельбы выпускает три заряженных болта одновременно.
- Эффективен на средних и дальних дистанциях.
- Требует тщательного прицеливания из-за низкой скорострельности и времени зарядки.