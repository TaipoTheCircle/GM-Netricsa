ОПИСАНИЕ:

Эти твари напоминают увеличенную копию земного скорпиона, только с более злобным выражением морды и полным отсутствием интереса к чему-либо, кроме убийства.

Обитают Жуки-Солдаты глубоко под поверхностью, где роют туннели и ждут, пока сверху упадёт что-нибудь тёплое и мясистое. Иногда выползают наружу, чтобы поохотиться или просто напомнить местным динозаврам, кто здесь настоящий хозяин подземелий.

В бою Жуки-Солдаты бегут на цель и пытаются зажать её в углу. Ближний бой - их стихия. Клешни хватают, а жало жалит, а если и этого мало - внутри каждого жука есть запас токсина, способного убить человека за несколько секунд.

СОВЕТЫ:

- Дробовик в упор решает проблему быстрее всего.
- Не подходи к упавшему жуку, пока не убедишься, что он действительно мёртв.
