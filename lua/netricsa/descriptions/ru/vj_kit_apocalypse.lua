КИТ БОДЕГА


КЛАСС:        Кит Бодега
МЕТАБОЛИЗМ:   Антропоморфная Кошка, Киборг
ВОСПРИЯТИЕ:   Член Команды "Саппортаж"
РАЗМЕР:       1,78 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Переменная
ОРУЖИЕ:       Когти, Бластер, КПК, Крокетовая Звёздная Пушка
НАГРАДА:      200
УГРОЗА:       Переменная

ОПИСАНИЕ:

Это антропоморфная кошка с бежевой шерстью, коричневыми волосами, розовыми глазами и характерными очками в жёлтой оправе с красными линзами.

Она носит жёлтый комбинезон-юбку с белой зигзагообразной полосой, дополненный чёрным поддоспешником, толстыми наручами и поножами с наголенниками, напоминающими кошачьи лапы. Её полное имя переводится как "Склад снаряжения" (Kit - набор, Bodega - склад), что отражает её способность материализовать вещи.

Кит родом из игрового мира, населённого антропоморфными котами, где она вместе со своим напарником Всячиной была главным героем. Они поклялись защищать родную деревню от злодеев Злобы и Погрома, но в результате их победы мир был уничтожен катаклизмом, убившим всех, кроме Кит и Всячины. В последний момент их спас таинственный Сумрак, завербовав в команду "Саппортаж".

Теперь задача Кит и Всячины - путешествовать по разным мирам и не давать местным героям победить злодеев. Ирония судьбы: они вынуждены играть роль злодеев, чтобы спасти миры от уничтожения, которое неизбежно наступает после победы героя. Вместе с ними путешествуют спасённые герои других миров: Обжора (динозавр) и Гребун (дельфин).

Кит отличается смелостью, оптимизмом и изобретательностью. Уничтожение её родного мира оставило глубокую психологическую травму: любой похожий на родной мир вызывает у неё воспоминания о гибели деревни, что иногда толкает на необдуманные поступки. Она корит себя за то, что не может взять эмоции под контроль, но именно это даёт ей энергию спасать тех, кого ещё можно спасти.

В бою Кит использует бластер, КПК для изменения внешности, а в критической ситуации объединяется с Всячиной в доспех "Крокету", становясь молниеносной и получая доступ к крокетовой звёздной пушке - лучу разрушительной мощности.

СОВЕТЫ:

- Мир разрушится после победы героя, а не после поражения. Помни об этом, когда будешь выбирать сторону.
- Защищай её, за ней судьба миров.
