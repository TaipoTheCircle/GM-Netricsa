НАГРАДА:      800
УГРОЗА:       Очень высокая


ОПИСАНИЕ:

Бронированный Солдат Overwatch с огнемётом Альянса - Усиленная бронёй, ходячая ирония инженерной мысли Альянса. Он облачён в яркий оранжевый защитный костюм с массивным респиратором, из-за чего выглядит скорее как аварийный рабочий, чем как боевой юнит. Но именно этот цвет выбран намеренно - его должны видеть. И бояться.

На спине солдата закреплён крупный топливный баллон, питающий огнемёт. Баллон нестабилен и крайне взрывоопасен: несколько точных попаданий приводят к детонации, которая с одинаковым равнодушием убивает и самого солдата, и всех, кто оказался рядом. Альянс считает это допустимой ценой за эффективность и устрашение.

В бою этот солдат не заботится о собственной выживаемости. Он медленно продвигается вперёд, заливая пространство огнём, выкуривая противника из укрытий и превращая коридоры в печи. Его задача - сломать оборону любой ценой, даже ценой собственной жизни.

СОВЕТЫ:

- Стреляй по баллону на спине - взрыв гарантирован.
- Не подходи близко, даже если он уже ранен.
- Взрыв убивает всех в радиусе - используй укрытия.
- Яркий костюм хорошо заметен, но времени на реакцию мало.