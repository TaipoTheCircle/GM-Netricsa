ОПИСАНИЕ:

Уильям Афтон - серийный убийца солдат Альянса, существо, которое давно перестало быть просто человеком. Когда-то он носил элитный костюм Overwatch, но фиолетового цвета, выделяющий его даже среди элиты. Вместе с Уоллесом Брином Афтон стоял у истоков бренда столовой в Цитадели, работая в заведениях Альянса на самых разных должностях. За фасадом "службы" скрывался хищник, методично изучающий слабости окружающих.

Со временем разгневанные духи его жертв загнали Афтона в собственный костюм, превратив броню в тюрьму и одновременно в саркофаг. С этого момента он стал ходячей резнёй. Его кровь фиолетового цвета, а тело демонстрирует аномальную живучесть, словно он уже частично перешёл грань между жизнью и смертью.

Афтон - выраженный социопат. Его нарциссизм пронизывает каждое действие, подпитывая манипулятивную натуру. Он не просто убивает - он подчиняет. Умело находя больные точки, Афтон заманивает солдат Альянса в изолированные помещения, где расправа происходит без свидетелей. Он наслаждается болью окружающих, превращая страх в инструмент.

В глубине души Афтон осознаёт собственную никчёмность, но компенсирует это искажённым величием, преувеличивая заслуги других и присваивая их себе. Он одержим наукой и технологическими достижениями, видя в них путь к заветной цели - бессмертию. Религиозный страх перед адом делает его ещё более жестоким: Афтон хочет жить вечно, любой ценой.

В бою он использует всё доступное оружие, меняя стиль от методичного подавления до кровавого ближнего контакта. Он не торопится. Он играет.

