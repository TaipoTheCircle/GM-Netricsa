ЧЁРНАЯ МЕЗА, КАПСУЛА ВОИНА ЗЕНА


Фух... вылезти из этой дробильной фабрики было всё равно что вылезти из мясорубки. И как раз когда я думала, что день больше не сможет нас удивить, угадай что? Он удивил.

Анализируя сектор, в который мы только что вошли, я заметила нечто необычное - нечто гораздо большее, чем хедкрабы или даже вортигонты. Оказалось, мы только что наткнулись на первое подтверждённое появление Воина Зена. Не обычный инопланетный пехотинец, нет. Этот парень - настоящий герой, награжденный командир фронта со стороны Зена (и дезертир). Он заперт в камере содержания, под охраной, его изучают... но он жив и здоров.

Перепроверка исторических данных: Инопланетные пехотинцы считаются основой межпространственных ударных войск. Они сильнее, умнее и обладают достаточной огневой мощью, чтобы противостоять целым отрядам. Они не должны были появиться на такой ранней стадии конфликта. Что делает его присутствие здесь... ну, скажем так, "вызывающим беспокойство".

А теперь самое интересное: телеметрия показывает, что он просто стоит. Наблюдает. Словно чего-то ждёт - или кого-то. И, между нами говоря, мне этот взгляд не нравится.

Итак, подведем итоги:

Мы избежали смерти от конвейера.

Нас встречают Хоундаи.

И, да, мы только что встретили первого инопланетного пехотинца этой войны.

Поздравляю! Ты официально перешёл с борьбы с вредителями на межпространственную политику. Постарайся не слишком резко реагировать, потому что если этот парень когда-нибудь выберется, слово "веселье" приобретёт совершенно новый смысл.