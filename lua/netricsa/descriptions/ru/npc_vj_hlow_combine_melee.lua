НАГРАДА:      400
УГРОЗА:       Средняя


ОПИСАНИЕ:

Рукопашный солдат Overwatch - редкая, но крайне неприятная единица Альянса, созданная для ситуаций, где стрельба неуместна или невозможна. Он сражается исключительно в ближнем бою, полагаясь на скорость, агрессию и модифицированную электродубинку, способную оглушать, ломать кости и вызывать судороги у цели.

Броня у этого солдата намеренно упрощена и практически отсутствует. Альянс пожертвовал защитой ради мобильности: боец быстрее стандартных солдат, легко преодолевает короткие дистанции и стремительно врывается в бой. Белый шлем с синим визором скрывает лицо.

Чаще всего такие солдаты используются в тюрьмах, узких коридорах и при подавлении бунтов. Их задача проста - добраться до цели и избить.

Несмотря на слабую защиту, в ближнем бою рукопашный солдат смертельно опасен.

СОВЕТЫ:

- Не подпускай близко - его электродубинка быстро выводит из строя.
- Используй дистанцию и огнестрельное оружие.
- Уязвим из-за отсутствия брони.
- В узких пространствах представляет большую опасность, чем обычные солдаты.