ДИКАРЬ ДУБИНОВ С М'ДИГБО


КЛАСС:        Дикарь Дубинов
МЕТАБОЛИЗМ:   Млекопитающее
ВОСПРИЯТИЕ:   Дружелюбный Дикарь, Отступник
РАЗМЕР:       2 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Шипованная Дубина, Самодельный бинокль из костей, Охотничьи навыки
НАГРАДА:      400
УГРОЗА:       Низкая

ОПИСАНИЕ:

Уникальный представитель Троглодитов с планеты М'Дигбо. В то время как его соплеменники под предводительством таинственной "Мамочки" заключили пакт с Менталом, согласившись стать его солдатами в обмен на бесконечный запас леопардовых стрингов, Дубинов выбрал иной путь. Он добровольно отрёкся от агрессивных общественных устоев своего народа, что сделало его на удивление дружелюбным дикарем.

Дубинов значительно превосходит среднестатистического Троглодита в интеллектуальном и социальном развитии. Он владеет уникальным артефактом - самодельным биноклем, искусно выточенным из костей павших врагов и животных. В отличие от своих сородичей, он предпочитает охоту на кабанов. Проживает в тропиках планеты вместе со своей супругой, ведя мирный, по меркам дикарей, образ жизни.

Утро Дубинова начинается в его пещере, расположенной в самом сердце тропиков. Первым делом он покидает жилище и направляется к ближайшему горному ручью. Этот ритуал неизменен: пьёт чистую воду и тщательно умывается, готовясь к новому дню охоты и наблюдения за горизонтом через свой костяной бинокль.

СОВЕТЫ:

- Дубинов не враг. Не нападай на него.
- Если встретишь его в тропиках, можешь поговорить. Он расскажет много интересного о планете М'Дигбо и её обитателях.
- Его бинокль - не просто игрушка. Дубинов может заметить опасность задолго до того, как она приблизится.
- Если поможешь ему на охоте, он поделится мясом и, возможно, научит паре примитивных трюков.
- Не пытайся украсть его бинокль. Это невежливо, а Дубинов очень привязан к своей вещи.
- Супруга Дубинова - отдельная история. О ней ходят слухи, но сам он её никому не показывает.
