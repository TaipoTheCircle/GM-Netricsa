ТРАВМАТИЧЕСКИЙ ПИСТОЛЕТ МР-80-13Т


ОРУЖИЕ:     МР-80-13Т
ПАТРОНЫ:    .45 Rubber
CC:         ~20 выстрелов/мин

ОПИСАНИЕ:

Травматический пистолет самообороны, модификация МР-79-9ТМ, сконструированный на базе пистолета Макарова. Производится Ижевским механическим заводом.

ПРИМЕЧАНИЯ:

- Подходит для самообороны, но не для боевых действий.
- Рекомендуется стрелять в голову, если хочется убить кого то с этого пистолета.

