МОРОЗНОЕ ЛАЗЕРНОЕ РУЖЬЁ


ОРУЖИЕ:  Морозное лазерное ружьё
ПАТРОНЫ: Ледяные энергетические лучи
CC:      Двойные выстрелы

ОПИСАНИЕ:

Морозное лазерное ружьё - модифицированная версия стандартной лазерной винтовки, адаптированная для эксплуатации в условиях экстремально низких температур. Конструкция оружия усилена термостойкими материалами, а корпус выполнен в обновлённом дизайне с жёлтыми и синими элементами, служащими визуальными маркерами холодной специализации.

Оружие сохраняет принцип работы базовой модели, выпуская два энергетических импульса подряд при каждом нажатии на курок. Однако вместо стандартных лучей, ружьё стреляет синими ледяными лазерами, насыщенными криоэнергией. При попадании они не только наносят урон, но и замедляют противника за счёт резкого охлаждения тканей, брони и приводных механизмов.

Морозное лазерное ружьё питается от модифицированного плазменного ядра с криостабилизацией, предотвращающей перегрев и деградацию компонентов на морозе. Встроенный реактор непрерывно перезаряжает оружие через магазин, обеспечивая стабильную работу в затяжных боях и арктических условиях.

ПРИМЕЧАНИЯ:

- Полностью приспособлено к холоду и низким температурам.
- Каждый выстрел выпускает двойной ледяной импульс, замедляющий противника.
- Криоэффект снижает подвижность живых целей и реакцию механических противников.
- Особенно эффективно на средних дистанциях и при групповом огне.
- Уступает в пробивной мощности против тяжёлой брони, компенсируя это эффектом замедления.
- Предназначено для лёгкой пехоты и подразделений, действующих в холодных зонах.