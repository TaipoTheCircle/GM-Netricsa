ОХРАННЫЙ КОМПЛЕКС ЧЁРНОЙ МЕЗЫ, КОМНАТА УПРАВЛЕНИЯ


Сэм, мы дошли. Компьютерный центр позади, буллсквиды остались в своей луже, а перед нами - комната управления.

Маленькое помещение, заставленное консолями, мигающими экранами и одним-единственным рычагом, который, судя по всему, и есть наше спасение.

Теперь осталось активировать вход в бункер и вернуться назад.
