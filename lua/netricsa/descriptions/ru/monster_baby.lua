МЛАДЕНЕЦ С ФЭВЕРСХОЛЬМА


КЛАСС:        Младенец
МЕТАБОЛИЗМ:   Порождение Кошмара
ВОСПРИЯТИЕ:   Враждебный Психологический Ужас
РАЗМЕР:       0,8 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Шип
НАГРАДА:      50
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это отражение самых тёмных уголков психики. Этот мелкий, быстрый и крайне агрессивный противник символизирует раздражение от плача новорождённых. Он невинен по форме, но смертелен по сути.

Внешне Младенец выглядит как маленькая, бледная фигура без одежды.

Встречается он в квартирах. У него нет сложной тактики, нет арсенала, нет даже инстинкта самосохранения. У него есть только одна цель: добежать до тебя и проткнуть себя шипом, вылетающим из головы.

Да, ты не ослышался. Младенец убивает себя, чтобы попытаться убить тебя.

Скорость у него приличная - он быстрее большинства врагов. Если он достигает цели, из его головы выстреливает длинный острый шип, который наносит урон. Сам Ребёнок при этом погибает. Его не нужно добивать - он делает это сам.

На урон от одной такой атаки невелик. Но проблема в том, что эти Ребята обычно появляются не по одному, а группой. Если в тебя врежутся двое или трое подряд, здоровье улетит ощутимо.

СОВЕТЫ:

- Младенец умирает от любого оружия с одного попадания. Даже из пистолета.
- Не давай ему подбежать. Держи дистанцию и стреляй на опережение.
- В узких коридорах они особенно опасны, так как негде увернуться. Используй дробовик в упор.
- Если ты слышишь детский плач и топот маленьких ног - готовь ствол. Они уже бегут.
- Не жди, пока они подойдут. Беги навстречу сам и расстреливай их с безопасного расстояния.

