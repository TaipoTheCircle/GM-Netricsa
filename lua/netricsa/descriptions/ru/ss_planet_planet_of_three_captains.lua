ПЛАНЕТА ТРЁХ КАПИТАНОВ


ПЛАНЕТА:        Планета Трёх Капитанов
ТИП:            Планета земного типа, Мемориальный мир
КЛИМАТ:         Искусственно поддерживаемый, пригодный для дыхания
СПУТНИКИ:       Неизвестно
ЛАНДШАФТ:       Первоначально безжизненный скалистый; оборудована генераторами атмосферы
РАСПОЛОЖЕНИЕ:   Неизвестная звёздная система Галактики Млечный Путь
МЕСТНЫЕ РАСЫ:   Отсутствуют (туристическая планета)
ПРИНАДЛЕЖНОСТЬ: Межгалактический мемориальный комплекс
МЕСТА:          Шестидесятиметровая статуя Трёх Капитанов, Музей космонавтики

ОПИСАНИЕ:

Уникальный мемориальный мир, названный в честь трёх легендарных героев космоса, совершивших множество выдающихся подвигов и открытий во время своих путешествий по Галактике. Изначально это была безымянная необитаемая планета, полностью непригодная для жизни из-за отсутствия атмосферы.

Для увековечивания памяти знаменитых капитанов планета была специально оборудована мощными генераторами, вырабатывающими кислород и создающими пригодные для дыхания условия, чтобы сделать её доступной для туристов со всех уголков Галактики.

Главной достопримечательностью и символом планеты является грандиозная шестидесятиметровая каменная статуя, изображающая Трёх Капитанов. Этот монумент виден издалека и служит ориентиром для всех прибывающих на планету.

Рядом со статуей расположен Музей космонавтики - уникальное собрание артефактов, документов и свидетельств эпохи Великих Космических Открытий. Музей был построен на добровольные пожертвования, собранные на восьмидесяти планетах звёздной системы, что делает его поистине всенародным галактическим памятником.

КЛЮЧЕВЫЕ МЕСТА:

- Шестидесятиметровая статуя Трёх Капитанов: Главный монумент планеты, изображающий трёх легендарных героев космоса. Служит ориентиром и символом планеты.
- Музей космонавтики: Уникальное собрание артефактов и документов, посвящённое эпохе Великих Космических Открытий. Построен на пожертвования жителей восьмидесяти планет.
