ПИСТОЛЕТ-ПУЛЕМЕТ COLT 9MM SMG


ОРУЖИЕ:     Colt 9mm SMG
ПАТРОНЫ:    Патроны 9x19
CC:         ~20 выстрелов/мин

ОПИСАНИЕ:

Американский пистолет-пулемёт калибра 9×19 мм "Парабеллум". Создан на основе автоматической винтовки M16, но использует принцип свободного затвора, в отличие от прототипа M16, использовавшего прямой газоотвод. 

ПРИМЕЧАНИЯ:
- Данная модификация имеет только одиночный режим огня.






