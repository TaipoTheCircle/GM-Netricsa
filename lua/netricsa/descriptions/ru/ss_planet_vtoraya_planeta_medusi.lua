ВТОРАЯ ПЛАНЕТА СИСТЕМЫ МЕДУЗЫ


ПЛАНЕТА:        Вторая планета системы Медузы
ТИП:            Планета земного типа
КЛИМАТ:         Умеренный, атмосфера для дыхания непригодна, наблюдаются водяные дожди
СПУТНИКИ:       Неизвестно
ЛАНДШАФТ:       Пустынная равнина, блестящие скалы, россыпи круглых камней
РАСПОЛОЖЕНИЕ:   Система звезды Медуза, периферия Галактики Млечный Путь
МЕСТНЫЕ РАСЫ:   Камни
ПРИНАДЛЕЖНОСТЬ: Неизвестна
МЕСТА:          Равнина приземления "Пегаса", Места появления иллюзий

ОПИСАНИЕ:

Это Безмолвный и пустынный мир, скалистая равнина которого усеяна круглыми, похожими на картофелины камнями. При взгляде с орбиты она кажется мрачной и негостеприимной. Атмосфера планеты абсолютно непригодна для дыхания людей, климат умеренный, и здесь идут водяные дожди. В недрах обнаружены источники тяжёлых углеводородов.

Главная и уникальная особенность планеты - её коренные обитатели. Местная флора представлена скудной растительностью, но настоящими хозяевами мира являются разумные существа неорганического происхождения, внешне неотличимые от обычных камней.

Эти существа обладают удивительными способностями: Они способны запоминать любые образы, которые когда-либо видели. Камни могут воспроизводить увиденное или даже прочитанное в мыслях посетителей в виде объёмных, реалистичных миражей. Считается, что делают они это от скуки, чтобы хоть как-то разнообразить безрадостные будни на пустынной планете.

Когда корабль "Пегас" совершил посадку на этой планете, пассажиры стали свидетелями череды призрачных видений: процессии мушкетёров из "Трёх мушкетёров", затем самих себя, а позже - космических пиратов, за которыми они гнались . Все эти образы были иллюзиями, созданными местными жителями.

Разгадка тайны произошла, когда дочь профессора Селезнёва подхватила один из камней, вокруг которого медленно гасло сияние, и камень превратился в космического капитана - одного из тех, кто ранее потерпел бедствие на этой планете и был "записан" в память камня.

КЛЮЧЕВЫЕ МЕСТА:

- Равнина приземления "Пегаса": Участок пустынной каменистой равнины, где совершил посадку исследовательский корабль. Именно здесь наблюдались массовые проявления иллюзий.
- Зоны повышенной иллюзорной активности: Районы скопления разумных камней, где вероятность появления миражей наиболее высока. Координаты этих зон нестабильны, так как камни могут медленно перемещаться.
- Место находки капитана: Точка на равнине, где был обнаружен камень, скрывавший в себе образ одного из пропавших капитанов.
