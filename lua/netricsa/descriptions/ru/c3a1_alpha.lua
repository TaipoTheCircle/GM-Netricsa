X-ЛАБОРАТОРИЯ РЕАКТОРА ЧЁРНОЙ МЕЗЫ, ПОГРУЗОЧНЫЙ ОТСЕК


Сэм, учёные привели нас. Не к выходу, как можно было подумать, а к новой порции проблем. Мы в X-лаборатории реактора. Место, где Чёрная Меза проверяла свои самые страшные теории на прочность. Судя по обстановке - теория провалилась, прочность выдержала, а мы остались разбираться с последствиями.

Центр находится в состоянии повышенной безопасности. Это значит, что тот, кто здесь был до нас, очень не хотел, чтобы кто-то зашёл или вышел. Единственный вход в лабораторию реактора закрыт. Рядом - лифт, ведущий к центру управления. Тоже закрыт. Всё закрыто. Кроме одного пути.

Канализация. Под шахтой лифта. Там, куда никто не хочет лезть, но где всегда есть проход. Потому что в Чёрной Мезе, если дверь закрыта, значит, нужно идти через трубы. Правило номер один.

Если мы проберёмся через канализацию и вылезем с другой стороны, то попадём в центр. Там - большое число пришельцев. Не патруль, а целая группа. И небольшой отряд H.E.C.U., которые, видимо, забрели сюда в поисках выхода и застряли.

Главный учёный, как выясняется, заперт в комнате управления. Он наверняка просит о том, чтобы его нашли, потому что без него мы, возможно, не разблокируем выход. Или не узнаем, как закрыть этот цирк.

Нам требуется нейтрализовать охранника в приёмной. У него есть карта доступа. Без неё мы никуда не попадём. 

В центре коммуникации нам нужно будет отыскать консоль. С её помощью необходимо снизить уровень безопасности и разблокировать оставшиеся двери. Те самые, что ведут к реактору. И, возможно, к выходу.

Если мы всё сделаем правильно, двери откроются, учёный выйдет, и мы узнаем, как выбраться из этого кошмара. Если ошибёмся - останемся здесь "Навсегда", как любит это делать Tazik29, среди пехотинцев, морпехов и собственных надежд.
