ПОГОН


ПЛАНЕТА:        Погон
ТИП:            Планета земного типа, Крио-мир, Военная база
КЛИМАТ:         Глубокая заморозка, сильные морозы, постоянный холодный ветер
СПУТНИКИ:       Неизвестно
ЛАНДШАФТ:       Высокие горные хребты, заснеженные равнины, ледяные плато
РАСПОЛОЖЕНИЕ:   Где-то между планетами Строггос и Земля
МЕСТНЫЕ РАСЫ:   Строгги (гарнизон)
ПРИНАДЛЕЖНОСТЬ: Империя Строггов
МЕСТА:          База Генерала Фарона, Горные перевалы, Заснеженные равнины

ОПИСАНИЕ:

Суровый заснеженный мир, расположенный в стратегически важной точке между Строггосом и Землёй. Экстремальные климатические условия делают эту планету естественной крепостью: сильные морозы, пронизывающий холодный ветер и отвесные горные хребты практически полностью исключают возможность успешных воздушных налётов или артиллерийских атак из космоса.

Ландшафт планеты сформирован высокими, неприступными горами, между которыми простираются бескрайние заснеженные равнины и ледяные плато. Постоянные метели и низкая видимость делают навигацию чрезвычайно сложной, а любые следы присутствия быстро заметаются снегом.

Именно здесь, в глубине горного массива, Строгги развернули одну из своих ключевых военных баз. Командование объектом осуществляет Генерал Фарон - элитный Танк Строггов на антигравитационной платформе, представляющий собой грозную боевую единицу, идеально приспособленную для ведения войны в условиях суровой зимы.

Выбор Погона для размещения базы продиктован не только её стратегическим положением на пути к Земле, но и естественной защитой, которую обеспечивает климат. Пока бураны и морозы сковывают любые попытки вторжения, Строгги беспрепятственно ведут свои работы в хорошо укрытых и отапливаемых комплексах, вырубленных в толще скал.

КЛЮЧЕВЫЕ МЕСТА:

- База Генерала Фарона: Основное укрепление Строггов на планете, комплекс туннелей и ангаров, скрытых глубоко внутри горного массива. Отсюда осуществляется координация действий и контроль над сектором.
- Горные перевалы: Единственные потенциальные пути наземного проникновения к базе, постоянно патрулируемые и заминированные. Большую часть года непроходимы из-за снежных заносов.
- Заснеженные равнины: Открытые пространства у подножия гор, используемые для испытаний техники в экстремальных погодных условиях и запуска дронов-разведчиков.
