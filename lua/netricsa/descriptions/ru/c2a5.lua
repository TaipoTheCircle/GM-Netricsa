ЧЁРНАЯ МЕЗА, ШТУРМ МОСТА


Телеметрия подтверждает наличие мощных сигнатур несущихся винтов - самолёт огневой поддержки "AH-64 Апач" захватил твою позицию. Какое место засады? Подвесной мост, перекинутый через глубокий промышленный овраг. Отличное место для осмотра достопримечательностей... если не обращать внимания на ударный вертолёт.

Анализ ситуации:

Угроза: "AH-64 Апач" вооружён пулемётами, ракетами и терпением очень злобной осы. Оставаться на открытом пространстве - смертный приговор.

Окружающая среда: мост узкий, частично повреждён и открыт со всех сторон. Ограниченное укрытие означает, что тебе придётся постоянно двигаться - уклоняться, маневрировать и не останавливаться, пока не найдёшь устойчивую огневую позицию.

Вторичные опасности: бойцы H.E.C.U. окопались по обеим сторонам оврага, обеспечивая огневую поддержку вертолёту на земле. Их координация действий позволяет предположить, что эта засада была спланирована.

Тактические советы:

Используйте несущие балки и обломки мостика в качестве частичного укрытия, но не задерживайся.

Старайся сохранить мобильность - пусть он преследует тебя, а не кружит вокруг в поисках лёгких целей.

Если ты захватил Тау-Пушку, это одно из немногих орудий, способных оказать давление на самолёт. В противном случае, лучшим вариантом будет поиск взрывоопасных предметов.

Вывод: пересечь этот мост - это не просто добраться до другой стороны. Это доказать, что тебя сложнее убить, чем летающий танк за миллиард долларов.