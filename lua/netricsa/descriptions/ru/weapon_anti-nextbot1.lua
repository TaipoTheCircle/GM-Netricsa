АНТИ-НЕКСТБОТ ЛАЗЕР V1


ОРУЖИЕ:  Анти-Некстбот Лазер V1
ПАТРОНЫ: Импульсная энергия AR2 / лазерные разряды
СС:      Непрерывный луч


ОПИСАНИЕ:

Модернизированная версия импульсной винтовки AR2, специально адаптированная для борьбы с Некстботами. Оснащён системой приоритизации целей, позволяющей эффективно уничтожать аномальные сущности без лишнего разрушения окружения.

Первый режим активирует направленный лазер, который мгновенно поражает любые живые и враждебные цели, полностью игнорируя пропы и объекты среды. Это делает его идеальным для точечной зачистки в ограниченных пространствах.

Второй режим отключает фильтрацию и позволяет наносить урон как врагам, так и пропам. Используется против устойчивых или нестандартных Некстботов, когда требуется грубая сила и полное уничтожение области.

ПРИМЕЧАНИЯ:

- Режим 1: уничтожает цели, игнорируя пропы и окружение.
- Режим 2: наносит урон всему, включая объекты и укрытия.
- Высокая эффективность против стандартных Нексботов.
- Второй режим опасен для окружающей среды.
- Эффективно на средней и дальней дистанции.
