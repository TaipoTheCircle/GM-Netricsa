ЧЁРНАЯ МЕЗА, ПРОБЛЕМА УЧЁНОГО


Сэм, визуально осмотрись впереди... о, выглядит не очень.

На том, что осталось от мостика, стоит учёный. И под "тем, что осталось" я имею в виду три ржавых болта, молитву и сильную гравитацию.

Я рассчитала вероятность выживания по всем своим моделям.

Результат: 1% вероятности, что он выживет. И это только если случится чудо - например, хедкраб внезапно превратится в батут. Спойлер: маловероятно.

Будь осторожен, потому что если ты подойдешь слишком близко, вибрации могут быть достаточно сильными, чтобы сбросить его в бездну. С другой стороны... если ты не подойдешь близко, он, вероятно, всё равно упадёт. Это один из тех классических проигрышных сценариев, только проигрываешь не ты.

Стратегический совет: не трать слишком много боеприпасов и сил. Если попытаешься его спасти и каким-то образом преуспеешь, поздравляю - вы только что превзошли математику. Но я бы не стала ставить на это свои очаровательные красные кеды.