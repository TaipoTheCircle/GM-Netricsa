ОПИСАНИЕ:

Тактический тюремный солдат Overwatch Альянса - это специализированная версия тактического бойца, подготовленная и экипированная для службы в Нова Проспект и аналогичных исправительно-карательных комплексах. В отличие от полевых подразделений, эти солдаты обучены действовать в замкнутых пространствах, среди камер, коридоров и зон массового подавления заключённых.

Его ключевая особенность - жёлтый визор и усиленный тактический шлем с защитным забралом. Конструкция шлема полностью нейтрализует уязвимость к выстрелам в голову: хедшоты не дают никакого критического эффекта, превращая попытки точного огня в пустую трату боеприпасов. Альянс намеренно сделал этих бойцов максимально устойчивыми к засадам и внезапным атакам внутри тюремных блоков.

Броня ещё плотнее, чем у обычных тактических солдат. Внутренние аугментации подавляют болевые реакции и поддерживают боеспособность даже при серьёзных повреждениях.

СОВЕТЫ:

- Хедшоты полностью бесполезны - даже не трать на них время.
- Взрывчатка и тяжёлое вооружение остаются единственным надёжным решением.
- В узких коридорах Нова Проспекта он особенно опасен.
- Жёлтый визор хорошо заметен - но это не слабость, а предупреждение.