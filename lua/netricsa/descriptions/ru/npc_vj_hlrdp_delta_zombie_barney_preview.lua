ЗОМБИ-ХЕДКРАБ ОХРАННИК БАЗЫ ДЕЛЬТА, ОБЫЧНЫЙ (АНОНС)


КЛАСС:        Зомби-хедкраб Охранник Базы Дельта, Обычный (Анонс)
МЕТАБОЛИЗМ:   Человек-Носитель + Паразитический Хедкраб Зена
ВОСПРИЯТИЕ:   Зомбированная сущность, бывший сотрудник службы безопасности
РАЗМЕР:       ~1,9 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Экстремальная
ОРУЖИЕ:       Когти, укус, захват
НАГРАДА:      350
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это то, во что превращаются сотрудники службы безопасности базы Дельта после того, как хедкраб берёт верх. В отличие от своих коллег-учёных в серых халатах или техников в промасленных комбинезонах, охранники изначально были крепче, массивнее и лучше подготовлены к физическим нагрузкам. К сожалению, это не спасло их от заражения - но сделало их гораздо более опасными противниками в зомбированном виде.

Охранники носят бронежилеты службы безопасности базы Дельта c кобурами, из которых давно выпали табельные пистолеты. Их тела менее деформированы, чем у учёных, - видимо, крепкая мускулатура сопротивляется паразитическому изменению дольше. Но это не облегчает встречу с ними. Они сильнее, выносливее и агрессивнее обычных зомби-хедкрабов.

СОВЕТЫ:

- Зомби-Охранник выносливее учёного. Не жалей патронов.
- В голову он умирает так же, как и остальные: голова отрывается.
- Не думай, что бронежилет можно пробить с одного выстрела. Целься в голову или в ноги.
- В группе охранник - приоритетная цель, из-за своей живучести.
