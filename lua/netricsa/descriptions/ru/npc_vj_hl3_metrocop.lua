ПЕРЕХВАТЧИК ГРАЖДАНСКОЙ ОБОРОНЫ АЛЬЯНСА


КЛАСС:        Подразделение гражданской Обороны Альянса, перехватчик
МЕТАБОЛИЗМ:   Человек, Мужской (ограниченные аугментации)
ВОСПРИЯТИЕ:   Улучшенное: оптические сенсоры в маске + радиоканал
РАЗМЕР:       ~1,85 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       SMG3 (HK MP7), Манхэк
НАГРАДА:      250
УГРОЗА:       Средне-высокая

ОПИСАНИЕ:

Перехватчик Гражданской Обороны Альянса - специализированный боец гражданской Обороны, адаптированный для действий в заснеженных и арктических регионах. Эти подразделения используются для патрулирования ледяных городов, полярных станций и удалённых территорий, где стандартные силы CP теряют мобильность и ориентацию.

Он облачён в защитный костюм серо-белого камуфляжного цвета, сливающийся со снежным ландшафтом. Униформа усилена ремнями, подсумками и креплениями для оборудования, рассчитанными на длительные автономные операции в экстремальном холоде. Чёрный бронежилет защищает корпус, а массивные чёрные ботинки обеспечивают устойчивость на льду и снегу.

Главная отличительная черта - маска с двумя крупными светящимися голубыми линзами, выполняющими роль тепловизионных и ночных сенсоров. Эти линзы позволяют Перехватчику обнаруживать цели сквозь метель, туман и снежные завесы, превращая плохую видимость в его преимущество.

Вооружён SMG3 (HK MP7), компактным и скорострельным оружием, идеально подходящим для ближнего и среднего боя в условиях ограниченного пространства. При необходимости Перехватчик может выпустить Манхэка, используя его для разведки, преследования беглецов или давления на противника с воздуха.

Перехватчики действуют быстро, редко вступают в затяжные бои и предпочитают тактику давления, окружения и добивания.

СОВЕТЫ:

- Не полагайся на укрытия в метели - его сенсоры видят лучше, чем кажется.
- Манхэк часто используется для дезориентации перед атакой.
- В ближнем бою MP7 смертельно опасен из-за высокой скорострельности.