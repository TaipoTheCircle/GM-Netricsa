ТОПОР


ОРУЖИЕ:  Топор
ПАТРОНЫ: Нет (ближний бой)
СС:      ~60 ударов/мин

ОПИСАНИЕ:

Топор - грубое, но надёжное ближнее оружие. Острый лезвийный край и массивный обух позволяют наносить сильные рубящие удары, ломая плоть и разрушая деревянные преграды. Простой в использовании и эффективный в паре с быстрым перемещением или засадами: один-два точных удара способны нейтрализовать слабых противников или серьёзно повредить более крупных врагов.

ПРИМЕЧАНИЯ:

- Высокий урон при прямом попадании; хорош для разрыва плотных групп врагов на близкой дистанции.
- Может разрушать деревянные объекты и некоторые укрытия в окружении.
- Простая механика - низкая сложность использования, но требует подхода в ближний бой.