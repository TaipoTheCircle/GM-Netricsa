ЮТАРАПТОР С ТЕРРАФОРМИРОВАННОЙ ПЛАНЕТЫ


КЛАСС:        Ютараптор
МЕТАБОЛИЗМ:   Хищник, Теропод
ВОСПРИЯТИЕ:   Стайный охотник
РАЗМЕР:       ~2,5 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Крайне высокая
ОРУЖИЕ:       Когти, зубы, скорость, стайная тактика
НАГРАДА:      400
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это основная боевая единица динозаврового населения планеты и главная головная боль любого, кто сунулся в джунгли без тяжёлого вооружения. Если ты представляешь рапторов как мелких шустрых ящериц ростом по колено - забудь. Эти твари ростом с человека, весом под полтонны и с когтями, способными вспороть бронежилет как картон.

Внешне ютарапторы напоминают гибрид птицы и ящерицы с явным уклоном в сторону убийцы. Длинные задние лапы позволяют развивать приличную скорость и делать резкие прыжки в стороны.

Главная особенность ютарапторов - они охотятся стаями. В одиночку раптор тоже опасен, но настоящий ад начинается, когда их трое, пятеро или десяток. Они окружают, заходят с флангов, отвлекают внимание и бьют со спины. Если дать им себя окружить - можно даже не пытаться вырваться.

СОВЕТЫ:

- Не стой на месте. Рапторы быстрее тебя, но предсказуемы в траектории атаки.
