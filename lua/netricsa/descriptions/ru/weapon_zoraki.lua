СИГНАЛЬНЫЙ ПИСТОЛЕТ ZORAKI STALKER M906


ОРУЖИЕ:     Zoraki Stalker M906
ПАТРОНЫ:    9MM PAK 
CC:         ~15 выстрелов/мин

ОПИСАНИЕ:

Самозарядный сигнальный пистолет турецкого бренда Zoraki. Подходит для самообороны, отпугивания диких животных, запуска фейерверков (с помощью насадки под ракетницу).

ПРИМЕЧАНИЯ:

- Не является боевым пистолетом и может убить только выстрелом в упор.
- Может ослепить человека если вставить в магазин патроны 9MM PAK Flash Defence.

