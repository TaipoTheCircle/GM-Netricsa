КИРПИЧНЫЙ ДОМ, КИРПИЧНЫЙ СИНДРОМ


Сэм, стройка века подходит к концу. Тот, кто проектировал этот комплекс, явно вдохновлялся архитектурой снов - где всё есть, а дверей нет, а красный кирпич мирно соседствует с белым, как будто так и задумано.

Встречай: финальная версия легендарного Кирпичного дома.

Ландшафт продолжает эволюционировать. Здесь целый посёлок:

Пруд. С правой стороны от дома, прямо на ровной траве, теперь плещется пруд. Не бассейн, не лужа, а полноценный пруд. Вода есть, рыбы нет, но кто знает, что заведётся.

Двухэтажка из белого кирпича. За домом, слева, выросла двухэтажная громадина. Белый кирпич, деревянный пол, и... никакой лестницы на второй этаж. Просто стены, пол и потолок, а между этажами - пустота и мечты.

Первый этаж: 8 окон. Много света, много стекла, много потенциальных точек входа для снайперов. Одна стена, зачем-то, красная. Видимо, старые запасы кирпича решили не выкидывать.

Второй этаж: 6 окон. Лестницы нет.

Внутри: пустота. Абсолютная. Ни стола, ни стула, ни даже консервной банки. Только стены, пол и окна. И красная стена на первом этаже, которая смотрит на тебя с немым вопросом: "зачем я тут?".

Первый кирпичный дом: всё те же прибитые ружья, закрытые ящики, запасы Софии и часы на стене. Двери нет, не ждали.

Гараж: двигатель и гаечный ключ всё так же ждут своего часа на деревянном столике.

Бассейн: полон воды, готов к приёму уставших сталкеров.

Четыре сломанных автомобиля: стоят, ржавеют.

Одиннадцать деревьев: стоят по периметру, одно по-прежнему на новом месте.

Теперь ты готов.
