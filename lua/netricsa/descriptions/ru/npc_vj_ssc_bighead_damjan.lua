БОЛЬШЕГОЛОВЫЙ КРОТИМОВЕЦ, ДАМЬЯН МРАВУНАЦ


КЛАСС:        Большеголовый Кротимовец, Дамьян Мравунац
МЕТАБОЛИЗМ:   Гуманоидный (на 99% состоящий из чистой музыки)
ВОСПРИЯТИЕ:   Фанат-энтузиаст / бог Музык
РАЗМЕР:       5,9 футов
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Нейтральная
ОРУЖИЕ:       Отсутствует (кроме сложности I Am Ugh-Zan)
НАГРАДА:      100
УГРОЗА:       Раздражающий фактор (с абсолютным музыкальным слухом)

ОПИСАНИЕ:

Кротимовцы - худощавые гуманоиды с гипертрофированными черепами, содержащими мозг разработчиков Croteam. Носят униформу Крутого Сэма, но с уникальными чертами лиц команды разработчиков. Проявляют навязчивый энтузиазм, преследуя тебя с возгласами "Привет!" и "Эй, вы куда!?". Абсолютно безвредны, но в сложности "Я - Угх-Зан" получают огненные и ледяные модификаторы. Спонтанно появляются в секретных зонах, блокируя проходы своим числом.

Дамьян Мравунац - тот самый человек, чья музыка является саундтреком к хаосу во вселенной Serious Sam. Его гипертрофированный череп - прямое следствие невероятной музыкальной одаренности, вмещающей в себя всё: от эпических оркестровых аранжировок до оглушительных хеви-метал рифов. Будучи штатным композитором Croteam с 1999 года, Дамьян - архитектор того самого "серьёзного" звучания, которое с первых нот погружает всех в атмосферу безумного экшена.

Его стиль - это уникальный коктейль из мощных перкуссий, запоминающихся мелодий, этнических мотивов и современной электроники. Он не просто пишет фоновую музыку; он создает звуковые ландшафты, которые стали неотъемлемой частью геймплея. От культовых тем The First Encounter до оркестрового размаха The Talos Principle 2 - его работы являются такой же визитной карточкой студии, как и сам Сэм Стоун.

СОВЕТЫ:

- Для очистки пути используй армейский нож или пистолет.
- На сложности "I Am Ugh-Zan" уничтожай дистанционно из-за их модификаторов.
- В групповом скоплении эффективна кувалда с горизонтальным ударом.
