ПЕЧЕНЬКОВЫЙ КРИПЕР КРИПЕР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Печеньковый крипер
МЕТАБОЛИЗМ:   Мучно-Кондитерский
ВОСПРИЯТИЕ:   Уникальный элемент артефакт
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Пассивная
ОРУЖИЕ:       Взрыв
НАГРАДА:      50
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это результат странного генетического эксперимента, где-то на стыке магии, кондитерского искусства и, возможно, неудачной шутки какого-то безумного мододела.

В отличие от своих зелёных, пороховых собратьев, этот экземпляр абсолютно безопасен с точки зрения урона. Он напоминает песочное печенье.

Главная и единственная особенность этого моба - его взрыв. При контакте со своим противником он не уничтожает постройки и не наносит урона здоровью. Вместо этого он с тихим хрустом распадается на множество осколков-печенек, которые разлетаются во все стороны. Эти печенья можно подобрать, и каждое из них мгновенно восстанавливает небольшое количество сытости и здоровья.

В отличие от обычной еды, печенье с моба не даёт никаких негативных эффектов, кроме легкого чувства вины за то, что вы только что "убили" оживлённую сладость.

СОВЕТЫ:

- Не бойся. Он не взрывает блоки и не наносит урон. Но если вы терпеть не можете сладкое, можете его игнорировать.
- Лучший друг на голодный желудок. Если у тебя кончилась еда, а до дома далеко, это настоящий подарок судьбы.
- Помни: это печенье имеет срок годности, примерно 3 месяца, потом оно черствеет и превращается в обычного зомби. Шутка.
