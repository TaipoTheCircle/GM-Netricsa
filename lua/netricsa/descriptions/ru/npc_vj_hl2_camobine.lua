МАСКИРОВОЧНЫЙ СОЛДАТ OVERWATCH АЛЬЯНСА


КЛАСС:        Маскировочный солдат Overwatch Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Дальнобойное разведывательно-снайперское подразделение Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Снайперская винтовка Overwatch MK2, импульсная винтовка AR2
НАГРАДА:      1600
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Маскировочный солдат Overwatch - это тень, которую ты замечаешь только в момент собственной ошибки. Альянс использует этих бойцов для охоты на командиров Сопротивления, наблюдения за ключевыми маршрутами и тихого подавления активности в зонах, где открытое присутствие Overwatch нежелательно.

В отличие от большинства солдат Альянса, у него отсутствует светящийся визор. Вместо него оптические линзы, скрывающие направление взгляда и исключающие демаскировку. Его броня полностью покрыта искусственными кустами, травой и волокнами маскировочного костюма, адаптированного под окружающую среду. На расстоянии он выглядит как кусок ландшафта, который вдруг решил стрелять.

Основное оружие - снайперская винтовка Overwatch MK2, способная поражать цель до того, как та осознает угрозу. AR2 используется как вторичное оружие на случай обнаружения или ближнего боя, хотя к этому моменту солдат предпочитает сменить позицию, а не геройствовать.

СОВЕТЫ:

- Никогда не задерживайся на открытой местности - он ждёт именно этого.
- После первого выстрела меняй позицию.
- Взрывчатка и огонь эффективны против маскировки, даже если ты не уверен, что он там есть.