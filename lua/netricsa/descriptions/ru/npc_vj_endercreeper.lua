ЭНДЕР-КРИПЕР С КРАЯ МИРА МАЙНКРАФТА


КЛАСС:        Эндер-крипер
МЕТАБОЛИЗМ:   Гибридно-Пороховой
ВОСПРИЯТИЕ:   Гибрид Существа Края, Элитный охотник
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Телепортация, самонаведение на цель, взрыв, укус
НАГРАДА:      300
УГРОЗА:       Абсолютная

ОПИСАНИЕ:

Это результат мутации обычного крипера, который слишком долго находился под воздействием эндер-энергии в Крае

Внешне Эндер-крипер отличается от обычного не только цветом - он быстрее и явно умнее. При движении за ним тянется шлейф фиолетовых частиц, как у Эндермена при телепортации.

Эндер-крипер унаследовал способности Эндермена: он умеет телепортироваться на короткие расстояния, уворачиваясь от стрел и атак. Когда в него пытаются выстрелить из лука или арбалета, он мгновенно исчезает и появляется за спиной или сбоку, готовый к атаке. Иногда он может телепортироваться прямо перед жертвой, преграждая путь.

Он преследует цель, телепортируется, чтобы зайти с неожиданной стороны, а затем взрывается.

СОВЕТЫ:

- Не пытайся стрелять в Эндер-крипера из лука или арбалета. Он телепортируется, и ты только потратишь стрелы.
- Используй воду, чтобы лишить его способности телепортироваться.
- В ближнем бою наноси быстрые удары мечом, не давая ему времени на реакцию.
- Эндер-крипер не взрывается мгновенно. Услышал шипение - отпрыгивай или используй щит.
- При убийстве Эндер-крипера есть шанс получить жемчуг Края. Это отличный способ фармить жемчуг, если ты не хочешь убивать Эндерменов.
