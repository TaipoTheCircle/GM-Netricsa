ОПИСАНИЕ:

Это бронированная капсула для безопасной доставки наёмников к месту назначения. В отличие от военных модификаций, утыканных пулемётами и ракетницами, эта версия представляет собой обычный армейский внедорожник, усиленный бронированными панелями и пуленепробиваемыми стёклами.

Вооружения на машине нет. Совсем. Ни пулемёта на крыше, ни гранатомёта на турели. Водители Y.S.M. предпочитают полагаться на броню и скорость, а если возникает угроза - просто давят врагов колёсами. Ездит HMMWV медленно - броня весит много, и мотор не резиновый. Но для того, чтобы доехать из точки А в точку Б, этого достаточно.

Внутри машины - два ряда сидений: спереди водитель и пассажир, сзади до четырёх наёмников в полной боевой выкладке. Окна бронированные, но тонированные, изнутри почти ничего не видно.

На базе Дельта Y.S.M. использует HMMWV для патрулирования периметра, доставки припасов и, конечно, для грабежа - на месте не всегда помещается в здание, так что приходится тащить добро к машине вручную, а потом грузить.

СОВЕТЫ:

- Без оружия HMMWV не опасен, если не стоять у него на пути. Не стой перед машиной - задавят.
- Броня крепкая, но днище уязвимо для взрывчатки. Граната под колёса - и машина превращается в груду металла.
- Водитель не вылезает стрелять.
- На тёмной дороге HMMWV легко заметить по свету фар.
