ПРИНАДЛЕЖНОСТЬ: Галактическая Республика (ранее), Галактическая Империя (ранее), Новая Республика (ранее), Галактический Союз (Сейчас)
МЕСТА:          Храм Джедаев, Здание Сената, Императорский дворец, Уровни Нижнего города, Горы Манараи, Западное Море

ОПИСАНИЕ:

Корусант - сверкающая планета-город, расположенная в самом сердце Галактики. Тысячелетиями она служила политическим, культурным и финансовым центром, где принимались решения, влияющие на судьбы триллионов разумных существ. Название планеты происходит от постоянной игры света на её поверхности при взгляде из космоса, напоминающей сияние драгоценных камней коруса.

Поверхность Корусанта полностью скрыта под бескрайним многоуровневым мегаполисом, который рос на протяжении тысяч поколений. Гигантские здания, некоторые из которых достигают 6 километров в высоту, покрывают каждый квадратный метр суши, похоронив под собой все естественные ландшафты - горы, моря и океаны. Вода была запасена в обширных подземных резервуарах, а единственным открытым водоёмом остаётся искусственное Западное Море с плавучими островами для отдыха.

Галактический город разделён на несколько тысяч квадратов и секторов. Верхние уровни занимают правительственные учреждения, финансовые центры и пентхаусы богатейшей элиты. Здесь воздух проходит многоступенчатую очистку, а движение транспорта строго регулируется автоматическими трассами.

Нижние уровни - это мрачный мир, куда веками не проникал солнечный свет. Там, в искусственном освещении, обитают беднейшие слои населения, царит преступность и разруха. Ещё глубже, в основании зданий, находятся забытые уровни с едва пригодной для дыхания атмосферой, где обитают мутанты, мусорщики и опасные существа вроде ктонов.

Климат планеты поддерживается системой орбитальных зеркал, перераспределяющих энергию звезды Корусант-Прайм, так как естественная орбита планеты слишком удалена для комфортного существования людей. Тысячи атмосферных регенераторов очищают воздух от колоссального количества отходов жизнедеятельности триллионного населения.

КЛЮЧЕВЫЕ МЕСТА:

- Храм Джедаев: Монументальное здание, возвышающееся над столицей, служившее штаб-квартирой ордена джедаев на протяжении тысячелетий.
- Здание Сената: Огромный куполообразный комплекс, где заседал Галактический Сенат - главный законодательный орган Республики.
- Императорский дворец: Бывший Храм Джедаев, перестроенный императором Палпатином в свою резиденцию. Расположен у подножия Гор Манараи.
- Горы Манараи: Один из немногих сохранившихся естественных ландшафтов планеты - двойной горный пик, место расположения императорского дворца и площади Монументов.
- Уровни Нижнего города: Многосотэтажный лабиринт туннелей и уровней, куда никогда не проникает солнечный свет. Место обитания бедняков, преступников и изгоев общества.
- Западное Море: Искусственный водоём с плавучими островами, созданный для отдыха и рекреации.
