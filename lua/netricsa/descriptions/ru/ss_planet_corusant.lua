ТИП:            Планета земного типа, Планета-город
КЛИМАТ:         Умеренный (искусственно регулируется орбитальными зеркалами)
СПУТНИКИ:       4 луны (Центракс, Хвивия, Висцунт, Куан)
ЛАНДШАФТ:       Полностью урбанизированная поверхность, многоуровневая городская застройка
РАСПОЛОЖЕНИЕ:   Ядро Галактики, сектор Корусант, система Корусант
