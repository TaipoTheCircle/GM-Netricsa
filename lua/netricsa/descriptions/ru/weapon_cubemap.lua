КУБИЧЕСКАЯ КАРТА


ОРУЖИЕ:      Кубическая карта

ОПИСАНИЕ:

Утилита для разработчиков, которая генерирует набор отражающих тестовых сфер (зондов кубической карты), используемых для предварительного просмотра и захвата отражений окружающей среды. При активации инструмент размещает в мире несколько сфер с различными предустановками материалов, чтобы художники и дизайнеры уровней могли проверить зеркальное освещение, отражения и смешивание кубической карты. Не является боевым предметом - чисто диагностический и визуальный.

ПРИМЕЧАНИЯ:

- Проверьте зеркальное освещение.