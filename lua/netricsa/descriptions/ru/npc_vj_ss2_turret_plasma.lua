ПЛАЗМЕННАЯ ТУРЕЛЬ RXZ - 200


КЛАСС:        Плазменная турель RXZ - 200
МЕТАБОЛИЗМ:   Энергетическая Конструкция
ВОСПРИЯТИЕ:   Стационарная огневая точка, гибрид плазменной пушки и лазерного привода
РАЗМЕР:       ~1,5 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Зависит от стрелка
ОРУЖИЕ:       Плазменные сферы
НАГРАДА:      400
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это результат инженерной мысли Ментала, который решил, что обычных пулемётов недостаточно, и пора переходить на энергию. Конструкция простая: овальный корпус с фиолетовым кругом в центре, начинённый плазменной пушкой и системой наведения, скопированной с лазерных турелей.

В отличие от пулемётной версии, плазменная турель стреляет не очередями, а одиночными сферами фиолетовой плазмы. Один такой шар обладает идеальной точностью и уничтожает практически любую цель с одного попадания - будь то обычный Орк, боевой вертолёт или даже сам Ментал, если не повезёт. При попадании плазма взрывается, нанося урон всему, что оказалось рядом.

Ментал скомбинировал собственную плазменную пушку с системой управления от лазерной турели, получив гибрид, который может использоваться кем угодно: и солдатами Орды, и орками с планеты Орацио.

Турели обычно встроены в базы Ментала на Сириусе и других планетах.

В бою плазменная турель опасна на любой дистанции. Единственный недостаток - низкая скорострельность. Между выстрелами проходит пара секунд, и если врагов много и они подобрались вплотную, турель может не успеть перезарядиться.

Зато если внутри сидит стрелок, он защищён корпусом и может использовать турель как дополнительный источник здоровья - она принимает на себя урон, а при разрушении просто выкидывает пилота наружу.

СОВЕТЫ:

- Если видишь турель с врагом - вали его первым. Турель без стрелка - просто железяка.
- Против бронированных целей турель незаменима.
- Взрывчатка уничтожает турель быстрее, чем стрельба из мелкого оружия.
- Если видишь, что из-за турели торчат чьи-то орочьи ноги - значит, там кто-то есть.
