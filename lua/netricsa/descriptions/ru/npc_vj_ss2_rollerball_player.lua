ОПИСАНИЕ:

Роллербол - это один из тех образцов инженерной мысли, глядя на которые хочется спросить: "Что за Гений это придумал и где ему памятник поставить?". Огромный шар, утыканный шипами, внутри которого будет бежать человек. Буквально бежать, как хомяк в колесе, только вместо беличьего колеса - многотонная сфера смерти, крушащая всё на своём пути.

Управление предельно простое: хочешь ехать - беги. Хочешь повернуть - наклоняйся. Хочешь убить - разгоняйся и направляй эту махину в ближайшего циклопа. От удара шипы входят в плоть, плоть разлетается на ошмётки, а роллербол катится дальше, даже не сбавляя скорости.

Конструкция позволяет прыгать. Как именно внутри шара можно подпрыгнуть и заставить эту махину оторваться от земли - загадка, но факт остаётся фактом: роллербол скачет, словно резиновый мяч, начиненный злобой и шипами.

Использовать эту штуку можно по-разному: как оружие, как транспорт, как способ быстро свалить из опасной зоны или как аттракцион для тех, кто всегда хотел почувствовать себя колобком-терминатором. Главное - не забывать, что шар тоже получает урон. Несколько мощных столкновений - и от него останутся только шипы и воспоминания.

СОВЕТЫ:

- Разгоняйся и бей.
- Роллербол быстрый. Используй это, чтобы уворачиваться от ракет и лазеров.
- Иногда лучше перепрыгнуть препятствие, чем пытаться его снести.
- Если видишь толпу врагов - катись прямо в неё. Один заход стоит тысячи патронов.
- Не стоит путать с ИИ аналогами.
