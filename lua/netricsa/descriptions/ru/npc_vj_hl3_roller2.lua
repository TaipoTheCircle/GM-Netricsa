ШАРОВАЯ БОЛТОВАЯ МИНА АЛЬЯНСА


КЛАСС:        Шаровая Болтовая Мина Альянса
МЕТАБОЛИЗМ:   Полностью синтетический
ВОСПРИЯТИЕ:   Активная взрывная мина Альянса
РАЗМЕР:       0,8 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Крайне высокая
ОРУЖИЕ:       Контактный взрыв, прыжковая атака, осколочное поражение
НАГРАДА:      250
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Шаровая Болтовая Мина Альянса - это двухфазное оружие уничтожения. Сферический корпус, напоминающий морскую мину, полностью покрыт массивными болтами и шипами, закреплёнными под углами, рассчитанными на максимальное рассеивание при детонации.

После обнаружения цели мина немедленно переходит в режим атаки, ускоряясь и совершая резкие прыжки. Она не отступает, не замирает - её поведение похоже на хищника без инстинкта самосохранения.

При взрыве происходит следующее: центральный заряд разрывает корпус, тем самым все болты и шипы разлетаются во все стороны с огромной скоростью. Зона поражения резко увеличивается, превращаясь в плотное облако металлических поражающих элементов.

Эти болты способны пробивать лёгкие укрытия, бронежилеты и незащищённые части синтетов. Даже находясь вне радиуса основной ударной волны, цель может быть поражена вторичным "болтерным дождём".

СОВЕТЫ:

- Держи максимальную дистанцию - болты летят дальше, чем ожидается.
