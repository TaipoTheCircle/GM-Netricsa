РАЗВЕДЫВАТЕЛЬНЫЙ СОЛДАТ OVERWATCH АЛЬЯНСА


КЛАСС:        Разведывательный солдат Overwatch Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Разведка и вызов подкреплений Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Модифицированный SMG (одиночный огонь), сигнальный пистолет
НАГРАДА:      350
УГРОЗА:       Очень высокая


ОПИСАНИЕ:

Разведывательный солдат Overwatch - глаза и уши Альянса на передовой. Он не создан для затяжных боёв и прямых столкновений. Его главная задача - обнаружить противника, зафиксировать позицию и немедленно превратить локальный инцидент в полноценную карательную операцию.

Вооружён модифицированным пистолетом-пулемётом, стреляющим строго одиночными выстрелами. Это повышает точность, снижает шум и позволяет экономить боеприпасы. Однако основной инструмент солдата - синий сигнальный пистолет. Один выстрел в небо, и через короткое время в зоне появляется элитный транспортник Альянса с десантом солдат.

Броня у разведчика намеренно слабая. Он должен двигаться быстро, прятаться, менять позиции и избегать прямого огня. Если его не устранить вовремя, ситуация резко выйдет из-под контроля.

Разведчик редко сражается до смерти. Его цель - выжить достаточно долго, чтобы вызвать подмогу. После этого он может либо отступить, либо поддерживать прибывших солдат огнём с флангов.

СОВЕТЫ:

- Уничтожь его как можно быстрее - один сигнальный выстрел может всё испортить.
- Слабая броня делает его уязвимым даже для лёгкого оружия.
- Не давай ему времени и дистанции для манёвров.