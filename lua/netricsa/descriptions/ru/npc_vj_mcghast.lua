ГАСТ С НИЖНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Гаст
МЕТАБОЛИЗМ:   Неопределённый
ВОСПРИЯТИЕ:   Враждебный летающий моб
РАЗМЕР:       ~4 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Огненные шары
НАГРАДА:      350
УГРОЗА:       Высокая

ОПИСАНИЕ:

Внешне напоминает огромную белую медузу с девятью щупальцами, которая бесцельно парит по Нижнему миру, издавая душераздирающие звуки, похожие на плач ребёнка. Глаза и рот у гаста обычно закрыты, отчего он кажется если не мирным, то хотя бы безразличным. Но это впечатление обманчиво.

Обитают гасты в пустошах Нижнего мира, базальтовых дельтах и долинах песка душ - везде, где есть достаточно открытого пространства, чтобы развернуться.

У гаста уникальный способ обнаружения цели: он замечает цель с расстояния до 100 метров, но стрелять начинает только при наличии прямой видимости. Любые твёрдые блоки, включая стекло и листву, прерывают зрительный контакт. При этом гаст не пытается приблизиться к жертве.

Главное оружие гаста - огненные шары, которые летят по идеально прямой траектории со скоростью снаряда и взрываются при ударе. Шар нельзя сбить с курса, но можно отразить обратно любым ударом: мечом, рукой, стрелой, снежком или даже яйцом. Попавший в гаста собственный снаряд наносит ему 1000 единиц урона, что гарантирует мгновенную смерть.

Несмотря на свой устрашающий вид и размер, гаст имеет меньше здоровья, чем зомби. Убить его можно из лука, мечом (если удастся подобраться), или отражённым огненным шаром. С мёртвого гаста выпадает порох и слеза гаста, необходимая для зелья регенерации и крафта кристалла Края.

У гастов много странных особенностей. Они не являются нежитью - тонут в воде, получают урон от зелий урона и лечатся зельями исцеления. При этом они полностью иммунны к огню и лаве, могут сидеть в вагонетке (продолжая стрелять), и их знаменитый плач слышен одинаково громко с любого расстояния. Если гаст залетит слишком близко к стене, он может подорвать себя собственным снарядом.

В последнее время в интернете гуляют слухи о «счастливых гастах», «детёнышах гастов» и «высушенных гастах» - всё это выдумки фанатов и моды, в официальной игре ничего подобного нет .

СОВЕТЫ:

- Услышал плач - подними голову и ищи, откуда летит. Гаст может быть далеко, но его слышно всегда.
- Не стой на краю обрыва из адского камня. Один взрыв - и ты летишь в лаву.
- Огненные шары можно и нужно отбивать обратно. Это самый быстрый способ убить гаста.
- Прячься под навесами или в узких тоннелях - там гаст тебя не видит.
- Для гарантированного убийства используй лук с зачарованием на силу.
- Если гаст висит прямо над лавой, готовься к тому, что дроп сгорит. Лучше замани его на твёрдую поверхность.
- Стекло, листва и порталы закрывают обзор - можно прятаться за ними.
