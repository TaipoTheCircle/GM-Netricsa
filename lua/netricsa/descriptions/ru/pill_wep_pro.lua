ПРОФЕССИОНАЛЬНАЯ ВИНТОВКА


ОРУЖИЕ:  профессиональная винтовка
ПАТРОНЫ: 7.62x51 мм NATO
СС:      ~12 выстр./мин


ОПИСАНИЕ:

профессиональная винтовка - высокоточная болтовая винтовка, основанная на платформе Точной Международной L96A1 / Арктическая война и предназначенная для профессионального дальнего боя. Конструктивно она сохраняет все достоинства оригинала: тяжёлый плавающий ствол, выдающуюся кучность и стабильность при стрельбе на больших дистанциях.

Однако винтовка обладает одной пугающей особенностью: внутри корпуса установлен мощный динамик с записанными сотнями искажённых, яростных человеческих криков. При каждом выстреле динамик воспроизводит их одновременно с выстрелом, создавая оглушающий акустический удар. Динамик намертво припаян к внутреннему каркасу и не подлежит удалению без полного разрушения оружия.

Оружие эффективно уничтожает цели, но представляет серьёзную угрозу для слуха стрелка и всех находящихся поблизости.

ПРИМЕЧАНИЯ:

- Исключительная точность и высокий урон на дальних дистанциях.
- При каждом выстреле создаёт экстремальный шум, способный вызвать временную или постоянную потерю слуха.
- Деморализует противников и дезориентирует ближайшие цели.
- Использование без защиты слуха крайне не рекомендуется.
- Считается психологическим оружием, а не просто снайперской винтовкой.