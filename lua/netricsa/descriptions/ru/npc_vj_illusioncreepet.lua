ИЛЛЮЗОРНАЯ КОПИЯ КРИПЕРА С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Иллюзорная Копия Крипера
МЕТАБОЛИЗМ:   Иллюзорный 
ВОСПРИЯТИЕ:   Тактическая Иллюзия, Живой Обманщик
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Бесконечная
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Имитация Взрыва
НАГРАДА:      Нет
УГРОЗА:       Психологическая

ОПИСАНИЕ:

Это не настоящий крипер, а обманчивый двойник, созданный Иллюзорным крипером для запугивания жертвы. Она выглядит в точности как оригинал: те же пропорции, та же текстура, то же угрожающее шипение. Но внутри неё нет пороха, нет взрывчатой плоти, нет даже желания убивать. Есть только одна программа: отвлекать, путать и заставлять своего противника тратить силы на пустое место.

Копия не имеет физической формы. При попытке ударить её мечом или выстрелить из лука оружие проходит сквозь неё. Она не получает урона, не отбрасывается, не взрывается.

Однако копия опасна не физически, а тактически. В суматохе боя жертва может спутать её с настоящим крипером и либо потратить драгоценные патроны на пустое место, либо, наоборот, проигнорировать настоящего, приняв его за копию. В худшем случае можно попытаться убежать от копии и наткнуться на настоящего, который поджидает за углом.

Копия может имитировать взрыв: она раздувается, шипит и издаёт характерный звук, но в момент детонации просто исчезает без следа, не нанося урона и не разрушая блоки.

СОВЕТЫ:

- Не трать ресурсы на копию. Ты не сможешь её убить.
- Игнорируй копию и ищи настоящего крипера.
- Если копий несколько, настоящий, скорее всего, прячется где-то сбоку или за спиной.
- Копия не может причинить тебе вред. Не бойся её.
