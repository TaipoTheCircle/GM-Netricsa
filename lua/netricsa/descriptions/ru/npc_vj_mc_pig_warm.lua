КРАСНО-РЫЖАЯ ТРОПИЧЕСКАЯ СВИНЬЯ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Красно-Рыжая Тропическая свинья
МЕТАБОЛИЗМ:   Теплокровное млекопитающее
ВОСПРИЯТИЕ:   Дикое / Домашнее Животное 
РАЗМЕР:       ~1 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Отсутствует
НАГРАДА:      25
УГРОЗА:       Низкая

ОПИСАНИЕ:

В отличие от классических розовых собратьев, эти свиньи приспособились к жизни в жарких регионах и получили характерный красно-рыжий окрас с подпалинами, который помогает им сливаться с красноватой почвой саванн и пустынь.

Встречаются тропические свиньи исключительно в биомах с высокой температурой.

По поведению это всё те же мирные хрюшки: бродят группами по 2-4 особи, жуют траву, не проявляют агрессии и при получении урона предпочитают быстро ретироваться.

Как и обычные свиньи, тропические отлично поддаются разведению (морковь, картофель, свёкла), могут быть осёдланы и управляемы с помощью удочки с морковкой.

Езда на такой свинье по жарким биомам - отличный способ не тратить ресурсы на ходьбу.

СОВЕТЫ:

- Ищи их в саваннах и джунглях - там они встречаются чаще всего.
- Разводи с помощью моркови, картофеля или свёклы для постоянного источника пищи в жарких регионах.
- Осёдланная тропическая свинья - лучший транспорт по пустыне, если нет лошади.
- Если скрещивать тропическую свинью с другими вариантами, окрас поросят может быть любым из родительских.
