БЫСТРЫЙ ЗОМБИ-ХЕДКРАБ, АРКТИЧЕСКАЯ РАЗНОВИДНОСТЬ


КЛАСС:        Зомби-хедкраб, Быстрый (Арктический)
МЕТАБОЛИЗМ:   Паразитический хозяин, устойчивый к холоду
ВОСПРИЯТИЕ:   Обострённые рефлексы + ориентация на тепло
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Клешни, прыжки, экстремальная скорость
НАГРАДА:      280
УГРОЗА:       Средне-высокая

ОПИСАНИЕ:

Арктический быстрый зомби-хедкраб - один из самых пугающих результатов заражения в условиях вечного холода. Его тело, изломанное паразитом, сохранило характерную скелетообразную форму, но теперь покрыто обрывками льда и обледеневшей плотью. Быстрый хедкраб глубоко вцепляется в череп, дополнительно защищая себя от холода и повреждений.

В отличие от обычных быстрых зомби, арктическая разновидность не теряет скорости на снегу и льду. Существо передвигается на четвереньках, резко отталкиваясь когтистыми конечностями и цепляясь за поверхность с пугающей уверенностью. Оно способно взбираться на обледенелые конструкции и совершать длинные прыжки, используя окружающий рельеф как охотничью площадку.

СОВЕТЫ:

- Не подпускай его на дистанцию прыжка.
- Короткие очереди огня могут остановить его во время атаки.
