ОТИС ЛОРЕЙ


КЛАСС:        Отис Лорей
МЕТАБОЛИЗМ:   Человек, Мужской
ВОСПРИЯТИЕ:   Охранник Чёрной Мезы
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Низкая-Средняя
ВРАЖДЕБНОСТЬ: Оборонительная / Союзническая
ОРУЖИЕ:       Пустынный Орёл
НАГРАДА:      100
УГРОЗА:       Низкая

ОПИСАНИЕ:

Отис - охранник Чёрной Мезы, часто ленивый, рассеянный и медленно реагирующий. Несмотря на свою неуклюжесть, он вооружён мощным Пустынный Орёл, что делает его удивительно опасным в ближнем бою.

В отличие от обычных охранников, Отис - скорее комическая фигура в хаосе Чёрной Мезы. Его плохая физическая форма ограничивает его эффективность, но готовность помочь и мощное оружие делают его полезным союзником в обороне.

СОВЕТЫ:

- Береги его - его Пустынный Орёл - мощный, но выносливость низкая.
- Не полагайся на него в долгих погонях или тяжёлых боях.
- Держи врагов на расстоянии, где он сможет безопасно стрелять.
