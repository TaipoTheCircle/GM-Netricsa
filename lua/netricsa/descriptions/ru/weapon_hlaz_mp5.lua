ПИСТОЛЕТ-ПУЛЕМЁТ MP5


ОРУЖИЕ:     Пистолет-пулемёт MP5
БОЕПРИПАСЫ: Патроны 9x19 мм + 40-мм гранаты
CC:         ~700 выстрелов/мин

ОПИСАНИЕ:

Универсальное автоматическое оружие ближнего и среднего радиуса действия. Обеспечивает непрерывный угнетающий огонь с приемлемой точностью. Встроенный подствольный гранатомет повышает тактическую гибкость в различных боевых ситуациях.

ПРИМЕЧАНИЯ:

- Эффективен против пехоты в лёгкой броне.
- Гранаты эффективны в плотных строях или ограниченных пространствах.
- Точность значительно снижается на больших дистанциях.