СИЛЬНЫЕ КУЛАКИ ВОДКИ


ОРУЖИЕ:      Кулаки, наполненные водкой
ПАТРОНЫ:           Нет (рукопашный бой)
CC:         ~75 ударов/мин

ОПИСАНИЕ:

Кулаки, заряженные водкой. Удары наносятся с сверхъестественной силой, и каждый удар несет в себе жгучую энергию, пронизывающую тело пьющего.

ПРИМЕЧАНИЯ:

- Сильный ближний бой с надежной силой.
- Эффект алкоголя остается активным, слегка влияя на движения.
- Эффективен в неожиданных столкновениях в ближнем бою.