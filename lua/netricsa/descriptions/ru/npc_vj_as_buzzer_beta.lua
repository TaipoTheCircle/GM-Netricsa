БУЗЗЕР РОЯ "БЕТА"


КЛАСС:        Буззер Роя "Бета"
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Летающий Разведчик
РАЗМЕР:       0,7 м.
ВЫНОСЛИВОСТЬ: Очень низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Колющий хоботок, ядовитый укус
НАГРАДА:      100
УГРОЗА:       Низкая

ОПИСАНИЕ:

В отличие от своих более поздних собратьев, эта особь обладает вытянутым телом и крупными перепончатыми крыльями, позволяющими ей подолгу находиться в воздухе без посадки.

Главным оружием существа служат длинные колющие биыни. Во время атаки Буззер стремительно пикирует на цель, стараясь нанести быстрый укол и немедленно отступить на безопасное расстояние. В организм жертвы при этом попадает токсичное вещество, вызывающее боль и временную потерю координации.

Светочувствительный орган зрения, расположенный в передней части головы, способен улавливать движение даже в условиях слабого освещения. Благодаря этому Буззеры часто появляются в пещерах, технических тоннелях и других местах, где более крупным существам Роя трудно обнаружить добычу.

Несмотря на агрессивное поведение, данная разновидность отличается крайне слабой защитой. Большинство особей погибает после нескольких попаданий, компенсируя это скоростью, численностью и исключительной назойливостью.

Некоторые ксенобиологи считают, что именно от этих существ произошли современные Буззеры. К счастью для науки, сами Буззеры научными дискуссиями не интересуются.

СОВЕТЫ:

- Следи за потолком и открытым пространством над головой.
- Не позволяй стае кружить вокруг тебя.
- Токсин опаснее самого укуса.
