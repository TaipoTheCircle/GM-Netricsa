ОПИСАНИЕ:

Вербовщики Гражданской Обороны Альянса - специализированные сотрудники Альянса, отвечающие за пополнение рядов оккупационных сил за счёт местного населения. В отличие от обычных патрульных, их задача - не подавление, а отбор. Они выявляют подходящих кандидатов, используя страх, давление и обещания стабильности в обмен на лояльность.

Их отличительная черта - ярко-синие светящиеся глаза. Эти глаза усиливают эффект психологического доминирования и позволяют вербовщику поддерживать непрерывный зрительный контакт во время "разъяснительных бесед".

В боевых условиях вербовщики стараются не вступать в прямое столкновение, полагаясь на поддержку обычных подразделений Метрополиции. Их ценность заключается не в огневой мощи, а в способности превращать граждан в будущих солдат Альянса.

СОВЕТЫ:

- Синие глаза хорошо заметны в темноте и выдают их позицию.
