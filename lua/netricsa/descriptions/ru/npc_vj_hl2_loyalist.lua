МЕТАБОЛИЗМ:   Люди
ВОСПРИЯТИЕ:   Гражданское вспомогательное формирование
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Пистолет-пулемёт (SMG), пистолет, электродубинка
НАГРАДА:      200
УГРОЗА:       Низкая

ОПИСАНИЕ:

Лоялисты Альянса - это гражданские лица, которые добровольно встали на сторону Альянса, променяв безопасность, пайки и иллюзию стабильности на лояльность режиму. Их легко узнать по чёрной одежде и излишне уверенной походке людей, которым пообещали, что "с ними всё будет хорошо".

В отличие от солдат Overwatch, лоялисты не носят броню и не проходят полноценной боевой подготовки. Их вооружение ограничивается SMG, пистолетами и электродубинками, что делает их скорее вспомогательной силой, чем настоящими бойцами. В прямом столкновении они уязвимы и погибают быстро - Альянс это знает и не считает проблемой.

Однако не все лоялисты одинаковы. Среди них немало тех, кто лишь притворяется сторонником режима. Такие двойные агенты передают информацию Сопротивлению, саботируют приказы и иногда "случайно" стреляют не в ту сторону. Альянс подозревает это, но предпочитает закрывать глаза.

СОВЕТЫ:

- Без брони они крайне уязвимы - не затягивай бой.
- Не каждый лоялист действительно враг, но выяснять это в бою - плохая идея.
- Электродубинка опасна вблизи, держи дистанцию.