ШОУ HALF-QUAKE, СОМОС


Ну вот мы и добрались до режиссёрской будки ада.

СОМОС стоит в центре зала, окружённый энергощитом, как король, который боится собственных подданных. Обычное оружие здесь бесполезно. Пули отскакивают, как жалобы зрителей на политику шоу.

Садисты оказались не только садистами, но и инженерами. Машина поддерживает своё "божественное" состояние через систему кнопок. Четыре узла. Четыре точки боли. Четыре аргумента против его вечности.

Кнопки охраняют вортигонты.

Ирония: они даже не в курсе, что силы Ментала считаются союзниками. Поэтому бьют всех подряд, включая тех, кто якобы пришёл "по контракту".

Чистый хаос, идеальная операционная среда для тебя.

Отключаешь первую четвёрку - и открывается следующая дверь. Там кнопка второго этапа капчи.

Да, Сэм. Даже сей ад внедрил двухфакторную аутентификацию.

После этого открывается последняя дверь. Кнопка снятия щита. Тишина. Момент истины.

И СОМОС останется просто большой, дорогой и очень уязвимой кучей металла.