пробиться через паркур, кишащий ордой Ментала, найти портал, попасть в пространство летающих платформ, нажать все эти милые кнопочки, активировать главный портал и лично объяснить СОМОСу, что у машин тоже бывают фатальные ошибки архитектуры. Обычно в области головы.

