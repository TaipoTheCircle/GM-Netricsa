БЛАСТЕРНАЯ ВИНТОВКА E-5


ОРУЖИЕ:      Бластерная винтовка E-5
БОЕПРИПАСЫ:        Энергетические элементы
ТЕМП ОГНЯ:         ~600 выстрелов/мин

ОПИСАНИЕ:

Стандартная бластерная винтовка для элитных подразделений. Выпускает быстрые энергетические заряды, способные пробивать легкую броню и наносить значительный урон на средней дистанции. Надежное, легкое и эффективное оружие как для подавления, так и для точного огня.

ПРИМЕЧАНИЯ:

- Высокая скорострельность с умеренной отдачей.
- Эффективно против пехоты и легкобронированных целей.
- Необходимо следить за зарядом энергетических элементов, чтобы избежать их разрядки во время боя.