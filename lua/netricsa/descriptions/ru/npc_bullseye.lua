БУЛЛСАЙ


КЛАСС:         Буллсай
МЕТАБОЛИЗМ:    Скриптовая сущность
ВОСПРИЯТИЕ:    Система отслеживания
РАЗМЕР:        ~1 фут (маленькая сфера)
ВЫНОСЛИВОСТЬ:  Низкая (хрупкая)
ВРАЖДЕБНОСТЬ:  Нейтральная (не атакующая)
ОРУЖИЕ:        Нет (перенаправляет атаки)
НАГРАДА:       1
УГРОЗА:        Очень низкая

ОПИСАНИЕ:

Буллсай - это специальная скриптовая сущность, используемая движком (и картостроителями) для перенаправления урона. По сути, это прокси-цель: всё, что связано с Буллсаем, будет перенаправлять урон или луч огня в его местоположение. Например, вы можете разместить Буллсай невидимым в комнате, привязать к нему модели NPC и атаковать NPC, попавших под удар, в истинном местоположении Буллсая.

С точки зрения игрового процесса Буллсай невидим и неосязаем; он не сражается и не реагирует. Его роль чисто техническая - он используется на картах для стрельбы сквозь стен или перенаправления атак в головоломках, ловушках или заскриптованных событиях.

СОВЕТЫ:

- Понимание Буллсай позволяет мапперам создавать огонь в определённые участки, будь то стеклянные стены или удалённые триггеры.