ОПИСАНИЕ:

Гибридный тюремный солдат Overwatch с дробовиком - это усиленная версия бойца Нова Проспект, временно или постоянно выведенная за пределы тюремных комплексов. Альянс использует этих солдат в ситуациях, когда стандартных сил недостаточно, а элиту привлекать нецелесообразно.

Солдат облачён в усиленную броню тюремного образца и оснащён тактическим шлемом с защитным забралом. Красный визор подчёркивает его принадлежность к караульно-штурмовым силам Нова Проспект. Несмотря на внешнюю защищённость, визор не является полностью пуленепробиваемым - точные выстрелы в голову остаются эффективными.

Основное оружие - SPAS-12, предназначенный для подавления бунтов, зачистки камер, коридоров и технических зон. Тактика бойца проста и жестока: быстрое сближение, подавляющий огонь и полное отсутствие попыток захвата, если приказом не указано иное.

СОВЕТЫ:

- Не переоценивай забрало - хедшоты по-прежнему работают.
- В ближнем бою SPAS-12 практически не оставляет шансов.
- Лучший вариант - дистанция и точный огонь.
- В узких коридорах он чувствует себя как дома.