ТАТУИН


ПЛАНЕТА:        Татуин
ТИП:            Планета земного типа, Пустынная Планета
КЛИМАТ:         Засушливый, экстремально жаркий днём, холодный ночью
СПУТНИКИ:       Отсутствуют
ЛАНДШАФТ:       Бескрайние песчаные моря, скалистые каньоны, пустоши, плато
РАСПОЛОЖЕНИЕ:   Внешнее Кольцо, сектор Арканис, система Татуин (двойная звезда: Тату I и Тату II)
МЕСТНЫЕ РАСЫ:   Люди (поселенцы), Джавы, Тускены (Песчаные люди), Хатты
ПРИНАДЛЕЖНОСТЬ: Пространство хаттов (де-факто), сектор Арканис (де-юре), Галактическая Империя (Ранее), Новая Республика (ранее), Галактический Союз (Сейчас)
МЕСТА:          Мос-Эйсли, Мос-Эспа, Анкорхед, Бестин, Дворец Джаббы, Дюнное море

ОПИСАНИЕ:

Негостеприимный планета-пустыня, вращающаяся вокруг двойного жёлтого солнца.

Некогда цветущая с океанами и джунглями, она был превращёна в пустыню тысячелетия назад в результате орбитальных бомбардировок Бесконечной Империи ракатов. Сегодня его поверхность - это бескрайние песчаные моря, выжженные двойным солнцем, изредка прорезанные скалистыми каньонами, такими как Каньон Нищего, образовавшийся около двух миллионов лет назад.

Климат суров: днём стоит невыносимая жара, а ночью температура резко падает. Вода покрывает менее процента поверхности, и основным источником влаги для поселенцев являются влагосборщики, конденсирующие воду из сухого воздуха. Благодаря уникальному сочетанию магнитных руд в мантии и атмосферных ветров существует несколько умеренных зон, где температура редко превышает 43 градуса - именно здесь и сосредоточены немногочисленные поселения.

Татуин находится на пересечении многих гиперпространственных маршрутов, что сделало его пристанищем для контрабандистов, наёмников и охотников за головами, скрывающихся от закона. Поверхность планеты буквально усеяна обломками древних кораблей, погребённых в песках. Де-юре планета считается частью сектора Арканис, но де-факто здесь заправляют криминальные кланы хаттов, такие как клан Джаббы Десилиджика, контролирующие контрабанду и прочие тёмные дела.

Коренное население представлено двумя расами, произошедшими от единого древнего народа. Джавы - маленькие, суетливые мусорщики в характерных балахонах, путешествующие по пустыне на гигантских сандулах в поисках металлолома для перепродажи. Песчаные люди, или Тускены - высокие воинственные кочевники, враждебные к чужакам и яростно защищающие свою территорию. Люди-колонисты живут в основном за счёт влагофермерства в таких поселениях, как Мос-Эйсли, Мос-Эспа, Анкорхед и столичный Бестин.

Несмотря на суровые условия, жизнь на Татуине разнообразна. Здесь обитают огромные мохнатые банты, используемые тускенами, медлительные ронто, стада эопи, а также опаснейшие хищники - крайт-драконы, в чьих желудках находят ценный драконий жемчуг, и чудовищные сарлакки, переваривающие жертву тысячу лет. Из растительности наиболее известна хубба-тыква - основной источник пищи для джав и тускенов.

КЛЮЧЕВЫЕ МЕСТА:

- Мос-Эйсли: Крупный космопорт и криминальный центр планеты с барами, ангарами и тёмными делишками.
- Мос-Эспа: Город-порт, известный своим иподромом, где проводятся знаменитые гонки на подах.
- Анкорхед: Старейшее поселение на планете, основанное ещё первыми колонистами.
- Бестин: Официальная столица Татуина, расположенная примерно в двухстах километрах к западу от Мос-Эйсли.
- Дворец Джаббы: Мощная крепость в пустыне, служащая резиденцией криминального авторитета Джаббы Хатта.
- Дюнное море: Огромная область подвижных песков, простирающаяся на многие километры.
