ОПИСАНИЕ:

Робот-пехотинец H.E.C.U. - это механизированная замена солдатам из боевого подразделения Hazardous Environment Combat Unit. Разработанные для опасных боевых зон, таких как вторжения в Зен или зоны резонансных катастроф, эти машины устраняют "человеческий фактор" - никакой усталости, никаких колебаний и никаких моральных проблем.

Их дизайн имитирует стандартные бойцы H.E.C.U., запугивая и вводя в заблуждение врагов, но под камуфляжем скрывается усиленный экзоскелет с боевым ИИ, который рассчитывает тактику в режиме реального времени. В отличие от роботов-убийц Чёрных Оперативников (оптимизированных для скрытности), роботы-пехотинцы H.E.C.U. служат ударными бойцами, ведя подавляющий огонь с нечеловеческой точностью и выносливостью.

Слухи по Чёрной Мезе предполагают, что эти роботы прошли полевые испытания во время инцидента, заменив отряды, которые "так и не вернулись". Выжившие шепчутся, что их холодные, синтетические голоса, кричащие "Цель захвачена", были последним, что многие слышали.

