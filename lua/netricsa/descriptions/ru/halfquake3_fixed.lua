ШОУ HALF-QUAKE, КНОПКИ


Мы в комнате, Сэм. Пока ещё комнате. Очень скоро она начнёт вращаться и перестанет притворяться архитектурой. Стены поедут, пол провернётся, и всё вокруг превратится в аккуратно перемешанный фарш из металла, плоти и дизайнерских решений шоу-проекта.

За одной из стен сидят два хедкраба. Терпеливо. Как будто знают, что им положена небольшая роль второго плана. За ними - гранаты и снарки. Приятный бонус для тех, кто привык проверять стены на честность. При создании организаторы называли это "возможностью". Я называю это ошибкой в расчётах.

Впереди туннель, ведущий к следующим комнатам. Здесь всё строится на принципе "туда - обратно". Кнопки расставлены так, чтобы ты постоянно бегал: нажал одну - возвращайся. Нажал вторую - снова назад. И так до тех пор, пока система наконец не признает, что ты достаточно настрадался, чтобы открыть проход.

И да. В самом конце, за трубой, спрятана секретная кнопка. Маленькая. Ненавязчивая. Для тех, кто смотрит чуть внимательнее, чем от него ожидают. Шоу не любит, когда находят такие вещи, но и убрать их не решается. Иллюзия честной игры должна сохраняться.

Так что бегай, Сэм.

Нажимай. Возвращайся. Успевай.

И постарайся не стать частью декора, пока сцена вращается.