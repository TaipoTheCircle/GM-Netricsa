ШОКОВЫЙ СОЛДАТ ИЗ ИЗМЕРЕНИЯ X


КЛАСС:        Шоковый Солдат
МЕТАБОЛИЗМ:   Плотоядный / Биоэлектрический
ВОСПРИЯТИЕ:   Раса X, Тяжёлая пехота
РАЗМЕР:       2,4 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Шоковый таракан (биоэлектрическая винтовка), удары когтями, пинки
НАГРАДА:      400
УГРОЗА:       Высокая

ОПИСАНИЕ:

Шоковые Солдаты - основная пехота Расы X, выведенная как элитные солдаты и командиры. Их высокие и мускулистые тела покрыты природным хитиновым покрытием, что обеспечивает им впечатляющую устойчивость к обычному оружию. Их фирменное оружие - Шоковый таракан, живой биоорганизм, служащий энергетическим бластером. Солдат и таракан образуют симбиотическую связь: когда таракан падает, он отделяется и продолжает сражаться как самостоятельное существо.

Шоковые штурмовики также используют Пещерных Трутней в качестве вспомогательных юнитов, направляя их вперёд в качестве разведчиков и атакующих на передовой. Их тактика организована и эффективна, в отличие от дикой агрессии фауны Зена. В бою ударные штурмовики уверенно наступают, ведя биоэлектрический огонь и используя свою мощь для сокрушения врагов в ближнем бою.

Их присутствие на Земле во время инцидента в Чёрной Мезе было знаком того, что Раса X стремилась к полномасштабному вторжению, соперничая с самим Зеном.

СОВЕТЫ:

- Отдай предпочтение Шоковому Таракану - это оружие мощное, но имеет ограниченную дальность стрельбы.
- Взрывчатка и тяжёлое оружие лучше всего работают против их толстой брони.
- Будь готов справиться с отсоединившимися Шоковыми Тараканами после смерти штурмовика.
- Избегай ближнего боя - они превосходны в ближнем бою, если им позволить приблизиться.
- Могут появиться редкие оранжевокожие Шоковые Солдаты; они сильнее и выносливее своих зелёных собратьев.
