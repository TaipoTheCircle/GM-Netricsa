ЗАРАЖЁННЫЙ ЗОМБИ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Заражённый Зомби
МЕТАБОЛИЗМ:   Нежить
ВОСПРИЯТИЕ:   Враждебный гуманоид
РАЗМЕР:       ~1,9 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Руки, упрямство и чужая экипировка
НАГРАДА:      800
УГРОЗА:       Средняя

ОПИСАНИЕ:

Заражённый Зомби - это бывший человек мира Minecraft, который однажды решил не ложиться спать и очень об этом пожалел. Медленные, настойчивые и удивительно целеустремлённые, они бродят по ночам в поисках всего живого, что ещё умеет думать.

Заражённый Зомби не отличаются интеллектом, но компенсируют это количеством. Они способны подбирать предметы, носить броню и иногда выглядят так, будто только что ограбили склад приключенца. При попадании под солнечный свет загораются, что, впрочем, редко заставляет их пересмотреть жизненные приоритеты.

СОВЕТЫ:

- Солнце - твой бесплатный союзник, используй утро с умом.