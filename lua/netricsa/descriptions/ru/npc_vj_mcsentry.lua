ВЗРЫВНОЙ ЧАСОВОЙ С ЭФИРНОГО МИРА МАЙНКРАФТА


КЛАСС:        Взрывной Часовой
МЕТАБОЛИЗМ:   Механический, Каменный Голем
ВОСПРИЯТИЕ:   Враждебный моб, Стационарная Ловушка
РАЗМЕР:       1 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Взрыв С Мощным Отбрасыванием
НАГРАДА:      90
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это одна из разновидностей охранных механизмов Эфирного измерения. Внешне он выглядит как каменный блок с вырезанным символом или глазом, который прикреплён к полу, стене или потолку. Долгое время он остаётся неподвижным, сливаясь с окружением, но стоит его противнику приблизиться, как он активируется.

В отличие от других Часовых, которые стреляют лазерами или снарядами, этот специализируется на самоуничтожении. При обнаружении цели детонационный сенсат начинает мигать, издавать звуковой сигнал, а затем взрывается. Сам взрыв наносит небольшой урон, но его главная опасность - в отбрасывании. Жертву подбрасывает высоко в воздух, и если над головой нет потолка, смертельное падение с высоты может стать причиной гибели.

Взрывные Часовые находятся в основном в подземельях Слайдера, где появляются из резных блоков-ловушек или через специальные спавнеры. Иногда их могут призывать Часовые Стражи.

СОВЕТЫ:

- Перед входом в подземелье осмотри стены, пол и потолок. Если видишь блок с подозрительным символом - обойди стороной.
- Если сражаешься в помещении, убедись, что над тобой есть потолок. Это спасёт от урона от падения.
- Используй лук или арбалет, чтобы уничтожить Часового на расстоянии до его активации.
- Если Взрывной Часовой уже замигал - отбеги назад и жди взрыва. Не пытайся проскочить мимо.
