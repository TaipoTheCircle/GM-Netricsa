ИЛЛЮЗОРНЫЙ КРИПЕР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Иллюзорный Крипер
МЕТАБОЛИЗМ:   Растительно-Пороховой С Элементами Иллюзорной Магии
ВОСПРИЯТИЕ:   Враждебный моб, Тактический Обманщик
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Клоны Иллюзии, Cамоподрыв
НАГРАДА:      60
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это разновидность крипера, которая не полагается на грубую силу, а использует обман. Этот крипер среднего уровня сложности, опасный не столько взрывом, сколько способностью запутывать жертву перед смертью.

Как и все криперы, Иллюзорный крипер обладает низким здоровьем, но его главная сила - создавать копии самого себя. Эта способность делает его одним из самых коварных противников: пока жертва пытается понять, какой из криперов настоящий, настоящий подбирается ближе и взрывается.

Иллюзорный крипер может создавать несколько копий самого себя, которые визуально неотличимы от оригинала. Копии двигаются, шипят и имитируют атаку, но при прикосновении исчезают или не наносят урона.

СОВЕТЫ:

- Не поддавайся панике, когда видишь несколько криперов сразу. Скорее всего, большинство из них - копии.
- Используй дистанционные атаки (лук, арбалет, снежки).
- Не трать ресурсы на убийство копий. Просто игнорируй их и сосредоточься на оригинале.
- Иллюзорный крипер особенно опасен в тёмных пещерах, где сложно отличить копии из-за плохой видимости. Носи с собой факелы.
