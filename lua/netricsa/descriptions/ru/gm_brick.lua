КИРПИЧ, КИРПИЧНАЯ БАШНЯ АДА


Сэм, мы нашли это. Место, где архитектор окончательно перестал стесняться и просто штамповал кирпичи друг на друга.

Если предыдущие версии были про горизонтальное расширение (дома, гаражи, бассейны), то здесь всё ушло в вертикаль. Серьёзно.

Всё это намешано в вертикальную конструкцию. Это не дом, не башня, а скорее кирпичный лабиринт. Стены, платформы, переходы - всё из кирпича. И повсюду лестницы. Много лестниц.

Главная механика:

Здесь есть одна ключевая особенность, которая превращает карту в философский эксперимент:

В какой-то момент ты решишь, что достаточно насмотрелся, и прыгнешь вниз.

И там, внизу, тебя встречает... та же самая лестница.

Ты приземляешься и понимаешь: лестница, по которой ты только что поднимался, стоит прямо перед тобой. Ты можешь снова начать подъём. И снова прыгнуть. И снова оказаться у её подножия.

Только давай договоримся: если через час мы снова будем стоять у подножия этой лестницы, я начинаю подозревать, что мы в аду. Кирпичном, серо-красно-жёлтом аду.
