МЕТАБОЛИЗМ:   Аугментированный человек, мужчина  
ВОСПРИЯТИЕ:   Командно-разведывательное подразделение Альянса  
РАЗМЕР:       1,8 м.  
ВЫНОСЛИВОСТЬ: Средняя  
