ОПИСАНИЕ:

Элитный офицер Overwatch - это полевой командир Альянса, сочетающий функции разведчика, координатора и психологического подавления. Он носит тёмную тактическую форму с усиленной бронёй на коленях, чёрные сапоги и чёрную беретку, подчёркивающую его статус среди обычных солдат.

Его шлем заменён на вариант с единственным бело-жёлтым светящимся визором, выполняющим роль тактического сканера и командного интерфейса. На плечи накинута серая накидка, используемая как маскировочный элемент и символ ранга. За спиной закреплена радиостанция дальнего радиуса действия, позволяющая вызывать подкрепление элитных транспортников Альянса.

Вооружён глушёным пистолетом для скрытного устранения целей и подавления свидетелей. При обнаружении угрозы офицер использует сигнальный пистолет, вызывая отряды солдат, элитных бойцов или дронов поддержки. Его задача - не вступать в длительный бой, а управлять боем, превращая локальный инцидент в полномасштабную операцию Альянса.

СОВЕТЫ:

- Убей офицера до того, как он вызовет подкрепление.
- Глушёный пистолет делает его почти незаметным охотником.
- Радиостанция - его главная сила. Без неё он обычный командир.
- Не недооцени его: он редко стреляет сам, но решает, кто будет стрелять по тебе.
