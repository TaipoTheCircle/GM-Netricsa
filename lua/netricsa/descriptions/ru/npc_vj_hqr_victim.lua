ЖЕРТВА С ШОУ HALF-QUAKE


КЛАСС:        Жертва
МЕТАБОЛИЗМ:   Человек, Киборг, Самец 
ВОСПРИЯТИЕ:   Узник Шоу, Восставшая Пешка
РАЗМЕР:       ~1,9 м.
ВЫНОСЛИВОСТЬ: Переменная
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       MP5, Дробовик SPAS-12
НАГРАДА:      500
УГРОЗА:       Высокая

ОПИСАНИЕ:

На первый взгляд, это бездушный робот. Зелёная броня, угловатые формы, тяжёлая поступь. Но за бронёй скрывается человеческая плоть. Кровь, мышцы, отчаяние и ненависть. Его лицо скрыто за металлической маской, но если приглядеться, видно что-то живое. Что-то, что давно потеряло надежду, но не потеряло ярость.

Он попал на шоу Half-Quake за "Свою страшную ошибку в жизни". Садисты не говорят, какую именно. Может быть, он убил не того человека. Может быть, он перешёл дорогу не тому садисту. А может быть, ошибки не было вовсе. Просто нужно было тело для нового выпуска. И для этого тела выбрали его.

Он вооружён MP5 и дробовиком SPAS-12. Не потому, что ему дали оружие. А потому, что он отобрал его у охранников, которых убил в прошлых выпусках. Он ненавидит Шоу. Он хочет сбежать. Он хочет уничтожить это место дотла. Но главное - он хочет больше никогда не умирать.

Жертва умирал много раз. Его резали, жгли, топили, взрывали, протыкали, скармливали монстрам. Но он воскресает. Снова и снова. Садисты считают, что это делает шоу интереснее. Сама Жертва считает, что это проклятие. Но каждый раз, открывая глаза после очередной смерти, он берёт в руки оружие и идёт дальше, дабы положить конец кошмарам.

СОВЕТЫ:

- Не стреляй в Жертву. Он не твой враг.
- Если встретишь его в коридорах Шоу, держись рядом. Он знает, где охранники, где ловушки и где выход.
- Жертва не доверяет никому. Не жди, что она будет делить с тобой патроны. Но он может прикрыть тебя в бою.
- Садисты ненавидят его. Сомос тоже. Тот факт, что она до сих пор жив (в каком-то смысле), бесит их сильнее, чем любой бунт.
- Не пытайся его спасать. Он не нуждается в спасении. Он нуждается в мести. И в конце концов, он либо уничтожит Шоу, либо умрёт в последний раз. Насовсем.
