ИМПЕРСКИЙ ДОЗОРНЫЙ С НИРНА


КЛАСС:        Имперский Дозорный
МЕТАБОЛИЗМ:   Имперец, Мужчина
ВОСПРИЯТИЕ:   Страж порядка в Имперском городе / Блюститель закона
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Средняя 
ВРАЖДЕБНОСТЬ: Условная 
ОРУЖИЕ:       Серебряный длинный меч, иногда магия разрушения
НАГРАДА:      200
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это столп порядка и законности в Имперском городе Сиродила. Эти бойцы в характерных доспехах Легиона - являются представителями Имперского Легиона и несут нелёгкую службу по охране правопорядка в столице провинции.

Внешность дозорного внушает уважение и некоторую опаску. Они облачены в полный комплект стальных доспехов Легиона, включая массивный шлем, и вооружены серебряным длинным мечом. Их задача - быть заметными, чтобы потенциальные преступники дважды подумали, прежде чем нарушать закон. Некоторые дозорные владеют заклинаниями школы Разрушения, что делает их опасными не только в ближнем, но и в дальнем бою.

Главная функция Дозорного - правосудие. Если ты совершишь преступление, будь то убийство, кража или нападение, ближайший стражник немедленно бросится к тебе с криком: "Стой, преступное Отродье!". У тебя будет выбор: заплатить штраф, отправиться в тюрьму или оказать сопротивление. Если ты решишь сдаться, стражник конфискует все украденные вещи и сопроводит тебя в замок для отбытия наказания.

Сопротивление аресту - плохая идея. Дозорные сражаются умело и слаженно, и если ты откажешься сдаваться, они без колебаний обнажат мечи, крикнув: "Тогда заплати своей кровью!".

Существует несколько специализированных подразделений Имперских Дозорных. Тюремные стражи охраняют Имперскую тюрьму и используют вместо меча серебряную булаву. У них всегда при себе ключи от камер, что может быть полезно для заключённого с хорошей ловкостью рук.

Некоторые стражники, увы, подвержены коррупции. Например, капитан Ауденс Авидий был уличен в вымогательстве денег у торговцев. Честные же стражи, вроде капитана Итиуса Хейна, готовы помочь в поимке преступников, но потребуют неоспоримых доказательств.

СОВЕТЫ:

- Если ты планируешь заняться преступной деятельностью, делай это в безлюдных местах и не попадайся на глаза страже. Их зрение и слух работают отлично.
- Если вас заметил за кражей, и стражник подошёл, лучший выход - заплатить штраф. Сопротивление аресту почти всегда заканчивается смертью.
- Имей при себе достаточно золота, чтобы покрыть штраф. Если денег нет, вариантов не остаётся - только тюрьма или бой.
- Помни, что после отбытия наказания все украденные вещи будут конфискованы. Прячь их в надёжном месте перед тем, как сдаться властям.
- Слушай, что говорят стражники. Они часто дают полезные советы о городе, его районах и последних событиях.
