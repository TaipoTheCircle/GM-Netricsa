ПЕРЕВЁРНУТЫЙ КРИПЕР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Перевёрнутый крипер
МЕТАБОЛИЗМ:   Гравитационно-Растительно-Пороховой
ВОСПРИЯТИЕ:   Враждебный Моб, Искажатель Реальности
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Взрыв Антигравитации
НАГРАДА:      200
УГРОЗА:       Средне-Высокая

ОПИСАНИЕ:

Это один из самых странных представителей элементальных криперов. Внешне это обычный крипер, но его голова перевёрнута. Из-за этого он выглядит так, будто кто-то взял обычного крипера, перевернул его голову, а тело забыл перевернуть обратно.

При приближении жертвы перевёрнутый крипер ведёт себя как обычный: шипит, раздувается и взрывается. Но эффект взрыва у него уникальный.

Когда перевёрнутый крипер взрывается, он не уничтожает блоки и не наносит прямой урон. Вместо этого он создаёт поле, которое меняет гравитацию для всех существ в радиусе взрыва.

Все противники в зоне поражения мгновенно "падают" вверх.

Существа поднимаются вертикально вверх, пока не достигнут потолка (если он есть) или не улетят в открытое небо / космос.

Если потолка нет, жертва улетает вверх на десятки сотен блоков, пока эффект не пропадёт.

Эффект длится несколько секунд, после чего гравитация возвращается в норму. Если в момент падения жертва оказалась достаточно высоко, она упадёт обратно и получит урон от падения. Если улетела в космос - считай, что уже на том свете.

СОВЕТЫ:

- Перевёрнутый крипер не опасен в помещениях с низким потолком. Ты просто ударишься головой о потолок и упадёшь обратно.
- На открытой местности он смертелен. Один взрыв - и ты улетаешь в стратосферу.
- Чтобы избежать взлёта, прижмись к блоку сверху. Если ты стоишь под навесом, гравитационное поле не сработает.
- Используй щит, чтобы заблокировать ударную волну. Щит не останавливает изменение гравитации, но уменьшает расстояние, на которое тебя отбрасывает.
- При убийстве перевёрнутого крипера есть шанс получить перевёрнутый осколок. Из него можно сделать зелье левитации, которое позволяет подниматься вверх по желанию.
- Не пытайся убежать. Он шипит так же, как обычный крипер. Услышал шипение - отпрыгивай в сторону, а не вверх.
