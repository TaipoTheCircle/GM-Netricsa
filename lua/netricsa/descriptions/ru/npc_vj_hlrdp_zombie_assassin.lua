ОПИСАНИЕ:

Это элитная оперативница частной военной компании Y.S.M., с которой встретился Хедкраб. Её латексный костюм сохранил технологию невидимости, и зомби-хедкраб научился ей пользоваться инстинктивно. Она становится почти полностью невидимой, двигаясь по коридорам базы Дельта. Её акробатическая подготовка никуда не делась - она так же быстро бегает.

Выдаёт её только слабое мерцание. При атаке невидимость падает: она становится видимой ровно настолько, чтобы вонзить когти в жертву, а затем снова исчезает в темноте. При убийстве в голову - голова отрывается.

СОВЕТЫ:

- При атаке она становится видимой.
- Дробовик с разбросом эффективнее, чем точное оружие - легче попасть по мерцающему силуэту.
- У неё нет брони, только латекс. Одно-два попадания - и она падает. Но попасть в неё - проблема.
- В стае с другими зомби она особенно опасна.
