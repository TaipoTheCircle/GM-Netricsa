ОПИСАНИЕ:

Этот экземпляр - бывший научный сотрудник базы Дельта, который имел неосторожность отправиться в Зен или работать в особо опасных условиях окружающей среды в фирменном пурпурном защитном костюме. По функционалу он полностью аналогичен H.E.V. из Чёрной Мезы: герметизация, усиление ударов, защита от радиации и прочих прелестей научной работы на грани реальности.

Даже это не спасло некоторых бедолаг от хедкраба. Паразит нашёл способ проникнуть в шлем - или, возможно, носитель сам снял его в критический момент - и сделал своё чёрное дело. Теперь передвигающаяся туша в пурпурном костюме - это зомби, но зомби с преимуществами.

Защитный костюм даёт ему двойное преимущество. Во-первых, живучесть. Пули и осколки гасятся усиленной тканью и пластинами костюма. Во-вторых, сила. Сервоприводы, которые помогали учёным поднимать тяжёлое оборудование, теперь усиливают удары зомби, делая их сокрушительными.

Пурпурный костюм испачкан кровью и грязью, шлем разбит или отсутствует. При убийстве в голову - голова отрывается.

СОВЕТЫ:

- У этого зомби самая высокая броня из всех стандартных разновидностей. Обычный пистолет почти бесполезен.
- Целься в голову. Костюм защищает тело, но не голову.
- Используй дробовик в упор или автоматическое оружие с крупным калибром.
- При убийстве в голову голова отрывается, как и у остальных.
- Не думай, что костюм можно снять и надеть. Он теперь часть монстра.

