ПЕРЕНОСНАЯ ПУСКОВАЯ УСТАНОВКА


ОРУЖИЕ:  Переносная пусковая установка
ПАТРОНЫ: Реактивные Нагретые ракеты (Кумулятивные боеголовки)
СС:      ~17,6 выстр. / мин.

ОПИСАНИЕ:

Тяжёлое пехотное оружие автоматонов, используемое ракетными рейдерами для уничтожения бронетехники, укреплений и плотных скоплений целей. Представляет собой переносную пусковую систему, стреляющую реактивными нагретыми снарядами с высокой пробивной способностью.

Кумулятивная боеголовка предназначена для разрушения укреплений и пробития тяжёлой брони, а взрыв дополнительно создаёт поражающую область, опасную для живой силы. Несмотря на относительно низкую скорострельность, каждая ракета обладает значительной разрушительной мощью и может переломить ход боя одним точным попаданием.

Из-за массы и ограниченного темпа стрельбы оружие чаще применяется с дистанции или из укрытия, позволяя ракетным рейдерам поддерживать штурмовые группы тяжёлым огнём.

ПРИМЕЧАНИЯ:

- Эффективно против бронетехники, укреплений и тяжёлых целей.
- Взрыв наносит урон по области и создаёт ударную волну.
- Наиболее эффективно на средней и дальней дистанции.
- Опасно в ближнем бою, выстрел рядом с пользователем часто приводит к фатальным последствиям.
- Кумулятивная боеголовка способна разрушать укрепления и пробивать тяжёлую броню.
- Медленная практическая скорострельность компенсируется высокой разрушительной силой.
