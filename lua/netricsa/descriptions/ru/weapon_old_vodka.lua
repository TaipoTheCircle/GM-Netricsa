ВОДКА ДРЕВНИХ


ОРУЖИЕ:      Водка Древних
CC:         Одна бутылка.

ОПИСАНИЕ:

После употребления зрение становится расплывчатым, мир искажается, но в жилах пьющего течет скрытая сила. Несмотря на спутанность сознания и головокружение, проявляется сила.

ПРИМЕЧАНИЯ:

- Дает сырую физическую силу.
- Сильное искажение восприятия.
- Опасно при точном или тактическом бою.