ТРАНКВИЛИЗАТОРНЫЙ ПИСТОЛЕТ


ОРУЖИЕ:     Berreta M9
ПАТРОНЫ:    Дротики с транквилизатором
CC:         ~10 выстрелов/мин

ОПИСАНИЕ:

Berreta M9 которая была переделена для стрельбы дротиками с усыпляющим веществом. Данная вещь используется специальными службами стран.

ПРИМЕЧАНИЯ:

- Если хочется постелсить или никого не убить, это оружие подойдет.
- Лучше стрелять в голову, потому что при попадении в головной мозг, вещество внутри дротика работает быстрее.


