КАДАВР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Кадавр
МЕТАБОЛИЗМ:   Пустынная Нежить
ВОСПРИЯТИЕ:   Враждебный моб
РАЗМЕР:       1,9 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Руки, эффект "Голод"
НАГРАДА:      160 FC
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это зомби, который слишком долго проторчал на солнце и теперь ему за это ничего не грозит. В отличие от обычных собратьев, эти твари не горят в пустыне днём, потому что их высушенная шкура давно потеряла чувствительность к ультрафиолету. Визуально кадавр выше обычного зомби на 2 см, что не позволяет ему пролезать в двухблочные проёмы.

Они терроризируют пустыни, саванны и прочие жаркие биомы. Водяться группами по 2–4 особи при свете ниже 7, исключительно под открытым небом.

Главная особенность кадавра - удар с эффектом "Голод". Если у него пустые руки (нет оружия), его касание высасывает сытость быстрее, чем песок высасывает воду. Длительность эффекта зависит от сложности и региональной сложности: от 7 до 30 секунд. Щит блокирует и урон, и голод, так что держи его наготове.

В остальном кадавр ведёт себя как обычный зомби: преследует жертв, жителей, железных големов и черепашьи яйца с расстояния 40 метров. Может подбирать предметы, ломать двери и организовывать засады. Если кадавр залезет в воду, через 30 секунд его начнёт трясти, а ещё через 15 он превратится в обычного зомби. Процесс необратим, даже если вытащить его на сушу. Дальше этот зомби может стать утопленником, если продолжит сидеть в воде.

При смерти кадавр оставляет гнилую плоть, изредка морковь, картофель или железный слиток. Картошка выпадает печёной, если моб сгорел. Если кадавр был с экипировкой, она выпадает с 100% шансом. В Хэллоуин могут заспауниться с тыквой на голове.

СОВЕТЫ:

- Кадавры не горят на солнце - не жди, что они сами сдохнут к утру.
- Удар кадавра накладывает голод - всегда имей при себе запас еды или щит.
- Загони кадавра в воду - через 45 секунд он превратится в обычного зомби, которого проще контролировать.
- Детёныши быстрые и могут сидеть на ком угодно - не дай себя окружить.
- Для гарантированного дропа используй зачарование "Добыча III".
- Кадавр чуть выше зомби - иногда застревает в проёмах, где обычная нежить может пройти.
