ПТЕРАНОДОН С ТЕРРАФОРМИРОВАННОЙ ПЛАНЕТЫ


КЛАСС:        Птеранодон
МЕТАБОЛИЗМ:   Летающий ящер, Птерозавр
ВОСПРИЯТИЕ:   Воздушный хищник
РАЗМЕР:       Огромный размах крыльев
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Клюв, когти, пикирующая атака
НАГРАДА:      400
УГРОЗА:       Средняя

ОПИСАНИЕ:

Один из немногих летающих ящеров, который не просто парит в небе, а активно охотится на всё, что движется по земле. Этот подвид настроен агрессивно и рассматривает людей как законную добычу.

В бою птеранодон использует тактику "нырнул - клюнул - улетел". Он появляется из-за пределов видимости, пикирует на цель, нанося удар клювом или пытаясь схватить когтями, и сразу уходит вверх, чтобы через несколько секунд повторить атаку.

СОВЕТЫ:

- Не стой на открытом месте.
- Обрез убьёт его с двух выстрелов.
