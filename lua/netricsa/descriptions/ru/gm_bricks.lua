КИРПИЧИ, ПУСТАЯ КОМНАТА


Сэм, нас занесло в место, которое сложно назвать локацией.

Это скорее ошибка в матрице, геометрический парадокс или просто чья-то очень странная шутка.

Представь себе одну-единственную комнату из красного кирпича. Без крыши, без окон, без дверей. Стены уходят вверх, но неба над ними нет - просто серая бесконечность.

Вокруг комнаты - пустота. Не тьма, а именно пустота. Ни звуков, ни смысла.

Что здесь есть:

Кирпичные стены - единственный объект в этой вселенной. Красные, грубые, настоящие. Можно пощупать, постучать, даже попробовать разбить. Но зачем? За ними ничего нет.

Открытый потолок - точнее, его отсутствие. Стены обрываются, и дальше - бесконечная тьма. Без источника, без направления. Просто есть.

Пол - обычный, кирпичный. Стоять можно.

Пустота за стенами - если каким-то чудом выбраться наружу (а ты умеешь, Сэм), там ничего нет. Вообще. Только ты и бесконечное ничто.

А что внизу?

А вот здесь самое интересное.

Если прыгнуть вниз (а ты обязательно это сделаешь, я тебя знаю), начинается... бесконечность.

Ты падаешь, падаешь, падаешь. Стены комнаты давно исчезли из виду. Вокруг только чёрный свет. Через минуту, час, день - ничего не меняется.

Просто падение. В никуда. Без остановки. Без дна. Без надежды когда-нибудь коснуться твёрдой поверхности.

Здесь нельзя укрыться, нельзя занять выгодную позицию, нельзя даже спрятаться. Ты либо в комнате, либо в бесконечном падении.

Если привести сюда врагов, битва будет короткой. Либо кто-то упадёт в бездну, либо все останутся в комнате и будут стрелять друг в друга, пока не кончатся патроны. А потом просто стоять и смотреть на кирпичи.

Психологический аспект - вот где настоящее оружие. Это место сводит с ума быстрее, чем любой хедкраб. Тишина, пустота, бесконечность за пределами комнаты. Через полчаса здесь начнёшь разговаривать с кирпичами. А через час - кирпичи начнут отвечать.

Ну главное не прыгай. Серьёзно, Сэм. Я знаю, что тебе интересно, я знаю, что хочется проверить, есть ли дно. Но его нет. Я просчитала траекторию, скорость падения, возможные варианты. Дна нет. Ты будешь падать вечно. А мне придётся вечно слушать твои крики "Ну когда уже долетим?".

Мы оба этого не переживём.

Погнали... хотя куда нам идти? Здесь только одна комната.

Ладно, просто постоим, посмотрим на кирпичи. Красивые, кстати. Красные. Кирпичные. Всё, что у нас есть в этом мире....
