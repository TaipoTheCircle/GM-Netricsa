РПГ


ОРУЖИЕ:      РПГ (реактивный гранатомет)
ПАТРОНЫ:                  Высоко взрывчатые ракеты
ТЕМП ПОТРЯСЕНИЯ:        ~4 ракеты/мин

ОПИСАНИЕ:

Тяжелая противотанковая система вооружения с лазерным наведением. Обладает чрезвычайной разрушительной силой, способна уничтожать транспортные средства, башни и крупных вражеских организмов.

ПРИМЕЧАНИЯ:

- Огромный радиус взрыва, смертельный для групп.
- Лазерное наведение обеспечивает точность попадания.
- Опасно стрелять в закрытых помещениях.