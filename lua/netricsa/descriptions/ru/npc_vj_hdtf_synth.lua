КЛАСС:        Солдат-Синтент Альянса 
МЕТАБОЛИЗМ:   Синтетический / биомеханический организм
ВОСПРИЯТИЕ:   Ранняя элита вооружённых сил Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       AR2, SPAS-12, OICW
НАГРАДА:      2000
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Солдат-Синтент Альянса - это одна из забытых ранних боевых юнитов, разрабатывавшегося для войск Альянс во время Семичасовой Войны. Он представляет собой высокоадаптированный биомеханический организм, совмещающий органическое тело и продвинутую синтетическую конструкцию, призванный заменить обычных солдат своим сочетанием мобильности, прочности и огневой мощи.

Их тактика сочетала высокую манёвренность, внезапные атаки и умение выдерживать попадания, которые остановили бы обычного солдата.

СОВЕТЫ:

- Выдерживает множество попаданий благодаря биомеханической броне; один-два выстрела не остановят его.
- На открытом пространстве лучше обойти с фланга и попытаться изолировать.