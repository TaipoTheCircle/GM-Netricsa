МЕТАБОЛИЗМ:   Человек, Мужчина
ВОСПРИЯТИЕ:   Купец, авантюрист, агент Ментала
РАЗМЕР:       ~1,6 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Средняя 
