НАГРАДА:      2200
УГРОЗА:       Средняя

ОПИСАНИЕ:

Колыван - знатный и богатый купец из времён Киевской Руси, человек, который умудрился проиграть буквально всё, включая самого себя. В очередной азартной авантюре он поставил на кон собственную свободу - и, разумеется, проиграл. Его оппонентом оказался Ментал. Так Колыван стал частью Орды Ментала, первым купцом в истории, продавшим душу без посредников.

Сохранив внешний облик - рост, полноту, зелёные  одежды с длинными рукавами, красные сапоги и колпак - Колыван внутренне изменился мало. Жадность, хитрость и трусость остались при нём, но теперь подкреплены инопланетной силой. В награду за службу Ментал наделил его ракетницами и способностью призывать двух Сирианских быков-оборотней. Активация способности происходит строго по голосовой команде, произносимой с особым пафосом:

"А теперь Горбатый!"

Ментал пообещал Колывану щедрую награду:  за помощь в уничтожении Сэма Стоуна, Ментал поможет в расправе над другими богатырями. Эта перспектива греет купеческое сердце Колывана сильнее золота, ведь личные счёты он ценит почти так же высоко, как прибыль.

