БЛОТ-ПАУК С БРОДСАЙД-БИЧ


КЛАСС:        Блот-Паук
МЕТАБОЛИЗМ:   Старлинг
ВОСПРИЯТИЕ:   Хищник-преследователь
РАЗМЕР:       6 м.
ВЫНОСЛИВОСТЬ: Бесконечная
ВРАЖДЕБНОСТЬ: Абсолютная (предположительно)
ОРУЖИЕ:       Зубы
НАГРАДА:      Жизнь Или Смерть
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Блот когда-то был первым и самым любимым персонажем Рекса Бродсайда, маленьким кроликом в кепке и комбинезоне, который оживал на экране и воровал сердца зрителей. Но Рекс по неопытности подписал контракт, отдав права на своего героя, и Блот исчез из его жизни. Спустя годы, после смерти самого Рекса, компания Broadside Animation выкупила права обратно, но не для того, чтобы снимать мультфильмы.

В секретной лаборатории Блот стал основой для жуткого эксперимента по созданию Старлингов - живых костюмов, в которые пытались поместить человеческое сознание, чтобы воскресить Рекса. Первая попытка вышла чудовищной. Грубая, несовершенная оболочка исказила образ милого кролика до неузнаваемости. Внутри неё до сих пор видны останки человека.

В процессе дальнейших трансформаций, которые Старлинг претерпел внутри искажённого мира, возникла ещё более чудовищная форма - Блот-Паук. Его тело обзавелось длинными, ломкими паучьими ногами, превратив его из охотника, прячущегося по углам, в стремительного хищника, который должен был нагонять ужас на свои жертвы.

В отличие от других форм, Блот-Паук не прячется и не маскируется. Он преследует. Быстро, неумолимо, заставляя свою жертву бежать без оглядки.

СОВЕТЫ:

- Эта форма Блота не просто бродит - она охотится. Не стой на месте.
- Его паучьи ноги позволяют ему быстро передвигаться. Ищи укрытия, где он не сможет до тебя добраться.
- В отличие от обычного Блота, который просто выпрыгивает из бочек, эта форма, вероятно, не имеет слабостей. Только бегство.
