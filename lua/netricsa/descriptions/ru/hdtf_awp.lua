АРКТИЧЕСКАЯ ВОЙНА L96A1


ОРУЖИЕ:  Арктическая война / L96A1
ПАТРОНЫ: 7.62×51 mm NATO (.308 Винчестера)
CC:      15 выстрелов в минуту
ВЕС:     7.5 кг.
ДЛИНА:   200 мм

ОПИСАНИЕ:

L96A1 (Арктическая война) - снайперская винтовка болтового типа британской разработки Точность Интернешнл. Спроектирована для высокой точности в холодных и суровых климатических условиях: жёсткая ложа, холоднокованый ствол и модульная конструкция обеспечивают стабильную кучность и надёжность. Типично оснащается регулируемым прикладом, сошками и оптическим прицелом, что делает её универсальной для антиматериального и противопехотного применения на больших дистанциях.

ПРИМЕЧАНИЯ:

- Длительные точные выстрелы на средних и больших дистанциях (эффективно ~800-1 000 м. и более в зависимости от патрона и условий).
- Надёжный отточенный механизм затвора и сменные стволы/принадлежности упрощают обслуживание и адаптацию под разные задачи.
- Часто используется полицейскими снайперскими подразделениями и армейскими частями по всему миру; в некоторых армиях принималась на вооружение под обозначениями национального образца.