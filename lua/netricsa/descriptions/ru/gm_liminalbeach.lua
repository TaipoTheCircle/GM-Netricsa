ПОГРАНИЧНЫЙ ПЛЯЖ, ГДЕ ВСЁ НЕ ТАК


Сэм, я проанализировала это место и... у меня нет слов.

А если и есть, то они теряются где-то между "WTF!?" и "почему здесь так не по себе".

Представь пляж, который никогда не видел солнца, бассейн, в котором никто никогда не купался, и небоскрёб, который построили просто потому, что могли.

Добавь сюда куклу на высоте, которая явно за тобой наблюдает, и получишь Пограничный пляж.

Небо здесь - отдельный персонаж. Тяжёлые, густые тучи нависают так низко, что кажется, до них можно допрыгнуть с вышки.

Что здесь есть:

Огромный бассейн - главный нонсенс. Зачем бассейн на пляже? Это как принести термос с чаем в сауну. Но он есть. Вода в нём чёрная, неподвижная, как зеркало, в которое страшно смотреть. Рядом - деревянные столики со стульями.

Будто кто-то собирался устроить пикник на краю реальности, но передумал в последний момент.

Обелиск-небоскрёб - монолит, уходящий прямо в тучи. Он здесь явно главный. Огромный, тёмный, подавляющий. Смотришь на него - и чувствуешь себя муравьём перед ботинком. Он не просто стоит, он... давит. Своим присутствием.

Кукла на высоте - а вот это, Сэм, самое странное. Где-то на верху, на каком-то выступе, сидит маленькая кукла. Не шевелится, не убегает, просто сидит и смотрит вниз. На нас. Или сквозь нас. Я бы сказала, что это чья-то глупая шутка, но в таком месте шуток не бывает.

У меня нет чётких данных. Мои алгоритмы не могут обработать это место полностью. Здесь нарушена логика, нарушена геометрия, нарушен сам смысл существования этого пляжа. Это лиминальное пространство в чистом виде - место, которое должно быть знакомым, но выглядит чужим. Ты должен здесь расслабляться, но хочешь бежать без оглядки.

Знаешь, Сэм... давай просто быстро пройдём через этот пляж, свалим куда-нибудь ещё и сделаем вид, что нас здесь не было. Погнали. И, ради всего святого, не оглядывайся.
