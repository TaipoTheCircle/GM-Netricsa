ОПИСАНИЕ:

Это Охранник в серой униформе, которую Садистское Шоу HALF-QUAKE выдаёт тем, кто согласился работать на них за еду и обещание не трогать их семьи. Униформа серая, невзрачная, с логотипом ">)", которую не видно из-за крови на спине. На самих охранниках - потрёпанные бронежилеты, шлемы и многодневная щетина. И кровь. Много крови. Своей и чужой.

Каждый охранник в конце концов должен пройти садистские испытания. Это условие контракта, которое они подписали, не читая финальную страницу. Садистам нравится над ними издеваться. Такова их натура. Охранников бьют, их травят, их заставляют бегать по лабиринтам смерти, а потом, если они выживают, их пускают на арену уже в качестве "обслуживающего персонала" для настоящих жертв шоу. Те, кто не выживает (Почти всегда), становятся частью декораций.

Это твой временный союзник поневоле. Он ненавидит Садистов так же сильно, как и ты, но он слишком сломлен, чтобы бунтовать. В лучшем случае он может отвернуться в нужный момент. В худшем - он просто сядет у стены и будет смотреть в одну точку, пока его не пристрелят за бездействие.

Некоторые охранники вооружены. Но даже с оружием они не бойцы. Они жертвы, которые ещё не поняли, что уже мертвы.

СОВЕТЫ:

- Не убивай их. Это единственные, кто может помочь тебе советом или патронами.
- Если охранник говорит "Беги", значит беги. Он уже многое повидал.
- Охранники знают расположение камер, тайников и выходов. Следуй за ними, если они бредут куда-то целенаправленно.
- Не пытайся их спасать. Ты не сможешь. Только умрёшь вместе с ними. А они умрут в любом случае.
