ОФФИСЫ ЧЁРНОЙ МЕЗЫ, МЕНТАЛ СНОВА ВЕРНУЛСЯ


Мы всё ещё в офисах, но обстановка начала напоминать универсам в час пик, только вместо покупателей - вооружённые до зубов ребята с двух сторон. С одной стороны - H.E.C.U., которые уже успели обосноваться наверху и смотреть на лестницу как на свою личную собственность. С другой - свежее пополнение меню: силы Ментала. Да, они здесь. Наконец-то.

Их встреча с морпехами пока не переросла в полномасштабную войну, но напряжение висит в воздухе плотнее, чем запах перегоревшей проводки. Ментал, судя по всему, тоже хочет попасть наверх. Или просто решил перекусить. Потому что мы сейчас находимся ровно между морозильником и кафетерием.

Морозильник.

Холодно. Стойки с замороженными продуктами, которые теперь никто не разморозит. Идеальное место для засады, если ты - Ментал. Идеальное место для засады, если ты - H.E.C.U. Идеальное место, чтобы получить пулю в спину, если ты - Сэм. Тяжелые двери, узкие проходы, эхо шагов. Если здесь начнётся стрельба, выживет только тот, кто первым успеет выбежать. Или тот, кто не заходил вообще.

Кафетерий.

Типичная столовая. Но без столов, стойка раздачи, за которой когда-то стояли люди в белых колпаках. Теперь там, вероятно, сидит орк-разрушитель Ментала с плазмой наперевес и ждёт, когда кто-нибудь сунет нос. В кафетерии есть второй выход. И этот выход ведёт к лестнице. Лестнице, которая уходит наверх. Туда, где морпехи.

Не лезь первым. Найди укрытие на границе между морозильником и кафетерием. Наблюдай. Когда силы Ментала пойдут в атаку на лестницу (а они пойдут, у них приказы), морпехи ответят. В этот момент - проскальзывай. Не геройствуй, не пытайся перестрелять всех. Просто добраться до лестницы и наверх.

Там будет следующий этап этого бесконечного лифта в ад.


