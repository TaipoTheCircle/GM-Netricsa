СНАЙПЕРСКАЯ ВИНТОВКА REMINGTON 700


ОРУЖИЕ:     Снайперская винтовка Remington 700
ПАТРОНЫ:    Различные калибры: .308 Win (7,62×51 мм), .30-06, .223 Rem, .300 Win Mag, .338 Lapua и др.
СС:         Одиночные выстрелы
ВЕС:        4,5 кг
ДЛИНА:      1300 мм

ОПИСАНИЕ:

Remington Model 700 - одна из самых распространённых в мире винтовок с продольно-скользящим затвором, выпускаемая с 1962 года. Известная своей точностью и надёжностью, эта платформа используется охотниками, спортсменами-стрелками, полицейскими стрелками и военными подразделениями (в модифицированном виде). Конструкция винтовки отличается высокой модульностью: легко менять приклады, стволы, устанавливать сошки, оптику и модернизировать спусковые механизмы, что делает 700-ю винтовку популярной основой для специализированных снайперских / борьбоносных сборок.

ПРИМЕЧАНИЯ:

- Заводские и соревновательные модели демонстрируют кучность попаданий; при правильном беддинге и настройке 700-я винтовка является точной платформой.
- Проблемы: судебные разбирательства и споры по поводу заводских спусковых механизмов (некоторые ранние партии сообщали о небезопасных / слишком чувствительных спусковых механизмах); многие пользователи устанавливают спусковые механизмы стороннего производителя для стабильной работы.