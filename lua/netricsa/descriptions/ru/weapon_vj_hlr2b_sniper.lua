СНАЙПЕРСКАЯ ВИНТОВКА OVERWATCH MK1


ОРУЖИЕ:      Снайперская винтовка Overwatch MK1
ПАТРОНЫ:                  Высокоскоростные снайперские патроны Альянса
ТЕМП ПОТРЯСЕНИЯ:        Полуавтоматический, ~30 выстрелов/мин

ОПИСАНИЕ:

Прототип снайперской винтовки, разработанный для элитных снайперов Альянса на ранних этапах оккупации. Оснащенная высокоточным прицелом, она стреляет сверхскоростными патронами, способными мгновенно нейтрализовать легкобронированные или незащищенные цели. В отличие от более поздней MK2, эта версия имела более экспериментальную конструкцию.

ПРИМЕЧАНИЯ:

- Чрезвычайно смертоносна на больших расстояниях; часто смертельна с одного выстрела.
- Полуавтоматический огонь позволяет быстрее производить последующие выстрелы, чем MK2.
- Редкая и ограниченная элитными снайперскими подразделениями Combine.
- Отражает переход к окончательной версии снайперской винтовки Overwatch MK2.