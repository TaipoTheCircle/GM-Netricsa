ПЕРЕМЕЩАЮЩАЯ ПУШКА XV11382


ОРУЖИЕ:      Перемещающая пушка (проект XV11382)
ПАТРОНЫ:                  Ячейки размерной энергии
CC:                ~1 выстрел/3 сек

ОПИСАНИЕ:

Экспериментальное телепортационное оружие, разработанное Чёрной Мезой до инцидента на объекте. Выпускает сконцентрированные энергетические заряды, которые могут перемещать живые и неживые цели на короткие расстояния. Альтернативный режим стрельбы телепортирует пользователя в случайное место, позволяя тактически переместиться или сбежать. Считается очень нестабильным, но чрезвычайно универсальным.

ПРИМЕЧАНИЯ:

- Основной режим стрельбы: телепортирует цели при попадании, нанося большой урон.
- Альтернативный огонь: телепортирует пользователя в случайную точку на карте.
- Чрезвычайно мощное, но рискованное; неправильное использование может нанести вред пользователю.
- Ограниченная доступность; проект остался экспериментальным.
- Использует мини-урановый реактор в качестве источника энергии, аналогично Tau Cannon или Gluon Gun.