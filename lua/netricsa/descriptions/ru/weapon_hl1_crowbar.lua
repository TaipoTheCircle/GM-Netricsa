ЛОМ


ОРУЖИЕ:                 Лом
ПАТРОНЫ:                  Нет (рукопашный бой)
ТЕМП ПАЛЬБЫ:       ~75 ударов/мин

ОПИСАНИЕ:

Культовый инструмент для рукопашного боя и оружие на случай чрезвычайных ситуаций. Простой стальной лом, используемый как для боя, так и в быту. Всегда надежен, никогда не заканчиваются «боеприпасы».

ПРИМЕЧАНИЯ:

- Бесконечная прочность, всегда доступен.
- Эффективен против ящиков, замков и слабых врагов.