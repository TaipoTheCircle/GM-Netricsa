ОПИСАНИЕ:

Уникальная планета, где всё живое достигло колоссальных размеров. Гигантские болота с гнилой водой, леса с деревьями, взмывающими к небу и скрывающими кронами свет, трава высотой с многоэтажный дом, огромные грибы и насекомые - здесь каждый элемент ландшафта подавляет своими масштабами.

Предположительно, планета является не до конца сформировавшимся газовым гигантом, однако естественное терраформирование сделало её пригодной для жизни. Климат приближен к земному, но плотность воздуха ниже, что позволяет гигантским формам жизни существовать, не будучи раздавленными собственным весом.

Низинные болота Магнора - самое негостеприимное место планеты. Вода здесь отдаёт гнилью, в трясине регулярно увязают животные. Редкие патрули прочёсывают эти места в поисках заблудившихся путников, рискуя самим стать добычей гигантских хищников, скрывающихся в тёмной воде.

В противоположность болотам, леса Магнора полны жизни. Местная раса Зикси, ростом значительно уступающая гигантской флоре, научилась использовать её в своих целях. Их главный город Ветвеция расположен высоко над землёй, практически у самых крон деревьев. Жители охотятся на живность, обитающую в ветвях, и собирают дождевую воду. Благодаря такому образу жизни они избегают опасностей, таящихся внизу, и наслаждаются мягким тёплым климатом.

Однако самая загадочная область Магнора - так называемая Гигантская мультяшная местность. Здесь среди лесов и полян разбросаны предметы явно рукотворного происхождения, но чудовищных размеров: столы, стулья, ножи, бутылки, гаечные ключи, банки из-под содовой и даже автомобильные шины. Кто и зачем создал эти артефакты, остаётся загадкой, порождающей множество вопросов и не дающей на них ответов.

КЛЮЧЕВЫЕ МЕСТА:

- Низинные болота: Огромная заболоченная территория с гниющей водой и разлагающимися тушами животных. Чрезвычайно опасная зона, патрулируемая для поиска выживших.
- Заброшенная база: Покинутое поселение в глубине болот. Причина заброшенности - удушающая вонь, сырость и постоянная угроза нападения гигантских существ.
- Город Ветвеция: Поселение Зикси, построенное высоко на ветвях гигантских деревьев. Основной способ защиты от наземных опасностей и центр цивилизации планеты.
- Гигантская мультяшная местность: Аномальная зона, усеянная предметами быта огромных размеров непонятного происхождения.
