ОПИСАНИЕ:

Коты - приручаемые нейтральные существа Верхнего мира Minecraft, чаще всего встречающиеся в джунглях, деревнях и хижинах ведьм. После приручения они становятся верными спутниками и следуют, периодически садясь, ложась или сворачиваясь клубком рядом.

Коты обладают врождённым инстинктом отпугивания: криперы и фантомы избегают котов, что делает их не только декоративными, но и стратегически полезными. Существует множество расцветок котов - от чёрных и рыжих до пятнистых и сиамских.

Приручённый кот может приносить дары по утрам - перья, гниль, нитки и другие мелкие предметы, оставляя их рядом со спящим хозяином.

СОВЕТЫ:

- Для приручения используй сырую треску или лосося.
- Держи котов рядом с базой для защиты от фантомов и криперов.
- Разные расцветки котов можно коллекционировать и разводить.