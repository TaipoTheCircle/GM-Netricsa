ОПИСАНИЕ:

Буззеры являются мелкими летающими организмами Роя, приспособленными для преследования и изматывания более крупных противников. По отдельности они редко представляют серьёзную угрозу, однако их появление обычно означает начало новых проблем.

Основным методом охоты служат стремительные атаки сверху. Используя высокую манёвренность, Буззер резко пикирует на цель, нанося удар ядовитым жалом, после чего вновь набирает высоту. Небольшие размеры делают его трудной мишенью даже для опытных стрелков.

Яд Буззера не отличается высокой смертельностью, однако способен вызывать боль, дезориентацию и снижение концентрации. Именно поэтому данные существа особенно опасны во время столкновений с другими представителями Роя. Пока жертва отвлекается на летающего вредителя, более крупные хищники получают возможность приблизиться вплотную.

Наблюдения показывают, что Буззеры редко проявляют осторожность и нередко атакуют цели, значительно превосходящие их по размерам. По всей видимости, эволюция компенсировала недостаток инстинкта самосохранения способностью быстро размножаться.

СОВЕТЫ:

- Не игнорируй Буззеров даже во время боя с более опасными противниками.
- Используй оружие с высокой скорострельностью или автоматическим наведением.
- Следи за пространством над головой.
- Если вокруг слышится раздражающее жужжание, значит кто-то уже решил испортить тебе день.
