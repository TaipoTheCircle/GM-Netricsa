НАГРАДА:      700
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Ракетный солдат Overwatch - это тяжёлая огневая единица, предназначенная для уничтожения техники, укреплений и особо опасных целей. Внешне он легко путается с солдатом с дробовиком: схожая броня, массивный силуэт и штурмовая посадка. Эта ошибка часто становится фатальной.

Основное оружие - ракетная установка Альянса, которую солдат носит постоянно и применяет без колебаний. В отличие от противотанковых бойцов, он не ждёт идеальной цели: ракеты используются агрессивно, для зачистки укрытий, проломов стен и подавления скоплений противника.

Часто действует в паре с обычными солдатами, которые защищают его во время подготовки выстрела.

Хотя броня у него не максимальная, один удачный запуск способен мгновенно переломить ход боя.

СОВЕТЫ:

- Не путай его с солдатом с дробовиком Альянса.
- В замкнутых пространствах ракетный солдат особенно опасен.
- Используй укрытия, которые выдерживают взрыв, а не просто скрывают обзор.