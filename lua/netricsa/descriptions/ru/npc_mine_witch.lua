ВЕДЬМА С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Ведьма Майнкрафта
МЕТАБОЛИЗМ:   Теплокровный гуманоид
ВОСПРИЯТИЕ:   Повышенное, с тактическим самосохранением
РАЗМЕР:       ~1,9 м.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Зелья (вред, замедление, отравление, слабость)
НАГРАДА:      400
УГРОЗА:       Высокая

ОПИСАНИЕ:

Ведьма - изолированный представитель деревенской цивилизации, выбравший путь зелий, алхимии и откровенного пренебрежения к общественному мнению. Внешне она напоминает жителя, но отличия заметны сразу: Нос с бородавкой, мрачная одежда и суровый взгляд.

Ведьма использует зелья не только для атаки, но и для защиты. При угрозе она мгновенно пьёт зелья огнестойкости, сопротивления, регенерации и водного дыхания, превращаясь в крайне живучего противника.

Обитает в хижинах на болотах или появляется во время рейдов, где действует как мобильный алхимический артиллерист, осыпая противников бутылками с крайне недружелюбным содержимым.

СОВЕТЫ:

- Постоянно анализирует угрозу и подбирает нужное зелье.
- Предпочитает изматывать противника, а не убивать быстро.
- Никогда не паникует. Никогда.
- Молоко снимает эффекты, но не решает проблему с ведьмой целиком.