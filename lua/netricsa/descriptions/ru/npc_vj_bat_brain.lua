БЭТМОЗГ С МОБИУСА


КЛАСС:        Бэтмозг
МЕТАБОЛИЗМ:   Никель-кадмиевый
ВОСПРИЯТИЕ:   Дрон-засадник
РАЗМЕР:       1,2 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Корпус, таран
НАГРАДА:      300
УГРОЗА:       Низкая

ОПИСАНИЕ:

Когда доктор Эггман понял, что содержание пещер и подземных уровней обходится дороже наземных, был объявлен тендер на создание максимально дешёвой охранной системы. Победил проект, основанный на старых архивах: чертежи летучей мыши, двигатель от игрушечного самолётика и процессор от калькулятора "Электроника МК-52".

Бэтмозг - единственный робот в империи Эггмана, который не бегает за целью, а просто ждёт, пока цель придёт к нему сама. В режиме ожидания висит вниз головой, сливаясь с потолком. При появлении живого объекта срывается с места, издавая дребезжащий звук, похожий на трель сломанного дверного звонка. Атакует строго сверху, целясь в макушку. Промахнувшись, некоторое время беспомощно хлопает крыльями, пытаясь набрать высоту, и цепляется за первый попавшийся выступ, делая вид, что ничего не произошло.

СОВЕТЫ:

- Опасен только в связке с пропастями и узкими мостиками.
- После атаки у него есть пауза в полторы секунды. Этого хватает, чтобы поправить причёску и пойти дальше.
