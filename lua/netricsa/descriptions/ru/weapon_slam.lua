ВЛУБ АЛЬЯНСА


ОРУЖИЕ:      ВЛУБ Альянса (Выбираемые Лёгкие Ударные Боеприпасы Альянса)
ПАТРОНЫ:                  Взрывчатые заряды с дистанционным взрывом
CC:        Ручное размещение, дистанционный взрыв

ОПИСАНИЕ:

Тактическое взрывное устройство, которое можно прикрепить к поверхностям. Может быть взорвано дистанционно, что позволяет устраивать засады, ловушки или очищать укрепленные позиции. Используется как силами Сопротивления, так и силами Альянса.

ПРИМЕЧАНИЯ:

- Позволяет стратегически размещать для блокирования территории.
- Дистанционное взрывание обеспечивает точное время взрыва.
- Может прикрепляться к стенам, полам или транспортным средствам.