ОТЕЦ ГРИГОРИЙ


КЛАСС:        Отец Григорий
МЕТАБОЛИЗМ:   Человек, Мужской (устойчив к яду хедкраба)
ВОСПРИЯТИЕ:   Психотический священник / Выживший в Рейвенхольме
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Дробовик Аннабель
НАГРАДА:      Безопасный проход через Рейвенхольм + огневая поддержка союзников
УГРОЗА:       Умеренная

ОПИСАНИЕ:

Отец Григорий - последний оставшийся житель Рейвенхольма, некогда шахтёрского городка, превратившегося в некрополь после обстрела капсулами с хедкрабами Альянсом. Человек веры, извращённый одиночеством и бесконечной войной с нежитью, он стал одновременно защитником и палачом разрушенного поселения.

Вооруженный дробовиком "Аннабель", Григорий охотится на зомби с почти радостным рвением, смеясь, сжигая, ловя и разрывая их на части. Несмотря на кажущееся безумие, он проявляет искреннюю доброту к Гордону Фримену, помогая ему пережить ужасы Рейвенхольма.

В отличие от других, Григорий таинственным образом невосприимчив к зомбификации. Яд хедкраба воздействует на его тело, но не трансформирует его. В его крови присутствуют следы инфекции, что говорит о том, что его сопротивление временно, а порча внутри него со временем усиливается. До этого момента он принимает свою судьбу вечного пастыря Рейвенхольма, сражаясь с бесконечным потоком проклятых.

СОВЕТЫ:

- Он союзник и не нападет, если его не спровоцировать.
- Его дробовик невероятно мощный - держитесь на расстоянии, если вы враждебны.
- Остерегайтесь его ловушек, расставленных по всему Рейвенхольму.
- Его выживание намекает на трагическую неизбежность - его иммунитет может не длиться вечно.

