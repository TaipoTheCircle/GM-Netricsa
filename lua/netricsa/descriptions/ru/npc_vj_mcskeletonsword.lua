СКЕЛЕТ-МЕЧНИК С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Скелет-Мечник
ВОСПРИЯТИЕ:   Нежить, воин ближнего боя
МЕТАБОЛИЗМ:   Магически поддерживаемая нежить
РАЗМЕР:       ~1,9 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Каменный Меч
НАГРАДА:      350
УГРОЗА:       Средне-высокая

ОПИСАНИЕ:

Скелет-Мечник - это скелет, который решил, что лук для слабаков и трусов, а настоящая нежить должна смотреть жертве в глаза... даже если глаз у неё давно нет. Вооружён мечом и сомнительным боевым опытом из загробной жизни, он без колебаний идёт в лобовую атаку.

Иногда создаётся впечатление, что Скелет-Мечник искренне обижен на живых - возможно, за то, что у них всё ещё есть кожа, нервы и возможность чувствовать боль.

СОВЕТЫ:

- Не подпускай слишком близко - он опаснее, чем кажется.
