ТЕНТАКЛ С ЗЕНА


КЛАСС:         Тентакл
МЕТАБОЛИЗМ:    плотоядная ксенофауна
ВОСПРИЯТИЕ:    вибрация + звук
РАЗМЕР:        30-40 футов (выходит из ямы)
ВЫНОСЛИВОСТЬ:  чрезвычайно высокая
ВРАЖДЕБНОСТЬ:  крайняя
ОРУЖИЕ:        звуковое обнаружение, разрушительные удары
НАГРАДА:       1200
УГРОЗА:        катастрофическая (в ограниченных пространствах)

ОПИСАНИЕ:

Тентакл - это огромный подземный хищник из Зена, встречающийся в зонах резонансного каскада, где он вылезает из глубоких ям. Будучи слепым, он полагается исключительно на звук и вибрацию, чтобы обнаружить добычу. Любой шум мгновенно привлекает его внимание, после чего он наносит сокрушительные удары своими хлыстообразными отростками.

Его бронированная кожа делает его практически неуязвимым для обычного оружия. Жертвы часто оказываются раздавлены в лепешку, даже не успев понять, что их обнаружили. Единственный верный способ уничтожить его - разрушить его ядро, спрятанное глубоко под землей.

СОВЕТЫ:

- Будьте тихими - слишком громкая ходьба, стрельба или даже перезарядка оружия приведут к вашей гибели.
- Взрывчатые вещества и энергетическое оружие неэффективны против его брони.
- Терпение и скрытность - ключ к выживанию.