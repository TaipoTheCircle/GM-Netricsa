ВИНДИКАТОР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Виндикатор
МЕТАБОЛИЗМ:   Зловредный житель, разбойник
ВОСПРИЯТИЕ:   Злобный боец ближнего боя, тяжелая пехота Илладжеров
РАЗМЕР:       1,9 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Железный топор
НАГРАДА:      200
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Это психопат в сером, который доказывает, что жители бывают не только полезными, но и смертельно опасными. Это "злые жители", или, как их официально называют, Илладжеры. Если обычный житель торгует с тобой и строит фермы, то виндикатор просто хочет разрубить тебя пополам топором. И ему плевать, что ты пришёл в гости.

Эти мрачные личности впервые появились как стражи Лесных особняков, где они до сих пор обитают в некоторых комнатах. Позже их стали замечать в разбойничьих патрулях и, что особенно неприятно, в рейдах на деревни.

Одет виндикатор в тёмную куртку, штаны и сапоги. Он имеет серьёзное и озлобленное выражение лица, напоминающее о его родстве с жителями. В бездействии он стоит, скрестив руки на груди, в точности как его миролюбивый кузен. Но как только он замечает врага (тебя или жителя), он выхватывает свой железный топор и срывается с места.

Главная фишка виндикатора - невероятная агрессия и скорость. Он бежит на цель быстрее скорости шага игрока, нанося сокрушительные удары топором. Этот топор не просто опасен - он пробивает щит, отключая его на 5 секунд. 

В отличие от своих родственников-изгоев, виндикаторы могут ломать деревянные двери во время рейдов на обычной и высокой сложности (как зомби, только быстрее).

Советы по выживанию:

- Если ты без брони, не приближайся. Один удар топором может быть фатальным.
- Используй лук или арбалет. Держать дистанцию против виндикатора - лучшая стратегия.
- Не полагайся на щит: его атака пробьёт его, и ты получишь урон.
- В Лесном особняке осматривай углы. Виндикаторы часто поджидают в засаде в небольших комнатах.
- Если ты увидел виндикатора по кличке Джонни - немедленно беги в противоположную сторону. Он уничтожит не только тебя, но и всех остальных мобов на своём пути.
