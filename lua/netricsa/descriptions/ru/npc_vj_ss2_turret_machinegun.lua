ПУЛЕМЁТНАЯ ТУРЕЛЬ MH-110


КЛАСС:        Пулемётная турель MH-110
МЕТАБОЛИЗМ:   Механический
ВОСПРИЯТИЕ:   Стационарная
РАЗМЕР:       ~1,5 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Зависит от стрелка
ОРУЖИЕ:       Спаренные пулемёты калибра 110 мм
НАГРАДА:      300
УГРОЗА:       Средняя

ОПИСАНИЕ:

Когда Ментал проектировал свои базы, он исходил из простого принципа: "Всё, что движется, должно быть либо рабом, либо трупом". Для тех, кто отказывался становиться ни тем, ни другим, он разработал стационарные пулемётные турели - спаренные стволы, способные перемалывать мясо быстрее, чем противник осознает это.

Большинство согласилось за первый вариант.

Имеет два пулемёта, спаренные вместе и бесконечный боезапас (ящик является источником магического создания патронов), и система наведения, которая позволяет стрелять даже тем, кто впервые видит оружие.

Скорострельность турели такова, что за пару секунд она способна превратить стаю гнааров в кровавое месиво, а циклопа - в решето.

Турели обычно установлены на базах Ментала, которые можно захватить и использовать против его хозяина.

Если за турелью сидит враг - он польёт свинцом всё, что движется. Если врага оттуда выковырять и запрыгнуть самому - можно устроить ответный фейерверк.

СОВЕТЫ:

- Если видишь турель с врагом - вали его первым. Турель без стрелка - просто железяка.
- Против быстрых тварей турель незаменима. Пули долетают мгновенно.
- Если видишь, что из-за турели торчат чьи-то орочьи ноги - значит, там кто-то есть.
