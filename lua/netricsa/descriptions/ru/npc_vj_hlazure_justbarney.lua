ОПИСАНИЕ:

Джастин Барнс - верный охранник Чёрной Мезы и один из коллег Барни Калхауна. Он вооружён стандартным оружием Чёрной Мезы и полагается на тактику отряда, а не на тяжёлую броню. Во время инцидента в Чёрной Мезе он проявил себя как храбрый союзник, пытаясь защитить персонал и противостоять как солдатам H.E.C.U., так и инопланетным силам.

Хотя его подготовка и невелика по сравнению с военными оперативниками, Барнс проявляет решимость и преданность. Отсутствие брони делает его более уязвимым, но компенсирует это упорством и мужеством в критических ситуациях.

СОВЕТЫ:

- Обеспечь прикрытие огнём - он не сможет выдержать длительный бой в одиночку.
- Держи его рядом с более сильной поддержкой.
- Избегай открытых перестрелок; его броня минимальна.
