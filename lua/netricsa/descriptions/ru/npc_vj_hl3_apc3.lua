БТР-ТРАНСПОРТЁР АЛЬЯНСА


КЛАСС:        БТР-Транспортёр Альянса
МЕТАБОЛИЗМ:   Механизированная боевая платформа
ВОСПРИЯТИЕ:   Тяжёлый бронетранспортёр Альянса
РАЗМЕР:       9 м.
ВЫНОСЛИВОСТЬ: Экстремально высокая
ВРАЖДЕБНОСТЬ: Экстремальная
ОРУЖИЕ:       Крупнокалиберный пулемёт (верхняя кабина)
НАГРАДА:      3000
УГРОЗА:       Критическая

ОПИСАНИЕ:

БТР-Транспортёр Альянса - увеличенная в полтора раза версия стандартного бронетранспортёра Альянса, переработанная для перевозки исключительно солдат Объединённой гвардии Альянса. В отличие от базовой модели, эта машина использует шестиколёсную схему: две управляемые колёсные оси спереди и четыре задних колеса, обеспечивающих максимальную устойчивость под тяжёлой нагрузкой.

Такое шасси позволяет Транспортёру сохранять мобильность даже при полной загрузке десантом и боевым снаряжением, а также двигаться по разрушенным дорогам, завалам и пересечённой городской местности без существенной потери устойчивости.

Машина оснащена двумя кабинами. Передняя кабина предназначена для водителя и полностью изолирована бронекапсулой. Верхняя кабина служит боевым постом пулемётчика и обеспечивает круговой обзор и подавляющий огонь во время движения и высадки.

СОВЕТЫ:

- Не позволяйте Транспортёру остановиться в ключевой точке.
- Если он прибыл - операция уже началась.
