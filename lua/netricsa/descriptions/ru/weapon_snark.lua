СНАРК-ГНЕЗДО, ОРУЖИЕ


ОРУЖИЕ:      Снарк-гнездо, оружие
ПАТРОНЫ:                  Инопланетные существа-снарки
СКОРОСТЬ ОГНЯ:        ~1 в секунду (скорость броска)

ОПИСАНИЕ:

Небольшие агрессивные инопланетные организмы, которые бросают как живое оружие. Снарки безжалостно атакуют ближайших врагов, пока не взорвутся.

ПРИМЕЧАНИЯ:

- Бесконечная агрессия после выпуска.
- Могут нанести вред пользователю, если нет целей.
- Лучше всего использовать для блокирования территории или отвлечения внимания.