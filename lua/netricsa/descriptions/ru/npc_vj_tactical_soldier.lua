ТАКТИЧЕСКИЙ СОЛДАТ OVERWATCH АЛЬЯНСА


КЛАСС:        Тактический солдат Overwatch Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Тяжёлое тактическое подразделение Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Импульсная винтовка AR2, SMG, гранаты
НАГРАДА:      750
УГРОЗА:       Очень высокая


ОПИСАНИЕ:

Тактический солдат Overwatch Альянса - это усиленная и доработанная версия стандартного бойца, предназначенная для затяжных боёв и прямого давления на сопротивление. Если обычный солдат - это инструмент контроля, то тактический - инструмент полного подавления.

Его отличительная черта - синий визор и шлем повышенной защиты. Конструкция шлема и внутренняя аугментация полностью нивелируют уязвимость к хедшотам: выстрелы в голову не дают мгновенного эффекта. Альянс намеренно сделал этих солдат таковыми, чтобы они могли удерживать позиции под плотным огнём.

Броня многослойная, усиленная, с внутренними демпферами и синтетическими вставками. Тактический солдат не полагается на скорость - он продвигается вперёд методично, используя AR2 для подавляющего огня, SMG в ближнем бою и гранаты для зачистки укрытий.

В бою он действует хладнокровно и без спешки. Его задача - не фланговать, а ломать линию обороны, заставляя противника отступать или погибать. Когда такой солдат выходит вперёд, бой перестаёт быть мобильным и превращается в испытание на выживание.

СОВЕТЫ:

- Хедшоты бесполезны - целься в корпус или используй взрывчатку.
- AR2 опасна даже на средней дистанции - укрытия обязательны.
- Лучший способ справиться с ним - тяжёлое оружие или координированная атака.