СВИНЬЯ С РОЗОВЫМИ НОГАМИ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Свинья с розовыми ногами
МЕТАБОЛИЗМ:   Теплокровное млекопитающее
ВОСПРИЯТИЕ:   Дружелюбное Дикое / Домашнее животное
РАЗМЕР:       ~1 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Отсутствует
НАГРАДА:      40
УГРОЗА:       Минимальная

ОПИСАНИЕ:

Один из самых стильных вариантов обычной свиньи. Свиньи разнообразны и уникальны. У этой розовые ножки.

Внешность у этой свиньи действительно примечательная. Основное тело тёмное, почти чёрное, с небольшим светло-серым пятном на левой стороне живота. Ноги при этом ярко-розовые, с разделителями в виде светло-серых участков на коленях. Уши тёмные, морда под пятачком светло-серая, а сам пятачок розовый. Выглядит так, будто свинья окунула ноги в розовую краску, пока никто не видел, и теперь гордо щеголяет своим маникюром.

По поведению ничем не отличается от обычной свиньи: бродит по миру, избегает лавы и обрывов, при получении урона убегает на несколько секунд, а затем возвращается к своим делам.

СОВЕТЫ:

- Ценность этой свиньи - в её внешности.
- Разводи таких свиней, чтобы получить потомство с таким же необычным окрасом.
- На седле такая свинья смотрится особенно эффектно - розовые ноги контрастируют с тёмным телом.
- Если встретил в дикой природе, считай, что тебе повезло.
