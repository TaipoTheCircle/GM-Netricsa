СНАЙПЕРСКАЯ ВИНТОВКА GIAT FR F2


ОРУЖИЕ:  GIAT FR F2 Sniper Rifle
ПАТРОНЫ: 7.62x51 мм NATO
СС:      ~15 выстр./мин
ВЕС:     ~5,8 кг с оптикой
ДЛИНА:   ~1 200 мм

ОПИСАНИЕ:

GIAT FR F2 - стандартная снайперская винтовка французской армии, разработанная как глубокая модернизация FR F1. Основным отличием является утолщённый "плавающий" ствол с тепловым кожухом, снижающим тепловое искажение при стрельбе, а также улучшенная эргономика ложи. Винтовка предназначена для точного поражения целей на средних и больших дистанциях и ценится за стабильность боя и надёжность в полевых условиях.

ПРИМЕЧАНИЯ:

- Высокая точность при одиночной прицельной стрельбе; идеальна для дальних дистанций.
- Болтовая схема обеспечивает отличную кучность, но ограничивает скорострельность.
- Эффективна против живой силы и слабо защищённых целей; против тяжёлой брони требует точных попаданий.