ЗОМБИ-УЧЁНЫЙ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Зомби-Учёный
МЕТАБОЛИЗМ:   Нежить
ВОСПРИЯТИЕ:   Разумная Нежить, Метатель Зелий
РАЗМЕР:       ~1,9 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Зелья слабости и урона
НАГРАДА:      300
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это редкая разновидность нежити, в которой мозг сохранил достаточно серого вещества, чтобы вспомнить старые привычки. В отличие от своих безмозглых собратьев, которые тупо прут на таран, этот экземпляр предпочитает тактику алхимика. Он не лезет в ближний бой - он забрасывает цель зельями с безопасного расстояния.

Он носит белый лабораторный халат поверх голубой рубашки, тёмные очки, аккуратный коричневый галстук и ремень. Выглядит почти как живой сотрудник, если не присматриваться к зелёной коже. Создаётся впечатление, что он умер прямо за рабочим столом, а потом просто встал и продолжил эксперименты - теперь уже над живыми.

В бою Зомби-Учёный использует два типа зелий. Зелье урона наносит прямой вред цели, а зелье слабости снижает силу атаки, делая жертву почти беззащитной перед другими мобами. В ближний бой учёный не вступает - если противник подходит слишком близко, нежить предпочитает отступить и возобновить обстрел с дальней дистанции.

Он не горит на солнце, как обычные зомби, - видимо, халат защищает от ультрафиолета не хуже, чем от кислоты.

СОВЕТЫ:

- Не подпускай учёного на дистанцию броска. Сближайся быстро и бей в ближнем бою.
- В ближнем бою он беспомощен и не сможет использовать зелья.
- Уклоняйся от зелий - они летят по дуге.
- Используй щит. Он не блокирует эффект зелья, но уменьшает прямой урон от попадания.
- Если получил слабость, отступи за укрытие и пережди эффект. Сражаться в таком состоянии - верная смерть.
- Зелья урона наносят урон и тебе, и другим мобам.
