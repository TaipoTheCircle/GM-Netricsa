БОЛЬШЕГОЛОВЫЙ ЗОМБИ КРОТИМОВЕЦ, РОМАН РИБАРИЧ


КЛАСС:        Большеголовый Зомби Кротимовец, Роман Рибарич
МЕТАБОЛИЗМ:   Гуманоидный, Мужской
ВОСПРИЯТИЕ:   Фанат-энтузиаст
РАЗМЕР:       5,9 футов
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Нейтральная
ОРУЖИЕ:       Отсутствует (кроме сложности I Am Ugh-Zan)
НАГРАДА:      100
УГРОЗА:       Раздражающий фактор

ОПИСАНИЕ:

Кротимовцы - худощавые гуманоиды с гипертрофированными черепами, содержащими мозг разработчиков Croteam. Носят униформу крутого Сэма, но с уникальными чертами лиц команды разработчиков. Проявляют навязчивый энтузиазм, преследуя тебя с возгласами "Привет!" и "Эй, вы куда!?". Абсолютно безвредны, но в сложности "Я - Угх-Зан" получают огненные и ледяные модификаторы. Спонтанно появляются в секретных зонах, блокируя проходы своим числом.

Роман Рибарич - не просто сооснователь и бессменный генеральный директор Croteam. Он - перводвигатель, сновидец и главный провокатор всего безумия, которое ты видишь на экране. Его гипертрофированный череп - источник первоначального импульса: именно ему приснилось название "Serious Sam", сменившее рабочее "In the Flesh". Он не только строил бизнес-стратегии, но и лично вдохнул жизнь в самого знаменитого врага серии, записав легендарный истошный крик Безголового Камикадзе.

Его управленческий принцип - фанатичная независимость и создание игр, в которые хочется играть самим - позволил Croteam выжить и процветать. А его скрытый гений дизайнера проявился в идее секретного уровня "Sacred Yards" в The First Encounter, который можно пройти, не убив ни одного врага - секрет, о котором игроки узнали лишь спустя 14 лет. Роман - это мозг, который сначала породил идею, а затем создал условия, чтобы эта идея могла эволюционировать в хаос, творчество и культовые игры. Без его сна и крика не было бы ничего.

Когда-то он был живым, но после того, как ты убил его в храме Хатшепсут, он восстал из мёртвых - став демоническим Зомби.

СОВЕТЫ:

- Для очистки пути используй армейский нож или пистолет.
- На сложности "I Am Ugh-Zan" уничтожай дистанционно из-за их модификаторов.
- В групповом скоплении эффективна кувалда с горизонтальным ударом.