БОЛЬШЕГОЛОВЫЙ АНГЕЛ КРОТИМОВЕЦ, МАЙКЛ ХАРРИС


КЛАСС:        Большеголовый Ангел Кротимовец, Майкл Харрис
МЕТАБОЛИЗМ:   Гуманоидный, Мужской
