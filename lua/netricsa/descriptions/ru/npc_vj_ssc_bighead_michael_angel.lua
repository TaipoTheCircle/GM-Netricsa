УГРОЗА:       Раздражающий фактор

ОПИСАНИЕ:

Кротимовцы - худощавые гуманоиды с гипертрофированными черепами, содержащими мозг разработчиков Croteam. Носят униформу крутого Сэма, но с уникальными чертами лиц команды разработчиков. Проявляют навязчивый энтузиазм, преследуя тебя с возгласами "Привет!" и "Эй, вы куда!?". Абсолютно безвредны, но в сложности "Я - Угх-Зан" получают огненные и ледяные модификаторы. Спонтанно появляются в секретных зонах, блокируя проходы своим числом.

Майкл Харрис (не путать с одноименным преступником) - один из ключевых бета-тестеров Croteam, внесший значительный вклад в отладку и полировку самых важных игр серии. Его гипертрофированный череп - прямое следствие сотен часов, проведенных в поиске мельчайших багов, проверке баланса и тестировании стабильности в The First Encounter, The Second Encounter, Serious Sam 2 и их HD-ремастерах.

В отличие от знаменитых разработчиков, имена тестировщиков часто остаются в тени, однако их роль неоценима. Именно такие специалисты, как Майкл Харрис, выступали последним рубежом между сырой сборкой и финальным продуктом, вылавливая ошибки коллизии, проблемы с производительностью и логические несостыковки, которые могли бы испортить впечатление игрокам. Его многолетняя работа в титрах отмечена как "Дополнительное тестирование и поддержка" - скромная, но жизненно важная функция, без которой ни один Serious Sam не вышел бы таким отполированным и стабильным.

Когда-то он был живым, но после того, как ты убил его в Храме Хатшепсут, он воскрес - став Ангелом.

СОВЕТЫ:

- Для очистки пути используй армейский нож или пистолет.
- На сложности "I Am Ugh-Zan" уничтожай дистанционно из-за их модификаторов.
- В групповом скоплении эффективна кувалда с горизонтальным ударом.