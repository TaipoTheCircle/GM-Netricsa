КАРМАННЫЙ НОЖ


ОРУЖИЕ:    Карманный нож

ОПИСАНИЕ:

Обычный карманный нож. Предназначен для использывания в разных ситуациях, где понадобится нож, в том числе и в драке.

ПРИМЕЧАНИЯ:

- При нажатии ЛКМ + R, позволяет сменить тип атаки.
- При режещем режиме рекомендуется целится по коненчостям для вызывания артериального кровотечение.
- При нажатии пкм по предмету примотанным скотчем, можно начать развязывать его.