РЕЙЗОРВИНГ С ТЕРРАФОРМИРОВАННОЙ ПЛАНЕТЫ


КЛАСС:        Рейзорвинг
МЕТАБОЛИЗМ:   Насекомоподобный
ВОСПРИЯТИЕ:   Хищное летающее насекомое
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Очень низкая
ВРАЖДЕБНОСТЬ: Крайне высокая
ОРУЖИЕ:       Кислотные плевки, жало, скорость
НАГРАДА:      150
УГРОЗА:       Высокая

ОПИСАНИЕ:

Рейзорвинги - это то, что получится, если скрестить стрекозу со скорпионом, добавить пару лишних лап и выпустить всё это на волю вулканического происхождения.

Обитали рейзорвинги где-то глубоко под землёй, пока вулканическое извержение не вытолкнуло их улей наружу. С тех пор эти твари регулярно доставляют проблемы всем, кто суётся в район их гнездовий.

В бою рейзорвинги полагаются на скорость и численность. Они носятся по воздуху быстрее, чем человек бежит по земле, и атакуют роем.

Основное оружие - кислотные плевки. Кислота не только жрёт плоть, но и замедляет движение.

Сами же рейзорвинги при этом дохнут от одного выстрела из любого ствола, но проблема в том, что их всегда много. Очень много.

СОВЕТЫ:

- Один выстрел - одна тварь. Не экономь патроны, но и не паникуй.
- Скорострельное оружие решает проблему роя лучше всего.
- Если рейзорвинг плюнул - уворачивайся. Кислота липнет и замедляет.
- Не дай себя окружить. В открытом поле стая собьёт тебя с ног за пару секунд.
