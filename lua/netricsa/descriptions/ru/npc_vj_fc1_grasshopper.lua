ТРОПИЧЕСКИЙ КУЗНЕЧИК С АРХИПЕЛАГА КРИГЕРА


КЛАСС:        Тропический кузнечик
МЕТАБОЛИЗМ:   Насекомое, прямокрылое
ВОСПРИЯТИЕ:   Сложные фасеточные глаза, чувствительность к вибрациям
РАЗМЕР:       ~0,01 м.
ВЫНОСЛИВОСТЬ: Крайне низкая
ВРАЖДЕБНОСТЬ: Отсутствует
ОРУЖИЕ:       Отсутствует
НАГРАДА:      1
