ОПИСАНИЕ:

Мелкое тропическое насекомое, обитающее в траве и на кустарниках архипелага. Ведёт себя схожим с мухой образом: при приближении угрозы совершает короткий шумный прыжок-полёт на несколько метров, а затем снова замирает. Не атакует и не взаимодействует с другими существами.

