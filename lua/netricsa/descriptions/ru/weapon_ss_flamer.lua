ОГНЕМЁТ XOP


ОРУЖИЕ:    Огнемёт XOP
ПАТРОНЫ:   напалм ЛЛ
СС:        непрерывный огонь
ВЕС:       9 кг. (пустой),
           14 кг. (с полным баком)

ОПИСАНИЕ:

В этом экспериментальном оружии для создания мощного огня используется легко воспламеняющийся напалм. Огнемёт очень эффективен против больших скоплений живой (и не живой) силы противника, струя пламени достаёт даже спрятавшихся. То, что они перед смертью ещё несколько секунд будут пылать, только усиливает эффект. Некоторые называют оружие такого рода "подожги и забудь".

ПРИМЕЧАНИЯ:

- Эффективен против больших скоплений живой силы противника.
