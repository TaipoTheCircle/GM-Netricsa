ПРИБОР НОЧНОГО ВИДЕНИЯ AN / PVS-7


ПРЕДМЕТ: псевдо-бинокулярный прибор ночного видения AN / PVS-7
ВЕС: 680 г

ОПИСАНИЕ:

AN / PVS-7 - это однотрубный "бинокулярный" прибор ночного видения: одна усилительная трубка формирует изображение, которое просматривается через два окуляра, обеспечивая удобство и экономию массы по сравнению с двумя отдельными трубками. Прибор был введён в эксплуатацию в 1980-х годах и долгое время оставался стандартом в вооружённых силах и у служб безопасности благодаря простоте, надёжности и возможности установки на шлем или головной обод.

ПРИМЕЧАНИЯ:

- Встроенная система автоматической регулировки яркости (ABC) и защита от яркого света (отсечка яркого света / автоматическое стробирование) предохраняют трубку от выхода из строя при внезапной осветительной нагрузке. 
- Встроенный ИК-подсвет (ИК-Осветитель) позволяет работать в полной темноте (режим заметен только при наличии ИК-датчика у противника).