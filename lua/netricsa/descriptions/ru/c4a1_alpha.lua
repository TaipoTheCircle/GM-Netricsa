УРОВЕНЬ 0 ЧЁРНОЙ МЕЗЫ???, ДОБРО ПОЖАЛОВАТЬ В ЗАКУЛЬЕ


Сэм, я не знаю, как это объяснить. Мои датчики сходят с ума. Одна секунда - мы были в реакторе, пахло озоном, железом и потом. Следующая - мы здесь. Где "здесь"? Я бы сказала "нигде", но это слишком просто. Это какое-то нелинейное пространство. Подсобка. Огромная и бесконечная, где все коридоры похожи, но ни один не повторяется.

Жёлтые обои. Влажные ковры под ногами, пропитанные чем-то, но не водой. Пить нельзя. Даже пробовать не советую. Лампы на потолке. Люминесцентные. Они мигают - хаотично, непредсказуемо - и гудят.

Визуально - всё одинаково. Но при этом нет двух одинаковых комнат. Закономерность? Закономерности нет. Пространство меняется. Идёшь по прямой - возвращаешься туда, откуда начал. Проходишь тот же маршрут - попадаешь в другой набор комнат. Компасы не работают. GPS молчит. Радиосвязь искажается, превращая голос в шипение. Даже мы с тобой... я тебя слышу, но чувствую, что сигнал гуляет. Как будто мы говорим через старый телефон в грозу.

Это главный вход в Закулисье - туда, откуда не возвращаются. Или возвращаются, но уже другими.

Сообщений о контактах с людьми внутри не поступало. Никто никогда никого не встречал. Но люди здесь были. Много. Они умирали от жажды, голода и того, что происходит с головой, когда вокруг бесконечные жёлтые стены и гудящие лампы. Сенсорная депривация, изоляция, отсутствие ориентиров. Психоз наступает быстро. А потом - тишина.

Но тел нет. Никто не находил тел. Они просто исчезают.

Я не знаю. Серьёзно. Впервые у меня нет плана.

Навигация невозможна. Можно просто идти. Куда-нибудь. Пока не выйдешь.

Выход есть. Он существует. Но как его найти - неизвестно. Иногда он появляется случайно. Иногда - если перестать искать. Иногда - если поверить, что его нет.

Запасы? У нас есть то, что есть. Воды немного. Еды почти нет. Здесь нечем торговать. Здесь ничего нет, кроме жёлтых обоев и гудения.

Психологическая устойчивость. Это главное. Не сходить с ума. Разговаривать со мной. Я буду с тобой, пока сигнал держится. Но если мы потеряем друг друга... не паникуй. Шагай. Жди. Выход есть.

Мы выберемся, Сэм. Я не знаю как, но мы выберемся. Потому что ты - человек, который выжил в каскадном резонансе, сражался с Менталом и проходил сквозь пространство, где реальность течёт как вода. Это просто жёлтые обои. Ничего нового. Поверь.
