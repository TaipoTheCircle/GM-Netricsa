ЗЕМЛЯ


ПЛАНЕТА:        Земля
ТИП:            Планета земного типа, Родина человечества
КЛИМАТ:         Разнообразный (от арктического до тропического)
СПУТНИКИ:       1 луна
ЛАНДШАФТ:       Континенты, океаны, горные хребты, пустыни
РАСПОЛОЖЕНИЕ:   Солнечная система, Рукав Ориона (Млечный Путь)
МЕСТНЫЕ РАСЫ:   Люди
ПРИНАДЛЕЖНОСТЬ: Независимое человеческое пространство; Частично оккупирована Ордой Ментала (в разные периоды)
МЕСТА:          Египет, Тунгуска, Каркассон, Помпеи, Теотиуакан, Крвавице

ОПИСАНИЕ:

Третья планета Солнечной системы. Родина человечества и центр человеческой цивилизации. Имеет один естественный спутник - Луну.

Ландшафт планеты крайне разнообразен: от ледяных шапок полюсов до экваториальных джунглей, от глубоких океанских впадин до высочайших горных пиков. Около 70% поверхности покрыто водой.

Земля издревле привлекала внимание внеземных цивилизаций. Тысячи лет назад её посетили Сириане, оказавшие влияние на развитие древних культур, особенно в Египте. Позже планета неоднократно становилась целью вторжений Орды Ментала, стремящегося уничтожить человечество.

В ходе войны Объединённые Силы Обороны использовали древние сирианские артефакты, включая Хранителя Времени, для противодействия захватчикам.

КЛЮЧЕВЫЕ МЕСТА:

- Египет: Регион, где сохранились многочисленные свидетельства пребывания Сирианцев. Здесь расположены Великие пирамиды, Карнак, Луксор, Долина Царей и подземные комплексы с сирианскими генераторами.
- Тунгуска: Место массированного вторжения сил Ментала, где во время Тунгусского наступления открылся гигантский портал Орды.
- Каркассон: Укрепленный город, где базировалась группа "Тройка-А" в ходе одной из операций против сил Ментала.
- Помпеи: Древнеримский город, в районе которого проводилась активация спутникового оружия для сдерживания войск противника.
- Теотиуакан: Древний город цивилизации Майя, где Сириане оставили один из временных порталов.
- Крвавице: Средневековый Польский город, где был обнаружен важный артефакт, именуемый "Книгой Мудрости".
