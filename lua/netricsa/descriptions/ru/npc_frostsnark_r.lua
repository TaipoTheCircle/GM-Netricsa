МОРОЗНЫЙ СНАРК С ЗЕНА


КЛАСС:        Морозный снарк
МЕТАБОЛИЗМ:   Плотоядное Насекомое (Криогенная Мутация)
ВОСПРИЯТИЕ:   Инопланетное насекомое, Живой Лёд
РАЗМЕР:       6 дюймов
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Абсурдно высокая
ОРУЖИЕ:       Ледяной Укус
НАГРАДА:      100
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это естественная мутация обычного снарка, которая возникла в ледяных районах Зена. Там, где ветры воют так, что кости стынут, а лёд не тает тысячи лет, обычные снарки не выживают. Но эволюция нашла выход: те, кто выжил, превратились в синих насекомых, чей панцирь светится изнутри холодным, ледяным светом.

В отличие от своих собратьев, которые взрываются от переизбытка энтузиазма через несколько секунд после вылупления, морозные снарки не подвержены самоуничтожению. Они могут жить долго. Очень долго. И всё это время они будут пытаться вас заморозить. Или сожрать. Или и то и другое одновременно.

Они носят полностью синий панцирь с голубоватыми прожилками, которые пульсируют, как лёд в глубинах ледника. Издали они сверкают и отражают свет, заметить их легко. Но как только ты подходишь ближе - они исчезают из виду, сливаясь со снегом и льдом.

Укус морозного снарка не просто болезнен - он замораживает цель. Одно попадание - и ты начинаешь замедляться. Два - и ты уже не можешь бежать. Три - и ты просто стоишь на месте, беспомощная статуя из плоти и льда, пока стая не добьёт тебя. Выжившие поговаривают, что эти твари специально нападают группами, чтобы наложить друг на друга эффект заморозки и парализовать жертву до того, как она успеет выстрелить.

Как и их огненные соседи, морозные снарки постоянно враждуют с морозными буллсквидами. Конфликт длится столько же, сколько и у их огненных аналогов. Никто не помнит причину. Возможно, буллсквиды наступили на чью-то любимую льдину. Возможно, снарки съели чей-то запас кристаллов. А может быть, они просто не любят друг друга за цвет панциря.

При смерти морозный снарк не просто умирает. Он распадается на куски льда, оставляя после себя лужу замёрзшей воды, которая ещё долго не тает.

СОВЕТЫ:

- Не подходи близко к морозному снарку. Он не взрывается, как обычный, но его укус замораживает.
- Издали их видно по отблескам. Используй снайперскую винтовку.
- В ближнем бою они опасны вдвойне. Держи дистанцию.
- Морозные снарки не умирают сами.
- Если видишь стаю, используй взрывчатку. Холод им не страшен, а вот взрыв - другое дело.
- Морозные снарки и морозные буллсквиды ненавидят друг друга. Если есть возможность, страви их. Пусть убивают друг друга.
- Термические гранаты работают против них лучше, чем обычные. Жара и холод - плохие соседи.
