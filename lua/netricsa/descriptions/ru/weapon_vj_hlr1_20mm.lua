20-ММ ПУШКА


ОРУЖИЕ:      20-мм пушка (установленная автопушка)
ПАТРОНЫ:           20x102-мм высокоскоростные снаряды
СС:         ~450 выстрелов/мин

ОПИСАНИЕ:

Тяжелая стационарная автоматическая пушка. Выпускает взрывные 20-мм снаряды с высокой скоростью, с разрушительной эффективностью уничтожая пехоту и легкую бронетехнику. Предназначена для контроля территории и обороны.

ПРИМЕЧАНИЯ:

- Чрезвычайная огневая мощь, высокоэффективна против групп противника.
- Стационарная установка ограничивает мобильность.
- Перегревается при длительном использовании, требует коротких очередей.