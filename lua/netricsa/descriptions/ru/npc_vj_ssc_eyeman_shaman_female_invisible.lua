САМКА-ШАМАН ГНААРА, ПРИМИТИВНАЯ РАЗНОВИДНОСТЬ


КЛАСС:        Самка гнаара, тропическая разновидность
МЕТАБОЛИЗМ:   Млекопитающее
ВОСПРИЯТИЕ:   Дрессированное Племенное Боевое Животное
РАЗМЕР:       1,5 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Зубы, Кулаки
НАГРАДА:      1200
УГРОЗА:       Средняя

ОПИСАНИЕ:

Небольшое двуногое животное без головы, с одним глазом и большой пастью. Происходит с Сириуса и выдрессировано Менталом для участия в боевых действиях. Спины самок-Шаманов раскрашены в узор Черепа, они больше размером и сильнее, чем самцы.

Вершина социальной пирамиды примитивного подвида. В отличие от самцов, которые лишь служат и носят леопардовые штаны, шаманами становятся абсолютно все самки племени. Никакого отбора, никаких испытаний - родилась самкой, выросла и стала шаманом по умолчанию. В племенах примитивных гнааров это не привилегия, а обязанность.

Внешне самка-шаман отличается от самцов кардинально. Её кожа зелёная, болотного оттенка, что делает её менее заметной. Но главное отличие - на её спине нарисован огромный красный череп. Ритуальный символ, который виден издалека и служит напоминанием: эта особь здесь главная, и спорить с ней себе дороже.

Шаманы не просто управляют племенем. Они направляют самцов, решают вопросы торговли и войны, и поддерживают связь с Менталом. В свободное от управления время они, как и самцы, частенько торгуются с тропической разновидностью гнааров, обменивая ресурсы и, возможно, секреты татуировок.

СОВЕТЫ:

- Не имеют оружия дальнего боя - просто держись подальше.
- Некоторые могут летать.
- Есть сообщения о гнаарах-невидимках.