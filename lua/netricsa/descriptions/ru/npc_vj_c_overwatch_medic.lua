ЭЛИТНЫЙ МЕДИК OVERWATCH АЛЬЯНСА


КЛАСС:        Элитный медик Overwatch Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Полевая боевая медицинская единица Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Элитная Медицинская пушка Альянса (лечебный луч и пулемётный альтернативный огонь), целебные колбы
НАГРАДА:      1200
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Элитный медик Overwatch - это не спаситель, а обслуживающий персонал бойни. Он носит белый комбинезон с чёрными полосами на ногах и чёрными вставками на плечах. Оранжевые полосы на плечах формируют крест - символ медицины Альянса, который скорее предупреждение, чем обещание помощи.

Шлем оснащён респиратором и большими выпуклыми голубыми линзами, придающими медику вид насекомого. От респиратора тянется трубка к аптечному блоку на спине - где хранится содержимое личинок муравьиных львов.

Медицинская пушка Альянса стреляет лечебным лучом, который доставляет в тело союзников переработанные яйца личинок муравьиных львов. Альянс выжимает из личинок целебные соки и использует их как биологическое топливо для регенерации тканей.

На боку медика закреплены дополнительные целебные колбы, используемые для экстренного восстановления бойцов. Альтернативный режим пушки активирует встроенный пулемёт, позволяя медику защищать себя и добивать врагов прямо под лечащим лучом.

В бою элитный медик редко участвует в атаке напрямую. Он держится позади элитных подразделений, превращая их в почти бессмертных солдат, пока сам остаётся под прикрытием.

СОВЕТЫ:

- Убей медика первым - иначе элитные солдаты станут практически неубиваемыми.
- Его броня слабее, чем у боевых элит, но он обычно окружён охраной.
- Лечебный луч выдаёт его позицию ярким свечением - используй это.
- Альтернативный огонь опасен на ближней дистанции, не недооценивай его.
