ГРАВИ-ПУШКА


ОРУЖИЕ:      Манипулятор энергетического поля нулевой точки (Грави-Пушка)
ПАТРОНЫ:                  Нет (манипулирует физическими объектами)

ОПИСАНИЕ:

Экспериментальный инструмент, способный поднимать, удерживать и запускать объекты с огромной силой. Может использоваться как для боя, так и для решения головоломок путем манипулирования окружающей средой. Незаменим для управления тяжелыми или опасными предметами на безопасном расстоянии. Украдена Чёрной Мезой у Aperture Science.

ПРИМЕЧАНИЯ:

- Не использует традиционные боеприпасы; зависит от физических объектов в окружающей среде.
- Может запускать обломки и врагов со смертельной силой.
- Необходим для взаимодействия с окружающей средой и творческого решения задач.
- Можно модернизировать для увеличения мощности, чтобы запускать более крупные объекты.