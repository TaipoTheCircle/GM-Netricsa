ЗОМБИ-ХЕДКРАБ ТЕХНИК БАЗЫ ДЕЛЬТА, ОБЫЧНЫЙ


КЛАСС:        Зомби-хедкраб Техник Базы Дельта, Обычный
МЕТАБОЛИЗМ:   Человек-Носитель + Паразитический Хедкраб Зена
ВОСПРИЯТИЕ:   Зомбированная Сущность, Бывший Технический Специалист
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Когти, укус, захват
НАГРАДА:      200
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это технически тот же самый обычный зомби-хедкраб с базы Дельта, просто в другой униформе. Но выжившие утверждают, что отличия есть, и они не только во внешности.

Если обычные зомби на базе - это бывшие учёные в серых халатах, то Техники - это те, кто раньше чинил генераторы, лазал по вентиляции и знал, где какой рычаг находится.

Главное отличие Техника от учёного - не в силе или скорости, а в поведении. Некоторые выжившие клянутся, что видели, как такой зомби чинил сломанный кондиционер. (Он просто бился головой о стену, пока не упал.)

При убийстве в голову - голова отрывается.

СОВЕТЫ:

- Техник-зомби не сильнее и не быстрее обычного. Просто одежда другая.
- Голову при убийстве отрывает так же, как у учёного.
- Держи дистанцию, стреляй, не давай окружить.
