ПИГЛИН-АРБАЛЕТЧИК С НИЖНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Пиглин-арбалетчик
МЕТАБОЛИЗМ:   Свиноподобный гуманоид
ВОСПРИЯТИЕ:   Примитивная раса воинов Нижнего мира
РАЗМЕР:       1,9 м. 
ВЫНОСЛИВОСТЬ: Средняя 
ВРАЖДЕБНОСТЬ: Условная 
ОРУЖИЕ:       Арбалет
НАГРАДА:      450
УГРОЗА:       Средняя

ОПИСАНИЕ:

Пиглины-арбалетчики составляют ровно половину взрослого населения пиглинов - вторая половина предпочитает золотые мечи и ближний бой.

Как и все сородичи, они одержимы золотом до такой степени, что определяют друга или врага исключительно по наличию золотой брони на теле.

Внешне арбалетчик отличается от мечника только оружием и тактикой. Вместо того чтобы лезть в ближнюю свалку, он держит дистанцию и стреляет арбалетными болтами каждые две секунды. При этом он пятится назад, стараясь не подпускать цель слишком близко, но если упрётся в стену или край обрыва - продолжает палить с места.

В отличие от разбойников-поборников, арбалетчики не заряжают оружие заранее - перезарядка происходит только после того, как они вступят в бой.

Обитают арбалетчики там же, где и весь народ - в багровых лесах, пустошах Нижнего мира и особенно в бастионах, где охраняют сокровища от всяких незваных гостей. При этом они боятся синего огня душ, зомбифицированных собратьев и зоглинов, но эта трусость мгновенно проходит, как только они замечают врага.

Если группе арбалетчиков удаётся завалить крупную цель, они собираются на месте убийства и устраивают небольшое празднество с характерными хрюкающими возгласами. Особенно они любят танцевать после удачной охоты на хоглинов.

СОВЕТЫ:

- Надень золотую броню. Одна деталь - и они тебя не тронут.
- В бастионах их много, и они не любят, когда трогают их золотое добро.
