ПОЛУАВТОМАТИЧИСКИЙ ПИСТОЛЕТ DRACO


ОРУЖИЕ:     Draco
ПАТРОНЫ:    Патроны 7,62 × 39 мм
CC:         ~30 выстрелов/мин

ОПИСАНИЕ:

Полуавтоматический пистолет Draco является укороченной версией АК и без режима автоматического огня. Большинство моделей Draco производятся в Румынии на оружейном заводе Cugir и импортируются в США. Популярен у гангстеров.

ПРИМЕЧАНИЯ:

- Имеет тот же калибр что и АК и это делает его мощный пистолетом.
- На ALT + E можно сменить положение рук.




