ОПИСАНИЕ:

Овцы - мирные травоядные существа, которые пасутся на равнинах, в лесах и горах Minecraft. Они бывают разных цветов шерсти (белые, чёрные, серые, светло-серые, коричневые, розовые), что позволяет получать разнообразную крашеную шерсть.

Можно стричь овцу ножницами, чтобы получать шерсть, и разводить овец с помощью пшеницы. После стрижки шерсть постепенно отрастает.

Овцы водятся группами и пасутся, двигаясь медленно, что делает их лёгкой целью для добычи ресурсов.

СОВЕТЫ:

- Стриги овец, а не убивай - шерсть можно восстанавливать.
- Баранина обеспечивает хороший источник пищи.
- Из шерсти можно построить кровать и отдохнуть от тяжкой работы.