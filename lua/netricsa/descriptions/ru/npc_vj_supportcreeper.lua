ПОДДЕРЖИВАЮЩИЙ КРИПЕР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Поддерживающий крипер
МЕТАБОЛИЗМ:   Растительно-пороховой
ВОСПРИЯТИЕ:   Нейтральный моб
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Нейтральная
ОРУЖИЕ:       Баффы для союзников
НАГРАДА:      120
УГРОЗА:       Средняя

ОПИСАНИЕ:

Это жёлтая, более миролюбивая версия классического крипера, которую можно встретить в тёмных местах Верхнего мира.

Внешне поддерживающий крипер отличается от обычного более жёлтым оттенком кожи и однопиксельным ртом, который придаёт ему почти безобидный вид. Но это впечатление обманчиво.

В отличие от своего зелёного собрата, который подкрадывается и взрывается, поддерживающий крипер - чистый саппорт. Его главная задача - усиливать других враждебных мобов. Он находит ближайшего монстра, следует за ним и дарит ему мощные баффы, а сам при этом убегает.

Усиление криперов: Если поддерживающий крипер баффает другого крипера, он "заряжает" его электричеством (как при ударе молнии), давая ему Огнеупорность. Себе при этом он даёт Огнеупорность и Скорость I.

Усиление любых других монстров: Всем остальным (зомби, скелетам и т.д.) он дарует сразу три эффекта: Огнеупорность, Силу I и Скорость I. Себе при этом он даёт только Скорость I.

Если его цель умирает, он ищет следующую. Если целей не остаётся, он просто убегает.

Несмотря на свою нейтральность, поддерживающий крипер может быть опасен.

Если у него остаётся меньше половины здоровья, он перестаёт убегать и начинает вести себя как обычный крипер - шипит и взрывается.

Как и обычный крипер, он может стать заряженным, если в него попадёт молния. Это повышает уровень всех его эффектов до II, делая его и его союзников ещё более смертоносными.

Убийство поддерживающего крипера редко, но может принести Жёлтое таинственное яйцо. Если его "высидеть", из него вылупится детёныш поддерживающего крипера, которого можно приручить. Взрослея, такой ручной крипер уже будет давать полезные баффы тебе, а не монстрам.

СОВЕТЫ:

- Убивай первым делом. В любой битве с группой мобов, если ты видишь жёлтого крипера, он должен быть приоритетной целью номер один. Убив его, ты лишаешь всю орду её баффов.
- Держи дистанцию. Если ты ранил его, но не убил, он побежит на тебя и взорвётся.
- Используй дальнобойное оружие (лук / арбалет). Это самый безопасный способ убить его, не давая ему приблизиться.
- Не дай ему убежать. Он быстрый, поэтому старайся загнать его в угол или использовать замедляющие эффекты (паутина, медленные зелья).
- Если видишь заряженного поддерживающего крипера - беги. Его баффы настолько сильны, что обычный зомби может стать мини-боссом.
