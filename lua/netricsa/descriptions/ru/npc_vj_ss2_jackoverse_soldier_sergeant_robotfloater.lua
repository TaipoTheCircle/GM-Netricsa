РАЗВЕДДРОИД СЕРЖАНТА ШТУРМОВИКОВ


КЛАСС:        Разведдроид
МЕТАБОЛИЗМ:   Управляемый ИИ, Летательный Аппарат
ВОСПРИЯТИЕ:   Тактический разведчик, Воздушный Сканер
РАЗМЕР:       0,3 м.
ВЫНОСЛИВОСТЬ: Очень Низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Лазерная Пушка В "Глазу" 
НАГРАДА:      25
УГРОЗА:       Высокая

ОПИСАНИЕ:

Однажды Сержант Штурмовиков увидел стандартный разведдроид Ментала и за считаные секунды в голове составил чертежи его миниатюрного экземпляра. В отличие от оригинальной версии, этот дрон получил красный "глаз" - сканер, способный анализировать местность и передавать данные в реальном времени.

Разведдроид Сержанта представляет собой лёгкий летательный аппарат, созданный для тактической разведки и наблюдения. Он парит в воздухе, сканируя местность и выявляя угрозы. Красный "глаз" позволяет ему видеть даже в условиях плохой видимости - туман, ночь, дым.

В бою дрон использует лазеры, предназначенную не для нанесения серьёзного урона, а для подавления и отвлечения внимания. Если дрон обнаруживает врага, он немедленно передаёт координаты Сержанту, а также может вызвать подкрепление.

Разведдроид также используется для патрулирования территорий и контроля за передвижением войск. В отличие от оригинального разведдроида Ментала, у этой модели нет системы штрафов - только поиск и уничтожение.

СОВЕТЫ:

- Дрон легко уничтожить любым оружием, но лучше всего - скорострельным.
- Если дрон заметил тебя, он передаст твои координаты Сержанту. Срочно меняй позицию или уничтожай дрон.
- Красный "глаз" - уязвимое место. Попадание в него выводит дрон из строя мгновенно.
- Не стой на месте. Дрон стреляет лазером, но его точность падает при движении цели.
- Если дронов несколько, они могут координировать огонь. Уничтожай их по одному, не давая собраться в группу.
- Дрон - глаза Сержанта. Если ты видишь его, значит, Сержант уже знает о тебе. Время уходить.
