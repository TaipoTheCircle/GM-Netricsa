ЧИСТЫЙ СОСУД C БЕЗДНЫ


КЛАСС:        Чистый Сосуд
МЕТАБОЛИЗМ:   Магический, Пустотный
ВОСПРИЯТИЕ:   Идеальный сосуд, Безмыслие
РАЗМЕР:       ~2,5 м.
ВЫНОСЛИВОСТЬ: Экстремальная
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Гвоздь, Теневые атаки, Копья Пустоты
НАГРАДА:      1500
УГРОЗА:       Абсолютная

ОПИСАНИЕ:

Это версия Сосуда (Полого Рыцаря), какой он был много лет назад - на пике своей формы, до заражения, до того, как Лучезарность начала разлагать его изнутри. Это "абсолютно чистый" экземпляр, идеальная оболочка, которая никогда не знала эмоций или сомнений.

Внешне Чистый Сосуд разительно отличается от своего сломленного "взрослого" "я". Он выше, подтянутее, движется плавно, но с неестественной, механической грацией. Его накидка цела и почти парит, а движения его Гвоздя быстры, как молния.

Чистый Сосуд демонстрирует, как выглядел первоначальный план Бледного Короля по сдерживанию Лучезарности. Сосуд должен был стать "пустым" телом, лишённым ума, голоса и воли. Но даже у Чистого Сосуда в бою есть признаки "нечистоты" - он дышит, его движения, хоть и молниеносны, но иногда содержат намёки на едва сдерживаемую ярость.

Он очень быстр, агрессивен и использует набор приёмов:

Стремительные рывки с ударом Гвоздями. Он покрывает ими почти половину арены за секунду.

Тройные рубящие атаки в ближнем бою.

Метание копий Пустоты. Он создаёт копья из пустоты, которые пронзают землю и арену, а затем выпускает сферу (аналогичную сфере Жнеца), которая расширяется и разрушает платформы.

Атака с потолка. Прыгает вверх, затем вонзает Гвоздь в землю, создавая ударную волну, или зависает и стремительно пикирует на противника.

Контроль. Он может молниеносно блокировать атаку, мгновенно переходя в свою очередь.

СОВЕТЫ:

- Не паникуй. Его атаки очень быстры, но предсказуемы. Самое сложное - это привыкнуть к скорости.
- Атака с копьями. Когда он застывает в воздухе и из земли начинают вырываться лучи, просто встань между ними. Атака с расширяющейся сферой требует прыжка через неё.
- Когда он блокирует. Он почти всегда ответит мгновенной контратакой.
- Он не чувствует боли и не знает жалости. Он - совершенное орудие.
