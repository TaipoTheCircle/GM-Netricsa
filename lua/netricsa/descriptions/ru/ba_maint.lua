ЧЁРНАЯ МЕЗА,  ПОЛОМКИ В СЕКТОРЕ "C"


Хорошо, ты в Секторе "C". У панели управления - суета. Охранник и двое учёных пытаются что‑то починить. Кнопки не отвечают, экраны моргают, провода дымятся. Панель сломана всерьёз - кто‑то дёргал рычаги не по инструкции, кто‑то спешил.

Запах озона и горелой изоляции режет нос. Пол дрожит и ещё этот холод, не физический, от которого затылок покалывает. Он здесь.

Человек в деловом костюме где‑то рядом. Ты не видишь его, но знаешь: он тут. Чемодан в руке, улыба, как лезвие. Наблюдает. Не вмешивается. Ждёт, когда механизм сломается до конца.

