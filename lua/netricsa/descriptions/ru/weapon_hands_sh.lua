РУКИ


ОРУЖИЕ:      РУКИ
ПАТРОНЫ:     Нет (рукопашный бой)
CC:          ~90 ударов/мин

ОПИСАНИЕ:

Эволюция дала приматам руки с пальцами. Этим руками можно хватать объекты, проверять окружающий мир и конечно же драться.

ПРИМЕЧАНИЯ:

- Если много бить, можно устать.
- Для эффективности боя, лучше применять разные приемы. Допустим после блока бить мощным ударом в челюсть.
