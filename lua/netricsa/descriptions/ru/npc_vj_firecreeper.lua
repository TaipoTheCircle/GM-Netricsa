ОГНЕННЫЙ КРИПЕР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Огненный Крипер
МЕТАБОЛИЗМ:   Растительно-Пиротехнический
ВОСПРИЯТИЕ:   Враждебный Моб, Ходячий Поджигатель
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Самоподрыв С Распространением Огня
НАГРАДА:      150
УГРОЗА:       Средне-Высокая

ОПИСАНИЕ:

Это то, что получится, если обычный крипер решит, что взрываться - слишком скучно, а настоящая стихия - это огонь. Эта разновидность элементальных криперов отличается от своего порохового собрата ярко-оранжевой листвой, которая напоминает тлеющие угли и языки пламени. Заметить его в пустыне или аду сложно, зато когда он уже рядом - ты узнаешь об этом мгновенно.

Обитают огненные криперы преимущественно в жарких биомах: пустынях, саваннах, мезах и, разумеется, в Нижнем мире (Аду). Они не боятся лавы и могут комфортно чувствовать себя там, где обычные криперы давно бы сгорели.

При взрыве огненный крипер не разрушает блоки с той же силой, что обычный, но его взрывная волна разносит огонь на значительное расстояние вокруг. Вся местность в радиусе взрыва покрывается пламенем, которое быстро распространяется на древесину, листву, шерсть и другие горючие материалы. Один такой взрыв может превратить лес в пылающий ад, а деревянный дом - в пепелище за считанные секунды.

СОВЕТЫ:

- Не сражайся с огненным крипером рядом с деревянными постройками. Один взрыв - и твой дом превратится в пепел.
- В лесу этот крипер особенно опасен. Пламя быстро распространяется на деревья, и ты можешь оказаться в кольце огня.
- Если ты в пустыне или в аду, будь вдвойне осторожен. В пустыне крипер сливается с песком, а в аду огонь и так повсюду.
- Держи при себе ведро с водой. При взрыве ты сможешь быстро потушить распространяющийся огонь.
- Огненный крипер не даёт опыта и не оставляет ценных ресурсов, кроме обычного пороха и иногда угля.
- При убийстве огненного крипера есть шанс получить огненный порох, который используется для создания элементальной взрывчатки. Взрывчатка, сделанная из такого пороха, при подрыве создаёт огненный шар, который поджигает всё вокруг.
