ШОУ HALF-QUAKE, НАЧАЛО


Итак, Сэм. Первое испытание.

Или, как здесь предпочитают говорить, первая "пытка".

Для шоу ты теперь не герой, не солдат и даже не ошибка временной линии. Ты - Жертва. Так тебя представил диктор, с той самой интонацией, с какой обычно объявляют рекламу или прогноз погоды.

Испытание начинается с серии комнат. Простых. Понятных. Смертельно честных.

В первой комнате выпустят вортигонтов. Без изысков. Классика жанра. Им не объяснили правила шоу, но это и не требуется. Их задача - быть препятствием. Твоя - пройти дальше и не задерживаться на гуманистических размышлениях.

Следующая комната уже интереснее. Шипы. Кнопка. Дверь. Чтобы пройти, нужно войти внутрь и нажать кнопку. Дверь откроется. Шипы при этом поедут к кнопке. Прямо к тебе. План организаторов очевиден: дать надежду, а потом проверить, насколько быстро ты умеешь бежать обратно в предыдущую комнату и нырять в открытую дверь. Тайминг решает всё. Ошибка - тоже решает, но уже окончательно.

Дальше - лавовый бассейн. Три плиты. Прыгать можно. Как только сходишь с плиты, она разрушится. Шоу любит движение и не поощряет сомнения. Задержишься - станешь частью декора.

Потом снова шипы. Потому что традиции нужно уважать.

Лифт поднимет тебя этажом выше, где тебя будет ждать комната, полная туррелей. Да, тех самых. Любезно выкупленных у Чёрной Мезы. Бизнес есть бизнес.

Где-то среди этого великолепия спрятана кнопка. Найдёшь - получишь возможность двигаться дальше. Но не напрямую. Придётся пройти по тонкому уступу к двери. Она заминирована. Подойти можно. Войти - нельзя. Нужно будет развернуться и уйти обратно. Тогда откроется другой проход. В другом месте. Логично? Нет. Эффектно? Вполне.

И, разумеется, всё это будет щедро приправлено войсками Ментала. Потому что шоу должно развиваться. А зрителям нравится, когда ставки растут.

Запомни, Сэм.

Они хотят, чтобы ты ошибся.

Они ждут, что ты сломаешься.

А ты просто проходи дальше.

Испытание только началось.