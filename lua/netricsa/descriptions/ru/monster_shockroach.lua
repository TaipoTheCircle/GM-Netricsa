ШОКОВЫЙ ТАРАКАН ИЗ ИЗМЕРЕНИЯ X


КЛАСС:        Шоковый Таракан
МЕТАБОЛИЗМ:   Паразитический / Биоэлектрический
ВОСПРИЯТИЕ:   Раса X, Биооружие
РАЗМЕР:       ~60 см.
ВЫНОСЛИВОСТЬ: Низкая (отдельно), Средняя (с хозяином)
ВРАЖДЕБНОСТЬ: Средне-высокая
ОРУЖИЕ:       Биоэлектрические энергетические разряды, укус
НАГРАДА:      100
УГРОЗА:       Средняя

ОПИСАНИЕ:

Шоковый таракан - это живое биологическое оружие, разработанное Расой X и обычно используемое Шоковыми солдатами. При соединении с хозяином таракан функционирует как органическое огнестрельное оружие, стреляя концентрированными зарядами биоэлектрической энергии. Симбиоз взаимовыгоден: солдат получает силу дальнего боя, а таракан питается за счет метаболизма хозяина.

Если шоковый таракан погибает, шоковый таракан отделяется и продолжает сражаться самостоятельно. Сам по себе он гораздо менее точен и живуч, но всё ещё способен атаковать близлежащие цели отчаянным укусом.

Известно, что некоторые шоковые тараканы мутируют, изменяя свой энергетический поток. Были задокументированы жёлтые варианты, выпускающие более мощные электрические разряды, чем стандартный синий вид.

СОВЕТЫ:

- Быстро изолируй и уничтожай отсоединившихся шоковых тараканов - они хрупкие, но опасны, если их игнорировать.
- Жёлтых шоковых тараканов следует считать приоритетной угрозой из-за их более мощных атак.
- Без хозяина они медлительны и уязвимы - воспользуйся этой слабостью.
