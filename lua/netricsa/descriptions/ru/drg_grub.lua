ОПИСАНИЕ:

Личинка является одной из ранних стадий развития организмов Роя. Чаще всего эти существа появляются из специальных выводковых мешков, которые раскрываются при обнаружении добычи либо под воздействием высоких температур.

В отличие от большинства представителей Роя, личинки не способны атаковать противника и практически беззащитны. Их примитивная нервная система позволяет лишь реагировать на движение и следовать за крупными живыми объектами.

Несмотря на отсутствие прямой угрозы, присутствие личинок может служить важным признаком активности Роя поблизости. Обнаружение выводковых мешков обычно свидетельствует о наличии более опасных организмов в данном районе.

По мере развития личинки проходят дальнейшие стадии метаморфоза, постепенно превращаясь в полноценных представителей своего вида. Какие именно формы жизни возникают в результате этого процесса, до сих пор остаётся предметом споров среди ксенобиологов.

Большинство бойцов впервые испытывают жалость к представителям Роя именно при встрече с личинками. Обычно это продолжается до тех пор, пока не выясняется, во что они вырастают.

СОВЕТЫ:

- Не отвлекайся на личинок во время боя.
- Их присутствие часто означает, что рядом есть нечто гораздо опаснее.
- Маленький безобидный организм сегодня может стать большой проблемой завтра.
