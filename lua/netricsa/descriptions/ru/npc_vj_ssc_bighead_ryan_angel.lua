БОЛЬШЕГОЛОВЫЙ АНГЕЛ КРОТИМОВЕЦ, РАЙАН


КЛАСС:        Большеголовый Ангел Кротимовец, Райан
МЕТАБОЛИЗМ:   Гуманоидный, Мужской
ВОСПРИЯТИЕ:   Фанат-энтузиаст
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Нейтральная
ОРУЖИЕ:       Отсутствует (кроме сложности I Am Ugh-Zan)
НАГРАДА:      100
УГРОЗА:       Раздражающий фактор

ОПИСАНИЕ:

Кротимовцы - худощавые гуманоиды с гипертрофированными черепами, содержащими мозг разработчиков Croteam. Носят униформу крутого Сэма, но с уникальными чертами лиц команды разработчиков. Проявляют навязчивый энтузиазм, преследуя тебя с возгласами "Привет!" и "Эй, вы куда!?". Абсолютно безвредны, но в сложности "Я - Угх-Зан" получают огненные и ледяные модификаторы. Спонтанно появляются в секретных зонах, блокируя проходы своим числом.

Райан - один из самых загадочных "мозгов" в пантеоне Croteam. В отличие от коллег с подробными титрами, его вклад остаётся нерасшифрованным, словно стёртый артефакт на древнем свитке. Его гипертрофированный череп хранит тайну: возможно, он исправил критический баг в последнюю ночь перед золотым мастером, написал скрипт для забытого Easter egg, или просто принёс ту самую пиццу, которая спасла команду во время кранча. Его имя затерялось в анналах разработки, но факт его присутствия в списках - нерушимое доказательство, что он что-то сделал.

Он напоминание, что за каждой культовой игрой стоит не только гений ведущих разработчиков, но и тихий вклад десятков "кого-то", без которых финальный продукт был бы чуть менее совершенным.

Когда-то он был живым, но после того, как кто-то убил его, он воскрес - став Ангелом.

СОВЕТЫ:

- Для очистки пути используй армейский нож или пистолет.
- На сложности "I Am Ugh-Zan" уничтожай дистанционно из-за их модификаторов.
- В групповом скоплении эффективна кувалда с горизонтальным ударом.