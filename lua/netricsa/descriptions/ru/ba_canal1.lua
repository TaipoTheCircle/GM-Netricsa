КАНАЛЫ ЧЁРНОЙ МЕЗЫ, ВЗРЫВ РЕАЛЬНОСТИ


Ну что, Сэм, вот и началось! Не тихо, не аккуратно - с грохотом, воплями и полным хаосом.

Нихилант решил: хватит ждать. Его твари хлынули в Чёрную Мезу, как вода в прорванную плотину.

Грузовой поезд с охраной сошёл с рельсов прямо возле группы учёных. Металл скрежещет, искры летят, а из обломков уже лезут ОНИ.

Хаундаи - эти вечно голодные существа - несутся за учёным, будто закуску увидели. Беги, умник, беги!

А у того, кто замер у окна... сюрприз! Вортигонт. Просто стоит и смотрит. Ну да, для них это, наверное, обычный вторник.

Лифт, в котором ты только что ехал с "милыми" учёными, не выдержал напряжения реальности. Рванул вниз, как камень. Двое - в смятку. Ты - чудом жив.

Оглядываешься - и вот картина маслом: охранник, который ещё минуту назад казался таким бравым, сейчас... ну, скажем так, становится обедом для хаундая. Аппетитно, ничего не скажешь.

Время Стрелять!