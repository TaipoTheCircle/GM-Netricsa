МЕСТА:          Ледяная воронка, Лабиринт Белой змеи, Темница Валтора, Камеры заключённых

ОПИСАНИЕ:

Это суровая планета-тюрьма, созданная для вечного заточения самых опасных преступников Магической Измерения. Её поверхность полностью скована вековым льдом и скрыта плотной облачной пеленой, просветы в которой - редчайшее явление. Ландшафт представляет собой раздробленную ледяную пустошь с глубокими пропастями, обрывами и одинокими скалистыми пиками, полностью лишённую растительности.

Уникальная топография планеты сформирована в виде гигантской ледяной воронки - визуальная концепция, вдохновлённая кругами ада из "Божественной комедии" Данте. Вход в измерение находится в верхней части этого разлома, куда ведёт портал, связанный с планетой Андрос. Вдоль стен центральной спирали воронки расположены тысячи камер заключённых - магов и ведьм, вмороженных в лёд прямо со скальными выступами.

В самой глубокой точке, подобно Люциферу в ледяном озере, находится темница Валтора - наиболее защищённое место во всём измерении.

Внутренняя структура планеты пронизана сложной сетью пещер, туннелей и переходов, образующих Лабиринт Белой змеи. Ядро Омеги обладает мощной магической энергией, которая питает Белую змею - хранительницу планеты, а та делится ею со змеями-стражами, патрулирующими лабиринт.

Поверхность и недра планеты завалены техническим мусором - остатками кораблей, доставлявших преступников, и обломками оборудования. Во время разрушения портала многие заключённые вырвались на свободу и самоорганизовались в кровожадные банды мародёров, рыскающие по планете и использующие эти отходы для создания убежищ и оружия. На самой поверхности обитают лишь редкие птицы, не спускающиеся в опасные глубины.

Система тюремного заключения на Омеге основана на магической заморозке. Вокруг тела преступника формируется сверхпрочный ледяной кокон, устойчивый даже к бомбардировке. Эти зеркальные блоки доставляются к воронке на космических кораблях и сбрасываются внутрь конуса, где навечно вмораживаются в скалистые стены.

КЛЮЧЕВЫЕ МЕСТА:

- Ледяная воронка: Гигантский спиралевидный разлом, формирующий основную структуру планеты. Вход в измерение и место сброса заключённых.
- Лабиринт Белой змеи: Сложная сеть подлёдных пещер и туннелей, пронизывающая планету. Охраняется ледяными змеями-стражами, потомством Белой змеи.
- Темница Валтора: Наиболее глубокая и защищённая точка воронки, где был заточён один из могущественнейших магов Тьмы.
- Камеры заключённых: Тысячи ледяных коконов с преступниками, вмороженные в стены воронки на разных уровнях.
