ДИКИЙ КАБАН С АРХИПЕЛАГА КРИГЕРА


КЛАСС:        Дикий Кабан
МЕТАБОЛИЗМ:   Травоядное/Всеядное
ВОСПРИЯТИЕ:   Роялист
РАЗМЕР:       3,5 фута
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Пассивная
ОРУЖИЕ:       Клыки
НАГРАДА:      50
УГРОЗА:       Отсутствует

ОПИСАНИЕ:

Дикий кабан - классический представитель фауны Архипелага Кригера, ценимый за сочную грудинку. Обладает характерными признаками: короткое плотное тело, длинная узкая голова и острые стоячие уши. Несмотря на грозный вид, демонстрирует исключительную пугливость и отсутствие интеллекта. При малейшей угрозе обращается в бегство, представляя интерес исключительно как источник пропитания.

Он проводит время в поисках пищи, игнорируя происходящее вокруг. При приближении человека предпочитает ретироваться. Не атакует.

СОВЕТЫ:

- Прекрасный источник пищи - одна туша обеспечивает несколько порций грудинки.
- Подходи близко - животное не представляет угрозы.
- Стреляй без опаски - единственная реакция кабана будет жёсткий побег под дабстеп.