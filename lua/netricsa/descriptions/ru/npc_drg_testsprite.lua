ОПИСАНИЕ:

Это одно из самых загадочных существ в известных вселенных. Никто не знает, откуда они взялись, кто их создал и зачем они здесь. Выглядят они как пиксельные стикманы разных цветов: красные, синие, зелёные, жёлтые и тд. Они плоские, словно нарисованные на листе бумаги, но при этом двигаются в трёхмерном пространстве, сохраняя свою 2D-форму.

Первое задокументированное появление было зафиксировано в тестовых средах, заброшенных симуляциях и странных цифровых пространствах, которые не поддаются классификации. Отсюда и название - "тестовые". Создатель, если он вообще существует, не оставил никаких следов.

При виде любого живого существа он начинает двигаться к цели. Не бежит, не крадётся, не атакует - просто идёт, с постоянной скоростью, по кратчайшему пути. Если на пути препятствие, он обойдёт его. Если дверь заперта, он будет стоять перед ней, пока не откроют.

Когда Некстбот подходит к объекту интереса, он останавливается и начинает прыгать. Прыгает он радостно, с энтузиазмом. Прыгать он может бесконечно долго, не проявляя никаких признаков усталости или агрессии.

При смерти просто исчезает. Не оставляет следов.

Самая маловероятная теория гласит, что они пытаются установить контакт, но не знают как, поэтому просто прыгают.

