ЛАБОРАТОРИЯ ПО ИССЛЕДОВАНИЮ ИНОПЛАНЕТЯН ЧЁРНОЙ МЕЗЫ, МАЛЕНЬКИЙ СКЛАД


Маленький склад. Тесный, заставленный ящиками, с единственным выходом - узким тоннелем, который ведёт в неизвестность.

Мы на грузовом лифте. Тяжёлом, медленном, который тащился сюда целую вечность, пока мы переминались с ноги на ногу и гадали, кто нас встретит наверху. Встретил склад. И выход.

Но выход, Сэм, это не просто дыра в стене. Это маленький тоннель. Узкий, извилистый. И он ведёт в лаборатории по исследованию инопланетян.

Туда, где когда-то брали образцы, делали анализы и, возможно, проводили эксперименты, которые никто не должен был видеть.

Теперь там, судя по данным, которые я улавливаю, - зона активных боевых действий.
