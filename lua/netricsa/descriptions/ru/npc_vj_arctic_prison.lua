АРКТИЧЕСКИЙ ТЮРЕМНЫЙ СОЛДАТ OVERWATCH АЛЬЯНСА


КЛАСС:        Арктический тюремный солдат Overwatch Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Карательно-штурмовое подразделение дальнего и среднего боя Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Импульсная винтовка AR2, SMG
НАГРАДА:      650
УГРОЗА:       Высокая

ОПИСАНИЕ:

Арктический тюремный солдат Overwatch - это переработанный страж комплекса Нова Проспект, перепрофилированный Альянсом для ведения боевых действий в экстремальных климатических условиях. Когда стандартных сил Overwatch недостаточно, Альянс без колебаний извлекает солдат из тюремного резерва и отправляет их в самые негостеприимные регионы планеты.

В отличие от дробовикового варианта, этот солдат выполняет роль подавления и огневого контроля. Он оснащён импульсной винтовкой AR2 или пистолет-пулемётом SMG, что позволяет ему эффективно действовать на средней дистанции, прикрывая продвижение других подразделений и расчищая подходы к целям.

Экипировка адаптирована под арктику: бело-серый камуфляж снижает заметность на фоне льда и снега, но поверх него всё ещё просматриваются элементы тюремной униформы.

Как и у других тюремных солдат, на спине присутствует голубая маркировка "Нова Проспект".

СОВЕТЫ:

- AR2 позволяет ему держать дистанцию и подавлять укрытия энергетическим огнём.
- Жёлтый визор хорошо различим в темноте, но плохо заметен в снежных бурях.
- Лучше выводить из строя издалека, пока он не занял выгодную позицию.