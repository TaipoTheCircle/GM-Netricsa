СИТИ-17, ЗЛЫЕ АРБУЗЫ ИДУТ НА ШТУРМ


Сэм, я даже не знаю, что сказать. Мы видели многое: зомби в подвалах, Ментала в космосе, кирпичные комнаты без дверей.

Но чтобы в Сити-17 поселились арбузы... и чтобы у них была рогатка... и чтобы они мстили... Это новый уровень абсурда, и он мне нравится.

Обычный уголок Сити-17: серые стены, пара зданий, магазинчик. Типичный день в оккупированном городе. Но что-то здесь не так. Возле магазина, вместо привычного патруля Альянса, стоит огромная рогатка. А вокруг - арбузы. Много арбузов. Они просто лежат, ждут своего часа. Ждут тебя.

Берёшь рогатку (желательно заморозить её, чтобы не улетела).

Кладёшь на неё арбуз.

Привариваешь арбуз к рогатке, выставив силу натяжения 2000 или больше.

Хватаешь рогатку, жмёшь R, оттягиваешь и отпускаешь.

Арбуз улетает крушить стены Сити-17.

Местная Легенда Гласит:

"Арбузный вор любит это место". Кто этот вор? Зачем он ворует арбузы? Почему именно Сити-17? Может, это местный сталкер, который нашёл способ борьбы с Альянсом? Может, это сам Гордон Фримен, переквалифицировавшийся в метателя бахчевых? Мы не знаем. Но легенда живёт.

Рогатка - главное оружие. Забудь на время о винтовках и гранатах. Здесь ты - артиллерист-арбузник. Целься во всё, что раздражает: стены, здания, врагов, случайных прохожих.

Арбузы - расходный материал. Их много. Используй с умом. Каждый арбуз должен нести правосудие.

Сэм, это место - глоток свежего воздуха в череде мрачных локаций. Здесь не нужно выживать, не нужно прятаться. Нужно просто взять арбуз, зарядить рогатку и отправить его в полёт. Наслаждаться траекторией, звуком удара, разлетающимися кусками. Это терапия. Арбузная терапия.

Сэм. Заряжай рогатку. Сити-17 ждёт своего арбузного возмездия.
