ЭНДЕРМЕН С КРАЯ МИРА МАЙНКРАФТА


КЛАСС:        Эндермен
МЕТАБОЛИЗМ:   Неопределённый
ВОСПРИЯТИЕ:   Нейтральный моб-телепорт / загадочный пришелец
РАЗМЕР:       2,9 м.
ВЫНОСЛИВОСТЬ: Высокая 
ВРАЖДЕБНОСТЬ: Нейтральная
ОРУЖИЕ:       Удар рукой
НАГРАДА:      1000
УГРОЗА:       Высокая

ОПИСАНИЕ:

Одно из самых загадочных существ, чьё истинное происхождение до сих пор вызывает споры. Его название - комбинация слов "Энд" (измерение, где он, предположительно, обитает) и "мен" (человек). Согласно фанатским теориям, когда-то эндермены были обычными людьми, которые бежали в Энд от катастрофы. Там, питаясь фруктами хоруса, они мутировали: вытянулись, потеряли цвет кожи, научились телепортироваться и строить энд-города.

Внешность эндермена устрашающа: длинное, худое, полностью чёрное тело, светящиеся фиолетовые глаза и рот, из которых исходят фиолетовые частицы, похожие на частицы портала в Нижний мир. Эндермены спокойно бродят по миру, пока ты не посмотришь им прямо в глаза.

Эндермены уникальны тем, что являются единственным мобом, который может естественным образом появиться во всех трёх измерениях:

Верхний мир: Ночью в большинстве биомов.

Нижний мир: Чаще всего в искажённом лесу.

Край: Основное место обитания. Здесь они спавнятся огромными группами.

Поведение эндермена сводится к двум вещам: телепортации и переносу блоков. Он может поднимать и перемещать определённые блоки (землю, песок, кактусы, цветы), оставляя после себя хаос. Если эндермен держит блок, при его смерти блок выпадет отдельно.

В спокойном состоянии эндермен просто ходит, игнорируя игрока. Но стоит направить на него перекрестие прицела или ударить, как он приходит в ярость. Тело эндермена начинает трястись, пасть открываться, и он издаёт жуткий, искажённый электроникой крик. Далее начинается бой:

Эндермен телепортируется за спину противника или по бокам, нанося мощные удары руками.

Любой снаряд (стрела, снежок, яйцо) вызывает телепортацию до попадания. Убить эндермена из лука невозможно.

Вода и дождь наносят эндерменам урон. При контакте с водой или во время дождя они паникуют и телепортируются прочь.

СОВЕТЫ:

- Надень вырезанную тыкву на шлем. Ты будешь видеть хуже, но эндермен не будет агриться, сколько бы ты на него ни смотрел.
- Эндермены имеют рост почти 3 блока. Построй навес на высоте 2 блоков от земли. Ты сможешь бить эндермена по ногам, а он не сможет достать до тебя.
- Прижмись спиной к стене или углу. Так эндермен не сможет телепортироваться за спину.
- Если бить эндермена по ногам, он не телепортируется от боли.
- Кинув воду себе под ноги, ты создашь барьер. Эндермены боятся воды и не подойдут.
- Лучшее оружие - меч. Наноси быстрые удары, стараясь держать врага перед собой.
- Эндермен ненавидит эндермитов (маленьких фиолетовых жуков) и будет преследовать их, даже если ты смотришь на него.
- Если эндермен умирает, держа блок, этот блок выпадает. Исключение - блоки без лута (например, коренная порода).
- Существуют фанатские легенды о Белом Эндермене, который якобы появляется редко, но официально это только текстурный баг или мод.
