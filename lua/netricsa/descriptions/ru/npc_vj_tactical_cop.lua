ТАКТИЧЕСКИЙ МЕТРОКОП ГРАЖДАНСКОЙ ОБОРОНЫ АЛЬЯНСА


КЛАСС:         Тактический Метрокоп Гражданской Обороны Альянса
МЕТАБОЛИЗМ:    Человек, мужчина
ВОСПРИЯТИЕ:    Полицейское штурмовое подразделение Альянса
РАЗМЕР:        1,9 м.
ВЫНОСЛИВОСТЬ:  Средняя
ВРАЖДЕБНОСТЬ:  Высокая
ОРУЖИЕ:        Пистолет, SMG
НАГРАДА:       450
УГРОЗА:        Средняя

ОПИСАНИЕ:

Тактический метрокоп - это переходное звено между обычной Гражданской Обороной и военизированными силами Альянса. Он экипирован менее формально, но заметно агрессивнее стандартных патрульных. Зелёные штаны военного камуфляжа выдают его принадлежность к уличным штурмовым группам, а отсутствие рукавов подчёркивает ставку на подвижность и ближний бой, а не парадный вид.

Чёрный бронежилет прикрывает жизненно важные органы и содержит встроенные демпферы, из-за чего выстрел в голову не приводит к мгновенному поражению. Это делает тактического метрокопа неожиданно живучим противником для тех, кто привык решать всё одним точным выстрелом.

Вооружён стандартно - пистолетом и SMG, однако действует более напористо, активно сокращая дистанцию и стараясь зажать цель числом. Часто используется для зачисток зданий, подавления беспорядков и операций в районах, где требуется жёсткий, но всё ещё "полицейский" подход.

СОВЕТЫ:

- Не полагайся на хедшоты.
- Опасен на средней и ближней дистанции, особенно в помещениях.
- Лучше устранять издалека или выводить из строя взрывчаткой.