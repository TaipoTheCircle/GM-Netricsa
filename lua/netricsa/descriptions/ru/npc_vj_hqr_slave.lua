ВОРТИГОНТ (РАБ) С ШОУ HALF-QUAKE


КЛАСС:        Вортигонт Шоу HALF-QUAKE (Раб)
МЕТАБОЛИЗМ:   Разумное млекопитающее
ВОСПРИЯТИЕ:   Порабощенная инопланетная раса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Вынужденная
ОРУЖИЕ:       Биоэлектрический разряд, когти
НАГРАДА:      300
УГРОЗА:       Средняя

ОПИСАНИЕ:

Когда Садистское Шоу HALF-QUAKE заключало контракт с Чёрной Мезой, в комплекте с техникой и хедкрабами шли и вортигонты. Много вортинтонтов. Их привозили партиями, сгружали в подсобки и некоторое время не знали, что с ними делать, потому что умное млекопитающее сложнее запрограммировать, чем паразита.

Проблему решили просто: старые псионические цепи Нихиланта подчинили себе, а каждому вортигонту перед выходом на съёмочную площадку наносили на лицо белую метку маркером ">)". Метка несмываемая, но не потому, что её чем-то закрепили, а потому что вортигонты боятся её стирать.

Сами по себе вортигонты не хотят воевать. Они до сих пор помнят свободу, а их звуки - это мольба о том, чтобы их кто-нибудь наконец пристрелил.

При виде противника вортигонт разряжает накопленную биоэлектрическую энергию, выпуская зелёные молнии из обеих рук.

СОВЕТЫ:

- Молния бьёт далеко и больно. Не высовывайся из-за угла без надобности.
- В группах они страшнее, чем поодиночке.
