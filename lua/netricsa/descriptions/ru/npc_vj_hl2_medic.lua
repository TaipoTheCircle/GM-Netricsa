МЕДИК ПОВСТАНЦЕВ СОПРОТИВЛЕНИЯ


КЛАСС:        Боец Сопротивления, Медик
МЕТАБОЛИЗМ:   Люди
ВОСПРИЯТИЕ:   Медик
РАЗМЕР:       ~1,7-1,8 м.
ВЫНОСЛИВОСТЬ: Средняя (лёгкая броня, медицинское снаряжение)
ВРАЖДЕБНОСТЬ: Высокая (к войскам Альянса)
ОРУЖИЕ:       Пистолет, пистолет-пулемёт, аптечки
НАГРАДА:      250
УГРОЗА:       Низкая

ОПИСАНИЕ:

Медики повстанцев - это бойцы Сопротивления, которые решили, что выживание отряда важнее личного счёта убитых врагов. Они появляются там, где бой уже пошёл не по плану, и делают всё возможное, чтобы этот план переписать. Их основное оружие - аптечка, но при необходимости они способны прикрыть себя и раненых огнём.

В отличие от обычных бойцов, медики стараются держаться чуть позади линии столкновения, постоянно перемещаясь между союзниками и оценивая, кто ещё может сражаться, а кого нужно срочно вернуть в строй. Их обучение ограничено, но практика бесконечна: каждый бой - новый урок анатомии под огнём.

Хотя они не предназначены для прямых атак, присутствие медика значительно увеличивает живучесть отряда. Солдаты Сопротивления сражаются увереннее, зная, что рядом есть тот, кто сможет поднять их на ноги даже после тяжёлого попадания.

СОВЕТЫ:

- Защищай их: потеря медика резко снижает боеспособность группы.
- Медик под огнём - признак того, что бой затягивается.