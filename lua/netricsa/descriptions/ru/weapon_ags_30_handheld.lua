АВТОМАТИЧЕСКИЙ СТАНКОВЫЙ ГРАНАТОМЁТ АГС-30


ОРУЖИЕ:      АГС-30
ПАТРОНЫ:     Патроны 30-мм
CC:          ~400 выстрелов/мин

ОПИСАНИЕ:

Российский автоматический станковый гранатомёт калибра 30 мм. Разработан в первой половине 1990-х годов в тульском КБ Приборостроения как более лёгкая замена гранатомёту АГС-17.

ПРИМЕЧАНИЯ:

- Можно устроить врагу дождь из гранат.
- Лучше быть аккуратным с этой штукой, можно себя подорвать.



