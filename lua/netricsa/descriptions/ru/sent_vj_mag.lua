ОПИСАНИЕ:

Доктор Арне Магнуссон - один из высших лидеров Сопротивления, руководящий операциями с базы в Белой Роще. Грубый, нетерпеливый и часто критичный, он нетерпим к ошибкам, но за его грубой внешностью скрывается непоколебимая преданность выживанию человечества.

Ветеран Чёрной Мезы, Магнуссон пережил Каскадный Резонанс и несёт на себе шрамы и знания об этой катастрофе. Его инженерный гений породил Устройство Магнуссона - экспериментальное взрывное оружие, разработанное специально для уничтожения страйдеров Альянса. Несмотря на свою необычность, устройство оказалось одним из мощнейших инструментов Сопротивления, позволивших переломить ход битвы.

Хотя он и не боец ​​первой линии, Магнуссон внушает Сопротивлению одновременно и уважение, и страх. Его гениальность сравнима лишь с его вспыльчивостью, и всё же в критические моменты его лидерские качества незаменимы.

СОВЕТЫ:

