ОПИСАНИЕ:

Элитный Арктический Страж Overwatch Альянса - специализированный вариант элитной пехоты, предназначенный для охраны стратегических объектов, ключевых персон и критически важных зон в экстремальных климатических условиях. В отличие от Коммандо, Стражи реже участвуют в стремительных наступлениях, но практически не уступают им в боевой эффективности.

Их легко узнать по ослепительно-белой броне и единственному красному визору. Ношение плаща не является обязательным элементом экипировки: некоторые Стражи носят его на плече как знак статуса или принадлежности к конкретному подразделению, другие полностью отказываются от него ради большей мобильности и снижения заметности в условиях сильного ветра и метели.

Как и у других элитных бойцов Overwatch, их броня оснащена встроенной системой жизнеобеспечения. Респиратор соединён толстой трубкой, проходящей через левое плечо и подключённой к кислородному рюкзаку на спине с логотипом Альянса. Система обеспечивает стабильное дыхание, фильтрацию токсинов и защиту от экстремально низких температур, позволяя Стражам часами удерживать позиции в арктических зонах без внешней поддержки.

СОВЕТЫ:

- Отсутствие плаща не означает меньший ранг или угрозу.
- Почти всегда действуют парами или в составе охранного периметра.