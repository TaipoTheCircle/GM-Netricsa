ОПИСАНИЕ:

Геночервь - колоссальный владыка Расы X, биоинженерное чудовище, служащее главным оружием в их вторжении. Появившись из пространственного разлома, он предстаёт в виде огромного червеобразного титана с гротескными жвалами, бронированной броней и пульсирующими мешочками. Его тело предназначено для терраформирования и доминирования в инопланетной среде, засеивая территорию формами жизни Расы X и уничтожая противника разрушительными атаками.

Биология существа представляет собой сплав паразитической ткани и генной инженерии, что делает его одновременно живым оружием и мобильной родильной камерой. Он извергает едкую кислоту и порождает стручки, выпускающие агрессивных миньонов для своей защиты. Его психическое присутствие может нервировать даже обученных солдат, подавляя разум чуждыми импульсами.

Во время инцидента в Чёрной Мезе Геночервь был авангардом вторжения Расы X - его успешное прибытие укрепило бы их позиции на Земле. Чтобы уничтожить его до полной стабилизации пространственного разлома, потребовалась мощная огневая мощь и колоссальные жертвы.

