ЖЕЛЕЗНЫЙ ГОЛЕМ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Железный Голем
ВОСПРИЯТИЕ:   Защитная конструкция
МЕТАБОЛИЗМ:   Страж Деревень
РАЗМЕР:       ~2,7 м.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Мощные кулаки, броски противников
НАГРАДА:      100
УГРОЗА:       Средняя

ОПИСАНИЕ:

Железный Голем - огромная, массивная конструкция, созданная для защиты жителей деревни от враждебных мобов. Несмотря на внушительную внешность, он дружелюбен к людям и деревенским жителям.

Он патрулирует деревню, реагируя на любые угрозы, будь то зомби, скелеты или другие враждебные существа.

Может поднимать и бросать мобов на расстояние, используя силу своих металлических рук.

Обладает огромной физической силой и высокой живучестью, что делает его практически непобедимым для большинства обычных мобов.

СОВЕТЫ:

- Используй Голема как щит - он отвлекает врагов и наносит им значительный урон.
- Несколько Големов вместе превращают деревню в почти неприступную крепость.
