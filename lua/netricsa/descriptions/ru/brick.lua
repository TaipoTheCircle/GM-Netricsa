КИРПИЧ, МЛАДШИЙ БРАТ ПО КИРПИЧУ


Сэм, похоже, мы наткнулись на минималистичного родственника тех кирпичных особняков, что мы облазили. Если предыдущая версия была "посёлком городского типа" с бассейном и двухэтажками, то здесь - суровая загородная безысходность в квадрате.

Два дома, кирпич, трава и полное отсутствие намёков на цивилизацию. Даже пруд не накопали.

Перед нами ровный зелёный газон, на котором стоят ровно два объекта:

Два квадратных кирпичных дома. Без дверей. Это уже традиция. Видимо, архитектор вдохновлялся концепцией "гостеприимство - иллюзия, проёмы - реальность".

Оба сложены из красного кирпича, оба квадратные, оба молчаливые.

Ничего больше. Ни забора, ни деревьев, ни машин. Только дом №1, дом №2 и ты.

Первый дом (он же "Лестничный"):

Есть лестница на второй этаж. Работает, ведёт наверх. Не сломана, не прибита, не охраняется гномом. Просто лестница.

На втором этаже - одно окно. Без стекла. Сквозняк, обзор, возможность высунуться и крикнуть "Вы Кто такие!? идите на ...". Или просто высунуться и получить пулю в лоб. Окно - дело такое.

Второй дом (он же "Пустой"):

Второго этажа нет. Вообще.

Внутри - ничего.

Это место - чистая геометрия. Два дома, трава, небо. Никаких отвлекающих деталей, никаких запасов Софии, никаких бассейнов. Только ты, враг и кирпич. Здесь всё решает скорость реакции и умение пользоваться единственным окном на втором этаже.

Можно начинать.
