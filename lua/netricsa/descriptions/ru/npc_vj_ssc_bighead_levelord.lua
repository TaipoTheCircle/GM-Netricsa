БОЛЬШЕГОЛОВЫЙ КРОТИМОВЕЦ, РИЧАРД БЭЙЛИ "ЛЕВЕЛОРД" ГРЕЙ


КЛАСС:        Большеголовый Кротимовец, Ричард Бэйли "Левелорд" Грей
МЕТАБОЛИЗМ:   Гуманоидный, Мужской
ВОСПРИЯТИЕ:   Фанат-энтузиаст
