АРКТИЧЕСКИЙ МЕТРОКОП ГРАЖДАНСКОЙ ОБОРОНЫ АЛЬЯНСА


КЛАСС:        Арктический метрокоп Гражданской Обороны Альянса
МЕТАБОЛИЗМ:   Человек, мужчина
ВОСПРИЯТИЕ:   Гражданская оборона Альянса
РАЗМЕР:       ~1,9 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Дубинка, пистолет, SMG
НАГРАДА:      150
УГРОЗА:       Низкая

ОПИСАНИЕ:

Арктический метрокоп - специализированный вариант рядового сотрудника Гражданской обороны Альянса, адаптированный для службы в холодных и снежных регионах. Одет в серо-белый камуфляж, который должен маскировать его на фоне льда и снега, хотя чаще всего маскирует только страх и плохую подготовку.

Поверх формы он носит противогаз, защищающий от холода, пыли, газа и, по слухам, от запаха собственных товарищей после долгих смен. Как и обычные метрокопы, выполняет функции патрулирования, разгона гражданских, охраны объектов и демонстрации "порядка", особенно там, где порядка давно нет.

