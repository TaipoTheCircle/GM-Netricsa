ТОПОР ТЫСЯЧИ МЕТАЛЛОВ


ОРУЖИЕ:  Топор Тысячи Металлов
СС:      ~0.9 удара / секунду
ВЕС:     9,5 кг.

ОПИСАНИЕ:

Массивное холодное оружие, выкованное из сплавов множества металлов и обладающее исключительной прочностью и тяжестью. Сочетает в себе смертельную мощь и устойчивость к разрушению: топор никогда не ломается в бою и сохраняет эффективность даже при постоянной эксплуатации.

В бою оружие позволяет выполнять несколько типов атак: обычные рубящие удары, широкие размахи для поражения группы целей и мощные вертикальные удары по земле, создающие ударную волну, распространяющуюся по земле и воздействующую на врагов на расстоянии, заставляя их подлететь. Дистанция поражения ударной волной зависит от расстояния до цели.

ПРИМЕЧАНИЯ:

- Эффективен как против одиночных целей, так и против групп на средней дистанции.
- Широкий размах наносит урон всем целям в секторе перед пользователем.
- Вертикальный удар по земле создаёт ударную волну, отталкивая и повреждая цели позади первых.
- Абсолютная неразрушимость делает его надёжным инструментом для длительных столкновений.