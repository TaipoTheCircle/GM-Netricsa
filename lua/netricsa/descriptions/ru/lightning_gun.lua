МЬЁЛЬНИР ПУШКА


ОРУЖИЕ:  Мьёльнир Пушка
ПАТРОНЫ: 9-мм патроны / электрическая энергия молнии
СС:      ~60 выстр./мин

ОПИСАНИЕ:

Компактный пистолет, заряженный силой древнего громового артефакта. Внешне выглядит как стандартное 9-мм оружие, но его истинная мощь раскрывается только в руках достойного пользователя.

Основной режим огня вызывает направленный удар молнии в указанную точку. Разряд спускается с неба с разрушительной силой, поражая цель мгновенно и оставляя после себя обугленную зону поражения.

Альтернативный режим используется преимущественно для демонстрации: в небе сверкают молнии без нанесения значительного урона, создавая устрашающий визуальный эффект и психологическое давление на противника.

ПРИМЕЧАНИЯ:

- Основной огонь вызывает мощный удар молнии по цели.
- Стрельба под себя крайне опасна - пользователь не обладает защитой уровня Тора.
- Альтернативный огонь создаёт молнии в небе без серьёзного устрашения.
- Эффективен на любой дистанции при наличии прямой видимости неба.
- Работает только в руках "достойного".
