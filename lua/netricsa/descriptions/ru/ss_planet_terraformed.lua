ТЕРРАФОРМИРОВАННАЯ ПЛАНЕТА


ПЛАНЕТА:        терраформированная планета
ТИП:            Планета Земного Типа, Искусственно терраформированная планета
КЛИМАТ:         Разнообразный (тропический, засушливый, полярный)
СПУТНИКИ:       Как минимум 2 луны
ЛАНДШАФТ:       Густые джунгли, горные хребты, пустыни, болота, полярные шапки
РАСПОЛОЖЕНИЕ:   Неизвестная звёздная система
МЕСТНЫЕ РАСЫ:   Люди, Дикие люди, различные виды динозавров и мутировавших существ
ПРИНАДЛЕЖНОСТЬ: Корпорация Мендель-Груман
МЕСТА:          Дождевые леса, Пустынные регионы, Заболоченные угодья, Подземные пещерные системы

ОПИСАНИЕ:

С орбиты эта планета напоминает Землю - зелёные континенты, окружённые сияющими голубыми морями. Однако её истинная природа кроется в катастрофическом эксперименте. Изначально это был нестабильный, пустынный мир с бушующей вулканической активностью, где расплавленная лава находилась непосредственно под тонкой корой.

Корпорация Мендель-Груман, возглавляемая Роландом Кейном, подвергла планету масштабному терраформированию. Но процесс пошёл непредвиденным путём: он вызвал экспоненциальный скачок эволюционной активности. Результатом стало внезапное появление на планете динозавров и других доисторических существ, многие из которых обладают аномальными мутациями.

Поверхность планеты покрыта преимущественно непроходимыми тропическими джунглями с гигантскими деревьями и высокими скалистыми пиками, отбрасывающими длинные тени. Здесь обитают стаи свирепых велоцирапторов, скрытные засадники-люркеры, а также более крупные хищники - тираннозавры, дилофозавры и гиганотозавры. Мирные травоядные, такие как брахиозавры и паразауролофы, сосуществуют с агрессивными трицератопсами.

Помимо джунглей, на планете существуют ограниченные пустынные регионы с мёртвыми деревьями и останками древних существ. Заболоченные угодья представляют собой бескрайние топи с уникальными водными формами жизни, включая амфибийных утарапторов. На полюсах расположены ледяные шапки, фауна которых неизвестна.

Глубоко под поверхностью простирается разветвлённая система пещер - от огромных гротов до тесных туннелей. В полной темноте здесь обитают существа, приспособившиеся к жизни без света: слепые хищники, гигантские скорпионоподобные Жуки-Солдаты и неопознанные угреподобные монстры.

Корпорация Мендель-Груман использовала уникальную флору и фауну планеты для разработки биологического оружия, которое планировалось продавать различным клиентам. Военные силы корпорации на планете возглавлял лично Роланд Кейн вплоть до своей гибели.

КЛЮЧЕВЫЕ МЕСТА:

- Дождевые леса: Основной биом планеты, покрывающий большую часть поверхности. Густые джунгли с многоярусной растительностью, где обитает большинство видов динозавров.
- Пустынные регионы: Ограниченные засушливые зоны с суровыми условиями. Здесь обитают бронированные разновидности динозавров с каменноподобной чешуёй.
- Заболоченные угодья: Бескрайние топи и болота, населённые уникальными водными формами жизни, включая амфибийных утарапторов и гигантских тираннозавров.
- Подземные пещерные системы: Разветвлённая сеть туннелей под поверхностью планеты, где обитают существа, приспособленные к вечной темноте.
- Объекты Мендель-Груман: Исследовательские станции и военные базы корпорации, включая объекты, построенные ещё на нестабильной, дотерраформированной поверхности.
