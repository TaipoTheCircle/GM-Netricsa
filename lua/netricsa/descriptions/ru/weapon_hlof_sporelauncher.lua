СПОРО-МЕТАТЕЛЬ


ОРУЖИЕ:      Споро-Метатель
БОЕПРИПАСЫ:        Взрывные споры
ТЕМП ОГНЯ:         ~6 выстрелов/мин

ОПИСАНИЕ:

Биологическое оружие, стреляющее взрывными спорами, которые взрываются при ударе или через небольшую задержку. Представляет собой раннюю стадию развития Шокового Солдата Расы X, сочетающую органический рост с наступательной способностью. В основном используется для очистки ограниченных пространств или разрушения вражеских формирований.

ПРИМЕЧАНИЯ:

- Взрывчатый заряд наносит урон по области действия.
- Низкая скорострельность; требует тщательного прицеливания.
- Может быть опасен при взрыве слишком близко к пользователю.