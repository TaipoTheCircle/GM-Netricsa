ОГНЕННЫЙ ПРИЗРАК С НИЖНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Огненный Призрак
МЕТАБОЛИЗМ:   Нежить, огненная призрачная сущность
ВОСПРИЯТИЕ:   Враждебный моб, Огненный летающий призрак
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Чрезвычайно высокая
ОРУЖИЕ:       Огненное Прикосновение
НАГРАДА:      400
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Это адская разновидность обычного призрака, которую можно встретить только в Нижнем мире. В отличие от своего бледного сородича из Верхнего мира, этот пылающий дух не просто наносит урон - он поджигает жертву, превращая битву в гонку со смертью от огня.

Внешне огненный призрак более агрессивен и опасен. Его прикосновение заставляет своего противника гореть не менее 15 секунд - примерно столько же, сколько длится горение после падения в лаву. В аду, где и без того жарко, такой ожог почти всегда фатален, особенно если рядом нет воды или огнеупорного зелья.

Несмотря на свою огненную природу, эти призраки парадоксальным образом уязвимы к солнечному свету. Как и обычные призраки, они умирают под прямыми солнечными лучами, хотя и не загораются при этом. Они полностью невосприимчивы к огню (что логично), но их убивает обычный дневной свет.

В остальном их поведение идентично обычным призракам: они парят, издают жуткие стоны, атакуют касанием и не сталкиваются с другими существами.

СОВЕТЫ:

- Если ты отправляешься в Нижний мир, будь готов к встрече с этими тварями.
- Огненный призрак - приоритетная цель. Его нельзя игнорировать, так как 15 секунд горения в аду почти гарантированно убьют тебя.
- Носи с собой зелья огнестойкости. Они сделают тебя невосприимчивым к огню и сведут на нет главное оружие призрака.
- Ведро с водой в Нижнем мире бесполезно - вода мгновенно испаряется.
- Используй лук или арбалет, чтобы держать дистанцию. В ближнем бою он тебя подожжёт.
- Если тебя всё же подожгли, попробуй отступить в безопасное место и быстро съесть золотое яблоко или выпить зелье исцеления, чтобы пережить урон от огня.
- В отличие от обычных призраков, эти не появляются в Верхнем мире, даже ночью. Их дом - только ад.
