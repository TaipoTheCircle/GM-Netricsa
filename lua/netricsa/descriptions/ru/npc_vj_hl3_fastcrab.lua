БЫСТРЫЙ ХЕДКРАБ С ЗЕНА, АРКТИЧЕСКАЯ РАЗНОВИДНОСТЬ


КЛАСС:        Хедкраб, Быстрый (арктический)
МЕТАБОЛИЗМ:   Паразит, устойчивый к экстремальному холоду
ВОСПРИЯТИЕ:   Усиленные рефлексы + тепловое чувство
РАЗМЕР:       30 см.
ВЫНОСЛИВОСТЬ: Очень низкая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Сверхбыстрые прыжки, рубящий укус
НАГРАДА:      180
УГРОЗА:       Средняя

ОПИСАНИЕ:

Арктическая разновидность быстрого хедкраба с Зена - это ещё более тревожная эволюция и без того агрессивного паразита. Попав в холодные биомы Земли, этот вид оброс коротким, ледяным покровом, который защищает мышцы от переохлаждения.

Его тело остаётся поджарым и гибким, а конечности развиты для резких, многократных прыжков даже по льду и снегу. Благодаря изменённой мускулатуре арктический быстрый хедкраб способен отталкиваться от скользких поверхностей, меняя направление прямо в полёте, что делает его атаки особенно труднопредсказуемыми.

При заражении хозяина он создаёт арктического быстрого зомби - более выносливого к холоду и менее чувствительного к боли, чем его умеренный аналог. Такие зомби способны быстро преодолевать снежные участки и карабкаться по обледенелым конструкциям, где обычные заражённые теряют сцепление.

СОВЕТЫ:

- Не стой на месте: их скорость рассчитана на статичную цель.
- Стреляй на опережение - они часто меняют траекторию прыжка.