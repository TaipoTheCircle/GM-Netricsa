СИНЯЯ ЛАБОРАТОРИЯ ПО ИССЛЕДОВАНИЮ ИНОПЛАНЕТЯН ЧЁРНОЙ МЕЗЫ, ВЫБОР БЕЗ ПРАВИЛЬНОГО ОТВЕТА


Сэм, мы внутри. Благодаря учёным. Тем, кого нашли, уговорили и провели через стрельбу, взрывы и собственные панические атаки. Они привели нас к синему входу, открыли двери... и упёрлись в стену. Потому что они знают, где выход. Примерно. Но не знают, как до него добраться. А те, кто знают точно - заперлись с другой стороны. В синей лаборатории. В центре. В окружении H.E.C.U.

И вот теперь ирония судьбы: учёные, которые нам нужны, закрылись внутри. От нас и от морпехов конкретно, а от всех. Захлопнули дверь с той стороны, где мы должны быть, а сами остались с той, где выход. Но проблема в том, что они не могут открыть дверь изнутри. Она заперта. Кем-то. Может, системой безопасности. Может, случайно. А может, они сами её заперли, когда услышали стрельбу, и теперь сидят и боятся. В общем, классика.

Теперь у нас есть два варианта. Оба - с подвохом. Оба - с риском для жизни. Оба - так себе. Но выбора нет.

Вариант первый: нажать кнопку.

Где-то на мостике, прямо в зоне видимости морпехов, висит заветная кнопка. Та, что откроет дверь. Нажать её легко. Проблема в том, что до неё нужно дойти. А морпех, который её охраняет, не спит, не курит и не смотрит в другую сторону. Он стоит. Смотрит. И готов стрелять в любого, кто приблизится.

Плюсы: быстро. Минусы: шум, стрельба. Но дверь откроется, учёные смогут выйти, и мы пойдём дальше.

Вариант второй: лезть через канализацию и вентиляцию.

Обходной путь. Прыгнуть вниз, в воду. Холодную, мутную, с неизвестным содержимым. Проплыть по канализации, найти лестницу, подняться, проникнуть в вентиляцию и уже через решётку попасть в комнату с учёными.

Проблема: дверь изнутри всё равно не открыть. Нужно нажать экстренную кнопку. Ту, что запускает сирены. И открывает всё, что было закрыто. Включая две капсулы с инопланетными пехотинцами, которые стоят прямо в этой комнате.

То есть: мы нажимаем кнопку - дверь открывается - сирены воют - капсулы шипят - пехотинцы вылезают - учёные оказываются с ними лицом к лицу.

Плюсы: тихо, незаметно, без лишних трупов (пока). Минусы: очень большой риск для учёных. Они не бойцы. Они - мишени.

Что собственно делать?

Сэм, я просчитала оба варианта. Шансы на выживание учёных при первом - около 70%, если мы быстро уберём морпеха и заберём их. При втором - около 40%, потому что пехотинцы окажутся ближе к ним, чем мы.

Но первый вариант - это прямой бой с H.E.C.U. Второй - почти гарантированная смерть учёных, если мы не успеем. Я бы выбрала первый. Ты любишь стрелять. Я люблю, когда мы не рискуем теми, кого спасаем.
