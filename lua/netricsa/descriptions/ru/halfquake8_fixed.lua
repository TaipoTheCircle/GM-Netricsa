ШОУ HALF-QUAKE, ПОБЕГ ИЗ ЛАБИРИНТА


Насчёт Охраника...

Каменная глыба решила всё быстрее любого диалога. Один охранник, две крысы и целый набор несказанных фраз теперь равномерно распределены по полу. Шоу не любит свидетелей. И уж точно не любит, когда у них появляется шанс что-то рассказать.

Ты вошёл через комнату охраны. Ирония плотная, почти материальная. Отсюда начинается лабиринт.

Он живой.

Не в поэтическом смысле - в практическом.

Коридоры петляют, стены сходятся под странными углами. Силы Ментала уже здесь. Вортигонты тоже.

Где-то наверху - кнопки управления испытаниями. Центральный пульт. Мозг этой мясорубки. Если верить логике, а она здесь редкий гость, именно оттуда переключают ловушки, открывают и закрывают пути, решают, кто сегодня "порадовал зрителей".

Задача простая по формулировке и неприятная по исполнению:

выбраться.

А если получится - ещё и выключить это место хотя бы на пару секунд.

Двигайся, Сэм.

Лабиринты не терпят сомнений.

И этот особенно не любит тех, кто думает слишком долго.