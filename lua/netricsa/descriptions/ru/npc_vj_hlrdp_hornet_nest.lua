УЛЕЙ ХОРНЕТОВ С ЗЕНА


КЛАСС:         Улей хорнетов
МЕТАБОЛИЗМ:    Биологический Организм Зена
ВОСПРИЯТИЕ:    Стационарный спаунер, Органическая Турель
РАЗМЕР:        ~2 м.
ВЫНОСЛИВОСТЬ:  Высокая 
ВРАЖДЕБНОСТЬ:  Абсолютная
ОРУЖИЕ:        Хорнеты
НАГРАДА:       150
УГРОЗА:        Высокая

ОПИСАНИЕ:

Это огромный органический нарост, который можно найти на стенах, полу и потолке (и, вероятно, в других местах, куда добрались существа Зена). Внешне он напоминает конус. Внутри этого нароста копошатся маленькие, но крайне неприятные создания - хорнеты.

Принцип действия улья прост и ужасен. Как только любая живая цель приближается к улью, он активируется. Один из хорнетов-смертников вырывается из нароста и летит прямо в цель.

Хорнет-смертник живёт ровно столько, сколько нужно для одного полёта. Он умирает после атаки, но улей производит их в неограниченном количестве. Через секунду после уничтожения одного хорнета из улья вылетает следующий. И так до бесконечности, пока улей не будет уничтожен или пока цель не уйдёт из зоны поражения.

СОВЕТЫ:

- Улей нужно уничтожать на расстоянии. Взрывчатка или тяжёлое оружие - лучшие друзья.
- Не подходи близко к улью. Хорнеты вылетают с небольшой задержкой, но если ты в зоне поражения - увернуться сложно.
- Одиночный хорнет слаб, но бесконечный поток изматывает. Убей улей, и проблема исчезнет.
- При уничтожении улей разваливается на куски плоти, иногда выделяя облако кислоты.
- Улей не двигается и не убегает. Это стационарная ловушка, но очень эффективная.
