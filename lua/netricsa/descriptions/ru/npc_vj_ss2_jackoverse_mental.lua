РАЗМЕР:       Неизвестен
ВЫНОСЛИВОСТЬ: Практически Неограниченная
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Псионические Способности, Древняя Магия, Технологии Хум-Тах, Бесчисленные Армии
НАГРАДА:      Мир
УГРОЗА:       Максимальная

ОПИСАНИЕ:

Ментал, также известный под истинным именем Тах-Ум, считается величайшей угрозой разумной жизни во всей известной Галактике. Он является последним известным представителем древней расы бессмертных богоподобных существ Хум-Тах, некогда владевших знаниями, значительно превосходящими современные технологии большинства цивилизаций.

Возраст Ментала исчисляется миллионами лет. За это время он покорил или уничтожил бесчисленное количество миров, стирая с лица Вселенной целые цивилизации. Захваченные народы либо полностью истребляются, либо становятся частью его бесконечной армии после биологической, механической или генетической модификации.

Большинство известных армий Ментала состоит не из добровольцев, а из порабощённых существ. Многие формы жизни были искусственно изменены специально для ведения войны. Пленные нередко подвергаются жестоким экспериментам или казням, после чего их тела используются для создания новых боевых единиц. Именно таким образом появились многочисленные безголовые солдаты, камикадзе и другие представители его войск.

Несмотря на практически безграничную власть, сам Ментал крайне редко появляется на поле боя лично. Вместо этого он руководит войной через генералов, полководцев и многочисленных наместников, координируя действия своих армий при помощи колоссальных псионических способностей. Наблюдаемая форма представляет собой лишь гигантскую тёмную проекцию, тогда как истинный облик Тах-Ума остаётся неизвестным.

Помимо невероятного интеллекта, Ментал владеет древней магией Хум-Тах, технологиями исчезнувшей цивилизации и псионическими способностями, позволяющими воздействовать на разум других существ, управлять огромными армиями и координировать вторжения сразу в нескольких звёздных системах.

За тысячелетия войн единственным противником, сумевшим неоднократно разрушить его планы, стал Сэм "Серьёзный" Стоун (Ты). Именно неспособность окончательно устранить одного человека вынудила Ментала перейти к наиболее радикальным методам ведения войны, включая уничтожение целых планет.

Несмотря на всю собранную информацию, происхождение Хум-Тах, предел возможностей Тах-Ума и место его истинного пребывания до сих пор остаются неизвестными.

