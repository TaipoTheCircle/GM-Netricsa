ГИБРИДНЫЙ СОЛДАТ OVERWATCH С ДРОБОВИКОМ АЛЬЯНСА


КЛАСС:        Гибридный солдат Overwatch с дробовиком Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Штурмовое универсальное подразделение ближнего боя Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Боевой дробовик SPAS-12, гранаты, Электрошокер
НАГРАДА:      650
УГРОЗА:       Высокая

ОПИСАНИЕ:

Гибридный солдат Overwatch с дробовиком - это модификация стандартного бойца с дробовиком, адаптированная под агрессивные штурмовые действия на ближней дистанции. Он сохраняет всё снаряжение обычного гибридного солдата, включая усиленную броню и устройство ночного видения, но его роль меняется радикально.

Оранжевый визор сразу выдаёт назначение бойца. SPAS-12 используется для молниеносного подавления противника в коридорах, тоннелях, жилых блоках и технических помещениях. Альянс применяет таких солдат там, где важна скорость и психологический эффект.

Устройство ночного видения позволяет бойцу действовать в полной темноте, а бинокулярный режим помогает быстро находить укрытия и маршруты противника. Электрошокер используется реже, но служит для добивания, устрашения или захвата живых целей.

В бою гибридный дробовик не тратит время на перестрелки. Он сокращает дистанцию, заходит с флангов и превращает замкнутые пространства в смертельную ловушку. Его тактика проста, но крайне эффективна.

СОВЕТЫ:

- Никогда не подпускай его близко - SPAS-12 смертелен почти гарантированно.
- Оранжевый визор хорошо заметен в темноте.
- В узких помещениях отступление безопаснее атаки.