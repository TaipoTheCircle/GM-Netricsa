ОПИСАНИЕ:

Тор Эриксон, более известный как Директор EdenSys BioTech, является высшим руководителем корпорации и человеком, ответственным за бесчисленные эксперименты над клонами, тотальный корпоративный контроль и устранение всех, кто представляет угрозу интересам компании.

Несмотря на своё положение, Тор не является профессиональным бойцом. Он избегает непосредственного участия в сражениях и предпочитает управлять войсками из безопасного места. Однако если обстоятельства вынуждают его выйти на поле боя, он делает это внутри тяжёлого четвероногого боевого меха, разработанного специально для защиты высшего руководства корпорации.

Мех представляет собой массивную шагающую платформу, оснащённую мощными сервоприводами и тяжёлым вооружением. Основной способ атаки заключается в запуске последовательных залпов самонаводящихся ракет, способных накрывать большие площади и вынуждать противников постоянно менять позицию.

Если цель подходит слишком близко, мех резко ускоряется и идёт на таран. Масса машины настолько велика, что столкновение способно мгновенно уничтожить большинство противников или отбросить их на значительное расстояние.

Главной особенностью машины является встроенный генератор силового поля. После активации вокруг меха формируется красный энергетический барьер, полностью блокирующий любой входящий урон. Пока поле активно, Тор становится практически неуязвимым и продолжает вести огонь без риска для себя. Генератор работает ограниченное время, после чего требует перезарядки.

СОВЕТЫ:

- Во время работы силового поля не расходуй боеприпасы впустую. Дождись его отключения.
- Постоянно двигайся, чтобы избежать попадания под ракетные залпы.
- Никогда не стой перед мехом во время его разгона: таран практически всегда смертелен.
- Используй укрытия между залпами ракет и атакуй только в моменты, когда защитное поле отключено.
- Несмотря на мощь машины, сам Тор полностью зависит от своего меха. После его уничтожения директор становится значительно уязвимее.
