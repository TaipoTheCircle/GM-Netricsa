ШОУ HALF-QUAKE, АД


Я просканировала местность.

И знаешь, Сэм, вот это по-настоящему портит атмосферу шоу.

В стороне, за декорациями боли и дизайнерского безумия, лежит охранник. Не актёр. Не мишень. Живой. Пока ещё. Весь в крови, доспехи больше похожи на реквизит после генеральной репетиции ада. Дыхание рваное. Движения осторожные, как будто само тело боится лишний раз напомнить о своём существовании.

Местные правила просты и омерзительны: Охранников тоже гоняют через испытания. Для мотивации. Для дисциплины. Для статистики. Шоу любит, когда страдают все без исключения. Равенство, так сказать.

Стоит поговорить с ним. Он мог видеть больше, чем диктор с микрофоном. Знать, где кнопки, где ловушки, где зрители особенно любят аплодировать. И, возможно, он ещё жив ровно потому, что ждал кого-то, кто не станет добивать по привычке.

А если сможешь помочь...

Что ж.

Это место называет себя Адом.