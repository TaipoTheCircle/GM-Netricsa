ОПИСАНИЕ:

Элитный генерал Overwatch - высший полевой командир Альянса, сочетающий в себе тактический интеллект, боевой опыт и полную аугментацию тела. Его внешний вид намеренно внушает страх и подчинение: чёрная беретка поверх тактического шлема с единственным светящимся белым визором создаёт ощущение холодного, нечеловеческого наблюдения.

Он носит камуфлированную куртку с чёрно-зелёными узорами и длинный тёмный плащ с золотой окантовкой - символ ранга и абсолютной власти над войсками. Плащ также скрывает дополнительную броню и командное оборудование.

В бою генерал не ограничивается командованием. Он активно участвует в столкновениях, используя AR2 с модифицированными режимами стрельбы и улучшенной стабилизацией. Его импланты позволяют обрабатывать тактические данные в реальном времени и корректировать действия отрядов.

Присутствие генерала на поле боя резко повышает эффективность всех подразделений Overwatch. Солдаты вокруг него действуют быстрее, точнее и агрессивнее, словно подчиняясь не приказам, а самому алгоритму войны.

