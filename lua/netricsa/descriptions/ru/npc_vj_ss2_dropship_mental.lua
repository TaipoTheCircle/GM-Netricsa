ДЕСАНТНЫЙ ЧЕЛНОК APC 404-08180


КЛАСС:        Десантный Челнок APC 404-08180
МЕТАБОЛИЗМ:   Воздушная Транспортная единица, бронированная десантная капсула
ВОСПРИЯТИЕ:   Десантный транспорт / Воздушный бронетранспортёр
РАЗМЕР:       6 м.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Отсутствует
НАГРАДА:      100
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это настоящий летающий броневик с пятизвёздочным комфортом. Ментал, который обычно не заботится о благополучии своих солдат, здесь почему-то расщедрился. Внутри корабля есть всё: джакузи, кожаные кресла, плазменный телевизор и место для двух дюжин бронированных солдат. Стены настолько толстые, что никакие атаки не беспокоят тех, кто внутри.

Внешне Десантный Челнок выглядит как массивная серо-оранжевая летающая коробка с двигателями.  Оружия на корабле нет - он не предназначен для боя, его задача доставить солдат в точку назначения и улететь.

СОВЕТЫ:

- У Челнока нет оружия. Можно подойти вплотную и расстреливать высаживающихся солдат прямо у трапа.
- Уничтожить Челнок можно, но сложно. Требуется несколько попаданий из ракетницы. Лучшее средство - CH-181 Ховербайк с его высокой скорострельностью.
- Лучшее время для атаки - пока Челнок летит. Он медленно движется по прямой и не может ответить.
- Не трать время на уничтожение корабля во время высадки. Проще убивать выпрыгивающих солдат - так ты сохранишь боеприпасы и не будешь отвлекаться от основной угрозы.
- Помни: внутри джакузи. Если когда-нибудь захватишь этот Челнок, сначала проверь, не забыл ли Ментал там бутылочку шампанского.
