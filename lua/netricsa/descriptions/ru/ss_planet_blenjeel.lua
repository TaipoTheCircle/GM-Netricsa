БЛЕНДЖИЛ


ПЛАНЕТА:        Бленджил
ТИП:            Планета земного типа, Пустынная планета
КЛИМАТ:         Засушливый, суровый
СПУТНИКИ:       Неизвестно
ЛАНДШАФТ:       Обширные подвижные пустыни, нестабильные скальные обнажения
РАСПОЛОЖЕНИЕ:   Между планетами Андо и Котлис, вдали от крупных гиперпространственных маршрутов
МЕСТНЫЕ РАСЫ:   Песчаные копатели (гигантские червеобразные существа)
ПРИНАДЛЕЖНОСТЬ: Нейтральная территория, внесённая в базы Новой Республики как опасная планета
МЕСТА:          Зона крушения торгового судна, Место падения челнока "Явинский гонец II"

ОПИСАНИЕ:

Пустынная планета, затерянная вдали от оживлённых космических трасс между системами Андо и Котлис. Её поверхность представляет собой бескрайние песчаные моря, обладающие уникальной особенностью - песок здесь находится в постоянном движении. Ландшафт непрерывно меняется: скалистые выступы, казавшиеся надёжными в один день, на следующий могут бесследно уйти в песчаную пучину.

Главная опасность Бленджила скрывается под его зыбучими песками. Планета населена гигантскими песчаными копателями - чудовищными существами, обитающими в глубине пустыни. Эти создания стремительно появляются из-под земли, атакуя ничего не подозревающую добычу из засады. Их постоянные перемещения и являются основной причиной нестабильности поверхности: именно копатели, роя свои туннели, заставляют песок двигаться и поглощать скальные обнажения.

Через десять лет после битвы при Эндоре торговое судно было атаковано имперским рейдером типа "Обездвиживающий-418" и потерпело крушение на Бленджиле. На спасательную операцию был отправлен начинающий джедай Джейден Корр на корабле "Явинский гонец II". Однако при заходе на посадку его челнок был поражён молнией и также разбился.

Джейден не смог спасти выживших членов экипажа - последний из них был съеден песчаным червём прямо на его глазах. Однако джедаю удалось выжить в пустыне, постоянно избегая контактов с хищниками. Он сумел добраться до обломков торгового судна, снять с него необходимые запчасти, отремонтировать свой шаттл и покинуть планету. После его возвращения на Явин-4 Бленджил был внесён в базы данных Новой Республики как опасная планета, не рекомендованная для посещений.

КЛЮЧЕВЫЕ МЕСТА:

- Зона крушения торгового судна: Район пустыни, где разбилось торговое судно, атакованное имперским рейдером. Место гибели экипажа и источник необходимых для ремонта запчастей.
- Место падения челнока "Явинский гонец II": Участок пустыни, куда рухнул спасательный челнок Джейдена Корра после удара молнии. Отсюда начинался его путь к выживанию.
