ОПИСАНИЕ:

Гражданская оборона Альянса, часто называемая Метрополицией или Метрокопами, служит первой линией правопорядка в городах, оккупированных Альянсом. Это обычные новобранцы, прошедшие обучение и преданные иерархии Альянса, которым предоставлены ограниченные аугментации и снаряжение для контроля над гражданским населением.

В их задачи входит патрулирование городских зон, обеспечение комендантского часа, проведение арестов и подавление сопротивления. Подразделения гражданской обороны (ГО) в значительной степени полагаются на запугивание, жестокость и использование своих знаменитых электрошоковых дубинок для усмирения граждан. В бою они действуют как легковооруженная пехота, более опасная в числе, чем поодиночке.

Несмотря на ограниченную броню и меньшую выносливость по сравнению с солдатами Overwatch, подразделения гражданской обороны играют важнейшую роль в городском господстве Альянса. Их белые маски и холодные, искаженные радиопомехами голоса стали символом угнетения по всей Земле.

СОВЕТЫ:

- Слабы по отдельности - воспользуйтесь их низкой выносливостью.
- Они полагаются на пистолеты и пистолеты-пулеметы; в ближнем бою часто используют электрошоковые дубинки.
- Будь готов к засадам и вызовам подкрепления по радиосвязи.
- На их патрульных постах часто можно найти припасы.
