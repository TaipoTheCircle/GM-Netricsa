ОФФИСЫ ЧЁРНОЙ МЕЗЫ, ДОБРО ПОЖАЛОВАТЬ В ХАОС


Хорошие новости. Мы в офисах. Наконец-то. Рабочие столы, переговорки, кофе-машины, которые уже никто не включит, и приятное чувство, что мы всё-таки куда-то пришли.

Лучше новости. Вжик и Циркулярная Пила снова с нами. Наши верные друзья, которые никогда не подводили, если не считать тех трёх случаев. А это значит, что Ментал уже где-то рядом. Или не здесь. Его солдат не видно, зато патроны лежат прямо на полу, словно кто-то раздаёт бонусы перед увольнением. Странно, да? Враг не пришёл, а боеприпасы оставил. Либо он очень щедрый, либо у нас скоро будет причина ими воспользоваться.

Плохие новости. H.E.C.U. уже здесь. Не "скоро прибудут", не "разворачивают силы". Они уже захватили верхние этажи офисов. Те самые этажи, через которые нам нужно пройти, чтобы выбраться из этой истории.

Сценарий знакомый. Морпехи прибыли выполнять приказ: зачистить всё, что движется, и не задавать вопросов. Для них мы - либо выживший учёный (расстрел), либо инопланетный шпион (расстрел), либо просто свидетель (тоже расстрел). Вариантов немного. Они заняли позиции наверху, перекрыли лестницы, установили огневые точки. Сейчас они чувствуют себя хозяевами положения. Они ошибаются.

Ментала нет. Пока. Но патроны не просто так валяются. Значит, либо он уже прошёл здесь и оставил после себя след, либо готовится.

Пошли Тихо. И пусть Вжик сегодня не подведёт.
