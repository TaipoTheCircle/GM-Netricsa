БЕЗГОЛОВАЯ ФЕМИКАНЗЕ


КЛАСС:        Безголовая Фемиканзе
МЕТАБОЛИЗМ:   Кибер-зомби
ОПРЕДЕЛЕНИЕ:  Мертвец, управляемая через Устройство Управления Жизнью (УУЖ)
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Очень низкая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Ручные гранаты
НАГРАДА:      350
УГРОЗА:       Высокая

ОПИСАНИЕ:

Фемиканзе - женский вариант безголового камикадзе с Сириуса, созданный Менталом с помощью УУЖ. Голова отсутствует, поэтому она полностью полагается на дистанционное управление и встроенные алгоритмы агрессии. Основная задача - максимально приблизиться к цели и взорваться вместе с гранатами, удерживаемыми в руках.

Внешне выглядит "модно и угрожающе": футболка имитирует голое тело с красным купальником, джинсы и яркие красные кеды. Бомбы - как аксессуары, но их эффект куда менее декоративный.

СОВЕТЫ:

- Взрывается сразу после смерти.
- Держи дистанцию и используй дальнобойное оружие.
- В группе превращает любой участок в хаотичное поле взрывов.
- Внешность обманчива: стильная футболка и кеды не снижают опасность.