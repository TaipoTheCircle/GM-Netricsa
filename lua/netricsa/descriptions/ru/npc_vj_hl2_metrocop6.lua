МЕТРОКОП-СТРАЖНИК С ЩИТОМ ГРАЖДАНСКОЙ ОБОРОНЫ АЛЬЯНСА


КЛАСС:        Гражданская Оборона, Штурмовой страж
МЕТАБОЛИЗМ:   Человек, Мужской
ВОСПРИЯТИЕ:   Стандартное (визор маски + радиосвязь)
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Тактический щит, электродубинка
НАГРАДА:      350
УГРОЗА:       Средняя

ОПИСАНИЕ:

Метрокоп-Стражник с щитом - специализированный сотрудник Гражданской Обороны, предназначенный для подавления массовых беспорядков и контроля плотных скоплений граждан. В отличие от обычных патрульных, он действует на передней линии, принимая на себя удары, выстрелы и отчаянные попытки сопротивления.

Его чёрная защитная форма украшена жёлтыми вставками на плечах и предплечьях, служащими визуальными маркерами роли и подразделения. Белая маска полностью скрывает лицо, лишая противников возможности считать эмоции или намерения.

Основной элемент снаряжения - серый тактический щит с жёлтым символом Альянса. Щит используется не только для защиты, но и как средство давления: им отталкивают, прижимают к стенам и ломают строй толпы. За щитом Страж продвигается вперёд медленно и неумолимо, вытесняя противников в заранее подготовленные зоны.

Появление Стражей с щитами почти всегда означает начало фазы активного подавления.

СОВЕТЫ:

- Атакуй с флангов или сзади - лобовой штурм малоэффективен.
- Щит снижает урон, но ограничивает обзор владельца.