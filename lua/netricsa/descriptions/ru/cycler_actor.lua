ОПИСАНИЕ:

Актёры-циклеры - это урезанный, связанный со стазисом подвид актёров-женериков, считающийся ранними экспериментальными "неудачными мимиками", которым так и не удалось достичь полной поведенческой репликации.

В то время как классические актёры-женерики могут действовать, думать и использовать способности, основанные на форме, которую они копируют, актёры-циклеры ограничены ограниченными анимационными циклами, имитируя только шаблоны движений, пассивное поведение или запрограммированные жесты.

Некоторые ксенобиологи считают, что актёры-циклеры - это протомимики, существа, которые сканировали биологическую форму лишь однажды, а затем застряли в постоянном «режиме демонстрации», неспособные развиваться или адаптироваться. Другие предполагают, что они были намеренно созданы - биологические манекены, предназначенные для обучения, психологической подготовки или репетиций проникновения.

Актёры-циклеры не могут сражаться, не могут убегать и не могут импровизировать. Они просто повторяют тот поведенческий цикл, в который была "заморожена" их нервная ткань.

Однако, поскольку они имеют ту же морфологию, что и Женерики Актёры, их сходство с людьми, инопланетянами или другими существами может быть пугающе идеальным... пока они не начнут двигаться.

СОВЕТЫ:

- Не паникуйте. Актёры-циклеры безвредны, если ими не управляют извне.
- Обращайте внимание на повторяющиеся циклы движения - в отличие от актёров-женериков, они не могут адаптироваться.
- Уничтожить их легко, но часто не нужно - они не представляют естественной угрозы.