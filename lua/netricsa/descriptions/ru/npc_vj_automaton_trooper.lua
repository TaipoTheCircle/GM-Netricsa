АВТОМАТОН ТРУПЕР С КИБЕРСТАНА


КЛАСС:        Автоматон Трупер
МЕТАБОЛИЗМ:   Механический
ВОСПРИЯТИЕ:   Лёгкая Пехота Автоматонов
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Импульсный пистолет-пулемёт "Сплав", осколочные гранаты, удар рукой
НАГРАДА:      200
УГРОЗА:       Низко-Средняя

ОПИСАНИЕ:

Это самая распространённая и самая слабая боевая единица фракции Автоматонов.

Внешность Трупера: тонкий металлический корпус, человекоподобная форма, светящиеся красные глаза. Цвет тела серый, голова белая с красными глазами. Труперы сделаны из хромированного металла и блестят на свету.

В бою Труперы полагаются на численность. Их тактика проста: увидел Хеллдайвера - стреляй очередями по пять патронов, затем перезаряжайся (4 секунды охлаждения). Если Хеллдайвер прячется за укрытием, Трупер бросает осколочную гранату. Если Хеллдайвер подходит слишком близко - бьёт рукой. При подавлении огнём Трупер начинает хаотично двигать оружием, снижая точность.

СОВЕТЫ:

- Труперы - не приоритетная цель, но их нужно убивать быстро, чтобы они не вызвали подкрепление.
- Голова убивает с одного выстрела, но попасть в неё сложно. Проще стрелять в корпус.
- Отстреленная рука лишает Трупера оружия, и он переходит в ближний бой.
- Отстреленная нога обездвиживает Трупера.
- Взрывчатка (гранаты, РПГ, воздушные удары) эффективна против скоплений.
- Труперы, управляющие турелями в аванпостах Автоматонов, могут быть убиты, а турель можно использовать против врагов.
