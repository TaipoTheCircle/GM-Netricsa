...Вот это уже интересно, Сэм.

Портал сработал. Земля. Гравитация знакомая. Но Чёрной Мезой тут и не пахнет. Зато пахнет зрелищем.

Добро пожаловать в Half-Quake. Садистский шоу-проект, где боль подают как развлечение, а выживание считается плохим тоном. Судя по вступительному монологу диктора, нас ждут три этапа "испытаний". Он, разумеется, имел в виду пытками.

Шоу давно и охотно закупалось у Чёрной Мезы. Турели, зеновские твари, экспериментальное железо. Всё, что можно использовать для причинения страданий под аплодисменты. Кто-то явно решил, что катастрофа - это бизнес-возможность.

Осмотрись.

Позади тебя большая кнопка с символом ">)". Выглядит как приглашение. Впереди дверь. Пока закрыта. Клетка, в которой ты стоишь, ждёт сигнала. Они хотят, чтобы ты нервничал. Чтобы ждал. Чтобы страдал заранее.

И так же диктор сказал, следующее:

"Мы здесь не ради спасения мира". Этим, по его словам, уже занимается Фриман. Или, если быть точнее, первый Сэм.

А ты здесь для другого.

Ты здесь, чтобы страдать.

За "огромную ошибку в своей жизни". Какую именно - не уточняют. Видимо, формат шоу не предполагает деталей. Возможно, за то, что ты вообще существуешь. Или за то, что истребляешь зеновских тварей и всерьёз намерен покончить с Менталом, вместо того чтобы красиво умереть по сценарию.

Плохая новость: шоу построено на этом.

Хорошая новость: они выбрали не того участника.

Есть ещё один нюанс. Ментал уже договорился с организаторами. Его войска теперь часть программы. Официально. С целью "покончить с Сэмом раз и навсегда". Приятно знать, что у тебя стабильная репутация.

Так что вот мой совет, Сэм.

Они хотят страдания - дай им его.

Но не своё.
