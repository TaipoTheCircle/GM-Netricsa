Стрелок РОЯ


КЛАСС:        Стрелок Роя
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Дальнобойный Хищник
РАЗМЕР:       2,1 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Кислотные Сгустки
НАГРАДА:      350
УГРОЗА:       Высокая

ОПИСАНИЕ:

Стрелки занимают в иерархии Роя нишу живой артиллерии ближнего действия. В отличие от трутней, предпочитающих разрывать добычу собственными когтями, эти существа научились решать проблему дистанционно.

Основным оружием Стрелка служат специальные кислотные железы, расположенные в передней части тела. С их помощью существо выбрасывает залпы едкой биологической массы, способной прожигать плоть, лёгкие бронекостюмы и настроение любого, кто оказался под обстрелом.

Наблюдения показывают, что Стрелки редко действуют в одиночку. Обычно они держатся позади основных сил Роя, обстреливая жертву кислотой, пока трутни сближаются для атаки. Подобная тактика выглядит удивительно разумной для вида, который большую часть времени решает проблемы при помощи когтей.

Некоторые исследователи предполагают, что Стрелок является специализированной ветвью развития обычного трутня. Другие считают его самостоятельным видом. К сожалению, ни одна из сторон пока не сумела получить комментарий от самих Стрелков.

СОВЕТЫ:

- Не стой на месте во время обстрела.
- Уничтожай Стрелков раньше, чем обычных трутней.
- Кислотный залп опаснее, чем кажется на первый взгляд.
