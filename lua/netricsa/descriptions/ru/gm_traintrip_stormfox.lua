ПОЕЗДКА НА ПОЕЗДЕ, ПОРА В ПУТЬ


Сэм, нам крупно повезло, что мы вообще сюда попали. Этот состав - настоящий подарок судьбы, хоть и висит, по словам местных инженеров, "на ниточках и молитвах". Садись, поехали смотреть, что тут есть.

Поезд - наше всё. Он тянет за собой кучу вагонов: 3 купейных, 6 плацкартных и один пустой, для разнообразия. Внутри можно открывать двери, включать свет и даже лазить по крыше - лестницы прилагаются.

Главный рычаг в кабине водителя - стоп / пуск, так что ты сам решаешь, когда ехать, а когда выйти и осмотреть окрестности. Маршрут, кстати, не один: есть три сменных петли, так что пейзаж будет меняться.

Мы едем через сельскую местность в город. И так - бесконечно. Петля замкнута, так что можешь расслабиться и смотреть в окно.

Особенности:

В поезде полно кнопок включения / выключения всего подряд. Любишь нажимать на кнопки? Тебе сюда.

Есть секреты. Где именно - не говорят, но без тайников тут явно не обошлось.

Садись в плацкартный или купейный вагон, дёргай рычаг остановки, когда захочешь осмотреться, и не забывай про кнопки. Если техника начнёт глючить — просто скажи спасибо, что мы вообще едем. Погнали, Сэм!
