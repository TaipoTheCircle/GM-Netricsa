ОПИСАНИЕ:

Щитоног Роя "Бета" является специализированной штурмовой особью, предназначенной для прорыва обороны противника и защиты менее устойчивых представителей Роя. Основу его защиты составляют две огромные бронированные конечности, которые способны полностью закрывать большую часть тела существа спереди.

Во время наступления Щитоног инстинктивно удерживает противника перед собой, используя природный щит для отражения атак. Благодаря особенностям строения его лобовая защита способна выдерживать повреждения, смертельные для большинства других организмов Роя.

Защитные пластины одновременно служат и оружием. Резким движением конечностей Щитоног наносит мощные удары, способные отбросить добычу на значительное расстояние. Подобная атака особенно опасна в тесных помещениях, на мостах и возле обрывов.

В отличие от более подвижных представителей Роя "Бета", данная особь полагается не на скорость, а на неумолимое продвижение вперёд. Обнаружив цель, Щитоног будет последовательно сокращать дистанцию, пока не получит возможность нанести удар.

Как и у большинства существ, чрезмерно уверенных в собственной неуязвимости, наиболее уязвимые органы располагаются именно там, где он меньше всего ожидает нападения.

СОВЕТЫ:

- Не атакуй Щитонога в лоб.
- Используй его медлительность для обхода с флангов.
- Избегай узких проходов во время боя.
- Чем больше выглядит его щит, тем меньше ему нравится, когда заходят со спины.
