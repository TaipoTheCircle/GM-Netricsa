ЖЕЛТОГЛАЗЫЙ ЭЛИТНЫЙ СОЛДАТ OVERWATCH С ЩИТОМ АЛЬЯНСА


КЛАСС:        Желтоглазый элитный солдат Overwatch с щитом Альянса  
МЕТАБОЛИЗМ:   Аугментированный человек, Мужчина  
ВОСПРИЯТИЕ:   Штурмовое щитовое подразделение подавления  
РАЗМЕР:       1,8 м.  
ВЫНОСЛИВОСТЬ: Средняя  
ВРАЖДЕБНОСТЬ: Чрезвычайная  
ОРУЖИЕ:       Тактический щит CMB, пистолет с глушителем  
НАГРАДА:      700  
УГРОЗА:       Высокая  

ОПИСАНИЕ:

Желтоглазый элитный солдат с щитом - это элитный боец, намеренно деградированный по экипировке, но не по навыкам. В отличие от обычных элитных солдат, он не носит тяжёлую броню, чтобы выглядеть как стандартный щитоносец и не вызывать паники до момента контакта.

Его жёлтый визор выдаёт принадлежность к элите, но большинство бойцов сопротивления осознают это слишком поздно. Поведение намеренно имитирует стандартного солдата с щитом: медленное продвижение, прикрытие союзников, подавление цели.

Однако в ближнем бою он раскрывает истинную природу. Пистолет с глушителем используется не только для стрельбы, но и как импровизированная дубинка. Он отталкивает противника, сбивает с ног, создаёт дистанцию и затем добивает точным выстрелом в упор. Тактика выглядит издевательски спокойной, будто боец заранее уверен в исходе столкновения.

Альянс использует таких солдат для психологического давления и подавления толп в городских зонах, где важно сохранить контроль, но при этом демонстрировать абсолютное превосходство.

СОВЕТЫ:

- Не пытайся пробивать щит в лоб - обойди или используй взрывчатку.
- Его слабая броня обманчива: навыки элиты делают его крайне опасным.
- Если он подходит слишком близко - готовься к толчку и добивающему выстрелу.
- Уничтожай с флангов или сверху, где щит бесполезен.
