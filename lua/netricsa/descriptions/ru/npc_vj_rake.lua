РЕЙК


КЛАСС:        Рейк
МЕТАБОЛИЗМ:   Падальщик
ВОСПРИЯТИЕ:   Ночной антропоморфный хищник
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Когти, фактор внезапности
НАГРАДА:      100
УГРОЗА:       Высокая

ОПИСАНИЕ:

Рейк - существо, которое существует ровно в той степени, в какой человек готов в него поверить. Городская легенда, обросшая плотью свидетельских показаний, форумных обсуждений и ночных экспедиций сталкеров. Ни одного задокументированного нападения со смертельным исходом, но тысячи рассказов о том, как нечто тонкое, бледное и когтистое стояло в углу спальни и смотрело, пока ты не откроешь глаза.

Согласно мифологии, Рейк - это худой гуманоид с бледной кожей, длинными острыми когтями и иногда собачьими чертами. Обитает в лесах и канализациях, избегает солнечного света из-за тонкого кожного покрова. Активен исключительно ночью. Питается падалью - чаще всего его замечают на обочинах дорог, где он подбирает сбитых животных. Людей боится и старается не встречаться с ними лично.

Существует несколько подвидов. Лесные рейки обитают в глубине густых массивов, предположительно владеют речью и жестами, легко идут на контакт и не представляют опасности. Канализационные - более серые, с хвостом и вытянутой мордой, передвигаются на четвереньках с опорой на передние лапы, как гориллы. Считаются опасными, хотя доказательств нет.

Особое место в легенде занимают ночные визиты. Рейк якобы забирается в дома, встаёт у кровати и смотрит на спящего, дожидаясь пробуждения. Учёные списывают это на сонный паралич: человек видит во сне то, чего боится, а поскольку Рейк страшный, он и является. Сталкеры же ездят по заброшкам и лесам с тепловизорами, надеясь поймать его в объектив.

СОВЕТЫ:

- Если ты в лесу и видишь бледную фигуру на четвереньках, не беги. Рейк быстрее.
- В канализации держись подальше от тёмных ответвлений - канализационные подвиды агрессивнее лесных.
- После встречи с Рейком тебе скорее всего будут сниться кошмары. Это нормально. Через неделю пройдёт. Или не пройдёт.
