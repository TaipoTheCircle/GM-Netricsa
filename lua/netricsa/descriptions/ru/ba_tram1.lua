ЧЁРНАЯ МЕЗА, ВАГОН ОХРАНЫ


Слушай, нас снова телепортировало в прошлое... И теперь ты Сэм‑охранник! Хочешь спросить "Почему?" - Ты в вагончике охраны. Ну и маскировка у тебя - в этой одежде ты выглядишь как... скажем так, нестандартный сотрудник Чёрной Мезы. Но пока учёные глазеют на флаг, а вертолёты кружат над головой, тебе дают шанс пройти незамеченным.

Вокруг - скалы, подозрительный мяч и тишина, которую рвёт только звук близжайшего вертолёта. Сверху, где‑то за стеклом, военные вертушки нарезают круги. Чувствуешь? Воздух гудит от напряжения. Где‑то уже другой Сэм шагает в тестовую камеру - скоро тут всё взлетит на воздух. Но пока... пока это просто поездка.

Смотри по сторонам: учёные болтают, охранник у флага скучает. Всё выглядит буднично. Слишком буднично. Знаем мы эти "спокойные" моменты - всегда чем‑то заканчиваются.

Держи руку на кобуре. Даже если сейчас тихо, завтра может грохнуть так, что этот вагон покажется детской каруселью.

Вперёд, "охранник". Пока ты ещё можешь просто ехать.