ОПИСАНИЕ:

Морозный Зорг-пехотинец - это результат редкого случая, когда зорги решили не загорать, а, наоборот, хорошенько промёрзнуть. Эти подразделения были переброшены Менталом на ледяные планеты и адаптированы к условиям вечной мерзлоты. Их тела покрыты плотной ледяной бронёй, намерзшей слоями прямо на коже и выполняющей роль естественного защитного панциря.

В бою ледяные зорги используют морозные лазерные ружья, стреляющие двумя последовательными синими импульсами. Попадание не столько убивает, сколько замораживает цель, временно сковывая движения и превращая противника в неподвижную мишень. Сам зорг при этом действует неторопливо и хладнокровно, словно никуда не спешит - что, в общем-то, так и есть.

