ХРАМ ХА И ЭША, ОАЗИС


Древний храм, вырубленный в скале, смотрит на нас пустыми глазницами окон. Это не просто руины - это место, которое сирианцы выбрали для своего убежища. Скрытая база. Склад данных об оружии, способном менять ход войны. И всё это - под носом у богов пустыни.

Ха и Эш. Боги западных границ Египта, покровители 7-го нома. Их имена почти стёрты временем, но здесь, в этом храме, они всё ещё правят бал.

Ха - рыжий, как песок. Защитник Египта от врагов с запада. Ему мы и обязаны тем, что оазисы вообще существуют. Эш - его спутник, такой же древний, такой же забытый. Вместе они охраняют границы жизни и смерти. И, вероятно, тот самый чертог, где сирианцы спрятали свои секреты.

Но просто пройти в храм недостаточно. Сирианцы - ребята умные. Они не полагались на замки и засовы. Они создали лабиринт из кнопок, рычагов и тайных механизмов. Только тот, кто найдёт и нажмёт все кнопки, сможет открыть скрытый проход.

А проход этот - в колодце. Да, Сэм. В старом, глубоком колодце, который когда-то давал жизнь оазису. Теперь он заполнен опасной жидкостью. Не водой. Не кислотой. Чем-то, что сирианцы сочли достаточно надёжной защитой для своих секретов.

Кнопки разбросаны по всему храму. За вазами, под камнями, на потолке, в тайных нишах. Одни видны сразу, другие - только если знать, куда смотреть. Нажимать их нужно в определённом порядке? Неизвестно. Скорее всего - все. Или почти все. Сирианцы не любили простых решений.

Прыгать в колодец без защиты - плохая идея. Но если проход откроется, жидкость должна уйти. Или не должна. Узнаем на месте.

Когда проход откроется - спускайся. Быстро, но осторожно. Сирианцы могли оставить там сюрпризы.
