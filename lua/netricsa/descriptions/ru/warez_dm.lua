НЕБОЛЬШОЙ СКЛАД ЧЁРНОЙ МЕЗЫ, СТАРЫЙ И ТЕСНЫЙ


Сэм, нас занесло в место, которое даже по меркам Чёрной Мезы выглядит как забытый чулан. Небольшой склад. Старый. Пыльный.

Вокруг - ящики. Много ящиков. Разной высоты. Кто-то складывал их аккуратно, кто-то - как попало. Некоторые стоят ровными рядами, образуя узкие проходы. Другие навалены друг на друга, создавая лабиринт из теней и мертвых зон. Третьи - просто посреди комнаты, будто их принесли и бросили, забыв, зачем они нужны.

Скорее всего, этот склад старый. Очень старый. Возможно, его построили ещё до того, как Чёрная Меза стала "Чёрной Мезой". Тогда, когда комплекс был просто секретным бункером, а не эпицентром межпространственного хаоса. Но время не пощадило стены, а катастрофы добавили копоти и трещин.
