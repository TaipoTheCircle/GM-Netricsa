ВИНТОВКА SW-022 KAR98K


ОРУЖИЕ:  SW-022 Kar98k (Snow Wolf) — болт-экшн пружинный снайперский реплик-винтовка
ПАТРОНЫ: 6 mm BB (шары)
СС:      Один выстре (ручная взводка перед каждым выстрелом)
ВЕС:     3.3 кг (в зависимости от варианта: ABS / real-wood). 
ДЛИНА:   1100 мм

ОПИСАНИЕ:

Рлегендарный Mauser Kar98k. Модель доступна в корпусе с настоящим деревянным прикладом или в ABS-варианте; выполнена с металлическими внешними деталями и классическим болт-акшн механизмом. Пружинная (air-cocking) конструкция требует взвода затвора перед каждым выстрелом - это даёт простоту обслуживания и надёжность. Подвижный регулируемый hop-up под прицелом повышает точность.

ПРИМЕЧАНИЯ:

- Подходит как для скёрмишинга, так и для коллекций/косплея - хорошее соотношение внешнего реализма и цены. 
- Регулируемый hop-up позволяет использовать более тяжёлые BB (0.25-0.30 g) для улучшения стабильности на дальних дистанциях.