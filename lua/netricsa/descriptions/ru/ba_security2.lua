ЧЁРНАЯ МЕЗА, ВНУТРИ ОХРАННОГО КОМПЛЕКСА


Ну что-ж, ты внутри. Стены давят, воздух пахнет металлом и дезинфекцией - типичный штаб охраны. Теперь надо закрепиться: тут каждый предмет может стать ключом к выживанию. Или к провалу.

Что делать дальше:

Бронежилет

В оружейке - ряды жилетов. Надень один. Не для красоты: когда начнётся заварушка, каждая пластина будет на счету. Да, тяжеловато, но лучше потеть, чем кровоточить.

Туалет (на всякий случай)

Да‑да, даже в апокалипсисе сверх-человеку нужно личное пространство. Но помни: Я всё вижу. Чип в твоём черепе транслирует каждый шаг. Так что не задерживайся - и не пытайся что‑то спрятать в сливном бачке. Я уже в курсе.

Пистолет

Без оружия тут как без рук. В оружейке лежит стандартный служебный пистолет. Возьми, проверь обойму, поставь на предохранитель. Не стреляй без нужды - но и не жди, пока в тебя прицелятся.

Камеры наблюдения

Пульт управления - на втором этаже. Лифт рядом, кнопка вызова подмигивает красным. Поднимайся, садись за мониторы и жди пока всё начнётся.