ВОДИТЕЛЬ БТР АЛЬЯНСА


КЛАСС:        Водитель БТР Альянса
МЕТАБОЛИЗМ:   Искусственный / Механический
ВОСПРИЯТИЕ:   Синтетический управляющий дрон (ИИ минного типа)
РАЗМЕР:       ~0,4 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Нет прямого; управляет пушками и ракетами, установленными на БТР
НАГРАДА:      250
УГРОЗА:       Средне-высокая

ОПИСАНИЕ:

Водитель БТР Альянса - это специализированный сферический дрон, визуально похожий на шаровую мину Альянса, который функционирует как система автопилота для бронетранспортеров при отсутствии человека-пилота или трансчеловека. После подключения сфера интегрируется в системы БТР, предоставляя машине автономное управление вооружением и движением.

В одиночку Водитель практически безвреден, хрупкий и неспособен атаковать. Однако внутри БТР он становится опасным продолжением мощи Альянса, управляя тяжёлыми пушками, станковыми пулемётами и ракетными установками с машинной точностью. После уничтожения или выброса БТР перестаёт функционировать и становится неподвижным.

СОВЕТЫ:

- Сосредоточься на уничтожении самого БТР; Водитель не уйдёт, если его не вынудить.
- Взрывчатые вещества могут вывести Водителя из строя быстрее, чем прямой огонь.
- Если его застанут вне машины, его можно нейтрализовать с минимальными усилиями.
- Не стоит недооценивать БТР, управляемый Водителем - он никогда не промахивается.
