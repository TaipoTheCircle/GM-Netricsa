МЭНХЭК АЛЬЯНСА


КЛАСС:        Мэнхэк Альянса
МЕТАБОЛИЗМ:   Полностью синтетический (дистанционное управление через аркадный интерфейс)
ВОСПРИЯТИЕ:   Автономный/дистанционно управляемый дрон
РАЗМЕР:       0,5 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Вращающиеся лезвия циркулярной пилы
НАГРАДА:      150
УГРОЗА:       Средняя

ОПИСАНИЕ:

Мэнхэк - зловещее изобретение Альянса, небольшой парящий дрон, вооружённый острыми как бритва лезвиями циркулярной пилы, предназначенный для подавления городских беспорядков и преследования беглецов. Его манёвренность позволяет ему проноситься по узким улочкам, вентиляционным шахтам и даже через разбитые окна, превращая целые городские кварталы в зоны поражения.

Большинство горожан не осознаёт, что мэнхэками управляют не солдаты, а гражданские лица. Альянс маскирует систему управления под безобидные игровые автоматы, где граждане считают, что играют в видеоигру ради развлечения, зарабатывая «очки», охотясь на виртуальных повстанцев. На самом деле каждое движение и каждое убийство реальны, а управляемые ими дроны убивают настоящих людей на улицах.

Этот жестокий обман не только отвлекает население, но и обеспечивает Альянсу постоянное наличие «операторов», не рискуя собственными силами. Для обычного гражданского это всего лишь игра, но для жертвы это смерть от тысячи порезов.

СОВЕТЫ:

- Мэнхэки хрупкие - несколько пуль или мощный удар в ближнем бою уничтожат их.
- Их пилы смертоносны на близком расстоянии - соблюдай дистанцию.
