КРИПЕР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Крипер
МЕТАБОЛИЗМ:   растительно-химический
ВОСПРИЯТИЕ:   Растительно-пороховое существо
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Самоподрыв
НАГРАДА:      100
УГРОЗА:       Средне-Высокая

ОПИСАНИЕ:

Крипер - одно из самых опасных и узнаваемых существ Верхнего мира. Внешне он напоминает высокое зелёное существо без рук, с четырьмя короткими ногами. Его тело покрыто камуфляжным узором, позволяющим легко сливаться с травой и листвой, что делает его особенно коварным противником.

Главная особенность крипера - способность к самоподрыву. При приближении к цели он начинает шипеть, после чего взрывается, нанося серьёзный урон противнику и окружающей местности. Сам крипер при этом погибает, не проявляя признаков самосохранения, что делает его скорее живым оружием, чем обычным мобом.

Происхождение криперов остаётся загадкой.

СОВЕТЫ:

- Может подкрасться незаметно.
- Взрыв разрушает блоки и постройки.
- При ударе молнией превращается в заряженного крипера, увеличивая радиус и силу взрыва.
- Держи дистанцию и используй лук.
- Щит значительно снижает урон от взрыва.
- Не сражайся с криперами рядом с постройками - они не ценят архитектуру.
