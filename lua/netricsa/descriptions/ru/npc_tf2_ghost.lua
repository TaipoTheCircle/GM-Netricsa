ОПИСАНИЕ:

Этот NPC с кожей Моссмена - это заменяющий элемент в виде гуманоида, используемый для тестирования и разработки. Хотя она выглядит как союзник-учёный из Восточной Чёрной Мезы, она выполняет только заранее запрограммированные действия - стоит, ходит, поворачивается или реагирует на триггеры. Она не атакует, но может открывать двери, ходить по комнатам и обеспечивать визуальное присутствие для демонстраций.

Считайте её тренировочным манекеном в костюме Моссмена: безвредным, иногда неуклюжим и идеально подходящим для проверки анимации или взаимодействия с окружением.

СОВЕТЫ:

- Полезно для тестирования поведения ИИ без добавления реальных угроз.
- Может запускать заранее запрограммированные события или следовать по маршруту.
- Отлично подходит для наблюдения за взаимодействием гуманоидных моделей с окружением.