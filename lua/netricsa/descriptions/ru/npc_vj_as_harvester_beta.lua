ЖНЕЦ РОЯ "БЕТА"


КЛАСС:        Жнец Роя "Бета"
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Поддержка Улья
РАЗМЕР:       ~2,5 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Очень высокая
ОРУЖИЕ:       Порождение Взрывных Паразитов, Таран, Едкая Кожа
НАГРАДА:      30
УГРОЗА:       Высокая

ОПИСАНИЕ:

Жнец Роя "Бета" является специализированной особью поддержки, предназначенной для снабжения роя живыми боеприпасами. Его массивное брюшко служит инкубационной камерой, внутри которой непрерывно развиваются взрывные паразиты. В отличие от большинства представителей роя "Бета", Жнец редко вступает в прямое столкновение и старается держаться позади основных атакующих особей.

Во время боя Жнец регулярно выпускает взрывных паразитов Роя "Бета". Эти существа ведут себя так же, как обычные паразиты: стремительно прыгают на ближайшую цель и преследуют её с высокой настойчивостью. Однако вместо заражения они детонируют при контакте, разрывая своё тело и поражая всё вокруг взрывом и биологическими тканями.

Даже после смерти Жнец остаётся опасным. Разрушение его организма вызывает аварийное раскрытие инкубационной камеры, из которой вырываются четыре взрывных паразита Роя "Бета", немедленно переходящие в атаку. Если уничтожить Жнеца в непосредственной близости, выжившие паразиты способны закончить то, что не успел их носитель.

Несмотря на сравнительно слабую защиту, Жнец представляет серьёзную угрозу в сопровождении трутней, щитоногов или других бойцов роя. Пока противник отвлечён ближним боем, он успевает создать новую волну паразитов, постепенно превращая поле боя в хаос.

СОВЕТЫ:

- Уничтожай Жнеца как можно раньше, пока он не выпустил слишком много паразитов.
- После его смерти немедленно приготовься к появлению четырёх взрывных паразитов.
- Держи дистанцию.
