РУКО-УЛЕЙ


ОРУЖИЕ:      Руко-Улей (инопланетное органическое оружие)
ПАТРОНЫ:           Хорнеты (самовосполняющиеся)
CC:        Полуавтоматическая или автоматическая, ~300 выстрелов/мин

ОПИСАНИЕ:

Зено-органическое оружие, стреляющее живыми Хорнетами. Боеприпасы восполняются со временем, что обеспечивает бесконечный запас. Может использоваться для самонаводящихся или скоростных атак.

ПРИМЕЧАНИЯ:

- Бесконечный боеприпас благодаря естественному восполнению.
- Самонаводящийся огонь эффективен против подвижных целей.
- Небольшой урон от каждого снаряда.