ГОНЗО-ПИСТОЛЕТ


ОРУЖИЕ:  Гонзо-Пистолет
ПАТРОНЫ: Яйца
СС:      Быстрые одиночные выстрелы

ОПИСАНИЕ:

Гонзо-Пистолет в буквальном смысле напоминает курицу и стреляет яйцами. Как и ожидалось, его яйца мгновенно убивают практически любого злодея, не являющегося боссом, одним выстрелом, что делает его невероятно мощным инструментом зачистки. Выглядит и звучит нелепо - играется точь-в-точь как оружие-затычка, но с серьёзной смертоносностью.

ПРИМЕЧАНИЯ:

- Каждый выстрел расходует одно яйцо; боезапас ограничен.
- Убивает врагов, не являющихся боссами, одним выстрелом; обычно неэффективно против финальных боссов.
- Лучше использовать экономно на важных целях или для зачистки особо опасных стычек.