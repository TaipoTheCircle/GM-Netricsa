ККТ РЕМАСТЕР СТРОЙКИ, НЕДОСТРОЙ


Сэм, помнишь ту самый стройку, где мы начинали? Пустую, бетонную, с парой ангаров и вечной стройкой, которая никогда не закончится? Так вот, кто-то решил, что ему этого мало. И сделал ремастер.

Не перестройку. Не завершение. Именно ремастер. Как у старых игр: те же локации, но текстур побольше, багов поменьше, и добавили пару фишек, чтобы ветераны не скучали.

Стройка по-прежнему не завершена. Но теперь здесь есть улучшения. Серьёзные. И странные. И, как обычно, с подвохом.

Что нового?

Ангар получил дверь.

Да, Сэм. Та самая дыра, в которую мы забегали, чтобы переждать обстрелы, теперь закрывается. И, наверное, ветер не дует.

Тёмная комната - свет и изгнание легенды.

Помнишь ту комнату, где всегда было темно, и мы боялись, что из угла выпрыгнет тот, кого нельзя называть? Так вот, там теперь есть рубильник. Включаешь - и темноты нет. А ещё, по слухам, оттуда изгнали мясного. Не спрашивай кто это. Спрашивать не о чем. Главное - его там больше нет. Или он просто научился прятаться лучше.

Белая комната - риск ослепнуть.

Перед входом висит предупреждение. Всем входящим рекомендуется надевать очки. Потому что там такой свет, что можно навсегда остаться с закрытыми глазами. Я не знаю, кто и зачем сделал комнату, которая может ослепить. Но Сэм, мы бы вошли.

Огнетушители.

Везде. На стенах, в углах, на столах. Видимо, кто-то понял, что на стройке без них нельзя. Или просто вспомнил технику безопасности. В любом случае, если что загорится - не придётся тушить ногами.

Колодцы и люки.

Да.

Парковка.

Наконец-то можно припарковать машину. Не в поле, не в ангаре, а на настоящей парковке. С разметкой. Мест много. Машин, правда, нет. Но мы можем пригнать свои.

И много чего ещё.

Перечислять долго. Слишком долго.

Осмотри стройку заново. Многое изменилось. Что-то стало лучше, что-то - страннее. Найди очки перед белой комнатой. Зажги свет в тёмной. Загляни в колодец. Припаркуй машину. И помни: огнетушители - это не игрушки. Пока не начнётся пожар.

Пойдём гулять по обновлённой стройке. Только очки не забудь. А то ослепнешь - кто тогда будет стрелять в хедкрабов?
