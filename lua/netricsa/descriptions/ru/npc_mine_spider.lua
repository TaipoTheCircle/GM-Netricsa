ПАУК С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:         Паук
ВОСПРИЯТИЕ:    Враждебный моб
МЕТАБОЛИЗМ:    Членистоногое
РАЗМЕР:        0,9 м.
ВЫНОСЛИВОСТЬ:  Средняя
ВРАЖДЕБНОСТЬ:  Переменная
ОРУЖИЕ:        Укусы, прыжки, лазание по стенам
НАГРАДА:       120
УГРОЗА:        Средняя

ОПИСАНИЕ:

Паук - классический ночной кошмар Верхнего мира. Днём он ведёт себя почти прилично и обычно игнорирует всех, будто притворяется обычным элементом ландшафта. Но с наступлением темноты вспоминает, что он вообще-то хищник, и начинает активно атаковать.

Главная особенность паука - умение лазать по вертикальным поверхностям. Стены, заборы и дома для него не препятствие, а приглашение.

Иногда пауки появляются с дополнительными "пассажирами" - скелетами, образуя знаменитого паучьего жокея, что резко повышает уровень проблем на квадратный метр.

СОВЕТЫ:

- Днём паук нейтрален - можно атаковать первым без риска.