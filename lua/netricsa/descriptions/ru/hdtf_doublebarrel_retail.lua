ДВУСТВОЛКА


ОРУЖИЕ:         Двустволка
ПАТРОНЫ:        двенадцатого калибра
СC:             два патрона, ручная перезарядка
ДЛИНА СТВОЛА:   50,8 см.

ОПИСАНИЕ:

Стандартный двуствольный дробовик двенадцатого калибра. Эффективен при стрельбе по врагам среднего размера на близких расстояниях. При стрельбе на большие расстояния неэффективен - кучность попаданий снижается. 

ПРИМЕЧАНИЯ:

- Низкая скорость ведения огня из-за ручной перезарядки.
- Неэффективна на больших расстояниях.
