УТОПЛЕННИК С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Утопленник
МЕТАБОЛИЗМ:   Водная Нежить
ВОСПРИЯТИЕ:   Нейтрально-враждебный моб
РАЗМЕР:       1,9 м.
ВЫНОСЛИВОСТЬ: Высокая 
ВРАЖДЕБНОСТЬ: Переменная
ОРУЖИЕ:       Руки, трезубе
НАГРАДА:      150
УТРОЗА:       Высокая

ОПИСАНИЕ:

Это результат неудачного купания обычного зомби. Если зомби проведёт в воде больше 30 секунд, он начинает трястись, пузыриться и через 15 секунд превращается в эту подводную тварь. Процесс необратим, так что если увидел трясущегося зомби в воде - готовься к встрече с утопленником.

Одет в рваные лохмотья, иногда с удочкой, трезубцем или раковиной наутилуса в руках.

Обитают утопленники в океанах, реках и болотах. Чаще всего их можно встретить на дне водоёма, где они бесцельно бродят, периодически булькая и выпуская пузырьки воздуха.

Поведение утопленника зависит от времени суток и локации. Днём на суше, утопленники нейтральны - они не нападают, даже если их ударить первым. Но стоит жертве хотя бы частично погрузиться в воду - вся стая мгновенно становится враждебной. Ночью или во время грозы утопленники выходят на охоту и преследуют жертв даже на суше, как обычные зомби.

Утопленники атакуют железных големов, маленьких черепашат, топчут черепашьи яйца (даже через узкие проходы), а если убьют деревенского жителя - тот может превратиться в зомби-жителя с шансом до 100%. При этом они невосприимчивы к атакам стражей и древних стражей.

Как и вся нежить, утопленники горят на солнце, если оказываются на суше днём. Но в отличие от зомби, они сразу же ищут ближайшую воду, чтобы спрятаться. В воде могут плавать с впечатляющей скоростью.

Иногда утопленники появляются верхом на курице, создавая редкого утопленника-наездника.

При смерти утопленник может оставить гнилую плоть, медные слитки, а если повезёт - трезубец или раковину наутилуса.

СОВЕТЫ:

- Днём утопленники на суше безопасны - можно пройти мимо.
- Услышал бульканье и увидел трясущегося зомби в воде - отойди подальше, скоро там будет утопленник.
- Главная угроза - утопленник с трезубцем. Не стой на открытом месте, используй укрытия.
- В бою днём, на суше утопленник горит на солнце - можно просто подождать.
- Если сражаешься в воде, старайся быстро выходить на сушу - утопленники не любят преследовать далеко.
- Щит спасает от бросков трезубцем.
- Для гарантированного получения трезубца используй зачарование "Добыча III" и убивай только тех утопленников, у кого трезубец в руках.
- Утопленники не водятся в тёплых океанах - там безопасно .
