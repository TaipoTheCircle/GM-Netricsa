КАНАЛЫ ЧЁРНОЙ МЕЗЫ, ОСОБЫЙ УЧАСТОК


Сэм, слушай внимательно. Путь к взрывному ящику не так прост, как кажется. Тебе нужно пройти в обход - там, где ты сейчас стоишь, "маленький участок" таит в себе сюрприз.

Из теней могут вылезти:

патроны - чтобы не остаться без огня;

броня - вдруг пригодится в ближнем бою;

оружие - возможно, твой старый друг "Вжик" обзаведётся компанией.

Но вместе с припасами придёт и орда Ментала. Они вылезут из всех пространственных щелей, как только почувствуют твоё присутствие. Будь готов к атаке!

Дальше - страшнее.

Вдали, за ржавыми трубами, на мосту ты увидишь охранника. Бедняга в ловушке: зомби тащат его в разные стороны, словно дети, спорящие за игрушку. Он кричит, но никто не придёт на помощь. 

После моста - путь к ящику.