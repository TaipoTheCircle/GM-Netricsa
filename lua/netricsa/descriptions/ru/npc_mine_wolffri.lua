ВОЛК С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Нейтральное / Приручаемое животное
МЕТАБОЛИЗМ:   Теплокровное млекопитающее
ВОСПРИЯТИЕ:   Острый слух, развитое обоняние
РАЗМЕР:       ~0,9 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Умеренная
ОРУЖИЕ:       Клыки
НАГРАДА:      60
УГРОЗА:       Средняя

ОПИСАНИЕ:

Волки - дикие хищники Верхнего мира Minecraft, обитающие в лесах, тайге и заснеженных биомах. В нейтральном состоянии они не нападают, но становятся крайне агрессивными, если их спровоцировать или атаковать сородича.

Приручённый волк превращается в верного боевого спутника. Он следует за хозяином, защищает его в бою и атакует цели, которые ударил его хозяин.

Состояние здоровья волка легко определить по положению хвоста: чем ниже хвост, тем сильнее он ранен.

СОВЕТЫ:

- Для приручения используй кости.
- Корми волка мясом для лечения и разведения.
- Волки особенно эффективны против скелетов и зомби, но уязвимы к лаве и взрывам.