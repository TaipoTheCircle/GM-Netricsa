ТРУТЕНЬ РОЯ "БЕТА", КРУПНЫЙ


КЛАСС:        Трутень Роя "Бета", Крупный
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Тяжёлый Стайный Хищник
РАЗМЕР:       3 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Очень Высокая
ОРУЖИЕ:       Когти, Зубы, Масса Тела
НАГРАДА:      600
УГРОЗА:       Высокая

ОПИСАНИЕ:

Крупный трутень представляет собой необычно разросшуюся особь Роя "Бета". В отличие от элитных трутней, отличающихся окраской, этот подвид внешне почти не отличается от обычных особей. Та же зелёная шкура, те же три глаза и та же непреодолимая тяга разорвать всё живое на части. Разница становится заметна лишь тогда, когда существо встаёт рядом с обычным трутнем.

Причины столь аномального роста остаются неизвестными. Согласно одной из теорий, некоторые личинки получают значительно больше питательных веществ, чем остальные. Согласно другой теории, Рой просто не умеет вовремя останавливаться.

Увеличенные размеры позволили этим существам обзавестись более мощной мускулатурой и значительно укрепить внутренние органы. В результате крупный трутень способен выдерживать повреждения, которые давно бы убили обычную особь, и наносить удары такой силы, что они способны отбросить жертву на несколько метров.

Несмотря на внушительные габариты, крупные трутни сохранили большую часть подвижности своих меньших собратьев. Это особенно неприятное обстоятельство для всех, кто рассчитывал, что такой монстр будет двигаться медленно и степенно.

СОВЕТЫ:

- Не позволяй размерам обмануть себя. Он быстрее, чем кажется.
- Держи дистанцию и используй мощное оружие.
- Если рядом замечены обычные трутни, сначала устрани наиболее опасную цель.
- Когда Рой "Бета" решает сделать трутня побольше, страдают обычно все окружающие.
