КСЕНОКЛЕЩ РОЯ


КЛАСС:        Ксеноклещ Роя
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Симбиотический Организм
РАЗМЕР:       0,4 м.
ВЫНОСЛИВОСТЬ: Крайне Низкая
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Атака В Прыжке, Кислотный Взрыв
НАГРАДА:      25
УГРОЗА:       Средняя

ОПИСАНИЕ:

Ксеноклещ представляет собой небольшую паразитическую форму жизни, выращиваемую Жнецами для защиты и нападения. Большую часть своей короткой жизни он проводит рядом с родительской особью, ожидая появления подходящей цели.

Несмотря на скромные размеры, ксеноклещ крайне опасен. Обнаружив жертву, он совершает стремительный прыжок и пытается приблизиться вплотную. В отличие от большинства существ Роя, его основной целью является не убийство противника, а собственная гибель в непосредственной близости от него.

Тело ксеноклеща заполнено нестабильными кислотными соединениями. После смерти или получения критических повреждений существо взрывается, разбрасывая вокруг едкую биомассу. Контакт с этими веществами способен вызвать тяжёлые химические ожоги даже через несколько секунд после детонации.

Продолжительность жизни ксеноклеща крайне мала. По всей видимости, эволюция данного организма полностью отказалась от долгосрочного выживания в пользу максимальной эффективности во время единственной атаки.

СОВЕТЫ:

- Уничтожай ксеноклещей на расстоянии.
- Не позволяй им приближаться вплотную.
- После гибели существа держись подальше от кислотных остатков.
- Если создание живёт только для того, чтобы взорваться рядом с тобой, спорить с его жизненными целями уже поздно.
