ЧЁРНАЯ МЕЗА, АВИАУДАР


Поздравляю - ты наконец-то выбрался наружу. Свежий воздух, открытое небо... и V-22 Оспрей кружит над головой, выплевывая отряды морпехов, словно конфеты из торгового автомата.

Это первая крупномасштабная зона развёртывания военных в Чёрной Мезе, и они относятся к ней как к плацдарму.

Но вот в чём загвоздка: интенсивная бомбардировка ведётся не с Osprey. Датчики подтверждают удары дальнобойной артиллерии - или, возможно, ракет класса "воздух-земля" - далеко за пределами периметра базы. Тот, кто нажимает на курок, держится на расстоянии, а это значит, что кто-то очень, очень хочет превратить это место в руины, не запачкав при этом руки.

Опасность для окружающей среды: открытое пространство. Укрытий мало, что делает тебя отличной мишенью как для морпехов, так и для дальнобойного огня. С другой стороны, "Оспрей" вынужден лететь низко, чтобы высаживать своих бойцов, что даёт вам отличную возможность напомнить ему, почему летающие машины и Сэм несовместимы.

Рекомендация: относись к открытому небу как к враждебной территории. Используй любые укрытия, которые сможешь найти, прореживай отряды по мере их высадки и будь бдителен.