ЖЕЛТОГЛАЗЫЙ ЭЛИТНЫЙ СОЛДАТ OVERWATCH АЛЬЯНСА


КЛАСС:        Желтоглазый элитный солдат Overwatch Альянса  
МЕТАБОЛИЗМ:   Аугментированный человек, Мужчина  
ВОСПРИЯТИЕ:   Скрытое элитное ударное подразделение Альянса  
РАЗМЕР:       1,8 м.  
ВЫНОСЛИВОСТЬ: Очень высокая  
ВРАЖДЕБНОСТЬ: Чрезвычайная  
ОРУЖИЕ:       Импульсная винтовка AR2, энергетические шары  
НАГРАДА:      850  
УГРОЗА:       Очень высокая  

ОПИСАНИЕ:

Желтоглазый элитный солдат Overwatch - это элитный боец, замаскированный под стандартного солдата Альянса. Его броня имеет обычную сине-белую раскраску рядовых бойцов, что позволяет ему растворяться в строю и вводить противника в ложное чувство безопасности.

Единственный выдающий признак - жёлтый визор. По внешнему виду он напоминает элитных тюремных солдат Нова Проспекта, однако по статусу числится среди обычных подразделений. Это сделано намеренно: Альянс внедряет таких бойцов в стандартные отряды, чтобы незаметно повысить их боевую эффективность.

По уровню имплантов, реакции и тактической подготовки он полностью соответствует элитным солдатам. В бою действует хладнокровно, координирует огонь обычных солдат и первым вступает в контакт с особо опасными целями. Энергетические шары используются для выбивания противника из укрытий, после чего он добивает цель точным импульсным огнём.

СОВЕТЫ:

- Если видишь большой жёлтый визор среди обычных солдат - это плохая новость.
- Он выглядит как рядовой, но стреляет как элитный.
- Уничтожай его первым - его присутствие делает весь отряд опаснее.
- Не путай с тюремным элитным солдатом: этот действует в поле и более мобильный.
