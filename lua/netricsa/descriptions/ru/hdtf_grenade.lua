ГРАНАТА АЛЬЯНСА


ОРУЖИЕ:     Граната Альянса
ПАТРОНЫ:    Взрывчатый заряд
СС:         Ручной бросок, ~10 бросков/мин.

ОПИСАНИЕ:

Стандартное взрывное устройство, используемое солдатами Альянса. Предназначено для тактического подавления, взрывается при ударе или по истечении короткого времени, нанося большой урон в небольшом радиусе. Идеально подходит для очистки помещений или разрушения вражеских формирований.

ПРИМЕЧАНИЯ:

- Эффективно для блокирования территории и борьбы с живой силой.
- Короткий запал требует тщательного расчета времени.
- Может быть опасно при броске с близкого расстояния.