ОПИСАНИЕ:

Гибридный элитный солдат Overwatch - это компромисс Альянса между показной элитностью и практичной жестокостью. Он объединяет лучшие элементы стандартных элитных солдат и альтернативных подразделений, превращаясь в универсальный инструмент для подавления сопротивления в любой среде.

Его броня белая, как у классических элитных бойцов, но усилена чёрными композитными вставками и дополнительным бронежилетом, придающим силуэту более тяжёлый и угрожающий вид. Эти солдаты выглядят так, будто их собирали не для парадов, а для выживания там, где другие элитники начинают ломаться.

Берет служит не украшением, а маркером опыта и права на самостоятельные тактические решения.

В бою гибрид действует агрессивно и расчётливо. AR2 используется для методичного давления и уничтожения целей, а гранаты - для разрушения укрытий и ломки построений противника. Эти солдаты чаще всего возглавляют штурм, первыми входят в зону контакта и последними покидают её, оставляя после себя тишину и запах озона.

Если альтернативный элитный солдат обманывает ожидания, а стандартный подавляет видом, то гибридный делает и то и другое одновременно, не оставляя противнику ни времени, ни иллюзий.

СОВЕТЫ:

- Чёрные усиленные части брони поглощают урон лучше стандартной защиты.
- Гранаты используются хладнокровно и точно.