ОПИСАНИЕ:

Жук Мортира Роя "Бета" представляет собой специализированную осадную форму, предназначенную для уничтожения противника на больших расстояниях. В отличие от трутней и рейнджеров, её тело почти полностью подчинено одной задаче: производству и запуску органических боеприпасов.

Большую часть передней половины туловища занимает массивная пасть, внутри которой формируются взрывчатые биокапсулы. После выброса они прилипают к поверхностям и детонируют спустя несколько секунд. Таким образом Жук Мортира способна перекрывать проходы, вынуждать добычу покидать укрытия и рассеивать обороняющиеся группы.

Строение существа заметно отличается от большинства представителей Роя "Бета". Органы зрения у него развиты слабо или скрыты под слоями ткани, а ориентирование в пространстве предположительно осуществляется с помощью вибраций, запахов и других биологических сенсоров. Это объясняет, почему Жук Мортира редко действует в одиночку и предпочитает находиться под защитой более подвижных особей.

При всей своей опасности данное существо плохо приспособлено к ближнему бою. Его массивное тело и специализированная анатомия делают Жука Мортиру гораздо менее манёвренной, чем большинство других представителей Роя.

СОВЕТЫ:

- Следи за местами падения снарядов.
- Не позволяй Жуку Мортире вести длительный обстрел.
- Используй её низкую подвижность против неё самой.
- Если существо размером с небольшой транспорт открыло пасть в твою сторону, лучше не ждать результата эксперимента.
