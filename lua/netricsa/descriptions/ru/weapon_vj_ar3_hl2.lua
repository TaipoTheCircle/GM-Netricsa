СТАЦИОНАРНЫЙ ПУЛЕМЁТ AR3


ОРУЖИЕ:  Стационарный пулемёт AR3
ПАТРОНЫ: Энергетические импульсные пули (тёмная энергия)
СС:      ~1800-2200 выстр./мин
ВЕС:     ~45 кг.

ОПИСАНИЕ:

Названия данного оружия известны лишь единицам. AR3 представляет собой стационарную версию импульсной винтовки AR2 OSIPR, адаптированную для длительного ведения огня и удержания позиций. Как правило, пулемёт устанавливается на специальные бронещитки с активным силовым полем, защищающим нижнюю часть тела оператора.

В отличие от AR2 с ограниченным боезапасом, AR3 постоянно питается от внешней батареи, заряженной тёмной энергией. При подключении к щитовой системе батарея непрерывно подпитывается, обеспечивая стабильную генерацию энергетических снарядов. Это делает AR3 оружием с практически неисчерпаемым боезапасом.

Высочайшая скорострельность позволяет эффективно уничтожать большие группы противников на средней дистанции. При этом оружие обладает заметным разбросом, что снижает эффективность точного огня на дальних дистанциях. По мощности AR3 уступает AR2 и AR1 из-за различий в типах батарей, однако попадание практически всегда приводит к тяжёлым ранениям или летальному исходу.

Для повышения боевой эффективности в условиях плохого освещения или в тёмное время суток на корпусе оружия установлены компактные прожекторы, предназначенные для обнаружения и сопровождения целей.

ПРИМЕЧАНИЯ:

- Не требует перезарядки при подключении к энергетическим щитам.
- Эффективен для подавления и удержания территории.
- Большинство повстанцев не знают официального названия данного оружия.
