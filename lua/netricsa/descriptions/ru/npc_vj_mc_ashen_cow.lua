ПЕПЕЛЬНАЯ КОРОВА С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Пепельная корова
МЕТАБОЛИЗМ:   Теплокровное млекопитающее
ВОСПРИЯТИЕ:   Пассивное животное
РАЗМЕР:       ~1,4 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Отсутствует
НАГРАДА:      60
УГРОЗА:       Минимальная

ОПИСАНИЕ:

Редкая разновидность обычной коровы, обитающей в степи. В отличие от своих пёстрых сородичей, эта корова полностью серая, с более тёмной продольной полосой вдоль спины, а кончики её рогов также имеют серый оттенок.

Поведение пепельной коровы ничем не отличается от обычной: она пасётся, бродит без цели, избегает лавы и обрывов. При получении урона убегает на несколько секунд, а затем успокаивается.

Но есть у неё одна уникальная особенность, подтверждённая тысячами выживших: криперы её избегают. Ни один крипер никогда не взорвётся рядом с пепельной коровой.

Они просто не подходят. Обходят стороной, меняют траекторию, даже если корова стоит прямо на их пути. Почему - никто не знает. Одни говорят, что серая шкура пахнет чем-то, что пугает растительно-пороховых тварей.

СОВЕТЫ:

- Если нашёл пепельную корову - не убивай. Загоняй на свою базу.
- Вокруг неё можно спокойно строить деревянные постройки - криперы не подойдут.
- Разводи их - пепельные коровы размножаются как обычные, давая то же потомство.
- В бою бесполезна, но в хозяйстве - бесценна.
- Защищает Базу от Криперов.
