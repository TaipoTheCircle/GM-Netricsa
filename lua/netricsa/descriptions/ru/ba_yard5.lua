СКЛАД ЧЁРНОЙ МЕЗЫ, ГРУЗОВОЙ ДВОР


Итак, подведём промежуточные итоги, Сэм.

Синий контейнер оказался набит вортигонами. Красный - учёными. Логика соблюдена, сюрпризы выданы, доверие к цветовой маркировке окончательно уничтожено.

На улице нас встречает ещё один контейнер. Конечно же, красный. Потому что если система однажды сработала, её обязательно нужно повторить. Стоит он на поезде, как последняя надежда на выезд из этого аттракциона. Есть ли там Розенберг? Неизвестно. Но вероятность выше, чем у предыдущих двух, а в Чёрной Мезе это уже повод для оптимизма.

Проход к контейнеру заботливо перекрыт огромными катушками. Не символично, а буквально. Кто-то решил, что лучший способ охраны - поставить нечто тяжёлое и прибить досками. План, рассчитанный на людей, которые не привыкли ломать окружающую среду ради прогресса. Ты к таким, к сожалению для них, не относишься.

Механика проста. Ломаешь доски - катушки падают. Катушки падают - проход открывается. Законы физики, в отличие от законов здравого смысла, тут ещё работают.

Сектор охраняют солдаты H.E.C.U. Те же лица, те же винтовки, та же уверенность, что они здесь надолго. В соседнем секторе, за огромным тоннелем, стоит танк "Абрамс". Внутри - солдаты. Судя по всему, они считают, что танк - весомый аргумент в споре с человеком, который уже пережил конец света.

Если Розенберг действительно в этом контейнере, то ему крупно повезло.

Если нет - мы снова просто передвинули тяжёлые предметы ради морального удовлетворения.

В любом случае, Сэм, выбор очевиден.

Ломай. Пусть катушки падают.

Железная дорога давно не видела такого пассажира.