БОЛЬШАЯ РЫБА ТОМ С БИКИНИ БОТТОМ


КЛАСС:        Большая рыба Том
МЕТАБОЛИЗМ:   Органический гуманоид, рукастая мутировавшая рыба
ВОСПРИЯТИЕ:   Крайне раздражительный гражданский субъект
РАЗМЕР:       ~2 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Крик, Агрессия
НАГРАДА:      300
УГРОЗА:       Средняя


ОПИСАНИЕ:

Большая рыба Том - массивный и крайне вспыльчивый обитатель Бикини Боттом, известный своей неутолимой страстью к шоколаду и патологической нетерпимостью к окружающим. Любое взаимодействие с ним почти гарантированно заканчивается криками, угрозами и вспышками неконтролируемой ярости.

Том критически реагирует на плохое обслуживание, особенно в "Красти Краб", где малейшая ошибка персонала способна довести его до состояния боевой истерики. При этом он не обладает никакими знаниями о секретной формуле крабсбургера, что делает его опасным, но бесполезным с точки зрения информации.

Его основной метод атаки - оглушающий, агрессивный, яростный крик.

СОВЕТЫ:

- Не приближайся без необходимости - любое внимание его раздражает.
- Дистанция и игнорирование - лучший способ избежать агрессии.