МАЧЕТЕ


ОРУЖИЕ:  Мачете
ПАТРОНЫ: Нет (холодное оружие, ближний бой)
СС:      ~ 75 порезов./мин

ОПИСАНИЕ:

Мачете - длинный, широкий нож с односторонней заточкой, часто используемый как сельскохозяйственный инструмент в джунглях и тропиках, а также как оружие ближнего боя. Универсален - может прорубать заросли, разделать пищу или стать смертоносным оружием при надобности.

ПРИМЕЧАНИЯ:

- Не требует боеприпасов и перезарядки.
- Отлично в ближнем бою, особенно против безоружных или слабозащищённых целей.
- Может использоваться для выживания и утилитарных задач, не только для боя.
- Часто легкий и удобный в обращении, но клинок длинный - может мешаться в плотной среде.