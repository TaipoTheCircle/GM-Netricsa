ОФФИСЫ ЧЁРНОЙ МЕЗЫ, ПУТЬ К ЛИФТУ


Мы почти у цели. Лестница осталась позади, силы Ментала мы обошли, и теперь перед нами последний отрезок перед лифтом. Коридор. Длинный, извилистый. В конце - открытая дверь. В шахту лифта.

Проблема: H.E.C.U. в коридоре. Они здесь. Не наверху, не за баррикадами - прямо здесь, между нами и шахтой. Они знают, что лифт - единственный быстрый путь наверх. И они не хотят, чтобы кто-то им воспользовался. Кто именно - им всё равно. Учёный, инопланетянин, случайный свидетель. Приказ один: никто не проходит.

Но есть и хорошая новость. Силы Ментала сюда не суются. Почему - не знаю. Лифт им не нужен, или коридор для них слишком узкий, или они просто решили, что морпехи - это проблема Сэма Стоуна, а не их. Так что тут только ты и H.E.C.U.

Лифт - это наш билет наверх. Может, к выходу. Может, к очередному уровню этого цирка. Но мы должны туда попасть. Потому что там, наверху, либо спасение, либо следующий бой. А внизу - только коридор с морпехами и шахта без дна.

Лифт ждёт. Надеюсь, он ещё работает.
