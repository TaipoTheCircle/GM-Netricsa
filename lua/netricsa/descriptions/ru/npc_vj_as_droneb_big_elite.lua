НАГРАДА:      1200
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Элитный крупный трутень считается одной из самых опасных разновидностей Роя "Бета". Он сочетает в себе размеры крупной особи и живучесть элитного трутня, образуя существо, которое многие колониальные гарнизоны предпочли бы никогда не встречать.

От своих собратьев он отличается лишь угольно-чёрным окрасом и внушительными размерами. Три глаза внимательно следят за окружающей обстановкой, а массивное тело покрыто плотным хитином, способным выдерживать повреждения, которые для обычного трутня стали бы смертельными. Некоторые очевидцы ошибочно принимали таких существ за молодых ульевых стражей, после чего быстро переставали интересоваться вопросами классификации.

Появление подобной особи внутри улья обычно свидетельствует о том, что колония существует уже достаточно долго и располагает значительными ресурсами. Проще говоря, если Рой смог вырастить такого монстра, значит дела у него идут весьма неплохо. Чего нельзя сказать о соседях.

Несмотря на огромную массу, элитный крупный трутень сохраняет агрессивность и скорость, характерные для Роя "Бета". Он не обладает сложным интеллектом или особыми способностями. Ему это и не требуется. Эволюция давно пришла к выводу, что четыре огромных когтя зачастую убедительнее любой тактики.

СОВЕТЫ:

- Не вступай с ним в ближний бой ни при каких обстоятельствах.
- Тяжёлое вооружение рекомендуется, лёгкое вооружение сочувствует.
- Уничтожай такую особь прежде, чем она приблизится к группе.
- Если обычный трутень напоминает проблему, то элитный крупный трутень больше похож на стихийное бедствие с когтями.
