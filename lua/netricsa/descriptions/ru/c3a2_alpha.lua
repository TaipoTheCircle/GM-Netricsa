Сэм, грузовой лифт сработал. Старая железяка, которая, казалось, вот-вот сорвётся, всё-таки доставила нас вниз. Мы в нижнем погрузочном отсеке. Там, куда не доходят даже самые смелые бригады уборщиков. Там, где Чёрная Меза когда-то складировала то, что боялась хранить наверху.

Теперь здесь хранятся не только ящики и оборудование. Здесь полноправные хозяева - инопланетные пехотинцы. Они чувствуют себя здесь как дома. Потому что дом теперь - весь этот комплекс. А мы - залётные гости, которые не знают местных обычаев. Например, обычая разрывать незваных посетителей на части.

Наш путь - к реактору. Только он может дать нам энергию, связь или, возможно, просто шанс переключить рубильник в нужное положение. Я не знаю. Но учёные сказали - туда. Значит, идём туда.
