СЕРЫЙ ВОРТИГОНТ (РАБ) С ШОУ HALF-QUAKE


КЛАСС:        Серый Вортигонт Шоу HALF-QUAKE (Раб)
МЕТАБОЛИЗМ:   Разумное млекопитающее
ВОСПРИЯТИЕ:   Порабощенная инопланетная раса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Вынужденная
ОРУЖИЕ:       Биоэлектрический разряд, когти
НАГРАДА:      400
УГРОЗА:       Средняя

ОПИСАНИЕ:

Когда стилистика Садистского Шоу HALF-QUAKE сменилась с индустриальных руин на Тёмный депрессивный мир, старые вортигонты перестали вписываться в интерьер. Их естественные цвета напоминали о Зене, а нужна была эстетика безысходности, унификации и корпоративного единообразия.

Решение пришло из хозяйственного отдела: чёрная латексная краска, сорок литров, валики и распылители. Вортигонтов согнали в ангар, выдали по респиратору (не для них, для садистов, чтобы не дышали парами) и покрыли каждую особь свежим слоем. Краска легла неровно, подтёками, кое-где остались заводские синие пятна, но в целом задача была выполнена: вортигонты стали чёрно-серыми, блестящими, а голос бедолаг - стал писклявым.

Глаза у новой модели белые - то ли от краски, то ли от того, что они видели достаточно в своей жизни. Сама краска, как выяснилось, действует как дополнительная защита. Теперь эти твари не только пугают своим видом, но и живут чуть дольше своих неокрашенных собратьев.

Служат они новой версии божества шоу - Сомосу V2, Сыну Садизма.

Сами вортигонты об этом не знают, им как-бы всё равно. Они просто выходят на арену, сверкая своим новым телом, и стреляют молниями во всё, что движется.

СОВЕТЫ:

- Белые глаза в темноте видно за километр. Используй это, чтобы определить позицию до того, как они увидят тебя.
- Краска блестит - если вортигонт стоит под светом, его видно по бликам.
- Защита выше, чем у обычных вортигонтов. Стреляй в голову или пользуйся взрывчаткой.
