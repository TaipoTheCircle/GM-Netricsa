ВЕКС С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Векс
МЕТАБОЛИЗМ:   Магическое призванное существо 
ВОСПРИЯТИЕ:   Летающий враждебный моб, прислужник Призывателя
РАЗМЕР:       0,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Железный меч 
НАГРАДА:      20 
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это маленькое, но крайне раздражающее летающее создание, которое Призыватель призывает, когда хочет превратить твою жизнь в ад. Несмотря на свои крошечные размеры, эти твари способны проходить сквозь стены и наносить сокрушительные удары. Если Виндикатор - это грубая сила, а Призыватель - интеллект, то Векс - это чистая, незамутненная злоба, запертая в миниатюрной форме.

Векс выглядит как маленькое серое привидение с крыльями, вооруженное железным мечом. Когда он атакует, его тело начинает светиться красным, предвещая смертельный рывок. Вексы - это испорченные Алаи. Считается, что Призыватели с помощью темной магии превращают дружелюбных Алаев (помощников, собирающих предметы) в этих агрессивных летунов.

У Векса есть три ключевые особенности, делающие его одним из самых опасных мобов:

Векс может пролетать сквозь любые блоки - землю, камень, обсидиан и даже коренную породу. Ты не можешь спрятаться от него в укрытии.

Когда Векс атакует, он издает пронзительный звук, его тело вспыхивает красным, и он бросается на цель.

Вексы не вечны. Призванные Призывателем, они начинают получать урон и умирают сами через 30-119 секунд (от 30 секунд до чуть менее 2 минут).

Векс атакует стремительно, пытаясь зайти со спины или фланга, что затрудняет блокирование щитом . Они не горят в лаве, не тонут в воде и игнорируют паутину, кроме блоков мёда.

СОВЕТЫ:

- Убей Призывателя первым. Это самый главный совет. Пока жив Призыватель, он будет призывать новых Вексов снова и снова.
- Не стой на месте. Вексы быстры и агрессивны. Постоянное движение и уклонение - твоё всё.
- Используй дальнобойное оружие (Лук / Арбалет). В ближнем бою увернуться от их атаки гораздо сложнее.
- Если тебя окружили Вексы - используй взрывчатку (например, "Фейерверк"). Их здоровье невелико, и площадь взрыва может быстро очистить пространство.
- Помни про самоуничтожение. Если ты чувствуешь, что не справляешься, попробуйте просто уклоняться и переждать. Через полторы минуты они начнут умирать сами.
