КОРОВА-АЛЬБИНОС С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Корова-Альбинос
МЕТАБОЛИЗМ:   Теплокровное млекопитающее
ВОСПРИЯТИЕ:   Пассивное животное
РАЗМЕР:       1,4 м.
ВЫНОСЛИВОСТЬ: Средняя 
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Отсутствует
НАГРАДА:      50
УГРОЗА:       Минимальная

ОПИСАНИЕ:

Редчайшая разновидность коровы, занесённая как пример того, что "бледность среди животных встречается довольно редко". Внешне она отличается от обычных коров полностью белой шерстью и бледной кожей. Копыта у неё светло-коричневые, рога бежевые, нос розоватый, а глаза - бледно-красные, что выдаёт в ней истинного альбиноса.

По поведению корова-альбинос ничем не отличается от обычной: она бродит по лугам, мычит, дышит, избегает воды, лавы и обрывов. При получении урона убегает на несколько секунд, а затем возвращается к своим пассивным делам.

В остальном это обычная корова, просто очень бледная и с красными глазами, что придаёт её взгляду лёгкий налёт безумия, хотя сама она об этом не подозревает.

СОВЕТЫ:

- Отличный экспонат для коллекционера редких мобов.
- Если у тебя есть загон с такими коровами, береги их.
- Доится, даёт мясо и кожу, мычит.
- Красные глаза в темноте не светятся.
