ГРЯЗНАЯ (ЗАЛЯПАННАЯ) СВИНЬЯ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Грязная (Заляпанная) свинья
МЕТАБОЛИЗМ:   Теплокровное млекопитающее
ВОСПРИЯТИЕ:   Дружелюбное Дикое / Домашнее Животное, Любитель грязевых ванн
РАЗМЕР:       ~1 м.
ВЫНОСЛИВОСТЬ: Средняя 
ВРАЖДЕБНОСТЬ: Низкая
ОРУЖИЕ:       Отсутствует
НАГРАДА:      35
УГРОЗА:       Минимальная

ОПИСАНИЕ:

Это не отдельный вид, а состояние души. Любая обычная свинья может стать грязной, если найдёт подходящую лужу и проведёт в ней несколько секунд, старательно пачкаясь.

После завершения процедуры почти всё её тело покрыто грязью, особенно сильно измазаны ноги, а на голове, как насмешка над чистотой, красуется цветок

Грязь со свиньи можно смыть двумя способами: заманить её в озеро или другой водоём, либо просто окатить ведром воды. После этого свинья снова становится обычной, розовой и чистой - но ненадолго. Если рядом есть грязь, она обязательно вернётся и повторит процедуру. Видимо, им это нравится.

СОВЕТЫ:

- Не пытайся отмыть грязную свинью, если тебе не нужна обычная свинья. Грязь - это не болезнь, это стиль свиной жизни.
- Такая свинья пахнет... ну, как свинья в грязи.
- Если разводишь свиней и хочешь грязное потомство - просто оставь лужу рядом с загоном. Они сами разберутся.
