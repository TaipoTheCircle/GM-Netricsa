ФУРНЕЙС МЕТАЛ СОНИК С МОБИУСА


КЛАСС:        Фурнейс Метал Соник
МЕТАБОЛИЗМ:   Механический Робот-Убийца
ВОСПРИЯТИЕ:   Машина для уничтожения, готовки / Личный повар Эггмана
РАЗМЕР:       ~1 м.
ВЫНОСЛИВОСТЬ: Очень высокая 
ВРАЖДЕБНОСТЬ: Абсолютная 
ОРУЖИЕ:       Встроенная печь в груди, огненные шары, когти, сверхскорость
НАГРАДА:      10000
УГРОЗА:       Абсолютная

ОПИСАНИЕ:

Это Метал Соник, которого безумный доктор Эггман, известный как Голодный, перепрофилировал под свои каннибальские нужды. Вместо того чтобы просто победить Соника, Фурнейс должен его поймать, запечь в печи у себя в груди и подать Эггману на ужин.

Фурнейс - это хладнокровный и расчётливый убийца. Его главная цель - подчиняться приказам своего создателя, и он подходит к этому со всей серьёзностью. Он не просто бездумная машина; он способен анализировать поведение жертвы, замечать её слабости и использовать их против неё. В одном из сценариев Фурнейс даже инсценирует собственную поломку, чтобы усыпить бдительность Соника перед решающей атакой.

Визуально Фурнейс отличается от классического Метал Соника. Он выглядит более индустриальным и утилитарным, собранным из грубых слоёв металла и болтов. Самая яркая деталь - это его грудь, которая представляет собой настоящую печь с решёткой, внутри которой бушует огонь. В отличие от Метал Соника, он не пытается доказать своё превосходство в гонке, он просто хочет убить.

Он грозный противник, способный на равных сражаться с Соником. Его арсенал включает:

Печь: Главное оружие. Может открываться, чтобы заточить жертву внутри, или испускать мощные струи пламени.

Сверхскорость: Способен развивать скорость, достаточную, чтобы преследовать бегущего Соника.

Огненные шары: Может стрелять сгустками пламени на расстояние.

Острые когти: Использует в ближнем бою.

Физическая сила: Намного сильнее обычного Метал Соника.

Прочность: Очень живучий и способен выдерживать мощные атаки.

СОВЕТЫ:

- Никогда не недооценивай его. Он умен и способен просчитывать твои действия.
- Говорят, что Фурнейс способен уничтожить даже своего создателя, если того потребуют обстоятельства. Всегда держи это в уме.
