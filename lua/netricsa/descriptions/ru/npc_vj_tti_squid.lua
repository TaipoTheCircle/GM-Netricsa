ОПИСАНИЕ:

Большой Сквидвард Джей Куинси Щупальца - осьминог, интеллектуально превосходящий своё окружение и глубоко от этого страдающий. Вынужден годами терпеть соседство с Губкой "Мясным" Бобом и Большим Патриком Старом, что привело к хронической раздражительности, цинизму и эмоциональному истощению.

Работает кассиром в заведении, которое презирает. Искусство для него - не хобби, а последняя попытка доказать миру собственную значимость. Считает себя гением, недооценённым толпой.

Несмотря на внешнюю пассивность, Сквидвард крайне образован, умеет водить практически любую технику и способен к логическому мышлению. В редкие моменты вдохновения демонстрирует реальный талант, особенно в музыке, но почти всегда саботирует собственный успех.

В условиях постоянного давления и издевательств способен на жестокие поступки, однако после осознания последствий почти всегда испытывает вину и пытается всё исправить.

