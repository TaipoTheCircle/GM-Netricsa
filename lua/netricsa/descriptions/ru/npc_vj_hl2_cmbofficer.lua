ОПИСАНИЕ:

Офицеры Гражданской обороны - это связующее звено между уличными метрокопами и лоялистами. В отличие от обычных бойцов ГО, они принципиально не используют противогазы, предпочитая открытые лица.

Их внешний вид варьируется. Некоторые носят стандартную броню Гражданской обороны, другие предпочитают береты, нашивки, наплечники и прочие побрякушки, подчёркивающие ранг и личные привилегии. Униформа менее стандартизирована, зато отлично подчёркивает разницу между теми, кто отдаёт приказы, и теми, кто получает разряды дубинкой.

Формально офицеры обязаны поддерживать порядок, координировать патрули и докладывать напрямую структурам Альянса. Неформально же всё сложнее. Некоторые из них, пользуясь доступом к информации и связи, тайно передают данные Сопротивлению.

В бою офицер Гражданской обороны опаснее обычного метрокопа за счёт опыта и хладнокровия, но всё ещё остаётся человеком без серьёзной аугментации.

СОВЕТЫ:

- Не стоит недооценивать их опыт, даже если они выглядят слишком "по-граждански".
- Некоторые офицеры знают больше, чем говорят.