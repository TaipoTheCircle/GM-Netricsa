САМОЗАРЯДНЫЙ ПИСТОЛЕТ TEC-9


ОРУЖИЕ:     Intratec TEC-DC9
ПАТРОНЫ:    9x19
CC:         ~20 выстрелов/мин

ОПИСАНИЕ:

Самозарядный пистолет, разработанный в Швеции в 1980-х годах. Выпущен на рынок в 1984 году. Был популярен у группировок из Лос-Анджелеса до закрытия Intratec в США.

ПРИМЕЧАНИЯ:

- Хороший пистолет для войн в трущобах.




