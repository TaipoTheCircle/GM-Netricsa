СНЕЖОК


ОРУЖИЕ:      СНЕЖОК
CC:          ~1 бросок/сек.

ОПИСАНИЕ:

Комки снега которые были слеплены вместе руками. Снежок можно кидать во что угодно и куда угодно. Говорят если добавить внутрь камень или желтый снег, можно сделать снежок опаснее.

ПРИМЕЧАНИЯ:

- Бесполезная вещь, ей можно только красить игроков и стены.
- Но если игрок в рэгдолле, ему можно сломать шею снежком (вроде бы, возможно пофиксили).