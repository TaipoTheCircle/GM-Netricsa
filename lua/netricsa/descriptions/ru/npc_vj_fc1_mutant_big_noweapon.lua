ГРОМИЛА С АРХИПЕЛАГА КРИГЕРА


КЛАСС:        Громила
МЕТАБОЛИЗМ:   Триген-мутант
ВОСПРИЯТИЕ:   Прямолинейный боец
РАЗМЕР:       7,2 фута
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Агрессивный
ОРУЖИЕ:       Ракетная установка (встроенная), когти
НАГРАДА:      1500
УГРОЗА:       Высокая

ОПИСАНИЕ:

Громилы получили своё название за исполинское телосложение и тяжелую поступь, слышимую за десятки метров. Большинство особей оснащены встроенной в правую руку ракетной установкой с бесконечным боезапасом, но медленными ракетами. Их грудь обнажена для вентиляции светящегося импланта - главной уязвимой зоны. Несмотря на военную форму (брюки и ботинки), их интеллект предельно примитивен: они не обходят преграды, а буквально прогрызают себе путь, иногда гибнут от собственных же ракет в узких проходах.

Примечательно, что это единственные тригены, сохранившие способность к речи. В бою они изрыкают угрозы вроде "Ты покойник!" и "Тебе конец!", хотя их тактика сводится к лобовой атаке. Не понимают концепции смерти сородичей - могут кричать "Кого-то убили!", наблюдая гибель другого мутанта. Реагируют на звуки и организуют патрули, но в закрытых пространствах становятся опаснее из-за невозможности уклонения от их ракет.

СОВЕТЫ:

- Целься в светящийся имплант на груди - обычные пули почти не вредят мутированной плоти.
- Используй манёвренность - медленные ракеты легко избежать на открытой местности.
- Остерегайся в коридорах - здесь их ракеты практически гарантировано находят цель.
- Встречается безоружная разновидность.