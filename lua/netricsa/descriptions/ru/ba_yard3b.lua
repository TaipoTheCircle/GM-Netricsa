СКЛАД ЧЁРНОЙ МЕЗЫ, ЛЮБИТЕЛЬ ТЕЛЕВИЗОРОВ


Сэм, ты всё ещё на складе Чёрной Мезы - но теперь тут царит сюрреализм чистой воды. В центре зала, среди штабелей ржавых ящиков и сваленных в кучу телевизоров, стоит одинокий вортигонт.

И нет, он не ждёт вечерних новостей - он изучает эту гору техники. Телевизоры не подключены, экраны мертвы, но вортигонт будто видит в них что‑то своё. Может, эхо иных миров? А может, просто забавляется.

Твоя цель - вентиляция наверху. Путь туда очевиден... но, как всегда, есть одно НО. И это "НО" - жирное, красное и чертовски взрывоопасное.

Рядом с вортигонтом, аккуратно между ним и лестницей к вентиляции, стоят взрывные бочки. Две штуки. Достаточно одной искры - и весь этот телевизионный парад превратится в огненный фейерверк.