КЛАСС:        Страйдерский Миниатюрный метрокоп Гражданской Обороны Альянса
МЕТАБОЛИЗМ:   Человек, Мужской (критическая биомеханическая компрессия)
ВОСПРИЯТИЕ:   Встроенный оператор тяжёлого боевого синтета
РАЗМЕР:       ~0,3 м.
ВЫНОСЛИВОСТЬ: Крайне низкая
ВРАЖДЕБНОСТЬ: Высокая
