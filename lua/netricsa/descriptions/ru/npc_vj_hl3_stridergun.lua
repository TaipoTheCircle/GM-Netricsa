НАГРАДА:      400
УГРОЗА:       Низкая

ОПИСАНИЕ:

Страйдерский Миниатюрный метрокоп - это ещё более радикальная форма интеграции человека в боевой синтет Альянса. В отличие от операторов Министрайдеров, эти метрокопы полностью вмонтированы в центральный биомеханический отсек Страйдера и не обладают никакой автономией вне его корпуса.

Их тела подвергнуты постоянной компрессии, фиксации и нейронной синхронизации с системой управления шагохода. Все жизненные функции поддерживаются исключительно за счёт бортовых систем Страйдера. Зрение, слух и ориентация в пространстве полностью заменены сенсорным потоком машины, поступающим напрямую в нервную систему оператора.

В отличие от версии, встроенной в Министрайдера, оператор Страйдера не может быть освобождён даже теоретически. Его компрессионная капсула является частью несущей конструкции. При уничтожении шагохода происходит мгновенная потеря жизнеобеспечения, разрыв нейросвязей и критическое разрушение тела оператора. Возврат к нормальному размеру невозможен.

СОВЕТЫ:

- Уничтожение Страйдера гарантированно уничтожает оператора внутри.
