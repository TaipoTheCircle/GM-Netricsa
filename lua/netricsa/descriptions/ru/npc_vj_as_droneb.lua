ТРУТЕНЬ РОЯ "БЕТА", МАЛЫЙ


КЛАСС:        Трутень Роя "Бета", Малый
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Стайный Хищник
РАЗМЕР:       2,2 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Очень Высокая
ОРУЖИЕ:       Когти, зубы
НАГРАДА:      250
УГРОЗА:       Средняя

ОПИСАНИЕ:

Рой "Бета" считается одной из наиболее примитивных ветвей развития инопланетной заразы, обнаруженной на окраинах освоенного космоса. Его трутни заметно отличаются от представителей более известных роёв: они выше ростом, обладают удлинёнными конечностями и практически лишены защитных хитиновых пластин, но имеют 3 глаза.

Отсутствие тяжёлого панциря делает этих существ уязвимыми для огня стрелкового оружия, однако недостаток защиты они компенсируют подвижностью. Трутни Роя "Бета" способны стремительно преодолевать большие расстояния и атаковать жертву серией быстрых ударов, не позволяя ей сосредоточиться на прицеливании.

Ксенобиологи до сих пор спорят, является ли Рой "Бета" предком современных роёв или тупиковой ветвью эволюции. Сами трутни научного интереса не разделяют и обычно прерывают подобные исследования ещё на стадии наблюдений.

Несмотря на внешнюю худобу, эти существа обладают удивительной живучестью и продолжают нападать даже после тяжёлых ранений. Вероятно, чувство самосохранения в их организме было признано необязательным органом и со временем исчезло.

СОВЕТЫ:

- Не подпускай их вплотную.
- В открытых пространствах они особенно опасны.
- Точный огонь эффективнее беспорядочной стрельбы.
- Если трутень несётся прямо на тебя, скорее всего, он уже всё для себя решил.
