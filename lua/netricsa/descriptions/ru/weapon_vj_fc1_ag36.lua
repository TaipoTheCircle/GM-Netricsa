ШТУРМОВАЯ ВИНТОВКА G36 С ПОДСТВОЛЬНЫМ ГРАНАТОМЁТОМ AG36


ОРУЖИЕ:  G36 с модулем AG36
ПАТРОНЫ: 5.56х45 мм NATO (основной) / 40х46 мм низкоскоростные гранаты (подствольный)
СС:      ~750 выстр./мин (винтовка); подствольный - одиночный выстрел

ОПИСАНИЕ:

Комплект "G36 + AG36" - стандартная современная штурмовая платформа с интегрированным подствольным 40-мм гранатомётом. G36 обеспечивает надёжную автоматическую и полуавтоматическую стрельбу 5.56x45 мм с хорошей управляемостью и умеренной точностью на средних дистанциях. AG36 добавляет огневую поддержку за счёт гранат для зачистки укрытий, создания заградительного огня или разрушения легкой техники - всё без смены основного оружия.

ПРИМЕЧАНИЯ:

- Универсальная комбинация: точечная стрельба и подавление (G36) + площадная взрывчатая сила (AG36).
- AG36 стреляет по дуге - учитывай падение гранаты на средних/дальних дистанциях.
- При стрельбе гранатой в замкнутом пространстве возможен урон для пользователя - держись за укрытием.
- Модульная конструкция позволяет быстро снять AG36 и использовать G36 как обычную винтовку.
- Оптические прицелы часто совмещены с отметками для стрельбы подствольным гранатомётом, что облегчает прицеливание на дистанциях 50-400 м.