ОПИСАНИЕ:

Крепость Ментала - это гигантская мобильная пирамида, служащая центральной базой и личным логовом Ментала. Она представляет собой автономную механизированную цитадель, созданную исключительно для ведения тотальной войны и уничтожения любых вторжений.

Конструкция полностью покрыта бронированными сегментами и вооружёнными платформами. Внутри располагается защищённое ядро управления, которое активируется при обнаружении угрозы. После активации крепость начинает выпускать волны истребителей, включая "толстяков-камикадзе", которые падают на цели и взрываются при контакте, нанося разрушительный урон.

Параллельно крепость обстреливает окружающее пространство плазменными огненными шарами, тяжёлыми пушечными снарядами и массированными ракетными залпами. Манёвренные самолёты-бомбардировщики "Чайка" сбрасывают кассетные бомбы, превращая поверхность вокруг крепости в зону тотального поражения.

Когда защитные будут системы ослаблены, центральное ядро временно откроется, позволяя атаковать ключевые узлы конструкции. В этот момент из нижних отсеков выдвигаются массивные вращающиеся колёса - автономные боевые модули, движущиеся по поверхности. Любой контакт с ними приводит к мгновенной гибели цели.

Крепость Ментала не является обычным сооружением - это ходячая машина уничтожения, способная подавлять армии, стирать города и функционировать как самостоятельная боевая экосистема.

СОВЕТЫ:

- В первую очередь уничтожай "толстяков-камикадзе" - они наносят максимальный урон.
- Манёвренные бомбардировщики опасны, но второстепенны.
- Когда ядро открыто, атакуй максимально агрессивно тяжёлым вооружением.
- Никогда не стой на месте во время огненных и артиллерийских атак, если не хочешь закончить как Квонго.
