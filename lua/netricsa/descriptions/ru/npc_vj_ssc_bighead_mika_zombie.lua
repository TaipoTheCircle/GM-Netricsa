ОПИСАНИЕ:

Кротимовцы - худощавые гуманоиды с гипертрофированными черепами, содержащими мозг разработчиков Croteam. Носят униформу крутого Сэма, но с уникальными чертами лиц команды разработчиков. Проявляют навязчивый энтузиазм, преследуя тебя с возгласами "Привет!" и "Эй, вы куда!?". Абсолютно безвредны, но в сложности "Я - Угх-Зан" получают огненные и ледяные модификаторы. Спонтанно появляются в секретных зонах, блокируя проходы своим числом.

Иван Мика - легендарный дизайнер уровней, чей первый же вклад в Serious Sam: The Second Encounter (TSE) вошёл в историю и терзает игроков до сих пор. Его гипертрофированный череп - прямое следствие монументального размаха его воображения: вдохновившись Теотиуаканом, он создал локацию такой эпической величины, что её пришлось разделить на три полноценных уровня, составивших четверть всей игры. Именно он подарил миру культовые (и ненавистные) эксперименты с трамплинами и гравитацией, которые стали одновременно и визитной карточкой, и проверкой на прочность для поколений фанатов.

Но Иван - не только художник, но и гениальный оптимизатор-самоучка. В разгар разработки TSE, когда новая система частиц гробила производительность, он в одиночку удвоил частоту кадров, обнаружив и удалив одну-единственную злосчастную строку кода, которая заставляла всю сцену рендериться дважды. Проработав в Croteam более 15 лет и пройдя путь до ведущего дизайнера, он стал тем самым "неизлечимым" элементом студии - неутомимым творцом, чьи уровни по-прежнему заставляют игроков восхищаться и проклинать его гений в одном дыхании.

Когда-то он был живым, но после того, как кто-то убил его, он восстал из мёртвых - став демоническим Зомби.

СОВЕТЫ:

- Для очистки пути используй армейский нож или пистолет.
- На сложности "I Am Ugh-Zan" уничтожай дистанционно из-за их модификаторов.
- В групповом скоплении эффективна кувалда с горизонтальным ударом.