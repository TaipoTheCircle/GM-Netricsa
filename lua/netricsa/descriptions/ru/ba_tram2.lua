ЧЁРНАЯ МЕЗА, ПРОЕЗД ЧЕРЕЗ КАФЕТЕРИЙ


Эй, Сэм‑охранник! Приготовься к небольшому экскурсу по "гастрономическим достопримечательностям" Чёрной Мезы. Сейчас проедем мимо кафетерия - там, на против дыры в скале, приютились "Big Tony's Pizza" и "Tesla's Taco". Место популярное: Отис Лорей, например, частенько тут торчит.

Гляди в оба: среди учёных и охранников шныряют "Монстры Женерики" и "Женерик Актёры". Кто они на самом деле - неизвестно. Где живут - тоже. Зачем маскируются - тем более. Но явно не за кофе пришли.

Они копируют поведение персонала: стоят у кофемашин, листают бумаги, делают вид, что спешат по делам. Но чутье подсказывает - эти "коллеги" ждут своего часа. Может, они следят. Может, готовят сюрприз. А может, уже сейчас кто‑то из них смотрит на тебя и решает, стоит ли нажимать тревожную кнопку.

Пока всё тихо. Кафетерий освещён, посетители (настоящие или нет) болтают, пахнет пиццей и тако. Но помни: в Чёрной Мезе даже обеденный перерыв может стать последним.

Держи руку поближе к кобуре. И не заглядывайся на вывески - вдруг это не еда, а приманка?

Вперёд, "охранник". Следующая остановка - неизвестно где.