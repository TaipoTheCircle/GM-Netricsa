ЗОМБИ-ГИГАНТ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Зомби-гигант
МЕТАБОЛИЗМ:   Нежить
ВОСПРИЯТИЕ:   Колоссальный зомби
РАЗМЕР:       ~18 м.
ВЫНОСЛИВОСТЬ: Экстремальная 
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Топот, удар 
НАГРАДА:      1000
УГРОЗА:       Абсолютная

ОПИСАНИЕ:

Это точная копия обычного зомби, увеличенная в шесть раз. Та же гниющая плоть, те же лохмотья, но размером с деревенский дом. Если обычный зомби едва достаёт до твоего лица, то гигант смотрит на тебя с высоты восемнадцати метров. Его шаги сотрясают землю, а одна единственная атака способна уничтожить противника.

У гиганта есть и другие странности. Он не является нежитью, хотя выглядит как зомби . На него действуют зелья вреда, он лечится от зелий лечения, его не берёт зачарование "Небесная кара", и он не боится солнца. В воде он не превращается в утопленника, а медленно задыхается. При этом железные големы всё равно атакуют его, как любого другого врага.

Если посадить гиганта в вагонетку, он будет парить над ней, как призрак, не касаясь рельсов.

СОВЕТЫ:

- Для убийства нужна дальнобойка. Стреляй только в ноги - попадание в тело и выше не засчитывается.
- Гигант не горит на солнце, не превращается в утопленника и не реагирует на зомби-призывы.
- Не пытайся посадить его на поводок. Не получится.
