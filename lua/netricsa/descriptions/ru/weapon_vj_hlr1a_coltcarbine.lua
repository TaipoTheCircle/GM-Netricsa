КАРАБИН КОЛЬТ M4A1


ОРУЖИЕ:      Карабин Кольт M4A1
ПАТРОНЫ:                  Патроны 5,56x45 мм НАТО + 40-мм гранаты
ТЕМП ПОТРЯСЕНИЯ:         ~700 выстрелов/мин

ОПИСАНИЕ:

Стандартное штурмовое ружье для сил H.E.C.U. Обеспечивает надежный автоматический огонь с хорошей точностью на средней дистанции. Оснащено подствольным гранатометом M203, что обеспечивает дополнительную огневую мощь для тактической гибкости.

ПРИМЕЧАНИЯ:

- Сбалансированное оружие для большинства боевых сценариев.
- Гранатомет эффективен против укрепившихся противников.
- Теряет точность на больших дистанциях при длительной стрельбе.