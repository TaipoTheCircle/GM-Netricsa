ЧЁРНАЯ МЕЗА, ВНЕ ШАХТЫ РЕАКТОРА


Сэм, ты оказался в одном из крупнейших сооружений комплекса - промышленной цистерне, обёрнутой вокруг испытательного ядерного реактора. Снаружи она выглядит как бетонная крепость, но внутри... это кошмарная симфония стали, эха и когтей.

Датчики подтверждают присутствие организмов класса "Тентакль". Представь себе растительный хлыст размером с небоскрёб с абсолютным слухом и скверным характером, и ты поймёшь. Эти твари не видят - ты издаёшь шум, и они бьют. Сильно. Одна только ударная волна может превратить твои рёбра в гравий.

Ситуация усложняется тем, что сверху - ребята из орды Ментала патрулируют периметр, и им всё равно, нападут ли тентакли первыми. Для них ты всего лишь мишень для стрельбы.

Опасность навигации: сам реактор. Скачки напряжения в каскаде дестабилизировали его системы, поэтому некоторые участки освещены всплесками радиации. Представь себе полосу препятствий, спроектированную сумасшедшим физиком.