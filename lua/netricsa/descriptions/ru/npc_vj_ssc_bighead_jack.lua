КЛАСС:        Большеголовый, Джек Пофигист
МЕТАБОЛИЗМ:   Гуманоидный, Мужской
ВОСПРИЯТИЕ:   Фанат-энтузиаст (Золоторукий созидатель)
РАЗМЕР:       5,9 футов
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Нейтральная
ОРУЖИЕ:       Отсутствует (кроме сложности I Am Ugh-Zan)
НАГРАДА:      9999
УГРОЗА:       Раздражающий фактор

ОПИСАНИЕ:

Большеголовые - худощавые гуманоиды с гипертрофированными черепами, содержащими мозг разработчиков. Носят униформу крутого Сэма, но с уникальными чертами лиц команды разработчиков. Проявляют навязчивый энтузиазм, преследуя тебя с возгласами "Привет!" и "Эй, вы куда!?". Абсолютно безвредны, но в сложности "Я - Угх-Зан" получают огненные и ледяные модификаторы. Спонтанно появляются в секретных зонах, блокируя проходы своим числом.

Джек никогда не работал в Croteam, но его вклад в популяризацию серии среди нового поколения неоценим. Он - мост между классическим шутером 2001 года и бесконечной песочницей 2006-го. Благодаря его аддону Serious Sam Classic's NPCs армии Ментала вторглись в тысячи пользовательских сценариев GMod: от дурацких ролевых серверов до эпичных анимаций. Он не просто портировал моделпак - он кропотливо воссоздал поведение, звуки и даже баланс врагов, чтобы хаос ощущался аутентично.

Его гипертрофированный череп - архив, хранящий саму душу любимых вселенных: тактическую свободу Far Cry 1, безграничный креатив Minecraft, чистый адреналин Serious Sam и абсурдный хаос Goat Simulator. Именно этот уникальный коктейль вдохновения делает его аддоны для GMod такими живыми и многогранными — он не просто копирует врагов, а вкладывает в них понимание геймплея из своих любимых игр.

Но главная его суперсила - менторство. Для Taipo он не просто коллега по цеху моддинга, а "самый лучший друг, когда-либо существовавший". Каждый совет по Blender или логике скриптов - это урок, на котором фанатское сообщество растёт и крепнет. Джек - это ядро той самой творческой цепочки, где опыт и страсть передаются дальше, обеспечивая вечную жизнь игровым вселенным. Он - воплощение гения. Обладая "золотыми руками", мастерским владением Blender и неутомимой энергией, он стал создателем культового аддона Serious Sam Classic's NPCs для Garry's Mod. Именно благодаря ему и его друзьям (Taipo и S199-Smirnof) безумие Serious Sam - Гнаары, Клиры, Безголовые Камикадзе и даже сами разработчики в виде Кротимовцев - вырвалось за пределы оригинальных игр и поселилось в песочнице GMod, чтобы терроризировать и веселить новые поколения игроков.

Его история - от энтузиаста с ограничениями Steam до уважаемого создателя контента - это современная сказка игрового сообщества. Джек Пофигист доказывает, что наследие живёт не только в официальных играх, но и в руках талантливых фанатов, которые продолжают лепить, анимировать и распространять его дух. 



СОВЕТЫ:

- Для очистки пути используй армейский нож или пистолет.
- На сложности "I Am Ugh-Zan" уничтожай дистанционно из-за их модификаторов.
- В групповом скоплении эффективна кувалда с горизонтальным ударом.
