ОПИСАНИЕ:

Мама-Шрам. И это имя она получила не за красивые глаза. У неё был целый выводок молодых, которых надо кормить, и она кормила их всем, до чего могла дотянуться. У неё есть глубокая борозда шрамов на правой стороне морды, через глаз и дальше в череп. Глаз под шрамом был мутный и, скорее всего, слепой, но это не мешало ей находить жертву снова и снова.

В бою Мама-Шрам - это стихийное бедствие. Она не будет бегать за тобой, как велоцираптор. Она просто пойдёт туда, куда захочет, и если ты оказался на пути - это твоя проблема. Скорость у неё приличная и поворотливость тоже.

СОВЕТЫ:

- Не лезь в открытую. Она быстрее тебя и сильнее любого укрытия.
