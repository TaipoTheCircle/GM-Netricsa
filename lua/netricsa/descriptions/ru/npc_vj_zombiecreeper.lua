ЗОМБИ-КРИПЕР С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Зомби-крипер
МЕТАБОЛИЗМ:   Нежить
ВОСПРИЯТИЕ:   Враждебный моб
РАЗМЕР:       ~1,7 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Чрезвычайно высокая
ОРУЖИЕ:       Самоподрыв, заражение жителей
НАГРАДА:      110
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Это гибрид, который возникает, когда два знакомых нам врага объединяются в один кошмарный коктейль.

Внешне зомби-крипер выглядит как классический крипер, но с элементами зомби: зелёная кожа крипера, одетая в лохмотья зомби, и его знаменитое лицо. Он не взрывается сразу при виде игрока, а сначала ведёт себя как зомби.

Как зомби, он преследует жителей деревни. Как и обычный крипер, при приближении к цели он взрывается.

В отличие от обычных зомби, зомби-крипер не боится дневного света. Он не горит, а значит, его можно встретить в любое время суток.

Он игнорирует механику крипера (подкрадывание) и просто бежит, чтобы покусать и взорваться.

Как и зомби, они могут атаковать группами, создавая эффект цепной реакции взрывов.

СОВЕТЫ:

- Держи дистанцию. Лучшее оружие против него - лук или арбалет. Не подпускай его близко.
- Используй окружение. Узкие проходы, вода или лава могут помочь замедлить его или уничтожить до того, как он взорвётся.
- Бей с разбега. Если ты вынужден сражаться в ближнем бою, используй технику "удар-отскок": подбеги, ударь и сразу отпрыгни назад.
- Блокируй удар мечом, но помни, что взрыв блок не остановит.
- При убийстве он может оставить до 4 блоков динамита (ТНТ).
