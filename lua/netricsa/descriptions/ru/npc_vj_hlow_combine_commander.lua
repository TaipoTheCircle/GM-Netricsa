ЭЛИТНЫЙ КОММАНДЕР OVERWATCH АЛЬЯНСА


КЛАСС:        Элитный Коммандер Overwatch Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Высший командный состав Альянса, аномальный тактик
РАЗМЕР:       1,9 м.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Импульсная винтовка AR2, портальные поля захвата
НАГРАДА:      2500
УГРОЗА:       Экстремальная

ОПИСАНИЕ:

Элитный коммандер Overwatch - это один из самых высокопоставленных боевых офицеров Альянса, сочетающий тактический гений и экспериментальные технологии подавления.

Он носит камуфляжный костюм с фиолетовыми вставками и чёрную накидку с красной окантовкой, подчёркивающую его статус и почти театральную надменность.

Его лицо скрыто под шлемом с единственным оранжевым светящимся "глазом", который служит не только визором, но и сенсорным узлом портальной системы.

Главная особенность коммандера - принудительный телепорт противника. Он создаёт жёлтый портал вокруг цели и мгновенно переносит её прямо перед собой. Укрытия, дистанция и манёвры становятся бессмысленными - жертва буквально доставляется к стволу коммандера.

Коммандер редко преследует врагов. Он предпочитает, чтобы враги сами "приходили" к нему по приказу пространства.

СОВЕТЫ:

- Если вокруг тебя появился жёлтый портал - готовься к мгновенному контакту с импульсной винтовкой.
- Чем дольше он жив, тем более извращённым становится поле боя - он превращает его в персональную комнату для расстрела.
