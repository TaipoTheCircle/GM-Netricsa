ОПИСАНИЕ:

Эти солдаты составляют основную боевую силу службы безопасности корпорации EdenSys BioTech. Большинство из них являются клонами, выращенными и обученными исключительно для ведения боевых действий, охраны объектов и подавления любых внутренних или внешних угроз.

Их экипировка рассчитана на эксплуатацию в условиях экстремального холода. Утеплённая форма, плотная маска и визоры позволяют действовать в арктической среде, сохраняя высокую мобильность и эффективность даже при крайне низких температурах.

Вооружение зависит от поставленной задачи. Солдаты способны использовать широкий спектр оружия: автоматы, дробовики, пистолеты-пулемёты, винтовки, пулемёты и другое современное вооружение корпорации. Благодаря постоянным тренировкам они уверенно обращаются практически с любым образцом стрелкового оружия.

Подготовка бойцов EdenSys считается весьма суровой, однако из всего курса обучения большинство солдат лучше всего усваивает одно: бесконечный запас нецензурной брани. В бою они постоянно выкрикивают ругательства, угрозы и оскорбления, что давно стало своеобразной визитной карточкой корпоративной армии.

Несмотря на это, недооценивать их нельзя. Они действуют организованно, быстро занимают выгодные позиции, активно используют укрытия и ведут плотный огонь, представляя серьёзную угрозу даже для хорошо подготовленных противников.

СОВЕТЫ:

- Используемое оружие заранее неизвестно, поэтому оценивай угрозу сразу после обнаружения противника.
- Солдаты предпочитают вести бой из укрытий и редко остаются на открытой местности.
- Их арктическая экипировка не делает их более защищёнными, чем обычных людей.
- Не отвлекайся на их постоянные ругательства: стреляют они гораздо лучше, чем разговаривают.
