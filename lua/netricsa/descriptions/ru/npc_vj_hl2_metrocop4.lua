МЕТРОКОП-КОМАНДИР ГРАЖДАНСКОЙ ОБОРОНЫ АЛЬЯНСА


КЛАСС:        Гражданская Оборона, Командир подразделения
МЕТАБОЛИЗМ:   Человек, Мужской (трансчеловеческая подготовка)
ВОСПРИЯТИЕ:   Улучшенное (тактическое мышление, радиоканал управления)
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Очень высокая
ОРУЖИЕ:       AR3 (станковый импульсный пулемёт)
НАГРАДА:      600
УГРОЗА:       Высокая

ОПИСАНИЕ:

Метрокоп-Командир - редкий и опасный представитель Гражданской Обороны, появляющийся в зонах повышенного сопротивления. Его чёрная униформа, усиленный бронежилет и белая маска с противогазом сразу выделяют его среди обычных сотрудников Метрополиции. Лицо полностью скрыто: маска оснащена характерными дыхательными отверстиями и круглыми боковыми элементами, что делает идентификацию личности невозможной.

На левом рукаве заметна красная повязка с белым кругом и чёрным символом Альянса - знак полномочий и безусловного подчинения высшей иерархии. Этот символ не столько обозначает ранг, сколько предупреждает: перед тобой не патрульный, а исполнитель приказов без права на сомнение.

В руках Командир удерживает AR3 - тяжёлое импульсное оружие, истинное назначение и официальное наименование которого известно лишь ограниченному числу офицеров Альянса. Это оружие предназначено не для разгона толпы, а для полного подавления сопротивления.

СОВЕТЫ:

- Не вступай в прямой бой без укрытий - AR3 подавляет пространство, а не цель.