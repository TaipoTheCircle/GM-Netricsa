МЕЧ1


ОРУЖИЕ:  Меч1
ПАТРОНЫ: Не требуется
СС:      ~1 взмах/секунда
ВЕС:     2,0 кг.

ОПИСАНИЕ:

Меч ближнего боя с уникальными визуальными эффектами. Изменяет цвет в унисон с физическим генератором и светится в темноте, обеспечивая заметность и эффект внезапности. Конструкция проста и надёжна, с минимумом сложных механизмов.

ПРИМЕЧАНИЯ:

- Лёгкий вес и простая конструкция делают его удобным для ближнего боя.
- Эстетический эффект усиливается свечением в темноте.