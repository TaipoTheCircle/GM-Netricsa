ШОКОВЫЙ-ТАРАКАН, ОРУЖИЕ


ОРУЖИЕ:      Шоковый Таракан (биологическое оружие)
ПАТРОНЫ:                  Биоэлектрические заряды (регенерирующиеся)
CC:         ~120 ударов/мин

ОПИСАНИЕ:

Инопланетный биологический организм, захваченный и превращенный в оружие. При удержании он прикрепляется к руке пользователя и выбрасывает потоки биоэлектрической энергии в цель. Функционирует как живое оружие, постоянно регенерируя собственные боеприпасы.

ПРИМЕЧАНИЯ:

- Неограниченные боеприпасы благодаря естественной регенерации.
- Основной огонь: быстрые электрические разряды, эффективные на средней дистанции.
- Вторичный огонь: непрерывный поток электричества для нанесения постоянного урона.