КРУТОЙ ПОСЁЛОК, ПОДВАЛЬНЫЕ ТАЙНЫ


Сэм, судя по названию, нас занесло в посёлок, который местные жители с иронией или вызовом окрестили "Крутым".

Только вот крутизна здесь специфическая: либо деревенский рэп снимали, либо экспериментальный полигон для скрещивания мух с муравьиными львами был.

Информации по официальным каналам почти нет, но молва ищет сарафанное радио.

Народная молва - штука понадёжнее любой документации. Говорят, в этом посёлке:

Подвалы - не для картошки. Местные поговаривают, что в тёмных, сырых подвалах кто-то держит зомби. То ли для охраны, то ли как домашних питомцев, то ли просто потому, что в российской глубинке без зомби уже скучно. Если услышишь из подпола характерное хрипение - не спускайся без дробовика.

Деревянный Туалет - зона отчуждения. В туалетах произошла мутация. Обычные мухи, которых тут всегда было полно, превратились в муравьиных львов.

Представь: заходишь по нужде, а на тебя из очка пикирует тварь с жвалами. Местные, наверное, уже привыкли и ходят вёдрами на огороде.

Домики - отличные укрытия, но заходить в них рискованно: в подвалах зомби, в туалетах монстры. Лучшая тактика - держаться открытых пространств и не заглядывать в окна. И никогда, Сэм, никогда не пользуйся местным туалетом. Даже если очень приспичит. Терпи до леса.

Погнали, осмотримся. Только носом не шмыгай.
