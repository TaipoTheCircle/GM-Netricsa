БОЛЬШОЙ АЭРО ГОРОД, ВАЙБОВЫЙ ГОРОД


Сэм, мы только что перенеслись в город будущего... из 2000-х.

Природа победила бетон. Треть города снесли (буквально "сровняли с землёй"), чтобы добавить зелени. Потому что в светлом будущем люди должны дышать.

Вода наконец-то появилась. Каналы, которые раньше были сухими и грустными, теперь наполнены водой. Можно мочить врагов или просто мочить ноги.

Бассейн. Это уже становится традицией. Видимо, без джакузи никакой уважающий себя рескин Aero не обходится.

Башни-близнецы (WTC) открыты. Туда можно подняться и осмотреть окрестности. Если не боишься высоты и самолётной кармы.

Никакой хоррор-атмосферы. Архитектор специально подчистил всё страшное, неприятное и давящее. Больше никаких скримеров в тёмных подворотнях. Только свет, зелень и спокойствие. Относительное, конечно.

Погнали купаться... то есть осматриваться.
