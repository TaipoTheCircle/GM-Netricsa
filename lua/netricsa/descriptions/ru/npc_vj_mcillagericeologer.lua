ЛЕДЯНОЙ КОЛДУН С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Ледяной Колдун
МЕТАБОЛИЗМ:   Магически Одарённый илладжер
ВОСПРИЯТИЕ:   Колдун / заклинатель
РАЗМЕР:       1,9 м.
ВЫНОСЛИВОСТЬ: Высокая  
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Призыв ледяной глыбы
НАГРАДА:      700
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Один из самых опасных магов среди илладжеров, использующий в бою мощную ледяную магию. Если обычный разбойник просто стреляет, а призыватель создаёт клыки и вексов, то ледяной колдун призывает на голову жертвы глыбу льда, которая не только наносит огромный урон, но и оглушает противника на несколько секунд.

Внешне ледяной колдун разительно отличается от своих сородичей - разбойников и поборников. Он одет в длинный синий балахон с белой окантовкой, а его глаза имеют красновато-коричневый оттенок. На лице у него синие узоры - ритуальные отметины, которые подчёркивают его связь с магией.

В бою ледяной колдун - классический маг поддержки. Он никогда не лезет в ближний бой, а предпочитает держаться на дистанции и поливать врага заклинаниями.

Главная и единственная боевая способность колдуна - призыв огромной ледяной глыбы прямо над головой противника. Глыба не падает мгновенно: она зависает на 1,75-4 секунды, преследуя цель, а затем обрушивается вниз. При столкновении с землёй или жертвой глыба взрывается, нанося огромный урон и оглушая всех, кто оказался в радиусе поражения.

Технически глыба представляет собой тройной куб льда, покрытый снегом.

Он ненавидит Светящихся Спрутов.

СОВЕТЫ:

- Ледяной колдун - приоритетная цель. Пока он жив, над твоей головой постоянно будет висеть ледяная глыба.
- В момент колдовства он неподвижен - это идеальное окно для атаки.
- Не стой на месте. Глыба преследует цель, но падает туда, где ты был в момент фиксации. Постоянное движение снижает риск прямого попадания.
- Услышал характерный звук заклинания - немедленно уходи в сторону.
- Используй укрытия. Глыба падает только там, где есть прямая видимость.
