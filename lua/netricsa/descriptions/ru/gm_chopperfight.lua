ШОССЕ-17, ВЕРТОЛЁТ ОХОТНИК ПРИБЛИЖАЕТСЯ


Мы находимся в ветхом здании, прилепившемся к скале прямо над пропастью. Внизу - камни и, судя по высоте, гарантированная смерть при падении в воду.

Вокруг - только небо, ветер и звук приближающихся лопастей. Охотник уже здесь. Он не спешит, кружит, выбирает момент. Знает, что ты где-то внутри.

Тут есть ящики с патронами для РПГ. Разбросаны по зданию.

Хлипкая платформа снаружи. К зданию пристроена дощатая конструкция, нависающая над пропастью. Выглядит так, будто развалится от чиха. Но именно оттуда открывается идеальный обзор на вертолёт. Идеальный, Сэм, и очень опасный.

Пора показать чьи пушки мощнее.
