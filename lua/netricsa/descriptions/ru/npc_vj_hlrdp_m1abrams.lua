Y.S.M. M1 ABRAMS


КЛАСС:         Y.S.M. M1 Abrams
МЕТАБОЛИЗМ:    Механический, Управляемый Наёмником
ВОСПРИЯТИЕ:    Средняя Бронированная Машина, Пехотная Поддержка
РАЗМЕР:        ~4,7 м. 
ВЫНОСЛИВОСТЬ:  Средняя 
ВРАЖДЕБНОСТЬ:  Экстремальная
ОРУЖИЕ:        Ракетная установка
НАГРАДА:       5000
УГРОЗА:        Очень высокая

ОПИСАНИЕ:

Это облегчённая, уменьшенная версия знаменитого танка, которую Y.S.M. используют для быстрых рейдов и поддержки пехоты на базе Дельта. В отличие от своего тяжёлого, неповоротливого оригинала, эта модификация пожертвовала бронёй и размером ради скорости и мобильности.

Внешность у машины характерная: тёмно-серый камуфляж с яркими жёлтыми полосами на бортах и башне. Меньший размер позволяет ему маневрировать в узких уличных пространствах и даже проезжать там, где обычный танк застрял бы. Броня тоньше - она рассчитана на защиту от стрелкового оружия и лёгких взрывчатых веществ, но прямое попадание из РПГ или противотанковой ракеты отправит машину в металлолом.

Y.S.M. Abrams стреляет чаще, выпуская ракету каждые несколько секунд. Ракеты имеют хорошую точность и радиус взрыва, но меньшую бронебойность, чем у оригинальной модели.

Они ездят быстро, стреляют метко и могут сменить позицию за считанные секунды.

СОВЕТЫ:

- Эта версия меньше и быстрее, но её броня слабее. Один или Два точных выстрела из РПГ могут вывести его из строя.
- Используй укрытия - ракеты летят часто, и если ты замешкался, следующая прилетит через пару секунд.
- Не пытайся атаковать в лоб. Заходи с фланга или с тыла.
