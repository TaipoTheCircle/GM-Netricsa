АЛЬЯНСКАЯ ЭЛЕКТРИЧЕСКАЯ ВИНТОВКА "ЖНЕЦ"


ОРУЖИЕ:      Электрическая Винтовка Альянса "Жнец"
БОЕПРИПАСЫ:        Высоковольтные энергетические элементы
ТЕМП ОГНЯ:         ~120 разрядов/мин

ОПИСАНИЕ:

Экспериментальное электрическое оружие, выдаваемое инженерам Альянса. Выпускает сконцентрированные электрические дуги, которые наносят парализующий удар по целям. Эффективно против пехоты и электронных систем, выводя из строя врагов и нанося серьезный урон благодаря непрерывной разрядке.

ПРИМЕЧАНИЯ:

- Основной огонь выпускает непрерывные электрические дуги.
- Чрезвычайно эффективно против небронированных и биологических целей.
- Может выводить из строя механизмы, турели и электронные устройства.
- Ограниченная дальность; требует средней или близкой дистанции.