ОПИСАНИЕ:

Солдат Overwatch с гранатомётом Альянса - это ходячее предупреждение о том, что укрытие больше не считается безопасным. Он облачён в светлую, небесно-голубую пятнистую броню, предназначенную для операций в заснеженных зонах, на высотных комплексах и в стерильных индустриальных районах. Синий визор подчёркивает его роль как бойца дистанционного подавления.

Основное оружие внешне напоминает массивный дробовик, но вместо картечи он выстреливает очередь из трёх гранат за один залп. Такой выстрел накрывает площадь сразу, превращая коридоры, лестничные пролёты и баррикады в зону мгновенного хаоса. Альянс использует этих солдат для выбивания противника из укреплённых позиций без затяжных перестрелок.

Гранаты запрограммированы на оптимальный подрыв, что делает бегство почти бессмысленным. Даже если первая граната не заденет цель, вторая и третья обычно завершают начатое. Солдат действует методично, выцеливая скопления врагов и предполагаемые маршруты отхода.

