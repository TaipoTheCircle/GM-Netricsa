СБРАСЫВАТЕЛЬ ТЕЛЕПОРТОВ СТРОГГОВ С СТРОГГОСА


КЛАСС:        Сбрасыватель Телепортов Строггов
МЕТАБОЛИЗМ:   Кибернетический четвероногий
ВОСПРИЯТИЕ:   Тактический ретранслятор / Живая фабрика
РАЗМЕР:       ~1,2 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайно высокая
ОРУЖИЕ:       Укус, вызов подкреплений
НАГРАДА:      2000
УГРОЗА:       Высокая

ОПИСАНИЕ:

Сбрасыватель Телепортов Строггов - это один из самых неприятных существ в арсенале строггов не потому, что он силён сам по себе, а потому, что он умеет делать так, что врагов становится больше. Прямо во время боя.

Внешне Сбрасыватель напоминает помесь собаки, насекомого и механизма размером с крупного тигра. Четвероногий, быстрый, юркий, с хищной мордой и кибернетическими конечностями.

Главная способность Сбрасывателя - ставить миниатюрные телепортационные станции. За одну раз он может установить до четырёх таких штук, из которых лезут подкрепления. Чаще всего это Стражи и Гранты, иногда - другие типы пехоты, но, к счастью, Гладиаторы и Танки через них не проходят.

СОВЕТЫ:

- Убивай первым. Сбрасыватель важнее любого Гладиатора, потому что он делает так, что врагов становится больше.
- Не давай ему бегать. Загоняй в угол и добивай скорострельным оружием.
- Если рядом есть другие строгги - всё равно вали Сбрасывателя. Они подождут.
