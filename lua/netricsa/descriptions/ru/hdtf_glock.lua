ПИСТОЛЕТ GLOCK 17


ОРУЖИЕ:      Пистолет Glock 17
ПАТРОНЫ:        9x19 мм
CC:         Полуавтоматический, ~15 выстрелов/мин

ОПИСАНИЕ:

Стандартное полуавтоматическое короткоствольное оружие. Надежное, легкое и всегда под рукой. Не обладает высокой останавливающей силой, но эффективно для точных выстрелов по слабым или удаленным целям.

ПРИМЕЧАНИЯ:

- Умеренная точность, низкая отдача.
- Неэффективно против бронированных целей.
- Полезно для экономии более тяжелых типов боеприпасов.