СКЛАД ЧЁРНОЙ МЕЗЫ, КОНТЕЙНЕРЫ


Добро пожаловать внутрь, Сэм. Здесь темнее, тише и пахнет так, будто склад давно понял, чем всё закончится, и смирился.

В центре зала два контейнера. Огромные. Один синий, другой красный. Классический выбор. Почти философский. Я бы сказала, что один ведёт к истине, а другой к неминуемой гибели, но, зная Чёрную Мезу, оба варианта могут быть верны одновременно.

Есть ли там Розенберг? Неизвестно. Возможно, он внутри. Возможно, под контейнером. Возможно, его там нет вообще, и это очередная проверка твоей веры в причинно-следственные связи. Учёные любят прятаться в местах, куда здравомыслящий человек не полез бы добровольно. А Розенберг, судя по всему, здравомыслием уже пожертвовал ради портала.

Контейнеры охраняют солдаты H.E.C.U. Им явно сказали: "Охраняйте. Что внутри - не ваше дело. Если спросят - стреляйте". Они и охраняют. Вопросы тут не приветствуются, как и выживание свидетелей.

Обрати внимание: охрана расставлена так, будто внутри что-то действительно ценное. Или крайне опасное. А чаще всего в Чёрной Мезе это одно и то же.

Синий. Красный. Два контейнера. Два возможных неправильных решения.

Выбирай аккуратно, Сэм.

Хотя... кого я обманываю. Ты всё равно откроешь оба.
