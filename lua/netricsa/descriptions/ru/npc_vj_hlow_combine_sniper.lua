ОПИСАНИЕ:

Усиленный снайпер Overwatch - это экспериментальная версия стандартного снайпера Альянса, созданная для максимальной эффективности на любых дистанциях. Его белый шлем оснащён двумя синими оптическими визорами, расположенными по центру. Первый визор оптимизирован для ближнего боя и городских дистанций, второй - для сверхдальнего наблюдения и стрельбы на экстремальных расстояниях.

Броня у бойца облегчённая, сине-белого цвета, что снижает защиту, но повышает мобильность и скорость передвижения. Альянс считает, что лучший снайпер - это тот, по кому не успевают выстрелить.

Главная особенность оружия - энергетический прицельный луч. В обычном режиме он синий, но за доли секунды до выстрела меняет цвет на красный, сигнализируя о критической фазе наведения.

СОВЕТЫ:

- Если луч стал красным - немедленно меняй позицию.
- У него слабая броня, но идеальная точность.
- Никогда не стой на открытых площадках, если он где-то рядом.
