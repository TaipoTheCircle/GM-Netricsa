ПЕЩЕРНЫЙ РАПТОР-АЛЬБИНОС С ТЕРРАФОРМИРОВАННОЙ ПЛАНЕТЫ


КЛАСС:        Пещерный Раптор-Альбинос
МЕТАБОЛИЗМ:   Хищник, Теропод
ВОСПРИЯТИЕ:   Подземный охотник
РАЗМЕР:       ~2,5 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Когти, зубы, темновая адаптация
НАГРАДА:      500
УГРОЗА:       Средняя

ОПИСАНИЕ:

Этот вид - результат эволюции в условиях полной изоляции от солнечного света. Тысячи поколений, прожитых в кромешной тьме пещер, сделали своё дело: эти твари потеряли пигментацию и стали альбиносами .

Внешне пещерный раптор отличается от своих наземных сородичей бледно-белой кожей с едва заметными полосами. Глаза светятся в темноте зловещим красным светом.

В отличие от обычных рапторов, альбиносы активны только в тёмных местах и никогда не выползают на солнечный свет самостоятельно.

