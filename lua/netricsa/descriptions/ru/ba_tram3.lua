ЧЁРНАЯ МЕЗА, ВСТРЕЧА С "ОТИСОМ"


Эй, Смотри‑ка - впереди "Отис" машет рукой. Только не торопись радоваться: это не он. Очередной Женерик‑Актёр примерил чужую личину. И, судя по всему, пока настоящий Отис набивает брюхо в кафетерии, этот двойник должен сыграть роль дружелюбного коллеги.

Запомни: Женерики - мастера маскировки. Могут прикинуться кем угодно: учёным, охранником, а то и вовсе предметом - книгой на столе или кожурой банана у входа. Мотивы? Неясны.

Этот "Отис" улыбается, называет тебя по имени - "Барни Калхаун". Звучит так, будто ты и должен быть на этом поезде. Только вот вопрос: куда ехал настоящий Барни? И почему его место занял... ну, скажем, ты? Хотя схожая ситуация с Физиком-Теортиком меня уже не удивляет.

Поезд тихо едет, за окном мелькают туннели. Впереди - зона охраны. Там, где всё началось в прошлый раз. Где‑то там другой Сэм уже направляется в тестовую камеру, а может ещё ничего не понял. Скоро грянет катастрофа. Но пока... пока ты просто пассажир. Почти.

Держи ухо востро. Если Женерик заговорил с тобой - значит, ему что‑то нужно. Или он просто проверяет, насколько хорошо ты играешь роль "своего".

Вперёд, "Барни". Зона охраны уже близко. И там точно будет не до масок.