ДИЛОФОЗАВР С ТЕРРАФОРМИРОВАННОЙ ПЛАНЕТЫ


КЛАСС:        Дилофозавр
МЕТАБОЛИЗМ:   Хищник, Теропод
ВОСПРИЯТИЕ:   Засадный хищник
РАЗМЕР:       ~2,5 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Крайне высокая
ОРУЖИЕ:       Таран головой, удар хвостом, челюсти
НАГРАДА:      1000
УГРОЗА:       Высокая

ОПИСАНИЕ:

Один из наиболее опасных хищников планеты, занимающий в пищевой цепочке промежуточное положение между стайными велоцирапторами и гигантами вроде тираннозавра. Вопреки распространённым заблуждениям, местный вид дилофозавров не имеет отношения к плевкам ядом и обладает вполне традиционным для своего размера набором охотничьих навыков.

Этот ящер представляет серьёзную угрозу даже для хорошо вооружённых людей благодаря трём ключевым особенностям. Во-первых, он способен развивать высокую скорость и настигать бегущего человека. Во-вторых, его атака представляет собой стремительный рывок с последующим тараном головой, способным сбить с ног. В-третьих, дилофозавр активно использует свой мощный хвост как гибкое оружие, разворачиваясь всем корпусом для хлесткого удара.

СОВЕТЫ:

- В одиночку он опасен, в паре - смертелен.
- Если дилофозавр начал атаку - уворачивайся.
