ПРОТИВОТАНКОВЫЙ СОЛДАТ OVERWATCH АЛЬЯНСА


КЛАСС:        Противотанковый солдат Overwatch Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Специализированное подразделение борьбы с техникой
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Очень высокая
ОРУЖИЕ:       Улучшенный SMG, ракетница, противотанковые ракеты
НАГРАДА:      750
УГРОЗА:       Высокая


ОПИСАНИЕ:

Противотанковый солдат Overwatch внешне почти неотличим от стандартного бойца Альянса. Почти та же броня, та же осанка, тот же холодный синий визор. Огромная ошибка - принять его за "обычного". Его истинная роль раскрывается только тогда, когда на поле боя появляется техника.

На спине солдата закреплён блок ракет, а ракетница извлекается мгновенно, как только система распознаёт цель в транспорте. Машины, бронетехника, импровизированные броневики сопротивления - всё это для него приоритет номер один.

Улучшенный SMG используется для самообороны и подавления пехоты, пока солдат занимает позицию для удара по технике. Однако затяжные перестрелки не его задача. Он действует быстро, бьёт больно и старается сменить позицию сразу после запуска ракеты, зная, что ответный огонь последует немедленно.

Альянс применяет таких бойцов для нейтрализации мобильности сопротивления. Один противотанковый солдат способен превратить колонну техники в пылающий металлолом за считанные секунды.

СОВЕТЫ:

- Никогда не подъезжай к нему на транспорте.
- Сначала убей его, потом используй технику.