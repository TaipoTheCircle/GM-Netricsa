ТАКТИЧЕСКИЙ СОЛДАТ OVERWATCH С ДРОБОВИКОМ АЛЬЯНСА


КЛАСС:        Тактический солдат Overwatch с дробовиком Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Тяжёлое штурмовое подразделение ближнего боя Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Боевой дробовик SPAS-12, гранаты
НАГРАДА:      800
УГРОЗА:       Очень высокая


ОПИСАНИЕ:

Тактический солдат Overwatch с дробовиком - это усиленная версия штурмового бойца, созданная для проламывания обороны там, где гибридные подразделения начинают нести потери. Внешне он напоминает гибридного солдата, но ключевое отличие скрыто под бронёй.

Тактический шлем полностью нейтрализует уязвимость к выстрелам в голову. Даже точные хедшоты не выводят бойца из строя. Альянс намеренно "перекормил" этих солдат бронёй, превратив их в неотвратимо наступающие таранные единицы.

SPAS-12 используется как инструмент давления и зачистки. Солдат продвигается вперёд под огнём, игнорируя попадания, сближаясь с целью до дистанции гарантированного уничтожения. Гранаты применяются для расчистки проходов и выбивания противника из укреплённых позиций.

СОВЕТЫ:

- Хедшоты неэффективны - целься в корпус или используй взрывчатку.
- Не позволяй ему сократить дистанцию - вблизи он почти гарантированно смертелен.
- Тяжёлое вооружение и ловушки работают лучше всего.
- Отступление и дистанция - ключ к выживанию.