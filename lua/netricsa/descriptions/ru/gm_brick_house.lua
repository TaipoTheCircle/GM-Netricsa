КИРПИЧНЫЙ ДОМ, ПУСТОШЬ И ДЕРЕВЬЯ


Сэм, это место пахнет сталкером, который решил осесть и забить кладовку на ядерную зиму. Перед нами одинокий кирпичный дом, стоит себе на зелёной траве, как будто его сюда просто поставили и забыли.

Рядом - никакого города, никаких соседей. Только 11 деревьев.

Дом крепкий, красный кирпич, крыша на месте. Но есть нюансы:

Двери нет. Вообще. Просто проём, зияющий чёрной пустотой. То ли сняли, то ли никогда не было.

Два окна по бокам от входа.

С правой стороны - три автомобиля. Когда-то на них можно было уехать, но теперь это просто ржавые коробки без колёс. Стоят как напоминание: отсюда не уехать.

Внутри - склад, а не жильё. Кто-то явно готовился к концу света:

Две лампы под потолком. Свет дают ровно настолько, чтобы разглядеть, что тут есть.

Пять ящиков: один большой, четыре маленьких. Закрыты намертво. Не открываются, не ломаются, не двигаются. Просто стоят и дразнят.

На правой стене - часы. Работают? Тикают? Показывают время хоть раз? Неизвестно. Но висят.

Там же - шкаф с двумя ружьями. Но не думай, Сэм. Они пригвождены. Кто-то пришёл, повесил стволы и зафиксировал их так, что не сдвинешь. Как предупреждение. Или как насмешка.

Запасы - вот где золото:

А вот тут начинается самое интересное. Кто-то очень тщательно подошёл к вопросу выживания:

"Sweet potatoes" - сладкий картофель, основа рациона.

"Apples - the world's finest". Самые лучшие яблоки в мире. Так и написано.

"Sofia's microwave onners" - 112 count. Микроволновые блюда от Софии. 112 штук. София либо гений логистики, либо просто очень любит готовить.

"Jabber's assorted snacks" - ассорти снеков от Джаббера. На любой вкус.

"Rolee Juice Drinks" - соки. Витамины.

"Vanilla Drink" - ванильный напиток. Для разнообразия.

"Diet soda" - диетическая газировка. Фигура важна даже в апокалипсисе.

"Fresh potatoes" - свежая картошка. Просто картошка. Свежая.

Дом - идеальная точка обороны, если бы не одно "но". Здесь нет двери. Любой может зайти. Так что это скорее ловушка, чем крепость. Если сюда заявятся враги, единственный выход - через тот же проём, где была дверь.

Запасы - отличная мотивация защищать это место. С таким набором можно пережить зиму, две зимы и ещё пару месяцев. Но ружья прибиты, так что придётся отбиваться тем, что принёс с собой.

Если решим обосноваться здесь, первым делом ищем дверь. Или доски, чтобы заколотить вход. А потом - пробуем вскрыть те ящики. Потому что кто знает, что там внутри. Может, там ключи от машины. Или от тех ружей. Или просто ещё консервы. Но любопытство, Сэм, оно своё возьмёт.

Погнали внутрь. Только тихо.
