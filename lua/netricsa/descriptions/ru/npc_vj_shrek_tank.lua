ШРЕК, ЗОМБИ-ТАНК С ТРИДЕВЯТОГО КОРОЛЕВСТВА


КЛАСС:        Шрек, Зомби-Танк
МЕТАБОЛИЗМ:   Биологический Огр-Мутант
ВОСПРИЯТИЕ:   Крупный Элитный заражённый боец ближнего боя
РАЗМЕР:       3 м.
ВЫНОСЛИВОСТЬ: Экстремально высокая
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Кулаки, камни, таран, рывки, рык
НАГРАДА:      5000
УГРОЗА:       Абсолютная

ОПИСАНИЕ:

Этот образец заражённого вызывает у выживших смешанные чувства: с одной стороны, перед ними огромный, зелёный, лысый мутант, который вырывает куски асфальта и швыряется ими. С другой - из его груди доносится песня "Smash Mouth All Star". Шрек, видимо, проглотил радио прямо перед заражением, и теперь динамик застрял у него где-то в желудке, на полной громкости транслируя "Эй, ты же звезда, давай, играй!".

В остальном его поведение полностью повторяет классического Танка. Он не чувствует боли, не останавливается перед преградами и обладает одной целью - размазать выживших по стене. Его любимые приёмы: вырвать кусок тротуара и запустить им в цель, а если дистанция сокращается - просто снести всё тяжёлой рукой. Удар такой силы, что выжившего отбрасывает на добрых десять метров.

Иногда Шрек издаёт рык. Обычный Танк орёт так, что уши закладывает. Этот рычит так же громко, но после рыка из динамика доносится "Я не самый умный человек на свете". Выжившие не знают, смеяться им или плакать. Большинство выбирает плакать.

Происхождение этого экземпляра неизвестно. Одни говорят, что в лаборатории пытались скрестить вирус с болотной экосистемой. Другие - что это просто какой-то здоровенный парень в зелёной коже, который слишком сильно любил этот трек и унёс любовь с собой в могилу. Третьи просто стреляют, не задавая вопросов.

СОВЕТЫ:

- Шрек кидается камнями - уворачивайся. Они летят медленно, но больно.
- В ближнем бою он смертелен. Не подходи, если не хочешь стать болотной жижей.
- По громкости можно определить, насколько Шрек близко.
- Если ты слышишь "Столько всего нужно сделать, столько всего нужно увидеть", а пол под тобой дрожит - беги. Беги прямо сейчас.
- Убить его можно, но это потребует тонны боеприпасов и удачи.
- Говорят, если слушать его внимательно, то заиграет припев. Но ты вряд ли будешь слушать - ты будешь просто рад, если он заткнётся.
