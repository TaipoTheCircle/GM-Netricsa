ШТУРМОВАЯ ВИНТОВКА M16A2


ОРУЖИЕ:     Кольт M16A2
ПАТРОНЫ:    Патроны 5,56x45 мм НАТО 
CC:         ~700 выстрелов/мин

ОПИСАНИЕ:

Надежное штурмовое ружье американского производства, широко используемое ВС США. Обеспечивает огонь очередью с приемлемой точностью. Его старшего брата, M16A1, использовали во вьетнамской войне.

ПРИМЕЧАНИЯ:

- Стандартное пехотное оружие с надежной производительностью.- 
- Точность страдает при длительной стрельбе.
