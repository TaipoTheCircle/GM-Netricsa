АРКТИЧЕСКИЙ ТЮРЕМНЫЙ СОЛДАТ OVERWATCH С ДРОБОВИКОМ АЛЬЯНСА


КЛАСС:        Арктический тюремный солдат Overwatch с дробовиком Альянса
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Карательно-штурмовое подразделение Альянса
РАЗМЕР:       1,8 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Боевой дробовик Spas-12, гранаты
НАГРАДА:      700
УГРОЗА:       Высокая

ОПИСАНИЕ:

Арктический тюремный солдат Overwatch с дробовиком - это боец Альянса, изначально приписанный к комплексу Нова Проспект, но временно переброшенный для выполнения задач в арктических регионах. Подобная практика применяется, когда в тюремных зонах наблюдается избыток живой силы, а в удалённых секторах ощущается нехватка штурмовых подразделений.

После переработки в системе Нова Проспект их психика подавлена почти полностью, а повиновение обеспечивается постоянным контролем и болевыми импульсами. В бою они действуют без колебаний и самосохранения.

Он носит арктический бело-серый камуфляж, модифицированный поверх стандартной тюремной экипировки. Визор шлема светится оранжевым цветом - характерная особенность тюремных солдат, выдающая их происхождение даже в снежной мгле. На спине отчётливо нанесена голубая надпись "Нова Проспект", служащая одновременно маркировкой и напоминанием о том, куда солдат будет возвращён в случае неповиновения.

Как и другие дробовики Альянса, этот солдат предназначен для ближнего боя. В условиях арктических станций, ледяных тоннелей и заброшенных полярных комплексов он используется для жестоких зачисток, подавления бунтов и устранения целей, где применение стандартной пехоты считается неэффективным.

СОВЕТЫ:

- Не рассчитывай на его осторожность - тюремные солдаты часто идут напролом.
- Оранжевый визор заметен, но реагирует он быстрее, чем кажется.
- Надпись Нова Проспект означает, что солдату нечего терять.
- В замкнутых помещениях дробовик делает его смертельно опасным.