МАНКУБУС, УТИЛЬ-РАКЕТЧИК С АДА

 
КЛАСС:        Манкубус, Утиль-Ракетчик
МЕТАБОЛИЗМ:   Восстановленое Демоническое Млекопитающее
ОПРЕДЕЛЕНИЕ:  Модифицированный Сшитый Из Трупов Бехема
РАЗМЕР:       4 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Две РПГ
НАГРАДА:      3000
УГРОЗА:       Высокая
 
ОПИСАНИЕ:
 
Демон, олицетворяющий несовершенство и жадность человека, чревоугодие и праздность. У него невероятно толстая кожа, массивные слоновьи ноги и маленькая голова. В целях уничтожения и взрывания жертв в предплечья манкубуса вживлены два РПГ, стреляющие Ракетами.

Те Манкубусы, что были убиты самыми ужасными способами, были сращены заново, в последствии чего становятся алыми. Их кожа приобретает цвет запёкшейся крови, а суть меняется. Ментал, который всегда ищет способы усилить свою Орду, выкупил несколько десятков этих тварей у Владыки Ада (вместе с Зомби-Солдатами и прочей нечистью) и отправил на перепрофилирование.

Вместо огнемётов им предплечья вживили ракетницы. Две РПГ, стреляющие не переставая, пока враг не умрёт. Даже если противник спрятался за стеной - ракеты будут лететь снова и снова, методично превращая укрытие в решето, а затем и самого стрелка в фарш.

В отличие от обыкновенного Манкубуса-огнемётчика, Манкубус Утиль-Ракетчик слабее по защите. Сваренный из кусков разных демонов, он не обладает монолитной бронёй, и убить его можно быстрее. Но только если успеть, пока он не превратил всё вокруг в воронку от взрывов.

СОВЕТЫ:
 
- Прячься за укрытием. Ракеты летят в разнобой, и каменная стена может спасти тебе жизнь, но ненадолго.
- Они стреляют бесконечно. Не жди, пока у них кончатся боеприпасы. У них их нет.
- Убивай быстро. Да, он слабее обычного Манкубуса, но каждая секунда промедления увеличивает шанс, что ты превратишься в кровавое месиво.
- Не стой на открытом месте. Ракеты имеют радиус разлёта, и прятаться за углом - единственный способ выжить.
- Не давай ему прицелиться. Если видишь, что Манкубус навёл на тебя свои ракетницы - немедленно уходи в сторону. Они стреляют не мгновенно, но когда начинают, остановить их почти невозможно.
- Используй взрывчатку. Одна метко брошенная C4 может уничтожить его быстрее, чем 5 выстрелов из дробовика.
