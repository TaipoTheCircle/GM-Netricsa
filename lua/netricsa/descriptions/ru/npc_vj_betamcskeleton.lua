БЕТА СКЕЛЕТ-ЛУЧНИК С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Бета Скелет-Лучник
ВОСПРИЯТИЕ:   Нежить, стрелковая единица
МЕТАБОЛИЗМ:   Магически поддерживаемая нежить
РАЗМЕР:       ~1,9 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Лук и стрелы
НАГРАДА:      200
УГРОЗА:       Средняя

Описание:

Бета Скелет-Лучник - классический пример того, как минимум плоти не мешает максимуму раздражения. Это оживший набор костей, удерживаемый магией, злобой и отличным чувством дистанции. В ближний бой он идти не любит, предпочитая методично превращать цель в подушечку для стрел.

При дневном свете скелет горит, что, впрочем, не мешает ему попытаться добить тебя последней стрелой, пока он красиво догорает.

Советы:

- Щит - лучший аргумент в споре со скелетом-лучником.
- Используй укрытия, иначе стрельба будет односторонней.
- Если скелет вдруг оказался верхом на пауке - считай, что он повысился в должности.
