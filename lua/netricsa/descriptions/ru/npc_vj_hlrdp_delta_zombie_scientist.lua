ЗОМБИ-ХЕДКРАБ БАЗЫ ДЕЛЬТА, ОБЫЧНЫЙ


КЛАСС:        Зомби-хедкраб, обычный
МЕТАБОЛИЗМ:   Человек-Носитель + Паразитический Хедкраб Зена
ВОСПРИЯТИЕ:   Зомбированная Сущность, Бывший Учёный
РАЗМЕР:       ~1,8 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Когти, укус, захват
НАГРАДА:      200
УГРОЗА:       Умеренная

ОПИСАНИЕ:

Это почти то же самое, что и стандартный зомби-хедкраб, но с одним важным отличием: тело носителя принадлежит бывшему учёному, который работал на засекреченной базе Дельта в пустыне Невады. Вместо белых лабораторных халатов их конкурентов из Чёрной Мезы, эти ребята носят серую униформу персонала - потрёпанную, рваную, пропитанную кровью и машинным маслом.

Процесс заражения тот же: Хедкраб вгрызается в череп, оставляя человеческое лицо частично видимым под инопланетным панцирем.

Они так же медлительны, как и их белые собратья, и так же неумолимы. Если такой зомби заметит живого человека, он будет преследовать его, пока не сократит дистанцию.

При убийстве выстрелом в голову - голова зомби отрывается.

СОВЕТЫ:

- Держи дистанцию. Они медленные, но если подойдут вплотную, когти и зубы сделают своё дело.
- Огнестрельное оружие эффективно. Не трать патроны на ближний бой с ними.
- При убийстве в голову голова отрывается.
- В группах они опасны. Один зомби - не проблема. Трое, зажимающие тебя в углу - уже проблема.
