ДРОИД АЛЬЯНСА


КЛАСС:        Дроид Альянса
МЕТАБОЛИЗМ:   Полностью синтетический (энергетическое ядро Альянса)
ВОСПРИЯТИЕ:   Тяжёлая роботизированная боевая платформа Альянса
РАЗМЕР:       ~3 м.
ВЫНОСЛИВОСТЬ: Экстремально высокая
ВРАЖДЕБНОСТЬ: Экстремальная
ОРУЖИЕ:       Чистая механическая сила, метание тяжёлых объектов, таран
НАГРАДА:      1200
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Дроид Альянса - это прямой, но искажённый ответ Альянса на Пса Сопротивления. Подсмотрев конструкцию у повстанцев, инженеры Overwatch переработали её, устранив "избыточную" импровизацию и эмоциональную составляющую, превратив идею в идеально выверенную боевую машину.

В отличие от Пса, Дроид полностью пропорционален: его корпус симметричен, движения выверены, а механика лишена резких, "живых" жестов. Голова развёрнута в иную сторону. Внешне он окрашен в глубокий чёрный цвет с красными и белыми элементами, а на корпусе отчётливо виден логотип Альянса.

Его задача проста: ломать, давить и уничтожать. Он способен с той же лёгкостью, что и Пёс, метать автомобили, вырывать турели из креплений и проламывать укрепления, но делает это методично и безошибочно.

Дроид интегрирован в тактическую сеть Альянса и часто действует в связке с солдатами Overwatch, прикрывая их, расчищая проходы или сея панику в рядах противника.

СОВЕТЫ:

- Прямое столкновение крайне не рекомендуется - его броня выдерживает колоссальные нагрузки.
- Используй окружение: обрушения, ловушки и мощные взрывы.