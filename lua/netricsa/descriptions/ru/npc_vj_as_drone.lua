ТРУТЕНЬ РОЯ


КЛАСС:        Трутень Роя
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Стайный Хищник
РАЗМЕР:       2 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Очень высокая
ОРУЖИЕ:       Серповидные когти
НАГРАДА:      200
УГРОЗА:       Средняя

ОПИСАНИЕ:

Они составляют основу любой армии Роя. Именно их чаще всего находят среди развалин колоний, в вентиляционных шахтах, тоннелях и прочих местах, где разумному существу находиться уже поздно.

Несмотря на относительно хрупкое телосложение, Трутень способен развивать высокую скорость и наносить удары четырьмя длинными когтями, способными вскрыть лёгкий бронекостюм быстрее, чем его владелец успеет пожаловаться на условия службы.

Точная биология Трутней до сих пор остаётся предметом споров. Одни учёные считают их взрослыми рабочими особями Роя. Другие предполагают, что это всего лишь промежуточная стадия развития более крупных существ. Сами Трутни в обсуждении участия не принимают, предпочитая использовать когти вместо аргументов.

Главная сила этих существ заключается не в индивидуальных качествах, а в количестве. Один Трутень редко представляет серьёзную проблему. Пятьдесят Трутней обычно придерживаются противоположного мнения.

СОВЕТЫ:

- Не позволяй Рою окружить тебя.
- Огнемёты и автоматическое оружие отлично сокращают популяцию Трутней.
- Если видишь одного Трутня, осмотрись. Остальные обычно уже бегут следом.
