ОПИСАНИЕ:

Взломанная повстанцами турель - это стандартная переносная турель Альянса, захваченная, грубо перекодированная и заново введённая в бой силами Сопротивления. Внешне она отличается изменённой окраской и следами полевого ремонта, но её смертоносная начинка остаётся прежней.

После перепрограммирования система распознавания целей больше не считает бойцов Альянса "своими". Теперь любой метрокоп или солдат Overwatch автоматически определяется как приоритетная цель. Турель немедленно открывает огонь при обнаружении движения или тепловой сигнатуры противника.

Повстанцы активно используют такие турели для обороны укрытий, подземных баз, коридоров и узких проходов. Благодаря компактности и высокой огневой плотности, одна турель способна удерживать позицию, на которую в ином случае потребовался бы целый отряд.

Несмотря на смену стороны, турель остаётся машиной Альянса по своей природе. Она не различает тактику, не экономит боеприпасы и не знает страха. Единственное, что изменилось - список тех, кого она должна уничтожить.

СОВЕТЫ:

- Размещай турели в узких проходах и на флангах - там они максимально эффективны.
- Следи за линией огня - турель не различает союзников, если они окажутся между ней и целью.