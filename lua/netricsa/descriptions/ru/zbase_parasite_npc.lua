ПАРАЗИТ РОЯ


КЛАСС:        Паразит Роя
МЕТАБОЛИЗМ:   Ксенобиологический Паразит
ВОСПРИЯТИЕ:   Инфицирующий Организм
РАЗМЕР:       0,3 м.
ВЫНОСЛИВОСТЬ: Очень Низкая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Инфицирование, Атака В Прыжке
НАГРАДА:      50
УГРОЗА:       Высокая

ОПИСАНИЕ:

Паразит является одной из самых многочисленных и опасных форм жизни Роя. Несмотря на небольшие размеры и хрупкое тело, эти существа представляют серьёзную угрозу даже для хорошо экипированных бойцов.

Основным способом охоты служит стремительный прыжок на жертву. После успешной атаки паразит внедряет в организм носителя личинки, которые начинают быстро развиваться внутри живых тканей. На ранних стадиях заражение может оставаться практически незаметным, однако со временем состояние жертвы стремительно ухудшается.

Особую опасность представляет жизненный цикл существа. В случае гибели заражённого организма личинки завершают развитие и покидают тело носителя, образуя новую стаю паразитов. Таким образом даже одна успешная атака может привести к быстрому росту популяции Роя.

Паразиты появляются из органических яиц, обычно размещаемых во влажных и защищённых местах. Внутренняя поверхность кладок заполнена густой биологической жидкостью, необходимой для развития молодых особей. По этой причине крупные скопления паразитов чаще всего встречаются в сырых тоннелях, подземных комплексах и заброшенных колониях.

СОВЕТЫ:

- Уничтожай паразитов прежде, чем они приблизятся на расстояние прыжка.
- Обнаружив кладку яиц, уничтожь её как можно быстрее.
- Один паразит редко представляет проблему. Проблемы обычно начинаются позже.
