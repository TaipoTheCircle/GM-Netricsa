ТРЕК АЛЬЯНСА


КЛАСС:        Трек Альянса
МЕТАБОЛИЗМ:   Механизированная платформа с интегрированным управлением
ВОСПРИЯТИЕ:   Тяжёлый транспорт Альянса
РАЗМЕР:       9 м.
ВЫНОСЛИВОСТЬ: Экстремально высокая
ВРАЖДЕБНОСТЬ: Экстремальная
ОРУЖИЕ:       Встроенная турель с пулемётом (управление изнутри)
НАГРАДА:      3200
УГРОЗА:       Критическая

ОПИСАНИЕ:

Трек Альянса - специализированный транспорт Альянса, конструктивно схожий с APC Транспортером, но адаптированный под перевозку Солдатов-Репликантов Альянса. Его форма более обтекаемая и замкнутая, а корпус выглядит цельным и глухим, словно литым. Главное отличие - полное отсутствие стёкол, открытых кабин и внешних постов управления.

Управление машиной осуществляется исключительно изнутри. Навигация ведётся через систему внешних сенсорных блоков, тактических камер и аналитических модулей Альянса. Экипаж не видит мир напрямую - только через фильтр данных. Это снижает уязвимость машины и полностью исключает человеческий фактор.

Турель Трека Альянса также управляется изнутри. Оператор подключён к системе наведения напрямую.

СОВЕТЫ:

- Отсутствие стёкол означает отсутствие очевидных уязвимых точек.
- Высаживает солдат.
