ЩИТОНОГ РОЯ


КЛАСС:        Щитоног Роя
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Тяжёлый Штурмовой Организм
РАЗМЕР:       3 м.
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Бронированные Конечности, Таранный Удар
НАГРАДА:      1000
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Щитоног является одной из наиболее защищённых боевых форм Роя. Передняя часть его тела покрыта массивными хитиновыми пластинами, способными выдерживать колоссальные повреждения. Благодаря этому существо способно наступать под плотным огнём, прикрывая менее живучих представителей Роя.

Главным оружием Щитонога служат его увеличенные передние конечности. Используя их как живой таран, он наносит мощные удары, способные отбросить противника на значительное расстояние. Известны случаи, когда жертвы погибали не от самого удара, а от последующего столкновения с окружающими объектами или падения с большой высоты.

Несмотря на практически непробиваемую лобовую защиту, организм имеет уязвимые участки. Наименее защищённой областью остаётся задняя часть тела, где располагаются основные органы жизнеобеспечения. По этой причине Щитоног инстинктивно старается удерживать противника перед собой и крайне неохотно разворачивается во время боя.

В отличие от большинства представителей Роя, Щитоног редко действует хаотично. Обнаружив цель, он способен долго и методично преследовать её, игнорируя менее опасных противников и концентрируясь на выбранной добыче.

СОВЕТЫ:

- Не трать боеприпасы на лобовую броню.
- Старайся атаковать с флангов или сзади.
- Не позволяй Щитоногу загонять тебя к обрывам и узким проходам.
- Если существо размером с транспортный модуль решило идти именно за тобой, это не повод для гордости.
