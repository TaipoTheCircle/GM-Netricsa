ПИСТОЛЕТ-ПУЛЕМЁТ MAC-11


ОРУЖИЕ:     MAC-11
ПАТРОНЫ:    Патроны 9x19 мм 
CC:         ~1200 выстрелов/мин

ОПИСАНИЕ:

Универсальное автоматическое оружие ближнего и среднего радиуса действия в городских боях. Используется в основном лос-анджелескими бандами по типу "Bloodz" и "Crips". Также был использован в фильме "Музыкант" 1992 года.

ПРИМЕЧАНИЯ:

- Быстро расходует боезапас.
- Точность значительно снижается на больших дистанциях.
- Вассап май бой мне тебя шотнуть за распальцовку?!