ОПИСАНИЕ:

SAS Overwatch Альянса - это перерождённая версия легендарной Special Air Service, подразделения, созданного Дэвидом Стирлингом во времена Второй мировой войны. Когда-то они проникали глубоко в тыл врага, устраивали диверсии и исчезали быстрее, чем противник успевал понять, что произошло. Альянс посмотрел на эту философию и сказал коротко: "Подходит".

После оккупации Земли бойцы SAS были интегрированы в структуру Overwatch и подвергнуты глубокой кибернетической модификации. Их нынешняя экипировка - белый тактический костюм с чёрным бронежилетом и массивный чёрный шлем с противогазом. Стёкла противогаза окрашены в зловещий красный цвет, за которыми скрываются кибернетизированные глаза, способные работать в инфракрасном, тепловом и низкоосвещённом режимах.

SAS Overwatch - мастера скрытного боя, диверсий и точечных ликвидаций. Они одинаково опасны как на дальних дистанциях, используя SSG-08, так и в ближнем бою с AR2 и MP40. Их стиль - быстрое проникновение, мгновенное уничтожение цели и исчезновение, оставляя после себя лишь тишину и отчёт в штабе Альянса.

Ирония судьбы в том, что подготовка SAS почти не изменилась. Как и раньше, учения остаются смертельно опасными и связано это с тем - что на учениях используются настоящие патроны (и иногда даже настоящие бомбы и ранцевые заряды).

СОВЕТЫ:

- Никогда не стой на месте - Солдаты SAS Overwatch любят работать из тени и с флангов.
- Их снайперы опасны даже на экстремальных дистанциях.