НАГРАДА:      1000
УГРОЗА:       Критическая


ОПИСАНИЕ:

Элитный тактический солдат Overwatch Альянса - это вершина эволюции наземных войск Дозора. Он носит стандартный белый костюм элитного солдата, поверх которого установлен чёрный бронежилет усиленной защиты. Эта комбинация делает бойца практически неуязвимым к стрелковому оружию и полностью исключает уязвимость к выстрелам в голову: хедшоты не работают ни при каких условиях.

Красный визор - не просто оптика, а боевой интерфейс, связанный с центральными протоколами Альянса. Он отслеживает цели, прогнозирует траектории и координирует действия с другими элитными подразделениями. Один взгляд такого солдата ощущается как приговор, вынесенный заранее.

В отличие от обычных элитных бойцов, тактический вариант не полагается на эффект неожиданности или демонстративную агрессию. Он методично продвигается вперёд, контролируя пространство, подавляя сопротивление точным импульсным огнём AR2 и расчищая зоны гранатами. Его движения лишены суеты, а решения принимаются быстрее, чем противник успевает осознать угрозу.

СОВЕТЫ:

- Забудь о точной стрельбе по голове - это бесполезно.
- Используй тяжёлое вооружение, взрывчатку и ловушки.