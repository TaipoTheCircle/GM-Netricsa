ТИРАННОЗАВР (МОЛОДОЙ) С ТЕРРАФОРМИРОВАННОЙ ПЛАНЕТЫ


КЛАСС:        Тираннозавр, Молодой
МЕТАБОЛИЗМ:   теплокровный хищник
ВОСПРИЯТИЕ:   Высший молодой хищник
РАЗМЕР:       ~2,5 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Крайне высокая
ОРУЖИЕ:       Клыки, когти, скорость
НАГРАДА:      800
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это подростковая версия того самого короля динозавров, которого все боятся. Он ещё не дорос до девяти метров и восьми тонн, но уже вполне способен превратить взрослого человека в кровавое пятно на асфальте за пару секунд.

Внешне подросток сильно отличается от матёрого хищника. Он более поджарый, длинноногий и медленный. Задние конечности длинные, что позволяет ему развивать приличную скорость и резко менять направление.

Челюсти уже мощные, но зубы пока не те "костеломы", что у взрослых.

Обитают молодые тираннозавры там же, где и взрослые - в джунглях. Но в отличие от матёрых особей, которые предпочитают засады и короткие рывки, подростки активны и агрессивны. Они могут преследовать жертву долго, используя свою скорость и выносливость.

В бою молодой тираннозавр опасен именно своей мобильностью. Он не будет стоять на месте - будет носиться вокруг, кусать, бить хвостом и пытаться схватить передними лапами.

СОВЕТЫ:

- В одиночку они встречаются редко. Если видишь одного, значит, мать где-то рядом. А их мать ты точно не захочешь встречать.
