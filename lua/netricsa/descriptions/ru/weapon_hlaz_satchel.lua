РАДИО-УПРАВЛЯЕМАЯ МИНА


ОРУЖИЕ:      Раудио-Управляемая Мина
ПАТРОНЫ:                  Пластиковые взрывные пакеты
CC:       Дистанционное взрывание

ОПИСАНИЕ:

Дистанционно управляемый взрывной пакет. Может быть установлен и взорван по желанию с помощью ручного передатчика. Полезен для ловушек или засад.

ПРИМЕЧАНИЯ:

- Позволяет стратегически размещать.
- Дистанционное взрывание обеспечивает точное управление.