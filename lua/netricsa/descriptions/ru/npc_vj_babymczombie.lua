ЗОМБИ-ДЕТЁНЫШ С ВЕРХНЕГО МИРА МАЙНКРАФТА


КЛАСС:        Зомби-Детёныш
МЕТАБОЛИЗМ:   Нежить
ВОСПРИЯТИЕ:   Враждебный моб
РАЗМЕР:       0,9 м.
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Чрезвычайно высокая
ОРУЖИЕ:       Руки, иногда с мечом
НАГРАДА:      300
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это доказательство того, что дело не в размере, а как его использовать. Маленький, быстрый и абсолютно безжалостный, он умудряется быть опаснее своего взрослого собрата, будучи в два раза меньше ростом.

Внешне Зомби-Детёныш выглядит как уменьшенная копия обычного зомби, но с парой отличий: он не становится взрослым со временем, никогда не вырастает и навсегда остаётся в этом крошечном теле. Настолько мал, что он пролезает в любые щели: через заборы, через полублоки, через решётки, через дыры в стенах, куда не пролезет даже житель. Зомби-Детёны может сидеть верхом на курице, образуя знаменитого "Куриного жокея".

Он бегает быстрее взрослых, не сбавляет темп на ходьбе и атакует непрерывно. Днём, в отличие от взрослых, он не горит на солнце - его маленькое тело как-то умудряется выдерживать солнечный свет. Он не тонет в воде, хотя и не плавает, и не превращается в утопленника.

В бою Зомби-Детёныш использует свою миниатюрность и скорость. Он прыгает, кружится вокруг цели, заходит с флангов, атакует спину. Попасть по нему сложно - удар мечом часто проходит над головой, стрела пролетает мимо. Если он вооружён мечом, урон становится ещё выше, а его размер не даёт использовать стандартные тактики удержания дистанции.

В отличие от взрослых зомби, детёныш никогда не интересуется деревенскими жителями и не пытается ломать двери. Его цель - исключительно жертва.

СОВЕТЫ:

- Не пытайся убежать - он быстрее тебя .
- Целься ниже. Удар мечом в ноги работает лучше, чем в голову.
- Используй щит - он блокирует атаки и даёт возможность ответить.
- Поднимись на блок. Детёныш не может прыгнуть выше 1,5 блоков.
- Узкие проходы не спасут - он пролезет в любую щель.
- При скоплении детёнышей используй взрывчатку или ловушки с лавой.
- Не оставляй его без присмотра рядом с базой - он пролезет через любой забор.
