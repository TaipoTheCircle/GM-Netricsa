ОПИСАНИЕ:

Одесса Каббедж - человек, который называет себя полковником с такой уверенностью, что большинство окружающих предпочитает не уточнять подробности. Он возглавляет небольшую ячейку Сопротивления на побережье и управляет боевыми действиями с безопасного расстояния, используя рацию и хорошо поставленный командный тон.

Основной вклад Каббеджа в борьбу с Комбайнами - логистика. Он выдаёт РПГ, объясняет, куда им стрелять, и затем наблюдает за процессом, не вмешиваясь. Его военная форма украшена нашивками, происхождение которых вызывает вопросы, но в условиях тотального вторжения такие мелочи считаются несущественными.

Несмотря на ограниченное участие в боях, Каббедж искренне верит в победу Сопротивления и в то, что правильная речь по радио иногда эффективнее, чем личное присутствие на поле боя.

