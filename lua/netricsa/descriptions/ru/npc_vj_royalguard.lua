КОРОЛЕВСКАЯ ИМПЕРСКАЯ ГВАРДИЯ С НИРНА


КЛАСС:        Королевская Имперская Гвардия
МЕТАБОЛИЗМ:   Мер, Мужчина
ВОСПРИЯТИЕ:   Личная стража Императора / Элитный спецотряд Имперского Легиона
РАЗМЕР:       1,8 м. 
ВЫНОСЛИВОСТЬ: Очень высокая 
ВРАЖДЕБНОСТЬ: Условная
ОРУЖИЕ:       Длинные мечи из тонкой стали, тяжёлые клейморы, Имперский щит
НАГРАДА:      7000
УГРОЗА:       Абсолютная

ОПИСАНИЕ:

Это не просто солдаты. Это "величайшие воины-маги Тамриэля", личная тень Императора, элита Имперского Легиона. Если обычный легионер защищает границы и ловит бандитов, то Королевская Гвардия отвечает за единственное - жизнь и безопасность Императора и его ближайшего окружения. Их казармы и тренировочные залы расположены прямо внутри Имперского Дворца, чтобы они могли реагировать на угрозу мгновенно.

Стать гвардейцем невероятно сложно. Кандидатов отбирают ещё в детстве, называя их "Потенциалами". Их обучают в секретных академиях, таких как Забытый Легион, где тренируют не просто бойцов, а воинов-магов, одинаково владеющих мечом и заклинанием. Каждый год проводится Турнир Потенциалов, где победители получают право на испытание. Лишь немногие выживают и проходят отбор в Королевскую Гвардию.

Королевская Гвардия существовала столетия, охраняя династию Септимов. Однако их история трагична. Во время Имперского Фарса (3E 389) генерал Талин Вархафт и многие гвардейцы были изгнаны в Обливион, а их места заняли двойники, созданные Джагаром Тарном. Позже, во время Кризиса Обливиона, когда император Уриэль Септим VII был убит Мифическим Рассветом, гвардия присоединилась к расследованию его смерти вместе с Имперским Легионом, Старейшинами и Гильдией магов.

После падения Септимов и установления Медской династии статус Королевской Гвардии неясен.

Гвардия подчиняется напрямую Императору и его ближайшим военачальникам. В их структуру входят следующие ранги:

Генерал Гвардии - верховный командующий.

Центурион - командует подразделениями.

Капитан - отвечает за конкретные объекты и операции.

Лейтенант - помощник капитана.

Сержант - младший командир.

Гвардеец - основной боевой состав.

СОВЕТЫ:

- Если ты планируете нарушить закон в Имперском городе - не делай этого рядом с Башней Белого Золота. Гвардейцы убьют тебя быстрее, чем ты успеешь достать оружие.
- Гвардейцы не поддаются на уговоры. В отличие от обычных стражников, их нельзя подкупить или убедить.
- В бою с гвардейцем используй дальнобойную магию и избегай ближнего контакта.
- Если ты видишь капитана гвардии - будь особенно осторожен. Они имеют более высокий уровень и лучшее снаряжение.
- В отличие от обычной стражи, броню гвардейцев нельзя снять с трупа. Не трать время на попытки обобрать их.
- Если ты встретил гвардейца, вооружённого клейморой вместо длинного меча - беги.
- Даже после смерти Императора гвардейцы продолжают охранять Дворец. Их долг не заканчивается со смертью Императора.
