ОБРЕЗ


ОРУЖИЕ:  Обрез
ПАТРОНЫ: Патроны 12-го калибра
СС:      ~46.5 выстр/мин 


ОПИСАНИЕ:

Обрез - парный двуствольный дробовик. При нажатии выстреливает оба ствола сразу, создавая мощный залп дроби. Отличается сравнительно узким разбросом для дробовика такого типа, что делает его эффективным на средних дистанциях. После выстрела имеется небольшая задержка перед следующим использованием. 

ПРИМЕЧАНИЯ:

- Эффективен против врагов средней прочности, таких как Kleer Skeletons, Levitators и др. 
- При стрельбе с близкой дистанции с полным попаданием может уничтожать многих слабых врагов одним выстрелом.