РАСКОПКИ ДРЕВНЕГО ХРАМА, ДРЕВНИЕ ИССЛЕДОВАНИЯ


Нас занесло на археологические раскопки. Исследовательский центр, вгрызающийся в руины чего-то очень древнего. И судя по тому, что здесь зарыто, копали они не просто так.

В центре - остатки древнего храма или монумента, сложенного из массивных каменных блоков.

Главное здесь другое - секрет, ради которого стоило копать.

Где-то на территории раскопок, под символом лямбды сопротивления, спрятан оранжевый куб, явно не местного происхождения.

Сломаешь куб. Разбиваешь внешнюю оболочку, и из неё выпадает... легендарный The Orange Box. Да, Сэм, та самая коробка, что подарила миру Half-Life 2, Portal и Team Fortress 2.

Сломаешь коробку. И вот тут начнётся светопреставление. Как только The Orange Box будет уничтожен на куски, в динамиках ворвётся Nuclear Mission Jam - тот самый эпичный трек, который любое побоище превращает в легендарную мясорубку.

Погнали искать куб.

И помни: не просто сломать, а с чувством. С толком. С Расстановкой.
