КОП ГРАЖДАНСКОЙ ОБОРОНЫ АЛЬЯНСА


КЛАСС:        Коп Гражданской Обороны Альянса
МЕТАБОЛИЗМ:   Человек, Мужчина
ВОСПРИЯТИЕ:   Оккупационная полиция / поддержание порядка
РАЗМЕР:       1,9 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Пистолет, электро-дубинка, манхэк
НАГРАДА:      350
УГРОЗА:       Средняя

ОПИСАНИЕ:

Коп Гражданской Обороны - самый узнаваемый "близкий к народу" представитель Альянса. Когда-то это были обычные люди: бывшие полицейские, охранники, добровольцы или просто те, кто решил, что свисток и дубинка лучше, чем стать Сталкером. Теперь они и Метрокопы - лицо порядка City 17 и других городов.

Он одет в тёмно-серую униформу: куртка, брюки, перчатки и ботинки, доведённые до одинаково унылого стандарта. На рукаве закреплены жёлтые опознавательные элементы с надписью "C17". Чёрный шлем с противогазом лишает его индивидуальности, а оранжевая полоса на нём служит почти декоративным знаком принадлежности к системе, которая смотрит на мир через фильтры.

За спиной у копа закреплён манхэк - механический "помощник", который по щелчку выпускается вперёд. Манхэк выполняет роль пугала, разведчика и карателя одновременно, а иногда кажется, что он умнее своего владельца. Сам коп чаще предпочитает угрозы, удары дубинкой и демонстрацию власти, чем реальный бой.

Брони почти нет, а подготовка минимальна. Их сила не в индивидуальных качествах, а в численности, страхе и поддержке Альянса. Тем не менее, в паре с манхэком такой коп способен создать серьёзную проблему, особенно для неподготовленных граждан.

СОВЕТЫ:

- Коп сам по себе слаб, но манхэк резко повышает опасность.
- Уничтожь манхэка первым - без него коп быстро теряет силу.