КОКОН АНТАРЕССКОГО ПАУКА С АНТАРЕС ПРАЙМА


КЛАСС:        Кокон (Яйцо) Антаресского Паука
МЕТАБОЛИЗМ:   Органическая стационарная капсула
ВОСПРИЯТИЕ:   Защитная капсула
РАЗМЕР:       0,5 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Пассивная
ОРУЖИЕ:       Выпуск одного Антаресского Паука при разрушении
НАГРАДА:      10
УГРОЗА:       Средняя

ОПИСАНИЕ:

Органическая структура, служащая инкубатором для одного Антаресского Паука. Состоит из сети прочных, жилистых биокорней, которые прикрепляют его к поверхностям - чаще всего к потолку, полу или стенам в местах, где пауки чувствуют себя в безопасности (пещеры, подвалы, заброшенные тоннели). Кокон не атакует. При разрушении (выстрелом, взрывом или ударом) из него вырывается паук, немедленно атакующий нарушителя.

СОВЕТЫ:

- После разрушения - будь готов к немедленной атаке паука. Заранее займи выгодную позицию.
