ОГНЕННЫЙ СНАРК С ЗЕНА


КЛАСС:        Огненный снарк
МЕТАБОЛИЗМ:   Плотоядное насекомое (Огненная Мутация)
ВОСПРИЯТИЕ:   Инопланетное Насекомое, Живые Угли
РАЗМЕР:       6 дюймов
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Абсурдно Высокая
ОРУЖИЕ:       Огненный Укус
НАГРАДА:      100
УГРОЗА:       Высокая

ОПИСАНИЕ:

Это естественная мутация обычного снарка, которая возникла в огненных районах Зена. Там, где лава течёт реками, а воздух раскалён до предела, обычные снарки не выживают. Но природа Зена нашла выход: те, кто выжил, превратились в чёрно-красных насекомых, чей панцирь светится изнутри, как тлеющие угли.

В отличие от своих собратьев, которые живут несколько секунд, а потом взрываются от переизбытка энтузиазма, огненные снарки не подвержены самоуничтожению. Они могут жить долго. Очень долго. И всё это время они будут пытаться тебя сожрать. Или поджечь. Или и то и другое одновременно.

Они имеют чёрное тело с ярко-красными прожилками, которые пульсируют, как лава в жерле вулкана. Издали они сверкают и отражают свет, заметить их легко. Но как только ты подходишь ближе - они исчезают из виду, сливаясь с тенями и раскалёнными камнями.

Укус огненного снарка не просто болезнен - он воспламеняет цель. Одно попадание - и ты горишь. Два - и ты уже не успеваешь потушить себя. Выжившие поговаривают, что эти твари специально нападают группами, чтобы жертва не успела среагировать на множественные очаги возгорания.

Огненные снарки постоянно враждуют с дьявольскими буллсквидами - своими соседями по огненным районам. Конфликт длится веками. Никто не помнит, с чего всё началось. Возможно, из-за территории. Возможно, из-за еды. А может быть, дьявольские буллсквиды просто отказались признавать, что красный цвет на панцире снарков - это не "розовый", как они утверждают.

При смерти огненный снарк не просто умирает. Он распадается на куски раскалённой лавы, которые разлетаются в разные стороны.

СОВЕТЫ:

- Не подходи близко к огненному снарку. Он не взрывается, как обычный, но его укус поджигает.
- Издали их видно по отблескам. Используй снайперскую винтовку.
- В ближнем бою они опасны вдвойне. Держи дистанцию.
- Огненные снарки не умирают сами.
- Если видишь стаю, используй взрывчатку. Огонь и так кругом, ещё немного - не помешает.
- Огненные снарки и дьявольские буллсквиды ненавидят друг друга. Если есть возможность, страви их. Пусть убивают друг друга.
