ЛОББИ ЧЁРНОЙ МЕЗЫ, ДЕМО-ВЕРСИЯ СИЛОСА


Сэм, мы в силосе. Не в боевом, не в полном - в демо-версии. Место, где Чёрная Меза когда-то тестировала ракетные двигатели, а теперь тестирует наши нервы.

Здесь выросли. Или спустились. Или их занесло ветром межпространственного коллапса. Три существа класса "Тентакль". Хотя сами тентакли - это единый организм с щупальцами, действующими отдельно. 

Проблема: нет огнемёта. Нет ракетного двигателя, который можно направить на них. Нет ничего, что могло бы их поджарить. А без огня эти твари бессмертны. Бесполезно что-либо предпринимать - так написано в протоколах. А протоколы Чёрной Мезы обычно пишутся кровью тех, кто их игнорировал.

Единственный способ, который когда-либо срабатывал (иногда, случайно, по воле багов и отчаяния) - поймать тайминг и стрелять из лучевой пушки непрерывно в корень щупалец. Не в ствол, не в присоски, а именно туда, где плоть переходит в пол. Там у них что-то вроде нервного узла. Может сработать. Может нет. Но выбора нет.

В данный момент с ними сражаются три охранника. Они не горят желанием умирать, но и отступать некуда. Стреляют из всего, что есть. Тентаклям это всё равно. Охранники - мясо. Вопрос времени, когда оно упадёт.

А ещё в одной из комнат засели H.E.C.U.

Они просто сидят в укрытии и ждут. Чего? Может, подкрепления. Может, нашей смерти. Может, когда всё само рассосётся. Но это место никуда не ведёт. Это тупик. Дальше только стены, трубы и тентакли.

Единственный выход - убить тварей. Или умереть.
