ОПИСАНИЕ:

Личинки муравьиных львов - это личиночная форма муравьиных львов, небольшие червеобразные существа, живущие под землей или во влажной почве. Несмотря на свою хрупкость, их численность и непредсказуемое появление позволяют им обитать в пещерах, туннелях или на разрытых землях.

