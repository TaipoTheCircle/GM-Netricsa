ХАЙПОРИ


ПЛАНЕТА:        Хайпори
ТИП:            Планета земного типа, Промышленно-пустынный мир
КЛИМАТ:         Сухой, засушливый
СПУТНИКИ:       Неизвестно
ЛАНДШАФТ:       Пустынные равнины, скалистые возвышенности, промышленные зоны, обломки космических кораблей
РАСПОЛОЖЕНИЕ:   Сектор Ферра, Внешнее Кольцо
МЕСТНЫЕ РАСЫ:   Люди (беженцы, члены Альянса), ситхи и их лоялисты (в период оккупации)
ПРИНАДЛЕЖНОСТЬ: Конфедерация Независимых Систем (Ранее); Последний оплот Альянса, под угрозой вторжения ситхов
МЕСТА:          Город Альянса, покои Королевы Мотмы, Большой бальный зал, система планетарной обороны

ОПИСАНИЕ:

Суровый скалисто-пустынный мир, расположенный в секторе Ферра Внешнего Кольца. Во времена Войн клонов планета стала местом ожесточённого сражения между Галактической Республикой и Конфедерацией Независимых Систем, когда флот Республики попал в ловушку, устроенную генералом Гривусом. Обломки республиканских кораблей, включая крейсеры типа "Аккламатор", до сих пор усеивают поверхность, напоминая о той битве.

Во время событий Войн Джедаев становится последним оплотом Альянса - сопротивления, сформированного Королевой Мотмой после падения Явина IV. На Хайпори расположен последний город Альянса, где укрываются выжившие джедаи, беженцы и силы сопротивления. Здесь находятся покои Королевы Мотмы, Большой бальный зал и система планетарной обороны, разработанная самим Джейденом Корром.

Город построен с учётом возможной осады, но его защитные системы не были готовы к полномасштабному вторжению. Когда флот ситхов под командованием Повелителя ситхов - представителя расы Кел Дор - достигает орбиты Хайпори, начинается битва за последнюю цитадель Альянса. Крушение командного корабля ситхов наносит городу катастрофические разрушения, однако именно это событие становится переломным моментом в войне.

В разные периоды планета служила убежищем для различных сил: во времена Галактической Империи здесь скрывались повстанцы, во время Войн клонов здесь стояли гарнизоны сепаратистов, а в эпоху владычества ситхов Хайпори становится символом последнего сопротивления.

Несмотря на стратегическое значение, планета остаётся малоизвестной и находится в стороне от основных гиперпространственных маршрутов, что делает её идеальным убежищем для тех, кто стремится остаться незамеченным.

КЛЮЧЕВЫЕ МЕСТА:

- Город Альянса: Последний оплот сопротивления на Хайпори. Здесь расположены административные здания, жилые кварталы беженцев и штаб-квартира Альянса.
- Покои Королевы Мотмы: Резиденция лидера Альянса в северной части города, рядом с Большим бальным залом. Во время вторжения ситхов здесь проходила финальная оборона.
- Система планетарной обороны: Сеть управляющих переключателей и защитных механизмов, разработанная Джейденом Корром. Способна противостоять армаде ситхов, но требует активации из разных точек города.
- Большой бальный зал: Центральное здание города, расположенное напротив покоев королевы. Место последних совещаний Нового Совета джедаев перед вторжением.
