БУРРОБОТ С МОБИУСА


КЛАСС:        Бурробот
МЕТАБОЛИЗМ:   Животно-Аккумуляторный
ВОСПРИЯТИЕ:   Подземный засадник
РАЗМЕР:       1,2 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Нос-бур, корпус
НАГРАДА:      400
УГРОЗА:       Низкая

ОПИСАНИЕ:

Когда доктору Эггману потребовалось контролировать подземные коммуникации и лабиринты, он обратил внимание на кротов. Их способность рыть тоннели и внезапно появляться из-под земли идеально подходила для организации засад. Так появился Бурробот - отдалённый предок более известного Грундера.

Вместо ушей - серые диски, вместо носа - массивное конусообразное сверло. На голове жёлтая каска. Вместо ног - гусеничные ленты с двумя колёсами по бокам: одно маленькое, другое побольше.

В более поздних модификациях получил жёлтые защитные очки и способность преследовать цель под землёй.

СОВЕТЫ:

- Следи за носом. Если из земли торчит серое сверло - под тобой Бурробот.
- Атакуй в момент выпрыгивания, пока он не приземлился.
- На поверхности он беспомощен и медлителен - можно не спешить.
- В гусеницы лучше не целиться - они прочнее корпуса.

