ОХРАННЫЙ КОМПЛЕКС ЧЁРНОЙ МЕЗЫ, ЗАТОПЛЕННЫЙ СКЛАД


Сэм, мы наконец-то добрались до места, где охранный комплекс окончательно махнул рукой на сухость и порядок. Здесь всё затоплено. Не частично, не лужами - по щиколотку, по колено, а в некоторых местах и по пояс. Вода мутная, холодная и явно не питьевая. Это бассейн для тех, кто не боится кислотных ожогов и внезапных прыжков из-под воды.

А бояться здесь есть кого. Буллсквиды. Эти твари облюбовали затопленные помещения Чёрной Мезы ещё до того, как мы сюда сунулись. Для них это идеальный дом: мокро,  много углов, где можно спрятаться, и много еды, которая иногда приходит в гости. Еда - это мы. Они не особо разборчивы.

Но у нас есть путь, как и любого ниндзя. Через склад тянется лестница. Ржавая, скользкая. Она ведёт туда, куда нам нужно. В компьютерный центр, через который м сможем попасть к лифту.
