ЧОППЕР С МОБИУСА


КЛАСС:        Чоппер
МЕТАБОЛИЗМ:   гидро-Животно-Аккумуляторный
ВОСПРИЯТИЕ:   Засадный охранник периметра
РАЗМЕР:       1,2 м.
ВЫНОСЛИВОСТЬ: Низкая
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Корпус, челюсти
НАГРАДА:      300 FC
УГРОЗА:       Низкая

ОПИСАНИЕ:

Когда доктор Эггман столкнулся с проблемой охраны мостов и водных переходов, он обратил внимание на пираний. Их манера молниеносно атаковать из воды легла в основу проекта "Чоппер". Первые модели собирали буквально из того, что плавало рядом: корпус от красной рыбки-попугая, плавники от старого вентилятора, зубы от садовых грабель и двигатель от кофемолки "Мрия".

Чоппер - это единственный бадник, который не преследует цель, а терпеливо ждёт под водой. Он висит в толще, скаля зубы, и наблюдает за поверхностью. Как только над мостом появляется силуэт, Чоппер выпрыгивает вертикально вверх, целясь прямо в живот незадачливому путнику. Если атака не удалась, плюхается обратно и через некоторое время пробует снова. В более поздних модификациях научился плавать горизонтально и даже устраивать коллективные засады.

Внутри корпуса плещется маслянистая жидкость, заменяющая мозг. Единственная команда, записанная в память: «Если сверху темно - кусай".

СОВЕТЫ:

- Атакуй в момент выпрыгивания, пока он висит в воздухе.
- Не стой на месте, когда переходишь мост - Чоппер целится по точке, где ты был секунду назад.
- Некоторые модели способны преследовать цель под водой.
