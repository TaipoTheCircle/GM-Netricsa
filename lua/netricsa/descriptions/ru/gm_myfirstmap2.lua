ГОРОД ДЛЯ ДРУЗЕЙ 2


В городе расположено несколько зданий, включая кирпичный гараж и группу небоскрёбов, состоящих в основном из окон без дверей. Некоторые дороги проходят по крышам и соединяют здания между собой. Атмосфера минималистичная - серое небо, зелёная трава и пустой город создают ощущение простора и лёгкой изоляции.