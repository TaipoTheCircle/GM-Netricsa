МУТАНТ ОМЕГА С АРХИПЕЛАГА КРИГЕРА


КЛАСС:        Мутант Омега
МЕТАБОЛИЗМ:   Финальная триген-мутация
ВОСПРИЯТИЕ:   Боевая платформа
РАЗМЕР:       12 футов
ВЫНОСЛИВОСТЬ: Максимальная
ВРАЖДЕБНОСТЬ: Абсолютная
ОРУЖИЕ:       Ракетные установки, мини-ракеты
НАГРАДА:      5000
УГРОЗА:       Катастрофическая

ОПИСАНИЕ:

Омега - кульминация экспериментов Кригера, финальная и самая совершенная форма тригенов. Визуально напоминает Толстяка, но превосходит его по всем параметрам. Оснащён тяжёлой броней и встроенным арсеналом, превращающим его в ходячую крепость. Использует комбинированное вооружение: ракеты ведут огонь с рук, а ракетные установки на плечах накрывают площадь мини-ракетами.

Он действует, как мобильная огневая точка. Чередует виды атаки для поражения целей на любой дистанции. Дробовики эффективны против ближайших врагов, ракеты - против удалённых групп, а залпы мини-ракет не оставляют шансов на укрытие. Броня поглощает значительный урон, делая его практически неуязвимым для стандартного оружия.

СОВЕТЫ:

- Используй тяжёлое вооружение - только гранатомёты и взрывчатка эффективны против его брони.
- Постоянно двигайся - стоять на месте равно смерти от ракетного залпа.
- Атакуй с флангов - основное вооружение сосредоточено в передней полусфере.
- Ищи укрытия от залпов мини-ракет - уходи в помещения или за капитальные строения.