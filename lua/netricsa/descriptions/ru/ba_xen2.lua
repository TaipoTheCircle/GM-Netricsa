ПОГРАНИЧНЫЙ МИР ЗЕН, ПЕЩЕРЫ ЗЕНА


Зен решил показать своё внутреннее устройство. Не метафорически. Буквально.

Пещеры. Лабиринты. Каменные коридоры, которые выглядят так, будто их проектировал кто-то, кто искренне ненавидел прямые линии и гостей.

Здесь полно зеновской живности. Она шевелится, дышит, плюётся и, в целом, считает тебя нарушением ландшафта. Между ними бродит орда Ментала, уверенно осваивая чужой мир, как будто Зен изначально шёл в комплекте с оккупацией. Два вида хаоса, столкнувшиеся в узком пространстве. Результат предсказуем: стреляют все.

Пещеры тянутся вглубь и в стороны, путая ориентиры и мысли. Здесь легко заблудиться. Ещё легче наткнуться на что-то, что предпочло бы, чтобы ты этого не делал. Кажется, Зен сам не до конца помнит, куда ведут его тоннели. Он просто продолжает их строить.

И да, под водой есть ещё одна пещера. Скрытая. Тихая. Там обитают Чамтоуды. Существа, о которых обычно узнают постфактум.

Так что будь внимателен, Сэм.