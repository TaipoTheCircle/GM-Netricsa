ЭЛИТНЫЙ ТРУТЕНЬ РОЯ "БЕТА", МАЛЫЙ


КЛАСС:        Элитный Трутень Роя "Бета", Малый
МЕТАБОЛИЗМ:   Ксенобиологический
ВОСПРИЯТИЕ:   Стайный Хищник
РАЗМЕР:       2,2 м.
ВЫНОСЛИВОСТЬ: Высокая
ВРАЖДЕБНОСТЬ: Очень высокая
ОРУЖИЕ:       Когти, Зубы
НАГРАДА:      450
УГРОЗА:       Высокая

ОПИСАНИЕ:

Элитные трутни встречаются среди Роя "Бета" значительно реже обычных особей. Их легко узнать по угольно-чёрному окрасу хитина и трём глазам, которые способны различать добычу даже в условиях практически полной темноты.

По строению тела элитный трутень почти не отличается от своих собратьев, однако его мускулатура заметно плотнее, а внешний покров гораздо прочнее. Многие колонисты ошибочно принимали таких существ за отдельный вид, пока не обнаружили, что элитные особи появляются в тех же ульях и подчиняются тем же инстинктам.

Среди ксенобиологов распространена теория, что чёрная окраска является признаком зрелости или результатом длительного выживания в особо опасных условиях. Иными словами, если перед тобой элитный трутень, значит ему уже удалось пережить множество ситуаций, которые должны были его убить.

В отличие от обычных трутней, элитная особь редко гибнет от первых попаданий и продолжает наступление даже после тяжёлых ранений. К сожалению, Рой "Бета" воспринимает такую живучесть как выдающееся достижение, а не как повод наконец начать беречь себя.

СОВЕТЫ:

- Не рассчитывай, что он погибнет так же быстро, как обычный трутень.
- В первую очередь уничтожай элитных особей в составе большой стаи.
- Темнота играет на его стороне.
- Если на тебя несётся чёрный трутень с тремя горящими глазами, значит предупреждающий этап знакомства уже завершён.
