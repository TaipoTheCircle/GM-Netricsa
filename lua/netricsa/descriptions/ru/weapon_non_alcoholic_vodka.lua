ОПИСАНИЕ:

После употребления дарует сверхъестественную силу без опьянения. Мышцы напрягаются, чувства обостряются, а тело становится оружием.

ПРИМЕЧАНИЯ:

- Нулевой алкогольный эффект.
- Чистое усиление, идеально подходит для боя.
- Ограниченная продолжительность - исчезает после боя.