ОПИСАНИЕ:

Арктический элитный солдат Overwatch Альянса - это специализированная версия элиты Альянса, предназначенная для боевых действий в условиях вечной мерзлоты, снежных бурь и мёртвых полярных городов. Его белый камуфляж практически сливается с окружающим льдом, а серо-синие элементы экипировки заменяют привычные красные детали, делая силуэт холодным, безэмоциональным и почти призрачным.

Единственный синий визор светится сквозь метель, фиксируя тепловые сигнатуры и движение на значительном расстоянии. По обеим сторонам ног проходят синие полосы, выполняющие роль опознавательных и технических элементов брони, а также визуального маркера элитного статуса.

Эти бойцы развёртываются в регионах, где обычные силы Альянса быстро теряют боеспособность. Усиленные импланты защищены от переохлаждения, а система жизнеобеспечения позволяет вести бой часами без укрытий и снабжения. Тактика стандартна для Overwatch-элиты: скоординированный огонь, подавление энергетическими шарами и методичное уничтожение сопротивления.

СОВЕТЫ:

- Белый камуфляж делает их трудно заметными целями на снегу.
- Энергетические шары особенно опасны в узких ледяных проходах.