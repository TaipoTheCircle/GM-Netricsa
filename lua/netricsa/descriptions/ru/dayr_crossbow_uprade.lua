УЛУЧШЕННЫЙ АРБАЛЕТ ВЫЖИВАЛЬЩИКА


ОРУЖИЕ:  Улучшенный арбалет Выживальщика
ПАТРОНЫ: Арбалетные болты
СС:      ~1 выстрел в секунду


ОПИСАНИЕ:

Доработанная версия стандартного арбалета, обладающая повышенной мощностью и более стабильной эффективностью в бою. Конструкция усилена, что позволяет наносить больший урон без значительного увеличения сложности использования.

Оружие остаётся простым и надёжным, но способно эффективнее справляться с более крепкими противниками. При наличии соответствующих навыков может использовать отравленные болты, нанося дополнительный урон с течением времени.

ПРИМЕЧАНИЯ:

- Наносит повышенный урон по сравнению со стандартным арбалетом.
- Может использовать отравленные болты, наносящие урон со временем.
- Эффективен на ближней и средней дистанции.
- Боеприпасы легко создаются из подручных материалов.

